<font face="Times New Roman, Times, serif" size="3">Furthering the tragedy, the added attention to Oaxaca brought by the death of an American accelerated a planned government crackdown. That repression brought two more deaths in the region, bringing the total count to 14 since the teachers' strike began in June.<p>
In Brad's adopted hometown of New York City, shock spread quickly, but his friends also jumped to action with, first, an emergency meeting, then a vigil at the Mexican consulate. More than 200 protesters--most of whom knew Brad--gathered at the consulate.<p>
Spirited direct action included an activist scaling a light pole using Brad's own climbing gear, where he hung a hand-painted banner picturing Brad and emblazoned with the message "One more night at the barricades." Others were able to block both entrances of the consulate. The action was part of a series of protests at dozens of Mexican consulates in the U.S. and abroad, to protest both Brad's murder and continued state repression in Oaxaca.<p>
In Oaxaca itself, Brad's funeral drew 200 mourners, and several shrines were erected honoring his memory.<p>
Brad was a fixture in local activism and a fervent believer in independent media, contributing to the growth of Indymedia in New York. In letters and articles, friends have recounted Brad's compassion and dedication. Many remembered him for his role in saving a squat on the Lower East Side.<p>
I met Brad in 2000, soon after first becoming active. I worked with him in several coalitions, both in the global justice movement and some of the early antiwar formations after 9/11. Brad was firmly committed to changing the world and smashing capitalism, but he was also deeply emotional and caring.<p>
Perhaps most vividly, I remember when many of us gathered at the Italian consulate after learning that Carlo Giuliani had been shot by police at a protest against the G-8 summit in Genoa in 2001. Brad sang songs of protest at the top of lungs.<p>
Activists in New York are planning a weekend's worth of activities starting November 11 to remember Brad. A memorial service will be held at St. Mark's in the Bowery at 1 p.m. on that Saturday.<p>
In his missive from Oaxaca, Brad ended on a note of optimism, admiring the direct democracy-building in Mexico and talking of the resolve to keep fighting. Brad's own words provide us guidance in our continued struggle.<p>
He wrote: "One more death--one more martyr in a dirty war--one more time to cry and hurt--one more time to know power and its ugly head--one more bullet cracks the night--one more night at the barricades--some keep the fires--others curl up and sleep--but all of them are with him as he rests one last night at his watch."<br>
<b>Petrino DiLeo</b>, New York City<p>

