<font face="Arial, Helvetica, sans-serif" size="2">Many well-known left-wing authors and activists have added their name to a letter honoring the memory of independent journalist Brad Will and supporting the struggle of the people of Oaxaca.<p>
"We are extremely alarmed," the letter reads in part, "to see that rather than cracking down on the violent paramilitaries who have been launching regular attacks on the people of Oaxaca, President Vicente Fox is using these murders as a pretext for escalating violence against the popular grassroots organization of the people of Oaxaca.<p>
Signers include Tariq Ali, Noam Chomsky, Mike Davis, Eduardo Galeano, Danny Glover, Naomi Klein, Camilo Mej&iacute;a, Oscar Olivera, Francis Fox Pivin, John Pilger, Katha Pollitt, Arundhati Roy, Wallace Shawn and Howard Zinn.<p>
To add your name to this letter--as well as for information on the struggle in Oaxaca and on events to honor Brad Will--visit the <a href="http://www.friendsofbradwill.org" target="_blank">Friends of Brad Will Web site</a>.
