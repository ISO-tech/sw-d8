<font face="Times New Roman, Times, serif" size="4">South African activist-poet Dennis Brutus</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A voice of hope and resistance</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Bill Keach</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Poetry &amp; Protest: A Dennis Brutus Reader, </b></i>edited by Lee Sustar and Aisha Karim. Haymarket Books, 2006, 415 pages, $16.<p></font>

<font face="Times New Roman, Times, serif" size="3">DENNIS BRUTUS is known around the world for his leading role in the heroic struggle to end apartheid in his native South Africa--and for his continuing fight against oppression, inequality and war.<p>
He has also been widely recognized as a writer of great eloquence and power. This fine collection of his poetry, essays and interviews makes available the full range of his work over half a century of activism.<p>
Lee Sustar and Aisha Karim have organized the collection in an effective way. The three main parts all begin with a concise historical and political introduction, then present Brutus' writing in a sequence moving from Memoir to Documents to Poems.<p>
The result is a rich sense of writing as integral to Brutus' activist commitments. Some of Brutus' writing is made available for the first time in this volume.<p>
In the early 1960s, he was imprisoned by the South African government on Robben Island, along with Nelson Mandela. Brutus' remarkable account of this imprisonment is published here for the first time, specifically edited for this volume by Aisha Karim.<p>
Of particular interest under the Memoir heading is Brutus' story of his work, while in exile, with the South African Non-Racial Olympic Committee. Brutus led the movement that was eventually successful in having South Africa banned from the Olympics, a move that helped build worldwide opposition to the brutality of apartheid.<p>
Another powerful Memoir section is titled "The Artist as Political Activist." Brutus explains that he "never saw poetry as a deliberate, strictly political instrument. It was political because my landscape was political. It was inescapably political."<p>
Raised in a South African "colored" community by parents who were both English teachers, Brutus grew up with an unaffected sense of poetry as a way of looking at the world and articulating his relationship to it.<p>
Though he has a sophisticated understanding of poetic techniques and forms, Brutus' poems communicate in direct, immediately engaging ways. Their subjects range from the horrifying memories of Robben Island in the 1960s to today's political struggles against the death penalty ("Sequence for Mumia Abu Jamal") and the exploitation of Hurricane Katrina survivors ("New Orleans").<p>
The Documents sections include letters, interviews, essays--and speeches that vividly evoke their original occasion. And sometimes the speeches themselves include a poem, as on Steve Biko Day, San Antonio, Texas, June 16, 1978:<p>
<blockquote>
<i>...he planned, dreamed,<br>
waged his struggle<br>
and hardened his will<br>
to confront the butchers<p>
to challenge their terror<br>
--even if they robbed him of his life</i>.
</blockquote><p>
Imagine this speech being given--this poem being read--today, in the middle of Bush country. Brutus traces his political perspective and principles back to a group of Trotskyist high school teachers.<p>
Across the whole span of writing included in <i>Poetry &amp; Protest, </i>you feel Brutus' uncompromising belief in the tradition of politics from below, in the collective power of ordinary working people to overcome class oppression and racial division.<p>
As he says in a poem written in Capetown on September 16-17, 2001:<p>
<blockquote>
<i>no glib formulations<br>
must obscure<br>
phantsi privatization phantsi<br>
forward to socialism<br>
our road is clear!
</blockquote><p>

