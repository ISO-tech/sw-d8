<font face="Times New Roman, Times, serif" size="5"><b>First look at bad deal for New York teachers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent, UFT</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The United Federation of Teachers (UFT), the union representing more than 100,000 teachers and school-based staff here, announced November 6 that they reached a tentative contract agreement with the city a full year ahead of the expiration of the current contract.<p>
The last contract--which was settled two years late and weakened teachers' rights, extended the work day and school year and created extra work for already overburdened and exhausted teachers--was ratified last fall by a narrow majority. As a result, the UFT created a new expanded negotiating committee and began discussing preparations for the next round of contract negotiations.<p>
The UFT also entered into a bargaining coalition with many of the other city unions--with the notable exceptions of AFSCME District Council 37, the biggest union in the city, and the police, who bargained separately.<p>
The UFT leadership clearly felt pressure to settle a contract in advance of upcoming union elections and declare it a victory.<p>
The proposed contract, subject to ratification, does provide a small raise of 7 percent over two years and a limited erosion of work rules. And unlike the previous two contracts, it contains no provisions for extra work time or major concessions on work rules.<p>
It does, however, contain a "voluntary buyout" clause for teachers who are not given an assignment in a school due to cuts in enrollment and who have not found a new job within a year. This could make it easier to pressure even high-seniority teachers to "voluntarily" accept a severance package rather than guaranteeing their continued employment, as the current contract provides.<p>
Already many union activists have recently found themselves "in excess" and unable to find new jobs due to their reputations as a union militant. The proposed contract doesn't preclude future concessions on health care, which is negotiated by the city's Municipal Labor Council and is headed by the UFT's Randi Weingarten.<p>
As the <i>New York Times</i> noted, the teachers' settlement "seemed to be part of a larger strategy by the Bloomberg administration to pave the way for separate talks aimed at achieving crucial savings on health care and pension costs, which have climbed sharply in recent years."<p>
Given the deteriorating work conditions, loss of union rights and additional workload teachers face as a result of the pattern of concessionary bargaining, this new contract is not enough.<p>
Members of opposition caucuses within the union, such as Teachers for a Just Contract and the Independent Community of Educators, urge teachers to vote "no" and send our union leadership a message that we can and should do better. We also need to reject this cynical attempt by the union leadership to gain votes in this year's union election.<p>
This is why the opposition is running in this year's UFT election to rebuild a democratic, rank-and-file led, militant union that can fight for better working conditions for all of us.<p>
