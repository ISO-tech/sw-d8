<font face="Times New Roman, Times, serif" size="5"><b>The harsh reality for working New Yorkers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Hannah Wolfe</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">TWO RECENT incidents in New York City and nearby New Jersey expose the reality behind the happy talk about the U.S. economy from politicians and the media.<p>
On November 3, more than 5,000 people turned up in Times Square in Manhattan in response to an ad for 200 jobs--only 65 of them full-time--at an M&amp;Ms World candy store. The ad promised $10.75 hourly pay--well above the $6.75 an hour minimum wage, though far from a living wage in New York City--full benefits and "on-the spot-hiring."<p>
Applicants traveled from miles around and began arriving as early as 1 a.m. When the crowd became so large that traffic was disrupted, mounted police were called in to "control" the crowd.<p>
"This is what unemployment looks like in New York City," said 28-year-old Tamika Jones, a mother of three, "I wanted to cry."<p>
Three weeks earlier, an eerily similar event unfolded in New Jersey when 2,000 people flooded the housing authority headquarters in the city of Orange to get 200 applications for vouchers that subsidize rent for low-income families. When the crowd became "unruly," according to reports, police were called in and pepper-gassed the crowd.<p>
New Jersey is the most expensive state for homeowners and third-most expensive state for renters, according to the Census Bureau. An estimated 38 percent of New Jersey homeowners and 47 percent of tenants spend more than 30 percent of their income on housing costs.<p>
"It shows you the state of the economy when this many people show up because they are in dire need of housing assistance," Karen Patton, a 48-year-old mother of three, told the New Jersey <i>Star-Ledger</i>. Patton, who has a full-time job, lined up for an application, but left when the cops moved in, because, she said, "it's more important for me to be alive than be crushed to death to get an application."<p>
Arnold Cohen, policy coordinator for the Housing and Community Development Network of New Jersey, told the <i>Star-Ledger</i>, "For people of lower incomes, the situation is desperate. There is no housing that addresses their needs except for public housing or Section 8. Otherwise, people have nowhere to turn."<p>
A report issued by Cohen's group earlier this year said average rent for a two-bedroom apartment in New Jersey is $1,085 a month, and 53 percent of renters can't afford the cost.<p>
Karen Patton is spending $951 on a three-bedroom apartment in Orange while earning below the Section 8 threshold of $42,200. "I do what I can do," she said. "But add utilities, gas to go to work and insurance, and it's hard to make ends meet. The state of the economy is not what people make it out to be."<p>
Reminiscent of the bread lines of the Great Depression of the 1930s, these two incidents point to the harsh disconnect between publicized statistics showing an economic recovery--and what life is really like for lower-income working people.<p>

