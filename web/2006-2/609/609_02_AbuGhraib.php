<font face="Times New Roman, Times, serif" size="5"><b>Torturer sent back to train Iraqi police</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AN IRAQI prisoner cowering naked and terrified as a U.S. soldier sics a dog on him. This photo--along with others, for example, showing a hooded prisoner hooked up for electric shocks--exposed the barbarism of the U.S. occupation of Iraq for the world to see.<p>
Nonetheless, the Army seems to have forgotten about the torture when it reassigned one of the soldiers handling the vicious dogs back to Iraq.<p>
Sgt. Santos Cardona, a military policeman from Fullerton, Calif., who served in 2003 and 2004 at Abu Ghraib as a dog handler, was on his way back to Iraq last week. His new assignment was to help train local Iraqi police.<p>
Cardona was brought before a military court earlier this year and convicted of dereliction of duty and aggravated assault. A military judge sentenced him to a fine and demotion in rank. Cardona spent no time behind bars, but served 90 days of hard labor at Fort Bragg, N.C.<p>
The court acquitted Cardona of seven charges, including attempts to harass a prisoner with his dog. His lawyers argued that Cardona should not be punished because his actions were condoned, if not approved, by officers in charge of the prison and senior Army officials.<p>
After an article about the soldier's return to Iraq appeared in <i>Time </i>magazine November 2, the Army announced that it had canceled Cardona's redeployment. According to an Army press release, Cardona is currently in Kuwait with his unit, the 23rd Military Police Company. His specific duties have not yet been determined.<p>
The Army says that it was concerned about Cardona's safety, since he is easily identified as one of the abusers at Abu Ghraib.<p>
But the outrage of putting a soldier who attacked Iraqi prisoners with vicious dogs in charge of training Iraq's police seemed lost on military officials. "This is America spitting in our face," Imad al-Hashimi, a Baghdad pediatrician, told <i>Time. </i>"The sheer arrogance of it is unbelievable."<p>

