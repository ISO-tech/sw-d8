<font face="Times New Roman, Times, serif" size="5"><b>Dobbs is a dead end for the Greens</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 10, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">IT IS a shame that Scott McLarty doesn't offer much more than condescending sarcasm in his defense of the national Green Party's recent decision to encourage members to reach out to right-wing xenophobe Lou Dobbs, because there are plenty of more political things that could be said about the strategy. ("Right to reach out to Dobbs," November 3)<p>
It's also unfortunate that Scott misrepresents the character of the October 11 call. It did not simply encourage members to try to get on Lou Dobbs' show in order to get broader public exposure for Green issues and candidates. As I tried to demonstrate by quoting the e-mail, it was a glowing endorsement of Dobbs' attack on the two-party system, which encouraged Greens to watch his show and called for them to let Dobbs know that the Green Party is the party for him and his viewers.<p>
I'm sure Scott can see how this is quite different from Todd Chretien's appearance on the <i>O'Reilly Factor</i>, to take his example. Did Todd tell Bill O'Reilly that he really appreciated the great work Bill was doing exposing the ills of our society, or that he hoped Bill would let his viewers know about the Green alternative? No. He called out Bill O'Reilly as the racist, right-wing hack that he is, and challenged him to come to San Francisco to debate the war.<p>
Or take, for another example, what Green Party member and immigrant rights activist Nativo L&oacute;pez told Lou Dobbs when he (temporarily) forced Dobbs to stop using the term "illegal alien" during his appearance on <i>Lou Dobbs Tonight</i> in April. "You're using language that's offensive to me and offensive to my people," L&oacute;pez said. "You pollute the air every day, Dobbs. You are absolutely wrong. That language is offensive, it's derogatory, it's denigrating, and don't use that terminology to me again, referring to my people."<p>
And now, Nativo is supposed to turn around and be a cheerleader for Dobbs' unremarkable critique of corporate influence in politics?<p>
I think this debate is not about a media strategy, but the nature--and future--of the Green Party itself. Is the Green Party a left-wing alternative to the Democrats, or is it a tent big enough for those from "across the political spectrum" who are fed up with the status quo? Are Green candidates running to get as many votes as possible, from whomever will vote for them, or are they running in order to present progressive ideas to wider layers of people? I think that ultimately the party will have to be one or the other.<p>
This does not make me an elitist. I agree that the left needs to reach out and convince broader sections of the population of our ideas. However, I believe the most effective way to do this is to tell the truth.<p>
We should challenge racists like Lou Dobbs and expose their attempts to divide us among ourselves--not welcome the demagogues of CNN and their racist supporters into our movement with open arms.<br>
<b>Ben Dalbey</b>, Baltimore, Md.<p>

