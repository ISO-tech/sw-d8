<font face="Times New Roman, Times, serif" size="5"><b>Year of the TV attack ad</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S NO secret that a flood of poisonous campaign ads is released onto the television airwaves every election year. This year, however, with Republicans increasingly desperate to hang onto power, some ads hit new lows for racist scapegoating, fearmongering and all-around lies.<p>
The most infamous may be an ad sponsored by the Republican National Committee against Harold Ford Jr., the Democratic Senate candidate in Tennessee. The spot featured a series of people in mock "man-on-the street" interviews talking sarcastically about Mr. Ford and his stands on issues like national security.<p>
But one--a young white actress playing the stereotype of a "dumb blonde"--says with a giggle that she met Ford, an African American, "at the Playboy party," and closes the commercial by saying, with a wink, "Harold, call me." As it turns out, Ford was one of 3,000 people who attended a party sponsored by <i>Playboy </i>magazine at last year's Super Bowl.<p>
The real intent of the ad is clear: play up racism against Ford--a conservative Democrat who could become the first Black senator from Tennessee since Reconstruction--by playing on fears of "race mixing."<p>
Other Republican lows include an ad against New York Democratic congressional candidate Michael Arcuri. The ad shows Arcuri leering at the silhouette of a dancing woman who says, ''Hi, sexy. You've reached the live, one-on-one fantasy line.''<p>
It then claims that Arcuri, the district attorney in Oneida County, dialed a phone sex line two years ago from a New York City hotel room, and billed taxpayers for the call. "Arcuri has denied it," the ad concludes, "but the facts are there."<p>
The real facts, however, show that one of Arcuri's aides made a one-minute call that cost $1.25, and the very next minute called the same phone number with a different area code--the number for the state Department of Criminal Justice Services. In other words, it was a wrong number.<p>
Then, of course, there's right-winger Sen. Rick Santorum (R-Pa.), who went "nuclear" in his ads.<p>
His campaign let loose with an ad against Democratic challenger Bob Casey that states "North Korea: Close to a nuclear missile to reach America--yet Casey opposes deploying a missile defense system now. Iran: also close--yet Casey opposes creating the bunker-busting bombs that may be needed to stop them." Cue the sinister music and pictures of rockets and mushroom clouds--superimposed next to Bob Casey's head.<p>
Elsewhere, Republicans have begun using an automated telephone-calling system to "educate" voters. In Montana, the automated voice asks voters if they believe judges who "push homosexual marriage and create new rights like abortion and sodomy" should be controlled.<p>
If they reply "yes," the voice "asks" them if "the fact that [Democratic Senate candidate] Jon Tester says he would have voted against common-sense, pro-life judges like Samuel Alito and John Roberts, and [Republican incumbent] Conrad Burns supported them, make you less favorable toward Jon Tester?"<p>
Don't count the Democrats out, though, when it comes to slinging sleaze. In Minnesota, for example, in an ad rivaling the worst of the Republicans' law-and-order rhetoric, the Democratic Congressional Campaign Committee accused Republican candidate Michele Bachmann of being soft on crime--by supposedly voting against putting "repeat sex offenders behind bars for life" (though she didn't).<p>
What's more, when the Democrats came under GOP attack, many responded by pandering to the right.<p>
For example, Tammy Duckworth, a Democratic candidate for the House from Illinois, was accused by her Republican opponent of being soft on "illegal immigration." So the Democrats called in their big gun--Illinois Sen. Barack Obama--to state in a rebuttal ad that "Tammy and I both support John McCain's plan, which does not include amnesty or benefits for illegal immigrants."<p>

