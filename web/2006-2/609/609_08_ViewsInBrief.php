<p><a name="vote"></a><font face="Times New Roman, Times, serif" size="4"><b>Vote for Leedham</b></font><p>

<font face="Times New Roman, Times, serif" size="3">JOE ALLEN'S article, while informative, falls short in articulating how this go-around for Leedham is not just important for rank-and-file Teamsters--the stakes are high for the entire U.S. labor movement ("What's at stake for the Teamster election," October 20).<p>
Hoffa and his pals in the Change to Win (CTW) coalition have done little to revitalize the go-along-get-screwed labor movement. In addition, CTW has largely been the conservative labor voice in the immigrant rights movement, a movement that has provided an opportunity for labor that we've not seen since the 1997 Teamster's strike at UPS--that is, to change the dynamic of class struggle.<p>
The top-down strategy, employed by Hoffa and CTW strongman Andy Stern is contrasted by Leedham's leadership and vision of building a new labor movement that seeks to provide confidence and the tools needed to rebuild the labor movement from below, grounded in democratic unionism and worker's power. Furthermore, while the AFL-CIO is adding to the Democrats' coffers this election season, Leedham favors using our dues for organizing and pressuring politicians, not throwing cash at them.<p>
A Leedham victory later this month in the country's second-largest union would be a huge step forward in the discussion and debate about the future of our labor movement. Teamsters have the opportunity to play a historic role in this election, not just for our own union but for workers all across the country.<p>
If you've lost or misplaced your ballot, or made a mistake on your ballot, call: 888-426-2006 to get a new ballot. Vote the Tom Leedham "Strong Contacts, Good Pensions" slate.<br>
<b>Jim York</b>, IBT Local 25, Boston<p>

