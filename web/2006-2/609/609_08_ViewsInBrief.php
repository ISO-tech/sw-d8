<font face="Times New Roman, Times, serif" size="4"><b>Standing up for choice</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, THE University of Vermont's student newspaper, the <i>Cynic</i>, ran a paid advertising insert sponsored by the anti-abortion group, the Human Life Alliance. This 12-page glossy piece of lies and propaganda attacked women's rights and people of color, as well as abortion rights.<p>
The response of the student body, faculty and staff of the university was one of outrage and disgust. Women felt attacked and ashamed, and as if their bodies, rights and voices did not matter. But also out of this anger came the underlying question: Why are women's rights and abortion rights under such militant attack from the right wing?<p>
In order to answer this important question and organize a public, activist response to the <i>Cynic's </i>unacceptable decision to run such a gross ad, the students of the International Socialist Organization on campus, along with support from the Feminist Club and the Women and Gender Studies Department, held a meeting entitled "We Won't Go Back, Stand Up For Abortion Rights!"<p>
The growth of the right was discussed, and how the lack of an independent, grassroots pro-abortion, pro-women movement has given the right the go-ahead to think it is acceptable to take away a woman's right to control her own body.<p>
This analysis was well-received, and we are focusing on not only how we can respond to this specific incident through a speak-out, but also what we can do to fight against sexism, women's oppression and attacks on abortion rights in a more general and radical way. Abortion rights are under attack, and the UVM community is going to stand up and fight back!<br>
<b>Mary Howland</b>, Burlington, Vt.<p>

