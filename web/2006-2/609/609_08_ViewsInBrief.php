<p><a name="myth"></a><font face="Times New Roman, Times, serif" size="4"><b>The myth of "humanitarian" invasion</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I THOUGHT clarification on what Nathan Kleinman wrote ("It's time to save Darfur," October 13) was needed. The goal as activists that we must keep in mind is that if we are trying to "save Darfur" for the refugees, outside intervention isn't an option here.<p>
While Kleinman admits that both the U.S. and UN have economic interests in Sudan, he falls into the argument that more aid will mean nothing "in the absence of security." This "security" amounts to setting Darfur up for what can amount to another of the failed "humanitarian" invasions, like that of the "famine relief" mission to Somalia, where 10,000 Somalians were killed; or the failed NATO war (led in the name of refugees) in Kosovo, where the number of refugees went from 45,000 to 800,000.<p>
Any "security" or "humanitarian" intervention that will increase military presence in Darfur gives the U.S. an even stronger position there.<p>
To look more at what a U.S. intervention really has in store for the people and refugees of Darfur, one only has to look at the fact that pro-Israeli groups, who justify the killings in Southern Lebanon and support the onslaught against Palestinians, and right-wing war hawks have used this "humanitarian intervention" as an opportunity to push their agenda in order to solidify the U.S. as the only imperial power.<p>
The fact is neither Franklin Roosevelt nor Bill Clinton nor President Bush has attempted to save the lives of millions of people unless it provided a step forward for U.S. global hegemony. To "save Darfur" means no "humanitarian" intervention and immediate economic aid.<br>
<b>Alvaro Lopez</b>, New York City<p>

