<font face="Times New Roman, Times, serif" size="5"><b>Virginia's choice of bigots</b></font><p>

<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 13, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">WATCHING THE catastrophic fumbles of Sen. George Allen (R-Va.) as he runs for re-election has made for some of the best political theater in this year's congressional election campaign.<p>
Allen was supposed to be a sure bet for re-election--and a possible contender for the Republican 2008 presidential nomination. Not any more.<p>
Allen's first major blunder came when he referred to a volunteer of his opponent with the racial slur "macaca." As S.R. Sidarth, who is of Indian descent, videotaped Allen, the senator told a laughing crowd of supporters, "Let's give a welcome to macaca, here. Welcome to America and the real world of Virginia."<p>
Allen later said he "made up" the word on the spot. But that's hard to believe from a man known for having connections to the openly racist Council of Conservative Citizens, for prominently displaying the Confederate flag, and for keeping, during his days as a lawyer, a noose hanging in his law office.<p>
Allen managed to dig himself in deeper a few days later when, during a debate, a reporter--following up reports that Allen had tried to cover up his Jewish heritage by claiming his grandfather was a French resister, but not Jewish--asked Allen if his "forebearers include Jews," and if so, "at which point [his] Jewish identity might have ended?"<p>
Allen blanched, and then angrily proclaimed that both he and his mother are Christians. He added that no one should make "aspersions about people because of their religious beliefs"--but never explained why being described as Jewish should be considered an "aspersion."<p>
Days later, Allen was forced to reveal that his mother informed him in August that she is, indeed, of Jewish descent. Allen told reporters that the day he found out, "I still had a ham sandwich for lunch. And my mother made great pork chops."<p>
With all of this, you might think that Allen's Democratic challenger would be coasting toward victory. Not James Webb, though.<p>
Webb has faced problems himself, especially for authoring a 1979 paper titled "Women Can't Fight" that attacked the idea women could serve in combat. In one passage, Webb described a U.S. Naval Academy coed dorm as "a horny woman's dream." In another, he declared that women's "presence at institutions dedicated to the preparation of men for combat command is poisoning that preparation."<p>
In an interview last month on NBC's <i>Meet the Press</i>, Webb wouldn't say that he was wrong, just that his article was just "too narrowly based."<p>
For Virginia voters, the only thing that's "too narrow" is the options they're faced with in this year's election for U.S. Senate.<p>

