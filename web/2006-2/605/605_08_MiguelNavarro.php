<font face="Times New Roman, Times, serif" size="5"><b>The anti-immigrant campaign hits home</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 13, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ESCALATING nationwide anti-immigrant campaign has hit home for Miguel Navarro, a high school student and activist in San Francisco. His father is an undocumented immigrant facing deportation proceedings after receiving a threatening letter from immigration authorities.<p>
Miguel organized and led a multiracial 200-strong student walkout in support of immigrant rights at Westmoor High School on March 31, 2006--Cesar Chavez Day. Some of the teachers actively supported the walkout because, as Miguel put it, "they knew we were walking out for the right reasons." Activists from the San Francisco State University chapter of MEChA also helped Miguel.<p>
The walkout drew coverage from local English, Spanish and Filipino television, and it gave a boost to the students' sense of power. "As we saw people united marching in the street, we realized we really can do something," he said.<p>
The school administration didn't want students coming to that realization and promptly gave everyone two days' detention after unsuccessfully trying to hand out more severe punishments. The Westmoor administration, however, did target Miguel for more harassment.<p>
"They were waiting for any little issue to retaliate," he said. So Miguel changed schools--and he's looking to start a political club at his new school, South San Francisco High.<p>
Now, his father faces similar retaliation against the immigrant rights movement. He is trying to sort through the issues with a lawyer, but if recent cases are any indication, he's likely to find that the courts and the enforcement authorities are stacking the deck against immigrants.<p>
"Many people are in the same situation as my father," Miguel said. "They come here to work. Let's say [Immigration and Customs Enforcement] came and pulled him out and deported him--it would be wrong, just wrong. My family is already on Section 8 housing, so it would be financially devastating."<p>
Miguel recognizes the hypocrisy of the U.S. government's use of the "war on terror" as an excuse to go after immigrants. "Coming to people's houses unexpectedly and deporting them--that's a form of terror," he says. "We need to question who the real terrorist is. The people at the top are trying to instill fear because they are afraid of the guy who's been at the bottom coming up."<p>
How can we resist this campaign of intimidation? According to Miguel, "We need to have people aware and organized for action. We can work peacefully now, but at one point, Malcolm X said, 'By any means necessary.' We need to unite Latinos and African Americans to fix what is wrong in this country."<br>
<b>Sid Patel</b>, San Francisco<p>

