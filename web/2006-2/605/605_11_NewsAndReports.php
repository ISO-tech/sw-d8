<font face="Times New Roman, Times, serif" size="4"><b>Stop the Minutemen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 150 immigrant rights activists met the racist Illinois Minutemen Project (IMP) with chants of "Hitler rose and Hitler fell! Minutemen go to hell!" on October 7.<p>
The IMP held its action against the Mexican Consulate as part of a so-called "National Day of Action" to stop the "invasion" of immigrant workers. The Web site for the action featured a racist cartoon of Latino workers depicted as the "seven dwarves."<p>
Even though the Minutemen never mustered a crowd of more than a dozen, police forced dozens of antiracist protesters who arrived earlier to make room for them in front of the consulate. But the Minutemen left without accomplishing their goal of spreading hatred in Chicago--with dozens of antiracists celebrating their departure.<p>
This was the second IMP protest at the Mexican Consulate--and this time, the racists had even fewer forces than last time, when nearly 200 immigrant rights activists confronted around two-dozen Minutemen and the racist Paul Revere Riders.<p>
Immigrant rights activists also drew the connection between the fight against far-right organizations like the Minutemen and the broader struggle for immigrant and workers' rights, such as the recent mass turnout against a proposed anti-immigrant law in nearby Carpentersville.<p>
As one protester remarked, workers shouldn't be penalized for crossing imaginary lines, drawn by politicians, to provide for themselves and their families. Protesters vowed to confront the IMP the next time they showed their heads at the Mexican Consulate--or anywhere else in Chicago.<p>
<i>Carole Ramsden, Orlando Sepulveda and Adam Turl and contributed to this report</i>.<p>

