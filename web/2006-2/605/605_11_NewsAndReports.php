<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 2,000 people marched October 5 calling for people to "Drive out the Bush regime!"<p>
Starting in the morning with a rally of 300 at the University of Washington, the crowd took over streets on the way to Cal Anderson Park on Capitol Hill, where it swelled to 2,000 by midday. Among other speakers, Sara Rich, mother of war resister Suzanne Swift, spoke about her daughter's case and her opposition to the Iraq war.<p>
From there, the march went on to the Federal Building downtown for an evening rally and an all night sit-in at the Federal Building Plaza. Those who participated were united in their opposition to the Bush regime, but beyond that there were many differences about what should come next for the left.<p>
Speakers from the Backbone Campaign and Progressive Democrats of America stressed the idea of electing a Democratic Congress to impeach Bush. Others, including socialists and activists in the campaign of Green Party member Aaron Dixon for U.S. Senate, called for independence from both corporate parties and for struggle to bring about real social change.<p>
Organizers promised future rallies and marches to keep the pressure up.<p>

