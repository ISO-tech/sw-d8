<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight racism and police brutality</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steven Wyatt and Martin Smith</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHAMPAIGN, Ill.--A multiracial crowd of 150 gathered here October 7 for the third annual Unity March aimed at ending racism and police brutality.<p>
The march went through the Garden Hills neighborhood, a community sharply affected by discrimination and poverty. Champaign is divided by a color line that separates this African American community on the northwest side by railroad tracks.<p>
With signs and banners held high, people marched through the streets, because there are no sidewalks in this neglected part of town. Residents say that they are frequently given jaywalking tickets as part of their daily experience of police harassment.<p>
Along the way, protesters stopped at points where police had brutalized members of the neighborhood, resting for a moment of silence to remember victims of police injustice. This past summer, a Black man was chased by several police officers and rammed by a police vehicle for merely possessing a gun.<p>
In the past two years alone, five Black men have allegedly committed suicide while in police custody in Champaign, though community members believe they were the victims of police wrongdoing. Black drivers make up one-third of all traffic stops in the city, while they are only 12 percent of the population.<p>
The march ended with a rally in a park, where people were encouraged to get involved in the struggle for justice.<p>

