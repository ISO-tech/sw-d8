<font face="Times New Roman, Times, serif" size="5"><b>USW strikes Goodyear to oppose givebacks</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 13, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 12,000 workers at Goodyear are on strike after the company demanded another round of major concessions from their union.<p>
The workers, members of the United Steelworkers (USW), walked out October 5 at 16 plants around the U.S. over the company's demands for givebacks, including cuts in retiree health coverage, and plans to close at least two plants that employ nearly 2,200 people.<p>
Goodyear's drive for concessions comes despite a promise made to the USW during 2003 contract negotiations that if the union agreed to concessions, workers would benefit from the company's success in the future. So the USW agreed to wage and benefit cuts and the elimination of 6,000 jobs--givebacks worth $2 billion.<p>
Goodyear, which had been near bankruptcy, recovered. As a reward, the company's CEO, Robert Keegan, got a $2.6 million bonus in 2005, in addition to his annual salary of $1.1 million.<p>
But after months of bargaining, the company wouldn't budge on demands that the USW give up still more.<p>
In the 2003 contract, the USW agreed to a closure of the Huntsville, Ala., facility as well as providing Goodyear with additional financial flexibility by accepting wage, pension and health care cuts.<p>
"We worked very hard with the company in 2003 to deal with a difficult situation," USW executive vice president Ron Hoover stated in a union press release. "While more work can be done, Goodyear has rebounded, and other stakeholders have been rewarded accordingly. Now the company seems determined to only take more away from our members."<p>
While details of the contract weren't disclosed, a slick Goodyear Web site gives a pretty good idea of what management is after.<p>
"To win in North America, we must see reality," the boss of the company's North American tire division, Jonathan Rich, said to the union at the outset of the negotiations. "We must significantly reduce our overall cost structure here," added Rich, who got a $680,000 bonus last year.<p>
Rich continued: "Work rules, job classifications and inflexible work schedules must be dealt with effectively. We must change the basic model between the company and associates for post-retirement health care benefits.<p>
"Health care costs are rising more rapidly than our sales or profits. This is not sustainable. In the future, we cannot afford to be the primary provider of retirement health care. For active employees, we must continue to strive to find the most cost-effective plans that can contain our costs and provide the security we all want for our families and ourselves."<p>
"Changing the basic model" and "cost-effective plans" are, of course, management code words for even greater givebacks, this time targeting retirees.<p>
Retirees and their survivors are already barely getting by. The <i>Akron Beacon-Journal</i>--the leading newspaper of the city where Goodyear is based--reported on a meeting of retirees' widows who said that they stopped receiving pensions when their husbands died, forcing them to rely on Social Security benefits alone. "If a new contract forces higher medical payments on them, that would be a real burden," according to the paper.<p>
The USW is preparing for a long strike. The International Federation of Chemical, Energy, Mine and General Workers' Unions, of which the USW is a member, has called for international solidarity.<p>
As the U.S. labor movement struggles to turn itself around, the battle at Goodyear will be an important test of whether unions can hold the line on Corporate America's endless demands for concessions.<p>

