<font face="Times New Roman, Times, serif" size="5"><b>A victim of three-strikes sentencing</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 13, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>RACHEL ODES reports on the case of Santos Reyes, a victim of California's "three strikes" law.</b><p>

<font face="Times New Roman, Times, serif" size="3">MEMBERS OF the group Families to Amend California's Three Strikes (FACTS), as well as Green Party members, gathered outside a Los Angeles courtroom September 14 to call attention to one of the many victims of California's "three strikes" law.<p>
Santos Reyes has already served nine years in prison for committing a third "strike" offense--in this case, taking a DMV test under a false identity so a cousin who could not speak English could get a driver's license.<p>
For this "crime," he has been sentenced to 26 years to life.<p>
The hearing on September 14 was the result of a rare victory for Reyes. An appeals court allowed him to challenge the circumstances of his second conviction, which was for attempted robbery in 1986. If the judge rules that he was falsely convicted in that case, then his subsequent offense would no longer be considered a last "strike," and the severe sentence could be revisited.<p>
Santos Reyes' first strike was stealing a radio in 1981, when he was 17 years old. This conviction counted as a first strike, although judges are given discretion over whether to count such offenses towards a third strike.<p>
Legal advisers to Reyes don't believe the circumstances of the second conviction will raise enough questions to overturn the three-strikes sentence. Instead, they hope to call attention to the draconian three-strikes sentencing law itself--using Santos Reyes' case to show of how such policies allow defendants accused of crimes of poverty to end up with life sentences.<p>
In their work, FACTS members highlight the many cases that involve a third strike of petty theft--stealing a loaf of bread, a slice of pizza or videotapes, for example. Their work also shows that the swelling of California's prisons is due primarily to the conviction of those who commit victimless crimes, like using drugs.<p>
At last count, California's prisons were 70 percent over capacity. This has become such a crisis that Gov. Arnold Schwarzenegger just declared the prison system to be in a state of emergency, which means he now can send inmates to other states' prisons and pay those governments to house them. Prisoners don't have to give their consent to be moved across the country, and they can be held there for five years or more.<p>
But Schwarzenegger can't complain about the crisis. Two years ago, he campaigned against Proposition 66, which would have reformed the three-strikes law--since then, he has kept the prisons filled to the brim and the rest of the state's social programs in the red.<p>
Peter Camejo and Donna Warren, the Green Party's candidates for governor and lieutenant governor, spoke at the press conference outside Reyes' hearing September 14, arguing for a much different policy that puts the needs of Californians ahead of "tough-on-crime" scapegoating.<p>
As Warren explained, "The two parties of greed have created this problem, and the poor and people of color are the ones who suffer. We have to organize to be part of the solution."<p>

