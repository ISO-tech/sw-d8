<font face="Times New Roman, Times, serif" size="5"><b>U.S. steps up threats against North Korea</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 13, 2006 | Pages 1 and 3</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH NEWS of North Korea's nuclear weapons test, the world saw the bitter fruits of the U.S. "war on terror." And U.S. officials are escalating the crisis by ramping up threats against the North Korean regime.<p>
House Speaker Dennis Hastert called the October 9 underground test of a nuclear device "the desperate act of a criminal regime." Democrats, meanwhile, tried to outflank George Bush to the right. "The Bush administration has for several years been in a state of denial about the growing challenge of North Korea, and has too often tried to downplay the issue or change the subject," said Senate Minority Leader Harry Reid.<p>
Bush claims his administration is committed to "diplomacy" to resolve the crisis. Yet after the Chinese government condemned North Korea's test, the U.S. tried to push through stricter international sanctions in the United Nations (UN), under Chapter 7 of the UN charter, which could authorize a military response.<p>
It's this sort of militaristic response by the U.S. that pressured North Korea into performing the tests to begin with.<p>
During the 1990s, the Clinton administration had promised trade, food aid, support for a civilian nuclear power program and, eventually, a formal peace treaty as incentives for the government to abandon its nuclear weapons development. The U.S. didn't come through--other than supplying fuel oil, and, through the UN, some food aid. Essentially, Clinton continued the longstanding policy of squeezing North Korea regime to the breaking point.<p>
Bush then made matters much worse when he came into office, rejecting direct negotiations with North Korea and adopting a belligerent attitude to the regime, including calling leader Kim Jong-il a "pygmy."<p>
After September 11, Bush named North Korea as a major threat in its "axis of evil," putting it on the short list of potential targets for pre-emptive war. The aim was to maximize U.S. political and military leverage in East Asia, one of the most important regions in the world economically.<p>
It's in this context that North Korean leader Kim Jong-il, a Stalinist dictator, unveiled a nuclear weapons program--in the hopes of guaranteeing survival of his regime. Once the U.S. invaded and occupied Iraq, North Korean leaders apparently concluded that only nuclear weapons could save their country from a similar fate.<p>
The Bush-initiated U.S. confrontation with North Korea is only the latest example of more than a half-century of military pressure from the U.S. and its client state, South Korea, beginning with a war between the Koreas from 1950 to 1953. Technically, the North and South have remained at war ever since--and North Korea has spent its meager resources building up its military to defend itself.<p>
It's far from clear when, or if, North Korea's nuclear devices could be turned into weapons.<p>
And when U.S. officials denounce North Korea for trying to obtain nuclear weapons, it's crucial to remember that the U.S. Navy--armed with nukes--is at the ready off North Korea's shores, permanently threatening the regime. The U.S., of course, is the only country to ever use nuclear weapons, dropping two atomic bombs on Japan during the Second World War.<p>
This crisis was avoidable. North Korea has repeatedly requested bilateral talks with the U.S., only to be refused by Washington. The Bush administration has also refused to okay a non-aggression treaty requested by North Korea, which would acknowledge the country's right to exist.<p>
The North Korean regime likely saw an opportunity to thumb their noses at the Bush administration amid the deepening crisis of U.S. occupations in Iraq and Afghanistan.<p>
The UN Security Council will likely come up with a new resolution against North Korea, perhaps including authorization for the council to take military measures against "threats to peace."<p>
The North Korea crisis "could be a good thing," Israeli Knesset member Ephraim Sneh told Israel Radio, explaining that Israeli policymakers should use the developments in North Korea to convince the international community to "do something [about Iran] before it's too late."<p>
The Japanese government is using North Korea's nuclear program as a rationale for its own aspirations for rearmament, including nuclear weapons. The possibility of a nuclear-armed Japan is reverberating through China and both North and South Korea, raising the specter of a new nuclear arms race in the region.<p>
For South Korea, the possibility of a U.S. military response is a frightening prospect. According to the defense analysts Stratfor, North Korea has some 10,000 fortified artillery pieces trained on Seoul, a city of 20 million people.<p>
No sane person wants a nuclear exchange--or a conventional war--on the Korean peninsula. But real disarmament has to start with the driving force of militarization of foreign policy in Asia--the United States government.<p>

