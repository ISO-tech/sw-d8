<font face="Times New Roman, Times, serif" size="5"><b>Rallying against bigotry in Illinois</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Freund</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 13, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">A SPIRITED crowd of 3,000 turned out in Carpentersville, Ill., October 3 to protest a proposed ordinance that would criminalize undocumented immigrants, forcing the village board to postpone discussion on the measure.<p>
Over the past 30 years, this outlying Chicago suburb has seen its Latino population increase sharply, and more recently, organized racists like the Minuteman Project have found a foothold in neighboring Elgin.<p>
Trustee Paul Humpfer co-wrote the proposed ordinance, which would prohibit "aiding and abetting of illegals," fine landlords for renting and businesses for employing undocumented immigrants, and make English the official language.<p>
Humpfer claimed to the <i>Chicago Tribune </i>that he has heard nothing but praise for his proposal from Carpentersville residents.<p>
He must not have spoken to the half of the town's 37,000 residents who are Latino. They turned out in huge numbers, mobilized by a grassroots initiative the preceding weekend that involved posting fliers, announcements at churches and more.<p>
On the night of the village meeting, dozens of supporters of the anti-immigrant ordinance, including members of the Minutemen, arrived to cheer on the racist proposal. But they were easily drowned out by those protesting the law.<p>
Even organizers were stunned the turnout. "The message we sent out was that if you don't show up today, you're going to be sorry tomorrow," Juan Silva, of the Mexican Civic and Cultural Organization, based in Elgin, told the <i>Tribune</i>. "And the reaction was good."<p>
Police usually keep opposing sides separated at immigration rallies--like in Batavia, south of Carpentersville, where thousands rallied on Labor Day weekend following a four-day march from Chicago to protest anti-immigrant legislation supported by the area's congressional representative, House Speaker Dennis Hastert.<p>
In Carpentersville, however, police and town officials seemed inexperienced with the political firestorm they had sparked. Latino opponents of the proposal and anti-immigrant supporters mingled throughout the night, with debates between them erupting everywhere.<p>
Patience Hagel, a white ESL teacher, summed it up when Minutemen supporters tried to hand her recruiting literature. "'English-only' is based on racist ideas," she said. "America is a pluralistic society, and it's going to stay that way. 'English-only' really means 'white-only.'" The crowd around her burst out in cheers of "&iexcl;S&iacute;, se puede!" and she joined in.<p>
The anti-immigrant ordinance is modeled after a recent law in Hazleton, Pa. Forty-one local governments nationwide have passed or are considering similar ordinances.<p>
Meanwhile, the Minutemen in the Chicago area are trying to convince local officials to train police to "identify" undocumented immigrants--and to use school records to uncover residents from immigrant families living in overcrowded homes.<p>
The Carpentersville village board is trying to reserve a local high school to hold their next meeting so the measure can be voted on. Some board members are opposed to the proposed law, but it will take more demonstrations like this one to drive the message home about these anti-immigrant ordinances: "&iexcl;Aqu&iacute; estamos, y no nos vamos!" "We are here, and we are not leaving!"<p>

