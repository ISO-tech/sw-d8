<font face="Times New Roman, Times, serif" size="5"><b>What the "Impeach Bush" liberals ignore</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 13, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">THE REPUBLICAN disarray amid the scandal surrounding disgraced Rep. Mark Foley may prove the "tipping point" that pushes Congress the Democrats' way in November.<p>
But until the Foley scandal broke, a Democratic victory wasn't clear. Liberals--the most committed Democratic partisans--faced the prospect of mobilizing a Democratic "base" that hates Bush and the Republicans, but also feels dismay, and at times disgust, with Democratic "leaders" in Congress.<p>
When the Democratic Party wants to campaign on being better able to fight the "war on terror" than the Republicans, what can a liberal antiwar activist do to motivate antiwar voters to the polls? How about demanding the impeachment of Bush, Cheney and the rest of the Bush gang?<p>
To be sure, the list of crimes for which Bush and Co. are responsible is enough to impeach a dozen presidents, not to mention landing them in the dock at the Hague, facing war crimes charges.<p>
But when advocates demand impeachment today, it is because, in the words of David Swanson of Progressive Democrats of America, "never before has the system of government by the U.S. Constitution been as seriously threatened" as it is by the Bush regime.<p>
Really? Can Swanson seriously consider the Bush administration a bigger threat to the republic than, say, the Confederacy, which plunged the country into civil war? Either the answer is yes, in which case Swanson has lost all historical perspective--or the answer is no, in which case the rhetoric about "saving the Constitution" and the rest is simply hyperbole to justify handing Congress over to that band of second-raters, the Democrats.<p>
While there's no doubt that impeachment is a constitutional remedy for abuses of executive authority like lying the country into a war, some advocates want to hold Bush accountable for a list of other right-wing policies.<p>
For example, Dennis Loo and Peter Phillips, authors of <i>Impeach the President</i>, advocate Bush's impeachment because of his denial of global warming and "placing oil-industry profits over the long-term survival of the human race and the viability of the planet." If Bush is to be impeached for this, so should all the members of the U.S. Senate--including every Democrat--who, in 1999, voted unanimously to reject the Kyoto global warming accords.<p>
And while we're impeaching Bush for supporting torture, secret prisons and denial of the right of habeas corpus even to naturalized U.S. citizens, perhaps we might want to add the names of Democratic senatorial candidate Rep. Harold Ford (D-Tenn.), considered a "rising star" in the party, or Sen. Robert Menendez (D-N.J.) to the charge sheet. They, along with 42 of their Democratic colleagues in the House and Senate, voted to legalize these atrocities as the U.S. Congress wrapped up its business last month.<p>
Of course, the liberal advocates of impeachment don't dwell on Democrats' complicity with Bush's impeachable offenses. Their strategy for impeachment depends on the election of a Democratic Congress that will have subpeona power to launch investigations of the Bush administration.<p>
This may be the biggest leap of faith that the pro-impeachment liberals regularly take. They hope that once Democrats run Congress, the same group of bumblers who would not mount a filibuster against the torture bill--for fear of facing Republican attack ads accusing them of being "soft on terrorism"--will now become champions of the people.<p>
They forget that if the Democrats win, they will set their sights on winning the White House in 2008. And their leaders will look on the impeachment of Bush as a "divisive" sideshow that will fire up Republicans and impede their ability to rake in corporate contributions.<p>
Already, mainstream Democrats like prospective House Speaker Nancy Pelosi are running away from impeachment because they don't want to appear to embrace something that so-called "swing voters" might consider too radical.<p>
One can already hear the Beltway pundits warning Democrats not to drag the country through the "trauma of impeachment" and to "look to the future, not to the past." You can be sure that the Democrats will pay more attention to these warnings than the urgings of <i>Nation</i> columnists or the afterdowninstreet.org Web site.<p>

