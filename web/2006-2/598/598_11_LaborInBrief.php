<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">August 25, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Chicago hotel workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adam Norden and Nate Goldbaum</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 2,000 hotel workers and their supporters marched through Chicago's Magnificent Mile shopping district August 9, as the contract deadline loomed for negotiations with local hotels.<p>
UNITE HERE Local 1 represents 7,000 workers at 26 area hotels in the current negotiations. The workers, largely African American and immigrants from Asia, Africa and Latin America, are seeking better pay and a halt to workload increases.<p>
The struggle is a perfect example of the unity of interests between African American and immigrant workers, even as anti-immigrant groups try to pit them against one another.<p>
Workers in red shirts and bandanas gathered in the playlot of Ogden Elementary School, blowing whistles and cheering as each hotel sent its contingent on the mile-long march to Daley Plaza. Bystanders watched and cheered.<p>
Deborah Hill, a union steward at Holiday Inn City Center, told <i>Socialist Worker</i> that room attendants have to clean 16 rooms each day for starting pay of just $9.50 per hour--a lower wage than workers are paid in other major convention cities.<p>
"Room attendants need to be treated like the women they are, not disposable machines," said Local 1 President Henry Tamarin. "They need a chance to grow old and collect a reasonable pension without being crippled by their jobs. They need affordable health care, decent wage increases and humane workloads."<p>
Last year, the hotel industry posted nearly $23 billion in profits in the U.S. In the first half of 2006, revenue per room in Chicago is up 18.2 percent over last year, and the union is confident of winning good contracts.<p>
The Hilton and Hyatt chains, however, have taken a harder line nationally. For that reason, the local has separated negotiations with those two chains from the other negotiations, and workers at the affected hotels will take strike authorization votes August 21 and 22.<p>
The union is getting solidarity from across the Chicago labor spectrum and from community and political organizations as well--support that was well represented on the march.<p>
The last time Chicago hotel contracts were negotiated, almost all area hotel workers won gains in wages, benefits and work conditions. A glaring exception were the workers at the Congress Hotel, where a three-year strike has gone nowhere.<p>
This time, negotiations are starting late, but workers are ready to fight. Said one worker at the W hotel, "Right now I think they are trying to bullcrap us, so we are going to march and let them know we are about business."<p>

