<font face="Times New Roman, Times, serif" size="4">Texas plans to execute a beautiful young man</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Don't let them kill Justin</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 25, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">MY FRIEND Justin Fuller is scheduled to be executed on August 24.<p>
Justin and I have been writing for about eight years and visiting for at least six years. Over the years, we have grown to know one another well. He has been a good friend to me, and I hope I have been to him.<p>
He is a smart, generous and beautiful young man. I'd like to do whatever I can to try to save his life, and I want you all to help me do that.<p>
His case has numerous problems, like most death row cases. The main issues in the case have to do with incompetent or ineffective assistance of counsel.<p>
The two things that stand out in particular are the fact that the prosecution offered Justin a plea before trial and his defense lawyers never told him about it, and that his state habeas lawyer filed a writ for Justin that was actually a writ he had done for a previous client named Henry Dunn. He didn't even change the name to Justin's in parts of the writ, and it contained facts that had to do with Henry's case, not Justin's.<p>
Justin has admitted his involvement in the crime that landed him on death row, but he has always maintained that he was not the triggerman in the murder. Four people took part in a robbery, including Justin, a man named Samhermundre Wideman and a young woman named Elaine Hays.<p>
Justin claims Wideman was the shooter, a claim substantiated by Hays. She has stated that she believes Wideman was the shooter. She says that after Fuller and Wideman returned to the vehicle, where she had been waiting for the two men, Wideman said, "It felt good to shoot someone."<p>
The state of Texas denied Justin's appeal, arguing that the state can still execute "non-triggermen." The case is at the U.S. Supreme Court right now. So far, all the state and federal courts have denied Justin's claims.<p>
The Supreme Court will only decide if it will hear his case when it comes back into session. If they do, Justin will get a stay. If they don't, the case goes back down to the state courts for the 11th-hour appeals. If that happens, Justin's lawyers will file a claim about lethal injection as well, and there could be a stay based on that.<p>
If Justin's execution goes forward, he has asked me to be a witness. This means I would travel to Huntsville, Texas, and will have to watch them put my friend to death--something I cannot even imagine.<p>
When I visited him this week, he told me about how he had to ask his parents to begin to make funeral arrangements. This is a healthy man, only 28 years old. It is so sick, I can hardly stand to think about it.<p>
I am asking everyone to please fax, call and e-mail the governor of Texas and the Board of Pardons and Paroles, asking them to stop this execution. If there isn't a stay, I am asking folks in Austin and Huntsville to participate in a protest against Justin's execution. Please take the time to contact these people. The phone numbers are listed below, as well as an option to send a fax and an e-mail requesting a stay. <br>
<b>Lily Mae Hughes,</b> Campaign to End the Death Penalty, Austin, Texas<p>

