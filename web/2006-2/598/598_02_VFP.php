<font face="Times New Roman, Times, serif" size="4">Camilo Mej&iacute;a reports on the Veterans for Peace convention</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Military resisters speaking out</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 25, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">CAMILO MEJ&Iacute;A was the first U.S. soldier who served in Iraq to go public with his refusal to continue fighting, and he served seven months' confinement for abiding by his conscience. Here, he reports from the Veterans for Peace convention in Seattle.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">WITH THE slogan "Sow Peace, Reap Justice," this year's Veterans for Peace convention highlighted the challenges facing women in the U.S. military, as well as the resistance against the Iraq war within the military's own ranks.<p>
The convention, which took place in Seattle on August 10-13, had an impressive variety of sessions for the more than 500 delegates who attended, including several women's caucuses, workshops and press conferences.<p>
One of the most moving testimonies--during the workshop "Voices of Women Veterans,"<i> </i>moderated by former Army colonel-turned-activist Ann Wright--was that of Colleen Helmstetter, who served in Vietnam as a nurse. It wasn't until the Iraq war that Colleen began to connect her trauma of many decades--nightmares, panic attacks, claustrophobia--to her own traumatic experience in Vietnam. Today, 35 years later, Colleen is receiving treatment for Post Traumatic Stress Disorder.<p>
The workshop also included testimony by Sara Rich, mother of soldier Suzanne Swift, who was sexually harassed and violated by members of her own unit while serving in Iraq. Upon the unit's return to the U.S., Suzanne reported the incidents to her superior officers, but instead of being helped, she was asked to waive her 18 months of stabilization time and was placed on orders to redeploy to Iraq with some of her attackers.<p>
Suzanne refused to return to Iraq and instead took refuge in her mother's house, where she was later detained by local police, locked in a county jail and then transferred to the Fort Lewis, Wash., Army base, where she awaits the military decision on whether she will be criminally charged or not.<p>
According to April Fitzsimmons, a former U.S. Air Force analyst, approximately 45 percent of women in the armed forces reported having been sexually assaulted, 79 percent reported having been sexually harassed, and at least 14 percent reported having been gang-raped by members of their own units.<p>
The convention had several press conferences. One featured Army Sgt. Ricky Clousing, who refused to redeploy to Iraq after witnessing crimes and abuse committed against Iraqi civilians by U.S. troops. During the same press conference, Iraq Veterans Against the War chairwoman Kelly Dougherty read a statement from yet another member of her organization, Mark Wilkerson--who, like Clousing, refused to redeploy to Iraq, condemning the war as both illegal and immoral.<p>
Both resisters could face jail time for resisting orders to redeploy following their surrender to the military.<p>
During a different press conference, peace activist Cindy Sheehan announced her intention to utilize her newly purchased Crawford, Texas, property to create a safe haven for service members resisting deployment orders to Iraq.<p>
One of the main speakers on the convention's closing night was Army Lt. Ehren Watada, the first commissioned officer to publicly disobey orders to deploy to Iraq. Like Clousing and Wilkerson, he considers the war both illegal and immoral. During his inspiring speech, Watada called on the people of the U.S. to support war resisters, pointing out that this is not a task we should leave in the hands of the Canadian people.<p>
The convention ended August 13 with a trip to the Peace Arch Park, on the border in the neutral territory on the border between the U.S. and Canada. The solidarity picnic brought together American war resisters from both sides of the fence.<p>
Kyle Snyder, a U.S. soldier exiled in Canada, expressed his feelings of betrayal during his six months of service in Iraq, where he says he fought normal Iraqis defending their homes. "And then I hear them called terrorists in my home country; it's absolutely ridiculous," he said. "Nobody can tell me that this war is not for profit, or it's not for oil."<p>

