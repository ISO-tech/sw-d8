<p><a name="Stand"></a><font face="Times New Roman, Times, serif" size="4"><b>Standing up to the right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, THE local newspaper in a small Wisconsin town ran the headline "Sensenbrenner Grilled on Immigration, Halliburton, Social Security."<p>
The night before, about a dozen participants in the Midwest Social Forum crashed Rep. James Sensenbrenner's (R-Wis.) town hall meeting, demanding an end to his immigrant bashing. Sensenbrenner is the author of the House Resolution 4437, which, if put into effect, would make it a felony to be--or even just to help--an immigrant without papers.<p>
After Sensenbrenner praised the defeat of slavery, one protester, seeing an opening, asked, "If you acknowledge that laws can be unjust, why do you rest your case against immigrants on their illegal status?"<p>
Sensenbrenner responded with some horse manure about the U.S. being the most gracious country in the history of the world toward immigrants. One protester hollered back, "Tell that to the Japanese in the internment camps, to the Jews who were turned back to Nazi Germany, and to the Irish who 'need not apply'!"<p>
Predictably, Sensenbrenner ended the session by cutting off the only immigrant in the room who tried to speak.<p>
Republicans are staging these town hall meetings to rev up racism against their punching-bag du jour: immigrants. As long as the likes of Sensenbrenner (and the neo-Nazi chorus he leads) are met with resistance, the headlines will continue to reflect the unpopularity of their agenda.<br>
<b>Chris Dols,</b> Madison, Wis.<p>
