<font face="Times New Roman, Times, serif" size="4"><b>Media bias in Seattle shooting</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON JULY 28, Naveed Haq, an American of Pakistani descent, shot six people at the Jewish Federation of Greater Seattle, killing one. Haq is a marginally employed man with 10 years of severe mental problems. He said his purpose in the shooting was to make a statement about "the situation in the Middle East" and to oppose "the Jews and what they are doing."<p>
The local press has presented this as a Muslim vs. Jewish issue. This story has highlighted the double standard of the press in its coverage of the "war on terror."<p>
Since 9/11, thousands of Muslims have been detained by the government and hundreds have been deported. None of the victims of the government witch-hunt were ever connected to 9/11.<p>
On top of that, dozens of attacks by bigots have taken place. Mosques have been vandalized and Muslims, and people who "look like" Muslims have been beaten and murdered by racists, encouraged by the government campaign. Yet very little of that has made it into the daily press or on TV.<p>
When "U.S. citizens" are victims of senseless violence, as in the Jewish Federation shooting, politicians and media pundits dwell on it. When victims of the war on terror seen as the "enemy" are attacked and persecuted, public officials pay little attention or openly justify it. The government whips up support for wars abroad by demonizing and scapegoating "enemies" at home.<p>
The approach of the press implies that all Muslims are somehow responsible for this irrational act or that opposing Israel encourages anti-Semitism. Lost in all this is one simple fact: Haq is not a Muslim! Naveed converted to Christianity. This fact was only reported once in numerous articles. The Christian community has not been blamed for this action in the press.<p>
This tragic shooting comes from many things--the alienation of people from each other in U.S. society, the priority the U.S. puts on war rather than on mental health care for people, and the insecurity people feel when they cannot find decent employment. Yet the press has focused on only one "cause"--supposed Muslim anti-Semitism.<p>
This shooting is in part the responsibility of George Bush and the Israeli government. The daily slaughter of Palestinians and Lebanese by Israeli military action was one factor that pushed Naveed over the edge. His attack on a Jewish institution was an irrational, tragic and misplaced attempt to strike back at the Israeli government. The innocent victims in the Jewish Federation office are six more victims of an Israeli-U.S. war in the Middle East.<p>
This shooting is further proof of the need to change the priorities of this system--mental health care, not warfare!<br>
<b>Steve Leigh,</b> Seattle<p>
