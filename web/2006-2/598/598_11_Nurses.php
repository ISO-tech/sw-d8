<font face="Times New Roman, Times, serif" size="5"><b>Nurses challenge NLRB ruling</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 25, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Close to 300 registered nurses (RNs) from across the U.S. rallied here August 8 against a ruling set to take effect this month by the National Labor Relations Board (NLRB) which poses a major threat to the democratic rights of registered nurses--and about 8 million other workers--to be in or form unions.<p>
This national demonstration, called by the National Nurses Organizing Committee/California Nurses Association, brought nurses unions and organizations across the U.S. together including Service Employees International Union,<i> </i>Massachusetts Nurses Association, Physicians for a National Health Program, Communications Workers of America District 4, and the Pennsylvania Association of Staff Nurses and Allied Professionals.<p>
Chanting "People, not profits!," they rallied in Daley Plaza and then marched to the American Hospital Association headquarters, where some nurses sat down, taking the street.<p>
Known as "Kentucky River," the case is one of the most significant rulings for the NLRB in years. At the request of the hospital and nursing home industry employers, the NLRB, which is now stacked with anti-union board members appointed by the Bush Administration, is poised to rule that thousands of RNs are "supervisors" because they make clinical assignments to Licensed Practical Nurses (LPNs) and other health care staff. Under federal law, labor supervisors are not eligible to join unions.<p>
A pro-management ruling by the NLRB might apply only to lead RNs, known as "charge" nurses, but the most anti-union employers want the restriction to apply to all RNs. As Marcia Straw, an RN in Florida, told <i>Socialist Worker</i>, "I'm proud to be a union nurse, and I want to stay a union nurse. If I'm a 'charge,' I don't want to be excluded from my union."<p>
If the NLRB rules in favor of employers, nurses who are already in unions could face attempts by their employers to revoke their union representation and their contracts, as well as increase their patient-to-nurse ratios. Sharnel Smith, another RN at the Chicago rally, said, "Every nurse needs to be unionized. It's the only way to keep our patients safe."<p>
In California, thousands of nurses have signed a pledge that they will "take all actions necessary, up to and including striking" if their employer moves to deny them the right to CNA union representation.<p>
We need to show support for the nurses and all workers who would be affected by this potential ruling.<p>
