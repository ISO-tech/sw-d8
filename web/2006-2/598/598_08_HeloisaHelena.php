<font face="Times New Roman, Times, serif" size="5"><b>Socialists support Heloisa Helena</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 25, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">IN 2003, the Brazilian Workers Party, the PT, ousted three national deputies and one senator--Jo&atilde;o Batista de Ara&uacute;jo, Lindberg F&aacute;ria, Luciana Genro and Heloisa Helena--for their opposition to the economic policies of President Luiz In&aacute;cio Lula da Silva.<p>
Today, Helena is the presidential candidate of the Party of Socialism and Liberty (PSOL). Below is a petition in support of Helena, signed by socialists and activists from around the world.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">MANY OF us signed, two years ago, a protest against the exclusion of Heloisa Helena from the PT, the Brazilian Workers Party. Today, Heloisa has become the presidential candidate of the new Party of Socialism and Liberty (PSOL), founded by several bureaucratically excluded or dissident members of the PT.<p>
While Lula's government followed a typical social-liberal course, disappointing millions of people who voted for him in the hope of a radical social and political change, and people all over the world who expected from Brazil a new impulse for anti-imperialist struggle, Heloisa Helena and her comrades remained faithful to the original anti-imperialist and socialist program of the PT.<p>
She is today the only candidate in the Brazilian elections who raises the historical banners of the Brazilian labor movement, of the peasants, the poor and the oppressed: a radical agrarian reform; suspension of the payment of the foreign debt; rejection of the Free Trade Area of the Americas (ALCA); a substantial reduction of working hours without loss of pay; a moratorium on the use of genetically modified organisms (such as Monsanto's Terminator seeds); and support for ALBA, the Bolivarian Alliance of the Americas (Venezuela, Bolivia and Cuba).<p>
The elections in Brazil are of concern for socialists everywhere in the world. In solidarity with the poor and the exploited Brazilian masses, we support the socialist candidate in the next Brazilian presidential elections, Heloisa Helena.<p>
