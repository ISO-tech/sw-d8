<font face="Times New Roman, Times, serif" size="5"><b>Taking on the Nazis in Madison</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Dols</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 25, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTIRACISTS are organizing to counter a planned Nazi demonstration in Madison, Wis., on August 26.<p>
The Nazi rally, organized by the National Socialist Movement (NSM), is scheduled to begin in front of the state capitol building at 2 p.m.<p>
The racists' opponents will be assembling an hour earlier at the State Street corner of the building. "And we'll stay there with our signs, chanting," said Jesse Zarley, a member of the No Nazis in Madison Coalition, "until these hateful scum scram."<p>
The Independent Riders, a motorcycle club, is mobilizing its members for the counterprotest and putting out the call to other clubs to join in, too. Shakti, a downtown gift shop, is closing its doors so employees can attend the counterdemonstration. Workers at the University of Wisconsin student gym are planning to show up, too. Three vanloads are coming from Winona, Minn., and dozens in Milwaukee and Chicago are part of the organizing efforts to mobilize from their cities as well.<p>
The debate among community leaders and activists--"counter them" vs. "ignore them"--has raged in the local press since the news broke that the weekly farmers' market would be shut down early to make space for the Nazis and their police protectors.<p>
Peter Mu&ntilde;oz of Centro Hispano urged activists to "just let the Nazis show their pathetic selves, and hopefully, they will leave town as soon as possible," according to the Madison <i>Capital Times. </i>But community activist Alphonso Zepeda-Capistran made the case for confronting the racists, maintaining that a counterprotest must show that "we welcome the immigrant community [that the Nazis] are attacking."<p>
According to the Southern Poverty Law Center, the NSM is the largest of the neo-Nazi formations in the United States and it--and the far right more generally--has been growing in recent years. Fifty members rallied in Indiana in 2003, 100 in Pennsylvania in 2004, and 150 in Virginia last year.<p>
Ignoring them because they aren't a larger force in politics misses the fact that the far right is growing--especially because of the anti-immigrant climate created by politicians and their parrots in the media. With greater economic hardship, the ground will become even more fertile for these forces to grow.<p>
When George Bush and Congress cast blame on immigrants for the state of our economy, white supremacists feel vindicated, and their confidence to commit hate-crimes grows.<p>
As Jesse Zarley put it, "Knowing that these groups are tagging along with the mainstream immigrant-bashing--which doesn't appear to be going away any time soon--countering the Nazis becomes an even greater imperative."<p>

