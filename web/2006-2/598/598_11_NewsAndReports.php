<font face="Times New Roman, Times, serif" size="4"><b>Protest the anti-immigrant vigilantes</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mark Clinton and Brian Chidester</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HARTFORD, Conn.--At least 100 activists protested the appearance of the racist Paul Revere Riders August 8 and prevented them from polluting the air with their immigrant-bashing hate speech.<p>
The Riders, whose Web site praises the Minutemen vigilantes as American patriots, are wrapping up their countrywide tour demanding a wall to seal off a militarized border to protect the U.S. from an "invasion" of Latinos bent on "colonizing" the country.<p>
But militant opposition to this hate was on display throughout the day in Hartford. When three bigots carrying signs demanding no amnesty for immigrants appeared shortly before the pro-immigrant rights rally began, we quickly confronted them with loud chants.<p>
Speakers at the rally spelled out some of the most important implications of the protest's demands.<p>
"It's not true we are here to take jobs from Americans," said Gabriel Acosta of SEIU Local 32BJ, noting that he had been working as a janitor in the U.S. for a decade. "We're here and we're not leaving. We're immigrants, not criminals."<p>
The protesters marched to the state capitol building, where four Paul Revere Riders and a dozen or so supporters were gathered. The police were deployed to protect the Riders and their pathetic supporters, but they couldn't stop the message of the counterdemonstrators.<p>
For the hour that the Minuteklan rallied, their hate speech was drowned out with loud and spirited chants.<p>
After the Riders left, Peter Gosselin of the Connecticut chapter of the National Lawyers Guild spelled out the lesson of the protest. "We came out here today to prevent the Minutemen from having their say," Gosselin said. "We did that. We shut them down."<p>
-- Meanwhile, in <b>Providence, R.I.</b>, some 40 people turned out to protest the Paul Revere Riders when they brought their message of hate to the state house August 8.<p>
The spirited counter-protest, larger than expected, brought together a broad range of immigrant rights activists in a concerted effort to disrupt the hate rally. The racists turned out about 20 people, with only a minority from Rhode Island apparently.<p>
The counter-rally began by marching right up to vigilantes, chanting and shouting in their faces, and covering up their banners with ours. Later, police moved our rally away from the racists--and told us not to swear, as that would be disorderly conduct.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Stop Chicago police violence</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rachel Cohen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The mention of this city's North Side Cabrini-Green housing project recalls racial tension, poverty and the city's failing public housing system. So when 14-year-old Cabrini Green resident Ellis Woodland was chased down by Chicago police and shot August 7, the community responded with days of protest, charged with all the anger that has been boiling beneath the surface for years.<p>
According to police, Woodland fit the description of one of two "Black male teens" suspected of attempted robbery. They say that when officers approached him, he ignored orders to stop.<p>
But moments later, as Woodland was apparently trying to place the BB gun he was carrying on the ground, police fired five bullets into his abdomen and thigh. Woodland was seriously injured and remained hospitalized in critical condition two weeks after the shooting.<p>
The largest protest took place August 10, when hundreds of family members, neighbors and activists marched the mile-and-a-half to Chicago's City Hall.<p>
Protesters remembered the seventh grader who arrived early to school every day to help his teacher up the stairs and into her classroom. Activists also say they have evidence that police harassed Woodland and his family several days before the shooting.<p>
But city officials refused to allow the crowd to enter City Hall and have their voices heard.<p>
The demonstrations over the last week have coincided with a broader anti-police brutality movement against decades of torture against African American suspects carried out by Chicago police under the tenure of the notorious former Commander Jon Burge. Forced to retire more than a decade ago, Burge continues to collect a generous city pension and lives on his houseboat off the coast of Florida.<p>
The protests after Woodland's shooting are part of the struggle to demand justice in Chicago.<p>

