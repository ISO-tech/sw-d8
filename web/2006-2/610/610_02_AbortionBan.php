<font face="Times New Roman, Times, serif" size="5"><b>Supreme Court to rule on abortion ban</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A case that could have wide-ranging consequences for a woman's right to choose abortion, the U.S. Supreme Court heard arguments last week on the constitutionality of a federal ban on a late-term abortion procedure.<p>
The law, often referred to as a ban on "partial-birth abortion," was passed in 2003, but immediately challenged in court because it didn't include exceptions for a woman's health.<p>
The procedure, which is actually called intact dilation and extraction, is used in less than 1 percent of abortions--almost always when a woman's life or health is in danger, or when a fetus suffers from severe developmental abnormalities. But anti-choice politicians and activists have used it as a wedge to chip away at the right to choose more broadly.<p>
In 2000, the U.S. Supreme Court struck down a Nebraska statute by a narrow 5-4 majority because it didn't include an exception for the woman's health and because the language used to define a "partial-birth abortion" was so broad that it potentially could have outlawed a range of procedures, including the most common form of second-trimester abortions.<p>
Nonetheless, Congress passed a federal ban virtually identical to the Nebraska law in 2003--with overwhelming support from both Republicans and Democrats--and Bush signed it into law.<p>
Supporters of the federal ban justified it by claiming that women never need dilation and extraction for the sake of their health. But that's simply not the case. During testimony over the legality of the ban, even a doctor who supports it said that "he had used and would use the procedure in some circumstances," according to National Public Radio.<p>
In 2004, three district court judges declared the ban unconstitutional. Now, lawyers for the federal government are appealing to the U.S. Supreme Court to keep the law in place.<p>
With two recently appointed conservative justices, they have a good shot. According to the <i>New York Times</i>, during arguments last week, Chief Justice John Roberts "appeared to be trying to bolster the defense of the statute by the solicitor general. At other times, the chief justice appeared eager to find differences between the federal law and the Nebraska law. Differences in the way the state and federal laws defined the procedure could be the basis for a decision that upholds the federal law without disavowing a recent precedent."<p>
If the law is upheld, it will likely have a chilling effect on a woman's right to choose. In 1998, when a similar law was passed in Wisconsin, every clinic in the state shut down for two days because the wording was so vague as to potentially criminalize all abortion procedures.<p>
Supporters of abortion rights should take hope--and a lesson--from the recent defeat of a sweeping anti-abortion law in South Dakota. There, activists mobilized for months to get the law repealed, collecting tens of thousands of signatures and even discussing the possible creation of an abortion clinic on Native American land not under federal or state jurisdiction in order to get around the law.<p>
With the Democrats firmly on the side of the ban on late-term abortions and a crop of newly elected anti-choice Democrats--including Pennsylvania's Sen. Bob Casey, and North Carolina's Rep. Heath Shuler--in Congress, we can't wait for any politician to fight for abortion rights.<p>

