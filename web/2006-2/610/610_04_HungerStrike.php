<font face="Times New Roman, Times, serif" size="3">When first asked by the <i>Houston Chronicle </i>to comment on the hunger strike, the Texas Department of Criminal Justice (TDCJ) denied that it was happening.<p>
Later, in response to an inquiry by the <i>New York Times</i>, a TDCJ spokesperson said that because inmates have filed no "formal" grievances, there will be no "formal" response from the TDCJ. This is ironic, as one of the issues addressed by protesting death row prisoners is that grievances that they file are either ignored or simply not processed.<p>
The TDCJ defended its denial of television access by claiming installation would be too expensive--but it was then forced to admit it had no research about what the costs would be. General population prisoners at Polunsky and women on death row (who are housed in a different unit) have access to television.<p>
The hunger strikers' demands include better meals, cell maintenance, adequate health care and proper hygienic and laundry necessities. They are also calling for a halt to excessive punitive measures used against death row prisoners, especially those making protests.<p>
Activists held a small protest outside Polunsky on November 4 to show solidarity with the hunger strike and other prisoners organizing on the inside. The demonstration grew as it was joined by families visiting loved ones inside the prison.<p>
The hunger strike may soon end, but resistance on death row continues. Members of the group DRIVE (Death Row Inner-Communalist Vanguard Engagement)--five death row prisoners who engage in nonviolent, direct-action protests--are planning further demonstrations. Prisoners also initiated protests on October 28 to show their solidarity with the annual Texas March to Stop Executions.<p>


