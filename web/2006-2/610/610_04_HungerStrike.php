<font face="Times New Roman, Times, serif" size="5"><b>Suffering on Texas' death row</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kelly Booker</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 17, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">A HUNGER strike has brought national attention to the cruel and unusual conditions faced by almost 400 death row prisoners housed in Texas' notorious Polunsky Unit.<p>
From information available as <i>Socialist Worker </i>went to press, the hunger strike--after reaching a high point of nine participants--is down to only one man, Steven Woods. Woods has now refused food for over three weeks.<p>
Mainstream media stories have given a glimpse of the conditions that the prisoners are protesting--23-hour confinement in 60-square-foot cells, and the elimination of group recreation, work programs, television access and religious services. No contact visits are allowed, and prisoners are only permitted one five-minute phone call every six months. Their mail is often censored, the quality of food is low, and health and dental services are inadequate.<p>

