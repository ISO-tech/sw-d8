<font face="Times New Roman, Times, serif" size="4">Alan Newton on his 22 years of wrongful imprisonment</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Half a lifetime stolen</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 17, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ALAN NEWTON was convicted of rape, robbery and assault in New York in 1985. This past July, almost 22 years after his wrongful conviction, he walked free, exonerated for a crime he spent half his life in prison for.<p>
Alan is one of six prisoners to have been exonerated through DNA testing in New York state in the past 10 months alone. The latest, Scott Fappiano, was freed in early October.<p>
"Scott Fappiano's case is the starkest yet in a long line of New York cases where innocent people were convicted based on eyewitness misidentification," Innocence Project lawyer Nina Morrison told reporters. "In case after case, we have proven that faulty eyewitness identification procedures in New York lead to wrongful convictions.<p>
Nobody can look at these cases and say there isn't a serious problem-- yet New York still hasn't taken problems with eyewitness identification seriously and implemented reforms. New Yorkers have to wonder how many innocent people are sitting in prison because the NYPD can't find evidence that could be subjected to DNA testing."<p>
In the face of the evidence that New York's criminal "justice" system is broken, lawmakers were compelled to hold hearings on standardizing crime scene evidence for prisoners to clear their names. Following the hearings, Alan spoke to <i>Socialist Worker's </i>DYLAN STILLWOOD, VANESSA McENERY and LEE WENGRAF about his case and the fight against wrongful convictions.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>CAN YOU talk about your experiences at the hands of the criminal justice system?</b><p>

I WAS convicted in 1985, and I started trying to get DNA testing done on the evidence from the crime scene in 1988. But I was told the samples had deteriorated. Then, in 1994, New York State passed a new law saying anyone requesting DNA testing could petition the court, and the state will pay for the testing. I was one of the first to put in a request.<p>
I started receiving inconsistent answers from the police about the status of the rape kit--that it couldn't be found, that the original voucher was destroyed in a fire. I didn't have a lawyer then--I did it on my own. In 1998, they found the victim's clothing. But the rape kit was still not found, and I was given yet another excuse.<p>
Around 2004, my family and I got in contact with the Innocence Project, and my lawyer asked the Bronx DA's office to try and find the kit. They wrote a letter to the police prop clerk office and said, "Let's bring this case to a close for once and for all."<p>
The kit was finally found in December 2005, in the same storage bin where it was placed originally, right where it was supposed to be. I was finally released July 2006.<p>
When I went to trial, you really saw how you're forced to put fate in someone else's hands. My lawyer didn't ask for input; they were just guessing. The jails are so full because of ineffective assistance of counsel.<p>
Look at the difference between a typical sentence and what you get if you plea bargain: trials are punishment for people asserting innocence. And the innocent plead guilty if you don't have money or representation, whether you did the crime or not. In my case, no one wants to plead guilty to a crime like that.<p>
The criminal justice system is supposed to be fair and unbiased. Unfortunately, unless you have a high-priced lawyer, you fall into their trap.<p>

<b>WHY DO you think the courts are so biased?</b><p>

WITH THE laws, it's all about how they're enforced. In certain communities, the laws are enforced one way. In a predominantly white community, police are not going to put through sweeps in the same kind of way they do in the Black community. Laws are enforced more harshly in the Black community.<p>
And they're designed in a biased way. Look at the crack-cocaine sentencing, where you need 500 times the amount of cocaine for the same sentence you get for crack. With these kinds of laws, the powers-that-be know they've discriminated against the Black community.<p>
So reforms are difficult. We need to deal with the politicians--hold them accountable for promises they make.<p>
Any time you're dealing with a system that incarcerates people wrongly for 22 years, reform is definitely needed. You're not talking about one or two years--you're talking about 20 years.<p>
That's where you get into the political side. I guess anything is possible, but are people willing to make change? You can pass all kinds of laws, but will those laws be followed? Even though they're on the books, the laws don't always get followed.<p>

<b>WHAT DO you think you and other people can do to change the system?</b><p>

ME BY myself, I'm nothing. I'm a pebble in a pond. But you see other exonerees lining up behind me, and that's where we can make change. One man by himself can't make a difference.<p>
The police department is really resistant to change. Someone [at the hearings] even asked the police commissioner about using computers [to keep track of DNA evidence], and he said a lot of departments didn't like that. I mean, we're in 2006, and you have evidence left in bottles from the 1920s. How can they be so resistant to change?<p>
The police department is a culture--it's passed down through the generations. They want to keep doing business the way they're used to, so it's really resistant to change. Why change when you don't have to?<p>
Keep doing what you're doing--lobbying, demonstrating, protesting. I know [the Campaign to End the Death Penalty] helped get the death penalty ruled unconstitutional in New York. You just have to keep pushing. It's a long road, the fight is hard, but you have to keep pushing.<p>

