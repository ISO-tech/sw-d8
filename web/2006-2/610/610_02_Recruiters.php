<font face="Times New Roman, Times, serif" size="4">A new scandal for the Pentagon</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Recruiters' lies caught on video</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 17, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">EARLIER THIS year, the U.S. military announced that, after months of shortfalls, it had met its recruiting goals for the 2005-2006 fiscal year.<p>
A recent ABC News investigation found out one way the military is able to attract new recruits, despite growing casualties in Iraq and Afghanistan. Lie to them.<p>
WABC television sent several students, equipped with hidden video cameras, into 10 Army recruitment offices in New York, New Jersey and Connecticut. The videos showed a pattern of lies aimed at lessening the students' fears about joining the military in a time of war.<p>
In one case, a student asked if anyone was "going over to Iraq anymore," and was told by a recruiter, "No, we're bringing people back." Another recruiter chimed in, saying, "We're not at war. War ended a long time ago." Still another recruiter told a different student that the chances of going to war "would be slim to none."<p>
In all, according to reporter Jim Hoffer, nearly half of the recruiters suggested to students that they had as great a chance of being killed at home as they would in Iraq.<p>
Other recruiters tried to downplay the seriousness of joining the military, with one telling a student that if he changed his mind about being in the Army, he could simply quit. "It's called a 'Failure to Adapt' discharge," the recruiter said. "It's an entry-level discharge so it won't affect anything on your record. It'll just be like it never happened." Though a Failure to Adapt discharge does exist, it's extremely difficult to obtain--particularly during wartime.<p>
Recruiters were also caught coaching potential recruits on how to avoid failing the Army's drug test. "It usually takes between 20 to 30 days for marijuana to get out your system. All right?" one recruiter told a student, "So [you've] just got to not smoke any more and drink a lot of water." Other recruiters told students that they could take the drug test multiple times--as many as it would take to pass.<p>
In May 2005, in the wake of numerous similar reports of inappropriate behavior and lies to potential recruits, recruitment was halted for a national day of "training." It seems the lessons didn't stick.<p>
When shown the footage of recruiters lying to potential recruits, however, Col. Robert Manning, head of U.S. Army Recruiting for the Northeast, told Hoffer that he found "it hard to believe some of the things that they're telling prospective applicants." But, he added, "I still believe that this is the exception more than the norm."<p>
"What are you saying, then," Hoffer asked, "that we just got wildly lucky to find recruiters, more than half out of 10 that we visited, to be stretching the truth, or even worse, lying?" Manning didn't have an answer to the question.<p>

