<font face="Times New Roman, Times, serif" size="5"><b>Janitors raise the stakes</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 17, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">CHEVRON TEXACO gas stations are the focus of a national day of action November 15 in support of striking janitors in Houston.<p>
The 1,700 workers--who earn little more than minimum wage and nearly all of whom work only part time--are on strike against five major national cleaning companies. But their wages and conditions are dictated by five major landlords in Houston--including Chevron Texaco, which hauled in $14 billion last year.<p>
For that reason, the strikers' union, the Service Employees International Union (SEIU), has called on supporters to hand out leaflets at Chevron and Texaco gas stations in 20 cities in order to highlight the workers' situation and pressure the company.<p>
Similar tactics have been used to organize janitors in Los Angeles and other cities when landlords pressured cleaning companies to settle. This time, SEIU is also taking picket lines to cities across the U.S., calling on the national cleaning companies--ABM, OneSource, GCA, Sanitors and Pritchard--to agree to give Houston janitors a wage increase to $8.50 per hour as well as health care coverage and full-time jobs.<p>
The November 15 action will highlight the struggle of striker Ercilia Sandoval, who cleaned Chevron offices in Houston, but who is now dying of cancer without any medical benefits.<p>
Meanwhile, the union has continued to put pressure on the landlords, including a sit-in by five union members and activists November 9 at Transwestern Commercial Services, one of the main commercial property mangers in Houston. About 100 janitors picketed outside, and union officials say they will bring more union activist janitors from around the U.S. to support the strike.<p>
The strike is the first test of SEIU's strength in Houston, where the union organized 5,000 janitors last fall. Management is playing hardball. At least 14 janitors have been fired for striking, and the union has filed 35 unfair labor practice charges with the National Labor Relations Board.<p>
"All I ever wanted was to have a better life and to be able to afford the basic things all families need," striker Mateo Portillo said. "But I was fired for speaking out for a better future for myself and for other hard-working families in our city."<p>
Strikers' morale has been boosted by widespread support nationally and in Houston, where a network of food banks is being operated in union halls, churches and campuses.<p>
