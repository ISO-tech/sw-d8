<font face="Times New Roman, Times, serif" size="4">The facts about Democrats and the Vietnam War</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cutting off funds for war?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 17, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">"THE VIETNAM War ended when Congress cut its funding." I first heard this claim about a year ago and must have heard it about a dozen times since in antiwar circles. I imagine that each time somebody says it, a few more activists pick it up. It's an urban legend in the making.<p>
Before it becomes as established as some of the right-wing Vietnam myths ("antiwar protesters spit on returning soldiers," etc.), let's review a quick timeline to set the record straight:<p>

<blockquote>
<b>1965: </b>Lyndon Johnson begins the invasion of South Vietnam.<br>
<b>1968: </b>The Tet Offensive convinces most analysts that the war is unwinnable; Students for a Democratic Society claims 100,000 members.<br>
<b>1969: </b>Nixon begins to withdraw ground troops while escalating the air war.<br>
<b>1970: </b>The U.S. invades Cambodia, antiwar protests reach their peak; a majority of the U.S. population turns firmly against the war.<br>
<b>1971: </b>The <i>New York Times</i> publishes the Pentagon Papers; Vietnam Veterans Against the War (VVAW) hold the "Winter Soldier" hearings in Detroit and a series of dramatic protests in the capital.<br>
<b>1973: </b>Nixon signs the Paris Peace Accords on January 27.
</blockquote><p>

The only serious congressional challenge to the war during this time was a 1971 bill extending the draft, which contained an amendment calling for withdrawal within nine months. However, once the bill was approved and the draft was safely extended, the crucial words "nine months" were changed to "earliest practical date."<p>
Thus, here's a scorecard for Congress and the Vietnam War:<p>

<blockquote>
<b>Length of war: </b>eight years.<br>
<b>Vietnamese deaths: </b>2 million (estimated).<br>
<b>U.S. deaths: </b>65,000.<br>
<b>Military dollars cut by Congress: </b>Zero.
</blockquote><p>

After Nixon signed the peace treaty, Congress, to its credit, finally took some decisive measures. Funding was finally cut in May 1973 for the lunatic bombing of Cambodia, and a few months later came the War Power Act, which requires the president to seek congressional approval for war within 90 days of deploying troops.<p>
It seems to me, however, that this flurry of legislation only exposes how little Congress did during the actual war. Not that this should be surprising. Contrary to all of our junior high school lessons about the Constitution, there are no checks and balances during wartime. Through all of American history, that 1971 amendment was the only time Congress passed legislation to end a war in opposition to the president.<p>
If you're hoping a new Democratic majority will break that tradition, don't waste your breath. An article in the <i>New York Times</i> before the election reported that many congressional Democrats claim that they do not have the power to end the Iraq war. "The people understand that we're not the Commander-in-Chief" was how New York Sen. Chuck Schumer explained it, neatly dodging the question of why he has voted for that Commander-in-Chief's wars at every opportunity.<p>
History dispels the myth that Congress ended the Vietnam War, but it doesn't explain why this myth is spreading at this particular moment. In <i>Spitting Image</i>, an excellent book about the myth of soldier, students, and saliva, Jerry Lemke argues that this right wing fable arose to help pro-war veterans who wanted to believe that they lost because they were betrayed by the people they were fighting for.<p>
I think that this new Vietnam myth serves a similar purpose for liberals. It helps people who oppose the Iraq occupation, but don't want to engage in the often messy struggles it will take to end it. They want to believe in an orderly world where wars can be stopped by calling your local congressman.<p>
Unfortunately, history has shown that wars only end when one side loses. In 1968, it became apparent that North Vietnam wasn't going to lose. Millions of students and soldiers joined the movement for a U.S. withdrawal.<p>
Their struggle wasn't easy. The actual story of how the Vietnam War was ended involves not Congress, but courageous Vietnamese fighters living and dying; millions of students standing up to campus administrators, police and the National Guard; and GIs resisting the war through drugs, open defiance and sometimes even killing their officers.<p>
Congress has never wanted to lose a war; it has the same overall goals of U.S. dominance as any president. That's why it's so paralyzed today in the face of another war that we are so obviously on the way to losing.<p>
Our movement needs to study the real lessons of Vietnam and revive those grassroots strategies that can provide support and inspiration to soldiers--American and Iraqi--who are already fighting to force the U.S. out of another disastrous war.<br>
<b>Danny Katch</b>, New York City<p>

