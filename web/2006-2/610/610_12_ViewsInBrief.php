<p><a name="Oaxaca"></a><font face="Times New Roman, Times, serif" size="4"><b>Power in Oaxaca</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE context of distorted and misleading corporate media accounts of the uprising in Oaxaca, your sympathetic coverage has been valuable. But the significance of the revolt goes beyond Mexico's post-election polarization or even the demand to oust the state's repressive dinosaur governor, Ulises Ruiz Ortiz.<p>
Precisely to engage with these conflicts, Oaxaca's toilers have improvised a form of democracy too profound to accommodate (or be tolerated by) capitalism. Constituted by unions, peasant groups, indigenous communities and others, the People's Assembly of Oaxaca (APPO) directly represents the will of the majority in the capitol, and possibly in the state as a whole.<p>
Because of this massive, self-mobilizing base, APPO, which was formed out of the consensus that Ruiz must go, has found itself the de facto power in Oaxaca. It has made the state's official bureaucratic and police apparatus ineffective, which is why the federal government has had to invade in the (so far fiercely resisted) effort to restore capitalist order.<p>
The Mexican paper <i>La Jornada</i> has rightly termed the revolution the "Oaxaca Commune," a reference to the 1871 Paris Commune, which Marx considered the first living example of working-class rule. As in 1871 Paris, or 1917 Petrograd, control by leaders from below is flourishing, illustrated by the teachers union president's failure to end his members' strike, in a vote organized in spite of him.<p>
APPO has seized control of government buildings and even begun to run the means of ideological production, in the form of the radio station. Remote Zaptec villages, which have struggled for centuries to maintain their own governing structures autonomous from the Mexican state, have joined APPO in the belief that it represents their empowerment, rather than their subordination.<p>
Socialists need to stand in active solidarity with Oaxaca's workers, but we should also use Oaxaca to highlight the workings of a real 21st century socialist revolution. Viva APPO! Viva Oaxaca!<br>
<b>Avery Wear</b>, San Diego<p>

