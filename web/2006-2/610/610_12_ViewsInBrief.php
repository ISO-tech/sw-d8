<p><a name="Kaiser"></a><font face="Times New Roman, Times, serif" size="4"><b>Poor treatment by Kaiser</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS an employee for Kaiser Permanente for nearly six years, until recently terminated on June 27, 2006. I had the union steward file a grievance on my behalf soon after.<p>
I was told by my union representative that I would be coming back to work with six weeks' of back pay, a two-week suspension and a new location, which would be the records facility in Livermore, Calif. That did not turn out to be the case.<p>
I have been lied to and am being told differently now, and am actually being pressured into taking my job back in the same previous department--Central Stores, also in Livermore--where I had already had conflict with my manager over harassment and discrimination trailing back to March of this year.<p>
I feel that my termination was an act of retaliation on my manager's behalf. I was terminated based on a misunderstanding and for being a "no-show" at a training that nine other employees missed as well--yet none of them had a disciplinary action taken against them. If anything, they were actually offered make-up dates.<p>
At this point, I am just trying to seek any kind of help I can get and let it be known that the management has failed to treat me equally.<br>
<b>Jose Corona,</b> from the Internet<p>

