<font face="Times New Roman, Times, serif" size="4"><b>Confronting Zionism at UMass</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON NOVEMBER 1, the Student Alliance for Israel invited Yaakov Katz, the defense correspondent for the <i>Jerusalem Post</i>, to speak on the University of Massachusetts-Amherst campus. Katz spoke mainly about his experiences while embedded with the Israel Defense Force (IDF) during the recent Israeli bombardment of Lebanon.<p>
His speech consisted of a pro-Zionist argument of the need for Israel to take a hard line against Hezbollah, Hamas, Syria and Iran, peppered with anecdotes that revealed his lack of concern for the Lebanese and Palestinians killed, injured or rendered homeless by IDF forces. For example, he expressed sympathy for an IDF soldier who was unable to speak to his wife on his cell phone due to the noise coming from the artillery cannons that were shelling civilians in Lebanon, while making no mention of the fate of those on the other side of the shells.<p>
Members of the International Socialist Organization from the Northampton branch plus individuals from the Muslim and Lebanese Students Associations attended the talk.<p>
We corrected Katz at several points during his talk (when he referred to Hamas as "terrorists," for example), and stumped him during the question-answer period when one person asked why it is unacceptable for Iran to fund Hezbollah while the United States funds Israel to the tune of billions of dollars per year.<p>
Another person strongly condemned Israel's barbaric destruction of Lebanon's civilian infrastructure and brutal oppression of the Palestinian people before challenging the Zionists to a debate, an offer that was not accepted.<p>
Hate crimes against Arabs and Muslims such as the case of Alia Ansari, the Muslim woman in Fremont, Calif., who was shot in the head while holding her daughter's hand, are tragic reminders of the importance of confronting bigots like Katz and other Zionist cheerleaders wherever they appear.<br>
<b>Gary Lapon</b>, Northampton, Mass.<p>

