<font face="Times New Roman, Times, serif" size="4">Sara Rich on her daughter's struggle</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Abused by the military she served</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 17, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">SARA RICH is the mother of Suzanne Swift, an Army specialist who is under arrest for desertion after refusing to be redeployed to Iraq in January 2006. Swift says that she was the victim of repeated sexual assaults while stationed in Iraq.<p>
Suzanne's mother Sara has been leading the effort to win justice for her daughter. In August, she and others began a "Camp Suzanne" outside Fort Lewis in Washington to show their support. Sara talked to CHANAN SUAREZ-DIAZ and DARRIN HOOP about her daughter's struggle.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT'S THE current status of your daughter's case?</b><p>

RIGHT NOW, the investigation is complete. They've charged her with two things: missing a movement and going AWOL. The investigation took three days. They interviewed Suzanne for one hour. In the initial investigation, they didn't interview the two main perpetrators in Iraq: the platoon sergeant and the squad leader.<p>
Then a general sent it back, saying it wasn't thorough enough. So they had to go back and interview those two sergeants. Both of the sergeants denied that they had anything to do with Suzanne.<p>
Now Suzanne's up at Fort Lewis in active duty, working as a military police officer filing papers. Her court-martial date is set tentatively for January 7, 2007.<p>

<b>WHAT HAS been the response to her case in the media or the antiwar movement?</b><p>

WHEN SHE was first arrested, we had a lot of support from alternative media. Amy Goodman and <i>Democracy Now!</i> have been champions. She's been amazing with this case and has been following it very closely.<p>
The mainstream media at first kind of vilified her and made it look like she should have gone back. They used her mug shot from when she was arrested in her pajamas on a Sunday night. Since then, the mainstream media has been varied. We had a good interview with ABC News and FOX News, surprisingly--and with three major newspapers, the <i>San Francisco Chronicle, Washington Post </i>and <i>New York Times</i>. They've all been very helpful.<p>
The community at large has been wonderful. The more I get out there and talk about what happened to Suzanne, the more support we get.<p>

<b>CAN YOU talk about the background to your daughter's case in terms of sexual harassment and abuse in the military?</b><p>

SUZANNE'S CASE is not isolated. It's not gender specific. We have just as many men coming forward and telling us about sexual abuse as we do women. So we've had vets from both genders coming forward and telling us their horror stories.<p>
I would say that in the 144 days since Suzanne was arrested, we've had over 300 people contact us and let us know their stories. The common theme is that they weren't believed, and the allegations were swept under the carpet. The perpetrators weren't even arrested or charged. In fact, most of them were promoted.<p>
I don't know if you know the statistics--85 percent of women in the military are sexually harassed, 65 percent are sexually assaulted, 14 percent are gang raped. And only 2 percent of the perpetrators are actually prosecuted.<p>

<b>WHAT IS your attitude to the war in Iraq?</b><p>

I THINK it's criminal. Have you seen the movie <i>V for Vendetta</i>? I think the same thing is happening. Our public is being fed a load of misinformation and fearmongering that has led to a misrepresentation of the genocide that is actually occurring in Iraq.<p>
If you invade a country for no real reason, then, of course, they are going to fight to get us out of there. There weren't any terrorists in Iraq before we invaded. This is something we created for oil-mongering and for big corporations.<p>

DO YOU think there's a problem with U.S. troops sexually harassing Iraqis?<p>

I THINK there's a problem with anybody sexually harassing anybody. But what we've done is taken these troops--these young men and women, especially men--we've taught them to think that sexual harassment is a norm. If we give them a moral hierarchy, and we put killing at the top of the moral hierarchy and say it's okay to kill, then why isn't it okay to rape? Why isn't okay to harass?<p>

<b>WHAT DO you think about the U.S. government slashing benefits for veterans?</b><p>

I THINK it's absolutely typical and criminal. We are using these children not just as human fodder, but as human sacrifices for oil. And we're bringing them back damaged, and we aren't taking care of them. They are committing suicide, and we aren't even supporting them.<p>
The very people who say "support our troops" are the ones who aren't supporting our troops, by voting for Republicans and these people who are directly responsible for cutting veterans benefits.<p>
We have kids coming back with poisoning from depleted uranium, and it's taking them a year to get tested. The number of people applying for disability who are being denied is astronomical. People are having to fight for their benefits, and there aren't even that many to fight for.<p>
One of the things that struck me the other day is that I've been working so hard since Suzanne was arrested to get her the benefits she deserves as a combat veteran. Then I realized there really aren't any benefits. It's very disillusioning to me.<p>

ARE YOU hopeful that the Democrats winning control of Congress will end the occupation of Iraq?<p>
I WOULD like to be optimistic that the Democrats will actually do something--will actually bring our troops home immediately. My faith in the Democrats and Congress in general is not high. They were the ones who voted and said it was okay, and who have been rubber-stamping and green-lighting things for George Bush.<p>
There are a handful of them who have said no. Cynthia McKinney, who lost her re-election, has been a champion. There are a few gems out there, and the rest of them are bad apples, and they need to be out.<p>

<b>WHAT WILL it take to end the occupation of Iraq?</b><p>

I THINK a couple of things. The first is our soldiers saying we aren't going to do it anymore. We starve the war machine by not letting any more kids enlist. We encourage and support our war resisters and our soldiers who are currently in Iraq to put down their weapons and say send us home--which the soldiers are starting to do. We should also starve the oil-mongers by driving hybrids and by using biodiesel. Get off our addiction to oil.<p>
Those are the things that I think will make an actual impact in our government and bring our troops home the fastest.<p>
The Iraqis should be allowed to create the government and type of community that works for them. Like Chanan said, you can't force a people to have a democracy at the point of a gun. It doesn't work. It's the exact opposite of what we say a democracy is.<p>
We need to pull our troops out and help Iraqis rebuild in the ways that they want to rebuild--not with mini-malls that we think they should have.<p>

<b>DO YOU have hope that we can build a mass antiwar movement?</b><p>

I DO have hope. I have to have hope. Why would I be out here devoting my life to doing this work? My son is 12 and I don't won't him to end up fighting World War Three in six years. There has to be hope.<p>
I have faith that we are going to create a peaceful global community. I think the more people visualize that and internalize that, the more people who are going to get on board and say no to all the fearmongering and hate--and say yes to peace and to love, because that's what's going to win them.<p>

