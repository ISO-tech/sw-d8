<font face="Times New Roman, Times, serif" size="5"><b>Charting a course in the fight for abolition</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 17, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 100 people from across the U.S. attended the sixth annual convention of the Campaign to End the Death Penalty (CEDP) in Chicago on November 11-12.<p>
Throughout the event, speakers shared moving stories of their firsthand encounters and ongoing struggles against the injustices of the criminal justice system.<p>
Barbara Becnel spoke about her continuing efforts to expose the cruel and unusual punishment of the death penalty as the one-year anniversary of California's execution of her friend Stanley Tookie Williams approaches on December 13. Becnel invited participants to come to the Bay Area on the anniversary of Williams' execution to attend the debut of a play that depicts his botched execution.<p>
Her speech established a sense of urgency that ran throughout the convention. One of the most moving speeches of the weekend was given by Derrel Myers, a member of Murder Victims Families for Human Rights, the Mobilization to Free Mumia Abu-Jamal and the CEDP. Myers' 23-year-old son Jo Jo White was killed in 1996 by an unknown assailant on a quiet street in San Francisco.<p>
Choking back tears, Derrel read from a statement, which appeared in the <i>San Francisco Bay Guardian,</i> that he and his wife co-wrote after their son's death.<p>
"Who, then, besides the gunman, is responsible for this outrageous crime?" they asked in the article entitled "Who killed Jo Jo?" "Jo Jo was killed by the same social system he was trying to change; a system that takes food, music, health and recreation programs from school children so that the wealthy corporate executives and stockholders can pay fewer taxes...<p>
"It's a system that, in the name of peace, wages endless war at home and abroad, militarizing our society and promoting more violence in the form of the death penalty and war on drugs. It is criminalizing poverty, youth and dissent, making justice even less accessible to the poor."<p>
As Derrel finished, quiet sobs from the audience were replaced by the sound of a warm standing ovation.<p>
Other featured speakers included former California death row prisoner Shujaa Graham; former Illinois death row prisoners Darby Tillis and Madison Hobley; Chris Ochoa, who spent 12 years of a life sentence in a Texas prison before he was exonerated in 2001; and Yusef Salaam, who was wrongly convicted in New York City's Central Park jogger case and spent six years behind bars before being exonerated.<p>
There were lots of new faces at the convention as well as many who had been to prior conventions. "This year's purpose seemed somehow clearer, and there have been some political changes that we were addressing that I think brought the issues more into our everyday context," said Katharine Smith, who was attending her third convention. "There was a really lively debate in the workshop on 'Torture: From Abu Ghraib to Chicago' that I went to, and it seemed like discussion was going in all of the groups for a long time."<p>
Darby Tillis, who was freed from Illinois death row in 1987, also thought the event was a step forward for the organization. "I thought the convention was great, even more electrifying than last year. And everybody is ready to fight. I think there's a feeling that with the change after the elections, that we have a chance to introduce new stuff," said Darby.<p>
Attendees at the convention decided to put a spotlight on the racist Texas death penalty machine by launching a campaign to free Rodney Reed, an African American man convicted for a murder he did not commit. The CEDP also plans to continue its emphasis on building local chapters across the country.<p>

