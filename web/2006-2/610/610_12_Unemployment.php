<font face="Times New Roman, Times, serif" size="5"><b>"What unemployment looks like"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 17, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">AS POLITICIANS brag about unemployment being at an all-time low, the search for job openings at a new Mars candy store in midtown Manhattan on November 4 laid bare the desperate reality of joblessness in a city whose mayor is a billionaire.<p>
According to the <i>New York Times</i>, in response to an ad that advertised $10.75-an-hour positions with heath benefits, "Several thousand people, mostly young, Black and Hispanic, had shown up to apply for fewer than 200 positions, only 65 of them full-time jobs. They came, they said, because of a phrase that had leapt out of the advertisements for the jobs: 'on-the-spot hiring.'<p>
"But there were too many people clogging the sidewalk outside the building on Eighth Avenue between 35th and 36th Streets where the company was conducting interviews, and everyone was abruptly told to go home and mail in the job applications."<p>
Those seeking jobs were not all unemployed (one is a part-time driver for UPS), and those interviewed by the <i>Times </i>ranged in age from 19 to 47.<p>
The even sadder news is that $10.75--nearly twice the minimum wage here--is hardly a livable wage in this city. The salary of a worker at the new Mars store will fall far below that required to pay rent in the city's latest "affordable" housing project in Queens West, which is intended for families with an income between $60,000 and $175,000 a year. The average income in Queens is $45,000, according to the <i>Queens Chronicle</i>.<p>
Residing in nearby Bedminster, N.J., is Jacqueline Mars, heir to one-third of the Mars fortune and a current owner of the company. She and her siblings John and Forrest have a net worth of $31.5 billion, and won't have to worry about looking for a job or an apartment any time soon.<p>
Yet for thousands in the city, the struggle to get by without a job or with one that doesn't pay the bills is a reality that, as Alphonzo Puzie told the <i>Times</i>, "burns the spirit." Another job-hunter, Tamika Jones, was moved not only by her own situation, but by those of her fellow job-hunters to tell the <i>Times, </i>"This is what unemployment looks like in New York City. I wanted to cry."<br>
<b>Sarah Wolf</b>, New York City<p>

