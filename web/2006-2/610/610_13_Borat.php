<font face="Times New Roman, Times, serif" size="5"><b>Comin' to America</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Jones</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 17, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><i><b>Borat: Cultural Learnings of America for Make Benefit Glorious Nation of Kazakhstan, </b></i>starring Sascha Baron Cohen and Kevin Davitian.<p></font>

<font face="Times New Roman, Times, serif" size="3">FIRST INTRODUCED to the world as one of three hilarious characters in Sascha Baron Cohen's <i>Da Ali G Show,</i> Borat Sagdiyev makes his big screen debut in <i>Borat.<p>
</i>The film's premise is that Borat, a Kazakh TV reporter, is sent by his government to America to learn what makes the country so great. Armed only with a small afro, a bushy moustache circa 1969, and a suit (which he never washes in order to smell "foreign"), Cohen uses this premise as a springboard for outrageous hi-jinks.<p>
One of my viewing companions laughed so hard she almost peed herself. Be warned.<p>
The hilarity originates from the fact that all of his costars (save Kevin Davitian as Azamat Bagatov, his producer) aren't in on the joke. Famous for never breaking character, Cohen's Borat gets himself into ludicrous (and even dangerous) situations as he gathers "learnings" from unsuspecting Americans, interviews dignitaries and tries to capture Pamela Anderson in a burlap sack.<p>
Much has been made of the Borat's virulent anti-Semitism. Cohen, himself a Jewish Briton, is of course poking fun at anti-Semitism. The concern has been raised that not everyone who watches will get the joke, but in this reviewer's opinion, it would be pretty hard not to get--unless we're talking about people who genuinely believe that Jews have shape-shifting powers.<p>
The real brilliance of the film is that it takes aim at all of the undigested bits of barbarism (to borrow a phrase from the Russian revolutionary Trotsky) in U.S. society.<p>
Racism, sexism and homophobia are Cohen's real targets. Borat comes off as a foolish, stupid and incredibly sexist foreigner, but he uses these attributes to get people to let down their guard and tell him what they think.<p>
Getting drunk with some fraternity brothers, they revel in sexist conversation ("How are the Russian bitches?" they ask Borat), and one of them admits he thinks that "minorities have too much power" and that America would be better if slavery was re-introduced!<p>
Borat stumbles upon a Pentecostal service that is visited by several high-ranking politicians (including a state supreme court justice!), and the parade of speakers alternate between praising Jesus, denouncing the theory of evolution and speaking in tongues.<p>
But Cohen doesn't buy into the red-state/blue-state thesis, and so smug coastal citizens aren't let off the hook here. Down South many strangers allow themselves to be greeted by kisses from Borat, while in New York the same thing nearly gets him killed.<p>
Constantly pushing limits, Borat exposes the boundaries of social acceptance.<p>
Apparently, there are many things you can do at a Southern high-society dinner and still be treated with grace and forgiveness--and Borat does them all. But when he invites a lower-class Black woman to join him at the table, he gets chased out of the house (literally). Interestingly, Borat befriends this woman (who is a prostitute), and she becomes the most sympathetic American in the film.<p>
Perhaps what Borat learns is that he is welcomed when he participates in prejudice (an SUV dealer tries to explain what a "pussy magnet" is), but when he crosses those lines (kissing men, befriending a Black woman), he is not.<p>
The fact that Borat is from a Muslim country is cause for concern. After all, the Jew-hating, gun-toting Muslim terrorist is an awful stereotype that plays right into the hands of Bush's war on terror. But while Borat is from a Muslim, Central Asian country, he reads more as a very secular Eastern European (actually Cohen got the idea for the character from a Russian doctor).<p>
Many on the left are dismayed at the way he is making use of such awful stereotypes about foreigners, but miss the real spirit of the film. Cohen does <i>not </i>make fun of everyone equally, as, for example, <i>The Daily Show </i>does to achieve political "balance."<p>
It's no accident that Borat finds his way to a Confederate gift shop or to a gay pride parade, or goes on a bloodthirsty war-on-terror rant at a rodeo, or that when he finally encounters the Jews he fears, they're the gentlest Bubbe and Zeda you've ever met.<p>
There is some making fun of the backwardness of Kazakhstan in the film, and many of Borat's appearances on TV talk shows give the impression that it's all about making fun of this character. But in <i>Borat, </i>the camera "loves" his village-mates back home in a way that it doesn't the politicians, racists and homophobes Borat tries to befriend in the U.S.<p>

