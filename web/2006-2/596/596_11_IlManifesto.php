<font face="Times New Roman, Times, serif" size="5"><b>Appeal from Italy's <i>Il Manifesto</i></b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>THE LEFT-wing Italian daily newspaper <i>Il Manifesto </i>is facing a financial crisis that threatens its future. Here, we print their appeal for support.</b><p>

FOR 35 years, <i>Il Manifesto </i>has represented a unique phenomenon in Italian, and not just Italian, newspaper history. <p>
Without a boss, other than the workers' cooperative that gets the paper out each day, equal (low) wages for all, and an independent, autonomous political journalism that has served as a mirror of the transformations that marked these years. Part of the common patrimony, but a true "monster," with the ambition of staying on the market while violating its laws, as an open space for the left. <p>
The front door is always open, and anyone can come in, even the unwelcome, as occurred a few years ago when a bomb exploded in front of our entrance. <p>
In these 35 years, we have lived dangerously (and recklessly); hundreds of thousands of people know this--our readers and all who have worked with us, and those who have used us to voice their own passions. <p>
The rhythm of our existence has been determined by financial crises, but we have always managed to overcome them, through our own efforts and those of "our world." But we are now at the point where 35 years of work could collapse on one summer afternoon; because freedom is expensive--above all, to those who practice it--and the moment when the payment of certain costs can no longer be postponed has arrived. <p>
To avoid collapse we need help--a lot of it, because this crisis is worse than the others and the survival of the paper is seriously threatened. This is not a cry of alarm, but a straightforward piece of information.<p>
As of today, a referendum on the future of the paper will be conducted, and the voting papers are to be found in the purses and wallets of the men and women for whom <i>Il Manifesto </i>matters. Because this crisis is not ours alone, it involves our most affectionate readers, but also all those who may have bought the paper just a few times over the years. <p>
We call upon the whole of the left (in the broadest sense of the term--parties, unions, associations etc.), and also on the world of the media, to which this paper has made a certain contribution, and will continue to do so. These are our "bosses," all those who believe (though they may look at us from afar) that democracy has need of a "monstrous" antidote against the risk that homogeneity of thought represents. <p>
It may seem presumptuous, but we firmly believe that our voice is essential; and that our functioning as an instrument of criticism of existing reality involves a cost that we shouldn't have to pay alone. <p>
For this reason, the question of "our" fate does not apply just to those of us who work in via Tomacelli, or those who intend to stay on the wrong side of the tracks, but also to those coming from the opposite perspective.<p>
And so, we are making our appeal to the open forum of the piazza--an appeal for subscriptions and contributions, and will constantly report on the results. The period of general mobilization begins today, and we are sure we will come through. <p>
We will be working hard for this, but we need you. Please let us know if you need us.<p>
