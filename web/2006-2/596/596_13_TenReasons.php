<font face="Times New Roman, Times, serif" size="4"><i>Ten Excellent Reasons Not to Join the Military</i> </font><br>
<font face="Times New Roman, Times, serif" size="5"><b>This book could save your life</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Charles Peterson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 28, 2006 | Page 13</font> <p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.10reasonsbook.com/" target="_blank">Ten Excellent Reasons Not to Join the Military</a>, </b></i>Elizabeth Weill-Greenberg, ed. New Press, 2006, 128 pages, $14.95.</font><p>

<font face="Times New Roman, Times, serif" size="3"><blockquote>
<i>Those at the top say:<br>
This way to glory.<br>
Those down below say:<br>
This way to the grave</i>.
</blockquote><p>
These lines, written decades ago by socialist German playwright and poet Bertolt Brecht, beautifully capture the main theme of the new collection of essays <i><a href="http://www.10reasonsbook.com/" target="_blank">Ten Excellent Reasons Not to Join the Military</a>.<p>
</i>The strength of the book is that, chapter by chapter, the authors, who include several prominent antiwar veterans, make the point that the people with the most stake in the war--the politicians and the military commanders--do absolutely none of the fighting. <p>
One veteran compares Iraq to "the tragedy of another American war in which unseen, distant commanders, whose own lives were never in danger, sent vulnerable young men and women into situations where war crimes become an everyday aspect of military conduct."<p>
Elsewhere, another author notes, "Not only are American Marines, reservists and soldiers expected to follow unlawful orders, but they are also expected to bear lifelong burdens of shame, guilt and legal culpability for the arrogance of their own commanders--who dispense life and death from an office computer."<p>
The ones who should bear the shame and guilt are the desktop commanders whose arrogance puts soldiers in such horrible, atrocity-making situations. As far as unlawful orders go, they are the rule rather than the exception. The authors share firsthand experience of being forced to participate in artillery strikes at civilian targets, shooting of passengers and the general terrorizing of the Iraqi population.<p>
Unfortunately, war crimes against civilian populations under occupation are nothing new. Writer and activist Paul Rockwell observes, "From the U.S. raids on hamlets in Vietnam, to the French raids in the Casbah in Algeria, to the ongoing door-to-door raids in Iraq, the main features of imperial occupation have never changed."<p>
This book does an excellent job placing the blame for these crimes squarely where they belong: on the politicians and policy-makers who crafted the disastrous occupation of Iraq. The book also makes an excellent case for one of the key arguments in the antiwar movement: immediately withdraw all the troops instead of "staying the course" in Iraq.<p>
The soldiers doing the fighting have no interest in the war in Iraq. Renowned antiwar activist Cindy Sheehan and journalist Nina Berman both argue that the only ones who benefit from this war are the ones who benefit from every war--the rich.<p>
Soldiers and their supporters have become aware that despite the hollow rhetoric of "support the troops," the government doesn't care about them. Veteran Robert Acosta, who was disabled during his tour in Iraq, observes, "The military doesn't care about you--you're just a number, a chess piece."<p>
Adele Kubein, the mother of an active-duty soldier, says of young people who enlist: "What they [the recruiters] don't tell them is that they are disposable equipment that can be discarded when its usefulness is over."<p>
It's the stories of such "chess pieces" that give the book much of its power. Just being able to read the stories of war resisters such as Aimee Allison, Pablo Paredes, Aidan Delgado and Jimmy Massey makes this book extremely valuable. They show that it is possible to retain one's humanity in horrific conditions, despite the pressure coming from above to abandon it.<p>
Their experiences point to the need for an antiwar movement in this country that can give confidence to more soldiers to refuse to participate in this illegal and immoral war. <p>
Soldiers are not just victims of the oppressive conditions of military service. They have the power to stop this current war, and all wars, by refusing to be pawns on the commanders' chessboard. Relying on the humanity of soldiers, and not the goodwill of politicians, is the only sure path to peace.<p>
The final chapter tells the story of Rae Abileah, a young woman who considered joining the military. Like many young people in this country, she considered the military as a way to pay for college. As her story shows, it is possible to get financial aid or scholarships. Many schools will even have counselors--although not enough--who can help you through the process.<p>
If you are considering joining the military, it is worth attempting to follow her example. It will most likely save your life.<p>

