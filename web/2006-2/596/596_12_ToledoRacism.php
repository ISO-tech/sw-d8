<font face="Times New Roman, Times, serif" size="4">An all-too-familiar story of police racism in Toledo</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pulled over for driving while Black</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">LANCE SELF, age 19, and Julian Mack, age 21, both African American men from Toledo, Ohio, are all-too-familiar with police racism. <p>
Julian has been pulled over for dubious reasons twice in recent weeks, while Lance has been pulled over three times in the past year for such "crimes" as "swerving" (for which he wasn't ticketed), a "registration check" (for which he wasn't ticketed) and "speeding" (going 35 in a 25 mile-an-hour zone). <p>
In their latest brush with the Toledo Police Department (TPD) on June 28, Lance and Julian were driving home after an evening of skating, wearing their seatbelts and driving under the speed limit. They were pulled over by a TPD cop (badge number 2376) under the guise of another "registration check." <p>
Julian wisely decided to videotape the episode on his camera phone, "so just in case he tries something, we can get it on camera." When the cop returned from "checking" their registration and IDs--20 minutes later--he demanded, with his hand on his gun, that Julian hand over his camera. <p>
When Julian stated that he wasn't doing anything illegal and commented that the police camera was taping them, this cop had the audacity to say that Julian's camera was "invading his privacy!" The cop detained them for another 30 minutes while he struggled to find the "erase" mechanism on the camera. <p>
Eventually, the exasperated cop figured out how to erase the camera, and, upset that he was unable to pin anything on them, slapped Lance with a $112 ticket for not wearing a seatbelt--even though he only unbuckled his seatbelt in order to access his registration papers. <p>
Julian and Lance have since filed a complaint with TPD. "We want to establish a paper trail because we know there are many others out there like us," said Julian. <p>
Yet the recent record--Jeffrey Turner tazed to death by the TPD for loitering, TPD and city officials providing protection for two Nazi rallies, and Mayor Carty Finkbeiner publicly referring to Black fire chief Mike Bell as "King Kong"--indicates that a grassroots antiracist campaign, outside the confines of "official" grievance channels, will be necessary to counter our enemies in blue. <p>
And those not in blue as well. A local radio station, Tower 98.3 FM, a Cumulus station, has been allowing a racist DJ to call and harass Asian restaurants live on air. The DJ, "Lucas," calls and harasses workers over their use of English. <p>
On a recent call to a Japanese restaurant, the DJ said such things as, "Me love you long time," "Ching Chong Chung" and "Me speakee no English." During a call on May 22, when the worker spoke perfect English, the DJ yelled, "Who are you? What are you doing there? What's with these white people working at Chinese restaurants? We're not calling Bob Evans!"<p>
Fortunately, Asian Communities United (ACU) at Bowling Green State University has taken the lead in organizing a fightback. ACU launched a petition drive demanding that the DJ be fired, thus far collecting over 1,300 signatures, and organized a publicity campaign to expose the station's racism. <p>
Yet Tower 98.3 has been unapologetic, and the offending DJ has allegedly started his own petition drive, presumably to defend his right to be a racist. The regional manager of Cumulus, Larry Blum, couldn't find anything offensive with the prank, telling one activist that he was "tired of having to walk on eggshells about stuff like this." <p>
But the growing campaign against the station's racism has forced Cumulus into offering some concessions, including a suspension of the DJ. In trying to defend the station's racism, John Dickey, executive vice president of Cumulus, produced tapes documenting similar racist stunts by rival station Kiss FM. <p>
However, organizers have been emboldened by the momentum of their campaign, and have so far not been willing to trade half-measure concessions for their silence. A demonstration in front of the radio station was called for July 10, taking the campaign into the streets. <p>
Racist DJs contribute to the climate of racism, increasing the likelihood of right-wing violence. Adding "street heat" to the fight against radio racism is a necessary component for pushing back the right wing. <br>
<b>Patrick Dyer,</b> Toledo, Ohio<p>
