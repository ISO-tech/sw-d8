<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Delphi</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RANK-AND-file autoworkers will picket the headquarters of bankrupt automaker Delphi August 11 to protest the company's threats to tear up its union contracts.<p>
The protest is timed to coincide with Delphi's plan to petition federal bankruptcy court Judge Robert Drain to cancel the company's contracts with the United Auto Workers (UAW), a move that UAW President Ron Gettelfinger says would trigger a strike.<p>
However, the UAW has already given Delphi CEO Steve Miller what he wanted--a buyout program in which 12,000 UAW members took an early retirement package or a lump sum in cash, depending on seniority levels.<p>
Another 8,600 UAW members at Delphi who weren't eligible for the first buyout are being offered another buyout program, as are 8,500 hourly workers in Delphi plants represented by the International Union of Electronic Workers-Communications Workers of America. Meanwhile 35,000 workers at General Motors have also accepted buyout offers.<p>
At Delphi, a far higher percentage of workers took the deal for fear of wage cuts being sought by the company. As a result, about 50 percent of workers in Delphi plants today are newly hired temporaries, according to Gregg Shotwell, a member of UAW Local 2151 at the Delphi fuel injector plant in Coopersville, Mich., and an activist in the Soldiers of Solidarity rank-and-file network.<p>
That means that veteran workers are effectively training their potential replacements in a strike, Shotwell said, pointing out that the temps make just $14 per hour, less than half the average wage of permanent employees. "Whatever Gettelfinger says, there won't be a strike," even if the union contracts are cancelled, he said. "We have no leverage."<p>
"The young people here as temp workers are literally our children," Shotwell said. "I am training kids of people I know. We're not only giving them a dead-end job with substandard wages and no benefits, but we're abandoning them to the wolves. I say 'abandon' because Delphi wants to turn the plant into a sweatshop."<p>
Unlike temporary workers hired under previous UAW contracts, the new Delphi temps apparently have no path to permanent employment even if they work 120 days, under terms of the buyout program negotiated by the UAW with Delphi and GM.<p>
Shotwell argues that this temp worker category was a contractual change that should have come to a vote in union locals. Instead, the UAW gave Delphi boss Miller the job cuts he wanted--but the CEO still wants more, including cuts to retirement benefits.<p>
That's why the Soldiers of Solidarity members who took the buyout offer are remaining active as retirees, Shotwell said. The picket at Delphi headquarters aims to keep the rank-and-file activism alive as the UAW prepares for the 2007 contract talks with Delphi, GM, Ford and Chrysler.<p>
<i>For more information on the picket, visit <a href="http://www.soldiersofsolidarity.com" target="_blank">soldiersofsolidarity.com</a></i>.<p>

