<font face="Times New Roman, Times, serif" size="4">Palestinian journalist on the crisis in Gaza:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The Israelis want to train us not to ask for our rights"</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 6</font><p>

THE WORLD'S attention has turned to Israel's assault on Lebanon, but the Israeli government's intensified war on Palestinians in Gaza is continuing unabated. Since the June 28 capture of an Israeli soldier by Palestinian militants, Israel has carried out a devastating assault, killing dozens of people--men, women and children--in continuous air and ground attacks.<p>
SENAN SHAQDEH, a Palestinian journalist living in the U.S., spoke to <i>Socialist Worker's </i>ELIZABETH SCHULTE about the deepening crisis for the people of Palestine.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>CAN YOU describe conditions for Palestinians in Gaza and the West Bank before the Israeli government launched its latest assault?</b><p>

THE TWO main economic resources for Palestinians are the people who work inside Israel and the Palestinian Authority (PA), which employs more than 150,000 or 160,000 employees.<p>
Even before the Israeli attack against Gaza, they weren't allowing anyone to work inside Israel. This means that two-thirds of the Palestinian workforce isn't working. And the PA was having financial problems, so the people who work for the PA didn't have enough money and were facing a crisis.<p>
Now with the Israeli attack on Gaza, the situation is very bad--to the point that a week ago, Palestinian children didn't have milk.<p>
A lot of Palestinian people in the U.S. and outside Palestine used to send money for one family; now, each of us sends money for five or more families. Everybody needs money. The people eat mostly bread and some vegetables--mostly tomatoes because they are very cheap. <p>
Another thing is that nobody in the Arab community can pay money to the West Bank and Gaza through Arab organizations here--either because they've closed them down or because the authorities will come after you in five years and tell you you were supporting "terrorism." There is no way for Palestinians to help their people through organizations, just by individual means. <p>

<b>WAS IT a surprise when the Israel began its assault on Gaza?</b><p>

NOT REALLY, because the Israelis were waiting for anything to start this assault.<p>
The unfair thing is that everyone is talking about this one Israeli soldier--Gilad Shalit--and nobody cares about some 10,000 Palestinian prisoners in Israeli jails. I know that six months ago, there were some 300 children and almost 300 women in prisons. Some of these children are one year old--they don't have anyplace to go, so they are there with their moms. This is totally unfair. <br>
I think the Bush administration is accountable for killing the children in Palestine and Lebanon because Bush is using a veto in the United Nations Security Council to stop any international attempt to stop the Israeli aggression. The Bush administration believes that Muslim children or Arab children are not children--they are just targets for killing, like animals. <p>

<b>IT'S OBVIOUS that this isn't about one Israeli soldier or three. What would you say is behind the attack?</b> <p>

THE ISRAELIS want to train us not to ask for our rights, which is our right to a Palestinian state and all the occupied area in 1967 and the refugees' right to return to Israel.<p>
They are trying to push us in the long run to accept the big jail. Gaza now is a big jail--when they closed the border, they made it the largest jail in the universe. They want to train us to accept anything that they offer, which will never happen.<p>
All my family is there, in the West Bank and Gaza. For 50 years, they fought Israeli aggression and Israeli occupation of their land, and before that, they fought the Western occupation of their land.<p>
The people are ready to fight this fight forever. Nobody will sign any settlement that will not allow the refugees to go back or that will not give the Palestinians all their rights. In fact, we have the right to historic Palestine, all of Palestine, because it is our country.<p>

<b>WHAT IS the U.S. government's role?</b><p>

UNTIL NOW, the main problem has been the poor south and the rich north. Now Bush is trying to make it something else. Whenever you have an act, you have a reaction. So when they elect the right wing in Israel, the Palestinians will elect Hamas.<p>
The Bush administration poured more oil on the fire. This is what he's doing when he gives legal cover to the Israeli government to keep killing children in Palestine and Lebanon. He's guilty for that. In fact, he is part of the killing. <p>
One day, I'm sure that Bush and his whole administration will be taken to the world criminal court. They claim that they are a broker in the battles in the Middle East, but they are clearly part of the killing of Palestinian and Lebanese children.<p>

