<font face="Times New Roman, Times, serif" size="5"><b>Huge rally protests stolen election in Mexico</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JON VAN CAMP reports from Mexico City on demonstrations to protest election fraud.</b><p>

THE BIGGEST demonstration in Mexico's history took place July 16 in Mexico City's Z&oacute;calo amid a struggle to force a recount in the fraud-ridden presidential election. Local newspapers and the populist Revolutionary Democratic Party (PRD by its initials in Spanish) said that more than 1 million people participated. <p>
It was the second demonstration called by the PRD's presidential candidate, former Mexico City Mayor Andr&eacute;s Manual L&oacute;pez Obrador, known in Mexico as AMLO, to protest the results of the July 2 election, which government authorities awarded to conservative candidate Felipe Calder&oacute;n of the National Action Party (PAN) despite documentation of widespread fraud.<p>
The July 16 protest was more than twice as big as the first, held the previous weekend. The Z&oacute;calo, a huge central square in the capital, was packed with protesters.<p>
Hours after the rally began, protesters were still marching toward the Z&oacute;calo. Most couldn't get in. Many struggled to find a place on one of the streets leading into the Z&oacute;calo where satellite speakers and video screens were set up.<p>
On the street I was on, the power kept giving out on the screens. Every time it did, the crowd would start chanting, "S&iacute; se puede" (Yes, it can be done), "El pueblo unido jamas ser&aacute; vencido," (The people united will never be defeated) and "Voto por voto, casilla por casilla" (vote by vote, ballot box by ballot box).<p>
Yellow flags of the PRD were interspersed with the red flags of left-wing parties, and amid the thousands of pictures of AMLO were those of revolutionaries Ch&eacute; Guevara and Emiliano Zapata. The PRD had commercially made signs and banners reading, "No al pinche fraude!" ("No to the damned fraud)!" but many marchers made signs of their own, expressing their own outrage at the corruption of the system and the arrogance of the right wing.<p>
The election pitted the right-wing PAN candidate Calder&oacute;n, supported by President Vicente Fox, against AMLO of the liberal populist PRD.<p>
The election officially ended in a virtual tie. But after a re-tabulation of votes, Calderon was supposedly ahead by half a percentage point and was declared the winner by the electoral commission, Mexican television and the administration. <p>
Fraud at the ballot box--reports of which are still surfacing--was only the latest part in an effort, orchestrated by Fox and the PAN, to prevent AMLO from winning.<p>
Fox's prosecutors initially tried to have AMLO disqualified over a minor legal matter, but backed down after mass protests last year.<p>
More recently, the Fox government whipped up a fear campaign that AMLO was a dangerous radical and ordered government forces to attack striking steelworkers and teachers. The most brutal police attack came weeks before the election in the town of San Salvador Atenco outside Mexico City, where residents had successfully mobilized to block an airport expansion.<p>
In reality, AMLO, as mayor of Mexico City, combined antipoverty programs with a moderate, pro-business agenda--and brought onto his team many veterans of the Institutional Revolutionary Party (PRI), which ruled Mexico for 70 years until Fox's victory in the 2000 elections.<p>
Nevertheless, the campaign of violence and fear chipped away at AMLO's initial big lead in opinion polls.<p>
The PRD has pointed out numerous instances of tabulation irregularities and vote-buying, and is demanding a complete recount.<p>
At one of the many pre-rallies held across the city to build for the July 16 march, one organizer said that if people wanted a Mexico free from foreign domination and if they wanted Mexico to control its own water and oil, they must support AMLO. <br>
However, at the rally, Obrador himself struck a much more moderate note.<p>
He stressed national unity, identifying himself with Benito Ju&aacute;rez, the liberal president of the 19th century, rather than people like Ch&eacute; or Zapata. He emphasized that a recount was necessary to preserve the legitimacy of the Mexican electoral system.<p>
AMLO called for another demonstration in two weeks and promised it would be twice as big. The demonstrators seemed in no mood to give in to the right wing, and AMLO has refused to do so.<p>
This electoral struggle may strengthen the confidence of the left in Mexico and lead to greater struggles ahead.<p>

