<p><a name="Congress"></a><font face="Times New Roman, Times, serif" size="4"><b>Congress guts minority med programs</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON JUNE 30, the House Appropriations Committee proposed eliminating funding for all 74 Health Career Opportunity Programs (HCOP) and 30 of 34 Center of Excellence (COE) programs in the country. The Senate Labor, Health and Human Services and Education Appropriations subcommittee was scheduled to consider its version on July 18. <p>
Many high school and college summer enrichment programs, pre-health student conferences, recruitment, tutorial and outreach programs would be eliminated if these cuts take place. <p>
For the last 20 years, these programs have supported activities to identity, support, recruit, admit, retain, train and graduate Black, Latino and American Indian students in medicine and other health professions. COEs also support minority faculty development, minority health issues curricular development, and research and community-based clinical training with minority patients.<p>
These programs were initiated to increase the number of physicians and other health professionals who practice in minority inner-city areas, as well as in rural areas, and to promote the cultural, experiential and linguistic skills to deliver effective health care to underserved minority patients. <p>
Ultimately, several million largely minority persons living in underserved areas will not have physicians and other health professionals accessible to them if these cuts are adopted.<p>
In fiscal year 2005, the HCOP was funded at $35.6 million and supported 74 programs. COE was funded at $33.6 million. For fiscal year 2007, the House appropriations bill would totally eliminate funding for HCOP and drop COE funding to $12 million--which would only support four of the original 34 programs.<p>
An action alert that went out on June 29 is asking people to e-mail Sen. Arlen Specter (R-Pa.), a "powerful chair" of the subcommittee, to urge him to restore full funding levels to full fiscal year 2005 levels. Who knows what will become of this effort. <p>
One thing is for sure: In a world where the official Pentagon budget is $450 billion and rising, while spending for social programs is rapidly declining, we are going to need to do something more than e-mailing our representatives.<br>
<b>Jean Howell</b>, American Indian Medical student, University of Minnesota<p>
