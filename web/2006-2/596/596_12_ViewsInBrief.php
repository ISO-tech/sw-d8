<p><a name="No"></a><font face="Times New Roman, Times, serif" size="4"><b>No justice yet for detainees</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THERE WAS great jubilation in liberal and progressive quarters upon the announcement on June 29 that the U.S. Supreme Court had ruled against the U.S. government's plans to hold military tribunals against the imprisoned Arab and Muslim men held in Guant&aacute;namo.<p>
Whenever the Supreme Court rules in favor of the oppressed, all should breathe a sigh of relief. This stinking body is one of the tripartite components of the U.S. capitalist government and as such is surely not a friend of the oppressed or the working class. <p>
My concerns are twofold, however. The first is that this ruling will not be discussed in its proper context, and secondly, that the entire war on terror will remain uncondemned. <p>
That is to say, will this ruling give credence to the war upon the Muslim and Arab people that the U.S. government unleashed after the World Trade Center was destroyed on September 11, 2001? Will the thousands of men imprisoned in secret CIA prisons around the world that languish in the terror cells and torture centers remain there forever?<p>
What is it that prevents the U.S. left, in unison, from saying: "Close the torture centers! U.S. out of Iraq and Afghanistan! No more Guant&aacute;namos!" I cannot really understand what prevents the antiwar movement and the socialist movement from finding common ground around these or other slogans. <p>
Because of this lack of political leadership, the ruling by the Supreme Court takes undue weight and import, although it is important in its own right. Legal fights are a legitimate part of the reform movement, but they are not revolutionary in and of themselves. The prisoners in Guant&aacute;namo, in all of the U.S. military jails and prisons, and in all of the CIA secret torture centers must be released now!<br>
<b>Steve Johnson,</b> Los Angeles<p>

