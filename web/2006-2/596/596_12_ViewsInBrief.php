<font face="Times New Roman, Times, serif" size="4"><b>Afghans' right to resist</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NICOLE COLSON'S report "U.S. escalates the violence in Afghanistan" does a good job showing the "mess" that the U.S. occupation has made of Afghanistan (<i>SW</i>, July 14). <p>
Unfortunately, I believe it takes a far too neutral stance on the resistance that sounds a lot more like mainstream media spin than the clear anti-imperialist position we should expect from <i>Socialist Worker</i>.<p>
The resistance in Afghanistan is clearly growing, and it is becoming more of a problem for the U.S. forces, NATO coalition troops and the "Afghan security forces" created to protect the isolated U.S. puppet Hamad Karzai. <p>
Attacks against these forces are entirely justified as legitimate resistance against the occupation forces and their collaborators. Colson does not point this out and emphasizes instead the Taliban's threats and attacks on Afghan laborers.<p>
With the demonization of Islam and racism against Arabs that suggests that any act of resistance in Iraq, Palestine or Afghanistan is some crazy act of religious zealotry, we have to take a clearer stand in our articles when we talk about Afghan resistance. The Taliban have backwards politics but the brutality of the U.S.-NATO occupation has helped organize Afghan resistance behind the Taliban (as well as other groups like Hizb-e-Islami).<p>
We can and should point out our criticism of resistance movements in occupied countries, but our position must always start from the right of the oppressed people to resist, our unconditional support of that resistance and the underling understanding that a defeat of the U.S. empire in any part of the world is good for workers everywhere.<br>
<b>Andy Libson</b>, San Francisco<p>
