<font face="Times New Roman, Times, serif" size="5"><b>Catering to the "pro-life" fanatics</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 28, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">COULD THE Bush White House have gone too far--even by its standards? White House press secretary Terry Snow had to take back a statement he made last week on the occasion of Bush's veto of legislation that would have allowed federal funding for embryonic stem cell research.<p>
"The president believes strongly that for the purpose of research it's inappropriate for the federal government to finance something that many people consider murder," Snow told reporters July 19. "He's one of them." Snow back-peddled on July 24, explaining that Bush evidently wouldn't have put it that way. He would have said "destruction of human life"--not "murder."<p>
During hearings on the bill, Republican Neanderthals used similarly anti-science, "pro-life" arguments to try to win points from their Christian right constituents.<p>
"Since that little embryo doesn't have a pair of eyes and a hair color, doesn't have a name, it's very easy to dismiss this entity as insignificant," said Sen. Rick Santorum (R-Pa.). "So we just kind of claim that there is a cloud as to what this is, and that allows us then to destroy that life and use it for our purposes."<p>
The bill that passed both the Senate and House would have allowed funding for research on stem cells obtained from discarded embryos--research that has the potential of finding treatments for diabetes; Parkinson's, Alzheimer's and Lou Gehrig's diseases; multiple sclerosis; cancer; and neurological and spinal-cord injuries.<p>

