<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The racist roots of the Israeli state</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 28, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">NOT LONG after the second full-scale Israeli invasion of Lebanon in 1982, Noam Chomsky wrote his classic book <i>The Fateful Triangle</i>. <p>
He wrote that Israel's overarching aims were "to integrate the bulk of the occupied territories within Israel in some fashion while finding a way to reduce the Arab population; to disperse the scattered refugees and crush any manifestation of Palestinian nationalism or Palestinian culture," and "to gain control over southern Lebanon."<p>
The details change, but the goals--and the brutality--seem to remain the same.<p>
Zionism has always been aggressively expansionist. Listen to this statement by Zionist leader David Ben-Gurion in 1938: "After we become a strong force as a result of the creation of the state, we shall abolish partition and expand to the whole of Palestine."<p>
Ben-Gurion's strategic objectives at the time were to "smash Lebanon, Trans-Jordan and Syria. The weak point is Lebanon, for the Moslem regime is artificial and easy for us to undermine."<p>
It is the racially exclusivist and bellicose character of Israel that makes it uniquely suited, again in the words of Chomsky, to be a "Middle-East Sparta in the service of American power."<p>
Zionism as a movement never defined itself as a national liberation movement. On the contrary, from very early on Zionism identified strongly with racist white South Africa--Israel was to form a close bond with the apartheid state.<p>
But the colonialist project of the Zionist movement also set itself apart from other colonizing efforts. Instead of exploiting native-born labor, as colonial societies did in South Africa, the purpose of Jewish colonization was to build an exclusively Jewish state based on the <i>removal</i> of the native population. <p>
To accomplish its goals, the Zionist movement required the backing of a major imperial power. Zionist leader Theodor Herzl sold the Zionist project to great powers as an "outpost of civilization as opposed to barbarism."<p>
Finally, in 1917, Lord Balfour committed Britain to supporting a "national home" for Jews in historic Palestine. Of course, there was the propaganda for public consumption that Palestine was "a land without a people for a people without a land."<p>
But to each other the Zionists were more sanguine. "All colonization," wrote revisionist Zionist leader Vladimir Jabotinsky, "must continue in defiance of the will of the native population. Therefore, it can continue and develop only under the shield of force which comprises the <i>Iron Wall</i> through with the local population can never break through."<p>
The Zionist movement set about creating a Jewish settler colony first by purchasing land and expelling the Arab peasants whose families had worked it for centuries. When it became clear that the Zionists' aim was to create an exclusively Jewish state on Arab land, the Arab population revolted against British rule in 1936.<p>
To help them quell the rebellion, the British created armed Jewish paramilitary groups. Ben-Gurion considered the formation and training of this Jewish "quasi police force"--which had reached over 14,000 men by 1939--an ideal basis for the formation of a Zionist army.<p>
In 1947, a United Nations partition plan allotted 55 percent of historic Palestine to the Jewish state, though they constituted 31 percent of the population and owned only 6 percent of the land.<p>
The Zionists almost immediately launched a war against the Arab population, seizing control of an additional 20 percent of historic Palestine and driving 750,000 Palestinians from their homes. The utmost brutality and terror were utilized to achieve this aim against a population without a central authority, and which had already been demoralized by the defeat of the nationalist revolt a decade earlier.<p>
Menachem Begin--later Israel's prime minister--led terrorist gangs into the village of Deir Yassin on April 9 and massacred 245 villagers, including 70 women and children. There were dozens of other smaller massacres. In the first five years of the new Zionist state, 350 of the 370 new Jewish settlements were built on land owned by Arabs who had fled their homes. <p>

