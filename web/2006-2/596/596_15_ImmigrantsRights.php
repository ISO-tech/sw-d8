<font face="Times New Roman, Times, serif" size="5"><b>Broadening the struggle for immigrant rights</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">IN JULY, activists across the country organized to keep up the momentum of the massive May 1 marches and broaden the fight for immigrant rights.<p>
-- In <b>Chicago, </b>10,000 marchers endured blazing heat July 19 to call for an end to deportations of undocumented workers and legalization for all.<p>
Thousands of workers and students turned out to the march, called by Centro Sin Fronteras and the radio show personality "El Pistolero," in spite of rumors of deportations. Small marches and protests against the racist Minutemen and Paul Revere Riders, as well as support rallies for workers threatened with deportation, have taken place in Chicago since June.<p>
At the march, activists from the March 10th Coalition announced the National Immigrant Rights Strategy Conference, to be held in Chicago August 11&#8211;13, where people will meet to discuss the next steps in demanding full legalization and stopping deportations and the militarization of the border.<p>
Elsewhere, activists have been organizing to counter the threat of the anti-immigrant right. <p>
-- In <b>Fremont, Calif., </b>50 antiracist protesters turned out July 14 to counter an anti-immigrant protest of 25 called by the East Bay Coalition for Border Security (EBCBS--a group seeking affiliation with the Minutemen).<p>
At the protest, police didn't lift a finger as a burly racist sporting an Iron Cross punched antiracist activist Brian Belknap in the nose. At one point, several antiracist activists were pushed into the street, and a car ran over one woman's foot and knocked another man to the ground. <p>
"The police are always going to be on their side," said activist Josephina Alvarez, "but I'm not going to ignore this. I'm here to have my voice heard."<p>
-- In <b>Bergenfield, N.J., </b>35 antiracists turned out July 15 to counter the Saturday Morning Citizens Coalition Project, a weekly event at which members of the anti-immigrant group United Patriots of America (UPA) harass day laborers at a local job site.<p>
Approximately a dozen bigots turn out each Saturday to intimidate day laborers and to harass contractors by photographing their license plates, and day laborers also face harassment from the all-white local police force. The July 15 protest was the first time in more than three months that the racists were unable to stop the day laborers from getting work.<p>
Immigrant rights activists also came together at forums and meetings last month, to debate the best way forward in building the movement. <p>
-- In <b>San Francisco, </b>more than 120 people gathered for a conference sponsored by the Regional Unity Coalition on July 14. After breaking into small groups to discuss questions facing the movement, the day ended with everyone chanting, "The people, united, will never be defeated!" in Tagalog, Chinese, Arabic and Spanish. <p>
-- In <b>Greensboro, N.C., </b>a statewide retreat took place July 22 at the University of North Carolina-Greensboro that brought together non-profit groups, grassroots leaders and activists in an effort to develop stronger relationships among organizations involved in the struggle. Activists also discussed the need to educate immigrants and non-immigrants on the specifics of the immigration legislation currently being debated.<p>
-- In <b>Providence, R.I., </b>more than 40 people gathered together July 8 at a forum, sponsored by the Immigrants United coalition. <p>
Michael Nelson, a legal resident from Jamaica, described being detained for seven months at a prison in Plymouth, Mass. Olivia Geiger of English for Action explained the repressive nature of both the House and Senate "immigration reform" bills, while Brian Chidester of the International Socialist Organization explained why these bills have died and called for political independence from Republicans and Democrats. <p>
Other speakers included Gladys Gould of AFSCME Local 94 and Vladimir X of the Nation of Islam, while a member of the NAACP spoke on the need for unity between Blacks and Latinos. <p>
<i>Bridget Broderick, John Green, Sarah Hines, Shaun Joseph, Jose Lovo, Emmanuel Santos, K.B. Udaya, Sarah Wolf and Jennifer Wright contributed to this report</i>.<p>

