<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">September 22, 2006 | Pages 10 and 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Los Angeles teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--In a blatant act of political retaliation, officials in the Los Angeles Unified School District (LAUSD) have forced the transfer of UTLA activist Alex Caputo-Pearl from his position at Crenshaw High School.<p>
With the District also demanding that teachers accept a salary freeze and two-tier health benefits in current contract talks, Alex's transfer shows that LAUSD feels supremely confident that it can push UTLA around and get away with it.<p>
Alex is one of the elected union representatives at Crenshaw and a leading activist in the Crenshaw Cougar Coalition (CCC)--a grassroots organization uniting scores of parents, teachers and students at the school.<p>
Gross administrative mismanagement led Crenshaw to nearly lose its accreditation last year. Without accreditation, Crenshaw graduates applying for jobs and college would be told that their diplomas were essentially worthless. CCC led a successful campaign to demand lower class sizes and more resources for Crenshaw. This campaign was central to the school's ability to maintain its accreditation. <p>
Yet LAUSD bureaucrats accused Alex of a "lack of commitment to Crenshaw's accreditation process." In reality, Alex helped to save Crenshaw's accreditation from LAUSD incompetence.<p>
Almost 200 UTLA members and Crenshaw parents and students picketed outside the administrative offices where Alex was formally notified of his transfer. Double that number protested outside the LAUSD Board of Education meeting a few weeks later. However, the Board took no action, postponing any decision on Alex's case until its September 26 meeting.<p>
This grassroots pressure has shaken LAUSD's confidence somewhat, but it needs to be even stronger and louder on the 26th.<p>
Unfortunately, UTLA leadership is hesitating in its commitment to organizing a fightback. UTLA President A.J. Duffy has reassigned Juan Parrino, the union's lead staff organizer, to work full-time on the gubernatorial campaign of Democrat Phil Angelides.<p>
This move has provoked controversy within Progressive Educators for Action (PEAC), the militant rank-and-file caucus within UTLA that supported the election of the new union leadership. PEAC has also been a leading force in the fight to keep Alex at Crenshaw, and several PEAC activists see a contradiction between the dire need for UTLA to organize and the leadership's emphasis on working with Democrats like Angelides and Los Angeles Mayor Antonio Villaraigosa.<p>
UTLA's ability to defend our members, lower class sizes, and fight for better schools will be solely determined by the level of organization of our rank-and-file membership--regardless of who's sitting in the governor's mansion in Sacramento.<p>
<i>To find out more about Alex's case, go to <a href="http://www.keepalexatcrenshaw.org" target="_blank">www.keepalexatcrenshaw.org</a></i>.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Waste Management Inc.</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Sanitation truck mechanics ratified a five-year contract with Houston-based Waste Management Inc. (WM) on September 12 by an 87 percent vote.<p>
The 45 mechanics, members of Teamsters Local 174, had been without a contract since December 31. They struck for three days as 350 WM truck drivers, also members of Local 174, refused to cross the picket line. A settlement came hours before the strike spread to an area that would have potentially affected a million people. <p>
The mechanics won a pay raise from $24 to $26 an hour, significant employer pension contributions, and some contract language gains. But for the first time, they will have to pay a part of their health care premiums.<p>
With upwards of 95 percent of all sanitation workers in King County covered by Local 174, the strike and the solidarity between the mechanics and truck drivers gave a glimpse of the tremendous power these workers have. The potential is there to win a contract that has only gains and no concessions.<p>

