<font face="Times New Roman, Times, serif" size="5"><b>N.J. nurses' strike wins gains</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 22, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW BRUNSWICK, N.J.--A nearly month-long nurses' strike at Robert Wood Johnson University Hospital (RWJUH) ended September 17 when an overwhelming majority of the strikers voted to return to work after extracting some concessions from management.<p>
Leaders of United Steel Workers (USW) Local 4-200, which represents the 1,300 nurses at RWJUH, said that this is the first contract for the nurses that doesn't contain givebacks. <p>
The deal provides raises of 3 percent in each year of the three-year contract, terms negotiated prior to the strike. Furthermore, the nurses showed that they could stand strong against management's union-busting attempts, as only 100 nurses crossed the picket line.<p>
The strike was triggered by RWJUH's attempts to impose a health plan that would have carried stiff penalties for seeking care at any other hospital. Workers would have had to rely on an "inner circle" of physicians at RWJUH, which would have made it difficult to receive care in a timely manner.<p>
As the strike entered its third week, a federal mediator brokered a new offer from management. Under the new terms, RWJUH agreed to cover emergency care and hospitalization at "inner circle" rates, and to cover nurses' children at out-of-state colleges. The new deal also guarantees that the hospital will provide 50 additional physicians as part of the "inner-circle" group.<p>
"We made significant gains in this contract," said Jerry Collins, President of Local 4-200, after the votes were counted.<p>
But RWJUH refused to budge on the penalties for non-emergency care and on a host of other issues. As this was the reason for the strike in the first place, many nurses see the new deal as not going far enough.<p>
"If we accept this offer, I'll feel like an idiot for having gone on strike," said one nurse, echoing the sentiment of strikers who gathered for a rally the day before the vote. Another nurse told <i>Socialist Worker</i>, "I have to vote 'no' not only on principle, but on the substance of the offer."<p>
Even some among the local leadership voted against settling. Nevertheless, 769 nurses voted to return to work, while 117 voted against, as many nurses began to express a lack of confidence about the strike.<p>
The national leadership of the USW, in fact seemed intent on settling the strike quickly. Instead of holding a general membership meeting where all the nurses could discuss the deal, the union held rolling "informational sessions" throughout the day September 17.<p>
This followed a weak strategy pursued by the USW during the strike. The union gave RWJUH notice of the strike ten days earlier than under the law, allowing management time to organize a scabbing operation.<p>
Once the nurses walked out, union officials argued that trying to stop the scabs would be "unprofessional." Instead, the local organized rallies and candlelight vigils as strikers were encouraged to find part-time jobs for the duration of the strike.<p>
This was necessary because the USW does not give workers strike benefits for the first four weeks of a strike--and after that a paltry $100 per striker would have been disbursed on the basis of need. <p>
As a result, picket lines were thin most of the time. And despite a high level of community support for the strike, union officials did little to build on this support. Now, to address unresolved health care-related issues, union officials are asking everyone to switch out of RWJUH's health care plan. <p>
Some nurses fear that the missteps of the USW have affected members' attitude towards unionism itself. But many are determined to build on the spirit of solidarity that they experienced, and to strengthen their union for future struggles.<p>

