<font face="Times New Roman, Times, serif" size="4">Lawyer faces federal prison</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Part of a drive to silence dissent </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 22, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 16, radical lawyer Lynne Stewart will find out whether a federal judge will take the advice of the government and sentence her to 30 years in prison.<p>
Last year, Stewart, along with two co-defendants, was convicted of five counts, including "materially aiding" terrorists and violating special prison administrative regulations. The charges stem from her role as a defense attorney for Sheik Omar Abdel Rahman, a Muslim cleric convicted in 1995 of conspiring with followers in the Islamic Group to bomb several New York City landmarks.<p>
In 2000, Stewart read a press release to a Reuters reporter in Cairo detailing Abdel Rahman's withdrawal of his personal support for a ceasefire between the Egyptian government and the Islamic Group. It was a legal strategy designed to keep Abdel Rahman in the public eye, and the government admits that no violence ever resulted from Stewart's actions.<p>
Yet almost two years later, then-Attorney General John Ashcroft--using the September 11 attacks as an excuse--indicted Stewart as part of the Bush administration's "war on terror."<p>
The trial that followed was a travesty, as federal prosecutors went out of their way to paint Stewart as a supporter of terrorism, even going so far as to play videotapes of Osma bin Laden in the courtroom, which was located less than a mile away from the site of the World Trade Center.<p>
Stewart was convicted on all counts, but the government witch-hunt didn't stop there. Earlier this month, federal prosecutors submitted sentencing recommendations to the court that asked for Stewart--a 66-year-old grandmother who was diagnosed with cancer last year--to receive 30 years in prison, effectively a life sentence.<p>
In the 129-page sentencing recommendation, Assistant U.S. Attorney Andrew Dember said that Stewart "deserves to be severely punished" for "blatantly and repeatedly violating the law." <p>
But in the government's eye, Stewart's real crime was standing up for the rights of her client. As the government admitted in its sentencing report, "We hope that this sentence of 30 years will not only punish Stewart for her actions, but serve as a deterrent for other lawyers who believe that they are above the rules and regulations of penal institutions or otherwise try to skirt the laws of this country."<p>
Activists are planning to send a message to the government that they can't get away with using Lynne Stewart as a scapegoat. The Lynne Stewart Defense Committee is asking the supporters gather outside at the courtroom of Judge John G. Koeltl on both September 25 and October 16, when Lynne's sentence will finally be handed down.<p>
<i>Supporters are asked to gather at 10 a.m. on both September 25 and October 16 at the courtroom of Judge John Koeltl, 500 Pearl St., Courtroom 12B, New York City</i>.<p>

