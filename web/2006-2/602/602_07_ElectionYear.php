<font face="Times New Roman, Times, serif" size="5"><b>Election-year war on immigrants</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 22, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH THE November election hanging in the balance, politicians from both parties are jostling for votes on this year's key "wedge" issue. "Securing our borders" is a vague phrase that teeters conveniently between the "patriotic" virtues of cracking down on Latino migration and vilifying Arabs and Muslims in the name of fighting terrorism.<p>
Congress' antics reached full swing on September 14, when the House of Representatives voted 283-138 for yet more sweeping border control measures, including the construction of a 700-mile wall along the U.S.-Mexico border--a meaningless provision, since it was already included in the notoriously repressive H.R. 4437 bill passed last December. <p>
But lawmakers seemed eager to demonstrate that, as the <i>Los Angeles Times </i>commented, "bipartisan support exists for significantly toughening border security--especially as the November election nears."<p>
California Sen. Dianne Feinstein, in a nod to her xenophobic supporters, announced that she was "delighted" that the House included a provision to criminalize the construction of border tunnels for transporting immigrants, while noting that she has proposed a similar measure in the Senate. Looking over her other shoulder at her agribusiness backers, Feinstein also mouthed disappointment that the bill did not include a guest-worker program for farmworkers.<p>
This is not the first election year in which opportunist politicians have whipped up racism against immigrants as a campaign ploy. In October 1996, just weeks before his re-election, Bill Clinton signed the Illegal Immigration Reform and Immigrant Responsibility Act (IIRAIRA). <p>
IIRAIRA is notable for its multifold expansion of deportable immigrant "criminals"--including those convicted of an "aggravated felony" or an act of "moral turpitude." <p>
But under Clinton's watch, the crime of shoplifting rose to aggravated felony status. Crimes of moral turpitude suddenly included jumping a subway turnstile. One Cambodian man was designated a "sex offender" for the crime of indecent exposure after urinating on a construction site.<p>
This Clinton-era legislation also targeted legal immigrants, deported for offences committed decades earlier--even if the crime was then defined as a misdemeanor, not a felony, and the immigrant had already served time for it. By 1997, the number of detained immigrants nearly doubled from the 1996 figure of 8,500. <p>
Many of the provisions credited to the Bush Administration's USA PATRIOT Act were included in the 1996 legislation. Most notably, the Clinton-era law, not the Patriot Act, first required detention during deportation proceedings, while imposing no time limit on the duration of detention. The Patriot Act went on to authorize the indefinite detention of any non-citizen suspected of suspected "terrorist" activities.<p>
Deportations have since risen to 200,000 in 2005, according to the Department of Homeland Security. Nearly 1.4 million immigrants have been deported since 1996.<p>
To demonstrate its resolve, the Department of Homeland Security's Immigration and Customs Enforcement (ICE) has moved into overdrive. In its "Operation Return to Sender" exercise, ICE has arrested and deported thousands of immigrants since June.<p>
On September 7 and 8, ICE arrested 107 immigrants in Northern California, which ICE described as "criminal aliens, foreign nationals with final orders of deportation, and other immigration violators." Yet only 19 of the 107 people arrested had prior criminal convictions. Most had arrived in the U.S. as children. <p>
Immigration officials refused to allow Mohammad Ramadan Hassan Salama, an Egyptian-born professor at San Francisco State University, to return to his wife and children after he traveled to Canada in June to update his temporary scholar visa. <p>
Officials told Salama that his name was similar to one of the men convicted in the 1993 World Trade Center bombings, Mohammed A. Salameh. "If we look for somebody with the last name McVeigh," the professor remarked, "should we just arrest them and harass them?"<p>
In early September, Georgia's Republican Gov. Sonny Perdue--up for re-election--pledged a statewide crackdown on undocumented immigrants. Within days, federal agents descended on the small town of Stillmore, rounding up more than 120 immigrants for deportation. <p>
Stillmore Mayor Marilyn Slater observed, "This reminds me of what I read about Nazi Germany, the Gestapo coming in and yanking people up."<p>


