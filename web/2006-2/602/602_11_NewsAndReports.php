<font face="Times New Roman, Times, serif" size="4"><b>Stop the raids</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kenny Gramsky and Owen Goodwin</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--Immigrant rights activists here organized a rapid response to the recent spate of sweeps by Immigration and Customs Enforcement (ICE) agents.<p>
Within five days of the first reports of the stepped-up raids, more than 100 people showed up for an emergency community meeting called by a diverse group of activists, including the Watsonville Brown Berets, ANSWER Youth and the International Socialist Organization. Speakers included neighbors of those arrested, naturalized citizens of various origins, local schoolteachers and community activists. <p>
A second meeting is planned to implement a network of "safe houses"--churches and community centers where members of affected communities and those seeking information about their rights can visit. There was also discussion of pushing Santa Cruz and Watsonville city councils to stop working with ICE agents and declare these cities "sanctuary cities."<p>
With school starting this week, it will be important to draw in the many student immigrant rights activists at the University of California-Santa Cruz campus. The Brown Berets are also planning a September 24 rally in Watsonville.<p>

