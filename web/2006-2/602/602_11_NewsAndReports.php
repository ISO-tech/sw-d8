<p><a name="IR"></a><font face="Times New Roman, Times, serif" size="4"><b>March for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston and Derron Thweatt</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--About 5,000 people came to the National Mall September 7 to march for immigrant rights. The mobilization drew some participation from other cities on the East Coast and was called by the National Capital Immigration Coalition (NCIC).<p>
The mobilization was called around demands including legalization for all and halt to deportations. The turnout was similar to numbers at actions around the country over the Labor Day weekend, but far below the hundreds of thousands that organizers had anticipated.<p>
In the spring, NCIC was a prominent supporter of the McCain-Kennedy immigration bill, and the subsequent Senate compromise. Yet in the lead-up to this march, NCIC publications have argued against the Senate bill for dividing families and criminalizing undocumented workers.<p>
Jaime Contreras, president of NCIC, spoke against the bill from the stage. Also speaking were several Democratic politicians, including Sen. Ted Kennedy (D-Mass.) and Rep. Luis Gutierrez (D-Ill.).<p>
In contradiction to the demands of the march, Kennedy declared that the Senate "passed a god bill." This praise is for legislation that would build a border wall and deport millions who have been in the U.S. less than two years.<p>
Other Democratic speakers at the rally and NCIC made sure to continuously emphasize voting in the midterm elections in November.<p>
There were also messages of solidarity. Bruce Gordon, president of the NAACP, spoke about the need for unity between Blacks and Latinos. "They want us to fight over the crumbs," he said. "We don't want the crumbs, we want a seat at the table."<p>
The speakers' list also included powerful testimony from local day laborers, and from the campaign to organize the Smithfield meatpacking plant in North Carolina.<p>
In spite of its size, chants on the march were quite spirited. The crowd was peppered with homemade signs, banners and T-shirts against deportations. Many on the march were aware of the struggle of Chicago immigrant rights activist Elvira Arellano, who is fighting back against being deported to Mexico.<p>
After the demonstration, questions remained about why the turnout had been so far below organizers' expectations. There were various factors, including the amount of mobilization at the grassroots level, the lack of clear demands for the march, the stalling of immigration legislation in Congress and the emphasis by many immigrant rights groups on voter turnout in November rather than activism.<p>
These are issues that activists will need to debate in charting a way forward for the movement. <p>

