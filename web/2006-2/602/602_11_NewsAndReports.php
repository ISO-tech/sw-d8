<p><a name="Burge"></a><font face="Times New Roman, Times, serif" size="4"><b>Prosecute police torture in Chicago</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Julien Ball</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--About 150 people marched through downtown September 15 to demand justice for victims of police torture under former police Commander Jon Burge.<p>
In July, special prosecutors Edward Egan and Robert Boyle released the findings of their four-year, $7 million investigation into the torture. Their report acknowledges that Burge and his underlings horrifically abused people in their custody, using beatings, suffocation and electroshock to elicit "confessions."<p>
But it allows these brutal cops to skate once again, accepting their claims that the statute of limitations on their crimes has expired. Meanwhile, many of their victims remain behind bars. <p>
Many of the demonstrators carried signs denouncing Burge, Mayor Richard M. Daley and Cook County States Attorney Richard Devine. When much of the torture happened, Daley was then Cook County State's Attorney, and Devine his top assistant. They both turned a blind eye to documented instances of torture.<p>
The demonstration, organized by a new group, Black People Against Police Torture (BPAPT), brought a largely African American but multiracial crowd to protest the torture of over 190 Black victims by racist cops. Other organizations in attendance included the Campaign to End the Death Penalty (CEDP), 8th Day Center for Justice and the Chicago Coalition to Protect Public Housing. <p>
The march was important as a means for people to express their anger, but this sentiment unfortunately was muted, as the event was billed as a silent demonstration. <p>
Still, activists with BPAPT plan to continue organizing to introduce a reparations bill for torture victims and to strip Burge of his taxpayer-funded pension and legal fees. Meanwhile, the CEDP, alongside family members of the torture victims, is keeping up the pressure to demand new trials for those still behind bars.<p>

