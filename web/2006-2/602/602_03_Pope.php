<font face="Times New Roman, Times, serif" size="5"><b>The pope's racist rant about Islam</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">September 22, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">POPE BENEDICT XVI angered Muslims around the world when he used a recent address at the University of Regensburg to publicly attack Islam. The pope quoted 14th-century Byzantine Christian Emperor Manuel II Paleologus: "Show me just what Muhammad brought that was new, and there you will find things only evil and inhuman, such as his command to spread by the sword the faith he preached."<p>
Coming amid George W. Bush's self-described "crusade" against terrorism, the U.S. occupations of Iraq and Afghanistan, and escalating threats of a war on Iran, the pope clearly took sides in the war on Muslims. <p>
After reactions of outrage, Benedict said that the passage he read "doesn't express in any way whatsoever my personal opinion." <p>
That's hard to believe. Benedict's life, both in and out of the Church, has been one long story of intolerance. As a teen in the early 1940s, Benedict was both a member of the Hitler Youth and a helper on a Nazi anti-aircraft unit.<p>
While a cardinal, his ultra-conservative views on Church doctrine earned him the nicknames "Cardinal Enforcer" and "God's rottweiller." He was a driving force behind the canonization of Jos&eacute;maria Escriva--an unrepentant fascist who served in the government of Spain's dictator Francisco Franco.<p>
The pope's statements against the use of violence to spread religion are especially hypocritical considering his former role as head of the Vatican office of the Congregation for the Doctrine of the Faith--formerly known as the Inquisition, the office responsible for the torture and death of Jews, Muslims and other "heretics" during the Middle Ages.<p>
As for religious violence, the Catholic Church has a long and horrific history--whether it was using brutality to spread its doctrine (and loot foreign lands) during the Crusades, or inciting prejudice and violence against Jews and other religious minorities.<p>
More recently, the Church supported--or at least took an ambivalent or bloody attitude toward--the most repressive and brutal dictators, including the Nazi Party under Hitler and Franco's regime in Spain. And Benedict has been part of a Church hierarchy that has resisted calls to take responsibility for this collaboration.<p>
The mainstream media are compounding the bigotry of the pope's remarks, focusing on angry protests of Muslims and some possibly related attacks on Christian churches in the Middle East.<p>
But at a time when millions of Arabs and Muslims are suffering both the threat and reality of war, occupation and repression, Benedict's remarks must be seen as a calculated attempt to incite prejudice. Those remarks--not the justifiably angry response of Muslims--deserve to be condemned.<p>

