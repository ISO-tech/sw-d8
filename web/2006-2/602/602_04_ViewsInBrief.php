<p><a name="Church"></a><font face="Times New Roman, Times, serif" size="4"><b>What Churchill really said</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS pleased to read Dana Cloud's defense of Ward Churchill, who has been under attack by the right wing for his words on the September 11 attacks ("Why is the right after Ward Churchill?" August 11). However, Cloud wrote Churchill was wrong to compare "workers at the World Trade Center to Nazi functionaries, or 'little Eichmanns.'"<p>
Churchill actually did not compare World Trade Center workers to the fascist Adolf Eichmann. In a press release, Churchill wrote, "It should be emphasized that I applied the 'little Eichmanns' characterization only to those described as 'technicians.' Thus, it was obviously not directed to the children, janitors, food service workers, firemen and random passers-by killed in the 9/11 attack."<p>
Elsewhere in the press release, Churchill points out the CIA office that was located in the World Trade Center, who it seems Churchill is referring to as the "little Eichmanns." The CIA is responsible for overthrowing, and attempting to overthrow, democratically elected governments all over the world, and has also brought drugs into this country. I'll leave it to the reader to decide what nickname the CIA deserves.<br>
<b>Dominic Renda,</b> New York City<p>

