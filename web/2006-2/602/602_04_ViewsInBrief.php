<font face="Times New Roman, Times, serif" size="4"><b>Why the levees broke</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I, AND I'm sure many other New Orleanians, appreciate the continued coverage of New Orleans' plight in <i>Socialist Worker</i>. <p>
There's one point I hope to clear up regarding Cindy Beringer's review of <i>When the Levees Broke</i> ("It's like they let it happen," September 1). The article reads: "As Lee's title reminds us, it was not Katrina that destroyed New Orleans, but the flood caused by the breaching of the levees. Computer mock-ups had long predicted that the levees would not hold up in a severe hurricane, and recently the Army Corps of Engineers was forced to admit to human error in the levee system designed for the city."<p>
New Orleans' floodwalls were only supposed to protect the city up to a Category Three hurricane. It's now agreed that Katrina was a powerful Category Three storm when it made landfall. But Katrina missed New Orleans. The winds and surge that destroyed the 17th Street and London Avenue canal floodwalls amounted to Category One force. <p>
What New Orleanians have now come to understand is that our "Category Three" floodwalls were really just a farce. Many of our "floodwalls" were, in fact, just concrete slabs stuck into the ground and made to look like functioning floodwalls. This amounts to total fraud on the part of the Army Corps of Engineers. <p>
I don't think any of the predictions referenced in the article predicted that those floodwalls would have failed under the conditions that they did. The suffering, death and destruction of New Orleans were not the result of a natural disaster, but the result of a crime carried out by the federal government on its own citizens.<br>
<b>Will Buckingham,</b> From the Internet<p>

