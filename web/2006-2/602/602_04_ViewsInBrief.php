<p><a name="Free"></a><font face="Times New Roman, Times, serif" size="4"><b>Freeing the innocent from prison</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM proud that my state government has created the North Carolina Innocence Inquiry Commission. This group will look at new evidence brought by people found guilty by the courts. <p>
Thanks to DNA testing and prosecutorial misconduct, many people now realize that some prisoners that say they are innocent may actually be telling the truth. I would like to encourage the other 49 state governments to create similar commissions in their states.<br>
<b>Chuck Mann,</b> Greensboro, N.C.<p>

