<p><a name="Beast"></a><font face="Times New Roman, Times, serif" size="4"><b>Taking on the beast of Bentonville</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I TOTALLY agree with the article on "Wal-Mart's drive to keep wages and benefits low" (September 8). As a member of the United Food and Commercial Workers, I have been close to some of the actions against Wal-Mart. <p>
I feel that the publicity campaign has been effective, as far as it goes. Picket actions have been effective to the extent that Wal-Mart has not opened a prospective store in various locations. I know of one such example in Canada. Strong union states have fewer Wal-Marts. The company concentrates in right-to-work states. <p>
One problem is that four in 10 people having a negative view of the company is not enough negative image to impact the world's biggest retailer. <p>
Many people shop at Wal-Mart even knowing that the company pays minimum wage and minimum health benefits. In many cases, Wal-Mart prices are lower. In small towns, they have driven the smaller retailers out of business. The public has to shop at Wal-Mart. We must picket even in the small towns to drive them out of town. Keep up the fight!<br>
<b>Lance LaVenture,</b> from the Internet<p>

