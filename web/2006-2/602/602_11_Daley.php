<font face="Times New Roman, Times, serif" size="5"><b>Chicago mayor vetoes "big box" living wage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 22, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Mayor Richard M. Daley exercised veto power for the first time in his 17 years as mayor last week to veto recently passed living-wage legislation covering big-box retail chains.<p>
The veto overturns a July 26 decision of the Chicago's City Council, which voted 35-14 in favor of an ordinance requiring big-box retailers to pay their workers $10 per hour, plus another $3 per hour in benefits, by 2010. This ordinance would provide a significant improvement over the $5.15 federal minimum wage and even the $6.50 hourly state minimum.<p>
This living-wage victory was the fruit of several years of labor by a broad coalition of community, labor and religious organizations. The Chicago Federation of Labor, Chicago ACORN and SEIU were among 30 or so other groups who knocked on doors, lobbied aldermen and held rallies and vigils to build support for the wage increase.<p>
Days after the vote, Target, Wal-Mart and Lowe's each announced that they were canceling plans to open several new stores in Chicago because of the ordinance--clearly a bluff. But this gave Daley some political cover. He also race-baited the unions, falsely claiming that labor failed to challenge big-box pay levels until the stores moved into the city's African American neighborhoods. <p>
On September 11, 30 minutes before the deadline, Daley registered his veto. The City Council vote to overturn the veto took place two days later.<p>
Hundreds turned out at City Hall in protest, many toting signs reading "Shame on you, Mayor Daley." All the seats in the chamber gallery were occupied when the meeting began. The 31-18 decision fell three votes short of the 34 needed to override the mayoral veto. Aldermen Solis, Cardenas and Coleman switched their votes after arm-twisting by Daley.<p>
Ald. Joe Moore, sponsor of the big-box ordinance, introduced the measure to override Daley's veto, and vowed to fight on even if it failed. "I can assure you this issue will not go away," Moore told the council.<p>
Unions, community groups and others plan to keep organizing to bring a living wage to the city's big retailers.<p>

