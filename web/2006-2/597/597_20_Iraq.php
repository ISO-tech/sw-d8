<font face="Times New Roman, Times, serif" size="4">Generals admit threat of civil war in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How the U.S. caused Iraq's nightmare</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 11, 2006 | Page 20</font><p>

<font face="Times New Roman, Times, serif" size="3">"SECTARIAN VIOLENCE probably is as bad as I've seen it, in Baghdad in particular," Gen. John Abizaid, the head of U.S. Central Command, told a Senate hearing on August 3.<p>
Saying out loud what most Iraqis have known for months, Abizaid continued, "It is possible that Iraq could move toward civil war." Gen. Peter Pace, chair of the Joint Chiefs of Staff, agreed.<p>
Meanwhile, the British press was trumpeting a leaked memo from the outgoing British ambassador in Baghdad, William Patey, to Prime Minister Tony Blair, which states, "The prospect of a low intensity civil war and a de facto division of Iraq is probably more likely at this stage than a successful and substantial transition to a stable democracy.<p>
"Even the lowered expectation of President Bush for Iraq--a government that can sustain itself, defend itself and govern itself and is an ally in the war on terror--must remain in doubt." The next five to 10 years, Patey wrote, will be "messy and difficult."<p>
These reluctant admissions from the men who oversaw the U.S.-led war and occupation of Iraq highlight the scale of the disaster Washington has caused in Iraq.<p>
Yet the Pentagon's next step in the occupation is certain to make the situation even worse for Iraqis. According to press reports, the U.S. deployed 3,700 <i>more </i>troops to Baghdad last week, from elsewhere in Iraq.<p>
"The move reflects genuine concern in Washington about the stability of the weak government of Prime Minister Nuri al-Maliki--even whether the secure Green Zone that houses the new Iraqi government and U.S. officials can hold," reported <i>Newsweek, </i>citing an anonymous Pentagon official. "It's now the yellow zone, not the Green Zone," defense expert Andrew Krepinevich told the magazine.<p>
Violence and fear are the everyday reality for the Iraqi population outside the Green Zone, where on average 100 died every day during May and June, according to a recent United Nations (UN) report--and where some 3 million people have been displaced since the invasion began in 2003<p>
The U.S. presence in Iraq--pitting Shia, Sunni and Kurdish Iraqis against one another--has fanned the flames of sectarian violence. From the beginning, the U.S. selected members of the Iraqi Governing Council to highlight sectarian divisions.<p>
Now, the Bush administration says it has to increase the troop level because of this unrest. But U.S. forces are the problem, not the solution in Iraq--as a steady series of headlines exposing atrocities committed against Iraqi civilians show. On August 3, U.S. troops fired on a convoy of supporters of Shia leader Moktada al-Sadr at a checkpoint south of Baghdad, wounding at least 16 people.<p>
The anger focused at the U.S. occupiers and anyone who supports them is easy to understand.<p>
The unraveling of the U.S. occupation has led some prominent Democrats to openly criticize the Bush administration. Sen. Hillary Clinton, for example, called on Donald Rumsfeld to resign last week during the defense secretary's reluctant appearance before the Senate Armed Services Committee.<p>
"I have never painted a rosy picture," said a nervous Rumsfeld. "I have been very measured in my words. And you'd have a dickens of a time trying to find instances where I've been excessively optimistic."<p>
Not such a dickens, actually. In July 2003, Rumsfeld appeared before the same committee and said, "The residents of Baghdad may not have power 24 hours a day, but they no longer wake up each morning in fear wondering whether this will be the day that a death squad would come to cut out their tongues, chop off their ears or take their children away for 'questioning,' never to be seen again."<p>
Death squads are as much a threat to Iraqis as they were under Saddam Hussein. Except now, the U.S trains them.<p>
Despite Clinton's strong words with Rumsfeld, she and the Democrats--who largely endorsed Bush's invasion three years ago--are no more interested in loosening their grip on Iraq than the Bush administration. They just want to manage the occupation differently.<p>
Washington's hold on the Middle East is falling apart in the face of fierce resistance to occupation--from Iraq to Lebanon. The militaries of the U.S. and its chief ally, Israel, armed with the most sophisticated weapons of mass destruction the world has ever known, are finding that they may have overwhelming firepower, but they can't silence resistance.<p>
Antiwar activists need to demand the immediate and complete withdrawal of U.S. troops from Iraq--and make the connections to the struggle to stop Israel's terror against Lebanon and Palestine.<p>

