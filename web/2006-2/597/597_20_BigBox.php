<font face="Times New Roman, Times, serif" size="4">A victory in Chicago</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Big box" living wage law passes</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 11, 2006 | Page 20</font><p>

<font face="Times New Roman, Times, serif" size="3">MEMBERS OF the Chicago City Council resisted high-pressure tactics from powerful corporations and passed an ordiance requiring a living wage at so-called "big box" retail stores.<p>
The 35-14 vote is a victory for unions and activists who organized a grassroots campaign supporting the measure. But Mayor Richard Daley, echoing the arguments of the retail giants, is threatening to use his veto power--for the first time in his years in office--to strike down the law, and corporations are vowing a legal challenge.<p>
In particular, Target, the mega-retailer with the most stores in Chicago, is threatening to close down its stores and abandon the city. "It's a corporate strategy by Target and whoever else to try to hold a gun to people's heads and say, 'If you don't back off, this is what we're going to do,'" said Chicago Federation of Labor President Dennis Gannon. <p>
Under the new law, companies with $1 billion in annual sales and 90,000 square feet of retail space in their stories will be required to pay workers a living wage of $10 an hour, plus $3 an hour in benefits, and schedule them for at least 10 hours of work per week by July 2010. This is significantly above Illinois' minimum wage of $6.50 an hour and about twice the federal minimum wage of $5.15. <p>
The ordinance is specifically aimed at highly profitable retailers like Target, Wal-Mart, Sears, Home Depot and Bloomingdale's. These corporations pulled out all the stops to block the living wage law, financing a last-minute smear campaign against the measure, complete with robotic phone-bank calls urging residents who "support jobs" to call their aldermen and demand a "no" vote.<p>
They were supported by an alliance of African American aldermen, joined by ministers from the South Side, who argued that rejecting the "big box" ordinance would bring jobs into impoverished sections of city. Wal-Mart, for example, has announced it will open 10 new stories in run-down areas of the city--the first is now being finished in Lawndale. <p>
But Ald. Joe Moore, the main sponsor of the ordinance, compared the "big box" law to similar legislation passed in San Francisco and Santa Fe, N.M.--where, he said, big box retailers are expanding. "Why do big box stores continue to operate in Santa Fe and San Francisco, and why are more stores opening up?" he said. "Because there's a buck to be made--a lot of bucks," he said.<p>
Ald. Ed Smith agreed. "I've heard these scare tactics before," he said. "There are a lot of dollars in Chicago. The big boxes don't have any other place to go because they've saturated suburbia. Chicago is a huge market. They don't want to leave those dollars to someone else." <p>
Likewise, Gannon told reporters he has trouble believing Target would turn its back on $58 million in city subsidies at the North and South Side projects under construction.<p>
But he admitted that Daley would probably be able to attract the two votes he would need to make a veto stick. That means the fight for a living wage in Chicago isn't over.<p>

