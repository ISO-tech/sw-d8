<font face="Times New Roman, Times, serif" size="5"><b>Oaxaca struggle grows </b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 11, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ANTHONY LeBEAU and AFSANEH MORADIAN report from Oaxaca.</b><p>

A TEACHERS' strike in southwestern Mexico that led to bloody clashes with riot police and mass solidarity demonstrations in June is continuing--and broadening its demands.<p>
Some 70,000 public school teachers in the state of Oaxaca went on strike May 22 and occupied the z&oacute;calo, the main city center, in the capital of Oaxaca City to press their demands.<p>
In mid-June, close to 3,000 state police with riot shields and clubs stormed the teachers' encampment, brutally beating strikers. According to eyewitness reports, 11 people died in the assault, including two children asphyxiated by the tear gas. But the strikers fought back, retaking the z&oacute;calo later in the afternoon. In the aftermath, huge demonstrations of hundreds of thousands marched through Oaxaca City in support of the teachers.<p>
Three weeks ago, the teachers union joined with 38 civil organizations to form a new alliance-- the Asemblia Popular de Pueblo de Oaxaca (APPO)--to press forward with their struggle. The alliance is demanding the resignation of Oaxaca's Gov. Ulises Ruiz Ortiz of the right-wing Institutional Revolutionary Party.<p>
On August 1, 3,000 women, banging pots and pans, marched in a protest against the company that owns the local pro-government television and radio stations. After protesting inside the company's headquarters, they began an occupation that continues today, with the protesters broadcasting political documentaries on television 24 hours a day and playing revolutionary music on the radio stations.<p>
Later in the week, demonstrators blockaded at least four government buildings, including the secretary of finance, using commandeered governmental vehicles for barricades. Government buildings have been taken over in 20 other towns.<p>
APPO also called for a boycott of Guelaguetza, a festival for tourists that celebrates the different cultures in Oaxaca. This succeeded in shutting down the first performance and making the second free.<p>
APPO has given Ulises a deadline to resign and plans to continue barricading government buildings until it wins its demands. "Workers are the foundation of the actions taking place," teacher Francisco Guerro said. "We need to continue in this manner and go forward if our demands aren't met. We should take the airport and the oil refinery so that Ulises falls." <p>

