<font face="Times New Roman, Times, serif" size="4"><b>A new steps for immigrant rights activists</b></font><p>

<font face="Times New Roman, Times, serif" size="3">JOE ALLEN'S article provides a useful framework for urgent discussions in the new (immigrant) civil rights movement ("The civil rights movement after Montgomery," July 28). <p>
The Montgomery Bus Boycott was a watershed event whose significance transcended the specifics of the struggle. It showed a generation that the sentiment existed for militant mass movements, opening the door for the 1960s. But it took three years of damaging backlash before those movements began.<p>
Similarly, last spring's gigantic immigrant rights movement, culminating on May 1, polarized the country. The Minutemen and allied forces have since gone on the offensive, but no clear next steps on the immigrant workers' side have yet taken shape. <p>
There is no need to wait three years. Socialists and other small forces cannot summon millions back into the streets at will, but we can now begin to build out of the May 1st generation. <p>
In most cities nationwide, thousands of people (at least) participated in protest, strike, or boycott action in the spring. Even small organizations can find dozens of people looking for next steps. <p>
Activists should think about viable grassroots campaigns on this scale. We can organize to set up day labor centers, for municipal laws to protect the undocumented, to protect immigrant workers from firings or deportations, against anti-immigrant crackdowns or vigilante activity, or a hundred other locally specific things.<p>
Now is the time for the left to raise its expectations, to experiment, be bold, and not to fear mistakes. Given the collective memory of May 1, local victories are possible and on the agenda. Taking this next step means taking the new civil rights movement forward, and preparing ourselves to organize on the scale of May 1 again in the future.<br>
<b>Avery Wear,</b> San Diego <p>

