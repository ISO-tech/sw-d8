<p><a name="Tex"></a><font face="Times New Roman, Times, serif" size="4"><b>No justice in Texas</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MY SON'S daughter died at seven weeks old, on August 17, 2004. Since he was the last one to hold her when she stopped breathing, authorities filed capital murder charges on him. <p>
Bond was set at $250,000. Since we are a low-income family, there is no way we can bond him out of jail, where he has been since she died. <p>
My son had a previous charge on him of assault of a public servant, so because of this, hospital doctors and police automatically assumed he did this to his daughter, and they did not do any testing for anything else that could have caused this. <p>
I have six doctors' reports all stating that her injuries stemmed from the birth process, and also a picture of her when she was born showing a bruise on her face from delivery, the same side the injuries were on. <p>
Our doctors' reports were sent to District Attorney Randall Simms more than three months ago. His excuse for not reading them was that "he is too busy and doesn't understand the medical lingo." So therefore, my son sits in jail, waiting until something is done. <p>
I know there is nothing you can do, but I just wanted you to know how the judicial system works in the state of Texas. <p>
How many people are innocent because of the lack of knowledge of authorities? How many innocent people will be sent to prison for life or get the death penalty for being falsely convicted because of the lack of knowledge? There are thousands of articles on this. Why won't doctors and police and prosecuting attorneys read up on this? <p>
It is so unfair. In this state, you are guilty until proven innocent, and God knows how long it will take for the proof to come out, if that person is in jail waiting. It could be years--years of a life wasted waiting.<br>
<b>Donna Shaw,</b> from the Internet<p>

