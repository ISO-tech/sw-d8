<p><a name="Tali"></a><font face="Times New Roman, Times, serif" size="4"><b>Taliban not a step forward</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN RESPONSE to Andy Libson ("Afghans' right to resist," July 28), anti-imperialists unequivocally oppose the United States "eagle putting its talons on any other land," to quote Mark Twain. <p>
Yet we see that not every opponent of the U.S. is by extension an ally of oppressed people (think of the Soviet Union, for instance, or Osama bin Laden). Anti-imperialists also unequivocally support the right of all people to choose their own leadership in the struggle against imperialism, no matter how we may disagree with that leadership. <p>
Lebanon's Hezbollah is an excellent example of a resistance group whose right to resist we enthusiastically support notwithstanding their reactionary social philosophies. Why? Because Hezbollah's popular support is an expression of the popular will for self-determination against the U.S./Israel.<p>
The Taliban in Afghanistan fails this criteria. Despite fearing and hating the United States, most Afghans still don't support the Taliban's fundamentalism, intimidation tactics or drug trafficking. <p>
The Revolutionary Association of the Women of Afghanistan have said when the U.S. smashed the Taliban they replaced "one fundamentalist regime with another--as brutal and misogynist as the Taliban."<p>
The Taliban's victory today would be a setback for the U.S. empire without being a step forward for the world's people--just like Stalin's occupation of Eastern Europe was bad for U.S. empire and worse for Eastern Europeans. Until and unless the men and women of Afghanistan embrace the Taliban as a nationalist response to the U.S., then these has-been gangsters deserve only our derision.<br>
<b>John Green,</b> Hayward, Calif.<p>

