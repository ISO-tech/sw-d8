<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Guards who suppress free speech</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 11, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">RACIST OPPRESSION and the suppression of political speech were the order of the day as several of us recently were selling <i>Socialist Worker</i> at a Peter Pan bus terminal in Springfield, Mass.<p>
Two of us were talking with two African American Iraq war veterans. One denounced "class society" as the source of problems in the world, and the other described having been beaten by local police when he was 15 years old and called joining the Marines "the worst mistake of his life." <p>
Suddenly, two "TPA Protective Services Officers" approached. They quickly proceeded to search one of the veterans, handcuff him and escort him into the back of the bus station. <p>
When we asked the officers why they were taking this man into custody, they gave no answer. Their uniforms displayed no name tags, their badges had no numbers, and only one of them grudgingly told us his name. <p>
Racist police oppression in Springfield, a largely deindustrialized city with underfunded social services and all the resulting urban problems, including street crime, reflects a long-term pattern. <p>
After his friend had been taken into custody, the other veteran related that shortly after his return from Iraq, he had gone out with friends to celebrate. They were stopped by Springfield police, who told them a violent crime had occurred 30 minutes earlier on the other side of the city. The cops told them, he said, that they had been stopped because they "fit the profile." <p>
As we continued talking with him, one of the officers came back and angrily told us that he was tired of seeing us selling newspapers outside the bus station, and that we were "banned from Peter Pan's property." <p>
Executives at Peter Pan Bus Lines Inc., which manages the bus terminal, attempt to portray their heavy-handed approach to security as evidence that it takes its passengers safety and security "very seriously." <p>
However, the place feels like something out of George Orwell's <i>1984</i>. Nearly 20 security cameras dot the premises, providing 24/7 surveillance. Monitors providing real-time surveillance are located in an office that is also available for use by the Springfield police department. Even buses operated by the Pioneer Valley Transit Authority, which shares the terminal with Peter Pan Buses, are equipped with surveillance cameras and sound recording systems. <p>
The police actively patrol the station at least once each shift, and Peter Pan contracts with TPA Protective Services, a private security firm that provides three full-time and two-part time security officers. <p>
The officers don't appear to carry guns, but they do display batons and pepper spray. The door of their office in the bowels of the terminal reads: "Department of Homeland Security" (DHS).<p>
Security contracting is big business with DHS. According to information posted on its official Web site, the top 10 security contractors with DHS netted nearly $3.5 billion in fiscal year 2005. <p>
At Peter Pan Bus Terminal in Springfield, TPA Security is part of the DHS gravy train for Corporate America--a component of the same system that enriches companies like Blackwater in the occupations of Iraq and New Orleans. <p>
We subsequently learned that TPA had charged the African American veteran with trespassing and banned him from the property. Despite the racist arrogance of TPA and its assault on free speech, the struggle to organize a multiracial fightback for social justice in Springfield is not over. It is only beginning.<br>
<b>Liz Clinton, Mark Clinton and Yuval Sivan,</b> Springfield, Mass.<p>

