<font face="Times New Roman, Times, serif" size="4">Big trouble for Bush's leading Democratic enabler</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>End of "Joe-mentum"?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 11, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">LESS THAN a week before his Connecticut Democratic primary race against cable television exec Ned Lamont, Sen. Joe Lieberman received bad news. A Quinnipiac University poll showed him losing to Lamont by a wide margin--54 percent to 41 percent.<p>
The possibility of Lieberman's loss sent a shiver through Washington's insular political establishment, which saw opposition to Lieberman as akin to McCarthyism. "What we're seeing is an ideological purge," Marshall Wittman of the Democratic Leadership Council (DLC) told Matt Taibbi in <i>Rolling Stone</i>. "It's a national effort by the left to get rid of somebody they've decided to demonize...We have concerns about narrow dogmatism."<p>
If Lieberman loses, George Bush will lose perhaps his greatest enabler on the Democratic side of the Senate.<p>
Last November, when Democratic Rep. John Murtha shocked Washington with his declaration that the Iraq war was a disaster, Lieberman led the counter-attack, penning a <i>Wall Street Journal</i> op-ed article heralding "progress" in Iraq and calling for "staying the course." Lieberman criticized opponents of the war for undermining the president in time of war.<p>
This isn't really new for Lieberman. From the time he arrived in Washington in 1989, he has sought to curry favor with the right.<p>
With few real legislative accomplishments to his name, he was mostly known for his public religiosity and his sanctimonious speechifying against popular culture. He was an "adviser" to various right-wing front groups--from the Parents Television Council, dedicated to protesting sex and violence on TV; to the American Council of Trustees and Alumni, a group that hunts liberal and radical "bias" in universities.<p>
In 2002, he joined with that other "centrist," Sen. John McCain (R-Ariz.), as a co-chair of the Committee for the Liberation of Iraq, a Washington lobby advocating invasion.<p>
Lieberman once famously said that although he wasn't a neoconservative himself, some of his best friends were. He has supported school vouchers and "faith-based" social welfare, while opposing affirmative action and filibusters against conservative Supreme Court nominees.<p>
For this reason, conservative Washington has always loved him more than liberal Democrats, despite his 90-percent-plus liberal voting scorecard. The right could overlook his votes on liberal issues because Lieberman could always be counted on to give the Republicans cover at crucial moments.<p>
Before being tapped to run for vice president on the Democratic ticket in 2000, he was probably most well known for his 1998 Senate speech blasting Bill Clinton for his "immoral" affair with Monica Lewinksy. During his ill-fated run for vice president, Lieberman managed to lose a debate to the most uncharismatic figure in politics today--Dick Cheney.<p>
During the brief period when he chaired the Senate Commerce Committee in 2001-2002, he somehow managed never to launch a serious investigation against Enron Corp.--worrying instead about creating a "witch-hunt" atmosphere against the corporate crooks.<p>
All of this might be good enough reason to send Holy Joe off to a well-compensated career as a corporate lobbyist, but we shouldn't ready our pronouncements of "good riddance" just yet.<p>
For one thing, much of the Democratic establishment--both in Connecticut and in Washington--was working to save Lieberman's skin as this column was being written. These party leaders have so internalized the idea that the Democrats can't be painted as advocating "cutting and running" from Iraq that they're willing to spend time and money to save one of the most slavish defenders of Bush's disaster.<p>
What's more, Lieberman has already prepared to run as an independent against Lamont in the event he loses.<p>
A humiliating defeat in the Democratic primary may take the wind out of an independent bid. But if he goes through with it, he will be on political ground where he's most comfortable: appealing to "centrists" and conservatives while bashing Democrats.<p>
If Lieberman does lose on August 8, no one should lose sight of an important point: His stand on the war and his surrenders to the Republican agenda differ only in degree--rather than in kind--from those of the mainstream of the Democratic Party.<p>
Remember, the congressional leadership of the party and the majority of Democratic senators voted to authorize the war. And Lieberman's "centrist" posture that convinced the party poohbahs to make him their vice presidential nominee in 2000 is still very much the reining orthodoxy among Democrats.<p>
So even if Lieberman isn't back in Congress next year, you can be sure that other Democrats will be auditioning to take over his role as the Republicans' favorite "opponent."<p>

