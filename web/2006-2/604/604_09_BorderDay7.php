<p><a name="Juarez"></a><font face="Arial, Helvetica, sans-serif" size="2"><b>JUSTIN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The heard and unheard cries of Juarez</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">CIUDAD JUAREZ is a boom town--a scene of electric lights, moving parts and working hands--that sits just below <i>El Paso del norte</i>. <p>
Since NAFTA, the city has grown into an industrial behemoth. Corporate America has taken advantage of the free-trade treaty to pour money into the maquila zone--factories that assemble products for the U.S. market. While it has fewer maquiladoras than Tijuana, they are larger and employ more workers.<p>
Juarez now casts an immense shadow over its smaller and industrially sapped neighbor across <i>la l&iacute;nea</i>. Unlike San Diego and Tijuana, it is the cross-border dollars of Juarez that sustain El Paso's economy, not vice versa. <p>
Beneath the din of prosperity lies an ugly truth about Juarez: it eats its own children. The maquiladora industry has grown so wealthy off the sweat of migrant workers (usually from the southern regions of Mexico) that it operates as a sort of mini-state, dominating every aspect of life in the border region. <p>
The underpinning of the system is a legal--and extra-legal--repressive apparatus that keeps workers powerless and unable to form organizations that can challenge the $6 to $10 a day wages. This is the elephant in the room as we discuss conditions with local activists. <p>
There is a labor shortage developing, as workers vote with their feet and cross to the north to avoid the violence and poverty of everyday life. With the system predicated on Dickensian exploitation and Orwellian repression, the maquiladora workers very right to live has been called into question by predators of every stripe. <p>
Ciudad Juarez is internationally known for its <i>desaparecidas</i>, as the capital of murdered and disappeared women. The bodies of 370 female workers (some as young as 13) have been discovered, bearing marks of rape and torture, while another 400 remain missing since 1993. <p>
Since laws protecting women from rape and abuse are either non-existent or rarely enforced in the state of Chihuahua and many other parts of Mexico, the cries of the victims go unheard. <p>
The cries of their loved ones left behind, on the other hand, are getting louder. One of the most potent forces in Juarez political life is a network of mothers of the disappeared, who are taking their struggle to international arenas. <p>
Despite their efforts, the local, state and federal governments continue to ignore or downplay the serial, sexual femicide, and there have yet to be any substantial convictions. <p>
Opening another front against the violence is Daniel Rocha, a former-maquiladora worker-turned-organizer who operates <i>Centro de Estudios y Taller Laboral </i>(Center for Worker Education), a small but feisty union movement involving veterans of some of Juarez's most significant strikes.<p>
It seeks to bring independent union organization and the concept of human rights to Juarez's workers. Despite the odds, Rocha is optimistic. "The people of Mexico are awakening," he says.<p>
Behind Rocha's desk is a whiteboard with a detailed breakdown of the day-by-day agenda of the upcoming Border Social Forum in Ciudad Juarez on October 13-15. The forum will bring together activists from all of the U.S. and Mexican border states, as well as immigrant and workers rights activists from elsewhere in the Americas.<p>

