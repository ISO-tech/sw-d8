<p><a name="People"></a><font face="Arial, Helvetica, sans-serif" size="2"><b>JUSTIN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The people vs. Samaniego</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">ONE BY one, they testified. Community activists from the Border Network for Human Rights approached the microphone to bear witness on behalf of El Paso's migrant community. <p>
We learn of a war that is taking place in the city's working-class districts, where the local sheriff-turned-vigilante is waging his own personal crusade against immigrants. Sheriff Leo Samaniego decided he would set up roadblocks in Latino communities to shake out the "illegals," meting out his own form of "frontier justice." <p>
With his pressed shirt, gold badge and sheepish grin, the septuagenarian sheriff looks the image of a benevolent crossing guard. But he's better thought of as another by-product of the racist backlash against immigrants, emboldened to take action against "the enemy."<p>
As a Texas lawman, he continues the tradition of the notoriously anti-Mexican Texas Rangers, who in their heyday "took matters into their own hands"--and lynched more than 5,000 <i>Tejanos</i>. <p>
Unfortunately for Samaniego, this is not the Texas of 1890. The El Paso of 2006 resides in a "sanctuary county," where local ordinances prohibit county agents, service providers or employees of any sort from engaging in immigration enforcement or requiring proof of citizenship for any transaction. <p>
Activists across the country are organizing to push for sanctuary codes, to counter hateful legislation and brazen acts of vigilantism by the likes of Samaniego. It's a Herculean effort given the level of opposition to immigrant rights that runs from the highest to the lowest echelons of government, but an achievable one. In El Paso, it has taken a sanctuary ordinance <i>and </i>a vigilant immigrant rights movement to ensure that the Minuteman-Sheriff is restrained. <p>
Because of this opposition, Samaniego has ceased the roadblocks, and after testimony at this county hearing, a majority of county commissioners reaffirm the rights of residents to live free of harassment. <p>
But the victory is bittersweet. As long as there is an open war on immigrants, "soldiers" like Samaniego will not go away, but wait in the wings for his next opportunity. <p>
