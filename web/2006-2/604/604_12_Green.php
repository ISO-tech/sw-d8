<font face="Times New Roman, Times, serif" size="5"><b>A Green alternative in Maryland</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 6, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE Maryland governor's race this fall, pro-corporate and anti-worker forces are uniting to maintain corporate control of our economy. The Republican incumbent, Robert Ehrlich, and his Democratic challenger, Baltimore Mayor Martin O'Malley, are both the darlings of big business, developers, many major religious institutions and the rich of all colors.<p>
Many progressives favor O'Malley as the "lesser evil," but the amount of animosity towards the mayor from working folks all over, especially in Baltimore, is staggering. A whopping 39 percent of registered Democrats say they won't even vote for him.<p>
But there is an alternative. For the first time in this state's history, the Green Party is mounting a serious challenge for the governor's race. <p>
Ed Boyd, an African American, disabled Navy veteran, an activist for the homeless (and formerly homeless himself) is running against the two major party candidates and their pro-corporate agendas. Boyd represents the activist wing of the party, fighting for social justice that is denied all working folks in this state and country.<p>
One of the biggest issues concerning voters is the utility rate hikes expected to be imposed soon by the regional monopoly Baltimore Gas and Electric (BGE). The Democratic-controlled Maryland legislature recently has ended rate caps and promised BGE a rate structure that could easily see hikes of over 129 percent after the November election.<p>
While both major gubernatorial candidates are firmly in BGE's pocket--having each received more than $70,000 in contributions from its parent company--Ed Boyd is calling for a state takeover of all private utilities (called "Marylandization"), restoring price caps and re-regulating the industry. As Boyd reminded supporters at a recent rally outside BGE headquarters, extreme rate hikes will force many people to choose between heating their homes this winter and buying the food or medicine they need to survive.<p>
Just as with utilities, O'Malley and Ehrlich favor letting the health care system remain in the hands of free-market fat cats, while Boyd supports universal health care and a single-payer plan. In addition, they totally ignore the needs of Baltimore's school children in favor of more affluent counties. Boyd demands that the state cough up the more than $1 billion for the schools that a court ordered several years ago and still hasn't paid.<p>
Although the mainstream press does its best to ignore him, reaction to the Boyd campaign is enthusiastic. So many people are looking for an alternative. African American voters, especially those least likely to see a difference in their lives next January when one of the corporate politicians is sworn in, are the most enthused. <p>
It may not be Maryland's time to elect a Green governor, but it is time for Marylanders to see that the Greens are no longer the stereotypical "80-year-old, tree hugging, wealthy, white, paternalistic liberal," but instead that there is a whole new breed of Green activists that offers a real alternative to politics as usual.<br>
<b>Myles Hoenig,</b> campaign manager, Ed Boyd for governor<p>
