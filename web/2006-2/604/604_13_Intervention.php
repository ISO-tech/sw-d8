<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is there a "good" U.S. intervention?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 6, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">THE TRADITION of anti-imperialism in the antiwar movement--principled opposition to U.S. intervention--is perhaps the weakest it has been in decades.<p>
The weakness expresses itself in a number of ways--for example, in liberal-progressive illusions in the Democratic Party, which is as committed to imposing U.S. power abroad as the GOP, but disagrees only when and where to strike first.<p>
It also manifests itself in a kind of grocery store approach--picking and choosing what U.S. interventions to oppose and which ones to support. This kind of selective antiwar sentiment accepts implicitly the chauvinist idea that there can be something positive about U.S. military invasions of other nations.<p>
There are those who argue, for example, that Iraq is the "wrong war" because it is a diversion from the legitimate "war on terror"--and thereby place themselves on the side of Bush's state terror, so long as it is inflicted judiciously and in the right places.<p>
This political approach automatically rules out, by definition, the right to self-determination of nations under attack or occupation by the U.S.--that is, to be free to determine their own future.<p>
Into this viewpoint creeps a kind of imperialist paternalism that criticizes, for example, the U.S. occupation of Iraq (where the U.S. has killed over the years more people than have died in the Darfur conflict) but calls on the U.S. to intervene in Sudan's Darfur region. It is as if somehow in this case, in defiance of all the others, Washington's humanitarian rhetoric <i>wouldn't </i>be a cover for regional conquest.<p>
It isn't simply that U.S. intervention in Sudan has already been disastrous for its people--witness Clinton's 1998 bombing of Al Shifa, a pharmaceutical plant that produced 90 percent of Sudan's major pharmaceutical products, causing untold number of Sudanese children to die from treatable diseases.<p>
There is also a fundamentally flawed logic to the position. The U.S. is apparently permitted to use force to submit Sudan to its will, on the grounds that Sudan's government must not be permitted to use force to impose its will inside Sudan. <p>
Apparently, the U.S. possesses a special dispensation to jackboot its way into different countries, a right it denies to all others but its closest allies, such as Israel. But if the Sudanese government is culpable for violently suppressing a rebel movement, wouldn't the Sudanese government be justified in its defense of Sudan against U.S. aggression?<p>
The only answer proponents of intervention can give is that they consider the U.S., at least in some instances, a legitimate global cop. This double standard is what has left the antiwar movement bereft of the kind of principled politics that would allow it to stand up consistently and strongly to U.S. aggression.<p>
A new, well-researched book on the U.S. occupation of Afghanistan, <i>Bleeding Afghanistan,</i> reveals these same weaknesses.<p>
The book relentlessly chronicles the way in which U.S. intervention in Afghanistan since the 1980s has killed thousands and destroyed the country, now carved up by repressive warlords who deny women's rights as much as their predecessors.<p>
Yet the book concludes that the U.S. cannot withdraw immediately because "the warlords and the still active Taliban movement will most certainly devour the country." The authors argue that the U.S. can only depart the country "after disarmament is complete and Afghans feel safe in their own country."<p>
So what begins promisingly as a strong case against U.S. intervention in Afghanistan ends with a call for the U.S. military to "pacify" the country.<p>
Yet pages after this assessment, the authors quote an Afghani women's rights activist who acknowledges that, "Many in Afghanistan are of the opinion that even the U.S. very trumpeted 'war against drugs and terrorism' and campaign to 'promote democracy' are bogus because the U.S. has forged a unity with the most infamous, anti-democratic, religious, terrorists and drug mafia forces in the history of Afghanistan."<p>
It is difficult to square this assessment with the idea that the U.S. should be asked to stay and promote peace in Afghanistan.<p>

