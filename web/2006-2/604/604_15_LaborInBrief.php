<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">October 6, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Woodfin Hotel</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Poly Manoli</b></font><p>
<font face="Times New Roman, Times, serif" size="3">EMERYVILLE, Calif.--Chanting "What do we want? Justice! When do we want it? Now!" and "&iexcl;S&iacute; se puede!" more than 50 activists rallied outside the Woodfin Hotel September 29 to support immigrant workers who are being intimidated by the management. <p>
Several community organizations and churches organized a rapid response to the recent harassment and intimidation by Woodfin Hotel management against immigrant workers who are trying to implement a local living wage called Measure C. <p>
In November 2005, Emeryville voters approved Measure C, which guarantees workers a living wage, job security when hotels are sold and reasonable workload limits. Emeryville's hotel workers--mostly immigrant women of color facing low wages, huge work loads and a lack of job security--hoped that Measure C would finally bring them fair pay and respect on the job. <p>
But while Measure C went into effect last December, the hotels have stubbornly refused to comply. And the Woodfin Hotel has repeatedly punished and threatened workers for discussing Measure C. <p>
Managers have given workers false information, held captive-audience meetings, grilled workers individually about their opinion of the law and fired a worker involved in Measure C outreach. <p>
In spite of this harassment, dozens of Woodfin workers have organized to stand up for their Measure C rights--educating other workers, presenting a petition to managers to demand compliance and speaking out before the Emeryville City Council.<p>
Now the workers are facing an even more severe threat. On September 27, managers gave workers 24 hours to re-submit work authorization documents and told them that anyone who refused or who was suspected of being undocumented would be fired. <p>
"This is the latest attack by Woodfin to retaliate against immigrant workers who are trying to raise their standard of living," Evelyn Sanchez, an activist with the Immigrant Workers Freedom Ride Coalition, told <i>Socialist Worker.</i> "It is clearly a tactic of intimidation."<p>
The workers targeted by the management are part of the committee to negotiate the living wage standard and are the most militant in a hotel that is anti-union. After an initial rally by some activists on September 28, management gave the workers two weeks' notice to get all the paperwork done. <p>
The city council, which is responsible for implementing Measure C, has dragged its feet, essentially giving a green light to corporations like Woodfin to victimize workers who fight for their rights under the ordinance. "Although the pay is $9.25 to $10.25 for house keepers, under this law the average wage all workers should get is around $11.00 an hour," said Sarah, a key organizer with the East Bay Alliance for a Sustainable Economy. <p>
"This law...could set a positive example for other cities to follow, and this is why Woodfin is fighting back," Sarah told the crowd. "There are going to be further retaliations for workers who were in support of the rally, so we need to step up the pressure and do what it takes."<p>
The good turnout for the emergency response rally--including workers from other unions who came to express their solidarity--shows that there's a willingness to stand up against fear and intimidation when there is community support.<p>
