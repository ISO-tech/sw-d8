<font face="Times New Roman, Times, serif" size="5"><b>Save public health clinics in Seattle</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 6, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 300 people demonstrated September 30 to save public health in King County.<p>
Democratic County Executive Ron Sims had proposed that two public health clinics that serve 80,000 poor people per year be closed. Under pressure from public health advocates, the day before the rally Sims amended his budget to keep the clinics open--but only through June of next year.<p>
The Washington Nurses Association, which represents public health nurses, and the International Federation of Professional and Technical Employees Local 17, which represents other public health staff organized the rally. They were well represented with yellow shirts from one clinic and red shirts from another, saying "Save Public Health."<p>
Patients and employees of the clinics explained how important public health is. "Public Health doesn't just serve the poor--it protects the whole community," said one speaker at the rally. "Well-off people think they're immune from public health problems but they're not. Epidemics can reach anyone, no matter their class."<p>
The priorities of the system were also a big issue for participants. "If the government can pay for the war on Iraq, they can spend the money needed for public health!" said one speaker.<p>
King County residents have paid over $3 billion in federal taxes for the war on Iraq. Keeping the clinics open would cost less than $3 million more for the rest of 2007.<p>
Aaron Dixon, Green Party candidate for U.S. Senate, called for a change in priorities. He discussed his experience in the Black Panther Party setting up a free health clinic in Seattle's Central Area and called for the government to fully fund health care.<p>
Organizers promise to keep the pressure up to demand full funding of public health in King County.<p>

