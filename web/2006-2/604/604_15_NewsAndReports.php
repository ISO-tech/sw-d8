<font face="Times New Roman, Times, serif" size="4"><b>Stop the Minutemen</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Osmand</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THOUSAND OAKS, Calif.--When Borders Bookstore hosted Minutemen founder Jim Gilchrist to sign his new book September 25, 30 people protested him, holding signs saying "No one is illegal" and chanting with bullhorns.<p>
Shouting "Shame on Borders" and "Racistas fuera! Racists go home!" activists rallied in front of Borders to condemn the vigilante Minutemen and Gilchrist's <i>Minutemen: The Battle to Secure America's Borders.<p>
</i>Gilchrist's visit to Thousand Oaks was the first stop in a book-signing tour at Borders that also included Mission Viejo in Orange County and San Diego. His book promotes civilian border patrols on the very cover, with a picture of a man surveying the desert with binoculars.<p>
Protest organizers felt that his dangerous anti-immigrant racist message must be challenged at every opportunity. Two local activists, Elliot and Carlos, initiated the protest.<p>
In a flyer advertising the rally, Elliot writes of Gilchrist, "We cannot allow them to scapegoat our communities for the consequences of our government's policies which lead to recession, war, and unemployment...We will not allow them to strut through our communities on this racist warpath. We will make them feel unwelcome and alien."<p>
By 2-to-1, protesters outnumbered supporters of the Minutemen, who had come to get a book signed. Gilchrist also did not speak to or take questions from the public.<p>
Francisco Romero, the Green Party candidate for Oxnard City Council, spoke about the 4,000 people who have died trying to cross the U.S.-Mexico border in the past 10 years.<p>
Protesters included folks from all over Ventura County and even Los Angeles. Organizations that supported the protest included the United Farm Workers in Oxnard, ANSWER Ventura County, Code Pink, International Socialist Organization, Somos Raza, Anti-Racist Action, All Power to the People Campaign, the May Day Coalition for Students and Workers Justice, and others. Many participants also signed a petition to make Oxnard an immigrant sanctuary.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Free Palestine</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Judd</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Columbia University students held a commemoration of the sixth anniversary of the start of the Al-Aqsa Intifada, when Palestinians rose up against generations of apartheid and a deceitful, backwards-moving "peace process."<p>
It was the first event organized by a new student coalition, Students for Justice in the Middle East, which came together just a few weeks earlier to oppose all imperial wars and occupations in the Middle East. Students held signs and Palestinian flags, wore keffiyehs and handed out informational sheets about the intifada to passersby. <p>
Though organized on short notice, it made a splash. Organizers made contact with numerous supporters, including Palestinians with personal experience of the Israeli occupation and a man who had participated in demonstrations that overthrew a U.S.-backed military dictatorship in South Korea in the 1980s.<p>
The event also got a hostile reaction from campus Israel apologists, including one man who tore up a "Free Palestine" banner. Another man started shouting, "Is anyone here a suicide bomber?" apparently believing that Palestinian activists are automatically terrorists. <p>
Arguments continued until campus security decided that the time for free speech was up. But the polarized reaction shows the importance of giving pro-Palestinian and marginalized Arab and Muslim people space for a confident defense of their cause--and of putting forward an organized opposition to the racist ideas that dominate mainstream debate of these issues.<p>

