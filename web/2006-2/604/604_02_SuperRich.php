<font face="Times New Roman, Times, serif" size="5"><b>Swelling fortunes for the super-rich </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 6, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>FORBES </i>MAGAZINE has published its annual list of the 400 richest Americans, and millionaires need not apply. <p>
"A nine-figure fortune won't get you much mention these days, at least not on these pages," <i>Forbes </i>reported. "This year, for the first time, everyone in the <i>Forbes</i> 400 has at least $1 billion. The collective net worth of the nation's wealthiest climbed $120 billion, to $1.25 trillion."<p>
In other words, just 400 people have a combined net worth equivalent to about 10 percent of the U.S. gross domestic product for a year. It's also "about the same amount of combined wealth held by the 57 million households who make up half the U.S. population," wrote left-wing economist Holly Sklar.<p>
A key reason for the billionaire bulge is the rise in profits as a share of the national income, while the portion going to workers' wages declined. Real wages have fallen despite the economic recovery that began in late 2001, with high gains in productivity going straight into the employers' pockets.<p>
Which means that the millionaires, despite being bumped off the <i>Forbes' </i>list, are doing just fine. "The wealthiest 1 percent of Americans (minimum net worth $6 million) owned 62 percent of the nation's business assets, 51 percent of stocks and 70 percent of bonds as of 2004, according to the latest data from the Federal Reserve Survey of Consumer Finances--which excludes the <i>Forbes </i>400," wrote Sklar. "That's way up from 1989, when the wealthiest 1 percent owned 54 percent of business assets, 41 percent of stocks and 52 percent of bonds."<p>
As usual, the <i>Forbes </i>list is headed by William Henry Gates III--better known as Bill--who's currently worth $53 billion. Just behind Gates is investor Warren Buffet, worth $46 billion. <p>
Five Wal-Mart heirs come next, their combined fortune of $82.5 billion built on the backs of their low-paid workforce. After that is Oracle Corp. CEO Larry Ellison, known for flying his $38 million private jet, sailing his $200 million yacht, and living in a replica of a Japanese feudal estate that cost $200 million to build.<p>
Others among the super-superrich have tried to create a more generous image. Warren Buffet, for example, made headlines a few months ago when he announced that he'd give away much of his fortune to the Bill and Melinda Gates Foundation for charitable work.<p>
Buffet, however, will keep all his money until he dies. And the Gates foundation makes contributions with lots of strings attached--ones that tend to benefit Microsoft.<p>
As journalist Lila Rajiva pointed out, the Gates' $100 million donation to combat AIDS and software donations to schools in India are aimed at capturing future license fees for Microsoft software. "The AIDS donation was a mere quarter of the moolah Microsoft was lobbing at the Indian computer market," she wrote.<p>
Meanwhile, U.S. workers are getting squeezed to boost the profits that created the billionaires' club. Some 24.5 percent of all workers in the U.S. are paid poverty-level wages--a figure that's actually increased<i> </i>as the recovery has continued. "The major development in the labor market in recent years has been the stunning divergence between rapidly rising productivity without a parallel rise in wages," the Economic Policy Institute (EPI) reported last month.<p>
Which means that accumulating even the most modest kinds of wealth--a house, for example--is out of the reach of tens of millions in the U.S. "[S]ome 30 percent of households had net worth below $10,000, and 17 percent had zero or negative net worth," the EPI noted. "In short, in 21st century America, wealth begets wealth, and those without wealth find it farther out of reach."<p>

