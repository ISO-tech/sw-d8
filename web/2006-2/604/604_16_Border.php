<font face="Times New Roman, Times, serif" size="4">Bipartisan vote for racist border wall</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Playing politics with immigrants' lives</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 6, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">IMMIGRANT-BASHERS cheered September 29 as the U.S. Senate voted 80-19 to approve construction of 700-mile high-tech wall on the U.S.-Mexican border.<p>
A few years ago, proposals for such extended fences were seen as the fantasies of hardline right-wingers like Pat Buchanan. But when the wall proposal came to a vote, 26 Senate Democrats lined up alongside Republicans to back what the Reuters news agency described as a "double-layered fence with access roads, lighting, cameras and sensors."<p>
As an editorial in the <i>Tucson Citizen </i>put it, "It is clear the idea of a multibillion-dollar barrier is being pushed now only so the Republican majority in Congress can claim it is addressing calls for border security. But the only thing members of Congress are serious about securing is re-election."<p>
The provision for extending the border wall was one part of anti-immigrant legislation--known as the Sensenbrenner bill, after its main sponsor, Republican Rep. James Sensenbrenner--passed by the House last December.<p>
That law was the main spark for the immense immigrant rights rallies this past spring, which effectively killed the chances of the Sensenbrenner bill passing the Senate.<p>
Nevertheless, the Senate version of immigration "reform" adopted many provisions from Sensenbrenner while adding a corporate-backed guest-worker program and a highly restricted "path to citizenship" for some undocumented immigrants. <p>
House Republican leaders in turn rejected the Senate bill for not being right wing enough. With the elections looming, they decided to pass parts of Sensenbrenner in separate bills, as both a sop to the Republicans' right-wing base and a challenge to Democrats to oppose them.<p>
The Democrats, eager to avoid any criticism from Republicans, caved once again. Thus, when House Republicans passed legislation on the wall, the Senate scrambled to follow suit before adjourning.<p>
The proposed fence is unworkable, according to veteran border patrol agents, who say that mountainous terrain will make construction almost impossible, let alone enforcement. Even the president of the Border Patrol officers' union, T.J. Bonner, hardly a friend of immigrants, said that the wall is "not going to deter people from coming across looking for jobs, people coming to work."<p>
Instead, those trying to cross the border will be forced to do so in ever more dangerous areas of the desert, adding to the death toll that has already skyrocketed since Operation Gatekeeper began during the Democratic Clinton administration.<p>
But on the border-wall vote, electoral calculations--that is, appealing to anti-immigrant racism--trumped all else. The GOP-controlled House passed several other parts of Sensenbrenner as well, including a bill to require voters to show photo identification to take part in federal elections and another that would deputize state and local police to enforce immigration laws.<p>
The Democrats tried to have it both ways. Senate Minority Leader Harry Reid called the border fence bill an effort to appease "the radical anti-immigrant right wing" of the Republican Party.<p>
But that didn't stop Senate Democrats from going along. "[I]f blowing billions on this symbolism is a sop to right-wing nuts," asked <i>Washington Post </i>columnist Sebastian Mallaby, "why did 26 Senate Democrats vote for the bill while only 17 opposed it?"<p>
Similar sentiments were voiced in newspaper editorials in the <i>Atlanta Journal-Constitution</i> and the <i>Dallas Morning News.</i> But these criticisms were mainly focused on Congress' failure to take up "comprehensive immigration reform"--a euphemism for the Senate "compromise" that will also make immigrants' lives worse.<p>
Senate Majority Leader Bill Frist, who orchestrated the border-wall vote, said he would support convening a lame-duck session of Congress following the November election to pass such "comprehensive" legislation.<p>
The bipartisan rush to build the border wall makes it clear that even a Democratic-controlled Congress won't stop bashing immigrants. Making real gains will depend--as it always has in U.S. history--on organizing a grassroots movement that can fight back.<p>

