<font face="Times New Roman, Times, serif" size="5"><b>Long overdue victory for the Hamouis</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Mobley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 1, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LONG ordeal of a Syrian-American family, caught up in the government's witch-hunt of Arabs and Muslims, is finally over. <p>
On August 15, Safouh Hamoui, a father of five, won permanent residency after a nightmarish four-year ordeal in which family members were thrown into federal detention, caught in a legal limbo and threatened with deportation back to Syria, where Safouh and his family fear being executed. "A true victory for the community," Safouh said after he got the news.<p>
The ordeal started on February 22, 2002, the first day of the Islamic holiday Eid. At 6 a.m., federal immigration and FBI agents raided the family's Seattle home, guns drawn. Safouh, his wife Hanan Ismail and their daughter Nadin (19 years old at the time) were put in detention. Safouh spent 10 months behind bars, and Hanan Ismail and Nadin nine months.<p>
The ordeal took a huge toll on the family. Safouh lost his business, a small Middle Eastern food market, and Hanan Ismail is facing surgery for a gastrointestinal condition called Crohn's disease that has worsened over the past five years.<p>
And what was the Hamouis' crime? Being Arab amid the racist scapegoating used by the federal government to justify the "war on terror." <p>
The Hamouis moved to the U.S. from Syria in 1992 and have been filing for political asylum for 14 years. But several lawyers failed to fill out asylum forms properly, leading federal authorities to conclude that the Hamouis had illegally overstayed their visas.<p>
The family was arrested under former Attorney General John Ashcroft's Absconder Apprehension Initiative, under which some 300,000 people facing deportation orders were rounded up. After September 11, the policy gave the green light for racial profiling of anyone of Arab descent.<p>
After their arrest, activists rallied to defend the Hamouis. The Arab American Community Coalition, the ISO and other organizations put pressure on the authorities with weekly pickets of dozens and sometimes hundreds of people outside the detention facility. Through these pickets and other activities, activists got the Hamouis' story out into the open.<p>
In the wake of the protest campaign, the three Hamouis were finally released from detention at the end of 2002. <p>
After years of wrangling in which authorities tried to deport the family twice, Hanan Ismail won permanent residency status this spring and Safouh this summer. Nadin is still trying to get her status secured, but it looks almost certain she will also win residency.<p>
Throughout the proceedings, the government tried every trick in the book to deport the family. Even federal appeals court Judge William Canby remarked that the Board of Immigration Appeals "abused its discretion," acting "arbitrarily, irrationally or contrary to the law."<p>
The <i>Seattle Post-Intelligencer </i>acknowledged the role that protest had played in winning "a victory that occurred because hundreds of people across the Pacific Northwest rallied to support the Hamoui family's cause."<p>
That victory is especially important now as the government's campaign of racist scapegoating against Arabs and Muslims intensifies as a result of the U.S. and Israeli wars in the Middle East. It is more urgent than ever that we say: "Being Arab is not a crime!"<p>

