<font face="Times New Roman, Times, serif" size="4">Ten years after Bill Clinton's law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The forgotten victims of welfare "reform"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON reports on the hidden story of Bill Clinton's welfare "reform."</b><p>

THE TEN-year anniversary of welfare "reform" took place in late August. But despite the claims of pundits and politicians, it was nothing to celebrate.<p>
When then-President Bill Clinton signed the Personal Responsibility and Work Opportunity Reconciliation Act on August 22, 1996, he said he was "ending welfare as we know it." The bill was the most extensive revision of federal welfare policy in more than 30 years.<p>
But the entire thrust of welfare "deform"--as critics began to call it--was to shift the burden of poverty even further onto the shoulders of the poor by instituting harsh work requirements and a five-year lifetime benefits cut-off.<p>
The welfare rolls have shrunk dramatically. According to the Department of Health and Human Services, in the 10 years since Clinton's welfare law, more than 2.5 million families have left the program--about a 60 percent decline. <p>
Champions of welfare "reform" claim that the drastic restrictions forced people who were "taking advantage" of the system to find jobs and take personal responsibility. But what politicians and members of conservative think tanks never discuss is <i>where </i>those people who dropped off the rolls have landed. <p>
As of 2003, according to the Center on Budget and Policy Priorities, about 1 million single mothers were neither working nor receiving cash benefits from welfare, disability or unemployment insurance, and were not living with a partner who had any of these income sources.<p>
According to the <i>New York Times</i>, other studies suggest that up to 20 percent of mothers who left welfare after the law passed didn't have work or other significant means of support a year later.<p>
And the truth is that many of the former welfare recipients who are considered "success stories" have simply moved into the ranks of the working poor, trapped in low-wage jobs, usually with no benefits.<p>
"Federal studies show welfare-leavers earn about $8 an hour--more than they would get from public benefits alone, but barely enough to survive," reported the <i>San Jose Mercury News</i>. "In Wisconsin, where welfare changes were early and pronounced, studies show that six years after leaving the reformed program, more than 80 percent of families still fall below the poverty line."<p>
And as the economy tightens, so does the noose around the necks of the working poor. As the <i>New York Times</i> noted, "Since 2000, employment rates for single mothers have fallen and child poverty has increased. Case loads have continued to fall, indicating that welfare is reaching fewer children at a time of rising need."<p>
Today, one in four American workers--more than 30 million people--are stuck in low-wage jobs that pay less than the federal poverty level for a family of four. Nearly a quarter of the country's children live below the poverty line.<p>
In other words, for most of those affected, being kicked off welfare has done nothing to alleviate poverty--because the talk about "ending welfare as we know it" was always divorced from any discussion about measures such as raising the minimum wage or providing health care, child care or other services to the poor and working poor.<p>
Tania Segovia, for example, told the <i>Mercury News </i>that she was forced on to welfare recently after she lost her job as a payroll administrator--because her car was stolen. Tania could have taken the bus to work, but it would have meant waking her 5 year old at 3 a.m. to make it on time.<p>
Likewise, Anita Galvan was recently forced on welfare for a second time--after she was forced to quit school to care for her father, who had suffered a stroke and heart attack. Now, the homeless mother of two told the <i>Mercury News</i> that she spends days in parks with her children, 6 and 5 years old, and stays at friends' houses at night.<p>
"I'm an able-bodied adult that can take care of my children on my own," Galvan said. "It's just right now in my life, there's an extraordinary amount of situations I can't get myself out of."<p>
During the Bush years, welfare has been saddled with even more cuts to already limited programs of aid for child care, and health insurance programs for low-income children. Yet predictably enough, the Bush administration took the opportunity of the anniversary of welfare "reform" to declare the law a success--and push for even more cuts.<p>
Secretary of Health and Human Services Secretary Michael Leavitt wrote in the <i>Salt Lake Tribune, </i>"Welfare reform has been an unqualified success, but now it is time to 'reboot the system' and strengthen work requirements while putting a renewed focus on marriage." In other words, shift even more of the burden onto working and poor families.<p>

