<p><a name="FF"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Palestine, Free Lebanon</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Sisco</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 75 people gathered outside the Hilton Towers Hotel August 16 to protest the presence of war criminals in Chicago. <p>
Former Israeli Prime Minister Ehud Barak and Eliza Olmert, the wife of current Israeli Prime Minister Ehud Olmert, held a banquet to raise funds for Israel--an incredible irony given the fact that Israel receives more aid than any other country in the world from the U.S.<p>
The protest was especially spirited and enthusiastic in the wake of the August 12 protests that brought tens of thousands across the U.S. in solidarity with Palestinian and Lebanese self-determination. Popular chants were "Free, free Palestine" and "Free, free Lebanon," and people also took up chants in support of Palestinian and Lebanese resistance to oppression by Israel and the U.S.<p>

