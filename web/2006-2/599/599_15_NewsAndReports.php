<p><a name="Violence"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop Chicago police violence</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Josh Gryniewicz</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--"They have turned our community into a police state." That's how Deidre Brewster, a long-time housing advocate and displaced former Cabrini-Green resident, explained the conditions in Chicago's North Side public-housing project. <p>
Over the past year, police in the 18th district, which has jurisdiction over Cabrini, have changed their tactics, arresting hundreds of residents for "trespassing" in their own neighborhood--often picked up visiting friends and family in neighboring buildings.<p>
Tension escalated in August following protests called in response to the shooting of 14-year-old Ellis Woodland by police. As many as eight young men involved in the protests have been arrested on a variety of false charges, including mob action and trespassing. <p>
"Since [the protests], youth who were involved are being targeted by police," Brewster said. "This is obvious retaliation--they are being retaliated against because they were part of the marches."<p>
One young man, Maurice Taylor, was arrested after his photograph appeared in the <i>Chicago Defender </i>and <i>Chicago Sun-Times</i>. He was held for over three days on $20,000 bail and says he was beaten in his cell. Another youth, 17-year-old Terrion Butler, told reporters, "They choked me, slammed me to the ground, then started stomping me and my back and my face, started to punch my face." <p>
Only 5,000 people still reside in the community in the wake of Mayor Richard Daley's "Plan for Transformation" for the Chicago Housing Authority. The plan is designed to clear housing projects from land coveted by real-estate developers catering to yuppies. <p>

