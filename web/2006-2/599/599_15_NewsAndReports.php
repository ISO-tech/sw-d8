<font face="Times New Roman, Times, serif" size="4"><b>Free speech under attack</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kristin Anderson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Josh Wolf, a Bay Area freelance journalist and activist, is sitting in federal prison in Dublin, Calif. His crime? Refusing to surrender unedited video footage taken at a G8 protest in San Francisco back in June 2005 to the San Francisco Police Department.<p>
Wolf's unedited video footage was subpoenaed by a grand jury because investigators think it showed a police car being deliberately set on fire. Wolf denies having any such footage. <p>
According to the appeal filed with the 9th U.S. Circuit Court of Appeals, "The San Francisco Police Department's own report on the damage suffered by the police car indicates that it was 'apparently drivable,' not 'burned,' and that damage to it consisted of 'Rt rear taillight.'"<p>
"Sending this journalist to prison for protecting his material is both a serious violation of press freedom and a negation of the U.S. Constitution's First Amendment," Reporters Without Borders said. "Journalists are not supposed to be judicial auxiliaries or police auxiliaries." <p>
A San Francisco judge ruled Wolf was in contempt of court because the government's right to see his video footage was more important than the protection he enjoys under the Constitution. The 24-year-old Wolf will remain in prison until he agrees to hand over his footage or until the grand jury's term expires in July 2007. <p>
Josh has worked as a freelance journalist covering local protests, and his work has helped defend San Francisco State University students for protesting military recruiters on their campus. <p>
The government's attack on Josh is part of the much broader attack on dissent in the U.S. This attack on free speech and the freedom the press cannot be allowed to go unchallenged.<p>
