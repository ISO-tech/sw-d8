<font face="Times New Roman, Times, serif" size="5"><b>Legal setbacks for gay marriage</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">GAY MARRIAGE suffered a series of defeats this summer, with the highest courts in New York and Washington deciding to limit marriage to one man and one woman, and judges in Georgia and Nebraska reinstating state constitutional bans that had previously been thrown out by lower courts. <p>
Lesbians and gays would be getting married right now in New York state if New York City Mayor Michael Bloomberg, in the midst of a primary campaign for re-election, hadn't appealed a previous court ruling in February last year that deemed banning gay marriage to be unconstitutional.<p>
The logic behind the New York court's majority decision was as clear on how the state sees the role of the family as it was dripping with homophobia. <p>
The majority wrote that, "Despite the advances of science, it remains true that the vast majority of children are born as a result of a sexual relationship between a man and a woman. Intuition and experience suggest that a child benefits from having before his or her eyes, every day, living models of what both a man and a woman are like." <p>
By their logic, heterosexual couples who don't want to, or can't, biologically have kids--such as infertile couples and the elderly--shouldn't be allowed to marry!<p>
Never mind that the media beams images at children every day showing the nuclear family as the only proper way for human beings to combine; never mind the fact that studies have repeatedly shown homosexual couples to be excellent parents; never mind that people, not the state, should decide how, when and with whom to raise children. <p>
Liberal strategists are now openly questioning whether we've gone too far, asked for too much too soon and should simply be happy to fight and win civil unions (which forego over a thousand legal rights that marriage entails), so as not to offend and "provoke" the right.<p>
But with the collapse of the fledgling gay marriage movement during John Kerry's presidential campaign two years ago, their best idea of "fighting" is to suck up to supposedly gay-friendly politicians.<p>
"On the day of the [New York] decision, we said in rallies all across the state that the best thing our community could do between now and next year, when the legislature goes into session, is get involved in the elections this fall," said Empire State Pride Agenda spokesperson Joe Tarver. Specifically, he encourages support of gubernatorial candidate Eliot Spitzer, who has told gay rights groups that he supports gay marriage.<p>
But history proved the bankruptcy of this route a decade ago when, during his election campaign, Bill Clinton promised to allow lesbians and gays into the military--a promise he could have fulfilled by executive order with the stroke of a pen. <p>
Once elected, however, he turned right around and instituted "don't ask, don't tell," which initiated witch-hunts and a wave of expulsions of lesbians and gays, and encouraged an increasingly hostile environment, illustrated by the tragic lynching of Matthew Shepard in 1998. Clinton then went on to sign the Defense of Marriage Act, absolving the federal government from recognizing gay marriages officiated in one state in any of the others.<p>
Instead of groveling for favors from lying politicians, gay marriage proponents need to be supporting grassroots movements for social change, which have the potential to broaden politically and take on the demands of their constituent groups. The immigrant rights movement has just such a potential, as LGBT and abortion rights supporters joining the massive marches in NYC showed.<p>
Supporting the campaign of the Green Party's Howie Hawkins for New York State governor is another thing to do. Howie is running on an antiwar, pro-gay marriage, pro-worker platform which has the potential to rock the two-party duopoly in New York and help mobilize thousands in struggle. <p>
It's time to stop supporting the people on top who don't give a damn about our rights and build more struggle from below, where it really matters.<br>
<b>Jake Kornegay,</b> Bronx, N.Y.<p>

