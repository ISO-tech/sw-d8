<font face="Times New Roman, Times, serif" size="5"><b>Marines forced to call up reserves</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 1, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. Marines are calling up reservists for the first time since the invasion of Iraq in what is being called a "backdoor" draft.<p>
Faced with continual shortfalls in recruitment, the Army has called up Individual Ready Reserves (IRR) throughout the war and occupation. But the Marines haven't resorted to an inactive reservist call-up since one just before the invasion began in March 2003--and that was mainly to serve at U.S. bases.<p>
The Marines' announcement--a mandatory call-up of 2,500 troops for up to 18 months of combat duty in Iraq--is a telling sign of the crisis facing the military as it tries to fill its ranks while its wars overseas grow increasingly unpopular.<p>
Promised reductions in troop deployment in Iraq haven't taken place. In fact, troop level in Iraq returned to 138,000 by the end of August, an increase of 11,000 over this summer's low point.<p>
Like the Army, the Marines have been stretched thin, especially infantry units ordered on multiple tours of duty. The Army has also used "stop-loss orders," forcing soldiers to remain with their units even after their active-duty commitments were finished. During the second half of last year, an average of more than 13,000 soldiers remained in Iraq because of stop-loss orders.<p>
The Marines' IRR are a non-drilling component of the corps consisting of soldiers recently released from active duty. As one military strategist admitted, "Even the best troops are likely to feel some resentment, having 'done their time' and started new lives" following a typical eight years on active duty and on active reserve.<p>
"You can send Marines back for a third or fourth time," said Paul Rieckhoff, founder of the mainstream Iraq and Afghanistan Veterans of America organization, "but you have to understand you are destroying their lives."<p>

