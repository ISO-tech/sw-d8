<font face="Times New Roman, Times, serif" size="5"><b>Days of action for immigrant rights</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">ACTIVISTS IN cities across the country are gearing up to mark Labor Day with demonstrations in support of the rights of immigrant workers and against deportations and other attacks on the immigrant community.<p>
-- In <b>Chicago, </b>immigrant rights activists from the March 10 Coalition have planned a four-day "March for Justice for Immigrant Workers" to begin on September 1 from Chinatown.<p>
Over the course of four days, marchers will make their way to Batavia, Ill., stopping along the way for immigrant rights meetings in houses of worship in Cicero, Melrose Park, Villa Park, West Chicago and elsewhere. On September 4, activists will converge in Batavia, where they will rally at the office of House Speaker Dennis Hastert--one of the main proponents of anti-immigrant legislation in Congress.<p>
-- In <b>Los Angeles, </b>activists from Hermandad Mexicana and the March 25 Coalition will hold a women-led march September 2 in support of Elvira Arellano, who is currently under the threat of deportation and took refuge in the Aldalberto United Methodist Church in Chicago. The march will kick off at noon from Olympic and Broadway Streets, and will join together with another Labor Day march that day for a final rally at City Hall. <p>
Activists are also planning a "Labor Day and Solidarity March" for September 4 in Wilmington, Calif., at Banning Park, near the ports of Long Beach. According to leading activist Nativo L&oacute;pez, the Teamsters, the Los Angeles County Federation of Labor and other union members will join immigrant rights activists and their supporters at the rally.<p>
"For the first time, this will be the immigrant rights' Labor Day with labor workers," L&oacute;pez recently told the <i>Pasadena Star-News</i>. "This will be a show of unity between the labor movement and the immigrant rights movement for fair immigration legislation."<p>
-- In <b>Washington, D.C., </b>the D.C. Amigos del Barrio and the National Capital Immigration Coalition have called for an East Coast mobilization for September 7 at 4 p.m. at the National Mall, with the slogan "Join in the demand for legalization for all!"<p>
-- In <b>Newark, N.J., </b>activists will hold a "March in Solidarity with the Rights of Immigrant Workers" on September 4, beginning in Lincoln Park.<p>
The march has been called in solidarity with immigrant workers in Riverside, N.J.--where a recent law punishes those who rent to or hire undocumented immigrants, and violent protesters waving Confederate flags and raising Nazi salutes recently disrupted a pro-immigrant demonstration. The march calls for an end to deportations and raids, and amnesty for the undocumented.<p>

