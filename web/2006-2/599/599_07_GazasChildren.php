<font face="Times New Roman, Times, serif" size="5"><b>The words of Gaza's children</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">MOHAMMED MUKHAIMAR is a trauma psychologist with the Gaza Community Mental Health Program. He put the words of children he has met together into this open letter. Thanks to the <a href="http://www.traprockpeace.org" target="_blank">Traprock Peace Center</a>, which first posted this essay.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>DEAR WORLD:</b></font><font face="Times New Roman, Times, serif" size="3"><br>
From here...from across the oceans...from Palestine...the land of open wounds, from our hearts and souls...we talk to you.<p>
Every day, when the sun rises in the morning, and sets in the evening, and the moon appears, we know there is a big world out there, and there we find you.<p>
We share the same sun, the same moon, but our days are not like yours, neither are our nights...we do not know why.<p>
Our days are full of fear and panic, of voices and sounds that you do not hear, full of images and events that you do not see, full of sounds from rockets and air jets that bring death to everywhere.<p>
Our days are full of worries about our mother, father, and sisters, brothers; whom we love and care about. We have fears of losing our house, our small toys and our swing. Fear of having our books and clothes get buried under the rubble.<p>
Hey, the Israeli army kicked us out of our home at 4 a.m. two days ago and destroyed everything, and they are staying in our house and using it for snipering...they forced us to leave as fast as we can...it was a nightmare...we couldn't take anything of our sentimental memories...we had to run away as far as we could...We don't know what to do...it was so hard for us...it was especially hard for the children and the older people. <p>
I personally had to carry my grandmother all the way because she couldn't walk. The children were speechless, crying...and the army is destroying everything, and they made many holes in our homes...the windows were down from the rockets they shot...the bullet holes marking the buildings...they are becoming insane...I saw people getting shot, I saw rockets and missiles fall on people, heavy bullets flew over my head, I saw parts of my house falling apart...the situation is so unbearable.<p>
Our days are full of funerals, tragic ceremonies, of children who are my age...our days are full of mothers weeping for the loss of their children and beloved, and of children's eyes searching for those who are gone and will never come back.<p>
Our days are hot and the beach is empty. There is no space left on the sand for our play and joy. Israeli military boats spread death on the sand and have colored the beach sand with blood. The ocean is meters away from our houses, but we are afraid to go there to avoid facing the same destiny of Huda's family [an 11 year old whose entire family was killed by an Israeli shell at the beach].<p>
We wish that the ocean and the summer be as it used to be, unlike nowadays, the "Summer Rain," as the Israeli army converted it to be.<p>
In our days, we do not play as other children do. We do not go far away from our houses, although its summer vacation, but there is no vacation while in fear and sadness.<p>
Our minds are full of horrible events about people who die, houses being demolished, cars being shelled, trees being uprooted, and green lands being bulldozed.<p>
Our minds are tired and overwhelmed of images of tanks, helicopters and sonic booms.<p>
Our games and vocabularies are only about soldiers and militant groups, jet fighters, drones, F-16s and machine guns. Simple joyful games and words have disappeared.<p>
Our night...(Oh don't remind me). It's dark; there is no electricity ever since Israeli air forces destroyed the power station. We get the lights from candles that melt similar to our dreams and hope.<p>
Our night carries the same events of the day, but it also carries nightmares about soldiers breaking into our homes, burning bodies, red sky, injured children crying, and the most terrifying is that our beloved die of rockets and shells.<p>
In our days and nights, we have many questions, but we have no answers for them.<p>
Why do they kill us? Why do they demolish our houses? Why do they terrorize us? Why are patients prevented from getting treatment? Why people die in hospitals? Why patients die at the checkpoints? Why we have been bleeding for so long? Why my father screams a lot and can't laugh anymore? Why my father comes back home without candies? Could it be because he has not received his salary for the last five months? Why my grandmother repeats the story of her uprooting in 1948, as if it is happening now.<p>
Why the past did not end yet? Moreover, why we receive the Israeli bullet alone?<p>
Our dreams are simple; we want our days and nights to be like yours; we want to be children, nothing more; we want freedom, safety and life. We want to live in peace.<p>
Do not leave us alone.<p>

