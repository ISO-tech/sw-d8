<font face="Times New Roman, Times, serif" size="4"><b>Torture: a tool of the U.S.</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE RECENT revelation of torture centers run by the Chicago police shocks even those who have a fundamental understanding of police terror and its role as a component of Black oppression in the United States. All of the victims of torture were African Americans. No one is being prosecuted. <p>
Chicago is home to millions of Blacks, many of whom are descendants of African Americans who fled the racist South in the early- to mid-1900s. Police terror has always been a component of Black oppression, especially in Chicago, a city whose founding history was based upon the Haitian Black trader, Jean Baptiste Point Du Sable.<p>
Police terror, however, is also an integral part of the torture centers run by the CIA in Guant&aacute;namo, Afghanistan or other centers internationally. Torture training was a part of the counter-insurgency training that Latin American military officers received from the School of the Americas in Georgia. There has been, and continues to be, international outrage against the CIA "extraordinary rendition" torture program. <p>
Let us not be fooled. Torture is an integral part of the imperialist arsenal of oppression against the oppressed and the exploited masses, both at home and abroad. The police terror program of Chicago cops demonstrates this fact ever so clearly. <p>
Readers of <i>Socialist Worker</i> should wage a campaign of outrage and opposition to the Chicago police torture program. We must do this in the name of fighting racism, imperialism and building a political movement for socialism.<br>
<b>Steve Johnson,</b> Los Angeles<p>

