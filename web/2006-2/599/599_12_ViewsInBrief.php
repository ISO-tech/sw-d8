<p><a name="Hez"></a><font face="Times New Roman, Times, serif" size="4"><b>Hezbollah can't bring liberation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE ISRAELI invasion of Lebanon is an assault on a whole population and a human rights disaster. While recent <i>Socialist Worker</i> coverage makes this point well, it mistakenly portrays Hezbollah, the Lebanese resistance organization, as a force of national liberation while downplaying or ignoring its reactionary character.<p>
Hezbollah translated literally means "The Party of God." It is an Islamist mass organization originally inspired by the Ayatollah Khomeini. Hezbollah to this day sees Iran as a model state and takes guidance from Iran. Its leader, the cleric Hasan Nasrallah, was trained in the seminaries of Iraq and Iran. <p>
Hezbollah is less illiberal than some Islamist currents, but that is cold comfort, a bit like discerning that George Bush is to the left of David Duke. Nasrallah's statements have been not merely anti-Israeli but grotesquely anti-Jewish. Hezbollah's deep-seated cultural conservatism includes bigotry toward gays. <p>
Lebanese Arabs and Muslims have a legitimate right to resist Israeli incursions. Affirming that right does not obligate the left to extend political support to Hezbollah, just as opposition to the Soviet invasion of Afghanistan in the 1980s did not necessitate endorsement of the mujahadeen. <p>
Israel's most recent offensive will increase sympathy in Lebanon and elsewhere in the Arab and Muslim worlds for Hezbollah. Socialists should not compound this tragedy by painting Hezbollah with celebratory brushstrokes.<p>
The rise of Hezbollah and other fundamentalist holy warriors in the Islamic world is a direct result of widespread resentment of the unjust policies of the United States and Israel, combined with the decline of two alternatives: secular national liberation movements (compromised by corruption) and Marxist organizations (discredited by dependence upon Soviet-style bureaucratic collectivist regimes and models).<p>
This dark moment conspires against us. A new alternative to imperialism and obscurantism is desperately needed in the Middle East. We can assist its birth only by adhering without compromise to the values of international solidarity, social equality, civil liberty, radical democracy, and secular rationality, including religious freedom. <p>
Hezbollah, a movement partially analogous to our own fundamentalist right, may be popular, it may be under unjust attack, but it neither represents liberation nor merits our political approval.<br>
<b>Christopher Phelps</b>, Mansfield, Ohio<p>

