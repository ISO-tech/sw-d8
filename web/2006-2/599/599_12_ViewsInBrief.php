<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>Nader wrong on Lieberman</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RALPH NADER recently told <i>Democracy Now!</i> that he hopes Sen. Joe Lieberman's "entry as an independent candidate will diminish some of the chronic opposition by the Democrats to anybody who...runs as an independent or a third-party candidate, like a Green candidate" <p>
Although Lieberman was soundly defeated in the Connecticut Democratic primary by presumably progressive Ned Lamont, Lieberman has continued his campaign to keep his Senate seat as an "independent Democrat." <p>
However, Lieberman doesn't offer a voice for independence from the two-party status quo. In fact, his run to maintain his incumbency is a bid to preserve the status quo. Moreover, some Republicans like arch-warmonger Newt Gingrich have abandoned their unpopular candidate in the race to endorse Lieberman, according to the <i>New York Times</i>. <p>
The Senate race is now between Lamont and Lieberman. Lieberman has never had much respect for democracy, even when he would have benefited. Where was his cry of outrage when Bush and Cheney stole the election from Gore and Lieberman, despite half a million more votes for the Democrats? <p>
I'm afraid that these developments will only serve to strengthen the resolve of many progressives to vote for the official Democrat, no matter who he is. Who is Ned Lamont? Although the cable executive says he is against the occupation of Iraq, his Web site claims troops should remain in Iraq indefinitely for logistical and training support only, declares Iran more dangerous, and says that "It is not for the United States to dictate to Israel how it defends itself."<p>
The question that remains is whether Connecticut progressives who want peace really have a candidate in the Senate race, or just the choice between the lesser of two evils.<br>
<b>John Osmand</b>, VoteCamejo.com campaign coordinator, Ventura County, Calif.<p>

