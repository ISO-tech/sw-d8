<font face="Times New Roman, Times, serif" size="5"><b>Israel's relentless war on Gaza</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 1, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE WORLD leaders hailed Israel's ceasefire agreement in Lebanon--an agreement Israel violated before the ink was dry--Israel's war continued against the Palestinian people.<p>
On August 27, the same day of the much-publicized release of two Fox News journalists by a militant Islamic group in Gaza, Israeli air strikes hit a Reuters truck, wounding a reporter and a cameraman. The same day, three Palestinians were killed by Israeli air strikes, and a fourth Palestinian was killed and a 6-year-old boy critically wounded by Israeli sniper fire,<p>
Israel is exacting a deadly revenge on the Palestinians after its failure to defeat Hezbollah in Lebanon. More than 200 Palestinians--44 of them children--have been killed and thousands forced to leave their homes since the beginning of Operation Summer Rains, the name the Israel Defense Forces (IDF) gave its 10-week-old intensified assault on Gaza.<p>
According to a report by the United Nations Office for the Coordination of Humanitarian Affairs, Gaza has been pounded by some 267 air strikes since the operation began June 28 after the capture of an Israeli soldier--imposing a cruel collective punishment on the Palestinian people.<p>
Israeli tactics can only be described as terrorism. For example, it is common practice for the IDF to warn families by telephone to evacuate their homes prior to an air strike. This means that Palestinian residents have been forced to repeatedly abandon their property and belongings in panic, whether there is an attack or not.<p>

