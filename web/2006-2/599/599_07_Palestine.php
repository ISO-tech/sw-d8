<font face="Times New Roman, Times, serif" size="3">Israel's bombs have hammered Gaza's infrastructure. More than a million people are without electricity after Israel bombed out a brand-new power station.<p>
According to the mayor of As Shoka, where 15 percent of the agricultural land in Gaza is located, the IDF is demolishing all the greenhouses and destroyed water and electricity networks. Crops that weren't destroyed by the bombing have been lost because Palestinians have not been able to work the land.<p>
The closing of a major crossing point at Karni has made it almost impossible to bring humanitarian aid to the 1.4 million Palestinians who live in Gaza. <p>
Some 30,000 people were trapped in Rafah, Gaza's only border crossing into Egypt, after Israel sealed the borders, according to <i>Al-Ahram Weekly</i>. "They deal with us like animals," said 37-year-old Riad Syiam, an electrical engineer trying to reach Abu Dhabi with his wife and three children. Like many other families in Rafah, they came to Gaza to visit relatives and were trapped by the Israeli assault.<p>
In the West Bank, Israel continues to kidnap and detain democratically elected Palestinian officials for their affiliation with the militant group Hamas. As the assault began, the IDF detained 64 Hamas officials, including eight ministers and 29 members of parliament. <p>
And as Prime Minister Ehud Olmert tries to regain credibility after the debacle against Hezbollah, his government is apparently putting a plan to withdraw Israeli settlements in the West Bank on the back burner--indefinitely. Assay Shariv, a senior aide to Olmert, told Inter Press Service: "Right now, we will deal with other issues. It's not that it [the withdrawal] was cancelled, but it is not on the agenda."<p>
Meanwhile, Israeli and U.S. sanctions imposed after the election of Hamas continue to strangle the Palestinian economy. Teachers and other government workers haven't been paid for months as a result of Israel withholding tax revenues.<p>
Israel's unrelenting war on the Palestinians is stoking ever-greater bitterness. "Listen, I have been living in Rafah for so long," Salman Abu Garad of Rafah told <i>Al-Ahram</i>. "I remember the 1948 war, and I can tell you that from then until the 1973 war and after, the Israelis have shown no interest in peace. They only understand one language: power."<p>

