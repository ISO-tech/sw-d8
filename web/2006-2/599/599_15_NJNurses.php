<font face="Times New Roman, Times, serif" size="4">N.J. hospital hires scabs in effort to break walkout</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Nurses strike for health care</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 1, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW BRUNSWICK, N.J.--Nurses at Robert Wood Johnson University Hospital (RWJUH) walked off the job here August 25. The strike by 1,330 nurses, members of United Steelworkers (USW) Local 4-200, began after months of negotiations with hospital management broke down over management's unwillingness to provide the nurses with decent health coverage.<p>
Nearly 1,000 nurses rallied at the hospital entrances on the first morning of the strike with passing cars honking their support.<p>
Over the term of the previous contract, which expired in June this year, the hospital enacted changes to the Qualcare health plan that have made it virtually impossible for nurses to receive adequate health care for themselves and their families. <p>
The current plan imposes stiff penalties for those who seek medical care outside a small group of RWJUH doctors. Nurses would be required to pay the hospital $1,000 for each hospital admission and $800 for each outpatient procedure, in excess of the standard deductibles that are part of the plan.<p>
The hospital has hired scabs through US Health Corps, a Colorado nursing recruiting station, which has flown in nurses from Alabama, Texas, California and Tennessee to replace the striking workers. <p>
But only 38 nurses have reportedly crossed the picket line, and scab nurses can't keep up the pace. As one nurse said, "You may be a good chef, but if you find yourself in someone else's kitchen, it's hard."<p>
The hospital is spending a small fortune to run its exorbitant scabbing operation--about $2.5 million to pay wages, hotel accommodations, extra security and so on.<p>
Several strikers reported that management had subjected them to a campaign of intimidation and threats in the weeks leading up to the strike. Supervisors have threatened to fire workers who strike and have told EMS workers that their replacements have already been hired. "These are unfair labor practices, and another reason why we are on strike," said Jerry Collins, President of Local 4-200.<p>
"The scabs are in the operating room right now, but there are patients who are already walking out," Susan Gonzales, a nurse who has worked at the hospital for 15 years, told <i>Socialist Worker</i>. "I think these scabs are a shame to the profession of nursing."<p>
Local newspapers have been trying to portray the strikers as "greedy" nurses who don't care for their patients. "Challenge them to become a nurse for a day," says striker Jonathan Grover. "Nurses take care of people so much more than they take care of themselves. The media try to make nurses feel bad about being on strike, but we are not making paper here--we are taking care of people, we are keeping people alive, holding their hands while they die, delivering their babies...You can't put a price tag on that."<p>
The strikers are confident they can win, because they have the support of the larger New Brunswick community. Building on that support, calling on other unions to show their solidarity, and developing a strategy to deal with the scabs will be crucial in the days and weeks ahead.<p>

