<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>United Teachers Los Angeles</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LA QUINTA, Calif.--As union activists convened in this desert resort town for the annual leadership conference of United Teachers Los Angeles (UTLA), the battles lying ahead for our union became increasingly clear.<p>
Reactionary school "reforms"--from charter schools to standardized testing to No Child Left Behind--run rampant while the Los Angeles Unified School District (LAUSD) is demanding a salary freeze and a two-tier health benefit system in current contract negotiations.<p>
But instead of focusing on how UTLA needs to organize its membership to fight back, UTLA leaders struck a deal this summer with LA Mayor Antonio Villaraigosa to support state legislation (AB 1381) that, if passed, will give the mayor more power in LAUSD decision making. This compromise was a foot in the door for Villaraigosa's reactionary, anti-union agenda of mayoral control of the schools and the proliferation of non-union charter schools.<p>
UTLA's new leaders were elected last year on promises of militancy and organizing, But in his "State of the Union" address, UTLA President A. J. Duffy spent 20 minutes defending this rotten deal. The audience received this part of his speech mostly with stony silence, occasionally punctuated by tepid applause.<p>
Villaraigosa was given the stage at two conference sessions to sell AB 1381 to angry (and sometimes heckling) UTLA members. <p>
On the other hand, there is a current of rank-and-file members hungry for an alternative. More than 80 members (about one-sixth of the conference) came to the caucus meeting of Progressive Educators for Action (PEAC).<p>
PEAC was the driving force behind the election of the new UTLA officers, three of whom are PEAC members. PEAC initially struggled to find its own independent identity as the new officers repeatedly slipped into the same habits of bureaucratic leadership they were elected to challenge. And PEAC was initially paralyzed by the deal with Villaraigosa, unable to take a position for or against AB 1381. <p>
But at the leadership conference, PEAC's newsletter <i>A Second Opinion </i>was sharply critical of both AB 1381 and the new leadership. "[The] new officers have done a poor job of working collaboratively with a broader layer of UTLA leaders," argued the newsletter's editorial.<p>
It's been a rocky year for UTLA and PEAC, but there are still a large number of UTLA members interested in an independent rank-and-file alternative to bureaucracy and reliance on politicians.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Gary, Ind., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adam Turl</b></font><p>

<font face="Times New Roman, Times, serif" size="3">GARY, Ind.--Gary's schools are totally shut down. School officials decided to suspend classes after a substitute teacher--hired to scab on striking teachers at Gary's Watson Academy--drove her car through two picketers, injuring them.<p>
On August 21, Gary teachers struck the schools--which serve around 16,000 students in this impoverished Chicago-area city, hit hard by de-industrialization and plant closures. This is the first Gary teachers' strike since 1984.<p>
The teachers have been negotiating with Gary Community School Corporation--which wants to keep raises low (around 2 percent)--and increasingly offload health insurance costs onto teachers. On Wednesday, school officials broke off negotiations, claiming that the teachers' strike was illegal under Indiana state law.<p>
The union is, so far, holding strong and refusing to return to work without a contract. Striking workers told <i>Socialist Worker </i>that they were widely supported among parents and students--despite the offer by school administrators to hire parents to help teach some of the classes.<p>
The local United Steelworkers of America (USWA) has helped distribute newsletters for the strikers, and teachers have held rallies and meetings in their union hall.<p>
Only a handful of teachers has crossed the picket line, and on the first days of the strike, less than half of students reportedly showed up for class. This is a blow to the corporation, since they had reportedly bragged that as many as 40 percent of teachers would fail to honor the strike.<p>
Solidarity has also been forthcoming from the Chicago Teachers' Union--which, along with teachers in other large districts, has its own contract coming up in a years' time. More solidarity is needed for these workers who have defied legal threats to turn back the escalating attack on schools and teachers.<p>

