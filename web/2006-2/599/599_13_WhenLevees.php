<font face="Times New Roman, Times, serif" size="4">Spike Lee's film recalls <i>When the Levees Broke</i></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"It's like they let it happen&#8221</b></font>;<p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 1, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>When the Levees Broke: A Requiem in Four Acts,</b></i> directed by Spike Lee, a two-part HBO documentary.</font><p>

<font face="Times New Roman, Times, serif" size="3">AS THE anniversary of Hurricane Katrina approached, the usual media barrage of retrospectives interrupted celebrity murder stories and filled the tube with images of the haphazard fury of nature.<p>
As Tropical Storm Ernesto passed over Jamaica and headed for the Gulf of Mexico, politicians and engineers nervously insisted that there was a plan in place for New Orleans.<p>
And the week before the anniversary, Spike Lee's two-part documentary, <i>When the Levees Broke: A Requiem in Four Acts, </i>premiered on HBO, making it clear that the continuing epic tragedy of Katrina is a failure on many levels which far exceeds a "natural" disaster.<p>
As Lee's title reminds us, it was not Katrina that destroyed New Orleans, but the flood caused by the breaching of the levees. Computer mock-ups had long predicted that the levees would not hold up in a severe hurricane, and recently the Army Corps of Engineers was forced to admit to human error in the levee system designed for the city.<p>
"If they knew it could've happened, it's almost like they let it happen," said a New Orleans resident.<p>
Acts 1 and 2 detail the lead-up to the disaster and people's struggle to survive in the weeks that followed. The last two acts prove Lee's thesis that "New Orleans isn't over with," as unrelieved human suffering continues to clash with the indifference and cruelty of the bureaucracy. "These people," said actor, singer and activist Harry Belafonte, "are socially, racially unimportant."<p>
Lee constantly reminds us that New Orleans is a unique culture in which music has always played a dominant role. Throughout each act, shots of trademark parades led by musicians are interspersed with images of property destruction and unimaginable agony. Swollen, rotten bodies--some being chewed by starving dogs--pollute the landscape and the waters while the situation worsens at the Superdome.<p>
The story is narrated by several voices--politicians and professionals along with numerous victims, Black and white, of the storm and the system. Narrators compare the devastation to the battlegrounds of the Second World War, Beirut and Hiroshima, and they are shocked that it could happen here. All blame government at all levels for failing to protect the property and lives of the non-wealthy residents and for continuing to fail to relieve the suffering.<p>
Making excuses as short time narrators, Bush and his cabinet, former FEMA chief Michael Brown, Homeland Security Secretary Michael Chertoff and the Army Corps of Engineers prove worthy of the blame they receive.<p>
Then, the National Guard comes in, and things begin to change. Residents become the enemy. "The feelings that residents had about the National Guard and the state police and the visiting police," said New Orleans City Council member Cynthia Hedge-Morrell, "is that they were an armed occupied city. I can't help but believe that is the same way they treated people in any city they go in and invade--Kosovo or wherever...I guess maybe that's the way the Iraqis feel--I don't know."<p>
After surviving days of hell, people were force loaded onto buses without even the simplest of kindness--being kept in their family unit or told where they were going. Infants were snatched from their mothers' arms.<p>
Gina Montana, a resident of Mid-City, said, "With the evacuation scattering my family all over the United States, I felt like it was an ancient memory. It was as if we had been up on the auction block."<p>
A year later, some families are still not reunited, bodies are still being found in houses marked "inspected," people homelessly wait for rebuilding and FEMA checks, and many can't return.<p>
Only 1 percent of those who have been lucky to have a "little ol' raggedy" FEMA trailer in their yard as they try to rebuild have electricity. "The aftermath is worse than the actual levees breaking," says Daina Saulny. "It's almost like you're stuck."<p>
A woefully inadequate number of mental health professionals are treating increasing numbers of cases of post-traumatic stress disorders. Post-Katrina deaths have climbed 30 percent, many of those suicides.<p>
Lee's documentary is a powerful history lesson. Unfortunately, not enough attention is given to the many heroic efforts of individuals and communities to provide needed services and save lives. And Lee romanticizes a few cases of evacuees who landed out of state, only touching briefly on the greatest diaspora in American history. That story awaits another hundred documentaries.<p>

