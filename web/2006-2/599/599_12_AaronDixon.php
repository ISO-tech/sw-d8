<font face="Times New Roman, Times, serif" size="5"><b>Showing a real alternative</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 1, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JULY 15, Aaron Dixon, 2006 Green Party nominee for U.S. Senate and former captain of the Seattle chapter of the Black Panther Party, returned to the same building where he helped start the Sidney Miller free medical clinic with the Panthers in 1972--this time to announce the grand opening of his new campaign headquarters.<p>
A racially diverse crowed of more than 150 people of all ages came to welcome Dixon's office opening in the Central District of Seattle. Poets Piece Kelly and E Mandisa inspired the crowd with lyrics that sliced through government hypocrisy, while Aaron's address spoke to the many wars the U.S. is engaged in.<p>
"We are living in a time of great danger. These maniacs--both Republican and Democrat--are destroying America and the world; weather it is with the war in Iraq, the war on Afghanistan, the war on Palestine, the war on terror, or the war on drugs," Dixon said. "We need a new movement in this country that can put an end to these insane wars and build a society where instead of locking up our youth we are educating them."<p>
The open house event came on the heels of a major election shakeup in the Washington state Democratic Party primary. Mark Wilson, the professed antiwar opposition candidate running in the primary against incumbent Sen. Maria Cantwell, announced he's dropping out of the race to join Cantwell's pro-war campaign.<p>
After campaigning for 18 months against Cantwell's unyielding support for the war in Iraq, calling the incumbent a "free-trading corporate elitist," capturing much of the support from the antiwar movement in Washington state, and obtaining prominent national endorsements from antiwar activists, Wilson has decided to cast aside his principles and encourage antiwar activists to support Cantwell.<p>
Wilson rationalized his support for Cantwell, saying he believed that there had been an "evolution" in Cantwell's stance towards the war in Iraq. <p>
Wilson must not have seen Cantwell's recent votes in the Senate. Cantwell has repeatedly reaffirmed her commitment to more carnage in Iraq, including voting for every one of Bush's emergency spending bills to wage war on Iraq and her vote in June against John Kerry's tepid proposal to set a partial deadline of July 2007 to withdraw some of the U.S. forces. <p>
Wilson publicly insists that the $8,000 a month salary that Maria is paying him to be the "outreach coordinator" for her campaign had nothing to do with his decision to join an openly pro-war campaign.<p>
Many of Wilson's supporters are furious with what they see as his betrayal of the antiwar movement and are looking either to Aaron Dixon's campaign or to the remaining Democratic primary challenger, Hong Tran. Tran opposes the war in Iraq, but has almost no chance of upsetting Cantwell in the primary and recently stated that if she loses, she will back the incumbent in November.<p>
In November, voters in Washington state will be faced with a stark choice: vote for the unabashed pro-war Republican or Democratic candidate, or vote for Aaron Dixon and his platform of "out of war and into our communities."<br>
<b>Jesse Hagopian,</b> Campaign manager, Dixon for U.S. Senate, Seattle<p>
