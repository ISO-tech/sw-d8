<font face="Times New Roman, Times, serif" size="4">Fifth anniversary of the U.S. attack on Afghanistan</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The other war based on lies</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 |  Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">THE OCTOBER 7 anniversary of the war on Afghanistan passed unnoticed on U.S. soil--sparing the Bush administration the embarrassment of accounting for the fate of Afghanistan's 30 million people five years after the U.S. launched the first "regime change" in its war on terror.<p>
An honest accounting is long overdue, however, not merely among those who prosecuted this disastrous war--but also for the antiwar movement, whose sole focus in opposing the war on Iraq sustains the fiction that the war on Afghanistan was a justifiable response to 9/11. It was not.<p>
A BBC News report dated September 18, 2001--long ignored by the U.S. media--showed that the U.S. was planning to bomb Afghanistan well before September 11. "Niaz Naik, a former Pakistani Foreign Secretary, was told by senior American officials in mid-July that military action against Afghanistan would go ahead by mid-October," the BBC reported.<p>
The events of September 11 provided the excuse to use the U.S.'s war on Afghanistan as a launching pad for reshaping the entire Middle East in its own interests. But ample evidence exists to show that the U.S.'s stated goals were based upon a set of lies equivalent in scale to those used to justify the war on Iraq.<p>
<b>-- LIE NUMBER ONE: The overthrow of the Taliban brought democracy to Afghanistan.</b><p>
During his gloating 2002 State of the Union Address, Bush claimed the U.S. victory over Afghanistan "saved a people from starvation and freed a country from brutal oppression," introducing former Unocal consultant and interim Afghan President Hamid Karzai to thunderous applause.<p>
In reality, the victory over the Taliban in 2001 involved striking a deal with the same warlords and mass rapists who ruled Afghanistan before the Taliban seized power in 1996--to bolster the puppet Karzai's wobbly government.<p>
Abdul Rashid Dustum, who journalist Robert Fisk described in 2001 as "one of the most powerful Alliance gangsters, whose men looted and raped their way through the suburbs of Kabul in the nineties," now serves as Defense Minister.<p>
With drug-trafficker Gen. Mohammed Daoud installed as Afghanistan's Deputy Interior Minister, it is not surprising that Afghanistan is now supplying up to 92 percent of the world's heroin.<p>
Meanwhile, "Afghanistan's people are starving to death," according to the Senlis Council, the British think-tank, last month. "One in four children born in Afghanistan cannot expect to live beyond the age of five, and certain provinces of the country lay claim to the worst maternal mortality rates ever recorded in the world," the report added.<p>
<b>-- LIE NUMBER TWO: The war on Afghanistan aimed to liberate Afghan women.</b><p>
After the fall of the Taliban in November 2001, President Bush gallantly ceded his weekly radio address to First Lady Laura Bush, who claimed: "Because of our recent military gains in much of Afghanistan, women are no longer imprisoned in their homes...The fight against terrorism is also a fight for the rights and dignity of women."<p>
U.S. bombs were never meant to bring the liberation of Afghan women. Indeed, Karzai's cabinet recently resurrected the Department for the Promotion of Virtue and Prevention of Vice--the Taliban's notorious religious police renowned for beating Afghan women for revealing their wrists, hands or ankles, or venturing in public without a close male relative.<p>
As a member of the Revolutionary Association of the Women of Afghanistan (RAWA) observed in an October 7 speech, in toppling the Taliban, the U.S. "just replaced one fundamentalist regime with another."<p>
<b>-- LIE NUMBER THREE: The Taliban could not be negotiated with--and was overthrown--for providing a "safe haven" for terrorists.</b><p>
The U.S. now appears ready to negotiate with the undefeated Taliban. Senate majority leader Bill Frist admitted in early October the Afghan government should include "people who call themselves Taliban."<p>
This idea gained traction among policy wonks. Stephen P. Cohen of the Brookings Institution argued, "This is distasteful, and might mean Karzai's departure, but it does preserve our one core interest in Afghanistan."<p>
As the Senlis Council bluntly concluded, "U.S. policies in Afghanistan have re-created the safe haven for terrorism that the 2001 invasion aimed to destroy."<p>

