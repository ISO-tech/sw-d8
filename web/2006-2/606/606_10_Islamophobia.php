<font face="Times New Roman, Times, serif" size="5"><b>Seeing the evidence of racist hate</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 20, 2006 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 3, I published a column in the <i>Cornell Daily Sun</i> titled "New Intolerance," decrying the anti-Arab and anti-Muslim sentiment that is present at Cornell University, and throughout the United States. This was my third column for the paper, my first two being about Elvira Arellano's fight against deportation and the antiwar movement's support of the troops.<p>
Although I knew the topic of my most recent column was controversial, I did not expect it to be any more controversial than my previous ones. On this, I was completely wrong.<p>
Within 24 hours of my column being published, I had received over 50 comments to my column online, most of them saying things like "Your hero Stalin must be proud," and "This is absolutely the stupidist [sic], ignorant, left-wing tainted article I have read this week."<p>
Beyond these more childish comments, there were many that disagreed with my basic premise that not all Muslims or Arabs are terrorists. A typical comment said something like this: "If you think this is an issue of race, and that 9/11 was not the fault of Islamists, you might as well get fitted for a burqa. What size are you? My grandmother can do wonders with fabric."<p>
I also received a negative e-mail from Jesse Petrilla, the founder and chair of the United American Committee, which I referenced in connection to the mock lynching of Osama bin Laden on the eve of the recent September 11 anniversary at a mosque in Los Angeles.<p>
The response to my column is an example of the emboldening of the right that is occurring across the country. Racist groups like the Minutemen, Save Our State, and the United American Committee should not feel comfortable expressing their intolerant views. This experience further proves that we must continue our fight against these right-wing bigots.<br>
<b>Laura Taylor</b>, Ithaca, N.Y.<p>

