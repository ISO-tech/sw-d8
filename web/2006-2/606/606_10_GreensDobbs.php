<font face="Times New Roman, Times, serif" size="4">Green Party courts an anti-immigrant extremist</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Lou Dobbs isn't our ally</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 20, 2006 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS truly surprised and deeply troubled to receive an e-mail from the Green Party of the United States on October 11 urging Greens to "ask Lou Dobbs to cover the Green Party."<p>
The e-mail lauds the CNN commentator's recent rallying cry for "middle-class Americans" to register independent because, Dobbs says, the Republican and Democratic Parties are both "bought and paid for by corporate America and special interests. And neither party gives a damn about the middle class."<p>
Without an independent middle-class "voice," to fight for middle-class interests, Dobbs says, "all the major decisions about America and our future will be made by the elites of government, big business and the dominant special interests. Those elites treasure your silence, as it enables them to claim America's future for their own."<p>
The Green Party e-mail encourages Greens to let Dobbs know that the Green Party can be that "strong, clear and vibrant voice," and rather than encourage people to register independent, he should tell his viewers to register Green.<p>
It seems to me that someone at Green headquarters is confused. Lou Dobbs--for those who have been fortunate enough never to have seen his show--is a right-wing nationalist who has taken it upon himself to slander and attack immigrants in general, and the immigrant rights movement in particular.<p>
Yes, he denounces "big business" and calls for raising the minimum wage, but he also propagates the idea that Mexican "illegals" are trying to "take over" the southwestern United States.<p>
Through his series of "Broken Borders" segments on <i>Lou Dobbs Tonight</i>, Dobbs has repeatedly given airtime and publicity--to a nightly audience of some 800,000 viewers--to the leaders and views of white-supremacist organizations such as the Council of Conservative Citizens. <i>The Nation</i> magazine recently pointed out that in Dobbs' commentary on the massive May 1 demonstrations in support of undocumented immigrants, he displayed anti-left credentials that would rival Joe McCarthy himself.<p>
"It is no accident that they chose May 1 as their day of demonstration and boycott," Dobbs said. "It is the worldwide day of commemorative demonstrations by various socialist, communist and even anarchic organizations...No matter which flag demonstrators and protesters carry today, their leadership is showing its true colors to all who will see."<p>
So when Dobbs talks about needing a voice for "middle class Americans," he means white, straight, male, patriotic Americans. And when he talks about opposing "the dominant special interests," he means not just corporations, but also the progressive groups that supposedly influence the Democratic Party.<p>
While his television audience is rapidly growing, Lou Dobbs is not a mass leader. However, the phenomenon of a right-wing nationalist movement claiming to represent "the little guy" against the "elites" is not unique in history--Hitler called it National Socialism.<p>
More concretely, if the Green Party is so desperate for votes as to reach out--at the national level--to the likes of Lou Dobbs, what would prevent local Greens from reaching out to the "disaffected middle-class Americans" building the Minutemen?<p>
Certainly many of the Minutemen and their supporters have felt the crunch of corporate globalization and the rich-get-richer policies of Democrats and Republicans. However, they have chosen to build a racist vigilante group to scapegoat Latino immigrants as their solution to the problem.<p>
Is this really the future of the Green Party? Class inequality in the U.S. is increasing, and U.S. public opinion continues to polarize more sharply between the right and left on many social issues. The Democrats and Republicans are indeed united in their agenda to pursue the interests of the rich at the expense of working people.<p>
In this context, it becomes all the more important for the Green Party to clearly and unequivocally stand for anti-racism and pose a left-wing alternative to the greed and corruption in Washington. This alternative must be opposed to--not a collaborator of--the growing right wing.<p>
Surely this project is more important than seeking the votes of racists for the midterm elections.<br>
<b>Ben Dalbey</b>, Baltimore, Md.<p>

