<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Not a dogma, but a guide to action"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">"THE REPORTS of my death," wrote Mark Twain, "are greatly exaggerated." The same could be said of Marxism as a body of ideas.<p>
Marx of course died long ago--1883 to be precise. The economist of the free market, Adam Smith, died almost a century before that, in 1790), yet, curiously, there are no books announcing that "Smithism" is dead.<p>
If Marxism is dead, asks author Markar Melkonian, "then why do non-Marxist economists, sociologists and historians worry about it so much?" His answer: "Capitalism itself has a way of putting socialism back on the agenda."<p>
The continued survival of capitalism may prompt its apologists to declare it the "final" form of human fulfillment--and for the hundreds of billionaires in the world, capitalism is certainly fulfilling. But for the majority who suffer low pay, poor health care, racial and sexual discrimination, and unemployment, capitalism is hardly fulfilling.<p>
The very survival of such an exploitive system guarantees that Marxism won't die--though there will be lots claims to that effect. Discovering what Marxism is, however, is not the easiest thing to untangle. That's because there seems to be so many different trends claiming to be Marxist, some of which are polar opposites.<p>
Is Marxism workers' control or control over workers? Is it democratic or dictatorial? One way to deal with this problem is to define Marxism as whatever calls itself Marxist.<p>
For pro-capitalist ideologues, this means linking Marxism with the horrors of Stalinism. For academic Marxists, it means cashing in on the latest intellectual fad, no matter how radically divergent it is from Marx's ideas.<p>
But if both workers' power and a state that oppresses workers can be considered "Marxist," then what we are dealing with is a meaningless label. Perhaps Marxism is just whatever Marx (and Engels) wrote. But this is a scholastic approach, treating Marxism as a body of sacred texts.<p>
Marxism, wrote Trotsky, "takes as its point of departure" experience, "and always returns to it," and "Experience is complicated, alive, and in constant flux." Society, like an organism, changes, but it changes according to certain identifiable patterns and laws. Marxism tries to identify those patterns and laws, not as a passive act of observation, but as a means to discover ways to change the world.<p>
Can we call someone who learns about the body and its biological processes, as well as use of the surgeon's tools, yet never performs an operation, a surgeon? That is why the term "academic Marxism" is such an oxymoron.<p>
As Lenin noted, citing Engels. "Our doctrine...is not a dogma, but a guide to action. This classical statement stresses with remarkable force and expressiveness that aspect of Marxism which is very often lost sight of. And by losing sight of it, we turn Marxism into something one-sided, distorted and lifeless."<p>
Marx and Engels believed that ideas had to be verified by experience. But by experience they didn't mean waiting passively on the sidelines and watching history pass by to see if it does what you predicted it would do. They meant the experience of struggle, the experience of trying to actively change society. In this sense, their ideas were neither religious nor academic, but revolutionary.<p>
Socialists prior to Marx had visions of a better world, but Marx and Engels put socialism on a scientific footing. "Communism is for us," wrote Marx and Engels in <i>The German Ideology,</i> "not a state of affairs which is to be established, an ideal to which reality [will] have to adjust itself. We call communism the real movement which abolishes the present state of things."<p>
What were their main arguments?<p>
The abundance created by capitalist methods of production had eliminated the material constraints that in the past had rendered class division necessary for historical advance; large scale industry had created working class with collective power and collectivist inclinations unlike previous exploited classes; and the contradictions of capitalism, in particular, periodic crises of "overproduction" would give rise to class struggle, and therefore, the possibility of a revolutionary overturn of the system and the development of a higher form of (classless) society in which material abundance was produced and shared in common.<p>
It was a theory that combined a firm grasp of the laws of historical development with a recognition of the role that active, human agency plays in transforming society.<p>

