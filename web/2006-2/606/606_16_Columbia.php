<font face="Times New Roman, Times, serif" size="5"><b>Columbia protesters face school charges</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jennifer Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE wake of a protest against Minutemen cofounder Jim Gilchrist, student activists at Columbia University in New York are facing disciplinary charges that could result in expulsion.<p>
The threatened punishment stems from the October 4 demonstration against a Campus Republican-sponsored forum featuring Gilchrist. The Minutemen were met by a demonstration of more than 300 people outside and a very angry, multiracial audience inside the auditorium.<p>
When two immigrant rights supporters climbed on stage with a banner that read "No one is illegal" in Spanish, English and Arabic, they were met with a standing ovation, and other students followed their lead.<p>
The Minutemen supporters physically attacked the protesters. Video footage shows one Latino student being kicked in the shoulders, head and face, and other students have since reported being assaulted.<p>
The student activists came under attack immediately. Denounced by the media, a number of them have reported receiving racist hate e-mails and threats.<p>
Columbia President Lee Bollinger has repeatedly condemned the protesters, but has yet to even acknowledge the violence directed at the students.<p>
The administration has begun calling students in for questioning sessions where they won't have the right to have an advocate or witnesses present. One of the first four students to face questioning will be the man who was shown on video being assaulted by a Minutemen supporter.<p>
The administration's response has angered many students on campus, especially students of color who have long felt that Columbia fails to protect them. There have been a series of racist incidents on campus in recent years, including an anti-affirmative action "bake sale," racist cartoons in a campus newspaper, attacks on Arab professors, and swastikas and other racist graffiti in public spaces.<p>
Bollinger was quick to speak out against pro-Palestinian professors or the views of Iranian President Mahmoud Ahmadinejad--whose scheduled speech on campus was canceled--but has remained silent on issues affecting students of color.<p>
Even the editors of a nonpolitical campus magazine, the <i>Blue and White,</i> asked on their Web site: "Why won't President Bollinger stand up for his students, and when will he speak out against the Minutemen?"<p>
On October 11, more than 50 students crowded into the Inter-Cultural Resource Center for a town hall meeting to discuss the administration's response and plan a defense campaign for the student protesters. They are planning a flyering campaign to get out the facts about who the Minutemen are and why the students protested, and to expose the administration's one-sided "investigation."<p>
The following day, the United Students of Color Council hosted another meeting that drew similarly large numbers.<p>
The Columbia administration wants to make an example of the student protesters and prevent future demonstration. This would give confidence to the College Republicans to continue inviting racist speakers and to the Minutemen to continue their attacks on immigrants--and contribute to a hostile atmosphere for students of color on campus.<p>
The Columbia protesters need the support of everyone who stands against racism.<p>
