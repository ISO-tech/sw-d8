<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: CAROLINE LUND</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A strong voice for rebuilding labor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 |  Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LONGTIME SOCIALIST and union militant Caroline Lund died October 14 after a long illness.<p>
As a college student, Caroline was among the young people who reinvigorated the Socialist Workers Party in the 1960s amid the rise of the New Left and the civil rights, antiwar and women's movements. With her skills as a writer, speaker and organizer, Caroline made important contributions to all of these struggles, and remained a committed socialist.<p>
As readers of <i>Socialist Worker's </i>labor coverage will know, Caroline was also devoted to rebuilding the labor movement at a time of crisis and decline.<p>
As a worker at New United Motor Manufacturing Inc. (NUMMI)--an auto assembly plant in Fremont, Calif., jointly owned by General Motors and Toyota--Caroline was on the front line of the struggle against "lean" and "flexible" labor practices, management euphemisms for the system of perpetual speedups and outsourcing that have since become generalized throughout the auto industry.<p>
Her newsletter, the <i>Barking Dog,</i> was a scrappy shop-floor handout in the best traditions of the United Auto Workers rank-and-file activism of the past--and, thanks to the Internet, was distributed in several plants across the U.S. While the newsletter focused on day-to-day battles with management and the fight for union democracy in UAW Local 2244, Caroline never shied away from taking up controversial political issues, such as U.S. wars from the Balkans to the Middle East.<p>
After years of organizing, Caroline won union office in 2003 as an independent ally of a reform election slate in Local 2244. "The backdrop for this election was Bush's war on Iraq," Caroline wrote then in <i>Socialist Worker</i>. "The [previous local leadership that supported the UAW International] beat the drums of patriotism, cosponsoring a 'support the troops day' with the company.<p>
"My plant newsletter, the<i> Barking Dog,</i> said the best support for the troops was to bring them home and explained that company and union officials 'are using sympathy for the troops to try to manipulate us into supporting the war on Iraq, silencing complaints on the shop floor and prettifying our union officials for the coming union elections.'"<p>
The new leadership made a big difference for the 4,000-plus-member local in the 2005 contract talks, mobilizing for a strike against NUMMI management's demands for unprecedented concessions. NUMMI tried to force workers to pay for 30 percent of their health insurance premiums--which had been entirely paid by the company in the past--along with a permanent lower wage for new hires.<p>
Panicked by a credible strike threat, management dropped its worst demands, and workers voted to okay the contract by an 80 percent margin.<p>
But as Caroline readily admitted, some serious concessions remained, such as an expanded period for temporary workers. "We have experienced how damaging it is to have second-class union members who do the same work we do, but with no benefits, raises or job security," she wrote in <i>Socialist Worker</i>.<p>
The mobilization at NUMMI foreshadowed the rank-and-file activism in the UAW the following year and the formation of the Soldiers of Solidarity (SOS) network, a group that formed in the wake of Delphi Corp.'s bankruptcy and massive downsizing at General Motors and Ford.<p>
Caroline was frustrated that her illness prevented her from personally taking part in SOS, but her UAW activism--such as supporting the long strike and lockout at the Accuride wheel plant in Kentucky--had helped prepared the ground for that activism.<p>
"I feel so proud to have been associated with the SOS cause," she wrote recently in an e-mail to UAW rank-and-file activists. "It turned out this time that Delphi and other workers were not ready for a real mass, serious fightback. But you never know when the tinderbox is going to be ready to explode. At least SOS was out there putting out the truth and a voice of resistance that could possibly have found a massive echo."<p>
Caroline's own voice of resistance has been stilled far too soon--but we are sure to hear its echo in the struggles of the future.<p>

