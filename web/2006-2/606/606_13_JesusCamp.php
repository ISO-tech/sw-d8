<font face="Times New Roman, Times, serif" size="4">What the evangelicals did over summer vacation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>God's army</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Jesus Camp,</b></i> directed by Heidi Ewing and Rachel Grady.</font><p>

<font face="Times New Roman, Times, serif" size="3">MANY OF us have fond memories of church camp. You could endure the preachin' and prayin'--and the swim sessions segregated by gender--by sneaking out into the woods with your buddies for some real fun when the counselors were busy making out in the cabins.<p>
In <i>Jesus Camp, </i>the church camp experience is frighteningly different. The most adorable bunch of pre-teens ever gather at the Kids on Fire Bible camp in North Dakota to be soldiers of God.<p>
At an age when most impressionable and eager to please, these kids have been brainwashed, laden with guilt and turned into evangelists spewing hate with tiny voices. Think miniature Pat Robertsons and Jerry Falwells with the faces of cherubs.<p>
The documentary focuses on the camp founder, Pentecostal Pastor Becky Fischer, and three especially precocious youngsters. Levi, 12, was "saved" at 5, likes to preach, and is eager to go to camp because he finds that nothing seems like fun anymore.<p>
Tory, 10, likes heavy metal Christian music and worries that she might just sometimes be dancing for the flesh, rather than for Jesus. The ever-so-serious Rachel, 9, takes notes during sermons and fearlessly shares with adult strangers her conversations with God about the state of their souls.<p>
Pastor Fischer and other church leaders believe that the world is messed up and tell camp goers that they are the special generation that can fix it. Palestinian mothers send their kids out by the age of 5 to be suicide bombers, says Fischer, who argues that Christian children should be similarly ready to lay down their lives for Jesus.<p>
Because of their unwavering faith and their ability to know the answer to all questions, Fischer and the other adults have no idea how obscene they appear. Thus they allow filmmakers unusual access into their lives.<p>
Of all the religions in the world, Fischer assures us, "we have the truth." Fundamentalists make up 75 percent of the nation's home-schooled children, and independent inquiry is not a part of the curriculum.<p>
After debunking chaos theory, evolution and global warming, one teacher/mother asserts that "science can't prove anything." The film opens with "God's army" dancing in camouflage fatiques and painted faces.<br>
At camp, children, even some of pre-school age, writhe upon the floor convulsively, weep and chant, and "speak in tongues." They pray before a cardboard cutout of President Bush.<p>
Speaking in tongues involves babbling a rapid string of non-translatable syllables which means one has been filled with the "holy spirit." A reluctance to babble leaves one out of the holy-spirit filling experience, so the pressure to conform is immense.<p>
During the hearings for Supreme Court Justice Samuel Alito, chanting prepubescents, tears streaming down their faces and tiny plastic fetuses in their hands, call for "righteous judges" who will overturn abortion laws.<p>
Childhood imagination is strictly forbidden. No ghosts, witches, fairies or warlocks--only devils. "Warlocks are enemies of God. And I don't care what type of hero they are, they are an enemy of God, and had it been in the Old Testament, Harry Potter would have been put to death. You don't make heroes out of warlocks. This is a generation that's gonna stand for purity, and righteousness and holiness and you're gonna serve the Lord all the days of your life."<p>
Filmmakers Heidi Ewing and Rachel Grady, who also made <i>Boys of Baraka, </i>argue that the film has no agenda and find Fischer "engaging."<p>
Since its opening, <i>Jesus Camp </i>has attracted considerable controversy. The message of the petite Pentecostals has been defended by Fox News as taken out of context, derided by Bill Maher, and treated sympathetically by the <i>Los Angeles Times.<p>
</i>Fellow evangelical Ted Haggard, who is shown in the film preaching in his Colorado Springs megachurch, concludes that the folks in <i>Jesus Camp </i>look "sinister" and warns his followers to skip the film.<p>
As sad as it is to watch these tortured children, there are reasons for hope. One youngster in the film admits to secretly watching all the Harry Potter films.<p>
When they reach the age of rebellion, these intelligent over-achievers possess all the tools to figure out that they've been duped--perhaps after watching melting glaciers from global warming wash away huge chunks of the coastline. They could very well turn all their passion and fervor against those who stole the magic of their childhood.<p>

