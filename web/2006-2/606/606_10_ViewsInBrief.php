<font face="Times New Roman, Times, serif" size="4"><b>Minutemen and the far right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN CONVERSATION today with an older Black woman, the subject of the recent Columbia University "melee" came up. This woman, a conservative liberal, felt that the International Socialist Organization was exposed as "intolerant" by "rushing the stage" while Minuteman co-founder Jim Gilchrist was speaking.<p>
"If the Ku Klux Klan came to Columbia to speak," I asked her, "and a group of Black students went up on stage and interrupted the speech by unfurling a banner, would you call them 'intolerant'?"<p>
She got the point, but responded that the KKK and the Minutemen are not the same thing. It's true, they're not <i>exactly</i> the same thing, but she was unaware that they were connected. After explaining something about this connection and reminding her of our nation's ugly history of racist vigilantism, she began to change her tune.<p>
This anecdote is revealing. From Columbia University President Lee Bollinger's statement to the right-wing blowhards on TV, the real question is <i>not</i> whether some absolute right to free speech was violated (the recent destruction of habeas corpus, ratification of torture, etc. should have demolished by now the myth that it exists).<p>
The real question is whether or not the Minutemen should be considered outside the pale of "reasonable" debate and discussion. They aren't, but they should be.<br>
<b>Brian Jones,</b> New York<p>

