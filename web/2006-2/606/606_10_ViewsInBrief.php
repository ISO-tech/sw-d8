<p><a name="3str"></a><font face="Times New Roman, Times, serif" size="4"><b>The hell of three strikes</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MY FIANCE is a candidate for early release under the amendment to California's three-strikes law, if it passes. He is, at this time, serving on a plea bargain for burglary and was misrepresented by his public defender.<p>
He learned in the sentencing hearing that the district attorney took one of his prior convictions that had nothing to do with the case and turned it into a prior strike--when he didn't have any priors--and then used it to enhance his sentence. With this enhancement, he received a longer sentence than what he plea-bargained for. He should have been paroled by now under his original understanding of the plea bargain, but instead he is not due to be paroled until April 2008.<p>
It would be so wonderful for the three-strikes laws to be amended to stop these unjust decisions made against non-serious or nonviolent criminal convictions, and to put a stop to the DAs who throw the laws around as if they are judge and jury in one.<p>
Also, it would be wonderful to open the eyes of the state of California's judicial system to look into these public defenders--who are paid for by taxpayers to defend our communities' low-income people who cannot afford the high-paying attorneies, but who often don't care what happens to their clients. I know in my community, the public defenders' office is not for the clients' rights; they seem to be on the DA's side of the courtroom most of the time.<p>
They treat their clients as if they are the scum of the earth half of the time, never seeming to make an effort to help them, and they surely don't help them to understand what they are up against when it comes to plea bargaining or simply agreeing to everything the DA wants. That is their attitude in most of the cases in my community.<p>
So if there is anything I can help with from my community, please feel free to contact me, and I would be glad to donate time to the cause. Thank you for your concerns and work on Proposition 66. It is a good thing we have you fighting for the cause. <br>
<b>Rochelle</b>, from the Internet<p>

