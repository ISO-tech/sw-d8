<p><a name="RV"></a><font face="Times New Roman, Times, serif" size="4"><b>Religion and violence</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE AGREEING that Islam generally has a peaceful history, Hannah Morong takes issue with Alan Maass' claim that it is not "more oppressive or violent than other religions," because, she says, the Koran justifies killing "infidels" ("Clarification on Islam," October 13).<p>
But the truth is that there are passages that are just as bloodthirsty in the major texts of other religious traditions.<p>
The Old Testament, for example, advocates killing not just nonbelievers, but their livestock, too: "Certain men, the children of Belial, are gone out from among you, and have withdrawn the inhabitants of their city, saying, Let us go and serve other gods, which ye have not known...Thou shalt surely smite the inhabitants of that city with the edge of the sword, destroying it utterly, and all that is therein, and the cattle thereof, with the edge of the sword."<p>
As a card-carrying atheist, I have no ax to grind for any religion. The truth is that all religions are flexible and ambiguous doctrines that have been interpreted and reinterpreted to serve the interests of different groups at different times.<p>
Ruling classes and reactionaries use religion for their own purposes, while oppressed groups have often articulated their demands for social justice in religious (including Islamic) terms. To characterize any of the major religions as inherently worse than the others is just wrong.<br>
<b>Phil Gasper</b>, Chair, Department of Philosophy and Religion, Notre Dame de Namur University, Belmont, Calif.<p>

