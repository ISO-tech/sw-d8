<font face="Times New Roman, Times, serif" size="4">The lawyer who prosecutors wanted to lock up for life</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to the Feds' witch-hunt</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 20, 2006 |  Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">CONVICTION-HUNGRY federal prosecutors have failed in their attempt to get radical defense attorney Lynne Stewart thrown behind bars for the rest of her life.<p>
On October 16, federal Judge John Koeltl sentenced Stewart to 28 months in prison for allegedly aiding terrorism when she was a lawyer for Sheik Omar Abdel Rahman, a blind Muslim cleric convicted in 1995 of conspiring with followers to bomb several sites in New York City.<p>
Though any jail sentence for an attorney who was representing her client is an outrage, the 28-month term represents a major concession by the federal government. What's more, Stewart--who said she was "bringing a toothbrush" to the courtroom in case she was taken to jail right away--remains free today, released on bail pending an appeal.<p>
Prosecutors had demanded that Stewart be given the maximum sentence allowable--30 years behind bars. That would been a life sentence for the 67-year-old Stewart, who recently underwent treatment for breast cancer.<p>
The Feds had hoped Stewart's sentencing would be a warning to other lawyers devoted to challenging the Bush administration's shredding of the constitution in the name of the "war on terror."<p>
But Koeltl refused to grant the prosecution's wish. Though claiming that Stewart was guilty of "an irreducible core of very severe criminal conduct," he said he didn't believe she represented the threat the government claimed.<p>
Koeltl emphasized that there was "no evidence that any victim was in fact harmed" by Stewart's actions, and he citied her career as a "lawyer to the poor and the unpopular." "It is no exaggeration to say that Ms. Stewart performed a public service, not only to the court, but to the nation," Koeltl acknowledged.<p>
Stewart told the media outside the courtroom that the judge's sentence was "a great victory against an over-reaching government"--and the result of "doing good work all one's life...You get time off for good behavior usually at the end of your prison term. I got it at the beginning."<p>
The Bush administration's failure to get Stewart locked away for life is also evidence of the widespread campaign of public support in her defense.<p>
Hundreds of protesters gathered on the day of her sentencing for an early morning rally outside the courthouse to show their solidarity. Some 200 supporters jammed the courtroom and surrounding hallways, and another 150 remained outside, chanting "Free Lynne, free Lynne."<p>
As retired lawyer Al Dorfman told the Associated Press, "It's not just Lynne Stewart who is a victim, it's the Bill of Rights that's the victim."<p>
Stewart's conviction--on counts of conspiracy, materially aiding terrorists and violating special prison regulations--highlights how the Bush administration has used the September 11 attacks as an opportunity to attack our civil liberties.<p>
The government admitted that no violence ever resulted from Stewart's actions--chiefly, the "crime" of reading a press release from her client to a Reuters reporter. Yet in the wake of the September 11 attacks and the passage of the USA PATRIOT Act, then-Attorney General John Ashcroft appeared on the <i>Late Show with David Letterman </i>to announce that Stewart would be indicted.<p>
During the trial, prosecutors subjected the jury to "evidence" they admitted had nothing to do with Stewart's own actions--including videotapes of Osama bin Laden. They also introduced secretly taped conversations between Stewart and Abdel Rahman--a clear violation of attorney-client privilege that the government still refuses to say if it had a warrant for.<p>
As Stewart told <i>Democracy Now</i> before her sentencing, she mourns not only the loss of her legal career, but the loss of civil liberties that her conviction represents.<p>
"I believe that I did the right thing as a lawyer," she told Goodman. "I think I said at the moment of my conviction, 'I'd like to think I would do it again.' When I said that, I really and truly meant that the kind of lawyering that we do, that kind that says the client must be protected from the ravages of the government...So much is lost, but I like to think that my principles, the principles I believe in for lawyering, are still intact--and they have nothing to do with criminality or terrorism or joining any conspiracy of my clients."<p>

