<font face="Times New Roman, Times, serif" size="4"><b>A celebration don't make it so</b></font><p>

<font face="Times New Roman, Times, serif" size="3">VIOLENCE IN Iraq is on the rise, and U.S. forces are facing increasing resistance attacks in Afghanistan. But that didn't stop congressional Republicans from setting aside millions to throw the Bush administration a "victory in Iraq and Afghanistan" party, according to recent reports.<p>
Tucked into the fine print of the military spending bill for this past year was $20 million to pay for a celebration in Washington "for commemoration of success" in Iraq and Afghanistan.<p>
Considering the way the "war on terror" has been going, it's no surprise that the money wasn't spent. But Congressional Republicans are hoping next year will give them a chance to celebrate. New military spending legislation approved by the Senate and House before their recess allows the $20 million to be rolled over into 2007.<p>
The original legislation empowered the president to designate "a day of celebration" to commemorate the success of the armed forces in Afghanistan and Iraq and to "issue a proclamation calling on the people of the United States to observe that day with appropriate ceremonies and activities."<p>
Democrats are calling attention to the measure now, in an attempt to embarrass Republicans a few weeks before the mid-term elections. But the legislation was approved in the Senate by unanimous consent, and by an overwhelming majority in the House.<br>
-- <b><i>New York Times</b></i>, October 4, 2006<p>

<font face="Times New Roman, Times, serif" size="4"><b>The field trip that got her fired</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ART TEACHER Sydney McGee is out of a job--apparently because her fifth-grade students may have seen nude artworks on a field trip to a museum.<p>
After 28 years of teaching, McGee is being pushed out from the Wilma Fisher Elementary School in Frisco, Texas, in the wake of a complaint the school received after she took her fifth-grade class to the Dallas Museum of Art.<p>
The trip last April was approved by the principal, and the 89 students were accompanied by four other teachers, 12 parents and a museum docent. But the day after the field trip, McGee was called on the carpet by the principal. She later received a memo in which the principal, Nancy Lawson, wrote: "During a study trip that you planned for fifth graders, students were exposed to nude statues and other nude art representations."<p>
No specific artwork was singled out, but some of the possibly "offensive" works on the field trip might have included: a marble torso of a Greek youth from a funerary relief, from around 330 B.C.; "Shade," a work by sculptor Auguste Rodin; "Flora" by sculptor Aristide Maillol; and Jean Arp's "Star in a Dream."<p>
Since then, McGee has been suspended, denied transfer to another school in the district and told that her contract would not be renewed.<p>
A representative of the Texas State Teachers Association calls it "the first 'nudity-in-a-museum case' we have seen." "Teachers get in trouble for a variety of reasons," said the association's general counsel, Kevin Lungwitz, "but I've never heard of a teacher getting in trouble for taking her kiddoes on an approved trip to an art museum."<p>
The principal and superintendent claim that "no teacher's job status...would be jeopardized based on students' incidental viewing of nude art," but McGee points out that her past job evaluations had been superior--and only turned negative after the museum trip.<p>
Many parents are coming to McGee's defense. "I thought she was the greatest," parent Maijken Kozcara said. But "knowing Texas, the way things work here" she said, "I wasn't really amazed."<br>
-- <b><i>New York Times</b></i>, October 2, 2006<p>

