<font face="Times New Roman, Times, serif" size="4">Court-martialed for refusing to fight a brutal war in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Army's message to resisters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 |  Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SGT. RICKY Clousing deployed to Iraq in November 2004 and spent four months as an interrogator whose job was to extract information from people detained by the infantry unit he served with.<p>
After his return to Fort Bragg, he spent months thinking about the abuse and mistreatment of Iraqi civilians that he witnessed--before deciding he could no longer report for duty. For months, he was absent without leave, before turning himself in August.<p>
On October 12, Clousing was court-martialed and sentenced to 11 months confinement for going absent without leave, but he will serve three months because of a pretrial agreement in which he pleaded guilty. The Army will also reduce Clousing's rank to private, dock two-thirds of his pay during his confinement and issue him a bad-conduct discharge.<p>
In her closing statement, the Army's trial lawyer made it clear that Clousing's prosecution was intended to deter others from following in his footsteps. "A message must be sent," said Capt. Jessica Alexander. "There are thousands of soldiers who may disagree with this particular war, but who stay and fight."<p>
But Clousing said that he would make the same decision again.<p>
On August 11 in Seattle, Clousing announced his decision to turn himself in, surrounded by several dozen members of Iraq Veterans Against the War, during the Veterans for Peace annual convention. He explained why he could no longer participate in what he considers an immoral and illegal war.<p>
"I witnessed the abuse of power that goes without accountability on a number of occasions," he said. "I saw an innocent Iraqi civilian killed.<p>
"I saw the physical, psychological and emotional harassment of civilians. I was with soldiers in convoys when the drivers of Humvees would smash into civilian vehicles on the highway for no reason, and even roll down the window and bash out windows of other vehicles. One of the soldiers in a turret on a convoy opened up fire on a civilian's livestock, and thought it was humorous and laughed about it.<p>
"The emotional harassment includes men and boys being incarcerated with no legal basis and no criminal evidence, and being unable to contact their families.<p>
"As an interrogator at an interrogation facility, I witnessed four brothers at one time, ages ranging from 12 to 24, who were detained with no evidence and no circumstances of capture that would warrant that type of action. They were kept for longer than the authorized 72 hours. They actually stayed at our facility for approximately three to four weeks. Their parents had no idea where they were--if they were killed by U.S. troops, kidnapped, blown up by an IED (improvised explosive device).<p>
"Some might view these incidences as "isolated," as the news commonly refers them. A lot of times, the only events in Iraq that make the media are "significant" events, such as [the massacre at] Haditha, the raping of the 14-year-old, the incidents in Abu Ghraib.<p>
"But the events I witnessed are basically the larger picture of the daily devastation of occupation, and there are daily incidents where innocent Iraqis are being killed. This is not reported by the media because it's not 'significant' enough."<p>

