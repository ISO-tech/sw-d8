<font face="Times New Roman, Times, serif" size="4">New policies on capping wages and limiting health care</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Wal-Mart tightens the squeeze on workers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 20, 2006 |  Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JOSH GRYNIEWICZ reports on Wal-Mart's new tactics in its war on workers' wages and benefits.</b><p>

<font face="Times New Roman, Times, serif" size="3">WAL-MART, the nation's largest private employer with 1.4 million workers, is leading the corporate attack on wages and benefits with renewed vigor.<p>
In spite of a recent "community-friendly" public relations campaign aimed at repairing its notorious reputation for low wages, poor benefits and labor violations, the company is putting an even tighter squeeze on employees--many of whom already live below poverty level--and its main competitors are following suit.<p>
Specifically, Wal-Mart is rolling out a campaign to cap employee wages and reduce what it spends on health care benefits--by increasing its part-time workforce and pushing out longer-standing employees.<p>
"Given the impact of tenure on wage benefits, the cost of an associate with seven years of tenure is almost 55 percent more than the cost of an associate with one-year tenure, yet there is no difference in his or her productivity," reads an internal corporate memo leaked to the <i>New York Times</i>. The memo recommends hiring more part-time employees, because "the shift to more part time associates will lower Wal-Mart's health care enrollment."<p>
According to Wake Up Wal-Mart, a UFCW campaign exposing the company's abuses, Wal-Mart already has the highest number of workers without health care coverage of any employer--it fails to provide benefits for 775,000 employees. As a result, an average of 13 percent of its workforce has to seek assistance from the Medicare and Medicaid public health care systems.<p>
Now, with its new strategy getting underway, Wal-Mart has announced that it is cutting health care options for employees, meaning this situation is likely to get worse.<p>
Under the system outlined in the 2007 Wal-Mart Medical Benefits Booklet, new hires now get to choose between the perversely titled "Value Plan" or "Freedom Plan."<p>
While Wal-Mart's marketing machine is heralding a new discounted generic drug program for customers, employees opting for the "Value Plan" could be saddled with a $300 pharmacy deductible, plus additional deductibles that could conceivably total $6,000. On the other hand, the "Freedom Plan" only really offers freedom from coverage--premiums are jacked up by 6.9 percent, and getting a spouse covered will cost an $1,800 surcharge.<p>
According to Wake Up Wal-Mart's analysis, a family depending on one of the offered plans could pay as much as 60 percent of their income for health insurance, and a single worker could pay as much as 30 percent.<p>
Wal-Mart is also planning on doubling to 40 percent the percentage of its employees who are part time. This will involve forcing full-time employees with seniority off the schedule.<p>
Some Florida stores have reportedly begun to institute this "weed-out" program by not permitting older employees with leg or back problems from sitting on stools while working as cashiers, store greeters or fitting-room attendants--even after using them for years.<p>
According to the <i>New York Times, </i>Wal-Mart employees say that management expects them to be available for any shift. In 1,900 stores, that means 24 hours a day.<p>
And for this around-the-clock loyalty, employees will have their already low wages capped--denying them annual raises if their pay is at or above cap.<i> The Times</i> didn't report whether Wal-Mart CEO Lee Scott would see a cap on his own compensation, which totaled $17 million last year--nearly 1,000 times higher than the annual wages for an average U.S. Wal-Mart "associate," according to the company's own figures.<p>
It's accepted fact that Wal-Mart's labor policies have lowered the bar on wages, benefits and working standards, not just in the retail industry, but throughout the private sector. Wal-Mart competitors Target and Sears have both adopted similar strategies to Wal-Mart's newest anti-worker offensive.<p>
A 2006 survey conducted by the Bureau of Labor Statistic (BLS) confirms the trend. Covering 10,370 private companies employing 105 million workers primarily in the private sector, the BLS survey found that only 71 percent of workers have access to health insurance through an employer.<p>
Low-wage earners making less than $15 an hour face a significantly less access--only 57 percent are offered health benefits, and only 38 percent participate.<p>
"The bottom line," the BLS report concludes, "is that union workers continue to receive benefits that are far superior to those provided to nonunion workers, and most low-wage workers are not covered by benefit plans or cannot afford to participate in them."<p>

