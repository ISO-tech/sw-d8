<font face="Times New Roman, Times, serif" size="5"><b>Starving Gaza into submission</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">"IS IT possible to force a whole people to submit to foreign occupation by starving it?" Israeli leftist Uri Avnery wrote in recent column.<p>
"[T]he governments of Israel and the United States, in close cooperation with Europe, are now engaged in a rigorous scientific experiment in order to obtain a definitive answer. The laboratory for the experiment is the Gaza Strip, and the guinea pigs are the million and a quarter Palestinians living there."<p>
Avnery wrote this as Israel was accelerating its assault on the Palestinians of Gaza--with Israeli forces intensifying ground raids and air assaults in Gaza last week.<p>
On October 12, 13-year-old Suhaib Kadiah became the 92nd Palestinian child to be killed this year when she was shot--along with four other family members--by Israeli troops who said they were looking for tunnels built by militants in Gaza's southern Khan Younis area. Already, the number of Palestinian children killed this year is twice as high as all of 2005, according to the United Nations Children's Fund.<p>
On October 13, at least seven Palestinians were killed and 10 wounded in an air strike in northern Gaza, bringing the number of Palestinians killed in Gaza since June 26, the start of Israel's "Operation Summer Rains," to at least 285.<p>
This new terror comes just after the release of a report by Italian journalists that Israel was testing a horrifying new weapon during its assault on Gaza. The legs of the injured were sliced from their bodies "as if a saw was used to cut through the bone," Dr. Habas al-Wahid, head of the emergency room at the Shuhada al-Aqsa hospital in Gaza's Deir el-Balah, told Italy's Rai News 24.<p>
In mid-July, Gaza doctors reported an unusual number of people who had lost legs and whose bodies were completely burned. Doctors said that the injuries weren't accompanied by the usual metal shrapnel, and some said that they removed particles from wounds that couldn't be detected with an X-ray machine.<p>
According to the Italian report, the new Israeli weapon is similar to one developed by the U.S. military called Dense Inert Metal Explosive, which produces an intense and lethal blast contained to a small area. "One of the ideas is to allow those targeted to be hit without causing damage to bystanders or other persons," Israeli Air Force Major Gen. Yitzhak Ben-Israel, formerly head of the Israel Defense Force's weapons-development program, told Rai New 24.<p>
Meanwhile, starvation conditions in Gaza and the West Bank are fueling tensions among Palestinian supporters of the former ruling Fatah party and the militant Hamas party, which won Palestinian elections earlier this year.<p>
A Fatah lawyer petitioned Palestinian Authority President Mahmoud Abbas to dismiss the Hamas-led government on October 16, claiming that it was to blame for clashes earlier this month between police led by Hamas and security forces loyal to Abbas.<p>
Crushing poverty and the lack of funds to pay state workers--the result of Israel's hold on tax revenue collected for the PA after Hamas' election--are adding to the tensions.<p>
Hamas leaders believe that the U.S. government is giving $42 million to Fatah, officially for conducting upcoming elections, but in reality for purchasing weapons and military training to take down Hamas. Israel and the U.S. will stop at nothing to make sure they call the shots in the Middle East.<p>

