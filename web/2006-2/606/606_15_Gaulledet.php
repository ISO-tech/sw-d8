<font face="Times New Roman, Times, serif" size="4">University students occupy campus to protest new president</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Revolution" at Gallaudet</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Yarrison</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 20, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--In two waves of arrests, police swept up 134 protesters October 13 at Gallaudet University, the nation's premier school for the deaf.<p>
The arrests were ordered by outgoing President I. King Jordan on the eighth night of a standoff between students who have seized their campus and an administration unwilling to consider their primary demand--that Jane Fernandes, the university's newly selected president, step down, and that the candidate search procedure be reopened.<p>
Jordan--who became president in1988 following a wave of student protests under the demand of "Deaf President Now"--is taking a hard line in his effort to install Fernandes, known as an aggressive and unaccountable budget-cutter in her six years as provost of Gallaudet. Jordan even issued a statement claiming he "did not choose to arrest the students, they chose to be arrested."<p>
The protesters peacefully complied with police, and there were no reports of misconduct. However, the arrests have only served to ignite students' outrage, and the next day their numbers swelled to over 300, a significant group on a campus of only 1,800 students.<p>
Students seized the central Hall Memorial Building October 12 in opposition to the selection process that put Fernandes in place. "Communication is very, very, very un-well at this campus," student Deborah Chen Pichler told the local news on the fifth day of the sit-in.<p>
The faculty also weighed in against Fernandes with two "no confidence" votes and a resolution calling for her to resign that passed October 16 by 138-24. The Student Congress passed a resolution refusing to recognize her as president, and the search process had already antagonized students by eliminating more qualified minority candidates.<p>
Through the struggle, more bitter grievances harbored by Gallaudet students have come out. On one of numerous blogs started in the past week by protesters, alumni and sympathizers, a student decried the de facto "interpreter ban" that deters students from communicating with the press. "The administration," she writes, "comprised of DEAF people, is cutting off the students' voices and telling them they don't have the right to equal access."<p>
The suppression of deaf voices has touched off deep-seated anger against audism--discrimination based on hearing disability. Campus interpreters are available, but must be scheduled and paid for, rendering their service prohibitive for working-class students engaged in spontaneous disobedience.<p>
A shallow and offensive editorial in the <i>Washington Times</i> ("Calm Down, Gallaudet") claims that the dissenters are "working against the university," that they disagree with the appointment of Fernandes on grounds that she isn't "deaf enough," having experienced hearing loss later in life rather than being deaf from birth. "It is the prerogative of the trustees, not the faculty or students, to appoint the university's president," the <i>Times </i>editorial declared.<p>
This condescending view reflects complete incomprehension of the situation at Gallaudet. Responding to a near-identical editorial in the <i>Times'</i> "liberal" counterpart, the <i>Washington Post,</i> blogger Mishka Zena wrote that "[the protest] was never about Fernandes not being deaf enough," but rather the unbalanced candidate selection procedure.<p>
The<i> Post </i>editorial also preached compromise, claiming that "if the protesters really care about Gallaudet, they will open up the halls to learning and work toward reaching a middle ground."<p>
Yet with active protesters making up a sixth of the student population, it is obvious that they care about the university, along with faculty, staff and alumni. It is the trustees who refuse to listen, marginalizing the students' voices in concert with the local press.<p>
Meanwhile, tent cities have appeared at deaf schools across the nation in solidarity. When this writer asked to see the tent city at the blockaded Gallaudet entrance, Alex Zernovoj, president of the Graduate Student Association, said through an interpreter, "There's no need to see the tent city. Tent city has become the whole campus. All of the student groups, the faculty, everyone has joined our struggle. This is a revolution."<p>

