<font face="Times New Roman, Times, serif" size="5"><b>What's at stake in the Teamster election</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 20, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JOE ALLEN, a former member of Teamsters Local 705 and driver at UPS, looks at the stakes in the upcoming Teamster union election.</b><p>

REFORMER TOM Leedham was mounting a strong challenge to incumbent James P. Hoffa as 1.4 million members of the International Brotherhood of Teamsters received mail ballots in the union's presidential elections.<p>
Hoffa has traded heavily on the name of his late father, former Teamster President James R. Hoffa, since being plucked from obscurity in the early 1990s to challenge Ron Carey, the Teamsters' first reform leader.<p>
It was under Carey that the Teamsters won a two-week strike at UPS in 1997, a struggle that captured the imagination of working people across the U.S. Hoffa first won the union's top office in 1998 after government overseers forced Carey to step aside following corruption charges that were later disproved in federal court.<p>
Today, the once-boastful Hoffa supporters are silent, a sign of the depressed state of those who thought the Hoffa name alone would stop the downward slide of the union.<p>
For Chicago UPS package car driver Dave Healy, a 25-year veteran of the union, the choice in the upcoming election is clear. "The last seven years under Hoffa have been a disaster," he said. "We have made no progress. It's time for a change."<p>
Healy supports Leedham, the secretary-treasurer of Local 206 in Portland, Ore., who also served as head of the union's warehouse division under Carey. As the leader of the Strong Contracts and Good Pensions slate, Leedham is challenging Hoffa for the third time in this fall's election.<p>
The winner will run the union for the next five years and negotiate some of the largest union contracts in the country.<p>
Nevertheless, voter turnout could be low. "In the previous two elections [1998 and 2001], Leedham ran against a myth. Today he is running against cynicism," one longstanding activist with the Detroit-based Teamsters for a Democratic Union (TDU) recently said in a telephone interview.<p>
TDU is the three-decades-old reform organization made up of rank-and-file Teamsters from across North America and Puerto Rico. It has spearheaded the fight for democratic reforms that make the Teamsters unique compared to most other Americans unions--including the right to vote on contracts, the direct election of convention delegates and, most importantly, the direct election of the top officers of the union by secret ballot.<p>
Despite these reforms, the Teamsters are still a very corrupt and authoritarian union--and, under Hoffa, the union has repeatedly failed to resist the employers' relentless offensive. For example, UPS now owns the former Overnite Transportation, a major nonunion freight trucking company that the Teamsters failed to organize in a humiliating three-year strike initiated by Hoffa in 1999.<p>
Today, UPS is an international giant in the freight industry, with a growing section of its workforce in the U.S. outside union contracts. The company is sure to try to squeeze a weakened Teamster union in next year's contract talks.<p>
Hoffa has also been unable to stop employers' efforts to gut the union's regional pension funds, as Teamsters' trustees on pension boards have repeatedly bowed to management demands to cut benefits sharply.<p>
At the same time, large swathes of the union remain steeped in corruption. In 1999, Hoffa appointed Ed Stier, a former federal prosecutor, to head up his newly created anti-corruption program. Stier's previous record of fighting corruption in a New Jersey Teamster local was so slow that many perceived him to be just a shill for Hoffa.<p>
To the surprise of virtually everyone, Stier's investigators swooped down on Chicago and produced a devastating report which concluded that one-third of Teamster locals in Chicago are under mob influence and involved in possible corrupt practices.<p>
All this baggage is weighing heavily on Hoffa's campaign. Yet the reform movement has had difficulty getting back on track since the ouster of Carey, who was barred from membership in the Teamsters by the Internal Review Board (IRB), the draconian investigative and disciplinary body created by the 1989 settlement between the Teamsters and the Justice Department.<p>
Although Carey was ultimately vindicated in court, his ban from the union remains in place--and the damage to the reform movement was done. Today, many Teamster activists feel caught between a weak and declining union on one side, and rich and immensely powerful corporations on the other.<p>
But as the crisis of Teamster pension funds continues and job security is threatened by the explosion of nonunion operations at UPS and other companies, this sentiment could force Hoffa from office.<p>
Whatever the outcome, the contest between Hoffa and Leedham is likely to be a much closer race that the lopsided Hoffa victory of five years ago.<p>

