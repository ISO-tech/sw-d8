<font face="Arial, Helvetica, sans-serif" size="2">September 29-October 6, 2006</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S.-Mexico border is more than just a boundary that separates two countries. But what else it represents depends on who you are.<p>

