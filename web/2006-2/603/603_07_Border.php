<font face="Times New Roman, Times, serif" size="3">For U.S. politicians, the border represents an intoxicating brew of issues to use for political gain--"illegal aliens," "drug dealers," "a threat to our way of life" and, of course, "terrorists."<p>
For U.S. corporations, the borderlands mean big profits, a zone where non-U.S.-born workers will work for low wages under inhuman conditions.<p>
For the vigilante groups that dot the border states, the rightward lurch in mainstream politics has enabled them to play out their "border war" fantasies on a much larger stage.<p>
For the millions of workers and poor across Central and South America struggling to feed their families, the border represents the hope of a better life <i>en el norte</i>. But crossing it often leads to great expectations giving way to grim realities.<p>
In August, <i>Socialist Worker's </i>JUSTIN AKERS CHAC&Oacute;N, ERIC RUDER and NOHELIA RAMOS traveled from San Diego/Tijuana to El Paso/Ciudad Juarez and back to speak with activists, experts and migrant workers. Here we publish their day-by-day account of the journey.<p>

