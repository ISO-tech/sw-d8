<font face="Arial, Helvetica, sans-serif" size="4" color="#990000"><b>What else to read</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">For an excellent survey of past and present political dynamics on the U.S.-Mexico border, read<i> <a href="http://www.haymarketbooks.org/Merchant2/merchant.mv?Screen=PROD&amp;Store_Code=Haymarket&amp;Product_Code=UHPNOI" target="_blank">No One Is Illegal: Fighting Racism and State Violence on the U.S.-Mexico Border</a>, </i>by Justin Akers Chac&oacute;n and Mike Davis. <i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&amp;location=http%3A%2F%2Fwww.amazon.com%2FOperation-Gatekeeper-Remaking-U-S-Mexico-Boundary%2Fdp%2F0415931053%2Fsr%3D8-1%2Fqid%3D1159165643%2Fref%3Dsr%5F1%5F1%3Fie%3DUTF8%26s%3Dbooks&amp;tag=socialistwork-20" target="_blank">Operation Gatekeeper: The Rise of the 'Illegal Alien' and the Remaking of the U.S.-Mexico Boundary</a> </i>by Joseph Nevins dissects border policy during the Clinton era and the anti-immigrant sentiment it stirred up.<p>
The <a href="http://derechoshumanosaz.net" target="_blank">Coalici&oacute;n de Derechos Humanos</a> and <a href="http://www.nomoredeaths.org/" target="_blank">No More Deaths</a>, both Arizona-based human rights organizations active around border issues, have useful Web sites that provide news analysis, statistics and details about their actions and initiatives.<p>
Among the many articles in the <i><a href="http://www.isreview.org" target="_blank">International Socialist Review</a> </i>covering the rise of border militarization as well as the eruption of the immigrant rights movement this past spring are <a href="http://www.isreview.org/issues/43/minutemen.shtml" target="_blank">"Vigilantes at the border: The new war on immigrants"</a> and <a href="http://www.isreview.org/issues/47/waronimmigrants.shtml" target="_blank">"War on immigrants."</a><p>
There are three recent films on border issues. <i><a href="http://crossingaz.com" target="_blank">Crossing Arizona</a></i> is an excellent documentary through the eyes of those directly affected. It's difficult to find--check <a href="http://crossingaz.com" target="_blank">the film's Web site</a> for information about upcoming screenings. <i><a href="http://www.maquilapolis.com/" target="_blank">Maquilapolis</a> </i>is a new film about workers in Tijuana's maquiladoras; its national broadcast premiere is October 10 on PBS's <i>P.O.V. </i>series. <i><a href="http://www.afscstore.org/store/product_info.php?cPath=79&amp;products_id=5148" target="_blank">Rights on the Line</a> </i>is Ray Ybarra and Tamaryn Nelson's documentary about the growth of the vigilantes operating along the border.<p>
For those interested in border activism, check out the upcoming Border Social Forum in Ciudad Juarez on October 13-15. More information is available at <a href="http://forosocialfronterizo.blogspot.com" target="_blank">forosocialfronterizo.blogspot.com</a>.
