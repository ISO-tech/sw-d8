<font face="Times New Roman, Times, serif" size="5"><b>ICE sets shameful trap for day laborers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Rebecca Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 29, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ELEVEN DAY laborers were arrested and detained by the Immigration and Customs Enforcement Agency (ICE) in a disgraceful sting operation in Danbury, Conn.<p>
The incident began at 7 a.m. on September 19 when the workers were gathering in Kennedy Park to wait for employers to come with work for that day. An unmarked silver van--with construction helmets and day-glow safety vests visible inside--made three stops, picking up 11 laborers in all. <p>
The van was being used by ICE agents--who, under the pretence of being an employer looking for workers, instead arrested and detained the laborers. <p>
The van took the 11 directly to the Danbury Police Department parking lot, where they were transferred to another vehicle bound for Hartford. They were held in Hartford long enough for processing, and were then transferred to a detention facility in Suffolk County, Mass. <p>
In the hours and days that followed, family members grew alarmed at the disappearance of the 11 laborers. But many were afraid to approach the police to ask if their loved ones were on the list of those arrested. "He just didn't come home," said the nephew of one of the missing workers.<p>
One year ago, anti-racist activists from several groups held a demonstration in the park where the detainees were picked up. The demonstration was a protest against the racist Connecticut Citizens for Immigration Control (CCIR), which occupied the park early in the morning, frightening away many workers, documented and undocumented alike, who wait there for work. <p>
The antiracist demonstrators set up across from the CCIR, chanting, "We don't want your racist fear, immigrants are welcome here!" and the racists were forced to abandon their protest early. <p>
A few months before, some 1,300 people held a silent march through Danbury to protest Mayor D. Mark Boughton's plan to deputize local police officers to enforce immigration laws. After the march, Boughton backed down from sanctioning mass deportation sweeps.<p>
This is the context in which the national crackdown by ICE has visited Danbury. The 11 workers arrested by the Feds need us to show our support. <p>
