<font face="Times New Roman, Times, serif" size="5"><b>Police protect the Nazis in Ohio</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Hughley and Shane Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 29, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">COLUMBUS, Ohio--About 70 people turned out here September 23 to protest 50 Nazis of the National Socialist Movement (NSM).<p>
The NSM was accompanied by the Ku Klux Klan and was protected by police on horseback and in riot gear, as well as two layers of security fence. One plainclothes officer even went so far as to spray paint the anarchist "A" on his poncho to try to fit into the crowd of demonstrators.<p>
Meanwhile, police led protesters into three city blocks of fenced-in area--the "protest pen." Protesters were searched and some were forced to leave personal items behind, and had to retrieve them from a trash bin afterward. Yet the white supremacists got free rein on state property to spew antigay, racist, anti-immigrant and anti-Semitic remarks.<p>
However, most of the local left stayed away, under pressure from Columbus Mayor Michael D. Coleman and other city officials, who did everything in their power to deter protesters. Meanwhile, thousands of dollars in city funding were used to pay officers overtime and protect hate speech.<p>
Mayor Coleman's efforts were countered by a small group of activists from around the state and a coalition of local protesters, who together marched from Ohio State University to speak out against racism.<p>
The march split into two sections when one group chose not to enter protest pen. This divided the protest and lessened the effectiveness of the demonstration as a whole--a lesson learned for the next time the racists come back. <p>
Despite the unequal terms of the protest, antiracists drowned out the fascists for the two hours their permit allowed. The message is clear: To fight racism and the right, the left needs unity in confronting the racists.<p>

