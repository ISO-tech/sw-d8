<font face="Times New Roman, Times, serif" size="5"><b>New setback for U.S. witch-hunt</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 29, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A defeat for the Bush administration's Justice Department, federal prosecutors announced that they were dropping the most serious charge against Palestinian activist Muhammad Salah.<p>
Salah was accused of providing material support to an organization deemed to be "terrorist" by the U.S. government--the Palestinian group Hamas. He was arrested in 2003 as part of a nationwide witch-hunt--led by George Bush's first Attorney General John Ashcroft--against Palestinian activists, charities and supporters.<p>
Hamas was declared a "terrorist" organization following the 1993 bombing of the World Trade Center in New York City, though it had no involvement in the failed attempt to destroy the skyscrapers. Hamas stunned the U.S. and Israeli governments earlier this year by winning Palestinian Authority elections.<p>
Salah, a U.S. citizen of Palestinian descent, is still under indictment on two other charges. But Salah's attorney, Michael Deutsch, told the <i>Chicago Tribune </i>that the material-aid-to-a-terrorist-group allegation was "the heart of the case. It really changes everything."<p>
The witch-hunt against Salah should concern anyone who cares about constitutional rights. In 1993, Salah was arrested by Israeli soldiers at a checkpoint in Gaza, carrying money that he said was for humanitarian aid.<p>
According to court papers, Salah says he was then "hooded, bound, deprived of sleep, housed in a refrigerator cell, threatened, physically abused, held incommunicado and denied access to a lawyer until he made oral statements and signed written statements in Hebrew, a language he did not speak or understand." <p>
This "confession" supposedly proves that Salah was a member of Hamas. Salah served four years in an Israeli jail before returning to his home in Bridgeview, Ill., a suburb southwest of Chicago.<p>
Salah's trial judge Amy St. Eve allowed the false confession obtained by the Israelis to be admitted as evidence against him.<p>
The only other "evidence" of Salah's alleged relationship with Hamas was the testimony of Jack Mustafa, an undercover informer who became friends and worked with Salah starting in 1997. But the FBI itself had "significant questions about the credibility of this informant," according to a bureau report obtained by the defense. The defense team's questioning of Mustafa's credibility apparently led prosecutors to drop the main charge.<p>

