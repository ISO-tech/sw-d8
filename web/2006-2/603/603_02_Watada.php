<font face="Times New Roman, Times, serif" size="5"><b>Resister punished for speaking out</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 29, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">EHREN WATADA faces another charge of violating the Uniform Code of Military Justice--for the "crime" of speaking at an antiwar event.<p>
In mid-September, Watada--an Army first lieutenant who is the first commissioned officer to go public with his refusal to deploy to Iraq--was notified that he would face a seventh charge of conduct unbecoming an officer, after he spoke at the annual Veterans for Peace (VFP) convention in Seattle.<p>
In July, Watada--who was stationed at Fort Lewis, just outside Tacoma, Wash.--was officially notified of six military charges against him stemming from his decision not to deploy. A final decision on the charges is expected this month, but Watada could faces up to eight-and-a-half years in prison. <p>
Watada says he believes the war and occupation of Iraq are illegal, and participation in the war would be illegal also.<p>
At the VFP convention, Watada talked about what he called a "radical idea. It became instrumental in ending the Vietnam War--but it has been long since forgotten. The idea is this: that to stop an illegal and unjust war, the soldiers can choose to stop fighting."<p>
"Awareness of the history of atrocities and destruction committed in the name of America--either through direct military intervention or by proxy war--is crucial," Watada continued. "[Soldiers] must realize that this is a war not out of self-defense but by choice, for profit and imperialistic domination."<p>
Watada's defense campaign is asking for groups to begin preparing for solidarity actions at federal buildings, military recruiting centers and elsewhere. Plans are in the works for an international day of action, with a mass protest to converge on Fort Lewis for the actual court-martial.<p>
