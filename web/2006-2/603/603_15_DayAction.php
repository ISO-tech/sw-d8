<font face="Times New Roman, Times, serif" size="4">Activists planning for a September 30 day of protest</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Action for immigrant rights</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 29, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEPTEMBER 30 will mark a day of action in support of immigrant rights across the U.S. Activists are focusing on turning back the recent anti-immigrant tide by pushing for legalization for all and calling for an end to the recent wave of raids and deportations of undocumented immigrants across the U.S. <p>
The day of action is sponsored by the recently formed National Alliance for Immigrant Rights (NAIR). Though more recent protests for immigrant rights have not seen the same kind of historic turnouts that occurred at the massive marches in the spring of this year, NAIR and other activists believe that the September 30 actions can be a tool to help strengthen local organizing and continue building the movement for immigrant rights.<p>
-- In <b>Chicago, </b>activists with the March 10 Coalition, the Chicago Workers Collaborative and more are planning a 10 a.m. rally at the Social Security office to protest the Department of Homeland Security's proposed oversight of the Social Security "no-match" letter system, which would lead to a wave of firings of undocumented immigrants. Under the new regulations, companies with workers whose Social Security numbers don't match Social Security files would be compelled to terminate them.<p>
Later in the day, an interfaith service, sponsored by Centro Sin Fronteras, will be held at the Adalberto United Methodist Church, to support Elvira Arellano, an undocumented mother who has taken sanctuary at the church since August.<p>
United Farm Workers cofounder Dolores Huerta is helping to organize a National Fast for Immigrant Justice, which will feature a 1 p.m. rally in Chicago's Federal Plaza for those taking part in the fast.<p>
-- In <b>Los Angeles, </b>at least 250 activists will reportedly participate in civil disobedience on Century Boulevard on September 28, following a march sponsored by the We Are America Coalition, UNITE HERE and others. The march and civil disobedience will take place near the Los Angeles International Airport where a dozen hotels employ thousands of immigrant workers.<p>
-- In <b>Connecticut, </b>local activists are organizing two actions. The first is an emergency response to the September 19 arrest and detention of 11 undocumented day laborers in Danbury, Conn. The men were picked up by police posing as contractors looking to hire workers. There will be a picket outside Danbury City Hall at 2 p.m. September 30 to demand their immediate release, a stop to the raids and deportations and legalization for all.<p>
On the same day, a march and rally will take place starting at 2 p.m. at the corner of Capitol and Broad Streets in Hartford, Conn., that aims to build unity between immigrant rights and antiwar activists around demands such as legalization for all, no militarization of the border, an end to the attacks on Arabs and Muslims and money for jobs and education, not for war and occupation.<p>
-- In <b>Toledo, Ohio, </b>the Farm Labor Organizing Committee (FLOC) has called for a march for immigrant rights at 4 p.m. on September 30, to be led by North Carolina FLOC members and Latino veterans of the armed services.<p>
<i>Rebecca Lewis contributed to this report</i>.<p>

