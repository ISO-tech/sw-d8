<font face="Times New Roman, Times, serif" size="5"><b>Greed is their secret ingredient</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Kirstin Roberts</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 1, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Fast Food Nation, </b></i>directed by Richard Linklater, written by Eric Schlosser and Linklater, based on the book by Schlosser, starring Greg Kinnear, Wilmer Valderrama and Patricia Arquette.</font><p>

<font face="Times New Roman, Times, serif" size="3">ERIC SCHLOSSER made a great choice in deciding to make his best-selling nonfiction expos&eacute; of industrial food production in the U.S., <i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&amp;location=http%3A%2F%2Fwww.amazon.com%2FFast-Food-Nation-Eric-Schlosser%2Fdp%2F0060838582%2Fsr%3D8-1%2Fqid%3D1164762429%3Fie%3DUTF8%26s%3Dbooks&amp;tag=socialistwork-20&amp;linkCode=ur2&amp;camp=1789&amp;c" target="_blank">Fast Food Nation</a>,</i> into a fictional drama. In doing so, he and director Richard Linklater have made a film that puts the human beings who are part of this system--above all, the workers--into the spotlight.<p>
While serving up no solutions to the insatiable greed of an industry that places profit over human lives and health every day, this film does put the issues onto the table in an immediate, gut-wrenching way.<p>
Of particular note in this era of immigrant-bashing is the central story of a Mexican family who cross the border illegally in order to find jobs in a Colorado slaughterhouse. After losing one of their fellow crossers to death in the desert, they are forced to labor in the meatpacking plant where workers are treated just as ruthlessly as the cows.<p>
<i>Fast Food Nation </i>also follows the story of a fast food company executive (played with just the perfect amount of charming sleaziness by Greg Kinnear) who is sent to investigate why "shit is turning up in the burgers," causing fears of an <i>E. coli</i> outbreak. Kinnear discovers the reality of how the meat is produced in conditions to maximize profit without regard to its edibility, but his crisis of conscience is outweighed by his desire to keep his cushy job.<p>
Teenaged fast food workers are also portrayed with believability as some plot to rip off their creepy manager and another quits her job in disgust after becoming involved with a environmental action group at the local college.<p>
Schlosser and Linklater manage to raise so many issues in this film, a few get lost in the shuffle, although better they be raised than not. Importantly, gross sexual harassment of immigrant workers in the plant by a supervisor finds its parallels in routine, "more acceptable" womanizing by company executives on a business lunch.<p>
While several good laughs come with Schlosser and Linklater's satirical take on strip mall, mass-produced culture, the only counterpoints to it are middle-class renegades who look back to bygone days where individuality reigned supreme. Activists and former activists discuss how to take on the industry but remain stuck in a sense of powerlessness around how to address the problems they see around them.<p>
Notably, the idea of class struggle, unionization or that the workers who figure so prominently in this film as its main characters hold the potential to fight the corporations goes completely unmentioned. Given the recent struggles by unionized and nonunion, predominantly immigrant workers in the food industry, this is a big oversight.<p>
Still, this is a film well worth seeing. Its final scene, where the audience is finally introduced to the notorious kill-floor of the slaughterhouse (Linklater managed to get permission to film the real thing) through the eyes of undocumented worker Sylvia (Catalina Sandino Moreno), is unforgettable.<p>
The look in her eyes as she's shown her spot on the line removing kidneys and intestines of the cows killed only 30 seconds earlier, translates both her despair and rage. This glimpse inside our lunch, into the reality of the workers' lives literally churned up in industrial meat grinders, is powerful filmmaking even if it doesn't give a sense of how to fight the power.<p>

