<font face="Times New Roman, Times, serif" size="5"><b>They want him to rot behind bars</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 1, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">JAILED PALESTINIAN activist Dr. Sami Al-Arian has been sentenced to an additional 18 months in prison for refusing to testify for the government.<p>
Al-Arian is a former University of South Florida professor who has spent more than three years behind bars on charges of aiding terrorism--despite the fact that the jury in his initial trial acquitted him on eight of the most serious charges and deadlocked on the others.<p>
Earlier this year, with prosecutors threatening to retry him, Al-Arian agreed to plead guilty to a single count of providing material support for nonviolent activities of the Palestinian Islamic Jihad, which the U.S. government deems a terrorist organization. His plea deal--deportation following additional time in prison--was, according to his attorneys, a way to end the government witch-hunt against him.<p>
But a federal judge and prosecutors have gone out of their way to prolong Al-Arian's suffering.<p>
First, Judge James Moody imposed the maximum sentence of 57 months on the one count Al-Arian pled guilty to, despite the fact that prosecutors had originally asked for less time.<p>
Under the sentence, Al-Arian would have been slated for release on April 13, 2007. Instead, Al-Arian was transported under harsh conditions to the Northern Neck Regional Jail in Warsaw, Va., last month as part of a new plan to force him to testify against the International Institute of Islamic Thought, an Islamic charity that the government claims aided terrorism.<p>
Now, Al-Arian has been found guilty of contempt for refusing to testify--despite the fact that, according to his lawyers, his original plea agreement was specifically designed to keep him from having to provide additional testimony.<p>
The government's anti-Muslim bias has been on display in its recent treatment of Al-Arian. The jail in Virginia where Al-Arian was transferred to initially denied him a Koran or prayer rug, according to reports.<p>
And one of Al-Arian's attorneys says that Assistant U.S. Attorney Gordon Kromberg responded to a request to delay Al-Arian's transfer to Virginia until after the Muslim holy month of Ramadan by saying, "If they can kill each other during Ramadan, they can appear before the grand jury--all they can't do is eat before sunset. "I believe Mr. Al-Arian's request is part of the attempted Islamization of the American Justice System," Kromberg reportedly said. "I am not going to put off Dr. Al-Arian's grand jury appearance just to assist in what is becoming the Islamization of America."<p>
Now, Al-Arian has been given an additional 18-month sentence--which he will be forced to serve out on top of the remaining 174 days from his original sentence unless he agrees to testify. He could now remain in prison until November 2008.<p>

