<font face="Times New Roman, Times, serif" size="5"><b>Killed in a hail of NYPD bullets</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jared Rodriguez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 1, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK City police pumped 50 bullets into a car carrying three unarmed African American men in the early morning hours of November 25, killing one man on his wedding day.<p>
The hail of bullets killed 23-year-old Sean Bell, a father of two children aged three years and five months, who was to be married later in the day. Two friends, Joseph Guzman and Trent Benefield were seriously wounded. All five NYPD officers on the scene fired at the car. One emptied two full magazines from his 9mm Glock, firing a total of 31 times.<p>
The three men were leaving a bachelor party at Club Kalua in Queens at 4 a.m. According to the police story, the men were followed out and ordered to stop by an undercover officer who suspected one was carrying a gun. When their car lurched forward and struck him, police officials say, the officer opened fire and began shouting, "He's got a gun," prompting other officers on the scene to begin firing as well.<p>
But the cops' version is directly contradicted by the eyewitness account of Trini Wright, a dancer at the club.<p>
Wright said that she was accompanying the men to a diner and was putting her bag in the trunk of their car when the police minivan appeared. "The minivan came around the corner and smashed into their car," Wright told the <i>New York Daily News. </i>"[The police] jumped out shooting. No 'stop.' No 'freeze.' No nothing."<p>
New York Mayor Michael Bloomberg tried to defend the police, but had to admit that "it sounds to me like excessive force was used."<p>
At the hospital, Guzman and Benefield--though they together had been shot a total of 14 times and were not charged with any crime--remained handcuffed to their hospital beds. They were only uncuffed when members of a 300-strong vigil outside the hospital demanded it.<p>
When Democratic Party leaders from Queens urged calm at the vigil, they were booed by the angry crowd.<p>
"I am fed up," said Charles Barron, a member of the City Council from Brooklyn. "I am not asking my people to do anything passive anymore...We're going to pray. We're going to march. We're going to do all of that stuff, and then we're going to sit down. And then, if they don't respond to that, then don't ask us to ask our people to be peaceful while they're being murdered. We're not the only ones that can bleed."<p>
The NYPD has a long and bloody record of innocent victims--like Amadou Diallo, a West African immigrant who died in a hail of 41 police bullets because, the cops claim, officers mistook the wallet he was taking out of his pocket for a gun. In the wake of the Diallo shooting, tens of thousands took to the streets and participated in sit-ins at police headquarters--as part of a burgeoning movement condemning both racial profiling and police brutality.<p>
Rev. Al Sharpton's National Action Network has called a day of action for December 6, including a protest at One Police Plaza. Mobilizations can again play a decisive role in challenging police violence.<p>

