<font face="Times New Roman, Times, serif" size="4">Top House Democrat proposes bringing back the draft</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Would a draft be more fair?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 1, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH SCHULTE explains what's wrong with Charles Rangel's call for a new draft.</b><p>

CITING THE racial and class inequality that determines who fights U.S. wars, Rep. Charles Rangel--one of the top Democrats in the U.S. House--renewed his call in mid-November for reinstating a military draft.<p>
"There's no question in my mind that this president and this administration would never have invaded Iraq, especially on the flimsy evidence that was presented to the Congress, if indeed we had a draft, and members of Congress and the administration thought that their kids from their communities would be placed in harm's way," said Rangel, who will chair the House Ways and Means Committee when Democrats take over as the majority in January.<p>
In 2003, Rangel proposed a similar measure that was overwhelmingly voted down, and incoming House Speaker Nancy Pelosi (D-Calif.) issued a statement saying that Rangel's proposal would <i>not </i>be part of the Democratic agenda.<p>
Rangel rightly points out that the vast majority of soldiers who serve in the military have few alternatives available to them.<p>
According to Pentagon figures, nearly half of new recruits come from lower-middle-class to poor households, a majority are from economically depressed rural areas. "Nearly two-thirds of Army recruits in 2004 came from counties in which median household income is below the U.S. median," reported the <i>Washington Post.</i> All of the Army's top 20 counties for recruiting had lower-than-national median incomes, 12 had higher poverty rates, according to the National Priorities Project.<p>
Because the U.S. military is disproportionately poor and working class, it is also disproportionately Black and Latino.<p>
According to a <i>Population Bulletin </i>report titled "America's Military Population," "Hispanic representation increased in the enlisted ranks of the military in the era of the volunteer force, from about 2 percent in 1975, when the Hispanic category was first used, to 10 percent in 2001. But the Hispanic share is still below that of African Americans, who have twice as many enlisted men and more than three times as many enlisted women."<p>
While Rangel may raise the right questions about who the U.S. government sends into battle, his solution is terribly wrong. Instituting a draft would not stop class and race inequality in America's military, because the wealthy and well-connected have always been able to avoid military service.<p>
During the Vietnam War, more than 75 percent of active-duty soldiers were from blue-collar families, according to writer Christian Appy. The trend continued even after preferential deferments were eliminated in 1969.<p>
Before Kennedy instituted the Selective Service System, Blacks were underrepresented in the military. The draft changed that. As a disproportionate part of the poor without the means to avoid the draft, African Americans were forced into military service in the early years of the Vietnam War.<p>
"In the early months of the Vietnam War, Blacks accounted for about 20 percent of combat deaths in Vietnam, although they were only 11 percent of the military-age population," states the <i>Population Bulletin</i> report.<p>
Well-to-do youth, by contrast, were able to use loopholes that working-class soldiers didn't have access to. If they did find themselves in the military, they had ways of being sure they would be far away from the action--like George W. Bush and his stint in the Texas Air National Guard.<p>
Ultimately, what's at the heart of the draft question isn't equality, but finding a better way to supply cannon fodder for U.S. wars. As Rangel said himself: "If we're going to challenge Iran and challenge North Korea and then, as some people have asked, send more troops to Iraq, we can't do that without a draft."<p>
Rangel and his fellow Democrats are some of the biggest proponents of sending more U.S. troops around the world to fight the "war on terror." They may disagree with the Bush White House over tactics, but they agree on the project--and the draft is part of the debate over how best to use U.S. power overseas.<p>
But the number of available U.S. soldiers is dwindling, and because of the disastrous occupation of Iraq, the armed forces have had trouble finding enough new recruits to keep their ranks filled.<p>
Much of the military brass--who prefer a well-trained and well-equipped force--agree that the draft is not their first choice. But under the imperial priorities set by both parties, that choice may not be open to them.<p>
It's worth recalling the Democrats have had better success with the draft than Republicans--initiated by Woodrow Wilson, expanded by Franklin Roosevelt, reactivated by Harry Truman.<p>
The draft was suspended in 1973 amid widespread opposition to the Vietnam War in the U.S.--and, more importantly, demoralization and open rebellion among U.S. troops. The draft ceased to be a good option for the Pentagon, a casualty of the Vietnam Syndrome. A strong antiwar movement and opposition among U.S. soldiers will keep it that way.<p>

