<font face="Times New Roman, Times, serif" size="5"><b>Palestinians defend targets of air strikes</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 1, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">IN AN act of immense courage, Palestinians in Gaza are forming "human shields" around buildings targeted by the Israeli military.<p>
On November 18, hundreds of people crowded in and around the home of Mohammed Baroud, a leader of the Popular Resistance Committees (PRC) after Baroud received a telephone call warning of an Israeli air strike.<p>
The calls from the Israeli air force are meant to terrorize Palestinians into fleeing. But Baroud refused to leave his home in the Jabaliya refugee camp, and hundreds of neighbors gathered outside the building, with about 50 climbing on the roof to chant anti-Israeli slogans.<p>
With dozens of people remaining in the house in a round-the-clock vigil, the Israeli military called off at least two air strikes, according to the <i>Kuwait Times</i>.<p>
Two weeks before, in the town of Beit Hanoun, some 200 women surrounded a mosque where a dozen Hamas militants were trapped inside by a siege of Israeli tanks and bulldozers.<p>
Responding to a call by Hamas commanders, the women marched in front of the vehicles as they prepared to demolish the mosque. According to reports, the women went into the mosque, helped the male fighters disguise themselves and led them to safety. During the standoff, Israeli forces opened fire on the women, killing two in a hail of bullets.<p>
A few days later, the Israeli military took revenge--with a nighttime air strike on an apartment building in Beit Hanoun that killed 19 people, including eight children and 11 members of the same family.<p>
Palestinian organizations are planning to repeat the tactic to deter future assaults. After Baroud's home was saved, a spokesperson for the PRC--which was organized by leaders of the former ruling Fatah Party, but has worked closely with Hamas to resist the assault on Gaza--told a reporter: "We call upon all the fighters to reject evacuating their houses, and we urge our people to rush into the threatened houses and make human shields."<p>
Even the pro-Israel U.S. media had to recognize how the demonstrations underscored the Palestinians' David-versus-Goliath struggle against an Israeli military machine armed with the world's most sophisticated and deadly weapons.<p>
As <i>Socialist Worker </i>went to press, a ceasefire agreement between Palestinians and the Israeli government appeared to be holding. But if it repeats its past record, the Israeli military will keep trying to provoke a Palestinian response that it can use as an excuse for a new round of carnage.<p>
Israel's six-month-long siege of Gaza has made life more and more unbearable for Palestinians, stoking the anger that found expression in the demonstration to defend Mohammed Baroud's home.<p>
"We cannot bear this any more," Issa Radwan, one of the Jabaliya camp residents who gathered outside Baroud's house, told Al Jazeera. "Israel has made our life miserable, and they claim that they are a democratic country and call for human rights. We want the whole world to know what is going on here, and to protect us from the brutal Israeli genocide and continuous attacks...<p>
"We came here to protest and to send a message to the Israelis that it does not make a difference for us if we live or die. The Israelis have deprived us of our freedom, and still kill us every day, destroy our houses and impose a tight siege on us."<p>

