<font face="Times New Roman, Times, serif" size="4">Seven charged in a "terror" plot</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Feds' frame-up in Miami</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">"HOME-GROWN terrorists" is how the Bush administration is referring to seven men arrested last week for allegedly plotting to blow up Chicago's Sears Tower in a September 11-style attack.<p>
The seven young, Black defendants are mostly U.S. citizens of Haitian descent or Haitian immigrants who lived in Miami's impoverished Liberty City neighborhood. In the indictment, the men are accused of attempting to provide "material support" to al-Qaeda, in addition to conspiring to blow up buildings.<p>
The government claims that the men, who reportedly follow a religion combining elements of Christianity and Islam, sought support from an undercover FBI agent who posed as an al-Qaeda representative. Their proof? They apparently gave the informant their shoe sizes so he could buy them military boots. <p>
Later, government documents say, one of the men gave the informant lists of other items needed for possible attacks: uniforms, binoculars, radios, vehicles, bullet-proof vests, machine guns and $50,000 in cash.<p>
Thus, the seven are accused of plotting to carry out a complicated terrorist attack on one of the most recognizable landmarks in the U.S., yet authorities admit they had no weapons or explosives, and had never been in contact with al-Qaeda or any other terrorist organization. <p>
The government also claims the men conducted "surveillance" of possible targets. But in reality, the group was apparently so disorganized that the <i>government </i>itself provided the surveillance vehicles as well as the cameras with which the strongest piece of evidence was captured.<p>
And according to news reports, the "plan" to destroy America's tallest building consisted of little more than wishful thinking, expressed by just one of the men to an FBI informant.<p>
Even FBI Deputy Director John Pistole admitted that the supposed terrorist plot was "aspirational rather than operational," and said that none of the seven had ever been on a terrorist watch list.<p>
Attorney General Alberto Gonzales called the plot a "new brand of terrorism." But Nathan Clarke, a lawyer for one of the men, thinks it's the phantom kind.<p>
"With respect to my client, from what I can read in the indictment, there's going to be a question of whether there's even sufficient evidence to sustain the burden of proof on conviction," Clarke told Reuters. "If by any chance there's a scintilla of that, then, of course, there's going to be the entrapment issue. This thing took place over eight months according to the indictment, and at the end of the indictment, it says that this thing became disorganized and nobody had ever done anything."<p>
Civil liberties groups are saying that the case is a particularly brutal example of the U.S. government's racism against Haitian immigrants "The fact that six of the man are either Haitian or Haitian Americans makes this case even more outrageous considering the U.S. government's record of supporting terrorism in Haiti and its racist discrimination against Haitian refugees here," the South Florida Peace and Justice Network said in a statement, <p>
In stark contrast, on the very day that the government was trumpeting the arrests of the men from Liberty City, Jose Antonio Llama, a former member of the Cuban American National Foundation, admitted that he and other right-wing Cuban exiles plotted terrorist attacks against the Cuban government from the U.S. As Llama told <i>El Nuevo Herald</i>, the arsenal to carry out these plans included a cargo helicopter, 10 ultralight radio-controlled planes, seven vessels and a large amount of explosive materials.<p>
But don't expect Llama--who lives in Florida--to face arrest or imprisonment. The media and the federal government only seem interested in the threat of "terrorism" when it involves Muslims.<p>
Following the arrests of the seven, Fox News' <i>Your World with Neil Cavuto</i> program invited commentator Bo Dietl on the air. Dietl called the men a "crew of mutts," and suggested that "[t]he people that are coming in to our country" are "like a cancer" and "[w]e need some chemotherapy now." <p>
He then urged law enforcement officials to "[g]o into your 7-Elevens or go into one of these stores that keep rotating young men who are Muslims"--and say "identify yourself."<p>

