<font face="Times New Roman, Times, serif" size="5"><b>Building the left and fighting the right</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 1,500 people came from across the U.S. and around the world to New York City for Socialism 2006, held June 22-25.<p>
Cosponsored by the Center for Economic Research and Social Change and the International Socialist Organization, the conference brought together activists from some of today's most important struggles--the fight for immigrant rights, the antiwar movement, and the battle against the death penalty here in the U.S.--as well as the struggle for socialism in Venezuela, Brazil and elsewhere around the globe. Sessions on Marxist politics and theory alternated with talks on radical history and presentations on issues facing our movement today. <p>

