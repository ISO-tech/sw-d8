<font face="Times New Roman, Times, serif" size="3">"I thought it was amazing, the level of debate and discussion," said Chicago student Andrew Sisco, who attended the conference for the first time. "And not just that, but the issues that were addressed--not only the current political struggles like the immigrant rights movement, but also the focus on educating people in Marxist theory--like the identity politics workshop, the one on Gramsci, the lecture on Trotsky and the struggle against fascism in Germany. All of those things are really diverse things that you can't really get discussion on anywhere else."<p>
During the panel discussion "Sports and Resistance" NBA Washington Wizards player and poet Etan Thomas--who spoke alongside Nation.com sportswriter Dave Zirin, and Toni Smith, the captain of the Manhattanville College women's basketball team who made headlines in 2003 for turning her back on the flag during the national anthem--related how he had been stopped at the airport en route to the conference.<p>
Thomas said he was a victim of racial profiling, prompted, he believed, by the arrests of seven Black men accused of plotting to blow up Chicago's Sears Tower.<p>
Other events included a performance of Howard Zinn's play <i>Marx in Soho</i> by Brian Jones, and readings from <i>Voices of a People's History of the United States,</i> featuring actors Lili Taylor and Wallace Shawn and writer and director John Sayles.<p>
Another highlight was Italian journalist Giuliana Sgrena's conversation with <i>Democracy Now's</i> Amy Goodman. Sgrena detailed her experience of being taken hostage while in Iraq, and later being shot and seriously wounded by U.S. forces following her rescue in 2005.<p>
Asked if she regretted going to Iraq, Sgrena immediately responded, "No, of course not." "I think we have to see the reality of Iraq," she said. "The occupation has been going on more than three years now, and the situation is worse every day." "I think that the only solution is the withdrawal of all the troops," she concluded to the cheers of the audience.<p>
War resisters Camilo Mej&iacute;a and Pablo Paredes spoke on a panel alongside fellow Iraq Veterans Against the War member Kelly Dougherty.<p>
During an evening rally, defense lawyer Lynne Stewart--who is facing 30 years in prison after being unjustly convicted of aiding terrorism--joined former death row prisoner Shujaa Graham, Mexican American Political Association President Nativo L&oacute;pez, and <i>International Socialist Review</i> editor Ahmed Shawki.<p>
Lopez brought the capacity crowd to its feet, when he put forward the case for amnesty for undocumented immigrants. "Why is it...that that which is produced by all workers--value--is embraced, valued and appropriated by capital, but the workers themselves are maligned and denigrated? If that which is produced by workers, immigrant workers in particular, is not deemed to be illegal, how can the worker be declared illegal?"<p>
Later, Shujaa Graham said of the conference, "I think it's great, people getting together and trying to debate and discuss how we can become a true organization of the people. It's a work in progress and a constant struggle and fight to see us come together, but I'm glad I could be here. We're helping to try to find the common ground to make us better."<p>
Campaign to End the Death Penalty activist Greta Holmes agreed. "Generally, the conference is just remarkable--the content that's available here," she said. <p>
"I've been an activist a long time, but it's the kind of thing that when you're sitting there, you wish your neighbor was here, you wish the ministers at some churches were here, you wish people who think that their only salvation is in the Democratic Party were here. And of course, some of those people <i>are</i> here, but I'm talking about the others that didn't make it. It's been an excellent experience."<p>

