<font face="Times New Roman, Times, serif" size="4">Accident involving NYPD cruiser sparks a response</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cop rampage in Brooklyn</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 30, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JUNE 10 in Brooklyn, New York, most of the 79th and 81st police precincts emptied out onto the streets of Bedford-Stuyvesant in what can only be described as the brutalization of this predominately Black community. <p>
Following a car accident in which an NYPD cruiser ran a red light and drove another vehicle into a nearby storefront, bystanders and witnesses were appalled that two injured officers were given preferential treatment, while the two women in the other vehicle were forced to wait. <p>
As the number of bystanders grew, the cops became panicky, pulling people from the crowd, chasing dozens down the streets and eventually beating several. Most eyewitnesses described the attacks as being completely unprovoked.<p>
A police spokesperson claimed, "It was a rowdy crowd, we were just doing our job, very cut and dry." However, the police wasted no time trying to intimidate the residents of Bed-Stuy, as they arrested six people and beat up several more, including two members of the Black Men's Movement, a local Black nationalist organization. Obviously, the police had another agenda than their much-touted motto "To Protect and Serve." <p>
Despite the violence used against them--police in riot gear, carrying shotguns and helicopters--a crowd of at least 200 stayed to let the police know what Bed-Stuy really thinks about them. <p>
One witness, Dorothy Morris, had the courage to scream at the cops, "These are the terrorists Bush should be sending the Army to go after!" A cop replied that "this wouldn't be a big deal" if people like her "weren't out here shouting," and he said she should "go back to the Islands," referring to her Caribbean accent. <p>
As racial epithets flew and billy clubs dropped on the heads of young Black men, the situation grew ever closer toward what could have become a mini-rebellion. In response, the Black Men's Movement held a press conference June 12 at the location of the incident, and was organizing to build a defense for all those arrested. <p>
June 10 exposed the racism and brutality that police forces thrive on, and showed the potential for ordinary people to organize against oppression and for their rights. <br>
