<font face="Times New Roman, Times, serif" size="5"><b>N.J. state workers face major cutbacks</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao, AFT Local 2364</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">TRENTON, N.J.--Facing a $4.5 billion budget shortfall, New Jersey's Democratic Gov. Jon Corzine is trying to balance the state's budget on the backs of workers. <p>
The biggest cuts will come in the form of reduced pension contributions towards state employees' pensions and cuts to higher education spending. Corzine's budget underfunds state workers' pensions by 30 percent, while cutting a whopping $169 million from the state's higher education budget. <p>
Austerity measures are already being proposed at various colleges and universities, even before the budget has been voted on in the state legislature. The College of New Jersey (TCNJ), for example, will lose funding for its Outstanding Student Recruitment Program (OSRP). <p>
Currently, OSRP provides financial aid for nearly 50 percent of TCNJ's incoming freshman class. Partly to make up for this shortfall, TCNJ trustees are proposing a one-week layoff of the entire college in January. Certain programs, such as the graduate program in English, might be eliminated as well.<p>
The Rutgers University system will bear the brunt of the cuts, with a projected reduction of $114 million. Montclair State University will lose about $5 million. Hundreds, perhaps thousands, of courses could be slashed, and layoffs might be next. <p>
State funding for higher education has in fact been decreasing for more than a decade now. In 1990, 40 percent of Rutgers' budget came from state funding, but by 2004, it was down to 21 percent. In 1983, the state spent about 10 percent of its budget on higher education. The 2006 budget allocates just 5.4 percent. <p>
The approach of the unions--the American Federation of Teachers (AFT), Communication Workers of America, AFSCME and the state AFL-CIO--has been confused at best. <p>
Corzine, a Democrat, was elected to office with massive union support. Now that he's slammed the door in their faces, union leaders seem paralyzed, and it looks like they've accepted the basic premise of a budget "crisis."<p>
To break the standoff, Corzine is likely to reduce the dollar amount of the cuts, and union leaders appear ready to accept some such "compromise." But this will only set the stage for further concessions next year, when faculty union contracts expire at the state's colleges and universities.<p>
In truth, the crisis is an artificial one: Corzine's budget <i>reduces</i> corporate taxes by $282 million, which is well over the $169 million that it takes out of education spending. Meanwhile, three other Democratic politicians--Sen. Stephen Sweeney and Assembly Members Jerry Green and Paul Moriarty--want to reduce the state's contributions to employee pensions by a further 15 percent, over and above the 30 percent shortfall already in Corzine's budget.<p>
In response, the AFL-CIO held a June 19 rally with the slogan "Support Corzine's budget"!<p>
Faculty contracts at the state's colleges and universities expire next year. The cuts <i>can</i> be fought and defeated, but this will require a confident, vocal and uncompromising opposition to the budget cuts. The process of mobilizing needs to begin now, <i>before </i>the cuts go into effect.<p>

