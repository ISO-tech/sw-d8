<font face="Times New Roman, Times, serif" size="4">An organizer on workers taking a stand for their rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Showing day laborers have the power to fight</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 30, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">CARLOS CANALES is the day labor organizer of the Workplace Project in Hempstead, N.Y., on Long Island. Carlos fled El Salvador's civil war in the 1980s and became a well-known organizer in New York. He talked to <i>Socialist Worker's </i>ALVARO LOPEZ and AFSANEH MORADIAN about the conditions that immigrant day laborers face and how they have organized.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>CAN YOU talk about what conditions were like for day laborers before there was a workers' center?</b> <p>

IT HASN'T changed too much. We can say that for day laborers who are on the official side, there are some differences. The most important difference is that contractors tend to respect day laborers more when they're in a workplace center we've organized than when they pick them up on the street. <p>
The majority of the nonpayment of workers that we see here every week--I'd say 99 percent of it--is workers who are on the streets. They're not coming from our workers' centers. <p>
That would be the most concrete difference. But there are other differences. For instance, workers in the workers' center are more organized. They participate in the process of improving their condition or quality of life. <p>
There's only one rule that you hear me repeat over and over again every day when I go visiting: "You do not have to obey rules that are coming from outside the trailer. You have to disobey any rules that are imposed on you." <p>
You have to create your own rules--you have to create your own government here. Whatever's happening at this trailer, you have to know what's going on. If you don't want to participate, that's okay, but you have to ask whoever the coordinator or organizer is. Nothing that's happening can be secret. It must be open. <p>

<b>HOW DO you organize workers' centers?</b><p>

MOST OF the time, workers call the workers' center. We usually go to visit the place and we start a long process, visiting every day to establish relationships with the majority of people, because it may have been two or three who called on behalf of the others. <p>
They'll have a big meeting, and we'll elect a board of workers. They are five or six workers, representing the rest. So after that, we train the new ones about what to say to the press, what not to say to the press, how we are going to face the issues, what the issues are. <p>
Once they're trained, in many cases, they realize they don't have the power by themselves to change what they want to change. Politicians know that they don't vote. So how will the gringo listen to us? <p>
Sometimes they say that we're immigrants, and they won't pay attention to us. Then we go look for support before we go to negotiations with local authorities. We visit churches, identify people who'll be in support of the workers, and then we organize a group of supporters to support the day laborers and a workers' center. <p>
The day laborers begin a steady process of negotiations, asking local authorities to assign an official place where they can stand and wait for contractors. So that the media and the authorities know that they have a place to stay, and the police from now on won't come to bother them. <p>
Also in the workers' center, there are other complementary benefits, like English as a Second Language, computers to use, workshops on basic labor rights, heath information about things like TB, immigration courses, and any other workshop that people want. <p>
Every Friday, we have general assemblies with workers, because the maximum authority is with the workers. They have to do the deciding in an open meeting. So they can debate and everybody can give opinions and vote.<p>

<b>HOW DID the contractors respond? Have you had run-ins with the Minutemen?</b><p>

IT DEPENDS. Here in Long Island, in Nassau County, the most difficult place that we've found to organize labor is Farmingdale. In Suffolk County, it's Farmingville. In Farmingville, it has been impossible to organize a working center because there are a lot of people who are anti-immigrant. <p>
In Farmingville, you have Sachem Quality of Life, a group of local residents who are organizing, and their only purpose is to target day laborers. They were protesting at a 7-11 to stop contractors from picking up day laborers. They told the contractors to go to the Department of Labor. <p>
They've done a lot of other things, like passing legislation that will prohibit contractors from picking up day laborers on the street. The last thing they did was lobby Steve Levy, the executive of Suffolk County, and made him order the police to arrest any day laborers standing at that 7-11 for more than 30 minutes. <p>

<b>IS SACHEM Quality of Life part of the Minutemen?</b> <p>

THEY ARE the Minutemen. <p>

<b>THERE HAVE been some counter-protests against the Minutement, right?</b><p>

THERE WAS a counter-protest against Sachem last December, when they came to Massapequa. <p>
Last year, the Minutemen targeted the border with Mexico. Then, by December, they changed their target and said the new target would be day labor. So they came to New York State. <p>
By last summer, they came to Massapequa to recruit people, supposedly to patrol the border with Canada. But then in December, they came to protest day labor sites. They were just 6 or 7, and we got about 50 or 60 local residents. <p>

<b>WHAT ROLE would you like to see day laborers play in the immigrant rights movement?</b><p>

A LOT of day laborers in the future will make a transition to more permanent jobs. So after some five or 10 years, they stop being day laborers. <p>
They participate in the immigrant rights movements--in the rallies. If a good law is passed, which is improbable, maybe they will be assimilated into the community, build families and stay here. A lot of them want to stay here and live here. <p>

