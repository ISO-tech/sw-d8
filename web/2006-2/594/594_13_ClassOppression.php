<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Marxism, class and oppression</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">MARXISM IS sometimes presented as concerning itself solely with economic questions.<p>
As ZNet's Michael Albert summarized in an Internet debate, "Marxism...tends to exaggerate the centrality of economics and gives insufficient attention to gender, race, [politics] and the environment." <p>
But the emphasis on economic relations does not prevent Marxists from dealing with questions of race and gender. The foundation of any society is its production relations, and its corresponding class relations. But society is not reducible to these relations.<p>
On their basis arise the legal and political superstructure and corresponding forms of beliefs, morals and consciousness--as well as and family relations. Marxism puts together an analysis of all these elements, showing them in their various connections but without losing sight of the role that social production plays as the core element.<p>
Marxism seeks not to separate exploitation from issues of oppression, but to show how they are connected, and how the solution to one cannot be separated from the solution to the other. It is not Marxism, but its critics, who tend to put the working class over on one side and the oppressed over on the other.<p>
But workers are men, women, gay, straight, Black, white, brown, speaking many different languages and coming from many different nationalities. If the working class is to successfully challenge capitalism, it must overcome these divisions. On this basis alone, it is essential to recognize the sources of division and inequality inside the working class if a strategy is to be devised to overcome them.<p>
Socialism is not only a theory of the liberation of the working class. It is a theory of the liberation of the working class as the foundation for the liberation of all of humanity--and not only from class exploitation, but all forms of oppression.<p>
As Lenin writes in his book <i>What is to Be Done, </i>"Working-class consciousness cannot be genuine political consciousness unless the workers are trained to respond to all cases of tyranny, oppression, violence, and abuse, no matter what class is affected."<p>
The working class can only lead a popular revolution if it is capable of fighting not only around economic demands, but for the interests of all the oppressed. It would be a strange if this were not the case.<p>
A revolution that destroyed economic exploitation, but failed to destroy the forms of oppression that buttress that exploitation, would be hard to pull off. And indeed, throughout history, all revolutionary movements consist in more than simply workers seizing control of production. Revolutions are, in Lenin's words, "festivals of the oppressed."<p>
Oppression can't be overcome unless the system that thrives on it, capitalism, is overthrown. Only the working class has an interest in taking the fight that far.<p>
Sexism, national oppression and racism affect people of all classes in the oppressed group--but it affects them in very different ways. The wealthy experience oppression far more lightly than do the poor and working class.<p>
Rich women can get abortions even if they are illegal, whereas poor women cannot. Only poor Black men are given the death penalty. Wealthy African Americans can afford good lawyers.<p>
Moreover, Black and women capitalists actually benefit from the way racism and sexism divide the working class and keeps them down. They may chafe an the inequality they face, but they defend the system that depends on oppression in the first place, and therefore are only willing to accept limited changes that do not alter capitalist social relations.<p>
Only the working class has an interest in carrying the fight the furthest, that is, beyond the limits its middle-class leaders wish to place on it.<p>
"None so fitted to break the chains as they who wear them, none so well equipped to decide what is a fetter," wrote the Irish socialist James Connolly. "But whosoever carries the outworks of the citadel of oppression, the working class alone can raze it to the ground."<p>

