<font face="Times New Roman, Times, serif" size="4">West Coast immigrant rights activists meet in LA</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rejecting the "compromise"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Avery Wear</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Three hundred immigrant rights activists from California and Oregon gathered at the United Teachers of Los Angeles headquarters June 17 for the first Western Regional Conference of the International Coalition for Liberty and Justice.<p>
Chairpersons for the conference included leaders of the Teamsters, United Food and Commercial Workers, laborers, teachers, and Service Employees International unions; Green Party leaders Peter Camejo, Renee Saucedo, Todd Chretien and Sarah Knopp; leaders of the Salvadoran and Filipino communities; and Hermandad Mexicana leader Nat&iacute;vo Lopez.<p>
Immigration attorneys Peter Schey and Isabel Rodriguez set the tone with a devastating critique of the so-called "compromise" Hagel-Martinez immigration legislation before the U.S. Congress. <p>
Schey estimated that no more than 4-5 million of the 12 million undocumented immigrants in the U.S. would be eligible for a 16-18 year "path" to citizenship, while the rest would be subject to stepped-up deportation procedures. Rodriguez highlighted aspects of the "compromise" bill that actually provide for more draconian enforcement powers than the Sensenbrenner bill: the creation of a national identification card, legalization of National Guard border deployments, mandating local police to enforce immigration law and making English the national language.<p>
As Ramon Sanchez, of the Alliance of Hondurans, said, "We don't want half a loaf, we want amnesty or nothing."<p>
The conference included a working group on Black-Brown Unity, and passed resolutions in support of displaced victims of Hurricane Katrina and for reparations for slavery. Plans were made for a march on Martin Luther King Jr.'s birthday. <p>
Minister Tony Muhammad of the Nation of Islam said, "I'm not going to let anybody separate me from my brown brother...We are not going to stop until immigrants have equal rights."<p>
About 40 activists also attended the student organizing working group, representing 15 colleges and high schools. They spoke of the need for building a student conference and network in the fall.<p>
The conference ratified proposals for an immigrant rights march on Labor Day in solidarity and collaboration with the union movement, for a national teach-in day on July 15, for local areas to organize "amnesty truth tours" to build consensus among immigrants on the demand for amnesty, and for a community referendum to be organized in immigrant communities on their legislative demands.<p>
As Nativo L&oacute;pez summed up, we need to defeat the Hagel-Martinez "compromise"; demand immediate amnesty for all undocumented people; and create a grassroots immigrant rights network independent of the Democratic Party.<p>

