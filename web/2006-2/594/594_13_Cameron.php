<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: JAMES CAMERON</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Survivor of Jim Crow terror</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kate O'Neil</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">AMERICA'S ONLY living survivor of a lynching and lifelong fighter for racial justice, James Cameron, died at the age of 92, on June 12. His 1994 chronicle of his lynching, <i>A Time of Terror</i>, is for us a stark reminder of the horror of racist violence in the Jim Crow South.<p>
In the summer of 1930, when James was just 16 years old in Marion, Ind., he and two friends, Thomas Shipp and Abram Smith, were jailed for robbing a white couple, murdering the man and raping the woman.<p>
Soon after, the woman stated that she had not been raped, the murder charge was dropped to manslaughter, and James himself was found to be far from the scene of the crime. But these facts mattered little in 1930s Indiana, where bloodthirsty lynch mobs could "administer justice" when the defendant was Black and the victim white.<p>
Cameron eloquently describes the terror he felt as racists hoisted the bloodied victim's shirt on a flagpole in the center of town, "like a red sheet to a bull." That shirt eventually attracted a crowd of 15,000, who broke into the jail, beat one of James' friends to death with a crow bar and dragged the other several yards to a nearby tree where they were both hung.<p>
James too would be beaten, dragged out of the jailhouse, and feel the noose around his neck, but for reasons that have to this day only been attributed to a miracle, he was spared. This was no thanks to official authorities.<p>
Cameron's account shows how the warden refused requests by Blacks and "freedom-loving whites" to transfer James to a jail in another town, how detectives forced James to sign false testimony against his friends, and how prison guards helped clear the path to the tree where he was to be hanged.<p>
Those who have not read Cameron's book are probably nonetheless familiar with his story from the famous black-and-white photo depicted on its cover. The image of Cameron's friends hung over a crowd of festive-looking lynchers is worth not a thousand words but the single, disturbing question: How can human beings be capable of such hate?<p>
James Cameron lived his life as the antithesis of this scene, struggling for Black dignity and equality, often at the risk of his own life.<p>
In the 1940s, he founded three NAACP chapters in Indiana and investigated civil rights infractions as that state's Director of Civil Liberties. In the early 1950s, death threats for this work forced him to move to Milwaukee, Wis., where he later played a leading role in the fight for integrated housing.<p>
His greatest contribution to civil rights, however, was to educate Americans about racism and the ever-present danger of the growth of hate groups. <p>
In 1988, he founded America's Black Holocaust Museum in Milwaukee. It was intended as "a memorial so that the world can never forget the wrongs done to [Blacks] in America. The emphasis must be on lynching because that is the method used mostly on us to hold back the progress of America."<p>
The museum features an impressive display of photos, objects, and documents of life under slavery and Jim Crow. But the museum was always more than an exhibition hall.<p>
I first went to the museum as a high school student to interview Mr. Cameron about his book for a social studies project. He always made himself available there to meet with anyone who would hear his story.<p>
Even more importantly, the museum was a center for organizing. Unlike many liberal politicians, Cameron did not believe racist violence could be countered by education alone but needed to be physically confronted.<p>
In 1999, he helped my branch of the International Socialist Organization to organize a press conference at the museum, publicizing a protest of the Ku Klux Klan in Madison. Cane in hand, Mr. Cameron personally showed up to many such protests in the late 1990s when the KKK and other fascists attempted to gain a foothold in southern Wisconsin and northern Illinois.<p>
Thanks to these mobilizations, the Wisconsin KKK went into crisis and their recruitment fell flat for several years.<p>
Today, as we face renewed growth of hate groups like the Minutemen, we can take inspiration from James Cameron--who never stopped fighting, never stopped telling the truth, and never stopped believing in a future where racial hatred is a thing of the past. He will be greatly missed.<p>

