<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Marcy Martin-Cavanaugh</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 30, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE FIGHT against injustice and oppression lost an admirable ally June 13, when Marcy Martin-Cavanaugh passed away. Marcy was one of the most positive people I've ever met, and has been a great influence. She was a committed activist and always had her sights set on building a society that put people as a first priority. <p>
Many may have encountered Marcy in her work around environmentalism; during her work with the Central American Task Force in Cincinnati in the 1980s; as a member of American Federation of State, County and Municipal Employees (AFSCME); or, more recently, as a member of the International Socialist Organization. Marcy marched with Dr. Martin Luther King Jr. in Washington D.C. <p>
I'll still never forget when I first met Marcy. It was amazing to see the way she could bring out the confidence of those around her. Marcy's energy and forward-looking approach was infectious.<p>
Marcy was many things to many people. A mother, wife, friend, a trade unionist, a socialist--but most importantly, she was a beautiful human being. She is a testament to the human spirit. Marcy is, and always will be, a fighter against injustice everywhere. <p>
Remember that the very best of what Marcy had to give is in so many of us. It's the strength of this memory that makes us all stronger fighters for a better world.<br>
<b>Shane Johnson,</b> Cincinnati<p>
