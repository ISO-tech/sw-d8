<font face="Times New Roman, Times, serif" size="5"><b>Immigration raids promote scapegoating</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 30, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">FEDERAL AGENTS descended on immigrant communities in late May and June, arresting a total of 2,179 so-called "fugitive immigrants" in a series of raids that took place over 19 days across 30 states.<p>
The arrests were part of series of sweeps organized by the Bureau of Immigration and Customs Enforcement (ICE) and insultingly dubbed "Operation Return to Sender." <p>
Federal officials claimed the arrests took "violent felons"--including "child molesters" and "gang members"--off the streets. "America's welcome does not extend to immigrants who come here to commit crimes," Julie Myers, assistant secretary for ICE told reporters, adding, "ICE will leave no stone unturned in hunting down and deporting aliens who victimize our communities."<p>
But a closer look shows that, behind the hype, the raids were more about government scapegoating of immigrants than the arrest of "criminals."<p>
Only half the people arrested in the sweeps had any criminal records at all--and more than 600 of those were for relatively minor offenses, such as failing to comply with a previous deportation order.<p>
In San Diego, where more than 400 were arrested, most of those apprehended were not even original targets in the raids--but were arrested as authorities demanded citizenship documents from other people they encountered.<p>
Day laborer Fredy Calleja, for example, told the Associated Press that his uncle was arrested while watering plants outside his home. When an agent asked him about someone suspected of selling drugs in the area, the uncle said he didn't know the drug dealer. Then, the agent asked if he was in the country illegally and arrested him when he said he was.<p>
In some areas, immigration agents even "invited" reporters and photographers along to document government progress in cracking down on undocumented immigrants.<p>
The raids caused intense fear in many immigrant neighborhoods. "People hide, and they don't come out," one man told the <i>San Diego Union-Tribune</i> following one of the raids.<p>
The timing of the raids was clearly designed to spread fear in immigrant communities in the wake of the massive pro-immigrant rallies held across the U.S. on May Day.<p>
With proposed immigration legislation reportedly floundering in Congress and midterm elections looming, the Bush administration is eager to prove to its right-wing base that it can be "tough on immigration," while finding a corporate-friendly legislative solution that includes a guest-worker program.<p>
Meanwhile, it will be immigrants who suffer--both those unlucky enough to be caught up in continuing ICE raids, as well as the hundreds who will die this summer as they attempt increasingly dangerous and remote border crossings in punishing summer heat to escape increased border militarization.<p>
With hundreds of U.S. National Guard troops policing the U.S.-Mexico border as of June 17--and 6,000 to be in place by August--immigrant rights activists are predicting this will be the worst summer on record for deaths of migrants attempting to cross the border for work. In mid-June, for example, at least six people died in a single weekend in Arizona trying to cross the border--two after running out of water in the desert.<p>
As Kathryn Ferguson, a volunteer for the Arizona humanitarian group the Samaritans, recently put it to the Associated Press, "If a major airliner crashed in the Arizona desert every year, people would be very upset about it. But it's only recently that people have taken note that 300 people die every year, like an airliner."<p>

