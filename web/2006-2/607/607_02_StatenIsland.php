<font face="Times New Roman, Times, serif" size="5"><b>The T-shirt they say is a threat</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 27, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>STEPHANIE SCHWARTZ describes her encounter with the racist reality of the "war on terror."</b><p>

ON MONDAY, October 9, I boarded the Staten Island Ferry in New York City, wearing a shirt that reads, "We will not be silent" in English and Arabic. Immediately after boarding, four armed Coast Guard officers positioned themselves around me and my friends.<p>
Every so often, you see the Coast Guard patrolling the ferry, but I've never seen a bunch of them standing in one place before. I joked to my friends that this was a response to my shirt, but I didn't really believe that they would put so much security on me. As I got off the boat, I was stopped by a Coast Guard officer.<p>
<b>Officer: </b>You'd better not wear that shirt here anymore.<br>
<b>Me: </b>Excuse me?<br>
<b>Officer: </b>It's not safe to wear that shirt in high security places.<br>
<b>Me: </b>What about my shirt makes it unsafe? What do you think it means?<br>
<b>Officer: </b>Isn't that Arabic writing?<br>
<b>Me: </b>Yes. Is there something wrong with Arabic writing?<br>
<b>Officer: </b>Well, do you remember what happened with that guy on Jet Blue?<br>
<b>Me: </b>Yes. I remember that. It was racial profiling.<br>
<b>Officer: </b>Well, you're obviously not a threat, but someone else wearing that shirt...<br>
<b>Me: </b>Are you saying that an Arab wearing a shirt in Arabic is a terrorist?<br>
<b>Officer: </b>I'm just saying you'd better not wear that shirt here anymore.<p>
This is one of many examples of anti-Arab racism that has become so prevalent in the U.S. since September 11, 2001. Now, it has come to the point when Arabic writing itself is under attack.<p>
On October 23, I returned to the ferry terminal to take part in a protest with about 75 people, most of them wearing the "We will not be silent" shirt in a variety of languages.<p>
As we gathered, the security started to get nervous and asked us what we were doing. We replied that we intended to ride the ferry as an act of protest. During the trip, the demonstrators were all energized, and we talked about being prepared for action if something like this happens again.<p>
We need to stand up to racist law enforcement officers who seek to silence and scare Arabs and Muslims from speaking out against the U.S. and Israel's wars in the Middle East.<p>

