<font face="Times New Roman, Times, serif" size="5"><b>Witnesses to an execution speak out</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 27, 2006 | Pages 15 and 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTI-DEATH penalty activists have been gathering around the country for a series of meetings featuring the voices of those who either have lost a loved one to execution or faced the prospect of execution themselves.<p>
The "Witness to an Execution" tour, organized by the Campaign to End the Death Penalty (CEDP) and cosponsored by a large number of groups, comes as courts are beginning to reconsider the legality of lethal injection. According to mounting scientific evidence, drugs are often improperly administered to prisoners, leading to excruciating deaths.<p>
But the "Witness to an Execution" meetings aim to show audiences that, regardless of the method of killing, there is no such thing as a "humane" execution.<p>
Speakers include former death row prisoners Billy Moore, Madison Hobley and Darby Tillis; California death row prisoner Kevin Cooper, who came within three hours of being executed before he won a stay of execution in 2004; and Bill Babbitt, who witnessed his brother's execution and is a board member of Murder Victims' Families for Human Rights.<p>
Barbara Becnel, the advocate, co-author and editor for Stan Tookie Williams, who was executed by the state of California on December 13, 2005, is a featured speaker on many of the tour stops, where she has described, in harrowing detail, watching her friend's murder at the hands of the state .<p>
 "It's very hard talking about how Stan was executed twice in a day, so I'm happy that it's inspiring people and moving people," said Becnel. "The reason why I'm willing to do it is because I want to help build the movement, and I can see that's happening."<p>
Becnel will also speak at the CEDP's upcoming national convention, which will bring together anti-death penalty activists, former death row inmates, family members and others working to bring an end to the death penalty machine in the U.S. The convention is scheduled for November 11-12 in Chicago.<p>
In each of the cities that have held "Witness to an Execution" tour stops, enthusiastic audiences have turned out. Nearly 100 students, faculty and staff gathered for a meeting at St. Mary's College in Moraga, Calif., 35 attended in San Francisco, 70 came out to a meeting in Harlem in New York City, and 50 gathered in Washington, D.C. Other tour stops are scheduled to take place in Toledo, Ohio, and other cities.<p>
In Chicago, an audience of 75 turned out to Hyde Park's University Church for a meeting featuring Becnel, Tillis and Dr. John Schumann of Physicians for Human Rights. As Becnel explained to the crowd, Stan's execution was political theater designed to inspire fear and awe as well as support for our state criminal justice system and the death penalty. Witnesses, she said, were even told that there was to be "no loud sobbing."<p>
In Oakland, Calif., 30 people came out to hear Becnel, former Georgia death row prisoner Billy Moore, and Kevin Cooper, who called in from his cell on California's death row.<p>
Moore recounted the psychological torture he was subjected to before his planned execution. "When I told my death watch guards I didn't want to watch any television in my last hours, in retaliation, they got out the log books of what other inmates had done and said before they were executed, and they just started reading this to me in my cell."<p>
Cooper told the audience that guards "strip-searched me to degrade me, to make it seem that even my body wasn't my own." "I'm doing what I can in here and you are doing what you can out there," Cooper added. "We need to have a full abolition of the death penalty."<p>
