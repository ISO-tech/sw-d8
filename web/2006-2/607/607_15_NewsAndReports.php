<font face="Times New Roman, Times, serif" size="4"><b>Unity for Gallaudet</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Yarrison</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--Chanting "Unity for Gallaudet" and "Jane K. Fernandes, step down" more than 2,000 Gallaudet University students, faculty, staff, alumni and supporters marched from campus to the Capitol building October 20.<p>
After 16 days of bitter student protests at the nation's only liberal arts college for the deaf, the administration may finally be feeling pressured to consider the students' and faculty's demands for Fernandes' ouster.<p>
Protesters say that the candidate selection process overlooked more qualified minority candidates--and in her six years as provost at Gallaudet, Fernandes alienated students and faculty.<p>
Fernandes has held firm to her position, but an October 20 <i>Washington Post</i> editorial reports that a third of the Board of Trustees responsible for selecting her "are having second thoughts about Ms. Fernandes and think she should step aside."<p>
It's not surprising that the Board, in the <i>Post's</i> terms, is losing its "backbone." That's because the students have shut down campus for three days and created bad publicity for the university, which only got worse when outgoing President I. King Jordan ordered 134 protesters arrested.<p>
The<i> Post </i>reported on October 19 that Fernandes had written to the trustees, "What we are dealing with on campus is anarchy and terrorism." As graduate student Ryan Commerson told the crowd at the Capitol, "The administration is the one in anarchy...This is democracy!"<p>
The protesters' numbers have exploded from 150 to more than 2,000 alongside a dramatic shift in tone. For many, the march to the Capitol recalled the March 11, 1988, Deaf President Now march that led to Jordan's rise as president of Gallaudet, the first ever deaf university president.<p>
According to Commerson, the struggle at Gallaudet is a "breakthrough" and a fight for civil rights. "It will have a ripple effect," he said, "where people here today can go back to their hometowns tomorrow and fight oppression there too."<p>
