<p><a name="HH"></a><font face="Times New Roman, Times, serif" size="4"><b>Let Howie Hawkins debate</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 30 people protested October 22 against the exclusion of Green Party candidate Howie Hawkins from the debate for New York senatorial candidates.<p>
Hawkins is running against Hillary Clinton and her Republican challenger John Spencer, who spent the debate fighting over which one had the best strategies to "win" the Iraq war and reduce the number of abortions.<p>
Hawkins is calling for immediate withdrawal from Iraq, universal health care and using half the military budget to convert the economy to renewable energy. A recent Zogby poll has him polling 21 percent among registered independents, which amounts to 250,000 votes.<p>
Unfortunately, Hawkins' platform is less popular with the media corporations who sponsored the debates. Their decision to exclude Hawkins prompted the League of Women Voters to withdraw its support for the debates in protest.<p>
During the rally outside the debate, some of Clinton's supporters tried chanting "&iexcl;S&iacute; se puede!" but we responded with "This country should be open to all! Hillary voted for a border wall!" From that point on, the Clinton crowd dropped any efforts at political debate and stuck with the monotonous chant of "Hillary! Hillary!"<p>
