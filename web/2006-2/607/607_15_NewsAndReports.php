<p><a name="IR"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter DiLeo</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 1,500 immigrant rights activists filled Union Square to protest Congress' recent passage of "security-only" legislation. It was the most widely orchestrated action in New York since the massive May 1 demonstrations with more than 70 organizations endorsing the call.<p>
Activists rallied at Union Square and then marched to Times Square. Speakers included Columbia University students Monique Dols and Karina Garcia, who were part of the group that protested the Minutemen at a campus event earlier this month; Bobby Khan from the Coney Island Avenue Project; and Joshua James, an 8-year-old citizen whose father was deported<p>
"This march signifies an important step in the immigrant rights movement in New York City," said Ariella Cohen, one of the event organizers with the No One is Illegal Coalition. "We must make it clear to the politicians that we defeated the Sensenbrenner bill and will not tolerate its piecemeal passage--as the Secure Fence Act, the Military Commission Act, or any other law that punishes immigrants and grants them anything short of full and equal rights."<p>
Marchers carried signs with messages like "Family reunification, not detention and deportation" and "Working is not a crime."<p>

