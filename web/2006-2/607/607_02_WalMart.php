<font face="Times New Roman, Times, serif" size="5"><b>Wal-Mart workers walk out in Florida</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Josh Gryniewicz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 27, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ALMOST 200 Wal-Mart workers at a Super Center store in the Miami suburb of Hialeah Gardens, Fla., walked off the job October 16 in protest of new policies that appear to be aimed at forcing out full-time employees.<p>
Nearly an entire morning shift took part in the action, which was probably the first walkout at a U.S. Wal-Mart. At least 15 department managers joined workers for an hour-long rally that rang out with chants of "We want justice."<p>
At least 400 workers also signed on to a letter calling the new policies "inhuman," which was submitted to Wal-Mart headquarters in Bentonville, Ark., as well as to Florida politicians. "It's the first time that Wal-Mart has faced a worker-led revolt of such scale," wrote <i>Business Week</i> magazine.<p>
In recent weeks, Wal-Mart, the nation's<b> </b>largest private employer with 1.4 million workers, put an even tighter squeeze on employees with wage caps, scheduling changes requiring workers to be available for any shift, and a new absentee policy that could potentially result in arbitrary "unexcused" absences.<p>
Already notorious for low wages, poor benefits and labor violations, Wal-Mart seems bent on using these new measures to increase the proportion of its workforce that is part-time to 40 percent--by forcing out longstanding employees, as indicated in an internal memo leaked to the <i>New York Times</i>.<p>
In the Hialeah Gardens store, in an effort to encourage workers to accept any available shift, management cut the full-time workweek to 35 hours across the board--resulting in dramatically reduced pay for workers. As a result of the around-the-clock scheduling, workers could potentially need 24-hour child care, and be deprived of the option of holding down another part-time job or keeping up a schedule of classes.<p>
On top of all this, employees are now obligated to phone an 800 number that dials into the main office in Bentonville before they can inform a shift manager that they are calling in sick.<p>
While the mass immigrant rights marches this past spring weren't explicitly credited as the inspiration for the Florida demonstration, Yahima Morales, one of the department managers involved, told <i>Business Week</i>, "We are a Spanish-speaking community, some from Cuba, some from Venezuela and the Dominican Republic, and if something affects my brothers and sisters, it affects me."<p>
According to press reports, the walkout was organized by store employees without help from outside organizations, including union-backed watchdog groups. Reportedly, two department managers, Guillermo Vasquez and Rosie Larosa, though personally unaffected by the new policies, decided that the company had gone too far, and organized the action by speaking with employees, one at a time, and gathering signatures in support of the protest.<p>
The walkout shows the growing frustration of Wal-Mart workers--and also underlines the shortcomings of organized labor's strategy at Wal-Mart. Rather than attempt an organizing drive, unions such as the United Food and Commercial Workers and Service Employees International Union have opted for public relations campaigns aimed at criticizing the mega-retailer.<p>
In fact, the union-backed Wake Up Wal-Mart Web site, while celebrating the walkout in Florida, took the opportunity to promote a new television advertisement geared toward getting Democrats elected into office.<p>
In spite of a recent round of rhetoric criticizing the company, the Democrats have a history of supporting Wal-Mart. Presidential hopeful Hillary Clinton once sat on the board of Wal-Mart, her law firm defended it, and in Chicago, Mayor Richard Daley just vetoed an ordinance that would have required Wal-Mart and other major retailers to pay a living wage to their workers.<p>
The walkout in Florida shows the bitterness at the greed of corporations like Wal-Mart--and the potential to take action that goes far beyond electing Democrats.<p>

