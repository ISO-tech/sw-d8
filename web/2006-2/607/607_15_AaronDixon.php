<font face="Times New Roman, Times, serif" size="5"><b>Arrested for trying to join debate</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Leela Yellesetty and Nick Hart</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 27, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Aaron Dixon, the Green Party's candidate for U.S. Senate, was arrested October 17 for protesting his exclusion from a candidates' debate.<p>
The debate included Democratic incumbent Maria Cantwell, Republican Mike McGavick and Libertarian candidate Bruce Guthrie. Candidates were included based on a complicated set of rules, which makes it exceedingly difficult for third parties to participate.<p>
Guthrie got in because he donated $1.2 million of his own money to his campaign--and despite the fact that Dixon polls three times higher and has raised more money from outside sources. To his credit, Guthrie stated in the debate that Dixon should have been included.<p>
A group of more than 50 supporters gathered to protest during the taping of the debate. Dixon, in an act of civil disobedience, entered the studio, bolstered by chants of "U.S. out of Iraq, Aaron in the debate" in reference to the fact that the two major party candidates are pro-war.<p>
He was promptly escorted out by police, put in handcuffs and driven off in a police car. After continuing their protest outside the station, the crowd made its way towards the police station where they were greeted by Dixon, who had been released.<p>
"I did this today because I thought it was important to make a statement about this country's corrupt political process," he said to the cheering crowd. "This has got to change. And the only way for this to happen is if we organize poor people and working people and demand a change!"<p>
A few days later, nearly 200 gathered for the final stop on Dixon's statewide tour "Out of war and into our communities." People from all walks of life gathered to hear from community leaders, spoken word and hip-hop artists--all united against the war and the twisted spending priorities of the two-party system.<p>
Dixon took the stage to thunderous applause and explained to supporters why he is running for Senate. He condemned both the Democrats and Republicans for the war in Iraq, the 655,000 lives it has claimed so far and the $300 billion wasted--funds desperately needed in our communities for education, jobs and health care. He also linked his campaign to the growing struggles for social change that are sweeping Latin America.<p>
One of the most powerful speeches came from Chanan Suarez, a decorated antiwar Iraq veteran. "The war on Iraq that Bush led has destroyed the lives of many thousands in Iraq," said Suarez. "But I also met many people, including a three-year old with leukemia, who suffered from lack of proper medical care that began under the Clinton sanctions. The domination of Iraq has always been a bipartisan effort."<p>

