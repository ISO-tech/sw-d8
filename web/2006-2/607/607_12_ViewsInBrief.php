<p><a name="OA"></a><font face="Times New Roman, Times, serif" size="4"><b>Violence in Oaxaca</b></font><p>

<font face="Times New Roman, Times, serif" size="3">Excellent article! ("Oaxaca strikers face the threat of a crackdown," October 13) But there is one glaring inaccuracy which should be corrected. Early reports of killings during the June 14 repression against the teachers' sit-in were never confirmed by anyone. Most early reports talked of two dead, and even these were never confirmed.<p>
More importantly, there have been several deaths subsequent to those events, which can be attributed to thugs and plainclothes cops and militaries under Institutional Revolutionary Party (PRI) control, the most recent being a man on the barricades, who was shot in the head by a plainclothes soldier, and died later that day. Otherwise, a fine job of reporting.<br>
<b>Tom</b>, New York City<p>

