<font face="Times New Roman, Times, serif" size="4"><b>U.S. won't save Darfur</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT SEEMS to me that Nathan Kleinman ("It's Time to Save Darfur," October 13) gives the U.S. and other governments far too much credit. In his letter in favor of U.S. intervention in the Sudan, he seems to proceed from the assumption that the occupying forces would actually stop the genocide. I can see no historical example from which this conclusion can be drawn.<p>
In the Balkans, NATO went in supposedly to stop ethnic cleansing, yet by the time their "Operation Storm" was over, 200,000 more Serbs were driven from their homes in Croatia. In Afghanistan, the U.S. promised women's liberation to Afghans oppressed by the Taliban. Yet our allies in the Northern Alliance have presided over some of the most brutal instances of misogyny.<p>
And, of course, there is Iraq, where Bush promised to free the people from Saddam's death squads and torture chambers. Now, three and a half years later, over 650,000 Iraqis have lost their lives because of these lies.<p>
There's just no reason to take imperialists at their word. Especially when oil's involved. While it may be painful to argue against U.S. action in a case as awful as the Sudan, the history of U.S. interventions shows that the best thing Bush and Co. can do in Sudan is keep their bloody hands off.<br>
<b>Paul Heideman</b>, Madison, Wis.<p>
