<p><a name="KY"></a><font face="Times New Roman, Times, serif" size="4"><b>Kyoto treaty is a fraud</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I MUST disagree with you very strongly about the Kyoto treaty ("Ignoring the threat," October 6). The Kyoto treaty is a fraud.<p>
First, let me tell you that I have a poor opinion of Bush. He is not very bright, and there is not one thing he has done right as president. He will go down in history as one of the worst, if not the worst presidents we ever had.<p>
Additionally, I am convinced that global warming is real, and that is was caused by humanity. In fact, I believe it is happening faster and will be worse than the conservative predictions of the climatologists indicate.<p>
Having said that I am opposed to the Kyoto treaty. The Kyoto treaty exempted India and China--this makes it a fraud. Manufacturing production was already in the process of moving to India and China when the treaty was first proposed. Since then, it has moved almost completely there. Go to any Sam's Club or Home Depot, and you will see almost all the merchandise manufactured in China.<p>
Moving the manufacturing to China and India is actually making the situation worse. Those countries have <i>no</i> reasonable record of pollution controls. I am sure that China's inefficient production will produce more carbon dioxide and pollution than the same production occurring here in the U.S.<p>
Now why would <i>Socialist Worker </i>support it? Are you trying to destroy the few remaining factory jobs here? That would indicate you don't really care about the worker, doesn't it?<p>
It is sad, but as I look at the participants in this debate on global warming, all I see on the "left" or "right" is politics, lies, and fraud. In pushing their own agendas, neither side seems to give a damn about the science, truth or what will really happen. There is no call to monitor the permafrost, which will add more carbon dioxide than all of humanity.<p>
There is no call to make plans for the saving of Venice, New Orleans, Holland, Bangladesh, etc. There is no discussion of what to do with the citizens of the island countries of the Pacific, which will be flooded out of existence.<p>
Even Al Gore ignores his own data which shows that the Arctic Ocean will be ice free by 2017, not 2100. But the "left" would rather slam Bush and promote the useless Kyoto treaty fraud for political points.<br>
<b>Craig</b>, Chicago<p>
