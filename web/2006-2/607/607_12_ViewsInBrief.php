<p><a name="Impeach"></a><font face="Times New Roman, Times, serif" size="4"><b>Why we're for impeachment</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM writing to comment on Lance Selfa's piece, "What the 'Impeach Bush' liberals ignore." (October 13)<p>
While it is true that there are a great number of people who want to see Bush/Cheney impeached who come from a lot of different political persuasions, including liberals, and that our book, <i>Impeach the President: The Case Against Bush and Cheney</i>, is an edited collection with 22 authors with different political views, Mr. Selfa ought to actually read our book before he labels us liberals who are letting the Democrats off the hook.<p>
It would appear that he merely read David Swanson's review, rather than actually looking at our book. If he had, he would have seen very clearly that we make crystal clear that the Democrats are complicit; that we cannot rely on the electoral road or on the Democrats; and that a popular upheaval is what must occur. I urge you to pick up a copy and read it. <br>
<b>Dr. Dennis Loo</b>, from the Internet<p>
