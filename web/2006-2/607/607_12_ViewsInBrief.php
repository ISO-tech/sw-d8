<p><a name="Work"></a><font face="Times New Roman, Times, serif" size="4"><b>Demeaning work tactics</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I'M GLAD you exposed Wal Mart's disgusting tactics ("In the belly of the beast of Bentonville," September 8).<p>
Let's not let Border's off the hook. Employees must endure routine bag searches that catalogue the reading material they bring into the store. We were also penalized for overtime--"full time" at Borders means 37.5 hours. Our hours were also subject to the fluctuations of the stock market. In the words of the general manager, "The rent on this space is $150,000 every month. It's not like we can argue with those costs."<p>
Our break room was a cubicle wall away from all the managers' desks. I sold copies of <i>SW</i> and had political discussions with coworkers. Even though I was told the district manager wanted me gone, the general manager and my direct manager called me in for a sit-down meeting about my union talk.<p>
I was told about how harmful unions are to workers. Had <i>SW</i> columnist Sharon Smith's book <i>Subterranean Fire</i> not been flying off the display, I might have believed them.<p>
I left as an "internal loss-prevention audit" was rolling through. Theft of an expired pastry in the cafe is grounds for termination--as is knowing about such a theft and not reporting it. A national-level stooge was interviewing one person at a time through the store, and people were vanishing. The store's loss-prevention department was no longer allowed to talk socially with other employees and were sent on rounds to watch us.<p>
Anger is high, but organization is low.<br>
<b>Mer S.,</b> San Francisco<p>
