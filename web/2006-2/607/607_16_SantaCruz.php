<font face="Times New Roman, Times, serif" size="5"><b>Protesters attacked in Santa Cruz</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alessandro Tinonga</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 27, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">A PEACEFUL protest by hundreds of students and workers at the University of California (UC)-Santa Cruz was attacked by campus police wielding clubs and pepper spray.<p>
The demonstrators had gathered October 18 to protest a meeting of the UC Board of Regents in the Humanities Lecture Hall on the Santa Cruz campus--to bring attention to the rising tuition costs, the unlivable wages for UC workers and the loss of affirmative action and diversity programs.<p>
For two years, activists have protested the annual Regent meetings, where the public is allowed a 30-minute comment period. Every time that students, workers or staff tried to raise concerns at these meetings, they had their comments shortened. Some people have been removed for speaking past 30 seconds.<p>
To expose the public comment session as a farce, students organized a peaceful civil disobedience action. First, students tried to stop the regents from entering the Humanities Lecture hall. Failing at this, they encircled the building, holding hands and demanding to have their grievances addressed.<p>
As tensions escalated, campus police tried to break through the demonstrators by rushing out the front door. They then targeted three activists, dragging them inside the building. When activists tried to prevent this, the police began clubbing and pepper-spraying the crowd.<p>
Despite the police action, students and workers continued to protest outside, demanding that the three students be released. After two hours of negotiations, the regents left peacefully, and all three students were released with citations. One of the students, Alette Kendrick, faces possible charges of assault and battery of police officers.<p>
Some media outlets claimed that the police attack was a response to students pepper-spraying the officers, but no one has offered any evidence to back this up.<p>
In April of last year, campus police attacked protesters at the "Tent State University" demonstration, an annual weeklong tent city held on several campuses, where activists camp out, hold alternative classes and "mass-democracy" sessions. Plus, government documents have revealed that the Pentagon spied on the antiwar group at UC-Santa Cruz, deeming its protests a "credible threat."<p>
Activists are organizing a campaign to defend the three students.<p>
