<font face="Times New Roman, Times, serif" size="4">Los Angeles teachers vote down a deal backed by union leaders</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>UTLA rejects city takeover</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 27, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--After United Teachers Los Angeles' (UTLA) reform leadership spent the entire summer negotiating--and then defending--a backroom deal with Los Angeles Mayor Antonio Villaraigosa that will give the mayor the leading role in controlling the Los Angeles Unified School District (LAUSD), UTLA members voted in a referendum last month to reject the deal by a margin of 56 percent to 44 percent.<p>
This deal turned into Assembly Bill (AB) 1381, passed in August by California's Democrat-controlled state legislature and enthusiastically signed into law by Republican Gov. Arnold Schwarzenegger. But after finally being given a chance to vote on the deal after it passed, rank-and-file UTLA members voted "no."<p>
It's the second time in 18 months that UTLA members have given their leaders a rebuke in the form of a union-wide membership vote. In the March 2005 UTLA elections, members voted out President John Perez and three other union officers in a landslide victory for the reform slate led by new President A.J. Duffy.<p>
The vote against AB 1381 demonstrated widespread anger at Villaraigosa, who rode hundreds of thousands of dollars in UTLA contributions into the mayoral mansion--only to turn around and campaign for union-busting measures like mayoral control and a vast expansion of charter schools.<p>
But this was also a vote of no confidence in the strategy of back-room deals and reliance on politicians pursued by both Perez before and Duffy today.<p>
Duffy, for his part, is missing the message sent by UTLA members. He is quoted in the UTLA press release announcing the vote saying, "The low voter turnout of 12,333, only about a quarter of our membership, suggests that while this issue is of concern to our members, it is not the most pressing issue."<p>
Duffy should know better. The fact that almost 7,000 UTLA members voted against AB 1381 represents a substantial outpouring of rank-and-file opinion, one similar to the vote that ousted John Perez and put Duffy into office last year. Duffy apparently plans to ignore the referendum. But it is now UTLA policy to oppose AB 1381, and UTLA's officers are constitutionally required to carry out the democratic will of the membership.<p>
Meanwhile, UTLA's 47,000 members have been working under an expired contract since July 1.<p>
As <i>Socialist Worker</i> went to press, a rank-and-file campaign of protests has forced LAUSD to reverse its punitive transfer of popular teacher and UTLA activist Alex Caputo-Pearl out of his school. This victory comes not a moment too soon, as UTLA starts to implement a timeline for a mobilization campaign to win our contract demands.<p>
Progressive Educators for Action (PEAC), a rank-and-file caucus within UTLA, has been instrumental in pressuring UTLA's reform leadership (supported by PEAC) into moving the campaigns for Alex Caputo-Pearl and our contract forward. We will continue to need this kind of rank-and-file organizing to get our leadership out of the business of cutting deals with politicians and turn UTLA into a fighting union.<p>

