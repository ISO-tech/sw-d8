<font face="Times New Roman, Times, serif" size="5"><b>Behind the clashes in Gaza</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 27, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">AN ATTACK by Fatah militants on the security team of Palestinian Authority Prime Minister Ismail Haniya of Hamas October 20 highlighted the widening confrontation between the two groups as Israel ratcheted up military attacks in Gaza. The attack was reportedly an attempt to settle scores in a previous clash between Fatah and Hamas forces, not an attempt at an assassination.<p>
Nevetheless, the violence set the stage for an informal ceasefire between the groups on the eve of Id al-Fitr, the celebration at the close of the Ramadan month of fasting. That agreement broke down two days later in the wake of the assassination of one Fatah militant and the kidnapping of another--actions for which Hamas denied responsibility.<p>
Those incidents set the stage for a resumption of Fatah protests against the Hamas-controlled Palestinian Authority for its inability to pay wages to government employees, due to sanctions imposed by Israel and backed by the U.S.<p>
The result is the slow starvation of Gaza, punctuated by Israeli attacks designed to break the Hamas government--including the August attack on a power generation plant that further devastated the already crippled economy in Gaza, as well as bombing runs and incursions by tanks.<p>
The Fatah old guard around Palestinian Authority President Mahmoud Abbas aims to exploit the situation and return to power via a unity government or through new elections.<p>
Meanwhile, Israel's onslaught--which killed seven Palestinians October 23--is likely to intensify with the appointment of right-winger Avigdor Lieberman to the position of deputy prime minister. Lieberman has in the past called for bombing Palestinian civilians and attacking Tehran with nuclear weapons.<p>
While Fatah retains political control of the personnel powerful security apparatus built up after the Oslo Accords established the Palestinian Authority, Hamas has, despite the horrific suffering in Gaza, retained a mass following--which is why Abbas has been unable to topple Haniya and Hamas.<p>
"If people did blame them, you would see the Hamas government fall," said Toufic Haddad, a Palestinian American and co-author of a forthcoming book on Palestinian social movements. "In fact, Hamas recently held a mass rally of 300,000, and basically said, 'Look, we are not just a bunch of people taking power--we will not legitimize Israel and give up our principles.'"<p>
In general, he said, the population of Gaza blames Israel and the Americans for trying to strangle the Palestinian Authority. "People are very aware," he said, "that when Abu Mazen (Abbbas) was in control, they got nothing."<p>

