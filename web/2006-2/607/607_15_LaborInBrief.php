<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">October 27, 2006 | Pages 14 and 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Goodyear</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Marshall Braun</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SUN PRAIRIE, Wis.--Three weeks into a strike by 12,000 Goodyear workers across the U.S., more than 250 union members are walking the 24-hour picket line in shifts in front of the Goodyear parts production plant in this small city outside of Madison.<p>
Workers are hoping for a fair contract and a quick end to the strike. However, the United Steelworkers (USW) union has told them to prepare for at least a couple months without their paychecks. The strikers, members of USW Local 904, are for now getting only food coupons from their strike fund.<p>
While Goodyear has taken out a $1 billion loan to weather the strike, the USW International's slogan is "one day longer, one day stronger."<p>
Most workers at the plant walked the line in 1997 and gave up huge concessions then and again in 2003 to get the company "back on its feet." This time however, the company is making huge profits, and the workers are demanding that they get their fair share.<p>
According to one member of Local 904, Goodyear wants to cut wages in half--from an average of $20 per hour to around $10--wages that won't support raising a family, she said.<p>
So far, no worker in Sun Prairie has crossed the picket line. Production by supervisors and workers from other plants is running at a mere fraction of what it was prior to the strike, although Goodyear is shifting production to nonunion plants in the U.S. as well as to facilities overseas.<p>
According to USW Local 904 Vice President Greg Leuptow, "We have a lot of support within the community. A lot of different local unions have stopped by and donated to our strike and defense fund."<p>
With a cold winter looming ahead, the strikers are going to need increasing support.<p>

<font face="Times New Roman, Times, serif" size="4"><b>University of California-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Shakarian</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--Custodial workers and students rallied October 18 together to get the long overdue raise for the University of California-Santa Cruz (UCSC) janitorial staff.<p>
In the neighboring town of Watsonville, a Cabrillo College custodial staff member starts at $13.90 an hour and in the course of seven years will earn $18.63 an hour. Here at UCSC, a custodial staff member starts off making $10.90 an hour and after seven years will earn just $11.97 an hour.<p>
The justification for this is that since the campus is located in a "rural" environment, UCSC workers can be paid less than Cabrillo, which is supposedly in an "urban" setting. But the reality is that UCSC workers do more work. For example, they are working on a far larger campus.<p>
The president of the UC system, Robert Dynes, had promised custodians $7.5 million in raises at the system's 10 campuses a year ago, but the workers have yet to receive it.<p>
To protest the lack of a pay increase, more than 50 showed up at the center of campus with signs that said "Dynes Release OUR Money," ready to fight another year for their long deserved wages. Students and workers joined in a spirited march down to the base of campus along the only two-lane highway into UCSC.<p>
Upon reaching the base of campus, protesters blocked one of the two main entrances into UCSC. Police were on call, lurking on the corner and across the street from picketers.<p>
Speakers took the bullhorn and inspired the crowd, declaring that Dynes needs to release the custodian's rightfully deserved money--or that next time, both entrances to campus would be shut down.<p>

