<font face="Times New Roman, Times, serif" size="5"><b>Bigots spew their hate at LA mosque</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kurt Krueger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 15, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 100 racists gathered across the street from the King Fahd Mosque in Culver City, Calif., September 10 to perform a mock lynching. <p>
Members of far-right organizations such as the Minutemen, Save Our State (SOS), and the newly formed United American Committee (UAC) assembled to "commemorate" the anniversary of September 11 by hanging Osama bin Laden in effigy from a gallows mounted on a rented truck. <p>
A group of antiracist counterdemonstrators was able to keep the bigots from taking over the sidewalk in front of the mosque, chanting "Brothers, sisters have no fear, Muslims are welcome here!" and "SOS, KKK, Racist scum go away!" However, the antiracists were outnumbered--at times by as much as 3-to-1.<p>
The UAC, the main sponsor of the protest, is a newly formed hate group. Its platform consists of anti-immigrant rhetoric that is nearly identical to the Minutemen and SOS, but it also targets the Muslim community, claiming to want to protect America from the "threats of Islamic extremism." <p>
Because the group was co-founded by Ted Hayes, a Black Republican, the UAC claims some cover against charges that it is racist. But the group's bigotry was exposed when Hayes himself hooded the mock executioner that slipped a noose around the effigy of bin Laden--and when the racists proceeded to beat the strung-up bin Laden figure with shoes and other objects, a horrifying reminder of what happened to African Americans in this country not so long ago. <p>
"I believe the mock lynching is frightening, extremely disappointing and childish," said Rev. Jason Gamble of the Culver City Presbyterian Church, representing a section of the counterdemonstrators who preferred a peaceful, non-confrontational approach. "I've never seen this done in America before." <p>
Brazilian Rev. Enock DeAssis added, "I moved to this country six years ago and thought that this was a country of inclusion."<p>
After the racists left, we used our bullhorn to hold a speak-out that included parishioners from local churches, Muslims from the mosque, and antiracist activists who oppose the Minutemen.<p>
However, the racists clearly were emboldened by their protest. Had activists not participated in the event, the bigots may not have been confronted at all, and could have easily surrounded the mosque. <p>
This shows the weakness of liberals and progressives in confronting the rising racism against Arabs and Muslims--and the urgent need to build an antiracist struggle that can confront the far right wherever they target their hate.<p>

