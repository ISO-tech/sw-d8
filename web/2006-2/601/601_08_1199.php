<font face="Times New Roman, Times, serif" size="5"><b>The top-down trend in our unions</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 15, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN THE Change to Win Coalition unions split from the AFL-CIO last year, part of the strategy they advocated was to merge all workers in particular industries into single, centralized monster unions. My union, the recently renamed 1199SEIU United Healthcare Workers East, is rapidly becoming the biggest of them all.<p>
Recently, we were called on to vote on changes to our union constitution that reflect the massive expansion of our union. This vote was taken in August, a month when delegate assemblies are cancelled and much union activity slows down. <p>
In the last 10 years that I've been a member, 1199 has gone from a New York City local of maybe 70,000 members to a union of over 275,000, with members as far flung as Massachusetts and Washington, D.C.--as it says on our Web site, "the largest local union in the world." <p>
While some of that growth has been new organizing (including a much-needed and very inspiring organizing drive of home health care workers), the vast majority of this expansion has been through mergers of pre-existing SEIU locals. <p>
It's sort of the corporate model of unionism: Instead of actual "locals," with local leadership and decision-making power, coordinated with other workers through an "international," just have one a massive union with streamlined and centralized leadership. <p>
Part of the way this is sold is the idea that a bigger union will make "us" bigger political players--in the halls of government, with large employers and maybe even with other unions. <p>
The new constitution also writes labor/management cooperation into our mission. The result is a top-down structure that makes it even harder for members and delegates to have a say in the direction and priorities of our union. <p>
On top of that, it is a shift away from seeing our real strength as lying in our membership itself: in the willingness and capability of our members to take on the bosses in their own workplaces, including striking if need be, to defend our jobs. <p>
No matter how big and powerful our union leaders think they can get, they can never compete with Corporate America's ability to buy, bribe and threaten politicians. And high-level labor/management deals can do little more than negotiate less-painful ways for the bosses to screw us over. <p>
The only thing that will reverse labor's decline is a fighting labor movement built from the bottom up. We need delegates and militants who are not just foot soldiers on Election Day, but who can organize and lead actions on the job. <p>
1199 has been more successful that most other unions. But that's not saying much. The rest of the labor movement is in crisis. And in 1199, at most, we've slowed the attacks on our jobs and on the health care industry, not stopped them. <p>
We should resist these top-down trends in our unions, but mostly we need to build the fights amongst our coworkers and in our workplaces that can point a different way forward.<br>
<b>Lucy Herschel,</b> Delegate, 1199SEIU, New York City<p>

