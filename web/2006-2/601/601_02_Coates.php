<font face="Times New Roman, Times, serif" size="4">Wife of SF drowning victim faces threat of deportation</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>Nightmare for a Kenyan immigrant</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 15, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">JACQUELINE MUHORO COATS is in the middle of what she can only describe as "a nightmare." <p>
On May 14, her husband Marlin died at San Francisco's Ocean Beach trying to rescue two drowning boys. As if that weren't tragedy enough, U.S. Immigration and Customs Enforcement (ICE) wants to deport her to Kenya for overstaying her student visa.<p>
"My life has taken a twist," she said. "Four months ago, I never thought it would be like this. Before Marlin passed away, I was so happy."<p>
Just two days before he died, Marlin Coats signed a petition stating that he and Jacqueline were married, and that she was seeking permanent residency. Though they were married a week after ICE deportation hearings began, that petition should have protected her. <p>
But the petition was never filed. As Jackie told <i>Socialist Worker, </i>"We had signed everything, [but] the immigration office didn't have the documents with them at the time [of Marlin's death]. We were putting everything together that they needed instead of going back and forth. We had signed everything and taken our pictures. On Wednesday, we were going to meet the lawyer to submit everything." Marlin died on Sunday.<p>
Jackie lost her student status when she dropped three credits under a full-time course load, after dropping a class that conflicted with courses required of international students. "I actually applied for reinstatement and explained to them why I fell out of status, and they denied my reinstatement," she said. <p>
Jackie described the particular challenges faced by students from African countries seeking to study in the U.S. "You have to prove you have so much money, that you're financially stable in Kenya to come to America," she said. "You could be accepted to a very good college, and they could say, 'We don't think your parents have enough money, so you can't come.' Getting a visa now to come to the U.S. is like luck. You have to spend money on application fees, you have to pay for the visa, and at the same time, you have to make an appointment two months in advance. You have to show bank statements for six months."<p>
After more than five years in the U.S.m returning to Kenya would be starting all over again for Jackie. "I lose my husband, and now I'm going through this," she says.<p>
Jackie, her lawyer and her union, ATU Local 192, are hoping California legislators will help pass a private bill that would allow her to stay in the U.S. Jackie has been building support by raising awareness about her case, speaking at local immigrant rights events. Several deportation hearings have been canceled and rescheduled over the last month as Jackie's case has gained momentum locally. The San Francisco Board of Supervisors as well as the San Francisco and Alameda Labor Councils have come out in support of her. <p>
"I think [the U.S. government] should give immigrants a chance because most people who come here come to make a better life for ourselves and our families," she said. "That's the number one reason why most people come. They should make it a little easier so they don't feel like they have to do something wrong or hide."<p>
There is a rally planned before Jackie's hearing at the San Francisco ICE office on September 28. Building visible public support and pressure for her case is the best chance we have for allowing Jackie, and millions more like her who have built their lives here, to be able to stay in this country legally, without threat of deportation.<p>
