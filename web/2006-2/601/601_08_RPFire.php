<font face="Times New Roman, Times, serif" size="5"><b>Why did six children die?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 15, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RAMIREZ children should have been attending their first day of a new school year on September 5. Instead, their parents were planning funerals for five of them.<p>
In the early hours of September 3, five of the Ramirez children--Vanessa, 14; Eric, 12; Suzette, 10; Idaly, 6; Kevin, 3--along with 3-year-old Escarlet Ramos, a neighbor the family was babysitting, were killed in an apartment fire in my neighborhood of Rogers Park, on Chicago's far North Side. <p>
As the children screamed, "We're burning, we're burning," frantic neighbors unsuccessfully tried to get them to try to jump out the windows. <p>
According to fire marshals, the fire that killed the children was likely started by candles that the family used for light. That's because ComEd, our power company, shut off the family's power in May.<p>
The implications of this family living for four months without power are truly staggering. It means that during some of the hottest months of the year, the family of nine children and two adults, living cramped in a three-bedroom apartment, must have gone without fans or air conditioning of any kind. <p>
One neighbor described tutoring the children, who were eager to read and learn, by candlelight. It's heartbreaking that this could happen anywhere, let alone in one of the wealthiest cities on the planet. <p>
ComEd, which is currently in the midst of a rate hike, has refused to say why the family's power was shut off, but we all know that there's only one possible explanation--failure to pay the bill. Their "bottom line" is more important than the health or safety of ordinary people. <p>
Jay Johnson, the owner of the building the Ramirez family lived in, also deserves his share of the blame. Johnson, who owns six low-income buildings in the area, claims that his units are equipped with smoke detectors, but fire marshals who inspected the apartment twice following the fire said that they could find no evidence of any smoke detectors in the Ramirez apartment.<p>
Last year, city officials found that conditions at one of Johnson's buildings were so bad that they ordered Johnson and other members of his development group to attend a city "landlord training program." As one resident told the <i>Chicago Tribune</i>, "So many things are broken, but the landlord has no problem collecting the rent on time."<p>
Johnson claims he didn't know that the Ramirez family was living in such desperate circumstances, but the family were regulars at a local soup kitchen and were so in debt that Johnson's company had started to evict them last year over $1,900 in back rent. Johnson recently told the media that he would let the Ramirez family live in one of his other units, rent free, for a "few months." As if that could make up for what has happened.<p>
Not surprisingly, Joe Moore, our local alderman, is defending Johnson--a close personal friend and political supporter. Moore has a reputation as one of Chicago's more prominent liberals--opposing the war in Iraq, as well as helping to push through the recent "big box" wage ordinance. <p>
But Moore has also been a driving force in pushing through rapid gentrification in Rogers Park, which has decimated Section 8 and other public housing and made the neighborhood much less affordable for poor and working-class people.<p>
Meanwhile, Chicago's Mayor Richard Daley had some harsh words for Johnson, but he himself has adamantly opposed not only a living wage for Chicago workers, but also reserving a percentage of new housing stock for affordable housing.<p>
Perhaps most stomach-turning, however, is the reaction of some right wingers--who blame the Ramirez family themselves for this tragedy. Message boards of Web sites like "Americans for Legal Immigration" and "Right Nation" have been filled with hateful, racist messages directed against the family--because the parents, Amado and Agosta Ramirez, came to the U.S. as undocumented immigrants from Mexico 15 years ago.<p>
For Amado and Agosta, who both worked full-time in a neighborhood laundry and still couldn't make ends meet, the "American Dream" must today seem like more of a nightmare than anyone can imagine.<br>
<b>Nicole Colson,</b> Chicago<p>

