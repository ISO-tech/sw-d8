<font face="Times New Roman, Times, serif" size="3"><b>ALAN MAASS, ERIC RUDER and DAVID WHITEHOUSE provide a graphic illustration of rising inequality in the U.S. today.</b><p>

<font face="Times New Roman, Times, serif" size="3">"ONE NATION under god, indivisible, with liberty and justice for all," goes the Pledge of Allegiance. But in reality, there are two Americas.<p>
Jean Reynolds lives in one of them. She is a 51-year-old nursing assistant who makes $11 an hour--more than double the federal minimum wage. Still, she finds herself struggling to make ends meet, taking care of a houseful of children and grandchildren, including her daughter Bridget, who suffers from advanced thyroid cancer. "I've worked hard all my life, and I'm still stuck," she told the makers of the documentary <i>Waging a Living, </i>which premiered on PBS recently.<p>
Robert Powell lives in an entirely different America. A California real estate developer, he recently spent $32,000 on rare wines at the Napa Valley Wine auction--as much on some bottles to be shared over dinner with his rich friends as Jean Reynolds will earn in 18 months of full-time work.<p>
In the richest country in the world, millions of people endure grinding poverty, and many millions more live like Jean Reynolds, scraping by and living in fear that a stroke of bad luck could land them in worse shape. Robert Powell lives in a different and very exclusive America--whose inhabitants have the wealth and power to indulge any whim.<p>
Look beyond the U.S., and this stark gap between the haves and have-nots is even more extreme.<p>
The number of millionaires around the world has almost doubled in the past 10 years to 8.7 million, according to the <i>World Wealth Report, </i>compiled by the Wall Street firm Merrill Lynch.<p>
Their combined financial wealth is $33.3 trillion--more than the value of all the goods and services produced last year in every economy in the world except the U.S. Their wealth dwarfs--by 10 times at least--the annual income of <i>half </i>the world's population, which lives on $2 a day or less.<p>
This is the truth: Not "one nation" or "one world," but two--divided between the haves and have-nots.<p>

