<font face="Times New Roman, Times, serif" size="5"><b>Pushed to the edge of famine</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 15, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THOUGH HIDDEN from the headlines, Israel's never-ending war on Palestinians in the West Bank and Gaza is continuing to take a terrible toll. Since Israel's assault on Gaza began at the end of June, 262 Palestinians have been killed and some 1,200 wounded.<p>
Israel's blockade on Gaza's borders means that food and other supplies are scarce--at any price. "This is the 10th store I approach to buy a single box of powdered milk for my little baby," Tareq Omar told IRIN News after searching Gaza's Al-Zawiya market for food for his 1-year-old. "I don't know what to do. I am ready to pay double price for having only one package, but there is none."<p>
With skyrocketing inflation pushing prices to record levels, few Palestinians can afford to buy necessities like milk, even when it can be found--some 85 percent of families in the Gaza Strip live below the poverty line.<p>
The devastation of the Palestinian economy is the result of sanctions imposed on the Palestinian Authority (PA) and the Israeli government's withholding of tax revenue following the victory of militant Islamist group Hamas in January elections. An estimated $100 million in aid has been kept from the PA, and hundreds of thousands of state workers haven't been paid for months.<p>
On September 10, PA President Mahmoud Abbas, leader of Fatah, the rival party to Hamas, announced plans for a "national unity government" being negotiated with Hamas Prime Minister Ismail Haniyeh. Abbas said he hopes that the agreement will encourage Israel and the West to lift sanctions on the PA--and reopen negotiations with Israel.<p>
At the same time, tens of thousands of public-sector employees are on strike over lack of pay. Many believe that Fatah is using the strikes to pressure Hamas into accepting negotiations with Israel.<p>
"Fatah in Nablus sound more concerned with fighting Hamas than in resisting the Israelis," journalist Patrick Cockburn wrote in Britain's <i>Independent </i>newspaper. "State schools have almost all closed because of a Fatah-backed strike by teachers demanding their pay. The Hamas government cannot meet the teachers' demands since it has no money. 'What is the point of putting pressure on a bankrupt government?' asked Abdul Rahman Imran."<p>
A previous announcement of "national unity" came at the end of June, with Hamas leaders agreeing to Fatah's push to sign an agreement renouncing resistance and recognizing the state of Israel--although Hamas leaders asserted that they had not altered their position.<p>
Within days of that announcement, Israeli tanks were rolling into Gaza, with Israel using the capture of an Israeli soldier as an excuse for all-out war. Israeli forces kidnapped dozens of the elected officials affiliated with Hamas.<p>
Even after its failure to defeat Hezbollah in Lebanon, Israel's attempt to crush Hamas has not ended--nor has its war on ordinary Palestinians. On September 10, a 14-year-old boy was killed and a 19-year-old wounded by Israeli fire in Rafah.<p>
As Cockburn wrote a few days earlier, "Gaza has essentially been reoccupied since Israeli troops and tanks come and go at will. In the northern district of Shajhayeh, they took over several houses last week and stayed five days. By the time they withdrew, 22 Palestinians had been killed, three houses were destroyed, and groves of olive, citrus and almond trees had been bulldozed."<p>
Meanwhile, on September 4, Israeli Prime Minister Ehud Olmert authorized West Bank construction projects for 690 new homes for Israeli settlements--in violation of promises he made to pull out thousands of settlers.<p>
The logic of the U.S.-led "war on terror" means that Israel will always have a reason to declare war on the Palestinians and occupy their land.<p>
Erica Silverman, writing for <i>Al-Ahram Weekly </i>from Gaza, said that the embargo would likely last so long as Hamas lawmakers still hold cabinet positions and reject the conditions demanded by the so-called Quartet--the U.S., European Union, United Nations and Russia. <p>
"Even if the Hamas-led government accepts the Quartet's conditions, Israel will not observe the agreement, and nothing will change," Ahmed Helles, a senior Fatah leader in Gaza, who had 10 family member killed by Israel forces, told Silverman. "The problem is that Israel thinks they must occupy and control this country."<p>

