<font face="Times New Roman, Times, serif" size="5"><b>N.J. nurses stand up to union-busting threats</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 15, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW BRUNSWICK, N.J.--As their strike entered its third week, nurses at Robert Wood Johnson University Hospital (RWJUH) were holding strong. <p>
The strike by 1,330 nurses, members of United Steel Workers Local 4-200, began August 25 following management's refusal to provide the nurses with adequate health coverage. The nurses are currently covered by a health plan administered by Qualcare that forces the nurses to come to RWJUH for their medical needs, and only to a small group of "in-network" doctors. <p>
Those who seek care elsewhere face harsh penalties of $1,000 for each hospital admission and $800 for each outpatient procedure. Many of the nurses who work at RWJUH live in cities around New Jersey, and some even commute from Pennsylvania. For them and their families, being forced to seek medical services only at RWJUH is a burden.<p>
Management flatly refused to consider alternative plans proposed by the union, and since then, RWJUH has refused to meet with union representatives. Instead, management has tried to encourage nurses to cross the picket lines, and has flown in scabs from across the country in an effort to break the strike.<p>
Even so, the hospital is having to divert trauma patients to other area hospitals. "The cath labs and the OR are functioning at 50 percent capacity," Nancy Kirby, First Vice-President of Local 4-200, told <i>Socialist Worker.</i> Some surgeons have also reportedly postponed or cancelled scheduled surgeries because of the incompetence of scab nurses.<p>
A week earlier, the hospital claimed that 250 nurses would cross the line, but it seems clear that only a handful have done so. "The mood out here is very strong," said Kirby. "There's a lot of camaraderie out here on the picket lines. We also have a lot of support from the community and we have been putting pressure on them."<p>
"We have heard about derogatory comments made about some of our nurses," Kirby said of the rumors that management has made racist remarks about the Filipino nurses, "and that's pissed us off even more, and made us more determined to stay out here together." "This is not just about health care," said another nurse. "They want to bust the union. They want us out of here."<p>
A federal mediator is scheduled to meet with the union and management on September 12. "The hospital continues to say that they want us to take another look at their 'best and final' offer...We feel that they are not coming to us with anything new," said Kirby.<p>
Some 800 workers took part in a rally and candlelight vigil September 7, and another candlelight vigil is planned for September 14. Mobilizing wider community support and solidarity from other unions will be important in the weeks ahead. <p>

