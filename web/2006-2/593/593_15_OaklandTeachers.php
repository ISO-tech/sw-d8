<font face="Times New Roman, Times, serif" size="5"><b>Givebacks shake up OEA leadership</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, Oakland Education Association</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 16, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER A contentious and protracted two-year contract battle, members of the Oakland Education Association (OEA) ratified a new contract last month--and then voted to oust the local president who negotiated the deal.<p>
The contract--settled hours before a planned one-day strike--brought into the open disagreements among longstanding union activists and officials over whether, and how, to fight against concessions.<p>
The central issue was health care, as Oakland was one of the few remaining school districts in California which provided fully paid health care premiums. The Oakland Unified School District (OUSD)--currently run by a state administrator--has sought to transfer some health care costs to OEA members.<p>
Other issues included the union's demand to rescind the 4 percent pay cut that OEA members took three years ago and stopping OUSD demands to cut elementary substitute teacher wages and strip work protections for teachers in restructured "small schools."<p>
These concessions ignited a strong vote "no" campaign that, while failing to defeat the contract, ousted the incumbent president, Ben Visnick, who had called off the one-day strike.<p>
The contract did save elementary enrichment, and substitutes will see a small pay increase. Elsewhere, there were givebacks. Starting in July, OEA members will contribute .5 percent of their salary to health care premiums. Also in the contract is a problematic provision that on the last day of the contract, June 30, 2008, all OEA members will begin to contribute 4 percent of the average cost of a health care plan.<p>
While the contract states that this 4 percent may not exceed $700 per year, linking our contribution to the rising cost of health care is deadly. On wages, the contract will provide a 6.25 percent increase over three years.<p>
However, that raise is in part due to additional workdays and is mainly the restoration of wages cut three years ago. Combined with health care contributions and three years without a raise, the pay increase will leave us behind inflation.<p>
One of the most insidious elements, and possibly the most misunderstood, is the language around "small schools." Oakland has for almost two years been implementing an extremely top down restructuring of schools aligned with the demands of the federal No Child Left Behind law.<p>
Under the new contract, a teacher in a school that becomes a "small school" will have to re-apply for his or her job, and previous protections like seniority are eroded--a deal breaker for many members.<p>
Debate over the contract culminated at the OEA membership meeting and ratification vote May 10. Unfortunately, many in the leadership, including people who have been activists and militants in the past, fell in line behind the contract on the grounds that it was the best we could do, and it passed by a 931-to-639 vote.<p>
Yet a recent independent fact-finders' report clearly detailed that the OUSD has the money to settle a better contract.<p>
The contract fight was against the backdrop of OEA officer elections. In the race for the OEA presidency, Visnick advocated for the contract, while union activist Betty Olson-Jones campaigned against it.<p>
As a member of her slate, I ran for first vice president on a platform of no concessions, union democracy, transparent bargaining and an alliance with the immigrant rights movement. Olson-Jones ultimately won a runoff election, although some in her slate--including me--did not win, having launched my campaign only when the contract proposal came to a vote.<p>
Olson-Jones is a welcome change from the business unionism that has taken hold of the OEA leadership recently. Her challenge will to be to maintain her commitment to democracy while activating the OEA members to wage a real fight in Oakland to preserve and improve public education.<p>

