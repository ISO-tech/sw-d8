<font face="Times New Roman, Times, serif" size="4">First Army officer to resist publicly</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Refusing to fight an illegal war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 16, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ARMY FIRST Lt. Ehren Watada became the first officer to go public with his refusal to deploy to Iraq on June 8.<p>
"It is my duty as a commissioned officer of the United States Army to speak out against grave injustices," said Watada. "I stand before you today because it is my job to serve and protect soldiers, the American people and innocent Iraqis with no voice."<p>
Watada made the announcement at a press conference in Tacoma, Wash., near Fort Lewis, where he is based. He explained that he couldn't find any moral and legal justification for the war in Iraq and didn't want to become "a party to war crimes." He therefore is refusing orders to deploy to Mosul, Iraq, with the rest of his brigade.<p>
Veterans for Peace, Iraq Veterans Against the War and the War Resisters League have all joined in the campaign to defend Watada, who faces a court-martial, dishonorable discharge and confinement of up to five years.<p>
Watada is not filing for conscientious objector status, because he's not opposed to all war. But he believes that the U.S. war on Iraq is a violation of international law, and any orders to participate in it are therefore also unlawful orders which soldiers have not only the right but the duty to refuse--a view that has plenty of legal merit, according to Marjorie Cohn.<p>
Cohn, a law professor and president-elect of the National Lawyers Guild, appeared at Watada's press conference and outlined the case that the U.S. has broken numerous international treaties and agreements in deciding to wage a pre-emptive war.<p>
A few days after he went public with his decision not to deploy to Iraq, Watada spoke to supporters at a meeting in Tacoma. "What finally hit me was, I think, that I was no longer willing to take seeing the pain and feeling the suffering of so many innocent civilians and soldiers coming home, and families that have to live with the loss of a loved one," said Watada.<p>
Watada joined the military in the run-up to the U.S. invasion of Iraq out of a "sense of patriotism," he said. He had his doubts about the Bush administration's stated reasons for war, but also thought it should be given the benefit of the doubt.<p>
"I didn't have any reason to think that the leaders of our country would betray the trust of our people and lead to something so horrible and tragic as war and sending people into harm's way," explained Watada. "The invasion was a lie and a deception of the American people into waging war against people who showed us no aggression."<p>
Recent revelations about U.S. massacres have only cemented his decision. "The things we're doing--Abu Ghraib and Haditha--are, I feel, a tip of the iceberg," said Watada. "I would like to see all of [the troops who oppose the war] come out, whether they were discharged from the military or had gone AWOL or dropped out of the rolls, to come together, so people really see that there is an outcry within the military against this war. Once people see that, they'll realize that not everybody is going along with the status quo."<p>
