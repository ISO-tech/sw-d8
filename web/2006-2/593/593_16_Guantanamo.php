<font face="Times New Roman, Times, serif" size="3">The prisoners, two Saudis and a Yemeni, reportedly were housed in "Camp One," the highest-security section of Guant&aacute;namo, reserved for what the U.S. deems the "worst of the worst." They were discovered early in the morning after having apparently hanged themselves using bed sheets and clothing.<p>
One of the prisoners, 22-year-old Talal Abdulah Yahya al-Zahrani, was just 17 years old when he was sent to Guant&aacute;namo in 2001.<p>
According to defense lawyers, there have been dozens of suicide attempts in the camp's four-year history, but none have been successful until now. Over one eight-day period in August 2003, for example, 23 detainees tried to hang or strangle themselves, including 10 on a single day.<p>
Hunger strikes have also become common at the camp. Earlier this month, at least 89 prisoners were participating in the latest one--a protest, according to lawyers, fueled by conditions at the camp that include brutal interrogation methods and indefinite detention.<p>
Lawyers and former detainees say the suicide attempts are an act of desperation in the face of the legal limbo prisoners are consigned to.<p>
"There is no hope in Guantanamo," Shafiq Rasul, a British man released from Guant&aacute;namo who went a hunger strike while behind bars to protest beatings, told the Associated Press. "The only thing that goes through your mind day after day is how to get justice or how to kill yourself. It is the despair--not the thought of martyrdom--that consumes you there."<p>
Bill Goodman, legal director of the Center for Constitutional Rights, told the <i>New York Times, </i>"The total, intractable unwillingness of the Bush administration to provide any meaningful justice for these men is what is at the heart of these tragedies. We all had the sense that these men were getting more and more hopeless. There's been a general sense of desperation that's been growing."<p>
But the Pentagon and the Bush administration dismiss the suicide attempts as stunts by "terrorists" to get public sympathy. In fact, suicide attempts are officially classified as "manipulative, self-injurious behavior."<p>
After the successful suicides this month, Colleen Graffy, U.S. Deputy Assistant Secretary of State for Public Diplomacy, sounded decidedly less than "diplomatic" as she told BBC News that the suicides were a "good p.r. move to draw attention," adding that they were part of a strategy and "a tactic to further the jihadi cause."<p>
Since detainees had access to lawyers, received mail and had the ability to write to families, she added, it was hard to see why the men had not protested about their situation using "other" methods.<p>
But since Guant&aacute;namo first began housing prisoners of the U.S. "war on terror" more than four years ago, human rights activists, lawyers and detainees themselves have continually protested the brutal treatment inflicted by the U.S.<p>
From interrogation methods that include extreme physical and psychological abuse--including sexual humiliation and mock drowning--to guards' reported desecration of the Koran, to brutal force feedings, prisoners have suffered well-documented abuse at the hands of their jailors.<p>
Just last month, the United Nations Committee Against Torture called for Guant&aacute;namo to be closed--in part because of the detrimental effects of prolonged detention and inhumane interrogation practices on detainees' physical and mental well-being.<p>
Today, an estimated 460 prisoners remain at the camp--several of whom were under the age of 18 when first brought to Guant&aacute;namo, a clear violation of international law. So far, just 10 have been charged with any crimes and are slated to have their cases heard by military tribunals.<p>

