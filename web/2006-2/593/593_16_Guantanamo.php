<font face="Times New Roman, Times, serif" size="4">Detainees commit suicide in U.S. prison camp</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"There's no hope in Guant&aacute;namo"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 16, 2006 | Page 16</font><p>

THREE DETAINEES at the U.S. prison camp at Guant&aacute;namo Bay, Cuba, took their own lives in early June. But as far as the U.S. government is concerned, their suicides were an act of <i>aggression </i>against the U.S.<p>
"They are smart, they are creative, they are committed," Rear Adm. Harry B. Harris Jr., the commander at Guant&aacute;namo, said of detainees following the suicides. "They have no regard for life, neither ours nor their own. I believe this was not an act of desperation, but an act of asymmetrical warfare waged against us."<p>

