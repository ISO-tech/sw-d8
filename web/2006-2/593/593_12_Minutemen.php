<font face="Times New Roman, Times, serif" size="4">Vigilantes discover they aren't welcome in Fremont</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Confronting their racism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 16, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ADD FREMONT, Calif., to the list of cities in which the Minutemen have tried--and failed--to organize.<p>
These racist vigilantes--and there is no doubt that that's what they are--call themselves the East Bay Coalition for Border Security and are planning to hold biweekly rallies this summer in support of their right-wing agenda. They declare that they are 100 percent in agreement with the Minutemen and are only waiting for their application to the group to be approved.<p>
The Minutemen are infamous for their armed border patrols, but this is a movement not confined to the U.S.-Mexico border. By attempting to build groups across the country, they seek to legitimize the worst kinds of anti-immigrant racism and bigotry.<p>
Of course, their kind of rhetoric isn't confined only to the extreme right wing. Talk of immigrants draining the economy and diluting "American culture," constant references to the "war on terror," and proposals to build a wall along the U.S.-Mexico border are common among mainstream politicians as well--Democrats and Republicans alike.<p>
In fact, these are two sides of the same racist coin, and they feed off each other. The bigots on the border and those rallying in cities like Fremont make it easier for Bush and his allies in Congress to pass off their anti-immigrant guest-worker program and second-class citizenship for immigrants as a "moderate compromise," while the persistent but less overt racism of the debate in Congress allows the most virulent forms of hatred against immigrants and people of color to grow.<p>
While the Minutemen pass themselves off as "patriots" who only want to "protect American values," the facts tell a very different story.<p>
For example, the president of the East Bay Coalition for Border Security, Casey Fargo, has written for the National Vanguard Web site, an openly white supremacist organization, arguing, "[We] should be demanding protection for whites and strong action against any who would abuse us. Building white community--which will include building our own new media--should be a top priority for all of us." It doesn't get much more racist than that.<p>
Our job is to make sure these groups can't grow by, confronting them at every turn and giving confidence to immigrants and activists that we can fight this racist garbage. This is a natural next step for the massive and inspiring immigrant rights movement that showed its power on May Day.<p>
When the East Bay Coalition held its first rally on June 2, we were there, with five times their numbers. Every time they raise their heads out of the sewer--be it in two weeks or two months or two years--we'll be right there to force them back down. We need to stand up to these bigots and declare "Immigrants are welcome here!"--in Fremont, Los Angeles, Chicago, Greensboro, N.C., and all over the U.S.<br>
<b>M.G. Smith,</b> Berkeley, Calif.<p>

