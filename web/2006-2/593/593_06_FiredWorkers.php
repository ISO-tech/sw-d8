<font face="Times New Roman, Times, serif" size="4">Three health workers and community activists fired in Boston:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"All we did is work for our community"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 16, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">ON MAY 24, three Latina health care workers and community activists were fired from Boston's Martha Eliot Health Center, a subsidiary of Children's Hospital. Their "crime"? Organizing for immigrant and workers' rights.<p>
The three--Dr. Ana Ort&iacute;z, Ana Lamarche and Nicolasa L&oacute;pez--were prominent in building for recent immigrant rights protests, and they were part of discussions about unionizing the workforce at the center.<p>
The three were fired without justification, despite many years of providing HIV and mental health services to the Latino and Black communities, particularly to the uninsured and undocumented.<p>
While Children's Hospital claimed their removal was due to a need for "new leadership," it is no coincidence that the "leaders" it fired were the women most involved around fighting for the rights of their fellow workers and members of the immigrant community.<p>
The three women have won substantial community support--100 people picketed on the day they were fired, 40 people turned out to a press conference in early June, and there are plans for upcoming pickets at both the Martha Eliot Health Center and Children's Hospital, accompanied by a petition drive. Martha Eliot patients and workers are attending weekly organizing meetings along with community members and organizations to plan a support campaign.<p>
Here, ANA LAMARCHE and NICOLASA L&Oacute;PEZ talked to <i>Socialist Worker's </i>ALPANA MEHTA and REBECCA BOR about their struggle for justice.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHY DO you think that you were fired?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Ana:</b></font> <font face="Times New Roman, Times, serif" size="3">There are many reasons, such as my political participation. I was the founder of a Latino group called Latino Voice and Feeling, and spent my time working with other workers and with patients when they had problems.<p>
It may have also been my participation in the community as an activist and in the movement for immigrant rights.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Nicolasa:</b></font> <font face="Times New Roman, Times, serif" size="3">I encouraged people to raise their voices to defend their rights, and I participated in movements and activities in the community.<p>
I believe that [Children's Hospital] doesn't want workers who defend human rights. I think that's the only reason they fired us. Because we didn't have any complaints against us, there was no process, and they didn't indicate that we had done something wrong. There was just no reason.<p>

<b>HOW WILL your firing affect the health center?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Ana:</b></font> <font face="Times New Roman, Times, serif" size="3">The workers in the center are in a state of terror and of fear. The patients have it worse than we do because many of the patients are undocumented, and there are many police inside and outside the center now.<p>
The workers say that if [management] can fire the leaders, the backbone of the health center, they will kill us here. This is how they provoke fear.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Nicolasa:</b></font> <font face="Times New Roman, Times, serif" size="3">I believe that this is a way of intimidating and keeping down a group of employees by making them afraid when they come to work. What they want is to act like nothing has happened. They are trying to cover the sun with one finger. The patients do not call the center, and no one talks about what has happened.<p>

<b>WHY DO you want to unionize?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Ana:</b></font> <font face="Times New Roman, Times, serif" size="3">Because Children's Hospital does not support its workers--it fires them.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Nicolasa:</b></font> <font face="Times New Roman, Times, serif" size="3">There's a woman who has been working for 18 years, and she still works at the base level. They don't promote workers; they don't give workers the opportunity to grow. They keep workers at the lowest salaries, and they bring workers from outside instead of training people already there. That's how workers stay in the same position for their whole lives.<p>

<b>WHAT ARE your demands?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Ana:</b></font> <font face="Times New Roman, Times, serif" size="3">First, for Children's Hospital to explain why they fired me and accused me like a murderer. Secondly, that they publicly say something to our community. Thirdly, that they give us our jobs back.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Nicolasa:</b></font> <font face="Times New Roman, Times, serif" size="3">We want them to give our community and us a public apology, because with the investigation of our performance, they treated us without respect--like criminals. We know that our community is a community of workers, and they deserve respect.<p>

<b>WHAT WILL it take to win these demands?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Ana:</b></font> <font face="Times New Roman, Times, serif" size="3">We need honest people. It doesn't matter the color, because when they abuse us, there is no color, no nationality.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Nicolasa:</b></font> <font face="Times New Roman, Times, serif" size="3">We will use every resource because we will not be left with our hands crossed. Children's Hospital is accustomed to people who stay quiet, and now, the workers don't have the right to speak about what has happened.<p>
But we will continue going forward. We deserve respect; we are not criminals or terrorists. The only thing we did is work for our community and any community that needs our help.<p>
We will be the voice for the undocumented immigrants. We are all united; this is one fight. We will fight on different fronts. We need everyone's support, and for everyone to fight with us against Children's Hospital.<p>

