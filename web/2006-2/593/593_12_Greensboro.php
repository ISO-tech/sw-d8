<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Greensboro victims' fight for justice</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 16, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON NOVEMBER 3, 1979, in Greensboro, N.C., American Nazi Party and KKK members shot and killed five members of the Workers' Voice Organization (WVO) participating in an anti-Klan rally in a Black neighborhood.<p>
Though the incident was videotaped and shown on international television, six Klansmen charged with murder were acquitted in 1980 by an all-white jury after they claimed their violence was in "self-defense." In a separate 1985 civil suit, Nazis, the Klan and two Greensboro police officers were found liable for the death of one victim.<p>
Survivors of what has become known as the "Greensboro Massacre" have long held that the Greensboro Police Department's absence at the time of the shootings of the five members of the Maoist organization was at best tragically incompetent, and at worst an act of criminal negligence.<p>
On May 25, 2006, the Greensboro Truth and Reconciliation Commission (GTRC)--a grassroots organization charged with investigating the 1979 shootings--published its final report. The report, according to the Associated Press, "laid the bulk of blame for the violence on Greensboro police who knew the white supremacists planned to attend the Death to the Klan march, but failed to take action." It continues: "[D]espite having a paid informant among the ranks of the Klansmen...the (Greensboro Police Department) showed a stunning lack of curiosity in planning for the safety of the event."<p>
On the other hand, the report also found that the communist activists bear "some, albeit lesser, responsibility" for provoking the Klan by beating on their caravan cars as they passed. "The commission finds that the WVO leadership was very naive about the level of danger posed by their rhetoric and the Klan's propensity for violence, and they even dismissed concerns raised by their own members."<p>
According to the Associated Press: "The commission concluded that some members of the communist Workers Voice Organization, which organized the rally, were armed and did fire at the Nazi-Klan members, but only after the Nazis and Klansmen had already fired at least two, and as many as five, shots."<p>
There is an ongoing debate about who is to blame for the violence. Some liberal detractors of the commission have condemned both anti-Klan protesters and the Klan themselves as being equally "violent extremists." But these myopic liberals make the mistake of slopping together the violence of a caravan of armed racists acting in collusion with the "neutral" Greensboro Police Department, and the verbal extremism of an anti-racist organization.<p>
The report includes many progressive recommendations that should be adopted by the city, including; police apologies for failing to protect the public on November 3; a monument at the site of the shootings; institutional reforms, including a living wage and anti-racism training for all city and county employees; and annual reports on race relations issued by the city.<p>
Greensboro has a long history of struggle for racial justice, starting with the underground railroad for escaped slaves and continuing to the lunch counter sit-ins for desegregation that began here in 1960 with four Black Agricultural and Technical (A&amp;T) State University students; a protest against racism at A&amp;T and Dudley High School nine years later; and the Greensboro Massacre of 1979.<p>
Currently, the Greensboro Police Department is mired in an internal racial profiling investigation. The former white police chief resigned last year after African American officers complained of being under surveillance by other officers linked to the former police chief.<p>
Progressive people should stand in solidarity with the victims of the Greensboro Massacre. We call on the city to implement the GTRC's recommendations and will continue organizing against racism in our community.<br>
<b>Ben Lassiter</b>, Greensboro, N.C.<p>
<i>The commission's report is available on the Web at <a href="http://www.greensborotrc.org" target="_blank">www.greensborotrc.org</a></i>.<p>

