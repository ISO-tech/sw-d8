<font face="Times New Roman, Times, serif" size="5"><b>Shaking up politics as usual in California</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 16, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE INCUMBENT Superintendent of Public Instruction in California was nearly forced into a runoff against SARAH KNOPP, a member of the Green Party and the International Socialist Organization.<p>
In the non-partisan election for the state's top public school official, Knopp won more than 560,000 votes, or 17.2 percent of the total--the best-ever showing for a Green Party member in a statewide election.<p>
Knopp's surprise showing was the highlight for the Green Party in the June 6 election in California.<p>
In primary voting, Peter Camejo--a previous two-time candidate for California governor and Ralph Nader's vice presidential running mate in the 2004 election--won the party's nomination for this year's governor's race against incumbent Arnold Schwarzenegger and Democratic challenger Phil Angelides. And Todd Chretien's Million Votes for Peace campaign won the party's nomination to challenge incumbent Democrat Sen. Dianne Feinstein.<p>
Here, Sarah tells <i>Socialist Worker </i>about the significance of her vote in the schools race.<p>

<b>WHAT HAPPENED in the June 6 election?</b> <p>

THE MILLION Votes for Peace campaign was really able to tap into the anger against the status quo--the war, racism against immigrants, the criminal justice system, and No Child Left Behind.<p>
All of our candidates made it past the primaries, and my campaign for superintendent of public instruction was able to attract over 560,000 voters. This was the highest number of votes for a Green Party candidate in a statewide race ever (although the race was non-partisan). My percentage of the vote was 17 percent, which I also believe is the highest in Green Party history in a statewide race.<p>

<b>WHY DO you think you did so well?</b> <p>

JACK O'CONNELL, the incumbent, had over $1 million and the backing of the Democratic Party in California and the statewide teachers union--the California Teachers Association. And yet, with 52 percent of the vote, he came within 2 percent of being forced into a runoff with me.<p>
Politicians take people for granted. They don't realize that there are grassroots movements beginning to take shape in small ways all over the place that can help to organize people's anger.<p>
This is what the Million Votes for Peace campaign is attempting to do by hooking up with movements for immigrant rights, against the war and against the criminal justice system.<p>
The turnout also shows that rank-and-file teachers aren't going to just blindly follow their union's leadership into voting for the "viable" Democrat. Teachers are mad as hell at the testing agenda that O'Connell is helping to ram down our throats. It sucks the joy out of education and forces teachers to act like robots, instead of critical thinkers.<p>
O'Connell has also put the Oakland School District in state receivership and is trying to impose all kinds of dictatorial "solutions" on the schools, without providing the resources they need to improve. At the least, tens of thousands of teachers registered a protest vote against O'Connell.<p>
Parents, for their part, voted for me because they realize that there is no real solution to our educational problems without a radical restructuring of the tax structure in California and the budget. No Child Left Behind is a smokescreen.<p>

<b>WHAT SHOULD people take away from the election as lessons?</b> <p>

I'D SAY that for our unions, there's a huge lesson: Stop endorsing Democrats who turn around and help to carry out the attacks on us. At a certain point, the membership is going to refuse to follow that line.<p>
For people who want to see a political alternative in this country, I'd say that the lesson is that the stronger and more confidently we attack the two corporate parties, the more people will respond.<p>
People are hungry for an alternative. We could have easily forced a runoff in this race if progressives were more organized.<p>
My campaign had very little infrastructure. This means that many, many people actually paid attention to the ballot statement we put in the voters guide. We shocked everyone by getting such a high vote. I think it shows that people are not apathetic or ignorant and will respond to a call for a fight for justice--if we organize it.<p>
Political campaigns will inevitably be stronger if the people involved in them are intimately connected with the work of rebuilding our unions--and building, for example, the immigrant rights movement.<p>

