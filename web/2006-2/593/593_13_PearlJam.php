<font face="Times New Roman, Times, serif" size="5"><b>Pearl Jam takes on the Iraq war's deadly toll</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Lee Wengraf and Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 16, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Pearl Jam, <b><i>Pearl Jam,</b></i> J Records, 2006.</font><p>

<font face="Times New Roman, Times, serif" size="3">PEARL JAM has long been known for their hard-driving yet soulful songs...and their left-wing politics.<p>
Most famously, they battled with Ticketmaster over the firm's exorbitant service charges on concert tickets, with band members testifying before Congress. In 2000, the band joined forces with other celebrities to campaign for Green Party presidential candidate Ralph Nader. They also took aim at the current president with the song "Bushleaguer" on their last album <i>Riot Act.<p>
</i>On their new self-titled album, Pearl Jam revisits familiar themes of loneliness, alienation and failed love. Songs like "Comatose," "Life Wasted" and "Severed Hand" are chock-full of powerful metaphors for deep-rooted alienation, with lines like "If nothing is everything, I'll have it all."<p>
But the album also contains some of the most overtly political work of the band's career. They've hinted at political themes in the past, but <i>Pearl Jam</i> exhibits a much broader and fully developed political voice. The war and occupation in Iraq lies at its heart, but its message reaches beyond the war itself to a larger picture of its human cost and social impact--and the priorities that drive it.<p>
The single "World Wide Suicide" is the standout song on the album--a hard-hitting denunciation of the toll the war has taken on U.S. soldiers. It begins with Eddie Vedder singing of coming across the familiar face of a dead soldier in the morning paper. But Pearl Jam also makes clear who and what lies behind it.<p>
Lyrics about the soldier "that the president took for granted, writing checks that others pay...laying claim to the take our soldiers save does not equate" show unequivocally that only a tiny minority benefit from the Iraq occupation.<p>
In "Marker in the Sand," Pearl Jam goes beyond mourning U.S. dead to denounce the horrors of Iraqi life under occupation and the hypocrisy that justifies it: "Walking tight-rope high over moral ground/Walk the bridges before you burn them down/There is a sickness coming over me/Like watching freedom being sucked straight out to sea."<p>
In promoting the album, Vedder and band mates have talked about how the environment after September 11 suppressed dissent, but they feel now isn't the time for silence.<p>
"We could talk about Democrats and why they aren't leading an antiwar movement," Vedder said in an interview with <i>Rolling Stone</i>. "Are they waiting for a shift in the polls?...<p>
"If you highlighted the classic aspects of this war, find out who's fighting and who's dying, and why are there billions of dollars being spent on this war and schools are crumbling and 45 million people in the U.S. don't have health insurance? This is all stuff I've been reading in a book on Iraq called <i>The Logic of Withdrawal </i>by Anthony Arnove. It seems like it's a class issue, because there are things going on underneath this spectacle of war, and the Bush administration is using it as a distraction for the ills of this country that are being not only ignored but exacerbated."<p>
At a time when the antiwar movement grows ever more silent, opting for support of the least-worst Democrat, <i>Pearl Jam</i> is a breath of fresh air for the millions in the U.S. who hate the war and its political backers.<p>

