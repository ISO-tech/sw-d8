<font face="Times New Roman, Times, serif" size="5"><b>Killed by Israeli shells on a Gaza beach</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 16, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SLAUGHTER of a Palestinian family on a Gaza beach by an Israeli artillery shell has highlighted Israel's bloody repression as it imposes borders on the Palestinians--while widening the split between Islamists and secular forces in Palestinian politics.<p>
The bombing left 12-year-old Huda Ghaliya an orphan--the shell killed her parents, three siblings and two others. Another 40 people were wounded.<p>
"The child arrived at the Al Awda Hospital with tens of injured and dead," wrote Mona Elfarra, a doctor at that hospital. "She was shocked and in a state of denial. She kept saying to me, 'Mom and dad did not pass away, they are in another hospital!'"<p>
Video footage of a grieving Huda, broadcast throughout the Arab world, recalled the televised image of Mohammed al-Dora, the Palestinian boy killed by Israeli soldiers at the outset of the Al-Aqsa Intifada in September 2000.<p>
The Gaza massacre has drawn attention to Israel's campaign of economic sanctions on the Islamist Hamas Party government, which won elections to run the Palestinian Authority (PA) in January.<p>
As a result, 160,000 employees of the PA--the main employer in Gaza--have been without paychecks for four months, until the poorest among them received one month's pay. According to the World Bank, the sanctions--backed by the U.S.--will drive the already high Palestinian poverty rate to 67 percent.<p>
The Israeli military's killings of Huda's family were explained away by government officials as the consequence of Palestinian attacks on Israeli towns using homemade Qassam rockets--even though the beach is nowhere near the supposed Qassam launching area.<p>
In reality, innocent bystanders are routinely killed by the Israeli military in assassinations authorized by Defense Minister Amir Peretz of the Labor Party, a supposed leftist. On June 8, Israeli forces launched a missile attack that killed Jamal abu Samhadana, a Hamas leader recently appointed to a top position in the PA government--a further provocation that added to Hamas' decision to stop abiding by a 16-month ceasefire.<p>
The Palestinian anger over these killings comes as PA President Mahmoud Abbas pushes for a July 26 referendum on a political document written by Palestinian prisoners from Fatah, the party that ran the PA until Hamas' victory this year.<p>
The prisoners' document calls for broad national unity; the revival of the Palestine Liberation Organization, including the participation of Hamas and Islamic Jihad; and the foundation of a Palestinian state based on the territories of Gaza and the West Bank occupied by Israel since the 1967 war.<p>
"But Abbas' [referendum] ploy has nothing to do with hastening the creation of a such a state, and everything to do with Fatah's inability to come to terms with its defeat in last January's elections," wrote Ali Abunimah on the Electronic Intifada Web site.<p>
In fact, Hamas has already effectively embraced the formula called for in the prisoners' document. Party leaders have stated that an Israeli withdrawal to the 1967 borders would lead to "many years" of a ceasefire.<p>
Hamas leaders accuse Abbas of using the referendum as a cover for a possible coup--and according to press reports, Abbas has asked Israel for permission to increase his presidential guard from 2,000 to 10,000, adding muscle to the heavily armed Palestinian security forces already controlled by Fatah. Moreover, Hamas and Islamic Jihad prisoners who signed the document have withdrawn their names.<p>
Israeli Prime Minister Ehud Olmert, meanwhile, proceeds with imposing Israeli borders that will incorporate East Jerusalem and much of the best land and water in the West Bank--all with the blessing of George W. Bush and Congress.<p>

