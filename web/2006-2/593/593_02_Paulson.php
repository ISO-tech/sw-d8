<font face="Times New Roman, Times, serif" size="5"><b>Bush's pal holds onto his fortune</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN AN administration already brimming with millionaires, Henry Paulson Jr., George Bush's choice as the new Treasury Secretary, towers above the rest, with an estimated net worth of at least $700 million. And thanks to his appointment to replace outgoing Treasury Secretary John Snow, Paulson will be able to hold on to even more of that wealth.<p>
Even if he wasn't dedicated to pushing the administration's conservative orthodoxy, Paulson would have to consider taking the job--because of the possibilities for a huge tax write-off.<p>
As the head of Goldman Sachs--with a CEO paycheck of $37 million in total compensation last year alone--Paulson amassed a huge stake in the Wall Street firm's stock, worth hundreds of millions of dollars.<p>
Paulson is required by government law to sell the majority of his Goldman Sachs holdings in order to avoid a conflict of interest in his new job. Under normal circumstances, he would have to pay at least $60 million in capital gains taxes to sell of his shares and get his hands on the proceeds.<p>
But Paulson is poised, according to <i>Forbes </i>magazine, "to take advantage of a tax loophole that allows government officials to defer capital gains taxes on assets they have to sell to avoid a conflict of interest, as long as the proceeds are reinvested in government securities or a broad array of mutual funds approved by the government within 60 days."<p>
As long as he holds onto the "replacement assets," Paulson <i>never </i>has to pay the taxes--saving him an estimated $60-100 million in taxes.<p>
As <i>Forbes </i>points out, the loophole, created specifically for members of the executive branch during George Bush Sr.'s administration, "was designed to ensure that the wealthy are not deterred from taking posts in government because they fear a big tax hit. But it amounts to a significant perk of public office."<p>
According to the <i>New York Times' </i>calculation, the sale of Paulson's holdings in Goldman Sachs would net him an estimated $325 million. The tax on the sale should be $48 million, which Paulson will get to keep under the Bush Sr. loophole.<p>
Don't expect any griping from the Democratic "opposition" about Paulson's good fortune. Leading Democrats had nothing but kind words for Bush's nominee. And no wonder--the last chief executive of Goldman Sachs to become treasury secretary was Robert Rubin, of the Clinton administration.<p>

