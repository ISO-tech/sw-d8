<p><a name="Bigots"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the anti-immigrant bigots</b></font><br>
By Rachel Odes<p>

<font face="Times New Roman, Times, serif" size="3">FREMONT, Calif.--The East Bay Coalition for Border Security (EBCBS), a group planning to affiliate with the racist Minuteman Project, has been holding biweekly rallies here since early May. The group, while claiming not to allow racists to participate in its events, hopes to recruit and give confidence to anti-immigrant forces who want armed vigilantes on the Mexican border.<p>
Fremont is one of the most multiracial towns in the Bay Area, and at the EBCBS' last demonstration on June 2, a handful of families and groups of high school students came from their homes to join anti-racist counterprotesters. While immigrant rights defenders outnumbered the right-wingers by a ratio of three-to-one last time, we want to mobilize even more people to their next planned action on June 16 at 5 p.m.<p>
EBCBS needs to be exposed as the racists they are, and the people of Fremont need to know that immigrants are welcome in their town.<p>
<i>Counter-protesters plan to gather at 4:30 p.m. at the intersection of Fremont Boulevard and Mowry Avenue on June 16 and every other Friday throughout the summer</i>.<p>
