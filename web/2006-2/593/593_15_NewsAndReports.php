<font face="Times New Roman, Times, serif" size="4"><b>Stop San Francisco's killer cops</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucas Nevarez</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--An angry crowd of 40 took to the streets on June 10 to protest the murder of 25-year-old Asa Sullivan by San Francisco police. Sullivan's crime? Being young and Black.<p>
Police fatally shot Sullivan, who was unarmed, but for the next 16 hours, the official police version of events was that Sullivan had fired first on the officers.<p>
Police said they were responding to a call that squatters were living in an abandoned town house. Two officers quickly arrested one man, Jason Martin, and they claimed that Sullivan took refuge in the attic and vowed he would not be taken alive. The police said that Sullivan fired at the officers through the attic floor, narrowly missing one of them, and that they returned fire, killing him.<p>
What really happened had practically nothing to do with this initial account. First, Martin was a legitimate tenant of the town house and had committed no crime. Sullivan was his guest. Martin said that the officers never told him why they were there before handcuffing him on the floor and that he explicitly told them that Sullivan did not have a gun.<p>
Second, police fired all the shots, yet they initially reported finding a gun at the scene. Later, they were forced to recant and admit that the murder of Sullivan had been entirely unprovoked. Yet they still tried to suggest that the unarmed Sullivan had somehow caused them to fire by waving a "cylindrical object" at them, which turned out to be an eyeglass case.<p>
"It's unacceptable, whatever the excuse may be," said Mesha Monge-Irizarry of the Idriss Stelly Foundation, an organization which provides support to victims of police brutality. "And we're not going to take it anymore."<p>
Protesters say the incident is another example of racism within the police department, and they're demanding an immediate investigation into all 17 murders committed by police in the last 10 years.<p>
