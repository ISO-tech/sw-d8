<p><a name="Right"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight the right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BUFFALO, N.Y.--Some 45 people gathered at Lafayette Square June 10 to protest a planned demonstration by the National Socialist Alliance (NSA), a neo-Nazi group. The NSA's leader showed up for 10 minutes but left, and a couple more shouted anti-Semitic slurs from a van and then drove off.<p>
While we chanted, construction workers working on a near-by building honked their horns and cheered us on. People came from as far away as Syracuse and Rochester, with signs that said, "No to racism, no to war" and "Smash fascism!"<p>
The fact that the NSA couldn't mobilize more than three people is a clear indication of the effectiveness of confronting racists--wherever they decide to show their faces.<p>
<i>Brian Lenzo, Ken Love and Ream Kidane contributed to this report</i>.<p>

