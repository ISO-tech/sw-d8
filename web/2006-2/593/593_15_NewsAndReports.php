<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>We support war resisters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Michael Smith</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--A Bay Area committee to defend Army Lt. Ehren Watada, the first officer to publicly refuse to deploy to Iraq, is forming. The committee will meet to plan rallies and other actions to show the Army and the country that there is no more honorable course of action than resisting an illegal and immoral war.<p>
The first organizing meeting--to plan a Bay Area action to coincide with Ehren's refusal of deployment orders and to strategize for a summer defense campaign culminating in nationwide actions during his court martial--will be held at noon on June 17 at the Veterans' War Memorial Building, 401 Van Ness Ave.<p>
Whether you are a member of an organization or a concerned individual, you are encouraged to attend this important meeting.<p>
<i>For information about Ehren's case, go to <a href="http://www.thankyoult.org" target="_blank">www.thankyoult.org</a>. For information about the meeting, call 415-335-0953</i>.<p>
