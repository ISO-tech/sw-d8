<P><font face="Times New Roman, Times, serif" size="4">Using jobs as the excuse to give more money to the rich</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Kerry catches tax cut mania</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">HOW WOULD you prefer that the wealthy get their new tax cuts--as individuals or through the businesses that they control? That's the choice between the economic programs of George W. Bush and John Kerry.</P>
<P>In a March 26 speech in Detroit, Kerry, the almost certain Democratic presidential nominee, declared that his plan to cut taxes for business would create 10 million new jobs--mainly by giving tax incentives to companies to move investments back to the U.S. and providing tax credits for companies that hire new workers.</P>
<P>"Some may be surprised to hear a Democrat calling for lower corporate tax rates," Kerry droned. "The fact is, I don't care about the old debates. I care about getting the job done and about creating jobs in America."</P>
<P>Kerry wants to corral Corporate America into putting profits earned abroad into investments in the U.S. by closing a tax loophole that lets businesses defer taxes on profits earned by their subsidiaries in foreign countries. By reducing taxes on such profits invested in the U.S. to just 10 percent for one year, Kerry argues, companies will be encouraged to invest--and create jobs--at home.</P>
<P>At the same time, small businesses, manufacturers and other industries affected by outsourcing would be given a credit on payroll taxes for new hires. The problem with the Kerry plan is his assumption that tax cuts will spur companies to invest and create jobs.</P>
<P>Bush made the same argument to justify his $1.7 trillion in tax cuts for individuals. These giveaways did stimulate consumer spending--but mainly among the very wealthy--the richest 1 percent of the population that will get 27 percent of the benefits over the next decade.</P>
<P>Kerry's plan calls for repealing the tax cuts for families earning $200,000 a year. But he will give the money back through business tax breaks for investment and hiring--and by reducing the top corporate rate from 35 percent to 32.5 percent.</P>
<P>Kerry argues that this will create jobs. Yet the U.S. <I>already </I>has "the lowest corporate taxes in the world--and far fewer social  services" than in Europe, according to Robert McIntyre of Citizens for Tax Justice.</P>
<P>Corporate taxes accounted for just 7.6 percent of federal revenue in 2001, down from 23 percent in 1966. Last year, after-tax corporate income (profits plus interest) accounted for a record-high 14.8 percent of national income--and Kerry now wants to hand employers an even bigger slice of the pie.</P>
<P>It is highly unlikely that tax breaks would spur much job-creating investment, since employers are reluctant to expand operations while facing overcapacity in almost every industry. Utilization of industrial capacity hit a 20-year low of 74 percent last year, and is barely higher today. Instead of adding jobs, employers are squeezing more from fewer workers, while allowing little or no wage growth.</P>
<P>With job creation at its worst levels since the Great Depression of the1930s, why not channel government spending into jobs programs like the Works Progress Administration (WPA) of that era, rather than tax cuts? "If that $300 billion [of Bush tax cuts] had been used to employ workers directly--a new WPA, anyone?--it would have created 6 million jobs," <I>New York Times </I>columnist Paul Krugman wrote last year.</P>
<P>Don't expect any such initiative from Kerry, however. His economics team is run by Wall Street executive Robert Altman, who wants to cut the federal budget deficit in half. And since Kerry proposes to maintain or expand the military, he would put an even greater squeeze on social spending than Bill Clinton, who reduced the size of the federal government to its lowest level in 40 years.</P>
<P>"In fact, the Clinton-era god of deficit reduction and private-sector supremacy is also worshiped in the Kerry camp," the <I>New York Times </I>observed. Kerry may be able to score points against Bush as long as the jobs picture remains so lousy. But he's offering no alternative at all.</P>


