<P><font face="Times New Roman, Times, serif" size="5"><b>New grocery battle looms for UFCW</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Safeway Inc. and Giant Food Inc. grocery stores are set to shut down Tuesday, March 30 to allow the 18,000 members of United Food and Commercial Workers Local 400 to vote on a new contract. With a five-month strike and lockout in Southern California and West Virginia fresh on everyone's mind, the companies have gone to great efforts to ready themselves for a strike. </P>
<P>They made a pact similar to the one the three companies struck earlier to lockout employees if the other company is struck. They further have agreed to share profits during the course of the strike. And most outrageous of all, Safeway and Giant have <I>already</I> hired 1,700 and 1,500 replacement workers, respectively, to scab in case of a strike--at $10 an hour.</P>
<P>The preparations by the union have been less visible. A union spokesperson announced last week that strike captains have been chosen, picketing schedules are out, and that picket signs will be on hand at the ratification meeting. And the union has encouraged the activity of Community Action Teams, a small network of labor supporters in the area who have been leafleting Safeway employees and customers on the issues of the strike.</P>
<P>Yet, it's far from a given that workers will reject the companies' offer on Tuesday. Union negotiators have been completely silent on the details of the negotiations. As a result, the workers who show up on Tuesday will be seeing the final offer for the first time--and only just a "highlights" sheet at that.</P>
<P>The companies have been distracting attention from their main demands by publicly announcing they want to end time-and-a-half pay for Sundays and increase the amount of seniority needed to get holidays off. While those issues matter, what's really at stake are health care benefits and a two-tier wage policies for new hires. </P>
<P>The companies together dominate over half of grocery sales in the metro Washington region.  Safeway was the driving force behind the Southern California strike and lockout; Giant is owned by Ahold, the Dutch multinational food retailer. Both Safeway and Giant are profitable. And both are crying broke in the face of nonunion competition--Wal-Mart and North Carolina-based Food Lion--to completely restructure their workforce. </P>
<P>The stakes are enormous. The UFCW lost round one of this battle in Southern California. Local 400 also bowed to concessions at Kroger Co. in West Virginia last fall.</P>
<P>There is little to indicate that the union is changing anything in its strategy in order to win round two, however.  And there are several more regional contracts set to expire soon. It will be up to rank-and-file members of the UFCW to start to draw the lessons from these battles--and to organize themselves around a strategy that forces the union to stand up and fight. </P>


