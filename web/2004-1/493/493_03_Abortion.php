<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">No apologies and no concessions to Democratic politicians</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Time to stand up for abortion rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 2, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE ANTI-CHOICE bigots are at it again. This time, they're selling their attack on abortion rights as a law to "protect" pregnant women and their "babies." Last week, the Senate voted to approve the Unborn Victims of Violence Act--federal legislation that makes it a separate crime to injure or kill a fetus during the commission of a violent crime.</P>
<P>Bush is jumping at the chance to sign this innocent-sounding law--not because he gives a damn about the lives of pregnant women, but because the law is another attack on abortion rights, by conferring legal rights on fetuses and using that to challenge a woman's right to abortion.</P>
<P>This strategy is nothing new for the anti-abortionists. Since the day that the U.S. Supreme Court handed down the <I>Roe v. Wade</I> decision legalizing abortion in 1973, the right wing has tried to chip at the right to choose, one piece at a time--from the 1977 Hyde Amendment that cut off Medicaid funding of abortions for poor women to the push for parental consent laws and mandatory waiting periods.</P>
<P>The clock has been turned back so far that South Dakota lawmakers failed by only a single vote last month to pass the first state law that tried to outlaw abortion entirely, except to save the life or health of the woman. On the federal level, last year saw the passage of the so-called "partial-birth" abortion ban, outlawing the late-term abortion procedure known as intact dilation and extraction. The ban is so broad that it could outlaw common abortion techniques used in the second trimester of a pregnancy.</P>
<P>Abortion rights groups were preparing to challenge the ban in court as <I>Socialist Worker</I> went to press. But more important is mobilizing in the streets to fight for our rights. Unfortunately, the mainstream pro-choice movement has given up far too much ground over the past three decades--often relying on Democrats to protect our right to choose, rather than building a movement that could effectively challenge the anti-abortionists' offensive.</P>
<P>Instead of taking to the streets as more and more abortion restrictions went into effect in the 1990s--something that the Clinton administration did nothing to stop--mainstream pro-choice groups overwhelmingly focused on lobbying and legal strategies. Today, that same orientation means that the mainstream pro-choice movement is overwhelmingly focused on supporting John Kerry for president--despite the fact that Kerry, whatever his past progressive record on abortion rights, is now moving further to the right in order to appear more "electable" (including, shamefully, skipping out on the vote on last year's late-term abortion bill.)</P>
<P>In a welcome step, the National Organization for Women, Planned Parenthood and NARAL Pro-Choice America have called for a "March for Women's Lives" pro-choice demonstration on April 25 in Washington, D.C. This will be an opportunity to begin rebuilding the fight for abortion rights in the streets, where it will have the most impact--and reach out to other activists.</P>
<P>After all, the assault on abortion rights is just one part of a broader right-wing offensive--which includes, most notably, the attempt to ban gay marriage. But march organizers have reportedly refused to allow a speaker on the fight for gay marriage on April 25--out of fear that this could "alienate" some moderates in the crowd.</P>
<P>The struggle for gay marriage has captured the imagination of activists, gay and straight, around the country. Building a connection between it and the fight for abortion rights would go a long way towards strengthening both movements, and making common cause against the right-wing assault.</P>
<P>It's time to stop apologizing for our demand for abortion rights--or dialing down demands to suit the preferences of the Democrats. We need to fight for abortion rights now!</P>


