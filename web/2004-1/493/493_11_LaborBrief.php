<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 2, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>University of Wisconsin-Madison</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--A three-hour meeting of about 400 members of the Teaching Assistants Association (TAA) overwhelmingly approved a series of steps that could result in a strike. The strike, if approved, will be a two-day strike set for April 16 and 17, as well as a grade strike.</P>
<P>After nine months of negotiations, the state is still trying to force teaching assistants--most of whom make only $10,000 a year--to begin to pay health care premiums and accept a tiny "pay raise" that amounts to a 3.1 percent decrease in real wages when inflation is taken into account. </P>
<P>Despite claims that the state is in a budget crisis, the state's chief negotiator was forced to admit that an attempt to do away with no-cost health care was a "political and philosophical" move. The TAA will need to collect over half of the members' signatures in each department in order for strike ballots to be issued. </P>
<P>Then, there will have to be a 75 percent vote in favor of a strike. To show undergraduate solidarity, student activists have collected hundreds of undergraduate signatures supporting the TAA. A rally is also being planned for April 1 that will connect the issues of the state's recent unprecedented tuition raises and the TAA's fight for a fair contract.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Washington state workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 300 members of Service Employees International Union (SEIU) Local 925 rallied last week to demand affordable health care. For the first time, all unionized Washington state employees are jointly negotiating the health care portion of their contracts.</P>
<P>Besides SEIU, other unions represented at the rally were the Graduate Student Employees Action Coalition (GSEAC-UAW) and the Washington Federation of State Employees. Beyond the health care issue, each of these unions will be negotiating for local agreements with state agencies covering wages and working conditions. </P>
<P>The health care negotiations cover nearly 60,000 workers and will go into effect in July 2005. The rallies were held at the University of Washington (UW) Medical Center and at Harborview Hospital, also a UW facility. </P>
<P>More than 100 workers rallied at the student union building and marched to the medical center, chanting "Affordable health care is our right! Take it away and we will fight!" Passing motorists honked and waved in support of an issue that everyone can identify with. </P>
<P>In 1995, state workers paid no premiums or co-pays. Since then, rates have gone up yearly. The average state worker now pays $100 per month in premiums plus more in copays.</P>
<P>Along with declining real wages, health care issues could cause a strike this summer if the state doesn't agree to lower the workers' costs. This is the first of many rallies in a contract campaign that will continue through October.</P>


