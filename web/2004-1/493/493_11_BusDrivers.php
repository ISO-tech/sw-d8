<P><font face="Times New Roman, Times, serif" size="4">Minneapolis transit agency out to break the ATU</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bus strikers hang tough</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A UNION-bashing governor and a right-wing hatchet man are determined to break the strike by 2,500 bus drivers and mechanics at Metro Transit in Minneapolis--but workers are holding the line. The strike began March 4 when the workers, members of Amalgamated Transit Union (ATU) Local 1005, walked out over demands that would give only a 1 percent pay raise in the second year of a deal--and sock union members with $500 or more in additional pay for health insurance.</P>
<P>"They refuse to negotiate," a striking mechanic, Howard Tappe, told <I>Socialist Worker. </I>"I think they're trying to starve us." Gov. Tim Pawlenty, a right-wing Republican, has given his full backing to Metropolitan Council Chair Peter Bell--who runs Metro Transit--in his efforts to break the strike.</P>
<P>Bell was an adviser to Presidents Ronald Reagan and George Bush Sr. and is a founder of the Center for New Black Leadership, a network of Black conservatives. Bell has offered $100,00 in grants to social service agencies to provide their own ride services, even as state officials blame the union for leaving the poor and disabled stranded. But this flies in the face of a $700,000 state budget cut for the agency.</P>
<P>And Bell's sympathy for those enduring hardships doesn't extend to veteran employees like Tappe, who's worked at Metro Transit for 33 years. "If they got their way, it would be a big cut in pay for us, because the health insurance--what we have to pay--would go way up. Another thing is that they want to take our retiree health insurance from new employees, and there are new employees on the property who would be affected."</P>
<P>Local 1005 has had the support of unions throughout the Twin Cities area in frequent rallies and cash donations. This has been badly needed, as strike pay was only $50 per week in the first two weeks, and $150 per week thereafter. "A lot of unions have made donations," Tappe said. "We put that into our food shelf, and it clears out almost immediately."</P>
<P>Local 1005 President Ron Lloyd has offered to make concessions--including agreeing to Metro Transit's demand for a seven-year progression for new hires to make full pay. But management said no, because they wanted to pay new hires just 60 percent of top pay, rather than 70 percent.</P>
<P>If workers find it hard to swallow management's claims of poverty, it's in part because Metro Transit has already given raises of 1 to 5 percent to senior managers. When the union asked the Metropolitan Council to look at other potential providers to contain health care costs, management refused to do so.</P>
<P>Like the big Southern California grocery strike, the fight over health care in Minnesota will affect other workers in the region and even nationally. Even if the workers were to accept management's offer, that's unlikely to be the end of demands for concessions, Tappe said.</P>
<P>"We're thinking that, down the road, they would keep cutting," he said. "This would set a bad example for other [state] workers." Local 1005 strikers need our support to put pressure on these union-busters--and win their fight.</P>
