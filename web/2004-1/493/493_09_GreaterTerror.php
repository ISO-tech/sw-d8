<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The greater terror of U.S. power</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A memorable scene from Gillo Pontecorvo's <I>Battle of Algiers</I>--a film about Algeria's struggle against French colonialism--a reporter asks a captured member of the National Liberation Front of Algeria if it isn't a "dirty thing to use women's baskets to carry bombs to kill innocent people."</P>
<P>The militant responds, "Doesn't it seem even dirtier to you to drop napalm bombs on defenseless villages with thousands of innocent victims? It would be a lot easier for us if we had planes. Give us your bombers, and we'll give you our baskets."</P>
<P>The concept of a "war on terror" is a logical absurdity. For what is war if not terror--which is defined in many dictionaries as the systematic use of violence?</P>
<P>The best-organized, best-armed users of terror--from "targeted assassinations" to aerial bombardment--are, of course, states. And the U.S. state is the best of the best--belching out the most lethal firepower the world has ever seen.</P>
<P>The U.S. has frequently funded and armed more "traditional" terrorist organizations, too. It armed and funded death squads in Haiti, El Salvador and many other countries, and it armed and trained the terrorist "contra" armies fighting to overthrow the Sandinista government in Nicaragua in the 1980s.</P>
<P>Indeed, the al-Qaeda cells that the U.S. now declares war on were spawned by the CIA in the 1980s, with help from Pakistanti intelligence and the Saudis, in order to force the Russians out of Afghanistan. Ronald Reagan called them "freedom fighters."</P>
<P>The list could go on. The difference, if there is any, between state-sponsored terrorism and "freelance" terrorism is one of degrees. What is lost in all this is that terrorism is a tactic, not a set of goals, not a political or religious movement, nor an identifiable group of people or states.</P>
<P>There are Islamic movements that use terrorism as a tactic, and many more that do not. Yet this is also true of Christians in the U.S. Though most right-wing Christians are not terrorists, a minority organize and offer support to terrorist cells that bomb abortion clinics and plan for the coming "Armageddon" with piles of weaponry in their compound basements.</P>
<P>For obvious reasons--that the "war on terror" is meant to justify whatever military action the U.S. wants to undertake overseas--homegrown terrorism sails in below the government's political radar.</P>
<P>U.S. politicians have attempted to define terrorism as what "they" do--with "they" defined as whoever the enemy of the week is, these days most often (but certainly not limited to) Muslims and Arabs. A Muslim who assassinates a political leader is committing an act of terror. When Bush or Clinton or Ariel Sharon assassinate a political leader, it is part of the "war on terror."</P>
<P>The real questions are these: What are the grievances? What are the issues?  If this is brought into focus, then the debate becomes: Are they legitimate grievances and legitimate demands? And if the grievances are legitimate, is the tactic justified or useful getting these grievances addressed?</P>
<P>Look at the case of the Palestinians. Both sides engage in acts of terror. The only difference on this score is that one side (Israel) has far, far greater means of terror at its disposal, thanks to the generosity of the U.S. government.</P>
<P>The question is: Who has right on their side? The fact is that the majority of Palestinians in the region are refugees squeezed into camps, forcibly removed from their homes (in 1948, in 1967 and on an ongoing basis ever since) by Zionist military force, and held in check by the utmost brutality.</P>
<P>One can criticize the futility of the tactic of suicide bombings--a tactic that Sharon's government continually provokes to justify its own oppressive policies. But if they were confronted by an Israeli reporter about the morality of suicide bombings, we shouldn't be surprised if Palestinians offered to exchange Israel's planes for the Palestinians' baskets.</P>


