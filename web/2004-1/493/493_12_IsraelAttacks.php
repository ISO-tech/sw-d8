<P><font face="Times New Roman, Times, serif" size="4">U.S. gives the green light for more atrocities</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel threatens new attack</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVER SINCE Israel's assassination of Hamas spiritual leader Sheikh Ahmed Yassin March 22, angry demonstrations have roiled the Middle East. The capitals of Syria, Egypt, Lebanon, Jordan, Sudan and Yemen filled with thousands of protesters who demanded that Arab leaders aid the Palestinian struggle.</P>
<P>Within Israel, thousands of Israeli Arabs--Palestinians who are second-class citizens of Israel--took to the streets in their largest numbers since Israeli Prime Minister Ariel Sharon's incendiary visit to the Al Aqsa Mosque in September 2000. Iraq's Grand Ayatollah Ali Sistani, the country's most important Shiite Muslim cleric, called for Arab and Islamic unity to fight for "liberation of the usurped land" of Palestine.</P>
<P>As <I>Socialist Worker </I>went to press, Hamas hadn't succeeded in carrying out a retaliatory strike against Israel--and Israel had begun to brag that Hamas wouldn't be able to follow through on its threats of "extraordinary retaliation." In fact, Sharon wants to <I>provoke </I>retaliation--to raise the stakes in Israel's war on Palestinians.</P>
<P>That's why, after Yassin's assassination, Israel Defense Force (IDF) Chief of Staff Moshe Ya'alon threatened Palestinian Authority chair Yasser Arafat and Hezbollah leader Hassan Nasrallah, warning them "that their turn is drawing near."</P>
<P>As usual, U.S. vetoed a United Nations Security Council resolution condemning Israel. "I'm afraid this U.S. veto will be taken by Israel as encouragement to continue on the path of violence and escalation, assassinations and reoccupation [of Palestinian territory]," said Palestinian cabinet minister Saeb Erekat. </P>
<P>Sharon will visit Washington April 14 to seek support for his plan to disengage from Gaza--which will take place without any input from Palestinians. In exchange, Sharon wants U.S. backing for the annexation of several large settlements in the West Bank.</P>
<P>He also needs a political diversion from his possible indictment for corruption. But Sharon doesn't want a pullout from Gaza to be taken for a retreat. "Part of the Israeli policy as we move toward disengagement is to make Hamas bleed, so no one can proceed on the assumption that an Israeli withdrawal is a victory for them," said an Israeli official.</P>
<P>The pullout may stretch into a process without end--and could finally be abandoned. Meanwhile, on March 23, 10 Israeli tanks--with helicopter gunships hovering overhead--penetrated 100 yards into Gaza's Khan Yunis refugee camp as bulldozers demolished several homes next to a Jewish settlement.</P>
<P>Gaza is home to 1.4 million Palestinians and 8,000 Jewish settlers. Add to that the area occupied by the IDF, and 20 percent of Gaza is controlled by .5 percent of the population.</P>
<P>Israeli journalist Amira Hass describes "the fear in the eyes of the schoolchildren when some whistling above turns into twisted crushed metal with charcoaled bodies inside" and "the peasants who for years nurtured the fruit with great love only to see it all turned to scorched earth left behind by Israeli tanks and bulldozers."</P>
<P>But Israel's overpowering military force can't squelch the resistance of Palestinians. Israel's war will only feed this resistance--and raise the prospect of a war that engulfs the entire Middle East.</P>


