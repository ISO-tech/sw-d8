<P><font face="Times New Roman, Times, serif" size="4"><b>Iraqis tell the world: "We want a free Iraq"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Your coverage of the March 20 international protests against the occupation of Iraq and Palestine was fantastic, particularly the on-the-ground report from Fayetteville, N.C., where military families and vets marched. There was one important demonstration that your roundup missed.</P>
<P>On March 19, 5,000 Iraqis in Baghdad took to the streets to demand an end to the occupation of their country. The demonstration was called by a coalition of secular and religious organizations, including both Shia and Sunni Islamic groups and labor unions.</P>
<P>According to the organization Occupation Watch, one bloc of protesters started from a Shia mosque, passed by a U.S. base and then converged with the other bloc, which came from a Sunni mosque. Among their banners were "No to Occupation" and "No to American Terrorism!"</P>
<P>In a statement by the coalition that organized the march, addressed to the international community, they explain, "The troops that occupy our country have brought neither the security nor the freedom and prosperity that were allegedly their objectives...American troops have brought only destruction and terrorism to Iraq. They have initiated chaos, and the division and disintegration of the country."</P>
<P>They continue, "The demonstrations that will take place internationally on March 20 aim to support our right to freedom and to independence. They will follow demonstrations in Iraq that will be held on March 19, during which Iraqis of all faiths and ethnicities will walk together to express their will to be rid of the occupation and to live together, with the same rights.</P>
<P>"And in spite of the catastrophe that Iraqi people are living, we will also walk for our Palestinian and Chechnyan brothers and for all oppressed peoples. We want a free Iraq in a world of justice, delivered from the demons of imperialism, colonialism and racism."</P>
<P>The courage and solidarity of these Iraqi demonstrators inspire me to keep struggling for a world where "freedom" and "democracy" are no longer meaningless phrases used by conquerors to cover up the crimes of their conquest. Hope your readers feel the same.<BR>
<B>Kirstin Roberts, </B>Chicago</P>

