<p><a name="Deryk"></a><font face="Times New Roman, Times, serif" size="4"><b>Deryk Alphonso's family needs him</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Deryk Alphonso served a jail sentence for drug charges in 1989 due to misrepresentation. He now faces deportation because of the 1996 immigration law. Deryk has lived in the U.S. since the age of 5, when his parents legally emigrated to the U.S. from Guyana. </P>
<P>He is now 42 years old and a father of four kids, all of whom are U.S. citizens. He also served in the U.S. military for six years, after which he received an honorable discharge. Even though he is not a U.S. citizen, his service to this country should be enough for him to remain in the United States.</P>
<P>But because the 1996 law is retroactive, the fact that he has lived a clean, moral life since the mistake of 1989 doesn't matter--and Deryk is now facing deportation. In 2001, after visiting the immigration office in Baltimore to renew his green card, he was detained and told that he was facing deportation charges. </P>
<P>He was sent to a detention center in Oakdale, La., and was held in prison for six months. After several court hearings, an immigration judge ordered his release and advised him to file for citizenship.</P>
<P>He returned home back to his family and children. After revisiting the immigration offices, Deryk checked in with the Immigration and Naturalization Service (INS) office regularly and filed the form to become a U.S. citizen, as ordered by the INS judge. </P>
<P>In January 2004, he was again detained by immigration authorities. He later learned that the application he submitted was denied and that the state has reinstated deportation proceedings. He is now awaiting deportation at the Howard County Detention Center in Jessup, Md.  </P>
<P>Deryk was good enough to serve and defend this country, but apparently is not good enough to remain in the country. His family and children need him home. <BR>
<B>The Alphonso Family, </B>from the Internet</P>

