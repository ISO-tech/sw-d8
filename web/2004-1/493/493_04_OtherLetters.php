<p><a name="Sexism"></a><font face="Times New Roman, Times, serif" size="4"><b>Sexist slogans aren't "empowering"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Last week, I was witness to a particularly disheartening spectacle that showed just how far the women's liberation movement has come from the militant days when we won the <I>Roe v. Wade</I> decision granting women the right to abortion. We were having our <I>Socialist Worker</I> paper sale inside the student center at Columbia University, next to activists from the chapter of Students For Choice (SFC). </P>
<P>SFC was selling bus tickets to the April 25 March For Women's Lives in Washington, D.C. But instead of having political slogans to emphasize how important it is to stand up to the recent attacks on a woman's right to choose, the (all female) students attempted to get people to the march with the slogans "Left-wing chicks are hot" and "Feminists put out."</P>
<P>It is disgusting that women who consider themselves feminists think that sexist phrases like these are empowering. This sort of tactic only serves to mirror the objectification of women in broader society and show the demoralized nature of the women's movement. </P>
<P>The SFC activists are absolutely right to table and build broad support for the abortion rights movement, but it's not enough to simply set out a table or fill buses (although that's an important first step). We need to be discussing with people politically about why we need to rebuild the women's movement and how we won abortion rights in the first place--in the context of a large, militant grassroots movement that called for "free abortion on demand."  </P>
<P>What will make our side stronger is not being apolitical or mirroring sexism in society in an attempt to "make the march appealing to everyone," but to begin a discussion about what will take the movement forward--and how to make sure that the fight for abortion rights does not end on April 25, but accelerates beyond it.<BR>
<B>Jessie Kindig, </B>New York City</P>


