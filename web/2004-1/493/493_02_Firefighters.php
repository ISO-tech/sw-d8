<P><font face="Times New Roman, Times, serif" size="4">Racist slurs and death threats</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Black firefighters protest bigotry</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SHOCKING RACIST incidents in Chicago's fire department have exposed a culture of bigotry that runs all the way through the department. The issue became public in early February, when a firefighter made racist comments about a Black motorist over the department's radio system. The firefighter, John Scheuneman, received a 90-day suspension, and his supervisor got a 30-day suspension.</P>
<P>But the racist broadcasts became more widespread--with messages sent anonymously so that they couldn't be tracked. Fire Commissioner James Joyce announced that sending a racist message would be a "fireable offense," but the department also claimed that it couldn't detect which stations or vehicles were broadcasting the messages. The city also had to shut down an Internet chat room for firefighters because of its racist content.</P>
<P>On March 1, Battalion Chief Nicholas Russell, who is president of the African American Fire Fighters League, received a death threat. Russell said the threat was written to resemble a "fake news story" and said that "a battalion chief" had been "found in a shallow grave" with "bullets in his chest" on June 19th, or Juneteenth, the African-American holiday celebrating the end of slavery.</P>
<P>"I need you to know that it does happen in the city," Russell told a local TV news station. "And if they can do it to a rank-and-file officer in the department, imagine what they do to the firefighters." Worse still has been the response of the firefighters union. James McNally, the president of the union in Chicago, accused African-American firefighters of sending the racist messages <I>themselves--</I>to "fan the flames to promote their own political agenda." </P>
<P>On March 23, Black and Latino firefighters and their supporters rallied outside union headquarters to call for McNally's resignation. "It's almost like a female being assaulted and the accuser saying it's her fault," said Lt. Barry Mitchell, a 17-year department veteran who is Black.</P>
<P>Take a look at McNally's record, and you won't be surprised by his comments. In 1987, McNally showed up at a firehouse in blackface to protest affirmative action. The union has used members' dues, including from Black firefighters, to pay for lawsuits that fight affirmative-action promotions. African Americans make up just 20 percent of firefighters; only 10 percent are Hispanic.</P>
<P>The Chicago Fire Department has faced scandals about racism before. In 1990, a videotape showed firefighters drinking, exposing themselves and spouting racial slurs during a firehouse retirement party. It's time for the city to admit that this simply isn't a problem of "weeding out a few bad apples."</P>
<P>"This guy didn't come to work four days ago and decide to do this," Capt. Ezra McCann, a 21-year department veteran, said of John Scheuneman's slurs that began the latest round of controversy. "An awful lot of things just don't get reported. If we know they've got that attitude going out there, are Black folks really going to get good service? Are they going to service a Black family how you would a white family? The answer is probably not."</P>


