<P><font face="Times New Roman, Times, serif" size="4">Looking at the politics of horror movies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The new <I>Dawn</I> that doesn't measure up</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Dawn of the Dead,</I> </B>directed by Zack Snyder, written by James Gunn, starring Sarah Polley, Ving Rhames and Jake Weber.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BEST horror movie directors are the ones who know what really terrifies us. Even more than the unknown things that go "bump" in the night, it's the man-made terrors that exist in the full light of day--like racism, poverty and John Ashcroft.</P>
<P>From Mary Shelley's <I>Frankenstein </I>to Stephen King's modern abused outcasts, horror has a long record of reflecting the darker aspects of social tensions. And zombie movies have a history of having a social undercurrent.</P>
<P>During the Great Depression, the 1932 film <I>White Zombie</I> addressed working-class oppression, with Bela Lugosi turning ordinary people into zombies to slave in his sugar mill. As fears of Nazism rose in the U.S., 1941's <I>King of the Zombies</I> had a mad scientist creating zombies for the Axis powers. </P>
<P>Given recent experiences with the "war on terror" and "orange alerts," I had hoped that the new reworking of director George Romero's classic 1978 zombie movie <I>Dawn of the Dead </I>would bring political horror--excuse the pun--"back from the dead." But new <I>Dawn </I>is more splatter than scare. And it doesn't quite succeed in showing anything about larger political questions.</P>
<P>With suburban Wisconsin crumbling into chaos caused by a plague of cannibalistic zombies, several survivors take refuge in a shopping mall. In a couple of standout moments when the fear and racism of the still-living humans are on display, the film does touch on larger themes.</P>
<P>For example, a band of survivors begs to be let into the mall, but the rent-a-cop refuses, saying that "If we start letting people in, eventually we're going to let in the wrong ones"--a fairly strong nod to anti-immigrant sentiment following September 11. But that's as far as it goes. The movie opts for standard Hollywood characters and plot lines--the "heroic but stoic" SWAT team cop, the budding romance-in-the-face-of-death, the yuppie scumbag who will betray the group.</P>
<P>Those looking for a more fun scare should check out the original <I>Dawn of the Dead. </I>Romero was inspired to make the film in the mid-1970s when he was taken on a tour of a shopping mall under construction.</P>
<P>"[T]hey had sealed-off rooms upstairs packed with civil defense stuff, which they had put there in the event of some disaster--and that's what gave me the idea," comments Romero. "I mean, my God, here's this cathedral to consumerism, and it's also a bomb shelter just in case society crumbles."</P>
<P>Reflecting the political turmoil of the time--from the corruption of Watergate to the U.S. defeat in Vietnam to the mid-1970s economic recessions--the movie's social commentary focuses on an anti-consumerist note. The film is also striking for its antiracist and anti-police brutality themes.</P>
<P>In one scene, racist SWAT police open fire on Puerto Rican and Black residents who refuse to leave their homes. Those themes are even more prominent in Romero's first zombie movie, the 1968 black-and-white movie <I>Night of the Living Dead.</P>
</I><P>Made in on a shoestring budget, the film set the tone for a wave of modern horror movies. Romero consciously evoked images of the civil rights era--mob lynchings, police dogs attacks and a Southern sheriff casually advocating shooting zombies in the head.</P>
<P>Focusing on the conflicts between a group of survivors trapped in a farmhouse besieged by zombies, <I>Night </I>is a meditation on racism in America, with an ending that never fails to stun. After a night of narrowly escaping zombies, the survivors believe they are saved when they hear voices outside.</P>
<P>But on exiting the farmhouse, Ben, the movie's Black hero, is killed by a white Southern lynch mob--and his body is thrown on the fire with the corpses of zombies. "As far as the tone of the piece, I think it just came from the anger of the times," Romero said. "It was 1968 and nobody was in a very gleeful mood about the way the world was going, and so it just seemed appropriate to put those themes into the film as well."</P>
<P>Meanwhile, I'll be looking forward to Romero's planned fourth installment of the <I>Dead </I>series. "The dead are the homeless," Romero has said. "We walk over them on the way to the theater."</P>


