<P><font face="Times New Roman, Times, serif" size="4"><b>Confront the antigay bigots</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Dan Keefe</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW PALTZ, N.Y.--Antigay fanatic Fred Phelps is bringing his hateful message here April 4. He and his followers will travel to New Paltz to picket "fags and fag-enablers" of this small village where "homo-fascist" Mayor Jason West has been issuing licenses and performing marriages for gay and lesbian couples. The infamous Phelps, who celebrated the gruesome murder of Matthew Shepard, is known for such nauseating notions as the claim that the September 11 attacks were an act of "divine retribution" for America's "tolerance" towards homosexuality. </P>
<P>In response to Phelps' protest, New Paltz activists are planning a festival on the other side of town, arguing that a counterprotest would only give Phelps the attention and publicity he's looking for. But we need to confront this bigot head on. </P>
<P>Staging a counterprotest to Phelps' antigay gang is more than simply opposing an isolated case of religious fanaticism. Phelps' rally can give confidence to gay bashers and bigots who attend the rally, inciting them to dole out violence even after Phelps leaves.</P>
<P>By confronting Phelps, we can keep him from reaching his potential audience. We have to stand up to Phelps--and demand equal rights now!</P>

