<p><a name="Hamoui"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for the Hamoui family</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Three members of a local Syrian family had a March 29 hearing at the Ninth Circuit Court of Appeals March 29 to determine whether they'll be deported. The Hamouis--Safouh, Hanan Ismail and their 20-year-old daughter Nadin--were arrested under Attorney General John Ashcroft's "Absconder Apprehension Initiative" in February 2002. </P>
<P>More than 300,000 immigrants--mostly Arab--have been targeted by this measure. Hanan and Nadin were detained for nine months, Safouh for 10 months. A campaign of weekly pickets for months outside the INS jail led by the Arab American Community Coalition were key to helping to win their release. </P>
<P>A decision could be handed down from within 24 hours to months from now. If the decision goes against the Hamouis, the deportation process could begin immediately. The struggle to keep the Hamouis from being deported needs to be supported by everyone. </P>

