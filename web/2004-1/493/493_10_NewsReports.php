<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight racism</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Julie Southerland</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">GREENSBORO, N.C.--An anti-affirmative action bake sale held by the College Republicans was a flop, thanks to activists who confronted them and shut them down. The bake sale was part of  "Morals Week," organized by the bigots in opposition to the pro-gay Pride Week.</P>
<P>A multiracial crowd of 60 students, including Unitarian Universalists and members of the International Socialist Organization, came out to challenge the reactionaries in the College Republicans. The heated debates ended with one Republican admitting, "We realize this is coming off as us being racist...!"</P>
<P>A flier distributed by the ISO outlined the reasons why affirmative action is necessary to address the imbalances caused by racism. The crowd took up chants of "Unite and fight! Together we can beat the right!"</P>
<P>The Republicans ended up shutting down their event early and left the cafeteria demoralized. They seem to have got the message--bigotry is not welcome on our campus. But if they didn't, we'll confront them again.</P>

