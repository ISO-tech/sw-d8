<p><a name="Hate"></a><font face="Times New Roman, Times, serif" size="4"><b>No to hate crimes</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ARLINGTON, Wash.--About 500 residents here marched and rallied against racism after a huge cross was burned on the lawn of one of the few Black families in this rural town. Two 16-year-olds who previously attended Arlington High staged the cross burning last week on the lawn of Pastor Jason Martin.</P>
<P>Racist harassment has been a problem at the high school, but after the cross burning, the town came together with one voice to say "Not in Our Town!" Many of the marchers were middle- and high-school students, who are classmates of Pastor Martin's children. </P>
<P>People of all ages participated. Marian Harrison, who was one of the first Black residents of Arlington and worked for Arlington Public Schools for 22 years, said, "We're not going to let fear come into our community."</P>
<P>The unity of the town was on display. Middle-school students made dozens of signs. A flower shop donated orange balloons. A hardware store donated orange tape for ribbons. Though the perpetrators of the racist act have turned themselves in, residents of the town expressed determination to prevent future hate crimes.</P>

