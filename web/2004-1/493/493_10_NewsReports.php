<p><a name="ABB"></a><font face="Times New Roman, Times, serif" size="4"><b>Anybody But Bush?</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--About 150 people packed into a small room at the Queens College Extension Center to hear Peter Camejo and Howie Hawkins of the Green Party speak on "Anybody but Bush? What Should Progressives do in 2004?" The event was part of a national speaking tour by Camejo, who ran for governor of California in 2003.</P>
<P>Both speakers argued for the need to build an independent electoral alternative to the left of the Democratic Party. Howie Hawkins, who is the chair of the Onondaga County Green Party in New York, argued that "Kerry might be able to beat Bush, but he won't beat Bushism."</P>
<P>Camejo spoke about the Green Party's growth in California, including the party's impressive showing in the gubernatorial recall race and the San Francisco mayoral elections. Both speakers favored endorsing Ralph Nader's campaign for president.  </P>
<P>When asked about the prospects for working inside the Democratic Party as a progressive, Camejo responded that "the AFL-CIO went into the Democratic Party with a small organization of 15 to 20 million people. They went from 37 percent of the U.S. [working population] to 13 percent." The event was co-sponsored by a number of left groups, including the ISO, Solidarity, Socialist Alternative and half a dozen local Green Party chapters.  </P>



