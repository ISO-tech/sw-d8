<p><a name="Cops"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Leela Yellesetty</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--Around 50 people demonstrated against police brutality here March 27. Organized by People Against Injustice, the small but spirited group marched from the police station to the courthouse to the jail, chanting "What do we do when communities fail? Build schools not jails!" and "Same thing every time, being Black is not a crime!"</P>
<P>At each stop the group gathered to hear speakers, who talked about their experiences with police brutality, the prison system and the war on drugs. "This is not a war on drugs," said one speaker. "This is a war on poor people, a war on minorities."</P>
<P>Speakers pointed out that the prison system was a business in itself, profiting from a large supply of cheap labor as well as overcharging prisoners for basic necessities. Demonstrators also objected to the state's practice of sending prisoners out of state, where family members would not be able to visit them.</P>
<P>Following the march, protesters gathered to hold a public forum and strategy session for upcoming actions. The forum ended with plans to bring activists from around Connecticut to Hartford on April 14 to rally in support of prison and sentencing reform legislation.</P>

