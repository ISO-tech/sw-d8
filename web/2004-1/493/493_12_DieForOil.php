<P><font face="Times New Roman, Times, serif" size="4">He wouldn't go to Iraq:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"No soldier should die for oil"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 2, 2004 | Page 12</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">STAFF SGT. Camilo Mej&iacute;a was officially charged last week with desertion because he failed to return to duty in Iraq after a two-week break. Mej&iacute;a spent five months in hiding and last week turned himself in to military authorities, becoming the first soldier to go public with his refusal to return to Iraq and seek "conscientious objector" status.</P>
<P>If found guilty, Mej&iacute;a faces a special court-martial, a bad conduct discharge and up to a year in prison--all for the "crime" of exposing the truth about the U.S. war and occupation of Iraq. "I don't think we're fighting terror in Iraq," said Mej&iacute;a. "The justification for this war is money, and no soldier should go to Iraq and give his life for oil."</P>
<P>"I have not committed a crime, and I should not run," he added. Mej&iacute;a's experiences of fighting in Iraq convinced him that the U.S. war and occupation is "immoral."</P>
<P>"When I saw with my own eyes what war can do to people, a real change began to take place within me," said Mej&iacute;a. "I have witnessed the suffering of a people whose country is in ruins and who are further humiliated by the raids, patrols and curfews of an occupying army. My experience of this war has changed me forever."</P>
<P>Mej&iacute;a was angered about the many Iraqi civilian casualties inflicted by U.S. forces. "When you join, you have no idea what war is like," Mej&iacute;a said. "The people who are paying for it with their blood are in the wrong place at the wrong time."</P>
<P>He was also horrified by his officers' callous disregard for the lives of U.S. soldiers. The military has already begun a slander campaign against Mej&iacute;a, saying that he "lost his nerve."</P>
<P>Capt. Tad Warfel, Mej&iacute;a's guard commander, was trotted out to "attack" Mej&iacute;a's character, claiming: "I think he's a mommy's boy, and his mom greatly influences him." Warfel also defended the criticism of Mej&iacute;a's squad for celebrating their avoidance of casualties. "It's not the infantryman's job to hightail it out of the area," said Warfel. "I would berate anyone who didn't close in and kill the enemy."</P>
<P>The senseless loss of life--of both Iraqis and U.S. soldiers--because of the Bush administration's pursuit of oil and empire must stop. End the occupation! Bring the troops home now! </P>

