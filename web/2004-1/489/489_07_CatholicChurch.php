<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The hypocrites in the Catholic Church</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 5, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ON FEBRUARY 23, Patrick McSorley died in a Boston apartment at the age of 29. McSorley had been unable to kick the heroin addiction that killed him. "The memories of what had happened to him he had to battle continuously," a friend described. "He would do almost anything to escape the pain."</P>
<P>As a 12-year-old boy, McSorley went out for ice cream with Father John J. Geoghan, who offered the comfort and trust of a priest after the boy's father committed suicide. Instead, Father Geoghan molested the boy in his car.</P>
<P>McSorley was just one of 85 victims of serial predator Father Geoghan, who was shuffled from parish to parish for two decades while Catholic Church leaders silenced his accusers. Geoghan was defrocked only after his arrest in 1998 for molesting a 10-year old boy.</P>
<P>McSorley came forward to tell his story two years ago and helped thousands of other victims to do the same, unearthing the web of conspiracy at the top of the Catholic hierarchy in covering up widespread child sexual abuse by priests. Ironically, last Friday--the same day McSorley's young body was laid to rest in a Massachusetts cemetery--Catholic Church officials convened a press conference to bury the abuse scandal.</P>
<P>Bearing two long-awaited reports on sexual abuse, Bishop Wilton D. Gregory, president of the Conference of Catholic Bishops, assured the world media, "The terrible history recorded here is history." To be sure, the reports contain wrenching statistics, documenting the abuse committed by 4,392 priests against 10,667 children between 1950 and 2002:</P>
<P>--Priests in 95 percent of U.S. dioceses have been accused of abuse--among them, one of every 10 priests ordained in 1970.</P>
<P>--Most allegations of abuse were never reported to police by bishops--and 95 percent of the priests were never charged with a crime.</P>
<P>--Nearly 30 percent of the victims were abused for two to four years, while an additional 10 percent were abused for up to nine years. </P>
<P>--Just 149 priests--serial pedophiles--abused 2,960, or 27 percent, of the children.</P>
<P>But the two reports--both commissioned by and controlled by the National Conference of Catholic Bishops--are actually an attempt by the Church's hierarchy to stave off attempts to drag it out of the Middle Ages.</P>
<P>Former Oklahoma Gov. Frank Keating, a member of the National Review Board of Catholic Lay People, which authored one report, was forced to resign after he compared the Church hierarchy's methods of concealing sexual abuse to those of La Cosa Nostra. Not surprisingly, the National Review suggested better "fraternal review" among bishops--a gentle rebuke, considering the bishops were collectively responsible for covering up the scandal for decades. The other report, by the John Jay College of Criminal Justice, relies exclusively on the questionable statistics provided by the bishops themselves.</P>
<P>Predictably, Church spokespeople aimed to steer the root cause of widespread pedophilia among priests away from obvious problems stemming from enforced celibacy among those ordained as religious authorities--pointing the finger instead toward homosexuality.</P>
<P>In reality, pedophilia has nothing to do with either straight or gay sexuality, but adult sexual oppression of a child. And celibacy for priests, like much of Catholic orthodoxy, originates not with devotion to the Almighty, but the Church's quest for wealth and power.</P>
<P>Until the 11th and 12th centuries, Catholic priests were allowed to have multiple wives and mistresses, and leave their property to their children. First, the Church banned inheritance to the children of clergy, so the property could become the Church's. Then it banned marriages and mistresses for priests, and in 1139, it voided all marriages of priests while ordering all new priests to divorce their wives.</P>
<P>The Catholic Church did not become one of the wealthiest and most powerful institutions in the world by following its own creed of human fellowship. Nothing could be more fitting than if the pedophilia scandal that exposed its hypocrisy so vividly were to bring its hierarchy down.</P>
<P>Then Patrick McSorley will not have fought--and died--in vain.</P>

