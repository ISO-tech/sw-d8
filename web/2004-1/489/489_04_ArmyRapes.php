<P><font face="Times New Roman, Times, serif" size="5"><b>Army excuses rape scandal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 5, 2004 | Page 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LAST YEAR, Washington's media circus turned the country's attention to the sensationalized rescue story of Jessica Lynch, a young female private taken prisoner in Iraq. But far less attention was lavished on the other dangers faced by women in the U.S. armed forces--such as the likelihood of being raped by fellow soldiers.</P>
<P>Military officials reported last month that over the last 18 months, women serving in the U.S. military in Iraq, Afghanistan and Kuwait have reported 112 cases of sexual misconduct, including being sexually assaulted or raped by fellow troops.</P>
<P>In a Senate hearing at the end of February, Christine Hansen, executive director of the victims' advocacy group the Miles Foundation, said that women reported that the military was grossly negligent in a number of areas--from a lack of emergency medical care and rape kits, to incomplete criminal investigations into women's reports, to retaliation by peers for reporting an assault. In addition, about two dozen women at Sheppard Air Force Base in Texas have reported to a local rape crisis center that they were assaulted in 2002.</P>
<P>The Pentagon tried to avoid criticism by pointing to a report showing a decline in the percentage of servicewomen reporting violations, from 6 percent in 1995 to 3 percent in 2002. But 3 percent still comes out to about 6,400 of the 214,000 women on active duty--6,400 too many! And it's likely that this is just the tip of the iceberg.</P>
<P>Defense Secretary Donald Rumsfeld was forced to order a high-level review of the handling of reports of sexual assaults, which will be conducted by a 10-member task force from the military services and the Pentagon's Joint Staff. The Army and Air Force have launched investigations as well. </P>
<P>But don't expect much from an organization steeped in sexism. Last year, the elite Air Force Academy in Colorado reported more than 50 assaults or rapes over the last decade. Male cadets had been raping female classmates for years, and the Pentagon ignored it. </P>
<P>Nearly one-fifth of the academy's 660 female cadets said that they had been victims of at least one assault or attempted assault. According to the report, sexual assaults "became a part of life" at the school--because of a "chasm of leadership" on campus and in the Pentagon.</P>

