<P><font face="Times New Roman, Times, serif" size="4">Death sentence overturned</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Delma Banks deserves justice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Marlene Martin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 5, 2004 | Page 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DELMA BANKS, a Texas death row prisoner who came within 10 minutes of being executed last year, had his death sentence overturned last week by the U.S. Supreme Court. By a 7-2 margin, the justices found prosecutors had withheld evidence during the original trial that probably would have convinced jurors not to impose a death sentence--and maybe not to find him guilty at all.</P>
<P>Banks was convicted in the 1980 murder of a former coworker mainly because of testimony from two key witnesses--both of whom lied on the witness stand. One witness claimed that he wasn't a police informant, and prosecutors never corrected this falsehood.</P>
<P>Another said that he hadn't been coached by police. In fact, he had rehearsed his testimony for so long that the transcript of his session with police was 74 pages long! Once again, prosecutors knew that this witness had lied--but said nothing.</P>
<P>Banks' case is filled with grotesque injustices. He is Black and was tried by an all-white jury. Prosecutors kept four African Americans off the jury, and Banks' lawyer didn't object. No physical evidence tied Banks to the crime--only the testimony of the two witnesses.</P>
<P>William Sessions, the former FBI director, was among a group of retired federal judges who wrote a brief endorsing a stay of execution for Banks. Last week, Sessions said that the Supreme Court decision was "a clear victory for justice."</P>
<P>Yet this case went through dozens of appeals, and Delma Banks came within 10 minutes of dying. He calls <I>that </I>justice?</P>
<P>The Supreme Court decision orders a lower court to hear a motion from Banks' lawyers about why the conviction itself should be set aside. After 24 years on death row, Delma Banks may finally get the justice that has been denied to him for so long.</P>


