<p><a name="Welcome"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigrants are welcome here</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Maya de Leon</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Some 700 people joined the March for Immigrant's Rights downtown Los Angeles February 28. There were families with children and activists out in twenty or so contingents with banners demanding "General Amnesty," "Ending the Raids and Round-Ups by La Migra/Immigration,", and the ISO's Querremos Un Mundo Sin Fronteras (We Want A World Without Borders). </P>
<P>The march was held in preparation for a larger November march against an ballot initiative aimed at repeating Proposition 187, which barred immigrants from receiving social services for until a court struck down much of the law. Today, the racist, anti-immigrant xenophobes are working on a new proposition to deny education, medical and other social services to immigrants.</P>
<P>There was also a lot of anger from the crowd about the recently revoked law to grant undocumented immigrants driver's licenses. Several organizations worked in coalition to build the march, including Hermandad Mexicana Nacion&aacute;l, Movimiento Latino USA(LMUSA), Korean Americans for Peace, the Bert Corona Foundation and many other activist groups.</P>
<P>The exciting news from these coalition meetings is that this new coalition will build the March 20 anti-war rally and march as well as other issues which must be tied together: women's rights, anti-death penalty work, immigrant's rights, anti-racist/pro-civil liberties organizing, and the right for gay/lesbian marriages. This event marked the beginning of solidarity between the immigrant communities and groups doing anti-war organizing. </P>

