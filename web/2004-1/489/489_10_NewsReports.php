<P><font face="Times New Roman, Times, serif" size="4"><b>Save Kevin Cooper</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Crystal Bybee</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Kevin Cooper's stay of execution, recently upheld by the U.S. Supreme Court, was a major victory for death penalty opponents. Yet Kevin is still on death row, and activists are now recognizing that we need to keep fighting for him until he is free.</P>
<P>At the same time, we have a historic opportunity in California to push for a moratorium on the death penalty. The Campaign to End the Death Penalty chapter at UC Berkeley will host a "Live From Death Row" event to celebrate the victory and continue the struggle.</P>
<P>The Campaign is also launching a new chapter at San Francisco State University with students who fought against the execution. The Committee to Save Kevin Cooper met February 16 and decided to re-launch with the name Free Kevin Cooper Committee for a California Moratorium.</P>
<P>The committee will continue to build public support for Kevin, while exposing the horrors of the largest death row in the country. We want to build opposition to the planned execution of inmate Donald Beardslee and all future executions in California.</P>
<P>We also want to publicize the case of Stan "Tookie" Williams, a death row inmate whose writings and work on death row have resulted in multiple nominations for the Nobel Peace Prize. The FX network has produced a movie about his life story, called <I>Redemption.</I> The movie will air on April 11, and has already been featured at the Sundance Film Festival and is being heavily promoted by the network.</P>
<P>All of these elements can come together in the Free Kevin Cooper Committee for a California Moratorium, which held its public launch in Richmond February 21 with Jim Brown, an NFL Hall of Fame member, and Barbara Becnel, an advocate for Williams.</P>
<P>The Committee is bringing together the activists, community members, and people of faith who fought to stop Cooper's execution. The aim is to link that struggle with other cases on California's death row, and push politicians and legislators to win real justice for Kevin Cooper--and end all executions in California.</P>

