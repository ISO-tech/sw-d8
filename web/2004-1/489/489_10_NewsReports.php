<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Stand up to racism</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jonah Birch</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Hundreds of students at Columbia University engaged in a week of protests following the latest in a series of racist incidents on campus. Last month, a student newspaper, <I>The Federalist</I>, printed a cartoon called "Blacky Fun Whitey," which read in part, "Black people were invented in the 1700s as a form of cheap labor."</P>
<P>The cartoon came three months after the Columbia University Marching Band issued racist, sexist, and homophobic flyers for a performance, and just three weeks after the Columbia College Conservative Club held an anti-affirmative-action bake sale in the main student center.</P>
<P>From February 23 to 25, students held silent protests on the steps of Low Library, wearing black clothes and signs reading "I am being silenced." The following two days, hundreds of people gathered for speakouts in which students denounced racism at Columbia and the complicity of the university administration. </P>
<P>After ignoring student requests that he address racism on campus for weeks, Columbia President Lee Bollinger finally issued a statement calling for greater "tolerance" from students. But the protesters are clear that they are fighting for structural change at Columbia, an institution at which people of color are underrepresented in both the student body and the faculty.</P>
<P>On February 27, representatives from the ad-hoc coalition Columbia University Concerned Students of Color presented a list of nine demands to university officials. These demands focus on the need for changes to Columbia's curriculum, more student control over faculty hiring, increased space and funding for groups and organizations representing students of color, and mandatory courses on racism for university administrators. </P>
<P>As one student leader said at the speakout: "It needs to be understood that we ain't going nowhere. We aren't just standing here, we want tangible results...We will be here until we see change, and let that be known."</P>


