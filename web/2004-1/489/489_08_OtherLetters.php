<P><font face="Times New Roman, Times, serif" size="4"><b>Cracking down on dissent</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Near universal disgust forced Rhode Island Gov. Donald Carcieri to halt his plan for a state homeland security bill more damaging to civil liberties than even Attorney General John Ashcroft's USA PATRIOT Act. A press release from the governor made the ridiculous claim that his "Act Relating to Homeland Security" is necessary to protect Rhode Island from terrorists with weapons of mass destruction. </P>
<P>In a state where 6,000 people are forced to live without heat, the idea that we need legislative protection from nuclear weapons is simply a cover-up of the real issues facing ordinary people. Terrorism, though, was nearly an afterthought in this legislation. This law was about cracking down on our right to dissent. </P>
<P>Carcieri hoped to revitalize unused and clearly unconstitutional First World War-era laws which make it a felony to "willfully speak, utter, print or publish any language" meant to "incite, provoke, or encourage" a "defiance or disregard of the constitution or laws of Rhode Island or of the United States." </P>
<P>Advocating even the most peaceful act of civil disobedience would be a felony punishable by 10 years in prison. Furthermore, Carcieri's definition of "terrorism" is so vague (action to "intimidate or coerce a civilian population" involving a "violent act") that it could have easily been used to hand out life sentences to striking workers trying to keep out scabs.</P>
<P>Carcieri's quick retreat suggests that he initially thought he could get this monstrosity passed without any fuss. This is an impressive oversight, considering that many Rhode Island cities have already passed resolutions condemning the comparatively milder USA PATRIOT Act, and that fully one-in-four Rhode Islanders live in an anti-PATRIOT Act cities. Carcieri, though, will likely try later to pass a slightly milder bill, and so we must take his first attempt as a warning: Our civil liberties will not be safe without a fight.</P>
<B><P>Alden Eagle,</B> Providence, R.I.</P>


