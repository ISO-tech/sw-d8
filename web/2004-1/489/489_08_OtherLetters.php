<p><a name="MoveOn"></a><font face="Times New Roman, Times, serif" size="4"><b>The Super Bowl ad CBS wouldn't run</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Brad Ward's letter about the Super Bowl half-time show (<I>SW</I>, February 20) left out the biggest scandal of all: CBS's censorship of MoveOn.org's "Bush in 30 seconds" advertisement. </P>
<P>The homemade ad, which criticizes George W. Bush's economic policies, was paid for by thousands of individual donations and was going to run during halftime--until CBS decided it was "too controversial." Instead, CBS chose to run an ad produced by the White House Office of Drug Policy. </P>
<P>To view MoveOn.org's ad, visit http://www.bushin30seconds.org.</P>
<B><P>Matt Nichter and Andrew Villiesse,</B> Madison, Wis.</P>


