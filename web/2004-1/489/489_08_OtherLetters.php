<p><a name="France"></a><font face="Times New Roman, Times, serif" size="4"><b>France's racist ban on the hijab</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Thank you for your article ("The racist hypocrisy behind the hijab ban," February 20) about the ban on Islamic headscarves being worn in French public schools. I think it's both racist and sexist to tell women of any religion or culture what they can and cannot wear. While it's sexist to force women to wear something like the burqa, it's equally sexist to ban them from wearing any particular thing for no good reason.</P>
<P> As far as the law is concerned, a headscarf or any piece of clothing should be considered just that--a piece of clothing or an accessory that they have no business either enforcing, nor banning. Not only do I think that the ban is not a step forward for women, but I think that it is a perfect example of modern-day sexist paternalism.  </P>
<P>We women of all different colors, shades, religions, etc., should band together internationally against this absurd attack on our freedom. </P>
<B><P>Lisa Baker,</B> Upland, Calif.</P>

