<p><a name="Democracy"></a><font face="Times New Roman, Times, serif" size="4"><b>A plan to reduce democracy</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I was present on Monday January 26 at a meeting organized by the Uniformed Services Council of Harlem, to discuss the proposal of the 125th Street Business Improvement District (BID) to put surveillance cameras on 125th Street in Harlem, at a cost of $800,000. Representatives from the BID, the NYPD, the comptroller's office and the District Attorney's office were invited to be present at this meeting, but they did not even answer the invitations.</P>
<P>Due to the fact that the invited speakers did not show up, the discussion was opened up to the 41 people attending. Several audience members spoke about their concerns with the question of who the cameras are protecting, and who are they criminalizing. </P>
<P>Audience members asked, "Are we so worried about our youth that we need to monitor them with cameras to protect ourselves, or can we say that this is a problem of the system that creates these conditions in the first place?" They also spoke of how "the whole camera thing might be good in the movies, but when it comes down to it, it's people that matter. They can do the same thing to you that they did to the civil rights leaders of the 1960--identify you as a threat, single you out and take away your civil rights."</P>
<P>The lack of representation by the BID and the NYPD brought more distrust of the real reasons behind the cameras. As one person commented, "When the police and other agencies don't care to show up, and they are supposed to be the backbone of our community, what does that say?" The last speaker, Norman Siegel of the New York Civil Liberties Union, summed things up: "You can put a camera on every corner, and while you will certainly reduce crime, you will also reduce democracy!" </P>
<P>At the end of the meeting, a vote was taken, and 36 of the 41 present said "no" to the cameras. I think it is very important that we get involved in these local struggles because of the systematic repression that they fight--as well as building the confidence of ordinary working class people to fight back.</P>
<B><P>Paul Grohman,</B> New York City</P>


