<p><a name="Native"></a><font face="Times New Roman, Times, serif" size="4"><b>Targeting Native rights in California</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
California Gov. Arnold Schwarzenegger (a.k.a. The Gropernator) is proposing that California place a tax on Indian gambling casinos. He claims that the tribes that operate these casinos should pay their "fair share." </P>
<P>This prompted Anthony Miranda, chairman of the California Indian Nations Gaming Association, to recently comment: "Tribes are governments. Governments don't tax other governments." He then added: "From our perspective, the governor's concept of 'fair share' is amusing and troubling. It clearly shows that he doesn't understand a very simple fact: that the state does not pay a single dime in compensation to the tribes. Nothing."</P>
<P>Miranda is right on both counts. These tribes are sovereign nations, most of whose land was stolen from them in the 19th century. California's first governor, Peter Burnett, declared, "A war of extermination will continue to be waged between the races until the Indian races become extinct."</P>
<P>There were 150,000 Indians in California in 1848. By 1860, ten years after California became a state, there were only 30,000 left. As one historian put it, the survivors were "pushed into the rocks." They were forced into remote areas where the land was unsuitable for farming or for anything else. </P>
<P>That's why the tribes have turned to operating casinos: it's the only way our society allows them to support themselves. Schwarzenegger's talk about the tribes paying their "fair share" is rank hypocrisy. </P>
<P>Some of the governor's advisors are pushing for a ballot measure that would allow racetracks and card rooms to have slot machines. According to the <I>Los Angeles Times</I>, "The prospect that he might embrace [the ballot measure] could be used as leverage in coming talks with the tribes."</P>
<P>Socialists should oppose this cynical ploy. California's state government is trying to extort money from the Indian tribes to clean up a fiscal mess created by the state's own corrupt political leaders.</P>
<B><P>Evan Kornfeld,</B> Los Angeles</P>


