<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Greenspan's Social Security scam</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 5, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ALAN GREENSPAN is ready to throw future senior citizens to the wolves. In testimony to Congress last week, the Federal Reserve Board chair said that soaring budget deficits from entitlement programs like Social Security and Medicare could lead to a "very debilitating" rise in interest rates and threaten the economy as Baby Boomers become eligible for retirement over the coming years.</P>
<P>"I am just basically saying that we are overcommitted at this stage," Greenspan told the House Budget Committee. His solution? Slash benefits by pushing up the retirement age for Social Security and Medicare--which is already set to rise to age 67 over the next two decades. Greenspan also said Congress should reduce Social Security payouts by setting cost-of-living increases at a half a percentage point below the official rate of inflation.</P>
<P>Greenspan's plan would be a disaster for millions of American workers who depend on Social Security. About 40 percent of Americans have saved almost nothing for retirement--as a result of shrinking pensions and 30 years of wage stagnation, according to the National Retirement Planning Coalition.</P>
<P>The number of companies offering a fixed-benefit pension has dropped by almost half in the past two decades, to about 21 percent of private-sector workers in 1998. Just 15 percent of working-age Americans have an individual retirement account (IRA), and 22 percent contribute to a 401(k) plan, according to the Employee Benefit Research Institute (EBRI). </P>
<P>Overall, American seniors will have $45 billion less in retirement income in 2030 than they will need to cover basic expenses, according to the EBRI. That means that Social Security is--and will continue to be--essential for millions.</P>
<P>In reality, there is no Social Security "crisis." The Social Security Trust Fund's own recent report says that it will be able to meet all of its obligations through the year 2042. Greenspan is pushing for Social Security cuts to balance the budget even though he endorsed making the Bush administration's 2001 tax cut giveaway to the super-rich permanent.</P>
<P>"[T]he whole idea that Social Security needs 'reform' is nothing but hype, the product of military-style disinformation," wrote Mark Weisbrot and Dean Baker of the Center for Economic and Policy Research last year. "The 'coalition of the willing' in the war on Social Security is led by the financial industry, which stands to earn hundreds of billions of dollars in fees and commissions if it can hold Social Security funds in individual accounts. They are joined by wealthy taxpayers who view any government dollar that is not paid out in corporate subsidies as a potential tax break in their pockets."</P>
<P>There's a simple solution to the "relatively minor long-term problems" that Weisbrot and Baker identify with the Social Security system--tax the rich. The current Social Security tax only applies to wage income and is capped at about $88,000. So a decently paid union autoworker pays the same amount into the Social Security trust fund as Donald Trump or Bill Gates! </P>
<P>Because this is an election year, Bush and the Democrats are letting Greenspan do the talking about Social Security "reform." But both mainstream parties endorse his underlying theme of balancing the budget on the backs of workers, and shifting more and more of the cost of what used to be entitlements onto ordinary people. We can't let them get away with it.</P>


