<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>United Food and Commercial Workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Marcia Thorndike</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Nearly 700 rank-and-file members of eight San Francisco Bay Area locals of the United Food and Commercial Workers Union (UFCW) participated in a mass mobilization meeting and activist training session March 14 at the International Longshore and Warehouse Union (ILWU) Local 10 hiring hall.</P>
<P>"The event in San Francisco today was really a shot across the bow," said University of California-Berkeley professor and labor expert Harley Shaiken. "The union is signaling early that they don't simply intend to accept the contract that was negotiated in Southern California." The meeting was the public debut of a rank-and-file mobilization campaign initiated September, 2003, before the start of the Southern California strike on October 11, 2003.</P>
<P>The eight Bay Area UFCW locals agreed to hire a "mobilization team" of several seasoned labor organizers --known as the Bay Area Coalition--to begin gearing up for the contract fight a year in advance. Their job is to reverse rank and file apathy and negativity built up over decades of UFCW history as a "service union" in "partnership" with the employers and inspire the kind of union pride and solidarity necessary to ward off major contract concessions.</P>
<P>The opening session mostly featured rank-and-file speakers, with the notable exception of the Democratic Senator from California, Barbara Boxer. Grocery clerk Diane Powe's speech got right to the point. She said this was the start of activist training, where workers would learn "what we need to do and know to get a good contract."</P>
<P>As she explained, "All the things working-class people want and depend on are disappearing. Why? Because the rich want to get richer.  It was the working class that built this country. Why should we step back when our employer doesn't want to give us our fair share?"</P>
<P>Breakout sessions went over employee rights, shop floor contract enforcement, and community outreach. The meeting also saw the first issue of a fledgling rank-and-file newsletter for UFCW members to bring back to their home stores. The paper, <I>The UFCW Employee Voice,</I> included "A Letter to Our Southern California Brothers and Sisters."</P>
<P>The message of the letter was that Northern California UFCW members will continue the struggle that their Southern California brothers and sisters sacrificed so much for. The letter concludes, "We have . . the opportunity to learn from your experience. There are important questions about strategy, tactics and the negotiation process that need to be answered for us to make sure we win fast and decisively on the picket line and our victory is not squandered at the negotiating table. Please write to our paper, and we will publish your letters."</P>
<P>The solidarity needed to fight to win will only come with real rank-and-file power, not just a membership mobilized to show up for rallies. Through the efforts of the Bay Area Coalition and creative independent organizing like the employee newsletter, there's hope that the virtual internal revolution required for members to take the reins of their own union may happen.</P>

