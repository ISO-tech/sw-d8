<P><font face="Times New Roman, Times, serif" size="4"><b>Do we really want gay marriage?</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
As an advocate for equal rights, I find myself in the political nowhere land with regard to gay marriage. "Separate is not equal," I hear--a powerful statement boldly stolen from the days of the American civil rights movement, e.g. <I>Brown v. Board of Education</I>. But marriage is hardly education. </P>
<P>For anyone who has made this issue their sole focus, I ask, "What are you fighting for?" Is marriage the institution within which we want to be included? Considering the history of this capitalist sham, let us think before we are determined to get our piece of this rotten pie.  </P>
<P>We stand in a unique space to truly challenge the institution of marriage and, consequently, the system of allocations of benefits. Let us not be blinded by our lack of inclusion to the point where we become solely focused on being a part of this corrupt system. </P>
<P>We should not fight for marriage, but we should fight for the benefits that come with marriage, independently of the institution itself. What I want is not to spend $50,000 on a ceremony, celebrating a business agreement, which has a 50-50 chance of survival. </P>
<P>What I want, just as one example, is for my partners and friends to have insurance so that everyone can get proper medical attention regardless of their marital status. By rejecting the idea of marriage, let us reject the compulsive consumerism of imposed heterosexuality. </P>
<P>We want more than "hers and hers" matching towel sets. We want more than a prepackaged concept of what a legitimate relationship looks like. Let us step out of the procession and boldly state that we do not require the state of coupledom to be productive members of the community. </P>
<P>Let us not have the veil pulled over our eyes. Marriage is not a step in the right direction. It is participation in the institution that promotes the normality of compulsive heterosexuality. It is a gigantic leap backwards. <BR>
<B>Ahoo Tabatabai, </B>Cincinnati, Ohio</P>

