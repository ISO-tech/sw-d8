<p><a name="Wait"></a><font face="Times New Roman, Times, serif" size="4"><b>We can't wait to demand our rights</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In a 1965 speech, Malcolm X explained the impact of the more radical Nation of Islam on the broader civil rights movement: "Ten years ago the NAACP was looked upon as a radical leftist, almost subversive movement. And then when the Black Muslim movement came along, the white power structure said, 'Thank the lord for Roy Wilkins and the NAACP!'"</P>
<P> This lesson should not be lost on us today, when the conventional wisdom on the left seems to be one of compromise. Even as the bigoted right wing has become more uncompromising, and is even taking to the streets, many leaders on the left argue that we have to put our faith in "centrist" Democrats and be careful not to alienate "mainstream America." </P>
<P>This approach, we are told, is the most pragmatic. A look at history, however, reveals the opposite: progressive change occurs only when we are outspoken and militant. Therefore, the truly "pragmatic" strategy is to be loud and confident in our demands for justice and equality, not to hide our views behind conservative Democrats in the hopes of winning piecemeal reforms. </P>
<P>The latter strategy, far from taking us closer to our goals, actually emboldens the right to ask for more. We must be bold and uncompromising about our demands--whether it be ending the occupation of Iraq or fighting for the right of gays and lesbians to marry, the right of everyone to free health care and decent wages, or an end to the racist death penalty (none of which are all that "radical" to begin with). This is the most effective strategy, both for winning concrete reforms in the here and now and in arming us in the fight for a new society.<BR>
<B>Leela Yellesetty, </B>New Haven, Conn.</P>


