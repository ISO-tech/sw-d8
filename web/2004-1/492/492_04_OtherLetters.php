<p><a name="Energy"></a><font face="Times New Roman, Times, serif" size="4"><b>This struggle is energizing people</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
The leading issue in the struggle for gay equality is clearly the issue of same-sex marriage. Whatever we think of marriage, this is <I>the</I> issue that is on the minds of millions of people.</P>
<P>Unfortunately, some on the left wish this wasn't so. They rightly see marriage as a restrictive, reactionary institution. They want to talk about the role of capitalism in oppressing gays and lesbians. </P>
<P>Certainly these are not incorrect points to make. But refusing to organize in support of gay marriage because the issue is "too conservative" is plain childishness. </P>
<P>Thousands of people are becoming energized to fight back against bigotry by the controversy around same-sex marriage--<I>clearly, this is a good fight.</I> This is <I>our</I> fight!</P>
<P>The choice is simple. Either we explain our politics while we work side by side with other activists to win gay marriage, or we can sit on the sidelines complaining like sectarians about conservatism as we let the Democratic Party co-opt the movement and <I>really</I> pull it rightward.<BR>
<B>John Green, </B>Davis, Calif.</P>

