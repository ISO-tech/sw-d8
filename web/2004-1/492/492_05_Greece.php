<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GREECE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Meaning of the elections</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 5</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>CHRISTOS PETRAKOS is on the editorial board of <I>Workers Left </I>newspaper in Greece, which is published by the International Workers Left (DEA), the International Socialist Organization's sister group. Here, Christos reports on the outcome of national elections in Greece earlier this month.</B></P>

<P><font face="Times New Roman, Times, serif" size="3">THE RESULTS of the elections on March 7 will create a new political situation in Greece. The winner was New Democracy, the party of the right wing, which got 45 percent of the vote. The big loser was the social democratic party PASOK, which controlled the government of Greece for the past eight years.</P>
<P>The causes of PASOK's defeat are clear. By following a neoliberal agenda, the governments of Prime Minister Costas Simitis created conditions of social desperation for large sections of the working class in Greece. A major part of PASOK's voting base--more than 500,000 voters according to researchers--voted against it, searching for a way to "punish" its policies.</P>
<P>In Greece, there is a strong left wing beyond the moderate politics of PASOK, with its origins in the Communist Party. This left didn't manage to offer an alternative to all of the people disillusioned with PASOK.</P>
<P>The left got roughly the same percentage of votes as in the last national election four years ago, which isn't a defeat, but is certainly not a victory. The Communist Party won 5.9 percent of the vote, up slightly from its 5.5 percent in 2000.</P>
<P>The other major left-wing reformist party is Synaspismos, which won 3.25 percent of the vote. In the lead-up to the election, Synaspismos threw out its right wing and ran in an alliance with radical left-wing organizations, including DEA. </P>
<P>The loss of its right wing cost Synaspismos about 40 percent of its previous electoral influence, but the coalition with the radical left more than made up for this. Polls show that 50 percent of voters for the coalition had never voted for Synaspismos before. </P>
<P>DEA's experience in the electoral campaign involved hundreds of meetings in neighborhoods and workplaces. We found ourselves in the center of a political process that clearly marked a new radical turn for the Greek left. Several other groups--including the Socialist Workers Party of Greece, which ran in the elections under the banner of the Anticapitalist Alliance, but with no other left-wing organization participating--won very small showings.</P>
<P>DEA is very satisfied with our showing in the elections. Our candidates proved to be the most advanced wing of the Synaspismos coalition, supporting a policy of fighting resistance, generalized questioning of the system and the demand for socialism. DEA won thousands of votes as part of the coalition, becoming recognized as a small but visible force that supports revolutionary politics.</P>


