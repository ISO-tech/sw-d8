<P><font face="Times New Roman, Times, serif" size="4">U.S. ally says: "We've been taken for a ride"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's war lies exposed again</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"BETTER OFF" and "more secure." That's what George W. Bush said his war on Iraq has done for the world over the past year. Congress got into the act, too, with the House of Representatives passing a resolution declaring that "the United States and the world have been made safer with the removal of Saddam Hussein and his regime from power in Iraq."</P>
<P>But the U.S. government's "coalition of the willing" doesn't seem so convinced by Washington's proclamations. "That they deceived us about the weapons of mass destruction, that's true," Polish President Aleksander Kwasniewski, considered one of the Bush administration's staunchest allies on Iraq, told reporters last week. "We were taken for a ride."</P>
<P>Italy's European Affairs Minister Rocco Buttiglione broke ranks with his own pro-war government to proclaim that "Arab democracy will not be born through the force of arms or because we have defeated Saddam." And Spain's new Prime Minister Jos&eacute; Luis Rodr&iacute;guez Zapatero is threatening to pull his country's 1,300 troops out of Iraq unless the United Nations (UN) takes over the occupation.</P>
<P>The Bush administration may get the UN it once dismissed as "irrelevant" to provide a fig leaf for the occupation. But even Washington insiders are starting to break ranks.</P>
<P>Last week, former White House terrorism adviser Richard Clarke confirmed that the Bush gang was dead set on war with Iraq from the moment the September 11 attacks took place. According to Clarke, less than 24 hours after the attacks, Donald Rumsfeld declared that "There were no decent targets for bombing in Afghanistan, and that we should consider bombing Iraq" instead, because it had "better targets."</P>
<P>So they say that the world is "safer?" Maybe they should ask Iraqis. "There is no stability, there is no security, there is no clear future, along with a feeling of humiliation," Alla Saad, an Agriculture Ministry engineer in Baghdad, told Knight Ridder last week. Or the people of Afghanistan--where, as <I>Socialist Worker</I> went to press, tank battles between rival warlords in the western city of Herat had killed more than 100 people following the assassination of the country's aviation minister.</P>
<P>Try telling the people of Spain that they're "safer"--after the country suffered the worst terrorist attack in its history earlier this month. Or the families of the more than 550 U.S. soldiers who died in Iraq--for nothing other than a war for oil and empire.</P>
<P>In its arrogance, the Bush administration ignores the suffering and horror it has caused. But we can send a different message. Like last weekend, when well over 1 million took to the streets around the globe--to show that the world still says no to war and occupation.</P>


