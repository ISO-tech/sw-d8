<P><font face="Times New Roman, Times, serif" size="5"><b>Pushed to a breaking point by this system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I rarely watch the local news because of its often depressing and misleading portrayals of people of color, women, gays and oppressed people generally. But I happened to be watching the news tonight, and caught a story about a Black man, Adam Perry, who had been arrested after a four-hour stand-off with NYPD officers while holding his grandmother "hostage." Perry allegedly had three guns on him.</P>
<P>While the newscasters characteristically dismissed Perry off as another depraved, ghetto loco, a closer look into his life tells a different story and reveals all that is wrong with this system. As it turns out, the grandmother whom Perry had held hostage was also the same one that, according to a friend, he had visited each day after work.  </P>
<P>Friends say that Perry had been pushed to this breaking point by recent tragedies in his life. Last August, his older brother (only 37 years old) died of a heart attack during the regional blackout--the one caused by negligent, profit-driven energy companies. </P>
<P>Perry himself had recently lost his girlfriend and his job as a mail clerk that he had held for eight years. Now, Perry will probably go to trial and have the book thrown at him like most young, working-class Blacks in this country who come before the racist criminal injustice system. </P>
<P>This latest incident is another example of why we need to fight for a different type of society, that guarantees jobs for all and places human dignity over corporate profit.<BR>
<B>Brook Griffin, </B>New York City</P>

