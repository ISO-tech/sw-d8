<font face="Times New Roman, Times, serif" size="5"><b>Punishing NYC students for the education crisis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK City Mayor Michael Bloomberg showed how little he cares about education last week when he rammed through a proposal that will result in 15,000 third graders being held back next year. The new policy will be to fail all students who don't reach a "Level 2" score on citywide reading and math tests. </P>
<P>Bloomberg's scheme is part of a national push toward high-stakes standardized testing, driven by George Bush's No Child Left Behind Act. Parents and teachers have reacted with widespread outrage. Even on the Mayor's Panel for Educational Policy, a rubber-stamp body that replaced the Board of Education two years ago, there was questioning of the proposal. </P>
<P>But that didn't stop Bloomberg, who fired three board members hours before a vote on the policy, replacing them with new appointees who were told to vote in favor of it. "Mayoral control means mayoral control, thank you very much," Bloomberg declared to reporters. "They are my representatives, and they are going to vote for things that I believe in."</P>
<P>The decision will likely quadruple the number of third-graders held back. One PTA vice president, Dee Reiber, called it "the educational equivalent to the current 'shock and awe' foreign policy in this country." </P>
<P>As Michael, a third-grade teacher, told <I>Socialist Worker</I>: "Out of four classes [of third graders], 75 percent of the students will be considered failing. This decision means that the education process has been flipped on its head.</P>
<P>"Administrators want us to come in early and set up weekend classes. I have two Kaplan books, two reading strategy books, 10 test-prep booklets, and that's all I do all day now. To expect my kids to reach the same standards as students in Westchester--with smaller classes, better-paid teachers and more resources--is ignoring all the historical barriers my students face."</P>
<P>United Federation of Teachers President Randi Weingarten pointed out that holding back third graders will help increase fourth-grade test scores--just in time for next year's mayoral election. But the teacher's union <I>supported</I> the imposition of mayoral control a few years ago, a blunder that teachers across the city are still paying for as the city imposes curriculum mandates across all levels. </P>
<P>Even the <I>New York Times </I>weighed in against Bloomberg's policy. "Dozens of studies have led many educators to conclude that policies forcing students to repeat a grade are costly and counterproductive, resulting in no gains in student achievement and sharp increases in dropout rates." Ironically, the city tried such a program in the early 1980's which was abandoned after just a few disastrous years. </P>
<P>The only things that can solve the education crisis are smaller class sizes, improved salaries so experienced teachers don't burn out or move to better-paying suburban districts, and more resources for the often dilapidated and overcrowded school system. Holding students back only punishes the most vulnerable students for the failures of the entire system.</P>

