<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What are the roots of terrorism?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITHIN A few days of the Spanish election that tossed out George Bush's biggest ally on the European continent, the one-party media in the U.S. was already establishing a "conventional wisdom" about Spanish voters' reaction to the March 11 terrorist bombings. Leading the pack was <I>New York Times </I>columnist David Brooks, who chided Spanish voters for deciding to "throw out the old government and replace it with one whose policies are more to al-Qaeda's liking. What is the Spanish word for appeasement?"</P>
<P>The self-proclaimed coiner of the phrase "axis of evil," former Bush speechwriter David Frum, appeared ready to add Madrid to the axis. "People are not always strong," Frum wrote in <I>National Review Online. </I>"Sometimes they indulge false hopes that by lying low, truckling, appeasing, they can avoid danger and strife."</P>
<P>Coming from these neoconservative boneheads, the charges of "appeasement" were unremarkable. But what was amazing about the analysis was how widespread it was across the political spectrum--including among so-called liberals.</P>
<P>Georgie Ann Geyer, a generally respected commentator on foreign affairs, chided the terrorists who "manipulated the bombings in such a way that they turned the massacre against the Spanish government instead of themselves--and when elections were held Sunday, the voters elected a new anti-American regime."</P>
<P>All of these analyses start from the wrong premise that "terrorism" is akin to a conventional military force that can be fought and defeated in a "war." This works just fine for the likes of Bush and Kerry, as the "war on terrorism" can then be used to justify any policy, from hugely increased military budgets to a decades-long occupation of Iraq.</P>
<P>But it doesn't reflect reality. However misguided terrorism is, it grows out of real grievances and political demands. People don't decide to join an outlawed movement, work in clandestine cells and even commit suicide when carrying out attacks unless their grievances--or their desperation at having them addressed through the "system"--are high.</P>
<P>Perhaps this is most obvious in Palestine, where hundreds of young men and women have joined the ranks of suicide bombers because they despair of ever seeing justice in their lifetimes. They weren't born wanting to become suicide bombers. But their lifetimes of humiliation at the hands of Israel's occupation authorities made them open to terrorism as a means to avenge their oppression at the hands of the Israeli army.</P>
<P>A recent Pew Center opinion poll showed that a majority of Jordanians and Moroccans, a plurality of Pakistanis and a sizable minority of Turks believed that suicide bombings by Palestinians against Israelis were justified. Is there something "evil" about the populations of these Islamic countries--generally considered among the most friendly with the U.S. in the Islamic world? </P>
<P>No. When they look at the Palestinians, they don't see the "evil fanatics" who shouldn't be "appeased" that the Brooks and the Geyers see. They see people living under occupation who are striking out at their occupiers in whatever way they can. Yet those who are so ready to vent outrage on suicide bombers never ask if ending the Israeli occupation might be the way to end "terrorism."</P>
<P>Understanding the roots of terrorism, however, doesn't justify terrorist actions directed against civilians who have no responsibility for the governments that the "terrorists" oppose. If it was indeed a protest against Spain's alliance with the U.S. in Iraq, the terrorist bombing of Spanish commuter trains--full of immigrant workers and students who, no doubt, opposed the war in Iraq in large numbers--was indefensible.</P>
<P>Yet the bombings were a predictable outcome of the U.S. government's adventure in Iraq. Many in the antiwar movement said as much before last year's invasion. The Spanish protesters who chanted, "Your war, our dead" got it right. And so a majority of Spanish voters took a very rational and courageous decision--to toss out a government that had lied them into a war almost none of them supported.</P>


