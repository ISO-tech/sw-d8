<P><font face="Times New Roman, Times, serif" size="5"><b>We won't settle for less than full marriage rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Offermann</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN MASSACHUSETTS, supporters of gay marriage are preparing for the next step in the fight to stop a ban on same-sex marriages. On March 11, state lawmakers approved legislation, sponsored by Senate President Robert Travaglini and House Speaker Thomas Finneran--both Democrats--that would institute same-sex civil unions but ban gay marriages.</P>
<P>A state high court has ruled twice in favor of gay marriages, and the state could begin issuing licenses as soon as May 17. The amendment needs to pass a final vote on March 29 when the Constitutional Convention reconvenes.</P>
<P>Next the ban must pass another Constitutional Convention in 2005, and then go to voters on the ballot in 2006. Republican Gov. Mitt Romney argues that, since this amendment passed, it would be "unfair" to allow marriages to take place in May, so he's vowed to block them.</P>
<P>The strategy being pursued so far by gay marriage supporters has largely depended on lobbying legislators and parliamentary maneuvers. For example, supporters voted for Travaglini's amendment during a procedural vote in order to block a nastier amendment that denies marriage <I>and </I>civil unions.</P>
<P>The Freedom to Marry Coalition calls the latest vote a "partial victory" since the worse amendment didn't pass. But now's no time for these long-shot strategies.</P>
<P>Thousands of supporters of gay rights turned out spontaneously at the statehouse on March 11 to demand gay marriage. From San Francisco to New Paltz, N.Y.--a new civil rights movement was born as politicians felt the pressure to break state laws to issue licenses and people took the streets to raise the demand. It's time for Massachusetts activists to organize to demand gay marriage now--no compromises!</P>
<I><P>For information on an organizing meeting in the Boston area, e-mail DefendEqualMarriage@yahoo.com. </P>
</I>
<P>-- In New Paltz, N.Y., activists are preparing for lunatic homophobe Fred Phelps when he comes to protest gay marriage on April 4. Activists are planning a festival to counter Phelps, but we need a counterprotest to confront him and make sure doesn't come back.</P>

<P>-- In New Haven, Conn., members of the New Haven Board of Alderman called a community meeting on gay marriage. Everyone agreed on demanding full marriage rights, not civil unions, but there were significant disagreements on what should be done and how quickly.</P>
<P>A smaller group plans to meet to organize a protest, including gay couples demanding marriage licenses at city hall. This event will serve to build for a statewide rally in May against a proposed Connecticut Defense of Marriage Act.</P>

<I><P>Leela Yellesetty and David Thurston contributed to this report.</I> </P>

