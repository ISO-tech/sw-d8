<P><font face="Times New Roman, Times, serif" size="5"><b>Just a couple of duck hunters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHEN DICK Cheney headed to Louisiana in January for a duck-hunting trip, he had another reason to smile besides the opportunity to kill small animals. Along for the ride was Supreme Court Justice Antonin Scalia. And that meant the two had an opportunity to get their stories straight.</P>
<P>Scalia and the other justices on the Supreme Court are scheduled to hear a case filed against Cheney by the Sierra Club, which says that the vice president should be forced to release documents from his secret energy task force meetings in 2001. The documents detail White House contacts with energy industry giants during the administration's discussion of its energy policy.</P>
<P>Cheney has appealed a lower court ruling ordering him to produce the documents. Responding to calls that he take himself off a case directly concerning his hunting buddy, Scalia last week refused to step aside--and issued a rambling 21-page rant to explain why.</P>
<P>Scalia claims that he never had close contact with Cheney during the trip--it turns out he accepted a lift to Louisiana on Air Force Two, Cheney's plane. Oozing arrogance, Scalia concluded: "Since I do not believe my impartiality can reasonably be questioned, I do not think it would be proper for me to recuse," he wrote in a 21-page rant.</P>
<P>But the best line has to be his claim that "political consequences are not my concern." Anyone remembering the 2000 election--and the lengths that Scalia and friends went to install George W. Bush in the White House--knows that Supreme Court is very concerned with certain political consequences.</P>

