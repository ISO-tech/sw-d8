<font face="Times New Roman, Times, serif" size="5"><b>What caused the violence in Kosovo?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEVERAL DAYS of rioting by mobs of ethnic Albanians in Kosovo has left at least 28 dead, 600 people injured and 4,000 homeless. It was the worst outbreak of violence since 1999, when the U.S.-led NATO war against Yugoslavia drove the Serbian army out of Kosovo.</P>
<P>Kosovo, a predominantly ethnic-Albanian province of Serbia-Montenegro that had been demanding independence, is now run by a United Nations (UN) administration. But UN "peacekeepers" have not stopped the violence in the province, as the current confrontations show.</P>
<P>The violence began after Serbs and Albanians accused each other of committing acts of violence--a drive-by shooting death of an 18-year-old Serb and the drowning death of two Albanian boys. Seven villages and 25 Serb Orthodox churches were torched. In the capital of Pristina, troops rushed to secure UN headquarters--and left a crowd to move on and destroy the only remaining Serbian church in the city.</P>
<P>Now, NATO is rushing in 2,000 more troops--on top of the 18,000 who are already there. During Bill Clinton's NATO war over Kosovo, U.S.-led forces flew thousands of bombing missions that killed thousands of Serbs and ethnic Albanians and devastated Serbia's infrastructure.</P>
<P>The war was fought ostensibly to stop the Serbian army's ethnic cleansing in Kosovo--but it created an even greater refugee crisis. And at the end of the war, under NATO's watch, the Kosovo Liberation Army drove two-thirds of the Serb minority--200,000 people--out of Kosovo.</P>
<P>In reality, U.S.-led forces used the nationalist aspirations of Kosovar Albanians as a pretext to demonstrate NATO's "credibility." The Western governments behind the war had no intention of supporting Kosovo's independence, fearing that it would destabilize other countries in the region that have ethnic Albanian minorities. The violence today, said one Western diplomat, "should remind us that we froze an insurgency in 1999, but we did nothing to solve the political reasons underlying it."</P>
<P>Serbia's main far-right nationalist party is demanding that Kosovo remain an autonomous part of Serbia-Montenegro, whereas ethnic Albanians, who are 90 percent of the population, continue to demand independence. Serbia's prime minister favors the partition of Kosovo.</P>
<P>UN occupiers have fuelled ethnic tensions by offering political posts on the basis of balancing ethnic groups. In addition, the violence is fuelled by despair. Unemployment in Kosovo is at 50 percent, and even higher for young people.</P>
<P>The latest round of violence is forcing Kosovo's occupiers to come up with new proposals. But any "solution" imposed at gunpoint by an occupying power will not work. Only the poor and oppressed of the region--over the heads of their own nationalist rulers and the great powers that manipulate them--can solve the crisis.</P>


