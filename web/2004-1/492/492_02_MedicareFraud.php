<P><font face="Times New Roman, Times, serif" size="4">Scandal over cost of Medicare "reform"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The fraud never stops</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 26, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE BUSH'S Medicare plan was a bigger fraud than anyone knew. Congress' "compromise" plan passed in November to cover <I>some </I>prescription drug costs for <I>some </I>seniors at a huge price tag for taxpayers--and huge profits for the pharmaceutical industry--always smelled fishy.</P>
<P>But it turns out that the Bush administration deliberately lied about the cost of the new program. Richard Foster, chief actuary at the Centers for Medicare and Medicaid Services, told Knight Ridder that his office concluded in June that the Senate version of Bush's Medicare proposal might cost as much as $551 billion over 10 years--well above the $395 billion figure the Bush White House claimed. </P>
<P>Foster says that he was ordered by his boss, former Medicare Administrator Thomas Scully, to withhold the higher numbers from Congress.  The threats "came in different forms," Foster told Knight Ridder. "Sometimes [Scully] would make a comment that 'I think I need another chief actuary,' or 'If you want to work for the Ways and Means Committee [which was drafting the bill], I can arrange it.' It was that sort of thing." </P>
<P>In June, Foster says, Scully even put a warning in writing that "was a direct order not to respond to certain requests, and instead to provide the responses to him." Based on the lower estimated figures, House members approved the legislation in November by a 220-215 vote. But 13 of the "yes" votes came from Republicans who had said that they'd vote against the bill if it cost more than $400 billion.</P>
<P>Even after this revelation, the fraud just kept on coming. Apparently, the administration spent millions of taxpayer dollars on phony television news reports to advertise the Medicare "reform" law. The Bush administration paid actors to pretend to be news reporters--and "interview" Health and Human Services (HHS) Secretary Tommy Thompson about the new Medicare program.</P>
<P>The ads were produced by the Thompson's department, which called them "video news releases" and sent them out to TV stations. News anchors were even provided with a script to lead into the fake segments. The phony reports actually made it on the air at dozens of local news stations.</P>
<P>But the Bush team says it did nothing wrong. "The use of video news releases is a common, routine practice in government and the private sector," Kevin W. Keane, a spokesperson for the Department of Health and Human Services, lectured the <I>New York Times</I>. "Anyone who has questions about this practice needs to do some research on modern public information tools."</P>


