<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Beginning to look like Bush Lite</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 3</font></P>

<I><P><font face="Times New Roman, Times, serif" size="3">"The war on terror is not a clash of civilizations. It is a clash of civilization against chaos; of the best hopes of humanity against dogmatic fears of progress and the future."</I></P>

<I><P>"I also believe that those who doubted whether Iraq or the world would be better off without Saddam Hussein, and those who believe we are not safer with his capture don't have the judgment to be president--or the credibility to be elected president."</I></P>

<I><P>"I will not wait for a green light from abroad when our safety is at stake."</I></P>

<P>GUESS WHO? George Bush? Dick Cheney? Some ghoul from the dark recesses of the Bush administration? Nope. It's the Democratic Party's "lesser evil"--John Kerry. With its mantra about picking the most "electable" Democrat against George Bush, the party establishment has anointed a candidate that Washington's hawks and corporate masters have little to quibble with.</P>
<P>Supporter of the Iraq occupation, opponent of gay marriage, bathed in money from corporate backers--Kerry has his differences with Bush, but they are much smaller than anyone might have guessed when the jockeying for the Democratic presidential nomination got started a year ago. But despite all this, the pressure is on for those who oppose war and stand for social justice to vote for a Democrat they disagree with on any number of issues--because "anybody is better than Bush."</P>
<P>Sadly, the renowned opponent of U.S. imperialism Noam Chomsky has joined the chorus. "Kerry is sometimes described as 'Bush Lite,' which is not inaccurate," Chomsky said in an interview with the British <I>Guardian</I> online. "But despite the limited differences, both domestically and internationally, there are differences. In a system of immense power, small differences can translate into large outcomes."</P>
<P>Indeed, the outcome could be different. Kerry has said that he wants <I>more </I>U.S. forces--40,000 troops, as a matter of fact--dispatched around the world to fight the "war on terror." Not everybody is buying into Kerry, however--as the surprising support for Ralph Nader's independent campaign for president shows.</P>
<P>Several recent national polls put support for Nader at between 6 and 8 percent--a better showing than at almost any point in the 2000 campaign, when Nader won nearly 3 million votes running as a Green. When Nader announced last month that he would run for president again, he faced a deluge of abuse from the "Anybody But Bush" crowd.</P>
<P>Yet most of Nader's surge comes from people who say they would back Kerry in a two-way race with Bush. In other words, because Nader's campaign symbolizes an alternative to the spinelessness of the Democrats, he has the backing of a significant minority of people who want something better than Kerry's Bush Lite policies.</P>
<P>Nader could see this evaporate if his overtures to conservative forces such as Ross Perot's Reform Party become what his campaign symbolizes. But his showing in the polls now indicates that large numbers of people believe that they deserve better than a choice between two barely distinguishable evils.</P>


