<P><font face="Times New Roman, Times, serif" size="4">Don't be afraid to speak out against all oppression</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We <I>should </I>rock the boat</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I work as a tech for Verizon in the Bronx, and recently, we had a rare meeting with our union local's president and treasurer. The meeting started off in a good way, speaking of the need to be building the union and not just waiting for contract time to do it. </P>
<P>But it quickly degenerated into a speech of how Bush being re-elected to office would be the worst thing that could happen to us as workers. The answer, we were told, was to do everything we can to get John Kerry elected, and that if this didn't happen, we are basically doomed. </P>
<P>This, by itself, is not particularly shocking, given the long history of the union leadership thinking this way. But the next thing brought up was. </P>
<P>Our supposed leaders went out of their way to tell us that we shouldn't "burden" the Democrats by demanding too much of them, especially around an issue like gay marriage--because those issues didn't concern us as workers. I was really sickened by this statement. </P>
<P>I wondered how it would make me feel if I was a gay person being represented by these people, and they were basically telling me that my issues are irrelevant. Unfortunately, it gets worse. </P>
<P>The only reason I was at this meeting was that my foreman told me about it that morning. So much for building "unity!" When we returned to our workplace, I made a bit of a snide comment to my foreman that maybe I should pay him my dues, because at least he told me about the meeting. </P>
<P>A little argument ensued between myself and a shop steward, and I was basically told that it was my fault that I didn't know about the meeting, because it was posted--in one place in a garage of over 100 people. After we were dispatched, several of my coworkers, who also didn't know about the meeting, came up to me and told me that I was right. </P>
<P>I was able to turn this into a longer discussion of the problem of lesser evilism, and about what had been said about gay rights. I was also able to sell several copies of <I>Socialist Worker</I>. We have to stand up against oppression in any form, regardless of who is spewing it out. We can't be afraid to argue around issues confronting us in our workplaces, and we have to stand up to the undemocratic way that unions like to do business.<BR>
<B>Paul Grohman, </B>Communication Workers of America Local 1101, New York City</P>

