<P><font face="Times New Roman, Times, serif" size="4"><b>Justice for the Hamouis</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Three members of a local Syrian family, the Hamouis, will have a court hearing March 29 before the Ninth Circuit U.S. Court of Appeals. At issue is whether the U.S. government is going to deport Safouh Hamoui and Hanan Ismail and their 20-year-old daughter, Nadin.</P>
<P>Their struggle began over two years ago, on the first day of the Muslim holiday Eid, at 6 a.m. When Safouh opened the door, immigration and FBI agents began scouring the house, guns drawn.</P>
<P>Hanan, a mother of four kids, was awakened with a flashlight and a gun pointed in her face. Authorities refused to allow her to wear her traditional hijab on the way to jail. She was handcuffed to a chair for four hours because she refused to wear prison garb without her other clothes.</P>
<P>Hanan also was separated from her three other children, Rham, 15, and Mohamed, 13, and Sam, 24. While in custody, Hanan had to go to the emergency room 11 times--and was taken to the hospital in shackles at least five times. Safouh was released one month later, but Hanan and Nadin were detained for nine months before they were released. </P>
<P>Community groups like the Arab American Community Coalition (AACC) and others organized a campaign to free them, holding weekly pickets outside the INS jail for months. The Hamouis were arrested under Attorney General John Ashcroft's "Absconder Apprehension Initiative." Yet the Hamouis have lived in Lynwood, Wash., for 10 years and run the Seattle Mediterranean Market in the town of Edmonds.</P>
<P>The Court hearing on March 29 will be the first time for the Hamouis to present to the courts evidence of the negligence of their previous legal representation and staements of four volunteer academic experts that state the family will be tortured, if not killed, in Syria.</P>
<P>The AACC is calling on all who support the Hamouis to pack the court room. A decision is expected within 24 hours of the hearing. We can't allow the Hamouis to be deported.  All out on March 29!</P>
<I><P>The hearing will take place at 1:30 p.m. in the U.S. Court of Appeals, 9th Circuit in downtown Seattle in the Park Place Building at 1200 6th Ave., 21st floor. The court requires a photo ID for admission. Send donations to the Hamoui Legal Defense Fund, c/o AACC, PO Box 31642, Seattle, WA 98103. Call 206-601-1141 with any questions.</I> </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Defend the Oakland 25</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Doug Cavers</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif. &#151; About 150 protesters rallied at the courthouse here March 19 to protest the continued prosecution of the Oakland 25, protesters who were arrested during a peaceful anti-war picket at the Oakland docks on April 7, 2003.</P>
<P>Police fired rubber and wooden bullets and concussion grenades, injuring several protesters and nine International Longshore and Warehouse Union (ILWU) dockworkers as they were trying to go to work. Those 25 protesters now face criminal charges for their activities.</P>
<P>  The rally was called by the ILWU as part of a weekend of protest against the U.S. occupation of Iraq. An ILWU  speaker said the docks in Iraq where there was a protest last year is now being, "attacked by the occupation army bringing bloodshed and oppression on the working class in Iraq...tomorrow we will shut  the Port of Oakland and the ILWU will lead the march" in San Francisco against the U.S. occupation.</P>
<P>ILWU Business Agent Jack Heyman said, "We will continue to fight until the charges are dropped. The struggle to build the anti-war movement goes hand in hand with the fight for people's rights to protest." Other speakers called for the police involved in the shootings to be held accountable and charged.</P>
<P>Representatives of the Service Employees International Union Local 4 and the Amalgamated Transit Union joined the rally in solidarity. Lindsey Parkinson, who was fired upon by the police, said, "They can't shut us up!" Henry Rosario said, they talk about terrorism from al-Qaeda, we are "against these atrocious acts of terrorism" by U.S. authorities. "We want freedom of speech and freedom of assembly."</P>
<P>Joel Sanders from the Free Mumia Abu Jamal Campaign said, "we need a political party to fight for our interests." The next event in  the Oakland 25 campaign will be a rally at the Oakland Police Department April 7 at 4 p.m. to mark the anniversary of the shootings, followed by a  5 p.m. march from the West Oakland BART station to the docks.</P>

