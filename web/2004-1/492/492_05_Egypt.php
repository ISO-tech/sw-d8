<P><font face="Arial, Helvetica, sans-serif" size="2"><b>EGYPT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Activists cleared of trumped-up charges</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 26, 2004 | Page 5</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EGYPTIAN ANTIWAR activist and political dissident Ashraf Ibrahim was acquitted of all charges by an emergency court after spending close to a year in prison. Under Egypt's repressive laws, people can be detained indefinitely for nothing more than disagreeing with the government. One of the charges against Ibrahim, for example, was sending "false information" about Egypt's human rights violations to "foreign organizations," such as Human Rights Watch.</P>
<P>Ibrahim was one of five antiwar activists who were arrested a year ago in mid-April. When the five were finally charged in September, they discovered that they were accused of forming an illegal socialist organization and possessing "printed materials related to the Revolutionary Socialists intended for distribution"--not to mention "damaging the prestige and status of the state."</P>
<P>Ibrahim was the only one of the five who was still behind bars, but all were acquitted in the emergency court decision earlier this month. In reality, the only "crime" committed by the five activists was to participate in antiwar protests as the invasion of Iraq began--as did millions of people around the world--and to monitor police brutality against the protesters.</P>
<P>Egypt is one of the U.S. government's main allies in the Middle East, receiving more aid from Washington than any country but Israel. The government is used to ruling with an iron fist. Its security forces were taken aback by the large protests against the U.S. invasion of Iraq and responded with a wave of arrests in the hope of squelching further dissent--aimed at the Egyptian government itself, and its policies of neoliberalism and support for Washington.</P>
<P>Antiwar activists and opponents of repression around the world rallied to the cause of the five. The acquittal of Ibrahim and the other activists is a victory for the movement in Egypt and abroad.</P>


