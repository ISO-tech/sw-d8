<P><font face="Times New Roman, Times, serif" size="4">Workers strike over health care in Twin Cities</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bus drivers draw the line</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 12, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MINNEAPOLIS--Transit workers in the Twin Cities have drawn a line against an attack on their health care benefits. Some 2,200 drivers, mechanics, bus cleaners and clerical workers for Metro Transit went on strike last week after management refused to retreat from demands for concessions on health care.</P>
<P>The members of Amalgamated Transit Union (ATU) Local 1005 have been without a contract since last July.  According to press reports, after months of negotiations the union was ready to agree to a 1 percent raise in the second year of a two-year deal--in other words, a wage cut, once inflation is taken into account.</P>
<P>ATU officials offered to submit disputed issues to arbitration--or to extend the current contract until July, effectively an interim one-year deal. But Metro Transit wanted to go for broke--and continued to insist on a restructuring of health care coverage for retirees and workers.</P>
<P>Other issues are simmering below the surface. After the end of a bitter 1995 strike, management won increased flexibility on work rules. The result, says labor historian Peter Rachleff, who teaches at Macalester College in St. Paul, is that more drivers work split shifts and spend longer years on the seniority roster to make top pay or get a better shift.</P>
<P>"That's eaten away at the drivers," Rachleff said. "Their jobs have gotten a lot harder." Also, Metro Transit has outsourced bus service in suburban areas--so that private contractors run the same buses along the same routes, but with a nonunion operation. </P>
<P>Nevertheless, the union's main fight is over health care--both for current workers and future retiree benefits. Under management's proposal, as of this year, new hires would lose retiree health coverage. For current employees, Metro Transit would cap its contributions for retiree health care at a set dollar amount, rather than the current two-thirds fraction--another cost-cutting move.</P>
<P>Current coverage would suffer as well. Management wants union members to contribute more toward health care premiums and start paying co-pays for doctor visits. That's $500 or more out of the pockets of workers in just the first year of the contract.</P>
<P>Peter Bell, the head of the agency that runs Metro Transit, claims that because of rising health care costs, he has no choice but to demand more from workers. But Bell's attack on transit workers is a piece of a premeditated right-wing offensive by his boss--Republican Gov. Tim Pawlenty.</P>
<P>The state government has cut transit funding by 18 percent over the past several years. Pawlenty "plucked a lot of these right-wing ideologues out of the private sector and put them in charge of shrinking the government," says Rachleff. Bell, for example, was a fixture at the Center of the American Experiment, a right-wing think tank based in Minnesota.</P>
<P>"I think they're trying to break the union," says Local 1005 President Ron Lloyd. "This has everything to do with the governor--it's his idea." But Pawlenty's anti-union attacks could backfire.</P>
<P>Minnesota labor is united behind the strike, Lloyd says, joining in demonstrations and walking the picket lines. More widely, "the number of folks honking horns, waving their hands, giving the clenched-fist salute, way outnumber people giving the finger," said Petter Benner, an official with AFSCME. "People standing up for their health care really resonates."</P>


