<P><font face="Times New Roman, Times, serif" size="4"><b>Gaining the confidence to take on bigotry</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>I am openly gay and work as a tech for the phone company in Queens, New York. I spend a considerable amount of time arguing with people about why antigay jokes and slurs are not funny or harmless. </P>
<P>Just as the gay marriage controversy was really hitting the headlines, I found some graffiti in a break room that read "AM=HOMO" (that would be me). It took me a long time to tell anyone about it, partially because the atmosphere at work is that no one complains about shit like this. But behind that is the feeling that no one would really have my back on it anyway. </P>
<P>But the flip side was that the more gay marriage was news, the more people started saying how much they hate Bush. One morning in a coffee shop, a couple of technicians were looking at the headlines about San Francisco, and even before I could say anything, one guy launched into a tirade about how you can't say there are equal rights for gays when you can't marry, and hasn't Bush heard about separation of church and state? </P>
<P>This was repeated at least three other times in the week that followed, and it was easy to connect this so-called religious issue with the fact that civil unions only offer--at best--a third of the rights that marriage offers. In fact, in our union contract, members with domestic partners can't get the top tier medical, so I got kicked out and put in an HMO because I refused to drop my partner. When people hear that, it's easy to see this is part of how bosses and politicians try to rip all of us off.</P>
<P>This has really boosted my confidence. I went back to the break room, and a coworker happened to be there when I crossed out the graffiti. He was totally shocked someone would go after me like that. We had a great conversation about why gays are targeted.</P>
<P>I think we will win equality for gays by showing that gay oppression is real, and that it lets the people who run our society get away with denying our rights--housing, health care, Social Security, etc.--along with making our lives painful and scary. We have an opportunity to build real solidarity around ending gay oppression. We should use it.<BR>
<B>Amy Muldoon, </B>CWA Local 1106, New York City</P>

