<p><a name="Homeless"></a><font face="Times New Roman, Times, serif" size="4"><b>Treating poverty as if it was a crime</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Madison, Wisconsin's homeless policy treats the poor like criminals. Recently, two homeless men froze to death in sub-zero temperatures because they smelled of alcohol, and thus were denied shelter. They were not disruptive or violent, but were simply seeking refuge from the harsh Wisconsin winter. </P>
<P>Last month, another man died under questionable circumstances, though most shelter residents report that he had made it known to staff that he was diabetic and had requested food, but was denied because "breakfast time" had just ended. He took insulin and died shortly after. </P>
<P>Not much fuss followed these tragedies, as the poor and homeless are swept under the rug. By making the poor seem less than human, the notion that they are to blame for poverty is perpetuated, which steers ideas away from structural implications and solutions. This is convenient for the ruling class, but tragic for the millions who live with the shame and are forced to struggle for basic survival. </P>
<P>In response to this injustice, we have begun the Madison Warming Center Campaign to bring attention to the city's inhumane treatment of the poor. Many of the homeless here have been looking for a way to express their indignation and have been active in the campaign. <BR>
<B>Ryan Spangler, </B>Madison, Wis.</P>
<I><P>To learn more about the campaign, visit www.madwarmingcenter.org.</I> </P>

