<p><a name="AntiChoice"></a><font face="Times New Roman, Times, serif" size="4"><b>Don't be silent when the right attacks</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Chris Danze wants to take his one-man anti-abortion show on the road after his recent success at stopping the construction of a $6.2 billion Planned Parenthood clinic in Austin, Texas. Danze, president of an Austin-based concrete contractor Maldonado and Danze, began his bullying campaign even before the September groundbreaking of the clinic. </P>
<P>He created the bizarre Austin Area Pro-Life Concrete Contractors and Suppliers Association, an informal group of every concrete supplier within a 60-mile radius of the Austin area. In a letter and phone blitz, Danze harassed the contractor and potential subcontractors.</P>
<P>He threatened to document them by video camera and send lists of participants to businesses and churches, which promised to deny them business--and concrete. By November, Browning Construction bailed out of its contract for lack of sub-contractors and suppliers.</P>
<P>At Whole Women's Health Center in Austin, Danze and his followers yell at and harass entering clients every Saturday morning in the hopes of intimidating the owner of the property to break the clinic's lease. A group of Austin activists had planned a recent counter-protest, but the clinic asked that the protest be called off in order not to "disturb the dignity" of their clients.</P>
<P>Construction resumed recently on the Planned Parenthood project, with the group acting as its own general contractor and promising that "the names of individuals and firms working on the project will remain confidential to protect their safety." Such behavior will not secure reproductive services for poor women, who need Planned Parenthood's services. </P>
<P>It's time for people who believe in the rights of all women to reproductive health to organize and fight back. Let's ratchet up the decibels on Chris Danze and his zealots!<BR>
<B>Cindy Beringer, </B>Austin, Texas</P>

