<p><a name="UFCW"></a><font face="Times New Roman, Times, serif" size="4"><b>UFCW workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Southern California grocery workers returned to their jobs after their 18-week strike to find that the company was still out to bust their union. Vons moved workers with more than 10 years seniority to other stores, as far as they could without having to pay for relocation fees--25 miles. </P>
<P>In densely populated Southern California, 25 miles can mean more than an hour driving in heavy traffic. This vindictive maneuver was intended to frustrate senior employees to the point of quitting. Union members are furious, and at some stores they are demanding the return of transferred workers.</P>
<P>For employees with less than 10 years, the condition of their returning to work was accepting demotion. So a checker that had been making $17.90 would have to accept a general merchandise position that pays only $12.45 an hour--or be fired. </P>
<P>What's worst of all is that the union accepted these terms in negotiations. The workers that sacrificed so much for the last five months deserve more from their union. Now is the time to begin building rank-and-file opposition within the United Food and Commercial Workers.</P>

