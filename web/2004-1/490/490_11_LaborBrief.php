<P><font face="Times New Roman, Times, serif" size="4"><b>University of Texas shuttle bus drivers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Dana Cloud</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--University of Texas shuttle bus drivers and mechanics in Amalgamated Transit Union (ATU) Local 1549 voted March 4--by a margin of 80 to 0--to reject a contract proposal from their employer, ATC/Vancom. The workers have been operating under the terms of a contract that expired more than two-and-a-half years ago.</P>
<P>Many believe that their recent publicity campaign to pressure ATC, the university and the Austin transit authority has been instrumental in producing the proposal now. But the contract is no different from previous offers.</P>
<P>"This contract is an insult," said Daniel Urea, a driver with eight years seniority. "It provides a 33 cent an hour increase each year and no retroactive increases. And new drivers get nothing. It's a joke." The proposed contract would also allow ATC to change health insurance providers and costs at will. "The offer is damaging to your welfare," reported the <I>High Beam,</I> the union's newsletter.</P>
<P>The contract also states that workers must work 750 hours or more every six months to qualify for full medical and dental benefits--or pay 25 percent of the premium. Since many drivers can't afford this cost, "this will knock a lot of people out of getting health insurance," said Norm Couture, ATU Local 1549 president.</P>
<P>Workers and campus supporters will hold a teach-in on campus in March to build further solidarity with students along with events aimed at reaching other drivers and mechanics. The main task right now is to get more drivers and mechanics involved in the struggle in preparation for future action. </P>
<P>"We are the driving force," commented one driver at the picket. "We need to show ATC that we deserve more. We can get what we want if we keep fighting." The union can't count on winning their demands at the negotiating table. The time is now to make serious preparations for a strike.</P>

