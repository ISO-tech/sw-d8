<p><a name="Powells"></a><font face="Times New Roman, Times, serif" size="4"><b>Powell's Book Store</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Ken Morgan, ILWU Local 6</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--Workers reached a tentative agreement with Powell's Book Store, one of the largest independent bookstores in the U.S., on March 2. Nearly 500 members of International Longshore and Warehouse Union (ILWU) Local 5 who work as cashiers, stockers, computer programmers, truck drivers and book buyers are covered by the deal.</P>
<P>Management at Powell's had attempted to roll back health care coverage by requiring workers to pay higher premiums and steep co-pays. Local 5 has been working without an agreement since October. </P>
<P>By mobilizing member's support, help from the international and other unions, the workers at Powells' were able to win a better agreement than management had originally proposed, including the preservation of 100 percent, employer-paid health care, a 2 percent annual wage increase during the three-and-a-half year contract and a $100 signing bonus. There were also changes that will help strengthen work rules and working conditions.</P>


