<p><a name="UW"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Wisconsin teaching assistants</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Nichter, TAA member</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--At an unusually packed meeting of the University of Wisconsin-Madison Teaching Assistants Association (TAA), members voted by a wide margin to begin preparations for a possible strike in April. The TAA is escalating its tactics in the face of stalled negotiations over the terms of teaching assistants' (TAs) and research assistants' (RAs) health care packages.</P>
<P>Currently, TAs and RAs have a health care option that carries no monthly premium. State negotiators insist that the TAA must accept higher premiums due to Wisconsin's budgetary crisis--and because leaders of virtually all other state employee unions have already agreed to higher premiums. But TAA members--most of whom earn barely $10,000 a year--recognize that concessions today, however small, will only lead to spiraling health care costs in the future.</P>
<P>Heated arguments have begun within the union over the ramifications of a strike. While the likelihood of arrests and firings is low, many members are concerned about alienating undergraduates and faculty. </P>
<P>But with the state bent on wringing concessions from the union, striking remains the most powerful way for us to make our voices heard. It was a 1969 strike that first won union recognition for the TAA--and the threat of a strike that got us our tuition waiver in 1998.</P>
<P>On March 25, members will make the final decision on whether or not to hold an official strike ballot. In the meantime, the union is planning a series of public actions to mobilize the membership and build student solidarity.</P>
<P>Campus activist groups, including the International Socialist Organization and the Student Labor Action Coalition, are brainstorming ways to demonstrate the widespread support that exists among undergraduates for the TAA.</P>

