<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The crime of Israel's apartheid wall</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 12, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ISRAEL carried out its deadliest attack on Palestinians in Gaza in 17 months last weekend with a raid on a refugee camp. At least 14 Palestinians were killed and more than 100 injured as Israeli forces hammered the camp from helicopter gunships and tanks.</P>
<P>As usual, the Israeli government justified the violence by claiming that the refugee camp was a "breeding ground" for terrorists. Yet the assault came in the wake of an inquiry into a new form of Israel's terrorism against Palestinians--the construction of the apartheid wall through the Occupied Territories.</P>
<P>Oral testimony on the legality of the wall ended last month at the International Court of Justice (ICJ) in The Hague. Israel claims that its "security barrier" is designed to keep out Palestinian suicide bombers. But Israel isn't building the wall along the "Green Line" that divides Israel from the Occupied Territories.</P>
<P>The wall cuts through Palestinian farmland and towns, cutting off 15 percent of the West Bank lands that Israel has occupied illegally since 1967. Almost 300,000 Palestinians will be trapped in a no-man's land between the wall and the "Green Line" that divides Israel from the West Bank. Israel's "security barrier" is really another ploy in a decades-long war to steal more Palestinian land and reduce the Palestinian population to abject poverty.</P>
<P>Conjuring up images of the Berlin Wall, Israel's barrier is such a blatant affront to human rights that most countries in the world condemn it. Even George W. Bush managed to criticize the wall last year. But actions speak louder than words.</P>
<P>Israel started construction on the wall in 2002, but it has continued to enjoy an uninterrupted flow of aid, investment and diplomats from countries around the world. The U.S. government's announcement last year that it would withhold $4 million in aid to protest the route of the wall is a joke--since the U.S. gives Israel about 1,000 times as much money in aid each year!</P>
<P>During the hearings in The Hague, the so-called champions of human rights in the U.S., Britain and the European Union issued statements criticizing Israel's wall--but claimed that the ICJ "wasn't the proper place" to challenge it. In his testimony, the Palestine Authority's United Nations (UN) representative Nasser Al-Kidwa asked the ICJ to rule against Israel--and compared the case to the court's ruling against the South African occupation of Namibia in 1971.</P>
<P>An ICJ ruling against Israel today would be an unmistakable message that the apartheid wall is no more legitimate that apartheid was in South Africa. But no one who cares about justice for Palestinians should get their hopes up. There are already a mountain of UN resolutions condemning Israeli atrocities against Palestinians.</P>
<P>As long as the "international community" doesn't want to challenge Israel's main patron--the U.S. government--Israel will thumb its nose at the ICJ. That's why we have to build a movement in solidarity with the Palestinian people that can put pressure on Washington to end its support for apartheid in Israel.</P>


