<P><font face="Times New Roman, Times, serif" size="4">A UFCW worker on the outcome of the strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Lessons of our fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 12, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
On February 28, after almost five months of striking, United Food and Commercial Workers (UFCW) members were able to vote on the last proposal by the company for our contract. We received the tentative agreement on the same day as our vote was to be cast.</P>
<P>I among so many of the rank and file of our local feel like we have been kicked in the teeth by the latest and final contract offer. We fought out on the picket line for our rights as laborers and were put down, ridiculed, with a great loss of money, property and family, and we truly feel we have been betrayed. </P>
<P>Even our union president was quoted in the <I>Los Angeles Times,</I> saying he "was not prepared for this strike." I don't think too many were ready, and we are paying the price. </P>
<P>Until you go through something like this, you can never know how it feels, but I know for myself that our struggle is not over. There is still much to do for preparing for our next contract. </P>
<P>We need to have more regular union meetings between the union and the rank and file. Right now, we only meet every third month! Also, we need to start a network of employees by way of the Internet. And our union officers shouldn't spend union dues on political campaigns or exorbitant salaries.</P>
<P>Things need to change, and only we can do it. History has been made here, and I would like to not have to repeat this. I can only hope that as people begin to see what has happened with this strike and the effect it will have on others in the workforce, they fully understand our struggle--their coming struggle--and prepare to make difficult choices for change. </P>
<P>The CEOs seem to feel this collective sense of superiority. That has to change. Only we can change this. We need to start now and make the difference for working America.<BR>
<B>Kathleen Doyle, </B>UFCW Local 770, Los Angeles</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Thank you for supporting us</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">To all of our ISO brothers and sisters,</B> <BR>
We can't thank you enough for all of the monetary support, as well as you guys coming out on the site to help with our cause. This has been a long and sometimes lonely struggle, but your contributions made it somehow more bearable. </P>
<P>All of us at Ralphs Silverlake and Vons Echo Park will be forever grateful. This particular struggle is finished with the ratification of the contract, but our fight is not over. Plans are under way to get some changes in the UFCW, so the struggle continues. Thank you so much again.<BR>
<B>UFCW grocery workers, </B>Vons Echo Park and Ralph's Silverlake, Los Angeles</P>


