<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Solving the "crisis" in Social Security</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 12, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANYONE WHO was still under the illusion that Federal Reserve Chairman Alan Greenspan is anything other than a high-priced snake-oil peddler might have been surprised when he endorsed cutting Social Security benefits and prolonging President Bush's tax cuts for the rich. But Greenspan's opinion had the beauty of laying out in clear terms what the free-markets ideologues' real agenda has been all along.</P>
<P>In the 1980s, at Greenspan's urging, Congress pushed up Social Security payroll taxes. So taxes on working people went up, with the consolation that Social Security benefits would be waiting for them when they retired.</P>
<P>Now Greenspan says that not only should benefits be cut, but that the Bush tax cuts should be preserved. In other words, trillions from ordinary workers should finance tax cuts for the very wealthy--all in the name of "saving" Social Security.</P>
<P>Until now, the Social Security "debate" has thrived more on myth than on reality. The assumption that Social Security faces a "crisis"--that it won't be able to pay benefits, especially to the soon-to-retire "Baby Boom" generation--is the starting point for all of this discussion.</P>
<P>But this notion of a crisis is phony. If the government did nothing to affect the Social Security trust fund in the next four decades, the system will still be able to pay full benefits for every expected retiree until 2042, according to the system's trustees. By that time, most of the Baby Boom generation will be dead.</P>
<P>In other words, the Social Security system will have accomplished what its critics say it can't do--provide for the majority of the biggest generation in U.S. history. The promoters of the Social Security "crisis"--predominantly the finance industry that is salivating at the prospect of getting its hands on trillions of dollars if Social Security is privatized--use the idea that fewer workers are paying into the Social Security system per retiree than in previous decades.</P>
<P>This image makes the supposed crisis easy to illustrate. But it ignores more than it enlightens. For one thing, workers today are much more productive than workers in the past. So fewer workers today can produce more wealth then they did in the past. However, a greater percentage of the wealth they produce is finding its way into the pockets of the rich and corporations, whose 25-year-long offensive against workers has kept wages in real terms at the levels they were in the 1970s.</P>
<P>So one answer to the "crisis"--that would make Social Security solvent even after 2042--would be to increase workers' wages and to lift the cap on the Social Security taxes for the rich. Unfortunately, these kinds of solutions are so sensible that they would never be tried.</P>
<P>That's because the partisans of Social Security privatization--Democrats and Republicans alike--are only interested in using the "crisis" to push their plans for privatization. And of course, they have all sorts of data intended to show that private retirement accounts will make everyone better off than the current bare-bones Social Security system. But these "facts" are just as fraudulent as existence of weapons of mass destruction in Iraq.</P>
<P>Argentina privatized its Social Security system in 1994. This cost the Argentine treasury about 1 percent of annual gross domestic product, according to an analysis by the Center for Economic and Policy Research. In fact, the amount of revenue lost to the privatization of the Social Security system nearly equaled the mounting budget deficits whose financing created the crisis that led to the collapse of the Argentine economy in 2001.</P>
<P>"The irony of this action is that Argentina's decision to privatize Social Security in 1994 helped to touch off a financial crisis, which ultimately forced much more draconian cuts in Social Security than ever would have been contemplated in 1994," wrote the Center's Dean Baker and Mark Weisbrot. The message from Argentina should be clear. We have to save Social Security from its would-be "saviors."</P>


