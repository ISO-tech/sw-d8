<P><font face="Times New Roman, Times, serif" size="4">New movement for the civil rights of gays and lesbians</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Spreading the fight for gay marriage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 12, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BATTLE for equal marriage rights for gays and lesbians is sweeping through the streets and city halls of cities coast to coast. This new civil rights movement began with thousands of couples lining up after San Francisco Mayor Gavin Newsom announced that the city would issue same-sex marriage licenses, despite state laws prohibiting them. When George W. Bush made his outrageous announcement February 24 that he would push for a constitutional amendment banning gay marriage, it only added fuel to the fire.</P>
<P>Cities--large and small--are taking sides in the debate. In Portland, Ore., last week, within three days of the announcement that the city would issue same-sex marriage licenses, more than 1,200 couples turned out to demand them. Meanwhile, Jason West, the Green Party mayor of New Paltz, N.Y., north of New York City, was under fire from state Attorney General Eliot Spitzer, a Democrat, for performing gay marriages. West faces 19 misdemeanor criminal charges for challenging New York's state marriage laws.</P>
<P>On March 11, gay marriage supporters will turn to Boston, where a second constitutional convention is scheduled to take up the right to marry in Massachusetts. Twice last year, the state's Supreme Judicial Court ruled in favor of gay marriage, arguing that "separate is seldom, if ever, equal." </P>
<P>In response, lawmakers--Democrat <I>and </I>Republican--are trying to pass amendments to the state constitution banning same-sex marriage. The so-called compromise being presented by some liberal lawmakers--and supported by the Democrats' likely presidential nominee John Kerry--would allow civil unions for gay couples, but ban marriage. But this is legalizing discrimination by another name.</P>
<P>"The fact that civil unions were considered a radical step in 2000 and they're now considered nothing more than second class-citizenship by the gay community...shows you how dramatically the issue has changed in just a few short months," Matt Foreman, executive director of the National Gay and Lesbian Task Force, told the <I>San Francisco Chronicle.</I> "It's mushroomed in ways we never dreamed possible even two months ago."</P>
<P>But conservatives are feeling the urgency, too--and, following Bush's lead, they've gone on the attack. Politicians in some 35 states are scrambling to pass laws to strengthen "traditional" marriage. Wisconsin's State Assembly approved a constitutional amendment banning gay marriages and civil unions. The state  Senate will take up the measure next week.</P>
<P>But even Republicans are under pressure. For example, California Gov. Arnold Schwarzenegger--who originally said that San Francisco's issuing of gay marriage licenses would lead to "riots" and "dead people" in the streets--had to retreat on his opposition.</P>
<P>In Georgia, a constitutional ban on gay marriage was put on hold after Black members of the state House of Representatives--many of them church leaders who support the state's current laws banning same-sex marriage--recognized the obvious comparison between today's gay civil rights struggle and the Black civil rights movement of the 1960s. "What I see in this is hate," Rep. Georganna Sinkfield of Atlanta told the <I>New York Times</I>, "I'm a Christian, but if we put this in the Constitution, what's next? People with dark hair? You're opening the floodgates for people to promote their own prejudice."</P>


