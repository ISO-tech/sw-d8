<font face="Times New Roman, Times, serif" size="5"><b>Sexist terror on the football field</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Dave Zirin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 12, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE BUSH, with grinning wife Laura at his side, stated over two years ago that the U.S. would commit billions of dollars and thousands of troops to "liberate" the women of Afghanistan as part of the "war on terror." Maybe the Bush gang should have taken a practice run in Boulder, Colorado.</P>
<P>The last several weeks have revealed Colorado University, home of the Buffaloes, to be ground zero in a campaign of sexist terror designed to recruit high school football athletes. Buffs Football Coach Gary Barnett oversaw a program where escort services, strippers and "alcohol-fueled sex parties" were just another part of "big-time football."</P>
<P>Barnett and his staff of pimps brewed an atmosphere of sex and entitlement that, at long last, has exploded in their faces. Seven women have come forward in the last several weeks with allegations of rape and sexual assault at the behest of the football program.</P>
<P>In the eye of this storm is a young woman who tried to be nothing more than a teammate to Barnett's Buffs, Katie Hnida. Like Mia Hamm, Lisa Leslie or Sue Bird, Hnida is a child of Title IX. Thanks to the women's movement of the 1960s and 1970s, she had athletic opportunities unimaginable to her mother's generation.</P>
<P>But Hnida strived to do more than star for the women's soccer or softball teams: she wanted to be a place kicker and play football, with the boys. As an all-world kicker in high school, Hnida thought she could make history by walking onto the Colorado football team.</P>
<P>If Barnett's Buffaloes so blithely abused women in their frat houses and classrooms, you can imagine what it was like for Hnida on the practice field. First she was told to go to hell. When telling her didn't work, footballs were whizzed at her head. Her body would be grabbed, her face slapped.</P>
<P>Barnett looked the other way as every practice became informally designed to drive her off the team. The escalation continued until, according to Hnida, she was eventually raped. </P>
<P>Coach Barnett, when confronted with Hnida's rape allegations, took the time only to smear her playing ability saying she was "only a girl" and a "terrible" player. This earned him a suspension with pay. Barnett damns himself as a coach, not only because of his lack of sensitivity or contrition, but a pathetic inability to judge character, especially the character of the "terrible girl."</P>
<P>The fact that Katie Hnida got out of bed every day to endure whatever the football team had in store shows her to be an athlete with far more determination and courage than Barnett could ever hope to appreciate. Yet Barnett knows the bottom line is wins, losses and revenue, not the wellbeing of Katie Hnida.</P>
<P>Big-time universities, especially state schools like Colorado, are becoming more dependent than ever on the cash a mega-football program can provide. There's tremendous pressure to secure the "best" talent, and since the National Collegiate Athletic Association (NCAA) insists they truck in only "amateur" athletes, services cannot be secured with money so "sex"--or the warped version of sex the Barnetts of the world traffic in--is presented as payment.</P>
<P>When the Katie Hnidas of the world step into such a situation, the very presence of a three-dimensional woman throws the whole system into question, forcing the Barnetts to either change or attack. He chose to attack because that required no courage and that was where the money is. </P>
<P>As Colorado professor Ira Chernus recently wrote, "Here in Boulder, they got private millions to enlarge the football stadium and deck it out with sky boxes. Every game day, those boxes are filled with corporate executives who stay warm and drink the game away at exorbitant rents--as long as the team is winning."</P>
<P>Hnida now plays for the New Mexico Lobos. She recently said, "I have been able to play a game I love so much and also be part of a team that is like a family." Her experience has been so rewarding that she's petitioning the NCAA for a sixth season of eligibility.</P>
<P>After all she has been through, Hnida wants nothing more than more time on the field and more time as part of the team. Pity Barnett and the Buffs. They pushed away the toughest jock on campus.</P>


