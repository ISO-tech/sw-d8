<P><font face="Times New Roman, Times, serif" size="5"><b>Bush adviser ties protest to al-Qaeda</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RIGHT-WINGERS wasted no time in heaping abuse on the April 25 March for Women's Lives in Washington, D.C. Karen Hughes, one of George Bush's main advisers, even compared the 1 million-plus protesters at the rally to al-Qaeda terrorists.</P>
<P>"I think that after September 11," Hughes declared, "the American people are valuing life more, and we need policies to value the dignity and worth of every life. President Bush has worked to say, let's be reasonable, let's work to value life, let's reduce the number of abortions, let's increase adoptions. </P>
<P>"And I think those are the kinds of policies the American people can support, particularly at a time when we're facing an enemy, and, really, the fundamental issue between us and the terror network we fight is that we value every life."</P>
<P>Apparently, "every life" for Bush and Hughes doesn't include the 152 death row prisoners that Bush executed while governor of Texas--or the tens of thousands of Iraqis who have died in the latest U.S. war on Iraq.</P>
<P>Meanwhile, anti-abortion psycho Randall Terry had a different smear ready. He compared pro-choice demonstrators to Nazis. "So what if they have a big crowd?" he told Rupert Murdoch's <I>New York Post. </I>"Adolf Hitler had big crowds in the '30s."</P>


