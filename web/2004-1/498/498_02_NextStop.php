<P><font face="Times New Roman, Times, serif" size="5"><b>Next stop in the "war on terror"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEXT STOP for the war on terrorism--Latin America. That's the opinion of Gen. James Hill, head of U.S. Southern Command for the Pentagon.</P>
<P>The task of the U.S. is to bulk up regional militaries, Hill told a House Armed Services Committee last month--even if that translates into human rights abuses. Terrorists throughout Latin America "bomb, murder, kidnap, traffic drugs, transfer arms, launder money and smuggle humans," Hill claimed</P>
<P>He narrowed the threat down to two sources: "traditional" terrorists, which includes drug traffickers and crime gangs; and "emerging" terrorists, which he called "radical populists" who tap into "deep-seated frustrations of the failure of democratic reforms to deliver expected goods and services." This was a thinly veiled reference to popular left-wing figures like Venezuelan President Hugo Chavez and Bolivian peasant leader Evo Morales.</P>
<P>Hill argued that the U.S. must "broaden military-to-military contacts"--and when "legal boundaries don't make sense anymore given the current threat," Latin American countries should change the rules. Hill also proposed raising the cap on U.S. troops in Colombia from 400 to 800 and from 400 civilian contractors to 600.</P>
<P>Naturally, Hill's comments alarmed groups who track human rights violations in Latin America in the past. "The notion that we should strengthen Latin American armies misses the role they played in human rights abuses in past decades, and how Latin American governments have been trying to diminish their role ever since," Harley Shaiken, chair of the University of California-Berkeley's Center for Latin American Studies, told the <I>San Francisco Chronicle</I>. "In many countries, they were the terrorist organization, killing and disappearing thousands."</P>

