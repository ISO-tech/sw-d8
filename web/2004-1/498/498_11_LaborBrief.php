<P><font face="Times New Roman, Times, serif" size="4"><b>Caterpillar</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE FUTURE of the United Auto Workers (UAW) at Caterpillar is hanging in the balance after 8,000 workers voted to reject a proposed six-year contract with massive concessions--the latest in a series of givebacks by top union officials.</P>
<P>It's not surprising that workers voted down the deal. It introduces monthly health insurance premiums for the first time--from $21 for individuals and $66 for families. Retirees would pay $60 for individuals and $120 month for family coverage.</P>
<P>Management tried to buy votes for the deal with a $3,000 signing bonus. But base wages--which are the foundation for future raises and benefits--would only increase at the level of the cost of living. This is an insult--especially considering the fact that Cat bagged $412 million in profits in the first quarter of this year.</P>
<P>Shamefully, UAW officials refused to provide any information on the agreement before union members voted on the proposal April 25. UAW leaders didn't recommend approval of the deal, but didn't call for a rejection either.</P>
<P>That's because they're worried about angering rank-and-file workers, who are still bitter at top UAW officials for cutting off strike benefits to force a return to work in a 1995 strike. But retreating now will only make it more difficult to resist Cat's attacks in the future.</P>
<P>With employers like this, there's no avoiding the battle. It will be up to the rank and file at Cat to draw the line.</P>

