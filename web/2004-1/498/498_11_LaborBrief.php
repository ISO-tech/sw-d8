<p><a name="MayDay"></a><font face="Times New Roman, Times, serif" size="4"><b>May Day</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jorge Torres</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--About 200 people showed up to rally for immigrant rights on May Day. Among the issues addressed were living wages and health care for all, stopping the attacks on workers and a general amnesty for all immigrants. </P>
<P>At the same time a mile away, right-wing bigot James Dobson of Focus on the Family held a rally against gay marriage. Two activists at the May Day rally linked the attacks on immigrant rights to the attack on gay marriage, making it clear that the forces that oppose gay marriage are the same ones against equal rights for immigrants and workers. </P>


