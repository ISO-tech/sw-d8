<p><a name="UoC"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Chicago Hospital</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--About 30 people came to hear nurses from the University of Chicago Hospital talk about their struggle for a decent contract. The event--organized by Students Organized and United with Labor--was designed to build solidarity for a possible nurses' strike.</P>
<P>The contract for the hospital's 1,500 nurses expired last September 30, and since then the Illinois Nurses Association (INA) and the hospital have been negotiating. On April 7, nurses voted overwhelmingly to authorize a strike after rejecting the hospital's last contract offer by a similar margin--the first time in the INA's history at University of Chicago that they've taken a strike vote.</P>
<P>"The university slapped us around, sent letters home, saying they were 'disappointed' in the vote," said Linda Jennings, a nurse with 27 years at the hospital who serves on the bargaining team. "Their parental approach is insulting."</P>
<P>The main issues are health care and wages. In the last three years, health care costs for nurses have increased by a whopping 33 percent. Nurse Judy Lucker-Mierzwa explained at the forum how she must pay $9,600 a year for health care coverage for her and her husband. </P>
<P>The INA, along with other unions at the hospital, is fighting for the reinstatement of 52 workers who were fired for parking violations. So far, only a handful have had their terminations reversed. Solidarity between the INA and other hospital unions will be critical in the fight for a good contract as well as winning reinstatement for the wrongfully fired workers.</P>

