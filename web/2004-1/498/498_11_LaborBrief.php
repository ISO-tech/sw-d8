<p><a name="Columbia"></a><font face="Times New Roman, Times, serif" size="4"><b>Columbia University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Graduate student teaching assistants (TAs) and research assistants (RAs) entered the third week of a strike for union recognition at Columbia University. Columbia's 1,900 grad student employees walked off the job April 19 after repeated attempts by the university to prevent unionization. Columbia's grad students have been trying to unionize with Graduate Student Employees United/UAW Local 2110 for more than two years.</P>
<P>When AFL-CIO president John Sweeney addressed an April 28 rally of 1,000, it fired up strikers and their supporters. "We'll fight beside you as long as it takes to bring Columbia to justice," Sweeney said. City Council member Christine Quinn and Service Employees International Union (SEIU) Local 1199 executive vice president Betty Hughly also spoke. </P>
<P>Grad student activists also picketed a university-sponsored lecture on the 1968 student strike at Columbia--an event that has been co-opted as part of Columbia's year-long 250th anniversary celebration and that organizers refused to move off campus to show solidarity. When one student tried to speak about the current strike during the discussion, he was quickly cut off by leftist history professor Eric Foner, who was moderating the event.</P>
<P>With final exams approaching, at least two professors with large lecture classes have canceled their exams in solidarity with the strike. But the university has raised the prospect of hiring scab labor to grade the thousands of papers and finals that grad students normally evaluate. </P>

