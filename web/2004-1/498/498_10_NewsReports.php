<P><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Trussell</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BOSTON--Thousands of gay and lesbian couples are preparing to apply for marriage licenses here on May 17, the day the Supreme Judicial Court's (SJC) historic <I>Goodridg</I>e decision banning marriage discrimination goes into effect. While city clerk's offices are preparing around the state to begin issuing the licenses and public health centers are performing the first-ever blood screenings for engaged same-sex couples, gay marriage opponents are continuing to seek legal maneuvers to block the licenses.</P>
<P>Antigay Gov. Mitt Romney is attempting to enforce a 1913 law to prevent out-of-state couples from marrying in Massachusetts if the marriages aren't recognized in their home state--a law intended to bar interracial marriages. Some town clerks have pledged to defy Romney's order to begin asking couples for proof of residency, and this week Boston Mayor Thomas Menino did the same, setting the stage for a possible showdown with the governor.</P>
<P>Meanwhile a group of 13 state lawmakers represented by the conservative American Center for Law and Justice (ACLJ) have filed a new motion to the high court challenging its jurisdiction in the <I>Goodridge</I> case. New far right groups such as "Article 8," an organization calling for the impeachment of the SJC majority judges, have also begun organizing across the state.</P>
<P>In New York City, pro-gay clergy gathered on the steps of the Bronx courthouse April 29 to marry gay couples. The same evening, more than 150 people came out for a forum on the gay marriage debate, and Marriage Equality New York has set a march for May 23.</P>
<P>In Chicago, a similar protest is scheduled for May 17. The strength of the movements on both sides of the issue will determine whether gay marriage rights in Massachusetts will be used to strengthen the fight for gay rights nationally, or whether the bigots will succeed in maintaining marriage segregation.</P>
<I><P>David Thurston contributed to this report.</I> </P>

