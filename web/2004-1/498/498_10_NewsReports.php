<p><a name="Cali"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight California education cuts</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Newman</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN DIEGO--Chanting "&#161;Arnold, escucha, est&aacute;mos en la lucha!" more than 1,000 students, faculty and staff from seven San Diego colleges and universities took to the streets April 26 to demand affordable higher education. "Governator" Arnold Schwarzenegger has proposed a 10 percent spending cut at all state universities and community colleges--a total of more than 140 campuses with 3.5 million students. </P>
<P>He also wants to hike tuition by 10 percent at state universities and by 44 percent at community colleges--after steep hikes last year too. In the most cynical and racist proposal of all, Schwarzenegger wants to eliminate popular and effective Educational Outreach Programs that recruit disabled, poor and minority students. </P>
<P>The crowd marched to the governor's regional field office, chanting "Money for schools, not for war!" While a delegation went inside to deliver 700 protest letters, hundreds outside sat down and blocked traffic. </P>
<P>This comes on top of mid-April protests against state budget cuts for elementary and high schools. As part of a week of action, more than 75 students marched 70 miles from San Pablo to Sacramento, where they demonstrated on the capital steps. Schwarzenegger said he didn't have time to meet with the students, and his chief of staff also gave them the runaround.</P>
<I><P>Jean Howell contributed to this report.</I> </P>

