<p><a name="Evanston"></a><font face="Times New Roman, Times, serif" size="4"><b>Evanston, Ill., Board of Education</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVANSTON, Ill.--Three hundred protesters marched and rallied here against elementary school budget cuts on April 26. Last month, the District 65 Board of Education sent out termination notices to one out of 10 of its 650 teachers. The board also announced the possibility of slashing crucial services such as library, social work and special education as well as foreign language and musical instrument instruction.</P>
<P>Then the board revealed that, in addition to other cuts, it might outsource the before- and after-school child care program to the YMCA. All child care workers, members of the Association of Childcare Professionals, would be fired from their jobs--losing their union and their seniority--and then be forced to apply as new hires at the YMCA, to work at part-time wages much lower than they currently earn.</P>
<P>The protest was a show of unity between parents, teachers, staff and students who chanted, "They say cut back, we say fight back!" as they marched to the Board of Education meeting. The school board angered the crowd when it allowed only 160 people inside--excluding everyone else--at a meeting that was "open to the public."</P>
<P>The board claimed "fire codes" made this decision necessary--but protesters learned that three days earlier, the fire department had formally recommended that the board move to a larger location to accommodate the large numbers expected! "I think this is surprising them," said child care workers' union president Natalie Copper. "I don't think they realized we were as strong as we are."</P>


