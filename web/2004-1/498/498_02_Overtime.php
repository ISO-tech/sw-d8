<P><font face="Times New Roman, Times, serif" size="4">White House determined to get away with rule changes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The plot to steal overtime pay</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ADD ANOTHER lie to the Bush administration's growing pile of deceptions. And it's about another war this time, too--the war on workers. For a year, the Bush administration has used misinformation and fuzzy math to campaign for its proposed new regulations that could steal overtime pay from an estimated 8 million workers.</P>
<P>Currently, the Fair Labor Standards Act (FLSA) requires employers to pay time-and-a-half wages to most workers for every hour worked beyond a 40-hour week. On April 23, the Bush administration published new overtime rules in the <I>Federal Register</I>, signaling the beginning of a 120-day "compliance period." That means bosses can begin calculating overtime pay according to the Bush rules as early as August 20.</P>
<P>This month, the Senate is likely to vote on an amendment proposed by Sen. Tom Harkin (D-Iowa) that would block the Bush overtime rules. This, of course, will face fierce opposition from the Bush administration.</P>
<P>On April 28, Labor Secretary Elaine Chao appeared before the House Education and Workforce Committee to oppose Harkin's amendment--and condemn the "campaign of misinformation" spearheaded by unions and other opponents of the new rules. But it's the Bush administration that's lying.</P>
<P>Bush's Department of Labor claimed that its changes to the FLSA would make 1.3 million low-income workers newly eligible for overtime. According to the old rules, which haven't been updated since 1975, white-collar workers had to make less than $155 a week in order to qualify for overtime. The new regulations update this figure to $425 a week.</P>
<P>But other new rules offset this benefit. For instance, many workers whose jobs made them eligible for overtime will now be reclassified. Workers with "double duties"--such as an assistant manager at a fast-food restaurant or department store who flips burgers or rings up customers while overseeing staff--could lose their rights. </P>
<P>Under the new rule, they could lose overtime pay, even if they make as little as $23,660 a year--a wage that qualifies a family for food stamps. In addition, any workers who have extra training--skills obtained while in the armed services, for example--could be considered "professionals" under the new rules and ineligible to overtime. According to the Economic Policy Institute (EPI), the new rules will translate into a benefit for less than 737,000 low-wage workers--a far cry from the 1.3 million claimed by the White House.</P>
<P>Meanwhile, more than 8 million workers would lose overtime protection, according to the EPI. The Department of Labor's estimate is just 644,000.</P>
<P>Employers are already investigating ways to avoid paying overtime under Bush's loophole-packed rules. James Coleman of the law firm Constangy, Brooks &amp; Smith advises bosses to raise their workers' salaries just above the cutoff number.</P>
<P>"An employer would have two choices: raise the salary level or consider converting them to nonexempt status," Coleman told the <I>Wall Street Journal.</I> Any way you slice it, Bush's overtime rules are a gift to big business--and they're chomping at the bit to take advantage of it.</P>


