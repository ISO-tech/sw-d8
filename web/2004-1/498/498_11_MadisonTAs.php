<P><font face="Times New Roman, Times, serif" size="4">UW-Madison grads fight health care cuts</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>TAA organizes a solid two-day strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville </b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--After 10 months of being stonewalled by the state of Wisconsin in contract negotiations, the Teaching Assistants' Association (TAA) representing teaching and project assistants (TAs and PAs) at the University of Wisconsin-Madison conducted a two-day strike on April 27 and 28, defying state law prohibiting state workers from striking and bringing normal university activity to a standstill on much of the campus.</P>
<P>At least 500 TAs and PAs turning out for picket duty, and hundreds joined in march through town. Many undergraduates refused to cross picket lines or joined their TAs on the picket lines.</P>
<P>Many professors canceled classes in a show of support, and more than 20 faculty departments passed resolutions supporting the TAA. And in an important show of solidarity, UPS drivers from Teamsters Local 344 refused to cross TAA picket lines. UPS management had to make deliveries themselves--in one case only after a large picket and sit-down by about 65 undergraduates.   </P>
<P>Health care, as in other disputes across the country, is the sticking point in this contract fight. According to Karen Timberlake, director of the State of Wisconsin Office of Employee Relations, "We've made a policy decision that all state employees need to be contributing something toward [health insurance]." </P>
<P>While the state cries poverty on health care, corporations pay $839.2 million less in taxes than in 1979, and Democratic Gov. Jim Doyle has upheld property tax exemptions of $2.5 billion for business and agriculture. The state even rejected a TAA offer that included zero-premium health care that cost $300,000 less than the state's latest offer, which would institute health care premiums.</P>
<P>The strike concluded April 28 with a spirited rally of about 700 people on the steps of the capitol building, followed by a march to Doyle's office.  On Sunday, the TAA's strike committee called off a grade strike that was to follow the two-day strike, instead focusing actions on Doyle and the state legislature.</P>
<P>The strike committee could have held onto the threat of the grade strike for leverage during this week's negotiations. Nonetheless, the threat of a backlash that could have isolate the union was very real, given the isolation of the TAA's fight from other state unions. Already, 8 of 19 state bargaining units have signed contracts ending zero-premium health care.</P>
<P>A successful fight against a state government hell-bent on forcing health care costs onto state workers will have to include all state workers. Other state unions should use the two-day strike as an example of the kind of solidarity, organization and militant action that will be necessary to successfully beat back the state's attack.</P>


