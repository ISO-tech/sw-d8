<P><font face="Times New Roman, Times, serif" size="4">New York AFSCME leaders push concessions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This is a horrible deal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Barton, shop steward, AFSCME Local 768</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--After working without a contract since July 1, 2002, some 121,000 New York City employees learned last week that leaders of AFSCME District Council 37 have recommended approval of a new contract slashing pay and benefits for new hires and attacking retirees.</P>
<P>Charges for prescription drugs will be increased next month, hitting retirees especially hard. Retiree Association President Stuart Leibowitz called the new agreement "a piece of rubbish."</P>
<P>For the period from July 2003 to July 2004, the deal offers a 3 percent raise, and beginning July 1, 2004, another 2 percent. Yet the workers, some earning so little they qualify for food stamps, will receive no pay increase for the period July 1, 2002 to July 1, 2003.</P>
<P>Instead, the city offered a $1,000 "signing bonus" if members approve the contract, which won't become part of their base pay to calculate the meager pay increases. First, future new hires will receive 15 percent less than current employees for their first two years, instead of the current 7 percent differential.</P>
<P>Other cuts for new workers include a reduction in vacation days, the elimination of the residents' day holiday, a cut in sick days from 12 to 10 during the first five years of employment, a two-hour cut in night pay differential, and a cut in terminal leave credit.</P>
<P>This bargaining strategy has been used successfully in private industry for years: cut pay for new hires and, after the agreement is approved, pressure the more expensive employees out the door to make way for the cheaper new labor.</P>
<P>Mayor Michael Bloomberg had threatened for over a year to apply all these cuts to present employees, claiming that the workweek would be extended from 35 to 40 hours before any raise would come. Workers were therefore set up to feel relieved that the deal wasn't worse.</P>
<P>City employees are so desperate for money they're likely to approve the deal, especially with a leadership that would rather take this poison than even hint at fighting back. The cuts will also apply to another 60,000 other city jobs and will be used as a stick to beat the city teachers, who are still in negotiations.</P>
<P>The DC 37 Delegates Assembly voted for the deal April 27, and members were given three weeks to return mail ballots. Opposition is weak and scattered.&nbsp; So far, the "reformers" who bitterly opposed Executive Director Lillian Roberts in her re-election earlier this year, haven't called for rejection.</P>
<P>The fingerprints of the national AFSCME leadership are all over the deal. Former DC 37 Administrator Lee Saunders, who ran the union after a series of scandals in the late 1990s, created a committee March 17 to stop the bitter infighting between DC 37 Executive Director Lillian Roberts and the opposition led by Charles Ensley, head of Local 371.</P>
<P>In sending Saunders to New York from AFSCME headquarters in Washington, D.C., to sit on the contending factions, AFSCME President Gerald McEntee said, "We will not have democracy run amok." There's little chance of that in DC 37--union members have no right to vote in the contest for executive director.</P>
<P>But the rank and file does have a chance to make their voices heard by voting to reject this terrible deal. </P>


