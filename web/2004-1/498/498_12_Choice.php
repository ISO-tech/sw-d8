<P><font face="Times New Roman, Times, serif" size="4">George W. Bush vs. John "Me too" Kerry</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is this really a choice?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 7, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>ELIZABETH SCHULTE asks what distinguishes the two presidential candidates of the mainstream parties.</B></P>

<P><font face="Times New Roman, Times, serif" size="3">CALL HIM the "me too" candidate. George W. Bush is for a stronger military. John Kerry says "me too." Bush shredded civil liberties with the USA PATRIOT Act. Kerry says "me too." Bush is for more tax breaks for the rich. Kerry says "me too." Bush is critical of Kerry's protest against the Vietnam War 30 years ago. Kerry says "me too."</P>
<P>The list could go on and on. From the right of gays and lesbians to marry to Israel's "right" to assassinate Palestinian leaders, the presidential candidates of the two mainstream parties agree on far more than anyone would have guessed when Election 2004 was getting underway last year.</P>
<P>Of course, Bush and Kerry--as well as the establishment media that zealously handicaps the presidential election like a horse race--regularly talk about huge differences between the two campaigns. But the differences--when they aren't purely a matter of the politicians' hot air--mask the broader agreements between Bush and Kerry, and the Republican and Democratic Parties as a whole.</P>
<P>In some cases, Kerry is trying to out-Bush Bush. On the war on terrorism, Kerry is determined to show that he's even tougher than Bush--and though he talks about taking a more multilateral approach to foreign policy, he's not afraid to say who's boss. He favors what he calls "muscular internationalism" and doesn't rule out "preventive" wars--the exact idea that the Republican White House championed with the Bush Doctrine.</P>
<P>In response to the Bush administration's June 30 withdrawal deadline in Iraq, Kerry accused Bush of "cutting and running." "We have to succeed in Iraq," Kerry said. "We simply can't allow it to become a failed state. That would mean a victory for extremism, new dangers in the Middle East and a breeding ground for anti-American terrorism."</P>
<P>As the Brookings Institution's Ivo Daalder told the <I>Boston Globe,</I> "'The world we live in is not going to be terribly different under a Bush presidency and a Kerry presidency. The United States is the most powerful country in the world, and therefore, the use of American power is going to be indispensable in getting anything done."</P>
<P>When it comes to a woman's right to abortion, Kerry supports it only in the most legalistic fashion--talking about how he is committed to upholding the "law of the land." If the presidency of Bill Clinton is any indication--in which states passed dozens of laws restricting women's access to abortion--we have to demand more of Kerry.</P>
<P>Kerry's own behavior during the Clinton years gives a good indication of what he might be like as president. When Clinton wanted to gut welfare in 1996 and enforce punitive work rules on welfare recipients--more than his Republican predecessors Ronald Reagan and George Bush Sr. had dared to hope for--Kerry backed him up. Likewise, when Kerry helped Clinton sell his 1996 crime bill--called the Effective Death Penalty and Anti-Terrorist Act--the way was paved for Bush's recent attack on civil liberties.</P>
<P>"I didn't even sign on to the Clinton [health plan] in '93," Kerry boasted in a recent <I>Wall Street Journal </I>interview. "I looked at it and said, 'Whoa. Too many boxes. Too bureaucratic. Too much government.'" Kerry's plan for health care?<U> </U>"I've tried to make it market-based and thoughtful...I personally talk to executives everywhere I go, in New York or elsewhere. They say, 'spectacular.'"</P>
<P>As awful as Kerry's campaign has become, millions of people are determined to vote for him anyway just to get Bush out of office. They're furious with Bush's lies, arrogance, militarism and pro-business policies--and they want him out of the White House.</P>
<P>Yet casting a vote for Kerry inevitably means accepting the ever-more-narrow political spectrum between the two parties. Kerry may bring a change in style, but the substance--pursuit of Corporate America's agenda and the aggressive use of U.S. imperial power--will remain the same.</P>
<P>John Kerry may defeat Bush--but his victory won't represent the defeat of Bush's policies. There is no shortcut to this. Ending the U.S. occupation of Iraq, stopping the corporate robber barons, winning a national health care system--all this can only be achieved through systematic organization and struggle.</P>
<P>How horrible do the Democrats have to be before we say "no"? Now's the time to build opposition to miserable policies of the Bush administration--and their "me too" counterparts in the Democratic Party.</P>


