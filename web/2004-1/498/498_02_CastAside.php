<P><font face="Times New Roman, Times, serif" size="4">Military's health care system is a scandal</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Used as cannon fodder and cast aside</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 7, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THEY'RE COMING after me pretty bad," Lt. Jullian Goodrum told a reporter after he was denied medical treatment at the U.S. Army's Fort Knox facility. Goodrum is a veteran of both Gulf Wars against Iraq and has served in the U.S. military for more than 14 years.</P>
<P>A few weeks after Goodrum was quoted in a United Press International story criticizing the Army for providing poor health care, he went to Fort Knox medical officials with emotional problems. "I told them I felt like I was having a breakdown right there," Goodrum said. </P>
<P>But instead of providing him with the care he needed, Fort Knox officials charged him with being "absent without leave"--which, according to Fort Knox spokesperson Connie Shaffery, means that Goodrum became ineligible for all pay and benefits. Goodrum isn't alone.</P>
<P>A recent Knight-Ridder report found that the military is so desperate to meet the demand for troops in Iraq that it has been deploying some National Guard and Army Reserve soldiers who aren't fit for combat. More than a dozen members of the Guard told the news service that their medical histories and chronic health conditions were ignored--and that the severe conditions in Iraq aggravated their problems and made them sicker.</P>
<P>Last August, for example, David Lloyd, a 44-year-old mechanic with the Tennessee National Guard, died of a heart attack in Iraq.  A thorough physical would have shown that he had serious heart disease. But as his wife said, "The only thing he had was the shots." As one reserve officer told Knight-Ridder: "It wasn't about healthy troops. It was about the number of troops."</P>
<P>In Goodrum's case, the attempt to label him AWOL is being focused on a man who was named "Soldier of the Year" in the 176th Maintenance Battalion. But when Goodrum returned from combat in Iraq and went to the Army's Inspector General and Congress to report safety violations that led to the death of a fellow soldier, he began having problems that have now been documented by the civilian doctor he turned to after being denied treatment at Fort Knox.</P>
<P>"Unfortunately, recent intimidation, threats of being arrested for staying on medical leave from his superiors has resulted in recurrent psychiatric symptoms," wrote Dr. Vijay Jethanandani on December 3. "Instead of following up on his complaints, it appears that some of his superiors on stateside may be penalizing him for reporting his superior officer in Iraq."</P>


