<P><font face="Times New Roman, Times, serif" size="4">The best way to avoid a strike is to prepare for one</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We fought back and won</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 7, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
When the Essex Junction, Vt., school boards tried to slash their budgets on the backs of teachers, the teachers' union fought back--and won. Traditionally a conservative, top-down, teachers union, the Essex Junction Education Association was revitalized through the creation of the Contract Crisis Intervention Committee (CCIC). </P>
<P>Taking the stance that a union is only as strong as its membership, the union's leadership and CCIC worked to keep all members informed and involved in the contract struggle, attracting over 50 new members as a result. Such an approach was needed to jumpstart negotiations bogged down by the district's refusal to budge on insulting compensation proposals and insistence on "management rights" that would deprive teachers of "just cause" language currently in their contract.</P>
<P>Emphasizing that the best way to avoid a strike was to prepare for one, union leadership, now including CCIC chairs Kevin Martell, Adam Weiss and Marilee Taft, regularly drew more than 100 members to meetings that discussed and explained the union's efforts to win a good contract.  These initial efforts included a petition to the boards requesting a fair contract, attendance at school board meetings and the publication of <I>Common Sense,</I> the CCIC newsletter edited by John Ferris.</P>
<P>When the school boards still didn't budge, members took efforts to the next level, outlining the final phase of their contract campaign and setting a date for a strike vote. More then 180 members of the union also packed the high school's board meeting, where they charged the board with delay tactics and unfair bargaining procedures.</P>
<P>With the strike date looming, the two school boards finally moved, negotiating an agreement that included significant improvements in the board's salary proposal and a reduction in the proposed insurance payment. Perhaps more importantly to the members, who recently voted overwhelmingly to ratify the contract, was the deletion of the offensive "management rights" contract language.</P>
<P>Other gains, according to Vermont National Education Association organizer Ellen David Friedman, include "a big membership increase" and "great vitality" in the union. This vitality is reflected in the words of one veteran teacher, not formerly a strong union member: "I prayed that God would soften the board members' hearts. But if he didn't, I was ready to walk!"<BR>
<B>Brian Walsh, </B>President, Essex Junction Education Association, Essex, Vt.</P>


