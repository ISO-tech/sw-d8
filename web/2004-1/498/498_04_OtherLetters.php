<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>Nader is headed in the wrong direction</b></font></P>
<B>
<P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Peter Lamphere's letter (<I>SW</I>, April 16) argues that the left should endorse Ralph Nader, because no matter how Nader spins it, "his candidacy is a referendum on the question of 'Anybody but Bush.'" Whether this is the case or not is unclear. </P>
<P>Lamphere implies that the left can shift the direction of Nader's campaign, as it did in 2000. However, there are a few differences this time around. Nader has gone around the Green Party and begun to organize on his own, targeting right-wingers, while hoping to get the Green Party's stamp of approval anyway. </P>
<P>Nader's independent run will free him from the left-wing base of the Green Party and the global justice movement. Without this left anchor, Nader appears willing to make explicitly right-wing arguments to win "disaffected conservatives." Nader's open letter to conservatives criticizes free trade agreements for challenging "national sovereignty" and criticizes corporate media as "subverting family values, parental discipline and wholesome childhoods."</P>
<P>Accommodating right-wingers necessarily strips his campaign of its left-wing content and emphasis, calling into question the idea that Nader's campaign is a referendum on lesser evilism. </P>
<P>The nature of a candidate's campaign within the context of the current political environment should also be considered when deciding whether the campaign should be supported or not.  Lamphere de-emphasizes the importance of Nader's positions on marriage equality and the occupation of Iraq, as if his stand and emphasis placed on these central issues for the left were of little concern to the nature of his campaign! </P>
<P>As the resistance against the occupation grows, the question of calling for "all foreign troops out of Iraq now" will be the biggest obstacle to building an anti-imperialist movement that can force the U.S. out of Iraq. Nader's Web site calls for "replacing U.S. forces with a UN [United Nations] peacekeeping force" in Iraq. </P>
<P>Socialists must be clear that a UN occupation is merely a fig leaf for U.S. imperialism. How can we be arguing this position with fellow activists while campaigning for someone who calls for a UN occupation?</P>
<P>A campaign that panders to reactionary protectionists and does not centrally take up important issues facing the left should not be endorsed by the International Socialist Organization.<BR>
<B>Bill Linville,</B> Madison, Wis.</P>

