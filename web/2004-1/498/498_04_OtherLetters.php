<p><a name="Vote"></a><font face="Times New Roman, Times, serif" size="4"><b><I>SW</I> should reject anti-voting agenda</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
After reading <I>Socialist Worker</I> for some time, I have come to notice a severe distrust of using electoral methods to accomplish liberal and leftist goals. Though I will be the first to admit that the Democrats are not a particularly progressive party, your newspaper has lambasted Ralph Nader and Dennis Kucinich while refusing to lend even a token of support to either one. </P>
<P>One subsection of an article even was labeled "The dead end of electoralism." I fully support and acknowledge the need for grassroots organizing and protesting as a way to put pressure on government officials, but it seems that the publishers at <I>SW</I> have a severe mistrust of the need for the working class to vote. </P>
<P>One active member of the International Socialist Organization, with whom I am friends, openly professes his refusal to vote in elections. It is understandable to be mistrustful of our corporate-run political campaigns. But I find it wrong, un-leftist, and even corporatist that <I>SW</I> does not actively encourage its readers to go out and vote. </P>
<P>Voting is the most fundamental exercise of working-class solidarity. It will necessarily be the basis of any type of socialist society that could come into being. </P>
<P>Without getting into debates about whether a socialist revolution is feasible and will result in a just and democratic society, one must be left to wonder how we will know when that revolution has been completed and the working class should be able to trust itself to vote once again. I think that time is now, and <I>SW</I> needs to get over its anti-voting agenda. <BR>
<B>Bill Anderson,</B> From the Internet</P>


