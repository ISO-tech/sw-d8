<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Anti-Arab racism on the increase</b></font></P>
<B>
<P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Libertarian radio talk show host Jay Severin recently said, "Let's kill all Muslims," on his afternoon program on WTKK-FM. The station's general manager, Matt Mills, told the Council on American-Islamic Relations (CAIR), an Islamic civil rights group, that in a discussion Severin claimed Muslims want to take over America, even if it takes centuries.</P>
<P>"I've got an idea," Severin commented, "let's kill all Muslims." Mills wrote in an e-mail, "I have spoken to Jay Severin, and he knows we take this seriously and do not condone offensive remarks toward any religious groups, and he will be apologizing on his show Monday afternoon. He did not intend to offend anyone."</P>
<P>Mills acknowledged that if Severin had said the same thing about African-Americans, he would no longer be on the air. This is the result of Arab and Muslim scapegoating that happened after September 11 in the name of the "war on terror."  </P>
<P>Anti-Muslim hate has also been increasing in the U.S. since the brutality involved in the "pacification" of Falluja in Iraq and Sharon's hit-list assassinations of Palestinians. Several incidents took place recently in Texas, such as a shooting at a Denton mosque, threats against the Islamic Center of El Paso, arson attacks on Muslim businesses in San Antonio, and racist graffiti scrawled on the interior of a Lubbock mosque.  </P>
<P>The U.S. has made the world a more dangerous place--not only in the Middle East, but also in the U.S., especially for Arabs and Muslims. <BR>
<B>May Vermouth,</B> Boston</P>

