<P><font face="Times New Roman, Times, serif" size="4"><b>What about the lives of Iraqi women?</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I was disgusted to see that former Secretary of State Madeleine Albright, a war criminal, and Sen. Hillary Rodham Clinton (D-N.Y.), a gung-ho supporter of Bush's war policies, were keynote speakers at the "March for Women's Lives" pro-choice rally in Washington on April 25. </P>
<P>Richard Nixon, the butcher of Vietnam, apparently was sympathetic to the plight of American Indians, but I wouldn't have invited him to an American Indian Movement rally.<BR>
<B>Paul D'Amato, </B>Chicago</P>

