<p><a name="UFCW"></a><font face="Times New Roman, Times, serif" size="4"><b>UFCW is ready for a possible strike</b></font></P>
<B>
<P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I disagree with Darrin Hoop's assessment that "union officials have done little to prepare for a possible strike." ("Grocery showdown in Seattle?" <I>SW,</I> April 23). I am a member of United Food and Commercial Workers (UFCW) Local 44 and have been following closely my union's preparations for a strike.  </P>
<P>We raised our dues by $10 a month for a strike fund two months ago that will be available for a year as other UFCW locals negotiate with the employers. The union has also been working with other unions, such as the Teamsters, the Boeing machinists, and longshoremen to build solidarity for a strike.  </P>
<P>The UFCW is also working with Jobs with Justice to insure that people hear what the employers want to do to workers. Add to these things that two weeks ago my local started to organize picket captains, and it appears that the UFCW is ready for a dispute.</P>
<P>That is not to say that I have not disagreed with the union leadership: they seem willing to allow a two-tier wage and benefit system, and should have raised dues back when the California strike started.<BR>
<B>Andrew Heyman,</B> Everett, Wash.</P>

