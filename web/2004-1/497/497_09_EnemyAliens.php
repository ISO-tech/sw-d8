<P><font face="Times New Roman, Times, serif" size="4">Immigrant targets of the war on terror</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Nightmare at Guant&aacute;namo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Virginia Harabin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">David Cole, <B><I>Enemy Aliens. </B></I>New Press, 2003, 256 pages, $24.95.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE ARABS and Muslims imprisoned in U.S. camps in Gu&aacute;ntanamo Bay, Cuba, endure a nightmare. Held on unnamed charges, denied access to lawyers and subjected to physical and psychological torture, they are among the most dehumanized and friendless victims of the U.S. war on terror. </P>
<P>They live in a legal limbo, christened with the sinister new term "enemy combatants," which, according to the U.S. government, means they're denied even the meager rights that international law grants prisoners of war. The U.S. rationalizes this detention on the grounds that they are being "preemptively" detained--in other words, they are presumed guilty in advance of committing any crime.</P>
<P>David Cole's excellent book <I>Enemy Aliens </I>is a generous resource for those seeking to understand the legal context in which the U.S. government has been able to name certain groups of people as exempt from civil and legal rights.</P>
<P>The book places the new wave of racism, harassment and scrutiny directed at Arabs and Muslims in the context of the U.S. history of targeting certain groups in preparation for assuming the kind of broad and invasive powers that have led to the McCarthy witch-hunts of the 1950s and the internment of Japanese Americans during the Second World War.</P>
<P>The U.S. government has a long history of using secret, undisclosed evidence against immigrants, and defending it on the grounds that non-citizens don't deserve the same due process protections as citizens. But, as Cole makes clear, the line between citizen and non-citizen is a fragile one. The differences can become easily blurred as the government claims new powers of surveillance and control.</P>
<P>In this context, it has been argued that people are not simply persecuted because they are foreign, but rather they are made foreign in order to subject them to persecution. Cole forcefully argues that the distinction between citizen and non-citizen is one that can be manipulated in order to grab rights away from the most vulnerable, only to increase the power of the government to exercise these same violations against citizens when it becomes convenient to do so.</P>
<P>Cole's history documents abuses carried out not only by Republican administrations like that of George W. Bush, but by liberal Democratic administrations as well. He argues that unless the power of the government is checked by public outcry, attacks on "enemy aliens" can lead to attacks on citizens who engage in dissent as the government carries out an ideological war at home.</P>
<P>All of us can easily be caught in the net cast wide to catch all enemies of the state. <I>Enemy Aliens</I> is a powerful argument for solidarity in the face of these attacks.</P>

