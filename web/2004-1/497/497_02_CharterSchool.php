<P><font face="Times New Roman, Times, serif" size="5"><b>Pocketing profits from students' labor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>A CHARTER school in Pensacola, Fla., has been charged with fraud for hiring out students with behavioral or academic problems to work on state road crews.</P>
<P>According to prosecutor Russell Edgar, the privately run but publicly financed Escambia Charter School accepted up to $140,000 from the state to educate a group of 140 students who are classified as "at risk."</P>
<P>At risk for being exploited, it seems. The school hauled in $250,000 from another state agency for putting the students to work on jobs such as cleaning the sides of roads.</P>
<P>Florida law requires that students spend 25 hours a week in class. The Escambia students spent just five hours in class--one hour a day. The rest of the time was spent on road crews.</P>
<P>Each student was paid $10 an hour for their work. But the school was paid $16.25 an hour for each student by the state. In other words, it was pocketing about $40,000 a year.</P>
<P>According to a prosecutor, in order to cover up the scam, school officials submitted false attendance records and grade reports.</P>

