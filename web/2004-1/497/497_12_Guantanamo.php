<P><font face="Times New Roman, Times, serif" size="4">New Supreme Court cases</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Challenge to the gulag in Guant&aacute;namo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WILL THE U.S. Supreme Court let the Bush administration get away with locking up hundreds of men indefinitely in a military gulag? That's the question being asked by human rights and civil liberties advocates as the court last week began to hear arguments challenging the Bush administration's assertion that "enemy combatants" at its prison camps in Guant&aacute;namo Bay shouldn't be considered prisoners of war under the Geneva Convention--and that U.S. courts can't legally intervene.</P>
<P>The administration has refused so far to allow the nearly 600 detainees--who are mostly from Afghanistan, Pakistan and Iraq--basic rights such as access to lawyers.</P>
<P>Maybe it's because the Bush administration is afraid for the world to find out the truth about the prisoners who Secretary of Defense Donald Rumsfeld once called the "worst of the worst." Until recently, "the worst of the worst" included three children between the ages of 13 and 15 years old.</P>
<P>Seventeen-year-old Canadian citizen Omar Khadr--who was just 15 when he was captured by the U.S. and thrown into the Guant&aacute;namo hellhole--is still in U.S. custody. "If he is alleged to have committed a crime, then we should see some evidence," Dennis Edney, a lawyer who filed a brief on Khadr's behalf with the Supreme Court, told the <I>Toronto Star</I>. "This is a kid with two bullet wounds who has lost the sight in one eye."</P>
<P>The Bush administration doesn't just want this kind of treatment to apply to "foreign combatants." They want it to apply to U.S. citizens, too.</P>
<P>As <I>Socialist Worker</I> went to press, the Supreme Court was getting ready to hear the cases of two American detainees: Yasir Esam Hamdi and Jose Padilla. Both are accused of aiding al-Qaeda, and both were only recently given brief access to lawyers. </P>
<P>But whether they're foreign or U.S. citizens, according to Paul W. Butler, special assistant to Defense Secretary Donald Rumsfeld, the Bush administration would like to hold at least some detainees indefinitely. "What I'm saying is that there is a large percentage right now who are either high threat or high intelligence value, that right now there's no intention to try them before a military commission," Butler told the <I>Boston Globe</I>. "They're dangerous. And we have a responsibility, both to our forces...and the rest of the world, to not let those people back out."</P>
<P>In other words, George Bush gets to decide who is entitled to rights and who isn't.</P>
<P>Plus, says Butler, the U.S. is reluctant to release certain prisoners for fear that the detainee wouldn't face a trial back home because evidence gathered at Guant&aacute;namo may not be usable in other countries' courts. In an ironic twist, the closer another country's legal system is modeled on the U.S. Constitution, the less likely it is that evidence gathered at Guant&aacute;namo can be used!</P>
<P>"Most of the statements that we've gotten from someone--if you're dealing with countries that have a Miranda-type law--are inadmissible, and so what do you have?" Butler said. "We don't want to be in the business of just turning people back to a country to have them let go."</P>
<P>So instead, the Bush administration wants to essentially ignore the U.S. Constitution and courts altogether.</P>
<P>As law professor David Cole explained in a recent <I>New York Times</I> op-ed article, "While the federal government has often introduced security initiatives by singling out foreigners, it has just as often sought to extend those tactics to citizens later...It used to take years to extend these tactics to American citizens. But things are speeding up. Tuesday, the Bush administration intended to defend its treatment of the Guant&aacute;namo detainees on the grounds that they are foreigners who do not deserve American legal protections. Next week, it will argue that it has just as much latitude to detain American citizens. The slippery slope has never been more slick."</P>

