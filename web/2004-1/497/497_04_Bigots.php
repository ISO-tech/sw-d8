<P><font face="Times New Roman, Times, serif" size="5"><b>How New York Democrats helped the bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 30, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</B> <BR>
On March 14, the steps of the Bronx county courthouse were filled with at least 5,000 antigay demonstrators. The rally was organized by Evangelical Latino clergy and Democratic State Sen. Ruben Diaz. This was shocking to most activists and far outnumbered the protests for gay marriage that we had seen. </P>
<P>About 75 of us, led by Latino and Bronx-based organizations, stood against this bigotry, with chants to keep us unified. Afterwards, it was important to try to understand why this event took place. </P>
<P>Many asked whether this showed that most Latinos are against gay rights. But the reality is much more complicated. According to a recent <I>New York Daily News</I> Poll, Latino public opinion is split very evenly in New York City, with 41 percent opposing gay marriage, and 41 percent in support.</P>
<P>The rally was a confident right-wing power play in the midst of a very polarized situation. But it was also extremely shady. As the rally gathered, religious services were held on the steps of a public building. A massive banner, reading "No to Homosexual Marriage, Yes to George Bush's Constitutional Amendment!" was hung on the county courthouse.</P>
<P>The office of Bronx Borough President Adolpho Carrion claimed to have played no role in organizing or permitting the event--but his office is in the courthouse building! Carrion also has an Office of Faith-Based Initiatives. City Administrative Services also claim that they don't know who authorized police overtime for the event.</P>
<P>The National Association of Latino Clergy and Christian Leaders had charter buses that brought people in from out of state. This is a group that has appeared alongside the far-right Traditional Values Coalition and has held meetings with Marilyn Musgrave, a leading sponsor of the federal ban on gay marriage.</P>
<P>The biggest lesson to take from this is that the right is organizing and is funding and supporting the efforts of conservative Latino and Black clergy to vocally oppose gay marriage. And in the Bronx, the antigay right has pull within the Democratic Party.<BR>
<B>David Thurston, </B>New York City</P>

