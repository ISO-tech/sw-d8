<P><font face="Times New Roman, Times, serif" size="4">Mordechai Vanunu freed after 18 years behind bars for...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Exposing Israel's nuclear arsenal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Phil Gasper</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>"I WILL continue to speak against all kinds of nuclear weapons, against all the world's nuclear weapons. Those were the defiant words of Mordechai Vanunu--the whistleblower who exposed Israel's secret nuclear weapons program--as he was released from prison April 21 after 18 years behind bars.</P>
<P>Vanunu was a technician at Israel's Dimona nuclear installation in the Negrev Desert from 1976 to 1985. In 1986, Vanunu traveled to Britain--where he gave the London<I> Sunday Times</I> extensive details and photos demonstrating that Israel was producing plutonium for nuclear bombs and that it possessed up to 200 nuclear warheads. Even before Vanunu's revelations were published, however, he was abducted by Israeli agents and taken back to Israel. After a secret trial, he was given the maximum sentence. </P>
<P>For 12 years, Vanunu was kept in solitary confinement in a tiny 6-by-9-foot cell--in conditions described by Amnesty International as "cruel, inhuman and degrading."</P>
<P>Now that Vanunu has finally been released, the Israeli government has placed unprecedented restrictions on him. He can't travel freely, is forbidden from leaving Israel and can't contact any foreign citizens without permission.</P>
<P>Israel claims that the restrictions are necessary to prevent Vanunu from revealing further secrets. But the idea that Vanunu has any new information to disclose is absurd.</P>
<P>The real reason for his continued harsh treatment is that Israel still refuses to acknowledge officially that it is a nuclear power. Israel is the only country in the Middle East that has refused to sign the Nuclear Non-Proliferation Treaty. Washington sends billions of dollars in military and economic aid to Israel every year--even though U.S. law forbids such aid to countries that are developing nuclear weapons and that don't permit international inspections of their facilities.</P>
<P>So both Israel and the U.S. maintain the fiction that Israel does not have a nuclear weapons program. In 1970, President Richard Nixon told the Israeli government that if they kept their weapons "in the basement," the U.S. would turn a blind eye. In 1998, Bill Clinton promised to support the improvement of Israel's "deterrent capabilities,"--which, as Britain's <I>Guardian </I>newspaper pointed out, is "a euphemism for nuclear weapons."</P>
<P>According to the <I>Guardian, </I>"The Israeli armed forces now possess missiles capable of delivering a nuclear payload up to 1,500 kilometers away and are developing others with much longer range. They have acquired more than 200 nuclear-capable aircraft, and have completed the land-air-and-sea triad by buying three nuclear-capable submarines. They probably have more nuclear warheads than Britain, including thermonuclear warheads."</P>
<P>If Vanunu were free to speak out about Israel's nuclear program, it would be a continuing embarrassment to both Washington and Tel Aviv--especially at a time when the Bush administration is pressuring other Middle Eastern countries not to develop nuclear weapons.</P>
<P>Israeli officials condemn Vanunu as a traitor. But his decision to reveal Israel's nuclear secrets was a courageous act in the cause of world peace. Antiwar activists must protest his continued outrageous treatment by the Israeli government.</P>

