<P><font face="Times New Roman, Times, serif" size="4"><b>Victory for Oakland 25</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Amanda Maystead</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>OAKLAND, Calif.--On April 22, the Alameda County District Attorney dropped all charges against 24 remaining defendants who were arrested during an antiwar demonstration on the Oakland docks last year. The decision was a surprise to supporters in the courtroom, as gasps and muted cheers were audible after the first defendant and then the rest were cleared.</P>
<P>On April 7, 2001, several hundred people demonstrated peacefully at the Oakland docks to protest the war in Iraq. Twenty-four protesters and International Longshore and Warehouse Union (ILWU) Local 10 business agent Jack Heyman were arrested when the cops, unprovoked, attacked demonstrators and dockworkers. Several people were injured after went on a rampage, using rubber bullets, tear gas and concussion grenades.</P>
<P>The court's change of heart, no doubt, was affected by an April report by United Nations (UN) Commission on Human Rights that cited Oakland police at the top of their list of human rights violators. To continue to trial would have constituted political suicide for the city and mayor, Jerry Brown.</P>
<P>Solidarity demonstrations outside the courthouse and supporters inside the courtroom also continued to apply pressure on city officials throughout the last year. "Labor action on the docks can stop police brutality and imperialist wars. French dockworkers did it during the war in Indochina." Heyman told <I>Socialist Worker</I>. "Our victorious struggle in Oakland shows that the fight for labor rights and civil liberties are inextricably linked. If we stand fast together, we can win!" </P>
<P>Presently, ILWU Local 10 is the lead plaintiff in a federal lawsuit against the Oakland Police Department for its brutal attack.</P>

