<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Demonstrate against George Bush</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Khury Petersen-Smith</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>BUFFALO, N.Y.--About 300 people protested George W. Bush as he gave a speech to promote the civil rights-shredding USA PATRIOT Act on April 20. Bush gloated about the government's racist targeting, harassment and arrest of members of the Yemeni community in nearby Lackawana.</P>
<P>The protest, which drew people from Buffalo and Rochester, was organized by Western New York Against Bush. Protestors defied intimidation by local, state and federal police, who promised a heavy police and military presence through the local media.</P>
<P>Demonstrators pointed to the Bush administration's lies about the invasion and occupation of Iraq. </P>
<P>Several veterans also spoke out. "I don't think we should give our blood for Corporate America's oil," said Ron Bassham, a veteran of the Korean and Vietnam wars and a member of United Auto Workers Local 686 Veterans Committee. John Curr, a veteran of the 1991 invasion of Iraq protested in his Army uniform--with the American flag patch on his shoulder turned upside down. Bush's policies "have nothing to do with supporting our troops and everything to do with an illegal war," he told the crowd.</P>
<P>Several protesters carried signs that pointed to Democratic John Kerry as an alternative to Bush. But Kerry's call to strengthen the military by 40,000 and his turnaround on past positions on war show that Kerry would promote no less war or deception than Bush.</P>
<P>The alternative to the warmongers in the Republican and Democratic parties lies in building a fighting antiwar movement.</P>

