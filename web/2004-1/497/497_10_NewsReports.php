<p><a name="Cat"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Caterpillar</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>PEORIA, Ill.--More than 300 people came here from across the state, the region and the country to Caterpillar's corporate headquarters to demand that the company stop supplying Israel with specially outfitted bulldozers used to demolish Palestinian homes.</P>
<P>Cindy and Craig Corrie, the parents of American activist Rachel Corrie, who was crushed to death under a Caterpillar bulldozer driven by an Israeli soldier, also came to the April 23 Peoria rally and addressed the crowd.</P>
<P>"This is a beautiful day," said Cindy Corrie, "seeing all of the signs and all of the faces and all of the people, dedicated, driving miles and miles to come here and to send a message to Caterpillar and to make a difference for the Palestinian and Israeli people. My daughter Rachel was passionate about justice. Rachel stood there that day protesting illegal home demolitions that the U.S. opposes on the record yet fails to stop."</P>
<P>Despite receiving a letter from the Corries, Caterpillar CEO Jim Owens refused to meet with a delegation of protesters.</P>
<P>Activists hope to organize a boycott of Caterpillar equipment that will force the corporation to end its sale of bulldozers to Israel.</P>

