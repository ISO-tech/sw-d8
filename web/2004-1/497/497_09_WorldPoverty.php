<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Plundering the world's poor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>GO TO the Web site of the World Bank, and you will find a handy, downloadable brochure entitled "Working for a World Free of Poverty." </P>
<P>In it, there are some very startling statistics. Over 2.8 billion people--more than the total world population in 1950, and almost half of the world's population today--"live" on $2 a day or less. About half that number live on $1 a day or less.</P>
<P>According to the brochure, the World Bank's primary "focus is on helping the poorest people and the poorest countries." The Bank has "working partnerships" with "more than 100 developing countries." As of 2002, it had approved loans and grants totaling $19.5 billion for 239 projects aimed to boost investment in "health and education, fight corruption, boost agricultural production, construct roads and ports, and protect the environment."</P>
<P>And if he lived today, a Roman conqueror might have produced a brochure called "A World Without War."</P>
<P>The truth is that World Bank loans have increased the very poverty to which the attractive brochure refers.</P>
<P>The reasons aren't hard to discover. The Bank has used structural adjustment programs (SAPs) as a financial weapon to force states to conform to the interests of international capital.</P>
<P>Here's how it works: The World Bank collects funds, primarily from the richest nations (the U.S., Europe, Japan). It then uses these funds to offer grants and loans to cash-strapped countries, to which it attaches stringent conditions. These conditions compel governments to slash government spending in order to free up cash to pay off the debt, which means cutting the "health and education" mentioned in the brochure.</P>
<P>Governments are also required to sell off state-owned enterprises to pay off the debt. The result has been a massive increase in costs to the poor for basic services (for which they must now pay "user fees") and mass layoffs.</P>
<P>World Bank SAPs have opened up countries to unrestricted investment, have compelled governments to lower trade barriers and have pushed local farmers to shift from subsistence to export-oriented cash crops. The result has been not only increased rural poverty, landlessness and hunger, but an increase in the debt load that the SAPs were supposed to reduce. The Ghanaian government, for example, spends five times more annually to pay its creditors than it spends on all of its social programs.</P>
<P>But what about those World Bank projects to build dams and roads? Surely they must be helping.</P>
<P>Many of these projects have destroyed valuable cropland, displaced thousands--and in some cases millions--of people, and have benefited only a few. To give a few examples: In 1978, the Bank provided $451 million to finance the Upper Krishna dam. Nearly 220,000 people were forcibly removed from their homes and land.</P>
<P>The $3.7 billion Chad-Cameroon Oil and Pipeline project, which opened in 2003, is run by Exxon-Mobil. The World Bank, though its investments were small in the project, helped reduce Exxon-Mobil's exposure and leveraged private-sector investment that would otherwise have considered it too risky. As a result of the pipeline project, in Cameroon, "thousands of people have had their lands expropriated, crops and other plants destroyed, and water sources polluted without adequate compensation," according to one report.</P>
<P>However it sells itself, the Bank is no more in business to reduce poverty than is a loan shark. Its approach to projects like hydroelectric dams and road-building are the same as its approach to SAPs--to create infrastructure that is most conducive for the exploitation of a region's labor and resources by international capital.</P>
<P>In a rare moment of candor, a senior Bank manager suggested that "we behave like merchant banks" and "stop talking about the environment, about women in development, about poverty alleviation and so on, as priorities." If the Bank were honest, it would produce a brochure entitled: "Putting Profits First."</P>

