<P><font face="Times New Roman, Times, serif" size="4">Democrats disagree about <I>how</I> to go to war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Questions they won't ask</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 30, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker</I>,</B> <BR>
The discussion around former intelligence czar Richard Clarke has really made me value the positions that <I>Socialist Worker</I> takes week in and week out.</P>
<P>Clarke's testimony about the actions of the Bush administration outlines its utter hypocrisy and futility even on the issue of fighting terrorism--the key plank of Bush's reelection campaign. The intense feeling about Clarke serves as yet another example of the polarization in the country about Bush--though Clarke himself was appointed by Ronald Reagan.</P>
<P>But readers of <I>SW </I>will see that Clarke is far from being a progressive. In fact, his attack on Bush and on the Iraq war is that the "war on terrorism" is not being fought well enough. Clarke doesn't want to address the roots of terrorism--the need for American capitalism to control the world's resources and the world's policies. Clarke doesn't want to talk about the civilian casualties in Iraq, or about how the U.S. claims the right to tell any country what to do.</P>
<P>No. Clarke disagrees with Bush about <I>how</I> the U.S. should carry out its imperialist aims. Clarke wanted Afghanistan to be bombed much earlier and much harder than it was in 2001. He criticized the "wag the dog" arguments of the Republicans who cast the 1998 missile strikes on Afghanistan as Clinton's way of distracting the country from the Monica Lewinsky scandal.</P>
<P>As <I>Socialist Worker </I>argued then, the "wag the dog" argument was <I>not</I> the way progressives should challenge the 1998 strikes--as a lot of radicals were doing. While "wag the dog" allowed us to poke fun at the politicians' cynical use of policy, it not only muddled our picture of how U.S. imperialism works, but also fell into the hands of a right-wing attack on Clinton that didn't further progressive causes one bit. Instead, <I>SW </I>argued, we should criticize the strikes in Afghanistan for what they are--expressions of the brutality through which the U.S. maintained its power in the world. </P>
<P>Today, we have to take the same position. The Iraq war and occupation is not wrong because it distracted us from the "real war" against terrorism--like Clarke and the Democrats say--but because it continues the imperialist policies that have made the world such a violent and unequal place.<BR>
<B>Pranav Jani, </B>New York City</P>

