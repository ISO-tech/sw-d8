<P><font face="Times New Roman, Times, serif" size="4">Columbia University grad employees on strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The union is about dignity" </b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 30, 2004 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Graduate student employees at Columbia University are entering the second week of a strike for union recognition after walking off the job on April 19. </P>
<P>Columbia's TAs and RAs have been fighting for more than two years to unionize with Graduate Student Employees United/UAW Local 2110. "We've tried everything to get the university administration to face the fact that the majority of TAs wants a union," said David Carpio, an organizer for the UAW and a former Columbia grad student. "This strike is the next step."</P>
<P>Grad students have been fighting against the university's legal maneuvering ever since the National Labor Relations Board ruled in their favor in early 2002. The university administration has consistently argued that TAs are getting "vocational training" and are therefore not workers. "Grading 45 of the same paper has nothing to do with education," said Mehmet Dosemeci, a fourth-year history grad student. </P>
<P>One striker carried a picket sign reading, "If we're not workers, then I'm not working," while another sign read, "Historians against 19th century labor laws."</P>
<P>During the first week of the strike, pickets of as many as 400 grad students and their supporters lined both sides of the entrance to the university. A high point came on Wednesday when undergraduates walked out to join several hundred grad students at a solidarity rally in the center of campus.</P>
<P>Stanley Aronowitz, the Green Party's 2002 candidate for governor of New York, addressed the rally with solidarity greetings from the Professional Staff Congress, the faculty and staff union for the City University of New York system. "When did an employer ever give us anything we didn't take from them?" Aronowitz asked.</P>
<P>"When [university] President Bollinger spends $20 million on his mansion while TAs are struggling to pay rent, that's not the logic of a university," said English professor Bruce Robbins. "That's the logic of Enron."</P>
<P>Columbia grad students teach the majority of classes in Columbia's required "core curriculum" as well as working as TAs and graders for large lecture classes. "In my four semesters as a TA, I have taught over 400 students," said Art History graduate student Kate Isard. "I have always held two other jobs to pay the rent."</P>
<P>Even though a majority of grad students voted in favor of a union, those actively striking are currently a minority--about 20 percent according to the school paper. Some departments have set up elaborate scabbing operations, with professors, adjuncts and other grad students taking over struck classes for the rest of the semester.</P>
<P>Despite the university's anti-union campaign, professors and students have been expressing their solidarity by signing petitions and moving classes off campus. "We've been ecstatic over the level of support from the community, especially professors and undergrads," said David Carpio. "The union provides a huge amount of dignity for us," said Dosemeci. "If the strike [at Columbia] is the beginning of something, as more and more battles are won, it will become easier for other schools."</P>

