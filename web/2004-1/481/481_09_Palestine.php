<P><font face="Times New Roman, Times, serif" size="5"><b>Personal accounts of Palestine's resistance</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Lauren Fleer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 9, 2004 | Page 9</font></P>

<font face="Arial, Helvetica, sans-serif" size="2"><P><B><I>Live from Palestine: International and Palestinian Direct Action Against the Israeli Occupation,</B></I> Nancy Stohlman and Laurieann Aladin, editors. South End Press, 2003, 215 pages, $17.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>DURING THE 1998 Al-Aqsa Intifada, international observers and activists traveled to Palestine to join the anti-occupation struggle and provide a measure of aid and protection to Palestinians suffering the brunt of Israel's brutality. <I>Live from Palestine</I> is a collection of writings by these international activists, as well as Palestinian members of the nonviolent resistance movement.</P>
<P>The book includes more than 40 essays and reports, including Adam Shapiro's report of Israel's invasion of Ramallah and the siege of Yasser Arafat's compound, Kristen Schurr's report from inside Bethlehem's besieged Church of the Nativity, and letters written by Rachel Corrie days before her murder by the Israeli military. The book includes a preface from Noam Chomsky and essays from well-known figures Edward Said, Kathy Kelly, Ali Abunimah and Hanan Ashrawi.</P>
<P>The authors' accounts capture the horror of Israeli assaults and the humiliation of daily life under Israel's illegal military occupation. Brian Wood, a volunteer with the International Solidarity Movement, conducted 40 hours of interviews with survivors from the Jenin refugee camp following Israel's massacre there in April 2002. "Over and over, the testimonies from the people in the camp confirmed the evidence: armored Israeli bulldozers leveled homes with living, dead and injured people inside," Wood writes.</P>
<P>Some 30 percent of all homes in Jenin were destroyed, leaving more than 4,000 people homeless and many uncounted for, buried under the rubble of their former homes. Such firsthand accounts of its crimes underscore Israel's role in the conflict as conqueror, not victim.</P>
<I><P>Live From Palestine</I> offers a variety of political analyses on what course the Intifada and international solidarity ought to take. While the principle of nonviolence is almost unanimously supported in the book, there's an interesting range of debate on what role it should play.</P>
<P>Palestinian legislator Abdul Jawad Saleh, who advocates for nonviolence within the Intifada, describes how the strategy has been defeated. "We, the Palestinians, have worked hard at nonviolent resistance," Saleh writes. "We have staged nonviolent marches, organized labor strikes and boycotted Israeli goods and banks...</P>
<P>"But what did nonviolence achieve? The Israeli Occupation Authority dissolved Palestinian municipal councils, deported some of their elected leaders and attempted the assassinations of others...The blockading of agriculture, denial of work permits, and resulting increase in impoverishment and hunger has begun to put a strain on the limits of pacifistic tolerance."</P>
<P>Ramallah-based human-rights advocate Hanan Elmasu writes bluntly, "I am wearied and sickened by the constant calls for Palestinians to take up nonviolent resistance in the face of Israel's brutal occupation. I am disheartened by those who have not lived under occupation telling me that if only Palestinians would call a halt to violence, then the world would support their cause."</P>
<I><P>Live From Palestine</I> is an informative volume on international solidarity efforts, and its publication is itself an activist victory. The authors succeed in showing the reality of the Palestinian struggle and describe movement building and divestment organizing for international activists.</P>



