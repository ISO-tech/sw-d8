<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 9, 2004 | Page 11</font></P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Sterling Laundry</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Dave Zirin</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--More than 100 people gathered December 12 at Plymouth Congregational Church in Washington, D.C., to rally around striking laundry workers, pledging money and energy to their struggle. Employees at the Sterling Laundry in Washington have been on strike for over three months.</P>
<P>In addition to calling for higher wages, affordable health insurance and better working conditions, they are fighting for the right to join the union, UNITE. Saturday's rally was sponsored by the Sterling Strike Committee, D.C. Anti-War Network, Black Voices for Peace and the ISO.</P>
<P> Speakers at the rally included Mayra Mendoza, a UNITE organizer for the Sterling campaign; Sexta a worker at Sterling Laundry, Jesse Hagopian, a member of the Washington Teachers Union and the ISO; and Rev. Graylan Hagler, pastor at Plymouth Congregational Church. Protest signs lining the walls of the chapel expressed support for the workers: "We Stand in Solidarity with Sterling Workers" and "An Injury to One is an Injury to All." </P>
<P>Sexta spoke of the conditions in Sterling Laundry, making audience members gasp when she described cleaning hospital laundry without appropriate safety equipment. She also spoke of coworkers who had worked for 20 years without a raise.</P>
<P>"I think it's clear that we've had enough," Hagopian said. "We are seeing an increase in strikes and labor actions like we haven't seen in a while. What we're doing right now is very important--we're laying the groundwork for what can become of our battle."</P>
<P>Rev. Hagler raised the roof and called for a broad-based campaign for the Sterling Workers whose laundry is a five-minute drive from his church. "[Exploitation] is going to end in our lifetime, because we can make a difference," Hagler said.</P>
<P>"It's right in my backyard, it's right in your backyard, and we should not have these types of deplorable conditions existing in any of our backyards." After Hagler's speech, the audience stood and cheered: "El pueblo, unido, jamas ser&aacute; vencido--the people united will never be defeated!"</P>
<P>"I think this [rally] was exceptional, a lot of good positive energy," said Patricia Campos, a representative of UNITE. "These are the people who care about justice, so to get them energized and engaged is very important."  The campaign is currently engaging in ambulatory picketing, which involves following the laundry's delivery trucks to their destinations and picketing customers at their homes and offices to educate them about the workers' struggle.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Borders</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Preston</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANN ARBOR, Mich.--A seven-week strike by Borders workers at the Ann Arbor flagship store came to an end December 30. Delegates from the union represented by UFCW Local 876 accepted a tentative agreement that will be voted on by the membership in early January.</P>
<P>Workers at the Ann Arbor store have been negotiating with the company for over a year. However, Borders' union-busting tactics, intimidation and refusal to bargain on such issues as fair wages, reasonable annual increases and medical benefits sent workers into the streets before the busy holiday season.</P>
<P>The employees at the downtown store are the second group that has sought to organize a union in their workplace. Workers in Minneapolis successfully organized in 2002, but any effort to negotiate a favorable contract at that location has been stalled.</P>
<P>Borders Inc. has been notorious for its union-busting efforts over the past decade. The current CEO feels that a 50-cent raise for an employee who has worked for their company for over 10 years is fair and equitable. This is robbery in the face of their $5.86 per hour base pay.</P>
<P>Although the parameters of the contract have not been disclosed, delegates say they will support an effort to adopt the offer by the membership. Whatever the details of the agreement, its already clear that efforts to make gains for Borders employees will be fought tooth and nail by the corporation. </P>
<P>The only palpable gains towards a decent wage and health care benefits--as well as the right to unionize--will come through a fight and workers' solidarity.</P>

