<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington hawks' new plan for war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 9, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEEK "REGIME change" in Syria and Iran, risk a nuclear war on the Korean peninsula--and punish France while you're at it. That's the advice of two leading Washington "hawks" for how the Bush administration should overcome the problems it faces occupying Iraq. </P>
<P>Richard Perle, a leading "neo-conservative" member of the Pentagon's Defense Policy Advisory Board, and David Frum, the former White House speechwriter who was fired after bragging that he came up with George Bush's "axis of evil" slogan, claim that their book <I>An End to Evil </I>is a "manual" for how to win the "war on terror."</P>
<P>The problem, they believe, is that the U.S. slowed down its military machine--when it should have stepped on the accelerator. Perle and Frum propose world domination in a few easy steps.</P>
<P>For starters, they say, the U.S. should cut off Syria's oil imports from Iraq, raid the country to hunt "terrorists" and seize arms sold by Iran. Next, Washington should give up on talks with Iran and equip Iranian dissidents with arms and military supplies.</P>
<P>The Bush White House should also demand an immediate end to North Korea's nuclear program--and impose an all-out blockade if its government refuses. And just 15 years after the fall of the Berlin Wall, Perle and Frum want Europe divided again. "We should force European governments to choose between Paris and Washington," they write.</P>
<P>Some of this bluster is an attempt to change the subject after the failure of hawks' predictions of a post-war paradise in Iraq. The daily misery and humiliation of living under the thumb of the U.S. military has only strengthened the resistance to occupation in Iraq--and mounting numbers of soldiers U.S. killed and maimed has made a mockery of the neo-conservatives' claim that Iraqis would welcome American troops.</P>
<P>This new neo-conservative manifesto for pre-emptive war will be used by some to justify a vote for the Democrat candidate against George Bush in the November elections. But in fact, Democratic frontrunner Howard Dean isn't opposed to pre-emptive military strikes. He only opposes bragging about them.</P>
<P>"Dr. Dean said he would strike all hard-edged references to pre-emptive strikes in the United States national security strategy--without actually abandoning pre-emption as an option," The <I>New York Times </I>reported. Dean himself stated: "Of course we're going to use our force at our discretion to protect the United States. To say that we've never had a pre-emption policy would be foolish."</P>
<P>The neo-cons around Bush are among the nastiest and most brazen proponents of U.S. imperialism. But the Democrats have their own lineup of imperialist politicians waiting in the wings.</P>



