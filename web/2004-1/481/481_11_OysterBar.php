<P><font face="Times New Roman, Times, serif" size="5"><b>Oyster Bar strikers stand strong in NYC</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sean Petty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 9, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--The striking workers at the Oyster Bar in Grand Central Station have maintained their vigilance as the one-month anniversary of their fight passed just a few days before <I>Socialist Worker </I>went to press.</P>
<P>An average of 50 workers have maintained a daily picket both in front of the restaurant and outside of Grand Central terminal. Metropolitan Transit Authority regulations only allow 11 workers to be outside of the actual restaurant at a given time.</P>
<P>The union, Hotel Employees and Restaurant Employees Local 100, has continued to mobilize actions throughout the holidays. On December 23, about 120 workers, community members and elected officials rallied to tell New York that Michael Harvey, co-owner and general manager of the Oyster Bar, is the real "Grinch who stole Christmas."</P>
<P>This rally was co-organized by the immigrant workers&#130; rights group New York Civic Participation Project (NYCPP). "I think the workers are really united," said Gouri Sadhwani, an organizer with NYCPP. "Especially during the holidays when it&#130;s a very difficult time to be out on strike."</P>
<P>This rally was followed by a number of other actions throughout the holidays. On Christmas Eve, a busload of 40 workers went to the ranch of another of owners, Marlene Brody, in upstate New York to let her know personally how they felt about her demands. Then, on New Year's Eve, Local 100 organized an "Oysters for Justice" campaign where passersby were given free oysters if they pledged not to patronize this union-busting restaurant.</P>
<P>The company is not budging on its demands to institute enormous co-pays for health care coverage for new hires, wage cuts for several job classifications (including dishwashers and bussers), and to have the ability to open up the contract every three months to negotiate the pension. Basically, the Oyster Bar management is trying to render to union irrelevant at this restaurant.</P>
<P>The question remains about the union is going to stop scabs from keeping the restaurant open. But the workers are not giving up on trying to hurt the Oyster Bar in any way possible. Local 100 president Bill Granfield told <I>Socialist Worker</I>, "I think the key to winning is going to be the daily pressure on the picket lines."</P>
<I><P>If you would like to contribute to the strike fund, please mail a check or money order to HERE Local 100, Oyster Bar Holiday Strike Fund, 321 West 44th Street, New York, NY, 10036.</I></P>

