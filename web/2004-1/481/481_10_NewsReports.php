<p><a name="CSF"></a><font face="Times New Roman, Times, serif" size="4"><b>Chicago Social Forum</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Nate Goldbaum</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Activists from many different struggles will come together at the first ever Chicago Social Forum (CSF) January 31 for discussions, debates and building solidarity. Presenters at the CSF will include participants in this year's World Social Forum in Mumbai (Bombay), India, which will have just concluded.</P>
<P>Among the dozens of presenters will be Dr. Quentin Young (Physicians for a National Health Program), Njoki Njehu and Soren Ambrose (50 Years Is Enough/US Network for Global Economic Justice) and Dr. Calvin Morris (Community Renewal Society) and Ahmed Shawki (<I>International Socialist Review</I>).</P>
<P>The CSF takes inspiration from the World Social Forum, which in 2001 attracted over 10,000 people to its first meeting in Brazil and 100,000 in 2003. The social forum movement now includes continental and regional social forums for Europe and the Americas as well as numerous cities. The CSF is the first of several local social forums, from New York City to the Pacific Northwest, underway as a step toward a proposed North American Social Forum in 2005.</P>
<I><P>The Chicago Social Forum will take place Saturday, January 31, from 9 a.m. to 6 p.m. at Jones High School, 606 S. State. For more information or to endorse, call 312-427-2533 or visit www.chicagosocialforum.org.</I> </P>

