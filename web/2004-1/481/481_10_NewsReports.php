<P><p><font face="Times New Roman, Times, serif" size="4"><b>Stop the war in Iraq</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Some 300 people packed the Washington Square United Methodist Church December 11 to hear a report back from military family members and U.S. veterans recently returned from a delegation to Iraq. The event was hosted by United for Peace and Justice.</P>
<P>Sponsored by Global Exchange, the Occupation Watch Center, Military Families Speak Out and Veterans for Peace, the delegation spent a week in Iraq speaking to soldiers and ordinary Iraqis alike about the realities of the occupation. The delegation included military parents Fernando Suarez del Solar, whose son Jes&uacute;s was killed during the invasion of Iraq last spring, and Annabel Valencia, who was able to visit her daughter while in Iraq.</P>
<P>One week later, antiwar and veterans' groups, including Veterans for Peace, organized a march of 50 people in through downtown Brooklyn. Several speakers, having just returned from a one-week visit to Iraq, shared stories of what they had seen and heard while meeting with Iraqis and with US soldiers.</P>
<I><P>Lucy Herschel and Alan Wallis contributed to this report.</I> </P>

