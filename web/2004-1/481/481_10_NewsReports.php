<p><a name="RNC"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest the Republican National Convention</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Grohman</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--It's time to start planning for the Republican National Convention, and to "welcome" it to New York City.  More than 60 activists attended a December meeting of the No RNC Clearinghouse, a place for the different groups planning the protests around the RNC to come together.</P>
<P>The Clearinghouse will currently serve to facilitate these groups coming together, but not as any sort of decision-making process. The meeting was attended by a number of groups, including those representing the homeless, the media, local community coalitions, activists from other countries, United for Peace and Justice and the antiwar group ANSWER. </P>
<P>There was a definite recognition of the "police state" created in Miami for the recent protests against the Free Trade Area of the Americas, and the need to evaluate what that will look like in New York City. A representative of the organization Picture the Homeless spoke of his experience in Miami and the so-called "Miami model" of police repression.</P>
<P>A big part of the planning in progress is trying to find ways of using "creative resistance." Some of the ways this is taking shape include: the building of a giant wall so that people can write down their feelings towards the RNC; a Life After Capitalism Conference with Naomi Klein and others to discuss ways to build toward abolishing capitalism and models for a post-capitalist society; and mass marches throughout the city coming together at Times Square. </P>
<P>In the face of the growing oppression in this country, it is very important that we are organizing tactically as well as strategically. We need public outreach, to invite labor unions to get involved and to make sure that this Clearinghouse is open to everyone who wants to protest the RNC.</P>
<I><P>To find out more, check out www.counterconvention.org, and www.rncnotwelcome.org.</I> </P>



