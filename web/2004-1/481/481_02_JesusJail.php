<P><font face="Times New Roman, Times, serif" size="5"><b>Jeb Bush opens a faith-based jail</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ON CHRISTMAS Eve, Gov. Jeb Bush gave Florida's religious right a generous gift--the nation's first-ever faith-based prison. "I can't think of a better place to reflect on the awesome love of our lord Jesus than to be here at Lawtey Correctional," Bush actually said to inmates. "God bless you."</P>
<P>Bush claims that the new program in Lawtey, Fla., will stop repeat offenders by "leading them to God" with religious studies, choir practice and religious counseling seven days a week. Lawtey will also offer classes in subjects such as "character building."</P>
<P>The program is voluntary, so the prison's 800 inmates were given a chance to relocate if they wished--111 have transferred out so far, but many who stayed said that they didn't necessarily agree with the program, but didn't want to move.</P>
<P>Think a faith-based prison might be a violation of the constitution and the separation of church and state? No way, says programming director Pastor Paul Smith. "The only thing that this program is a violation to is the devil, and the fact he wants to have more souls go to hell," Smith said.</P>



