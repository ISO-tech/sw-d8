<p><a name="Zapatista"></a><font face="Times New Roman, Times, serif" size="4"><b><i>SW</i> was wrong on the Zapatistas</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>I believe that there are several mistakes in the story on the 10th anniversary of the Zapatista uprising (<I>SW</I>, January 2). According to Subcommander Marcos, the Zapatista National Liberation Army (EZLN) was founded by three ladino and three indigenous people, not by 12 members of the group Popular Politics. </P>
<P>Furthermore, the Zapatista National Liberation Front (FZLN) and the EZLN have extreme commitments to democracy, which often makes them somewhat inefficient. While this is undesirable in many ways, this connection with the people has helped to generate widespread support. </P>
<P>Your reference to the EZLN's refusal to try to seize power represents this democratic, and very un-Western, idea of rule by consensus and "leading by obeying." The EZLN has explicitly renounced vanguard movements. </P>
<P>Also, no one really knows the exact power structure of the EZLN and their support communities. However, Marcos is automatically assigned a prominent role in the <I>SW </I>article as ruling over the EZLN and the support communities and groups. Was this done because he is a European-looking male? </P>
<P>The EZLN is ruled by a council, and Marcos is a military commander and speaker, nothing more. There is a reason he refers to himself as a "subcomandante." Personally I would not want to be ruled by any sort of vanguard movement, and I suspect many other left-wingers would not want to be either. If a select elite ruled, would it really be socialism? </P>
<P>Finally, if one examines the revolutionary laws that the EZLN issued on January 1, 1994, especially the agrarian law, it becomes perfectly clear that they <I>are</I> socialists, though maybe not Western socialists. "Indigenous socialist" would be the best way to describe their philosophy. And I think they would want it that way, since they are the ones who have to live with it.</P>
<B><P>Lukas, </B>from the Internet</P>
