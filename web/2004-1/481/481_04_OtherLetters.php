<p><a name="Lawyer"></a><font face="Times New Roman, Times, serif" size="4"><b>Death penalty for the worst lawyer</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>, <BR>
</B>Elizabeth Schulte argues that the rich don't get sentenced to death ("Billionaire killer buys a verdict," <I>SW,</I> November 21, 2003). And that's true, they don't. But the reality of the death penalty is even more stark. </P>
<P>U.S. Supreme Court Justice Ruth Bader Ginsburg herself stated that people who are well-represented at trial do not get the death penalty. Thus, it's not just the rich who avoid the death penalty--it's any defendant who is simply "well-represented."</P>
<P>The best representation money can buy is one thing, but "well-represented" is supposedly what every capital defendant--even the most indigent--should receive under the Sixth Amendment.</P>
<B><P>Ward Larkin, </B>From the Internet</P>

