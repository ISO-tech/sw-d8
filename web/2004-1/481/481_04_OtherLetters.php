<p><a name="Challange"></a><font face="Times New Roman, Times, serif" size="4"><b>A challenge to Corporate America</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
There is an important lesson to be drawn from the struggle by the Southern California grocery workers. Why were these employees forced to strike to preserve their health care and not the other way around? </P>
<P>Safeway and Albertsons management did not shut their stores' doors, refusing to let anyone work or shop, until the employees accepted their demands. This shows the unequal relationship between the two groups: a small number of executives and stockholders have most of the say in the operation of stores, while on the other hand, tens of thousands of workers have virtually no meaningful input or control over their own workplace. </P>
<P>Who sets the prices? Who decides the store hours? Who picks the location for new stores? Not the clerks and baggers! The owners' power derives from <I>owning</I> the grocery stores, the places of employment for tens of thousands of people. </P>
<P>The United Food and Commercial Workers' strike is a challenge to the authority of the corporations. It is an assertion of the employees' desire to have influence over the affairs that directly impact their own lives. </P>
<P>The basic characteristics of the fight in Southern California exist everywhere. Thus, we are faced with a choice: Which side do we stand on? The status quo of corporate power, or the challenge by labor?</P>
<B><P>John Green, </B>Davis, Calif.</P>
