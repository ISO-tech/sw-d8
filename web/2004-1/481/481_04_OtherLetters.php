<P><p><font face="Times New Roman, Times, serif" size="4"><b>Unite and fight against these cruel cuts</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
A group of 14 employees, people with AIDS (PWAs) and their caregivers attended a December 9 meeting of the San Diego County Board of Supervisors to demand that the county not eliminate its AIDS Waiver program, which provides PWAs with in-home services so they can live independently. At the meeting, the county's top appointed official, Walt Ekard, stated that the planned closure of AIDS Waiver is "only the tip of the iceberg," and that the next few months will see cuts in many more county programs due to the state budget crisis.</P>
<P>Ekard's words mean we have to do more. Separate campaigns around several programs will lead to county workers and program consumers fighting each other--and will only reinforce the perception that there are too many needs to be met. </P>
<P>Instead, we need a united campaign across programs, against any and all cuts. The two main unions of county workers, Service Employees International Union (SEIU) Locals 2028 and 535, should immediately begin preparations with their more than 8,000 county worker members.</P>
<P>We should discuss which programs are most likely to be cut, how to fight those cuts and what different programs can do to support each other. We should build a broad community coalition, like the one already in formation at AIDS Waiver. </P>
<P>We should build e-mail and letter-writing campaigns, button-wearing days and lunchtime picketing to a big countywide rally. We should build toward strike readiness to show we mean business.</P>
<P>It's never a problem for the state to find the money to take over another country, to shower tax breaks on the rich, to pay off electric company blackmail, or to renovate the death row at San Quentin. Workers and the communities they serve are always the ones to get the squeeze--unless we fight back.</P>
<B><P>Avery Wear, </B>SEIU Local 2028, San Diego</P>
