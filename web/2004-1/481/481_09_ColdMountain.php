<P><font face="Times New Roman, Times, serif" size="4">Hollywood lies about the Civil War</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Knee deep on <I>Cold Mountain</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Donny Schraffenberger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 9, 2004 | Page 9</font></P>

<font face="Arial, Helvetica, sans-serif" size="2"><P><B><I>Cold Mountain,</B></I> written and directed by Anthony Minghella, starring Nicole Kidman, Jude Law and Renee Zellweger.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WHEN HOLLYWOOD goes to the Civil War, it doesn't know what side to take. Instead of a war for Black liberation from the horrors of slavery, the big movie studios usually churn out ambiguous or sometimes even pro-Confederate crap.</P>
<P>A recent example is the terrible movie <I>Gods and Generals, </I>which makes Confederate Gen. Thomas "Stonewall" Jackson into a saint. The movie glowingly portrays the "righteous" Southern cause--not to keep slavery, but to stop "Northern aggression."</P>
<P>The new movie <I>Cold Mountain</I> isn't as bad. But it's a problem film all the same.</P>
<P>On the surface, <I>Cold Mountain </I>is a tale of two would-be lovers separated by the horrors of war. Jude Law's character, Inman, joins the Confederate Army, even though he is hardly enthralled with the cause. He barely knows Ada (Nicole Kidman), yet they both fall instantly in love, and she vows to wait for his return.</P>
<P>In the movie's only--and likely expensive--battle scene, Inman is stationed outside Petersburg, Va., where the famous Battle of the Crater took place in 1864. He is war weary and longs to go home to Cold Mountain and his love.</P>
<P>During this real battle, the Confederate army had entrenched itself like moles, making any direct attack by the Union Army impossible without taking huge casualties. So the Union Army dug tunnels underneath the Southern trenches and set off a massive explosion to create a giant crater in the Confederate lines. The Union soldiers ran down into the crater and were slaughtered by the surviving Confederates, who shot at them from above.</P>
<P>In an interesting historical aside, Black Union soldiers were specially trained to lead the attack after the explosion. They were supposed to go around the crater, not into it, to be slaughtered. At the last minute, Union generals replaced the Black troops with white ones in the lead--no doubt with racism playing a major part in this decision.</P>
<I><P>Cold Mountain </I>is about the horrors of war, and how war separates people from the ones they love. This might make a good theme for a movie about Vietnam--or the ongoing war and occupation in Iraq. The problem is when it's applied to the American Civil War.</P>
<P>The Civil War was vastly different from the wars the U.S. has fought in the last 150 years since. It was a revolutionary struggle that pitted progressive Northern capitalism, with all of its contradictions, against a completely reactionary slave-owning system.</P>
<P>A Union victory meant the abolition of slavery and the beginning of a halting recognition of Black rights in this country. The war didn't end racism or inequality, but a Southern victory would have meant the continuation and possibly the spread of slavery. </P>
<P>The German socialist Karl Marx sided with the North not because he liked capitalism, but because the North was fighting a war that would end the enslavement of a whole race of people. This is why free Black men signed up to fight for the Union--despite the terrible risk they would face if captured.</P>
<P>In <I>Cold Mountain,</I> the only up-close portrayal of Union soldiers is when they attempt to rape a woman, while they abuse her newborn baby. It's not that there weren't Union atrocities in the Civil War, as there are in all wars.</P>
<P>But there are no counterbalancing scenes that depict real events, such as Union soldiers liberating Southern plantations from slavery. Instead, this scene could have easily fit in with the racist pro-Klan movie <I>Birth of a Nation.</P>
</I><P>Even more incredibly, this movie about the slave South during the Civil War is almost 100 percent lily-white. There are only a few glimpses that the South might have had African Americans living there. In one of the only scenes in which Blacks appear, runaway slaves refuse to give a starving Inman eggs when he pleads for food.</P>
<I><P>Cold Mountain</I> reminds me more of a Harlequin romance than a movie slotted for Oscar contention. It even has stock villains, members of the local Confederate Home Guards, who kill the citizens of Cold Mountain with impunity for harboring runaway rebel soldiers.</P>
<P>Don't waste your money and bruise your butt viewing the two-and-a-half-hour <I>Cold Mountain. </I>Instead, rent <I>Glory, </I>the brilliantly acted movie about a regiment of Black Union soldiers and their struggle to gain respect and fight for freedom.</P>



