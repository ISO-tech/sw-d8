<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The "party of working people" that puts big business first</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why the Democrats are not our party</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 9, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">STANDING UP to George W. Bush. This, we're told, is the main reason for voting for the Democratic candidate for president in 2004.</P>
<P>But "standing up" clearly means different things to different people. For the Democratic Party establishment, it means a candidate who will challenge Bush...but not too much--certainly not so much that they might offend the mythical conservative "swing voters" on Election Day.</P>
<P>So Joe Lieberman, Wesley Clark and Dick Gephardt hammer frontrunner Howard Dean for not being "mainstream" enough. Is he happy enough about the capture of Saddam Hussien? Is he ready to execute Osama bin Ladin without a trial? Is he someone American business can trust? Will he fritter away the budget on social spending? Does he know his Bible?</P>
<P>In other words, the measure of success is how far to the right the Democratic candidate is willing to go. But for the people who actually vote for the Democrats, "standing up to Bush" means more than a "kinder, gentler" version of the Republican Party platform. </P>
<P>That's why Dean's criticism of the invasion of Iraq and his "I'm the Democratic wing of the Democratic Party" rhetoric has attracted many activists to his campaign. But while it's a nice change of pace to see establishment candidates like Lieberman and Clark get flustered about Dean's "insurgency," it won't amount to much.</P>
<P>First of all, despite his campaign talk and rolled-up-sleeves image, Dean isn't much of an insurgent. But more fundamentally, for the Democrats, the disagreements among the leading candidates for the nomination boil down to questions of strategy--not party principles.</P>
<P>Historically, the Democrats have acknowledged the concerns of their liberal base in order win their support--and then ignored them once their votes were reliably sewn up. Because, in the end, the most compelling reason to back the Democrat--whoever it is--will be the fear of another four years of George Bush, not the hope that a Democrat would be a great alternative.</P>
<P>For months already, the heat has been turned up on activists to drop everything and back a Democrat--any Democrat--a lesser evil to Bush. Of course, the Democrats are different from the Republicans on at least some issues of real importance to working people. But this view is too narrow--because it misses the much wider areas of fundamental agreement between Republicans and Democrats.</P>
<P>The machinery of the Democratic Party is greased by many of the same corporate interests as the Republicans. So just as the bosses at IBM dictated the level of Dean's real commitment to workers' rights or the environment while he was the governor of Vermont, you can bet that U.S. corporate interests will guide his actions if he's elected president.</P>
<P>Likewise, the priorities of U.S. imperialism will guide the next president's actions in Iraq and elsewhere--whatever party he's from. The tactics of the parties may differ--multilateral or unilateral, with the United Nations or not--but the ultimate goals are the same.</P>
<P>As for those who believe that left-wing activists can win back the "soul of the Democratic Party," Eugene Debs, who ran for president five times in the early part of the last century on the Socialist Party ticket, had a message for them.</P>
<P>"The radical and progressive elements of the former Democracy have been evicted and must seek other quarters," he said. "They were an unmitigated nuisance in the conservative counsels of the old party. They were for the 'common people,' and the trusts have no use for such a party...[E]very true Democrat should thank Wall Street for driving them out of a party that is democratic in name only, and into one that is democratic in fact."</P>
<P>Debs said that radicals should build an alternative outside the Democrats--a socialist alternative. But he didn't stop there. Debs made it clear that fundamental change wasn't about what happens on Election Day--but about organizing on the streets and in workplaces and communities. The power of a united struggle, he believed, could grow so great that the politicians could not ignore it.</P>
<P>"Too long have the workers of the world waited for some Moses to lead them out of bondage," Debs told a New York audience in 1905. "I would not lead you out if I could; for if you could be led out, you could be led back again. I would have you make up your minds that there is nothing that you cannot do for yourselves."</P>
<P>Protesters who took to the streets around the country to oppose Bush's war on Iraq, striking grocery workers fighting for decent health care--these are the people and struggles that hold the key to changing the political climate in this country. This is how we can really stand up to Bush.</P>



