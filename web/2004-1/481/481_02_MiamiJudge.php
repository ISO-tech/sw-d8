<font face="Times New Roman, Times, serif" size="5"><b>Judge denounces police crackdown</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"A DISGRACE for the community." That's how a Florida circuit judge described police conduct at demonstrations against the Free Trade Area of the Americas (FTAA) summit in Miami in mid-November. Judge Richard Margolius, who attended the protests, decided that he couldn't help but speak out--even though he was presiding over the cases of arrested protesters.</P>
<P>During the anti-FTAA demonstrations, police went on a rampage, arresting 231 people. Since then, at least 27 misdemeanors charges have been dropped. In court, Margolius said he saw "no less than 20 felonies committed by police officers."</P>
<P>"Pretty disgraceful, what I saw with my own eyes," Margolius said. "And I have always supported the police during my entire career. This was a real eye-opener." Margolius said that he would probably have to remove himself from further cases involving the protests. "I probably would have been arrested myself if it had not been for a police officer who recognized me," he said.</P>
<P>The judge also questioned how much the "whole episode" cost taxpayers. "There were police officers from every agency--I couldn't believe the sheer numbers," he said.</P>
<P>Two citizens' panels have scheduled a meeting on January 15 to hear complaints about police conduct during the protests. Amnesty International, the AFL-CIO and the United Steelworkers of America have called for independent probes.</P>



