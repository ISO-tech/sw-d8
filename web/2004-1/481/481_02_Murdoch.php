<P><font face="Times New Roman, Times, serif" size="4">Bush's FCC approves News Corp. buyout</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Murdoch's grip tightens</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MEDIA MOGUL Rupert Murdoch is getting ready to tighten his already powerful grip on what you see and hear. In late December, the Federal Communications Commission (FCC) approved the sale of DirecTV, the number-one satellite broadcaster in the U.S., to Murdoch's News Corp.</P>
<P>The News Corp. empire was already enormous. In the U.S., it controls one of the four major broadcast networks, 11 national and 22 regional cable and satellite channels, 35 television stations that reach almost half of the U.S. population, and production studios that make movies and TV programs--as well as major newspapers, magazines and book companies.</P>
<P>Also among News Corp.'s holdings is PanAmSat Corp., which owns the satellites that send signals to most major cable systems--DirecTV's main competitors. The DirecTV deal puts Murdoch's empire in a class of its own, bigger even than the merged Time-Warner operation. In television especially, News Corp. will have an incredible stranglehold over every nook and cranny of the industry.</P>
<P>The FCC vote caps a years-long crusade by Murdoch. Initially, News Corp. lost a bidding war for DirecTV to EchoStar, the number two U.S. satellite broadcaster. "Murdoch responded with a ferocious lobbying campaign to get regulators to block the EchoStar deal on antitrust grounds," wrote Edward Wasserman in the <I>Houston Chronicle.</P>
</I><P>"Murdoch is reported to have spent $10 million in lobbying from 1999 to 2002, and gave lavishly to political campaigns, more than $1.7 million in the 2000 and 2002 election cycles." Meanwhile, his newspapers and TV channels were openly shilling for George W. Bush and his free-market agenda.</P>
<P>In 2002, Bush's FCC appointees rejected EchoStar's bid for DirecTV, opening the way for Murdoch to snap up the satellite broadcaster--at a substantial savings over the bid he initially made.</P>
<P>Murdoch's empire is enough to make Italian Prime Minister Silvio Berlusconi jealous. Berlusconi is Italy's most powerful media baron, and he's ruthlessly used this control to promote right-wing causes and his own political career.</P>
<P>But Berlusconi has nothing on Murdoch--whose empire spans the globe. Murdoch isn't shy about pushing his political agenda in the media he owns--including the supposedly "objective" news media, as one look at the conservative blowhards and buffoons on Fox News proves.</P>
<P>But Murdoch knows when to abandon principles to make a buck. When he was trying to expand his international Sky TV network into China, Murdoch agreed to censor broadcasts to please the Chinese regime--in particular, yanking the BBC's World Service Television channel, for offending China's rulers.</P>



