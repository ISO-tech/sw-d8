<P><font face="Times New Roman, Times, serif" size="4">Send a signal that we won't go away until justice is done</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The fight to free Mumia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 16, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In 1999, the famous death penalty case of ex-Black Panther journalist Mumia Abu-Jamal entered its final, federal level. Escaping the vicious and vengeful grasp of the criminal justice system in Pennsylvania, with its often blatant anti-Mumia bias, Mumia now stood the chance of meeting fairer judges at the federal district level--with his opportunities to appeal his unjust conviction and death sentence progressively being used up at the same time.</P>
<P>And indeed, in December 2001, a federal judge suspended Mumia's death sentence. But he upheld Mumia's murder conviction, and consequently, both sides appealed.</P>
<P>Contrary to what many people all around the world now assume, Mumia is still in danger of being executed. And there is still the possibility that finally, he will leave prison as a free man after more than 20 years on death row.</P>
<P>Ironically, in the last three years, with the struggle for Mumia's life and freedom approaching its decisive point, the international movement in support of Mumia has taken a sharp and tragic downturn. As the pressure on the movement increased, unity was replaced by dissension, the tolerance that had once guided the movement was supplanted by narrowness, and the broad-based approach that had attracted such a large spectrum of people and had, in fact, made the movement exemplary and powerful, was lost.</P>
<P>This is not the place to analyze the reasons for this unwarranted and extremely untimely development, much less to assign the blame to one or the other current among those who still struggle to save Mumia. Rather, as Mumia turns 50 years old on April 24, and will enter his 22nd year on death row on May 25, it is high time for us to struggle to regain what we have lost, and to rally and unite around the points in Mumia's case that every fair-minded person with some knowledge of the facts and an elementary sense of justice can consent to.</P>
<P>It is in that spirit that one of the German organizations supporting Mumia, the National Network for Mumia Abu-Jamal, has initiated a loose association called The Friends of Justice for Mumia Abu-Jamal.</P>
<P>The "Friends" are unified around a <a href="495_00_FriendsOfJustice.shtml">simple platform</a> stating the elementary facts of Mumia's case, resulting in the demand that the unconstitutional verdict of guilt and death sentence against Mumia Abu-Jamal be overturned; that the evidence, all evidence, in his case be heard by the courts; and that he is either set free after having been imprisoned for more than two decades or given a new trial, whose constitutionality is guaranteed by independent supervision.</P>
<P>No other duties than consent to <a href="495_00_FriendsOfJustice.shtml">this platform</a> are involved. Until Mumia's birthday, we want as many people and organizations to sign the platform of "Friends of Justice for Mumia Abu-Jamal" with their name, to give a powerful signal to the public that this issue won't go away and that we won't rest until Mumia's death sentence and murder conviction are overturned and he is free.</P>
<P>At the same time, there is an additional statement on the latest Pennsylvania Supreme Court (October 8, 2003) decision rejecting yet another one of Mumia's appeals: <a href="495_00_TimeToAct.shtml">"It's Time to Act for Justice for Mumia Abu-Jamal."</a> To sign that too, or ask for more information, e-mail <A HREF="mailto:mikschiff@t-online.de">mikschiff@t-online.de</A>. We hope to collect and publish as many signatures as possible until April 24 and will vigorously continue the drive thereafter.</P>
<P><B>Michael Schiffmann, </B>Heidelberg, Germany</P>

<P><I>For more information, or to sign onto the platform, e-mail <a href="mailto:mikschiff@t-online.de">mikschiff@t-online.de</a>.</I></P>


