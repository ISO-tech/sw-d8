<P><font face="Times New Roman, Times, serif" size="4">Demonstrators across the U.S. challenge the war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We fight in the name of justice"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 16, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANTIWAR PROTESTERS took to the streets across the U.S. last week in emergency demonstrations against the U.S. blitz on the Iraqi town of Falluja and other attacks. In San Francisco, some 1,500 turned out April 10 for a protest called on two days' notice.</P>
<P>The demonstration drew together groups protesting the occupations of Palestine and Haiti as well as Iraq. "Over the past week, we Palestinians have commemorated the Israeli slaughter of Palestinians 56 years ago in the Deir Yassin," Eiyad Kishawi of the Free Palestine Alliance told the crowd. </P>
<P>"As we commemorate our dead we see another occupation just as deadly. Five hundred Iraqis have been massacred by the killers Bush, Rumsfeld and Rice. We are calling for freedom for them, the freedom to live without bombs raining down on their homes and schools. And we are calling for freedom for ourselves, here in the U.S., where our schools are also crumbling. The deficit in the school district of Oakland is equal to the cost of one Blackhawk helicopter. Shame on the Bush administration!"</P>
<P>Pierre Le Bossier of the Haiti Action Committee made a similar connection. "The wars in Iraq and Haiti are like all the wars at home-the war on drugs, the war on crime, the war on terror. They are wars on poor people and people of color, not drugs, not crime, not terror--wars on us! And we will fight them in the name of justice."</P>
<P>One protester, Brice Gamble held a sign with the slogan "Bring our troops home now" and a picture of his brother Jason Gamble, on duty in Iraq as a member of the army reserves. "Without speaking too much for him, I think my brother was looking for a way to pay off his student loans and put some structure into his life. They sell it as one weekend a month, two weeks a year. They never mention that it comes with a price."</P>
<P>Honor Quain, whose son Alex is a corporal in the U.S. Marines, reacted angrily to the idea that Iraqis cannot rule themselves. "It's totally racist," she told <I>Socialist Worker</I>. "I mean, my son is Black--who else is in the military besides minorities?--so I know all about racism."</P>
<P>In West Hartford, Conn., some 75 demonstrators gathered in April 10 for an antiwar protest and to hold a vigil for Rachel Corrie, an American activist with the International Solidarity Movement who was killed in Palestine last year. A day earlier, several hundred people gathered in Times Square at a protest called by the International ANSWER coalition.</P>
<P>Many speakers connected events in Iraq to the U.S.'s other occupations in Afghanistan and Haiti, as well as to Israeli oppression of the Palestinian people. Meanwhile, in Chicago, more than 300 people turned out at Federal Plaza to protest. Several speakers denounced Democratic presidential candidate John Kerry for his plans to send additional troops to Iraq.</P>

<I><P>Jonah Birch, Robin Hodges and Ragina Johnson contributed to this article.</I> </P>


