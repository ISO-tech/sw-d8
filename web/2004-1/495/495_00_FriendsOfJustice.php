<P><font face="Times New Roman, Times, serif" size="5"><b>The Friends of Justice for Mumia Abu-Jamal</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>More than twenty years ago, on May 25, 1983, Judge Albert F. Sabo pronounced the death sentence against the African American journalist Mumia Abu-Jamal:</P>
<blockquote>The punishment shall be inflicted by causing to pass through your body a current of electricity of intensity sufficient to cause death and the application of such current to be continued until you are dead. May God in His Infinite Goodness have mercy on your soul.</blockquote>
<P>Abu-Jamal was sent to the Commonwaelth of Pennsylvania's death row and has, since that time, been fighting to overturn the jury decision that held him culpable of murdering a police officer and sentenced him to death. It was only in the 1990s that the scandalous circumstances that led to this decision became wider known, in the United States as well as internationally.</P>
<P>The case of Mumia Abu-Jamal has since turned into a symbol for the deeply rooted injustices in the judicial system of the most powerful state in the world, the United States of America. In addition to this, during the last few years, an accumulating body of evidence has come to light that points to the actual innocence of Mumia Abu-Jamal.</P>
<P>Although the death sentence against Abu-Jamal was temporarily (the prosecution immediately announced an appeal) lifted by a federal court on December 18, 2001, the verdict of guilty was allowed to stand, and then as now all courts have rejected even to hear the massive evidence for the unfairness of his trial and the accumulating evidence for his innocence. </P>
<P>As the case of Mumia Abu-Jamal is entering its last and final stage before a U.S. Federal Appeals Court, time is running out for the correction of the many injustices in this case to be corrected and justice to be done. The time has come to act, and the time is NOW.</P>
<P>The "Friends of Justice for Mumia Abu-Jamal" are a loose association pursuing the goal of winning justice for Mumia Abu-Jamal. The "Friends" concentrate on the case of Mumia Abu-Jamal since 1) here the many injustices pervading the criminal justice system of the U.S.A. are concentrated in a particularly glaring manner, 2) in this case, there is a justifiable fear that it is about silencing a political dissident for good by putting him to death, and 3) "an injustice against one is an injustice to all."</P>
<blockquote><B>In this spirit, the Friends of Justice for Mumia Abu-Jamal (FJMAJ) demand that the unconstitutional verdict of guilt and death sentence against Mumia Abu-Jamal be overturned, that the evidence, all evidence, in his case be heard by the courts, and that he is either set free after having been imprisoned for more than two decades or given a new trial whose constitutionality is guaranteed by independent supervision.</B></blockquote>
<P>Member of the FJMAJ is everybody who agrees to the publication of his/her name in support of the present text. The FJMAJ will have a board consisting of some of its more active and/or prominent members aiding in its activities and helping it to get more publicity. For the purpose mentioned above, the FJMAJ will, from time to time, issue declarations commenting on the development of the case. These are subject to the consent of those on the board, and the widest possible support for them in the form of signatures will be sought.</P>
<P>For the time being, the FJMAJ will be coordinated by three supporters of justice for Mumia Abu-Jamal: Jakob K&ouml;llhofer, German-American Institute, Heidelberg; Michael Schiffmann, Freedom for Mumia Abu-Jamal, Heidelberg; Sabine Schubert, Action Coalition for Mumia Abu-Jamal, Berlin. Their contact information is given below.</P>

