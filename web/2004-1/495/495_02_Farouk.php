<P><font face="Times New Roman, Times, serif" size="5"><b>Defeat the witch-hunters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 16, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A victory for all of the immigrant detainees in George Bush's "war on terror," a federal judge last week ordered New York-based Palestinian activist Farouk Abdel-Muhti to be released. As <I>Socialist Worker </I>went to press on April 12, 2004, Abdel-Muhti had been released from an Atlanta penitentiary and was scheduled to board a plane to return home to New York City.</P>
<P>Federal officials secretly moved Abdel-Muhti from detention in New York to Atlanta--more than 800 miles from his family, friends and legal team--on April 5, holding him there virtually incommunicado. His supporters only learned of Abdel-Muhti's whereabouts last weekend, when he was finally allowed to call.</P>
<P>Abdel-Muhti was first detained in April 2002, one month after he began to work at the left-wing radio station WBAI in New York City, arranging live interviews with Palestinians living in the West Bank. He has been transferred numerous times to keep him away from family and supporters, and he has been assaulted and harassed by guards.</P>
<P>After almost two years of stalling, the government finally had to respond to Abdel-Muhti's lawyers last month. U.S. District Judge Yvette Kane ordered authorities to free Abdel-Muhti, agreeing that he was a "stateless man" who cannot be deported to the Occupied Territories, as the federal government has argued.</P>
<P>In her decision, Kane cited "the numerous letters of support and affidavits submitted in support of Petitioner." The Committee to Free Farouk Abdel-Muhti has organized rallies, letter-writing and other activities against this unjust detention. Abdel-Muhti's release marks a tremendous victory against the Bush administration's war on immigrants and activists.</P>
