<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Uprisings in Iraq raise the specter of Washington's defeat</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is this Bush's Vietnam?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 16, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE PRESIDENT and his men fabricated a justification for an all-out war halfway around the world--and then claimed that the U.S. was fighting for "freedom." But the war was exposed as an unjust disaster that the U.S. was losing when resistance fighters took the offensive in a surprising show of strength.</P>
<P>Two months after Vietnamese liberation fighters launched the Tet Offensive in January 1968, Lyndon Johnson announced that he wouldn't run for re-election as president--essentially choosing to retire rather than answer for Vietnam. Will history repeat itself? Will George Bush be taken down because of his war for oil and empire in Iraq?</P>
<P>The resistance in Iraq today is nowhere near as advanced as Vietnam's national liberation struggle, and the U.S. military hasn't been hit as hard in Iraq today. Nevertheless, the depth of the crisis facing the Bush administration can be measured by the fact that even the mainstream media are using the "V" word--and asking if Iraq is "Bush's Vietnam"?</P>
<P>The White House may still claim to be bringing "democracy" to Iraq and the whole Middle East. But there was no more certain image of what the U.S. represents in Iraq today than the pictures from Firdos Square in Baghdad last week--one year after a statue of Saddam Hussein was pulled down by a U.S. military vehicle as a few hundred Iraqis watched, becoming television's chief image of the fall of the regime.</P>
<P>This year, ABC News video footage showed the square completely empty--as a U.S. military vehicle on patrol broadcast a message that anyone who gathered there would be shot. There is no democracy in Iraq--nothing even approaching it. </P>
<P>George Bush clearly hoped that his inflated wartime popularity would be his ticket back to the White House. But the crisis of the occupation is becoming the administration's biggest election problem. One recent opinion poll--taken before the height of the fighting last week--found that a solid majority of people disapprove of Bush's handling of the Iraq war--and nearly a majority think that U.S. troops should be withdrawn.</P>
<P>If Bush has any reason for hope, it lies in the pathetic "opposition" he faces within mainstream politics. Democrats briefly came to life last week to criticize the White House as reports of the erupting violence in Iraq emerged.</P>
<P>But what are the Democrats proposing? <I>More </I>U.S. troops in Iraq--and an extension of the occupation.</P>
<P>Incredibly, the Democrats are criticizing Bush for setting "an arbitrary deadline" of June 30 for the supposed "handover" of power to the Iraqi Governing Council. But the "handover"--to an "interim" council handpicked by the U.S.--was always a joke. </P>
<P>Bush may have wanted to be able to install his stooges by June 30, well ahead of the November election, but he has little to lose from delaying this date. If he does, he will have the Democratic Party to thank for covering his line of retreat--and opening up an indefinite period to try to strangle all opposition in Iraq.</P>
<P>All this confirms a basic fact about the U.S. political system--that the Republican and Democratic Parties aren't all that different. They represent two wings of a common power structure that is united in its commitment to furthering the reach of U.S. military, political and economic power. On most important issues, they differ only on the details, not on the aims--or even the general methods for achieving those aims.</P>
<P>The real opposition to Bush won't come from within official Washington. But this is exactly why Bush is facing the most serious threat of his presidency. The armed uprisings last week raised the specter of the U.S. being driven out of Iraq.</P>
<P>It may regain the upper hand and impose the appearance of stability--even enough to go ahead with the "handover." But Washington can't arrive at a long-term solution because it can never address the glaring injustice at the heart of the conflict--that the U.S. government is ruling Iraq, not the Iraqi people. As the U.S. learned in Vietnam, occupation and oppression breed resistance--and even the world's most powerful military can't crush it.</P>
<P>Being forced to abandon Iraq will be a huge blow to Washington's imperialist agenda. In that sense, Iraq won't be "Bush's Vietnam" alone--but another Vietnam for the whole Washington establishment. Our job should be to hasten the day when the war makers in Washington have to admit defeat and end the occupation.</P>


