<P><font face="Times New Roman, Times, serif" size="5"><b>Iraq's uprising against occupation</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 16, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">REBELS ARE defying the U.S. killing machine in Iraq--and despite the butchery by Marines in Falluja, their struggle for national liberation will continue. Fighting had lessened in Falluja as <I>Socialist Worker </I>went to press this week, but not before U.S. forces blasted through the city, bombing a mosque and shooting up residences, killing an estimated 600 people.</P>
<P>"During the course of roughly four hours at a small clinic in Falluja, I saw perhaps a dozen wounded brought in," U.S. author and activist Rahul Mahajan said from Iraq. "Among them was a young woman, 18 years old, shot in the head. She was having a seizure and foaming at the mouth when they brought her in; doctors did not expect her to survive the night. Another likely terminal case was a young boy with massive internal bleeding.</P>
<P>"Makki al-Nazzal, a lifelong Falluja resident who works for the humanitarian NGO InterSOS, had been pressed into service as the manager of the clinic, since all doctors were busy, working around the clock with minimal sleep. He told us about ambulances being hit by snipers, women and children being shot. Describing the horror that the siege of Falluja had become, he said, 'I have been a fool for 47 years. I used to believe in European and American civilization.'"</P>
<P>The U.S. iron fist first crashed down on Falluja a year ago, when troops shot and killed at least 17 unarmed demonstrators. This has spurred resistance ever since--and this time, the U.S. is determined to destroy it once and for all.</P>
<P>"Diplomacy is just talk unless you have a credible force to back it up," Marine Lt. Col. Brennan Byrne told a reporter. "People will bend to our will if they are afraid of us." But fear gave way to fury across Iraq over the killings in Falluja--and people across Baghdad and cities around central and southern Iraq joined the Shiite Muslims militias of Moqtada al-Sadr in launching attacks on U.S. and coalition troops.</P>
<P>Shiite and Sunni Muslims united as Iraqis to fight as growing hatred of the U.S. occupation spilled over into widespread rebellion. Even Washington's puppets on the Iraqi Governing Council denounced the Falluja siege as "collective punishment." Now U.S. troops--increasingly bitter and demoralized--are being ordered on extended deployments, to be killed or maimed in Washington's war.</P>
<P>"Despite U.S. officials' claims that the uprisings have no grassroots support, the public's adherence to a cleric's call for a general strike demonstrates just how much the relationship between Americans and Iraqis has deteriorated in the past few weeks," the <I>Wall Street Journal </I>reported. "The streets of Baghdad were largely empty over the weekend, with the majority of businesses closed. Schools, universities and government buildings also shut down."</P>
<P>The Iraqi people are right to take up an armed struggle against foreign invaders who aim to grab their country's oil--and create a puppet state to consolidate their grip on the Middle East. Conquest for oil and empire--those are the real aims of the U.S. in Iraq. That's why we support the Iraqi resistance--and demand an end to the occupation. Bring the troops home now!</P>


