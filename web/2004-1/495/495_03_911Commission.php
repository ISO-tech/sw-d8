<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What the 9/11 commission won't ask</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 16, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE GASPS could be heard throughout the audience that included family members of September 11 victims. "I believe the title was, 'Bin Laden Determined to Attack Inside the United States,'" was National Security Adviser Condoleezza Rice's answer at the hearing of the commission investigating the September 11 hijackings.</P>
<P>She had to be asked several times before she repeated the title of the now-declassified Presidential Daily Briefing presented to George W. Bush on August 6, 2001--one month before planes hit the World Trade Center. Now the administration that wrapped itself in the flag and exploited the tragedy to launch a worldwide "war on terror" is under fire for its pre-September 11 intelligence blunders.</P>
<P>After months of stalling, the Bush administration finally let Rice testify before the commission last week. Her appearance came after Bush's former counterterrorism czar Richard Clarke spilled the beans about the Bush administration's obsession with linking September 11 to Iraq, even when it was clear that the al-Qaeda network was responsible.</P>
<P>The president "grabbed a few of us and closed the door to the conference room," Clarke writes in his tell-all book <I>Against All Enemies.</I> "'Look,' he told us, 'I know you have a lot to do and all...but I want you, as soon as you can, to go back over everything, everything. See if Saddam did this. See if he's linked in any way.'</P>
<P>"I was once again taken aback, incredulous, and it showed. 'But, Mr. President, al-Qaeda did this.' 'I know, I know, but...see if Saddam was involved. Just look. I want to know any shred...'" The Bush gang got the war on Iraq they wanted--by using the memory of September 11 and adding a few lies about Saddam Hussein's "weapons of mass destruction."</P>
<P>It's a pleasure to see the Bush administration squirm. But in the end, this commission won't recommend that the Bush administration pay for its lies and manipulations.</P>
<P>Instead, the commission will likely recommend many of the same repressive policies that Bush and his pals support. That means more USA PATRIOT Acts, more orange alerts, more deportations and harassment of Arabs and Muslims, more workers being told to tighten their belts while Washington shovels money into the Pentagon sinkhole.</P>
<P>No repressive measures will stop the threat of terrorism against the U.S.--not because of intelligence errors, but because of what the U.S. government does every day around the world. Its bloody occupation of Iraq serves as only the latest example of the fact that the U.S. is the world's greatest purveyor of violence--and terror.</P>
<P>This reality--that the violence of the U.S. government stokes anger and despair around the world--won't be taken up by the 9/11 commission. But it should be by opponents of Bush's "war on terror."</P>
<P>"Terrorism, by definition, strikes at the innocent in order to draw attention to the sins of the invulnerable," foreign policy expert Chalmers Johnson wrote in his prophetic book <I>Blowback, </I>published a year before September 11. "The innocent of the 21st century are going to harvest unexpected blowback disasters from the imperialist escapades of recent decades.</P>
<P>"Although most Americans may be largely ignorant of what was, and still is, being done in their names, all are likely to pay a steep price--individually and collectively--for their nation's continued efforts to dominate the global scene."</P>


