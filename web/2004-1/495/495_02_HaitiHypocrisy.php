<P><font face="Times New Roman, Times, serif" size="4">U.S. rejects inquiry into toppling of Aristide</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Powell's Haiti hypocrisy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 16, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. isn't making of mockery of justice in Haiti--it's simply trampling on it. In early April, Secretary of State Colin Powell traveled to Port au Prince, Haiti's capital, to monitor the military occupation that began with the U.S.-engineered coup on February 29 that overthrew Jean-Bertrand Aristide, Haiti's democratically elected president.</P>
<P>While in Haiti, Powell announced that the U.S. was considering whether Aristide should be brought up on corruption charges. "There are inquiries being made by our judicial authorities in the U.S. to see if there is any evidence of wrongdoing on his part," said Powell.</P>
<P>Powell made the announcement during a joint press conference with G&eacute;rard Latortue, Haiti's newly installed prime minister. Meanwhile, Powell rejected the demand by the 15-nation Caribbean Community, or Caricom, for a United Nations investigation into the events surrounding Aristide's removal from power. "I don't think that any purpose would be served by such an inquiry," said Powell.</P>
<P>Incredible--Powell wants an investigation into Aristide's alleged corruption, but won't tolerate an inquiry into the coup that ended Aristide's presidency! "This shows how much they fear the truth getting out," Ben Dupuy, secretary general of the National Popular Party and contributor to the weekly newspaper <I>Haiti Progres, </I>said to an April 7 rally of 2,000 people at the Center for the Performing Arts at Brooklyn College.</P>
<P>"To add insult to injury, the U.S. is promoting diversionary investigations into Aristide's alleged drug trafficking, human rights abuses and corruption. Meanwhile, to carry out their coup, Washington is collaborating with death-squad leaders and soldiers universally recognized as corrupt drug-dealing human rights abusers."</P>
<P>Powell's call for bringing Aristide up on charges echoes the demands of the right-wing rebels who began the armed revolt in northern Haiti that culminated in the coup. Latortue, who is actually a resident of Boca Raton, Fla., and only recently returned to Haiti, gave a speech in the city where the right-wing rebellion began, Gona&iuml;ves, calling the rebels "freedom fighters" as they chanted "Arrest Aristide."</P>
<P>The growing focus on prosecuting Aristide's inner circle comes as Haiti's new justice minister floated the idea of pardoning Jean-Pierre Baptiste, a rebel leader with close ties to the new government, who was convicted of participating in a massacre in Gona&iuml;ves in 2000. As Joanne Mariner, deputy director of Human Rights Watch's Americas Division, put it, "The contrast between the Haitian government's eagerness to prosecute former Aristide officials and its indifference to the abusive record of certain rebel leaders" is alarming.</P>


