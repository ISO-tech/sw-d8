<P><font face="Times New Roman, Times, serif" size="4">One year behind bars for supporting Palestinian rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The witch-hunt of Sami Al-Arian</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAMI AL-ARIAN is marking a grim anniversary this month. One year ago on February 20, the former University of South Florida computer science professor was arrested on dozens of charges, including racketeering, conspiracy and materially aiding supposed "terrorists."</P>
<P>In truth, the government witch-hunt against Al-Arian has nothing to do with "terrorism." He's being targeted for supporting the right of Palestinians to resist Israel's brutal occupation.</P>
<P>Al-Arian--along with Sameeh Hammoudeh, Hatem Fariz and Ghassan Ballut--were arrested last year and accused of using an academic think tank, a Muslim school and a charity as a cover for raising money for the Palestinian Islamic Jihad (PIJ), a group that the U.S. State Department has labeled a "terrorist organization." But many of the charges against Al-Arian and the others stem from their alleged support of the PIJ <I>before</I> the U.S. declared it a terrorist group.</P>
<P>Until 1995, it wasn't even illegal to raise money for the PIJ or even be an active member. But once the organization was designated a terrorist group in 1995 by Bill Clinton, Al-Arian came under fire. His home and office were raided by the FBI, and he was investigated by both the University of South Florida and a federal court.</P>
<P>No evidence was found that either of the charitable organizations Al-Arian headed were connected to the PIJ. But that didn't stop Ashcroft from unleashing this latest witch-hunt--after University of South Florida President Judy Genshaft landed the first blow by firing Al-Arian. </P>
<P>As Al-Arian told <I>Socialist Worker</I> in 2002, "This is an orchestrated political campaign by people who would like to settle political scores, and they found an opening after September 11. They basically did away with the guarantees of the Constitution and the Bill of Rights. Freedom of speech, academic freedom, due process--all these values were put aside."</P>
<P>Today, prosecutors are pressing to use an anonymous jury for Al-Arian's trial and are refusing to provide the defense translations of the more than 21,000 hours of prosecution wiretap tape recordings in Arabic--or the numerous documents in Arabic and Hebrew that are the bulk of their supposed "evidence." In fact, some of the documents that the prosecution cites as evidence are still in the hands of Israeli authorities!</P>
<P>While he waits to go on trial, Al-Arian is being subjected to inhumane prison conditions. According to lawyer William Moffitt, Al-Arian is being kept in a lockdown prison ward, where officials have prohibited his family from visiting and have cut his telephone privileges to one call per month.</P>
<P>And his case isn't scheduled to go to trial until January 2005. "We're talking about treating this man like this for two years,'' Moffitt told the <I>Tampa Tribune</I> in November. "Today, Sami Al-Arian. Tomorrow, maybe a member of your family. We believe these are the civil rights cases of the 21st century. It tells us a lot about the type of society we're going to be post-9/11." </P>

