<P><font face="Times New Roman, Times, serif" size="4">Will UN give cover for the occupation?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. out of Iraq now!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BOMBINGS at Iraqi police stations earlier this month have underscored the crisis of the U.S. occupation of Iraq--and made Washington more eager to use the United Nations (UN) for political cover. According to UN Secretary General Kofi Annan, the UN will help the U.S. "to find a mechanism to create the caretaker government" that Washington wants to install by June 30.</P>
<P>That way, George W. Bush hopes to hide the growing disaster of the occupation behind a handpicked Iraqi government--in hopes of getting another four years in the White House. "Many in the administration say that while they have no proof that the urgency to install a government is politically motivated, it feels to them like part of a White House plan to permit President Bush to run for re-election while taking credit for establishing self-rule in Iraq," the <I>New York Times </I>reported.</P>
<P>Whether or not the UN gives its blessing, the Iraqi "transitional government" will remain a U.S. puppet state--and the occupation will continue. That's why it's more important than ever to build a movement here at home to get the U.S. out of Iraq.</P>
<P>March 20 demonstrations in San Francisco, New York City, Washington, D.C., Chicago and elsewhere can be part of the effort to build a movement against the occupation--not only of Iraq, but Afghanistan and Palestine as well. In Fayetteville, N.C., military families and veterans will gather at Fort Bragg to send a message: Bring the troops home now!</P>
<P>"We called Saddam evil for killing Iraqis, yet we are responsible for 8,000 to 10,000 deaths ourselves, many of them innocent civilians," Lou Plummer, of Fayetteville Peace with Justice and Military Families Speak Out, said in a recent statement. </P>
<P>"The 500-plus U.S. soldiers who have been killed and the thousands of wounded and disabled service members are victims of a government that does not care about them. We're holding the march here in Fayetteville to show the world that there are military families and vets in solidarity with the millions of others who are negatively affected by war and occupation in Iraq, and by its consequences at home."</P>


