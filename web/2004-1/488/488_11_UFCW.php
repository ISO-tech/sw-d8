<P><font face="Times New Roman, Times, serif" size="4">California grocery strikers kept in the dark</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>UFCW headed for a deal?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--The four-month-old strike and lockout of grocery workers could be winding down, according to press reports at a bargaining session. Representatives of the United Food and Commercial Workers Union (UFCW) and the Safeway Inc., Kroeger Co. and Albertsons Inc. companies had held continuous meetings for nearly two weeks as <I>Socialist Worker</I> went to press.</P>
<P>Some 59,000 workers have been on strike or locked out since October 11-12; another 11,000 workers employed at regional chains will be covered under the terms of the main contract. Yet many union activists are also worried that members may approve a contract with too many concessions simply because they are exhausted from a long and demoralizing strike--and that they won't be given sufficient time to review the contract before voting.</P>
<P>Numerous mistakes in strike strategy by the UFCW leadership have contributed to the fatigue of UFCW members. First, the leadership weakened the strike's power by removing picket lines from Ralphs grocery stores. They have failed to mount any pressure on the companies at a national level through boycotts or job actions outside of Southern California.</P>
<P>They pulled picket lines from most of the distribution centers after one month, and the Teamsters union has, shamefully, allowed its truck drivers to continue deliveries. As a result, the grocery stores have been able to continue doing significant business during the strike, despite some big losses.</P>
<P>Workers, by contrast, have been hard hit. In late December, strike pay for most union members was cut in half to just $125 per week, forcing many to find other jobs. Thousands of others have applied for hardship funds from the union because of the threat of evictions, foreclosures, and even the prospect of giving up their children to relatives or spouses. The union itself reports that 9 percent of its members are crossing the picket line.</P>
<P>In another disastrous move Saturday, the union pulled dozens of its members off the picket line and paid them $10 an hour to campaign door-to-door for Democrat Karen Bass for city council. "My union rep told me it was for 'phone work,'" Albertsons employee and UFCW member LaTanya Acosta told <I>Socialist Worker</I>.  "When I found out that it was political, I thought, 'this isn't right.'"</P>
<P>These mistakes in strategy are not accidents, but come from the UFCW's history of building partnerships with management and Democratic Party politicians instead of organizing the power of the union's rank and file. In a recent interview with the <I>Los Angeles Times</I>, UFCW Local 770 President Rick Icaza revealed that he had been totally unprepared for the strike.</P>
<P>Icaza said that he "had always worked out solutions with the stores &#151; and expected he could again.  I felt that by having that relationship ... we had passed the era of a need for strikes." Icaza, already a multimillionaire from his investments in California real estate, plans to retire in 2006 but has continued to earn his salary of $270,000 during the strike.  </P>
<P>After everything they have sacrificed for this strike, union members should demand that they are given sufficient time to review the contract before voting. And they should not accept a contract that institutes a two-tier wage system or gives away too much on health care and pensions.</P>
<P>In January, rank-and-file union members circulated a petition demanding more information from their union leadership, a more aggressive strike strategy, and a democratic process for approving the contract. More of this kind of initiative from below will be needed now and after the strike to transform the UFCW into a fighting, democratic union.</P>


