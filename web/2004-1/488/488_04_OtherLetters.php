<p><a name="Protest"></a><font face="Times New Roman, Times, serif" size="4"><b>We need protest, not the Democrats</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
<I>SW</I> should be proud to be one of the only publications on the left today that not only has the courage to tell the truth about Bush, but also about the Democratic Party. The Democrats are against Bush the man, but not his main goals: helping the rich at taxpayers&#130; expense, waging wars for oil and empire, slashing social spending and incarcerating minorities. </P>
<P>In addition to pointing out the Democrats' betrayals of the left, Blacks, women, gays and working people, we should add another weapon to our arsenal of arguments against voting for the "lesser evil." Voting for the Democrats didn't stop Bush in 2000. While Bush was throwing out Black votes in Florida, Al Gore kept his mouth shut about it and instead whined about "chads." </P>
<P>Millions of people who voted against Bush in 2000 looked on helplessly as Bush slithered into the White House, despite the popular vote.The only way we could have stopped Bush from stealing the White House last time was through massive protests, demonstrations, walkouts and strikes. </P>
<P>Today, our greatest hope for change is not Democrats who talk left and walk right, but the actions and organization of ordinary people: workers and guerillas in Iraq, protestors booing Bush at Martin Luther King's grave or striking grocery workers in California.<br>
<B>Pham Binh,</B> New York City</P>

