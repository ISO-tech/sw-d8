<p><a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Building the fight for abortion rights</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> </B><BR>
Nationally the pro-choice turnout on the January 22 anniversary of <I>Roe v. Wade</I> may have been pathetic, but here in Providence, abortion rights activists took to the streets to defend two clinics against the right-wing zealots. With little more than a day's notice for some, about 50 people rallied in front of Planned Parenthood on Point Street to confront a "funeral for the unborn" that was organized by the religious right. </P>
<P>The action was very spirited, with many signs advertising the April 25 March for Women's Lives in Washington, D.C. People chanted, "My body, my choice!" and "Bush says go back, we say fight back!" Many folks were angry at Bush's ban on late-term abortions.</P>
<P>When the hearse and funeral procession arrived, the somber crowd of bigots was greeted by shouts of "Pro-Life? That's a lie: You don't care about women's lives!" The raucous group of activists outnumbered the morose coterie, who were visibly shaken by the confrontation. </P>
<P>Another abortion clinic targeted by the right in Providence was also defended. Standing strong against the sexists is the only way to win full rights to abortion. In fact, the energetic response has helped galvanize organizing for the demonstration in Washington, D.C.<br>
<B>John Osmand,</B> Providence, R.I.</P>

