<p><a name="4more"></a><font face="Times New Roman, Times, serif" size="4"><b>We can't take four more years of Bush</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I am a fan of your Web site. Your refreshingly original and true-to-life view of the world at large and especially the United States is so unlike the image of U.S. that an outsider like me has come to encounter time and again. However, I have noticed a strange ambivalence on your part as to who among the candidates for the U.S. presidential election you support. </P>
<P>There are wildly divergent views from various column writers, who seem to criticize anybody and everybody in the race, and I wonder who they stand for today--when merely counting on principles does not help. You need to find at least a majority of the principles in some candidate that is still running. </P>
<P>As an opinion-maker and view-mobilizer, your paper carries a social responsibility, because I see that the campaign is presently being hijacked by media opinions and inane issues like the Dean "grunt." </P>
<P>Many who trust your paper would be helped, if you--in your clear style of calling a spade a spade--would spell out an unbiased commentary on each candidate on various issues of national interest, finally giving out why you would recommend a particular candidate. Please do this, for a nasty U.S. president would be a liability not only for the U.S., but for the entire world.<br>
<B>Krishna Dharasurkar</B>, Pune, India</P>

