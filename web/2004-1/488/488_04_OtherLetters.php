<p><a name="Kucinich"></a><font face="Times New Roman, Times, serif" size="4"><b>Kucinich enables the Democrats</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In response to Walt Creekmore's letter (<I>SW,</I> February 6) about Dennis Kucinich: it is good that Kucinich is a good union man. The problem lies with the fact that he is enabling a political party that is not a friend of workers or our unions, but a friend of corporations.</P>
<P>He is putting out left rhetoric to pull people back into the Democratic Party, but as he will not get the nomination, that will leave the people he pulled in to support the "lesser evil"--whoever that will be. Democrats like Harry Truman have stabbed workers in the back. Truman claimed to oppose the anti-union Taft-Hartley Act, but used it to stop a miners' strike, as did Jimmy Carter during his presidency.</P>
<P>Franklin Roosevelt granted reforms like the Wagner Act giving the right to organize, Social Security, welfare, etc., not because he had good politics, but because of the political pressure from the grassroots movements from workers and the poor and unemployed. </P>
<P>We need a political alternative to the twin corporate political parties--ultimately, a socialist alternative. Until this happens, we will be repeating the same process every four years, and we need to stop the poverty, attacks on living standards, union busting, racism, war and everything that is institutionalized in the capitalist system.<br>
<B>Patrick Switzer,</B> Seattle</P>

