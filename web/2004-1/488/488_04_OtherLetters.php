<P><font face="Times New Roman, Times, serif" size="4"><b>Kerry: Arrogant and elitist</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Now that he's emerged as the Democratic frontrunner, Sen. John Kerry (D-Mass.) is busy retooling himself as a man of the people. Here's a different view: Twenty years ago, when Kerry first ran for the Massachusetts senate seat, I worked as a secretary for his campaign treasurer.</P>
<P>Every day when the candidate passed by my desk on his way to see my boss, I said, "Good morning, Mr. Kerry," "Good afternoon, Mr. Kerry," and "Here's your coffee, Mr. Kerry." Know what? Not once did Kerry bother to reply</P>
<P>My boss, by the way, was a real-estate developer who repackaged low-income housing as tax shelters for rich investors like Kerry. It was a sick business that only brought more misery to New York City's housing projects. My boss was eager to serve as Kerry's campaign treasurer, explaining to me that this was how "politics" works: "You pay them [the politicians], they do what you want."</P>
<P>History tells us that the only way ordinary people have ever gotten what they want and need is through mass, independent movements. That's what we need now: an independent movement that can represent our--not the bosses'--interests.<br>
<B>Nancy Welch,</B> Burlington, Vt.</P>

