<P><font face="Times New Roman, Times, serif" size="4">Aristide in the crosshairs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. gives green light to Haiti's killers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON IS pushing for regime change in Haiti--by another name. While calling publicly for negotiations, it has given a green light to the right-wing armed rebellion against President Jean-Bertrand Aristide.</P>
<P>Aristide agreed to the U.S. ultimatum for a deal that will bring the right back into Haiti's government. But the opposition refused, giving time for armed thugs to continue their bid to seize power.</P>
<P>Secretary of State Colin Powell couched the U.S. deal as diplomacy. But the proposal would leave Aristide--elected to a six-year term in 2000--as president in name only. Under the terms of the U.S. proposal, Aristide's cabinet and control of the police force would be subject to the approval of the opposition's Group of 184--an amalgamation of big business chiefs, hangers-on of the former military regime and a few former leftist groups.</P>
<P>The U.S. deal would allow a three-person committee--one representing Aristide, another the opposition and a third from an "international committee"--to select a seven-person commission that would choose a prime minister and the government. This would open the way for the U.S.--operating through the Organization of American States and the Caricom group of Caribbean nations--to effectively choose Haiti's new leaders, while still paying lip service to the result of Haiti's democratic elections.</P>
<P>Aristide accepted this proposal, including an agreement to disarm his supporters. But the opposition, sensing a possible victory after rebels took control of the important city of Cap Haiten, has refused to approve the agreement until Aristide resigns.</P>
<P>The opposition claims to represent the interests of civil society, accusing Aristide of behaving like a dictator following disputes over the 2000 parliamentary elections. In fact, even if the disputed Senate seats had gone to the opposition, Aristide's Lavalas Family party would have retained control.</P>
<P>But the controversy provided political cover for a U.S. aid boycott and opposition political activities--not just the protests and bosses' strikes reported in the U.S. media, but assassinations and military raids by commandos from the former dictatorship, based in the neighboring Dominican Republic. These ex-military and death-squad elements were tied to the former military government that overthrew Aristide's first presidency in a 1991 military coup.</P>
<P>Aristide, who rose as a mass leader of Haiti's poor, was returned to office by U.S. troops in 1994. He made a deal with Washington to carry out free-market "reforms" at the expense of social programs--and concluded a similar agreement with the International Monetary Fund after he was re-elected in 2000.</P>
<P>This turn to the right undermined his popular base--and Aristide has increasingly relied on a patronage network for political support. This gave the opposition the space to posture as defenders of "democracy"--a necessary maneuver, given the widespread hatred of the now-disbanded military and the wealthy backers of the 30-year Duvalier dictatorship that fell in 1988.</P>
<P>Now, however, the killers have come out into the open as leaders of the rebellion--murders like Guy Philippe, a former Army captain, and Louis-Jordel Chamblain, a former leader of FRAPH, a death squad responsible for killing hundreds after the 1991 coup. The disaffection with Aristide--along with support of one-time left-wing groups like the MPP, or Popular Peasant Movement--has allowed the rebels to seize power easily in the north of the country, with no resistance from the poorly armed police.</P>
<P>The U.S., which never trusted Aristide because of his roots in the mass movement, won't reign in the rebels until they bring the Haitian president to heel. And Aristide's willingness to bow to Washington's outrageous demands has further demoralized his supporters.</P>
<P>The stage is set in Haiti for a potentially bloody confrontation in the capital of Port au Prince. Defeating the right-wing power grab means not only defeating the rebels, but rebuilding Haiti's left and social movements to fight for the interests of workers and the poor.</P>


