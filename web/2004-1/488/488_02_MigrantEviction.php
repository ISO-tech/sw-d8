<P><font face="Times New Roman, Times, serif" size="5"><b>Migrants evicted from California camps</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FEBRUARY 17 was a shameful day for the city of Carlsbad, Calif. Famous for its lush flower fields, the Carlsbad city government--along with local landowners such as the San Diego Gas and Electric Co.--forcefully evicted 70 migrant workers from makeshift camps set up in the canyons near coastal farms. </P>
<P>The workers, who can labor up to 10 to 12 hours a day, usually earn the minimum wage or less, which means they can't afford the expensive local market. To stay close to the fields, workers have set up camps, using salvaged plywood, recycled mattresses and other discarded material. But for Carlsbad's city officials, the camps were an inconvenient eyesore.</P>
<P>Immigrants rights activists have been fighting to get the city to provide shelters or low-income housing in the area, but the budget always comes up short. Instead, the city has apparently decided that it would be cheaper to simply push migrants deeper into the brush and other isolated and hazardous areas. </P>
<P>The city's eviction order cited the problem of "pollution" as the justification for clearing out the camps and disposing of the few belongings left behind. Doug Duncanson, Carlsbad's deputy public works director, even had the gall to claim that the camps posed a "threat" to the habitat of a threatened bird species, the California Gnatcatcher. But the truth is that the camps lie in the path of future development projects.</P>
<P>To the city of Carlsbad, the health and safety of migrant workers is worth even less than the local bird population. As migrant worker advocate Michael Wischkaemper put it, "We have a multimillion-dollar golf course going up here, but we can't find a place for these men to live."</P>

