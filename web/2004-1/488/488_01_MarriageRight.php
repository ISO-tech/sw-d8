<P><font face="Times New Roman, Times, serif" size="4">Marriage is a civil right</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We'll stand up to the bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 27, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THEY CAME from all over the country, waiting in line for hours, some even camping out in the pouring rain. But it was worth it--for the more than 3,000 gay and lesbian couples who came to San Francisco's city hall to be married were making history. And that fact wasn't lost on another group--those who would rather turn back the clock on history.</P>
<P>The two scenes couldn't strike a sharper contrast. On the one hand, thousands of jubilant gays and lesbians exchanging wedding vows and celebrating their defiance of a California law that bans same-sex marriages. On the other, grim-faced right wingers from a group called the "Campaign for California Families" headed to court to try to block the licenses.</P>
<P>When the legal route didn't work, the bigots turned to their friend in the governor's mansion, Arnold Schwarzenegger. "In San Francisco, it's a license for marriage of same sex," Schwarzenegger said on NBC's <I>Meet the Press</I>. "Maybe the next thing is another city that hands out licenses for assault weapons, and someone else hands out licenses for selling drugs."</P>
<P>George W. Bush put in his two cents, saying that that he was "troubled" by the example set in San Francisco. But Phyllis Lyon, who was first in line to get married to her partner of 51 years, had a response for Bush. "I'm troubled that our president is troubled by the forwarding of human rights,'' she said.</P>
<P>What should be "troubling" is the second-class status gays and lesbians live under in the U.S. "I have a friend who's a nurse," says Tom Barbera, a member of Service Employees International Union Local 509, one of several unions in Massachusetts that has endorsed gay marriage.</P>
<P>"She was with her partner for 30 years. Her partner had cancer and no health insurance. This woman, who is a state worker, will never be able to retire, because she has to pay off these bills. That's unfair. That's ugly to me."</P>
<P>For tens of thousands of gays and lesbians and their supporters across the country, the fight for gay marriage--from San Francisco to Boston, where legislators are trying to reverse recent state Supreme Judicial Court rulings in favor of gay marriage--has taken the shape of a new civil rights movement. </P>
<P>"I felt free," Doretha Flournoy, who took part in a 2,000-person gay and lesbian wedding reception last weekend, told the <I>San Francisco Chronicle.</I> "It really reminds me of when I hear Martin Luther King speak--of when I watched and read about the civil rights movement...As a Black lesbian, I felt the same type of freedom a moment ago as I felt then." </P>
<P>Make no mistake--we have a fight on our hands. If we're going to stop Bush and his clones from getting their way, we have to get organized. We have to stand up to the bigots--and send the message that we want civil rights and equality for all, and we want it now!</P>

