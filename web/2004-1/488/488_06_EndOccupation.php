<P><font face="Times New Roman, Times, serif" size="5"><b>From Iraq to Palestine: No to occupation!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 27, 2004 | Pages 6 and 7</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">ERIC RUDER reports on the crisis facing the U.S. occupations.</B></P>

<P>AGAIN AND again through history, foreign powers have invaded and occupied "savage lands" and "failed nations" in the name of "civilization," "democracy" and "freedom." And again and again--from the Philippines to India to Algeria to Vietnam--the "savages" have risen up and thrown out the occupiers.</P>
<P>But the Bush administration--drunk with imperial arrogance, exploiting the alibi of September 11 and driven by fantasies of religious destiny--thought it could bury this history. It convinced itself that Iraqis would welcome a U.S. occupation, with cheering crowds and wreaths of flowers.</P>
<P>But the reality is very different. U.S. soldiers face decentralized guerrilla warfare--the classic first stage of an anti-colonial struggle. </P>
<P>U.S. attempts to use Iraqi soldiers and police to put a friendly face on the occupation have only transformed the targets of the Iraqi resistance. "In Vichy France and occupied Yugoslavia and later in Vietnam, Algeria, Guinea and Angola, collaborators were regularly targeted," writes author and activist Tariq Ali in Britain's <I>Guardian</I> newspaper. </P>
<P>"Then, as in Iraq today, the resistance was denounced by politicians and the tame press as 'terrorists'...[T]he Achilles tendon of the occupation is its incapacity to control a hostile population. Hence the need for collaborators. Destroying states by overwhelming military power is one thing. State building is a more complex operation and requires, at the very least, a friendly if not a docile population."</P>
<P>It's obvious why the Iraqi population isn't more "friendly." Susan Galleymore, the mother of a U.S. soldier deployed in Iraq, traveled to the country a few weeks ago. Here's what she wrote upon her return: "[J]ittery GIs shoot Iraqi civilians in the streets...GIs smash down doors of Iraqi residences, order mothers and children outside in their nightclothes, and question fathers whose faces are ground into the dirt by a heavy military boot on their neck. Iraqis agree, 'Not even Saddam treated us like this.'"</P>
<P>These tactics are no different than the ones that any occupying force has used. Thus, the U.S. has drawn on the experience of the Israeli military and its brutal occupation of Palestine.</P>
<P>But the similarities between the U.S. occupation of Iraq and the Israeli occupation of Palestine don't end there. In both places, the occupiers complain about the "violent, terrorist methods" of the resisters--while they pummel the local population with their vastly superior military hardware, supplied by the same U.S. defense contractors. In both places, the occupiers claim to be committed to "democracy"--while they determine who the "legitimate representatives" of people living under occupation are to be.</P>
<P>Obviously, Iraq's vast oil resources represent a significant prize for the U.S. But Pentagon officials also consider a long-term presence in Iraq as critical to their plans to remake the map of the Middle East.</P>
<P>"One of the most important things we can do right now is start getting basing rights with [the Iraqi authorities]," retired U.S. Gen. Jay Garner, who oversaw the occupation of Iraq until last May, said in a recent interview. "We'd want to keep at least a brigade, [which is about 5,000 troops]. Look back on the Philippines around the turn of the 20th century. They were a coaling station for the Navy, and that allowed us to keep a great presence in the Pacific. That's what Iraq is for the next few decades: our coaling station that gives us great presence in the Middle East."</P>
<P>In Afghanistan, the dynamics are different--mainly because the U.S. wants to escape from a quagmire with no end in sight, and Afghanistan has no resources or strategic importance that would make the U.S. want to stay. As a result, the U.S. is trying to hold off elections in Iraq, while it pushes for elections in Afghanistan, despite warnings that this could spark a civil war and escalation of the violence between dueling warlords.</P>
<P>As Israel's occupation of Palestine shows, brute military force can succeed in suppressing resistance for a time. But it can't eliminate resistance. </P>
<P>Meanwhile, the violence, death and despair only produce more desperation. From Iraq to Afghanistan to Palestine, the only solution is an immediate withdrawal of U.S. arms, soldiers and military bases--and self-determination for the people whose lands have been taken from them.</P>

