<P><font face="Times New Roman, Times, serif" size="5"><b>Free from North Carolina's death row</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 27, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A NORTH Carolina man walked free last week after spending almost a decade on death row for a murder he didn't commit. Alan Gell was found not guilty of the 1995 murder of a retired truck driver. Gell's retrial took place after an appeals court ruled that prosecutors had withheld evidence from his original trial lawyers--including the fact that the state's chief witness had said she had to "make up a story" about the murder.</P>
<P>During the retrial, three scientific experts testified that the victim was murdered at least a day after prosecutors' claimed he was--when Gell couldn't have been the killer because he was out of state or behind bars on a car theft charge.</P>
<P>This latest exoneration from death row comes less than two weeks after another North Carolina man, Darryl Hunt, was cleared of all charges in a 1984 rape and murder. In this notorious case, Hunt was found guilty by a jury in two separate trials. But DNA testing finally cleared Hunt--after two decades in jail.</P>
<P>This appalling record is fueling public sentiment for a moratorium on the death penalty. Last year, the state senate passed a bill to halt executions for two years while the state studies capital punishment, but the house failed to act.</P>
<P>"This latest demonstration of incompetence and dishonesty in what North Carolina is pleased to call its 'criminal justice system' proves beyond doubt that we can't trust that system to detect and convict the guilty--or to exonerate the innocent," wrote the <I>Wilmington Star-News</I> in an editorial. "[Lawmakers] can no longer deny that innocent people are going to prison and death row."</P>


