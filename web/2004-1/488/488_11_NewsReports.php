<p><a name="CUNY"></a><font face="Times New Roman, Times, serif" size="4"><b>City University of New York</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Shaun Harkin</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Around 40 students and faculty from multiple schools in the City University of New York (CUNY) attended a CUNY Board of Trustees (BOT) public hearing in Brooklyn February 17 to protest last semester's $800 tuition increase. The tuition hikes have created additional financial hardships for CUNY's predominately working-class and minority student body, forcing some students to drop out altogether.</P>
<P>BOT members voted themselves an 18 percent pay increase at the same time that they imposed the tuition hikes. Chancellor Matthew Goldstein received a 40 percent pay increase, bringing his annual salary to $350,000--not including his $90,000-a-year stipend and his car and driver.</P>
<P>"I can't understand why CUNY isn't free now when it was free during the Great Depression," a student from the Borough of Manhattan Community College told BOT members. "Goldstein gave himself a $100,000 raise, and I'm pretty sure that money came from low-income students."</P>
<P>Many students testified by reading poems protesting the tuition hikes. Maria, a student from Hunter College, read out a poem she had prepared for the hearing:</P>
<P>"I work two jobs so my tuition is paid/Meanwhile, Chancellor Goldstein, you gave yourself a $100,000 raise/Us CUNY students, we're straight up getting played/You hiked the price of our education up $800/You're a bunch of thieves in white collars/We're tired of being robbed/It's time to take back what's ours."</P>
<P>Activists from the Student Liberation Action Movement at Hunter College have declared their determination to keep the heat on the BOT and vowed to continue raising awareness among the student body by organizing monthly protests to demand the tuition hikes be repealed.</P>


