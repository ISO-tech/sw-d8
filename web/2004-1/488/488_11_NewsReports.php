<p><a name="KKK"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the Klan</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Ivey and Dina Roy</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">RALEIGH, N.C.--About 1,000 people protested against the Nazis and the Ku Klux Klan here February 21. The Nazis tried to gather all their forces from the region, but the best that they could muster was 20. A diverse, multiracial crowd confronted them, chanting, "Not in our city, not in our state, we don't want your racist hate!"</P>
<P>The Klansmen hid behind their sheets and the protection of the cops while some protesters were arrested for wearing bandanas! Several progressive groups helped build the anti-Nazi protest. Many people also heard about it through media coverage. </P>
<P>"The Nazis go against the core of my being, and I had to be here to protest against them," one protester told <I>Socialist Worker.</I> Our side arrived early and stayed late while the Nazis arrived late and scampered off a few minutes before their stated end time.</P>
<P>Meanwhile, in nearby Goldsboro, N.C., nearly 100 farmworkers and their families rallied for immigrant rights and against Bush's proposed "guest worker" program. The Farm Labor Organizing Committee sent a message of solidarity to the activists confronting the Nazis in Raleigh and called for "consistent opposition to racism."</P>

