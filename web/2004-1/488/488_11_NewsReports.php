<P><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--University of Chicago students and community members have formed the Coalition Against Police Brutality (CAP Brutality) after a recent incident of police brutality against an African American student on campus. </P>
<P>On January 24, University of Chicago police assaulted graduate student Clemmie Carthans as he was walking to meet a friend at her dorm. Police approached Carthans and asked him why he was in the area, where he lived and why he was out at that hour. </P>
<P>Carthans, while offended by the implications of this line of questioning, answered, and the officer moved on. But moments later, when Carthans approached and hugged a female friend, the officer ran back, screaming at them, and the interrogation began again. </P>
<P>After showing the police his student identification card, they were still not satisfied and demanded to see his driver's license. When Carthans reached for his wallet, the cop placed his hand on his gun and became more agitated. </P>
<P>Still not having any reason to have stopped Carthans in the first place, the cop demanded that Carthans lay in the snow. Carthans refused and the cop called for backup. </P>
<P>Once backup arrived, they grabbed him by the throat, threw him to the ground, punched him, and later picked him up and slammed his head into the police car three times. After all this, the police released him, and Carthans went to the emergency room.</P>
<P>"I didn't expect it," Carthans said of the assault. "But once it started, I thought, 'Here we go again.' I come from the West Side [of Chicago]. You get it all the time in the neighborhood, and you think you can try to get away from it." </P>
<P>CAP Brutality is demanding the firing of the officers involved and that police receive sensitivity training. However, Clemmie added, "it's more complex than this. This has been happening for years, being swept under the rug, and it won't go away overnight." </P>
<P>The group is holding a March 3 community forum at University Church and is reaching out to other organizations that are also fighting against police brutality and criminal injustice in Chicago. Later in the month, the group will march on to the campus police headquarters to turn in signatures of support and rally for their demands.</P>

