<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why is Kucinich still in the race?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVERY TIME <I>Socialist Worker</I> publishes a story that mentions a criticism of U.S. Rep. Dennis Kucinich, we get dozens of letters complaining that we are giving Kucinich short shrift. No doubt this column won't be any different.</P>
<P>This tells us that Kucinich has a loyal group of supporters who believe that it's important to argue for their candidate in left-wing forums. It would be hard to conceive of anyone having the same amount of devotion to presumptive Democratic nominee John Kerry.</P>
<P>Nevertheless, it's important to ask just what Kucinich is up to today--and what he's asking his supporters to do--long after Kerry has sewn up the Democratic nomination. He insists that he will stay in the race until the Democratic convention in July to fight for progressive policy planks in the Democratic platform.</P>
<P>As he put it in an April 14 appeal for funds on his campaign Web site: "Help keep alive the debate about Iraq. Help keep alive the hopes of people for a single-payer, national health care system. Help keep alive workers' rights, human rights, and environmental quality principles and trade agreements. Help keep alive the efforts to repeal the PATRIOT Act. Help keep alive the progressive movement inside the Democratic Party."</P>
<P>While this succinctly summarizes Kucinich's reason for staying in the race, it also highlights the biggest problem with his campaign. If his candidacy is necessary to "keep alive" all of these progressive goals, what does that say about the organization--the Democratic Party--that is trying to kill them?</P>
<P>It should underline the essence of the Democratic Party--a bosses' party that occasionally (and less frequently) uses populist rhetoric to get enough workers, women and racial minorities to vote for it to help it win elections.</P>
<P>Its convention doesn't really debate issues, and its platform isn't worth the recycled paper it's printed on. While it depends on workers' votes to get into office, it depends on--and caters to--their bosses.</P>
<P>In his response to Bush's April 13 press conference, Kucinich asked, "Given the president's unhesitating willingness to send more and more of our young men and women to a war that was launched on lies and exaggerations, how long will it be before he resorts to a reinstatement of the draft to feed the demands of a thoroughly flawed and totally failed foreign policy?"</P>
<P>He could substitute "Kerry" for "the president" and he would still have an accurate statement. In fact, with Kerry and Sen. Hillary Rodham Clinton calling for 40,000 more troops to be sent to Iraq, while Bush orders "only" 20,000, Bush would seem to be the "lesser evil" on this question!</P>
<P>Yet Kucinich has made it quite clear that he has no intention of leaving the Democratic Party over this or any other questions. He said, "The Democratic Party created third parties by running to the middle. What I'm trying to do is to go back to the big tent so that everyone who felt alienated could come back through my candidacy."</P>
<P>Come November, Kucinich will face pressure to endorse and campaign for a candidate who is "running to the middle." Kucinich campaigns against the USA PATRIOT Act, yet he will urge his supporters to work to elect a man who voted for it. Likewise with a host of other issues, from the No Child Left Behind Act to the war in Iraq.</P>
<P>Unfortunately, Kucinich is going to tell his supporters that the only responsible thing to do in November is to elect a man who stands closer to Bush on these issues than he does to them. That's the ultimate tragedy of reducing elections to "anybody but Bush" or a choice of the "lesser evil."</P>
<P>No doubt the vast majority of Kucinich's rank-and-file supporters want to see a real political alternative. That's why they should build that alternative outside the Democratic Party, an organization one of whose main purposes is to assure that such an alternative never develops.</P>


