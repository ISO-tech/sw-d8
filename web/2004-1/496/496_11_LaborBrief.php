<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 23, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--About 200 teachers attended the United Teachers Los Angeles (UTLA) Health Care Crisis Symposium on April 16-17. Most of the symposium was dedicated to long-winded doom-and-gloom presentations about the health care crisis.</P>
<P>The keynote speaker even went so far as to lecture us that our "poor lifestyle habits" were to blame for "as much as 30 percent to 50 percent of health care costs." UTLA leaders kept a tight lid on the symposium, insisting that teachers merely ask questions and interrupting those who dared to express disagreement from the floor.  </P>
<P>They wanted us to accept the "reality" that health care cuts are inevitable. But opposition to cuts exploded in breakout sessions in which teachers were finally allowed to speak our minds.  </P>
<P>Activists in Progressive Educators for Action (PEAC), an opposition group within UTLA, played a central role in shifting the event's tone. PEAC stickers with slogans like "I'm willing to strike to defend my health care!" were especially popular.</P>
<P>At the final panel discussion, in which teachers from each breakout group reported back to the whole body, most of the panelists were wearing PEAC stickers! Panelists made passionate arguments for the union to stand firm against the cuts--to huge applause from brothers and sisters in the audience.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>University of Wisconsin-Madison</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Amy Kemery</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--A group of about 70 state workers and supporters met at the state capitol to observe "Tax Rip-Off Day." Members of AFSCME and the American Federation of Teachers (AFT), many of whom don't have contracts, are facing cuts in health care and a proposed elimination of 10,000 jobs due to the state budget crisis. </P>
<P>The job cuts would give Wisconsin the lowest number of state workers per capita in the nation. Organizers blamed the crisis on corporate giveaways and outsourcing state services to out-of-state contractors, which costs the taxpayer more than having in-state workers do the same job.</P>


