<P><font face="Times New Roman, Times, serif" size="4">Texas creation myth according to Disney</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Forget<I> The Alamo</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I> The Alamo,</B></I> directed by John Lee Hancock, written by Hancock, Leslie Bohem and Stephen Gaghan, starring Billy Bob Thornton, Dennis Quaid and Jason Patric.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN CASE you were hoping otherwise, the much hyped "historically accurate," "told from all sides" Disney movie <I>The Alamo</I> leaves the Texas creation myth disgustingly intact. What makes this movie different from previous Hollywood versions of the 1836 battle for Texas' "independence" from Mexico is its attempt to include the contributions of Texas Mexicans (known as Tejanos) in the fight.</P>
<P>Perhaps Disney has become aware of the political and economic clout of the rising Latino population in Texas. The Tejanos in <I>Alamo, </I>however, are one-dimensional and have few lines. Mexican Gen. Antonio Lopez de Santa Anna is pure villain. </P>
<P> Most Latinos in Texas are well aware that Texas was stolen from the Mexicans "fair and square," and this film probably won't change that impression. Despite its feeble attempts at multiculturalism, this Alamo story is still dominated by the Anglo settlers in Texas (known as Texians).</P>
<P>The film hints at the darker pasts of the Texian leaders, only to see them redeemed in the end by their brave fight in pursuit of senseless mass martyrdom. There's no discussion of what exactly the Texians and Tejanos were fighting for. The night before the final attack, soldiers pray and write home about the beauty of the land called Texas, which is worth many men dying for several times over.</P>
<P>What the Alamo mythmakers don't want the audience to know is that the fight for Texas "independence" was a fight by slave owners for the independence <I>to own slaves.</I> Shrouded in terms like "federalism" (local control) and "centralism" (strong centralized government), this was a fight for local areas to avoid obeying Santa Anna's annoying enforcement of anti-slavery laws.</P>
<P>Texas and other Southern states have often used the concept of local control, or states' rights, to deny freedom and justice rather than expand them--as in the case of blocking racial integration, and, more recently, gay marriage. Santa Anna allowed U.S. settlers into Texas in hopes that they could control the fierce Comanches.</P>
<P>The settlers came to enrich their fortunes by acquiring cheap or free fertile land on which to start cotton plantations--at the time, cotton in Texas economy was as valuable as oil. The plantations were dependent on slave labor. The Tejanos were extremely helpful to the slave-owning Texians. Like the Texians, they came from families of wealth and prestige, and many were slave owners.</P>
<P>As soon as independence was won, the Tejanos got an early lesson in Texas injustice. Most of their ancestral land holdings were taken over by Anglo settlers through the first Texas Constitution.</P>
<P>The two slaves who appear in the movie--the personal slaves of Jim Bowie and William Travis--are stereotypes with bit parts. This is an insult to the many slaves who suffered for an independence that didn't include them.</P>
<P>Women are few and incidental, appearing as sex objects or cowering in a corner. Native Americans are completely ignored, unless you count Davey Crockett's horrific tale of massacre and cannibalism in an early Indian fight.</P>
<P>It's easy to be caught up in the music of Crockett's banjo and the fate of those trapped inside the Alamo if you don't know the real story. But the ballad "<A NAME="OLE_LINK1"><A NAME="OLE_LINK2">Remember the Alamo</A></A>" is little more than inspiration for ruling-class butchery and a myth backed up by lies and distortion.</P>


