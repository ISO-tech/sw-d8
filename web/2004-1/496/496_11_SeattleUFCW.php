<P><font face="Times New Roman, Times, serif" size="4">UFCW members gear up ahead of May 2 contract deadline</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Grocery showdown in Seattle?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--The stage is set in the Puget Sound region for the latest round of contract negotiations between the United Food and Commercial Workers (UFCW) union and the big three grocery stores--Kroger Co., Safeway Inc., Albertsons Inc.--as well as several smaller area groceries.</P>
<P>On May 2, the contract for 25,000 workers in UFCW Locals 44, 81, 367, 381 and 1105 will expire. The companies grouped together as the Allied Employers, who have forced concessionary contracts onto UFCW members from Southern California to Washington, D.C., and elsewhere around the country, are attempting more of the same here. </P>
<P>On April 15, the companies issued an ultimatum. Either accept their first proposal, which includes a two-tiered health care plan that they say isn't as bad as the Southern California settlement, or they will withdraw it and put the Southern California settlement on the table.</P>
<P>Already the unions are talking about taking concessions on health care benefits, which have been the main issue for UFCW members all around the country. But the companies are rolling in money. Between 1998 and 2002, their profits rose 91 percent. </P>
<P>While executive compensation--not including stock options--at the Big Three increased 218 percent in that period, workers' pay in the Puget Sound region rose only 12 percent.</P>
<P>Despite rank-and-file demands to start preparing for negotiations last summer and the long strike in Southern California, union officials have done little to prepare for a possible strike. </P>
<P>Union leaders have said they'd be willing to extend the contract past the May 2 deadline, but only if the companies "bargain in good faith." But the companies have already begun holding captive audience meetings with employees, cutting hours and changing workers' schedules.</P>
<P>The union needs to call a strike authorization vote now, and locals needs to take steps to avoid weaknesses of the Southern California strike--for example, insuring that pickets go up at every single grocery store, building mass pickets so scabs will not be able to cross picket lines and organizing solidarity with other unions, especially Teamsters, so they won't cross picket lines.</P>
<P>Despite the lack of preparation, anger towards the companies among the grocery workers is tremendous. "If we give in now, it's opening up a box that won't close," says Liz, a five-year employee of QFC, which is owned by Kroger. "We're going to stand up for what we believe in."</P>
<P>The potential for a victory exists. Unions throughout the Puget Sound region have pledged their support for the UFCW's struggle. If the labor movement can rise to the challenge, it could potentially spark workers here and around the country to stand up to their own bosses and help rebuild a fighting labor movement.</P>


