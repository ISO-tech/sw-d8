<P><font face="Times New Roman, Times, serif" size="4">Abortion rights: No restrictions, no concessions...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No apologies!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A MARCH so big that even George W. Bush can't ignore it. That's what activists are hoping to see in Washington, D.C., at the March for Women's Lives rally for abortion rights on April 25.</P>
<P>Organizers from Planned Parenthood, NARAL Pro-Choice America, the National Organization for Women and other organizations are expecting more than half a million people to turn out to send Bush a message--that we won't stand for any more attacks on a woman's right to abortion.</P>
<P>Bush has had abortion rights on his hit list from day one of his presidency. But he has stepped up the attack recently, signing the ban on the late-term abortion procedure intact dilation and extraction (mislabeled "partial-birth" abortion by the right) and a law that gives rights to fetuses.</P>
<P>"We all feel that we're at a turning point," Eleanor Smeal, president of the Feminist Majority, one of the rally sponsors, told the <I>San Francisco Chronicle</I>. "It's our responsibility to tell people how dire the situation is." Unfortunately, the "situation" for abortion rights has been dire for a long time.</P>
<P>This week's march in Washington is a welcome show of strength against the right wing. But it is a long time in coming. The last national demonstration was three years ago, and the anti-abortionists have been chipping away at the right to choose ever since.</P>
<P>At the rally, organizers will undoubtedly showcase a host of Democratic politicians--and the overwhelming message will be to "get out the vote" for Democratic presidential hopeful John Kerry. But this ignores the fact that the Democrats have done little to stop the attacks on abortion rights--and in some cases have aided them.</P>
<P>Women in the U.S. came out of the eight years of the Clinton-Gore administration with less--not greater--access to abortion and more restrictions in place. But during Clinton's presidency, mainstream pro-choice groups kept silent, refusing to mobilize while "our" candidate was in the White House.</P>
<P>More recently, many Democrats crossed the aisle to join Republicans in voting in favor of both the ban on late-term abortions and the Unborn Victims of Violence Act. Not holding them just as responsible as the Republicans for undermining abortion rights lets the Democrats off the hook.</P>
<P>What's more, march organizers reportedly refused to allow a speaker to address the crowd on the struggle for gay marriage--because it might "offend" some in the "mainstream." Translation: Leaders of mainstream groups don't want to put Kerry--who opposes gay marriage--on the spot.</P>
<P>Abortion rights won't be saved by pulling a lever for John Kerry in November--or relying on the Democratic Party to stand up for women's rights. Instead, we need to focus on building a movement in the streets that's too big for either party to ignore--and send a message that we won't go back to the days of the back alleys!</P>


