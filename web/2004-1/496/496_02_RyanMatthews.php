<P><font face="Times New Roman, Times, serif" size="4">Death row prisoner Ryan Matthews wins a new trial</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A step closer to getting justice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AN INNOCENT man is one step closer to justice. Last week, prosecutors in Louisiana's Jefferson Parish, which borders New Orleans, finally agreed to a new trial for Ryan Matthews, who has spent the last five years on death row. The state's decision to accept a new trial came after mounting pressure from Ryan's supporters.</P>
<P>One year ago, Ryan's lawyers announced that they had DNA tests confirming Ryan's innocence in the murder of convenience store owner Tommy Vanhoose in April 1997--and pointing to the identity of the real killer. But prosecutors continued to drag their feet--only agreeing to an evidentiary hearing this February.</P>
<P>"Every day that he's been there, the injustice grows," Monique Matthews, Ryan's sister, told <I>Socialist Worker. </I>"I don't feel like it was a mistake on the state's part. The intent was to convict Ryan--they didn't care whether he was the right person or not." But the struggle for Ryan--both inside and outside the courthouse--finally forced the state to agree to a new trial that prosecutors can't possibly win, according to Ryan's supporters.</P>
<P>Ryan will be arraigned for the new trial on April 29. At that time, Ryan's supporters hope that prosecutors will decide to drop all the charges and not retry him.</P>
<P>This case shows beyond a shadow of a doubt the brutality and racism of the American "justice" system. Ryan, who is mentally retarded, was arrested just two months after his 17th birthday. He was railroaded in a three-day trial--and sent to death row by an almost all-white jury. </P>
<P>Jefferson Parish--the home to Klansman David Duke--is infamous for Southern-style "justice." The lead prosecutor in Ryan's case, Paul Connick, was known for wearing neckties decorated with nooses--and bragging about lethal injection syringes covering his walls. </P>
<P>Death row in Louisiana is housed at the Angola State Penitentiary, a former slave plantation that was one of the first prisons to welcome back the chain gang. Today, Angola's overwhelming African American prison population is forced to work the fields of this plantation for a few cents a day--while the state of Louisiana makes millions from their labor. </P>
<P>Ryan's family has been fighting for years to win justice--speaking out in the U.S. and in Europe to build the campaign for Ryan's freedom. In recent years, the activism of the William Kunstler Fund for Racial Justice, a petition drive by the Campaign to End the Death Penalty (CEDP) and the work of lawyers from the Louisiana Crisis Assistance Center has upped pressure on prosecutors.</P>
<P>"The struggle will continue, even when Ryan is freed," says Monique, "because there are other Ryan Matthews in Louisiana and across the country. My goal is always to have a voice in the CEDP and in the movement, even when Ryan is freed--to vent my anger against the criminal justice system and because my voice has found a home."</P>


