<P><font face="Times New Roman, Times, serif" size="4">New powers for local police</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Deputies in a war on immigrants</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE DEPARTMENT of Homeland Security has a new ally in its hunt for undocumented immigrants: Alabama state troopers. In the last year, the state governments of Alabama and Florida have entered into "partnerships" with the Department to allow local and state police officers to participate in immigration enforcement. </P>
<P>These moves are the prelude to a more sweeping attempt to control the immigrant population in the U.S.--in the name of "fighting terrorism." Deputizing the nation's 650,000 cops into armed agents of immigration control is one of several features of a bill now before Congress--called the CLEAR Act, or the Clear Law Enforcement for Alien Removal Act. </P>
<P>With 120 cosponsors, the legislation is a wish list for the far right and other anti-immigrant forces. The law would work hand-in-hand with the Bush administration's recent "reform" proposal to create a temporary "guest worker" system. Together, the two initiatives would further disenfranchise millions of undocumented workers who already suffer the worst forms of exploitation and neglect because of their status. </P>
<P>Under the CLEAR Act, law enforcement officials would have access to the National Criminal Information Center--a digital database with records on over 40 million people, from people with past criminal records to those who are considered "fugitives" or wanted for questioning--to help track and detain undocumented immigrants. </P>
<P>In the aftermath of the September 11 attacks, the database was expanded to include "non-criminal" data on thousands of Arab and Muslim immigrants--and information on more than 400,000 immigrants who failed to renew their visas.</P>
<P>Giving local and state law enforcement federal authority would allow the Department of Homeland Security to override cities like Los Angeles and Houston that have passed "sanctuary ordinances"--local laws that prevent city employees from engaging in immigration enforcement. Any local or state government or law enforcement agency that hesitates to work with the Feds would be given an ultimatum--comply or lose federal funding.</P>
<P>The inevitable result of the CLEAR Act passing would be an increase in racial profiling and civil rights abuses. "Some officers would inevitably stop and question people based on their ethnic background or their accent, leading to violations of the rights of U.S. citizens and legal residents whose only offense is 'looking foreign,'" the ACLU said in a statement.</P>
<P>But the CLEAR Act's promoters know this full well. That's why they included a clause that would exempt law enforcement agencies from lawsuits for civil violations performed on the job.</P>
<P>The CLEAR Act is designed to silence immigrants. Victims of domestic abuse or workplace violations who are undocumented will be less likely to report the crimes for fear of being deported. But the legislation is an attack on all workers.</P>
<P>Giving police--already on the front line against strikes, protests and grassroots movements--the powers of "la migra" would hurt all struggles for justice. We have to stand up against this immigrant-bashing law.</P>


