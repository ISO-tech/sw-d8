<font face="Times New Roman, Times, serif" size="5"><b>John Kerry tries to out-Bush Bush</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 23, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JUST A few months ago, the candidates for the Democratic presidential nomination were battling for votes in the primaries--and using fiery anti-Bush rhetoric to try to win support. That was then.</P>
<P>Now, John Kerry, the lamest of the bunch, has a lock on the nomination--and he's shed the "fighting for working people" message that he tried on for size in Iowa and New Hampshire for a more comfortable fit. Acting like a carbon copy of George Bush.</P>
<P>"I am not a redistribution Democrat," Kerry assured high rollers at a $25,000-a-plate breakfast fundraiser at the posh "21" Club in Manhattan last week. "Fear not. I am not somebody who wants to go back and make the mistakes of the Democratic Party of 20, 25 years ago."</P>
<P>Kerry is determined to out-Bush Bush on any number of issues--particularly foreign policy. In an hour-long interview on "Meet the Press" on April 18, Kerry apologized for the rhetoric he used to criticize the Vietnam War 30 years ago, calling it "excessive" and "over the top."</P>
<P>Asked about Israel's assassination of Hamas leader Abdel Aziz Rantisi, Kerry gave it the okay. "I believe Israel has every right in the world to respond to any act of terror against it," Kerry said--as oblivious as Bush to Israel's far greater terror against Palestinians.</P>
<P>Meanwhile, Kerry's strategy for dealing with Iraq is winning the praise of arch-conservatives. Commenting on Kerry's criticism of Bush's policy in Iraq, William Kristol of the right-wing <I>Weekly Standard </I>commented to the <I>Washington Post: </I>"The most important thing...I thought is how similar it really is to Bush administration policy, and that I say in praise of him...He is not willing to cut and run from Iraq...He wants the UN to be more involved, but he doesn't say if we can't get the UN more involved, we should get out. President Bush is trying to get the UN involved, too."</P>
<P>So do any of Kerry's critics during the primary campaign have any complaints? Like liberal "maverick" Howard Dean, for example? Of course not.</P>
<P>In time-honored fashion, Dean has forgotten everything bad he ever said about Kerry. Instead, he's herding his liberal supporters behind Kerry--and rather than attacking Bush as he did during primary season, Dean is saving his harshest rhetoric for independent presidential candidate Ralph Nader.</P>
<P>In an op-ed article in the <I>New York Times </I>last week, Dean joined the liberal slander campaign against Nader. Meanwhile, in Seattle, former Dean campaign supporters picketed Nader, and Dean's former Web guru told the British <I>Guardian </I>that he's about to launch a new site called votenaderelectbush.com.</P>
<P>Anyone who has fallen for the rally-round-Kerry message of the Democrats--liberal and conservative alike--needs to ask if they want to follow Kerry on his lurch toward Bushism.</P>
<P>"We're putting together a powerful 'Republicans for Kerry,'" Kerry told a room of potential donors. Remember what that did for Ronald Reagan in 1980, 'Democrats for Reagan.' Now Jimmy Carter couldn't put together 'Republicans for Carter,' and I don't think George Bush could put together a real 'Democrats for Bush.' So we need to go out and do this."</P>
<P>Translation: You can get Bush policies without voting for Bush. The only votes that Kerry deserves are from his buddies on Wall Street. Not ours. We have to build opposition to everything that's rotten in Washington--Democrats and Republican alike.</P>


