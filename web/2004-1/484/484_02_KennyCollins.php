<P><font face="Times New Roman, Times, serif" size="5"><b>Death sentence overturned in Maryland</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MARYLAND PROSECUTORS finally admitted in court what defense attorneys and activists have been saying for years--that death row prisoner Kenny Collins never got a fair trial. Baltimore County prosecutors filed legal papers saying that Collins' attorney didn't represent him adequately when he was sentenced to death 16 years ago for the killing of Wayne Breeden.</P>
<P>The prosecutors have publicly stated that they have no intention of seeking another death sentence against Collins. This dramatic development comes after the U.S. Supreme Court overturned the death sentence of Maryland death row prisoner Kevin Wiggins because his attorney had failed to prepare an argument or conduct any investigation during the sentencing phase of his trial.</P>
<P>Similarly, Collins' attorney failed to conduct an investigation or call witnesses to testify on Kenny's behalf. Since receiving a death sentence, Collins has become a nationally known activist in the fight against the death penalty.</P>
<P>As one of the original participants--along with Tyrone X Gilliam, who was executed in 1998--in the Campaign to End the Death Penalty's Live from Death Row forums, Collins has spoken out against the death penalty by speakerphone before thousands of listeners.</P>
<P>On February 5, Maryland Circuit Court Judge Daniel Long will hear arguments on Collins' appeal for a new trial. Despite the fact that the state's case against him has virtually collapsed, it's not clear if Long will rule in Kenny's favor.</P>
