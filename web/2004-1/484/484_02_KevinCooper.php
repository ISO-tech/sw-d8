<P><font face="Times New Roman, Times, serif" size="5"><b>Save Kevin Cooper</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Abraham Gutierrez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DURING THE first hour of February 10, the state of California wants to murder Kevin Cooper. Cooper is an African American prisoner who was accused of murdering three members of the Ryen family, along with a young house guest, in 1985. </P>
<P>But the case against Cooper highlights everything that is wrong with the "justice" system in California and around the country. Activists are determined to cast a spotlight on this case--and stop Kevin from being put to death.</P>
<P>Much of the evidence tying Cooper to the murders was circumstantial. Cooper won DNA testing to prove his innocence in 2001. But before the tests were completed, a criminologist involved in Cooper's prosecution--unbeknownst to the defense team, or even the judge--had checked out the evidence kit, along with Cooper's blood and saliva samples, and then checked them back in 24 hours later. </P>
<P>Cooper's defense attorneys discovered this by accident, before the testing was finished. So it was no surprise that the DNA results came back with a result that favored prosecutors. Yet the state is refusing to do tests that could show if the blood evidence was tampered with. </P>
<P>There are other unanswered questions. For example, blond hair--which couldn't possibly belong to Cooper--was found in one of the victim's hands, but the jury at the original trial never heard about it.</P>
<P>Cooper has always maintained his innocence. Meanwhile, another prisoner has confessed to the Ryen murders, providing his cellmate with information about the crime that wasn't reported in newspapers. </P>
<P>The specific issues in the Cooper case highlight the racism and bias against the poor of the death penalty system. Fully 35 percent of California's death row--the biggest in the country--is African American, while only 6.7 percent of the state's population is Black. </P>
<P>Activists are determined to stop this injustice. On February 3, the Committee to Save Kevin Cooper has called a national day of action. Plans include several press conferences around the state, as well as a campaign to flood Gov. Arnold Schwarzenegger's office with phone calls and faxes.</P>
<P>On January 31, the Campaign will hold a Live from Death Row forum--with Kevin calling in via speakerphone from death row at San Quentin Prison. Kevin's supporters also joined the annual Martin Luther King Day March in San Francisco on January 19 with a contingent that was 200 strong. Unfortunately, the committee was forced to take the back of the march--along anti-war demonstrators--because organizers argued that they wanted to keep the activities "apolitical."</P>
<P>The Committee is also planning a signature ad pleading Kevin's case to appear in several major newspapers. Among those who have signed on already are Danny Glover, Janeane Garafalo, Howard Zinn, Noam Chomsky, Peter Camejo, Stephen Bright, Jesse Jackson, Ruben "Hurricane" Carter and Angela Davis.</P>

