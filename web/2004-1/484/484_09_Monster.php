<P><font face="Times New Roman, Times, serif" size="4">Film tells Wournos' story with humanity</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The making of a "monster"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Susan Dwyer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Monster,</B></I> written and directed by Patty Jenkins, starring Charlize Theron, Christina Ricci and Bruce Dern.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITHIN MINUTES of her arrest in January 1991, Aileen Wournos' painful and pathetic life was sensationalized in the nation's media. Wournos, accused of shooting to death seven men over a period time and abandoning them on Georgia and Florida highways, became "America's first female serial killer" and the "damsel of death."</P>
<P>Before 24 hours had passed, three of the arresting cops had hired lawyers to pimp their stories to the press and Hollywood. They were the first in a long line of people who attempted to profit from their association with Wournos.</P>
<P>For the press and the justice system, she was the perfect criminal--a prostitute living in a lesbian relationship, a petty criminal who had spent her entire life in trouble. She was also mentally unbalanced, but that, of course, is never part of the story.</P>
<P>Patty Jenkins' amazing new film <I>Monster </I>tells Wournos' story with humanity and understanding. Although the film is frightening, Jenkins and her three splendid lead actors--Charlize Theron, Christina Ricci and Bruce Dern--never cheapen the story with cliched, Hollywood-style sensationalism.</P>
<P>Most of the murders are shown on screen, but the point of the film is not what Wournos did, but why she did it. Jenkins depicts her first killing, that of Richard Mallory, as self-defense--Wournos' only way out of a brutal rape. Considering that Mallory's death was the only crime Aileen was ever formally charged with, the fact that she claimed rape and self-defense should have been crucial evidence.</P>
<P>But Florida prosecutors and police never found a criminal record for Mallory nor seriously investigated Wournos' claim of rape. It was Michele Gillen of <I>Dateline NBC </I>who ran Mallory's name through the FBI's system and found that he'd spent 10 years in prison for violent sexual assault.</P>
<P>Aileen Wournos was ignored all of her life. Her mother was 14 years old when she gave birth to Aileen. Her father took off before she was born, later committing suicide while he was in prison for the rape of a 7-year-old girl.</P>
<P>By the time she was 4, Aileen's mother took off as well, leaving her two children with their grandparents, who were alcoholics. Given the stress of her childhood, it's not surprising that Aileen had an uncontrollable rage.</P>
<P>By age 11, she was prostituting herself. At 14, she was raped and became pregnant. After she gave birth in a home for unwed mothers, her grandfather refused to take her back. So at age 15 Wournos was on her own, hitchhiking around America.</P>
<P>Jenkins includes most of these facts in the film through a voiceover that's culled from Wournos' letters and writings. Jenkins has the good sense and courage to let Wournos speak for herself.</P>
<P>And Charlize Theron, who plays Wournos, gives us a character desperate for love and attention, with no idea of how to achieve either. She is full of ticks and jerks, never still, and never in control of reality. To her, a typical middle-class life was as fantastic and unreal as a Hollywood romance.</P>
<P>Wournos had spent her life being blamed for the horrors that were pushed on her, and reacting to both the blame and the misery by sliding into petty crime, murder and madness. In the end, between fits of rage and a desperate search for salvation, she accepted the blame for all seven killings. She was executed in 2002.</P>
<P>The owner of the bar where Wournos was arrested allows his regular clients to pick one brick on which to write whatever they like. Wournos chose one a few weeks before her arrest. It reads, "I was raped."</P>
<P>See <I>Monster.</I> Cross your fingers that the filmmaker and actors receive the awards they deserve for this movie. But more importantly, fight against this system that allows such pain and suffering, only to legally murder those who snap under the pressure.</P>



