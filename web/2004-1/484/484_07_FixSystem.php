<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should we try to fix the system?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE OFTEN-heard criticism of socialism is that workers' revolution--the overthrow of the existing system of capitalism--is too "drastic." If we work hard to reform the current system, the argument goes, then we wouldn't have to resort to extreme actions like overthrowing capitalism.</P>
<P>This important debate--reform or revolution--has rightly been the great divide for generations of socialists. Some of the most famous debates on this issue took place in the late 1800s and early 1900s between Eduard Bernstein and Rosa Luxemburg, two leading members of the main German socialist party, the Social Democratic Party (SPD).</P>
<P>Bernstein's writings were published in the book <I>Evolutionary Socialism; </I>Luxemburg's in the pamphlet <I>Reform or Revolution. </I>Bernstein argued that it was possible for the present system to be transformed into socialism through things like pro-worker laws or pressure from trade unions.</P>
<P>Writing at a time of relative economic prosperity, Bernstein also threw out another main tenet of Marxism--capitalism's tendency to fall into crisis. According to Bernstein, capitalism has found new ways to adapt and remain stable, such as cartels and the use of credit. The tendency toward crisis that Marx had observed, Bernstein believed, was no longer inevitable.</P>
<P>Influenced by the utopian socialists, Bernstein also looked to the spread of "cooperatives," in which workers would jointly control workplaces. In Bernstein's view, the SPD needed to abandon the idea that capitalism had to be overthrown in order to win socialism. In many ways, this fit with the practical work of the party, whose members were concentrated in parliament and in day-to-day trade union activity.</P>
<P>The debate between reform and revolution had been brewing within the socialist movement for years, but had been swept under the rug in favor of unity. It finally cracked open when Bernstein declared, "The final aim of socialism, whatever it may be, means nothing to me; it is the movement itself which is everything."</P>
<P>One of the greatest thinkers of the Marxist tradition, Rosa Luxemburg, wrote some of her best polemics in the debate with Bernstein. By characterizing the debate as "Reform or Revolution," there could be no mistaking that sides had to be taken--and that the future of the movement was at stake.</P>
<P>Luxemburg argued that, like previous forms of class society, capitalism could not be reformed away, but had to be overthrown by the only part of society with the power to do it--the working class. She argued that what Bernstein proposed had nothing at all to do with winning socialism.</P>
<P>"It is contrary to history to represent work for reforms as a long-drawn out revolution and revolution as a condensed series of reforms," Luxemburg wrote. "A social transformation and a legislative reform do not differ according to their duration but according to their content...</P>
<P>"That is why people who pronounce themselves in favor of the method of legislative reform <I>in place of, and in contradistinction to,</I> the conquest of political power and social revolution, do not really choose a more tranquil, calmer and slower road to the <I>same</I> goal, but a <I>different</I> goal. Instead of taking a stand for the establishment of a new society, they take a stand for surface modifications of the old society."</P>
<P>This isn't to say that Luxemburg didn't care about winning reforms. She considered the struggle for reforms within the existing system the most important way for socialists to "work in the direction of the final goal."</P>
<P>It's also during struggles for reform that workers come to see that they have an interest in overthrowing the present society--and gain the confidence and understanding that they have the power to do it. "In a word," Luxemburg wrote, "democracy is indispensable not because it renders <I>superfluous</I> the conquest of political power by the proletariat, but, on the contrary, because it renders this conquest of power both <I>necessary</I> and <I>possible.</I>"</P>
<P>In reality, short of a revolutionary upheaval, socialists spend most of their efforts mobilizing pressure to win changes in the existing system. But at the same time, they also have their eyes on the struggle for the future--to overthrow the existing system and build a new world altogether.</P>
<P>Socialists may spend the bulk of their time in struggles for reforms. But it makes all the difference in the world whether your ultimate goal is revolution.</P>


