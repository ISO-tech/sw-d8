<P><font face="Times New Roman, Times, serif" size="4">Right-wing bigots taking aim at abortion rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Time to fight for choice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 30, 2004 | Page 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</B></I> <BR>
I was disgusted to read that last week, on the 31st anniversary of the <I>Roe v. Wade </I>Supreme Court decision that legalized abortion, thousands of anti-choice bigots gathered in Washington for a "March for Life" rally. George W. Bush addressed them by phone, saying that "all life is sacred and worthy of protection" and that we have to build a "lasting culture of life, a compassionate society in which every child is born into a loving family and protected by law."</P>
<P>The bomber of Afghanistan and Iraq pleading for the sanctity of life? Give me a break. But what upset me even more than Bush's speech was that there were almost no pro-choice marchers out on the street that day to confront these bigots.</P>
<P>The erosion of abortion rights is greater than at any time since <I>Roe v. Wade</I> was decided. Abortion is unavailable in more than 90 percent of counties in the U.S., and parental notification laws and mandatory "waiting periods" are more common than ever before. But one reason for this is precisely because of the strategy pursued by mainstream pro-choice organizations for decades--lobbying for Democrats, rather than building a movement in the streets.</P>
<P>Arguing that the "lesser-evil" Democrats would protect our right to choose has meant backing people like Bill Clinton--even though as governor of Arkansas, he refused to take a stand against an anti-choice amendment to the state constitution against abortion funding. It meant backing Al Gore--even though, as a senator, he had an approval rating of 86 percent from the National Right to Life Committee. </P>
<P>No wonder that during eight years of Clinton-Gore, the number of counties in the U.S. offering abortion access got smaller.  What happened to the days when the pro-choice movement stood up and said, "I'm not sorry" for having an abortion? Why is it that prominent feminists like Naomi Wolf, and even NARAL president Kate Michelman, today talk about how women should be "preventing unwanted pregnancies" rather than fighting harder than ever for the right to choose?</P>
<P>On April 25, there will be a "March for Women's Lives" in Washington, D.C., called by the NOW, NARAL Pro-Choice America and Planned Parenthood. While the sponsors of this rally will undoubtedly use it as a platform for the Democrats, activists should welcome this demonstration and build it as large as we can.</P>
<P>To win back the ground that we've lost, we need to build a real grassroots movement from the bottom up that particularly includes poor and minority women--the very people who need access to abortion most, yet who are often ignored by the mainstream feminist movement. Ultimately, protecting the right to choose will mean building a movement that has as its rallying cry, "Free abortion on demand--without apology."</P>
<B><P>Nicole Colson, </B>Chicago</P>



