<p><a name="Ollie"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Oliver North</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Laura Nelson and Matt Tucker</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Community members and University of Wisconsin-Madison students came together to "welcome" Oliver North in mid-January. North came to Madison as an invited guest of the Wisconsin Manufacturers and Commerce (WMC), a pro-business lobbying group. </P>
<P>Ollie is best known as the brains behind the 1980s Iran-contra scandal, in which members of the Reagan administration sold weapons to the Iranian government and used the money to finance the Nicaraguan right-wing paramilitary contras. After his indictment, Oliver North was sentenced to a suspended prison sentence, which was later overturned on federal appeal.</P>
<P>The week of events began January 11 with a panel at the First Unitarian Church on the repercussions of free trade. Approximately 150 people attended the panel to ask speakers about a range of topics, from the necessity of drug trade to the Nicaraguan economy.</P>
<P>More than 200 protesters picketed January 13 at the swank Madison Club, the local "country club without a golf course." North spoke to a crowd of invited guests as the protesters outside chanted "Hey, Hey, Corporate Hack, Where'd you hide those guns and crack?" and held signs condemning both Ollie and globalization.</P>
<P>On January 14, the crowd returned to the Monona Terrace to protest Ollie's $30,000 speech to the WMC. The protesters gathered in the rotunda of the building and were prevented from entering the speech by a line of police officers. </P>
<P>This proved to be beneficial, as each of the invited guests had to make their way through the crowd. Creative People's Resistance provided street theater while the crowd chanted both anti-WMC and anti-North slogans.</P>

