<p><a name="MLK"></a><font face="Times New Roman, Times, serif" size="4"><b>Martin Luther King Day</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Kolya Ludwig</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATLE--"Gay, straight, black white--same struggle, same fight!" Seattle's Martin Luther King Day brought thousands of workers and activists to workshops at Garfield High School, and later to a rally in downtown. "Justice begins at home" was this year's theme.</P>
<P>Several unions--including the United Food and Commercial Workers, the International Association of Machinists and the International Longshore and Warehouse Union--were part of the multiracial crowd that participated in educational workshops that covered a diverse range of issues, such as racism and international questions. </P>
<P>After the march downtown, which was animated by chants against cutbacks and occupation, speakers engaged the audience in pertinent discussions. They focused on labor, class, free trade and war profiteering.</P>
<P>One militant speaker raised the argument that agreements such as the Free Trade Area of the Americas lead to adversity among working people here. The day's events served as a revitalizing force for local activist and labor groups.</P>

