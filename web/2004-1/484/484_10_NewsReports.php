<p><a name="Muslims"></a><font face="Times New Roman, Times, serif" size="4"><b>Oppose the persecution of Muslims</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Christian Wright and Katrina Yeaw</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS IN cities across the U.S. protested at French consulates January 17 to demand religious freedom in France. The demonstrations were part of an international day of protest against French President Jacques Chirac's proposed ban on "conspicuous" religious symbols in public schools. If passed, the bill will make it illegal for the hijab, or Muslim headscarf, the Jewish yarmulkes, large Christian crosses or Sikh turbans to be worn in schools. </P>
<P>In Paris, 30,000 protested against the bill, and in London, 2,000 took to the streets. Although claiming to protect "secularism," the bill is a direct attack on France's more than 5 million Muslims. </P>
<P>In San Francisco, more than 200 people marched from city hall to the French Consulate. Led mostly by women wearing the hijab, the lively crowd chanted "Shame on France" and "Religious freedom for all." Protesters carried signs that read "My scarf, my choice" and "Secularism does not equal religious oppression." </P>
<P>Speakers shared experiences about wearing the hijab while connecting the ban to the larger issues of religious freedom and human rights. Representatives from a number of interfaith organizations delivered more than 7,000 signatures against the bill to the French deputy consul. </P>
<P>The protest was sponsored by the Muslim Student Association and brought human rights, religious organizations and antiwar groups from across the Bay Area, including representatives from the Council for American-Islamic Relations, the Nation of Islam, International ANSWER, the Campus Antiwar Network and members of the Christian and Sikh community. </P>
<P>In Washington, D.C., more than 100 people turned out the same day--despite temperatures below freezing. Activists chanted slogans denouncing the French government for more than two hours and challenged the crowd to educate others about Islam. This was the second week in a row that there were demonstrations at the French embassy over this issue, and many of the activists involved plan to attend the March 20 protest against the occupation of Iraq and Palestine. </P>

