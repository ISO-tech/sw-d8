<p><a name="Choice"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Taliya Cohen</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--About 200 pro-choice demonstrators gathered here January 22 to defend abortion rights and celebrate the 31st anniversary of the <I>Roe v. Wade</I> Supreme Court decision that legalized abortion. Participants marched to the steps of city hall, chanting, "Not the church, not the state, women must decide our fate," and later gathered at United Nations Plaza to listen to speakers.</P>
<P>"It was not the judges that won <I>Roe v. Wade</I> back then," said one protester. "It was the movement of women just like us right here today." While a few women emphasized the need for a movement, most focused on urging people to vote for a Democrat in 2004. </P>
<P>The march was called by the National Organization for Women, NARAL Pro-Choice America and Code Pink, among others. Many of these same groups have called for a pro-choice "March for Women's Lives" April 25 in Washington. We need to continue to build the grassroots fight for women's rights.</P>



