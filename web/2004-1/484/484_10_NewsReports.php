<P><p><font face="Times New Roman, Times, serif" size="4"><b>Bring the troops home now</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alex Billet</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--About 300 people turned out in front of the Capitol Building despite frigid temperatures to hold a vigil remembering the people killed during the U.S. occupation of Iraq. Headed up by Military Families Speak Out, participants were asked to read aloud the name of an Iraqi or U.S. troop killed in Iraq one by one before Bush gave his State of the Union address.  </P>
<P>When asked why they came out, people were quick to make connections between the war at home and the war abroad. "We're spending $12,000 dollars a second on the military while people in my community are starving," said Susan Cran, an organizer with Jona House Community in Baltimore.</P>

