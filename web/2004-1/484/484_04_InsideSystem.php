<P><p><font face="Times New Roman, Times, serif" size="4"><b>Doh! No lapdogs allowed</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BRITISH PRIME Minister Tony Blair has more to worry about than where Saddam Hussein's supposed "weapons of mass destruction" are. He also has to worry about his public image as the "lapdog" of George W. Bush.</P>
<P>Blair is so concerned that he recently refused to appear in an episode of <I>The Simpsons</I> alongside a cartoon dog. The original script had Blair giving Homer a corgi as a gift. </P>
<P>According to <I>Simpsons</I> producer Al Jean, "There were a couple of references that had to be changed. In the original script, there was a scene where Mr. Blair presents the Simpsons with a corgi dog as they get off the plane. </P>
<P>"This had to be cut because Downing Street felt there was a risk that he would be seen as America's poodle." In the end, the dog was cut from the script, and Blair went ahead with his guest appearance.<BR>
-- <I>Daily Mirror</I>, December 31, 2003 </P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Replacing Smokey the Bear</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">DUBYA may not have been able to push his faith-based initiative through Congress yet, but that hasn't stopped him from putting a little bit of God into the National Park Service. </P>
<P>According to a late-December press release issued by Public Employees for Environmental Responsibility (PEER), the National Park Service recently "approved the display of religious symbols and Bible verses, as well as the sale of creationist books giving a non-evolutionary explanation for the Grand Canyon and other natural wonders within national parks."</P>
<P>In <I>Grand Canyon: A Different View</I>, editor Tom Vail writes: "For years, as a Colorado River guide, I told people how the Grand Canyon was formed over the evolutionary time scale of millions of years. Then I met the Lord. Now, I have a different view of the Canyon, which, according to the Biblical time scale, can't possibly be more than about a few thousand years old."</P>
<P>In addition, PEER says that pressure from conservative groups is causing the Park Service to consider editing a videotape, shown at the Lincoln Memorial since 1995, which contains images of demonstrations--including gay rights and abortion rights rallies--that took place at the memorial. The National Park Service agreed to review the footage, according to PEER, because conservatives argued it implied that "Lincoln would have supported homosexual and abortion 'rights' as well as feminism." </P>
<P>The Park Service "promised to develop a 'more balanced' version that include rallies of the Christian group Promise Keepers and pro-Gulf War demonstrators, though these events did not take place at the Memorial," PEER said. PEER also says that in July, National Parks Service Deputy Director Donald Murphy "ordered the Grand Canyon National Park to return three bronze plaques bearing biblical verses to public viewing areas on the Canyon's South Rim"--a constitutioanl violation.</P>
<P>The Park Service is also fighting "to continue displaying an eight-foot-tall cross, planted atop a 30-foot-high rock outcropping in the Mojave National Preserve in California," PEER reports. "The Park Service leadership now caters exclusively to conservative Christian fundamentalist groups," said PEER Executive Director Jeff Ruch. "The Bush Administration appears to be sponsoring a program of Faith-Based Parks."<BR>
-- WorkingForChange, December 26, 2003</P>

