<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 30, 2004 | Page 11</font></P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Chicago Transit Authority</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Glenn Allen</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--The Chicago Transit Authority (CTA) is expanding the number of hours train operators are required to work. Starting January 25, drivers on the Orange elevated train line can be required to work up to 13 hours a shift. These hours have already been instituted on several other train lines.</P>
<P>Extended hours are just part of a larger attack on public transit workers in Chicago. Bus workers were recently forced to accept a contract that expanded the number of part-time workers and made work-rule concessions. Workers had been without a contract for four years until the union agreed to binding arbitration.</P>
<P>Job cuts in the 1990s eliminated conductors from CTA trains, replacing them with lower-paying platform attendant positions. Previously, conductors operated train doors, monitored passengers entering and leaving the trains and responded to any problems. </P>
<P>Today, drivers are responsible for these tasks, as well as driving the train. Long hours raise a serious concern for driver fatigue and the possibility of accidents. Drivers are frequently forced to work split shifts, working both the morning and evening rush hours without being paid for the time in between.</P>
<P>Federal laws tightly regulated the number of hours worked by truckers, airline pilots and other transportation workers, yet no such law exists for mass-transit train or bus drivers. Local 308 of the Amalgamated Transit Union has filed a complaint with the National Labor Relations Board.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Centre City Towers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Tina Gross</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PITTSBURGH--More than 200 union members and supporters braved frigid early morning temperatures January 23 to rally in support of nine janitors unjustly fired from their jobs at Centre City Towers. A bargaining group representing 800 janitors in major downtown buildings recently won a new contract providing janitors with affordable family health care benefits for the first time, but building management at Centre City is determined to get out of the contract by switching to a non-union subcontractor. </P>
<P>Centre City's viciousness was demonstrated the day before the rally, when a delegation of local clergy attempted to meet with building management on behalf of the workers, only to be arrested for trespassing.</P>
<P>The janitors, members of Service Employees International Union (SEIU) Local 3, have worked at Centre City an average of 11 years. They picketed the building throughout the preceding week, and they've received strong support from building tenants, who participated in the rally and have disputed management's claims that the dismissals were partly related to complaints about the quality of cleaning services.</P>
<P>With hundreds of union cleaning jobs at stake if Centre City succeeds in dumping union workers and other building managers follow their example, SEIU Local 3 has filed several unfair labor practice charges and a federal lawsuit and plans to keep the pressure on with a campaign of pickets and rallies.</P>



