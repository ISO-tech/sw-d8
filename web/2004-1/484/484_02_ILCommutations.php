<P><font face="Times New Roman, Times, serif" size="5"><b>Commutations in Illinois upheld</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OPPONENTS OF the death penalty cheered a year ago when outgoing Illinois Gov. George Ryan emptied the state's death row, pardoning four men and commuting the sentences of 167 others. But it wasn't long before the new attorney general, Democrat Lisa Madigan, tried to turn the tide back.</P>
<P>Madigan challenged 32 of the commutations in court, saying that Ryan didn't have the authority to grant clemency to prisoners who hadn't specifically filed clemency petitions. But last week, the Illinois Supreme Court disagreed.</P>
<P>Though the justices said that Ryan should have granted commutations on an individual basis, they said that the "failure of certain inmates to consent to their petitions was irrelevant"--because Ryan based his decision on the bias and flaws of the death penalty system itself.</P>
<P>In other words, Ryan came to recognize what should be evident to Madigan and every other politician after seeing 17 innocent men freed from death row since 1987--that the execution system is broken beyond repair. Today, Madigan and other Illinois politicians are pushing to gear up the machinery of death once again--saying that a recently signed death penalty reform bill will restore the "integrity" of the system.</P>
<P>But Ryan's commutations shined a spotlight on the racist death penalty in Illinois and across the country. And no matter how much politicians tinker with the death penalty, they can't reform away the racism, bias and error of the criminal injustice system.</P>



