<P><font face="Times New Roman, Times, serif" size="5"><b>Sterling Laundry strikers get a lift on MLK Day</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Josh Brand and Jesse Hagopian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 30, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The ongoing strike at Sterling Laundry got a needed boost on Martin Luther King Day, thanks in part to Rev. Graylan Hagler. Rev. Hagler is the national president of Ministers for Racial, Social and Economic Justice, and the senior minister of Plymouth Congregational United Church of Christ.</P>
<P>The day began with a memorial service for King that recognized his fight for equality, social justice and workers' rights, and featured both workers from Sterling and the Washington Teachers Union. Following the service, approximately 75 people chanted and marched to the laundry site, led by Sterling workers. </P>
<P>Once there, they formed a picket line, while Rev. Hagler tried unsuccessfully to get a company representative to speak to him. Minutes later, Rev. Hagler found an unlocked door around the side of the building and went inside. About 25 more people made it inside the building after some local activists got the back door open.  </P>
<P>Immediately, the manager and three security guards charged the crowd and began physically restraining the strikers as well as Rev. Hagler. When the crowd tried to enter the back area where the workers clean the clothes, a guard pulled Hagler's beard and shoved one of the strikers. In response, the crowd began chanting, "No justice, no peace!"</P>
<P>After the manager and guards had secured the door to the laundry room, the group left the building and continued to march and protest in front of the building. According to Victor Caraballo, the lead UNITE organizer at the demonstration, the union plans to "keep the workers strong and focus on the customers," such as the Montgomery County Police Department and Sibley Hospital here in D.C.</P>



