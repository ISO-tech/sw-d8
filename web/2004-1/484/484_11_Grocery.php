<P><font face="Times New Roman, Times, serif" size="4">AFL-CIO takes major role in grocery struggle</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Get involved in the fight"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 30, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE AFL-CIO is taking a central role in the strike and lockout of 70,000 grocery workers in Southern California, with solidarity actions set to take place across the country. After a big Los Angeles labor solidarity rally set for January 31, unions in New York City plan to converge on Wall Street February 5 for a protest to put the heat on the backers of the three grocery chains involved--Kroger Co., Albertsons, Inc. and Safeway, Inc.</P>
<P>"This is not about the grocery industry--it's about a rising crisis of health care in this country," said Ron Judd, Western Region Director for the AFL-CIO. "[Safeway CEO] Steve Burd is sending a signal to other employers that there is a strategy you can take" by attacking unions, Judd said. "Everyone who cares about health care, fairness and a more equal distribution of wealth in this country needs to get involved in this fight."</P>
<P>Plans to support the fight by the United Food and Commercial Workers (UFCW) include informational pickets at some stores across the country as well as rallies and support committees. There was some escalation of strike activism in Los Angeles in mid-January.</P>
<P>Picket lines returned to some stores in the Kroger-owned Ralphs chain, where pickets had been pulled in late October. UFCW Local 1442, which covers Santa Monica, Culver City and West Los Angeles, has organized big pickets of 200 UFCW members and community supporters that have shut several stores down during the protests. </P>
<P>As <I>Socialist Worker </I>went to press, UFCW members and activists were planning a visit to Safeway/Vons CEO Steven Burd's home in Pleasanton, Calif. The workers planed to bring their children and puppies--highlighting the fact that Burd has apparently donated generously to animal rescue and related causes and apparently cares more about the health care of animals than that of his own employees and their families.</P>
<P>Solidarity actions have been taking place throughout the West Coast. In San Francisco January 24, nearly 500 labor and community activists turned out for a feisty picket at the Safeway store at Church and Market Street. With chants of "They say cut back, we say fight back" and "They say co-pay, we say no way", picketers jammed the entrances and spilled out into the parking lot, deterring the vast majority of shoppers from setting foot in the store.</P>
<P>At one point, activists sneaked into the store with signs and banners, and continued the protest. When police "escorted" activists into a police van, fired-up picketers then surrounded the van and chanted until the police backed down.</P>
<P>On the same day, a spirited group of around 60 people picketed a Safeway store in Portland, Ore. The picket, organized by Jobs with Justice, was attended by members from many unions.</P>
