<P><font face="Times New Roman, Times, serif" size="4"><b>Making kids criminals</b></font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
On February 4, Alamance County, N.C., police officers cashed in on a covert drug sting operation. Forty-nine people were arrested and charged with trafficking illegal drugs (mostly marijuana). These 49 people were all students or of high-school age, and were rounded up by undercover cops who had enrolled in all six Alamance county high schools the previous fall. </P>
<P>What's more, the cops were <I>invited </I>by Superintendent James Merrill. According to Alamance County District Attorney Rob Johnson, "We've made a good solid start on it. So far, things are going on track."</P>
<P>On track for what? Revamping the county's juvenile system? Criminalizing a new generation of poor North Carolinians? If Superintendent Merrill were really concerned about education, he would argue to transfer the money spent on drug sting operations to his crumbling schools. </P>
<P>Instead, 49 high school-aged kids, all but two with clean criminal records, are having to work their way through our sick criminal justice system. Over half have already been sentenced--most of them for selling small amounts of marijuana one time--and won't be allowed back into school until next year. </P>
<P>What are their chances of getting out of economically depressed rural North Carolina now? We have to stand up against the criminalization of our schools and forcefully put forward the slogan "Money for jobs and education! Keep the police out of our schools!"<BR>
<B>Brad Ward,</B> Greensboro, N.C.</P>

