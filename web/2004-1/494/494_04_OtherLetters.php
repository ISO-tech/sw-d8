<p><a name="Race"></a><font face="Times New Roman, Times, serif" size="4"><b>Fighting to stop a race to the bottom</b></font> </P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Lee Sustar's article "What's to blame for job losses?" (<I>SW</I>, March 26) put the whole issue of "outsourcing" in its proper context. The problem is not "American" jobs going to India--but capitalist attacks on workers the world over, using job transfers as one weapon. </P>
<P>We should oppose any attempt to lower wages or working conditions. We should oppose and try to prevent any transfer of jobs from higher-paid to lower-paid situations--whether from union to nonunion in the U.S., public to private in the U.S., or from the U.S. to countries with lower wages.</P>
<P>But we need to be clear that the reason we oppose job transfers is not because these jobs should belong to Americans. As internationalists, we don't say "jobs for Americans." We say "jobs for all." The only way to successfully oppose capitalist attacks on all workers is to unite. </P>
<P>We need solidarity across national , ethnic and racial lines. We can't have that if we support "Buy American" or "Keep American jobs in the U.S." We need to be clear that we don't oppose "American" jobs going to India, China or Canada. </P>
<P>We oppose the capitalist attempt to drive down wages worldwide by exploiting the lowest waged workers possible. Our solution is to raise wages everywhere, <I>not</I> to keep all "American" jobs in the U.S. </P>
<P>The union movement needs to be more militant in fighting against privatization, outsourcing to nonunion workplaces and speedup of work. It needs to oppose even more vigorously U.S. attempts to impose neoliberalism (deregulation, privatization, tax-cutting, etc.) on other countries. We can only stop the "race to the bottom" by internationalism, not by supporting the nationalist appeals of Bush or Kerry.<BR>
<B>Steve Leigh,</B> Seattle</P>

