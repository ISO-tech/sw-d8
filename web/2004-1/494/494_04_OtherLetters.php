<p><a name="LGBT"></a><font face="Times New Roman, Times, serif" size="4"><b>Transgendered need to be included</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I have been following <I>SW</I>'s coverage of the same-sex marriage struggle for some time, and have noticed that the transgendered are often left out. Many times, <I>SW'</I>s articles will talk about "gay and lesbian" people and "gay and lesbian" liberation.  </P>
<P>According to a study done by the National Gay and Lesbian Task Force on campus climate, transgendered people are the most likely to be victims of a violent crime, most likely to be harassed and most likely to be discriminated against because of their gender expression.</P>
<P> The inclusion of the transgendered is politically important because some within the lesbian, gay, bisexual and transgendered (LGBT) community argue that the issue of same-sex marriage ignores the needs of this marginalized group entirely.To be more inclusive, I would strongly recommend that <I>SW</I> use the common term "LGBT" instead of just "gay and lesbian."<BR>
<B>Brian Rainey,</B> Providence, R.I.</P>

