<p><a name="Thugs"></a><font face="Times New Roman, Times, serif" size="4"><b>The model for Haiti's new thugs</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I was disgusted to read an article published in the February 28 <I>Miami Herald</I> interviewing Guy Philippe, a leader of the Haitian "opposition" who recently forced Jean Bertrand Aristide out of office. Philippe claims that his renewed opposition after being expelled in 2000 for fomenting a coup is because Aristide "became a tyrant". </P>
<P>Yet, in the same interview, he says that the man he most admires is General Augusto Pinochet. Pinochet was the brutal dictator of Chile from 1973 to 1990. He was responsible for murdering 30,000 socialists, unionists and Allende supporters in the year after he took power via a U.S.-backed coup against the popularly elected socialist Salvador Allende. </P>
<P>Pinochet also implemented U.S. friendly "reforms" that doubled the poverty rate in Chile from 20 to 41 percent from 1970 to 1990 and led to vast disparities in wealth. Philippe says that this man "made Chile what it was." He is right. </P>
<P>This right-wing thug wants to lead Haiti down the same path as Chile under Pinochet. He is now parading through Haiti, providing "security" to Port-au-Prince and promising to "reconstitute the military." Philippe received his military training from U.S. Special Forces in Ecuador in the early 1990s. </P>
<P>The U.S. for weeks leading up to the coup portrayed the opposition lead by thugs like Philippe as "freedom fighters." This shows it has no interest in supporting "democracy" in Haiti. We must demand that the U.S. leave Haiti now. <BR>
<B>Bill Linville,</B> Madison, Wis.</P>

