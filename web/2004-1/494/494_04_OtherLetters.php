<p><a name="UPS"></a><font face="Times New Roman, Times, serif" size="4"><b>Looking for help to take on UPS</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I am a driver for UPS and through the years I have had multiple injuries on the job. Two of them required surgeries, and now, I am on workers' compensation for the second time with a lower back injury.</P>
<P>The doctors say it's muscle problems, and that I need to make up my mind if I want to keep my job or not. I could be retrained in another field, but would probably make a lot less than I was making at UPS. What I am seeking is for UPS to create a modified job with less physical work, since I have just about beaten my body to hell for them. </P>
<P>UPS's position on this is that if they do it for me they will have to do it for everyone. This statement is false. There are at least three to four workers they have created a job for in my center alone.</P>
<P>I am looking for a lawyer who is not afraid of UPS to push this issue to the fullest before giving up a very good-paying job with full benefits for me and my kids. I am located in Central California. If there is anybody that could assist me please drop me an e-mail at COTTA1C@aol.com.<BR>
<B>Christopher M. Cotta,</B> From the Internet</P>

