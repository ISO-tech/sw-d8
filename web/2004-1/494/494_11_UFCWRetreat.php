<P><font face="Times New Roman, Times, serif" size="4">D.C. grocery workers agree to big concessions</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Another retreat by UFCW</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 9, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Workers at Safeway Inc. and Giant Food Inc. supermarkets overwhelmingly approved a new four-year contract March 30. Well over 10,000 employees turned out for two back-to-back meetings to hear the details of the agreement and vote.</P>
<P>The contract was hailed as a victory by the leaders of United Food and Commercial Workers (UFCW) Local 400, by the media and by many workers themselves. A strike like that in Southern California was "averted" and the union "held the line" on health care, the argument goes.</P>
<P>But closer inspection of the details reveals a defeat. Current workers' health care deductibles increase from $100 to $200 a year. While workers still pay no monthly premium for health coverage, the cap on out-of-pocket expenses was raised from $2,500 to $4,000 per year, and the companies will contribute less for prescription drug coverage.</P>
<P>In addition, workers got a paltry 30 cents an hour raise each year--below the rate of inflation. What's more, new hires are not eligible for equal pay and full benefits until they have worked for <I>six </I>years.</P>
<P>New employees will get pay differentials for Sunday and holiday work of just an extra $1 per hour, compared to time-and-a-half pay under the new contract. Those new workers will have to wait 60 months until their pay differentials reach top levels.</P>
<P>Given the high rate of turnover in grocery work, this ensures the companies greater profits over the long haul as new hires who earn less become the majority. Karen, a Safeway worker from Waldorf, Md., described the bosses' strategy clearly. "They won't lower prices [for customers] if they cut our benefits. It all just goes in their pockets," she told <I>Socialist Worker</I>.</P>
<P>The turnout of over 12,000 workers at the two meetings--many bused in from outlying areas--underscore how much was at stake with this contract. Yet workers were told nothing about the deal until they arrived that morning. They heard a speech by Local 400's president, Jimmy Lowthers, and then agreed to a standing voice vote. </P>
<P>Given this lack of preparation, only a handful of Safeway workers stayed seated during the vote, indicating a "no," along with some 200 Giant workers. But given the UFCW's disastrous handling of the Southern California strike and lockout, it's not surprising that workers voted to take the deal.</P>
<P>Local pundits argue that the strike in California--along with Local 400's own losing battle against Kroger Co. in West Virginia last fall--deeply impacted both sides in negotiations. They suggest the companies weren't pushing so hard this time on health care costs and that the union didn't take as strong a stand.</P>
<P>In fact, this contract not only makes concession on health care, but also sells out the next generation of workers who will no longer be able to make a living wage working for these companies. Until labor leaders are pushed by an organized rank and file to do more, we run the risk of more "victories" like these.</P>


