<P><font face="Times New Roman, Times, serif" size="5"><b>The genocide they ignored</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"I'LL ALWAYS regret that Rwandan thing." Famous last words from Bill Clinton, whose administration 10 years ago this month looked away while genocide took place in the African country of Rwanda. Beginning in March 1994, the Hutu-led government of Rwanda oversaw a several-months-long slaughter that took the lives of up to 1 million ethnic Tutsis.</P>
<P>U.S. forces, which just a few months earlier had been forced to leave their brutal "humanitarian" mission in Somalia, weren't sent on a United Nations (UN) peacekeeping mission in Rwanda. In fact, the U.S., along with Belgium and France, told the UN to <I>reduce </I>its forces in Rwanda.</P>
<P>Later, the Clinton administration pleaded ignorance. "All over the world, there were people like me sitting in offices who did not fully appreciate the depth and speed with which you were being engulfed by this unimaginable terror," Clinton told reporters during a stopover in Rwanda in 1998.</P>
<P>But a recent National Security Archive report provides more evidence that the White House was anything but ignorant of the horror in Rwanda. According to reports for this period obtained through the Freedom of Information Act, a regular CIA briefing circulated to Clinton, Al Gore and hundreds of officials included almost daily reports on Rwanda.</P>
<P>A report dated April 23, 1994, said rebels would continue fighting to "stop the genocide, which...is spreading south." On April 26, White House officials were told of "genocide and partition"--and of declarations of a "final solution to eliminate all Tutsis."</P>
<P>As early as April 8, a National Security Archive report notes, "The massacres in Rwanda were reported on the front pages of major newspapers and on radio and television broadcasts almost daily." Nevertheless, the administration deliberately avoided using the word "genocide" publicly--because that would have obligated the U.S. government to take action.</P>
<P>As Rom&eacute;o Dallaire, the general in charge of the UN operation in Rwanda, told Scotland's <I>Sunday Herald, </I>"I still believe that if an organization decided to wipe out the 320 mountain gorillas [in Rwanda's Virunga Mountains], I believe there would still be more of a reaction to curtail or stop that today than there would be in attempting to protect thousands of human beings slaughtered in the same country. Not all humans are 'human' in the international context."</P>


