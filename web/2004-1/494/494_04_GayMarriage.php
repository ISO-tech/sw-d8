<P><font face="Times New Roman, Times, serif" size="5"><b>We can't afford to ignore the gay marriage fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 9, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I><BR>
</B>If marriage rights would really be such a "gigantic step backwards" for gays and lesbians, as Ahoo Tabatabai claims (<I>SW,</I> March 26), why is the right so determined to prevent this change from happening? What the right wingers understand, but Tabatabai apparently does not, is that winning equal access to marriage would constitute a major blow to the legitimacy of homophobic ideas--and would be a shot in the arm to the gay rights movement more generally.</P>
<P>Defending equal access to marriage does not mean uncritically supporting marriage as an institution. But allowing the right to pass laws that openly discriminate against gays and lesbians only reinforces the dominance of conservative ideas about what constitutes a "legitimate" relationship. </P>
<P>Moreover, it is fantasy to think that by ducking the fight against the right on the marriage issue, we'll somehow be in a stronger position to achieve national health care and other reforms that would make it easier for people who don't want to get married to live as they choose.</P>
<P>It is worth remembering that, during the 1940s, the cutting edge of the African American civil rights movement was the campaign to desegregate the military. The military is, of course, a thoroughly reactionary institution (much more unequivocally so than marriage, in my opinion). </P>
<P>When A. Philip Randolph organized a civil disobedience campaign in 1948 to pressure Harry Truman to end Jim Crow in the armed forces, it paved the way for a host of legal victories in the early 1950s, including <I>Brown v. Board of Education</I>. It also inspired Blacks to demand equality in other spheres. </P>
<P>Would Tabatabai advocate barring Blacks from the military? We can't afford to duck any fight against discrimination--even as we struggle to transcend or abolish the institutions that practice it.</P>
<B><P>Matt Nichter</B>, Madison, Wis.</P>


