<P><font face="Times New Roman, Times, serif" size="4">Cop's sister confirms Black suspects were brutalized</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Chicago police torture exposed</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Susan Dwyer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE SILENCE has been broken. After more than 20 years, someone with inside information has come forward to confirm the stories told by dozens of African American men--that Chicago police under the command of Jon Burge tortured suspects at Areas 2 and 3 police headquarters on Chicago's South Side.</P>
<P>In 1987, Chicago Police Detective Robert Dwyer bragged to his sister Ellen Pryweller, "I can make anyone confess to anything." Anyone meant the likes of Madison Hobley, Leroy Orange, Aaron Patterson and Stanley Howard, members of the Death Row 10 who were pardoned by former Gov. George Ryan in January 2003. </P>
<P>Pryweller has videotaped a statement about her brother's boast that investigative journalist Carol Marin obtained from Hobley's lawyers and broadcast on NBC News in Chicago. Hobley said that Pryweller's willingness to testify on his behalf against Chicago's police torturers is a "blessing"--and "a long time coming."</P>
<P>Pryweller was present at Northwestern University Law School when Ryan commuted all death sentences in 2003. Ryan's speech that day caused Pryweller to connect the facts about allegations of police torture to the conversations she had with her brother. </P>
<P>She has also reportedly overheard Dwyer, Burge and other officers bragging about their torture skills. "They began to boast about power and what really happens in a police station," she said.</P>
<P>Hobley's case is a brutal example of the ability of Burge's cops to get "anyone to confess to anything." Hobley was arrested and charged with murder for setting a fire that killed his wife, child and five other people. Almost before the Fire Department had determined the cause of the fire, Jon Burge and his gang decided that Hobley was a convenient Black suspect--and began torturing him into making a false confession.</P>
<P>In fact, anti-death penalty activists and defense lawyers know of more than 60 men who were tortured by Burge and his gang in blue. But no one--not Mayor Richard Daley, who was state's attorney at the time, nor Cook County State's Attorney Dick Devine, who was an assistant, nor the dozens of other officials and police personnel who were in and out of Areas 2 and 3 during Burge's reign of torture--ever said a word.</P>
<P>In 1993, the Illinois Supreme Court concluded that Burge and his men had carried out years of "systematic torture." That embarrassed the city enough to force Burge into retirement.</P>
<P>But even after the Supreme Court's ruling, every attempt by defense attorneys to raise the issue of torture to challenge the convictions of their clients were thrown out. No criminal court judge has been willing to take the word of any prisoner over that of the police department and prosecutors.</P>
<P>In April 2002, anti-police brutality and anti-death penalty activists won a victory when a special prosecutor was appointed to investigate Burge and the cases of the African Americans he tortured. But the investigation has dragged out, with no word being made public. With the emergence of Ellen Pryweller's testimony, activists should step up the pressure and demand a swift and public investigation of Burge and his thugs.</P>
<P>Members of the Enough Is Enough Campaign against police torture and the Campaign to End the Death Penalty are cosponsoring a picket and press conference at the special prosecutor's office on April 6, and a public forum to expose the allegations of torture on April 20. We need to put more pressure on the Chicago police and prosecutors to break their conspiracy of silence--and finally give justice to the men who were tortured by Chicago's finest.</P>

