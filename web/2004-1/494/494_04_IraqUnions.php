<P><font face="Times New Roman, Times, serif" size="4">Iraqi governing council chooses union for workers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>So this is "liberation"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 9, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I><BR>
</B>The U.S. occupation's handpicked Iraqi Governing Council is following in the footsteps of the former Baathist regime by choosing a labor federation to represent Iraqi workers. The Iraqi Federation of Trade Unions (IFTU) has been designated as the sole "legitimate representative" of Iraqi workers. </P>
<P>This is a violation of the rights of Iraqi workers, especially those who have chosen membership in other labor organizations, such as the Federation of Workers Councils and Unions (FWCU) and the Union of the Unemployed.</P>
<P>There is speculation that the IFTU was favored due to many of its leaders being members of the Iraqi Communist Party, which is part of the governing council picked by the U.S. The leadership of the FWCU, which many Iraqi as well as international labor activists regard as more militant than the IFTU, includes members of the Workers Communist Party, who have refused to recognize or serve on the governing council.</P>
<P>Since the fall of the Baathist regime, there has been an upsurge in union organizing among Iraqi workers, in spite of a 70 percent unemployment rate. Iraqi workers have to defy the U.S. occupation force. One law from Saddam Hussein's rule that the U.S. occupation has kept in force is a measure prohibiting Iraqi workers in the public sector, which includes a large percentage of the workforce, from unionizing or striking. </P>
<P>In their statement condemning the governing council's decision, the FWCU calls for a national conference of Iraqi workers to decide independently what labor organizations they should organize and belong to. Their statement reads, in part: "[W]e totally reject this resolution and regard it as an attempt to enforce the practices of the ousted Baath regime, which denied workers any control over their own affairs and erected bureaucratic and repressive bodies which had nothing to do with the interests of workers. </P>
<P>"The resolution number 3 is a part of the attempts by the state apparatus to control workers despite all rhetoric about freedom...Genuine and influential labor organizations, which represent workers, can only be established when workers themselves freely elect their own representatives."</P>
<P>This attack on workers democracy in Iraq shows the hypocrisy of the claims of the U.S. government to "liberate" the Iraqi people.  </P>
<B><P>Ken Morgan, ILWU Local 6</B>, San Francisco </P>
<I><P>To read the full statement, visit www.uuiraq.org on the Web.</I> </P>

