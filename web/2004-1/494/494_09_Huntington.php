<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Beware racism in scholar's clothing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 9, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE OF the oldest racist jokes plays on the misunderstanding of a Mexican named Jos&eacute; who thinks he hears his name every time he listens the U.S. national anthem. So it was appropriate that <I>Foreign Policy</I> magazine chose to put the punch line of that joke--"Jos&eacute;, Can You See?"--on the cover of its May/April, 2004, issue to headline its lead article, Samuel Huntington's "The Hispanic Challenge."</P>
<P>Huntington's article is certainly crude and racist. But it's not a joke. Its appearance in a major foreign policy journal marks a new low for what is considered intellectual discourse in the U.S. The message of "The Hispanic Challenge" echoes nativist outfits like the Federation for American Immigration Reform or figures like Patrick Buchanan.</P>
<P>Its main point is simple: that Latino immigration--and in particular immigration from Mexico--threatens the cultural identity of the U.S. whose pillars are the English language, free-market capitalism and Protestant Christianity.</P>
<P>To prevent this outcome, the U.S. should make clear that "There is no Americano dream. There is only the American dream created by an Anglo-Protestant society. Mexican Americans will share in that dream and in that society only if they dream in English." Huntington even raises the specter of a Latino "reconquest" of the U.S. Southwest that could even lead for demands of autonomy for the Latino minority.</P>
<P>One could note the irony of Huntington's complaint about an area that was, after all, part of Mexico until the U.S. stole it in a war in 1847. But leaving that aside, "The Hispanic Challenge" is more remarkable for its ability to twist what few empirical facts it cites to make its case.</P>
<P>For instance, it horrifies Huntington that Cuban Americans dominate business, the media and politics in South Florida. Isn't success in business and politics supposed to be a sign of attaining the American dream?</P>
<P>Another absurd claim is--Shock! Horror!--bilingual families in Miami have higher incomes than families who only speak English or Spanish. In most countries, the ability to speak more than one language is considered good thing.</P>
<P>So Huntington attacks Latinos in Miami for being too successful, while attacking Mexican immigrants for not being successful enough. He worries about the creation in the Southwest of a large population whose "Hispanic traits" include "lack of initiative, self-reliance, and ambition; little use for education; and acceptance of poverty as a virtue necessary for entrance into heaven."</P>
<P>One wonders how millions of people who make harrowing journeys across the border and who work multiple jobs at low wages lack ambition and self-reliance. The kind of rubbish that Huntington piles on Mexican immigrants is the same that was laid on immigrants from Southern Europe in the early 1900s--who are now, in his view, good Americans.</P>
<P>Huntington--a Democrat and prominent supporter of former President Bill Clinton--has been one of the leading right-wing ideologues of the last generation. In the mid-1970s, he co-authored <I>The Crisis of Democracy</I>, an alarmist tract that argued that democratic governance in the U.S. and Western Europe faced threats from rising popular demands from workers and oppressed groups. In hindsight, <I>The Crisis of Democracy</I> can be seen as an ideological blueprint for the conservative attack on the gains of the 1960s-1970s movements.</P>
<P>When the U.S. ruling class was trying to come up with a post-Cold War rationale for U.S. foreign policy, Huntington published <I>The Clash of Civilizations</I> in 1996. Debated respectfully in the journals of the foreign policy elite, <I>Clash of Civilizations</I> proposed a worldwide conflict between different "civilizations" that appear to be based on little more than racial and cultural stereotypes.</P>
<P>In "The Hispanic Challenge," Huntington feigns worry about the threat of a "white nativism" that would grow as a backlash against "cultural and linguistic" threats from Latinos--as if his screed would have nothing to do with stirring up such a backlash.</P>
<P>Huntington's latest is merely an attempt to give academic gloss to barroom prejudice. It deserves nothing but contempt.</P>


