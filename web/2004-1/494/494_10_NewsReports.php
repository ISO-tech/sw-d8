<p><a name="AlAwda"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Palestine</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Palestinian rights supporters will gather April 16-18 for the second national convention of Al-Awda the Palestine Right to Return Coalition. The event will bring together hundreds of activists, academics and organizers from the U.S. and around the world for workshops and strategy sessions dedicated to strengthening the fight for Palestinian rights.</P>
<P>"The fundamental right for Palestinians to return to their homes, from which they were forcefully removed, is under constant threat," Al-Awda states in its call for the convention. "The U.S.-sponsored 'road map' initiative, which, from its inception, has been a ploy to formalize the expansion of an already illegal occupation...to the Apartheid Wall that carves up Palestine into concrete ghettos, splits communities and isolates Palestinians...activists and people of conscience across the world refuse to waiver in their support for the Palestinian struggle for liberation, self-determination and return."</P>
<I><P>Visit www.alawdaconvention.org to register.</I> </P>

