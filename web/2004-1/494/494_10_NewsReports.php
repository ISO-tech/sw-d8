<p><a name="Farouk"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Farouk Abdel-Muhti</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">HARRISBURG, Pa.--Political activist Farouk Abdel-Muhti had his first hearing in federal court March 30 on his petition for release from immigration detention. Abdel-Muhti, a stateless Palestinian, was arrested on a 1995 deportation order in April 2002, a month after he began working with WBAI-FM to arrange live interviews with Palestinians in the Occupied Territories. </P>
<P>The government claims Abdel-Muhti can be deported under a new procedure in which Palestinians on Israel's population registry for the Occupied Territories can be repatriated through Israel. Yet Abdel-Muhti isn't on this registry, having left the West Bank in 1960, before the Israeli occupation. </P>
<P>April 26 marks the second anniversary of Abdel-Muhti's arrest. "We plan to mark the date with action," said David Wilson, a member of the Committee for the Release of Farouk Abdel-Muhti. "Hopefully, it will be a celebration and not another protest."</P>
<I><P>For more information, go to www.freefarouk.org, or contact the committee at 212-674-9499 or freefarouk@yahoo.com.</I> </P>


