<P><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--A multiracial rally of 700 people sounded their anger at the police murder of James Jahar Perez, the second unarmed Black motorist to be killed by city cops in 10 months. At the rally organized by the Coalition of Black Men, friends, family and other victims of police violence expressed their of fear of "DWD"--driving while black.</P>
<P>"We are not going to sit back and be brutalized anymore," said a Portland State University student representing the NAACP. "We need to take it to the streets where it belongs."</P>
<P>Within 30 seconds of being stopped by police officer Jason Sery for allegedly failing to give a turn signal, the officer shot Perez, who was sitting in his car, three times. After he was shot, another cop fired a tazer at him, zapping Perez with electric current for over three minutes.</P>
<P> The shooting comes 10 months after Kendra James, a young Black mother of two, was shot and killed by another cop, and two days before the third anniversary of the cop killing of Mejia Poot, a Mexican migrant.</P>
<P>This isn't Sery's first incident of brutality. Just three weeks earlier, he pulled his gun on a man who had his hands in his pocket, and was also the subject of a $5,000 lawsuit against the city.</P>
<P>The African American community is outraged. "Is there a vigilante group in the Portland police department?" asked Oscar Eason Jr., the president of the NAACP's state conference encompassing Oregon, Washington and Alaska.</P>

