<p><a name="CAN"></a><font face="Times New Roman, Times, serif" size="4"><b>East Coast Campus Antiwar Network</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Julie Keefe and Afsaneh Moradian</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--On April 3-4, student antiwar activists representing 21 schools, from Maine to Georgia, gathered for the Campus Antiwar Network's (CAN) East Coast conference.</P>
<P>Should CAN support a United Nations (UN) occupation of Iraq? Should CAN support the Iraqi resistance?  These two debates were repeated in the workshops, evening panel and proposals throughout the weekend.</P>
<P>Workshops included discussion of the lessons from the Vietnam War, the 2004 election, and the Palestinian struggle. The evening panel put a human face on the occupation, with CAN's Khury Petersen-Smith reporting on his recent trip to Iraq and Mike Hoffman, a member of CAN and Vets for Peace, describing the horrific conditions faced by U.S. troops.</P>
<P>The conference passed some significant points of unity, including those against the invasion and occupation of Afghanistan and against a UN occupation of Iraq. Resolutions passed included those that support the right of Iraqis and U.S. troops to oppose this occupation and call for the immediate withdrawal of troops from Haiti.</P>

