<P><font face="Times New Roman, Times, serif" size="4">Why pump prices are rising</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gouged by the oil companies</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 9, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NOW THAT Iraqi oil is in the clutches of the U.S. empire, why are gas prices so high? Conservative media commentators bash the Organization of Petroleum Exporting Countries (OPEC) for cutting production and driving gas prices higher. Other analysts point to the ongoing crisis in the Middle East.</P>
<P>In any case, they say, stop your whining, because it could be worse. "[T]oday's average price doesn't approach the one reached in 1981, around $2.80, adjusted for inflation, and prices in Europe are typically far higher than that," oil industry analyst and free-market propagandist Daniel Yergin wrote in the <I>New York Times</I>.</P>
<P>Yet even if gas is relatively cheaper than it was in the early 1980s, the stagnation in real wages since then means that gasoline takes a bigger bite out of most people's income. Plus suburban sprawl and the lack of public transportation in most cities requires longer commutes--and more gas--for today's drivers. For working people living paycheck to paycheck, a higher price at the pump means a squeeze on other spending--groceries or clothes for the kids, for example.</P>
<P>According to Democratic presidential candidate John Kerry, the blame should be put on George W. Bush. Kerry accused oil companies of getting "Halliburton profits" by gouging consumers.</P>
<P>By pointing the finger at Dick Cheney's old oil services company, Kerry apparently hopes that no one will notice that his wife has substantial investments in companies like the big Canadian company Imperial Oil, the global giant Royal Dutch Petroleum, and Classic Petroleum Inc., a company specializing in finding oil reserves, among several others. Kerry's oil interests shed light on the real question: Who really benefits from higher oil prices?</P>
<P>Washington's longstanding hatred of OPEC isn't really about higher oil prices, but who profits from them. The decline in the value of the dollar on world currency markets--a policy pursued by Washington--has compelled OPEC to charge higher prices, since oil is priced in dollars.</P>
<P>And by limiting refining capacity, U.S. oil companies themselves are doing exactly what they accuse OPEC of doing--manipulating supply to boost the bottom line. The oil companies' argument that various gasoline formulas used by different states are a factor in higher prices is another excuse--and an argument to roll back restrictions on emissions that reduce air pollution.</P>
<P>As the <I>Detroit News</I>, a mouthpiece of the automobile industry, put it in an editorial, "[T]he one thing that government could do to is relax, even temporarily, its requirements for specially blended fuels for environmental reasons for different parts of the country." So the next time a right-wing blowhard bashes Arab countries for "manipulating" oil prices, take a look the corporate logo at your local gas station--and put the blame closer to home.</P>


