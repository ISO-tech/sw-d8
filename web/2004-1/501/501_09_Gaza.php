<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No refuge for Rafah refugees</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE NEWS of Israel's assault in the Rafah refugee camp in the Gaza Strip is shocking--more than 40 killed, many of them children, and hundreds of houses bulldozed without notice with plans to bulldoze hundreds more. Since 2000, Israel has driven more than 11,000 people (about one in 10 people in this refugee camp) from their homes.</P>
<P>Am I reading it right? Israel has made refugees out of people who were already refugees? You would think this might warrant some kind of explanation--something like this: "Gaza became a refuge for about two-thirds of the 700,000 Palestinian who had been forced out of their villages by Zionist militia in the drive to create the State of Israel in 1948."</P>
<P>Gaza--a tiny area 27 miles long and 3 to 7 miles wide--is full of refugees whose original homes and land (now occupied and owned by Israelis) are only hours from where they now live in destitution. Completely fenced in, Gaza has only one crossing-point into Israel at its northern tip, where every day thousands of Palestinians line up to try and get to jobs in Israel. </P>
<P>A majority of the population is unemployed, dependent on United Nations (UN) relief to survive. There are 7,500 Jewish settlers who occupy 30 percent of the best land in the strip in settlements that are connected by exclusive Jewish roads and border crossings.</P>
<P>Since 1948, Israel has invaded the strip many times (occupying it after the 1967 war, before which it was controlled by Egypt), and each time has seized more land, demolished more homes, killed more "militants" and created more Jewish-only roads. In 1971, for example, Ariel Sharon ploughed 50-meter-wide roads through the Gaza refugee camps to facilitate army patrols, destroying 2,225 homes.</P>
<P>You can't understand why refugees are made of refugees without knowing the history. In 1917, the Balfour Declaration proclaimed British support for the formation of a "Jewish homeland" in Palestine, then essentially a British colony.</P>
<P>The Zionist movement secured this deal by agreeing to set up a loyal colonial-settler outpost. "What the French could do in Tunisia the Jews would do in Palestine," said Zionist leader Chaim Weizmann.</P>
<P>With British imperial backing, Zionist organizations conducted a systematic program of land purchases and immigration of Jews to the region. The Zionist plan was to establish an exclusively Jewish state, based upon "Jewish land" and "Jewish labor," by displacing the 1.4 million Arabs already living there.</P>
<P>The Jewish population went up from 10 percent in 1916 to 31 percent in 1947. According historian Baruch Kimmerling, Jewish settlers and organizations owned only 7 percent of the land of Palestine in 1947, "all the rest was taken by sword and nationalized during the 1948 war and after."</P>
<P>The UN Partition Plan awarded the Zionists 55 percent of the land of Palestine, and Zionists militias took the rest (90 percent of the total land) by outright terror. Zionist forces razed 385 out of the 475 Arab villages to the ground.</P>
<P>Tens of thousands were induced to flee after Zionist death squads murdered all 254 inhabitants of Dier Yassin. Massacres took place in many other villages. Raphael Eitan, chief of staff of the Israeli armed forces at the time, declared: "The Arabs have no right to settle on even on centimeter of Eretz Israel...When we have settled the land, all the Arabs will be able to do will be to scurry around like drugged roaches in a bottle."</P>
<P>Eretz Israel stands for "greater Israel"--the area Israel would occupy if it had expelled <I>all</I> Arabs from Palestine. Israel hasn't achieved its complete goal of creating an all-Jewish state in all of historic Palestine.</P>
<P>Indeed, it can't decide whether to seize most of the West Bank and leave Gaza as an open-air prison and pull out Jewish settlements. But it isn't possible to understand what happens in Gaza and the West Bank today without knowing that Israel is founded on displacing another people.</P>

