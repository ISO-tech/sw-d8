<P><font face="Times New Roman, Times, serif" size="5"><b>"You cannot kill the Haitian people's spirit"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Winston Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>The Agronomist,</B></I> a documentary by Jonathan Demme.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE ONLY time we hear about Haiti in the media, the island is presented as an incomprehensible mix of poverty, violence, corruption and an unending succession of dictators and coups. <I>The Agronomist,</I> a documentary by Jonathan Demme about Jean Dominique, Haitian social activist and owner of independent Radio Haiti, offers a needed explanation.</P>
<P>Demme uses 20 years' worth of interviews with Dominique and archival video footage to tell the history of Haiti. The film's title comes from Dominique's idiosyncratic description of himself as "an agronomist who became a journalist."</P>
<P>Dominique's introduction to politics came at the age of 4, when his father told him to look away from the U.S. occupying troops whenever they passed the house. His life spans two U.S. occupations (1915-34 and 1994), two U.S.-backed dictators ("Papa" and "Baby Doc" Duvalier) and two exiles.</P>
<P>Dominique gradually changes his radio station from apolitical light entertainment to current events reporting, with the important distinction of being the first to broadcast in Kreyol, the language of the poor. In 1986, Dominique's radio station is fired on and ransacked, and Dominique forced to flee to New York.</P>
<P>The film chronicles Haiti through the 1980s and '90s--the mass movement to overthrow Baby Doc Duvalier, who the U.S. flew to safety in France; the rise of Jean-Bertrand Aristide, who vowed to represent the poor; the coup that overthrew him and the subsequent machinations of the CIA and the Clinton administration. There's no solace here for those hoping to portray the Democrats as the lesser evil, as Bill Clinton orders the Coast Guard to send back Haitian boat people fleeing repression.</P>
<P>Despite Dominique's initial joy at Aristide's eventual return, he publicly breaks with him after his betrayals of the Haitian people, and sets out to campaign for a truly free Haiti. Dominique continuously points to the people, not the politicians, as the agents of any real change.</P>
<P>After publicly disagreeing with one of Aristide's security personnel, he is gunned down outside the gates of Radio Haiti in April 2000. Those seeking to understand U.S. objectives in Iraq will instantly see parallels.</P>
<P>If he were alive today, Dominique would no doubt be scathing in his attacks on the new U.S. invasion of Haiti, once more guarding an un-elected government. As Dominique used to say, "You can kill the body, you thieves; you can end a life, you liars; but you cannot kill the spirit which lives on in the minds of the people."</P>

