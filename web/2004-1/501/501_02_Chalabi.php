<P><font face="Times New Roman, Times, serif" size="4">Why the Bush administration turned on Ahmed Chalabi</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>From stooge to scapegoat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration has turned the tables on its once-favored stooge. Last week, U.S. troops raided Iraqi National Congress leader Ahmad Chalabi's home and office in Baghdad, holding a gun to Chalabi's head as they seized documents.</P>
<P>Before the U.S.-led invasion in March 2003, Chalabi's word was considered gold by Bush administration "hawks" desperate for someone to confirm their fantasy that Iraqis would welcome a U.S. war for oil and empire. Chalabi even sat behind Laura Bush as an honored guest during the State of the Union address earlier this year.</P>
<P>Yet now, according to journalist Andrew Cockburn, George W. Bush recently told Jordan's King Abdullah, "You can piss on Chalabi." And Chalabi--whose organization pocketed $33 million from the U.S. government between 2000 and 2003--is suddenly talking about self-determination. At a press conference after the U.S. raid, he declared: "Let my people go. Let my people be free. It is time for the Iraqi people to run their affairs."</P>
<P>This turnaround shows the depth of Washington's crisis in Iraq. Until a few months ago, nothing could convince Washington's war markers that Chalabi--the child of one of Iraq's wealthiest families--was a con man.</P>
<P>It didn't seem to matter that Chalabi had no base of support among Iraqis--since he had left his native country nearly half a century earlier and owed his status as a "leader" of the Iraqi opposition entirely to his U.S. masters.</P>
<P>No one even raised an eyebrow over Chalabi's criminal record. Chalabi fled Jordan after his bank went belly up in 1989. He was later tried in his absence, found guilty on 31 charges of embezzlement, theft, misuse of depositor funds and currency speculation, and sentenced to 22 years in jail if he ever set foot in the country again--which he took care not to do. </P>
<P>Despite all this, the U.S. promoted Chalabi as a future leader of the new post-Saddam Iraqi government. He only began to lose his influence recently as Washington struggled to cope with growing resistance to its rule.</P>
<P>U.S. officials began making deals with elements of Saddam's Baath Party--Chalabi's most feared political enemies. Meanwhile, Chalabi tried to build himself up as a political leader of Shia Muslims--the majority group in the Iraqi population, and long oppressed under the former regime, which was based among Sunni Muslims.</P>
<P>U.S. officials now accuse Chalabi of passing intelligence to the Iranian government, which is led by Shiite clerics who have influence among Iraqi Shiite leaders. Chalabi's criticism of the U.S. increased when it became clear that he would be cut out of the interim "government" that the U.S. is supposed to "hand over" power to June 30.</P>
<P>But while he may have fallen out with his former masters, Chalabi won't disappear. Since returning to Baghdad after the fall of Saddam's government, Chalabi has built up a business empire by grabbing control of parts of Iraq's ruined economy. He also seized the intelligence files of the old regime--to use against his political opponents.</P>
<P>This leopard hasn't changed its spots. He remains a scheming hypocrite who won't hesitate to make a deal with anyone--including the U.S. forces he currently denounces as occupiers--if he thinks it will expand his own power.</P>

