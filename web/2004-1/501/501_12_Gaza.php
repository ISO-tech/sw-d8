<P><font face="Times New Roman, Times, serif" size="4">"I don't think that an earthquake could do what the Israeli army did here"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's terror in Gaza</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>"I HARDLY recognized my own street. I don't think an earthquake could do what the Israeli army did to this area." Those were the words of Abdel Rahim Abu Jazer, after Israeli troops left Gaza's Rafah refugee camp--after a six-day siege by Israeli forces that left 42 Palestinians dead.</P>
<P>Scenes of devastation awaited residents around every corner, as many people left their homes for the first time in a week to survey the damage--flattened cars, leveled buildings, toppled power lines and raw sewage flowing in the streets. The <I>New York Times </I>described the assault as the most devastating since Israel occupied Gaza in 1967.</P>
<P>Israel claims that its operation was designed to stop "terrorists" from using underground tunnels connecting Gaza and Egypt to smuggle arms and explosives. But Israel's atrocities left no doubt as to who the real terrorists are.</P>
<P>Even Israeli Justice Minister Yosef Lapid, a Holocaust survivor, denounced the atrocities in a Cabinet meeting, saying that the scenes reminded him of his grandmother, who died in a Nazi death camp--a startling comparison from an Israeli minister that sent shock waves through the establishment.</P>
<P>"When I saw a picture on the TV of an old woman on all fours in the ruins of her home, looking under some floor tiles for her medicines, I did think, 'What would I say if it were my grandmother?' We look like monsters in the eyes of the world," said Lapid, who is the leader of the centrist Shinui Party.</P>
<P>The Israeli atrocity that gained the most international attention and outrage occurred when an Israeli helicopter gunship fired two missiles into an unarmed, civilian demonstration. The marchers were protesting the second day of Israel's siege on the neighborhood of Tel Sultan, where rooftop snipers fired on anyone who dared to leave their homes.</P>
<P>The marchers were still 500 yards from an Israeli checkpoint that sealed off the area when the missiles were launched, without warning, followed by bursts of machine gun fire. At least 10 Palestinians were killed instantaneously, more than 50 others were wounded. The United Nations Security Council condemned "Operation Rainbow"--the name Israel used for its bludgeoning of Rafah. Fourteen countries voted in favor and one abstained--the U.S., of course.</P>
<P>Israeli Prime Minister Ariel Sharon, whose plan for a unilateral withdrawal from Gaza was voted down by his Likud Party in early May, plans on bringing a new, "more gradual" withdrawal plan to a cabinet vote within the week. In addition to the military goal of creating a wider corridor between Gaza and Egypt, Israeli officials also made clear that the demolition and killings in Rafah were intended to send a message that talk of withdrawal had nothing to do with Palestinians' success in destroying two armored personnel carriers earlier in the month.</P>
<P>"The terrorist organizations entertain the notion that if we're going to execute this unilateral withdrawal, it's going to be under fire," said Raanan Gissin, a Sharon spokesperson. But if so, he said, "It's going to be our fire, not their fire."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Another front in Washington's war</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>HOUSE MAJORITY Leader Tom DeLay (R-Texas) revealed the truth in mid-May. The U.S. war on Iraq and Israel's war on Palestinians, DeLay said, constituted "two fronts" in a single "war on terror," Reuters reported. "And we will win it," said DeLay, speaking before the American Israel Public Affairs Committee (AIPAC).</P>
<P>At the same AIPAC meeting, Rep. Steny Hoyer (D-Md.), a top Democrat in the House leadership, likewise declared, "Israel's fight is our fight." Hoyer announced his support for Bush's pronouncement that Palestinians shouldn't be given the right to return to homes taken from them by Israel.</P>
<P>With U.S. politicians explicitly making the connection between the occupations of Iraq and Palestine, it's clear that the U.S. antiwar movement was right to do so, too.</P>
<P>The wars in Iraq and Palestine are two parts of Washington's effort to remake the Middle East to meet its own interests. A setback for U.S. plans in Iraq and Israel's ethnic cleansing of Palestine will advance the fight against imperialism and empire-building everywhere.</P>

