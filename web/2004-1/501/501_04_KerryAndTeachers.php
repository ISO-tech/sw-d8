<P><font face="Times New Roman, Times, serif" size="4">Union backs Kerry despite awful education plan</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Teachers deserve better</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 28, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B><BR>
The fruits of "lesser evilism" are getting more and more bitter. Regular readers of <I>Socialist Worker</I> will remember the battle that teachers in Washington, D.C., had to go through last fall in order to win a 9 percent raise promised to us in our contract.</P>
<P>At times, we had to fight just as hard with our own union leadership to do the most basic things to help build rallies. Foremost among them was mailing flyers to each of the 5,000 teachers in D.C. schools. Our union trustee always hemmed and hawed about how expensive and time-consuming such mailings were.</P>
<P>Yet no sooner had John Kerry won the horse race to be the presidential candidate for the Democrats than not one, but two, letters came to each and every member of our union, painting an end-of-the-world scenario if we don't get Kerry elected. What will our local and our union get in return for such steadfast support for Kerry? </P>
<P>The results are shocking. While speaking at a California high school recently, Kerry bragged about a multibillion-dollar "new bargain" with teachers. </P>
<P>While straining to connect with the legacy of "New Deal" programs, Kerry actually stressed that he is an "entrepreneurial" Democrat. "I believe we need to offer teachers more pay...and we must ask more in return," Kerry said. "That's the bargain." For those teachers unable to raise test scores? "Greater flexibility" in "setting up processes to remove them."</P>
<P>There's nothing new in these ideas. They are the same attacks on public school teachers we've been facing for two decades. The difference now is that we're getting the same attacks from the very man that our union--a teachers' union on record time and again against merit pay and evaluations based on test scores--is telling us to vote for!</P>
<P>How treacherous do the Democrats have to be before our union leaders stop telling us to vote for them and start building a fightback? We can do better than to choose between two bluebloods who want to undo public schools. </P>
<P>So when your union mails out those apocalyptic letters demanding your vote for Kerry, toss it in the trash bin where it belongs, and get down to your next union meeting instead.<BR>
<B>Jeff Bale,</B> Building Representative, Washington Teachers' Union, AFT Local 6, Washington, D.C.</P>

