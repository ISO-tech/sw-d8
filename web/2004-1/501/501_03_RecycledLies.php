<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Scrambling to repackage the occupation for June 30 deadline</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's recycled lies</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 28, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>DID THEY really expect us to buy the same old lies? George W. Bush's May 24 speech to the nation was billed as an attempt to lay out the U.S. strategy for a "transitional" government in Iraq.</P>
<P>Instead, it was sheer public relations hot air--another lame attempt to sell the deadly and sadistic conquest of Iraq as "liberation"--and, as of June 30, "Iraqi sovereignty." To pull this off, Bush's speechwriters had to turn reality on its head.</P>
<P>"This vile display shows a contempt for all the rules of warfare, and all the bounds of civilized behavior," Bush declared. That's a good description of the U.S. torture of Iraqi prisoners--but Bush was talking about the murder of U.S. businessman Nicholas Berg, who likely would be alive today if he hadn't been detained by U.S. authorities before he was kidnapped.</P>
<P>Bush insisted that the U.S. record in Iraq would ultimately "discredit" a "narrow ideology." True enough--if he meant the neoconservative "hawks" who dreamed up this imperialist war. But Bush, of course, was talking about the supposedly small number of "terrorists" responsible for the armed opposition in Iraq--a laughable idea given the widespread character of the resistance.</P>
<P>When Bush finally got around to mentioning the still-expanding prison torture scandal, he choked on the name of the prison involved, Abu Ghraib, and mentioned only "disgraceful conduct" by "a few American troops," despite mounting evidence that the top military brass knew--and approved--of what was happening.</P>
<P>Bush's proposed solution? Tear down Abu Ghraib and build a new prison. Perhaps a high-security Supermax facility like those in the U.S., where no cameras are permitted to record the routine brutality against prisoners.</P>
<P>Bush's speech went over well with the military audience at the Army War College, but it's unlikely to appease his critics, a growing number of whom come from within the ranks of the Republican Party. Retired Gen. Anthony Zinni, the former head of Central Command, recently denounced the Pentagon's civilian leadership headed by Defense Secretary Donald Rumsfeld--and said that while Bush wanted to "stay the course" in Iraq, "the course is headed over Niagara Falls."</P>
<P>Sen. Richard Lugar (R-Ind.), the head of the Senate Foreign Relations Committee, used a college commencement speech to declare that the U.S. has "relied heavily on military options and unilateral approaches that weakened our alliances." Now, Bush is hoping that such "alliances"--in the form of a United Nations (UN) Security Council resolution--will give the U.S. the political cover it needs to maintain the occupation, and perhaps some more foreign troops to attract bullets away from American ones.</P>
<P>But there are still no answers about just who U.S. authorities will hand "sovereignty" to on June 30--even if you accept the absurd idea that the self-determination of a country of more than 25 million people can be passed around like a football. In reality, the U.S. will continue to call the shots in Iraq--and, as Bush explained, the new U.S. embassy will have branch offices in every city to "work closely with Iraqis at all levels of government."</P>
<P>This pathetic speech might seem like a political gift to Democratic presidential candidate John Kerry. But Kerry's program for Iraq is the same as Bush's--involve the UN, make deals with allies and send <I>more </I>troops. Because Kerry, like Bush, doesn't want to end U.S. control of Iraq, but rather repackage it in order to maintain Washington's dominance in the Middle East.</P>
<P>The resistance in Iraq and the spreading torture scandal may upset Bush's plans sooner rather than later. But in any circumstance, one thing is clear: The killing, suffering and torture of Iraqis won't end until the U.S. gets out. The time to go is now. </P>

