<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What's holding back antiwar activism?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 7</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WHEN RIGHT-WING pundit Thomas Friedman starts clamoring for "regime change here at home," as he did on May 14, you know that conservatives are deserting the Bush administration's sinking ship. Growing factions of the military and political establishment are now scrambling to find an "exit strategy" from Iraq, while their entire project to reshape the Middle East is in peril.</P>
<P>This is the moment we on the left have been waiting for. Where, then, is the sense of euphoria? The crisis at the top of society has not been matched by a surge in confidence from the antiwar movement below.</P>
<P>Weeks into the prison torture scandal, there have been no mass protests to demonstrate Americans' outrage at the torture of Iraqi prisoners--and to demand that U.S. troops be brought home now--despite opinion polls showing that roughly half of the U.S. population shares these sentiments.</P>
<P>Ironically, the U.S. left finds itself falling behind the times. Having adopted "regime change at home" as its undiluted priority, the left is ill equipped to advance the movement now that the likes of Friedman have begun to repeat this same slogan.</P>
<P>The "Anybody But Bush" left now finds itself hamstrung by its chosen presidential candidate, who barely satisfies even this minimal requirement. With the Iraq prison scandal swirling around him, John Kerry pledged to bring U.S. troops home from the "death zone" in Iraq--by the end of his first term, that is.</P>
<P>"I will get our troops home from Iraq with honor and with the interests of our country properly protected," Kerry continued, harking back to the 1968 campaign pledge of Richard Nixon--who went on to launch the invasion of Cambodia. And if anyone in the antiwar movement needed further evidence of the link between the struggle in Palestine and the struggle against the U.S. occupations of Iraq and Afghanistan, Tom DeLay (R-Texas) was explicit when he rushed to Israel's defense after its massacre of Palestinians at the Rafah refugee camp in Gaza last week.</P>
<P>"America's interests are Israel's interests, the interests of all free people, and no amount of violence or intimidation, let alone diplomatic pretenses of moral equivalence, will ever change that fact," said DeLay. Kerry's position is indistinguishable: "The cause of Israel must be the cause of America," he argues, emphasizing his agreement with the Bush administration.</P>
<P>On the home front, John Kerry is hell-bent on shedding the liberal label as he aims for the swing vote in November, leaving his left supporters paralyzed on every front. "I voted for Judge Scalia," Kerry noted recently, to illustrate his willingness to appoint anti-abortion judges.</P>
<P>Yet the pro-choice movement did not manage a word of criticism in response. On the contrary, Elizabeth Cavendish of NARAL Pro-Choice America sniffed, "There's a huge difference between Bush and Kerry on choice and this is not going to undermine the pages-long documentation that Kerry is pro-choice."</P>
<P>Even the celebration of gay marriage, legalized in Massachusetts on May 17, was muted--because John Kerry opposes gay marriage. "There's a terrible sense of dread filtering across America," liberal <I>New York Times </I>columnist Bob Herbert wrote on May 24--a fear that "we may all be passengers in a vehicle that has made a radically wrong turn...with someone behind the wheel who may not know how to drive." Herbert could have added, "Neither does the one person close enough to grab the steering wheel."</P>
<P>The left, fighting for social change, has painted itself into a corner with a staunch defender of the status quo. As the editors of the liberal <I>Nation</I> magazine, worried, "We hope Kerry summons the courage to call for a...bold change in course"--as if the most the left can do is cross our fingers and "hope."</P>
<P>In reality, by offering Kerry its unconditional support, the broad left is effectively preventing the launch of a fight for fundamental social change in this election year. That is why comparisons with My Lai do not apply to the Iraq prison torture scandal. Without a principled anti-imperialist movement from below, the status quo will remain unchallenged.</P>

