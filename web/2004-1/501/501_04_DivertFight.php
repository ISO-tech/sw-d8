<P><font face="Times New Roman, Times, serif" size="5"><b>We can't let the Democrats divert our fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 28, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I> <BR>
In the fight for marriage equality in Wisconsin, activists have learned mountains about how to fight for civil rights. Madison could join the ranks of San Francisco and New Paltz, N.Y., if Dane County Clerk Joe Parisi were to follow through on his support for marriage equality. </P>
<P>A new organization of students and community activists, the Civil Marriage Equality Coalition, recently formed and called for Parisi to issue marriage licenses for same-sex couples.  Parisi, supported by Action Wisconsin--the primary gay lobby organization in the state--refuses to break Wisconsin's bigoted marriage laws, which define marriage as exclusively between a "husband and wife." </P>
<P>His argument is that breaking the law will only further alienate Dane County (seen as an island of liberals in conservative Wisconsin), and when the referendum banning civil unions comes up for a vote in one year, the right wing will be mobilized to set us back even further.</P>
<P>Activists need to reject this wait-and-see approach and demand an end to all discrimination immediately. If that means the Christian Right will organize, so be it. They are already more organized than our side. </P>
<P>Civil rights cannot be up for popular election. They need to be fought for in the face of discrimination, just like in every past civil rights movement. We need a movement that exposes the bigoted politics of the right wing, while fighting against all discrimination everywhere--not strategically ignoring it for a little while. </P>
<P>In fact, when the Democratic Party heard about the new coalition, one of their state chairmen promptly gave us all the details on how to join the Democratic Party, set dues and argue within the party for a position for gay marriage rights. Unsurprisingly, he also told us not to protest any more--supposedly, it's not worth our time. </P>
<P>It shouldn't be surprising that during an election year, while John Kerry opposes gay marriage, the Democratic Party reacts quickly to any organizing for winning marriage equality. These attempts at taming the movement and overt co-option are obstacles to overcome. </P>
<P>Our movement needs to think hard about what strategies will defeat the right. Waiting out legislative battles in the face of discrimination is not one of them. <BR>
<B>Chris Dols, </B>Madison, Wis.</P>

