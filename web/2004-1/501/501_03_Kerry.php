<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should we trust Kerry?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 28, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>MANY PEOPLE who don't like John Kerry but plan to vote for him anyway in November think that he will at least safeguard women's right to abortion. Well, maybe.</P>
<P>Last week, Kerry stabbed his pro-choice supporters in the back, telling a reporter that he would consider appointing anti-choice judges to federal courts--as long as it didn't lead to the U.S. Supreme Court overturning <I>Roe v. Wade</I>. That would mean more federal judges to chip away at abortion rights even if they allow abortion to remain legal--precisely what the courts have done ever since the 1973 <I>Roe </I>decision.</P>
<P>Kerry even bragged that in 1986 he voted in favor of confirming anti-choice Supreme Court Justice Antonin Scalia--an ultraconservative who once referred to a late-term abortion procedure as "infanticide."</P>
<P>"I will not appoint somebody with a 5-4 court who's about to undo <I>Roe v. Wade," </I>Kerry commented. "I've said that before. But that doesn't mean that if that's not the balance of the court, I wouldn't be prepared ultimately to appoint somebody to some court who has a different point of view."</P>
<P>Despite these outrageous statements, mainstream women's organizations couldn't bring themselves to offer even mild criticism of Kerry. "There's a huge difference between Bush and Kerry on choice, and this is not going to undermine the pages-long documentation that Kerry is pro-choice," said Elizabeth Cavendish, interim president of NARAL Pro-Choice America.</P>
<P>We deserve better than a "maybe" on the question of abortion rights. We deserve abortion rights without apology--something that John Kerry has shown that he won't provide.</P>

