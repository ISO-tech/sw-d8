<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is Nader offering a left alternative?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 28, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>MANY MORE people are considering a vote for Ralph Nader as a protest against war and corporate control of Washington than anyone would have guessed when Nader announced his independent presidential campaign in February. But Nader's recent moves--his acceptance of an endorsement by the right-wing Reform Party USA and his all-too-friendly meeting with Democratic presidential nominee John Kerry--have done a disservice to his supporters and severely undermined the case that he represents a left-wing alternative to the Washington status quo.</P>
<P>Earlier this month, leaders of the Reform Party--launched in 1992 as a vehicle for the presidential campaign of millionaire Ross Perot and represented in the 2000 election by far-right bigot Pat Buchanan--endorsed Nader and offered its spot on the ballot in seven states. Nader's left-wing platform is wholly opposed to the Reform Party's on numerous issues--most prominently on immigration, where the Reform Party calls for expelling all illegal immigrants, a position to the right of George Bush.</P>
<P>Campaign organizers privately assure supporters that Nader does disagree with the Reform Party. But shamefully, Nader hasn't uttered a word of criticism in public.</P>
<P>Instead, the campaign's official statement welcomed Reform Party support and bent over backwards to find points of agreement. The net effect is that the Nader campaign gave a dying right-wing organization a breath of life--when it should have been shunned.</P>
<P>Within a week of the Reform Party fiasco, Nader was meeting with John Kerry--and instead of challenging Kerry for trying to "out-Bush Bush," as he put it a few days earlier, Nader absurdly praised Kerry as a "green spruce" compared to the "petrified wood" of the Democrats' last candidate, Al Gore. Nader explained that the meeting was part of his strategy of "opening up a second front" that will help to defeat George Bush.</P>
<P>Maybe Nader thinks this rhetoric will deflect some of the abuse heaped on him by Democrats who despise him for the "crime" of daring to run at all. If so, he's wrong. Democrats continued to snipe at Nader anyway. </P>
<P>But the more important point is that Nader's kid-gloves treatment is letting Kerry off the hook. For example, Nader says that he brought up the topic of Iraq during his meeting with Kerry, but that Kerry refused to discuss it and simply said that he had a "plan."</P>
<P>Rather than push Kerry on the most important issue in U.S. politics today, Nader didn't pursue it, nor did he criticize Kerry's support for the war and occupation during comments to reporters afterward. This is absurd. Even Kerry supporters recognize that Nader's recent jump in support is because he is the only candidate who calls for U.S. withdrawal from Iraq.</P>
<P>Nader is giving up opportunities to do what he says is his main goal--offer an alternative to the two-party "duopoly" in Washington--on a decisive issue where the Republicans' and Democrats' similarities are plain to see.</P>
<I><P>Socialist Worker </I>was a proud supporter of Nader in the 2000 election. We believe his campaign was a lightning rod for millions of people fed up with corporate domination of the U.S. political system. Nader himself was no socialist and had political weaknesses on a number of questions. But, by and large, he spoke out for an unapologetic left-wing alternative.</P>
<P>Because of his enthusiasm for the Reform Party endorsement and his overtures to Kerry, the same cannot be said today. This is why we can't recommend a vote for Nader at this time.</P>
<P>The urgency of building a left-wing alternative to the two parties is profound. John Kerry and George Bush represent two candidates of the Washington status quo whose differences--when they exist at all--are overwhelmed by what they share.</P>
<P>Nader could be that alternative in Election 2004--but not if he continues to celebrate his support from the right, and not if he pulls his punches when it comes to Kerry. We urge Nader to renounce the Reform Party's right-wing politics--and to argue a consistent case against John Kerry's Republican Lite agenda.</P>

