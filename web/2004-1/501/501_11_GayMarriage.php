<P><font face="Times New Roman, Times, serif" size="5"><b>Equal marriage can't wait</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 2,000 people marched across the Brooklyn Bridge on May 23 to celebrate the victory for gay rights in Massachusetts, which became the first state to issue marriage licenses to gays and lesbians on May 17, and to demand that New York officials begin issuing licenses. Protesters chanted, "Hey, hey, ho, ho, no gay Jim Crow!" and "Massachusetts, New York State, equal marriage cannot wait!"</P>
<P>After the march, a crowd of several hundred gathered for a rally in Battery Park. Rev. Pat Bumgardner, a pastor at the Metropolitan Community Church of New York, kicked off the rally. "There are legal battles ahead, and we must fight them, but legal sanctions are not enough," the reverend said. "We need to be sitting in at the marriage license bureau like they did in Chicago."</P>
<P>Unfortunately, many of the other speakers' speeches focused on the details of legislative efforts in New York and "getting out the vote" in November. A notable exception was gay activist Brendan Fey, co-founder of Civil Marriage Trail, who asked, "All those politicians who say they support us, where are they today?"</P>
<P>The march was organized by Marriage Equality New York and co-sponsored by a number of lesbian, gay, bisexual and transgender and progressive groups, including the Human Rights Campaign, National Gay and Lesbian Task Force, New York University's Spectrum Equality and the International Socialist Organization.</P>

