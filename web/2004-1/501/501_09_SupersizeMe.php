<P><font face="Times New Roman, Times, serif" size="4">Filmmaker lives a month on fast food diet</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>His last supper?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Kirstin Roberts</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 28, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Super Size Me,</B></I> a documentary by Morgan Spurlock.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>OBESITY IS now the second leading cause of preventable death in the U.S. Sixty percent of adults are overweight or obese, and one third of our children will develop diabetes in their lifetimes.</P>
<P>Morgan Spurlock's film <I>Super Size Me </I>starts by posing a simple question: Is this public health disaster the result of bad personal choices or is the $110-billion-a-year fast food industry to blame? Spurlock sets off to find the answer, crisscrossing America, interviewing the people who eat and serve fast food, doctors, writers and school officials--all the while eating nothing but McDonalds for every meal for a month.</P>
<P>Spurlock proves with his own body, in hilarious and grotesque detail, that fast food is unfit for human consumption. He starts the film with a clean bill of health from three separate doctors. By the end of the month, he's overweight, suffers from headaches, is depressed, has high cholesterol and is developing cirrhosis of the liver.</P>
<P>One of the doctors monitoring Spurlock comments that he had seen that kind of deterioration in binge drinkers but would never have imagined that simply eating fast food could inflict that amount of damage.</P>
<P>The junk food industry's predatory targeting of children as their favorite consumer group is highlighted throughout the film. In a tragically funny scene, Spurlock shows flashcards of famous people to a group of young kids. Ronald McDonald is the only "famous person" the children can identify.</P>
<P>Given that the average child will watch over 10,000 TV commercials a year, and that the majority of advertising aimed at kids is for junk food, this should come as little surprise. By the end of the film, it is clear that the "freedom" to live a healthy lifestyle is an illusion under capitalism.</P>
<P>Unfortunately, while Spurlock illustrates the fast food industry's crimes against public health in all their gruesome detail, the film itself can't provide any answers other than "stop eating the crap." But in an economy in which overworked, overstressed parents are forced to feed their kids (and themselves) on limited budgets and children are literally hooked on addictive junk food by cartoon-character pushers, "stop eating the crap" isn't a solution.</P>
<P>I enjoyed <I>Super Size Me, </I>finding the diary-style muckraking appealing and Spurlock himself much more likeable than his fellow muckraking filmmaker Michael Moore with his super-sized ego. Aside from a really annoying tendency to focus his lens on obese people in ill-fitting clothing to produce cheap laughs from the audience, <I>Super Size Me </I>is a tasty, if not completely satisfying, look at the corporate vultures who are poisoning us for profit.</P>

