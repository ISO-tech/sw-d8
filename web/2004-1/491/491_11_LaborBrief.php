<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 19, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>American Axle &amp; Manufacturing</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">DETROIT--A union local that rejected a contract at auto parts maker American Axle &amp; Manufacturing (AAM) after a one-day strike February 27 is now fighting the two-tier wage included in the deal. United Auto Workers (UAW) Local 235 in Detroit was the only plant to reject the contract, as members realized that a $5,000 signing bonus wouldn't make up for low wage gains that would leave workers' base pay 5 percent lower than their counterparts in the Big Three auto assembly plants.</P>
<P>"That's $12,000 over the life of a four-year contract," Local 235 President Wendy Thompson told <I>Socialist Worker. </I>"People understand that." Thompson was the only member of the national negotiating committee to reject the deal, which was negotiated with the full involvement of UAW national staff.</P>
<P>Ironically, she said, American Axle wanted a strike to put pressure on General Motors, which spun off the company several years ago--and 6,500 workers walked out. As a critical supplier of axles, chassis and other parts for GM's profitable SUVs, AAM used the brief walkout to pressure GM into signing contracts for the future.</P>
<P>Once GM agreed, AAM bowed to UAW demands for new products for AAM's big plant in Buffalo, N.Y. Yet the deal has the same two-tier wage agreement that the UAW agreed to accept last year at two other major auto parts companies--Delphi Corp., also formerly part of GM, and Visteon Corp., a spinoff of Ford Motor Co.</P>
<P>Under AAM's original offer, starting pay would have been lowered to $13.50, topping out after eight years at $17.50--compared to the current top wage of about $24 per hour. But under the terms of the deal, the UAW and management will negotiate the lower-tiered wage within 90 days after the settlement of the contract--the same agreement that the union made at Delphi and Visteon.</P>
<P>UAW leaders pooled the parts plant workers' votes with those from GM and Ford to ensure that the deal would pass. However, no agreement has yet been announced in the Delphi and Visteon agreements--and Thompson and Local 235 members are trying to use a loophole in the parts industry contract that specifies that if there's no agreement by a specified deadline, the standard pay will apply.</P>
<P>To that end, they're launching a petition campaign calling on UAW leaders to reject two-tier pay and are seeking the support of workers at Delphi and Visteon as well as AAM. "My idea is to have at least one profitable auto parts supplier hang in there with higher wages," Thompson said, "so that the union can develop a strategy to bring higher wages back across the board."</P>

