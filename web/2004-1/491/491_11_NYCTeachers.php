<P><font face="Times New Roman, Times, serif" size="4">New York City teachers have a choice in UFT election</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vote for a fighting union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Last week, ballots for the citywide union elections were sent out to 100,000 teachers and school workers in the United Federation of Teachers (UFT). The upcoming elections provide a means for UFT members to express their anger at current President Randi Weingarten's failed strategy of business unionism. As this year's elections take place, New York teachers have been without a contract since last May--after the last contract, which extended the work day by 100 minutes a week, expired.</P>
<P>Negotiations have stalled, and multimillionaire Mayor Michael Bloomberg has made it clear that he wants to run the school system like a corporation, constantly demanding productivity increases in exchange for raises. Bloomberg, who promotes himself as the "education mayor," has launched a campaign of micromanagement at all levels after winning full mayoral control of the schools and yet repeatedly blames teachers when his policies fail.</P>
<P>Weingarten, who is part of the Unity Caucus, has angered many union members by her losing strategy of offering concession after concession in an attempt to strike a deal with the city, despite the great cost of these concessions to workers. On top of the offer to eliminate work rules in 150 city schools, Weingarten has also made agreements to make it easier to fire teachers, and she negotiated a deal with the city to raise health care copays for a large percentage of city teachers.</P>
<P>The largest opposition caucus in the union, New Action, has recently made a deal with Weingarten's Unity caucus. New Action won't run a presidential candidate against Weingarten in exchange for Unity agreeing not to challenge New Action incumbents in six high school executive board seats.</P>
<P>As a result of this corrupt deal, activists in the union have been organizing opposition caucuses to help build a militant rank-and-file alternative to Weingarten and the Unity caucus. Teachers for a Just Contract (TJC), one of the rank-and-file groups opposing Weingarten, has been active for many years organizing protests against merit pay and a longer work day among other actions.</P>
<P>This election marks TJC's first time putting forward a slate. In its campaign material, TJC argues that "a union that has lost its ability to convince the employer that it can win a strike is at the mercy of the employer."</P>
<P>TJC's presidential candidate, Nick Licari, and other members of the slate are committed union activists who stand for union democracy and rank-and-file militancy as an alternative to Weingarten's strategy. In addition, TJC is running seven high school candidates, half of whom are members of ICE/PAC, another opposition caucus.</P>
<P>All of these candidates are cross-endorsed by both groups to maximize their chances of winning these seats, which are currently held by New Action. The upcoming elections provide an opportunity for UFT members to send Weingarten and the Unity caucus a message that we want to organize a militant rank and file, which is capable of fighting back against the attacks on education by Bloomberg and Klein. Vote Teachers for a Just Contract--and organize to win! </P>
