<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The terrible toll of racism in the U.S.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">HALF OF all Black men in New York City can't find a job, while Black teenage unemployment stands at 37 percent nationwide. These statistics show a crisis among Black Americans that should be setting off alarm bells in election year 2004.</P>
<P>Yet even John Kerry, the candidate whose party's voting base includes the vast majority of Blacks, has issued barely a sound bite. This should come as no surprise, since Black lives, Black votes and Black rights have been devalued since the Founding Fathers. </P>
<P>The original U.S. Constitution permitted slavery and counted Black slaves as three-fifths of white persons in determining both Congressional representation and taxation, embedding racism in the very foundation of U.S. society. The institution of slavery was abolished only through Civil War, a bloody second American Revolution that cost at least 600,000 lives.</P>
<P>But racism outlived slavery and flourished for the next 100 years in the form of Jim Crow segregation, in which the majority of states, from North Dakota to Texas to California, made it a crime for Blacks to intermingle with whites in all walks of life--from hospitals to cemeteries, lunch counters to phone booths, military service to marriage.</P>
<P>Jim Crow segregation laws were challenged and finally struck down only because of a massive civil rights struggle stretching over more than two decades, from the 1955 Montgomery bus boycott to the fight to enforce court-ordered school desegregation in the 1970s. But<I> de facto </I>segregation continued, North and South, while accusations of "reverse racism" and Black "welfare dependency" emanated from the political establishment, injecting racism with new life in the post-civil rights era.</P>
<P>Politicians from both the Democratic and Republican Parties scrambled to appear "tough on crime," embracing the so-called war on drugs, which tripled the prison population between 1980 and 1995. Two-thirds of those who entered the prison system during that period were Black, Latino or poor, and the vast majority of them were nonviolent drug offenders.</P>
<P>Today, with the prison population swollen to more than 2 million, African Americans make up just 12 percent of the U.S. population and only 13 percent of drug users, yet account for 35 percent of drug arrests and 53 percent of drug convictions. Blacks are also 43 percent of those on death row.</P>
<P>Last year, the U.S. Bureau of Justice Statistics estimated that 30 percent of 12 year-old Black boys will spend time in jail in their lifetimes--far more than will attend college. And because many states have laws denying present and former inmates the right to vote, an estimated 13 percent of all Black men--including one in every three in Alabama and Florida--have been disenfranchised.</P>
<P>Racism, not criminal records, explains the high unemployment rate for Black men today. A recent <I>Wall Street Journal </I>report showed that in the city of Milwaukee, a white job applicant with a criminal record has a better chance of being called for an interview than a Black man with no criminal record. </P>
<P>"The disadvantage carried by a young Black man applying for a job as a dishwasher or a driver is equivalent to forcing a white man to carry an 18-month prison record on his back," concluded reporter David Wessel. And only racism can explain these statistics:</P>

<P>-- Segregation in public schools, which decreased continuously from the 1950s to the late 1980s, has now returned to levels not seen in three decades.</P>

<P>-- Black infants are almost two-and-a-half times more likely than white infants to die before the age of one, a wider gap than in 1970.</P>

<P>-- In 2002, 79 percent of Blacks aged 25 and older were high school graduates, compared with 30 percent in 1968. Yet the typical Black household had a net worth of just $19,000, compared with $121,000 for whites. </P>

<P>More than 200 years since slavery was written into the U.S. constitution, its racist legacy remains--and the words of abolitionist Frederick Douglass remain true: "Without struggle, there can be no progress." Only a struggle that shakes the foundation of U.S. society can end racism.</P>

