<P><font face="Times New Roman, Times, serif" size="4">Credit card boss gives cash to both parties</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Buying off politicians</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Annie Levin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON IS for sale to the highest bidder. That's the only conclusion you can draw from the story of Charles Cawley. Until recently, Cawley ran MBNA, the largest independent credit card issuer in the world. </P>
<P>With more and more working-class people accumulating massive credit card debt to keep their heads above water financially, Cawley has been raking it in--to the tune of $50 million a year for the past two years. He spent MBNA's money on a lavish lifestyle of corporate jets, priceless art, cars...and politicians.</P>
<P>Cawley, it seems, never met a politician he couldn't buy. According to a recent article in the <I>New York Times, </I>Cawley is a personal friend and benefactor of George Bush Sr. and idiot son George Jr.</P>
<P>MBNA is the biggest contributor to Bush Jr. during his political career, with one exception--the bankrupt energy giant Enron. Laura Bush recently spoke at Cawley's "baronial mansion in Wilmington, Del.," the <I>Times </I>reported--as a show of "gratitude" for a $350,000 campaign contribution.</P>
<P>Cawley has donated $1 million to the foundation that is building Bush Sr.'s presidential library, and he also recently had both George Sr. and wife Barbara come to speak at one of his estates--and paid them more than $300,000 in speakers fees. Top management of MBNA is "studded with retired FBI officials," according to the <I>Times, </I>including former FBI director Louis Freeh and Gen. Charles Krulak, former commandant of the Marine Corps. CIA chief George Tenet was another recent guest at Cawley's mansion.</P>
<P>But if you were worried that the Democrats were being left out of the corporate feeding frenzy, fear not.  Cawley obviously believes in greasing the wheels of both capitalist parties. MBNA gives 65 percent of its political donations to Republicans and 35 percent to Democrats. </P>
<P>Since 1993, it has been the number one donor to the campaigns of Delware Democratic Sen. Joseph Biden. MBNA also pays Biden's son, a corporate lawyer, $100,000 a year as a "retainer" for services as a "consultant." In return, Biden has been, according to the <I>Times, </I>a "consistent advocate for MBNA. </P>
<P>He has actively supported the proposed Bankruptcy Reform Act, a favorite piece of legislation among credit card companies that would make it more difficult for consumers to escape credit card debt. Biden even tried to get the new bankruptcy law passed by tacking it onto a foreign relations bill in 2000. </P>
<P>Most outrageous of all is the fact that everything Cawley is doing is perfectly legal. After all, it's the political lackeys at MBNA who make all the laws. </P>

