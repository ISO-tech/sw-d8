<P><font face="Times New Roman, Times, serif" size="3">KEVIN COOPER is a California death row prisoner who came within four hours of being executed in February before the U.S. Supreme Court upheld a stay of execution until testing can be done on evidence that he hopes will clear him. Kevin has given <I>SW </I>permission to print excerpts from his essays, written from death row.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WHENEVER WE think about deadly disease, we often think of things like cancer, tuberculosis, HIV/AIDS, Ebola or influenza. They each have various strains that damage different parts of the body. There are thousands of deadly diseases in this world. Some we know about, others we are just learning about, and still others we know nothing about.</P>
<P>Most of these diseases in one form or another have been on this earth before man arrived, and others came about because they are man-made. The disease that I am writing about now is most definitely a man-made disease, and it has been around as long as man has. </P>
<P>This disease also has different strains, forms and names. But no matter what you call them, the end result is the same--death! These man-made diseases that I am now speaking of are called capital punishment, execution, revenge, retribution, justice. </P>
<P>Their strains or forms are called lethal injection, electrocution, hanging, firing squad--just to name a few. There used to be other strains or forms of this disease back when man was said to be less civilized. The new strains or forms--especially the poison--is supposed to be civil or human! I honestly don't think that there has ever been or will ever be a civil or human way to murder another human being. </P>
<P>One of the main things about this man-made disease is the very real fact that only poor people seem to catch or come down with this disease. The state of California wanted to infect me with a deadly dose of their civil and humane disease on February 10 at 12:01 a.m. But fortunately for me, they did not do it! </P>
<P>They wanted to pump their red, white and blue poison into my Black body that night, just like they have historically done to poor people in one form or another. There is a cure for this man-made disease, and it's called "abolition." We must help cure America from this deadly disease! While I am alive, I plan to do just that!!</P>
<P>In Struggle From Death Row at San Quentin Prison,<br>
Kevin Cooper</P>

