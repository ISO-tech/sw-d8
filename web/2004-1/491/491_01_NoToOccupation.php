<P><font face="Times New Roman, Times, serif" size="4">Iraq...Palestine...Afghanistan...Haiti</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No to occupation!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 19, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IT'S BEEN a year of lies for the Bush administration. A year of pretending that Saddam Hussein's phantom weapons of mass destruction and nonexistent connections to al-Qaeda were justifications for invading Iraq. A year of claiming that U.S. soldiers in Iraq would be greeted as liberators and that the war and occupation would be "over soon."</P>
<P>A year of promising "liberation" and "democracy" for Iraqis--but delivering only misery, while well-connected U.S. corporations bleed the country dry. "Liberation, Democracy, a New Middle East. There was no end to the ambitions of the conquerors," <I>Independent</I> journalist Robert Fisk wrote last week. "I remember how anyone who attempted to debunk this dangerous nonsense would be set upon.</P>
<P>"Try to explain the crimes against humanity of [September 11], and you were anti-American. Warn readers about the crazed alliance of right-wingers behind President Bush, and you were anti-Semites. Report on the savagery visited upon Iraqi civilians during the Anglo-American air bombardment, and you were anti-British, pro-Saddam, sleeping with the enemy...When, a day after Baghdad's 'liberation,' I wrote in <I>The Independent </I>that the 'war of resistance' was about to begin, I could have papered my bathroom wall with the letters of abuse I received. Letters like those no longer arrive."</P>
<P>Everything that the antiwar movement said more than a year ago has proven to be true: that Iraq's supposed weapons were a fabrication; that the invasion and occupation would bring more chaos and death; that the real reasons for the war were Washington's drive for oil and empire. It was right to oppose the war a year ago, and it's right to oppose the occupation today.</P>
<P>Iraq isn't Washington's only colonial outpost. There's Afghanistan, where a handpicked U.S. stooge remains president, thugs and warlords control the country, and women--after similar promises of "liberation"--remain as oppressed as under the Taliban. Or Haiti, where Marines patrol the streets of Port-au-Prince after a U.S.-engineered coup ousted democratically elected President Jean-Bertrand Aristide. Or Palestine, where Washington's Middle East watchdog Israel continues its all-out war on Palestinians.</P>
<P>The March 20 protests called to mark the one-year anniversary of the invasion of Iraq may not match the size of huge demonstrations before the war, when more than 10 million people turned out across the globe. But they are an important step for a movement that is developing a deeper understanding of the tasks ahead--as shown by the emergence of the issue of Palestine as one of the occupations that activists will take a stand against.</P>
<P>We need to build a movement that challenges the idea that Washington has the right to shape the world as it sees fit. That means opposing both the Republicans of the Bush administration--and the Washington establishment's junior partners in the Democratic Party. The Democrats' certain presidential nominee John Kerry voted in favor of congressional authorization for the invasion of Iraq--and he wants to send <I>more </I>U.S. soldiers for the occupation of Iraq.</P>
<P>We can't rely on politicians to take a stand against war and imperialism. We need to build a movement that says no to Washington's brutal occupations around the world.</P>


