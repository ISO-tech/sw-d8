<P><font face="Times New Roman, Times, serif" size="5"><b>Minority graduation rates fall</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BLACK AND Latino students are dropping out of high school before graduating at shockingly high rates. And the situation is growing worse because of the Bush administration's education policies.</P>
<P>While the graduation rate for white students in 2001 was 75 percent, only 50 percent of Black students, 51 percent of Native American students and 53 percent of Latino students graduated, according to a new report by the Urban Institute and the Civil Rights Project at Harvard University. "We have a tragic situation today under which high school graduation in America now is literally a 50-50 proposition for minority students," said Christopher Edley, co-director of the Civil Rights Project.</P>
<P>The report unearthed a crisis in minority graduation rates that often goes unrecognized and unreported due to inaccurate and misleading state figures. For example, some school districts count all students who leave school as transferring elsewhere--even if there's no proof that the students are still attending classes.</P>
<P>The report also found that the federal No Child Left Behind Act's overemphasis on standardized tests has led school districts to force out low-performing minority students before they graduate--in order to maintain the illusion of higher test scores. Education Secretary Rod Paige helped school districts accomplish this crime by weakening federal accountability guidelines for minority graduation rates.</P>

