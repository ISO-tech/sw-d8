<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why U.S. troops should leave Haiti</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE ROMANS," complained the Carthaginian general Hannibal, "claim to make the world their own and subject to their will. They demand the right to dictate to us who our friends should be and who our enemies. They circumscribe our liberties, barring us in behind barriers of rivers or mountains beyond which we may not pass--but they do not themselves observe the limits they have set."</P>
<P>It's perhaps not surprising when a great imperial power like Rome--or the U.S.--looks at the world this way. Unfortunately, this presumptuousness can be found also among critics of U.S. foreign policy. In the congressional debate around Haiti, for example, the lines are not drawn as pro-intervention versus anti-intervention, but over the character of U.S. intervention.</P>
<P>Useful as it is to point out that the U.S. conveniently waited until Aristide was overthrown to send troops, this in and of itself isn't an argument against sending troops, but merely over the timing. Few decry the fact that U.S. ruling class "circumscribes" the liberties of Haiti, yet "do not themselves observe the limits they have set."</P>
<P>The debate is over whether the U.S. should have invaded earlier to shore up Aristide's rule rather than sanction his removal. Whatever side you fall on, it entails no challenge to imperialism to call for one or the other, because both accept unquestioningly the idea that the U.S., like Rome, has the right to intervene.</P>
<P>We would laugh off the idea of Haitian troops coming to Florida to supervise an election in Florida, but no one bats an eyelid when U.S. marines patrol the streets of Port-au-Prince, shooting in "self-defense" anyone who challenges their right to be in Haiti.</P>
<P>Take this statement by Gen. James Hill, in charge of U.S. Southern Command, on the rules of engagement of U.S. troops in Haiti: They are to disarm any Haitian "unless he has a valid permit by Haitian law and is in the process of conducting some valid security job."</P>
<P>The unspoken presumption here is that, first of all, U.S. troops are not subject to Haitian law, and secondly, that U.S. troops "are conducting some valid security job" in Haiti; namely, disarming all Haitians who resist them. With this kind of logic, any act of military force is practically self-justifying. Legitimacy and power come through the barrel of the biggest gun.</P>
<P>Even organizations like Human Rights Watch don't question the right of the U.S. to send troops to Haiti--merely what they do with them. The group recently sent a letter to Secretary of State Colin Powell asking that the U.S. marines expand their patrols beyond Port-au-Prince--i.e., to extend the occupation--in order to prevent right-wing violence.</P>
<P>The letter also asked that the U.S. forces arrest Louis Jodel Chamblain, a former military officer and key rebel leader who was sentenced in absentia to life in prison for his involvement in death squad assassinations. But all evidence shows that the Bush administration backed Aristide's removal by the likes of Chamblain, and that FRAPH, the paramilitary death squad organization that Chamblain helped lead in the early 1990s, was CIA supported. </P>
<P>The coup makers, backed by the U.S. and led by rich Haitians and ex-military and paramilitary butchers, free from prison many of the country's paramilitary killers who had been sentenced to prison under Aristide. Now Human Rights Watch comes along and demands that the U.S. expand its occupation in order to save the Haitian people from potential violence that might come from these butchers who have been released from prison. </P>
<P>What's wrong with this picture? The clich&eacute; about foxes guarding henhouses fully applies here. Sincere opponents of our modern-day Rome must oppose all forms of U.S. intervention in whatever guise it is cloaked--not advise the greatest enemies of democracy and self-determination as to how to conduct their occupations in a more reasonable way.</P>


