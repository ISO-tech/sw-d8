<font face="Times New Roman, Times, serif" size="5"><b>Threat to ban abortion in South Dakota</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 19, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SOUTH DAKOTA could soon be the first state in the U.S. to do away with a woman's right to abortion. As <I>Socialist Worker</I> went to press, the state legislature was getting ready to pass a bill outlawing <I>all</I> abortions, except to save the life or health of the woman. Gov. Michael Rounds has said he will sign the legislation.</P>
<P>The bill was passed in late February, but was initially vetoed by Rounds because he wanted minor changes that would make the law more likely to withstand a court challenge. Under the legislation, anyone who performs an abortion, except to save the life of the woman, would face a felony charge, and up to five years in prison and a $5,000 fine.</P>
<P>Lawmakers in both the House and Senate even rejected an exception for cases involving rape or incest. This bill is designed to pave the way for the U.S. Supreme Court to overturn its 1973 <I>Roe v. Wade</I> decision that legalized abortion.</P>
<P>Its language is so strict that it could effectively outlaw some forms of contraception. Even some anti-choice groups are worried that the ban could spark outrage. The South Dakota Right to Life and National Right to Life committees <I>oppose</I> the bill, arguing that an election year is not the "right time" to attempt a ban on abortions.</P>
<P>In the state Senate, the bill initially passed by a margin of 18 to 15--the minimum number of yes votes needed. That means that even one state senator dropping from the "yes" column would have killed the measure. Out of the 18 who voted for the bill in the Senate, <I>five </I>of the yes votes came from Democrats.</P>
<P>"So much for the notion that Democrats are pro-choice and Republicans are pro-life," state House Democrat Gil Koetzle smirked to the Associated Press. In fact, the only problem that some Democrats seem to have with the legislation is that they wouldn't get as much credit for attacking abortion rights as their Republican colleagues. State Sen. Paul Symens actually had the nerve to <I>complain</I> that he and his fellow Democrats aren't getting enough press coverage for their anti-choice stance.</P>
<P>Thirteen years ago, Symens voted against a similar bill. He lost the following election, largely on the issue of abortion rights. But now, he says he's in favor of an anti-choice amendment to the Constitution. How typical. Symens and his ilk in the South Dakota state legislature are willing to strip away women's rights to pander to the right for votes.</P>
<P>As drastic as the South Dakota bill is, it's just one in a slew of recent challenges to the right to abortion. In Iowa, one measure under consideration would grant legal status of an adult human being to a fertilized egg. Another would require any adult pregnant woman who wants an abortion to get judicial permission.</P>
<P>In Michigan, anti-choice activists recently used a provision of the state constitution to override the pro-choice governor's veto of a vaguely worded bill outlawing late-term abortions--and possibly other procedures as well. The law does not even provide an exception for the health of the pregnant woman.</P>
<P>These attacks come at the same time as the Bush administration has stepped up its attacks on abortion rights--with the recent recess appointments of two fanatical anti-choice judges to the federal courts, as well as the passage of a federal ban on late-term abortions and the so-called "Unborn Victims of Violence" Act, a thinly veiled anti-choice measure that makes it a crime to "harm" a fetus.</P>
<P>Once again, the Democrats helped Bush out--giving Republicans a wide margin of support on both bills. We can't trust the Democrats to defend a woman's right to choose abortion.</P>
<P>The struggle for abortion rights has to include building a movement in the streets that can send a message to the politicians and the courts that we won't go back to the days of back-alley abortions. The April 25 March for Women's Lives in Washington, D.C., can be a step in building that movement.</P>


