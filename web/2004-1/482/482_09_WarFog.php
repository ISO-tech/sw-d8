<P><font face="Times New Roman, Times, serif" size="4">Straight from the war criminal's mouth</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Talking with McNamara</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Peter LoRe</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 16, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>The Fog of War: Eleven Lessons from the Life of Robert S. McNamara, </B></I>directed by Errol Morris.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE HUMAN race needs to think more about war, about conflict, is that what we want in this century?" asks former Secretary of Defense Robert McNamara in Errol Morris' new documentary, <I>The Fog of War.</I> Quite a statement from the man who helped oversee the firebombing of Tokyo, brought the world to the brink of nuclear war and authorized the use of Agent Orange on the people of Vietnam.</P>
<P>In <I>The Fog of War,</I> Morris blends interviews with the now 85-year-old McNamara, archival footage and released White House tapes. The film is organized around McNamara's "11 Lessons"--such as "Never Say Never," "Maximize Efficiency" and "You Can't Change Human Nature"--which McNamara uses to rationalize the horrors he was a party to during his military career and as defense secretary for Presidents Kennedy and Johnson.</P>
<P>McNamara details serving under Gen. Curtis LeMay in the Second World War, during which they planned the firebombing of Tokyo and 67 other Japanese cities. He justifies the crime by claiming that military commanders are "just part of a machine," suggesting that "during war, no one really knows anything."</P>
<P>If the U.S. had lost the war, McNamara remembers LeMay asking, would they have been prosecuted as war criminals? "What makes it immoral if you lose and not if you win?" McNamara asks.</P>
<I><P>Fog of War </I>then turns to McNamara's role in Vietnam. White House tapes from 1964 replay a conversation between President Lyndon Johnson and McNamara before the landing of ground troops in Vietnam, in which McNamara tells Johnson to withdraw troops. Johnson, the "peace" candidate, strongly opposes him. But of course McNamara is no dove.</P>
<P>McNamara is asked about the use of the herbicide Agent Orange against the Vietnamese population, which is still causing birth defects to this day. "The use of Agent Orange never would have been authorized if it was against the law," McNamara remarks,  "but it wasn't."</P>
<P>A flaw of the movie though is that McNamara isn't challenged. So for instance, in a scene in which he's discussing the attack on the <I>USS Maddox--</I>an attack the U.S. planned to provoke to justify escalating the war--McNamara concedes that the attack never happened, but insists he and Johnson truly believed it did. McNamara gets let off the hook.</P>
<P>This aside, <I>Fog of War </I>is a great resource, especially for antiwar activists. Through his lies and evasion, McNamara demonstrates how the U.S. government does anything to get what it wants--just as George W. Bush lied to get his war in Iraq.</P>
<P>"The human race needs to think more about war, about conflict, is that what we want in this century?" We do need to think about war, about what ended McNamara's war in Vietnam, about how we can put a stop to all war. <I>The Fog of War</I> is a movie that can help our movement do so.</P>



