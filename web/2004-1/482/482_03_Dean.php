<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dean vs. the Democratic Party?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 16, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHY DO some leading Democrats seem to hate Howard Dean more than George W. Bush? With the opening contests of the Democratic presidential primary approaching, attacks on the front-runner Dean have reached a fever pitch--and most are coming from fellow Democrats.</P>
<P>Naturally, many are from Dean's opponents for the nomination, who want to stop him from winning big in the Iowa caucuses and the New Hampshire primary later this month. But other party leaders have been intervening, too--openly or behind the scenes--against the former Vermont governor.</P>
<P>They claim that Dean is "unelectable"--that he is "too angry," he "doesn't understand" national security, he is too radical. Some of this is just silly. Too angry? Anyone who thinks that Howard Dean is "too angry" should ask one of the millions of people who still can't find a job long after their unemployment benefits have run out.</P>
<P>As for national security, the likes of Joe Lieberman and John Kerry think that Dean should have copied their own groveling praise for George Bush and his "war on terror" when Saddam Hussein was captured. But that's exactly why Dean broke out from the pack in the first place--because he was willing to highlight his criticisms of the Bush White House, however much he shares the same assumptions and goals.</P>
<P>Dean isn't really antiwar. He says, for example, that he would have supported an invasion of Iraq if the U.S. had worked out a deal to win United Nations approval, and he wants U.S. soldiers to stay in Iraq "now that they're there." But because he was the only major candidate to speak out against Bush's unilateral war, he won the support of millions of people who see him as an alternative to the war-hungry status quo in Washington.</P>
<P>A recent poll of Iowa Democrats found that many feel as strongly about their frustration with party leaders in Washington for failing to stand up to Bush as they do about important issues like health care. Dean picked up on this sentiment long ago--and made criticism of the Democratic Leadership Council (DLC), the conservative grouping that set the tone for the party's move to the right over the last two decades, a central point of his campaign.</P>
<P>This is the real reason that leading Democrats are going after Dean. They view his campaign as a threat to their control of the Democratic Party.</P>
<P>But this doesn't mean that Howard Dean is a threat to the corporate interests that the Democratic Party ultimately serves. His actual positions on any number of issues--from war, to government spending and taxes, to social issues--aren't even all that liberal. </P>
<P>And there are plenty of leading Democrats who have been willing to get on board the Dean bandwagon--including Al Gore, the one-time darling of the DLC who spent eight years in the White House "triangulating" with Bill Clinton. These Democrats recognize the difference between Dean's rhetoric--which can energize the party's liberal base to turn out in bigger numbers to vote against George Bush--and his much more moderate political agenda.</P>
<P>In reality, says <I>Newsweek </I>magazine's Tom Watson, Dean has been "deft at wooing the establishment even as he bashes it." If he wins the presidential nomination, it might change some of the faces at the top of the Democratic Party--but not the priorities of a political machine that is organized to deliver, first and foremost, for Corporate America.</P>



