<P><font face="Times New Roman, Times, serif" size="4">Bush wanted war at any cost</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Iraqis paid the price</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 16, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH was planning his cruel war on Iraq from the moment he stole his way into the White House. According to the bombshell revelations of former Treasury Secretary Paul O'Neill, Saddam Hussein was "topic A" 10 days after Bush's inauguration--eight months before September 11 and more than two years before the invasion of Iraq. </P>
<P>"It was all about finding a way to do it," O'Neill says of early Cabinet meetings in a new book. "That was the tone of it. The president saying 'Go find me a way to do this.'" So much for "weapons of mass destruction." So much for stopping "terrorism." So much for "liberating" the Iraqi people.</P>
<P>The Bush gang left a paper trail documenting their designs on Iraq. "There are memos," author Ron Suskind told <I>60 Minutes</I> reporter Leslie Stahl. "One of them marked 'secret' says 'Plan for Post-Saddam Iraq.'" </P>
<P>Another Pentagon document, titled "Foreign Suitors for Iraqi Oilfield Contracts," outlines areas of oil exploration. "It talks about contractors around the world from...30, 40 countries, and which ones have what intentions on oil in Iraq," Suskind said.</P>
<P>In other words, the White House had its war plans ready and was waiting for the right opportunity. When the September 11 attacks took place, the administration cynically used them as the pretext for targeting Iraq. Even Secretary of State Colin Powell finally admitted the truth earlier this month--that there was never any connection between Saddam Hussein and al-Qaeda, despite the administration's lies to the contrary.</P>
<P>The maps for oil exploration in occupied Iraq, drawn up in the first days of the Bush administration, tell the <I>real</I> story about the U.S. agenda in the Middle East.</P>
<P>The hundreds of thousands of Iraqis who died in George Bush Sr.'s first Gulf War weren't killed because of the military threat that the Iraqi regime posed in the region. The more than half a million Iraqi children who lost their lives as a direct result of crippling economic sanctions didn't die because Saddam Hussein had weapons of mass destruction. The tens of thousands killed in Bush Jr.'s invasion--and the many more who continue to suffer under the iron fist of U.S. occupation--aren't the unintended victims of a war against "terrorism."</P>
<P>George Bush's war has always been about oil profits and expanding U.S. empire. As long as Iraq remains occupied, there will be no freedom or democracy for ordinary Iraqis. End the occupation now!</P>


