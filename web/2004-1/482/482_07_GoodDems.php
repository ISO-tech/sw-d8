<P><font face="Times New Roman, Times, serif" size="5"><b>Should we support "good" democrats?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 16, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FOR A few candidates in the race for the Democratic presidential nomination, this is a fight for the "soul" of the Democratic Party. Frontrunner Howard Dean regularly claims that he represents the "Democratic wing of the Democratic Party."</P>
<P>In fact, Dean's record demonstrates that this is more of a pose than anything else. His actual positions on a range of questions aren't much to the left of establishment candidates like Joe Lieberman and John Kerry--if they are to the left at all.</P>
<P>But long-shot candidates like Dennis Kucinich, Carol Moseley Braun and Al Sharpton say that their campaigns are about raising the questions that voters actually care about--and returning the Democrats to their liberal roots. Though they won't say it outright, these three aren't running to win the party nomination--because they don't stand a chance.</P>
<P>Realistically, they can only hope that their campaigns, by their very presence, will transform the shape of the political debate within the Democratic Party. "I want to make the Democratic Party more relevant," Kucinich said in a <I>Concord Monitor/Washington Post</I> online Q-and-A in November. "That is why I am running as a Democrat. I believe my presence in the campaign is already moving the entire party in a more progressive direction."</P>
<P>But what do these "outsider" candidates--Kucinich, Sharpton and Braun--really bring to the table? Are they forcing the Democratic Party to address the issues that voters are concerned about? Or are they bringing those voters, despite their concerns, back into the Democratic Party?</P>
<P>The outsiders themselves are pretty clear about their role. "What people don't understand is that before you can turn people out, you have to turn people on," Sharpton said at a Congressional Black Caucus Institute debate in September.</P>
<P>"And the only way that you're going to turn people on is you must address their interests and address their issues. We have to deal in these primaries and in the convention with those of us who feel that segments of the party have turned on labor and turned on minorities and turned on women."</P>
<P>The same argument was made by Jesse Jackson in his 1984 and 1988 Rainbow Coalition presidential campaigns. Jackson shook up the party establishment far more than Sharpton or Kucinich.</P>
<P>In the 1988 "Super Tuesday" Democratic primary of mostly Southern states, Jackson placed first or second in 16 of 21 primaries and won the Michigan party caucuses outright.<SUP> </SUP>He ended the primary race with an impressive 7 million votes--some 30 percent of the total.</P>
<P>Millions of Jackson supporters believed that Jackson's campaign would force the Democrats to shift to the left. Yet afterward, Jackson dismantled the Rainbow Coalition--and his top aides became mainstream operatives in the Democratic Party. </P>
<P>Why has this happened again and again in the history of the Democrats? The most important thing to understand is that while the Democrats claim to represent working people, they are ultimately a capitalist party--which puts the interests of Corporate America first before they even consider the concerns of their liberal voting base.</P>
<P>Over the decades, the Democrats have spent a lot of time, money and fear-mongering to squash any attempt at an electoral alternative to their left. We got a taste of this fear-mongering in 2000 when Ralph Nader ran a popular campaign on the Green Party ticket, winning nearly 3 million votes.</P>
<P>During the great upheavals of the 1930s, the opportunity existed to create a labor party --one based on the unions and independent of big business. In fact, a 1937 Gallup Poll showed 21 percent support for forming such a party.</P>
<P>But the Democrats--together with labor leaders--derailed any possibility for a labor party. Without an alternative beyond the Democrats, workers who want to cast a vote in their interests really have no option, other than to stay home. And the Democrats count on this.</P>
<P>That's why, as soon as the liberal vote is sewn up--with the help of the "outsiders" who bring progressives into the Democratic fold--the party is free to go in any direction it likes.</P>
<P>This year, the pressure to back any Democratic candidate for president, no matter how conservative, is especially fierce because of the overwhelming desire to get rid of George Bush. So even those who were brought to the Democrats by the dreams of a Kucinich will end up stuck in a party of Kerrys and Liebermans--out of fear and loathing of Republicans.</P>


