<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A new strategy to reverse labor's decline?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 16, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH THE AFL-CIO in decline, a group of major labor leaders join together to propose a dramatic reorganization of the unions. Their argument: Without drastic change, organized labor faces a crisis. So they move ahead to set an example. Word goes out that the top AFL-CIO leaders will be dumped.</P>
<P>All this will sound familiar to anyone who has followed the development of the New Unity Partnership (NUP), a grouping of big unions formed last year to propose a vast restructuring of the labor movement. But it's really a description of 1995--when the United Auto Workers (UAW), International Association of Machinists (IAM) and United Steelworkers of America (USWA) announced a three-way merger in a move aimed at strengthening union power in the metalworking industries.</P>
<P>At the same time, a faction of union leaders maneuvered to force the resignation of AFL-CIO President Lane Kirkland, a conservative who pushed for concessions to employers and support for U.S. policy during the Cold War. Kirkland's successor, John Sweeney, the head of the Service Employees International Union (SEIU), was known for aggressive organizing.</P>
<P>"Organize or die" became the slogan of Sweeney's "New Voices" leadership campaign. Two years later, the big Teamsters strike victory at United Parcel Service electrified the nation and appeared to mark a real turnaround for labor.</P>
<P>But when Teamsters President Ron Carey was subsequently ousted by the government, labor turned its back. AFL-CIO Organizing Director Richard Bensinger was fired when he stepped on too many toes of top labor leaders, demanding that they spend at least a third of their resources on new organizing.</P>
<P>Lack of resources for organizing wasn't the whole story. New organizing was also undermined by labor's unwillingness to mobilize to win difficult strikes and lockouts at employers such as Caterpillar and the Detroit newspapers in the mid-1990s.</P>
<P>The UAW, for example, damaged its own efforts to organize nonunion Japanese-owned auto plants when it abandoned workers at the Accuride wheel plant in Kentucky after a strike and four-year lockout. Now the UAW has agreed to lower wages for newly hired auto plant workers, too--helping to drive down wages in manufacturing and further undercutting labor's appeal.</P>
<P>For its part, the IAM accepted huge wage cuts in the airline and aerospace industries--and the USWA allowed the elimination of retiree health care and pension cuts through bankruptcy. What of the planned merger of these unions? It was quietly killed by bureaucratic rivalries years ago.</P>
<P>Labor's share of the workforce in the private sector is just 9.6 percent today and 13.2 percent overall--with the numbers expected to drop when new figures are released next month. It's in this context that the NUP has emerged.</P>
<P>Led by the SEIU, Sweeney's old union, the NUP includes the Hotel Employees and Restaurant Employees (HERE) and the textile and garment union UNITE, along with the Laborers and, unofficially, the Carpenters' union, which is outside the AFL-CIO. Their proposal: a top-down restructuring in which small unions would be merged with larger ones and jurisdictions redrawn according to 15 targeted industries.</P>
<P>According to documents leaked to the media, local labor councils would be abolished and replaced by appointed officials, while 77 percent of union budgets would be devoted organizing, with most of the rest slated for politics. But ruthless centralization and greater resources for new organizing won't reverse labor's decline.</P>
<P>First, union democracy isn't an optional extra. It's the essence of what makes labor a <I>movement</I>. A leaner, more energetic labor bureaucracy is still a bureaucracy.</P>
<P>And there's no way labor will recruit new members to reverse its decline if it can't defend the interests of the members it has today. That's why the ongoing strike and lockout of 70,000 grocery workers in Southern California is more important for labor's future than the charts and spreadsheets of the NUP strategists.</P>
<P>A labor movement that routinely accepts concessions will never be able to organize the unorganized on a mass scale. And that's a question that the emerging debate around the NUP has so far ignored.</P>




