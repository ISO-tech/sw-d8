<P><font face="Times New Roman, Times, serif" size="4">Kevin Cooper speaks out from Calif. death row:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I ask you to protest for me"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 16, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">CALIFORNA DEATH row prisoner KEVIN COOPER is scheduled to go to the execution chamber on February 10--despite the fact that evidence which could exonerate him has not been reviewed or heard in court. In this statement to fellow abolitionists, Kevin explains why it is important for all who care about justice to take up his case.</P>
</B>
<B><I><P>My brothers and sisters in struggle,<BR>
</B></I>I, Kevin Cooper, am writing this to you so that you will know from me where I stand concerning my upcoming murder by the state of California, and your reactions to it. While I am truly grateful and thankful for the prayers that will be said for me, I respectfully ask that more than people praying will be done, not just on my behalf, but on behalf of this crime against humanity that is about to happen to me.</P>
<P>I respectfully ask that people protest and demonstrate against my murder by the state of California! I respectfully ask that those of you who believe in God will use that part of God that is within you to get up off your knees and take to the streets in protest!</P>
<P>I am an innocent man, and while no human being has the right to murder/execute another human being, this is more than true when it comes to innocent people! We poor people, who are the only ones murdered by our government, are the only ones who can put a stop to it, and we must protest as well as pray.</P>
<P>There can be a vigil for me after I am dead, but while I am alive, we must protest against my murder and this crime against humanity! I thank you for you help and understanding in this most important matter.</P>
<P>With respect and appreciation,<BR>
<B>Kevin Cooper, </B>San Quentin, Calif. </P>


