<P><font face="Times New Roman, Times, serif" size="5"><b>Ethnic slur from a darling of liberals</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 16, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AT A campaign event for a fellow Democrat in St. Louis, Sen. Hillary Rodham Clinton (D-N.Y.) thought that it would be funny to use a racial stereotype for some cheap laughs. To introduce a quote from Mahatma Gandhi, Clinton said, "He ran a gas station down in St. Louis for a couple of years."</P>
<P>After laughter in the crowd of 200 quieted down, she continued, "No, Mahatma Gandhi was a great leader of the 20th century." In response to calls for her to apologize, Clinton sounded like she thought that people who took offense were the problem.</P>
<P>"It was a lame attempt at humor, and I am very sorry it might have been interpreted in a way to cause distress to anyone," read a statement issued by her office. But for many in the Indian community, the damage was done.</P>
<P>"I'm so angry with Hillary Clinton, I heard her on TV," said Amir Sabri. "When Republicans say stupid things, they have to be re-educated. Hillary is a Democrat, but she should go to diversity school, too, to learn about her stupid stereotypes."</P>
<P>Or maybe Clinton should reread her 1996 book <I>It Takes a Village, </I>in which she wrote, "Within and beyond their homes, adults must speak out against racial, ethnic, religious or gender slurs."</P>



