<P><font face="Times New Roman, Times, serif" size="5"><b>A Medicare "reform" rip-off</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 16, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CORPORATE EXECUTIVES are cheering the new Medicare program that covers prescription drugs for retirees, according to the <I>Wall Street Journal. </I>That's because the legislation is written to give some of the biggest corporations in the U.S. a huge giveaway if they cut back on retiree health coverage.</P>
<P>A provision in the so-called Medicare "reform" calls for government payment of subsidies to corporations that have a prescription drug program for retired workers. Thanks to a little-noticed part of the legislation, the amount of the subsidy will be calculated based on both what employers pay out, and what retirees pay in out-of-pocket expenses.</P>
<P>That means that employers will save money by forcing more and more of the costs of prescription drugs onto workers. "As a result, when combined with tax and accounting rules, the program allows employers in some cases to use the subsidy to erase the entire cost of prescription drugs for retirees, or even turn a profit from a drug plan," wrote the <I>Wall Street Journal.</P>
</I><P>Corporations like General Motors, Dow Chemical, Lucent Technologies and SBC Communications stand to reap hundreds of millions each. Lucent, for example, has about 240,000 retirees and dependents. Under the new program, it stands to save up to $1,330 per retiree--for a total of about $320 million!</P>
<P>The Medicare "reform" fraud pushed by Republicans and Democrats alike has turned out to punish some of the most vulnerable in society. And in the process, politicians figured out yet another way to funnel millions of dollars to their campaign contributors in Corporate America.</P>



