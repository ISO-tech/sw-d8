<P><font face="Times New Roman, Times, serif" size="4">Fannie May closes down its factory and stores</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We feel betrayed"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 16, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Archibald Candy Corp. abruptly announced that it would close its Fanny May candy plant here and shut down its 250 Fannie May and Fanny Farmer retail stores by February 15. January 16 will be the last day of work for 500 production workers and 125 office workers, violating the 60-day notice Archibald is required to give its workers under federal law.</P>
<P>"They didn't tell us about the shutdown until the last minute," Don, who has worked at Fannie May for 23 years, told <I>Socialist Worker.</I> "Yeah, we feel betrayed. What else could we feel? If someone hadn't come across a paper talking about their plans, I think that we would have shown up for work one day and the doors would have been closed. I don't even know if that's legal."</P>
<P>Archibald's January 10 announcement ended a frantic two weeks in which workers had sought intervention from city, state and congressional leaders after they discovered an internal company memo detailing management's plan to close the plant.</P>
<P>Chicago is known as the capital of the U.S. candy industry. But for years, candy corporations have been moving their operations elsewhere. Marshall Field's ceased production of its Frango Mints here about five years ago, and in early January Brach's Confections, founded in 1904, finished closing its huge plant that once employed 3,500 people.</P>
<P>Archibald bought up Fannie May in 1991, taking over a plant that's produced candy for the last 70 years. Employers claim that their exodus is being driven by high domestic prices for the main ingredient in candy--sugar--due to U.S. import restrictions.</P>
<P>That's only part of the story. After all, more than 100 candy producers--accounting for $4 billion in economic activity--remain in Chicago. </P>
<P>What's more, Fannie May workers made a series of painful givebacks to management over the years. "People here have given up as many concessions as possible, like vacation days, because the company said it was facing hard times," said Don. "And then we hear that executives are getting fat bonuses--like $250,000 bonuses."</P>
<P>"We were shocked," Victor Davila, who worked for 26 years at Fannie May, told <I>Socialist Worker</I>. "It just came so suddenly. And it would have been worse except someone let the cat out of the bag." </P>
<P>What's more, the company's sudden shutdown may be a violation of the WARN Act that requires 60-day notice of a plant closure. The company also may be trying to wriggle out of its contractual obligations to its workers, who are represented by Teamsters Locals 781 and 705 and Service Employees International Union Local 1.</P>
<P>"They're leaving us high and dry here," continued Don. "There's a clause in our contract that if the company moves more than 270 miles, then we're guaranteed severance pay of one week for each year of service. But they're not giving us that, even though it looks like they're probably moving to Canada. And they're saying that vacation doesn't start until April so we can't take that. </P>
<P>"And even though there's a buyout plan for vacation, they're saying that you can only 'sell' two weeks of vacation if you've got five built up. But if you've got less than five, then you can't sell any."</P>
<P>Shamefully, the workers' unions did almost nothing to organize opposition to the shutdown or and the company's possible violation of the WARN Act. It was only a protest by 100 workers at City Hall that gained public attention.</P>
<P>With factory shutdowns across the U.S. continuing in the midst of the recovery, its urgent that organized labor mobilizes to stop these closures and protect workers' rights.</P>



