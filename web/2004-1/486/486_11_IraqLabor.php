<P><font face="Times New Roman, Times, serif" size="5"><b>Fighting for labor rights from Iraq to the U.S.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Grohman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 13, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 200 activists, union workers and veterans gathered February 3 for a meeting, "The Fight for Labor Rights, From Iraq to the U.S.," sponsored by New York City Labor Against the War (NYCLAW). The speakers were Clarence Thomas, a member of the executive board of International Longshore and Warehouse Union Local 10 and labor journalist David Bacon, who traveled to Iraq to meet with Iraqi trade unionists, and Gulf War veteran Michael McPhearson, who visited Iraq with a Military Families Speak Out delegation.</P>
<P>Thomas described the impact of George W. Bush's use of the anti-union Taft-Hartley law during the lockout of the ILWU in 2002. "For the ILWU this made the connections with our struggles here and the war in Iraq.  Foreign policy affects the labor movement directly.  What Bush is doing in Iraq is just a preview of what he wants to do here."</P>
<P>He described workers at a refinery in Iraq, where workers are paid $60 per month--less than they received under Saddam Hussein. Many work two shifts, lack safety equipment and have no insurance in case of injury.</P>
<P>"This is not about bringing democracy to Iraq--this is about finding further ways to exploit the workers of Iraq," Bacon said, adding that workers are still under Saddam's anti-union laws and want union rights. "They also want to be able to oppose the privatization that is costing them their jobs. Finally, they want the occupation to end. The most important thing that we can offer is solidarity to end the occupation."</P>
<P>McPhearson, a Gulf War vet, added, "we need to keep calling for the troops to come home now. We cannot let Bush forget about it, but I don't believe if there is a Democrat in office that it's going to make a difference."</P>
<P>Babara Bowen, president of the Professional Staff Congress union at the City University of New York, summed up the event: "Meetings like this show the possibility of the strength that we have as unionists, and we should go back to our unions and pressure them to get involved in the fight for Iraqi labor rights and the rights of all workers."</P>


