<P><font face="Times New Roman, Times, serif" size="4">How U.S. spies have always served up war lies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The CIA cooks the facts</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 13, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>Under the microscope for lying about Iraq, the Bush administration looks like it's going to opt for the least damaging course--investigate the "intelligence failures" of the CIA. By doing this, the Bush people hope to deflect as much as possible the blame from themselves. </P>
<P>"We thought Saddam Hussein was an immanent threat because of what the CIA told us," they want to say--whereas the truth, as it always is with these kind of intelligence issues, is that the CIA (in full cahoots with Bush) cooked the facts to serve a political and military agenda already established by the Bush administration. </P>
<P>That is one of the CIA's jobs--in addition to organizing assassinations, coups and dirty wars. In his 1972 book, <I>CIA: The Myth and the Madness,</I> former CIA agent Patrick McGarvey explained how so-called "intelligence gathering" works. </P>
<P>He described a situation from the mid-1960s, when the military wanted to expand the U.S. bombing campaign. Not impressed with the evidence, McGarvey wanted to write a report explaining that the bombing was ineffective. </P>
<P>A man from the Defense Intelligence Agency explained to him that this is not an option. "Pat, there is one thing you haven't learned yet," he said. "The cold fact is that the J-3 has already decided that we are going to step up our bombing in Laos. He is now going through the fire drill of substantiating the exercise. We can in no way influence his decision."</P>
<P>McGarvey then asked the official what would happen if he wrote a report that questioned the value of stepping up the bombing of Laos. Here was the official's response: "They'd take your report, call a meeting with you and perhaps six staffers from the service intelligence and operations staffs, and spin you around in circles. </P>
<P>"It would probably last from about 11:00 a.m. to past midnight. You could fight all you want, but the results would be the same. They'd water down the original paper to the point where your objections would be merely footnotes to the opinions of the others assembled."</P>
<P>I see no reason not to believe that things still operate that way.</P>
<B><P>Paul D'Amato, </B>Chicago</P>

