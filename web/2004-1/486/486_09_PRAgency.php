<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>P.R. agency for U.S. occupation</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 13, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ABOUT THIS time last year, the airwaves were thick with right-wing denunciations of the United Nations (UN) as an "irrelevant" cats-paw of wimpy multilateralists, bent on preventing the U.S. from bringing liberty to the people of Iraq. Bush and his neoconservative backers got their invasion after the UN Security Council refused to authorize it.</P>
<P>But the unfolding disaster of the occupation of Iraq has forced the Bush administration to seek the UN's help. The latest gambit involves UN Secretary General Kofi Annan's mission to Iraq to determine if free elections can be held before July 1, 2004--when the U.S. wants to, as the Bush administration describes it, "return sovereignty" to the Iraqi people.</P>
<P>At a February 3 meeting, Bush--who last year was calling the UN the modern-day equivalent of the League of Nations--said he would abide by whatever agreement the UN can arrange to hold elections by the deadline. "We are going to help them [the Iraqis] work out this problem, and hopefully, they will come to some consensus and agreement as to how to move forward," Annan said, noting that the U.S.-backed puppets in the Iraqi Governing Council agreed to accept the UN team's recommendations.</P>
<P>An unnamed Bush administration official quoted in the <I>New York Times</I> was more forthright about what the UN's role would be. "We are trying to put this issue in Kofi Annan's lap and let him run with it," the official said. "There's still very much the intention to stick with the date of June 30. But there's a lot of pressure on Kofi Annan to come up with the right solution."</P>
<P>The crisis that precipitated Bush's sudden willingness to work with the UN has been unfolding since November, when the hand-picked U.S. puppets in the Iraqi Governing Council agreed to a U.S. plan to form some kind of transition government in July. The initial U.S. scheme called for delegates to a constitutional convention and transitional parliament to be selected through a complicated series of caucuses that were to be appointed by U.S.-approved village notables.</P>
<P>Shia leader Ayatollah Sistani rejected the plan and called for direct elections to these posts. He backed up these demands with mass demonstrations of hundreds of thousands of his followers in January.</P>
<P>Even the dolts who run the Coalition Provisional Authority (CPA) know that if the majority Shia population moves into active opposition to the occupation, it's finished. Sistani gave the CPA a way out of an immediate confrontation when he let it be known that he would abide by a UN decision on the feasibility of direct elections in Iraq.</P>
<P>The U.S. plan to create an Iraqi government on July 1 has nothing to do with handing over real sovereignty to Iraqis. In the short term, it's a Bush administration election-year ploy designed to present to the American people the image of "progress" and "success" in Iraq. In the longer term, it's an attempt to create a pro-U.S. regime that sinks deeper roots than the current quisling governing council.</P>
<P>The two biggest questions that would define genuine Iraqi sovereignty--getting all U.S. troops and military bases out of Iraq and allowing Iraqis full control over their oil resources--are not on the table. "Both Iraqi and American officials now appear to think that a significant UN role would not only give the process legitimacy in the eyes of the world, but might also defuse opposition among some Iraqis," the <I>New York Times</I> commented.</P>
<P>In other words, Kofi Annan has offered up the UN to play a role as a public relations agency for the U.S. occupation. Antiwar activists should be clear about this.</P>
<P>However humiliating it is for the Bush administration to seek the UN's help, it has no plans to surrender control of Iraq to the UN or to Iraqis who are not willing to do its bidding. UN intervention in the elections is a step toward the "multilateralization" of the occupation--not a step toward the <I>end </I>of the occupation.</P>

