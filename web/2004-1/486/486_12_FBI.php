<P><font face="Times New Roman, Times, serif" size="4">Activists under fire</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>FBI demands records from Iowa school</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 13, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FEDERAL PROSECUTORS are targeting the antiwar movement. They've subpoenaed four people who attended a November 15 antiwar forum at Drake University in Des Moines, Iowa. And they got a judge to order the university to hand over all available records of who attended the forum--a measure that may be the first of its kind in decades.</P>
<P>Those served with subpoenas--the leader of the Catholic Peace Ministry, a former coordinator of the Iowa Peace Network, a member of the Catholic Worker House and an activist who traveled to Iraq in 2002--had to appear before a federal grand jury on Tuesday.</P>
<P>So while the Bush administration claims to have gone to war to "bring freedom to Iraq," it's putting basic rights to free speech and assembly in jeopardy at home. Prosecutors are also demanding that the local chapter of the National Lawyers Guild (NLG) turn over all of its records dating back to 2002. The NLG says that it will defy the order.</P>
<P>"The law is clear that the use of the grand jury to investigate protected political activities or to intimidate protesters exceeds its authority," said NLG President Michael Ayers in a statement.</P>
<P>On Drake's campus, members of the faculty were outraged by the subpoenas and are planning how to show their opposition. "Does this mean the university can't hold conferences that deal with political issues?" asked journalism professor Herb Strentz. "This is certainly a stark contrast to the notion of a university being a place for the wide open exchange of ideas and debate."</P>
<P>The forum--titled "Stop the Occupation! Bring the Iowa Guard Home!"--took place one day before an antiwar rally at Iowa National Guard headquarters where 12 demonstrators were arrested. At the forum the night before, organizers held nonviolence training to plan for the demonstration.</P>
<P>Tom Lewis, a professor of Spanish at the University of Iowa and a member of the International Socialist Organization, spoke at an educational workshop at the event. "I'm proud to have participated in the antiwar event," Lewis wrote in a letter to the <I>Des Moines Register.</P>
</I><P>"[T]he current grand jury investigation has but one purpose: to fire the first salvo in Iowa of a new Nixon-style wave of grotesque violations of the constitutional rights of citizens or groups who somehow make it on to John Ashcroft's and George Bush's personal 'enemies list.' The educational events and the methods of protest employed at Drake...fall well within the bounds of protest accepted by America's historical civil rights movements."</P>


