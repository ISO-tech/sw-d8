<P><font face="Times New Roman, Times, serif" size="5"><b>Labor's fight for social justice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 13, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Dan Clawson, <B><I>The Next Upsurge: Labor and the New Social Movements.</B></I> Cornell University Press, 2003, 235 pages, $18.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE LABOR movement is in state of crisis, with only 13.5 percent of workers organized in unions. Some at the top have sounded the alarm--most notably the New Unity Partnership (NUP), led by the heads of three major national unions, which plans to challenge John Sweeney for AFL-CIO leadership and, among other things, proposes that unions devote 77 percent of their budgets to organizing.</P>
<P>But what would it really look like for the labor movement to turn itself around? Dan Clawson's <I>The Next Upsurge</I> attempts to answer this question by looking at some of the innovative organizing of the 1990s--the anti-sweatshop and globalization movements, community organizing campaigns, the 1997 United Parcel Service strike and the Justice for Janitors campaigns.</P>
<P>Clawson starts from the point of view that the U.S. labor movement has only gained significant ground during periods of upsurge, not through incremental growth. He argues that explosive expansions occur only when the labor movement fuses with social struggles against racism and for broader economic justice, as it did in the 1930s.</P>
<I><P>The Next Upsurge</I> also argues that in order to succeed, organizing drives must empower rank-and-file workers themselves. This, he argues, means organizing from below rather than bureaucratically from above, a refreshing counterpoint to the top-down strategies on offer from AFL-CIO and NUP leaders.</P>
<P>For example, a chapter focuses on the community organizing efforts in Stamford, Ct., where a coalition of local unions took on the defense of low-cost public housing because it was a key issue for their members. While it wasn't a union organizing drive, the connections to the community that the unions made during their campaign that successfully stopped the privatization of two public housing projects, led to a stronger organizing base for future local union campaigns.</P>
<P>Clawson also examines what can happen when students and community support groups get involved in union campaigns--for example, the United Students Against Sweatshops, in which thousands of students around the country occupied administration buildings to demand that clothing with their school logo be sweatshop free. This movement has continued with a series of important local campaigns to organize for a living wage laws.</P>
<P>At the same time, the book stresses that organizing <I>on behalf </I>of workers can't replace workers organizing themselves. Clawson does not look at some of the organizing going on for union democracy within the big industrial unions like the Teamsters and the United Auto Workers.</P>
<P>He seems to have in part accepted the idea that manufacturing jobs are irreversibly moving overseas, and so that only by organizing service-sector workers can the labor movement recover. This is unfortunate, because new organizing is much more likely to be successful if labor is winning victories for the members that it already has.</P>
<P>Certainly organizing campaigns at corporations like Wal-Mart will play an important part in any resurgence of the labor movement, but these campaigns would benefit from militant tactics within already-organized industry as well. Nonetheless, <I>The Next Upsurge</I> is a must-read for all activists--in unions or otherwise--who want to see the labor movement turn around.</P>

