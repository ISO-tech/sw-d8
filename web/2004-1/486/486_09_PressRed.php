<P><font face="Times New Roman, Times, serif" size="5"><b>The red who helped desegregate baseball</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Peter DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 13, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Irwin Silber, <B><I>Press Box Red. </B></I>Temple University Press, 2003, 248 pages, $19.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN 1997, baseball teams across the U.S. celebrated the 50th anniversary of Jackie Robinson smashing the sport's color line. But the campaign to desegregate baseball actually began years before--with the Communist Party (CP) in the late 1930s.</P>
<I><P>Press Box Red </I>documents the work of Lester Rodney, the sports editor for the CP's <I>Daily Worker </I>newspaper for more than 20 years. While the book is first and foremost a history of Rodney's efforts, parts of this book are of particular interest to revolutionary socialists.</P>
<P>For one, the whole strategy of starting a sports section (and agitating for integration) were parts of the Communist Party's "Popular Front" shift in 1935. The <I>Daily Worker </I>was reconceived from an organ addressing the party to one that appealed to a broader mass of Americans sympathetic to issues of radical social change.</P>
<P>Almost overnight the CP went from labeling sports an "opiate of the masses" to seeing it an important component of people's lives. And the <I>Daily Worker's </I>sports coverage--and in particular its agitation for integration--was seen as a way to aid the Communist Party's organizing efforts in Black communities.</P>
<P>On August 13, 1936, the paper ran the first article on segregation in baseball under the headline "Outlawed by Baseball!" That kicked off a year-long campaign on the issue.</P>
<P>The other interesting aspect of the fight for integration was that it illustrates how a revolutionary paper can concretely serve as an organizing tool. The move for integration of baseball began purely as an agitation campaign. But Rodney's editorials were eventually used as a centerpiece to a massive petitioning campaign.</P>
<P>First the CP, and later countless unions, began a petition drive that generated 2 million signatures in support of baseball's desegregation. <I>Press Box Red </I>not only sheds light on the fight to desegregate baseball but provides insight into understanding the application--and limitations--of the Communist's Popular Front strategy.</P>


