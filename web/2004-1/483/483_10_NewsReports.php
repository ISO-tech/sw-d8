<P><p><font face="Times New Roman, Times, serif" size="4"><b>Defending Martin Luther King's legacy</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Desmond Gardfrey</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ATLANTA--"Bush, go home!" chanted more than 700 people as George W. Bush arrived to place a reef on Martin Luther King's tomb. Adding to his record of outrageous and hypocritical public relations stunts, Bush's visit to Atlanta angered people throughout the U.S. and beyond. </P>
<P>As he hid behind a heavy police presence and city buses used as barricades, Bush laid a wreath on King's grave and tried to ignore the chorus of boos from the crowd--and then he scurried away after two minutes. Meanwhile, several hundred people managed to push past Secret Service barricades, and two protesters were arrested.</P>
<P>The obvious contradiction between Bush's warmongering and racism and King's antiwar and civil rights efforts fueled the anger. "When I heard Bush was coming here, I couldn't believe it," Kathy Nicholas, a flight attendant from Atlanta, told reporters. "I was outraged and disgusted, and I just think it's a photo op. It's so transparent."</P>
<P>After leaving King's tomb, Bush rode off to attend a $2,000-a-plate fundraiser where he collected campaign contributions from 700 wealthy Republicans and Democrats. Democratic Sen. Zell Miller as well as other Georgia Democrats were in attendence to voice their support for Bush's policies. All in all, Bush raised $2.3 million during his trip to Georgia.</P>
<P>In its trademark "fair and balanced" style, Fox News channel's Hannity and Colmes attempted to portray Bush as an "honorable" man and the protesters as hypocrites who said "disgusting" things about the president--as if Bush's attack on affirmative action during King's 2003 birthday ceremony wasn't the height of hypocrisy. Thousands of people will take to the streets in the annual MLK Day March to show their for the real peace and justice that Dr. King stood for, not Bush's sham version. </P>

