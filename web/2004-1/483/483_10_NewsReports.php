<p><a name="Iraq"></a><font face="Times New Roman, Times, serif" size="4"><b>No war on Iraq</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Wallis</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Despite cold that caused school closings and even a few deaths around the region, hundreds gathered in front of Madison Square Garden to voice their opposition to the war in Iraq and to the U.S.-sponsored occupation of Palestine. A protester from Georgia pointed out that Dr. Martin Luther King's treatment in the mainstream media tends to completely ignore the more radical views that he began to express in the year or two before being assassinated. </P>
<P>The same protester also explained the significance of gathering in the record-breaking cold: "It means the people of the left are tough, and we're gonna win!" At an indoor rally that the crowd marched to later, a similar confidence was expressed in many of the speeches. </P>
<P>Despite the vast array of groups they represented, all speakers seemed to agree with labor union organizer Brenda Stokely of District Council 1707, who called for an alternative to the two-party system. "I'm not going to cast a vote for Imperialist A or Imperialist B!" exclaimed Stokely.</P>
<P>Nelly Bailey of the Harlem Tenants' Council also spoke of imperialism when she explained that landlords' efforts to remove tenants from affordable housing are part of a drive to maximize profits by authorities everywhere. "Homelessness is increasing by leaps and bounds," she added.</P>

