<p><a name="EIE"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop police torture</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor and Alice Kim</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Last week, about 25 activists and family members of wrongfully convicted Black and Latino men and women gathered at the office of Illinois Attorney General Lisa Madigan to protest her unwillingness to investigate documented claims of abuse, torture and other misdeeds in the cases of at least 90 Black men who are currently imprisoned in the state of Illinois.  </P>
<P>Protesters--including former death row inmate Aaron Patterson--picketed outside and then marched up to her office inside of the State of Illinois Building. The protest at Madigan's office was the first action by a newly formed group, Enough is Enough (EIE). </P>
<P>Once inside protestors demanded to speak with Madigan--wanting to know when she would investigate torture and abuse claims. Madigan's chief investigator promised the State's Attorney was interested in speaking with the group, but that she was out of town.</P>
<P>Protestors promised to keep the pressure on Madigan until she agreed to meet and launch an investigation. "This [police corruption] has gone on for too long," one protester, Gwen, told <I>Socialist Worker.</I> "It's time we stand up and do something about it because while it's us standing here today--it could be you tomorrow."</P>
<P>EIE--initiated by activists from the Campaign to End the Death Penalty (CEDP)--formed to launch a wider campaign to fight for justice around the 90 cases involving police torture and misconduct and their wrongful convictions. The CEDP is also planning to mark the fourth anniversary of the Illinois moratorium on executions at a forum on January 31. </P>
<P>Although the Illinois legislature recently passed several death penalty reforms, activists are continuing to fight for abolition as the only solution to the ongoing corruption and racism in the administration of the death penalty.</P>
<P>A lawsuit filed by Madigan seeks to overturn the commutations of 32 men who did not sign their clemency petition or whose death sentences had previously been vacated. Renaldo Hudson, who is named in Madigan's suit, will speak at the forum from his prison cell via a telephone hook-up along with freed Illinois death row prisoner Madison Hobley, Jennifer Jenkins of Murder Victims Families for Reconciliation and CEDP activists.</P>



