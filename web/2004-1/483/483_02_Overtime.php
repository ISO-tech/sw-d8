<P><font face="Times New Roman, Times, serif" size="4">Rights of millions at stake</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The White House overtime rip-off</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AND THEY call it the Department of "Labor"? As the Bush administration makes its latest effort to push through new rules that would take away the overtime rights of millions of workers, its Labor Department is offering tips to bosses on how to use the new guidelines.</P>
<P>On January 20, the Senate is scheduled to vote on a massive federal spending bill that includes important changes in the 65-year-old Fair Labor Standards Act (FLSA). Under the FLSA, hourly employees who work more than 40 hours a week are entitled to time-and-a-half pay.</P>
<P>In March 2003, the Bush administration proposed changes to the law--under the guise of "expanding" overtime--that would reclassify millions of workers as "managerial" or "professional," thus making them ineligible for overtime pay. Workers could lose overtime rights if they gained certain skills through job experience, military training and technical school--or if they supervised two or more employees.</P>
<P>All told, according to the Economic Policy Institute, some 8 million workers could lose their right to overtime pay. "It's a pay cut and an attack on workers' rights," says John Garrity, an electronic technician with the U.S. Navy, who fears he could lose about $6,000 a year if Bush gets his way. "They're trying to roll the clock back," Garrity told Inter Press Service. "People fought and went to jail to have a 40-hour workweek so they could spend more time with their families. The president just wants to pay back his corporate friends."</P>
<P>Under pressure from labor activists, House and Senate members voted last year to approve an amendment to the Bush proposal that would prohibit the Labor Department from implementing the new rules. But in November, after Bush threatened to veto the legislation, the amendment was dropped. This month, the spending bill--and Bush's overtime rules--go back to the Senate.</P>
<P>As if this weren't bad enough, the Labor Department is issuing tips to employers on how to avoid paying any overtime at all. One option suggested by the Labor Department's March 2003 summary is making a "payroll adjustment" that results "in virtually no, or only a minimal increase in labor costs."</P>
<P>In other words, cutting wages so that employees would work the same hours for less pay. "Apparently, the Labor Department, which is supposed to advocate for the country's workers, is not," said Diane O'Brien, spokeswoman for the Minnesota AFL-CIO.</P>



