<P><font face="Times New Roman, Times, serif" size="4">Protesters from Vieques get harsh sentences</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sent to jail for protesting</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE VIEQUES 12--a group of anti-Navy activists accused of destroying U.S. government property at a former military base on the Puerto Rican island of Vieques--were convicted and received harsh prison sentences in December. The charges date back to May 1, 2003--the day that the U.S. Navy abandoned its bombing range in Vieques in the face of mounting pressure.</P>
<P>Thousands of people had gathered at the base to celebrate this victory. But they found out that the U.S. government had no intention of returning the land to the people of Vieques, instead transferring control of most of the base to the U.S. Fish and Wildlife Service--as a nature preserve!</P>
<P>The thousands of demonstrators rebelled, destroying the guard station and several military and police vehicles. Federal prosecutors chose 12 people from among the thousands of participants to set an example that would intimidate activists from future actions to demand a full cleanup of the Vieques base--after 60 years of contamination from U.S. weapons--and control over the land.</P>
<P>Absurdly, the Justice Department claimed that the spontaneous protests on May 1 were the result of a conspiracy led by the Vieques 12. Among the 12 is Nilda Medina, leader of the Committee for the Rescue and Development of Vieques (CPRDV), the main Vieques-based activist group that led the protests that kicked out the Navy. Also convicted was Jesus Delgado, former president of the Puerto Rican Federation of Teachers.</P>
<P>All but one of the 12 pleaded guilty to avoid a trial that could have resulted in a 20-year prison sentence. The only person who faced a trial was Jos&eacute; Perez, who was sentenced to five years in prison. Many in Puerto Rico consider Perez's sentence excessive--especially when compared to the easy sentences handed out by the same federal court to corrupt corporate and government officials.</P>
<P>Sadly, because of its pacifist politics, the movement has remained on the defensive against accusations of having used violence in the struggle to liberate Vieques--and didn't really mobilize to stop the victimization of the 12.</P>
<P>In Vieques, the struggle to get the lands cleaned up and returned continues. The federal government insists that the former base won't be inhabited, so it wants only a minimal cleanup.</P>
<P>The Fish and Wildlife Service today occupies 80 percent of former Navy base, and it fines fishermen who fish in nearby waters. "This is the same agency that stood by while the Navy bombed the flora, fauna and wilderness, without raising a finger in protest, and now they're fining people for fishing crabs," said Roberto Rabin from the CPRDV. "This is insulting and completely unacceptable."</P>
<P>Meanwhile, unemployment in Vieques runs as high as 50 percent, and 81 percent of children live in poverty, the highest rate in Puerto Rico. One year ago, in a widely publicized media event, the administration of Puerto Rico's Gov. Sila Calderon promised to create some 500 new jobs in Vieques.</P>
<P>Today, Carlos Zenon, a leader of Vieques fishermen, says: "The government has left us orphans," with no new jobs nor affordable housing. At the same time, the people of Vieques face the danger of real estate speculators, who are looking to get hold of the island's lands to build new tourist areas and summer homes for the rich.</P>
<P>This would consign the people of Vieques to the same marginalization that residents of the neighboring island of Culebra faced when they expelled the Navy from their lands in the 1970s. Today, two-thirds of the residents of Culebra live in poverty.</P>
<P>"We will continue to pressure the Government--in Vieques, in Puerto Rico and the U.S.--for genuine community participation in the decision making process related to the future of our island," says the CPRDV. "We will not allow another powerful force--<I>private property</I>--to take control of our Vieques now liberated from the Navy. It is our duty and our commitment with the next generations of Vieques to struggle for a Vieques in peace with justice." </P>



