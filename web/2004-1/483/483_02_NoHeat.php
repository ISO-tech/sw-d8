<P><font face="Times New Roman, Times, serif" size="4">The victims of a system that puts profits before people</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Struggling to survive without heat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sean Petty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHEN BRUTALLY cold temperatures and arctic winds hit the Northeastern U.S. last week, the media advised people that it was dangerous to go outside unless they had to. But for tens of thousands of people in New York City without heat in their apartments, staying at home brought no relief.</P>
<P>Since October 1, the city's non-emergency hotline has received more than 100,000 calls complaining of heat or hot water problems, according to the <I>New York Times. </I>Seven deaths were blamed on the record cold. Two of the victims were homeless men. One froze to death under a bridge, and the other died a fire that he had started to keep warm in an abandoned building.</P>
<P>Meanwhile, even people with a roof over their heads endure horrific conditions. Many people turn to electric space heaters--sometimes with deadly results. During just one month last winter, five New Yorkers died in fires related to using space heaters to keep warm.</P>
<P>Even when the heaters work, they shift the cost onto tenants, who face hundreds of dollars in electricity bills. Twana Frye bathes her baby girl Inijah using water warmed on the stove--the only source of heat in the apartment. </P>
<P>Carol Parker puts four pots on the stove and a wok in the oven, fills them with water--and boils the water. She closes all the doors in her apartment and opens them one at a time so that the steam circulates. Parker has been logging temperatures in her home all winter long. On December 4, it dropped to 36 degrees.</P>
<P>The building where Parker lives has been cited for 264 violations, ranging from insufficient heat to water leaks, since 1978. So Parker and the other tenants don't expect much help from the city--which has only 300 building inspectors to follow up on heat complaints. </P>
<P>Even more insane is that landlords are given notice before the inspectors show up. So, of course, say tenants, the heat is mysteriously back on when the inspectors arrive--and is turned back down when they leave.</P>
<P>Clearly, poverty and greed, not nature, are the villains--in New York City and around the country. In Chicago, an estimated 10,000 households are without heat this winter. Yet families can't get emergency government assistance to get the heat turned back on--because money for both federal and state aid programs have run out.</P>
<P>Such problems aren't the exception. They are inevitable in a society that puts profits before people.</P>



