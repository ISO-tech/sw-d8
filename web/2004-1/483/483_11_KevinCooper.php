<P><font face="Times New Roman, Times, serif" size="5"><b>Don't execute Kevin Cooper!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Corrie Westing</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--With a February 10 execution date looming, the Committee to Save Kevin Cooper and other activist coalitions in California are in high gear to stop the execution of this innocent man. Because Gov. Arnold Schwarzenegger is an open supporter of capital punishment, it's critical to build a loud and high-profile movement to put pressure on him. Will the "Governator" kill a Black man during Black History Month, while also gutting the budget for education and social services?</P>
<P>The most exciting development in the movement to save Kevin's life is a signature ad to be published in a few large California daily and weekly newspapers, featuring the support of Noam Chomsky, Angela Davis, Howard Zinn, Jesse Jackson and actors Janeane Garofalo and Danny Glover. Through this ad and other upcoming events, we will show the Governator that the whole world is watching.</P>
<P>From the beginning, Cooper's case has been tainted by racism, error and the possibility of police tampering with evidence used to convict him. And while Cooper has always maintained his innocence, another prison inmate actually confessed to the crime, providing his cellmate with information about the crime that was not reported in newspapers.</P>
<P>Fully 35 percent of California's massive death row is Black, while only 6.7 percent of the population is Black. On February 3, activists across the state are holding a Day of Action to Save Kevin Cooper. Highlights include a press conference held with people of faith in the Los Angeles area, including the priest of the governor's church, as well as protests and press conferences in Fresno, San Francisco, Santa Cruz, Sacramento and Riverside.</P>
<P>It's urgent that everyone get involved now to save this man's life--and join the movement to abolish the death penalty once and for all, in California and across the U.S.</P>
