<P><font face="Times New Roman, Times, serif" size="4">What's at stake in the California grocery strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The race to the bottom</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 23, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Seventy thousand Southern California employees of the giant supermarket chains Vons, Pavilions, (both owned by Safeway), Ralphs (owned by Kroger) and Albertsons are now in their 15th week of a strike and lockout that began in October. Their union, the United Food and Commercial Workers (UFCW), has significantly revved up the fightback by calling for a nationwide boycott of all Safeway stores and subsidiaries.</P>
<P>Since early November, teams of striking/locked out Southern California grocery workers have been traveling to Northern California to walk in "informational" picket lines in front of the doors of Safeway stores in San Francisco and many other Bay Area cities. Safeway is the target of these extended actions because the company and its CEO Steve Burd are leading the attack on workers and their health benefits that provoked the strike.</P>
<P>Burd insists that drastic reductions in labor costs are necessary for the company to remain competitive with Wal-Mart. The average salary for Wal-Mart employees is $14,000 a year, and they pay for about 45 percent of their health care costs. Wal-Mart even helps its employees apply for public assistance&#150;for which they qualify. Yet the average salary for a union grocery worker is $19,000 a year. </P>
<P>The intent of the coast-to-coast boycott is to send a message to Burd that working people everywhere realize that converting a huge swath of living-wage jobs into poverty-wage jobs is not an investment in anybody's future.</P>
<P>For workers on the picket line, it gets harder and harder to survive as the days go by&#150;whereas for the company it gets easier, as scabs become more experienced and alternative routines are established. The UFCW's decisions to wait six weeks before placing picket lines at the distribution centers, and then to pull pickets from Ralphs stores to allow customers to patronize supermarkets with scab employees, are questionable.</P>
<P>As the striking grocery workers understand, if we lose this fight it will be open season on all workers, and we'll be dragged down in the corporate race to the bottom of wage and working conditions. To win, we need the kind of solidarity that reminds the bosses that their profits come from our work and that they depend on us&#150;and not the other way around. </P>
<B><P>Marcia Thorndike, </B>Retired UFCW Local 101 member and 25-year Safeway worker, from the Internet</P>


