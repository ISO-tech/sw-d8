<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A color-coded system for profiling</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 23, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LAST WEEK, the Department of Homeland Security quietly announced that it will roll out a new "Big Brother" program to attack our civil liberties. Beginning this summer, the Transportation Security Administration will implement the "Computer Assisted Passenger Prevention Screening System" (CAPPS II)--a screening system that will feed air travelers' personal information, flight itineraries and criminal histories into databases to assess the likelihood that they are "terrorists." </P>
<P>Each passenger will be assigned a color code based on the estimated security risk--green, yellow or red. Never mind that CAPPS has an error rate of 3 to 4 percent--affecting an estimated 20 to 25 million passengers per year. </P>
<P>Never mind that CAPPS is a glorified "profiling" system that will certainly target minorities, Muslims and political activists. Washington says that it is necessary to protect us from "terrorists."</P>
<P>As the country celebrates Martin Luther King Day, it's worth remembering that this kind of attack on civil liberties is nothing new. King's home, office and hotel rooms were routinely tapped by the FBI. He was sent anonymous letters taunting and threatening him. </P>
<P>FBI Director J. Edgar Hoover said that King was "an instrument in the hands of subversive forces seeking to undermine our nation." In other words, King was treated like a criminal precisely because he took a stand against racism, poverty and war--and made the connections between a government that attacked the rights of its citizens at home, while waging war abroad.</P>
<P>"I knew," he said in his 1967 speech opposing the Vietnam War, "that I could never again raise my voice against the violence of the oppressed in the ghettos without having first spoken clearly to the greatest purveyor of violence in the world today, my own government."</P>
<P>Ultimately, the stand that King took--and the strength of the social movements of the 1960s--shined a bright light on the FBI's dark dealings and discredited the government's dirty tricks. Today, it's important that activists continue this fight to defend our civil liberties.</P>



