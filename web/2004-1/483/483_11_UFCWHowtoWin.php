<P><font face="Times New Roman, Times, serif" size="4">Debate opens up about direction of grocery strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can the UFCW win?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--How can we win the grocery strike/lockout? That was the central question in a meeting of 80 community activists in LA's Silverlake neighborhood January 15.</P>
<P>The event was organized by Neighbors for Peace and Justice and featured John Grant, an in-house attorney for United Food and Commercial Workers Local 770; Ed Rendon, director of public affairs for Teamsters Joint Council 42; representatives of City Councilman Eric Garcetti; and the LA Alliance for a New Economy. The panelists focused on redoubling solidarity efforts from the community for the three-month-old strike and lockout of 70,000 workers at three grocery chains in Southern California--and keeping Wal-Mart out of LA.</P>
<P>This writer has raised money, organized picket visits and raised support as an activist in United Teachers Los Angeles. Many others in attendance had spent hours on picket lines, leafleting in front of Ralphs stores in lieu of pickets and had raised money to support the United Food and Commercial Workers (UFCW).</P>
<P>Three UFCW members spoke at the opening of the meeting. Although they had been asked not "to say anything negative about the union" by one of the event organizers, they raised criticisms of the handling of the strike.</P>
<P>Almost in tears, one of the locked-out workers said, "We acknowledge that we wouldn't have anything to fight for if it wasn't for our union. But we feel like we're alone out there. There has to be more open communication." Several people questioned the union's decision to pull down picket lines at Kroger Co.'s Ralphs stores last Halloween.</P>
<P>Grant argued that "strikes are different in the U.S. We want to create a situation of imbalance between the employers." He said scab workers were unprepared to handle the increased workload--as if increased business for Ralphs was a sign of a successful union strategy! </P>
<P>When this writer respectfully asked that UFCW be willing to acknowledge a mistaken strategy and move on to shutting down the stores, wide applause came from the audience. The solidarity work of activists like those in attendance is crucial in raising workers morale. But this alone won't win the struggle--nor will keeping Wal-Mart out of LA.</P>
<P>In the meeting, I applauded the Teamsters for the active solidarity they showed during and after Thanksgiving by refusing to deliver goods to stores for month. Rendon called it a "strategic and brief" move in support of the UFCW. More action like this what it will take to shut down these corporate giants. </P>
<P>Even if the Teamsters' month-long pickets were a partial step, but it was a step toward challenging the laws against solidarity strikes in this country. That same day, in Garden Grove, Orange County leaders of the California labor movement such as Art Pulaski of the California Federation of Labor and presidents of UFCW and SEIU locals were arrested for blocking the entrance to a Vons, owned by Safeway Inc. store.</P>
<P>While these orchestrated events get needed media attention, other locals should follow the example of the Santa Monica UFCW Local 1422, which has organized mass pickets with the aim of shutting down stores. </P>
<P>The fight the UFCW is facing now is what almost all workers are facing--attacks on healthcare, pensions and the basic right to have a union at all. Activists in unions and community organizations need to mobilize make the solidarity rally planned for January 31st in Inglewood a massive showing of union strength.</P>



