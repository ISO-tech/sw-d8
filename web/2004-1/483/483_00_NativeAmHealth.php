<P><font face="Times New Roman, Times, serif" size="5"><b>Natives suffer higher rates of disease</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jean Howell</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THIS POPULATION has higher rates of diabetes, cancer, childhood respiratory disease and death from injuries. Experts blame poverty, education levels and poor access to care." That was the conclusion of <I>American Medical News, </I>a newspaper for doctors--about the state of health care for Native Americans. The article cited grim statistics concerning the health of Native American and Alaska Native children--including much higher rates of diabetes, bronchial infections, cancer and deaths caused by injuries or violence.</P>
<P>Access to health care is also poor. Many Native Americans receive health care through the government-run Indian Health Service (IHS)--but the agency itself says that it would need nearly double the funds to provide health services equal to the rest of the country. One of the most disgusting examples of how corporate and military needs have been put ahead of the health of the Native population is the nuclear industry's use of lands occupied by Native Americans.</P>
<P>Native communities have suffered especially from the mining and processing of uranium--and the development and testing of nuclear weapons. "By the time of the mid-1970s, <I>all </I>U.S. uranium came from Indian homelands," says Annie Ross, of the Department of Native American Studies at the University of California-Davis. "Beginning in the mid-20th century, corporations working in the nuclear bomb industry mined Navajo Nation and Pueblo land for uranium. Navajo Nation land is the site of the largest nuclear-related and radioactive accident in U.S. history." </P>
<P>Ross believes that Native communities ended up as victims because they lack political clout. "The most significant factor in determining the siting of hazardous waste facilities, nationwide, was race," Ross said. "Deadly nuclear and chemical trash is deposited by industry and government in communities made up of people of color." This is certainly a factor in higher rates of cancer and other diseases among Native Americans.</P>



