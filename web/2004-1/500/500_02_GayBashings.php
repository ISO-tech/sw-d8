<P><font face="Times New Roman, Times, serif" size="5"><b>Report shows rise in attacks on gays</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHEN POLITICIANS promote homophobia, physical attacks against gays and lesbians increase. This is the conclusion of reports by the National Coalition of Anti-Violence Programs (NCAVP), which showed an alarming increase in gay bashing following last summer's Supreme Court ruling that abolished state laws against gay sex.</P>
<P>The report notes an 8 percent increase in violence against lesbians, gays, bisexuals and the transgendered (LGBT) for the whole of 2003. But the spike in reports of violence happened as the debate on gay marriage has taken shape.</P>
<P>"In the first half of the year, the increase in anti-LGBT violence was 3 percent," said NCAVP Acting Director Clarence Patton. "However, from July to December, that increase had jumped to 26 percent."<B> </B>For the second half of 2003, the NCAVP reported a 43 percent increase in gay bashing in New York, a 133 percent increase in Colorado and a 120 percent increase in Chicago.</P>
<P>Figures are based on incidents reported to organizations providing services to hate crimes victims. Many hate crimes are never reported to police for fear of retaliation in some cases, or because antigay violence is often ignored.</P>
<P>But authors of the report say that part of the reason for the statistical increase may be increased confidence among gays and lesbians to report antigay incidents. A confident movement that challenges homophobia is our best hope for isolating antigay bigots--and giving victims the confidence to speak out and fight back.</P>


