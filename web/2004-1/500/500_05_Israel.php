<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israeli reign of terror in Gaza</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 5</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ISRAEL HAS focused its reign of terror on Gaza. As <I>Socialist Worker </I>went to press, Israeli soldiers had cut off the Rafah refugee camp--the home to 90,000 Palestinians--from the rest of Gaza.</P>
<P>The Israel Defense Force was preparing to "create a new reality"--in the ominous words of Israeli Defense Minister Shaul Mofaz--along Gaza's border with Egypt. Already during the last week, Israeli troops rampaged through Rafah, killing 32 Palestinians and demolishing at least 88 homes, which left more than 1,000 Palestinians homeless.</P>
<P>"We started continuous air strikes," Mofaz told the Israeli cabinet May 16. "We will deepen the fighting." Helicopters pounded other smaller Palestinian refugee camps along the border while troops used tanks and armored personnel carriers to cut off all roads into Rafah.</P>
<P>Even before Mofaz's announcement, Paul McCann, spokesperson for the United Nations Relief and Works Agency, said that Israel's battering had turned Rafah into a "humanitarian catastrophe." McCann condemned Israel's assault on Rafah as an example of "collective punishment" against Palestinian civilians that violated international law.</P>
<P>"The overwhelming majority of the more than 11,000 Palestinians who have lost their homes in Gaza since the start of the Intifada [uprising that began in September 2000] have been guilty of nothing more than being in the wrong place at the wrong time."</P>
<P>Though Israel defended the demolitions and killings as a "legitimate defensive measure," the real reason for the assault was to seek revenge for the killing of 13 Israeli soldiers in three separate incidents during the prior week--Israel's worst military losses since it carried out the Jenin massacre two years ago. Two of the resistance operations destroyed Israeli troop carriers.</P>
<P>"An eight-story apartment block close to where [one] Israeli troop carrier was blown up had been flattened," according to Britain's <I>Guardian </I>newspaper. "Its owner, Salman Hajer, had built the flats to house his family. His crime was to be the owner of the building closest to where the troop carrier had been blown up." As Hajer told the <I>Guardian, </I>"This building was the result of 40 years' work. It was meant to be the home for my sons and their families. They have destroyed my life, but I don't care. The price I paid does not matter. They were punished for a change."</P>
<P>The escalation in Rafah promised by the Israeli military also serves as its answer to an Israeli demonstration of 100,000 in Tel Aviv calling for a withdrawal from Gaza--the largest such demonstration in years. When Israeli Prime Minister Ariel Sharon's conservative Likud Party rejected Sharon's plan to unilaterally withdraw from Gaza, the Israeli "peace" movement took up the demand and organized the protest--exposing how much ground the war criminal Sharon and his "left" opponents share.</P>
<P>Israel's destruction of Rafah is, in fact, part of the withdrawal plan, reinforcing Israel's control of a five-mile strip running between Gaza and Egypt dubbed the "Philadelphi route." The house demolitions are designed to widen this strip, which Israel will retain after its so-called withdrawal, so that Israeli forces can control all movement in and out of Gaza.</P>
<P>Gaza is home to 1.3 million Palestinians and 7,500 Israeli settlers--known for their vile anti-Arab racism, armed terror and religious fanaticism. George W. Bush helped precipitate this crisis by sanctioning Sharon's unilateral withdrawal plan--while simultaneously backing Israel's demands that Palestinians will have to give up large parts of the West Bank as well as their right to return to homes stolen from them by Israel with its founding in 1948.</P>
<P>With Israel promising fresh atrocities and further ethnic cleansing, the U.S. offered up a few words of criticism of its Middle East ally. But until a movement can be built to challenge U.S. backing for Israel, Washington will continue cooperating with Israel to "remake" the Middle East to suit U.S. needs.</P>

