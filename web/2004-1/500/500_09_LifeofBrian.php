<P><font face="Times New Roman, Times, serif" size="4"><I>Life of Brian</I>: An antidote to <I>The Passion</I></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What would Brian do?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Life of Brian, </B></I>directed by Terry Jones, written by and starring Monty Python, including Graham Chapman, John Cleese, Terry Gilliam, Eric Idle and Michael Palin.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"IN VIEW of the furor over Mel Gibson's <I>The Passion of the Christ,</I> we think it important to offer an alternative view from Monty Python and are planning a theatrical re-release (or second coming) of <I>Life of Brian,</I>" announced Monty Python's producer. The 1979 film opened in New York and Los Angeles in April, and if all goes well, will open more generally.</P>
<P>Monty Python were already famous for their British BBC television sketch comedy series and their 1975 film, <I>Monty Python and the Holy Grail,</I> when they came together to make <I>Brian.</I> The first working title was <I>Jesus Christ--Lust for Glory. </I>But they decided that Jesus himself wasn't very funny.</P>
<P>That's when they hit on telling the story of "Brian," a contemporary of Jesus who joins the movement against Roman occupation and then spends the rest of the film trying to convince unwanted followers that he's not the messiah. The Python team did two weeks of research for the film, which included reading the Gospels and watching overblown Hollywood epics like <I>The Greatest Story Ever Told.</P>
</I><P>"In a strange way, we were being very cautious about not being blasphemous," says Terry Gilliam, "by being totally blasphemous but about another guy." The original producers (the British film company EMI) got cold feet after an executive finally read the script. U.S. producers also weren't biting. Ex-Beatle George Harrison, an avid Python fan and friend of Eric Idle, put up his mansion as collateral to fund it.</P>
<P>The Python team felt they were criticizing the uses of religion more than religion itself. "They seem to forget about things like loving one another," said Graham Chapman, who played Brian, of the Christian church, "more interested in joining their own little club and then thinking of other people in terms of--'that lot won't go to heaven, just us.'</P>
<P>"That movie, if it said anything at all, said think for yourselves, don't blindly follow, which I think isn't a bad message and I'm sure Mr. Christ would have agreed." In a hilarious scene in front of an adoring crowd, for example, Brian implores his followers to think for themselves--to which they express, in unison, complete agreement.</P>
<P>The movie has too many funny scenes to recount, but here are a few:</P>
<P>At the Sermon on the Mount:</P>
<P>"What was that?"</P>
<P>"I think it was 'Blessed are the cheesemakers.'"</P>
<P>"Ah, what's so special about the cheesemakers?"</P>
<P>"Well, obviously, this is not meant to be taken literally. It refers to any manufacturers of dairy products."</P>
<P>True to its message of individualism, the film is as irreverent toward the anti-Roman "left" as it is toward the Romans. The latter are portrayed as bored guards and lisping buffoons, the former as cowards, full of talk and more willing to fight each other than the Romans.</P>
<P>"They've bled us white...And what have they [the Romans] ever given us in return?" asks Reg, leader of the "People's Front of Judea" (played by John Cleese). To which the other activists chime in: "The aqueduct?" "Sanitation." "The Roads." "Irrigation." "Medicine." "Education." "Pubic Baths." And, finally, "Peace." The scene is very funny, but on a second viewing I couldn't help thinking that it wouldn't make a Donald Rumsfeld squirm like a <I>Dr. Strangelove</I> or a <I>Duck Soup</I> might.</P>
<P>The film opened to protests, especially in the U.S. According to Idle, some Christian groups protested "because they realized that it was actually poking fun at them and rightly so, because it was...poking fun at people who set up as being authorities and speak for God." The film finishes with a big finale, sung by Brian and the other men hanging on their crosses, "Always Look on the Bright Side of Life"--a perfect antidote to Gibson's overblown, blood-drenched crucifixion scene.</P>
<P>A good comedy can convey a serious point in a way drama sometimes cannot. In the final scene of <I>Dr. Strangelove,</I> Slim Pickens rides a nuclear bomb to oblivion, followed by shots of a billowing mushroom cloud, all to the song, "We'll Meet Again." Chapman's partner, David Sherlock, tells a story of how a friend of his took her parents, Holocaust survivors, to a showing of <I>Brian,</I> and during the final scene her mother whispered, "My God, the singing, it's what we did when somebody left the camp."</P>
<P>In an era and in a country where spoofing religion seems completely off-limits, <I>Brian</I> is positively radical, and a perfect answer to <I>The Passion</I>.</P>


