<P><font face="Times New Roman, Times, serif" size="4">Union fails to mobilize for contract in Seattle</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why won't UFCW fight?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--With a 72-hour notice for a strike or a lockout, contract negotiations covering grocery workers in the Pacific Northwest continued as union leaders went through the motions of a strike preparation campaign. </P>
<P>Washington State Jobs with Justice and Washington Citizens Action held a "Rally for Affordable Health Care" May 12 to support 16,000 members of the United Food and Commercial Workers (UFCW), which is currently in negotiations with Kroger Co., Safeway Inc. and Albertsons Inc. Another 9,000 UFCW members in smaller stores will automatically receive whatever contract is ratified. </P>
<P>Around 125 people--fewer than 20 of them rank-and-file grocery workers--attended the May 12 rally. As Andy Heyman, a meatcutter at Fred Meyer, which is owned by Kroger Co., and a member of UFCW Local 81, told <I>Socialist Worker</I>, "Union officials from Local 1105 came into my store two days after the rally and handed out flyers that told us to save the date for the rally and one other event that had already taken place!" </P>
<P>Despite the low turnout at the rally, the Seattle police arrested Robby Stern, legislative director of the Washington State Labor Council on misdemeanor charges of obstruction for leading the crowd in a chant of "power to the people." About 30 supporters marched to the nearby police station and picketed for three hours until Stern was released. </P>
<P>While Stern's arrest and release gained media attention, contract negotiations continue to be a one-sided affair. According to Local 1105's contract hotline, the union's consultants are seeking "alternative cost saving methods for the health care plan". The companies are demanding health care concessions that would total over $550 million.</P>
<P>Wages would be frozen over the three year contract and overtime, Sunday and holiday pay would be cut or lowered. The companies also want a two-tier contract that will lower wages, pension, health care, vacation and holidays for new hires. </P>
<P>As <I>Socialist Worker </I>went to press the final two days of negotiations are set for May 19 and 20. As Heyman commented, "We have no idea where the negotiations are at. We haven't seen much in the way of facts from the union. We're just waiting for the next round of negotiations. </P>
<P>"I think we'll see the unions attempt to sell a settlement that isn't as bad as the Southern California one. It will contain health care concessions. It will bring in a two-tier system. They'll find a way to placate the long-term employees. But new workers will get screwed. There will be no reason to start a new career in this business. I know I'll be telling all my co-workers to vote no on whatever contract gets presented to us." </P>
<P>Every member possible needs to continue to circulate the flyers and petitions that call for no concessions and no two-tier contract. The members should demand at least a week to look over any final contract proposal before a vote is taken. If the union officials aren't willing to fight, it will be up to the members to take on the companies. </P>


