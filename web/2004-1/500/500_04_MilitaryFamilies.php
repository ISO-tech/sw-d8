<P><font face="Times New Roman, Times, serif" size="5"><b>Military families say "Bring them home now!"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
On Saturday May 1, the new San Diego Chapter of Military Families Speak Out joined other organizations in a protest against the occupation of Iraq and to call for a return of U.S. troops. The protest was held in Oceanside, Calif., the location of the Camp Pendleton Marine Base, which has provided most of the ground forces in the recent assault on Falluja.</P>
<P>It was held to call attention to the growing death toll--both Iraqi and American--that is a result of the Bush administration's quest for empire. Along with the untold thousands of Iraqis, at least 73 marines from Pendleton have been killed in the fighting in recent months.</P>
<P>About 100 demonstrators, including many veterans, straddled a busy intersection frequented by Marines and their families going to the beach. Protesters carried signs and chanted, while two large banners were on display to oncoming traffic. One was a tapestry bearing a cross and the name of each soldier killed in Iraq, and the other was a mural of over 400 pictures of those killed.</P>
<P>Rosa Gonzalez, whose son was killed in Iraq, moved many to tears with her story. "My son will not be returning because of this terrible war," she said. Her voice then moved from sobs to confident defiance. "I will not give up protesting this war, now that my son is gone," she said. "I want to fight even harder to stop it so that others will not have to go through what I have."</P>
<P>Another emotional moment occurred when a woman passing by stopped her car and stared momentarily at the protest signs. After looking over the faces of the fallen soldiers, she turned to a group of us and told us that her husband, a Marine in Afghanistan, was killed only days before. </P>
<P>Through tears, she told us, "my husband won't be coming home." She then parked her car and joined with us in protest.</P>
<P>Our demonstration received a lot of attention from Marines--some negative, but this was largely overshadowed by the constant honking of horns in support and the occasional crewcut flashing us the peace sign. Military Families Speak Out will be calling for a day of action June 26 to call attention to the lies of George Bush and the phony "handover of power," and to demand an end to the occupation and to bring the troops home now!<BR>
<B>Justin Akers, </B>San Diego</P>

