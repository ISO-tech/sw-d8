<P><font face="Times New Roman, Times, serif" size="5"><b>Immigrant detainees beaten in N.J. prisons</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Rafael Greenblatt</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 2</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">REPORTS FROM the jails of New Jersey are showing once again that torture doesn't happen only in Abu Ghraib or Guant&aacute;namo Bay. According to the New Jersey Civil Rights Defense Committee (NJCRDC), in February, a group of guards at the Passaic County Jail--where immigrant detainees are held--forced a Dominican man to strip naked and then beat him.</P>
<P>The guards then took the main to a cell, handcuffed him, beat him again and sicced a dog on him. After all this, the man--who is behind bars for nothing more than an immigration violation--was denied medical treatment twice. After he was finally taken to a local hospital, he was thrown into solitary confinement.</P>
<P>Three weeks later, at the beginning of March, two immigrant detainees held in the Hudson County Correctional Center were beaten by guards in the full view of their cellblock. One victim suffered three broken teeth--and was taken to another jail 30 miles away and back before receiving medical attention.</P>
<P>In the months since these incidents, neither one--and these are only two of the most shocking stories of brutality at immigrant detention facilities in New Jersey--has been reported in the mainstream press. Probably no one outside of the prisons would know about them if not for the efforts of immigrant solidarity groups, who have built up connections with detainees in the course of their struggle against arbitrary detentions.</P>
<P>NJCRDC is circulating a statement and preparing to organize public events to link the abuses in New Jersey jails to the torture in Abu Ghraib. As Jeannette Gabriel of NJCRDC put it, "The only way to stop the abuse of detainees is to free the detainees. As long as people are being held in violation of the Constitution they can be subjected to violence."</P>


