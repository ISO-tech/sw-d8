<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">White House whips up racism to shift attention from Iraq torture</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The real barbarians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHIPPING UP racism and hatred of Iraqis is the White House's last refuge as the Iraq torture scandal grows and George W. Bush's approval ratings drop.</P>
<P>People around the world were horrified by the videotape showing the decapitation of U.S. businessman Nicholas Berg, apparently by a leader of al-Qaeda in Iraq. But among the Washington political establishment, it's no exaggeration to say that Berg's murder came as a relief--because it shifted attention from the spreading scandal surrounding the torture of Iraqi detainees in Abu Ghraib prison.</P>
<P>Right-wing blowhard Rush Limbaugh declared that Berg's decapitation showed that "[t]hey're the ones who are sick. They're the ones who are perverted. They are the ones who are dangerous. They are the ones who are subhuman. They are the ones who are human debris, not the United States of America and not our soldiers and not our prison guards."</P>
<P>To White House operatives--skilled at manipulating tragedy after using the September 11 to launch two wars--Berg's death was a golden opportunity. "Even though there was widespread revulsion to the prison abuse, the moment they see this barbaric decapitation, they say, 'Yep, that's why we're there. This is what we're up against,'" a Republican strategist told the <I>Washington Times.</P>
</I><P>Speaking on NBC's <I>Chris Matthews Show, </I>Republican commentator Peggy Noonan even called the Berg video "something of an antidote" to the furor surrounding the Abu Ghraib torture. For his part, Bush promised to "stay on the offensive until these killers are defeated," and Secretary of State Colin Powell--the administration's supposed dove--issued a veiled threat to "the Arab world" to show "more outrage" at Berg's murder.</P>
<P>Actually, the Bush administration bears a great degree of responsibility for Berg's death. Berg was detained by U.S. authorities in Iraq, according to an e-mail that his family released after the Pentagon tried to pin the blame on Iraqi police.</P>
<P>FBI agents grilled Berg at least three times during his 13 days behind bars, according to press reports--forcing Berg to miss a flight back to the U.S. "My son died for the sins of George Bush and Donald Rumsfeld," said Nick Berg's father, Michael. "This administration did this." </P>
<P>With the claim of Iraqi weapons of mass destruction discredited and the idea that the U.S. is "liberating" anyone wiped out by the torture scandal, Washington is using Berg's death to put forward the only justification left for occupation: Iraqis are barbarians who are unfit to rule themselves and whose lives aren't worth much.</P>
<P>"If civilian deaths are not recorded, let alone published, it must be because they do not matter, and if they do not matter, it must be because the Iraqis are beneath notice," author Luc Sante wrote in the <I>New York Times</I>. "And that must mean that anything done to them is permissible, as long as it does not create publicity that would embarrass the Bush administration."</P>
<P>Even some people who opposed to the invasion of Iraq believe that the U.S. has the responsibility to repair the damage they've done by maintaining an occupation--to prevent the kind of people who killed Berg from taking power. But Berg's death is being cynically used by the White House to try to justify U.S. atrocities in Iraq on an even greater scale.</P>
<P>No reformed system of occupation--not better rules for imprisoning Iraqi detainees, nor different orders for U.S. troops on patrol in Iraqi cities, nor troops from the United Nations or NATO--will do away with the fundamental injustice of one country conquering another and dictating the fate of its people. Iraqis deserve the right to determine their own future--and the resistance will continue until they do.</P>
<P>A struggle that loosens Washington's grip on the Middle East will save countless lives and advance the cause of a more peaceful and just world. That's why it's right to resist the U.S. in Iraq--and right to support all struggles against oppression.</P>


