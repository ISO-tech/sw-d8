<P><font face="Times New Roman, Times, serif" size="4">Confronting a war criminal as he spouts his lies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fire Donald Rumsfeld!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
Donald Rumsfeld may be the most powerful orchestrator of mass violence on the planet, but that doesn't mean you can't tell him to shut up. On May 7, a handful of antiwar activists here in Washington, D.C., disrupted the first session of the defense secretary's congressional testimony about torture at Abu Ghraib prison, splashing the message of "Fire Rumsfeld" and "War criminal!" and "End the occupation now!" across TV screens and newspapers throughout the country. </P>
<P>The last-minute protest was pulled together by Code Pink Women for Peace, and included activists from the International Socialist Organization and the D.C. Antiwar Network. We called out from work, put some nice clothes on over our antiwar T-shirts, tucked a small "Fire Rumsfeld" banner under a jacket and stood in line with all the unsuspecting Capitol Hill interns to get a seat for the open hearing. </P>
<P>Once inside, we waited for the proceedings to begin, trying to stay calm and finalizing our plan. When Rummy and his men walked in, the sound of the camera shutters was like a force of nature. Then the hearing began--a sick stage play scripted around explaining away the horror that no U.S. civilians were supposed to know about. </P>
<P>Then Rumsfeld started talking--and we stood up and let it rip. As someone who's done his fair share of insulting Rumsfeld's conceited smirk when he's appeared on the TV screen, I can certify that it's much more satisfying to do so in person. </P>
<P>And while we could have been arrested and fined or jailed for the disruption, the idea of creating a "Rumsfeld Seven" at the embattled secretary's hearing was too much for the Bush administration to handle. So instead, the U.S. Capitol Police showed us the door and told us to have a nice afternoon. </P>
<P>For the first time, an opinion poll recently found a majority of Americans feel that the war in Iraq was a mistake. Millions of people have seen the reality of Abu Ghraib, and the pictures and names of the hundreds of young U.S. soldiers whose lives were destroyed to make it possible. Hundreds of thousands know them, know someone who's there or someone who's about to go. </P>
<P>As the candidates of the two war parties travel the country on the campaign trail, we shouldn't miss an opportunity to show up, shut them up and tell them to end the occupation and bring the troops home now.<BR>
<B>Ben Dalbey, </B>Washington, D.C.</P>

