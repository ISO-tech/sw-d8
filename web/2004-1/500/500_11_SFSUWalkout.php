<P><font face="Times New Roman, Times, serif" size="4">1,500 students sweep through campus</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>SFSU walks out against budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Taliya Cohen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--About 1,500 students at San Francisco State University (SFSU) walked out May 12 to protest harsh budget cuts hitting the California State University system. The day began at the center of campus with a rally in Malcolm X plaza, which became an emotionally charged march through campus.</P>
<P>As marchers went through classroom buildings, many students saw the protest and left their classes to join it. After rallying in front of the administration building and listening to speeches from organizers, the students took over the main intersection next to campus. </P>
<P>Within 15 minutes, organizers made the decision to leave after police threatened to arrest anyone still in the intersection after 12 noon. The protest ended on a strong note as a mass of students returned to Malcolm X Plaza with their fists raised in the air, savoring the taste of their power as a united force. </P>
<P>After overcoming many difficulties and disagreements during the planning process, the demonstration was an enormous success that can now inspire other students to get involved--and put real pressure on school administrators as well as state officials like Gov. Arnold Schwarzenegger. Especially moving was the participation of all of the affected departments, including dance, social work and others, as well as the democratic, bottom-up planning that went into organizing the walkout. </P>
<P>Students chanted "Tax the rich," and some called for cutting administrators' salaries, both of which acknowledge the injustice of a system that denies a decent and free education to all working-class people. An assessment meeting is planned for May 20 so that we can all learn from one of the largest walkouts in recent history and seize the opportunity to give a huge boost to organizing on campus.</P>


