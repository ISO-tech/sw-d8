<p><a name="Key"></a><font face="Times New Roman, Times, serif" size="4"><b>Struggle is the key to change</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I disagree with Bill Anderson's (<I>SW,</I> May 7) assertion that "voting is the most fundamental exercise of working-class solidarity." He takes voting, as a tool of democracy, completely out of any context and then criticizes <I>Socialist Worker</I> for being "corporatist" and having an "anti-voting" agenda.</P>
<P>In the sense that Anderson means, voting in the U.S. for public officials has been the <I>least </I>effective mode of solidarity and struggle. Don't take it from me, look at history. </P>
<P>Voting for Woodrow Wilson didn't keep us out of the First World War, voting for John F. Kennedy didn't get us civil rights, and voting for Lyndon Johnson didn't pull us out of Vietnam. Note that they were all Democrats.</P>
<P>Struggle won those things, and struggle will always win the things that matter to working people. <I>Socialist Worker </I>stands in the tradition that looks to break this vicious cycle of voting for this or that candidate in order to change society for us.  </P>
<P>We argue that ordinary people can change society. These are hardly "un-leftist" or "corporatist" principles. In fact, they are the core of what remains of a left in the U.S. and the foundation from which an effective and fighting left will stand on and win.<BR>
<B>Brian L., </B>Rochester, N.Y.</P>

