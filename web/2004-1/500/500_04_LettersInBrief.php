<P><font face="Times New Roman, Times, serif" size="4"><b>Don't let Davis honor racism</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
A six-year-old controversy has been revived in Davis, Calif. It is about a street named "Sutter Place" after John Sutter, one of the supposed founders of modern Northern California. </P>
<P>It has come to light that Sutter kept Native Americans as slaves and was a sexual predator of Native American children. Often in financial trouble, he would pay off his debts by giving away young Indian children. </P>
<P>The conditions under which he kept his slaves were horrible. He fed them in troughs as if they were animals. He kept young girls in a special anteroom adjoining his office. There is evidence that he even killed one of the children. </P>
<P>The recent controversy was kicked off when American Indian activist Steven Jerome-Wyatt declared at a Davis city council meeting that he was going on a hunger strike in protest of the street name. At a following Davis city council meeting, University of California (UC)-Davis student activists from the Native American Student Union, the International Socialist Organization, MEChA, and the lesbian, gay, bisexual and transgender community organized to demand that this be made an agenda point for the city council to take up. </P>
<P>Students from all of these groups spoke to the racist nature of honoring John Sutter with this sign. Soon after, the UC-Davis student government passed a resolution calling for a name change.</P>
<P>On May 4 one of the city council members put forward a resolution to change the sign name. At the city council meeting, community members spoke out against the street name, calling it a racist street sign that is hurtful to Native Americans. Only a handful of people spoke against changing the sign--including business people from the community. </P>
<P>But after all was said and done, the resolution failed three to two, with the lame excuse being the cost to Sutter Hospital for changing their stationary and business cards. This was just an excuse, since the resolution was calling for the change to take place after the current stationary was already used up. Community activists are determined not to let this issue go until the street name is changed. <BR>
<B>John Howell, </B>Davis, Calif.</P>

