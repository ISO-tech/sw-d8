<p><a name="Sick"></a><font face="Times New Roman, Times, serif" size="4"><b>Laid off because of their sick priorities</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I am being laid off from my job at the University of Washington due to "lack of funds" and "lack of work."  This, at a university that just hired a new president who will be making $470,000 a year--along with bonuses and "golden handcuffs" which he will receive if he stays a certain number of years.</P>
<P>There is a clear lack of priorities in our system to provide decent wages and working conditions and affordable education for all. The money being spent on tax breaks for corporations, the war on terrorism and the war on and occupation of Iraq have to be opposed by building mass movements in the streets to fight the messed-up priorities of the system.</P>
<P>There is also a possibility that, due to the fact that I am a vocal union activist and shop steward, this is being used as an excuse to force me out of the department I work in. These attacks on our wages, working conditions and rights have to stop. We must rebuild rank-and-file militancy and a socialist presence in our unions.<br>
<B>Patrick Switzer, </B>Seattle</P>


