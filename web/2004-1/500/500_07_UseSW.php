<P><font face="Times New Roman, Times, serif" size="4">How we use <I>Socialist Worker</I></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Connecting to what we can do</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"> May 21, 2004 | Page 7</font>

<B><P><font face="Times New Roman, Times, serif" size="3">AMY MULDOON is an ISO member in New York City and a technician at Verizon.</B></P>

<P>I work for Verizon and am active in the Communication Workers of America. I sell <I>Socialist Worker </I>to a handful of coworkers every week, and it has been the best tool I can imagine to help people see beyond the common garbage that the mainstream media puts out. </P>
<P>Besides being a great source of "alternative" information, I use the paper to draw out why the things we experience are the way they are: It's not just that our managers are scumbags, (which they are) but there is pressure in the telecommunications market to raise productivity and maximize profits. </P>
<P>And being able to write for the paper means shows my coworkers that there is a forum for our issues that other working people can read and respond to. This was helpful, especially when I was part of building a reform movement in our local, in showing that we are not alone. </P>
<P>Millions of people are disgusted and want change, but our "leaders" and "representatives" aren't offering any real options. Within the union, the labor pages give me ammunition to show how we can fight the bosses and rebuild our union power. </P>
<P>Lately, the coverage of John Kerry has been invaluable to confirm the gut distrust and disappointment so many people feel. <I>Socialist Worker </I>is not just something we read--it connects us to what we can do.</P>

<B><P>ADRIENNE JOHNSTONE is an ISO member in San Francisco and a 3rd grade teacher at Carver Elementary School.</B></P>

<P>HOW DO I use <I>Socialist Worker </I>at my job? If you come to my classroom, you will see the "Bring the Troops Home NOW!" poster outside of my door. It's been there all year and has brought coworkers and parents into my classroom to talk about politics and ask questions about what is going on with the war. It has put me out there as an activist and open socialist at my workplace. </P>
<P>I sell the paper in sporadic fits and starts to my coworkers, mostly around issues of the war, but also around the case of Kevin Cooper--a San Quentin inmate on death row whose execution was stayed this spring due to pressure from anti-death penalty activists in the Campaign to End the Death Penalty, the ISO and others.</P>
<P>People know that <I>Socialist Worker </I>doesn't claim some false "objective news coverage." They appreciate that our paper has an analysis and takes a stand for working people on the issues they care about.</P>
<P>Following the coup in Haiti, I approached a coworker who used to live there and offered her a paper so she could check out what socialists have to say about the role of the United States, the World Bank and the IMF in recent Haitian history. </P>
<P>She is now a regular reader of the paper and bought the most recent <I>International Socialist Review. </I>She also attended our regional conference this spring and spent a good deal of time at our literature table. She has become a more serious and dedicated union member at our school and supports the work of socialists in struggles around the war and gay marriage.</P>


