<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>International Brotherhood of Teamsters</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Allen, shop steward, Teamsters Local 705</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--The bombshell resignation of Ed Stier, chief of the Teamsters' anti-corruption RISE program reveals once again the deep-seated corruption in the Teamsters union. Stier, appointed by Teamsters President James Hoffa, resigned in late April along with his entire staff of 20 lawyers and investigators.</P>
<P>In his resignation letter, Stier declared: "In spite of our efforts to convince General President Jim Hoffa to remain committed to fighting corruption...I have concluded that he has backed away from the Teamsters' anticorruption plan in the face of pressure from self-interested individuals."</P>
<P>The <I>New York Times </I>reported, "Mr. Stier also asserted that Mr. Hoffa and several people around him were trying to shut down a wide-ranging investigation in to charges that various Teamster members and officials in Chicago were associates of organized crime, and that some Teamster officials had participated in a deal in which a mob-run company was allowed to use nonunion workers to replace union workers in construction and convention jobs."</P>
<P>James Kossler, Stier's chief investigator, declared in his resignation letter: "Because of this most recent situation where they've stopped the investigation we were pursuing in Chicago, I just don't want to be a party to it." According to the <I>Chicago Sun-Times, </I>"Hoffa's administration blocked his investigators from putting Mayor [Richard] Daley's scandal-plagued Hired Truck Program and other possible union-related corruption under a microscope." </P>
<P>For Chicago Teamsters, this is a familiar story. Just a few years ago, Dane Passo, former special assistant to Hoffa, and Bill Hogan, former international representative, Hoffa running mate and president of Chicago Joint Council 25, were expelled and permanently barred from membership in the Teamsters for corruption and ties to organized crime.</P>
<P>Teamsters can no longer stand to be in a situation where the leader of our union protects his cronies who are preying upon us. Hoffa promised to fight corruption in our union--but he is the major barrier to getting rid of it. This can only lead to one conclusion: that General President James P. Hoffa should resign from his office and membership in the union.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Save Fircrest School</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--About 300 supporters of Fircrest School rallied May 15 because the Department of Social and Health Services wants to close it. The State wants to make money by selling this state-run school and residential facility for physically or developmentally disabled people to developers who want the land.</P>
<P>The social service cuts are in part caused by recent tax breaks to big business--including $3 billion to Boeing alone--and a regressive tax structure, which doesn't raise enough money to fully fund social services. If the state plan goes through, some of the residents would be shipped to another state facility, Buckley, several hours south. This would separate residents from their families and force workers to move or find other jobs while some residents would be forced to live on their own.</P>
<P>The closure of Fircrest is part of a pattern of state service cut backs. "My adult, developmentally-disabled son gets SSI, which is just enough to pay rent and utilities," said one mother at the protest. "His food stamps are only $35 a week. What is he supposed to do if he wants to buy a coat or cleaning supplies? I asked the social worker, 'What if the food stamps are not enough?' She replied, 'I guess he can line up at a food bank.'</P>
<P>So this is Washington State's policy for the disabled--rely on food banks! One recent Fircrest resident was forced to move to an apartment too small for his wheelchair, so now he must drag himself around on the floor.</P>
<P>After rallying at an elementary school, the demonstration marched to Fircrest School for a rally addressed by AFSCME national president Gerald McEntee. The rally was also supported by several other unions including the Service Employees International Union and the United Food and Commercial Workers.</P>


