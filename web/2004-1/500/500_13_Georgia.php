<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GEORGIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's ally tightens his grip</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Phil Gasper</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE U.S.-backed president of the former USSR republic of Georgia, Mikhail Saakashvili, strengthened his power by reestablishing control over the previously autonomous province of Ajaria.</P>
<P>For 12 years, Ajaria was ruled with an iron hand by Aslan Abashidze. Abashidze used revenues from the port of Batumi to enrich himself and his family at the expense of the population. After blowing up three of the four bridges that connect Ajaria with the rest of Georgia, Abashidze fled into exile on May 5--after his Russian allies said they couldn't support him in a military showdown with the Georgian army.</P>
<P>Most Ajaris are happy to see Abashidze go, but his departure is hardly a victory for democracy.</P>
<P>Saakashvili came to power last November, when demonstrations drove former Georgian President Edward Shevardnadze from office. Shevardnadze ran a corrupt and brutal regime, but for most of his presidency, the U.S. supported him to increase its influence in the oil and gas-rich Caspian Sea region. When Shevardnadze began tilting towards Moscow, Russia, Washington sent money and help to his opponents--leading many to call November's "rose revolution" a U.S.-orchestrated coup.</P>
<P>Saakashvili has titled Georgia closer to Washington again, but domestically, his rise to power has changed little. An admirer of Joseph Stalin, Saakashvili was Shevardnadze's minister of justice. His prime minister, Zurab Zhvania, held the same position under Shevarnadze. </P>
<P>With Russia and the U.S. both seeking to control Caspian oil--and with a pipeline from Baku (in neighboring Azerbaijan) to Batumi under construction--Georgia has become a pawn in a global game of power politics. So long as Saakashvili plays ball with the U.S., Washington will ignore his abuses of power--just as it ignored Shevardnadze's abuses when he was an ally.</P>

