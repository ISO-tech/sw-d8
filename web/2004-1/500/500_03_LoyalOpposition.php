<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Kerry's loyal "opposition" to Bush</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DOES THE Bush administration have to implode completely before John Kerry moves ahead in the polls? The failure of the presumed Democratic presidential nominee to emerge as an alternative to George W. Bush has party leaders scrambling to explain his awful campaign.</P>
<P>They say that the fact that Kerry is only neck-and-neck with Bush today is nothing to worry about, since Bill Clinton and Ronald Reagan were in the same position at similar points in their presidential campaigns and went on to win the White House.</P>
<P>Yet given Bush's catastrophe in Iraq, Kerry should be far ahead. Instead, he is actively shoring up Bush's position by promising to send more troops to Iraq--and refusing to "cut and run."</P>
<P>Kerry is still courting the pro-Bush camp--even after a Pew Research Center poll found that 51 percent of respondents believe the Iraq war isn't going well. This reflects the strategy of the Democratic Party over the past 20 years--take the unions, African Americans, liberals and the left for granted while trying to win over conservative "swing voters." But Kerry's rightward tilt goes beyond electoral calculations.</P>
<P>As a 20-year senator, he's just as dedicated to the interests of U.S. imperialism as George W. Bush--and shares the establishment consensus that Iraq is far too important to lose. That's why Kerry doesn't so much oppose Bush's strategy as promise to implement it better, with more troops and greater international involvement--steps that the Bush White House has already promised to take.</P>
<P>The dynamic is similar on domestic issues. By vowing to cap increases in federal spending other than national security and education to the rate of inflation, Kerry would lock in the inequities of the Bush budgets and require huge cuts in social programs--Social Security or Medicare, for example.</P>
<P>Kerry's election platform boils down to this: more troops and more money for an imperial war in Iraq, and budget cuts to please Wall Street. Hardly inspiring stuff--unless you're the Pentagon brass or an investment banker. </P>
<P>Despite Kerry's incompetence, there's a chance that Bush could still be driven from office as the outrage against him grows. But if Bush slips back into the White House next January, it's because Kerry opened the door.</P>


