<P><font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 21, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>University of California-Berkeley</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Michael Smith</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH THE number of minority students admitted to the University of California-Berkeley plummeting, students have formed a new coalition to demand more diversity on campus. United Students for Diversity (USD) was formed in response to admissions data released last month that show a 29.2 percent drop in African American admissions, a 7.3 percent decline in Latino students admitted and a 21.6 percent in Native Americans admissions--<I>in one year</I>.</P>
<P>This is on top of the decline in minority student enrollment at Berkeley since the University of California Board of Regents banned affirmative action programs in 1995. "As a Black student on this campus, it really feels like they don't want us here," said James Drake, a 23-year-old Berkeley senior and activist for affirmative action. "I know there are qualified Black students out there who could go to this university."</P>
<P>On May 11, the new coalition held a rally on campus to highlight the increasing segregation of the university--and to call on administrators to take decisive steps to reverse the trend. More than 150 people--most of them minority students--chanted and got hundreds of passersby to sign a petition against the drop in minority admissions.</P>
<P>The decline is like the result of steep budget cuts in the university's minority outreach programs and the nearly 40 percent tuition hike which prices Berkeley out of the reach of students from low-income families. The number of admitted students who qualified for federal Pell grants--which are given to students with an average family income of $35,000 to $40,000--dropped 10 percent.</P>
<P>"I'm ashamed to promote this institution as a place that promotes equal access to all communities," said UC-Berkeley junior Daniel Goldtooth, a coordinator of the Native American Recruitment and Retention Center. "I can no longer tell a Native American student to come to Cal...I can no longer do a disservice to my community by telling them to come to a campus that does not support them."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Amanda Maystead</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Activists and residents organized a press conference in the Bayview District to publicize their anger and disgust at the latest rampage of the San Francisco Police Department (SFPD). In the weeks following the shooting death of Officer Isaac Espinoza, the SFPD has launched a retaliatory offensive against the poor and predominantly Black area where he was killed. </P>
<P>First, there was the murder of an unarmed, disabled Black man who was trying to surrender to police. Then, the SFPD and California Highway Patrol (CHP) embarked on a three-day joint effort to use a patrol plane, helicopter and several dozen squad cars to hunt for "traffic violations," which was essentially a pretext for harassment and brutality at the whim of the cops.</P>
<P>Meanwhile, State Attorney General Bill Lockeyer is chomping at the bit to override San Francisco District Attorney Kamala Harris, who decided against seeking the death penalty for 21-year-old David Hill, a suspect being held in the shooting of Officer Espinoza.</P>
<P>The air was thick with anger and emotion as people spoke out during the press conference. One woman passed around photos of her injuries and described how police dislocated her shoulder though she had done nothing to provoke them. </P>
<P>Mesha Irizarry, co-founder of the Idriss Stelley Foundation (which called the press conference), spoke of how the police shot and killed her son in a movie theater although he was clearly mentally disturbed at the time. Others reported feeling humiliated and under siege as the SFPD and CHP hovered over their neighborhood and ran checkpoints all day. </P>
<P>There were many different opinions about how to fight back, ranging from pushing for reforms in the SFPD to stopping the gentrification that aims to throw poor people out of their homes. But everyone agreed that the last thing that we need in Bayview is more cops--we need money for schools and for jobs!</P>


