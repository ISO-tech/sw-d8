<P><font face="Times New Roman, Times, serif" size="4">Senate passes $170 billion in new business tax breaks</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will their corporate handouts ever stop?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 21, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CORPORATE AMERICA got more than a little help from its friends in Washington--yet again. Last week, the Senate voted by a 92-to-5 margin to approve legislation that will grant a whopping $170 billion in new corporate tax breaks over the next 10 years.</P>
<P>The justification for the massive giveaway, according to the Senate and White House, is that the bill will close down a number of corporate tax shelters. Most prominent was a $5 billion annual tax break for U.S. exporters--which the World Trade Organization had already declared illegal, opening the way for the European Union to begin levying punitive tariffs.</P>
<P>But closing this loophole was the excuse for opening up dozens of others. "The measure that passed was a 900-page behemoth that offered something for almost every business interest--Rust Belt manufacturers, pharmaceutical companies, oil and gas producers, railroads and cruise-ship operators, timber companies, Oldsmobile dealers and even owners of Nascar automobile race tracks," the <I>New York Times</I> reported.</P>
<P>According to Keith Ashdown, vice president of Taxpayers for Common Sense, "Some of the highlights in the bill include: breaks for big energy, ranchers, Oldsmobile dealers, horses, horse racing, energy derived from chicken poop, Hollywood, rail, shipbuilders, small planes and jets, the timber industry and the liquor industry...Instead of just simply fixing the $5 billion illegal export subsidy, lawmakers have lined this bill with parochial tax pork that does nothing but bust the budget."</P>
<P>The "tax pork" includes such boondoggles as: a provision that allows the cruise ship industry a one-year delay in paying taxes on airplane tickets, hotels and other excursions that it sells in the U.S.; a tax credit for natural gas production in Alaska; $14 billion worth of tax breaks for every special energy interest in the country, including oil, gas, nuclear and utilities; and $92 million in tax breaks to allow NASCAR track owners to write off their grandstand facilities.</P>
<P>Even more disgusting, Senate Democrats had the opportunity to add an amendment to the bill that would have extended unemployment benefits for 1.5 million workers who have used up their benefits since December 31. But they blew it.</P>
<P>Extending the benefits would have cost just $5.8 billion--next to nothing compared to the massive giveaway to corporations. But in the end, the amendment fell one vote short of the 60 needed to pass.</P>
<P>That one vote could have been cast by Sen. John Kerry (D-Mass.)--but he was too busy on the campaign trail to come back to Washington. Kerry says that the vote on the amendment was orchestrated by Republicans to make him look bad. This from the man whose proposal for creating more jobs is...you guessed, more tax breaks for Corporate America.</P>


