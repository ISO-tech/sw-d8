<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">U.S. capture of Saddam Hussein is about anything <I>but</I> justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Put Washington on trial</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 2, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ISN'T THE world better off with Saddam Hussein behind bars? That's the claim of the Bush administration, as it hopes that video footage of Saddam Hussein in the grip of U.S. soldiers will make people forget about last May's "Mission Accomplished" banner behind George W. Bush as he posed for pictures on an aircraft carrier.</P>
<P>Maybe the weapons of mass destruction haven't been found, the argument goes. But at least a murderous dictator is on ice and will face trial for his crimes. Pro-war politicians and media hacks, forced to keep their heads down in recent months as the U.S. occupation of Iraq faced mounting problems, crowed long and loud.</P>
<P>They claim that Saddam Hussein's fall was the key factor in the decision of Muammar el-Qaddafi of Libya--another supposed "rogue state"--to give up his weapons programs and allow international inspections. </P>
<P>Yet the cheers for Saddam's capture came not only from Bush's bunch, but from those opposed to the war as well. "No matter what you think about the war, the world is better off with Saddam locked away in prison, reduced to a scared, ragged old man, utterly alone," wrote Joan Ryan in the <I>San Francisco Chronicle</I> in an article headlined "Capture Doesn't Justify Invasion."</P>
<P>There's a huge contradiction for opponents of war in that headline. Supporting Saddam's capture inevitably means endorsing his captors and the means they used to capture him--ultimately, the U.S. government's claimed "right" to intervene militarily wherever and whenever it sees fit. This reduces opposition to the U.S. invasion and occupation of Iraq to tactical considerations--like the criticisms raised by Democratic presidential contender Howard Dean, for example--rather than a principled objection to Washington's use of military force to impose its will on the world.</P>
<P>No doubt the pro-war camp will claim that anyone who refuses to welcome Saddam's capture must sympathize with brutal and sadistic regimes. But the left-wing organizations and activists of the antiwar movement opposed Saddam Hussein back in the 1980s--when current Defense Secretary Donald Rumsfeld was shaking his hand on a mission for the Reagan administration.</P>
<P>After all, Saddam Hussein was a longtime CIA asset who served as Washington's enforcer, invading neighboring Iran at the behest of the U.S. Funny how none of the prowar media pundits ever mention the story of the <I>USS Stark</I>, the American warship hit with an Iraqi missile in 1987 during the Iran-Iraq War. Some 35 U.S. sailors died--but Washington forgave Saddam Hussein and actually increased its support for Iraq in the war.</P>
<P>Now Washington claims that it will involve Kurds, Shiite Muslims and other victims of the old regime in the trial of Saddam. But the warmakers in Washington aided Iraqi Kurds only to undermine Saddam Hussein--while giving Turkey a green light for its own genocidal anti-Kurdish campaign.</P>
<P>And it was George Bush Sr. who gave Saddam Hussein the green light to savagely suppress the Shiite uprising after the 1991 Gulf War. It is the antiwar movement in this country and internationally, not the U.S. war machine, which genuinely stands with the oppressed and working people of Iraq and throughout the world against vicious dicatorships--governments backed by Washington more often than not.</P>
<P>If there are going to be trials for the mass killings of Iraqis, then there had better be room in the courtroom for Bush I and Bush II, Bill Clinton and all the countless U.S. commanders and United Nations bureaucrats responsible for the bombings and economics sanctions that left more than 1 million dead. If Washington has its way, Saddam will be replaced by another "CIA asset"--and there's plenty waiting to audition for the job if current Pentagon favorite Ahmed Chalabi can't get his act together.</P>
<P>The U.S. capture of Saddam is about anything <I>but </I>justice. It's the latest propaganda ploy by a government intent on maintaining support--and winning reelection--to further the agenda of projecting U.S. imperial power.</P>
<P>That's why we can't limit our aims to ending Washington's occupation of Iraq. We need to expose this latest attempt to sell the U.S. war machine as a force for good--and build a movement that rejects U.S. imperialism on principle.</P>



