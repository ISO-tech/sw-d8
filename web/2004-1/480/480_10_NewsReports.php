<P><p><font face="Times New Roman, Times, serif" size="4"><b>Stop police torture and the death penalty</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font> </P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--December was a busy month for activists in against police torture and the death penalty. On December 18, more than 60 people attended the first public forum of the Enough is Enough Campaign, an initiative to fight for justice for the many victims of police torture and wrongfully convicted prisoners languishing behind bars in Illinois.</P>
<P>The event brought together family members of the wrongly imprisoned, activists, religious leaders and those who have experienced police abuse firsthand. Madison Hobley, one of the four men pardoned from Illinois death row by former Gov. George Ryan, spoke about the pain, grief and desperation he felt--and the urgent need to organize for justice today so no one else pointlessly suffers behind bars.</P>
<P>Campaign activist Joan Parkin summed up by arguing for a grassroots struggle to force politicians--whether Republicans or Democrats--to do something about these injustices.</P>
<P>A few days later, the Campaign to End the Death Penalty held a press conference in the State of Illinois Building to turn up the heat on Illinois Attorney General Lisa Madigan. Madigan has filed a lawsuit challenging former Gov. Ryan's commutations of 32 former death row prisoners.</P>
<P>Alice Kim of the Campaign to End the Death Penalty read from a letter by Renaldo Hudson, one of the men Madigan wants to send back to death row. "We believe that sending even one person back to death row would be nothing short of psychological torture and would only serve to deepen the wounds of victims and the prisoners' family members even more," Hudson wrote. </P>
<P>Rev. Jesse Jackson called for abolishing the death penalty. "The risk of killing the innocent is too great," Jackson said. "Even for the guilty, the revenge and retribution is no solution to those who have lost their lives." Campaign activists then delivered a stack of nearly 500 petitions to Madigan's office calling on her to drop the suit.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Stop California's cuts</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Yee</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SACRAMENTO--Hundreds of demonstrators rallied here December 10 against Governor Arnold Schwarznegger's proposed budget cuts that target the disabled and higher education. Protestors were angry that Schwarznegger wants to suspend the historic Lanterman Act, which ensures disabled people's access to important government programs.</P>
<P>Schwarznegger has also proposed eliminating the University of California's K-12 outreach budget. Many are angry that Schwarznegger--a former proponent of the Special Olympics--was going to light the state Christmas tree with help from a developmentally disabled child at the same time he proposed to suspend the Lanterman Act. One protestor carried a sign that read, "Kids with disabilities are more than just a photo op."</P>
<P>Nearly 50 students from several universities gathered to protest the elimination of outreach--the main tool to enroll minority students following California's abandonment of affirmative action. "Outreach is already less than one percent of the UC budget and has taken a 50 percent reduction this year...the hardest hit students are already underserved by the California education system," Le&oacute;n Arellano of MEChA told <I>Socialist Worker.</P>
</I><P>Many passer-bys honked in support of the large banner proterrs carried, which read, "Arnold TERMINATES Education."</P>

