<font face="Times New Roman, Times, serif" size="5"><b>Virgina jury rejects death for Malvo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A VIRGINIA jury refused to sentence sniper suspect Lee Boyd Malvo to death last month, instead giving him a term of life in prison without parole. The sentence was a setback for prosecutors and politicians who whipped up a hysteria around the two recent trials of the Washington, D.C.-area sniper suspects.</P>
<P>Malvo was shipped to Virginia specifically because, as a juvenile at the time of the sniper killings, he could be sentenced to death if he was convicted. Anti-death penalty activists said the decision could be a turning point in the fight against juvenile executions. </P>
<P>The U.S. is one of only a handful of countries that allow the execution of juvenile offenders, and Virginia is one of only 21 states that still allow the practice. Malvo's lawyers argued that the defendant's young age and unstable upbringing allowed him to be "brainwashed" by the other sniper suspect, John Allen Muhammad. Under the law that Malvo was convicted of breaking, prosecutors had to prove that he was the triggerman in the shooting of FBI employee Linda Franklin outside a Home Depot in Fairfax, Va. </P>
<P>The case against Malvo was based almost exclusively on circumstantial evidence. The only evidence fingering Malvo as the shooter was a confession--later recanted--that was obtained after Attorney General John Ashcroft personally intervened to have federal charges dropped and Malvo shipped to Virginia. </P>
<P>During this time, Malvo was denied access to his lawyers and subjected to a 7-hour interrogation--during which he made a statement. The jury ultimately accepted this "confession" and found Malvo guilty. But they also seemed to accept the defense team's argument that Malvo was manipulated by Muhammad--and chose not to sentence him to death.</P>
<P>Prosecutors aren't giving up, however. After Malvo was convicted in Fairfax County and Muhammad in Prince William County, prosecutors asked Virginia Gov. Mark Warner to let them swap jurisdictions and try each of the men again. Plus, prosecutors from Alabama, Louisiana, Maryland and Washington, D.C., have also requested that the pair be transferred to stand trial in their home states.</P>


