<P><font face="Times New Roman, Times, serif" size="5"><b>Morning-after pill closer to approval</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DESPITE AN all-out attack by the religious right, two advisory committees have recommended that the Food and Drug Administration (FDA) make the "morning-after" contraception pill available without a doctor's prescription. The pill--which is used to stop pregnancy within 72 hours after unprotected sex, or when contraception fails--would allow women the freedom to prevent an unwanted pregnancy without a time-consuming and costly doctor's visit.</P>
<P>The treatment consists of two progestin pills taken 12 hours apart--and would cost about $30 without a prescription. The pill, which has been approved in 101 countries and is sold without a prescription in 33 of them, was approved in the U.S. on a prescription-only basis in 1999. Since then, it has been used by some 2.4 million women--even though it is not widely available.</P>
<P>Among the advisory panel members, anti-abortionists like W. David Hager of the University of Kentucky and Joseph Stanford of the University of Utah want to force manufacturers of emergency contraception to print package warnings that the pills may "cause abortion." The bigots believe that women shouldn't be able to make decisions about whether to stop an unwanted pregnancy.</P>
<P>The FDA is expected to make a final decision in early February on whether the pills will be available over the counter. If they do, it will be a long overdue victory for women's right to control their own bodies--and a blow to the right-wing fanatics who want to see that right taken away. As one young woman from the Gainesville, Fla., area chapter of the National Organization for Women told the advisory panels, "Women deserve access at any time, at any age, for any reason."</P>



