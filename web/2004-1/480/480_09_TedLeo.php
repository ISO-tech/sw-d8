<P><font face="Times New Roman, Times, serif" size="4">An interview with musician Ted Leo</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Where have the rude boys gone?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Kirstin Roberts</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 2, 2004 | Page 9</font></P>

<I><P><font face="Times New Roman, Times, serif" size="3">"I'LL UPROOT it without tears, and I'll change it if I can, and<BR>
No more shall I be loyal to my sorrowful country." <BR>
</I>--"Loyal to My Sorrowful Country" by Ted Leo and the Pharmacists</P>

<P>TED LEO and the Pharmacists are a rarity in the world of independent music today. They're bringing punk rock back to its roots of music with a message. In an era when most musicians--with a few important exceptions--seem content to leave politics to the politicians, Leo's lyrics deal with issues ranging from police brutality to U.S. foreign policy.</P>
<P>Often compared to early punk legends the Clash and the Jam, Leo's music is consciously left wing, without succumbing to sermonizing. His distinctly subversive yet subtle lyrics raise more questions than answers.</P>
<P>"My songs often start with a question or a frustration about what the government is doing or how crappy the business world is, and then it usually winds up expressing my personal feelings," Leo told <I>Socialist Worker </I>before a sold-out show at Chicago's Fireside Bowl in November. "I don't have the acumen to write a political treatise, but maybe the plus side of that is that by expressing my own confusion about the world, it becomes a little more universal and understandable."</P>
<P>Their critically acclaimed third album <I>Hearts of Oak, </I>written in the wake of September 11, offers up songs that clamor against Bush's drive to war, and the fear and propaganda that accompanied it.</P>
<P>After the release of <I>Hearts of Oak </I>in February 2003, Ted Leo and the Pharmacists appeared on <I>Late Night with Conan O'Brien, </I>with "No War" spelled out on Ted's guitar in duct tape. "That was a very eye-opening experience because not only were the people making this very mainstream TV show allowing us to do this," Leo said, "they were running around trying to find the duct tape for us before the show."</P>
<P>One track from <I>Hearts of Oak, </I>"Where Have All the Rude Boys Gone?" a tribute to the multiracial ska bands of the late 1970s, laments the absence of consciously antiracist and political message music being made today. "While its not my place to tell other musicians they have to make serious songs, I get a little frustrated sometimes that most musicians don't use this amazing opportunity we have to put something more meaningful out there," Leo said.</P>
<P>"The world needs good-time rock and roll, but there is plenty of that out there. The world right now really needs something more. I personally feel compelled to be a more political artist."</P>
<P>Ted Leo and the Pharmacists' four albums, <I>Treble in Trouble </I>(2000), <I>The Tyranny of Distance </I>(2001), <I>Hearts of Oak </I>(2002) and <I>Tell Balgeary, Balgury Is Dead </I>(2003) available from Lookout Records at www.lookoutrecords.com</P>


