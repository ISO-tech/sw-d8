<P><font face="Times New Roman, Times, serif" size="4">California sets execution</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Don't let the state murder Kevin Cooper</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Cameron Leckrone</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 2, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE STATE of California is set to execute Kevin Cooper on February 10 at San Quentin State Prison. But opponents of the death penalty are organizing to fight for Kevin's life.</P>
<P>They know that while the case against Cooper is full of holes, the case against the California criminal injustice system is airtight. Cooper was convicted in 1985 for the murder of Peggy, Doug and Jessica Ryen, along with their young houseguest Christopher Hughes.</P>
<P>Much of the evidence tying Cooper to the murder was circumstantial. In 1997, Cooper sought DNA testing to prove his innocence, which state officials finally granted in 2001. </P>
<P>Unknown to the defense team, or even the court, a criminologist involved in Cooper's prosecution had checked out the evidence kit--along with Cooper's blood and saliva samples--and then checked them back in 24 hours later. Cooper's defense attorneys discovered this by accident, before the DNA testing was finished.</P>
<P>It's hardly a surprise that the DNA results came back with a result that favored prosecutors. Yet the state is refusing to do tests that could show if the blood evidence was tampered with.</P>
<P>There are other unanswered questions in this case. For example, blond hair--which couldn't possibly belong to Cooper, who is Black--was found in one of the victim's hands, but the jury at the original trial never heard about it.</P>
<P>These and other problems raise fundamental questions about the fairness of Cooper's trial, but both state and federal courts have refused to look into them. The truth is that Kevin's case and the more general flaws in California's criminal injustice system are bound together in a knot of racism, poverty and political expediency.</P>
<P>According to California Department of Corrections statistics, almost 60 percent of the more than 630 people on death row are non-white. Hundreds lack appellate attorneys because the state-sponsored Habeas Corpus Resource Center is overwhelmed with cases.</P>
<P>The Committee to Stop the Execution of Kevin Cooper is already planning events, such as a Live From Death Row at the University of California-Berkeley, a statewide day of action on February 3 and--if the state pursues its execution date--a march to San Quentin and protest on the night of the execution. </P>
<P>We are also encouraging supporters to flood the office of the governor with letters and phone calls, and to write letters to local newspapers. For years, Kevin Cooper's supporters have been building support for his case. Now is the time to mobilize.</P>

