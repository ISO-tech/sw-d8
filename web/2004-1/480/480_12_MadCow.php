<P><font face="Times New Roman, Times, serif" size="4">Mad cow crisis exposes bosses' greed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Putting lives at risk for profit</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 2, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">U.S. OFFICIALS sprang into action last week, mounting an all-out campaign after the revelation that a cow in the U.S. was infected with mad cow disease. But their actions weren't aimed at protecting consumers.</P>
<P>Instead, the administration was more concerned with protecting the profits of the U.S. beef industry. When the report of the infected cow in Washington state first surfaced, Agriculture Secretary Ann Veneman rushed to declare that the U.S. beef supply was safe--even before it was possible to know whether any other cattle in the herd were infected.</P>
<P>In fact, because of the lack of an adequate record-keeping system in the U.S., it still wasn't clear weeks later how many other cattle may have contracted bovine spongiform encephalopathy (BSE), the scientific name for mad cow disease. But that didn't keep Dr. Ron DeHaven, chief veterinarian for the Department of Agriculture, from chiding the 30 countries that banned U.S. beef imports for making their decisions based on "perceptions of the disease" instead of "scientific facts."</P>
<P>DeHaven should direct his message at the Bush administration. The U.S. still has a ban on importing many cuts of Canadian beef following the discovery of a single infected cow in Canada last May. "[I]f we're imposing these bans on another country, who are we to ask other countries not to impose bans on the U.S. when they question our supply?" said Dr. Paul Brown of the National Institute of Health, who has been studying BSE since 1986.</P>
<P>News that the infected cow in Washington came from the Canadian province of Alberta led U.S. officials to try to pin the blame on Canada. This is ridiculous.</P>
<P>First, Canada exports about 1 million cows to the U.S. annually, so any problem in Canada is automatically a problem in the U.S. Second, the U.S. government has prevented earlier cases of mad cow disease in the U.S. from being discovered by keeping its testing system incredibly feeble.</P>
<P>In other words, beef consumers have certainly already eaten the evidence of earlier infections in cattle slaughtered in the U.S. Only about 20,000 of the 35 million cows slaughtered annually in the U.S. are tested for BSE. Compare that to Japan and many European countries, where every cow slaughtered for human consumption is tested before it enters the food supply.</P>
<P>What's more, the beef industry effectively lobbied Congress not to ban the slaughter of "downers"--that is, cows that are too sick to walk, the chief symptom of mad cow disease. After the announcement of the infected cow in Washington, health officials recalled more than 10,000 pounds of beef and warned consumers in eight Western states and Guam that meat from the infected cow may have found its way onto store shelves.</P>
<P>For just two or three cents a pound, the beef industry could have already implemented the testing of all meat, according to Dr. Stanley Prusiner, the scientist who won a Nobel Prize for his work on BSE. But the barons of beef would rather allow consumers to eat infected meat--than let tighter inspection procedures eat into profits.</P>



