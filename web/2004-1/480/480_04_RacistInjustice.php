<P><font face="Times New Roman, Times, serif" size="4">Sick truth about the "war on drugs" in Maryland</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A racist injustice system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 2, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
According to a recent report by the Justice Policy Institute, racial disparity in Maryland's prison population has grown steadily since the mid-1980s as the state has expanded its use of prison for drug offenses, despite studies that show nearly equal rates of drug abuse across racial lines. The study, commissioned by the Legislative Black Caucus, found that Maryland "currently has the dubious distinction of having the largest percentage of African Americans incarcerated in the nation." </P>
<P>Some 28 eight percent of the state's population is African American, but they compose 68 percent of drug arrests, 76 percent of the overall prison population and 90 percent of those incarcerated for drug offenses. "The data is astounding to say the least," said State Delegate Obie Patterson. </P>
<P>A 2002 study shows a higher percentage of white youth between age 12 and 17 have taken drugs in the last month than Black youth. Yet Black youth composed 96 percent of the new youth prison admissions for drug offenses between from 1986 and 1999. All told, 94 percent of the prison growth for drug offenses during those years were African American inmates. </P>
<P>These terrifying increases correspond to the government's racist war on drugs. In 1986, although African Americans still were a higher percentage of the prison population, only 15 percent of African Americans behind bars were in jail for drug offenses. By 1999, nearly half of all African American prison admissions were for drug offenses. Today, African Americans are eight times more likely to be arrested for narcotics than whites. </P>
<P>What do all these numbers mean? They mean that racism has continued to destroy the Black working class by incarcerating them at growing rates for nonviolent crimes. </P>
<P>Nonviolent drug offenders deserve treatment, not jail time. The "war on drugs" hasn't done anything but declare war on the poor. Men and women who cannot afford adequate legal representation and who are targeted by the police for the color of their skin are going to jail in record numbers.</P>
<P>We need to expose the racism and economic injustice of this sick system. The Legislative Black Caucus and other lawmakers are currently exploring legislation passed in other areas to fund treatment and reentry programs. While this is a start, we have to fight back against racism in our communities and fight for a better world, free of economic and social injustice.</P>
<B><P>Nick Chin, </B>Washington, D.C.</P>

