<P><font face="Times New Roman, Times, serif" size="4">Nightmare continues in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>This isn't liberation</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 2, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"WE GOT him." Washington's colonial overseer in Iraq, Paul Bremer, couldn't have been more smug as he announced to the world that U.S. forces had finally snared the "ace of spades." George W. Bush oozed arrogance as he smirked his way through his photo ops, and the pro-war media cheerleaders rejoiced as they endlessly broadcast the video image of Saddam Hussein, being poked and prodded like an animal during a supposed medical examination.</P>
<P>The message from the Washington warriors was clear--they had finally scored a victory by "getting their man." But in a matter of weeks, the truth reemerged from behind the Bush gang's latest smokescreen--that the nightmare of Washington's occupation of Iraq continues.</P>
<P>Saddam's capture will have been little comfort, for example, to the families and friends of the eight U.S. soldiers killed on Christmas in mortar and bomb attacks. Or for the thousands of U.S. reservists, promised that they would be greeted in Iraq as "liberators," but now facing indefinite tours of duty and an increasingly hostile resistance.</P>
<P>Little comfort, too, for the families of the Thai and Bulgarian soldiers killed last week in a series of coordinated car bombings in the southern city of Karbala. Or the Iraqis whose casualties rarely make U.S. headlines.</P>
<P>Try telling ordinary Iraqis living under the heel of the U.S. occupiers about how "liberated" they are now that Saddam's gone. Iraqis like 31-year-old Maouloud Hussein, a farmer who was shot in the back in December as he tried to protect his five young daughters and son during a U.S. raid in the city of Samarra.</P>
<P>"You said you would bring us freedom and democracy, but what are we supposed to think?" Hamed Hussein, Maouloud's brother, asked <I>Independent</I> journalist Robert Fisk. "My neighbor, the Americans took him in front of his wife and two children and tied his hands behind his back, and then a few hours later, after all this humiliation, they came and said his wife should take all her most expensive things, and they put explosives in their house and blew it up. He is a farmer. He is innocent. What have we done to deserve this?"</P>
<P>The answer is nothing. Iraq today is a bloody nightmare that has nothing to do with freedom or justice for ordinary Iraqis. It has nothing to do with ending the tyranny of a strongman once enthusiastically backed by the U.S. </P>
<P>George Bush's war and occupation was always about oil and empire. That's why resistance to U.S. rule will continue to grow. It's time to say: End the occupation now!</P>



