<P><font face="Times New Roman, Times, serif" size="5"><b>Tyson strike goes down to defeat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 11</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--After striking for nearly 11 months against Tyson Foods, the members of United Food and Commercial Workers (UFCW) Local 538 voted 293-70 to accept Tyson's latest offer, after rejecting a similar offer on January 11 by a 242-74 margin. UFCW International and local leaders pushed the "yes" vote due to the threat of union decertification.</P>
<P>After a year on strike, the scabs legally could have voted the union out.  As striker Bill Schmieder told the <I>Capital Times</I>, "We're not getting really what we wanted, but we need to keep the union." </P>
<P>But the contract is itself designed to bust the union. It includes all of Tyson's original demands for cuts in wages, health care and pensions. This includes a two-tier wage scale with new hires starting at $9 hour--$2.10 less than the previous contract--and a $2 decrease in maximum pay.</P>
<P>The agreement also includes a freeze in pensions for workers hired before the strike, beginning January 1, 2005--and eliminates pensions for workers hired after the strike. Vacation time is also cut. </P>
<P>The deal also includes an employee contribution of 25 percent of health care costs starting January 1, 2005 for a far less comprehensive health care system. "There are few doctors in the area [under the new plan]," Local 538 member Debi Fleming told <I>Socialist Worker</I>. The contract also allows the approximately 300 scabs to stay in the plant, with union members set to fill any remaining jobs and to take jobs as scabs leave. </P>
<P>The offer that was voted down on January 11 restricted rehiring of union members to a one-year window after the strike. The new offer allows Local 538 members to stay on a waiting list indefinitely. But with scabs now eligible for raises under the new pay scale, most are likely to stay on--so the threat of decertification remains.</P>
<P>Tyson workers voted to accept this terrible agreement under enormous pressure from the company. According to union members' accounts of a meeting of strikers, UFCW representatives took a neutral stance--but warned of the consequences of a no vote. </P>
<P>Yet for 11 months, UFCW leaders espoused the slogan "One day longer, one day stronger", while refusing to up the ante tactically. They even hinted that the union could wait out the strike until March, when other Tyson plants' contracts will expire. </P>
<P>The UFCW strategy was to encourage other groups to initiate boycotts, but the union stopped short of endorsing a full boycott of Tyson itself. One of the biggest campaigns initiated by union leadership was a reactionary campaign targeting felons who were working as scabs. </P>
<P>Union leaders avoided discussion of spreading the strike to other Tyson plants where pepperoni--the Jefferson plant's product--was produced. There was widespread rank-and-file sympathy for new, more aggressive tactics. Yet union leadership saw picketing outside the plants as a symbolic gesture, rather than as a means to disrupt and shut down plant's production. Over time, the symbolic pickets let to the strikers' disillusionment.</P>
<P>Many workers wrote off coming back to work at the plant and stopped coming to picket lines and rallies. For its part, Tyson was willing to take a relatively small hit to their huge bankroll in order to weaken to break the union if possible. As chief steward Gregg Peters told <I>Socialist Worker</I>, "They [Tyson] told us right away that it wasn't economic, it was philosophy."</P>
<P>In other words, Tyson wanted to use Jefferson as an example to its plants elsewhere in the industry: if you mess with us, you get burned. In future battles, rank-and-file workers will need to initiate aggressive, confrontational tactics that build on labor solidarity to hit big corporations like Tyson where it hurts most--in the pocketbook.</P>

