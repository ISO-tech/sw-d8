<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Workers' revolt to revolution</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE MASSES go into a revolution not with a prepared plan of social reconstruction, but with a sharp feeling that they cannot endure the old regime," wrote Leon Trotsky in his famous <I>History of the Russian Revolution.</I> Revolutions are windows of opportunity where the old habits of deference and passivity are suddenly destroyed on a mass scale among ordinary people.</P>
<P>But the dead weight of tradition dies hard. Alongside the process of "self-emancipation," where workers begin to develop their own capacity and strength in struggle, the old idea that change can only come from above still survives.</P>
<P>A revolution awakens millions of people who hitherto were passive, having little belief in their own capacity to run society. As a result, it first strengthens <I>reformist</I> consciousness--the idea that we must rely on others to change society for us.</P>
<P>Workers are accustomed to believing that they are incapable of running society--that insofar as change is possible, they must depend not upon themselves, but upon representatives who will act on their behalf. Mass struggle begins to break the sense of subordination and deference among ordinary people, but it does not wipe it out in one stroke. </P>
<P>The result is that in the first phase of every revolution, there is a general shift to the left in mass consciousness, but the center of gravity of mass consciousness remains reformist. There is a difference between the reformism of trade union and movement leaders, who are more or less "hardened" in their reformism, and the reformism of workers whose struggle points a way beyond reform but whose own consciousness at first still tells them that reform is the best they can expect. </P>
<P>In the first phase of a revolutionary movement, the spontaneous element predominates. Workers' consciousness changes in struggle, but consciousness lags behind experience. As a result, workers are capable of overthrowing the system before they become fully aware of what alternatives they are capable of posing to it.</P>
<P>In 1917, for example, several days of mass protest cracked the tsar's armed forces and the tsar was forced to abdicate. Immediately after, the main socialist parties put out a call for forming soviets.</P>
<P>The Petrograd Soviet of workers' and soldiers' deputies had within its grasp the ability to take the reigns of power, but it did not. This was recognized by the bourgeois politician Rodzianko, who told Cheidze, the reformist leader in the Petrograd soviet, "You have the power, you can arrest us all."</P>
<P>There is a need, therefore, for an organization of revolutionaries that can fight inside the movement to break past the constraints of reformism and win the majority of workers to the idea that they most pose a new alternative to capitalism. The possibility that workers might be able to translate their power into more than just opposition to the way things are is not at first apparent to them--it becomes so only through a period of hard lessons in the course of struggle.</P>
<P>In the process of struggle, ideas of solidarity, equality and opposition to oppression come to the fore. But workers don't become aware of their position and power in society at the same time.</P>
<P>Some move faster than others and are ready to take the lead. The role of revolutionary organization is to unite the most militant workers and activists in the struggle--those who have a clearer grasp of the possibilities for revolutionary change--so as to be able to turn revolutionary <I>potential </I>into reality.</P>
<P>Without such a party, the revolutionary moment is lost and the movement either goes into decline or is militarily defeated. Either way, society begins to flow back into its old channels and "order" is restored once again.</P>
<P>"Without a guiding organization, the energy of the masses would dissipate like steam not enclosed in a piston-box," writes Trotsky. "But nevertheless what moves things is not the piston or the box, but the steam."</P>


