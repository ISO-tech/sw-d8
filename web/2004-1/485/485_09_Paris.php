<P><font face="Times New Roman, Times, serif" size="4">Rap artist takes on Bush's America</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Paris' Sonic Jihad</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Paris, <B><I>Sonic Jihad, </B></I>Guerrilla Funk Recordings, 2003.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"BRINGIN' YA back what you miss in hip-hop: hard truth!" proclaims rap artist Paris in his fifth album, <I>Sonic Jihad</I>. With his trademark fury and "guerrilla funk" beats, his new release is a direct confrontation with Bush's America and the state of rap music.</P>
<P>From the war in Iraq to the daily assault on Black communities, Paris brings the same jagged political edge that earned him the scorn of the musical establishment with his past release of such songs as "Bush Killa." <I>Sonic Jihad </I>packs 16 tracks that smash through the fa&ccedil;ade of "post 9/11 sensibilities."</P>
<P>The title and cover of the album--an image of a jetliner about to slam into the White House--inverts and twist images and language to use them as a weapon. But Paris is not seeking to simply startle and shock.</P>
<P>His songs are a passionate appeal to the "hip-hop generation" to exhort them to struggle. In songs like "AWOL," Paris exposes the class nature and brutality of war. A na&iuml;ve working-class youth is lured into the military by an unscrupulous recruiter who promises him buckets of money for school.</P>
<P>Instead, the boy is plunged into a violent and degrading world, and eventually war. The song switches between a narrative against the war with the conscience of the young soldier, who watches in horror as innocent people and fellow soldiers are gunned down in the name of "democracy."</P>
<P>Back at home, neglected and broken down in body and mind, the young man laments the lies he was fed: "I'm back home bitter and sick and contagious, and I'm knowing that we bullies that's why everyone hate us." He concludes, "I should have listened when my homie said we murder for oil...war is pain."</P>
<P>In "Sheep to the Slaughter," Paris denounces the slanted media and pays tribute to the mass antiwar movement that poured into the streets last year. The song erupts into a prowling beat that rolls into a cadence of antiwar chants and voices from the movement. "When you see me understand I'm representing the voice the majority would feel if they were given a choice," he concludes.</P>
<P>In the song "How We Do" Paris seeks to reclaim hip-hop from the clutches of Corporate America and restore it to its socially conscious and afro-centric roots. He throws down the gauntlet to cookie-cutter "fake thugs" who promote pool-side hedonism, sexism and self-hatred, and whose affects only serve an industry seeking cheap profits.</P>
<P>As he puts it, "Before 'blingin' we was singing what it means to be black...you see I'll die before I live on my knees." Paris enlists other radical acts Dead Prez and Public Enemy to rally around this message in the song "Freedom."</P>
<P>This album should provide some "fight songs" for future struggles or at least something to bump to loudly in your room to get your blood flowing. Grab a copy of this CD and check out Paris' Web site at </FONT><A HREF="http://www.guerrillafunk.com/"><FONT FACE="Times">www.guerrillafunk.com</FONT></A><FONT FACE="Times">.</P>


