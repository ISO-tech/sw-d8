<P><font face="Times New Roman, Times, serif" size="5"><b>The Civil War wasn't just a "rich man's war"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 6, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I></B><br>
I wanted to take issue with some of the characterizations of the Civil War that Sarah Hines ("Message of <I>Cold Mountain," </I>January 23) made in her rebuttal to Donny Schraffenberger's review (January 9) of the movie <I>Cold Mountain. </I>I haven't seen <I>Cold Mountain, </I>so I'm not taking sides in the critique of the movie. </P>
<P>However, I think there are problems with how Hines views the Civil War. Hines calls it a "rich man's war" and focuses--as presumably the movie does--on the "horrific realities of warfare" as experienced by poor Southern whites.</P>
<P>The first thing that should be said is that Marxists don't oppose war just because it is horrific. In fact, Marxists during the period of the Civil War--including Karl Marx himself--supported the North, not just because the North and South were "two incompatible economies," but primarily because it was a revolutionary war for the liberation of Black slaves.</P>
<P>If the Civil War was a "rich man's war," there would not have been hundreds of thousands of slaves willingly taking up arms to defeat the South. Likewise, hundreds of thousands of white Northern soldiers recognized the revolutionary cause of the Union army--having been radicalized by both the Northern abolitionist movement, as well as the example of the bravery and courage of slaves who fought harder than anyone else to destroy the South. Make no mistake about it. The Civil War was absolutely brutal, as the North unleashed total war on the South.</P>
<P>Did poor whites benefit from slavery? No, but I don't think that means you can portray all poor whites as illustrations of "humanity and resilience." </P>
<P>Which poor whites are you referring to? Hopefully not the whites who made their living capturing slaves and returning them to slavery. Hopefully not the whites who gleefully took up the cause of the South, driven by racism as much as "wartime fervor." And hopefully not the poor whites who went on to make up the shock troops of the Ku Klux Klan when the war was over.</P>
<P>At the same time, there were poor whites who helped slaves escape, and poor white farmers who openly supported the North--like in Tennessee--because the system of slavery destroyed their farming opportunities. It's important to make these distinctions--to avoid making it sound like the plight of poor Southern whites was somehow on par with the atrocities committed against 4 million Black slaves, who were freed as a result of the bloodiest war in American history.<br>
<B>Keeanga-Yamahtta Taylor, </B>Chicago</P>
