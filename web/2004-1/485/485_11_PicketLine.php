<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 6, 2004 | Page 11</font></P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>University of Texas</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Dana Cloud</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--About 75 University of Texas shuttle drivers and mechanics picketed the board meeting of the Austin Transit Authority January 26. Members of other unions, including the Texas State Employees Union and the Education Austin teachers' union, came out in solidarity.</P>
<P>The drivers and mechanics--represented by the Amalgamated Transit Union (ATU) Local 1549--have been on a contract extension for two and a half years. Their employer, ATC/Vancom of Texas--a subcontractor for the Capital Metro Transit Authority--has raised the health insurance costs.</P>
<P>Many workers have not had raises in three years. Chanting "Fair Contract Now!" the picketers waved signs reading "We Are the Driving Force," and "Restore Health Benefits" at passersby.</P>
<P>Capital Metro representatives hid behind their subcontractor, saying that the board's hands were tied since the transit authority is not the official employer of the workers. "We are being railroaded," said driver "Brother Perry" Wren. "We are fighting for something that is long overdue. It's time to stand together right now."</P>
<P>The union and supporters are planning a rally on the University of Texas campus on February 15 in order to raise awareness among students, faculty and staff of the situation of their bus drivers.</P>


