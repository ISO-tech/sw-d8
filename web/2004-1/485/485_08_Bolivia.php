<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BOLIVIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>General strike threat wins concessions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 8</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BOWING TO the pressure of a planned general strike and threats of rebellion across the country, Bolivia's President Carlos Mesa last weekend announced a series of measures affecting multinational corporations and the country's rich. Mesa annulled the law that privatized Bolivian natural gas and petroleum in1997. Bolivian gas will now revert to state ownership, with the government promising to strengthen the state-owned oil company.</P>
<P>Mesa further announced a "complementary" tax on foreign oil and gas companies, whose contracts call for only 18 percent of profits to be remitted to the Bolivian state. Under the new tax, an additional fee of up to 32 percent can be assessed, depending on the size of the gas fields and the volume extracted. The total gas tax would then equal 50 percent of profits.</P>
<P>Bolivians had feared that Mesa would announce a sudden and steep increase in the domestic price of gas. Instead, he opted for a series of gradual increases based on allowing the international price of oil and the exchange rate of the dollar to determine domestic prices. Some early critics denounced this move as a "stealth" increase.</P>
<P>Other economic measures included a tax hike on Bolivians whose net worth exceeded $50,000. Mesa's actions came in response to a call by Bolivia's main labor confederation--the Central Obrera Boliviana (COB)--for an indefinite general strike beginning in mid-February. Bolivia's National Peasant Workers Union (CSUTCB) and the national truckers' union backed the strike.</P>
<P>After last October's gas rebellion, which overthrew President Gonzalo S&aacute;nchez de Lozada, the COB and CSUTCB gave Mesa 90 days to fulfill the demands of Bolivia's social movements. The announced aim of this month's strike is to topple the Mesa government, close down the National Congress and open a Constitutional Assembly of the People.</P>
<P>Opposition leader Evo Morales opposes the strike and has called instead for an electoral solution. As <I>Socialist Worker</I> went to press, opposition groups were meeting to decide whether to proceed with the strike, or wait to see the results of Mesa's plan.</P>


