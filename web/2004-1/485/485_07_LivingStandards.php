<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The hidden decline in living standards</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"CONSUMERS KEPT the nation's cash registers busy in December, boosting their spending by a healthy 0.4 percent in a fresh sign that the economy's recovery has legs." So Fox News reported this week, as mainstream economists put a positive spin on what was overall a gloomy year-end wages and jobs report.</P>
<P>The economic recovery may have "legs," but it is built upon a strategy aimed at slashing working-class living standards in order to maximize corporate wealth. While corporate profits soar once again, the working-class majority in the U.S. faces a quietly unfolding crisis barely acknowledged by the mainstream media.</P>
<P>Official statistics show unemployment falling to 5.7 percent in December. In reality, the U.S. experienced a net job loss in both 2002 and 2003--the first time since 1944 to 1945 with two years of consecutive job losses--and 15 million people are either unemployed or are working part-time because they can't find full-time jobs.</P>
<P>George W. Bush has pledged to make tax cuts permanent for the wealthiest Americans, yet Congress failed to pass even a 13-week extension for the 375,000 workers whose unemployment benefits ran out last month. Two million unemployed workers are expected to exhaust their unemployment benefits by June, according to the Center on Budget and Policy Priorities.</P>
<P>Government "wage and salary" statistics likewise disguise the dramatic decline in workers' living standards. Most year-end economic reports noted a 0.7 percent rise in workers' wages and benefits, but this is profoundly misleading.</P>
<P>The Economic Policy Institute (EPI) noted that workers' wages fell for everyone earning below the median--the point at which half are above, and half are below--with the most dramatic decline in the lowest fifth of wage-earners. All those above the median rose, with the sharpest increases at the top--the wage category that includes $75,000-a-year-and-up salaried managers, as opposed to hourly workers.</P>
<P>Wages for the majority of workers are in a downward spiral, as profitable employers seek to force workers to accept lower pay or face unemployment. The EPI estimates that the jobs added in the economy between November 2001 and November 2003 pay 13 percent less than jobs that have been lost.</P>
<P>The United Auto Workers' fall contracts with the two top auto parts suppliers, for example, accepted a two-tier wage system that will pay new hires $10 less than those companies' current wage of over $25 an hour. This decline in wages has led to a median loss of $491 in household income during the last year and $1,439 over the past two years--a decline of 6.3% for African Americans, 4.4% for Latinos, and 1.6% for non-Hispanic whites.</P>
<P>But even an accurate measurement of falling wages does not take into account the impact of rising health care costs, rents and mortgages, state and city taxes, and home heating bills. A recent ABC poll found that six in 10 people are worried about being able to afford health insurance in the future, while more than one in six already have no insurance.</P>
<P>During this winter's frigid January temperatures, 10,000 Chicago households went without heat, as did 6,000 Rhode Islanders. In separate incidents in Chicago, two wheelchair-bound women whose heat was cut off were killed in apartment fires--one had been forced to use a toaster oven as her only source for heat.</P>
<P>Ingraham Project Fuel in Portland, Maine has reported a 48 percent increase in requests for heating assistance since last year. Yet the federal government has cut $300 million from federal heating assistance subsidies to needy families.</P>
<P>Like Ronald Reagan before him, Bush has given the green light to corporations to attack workers' living standards and break unions, using fear of unemployment as their weapon. Unionization rates declined last year to 12.9 percent overall, and 8.2 percent in the private sector, rivaling levels before the mass unionization struggles of the 1930s.</P>
<P>While the U.S. economy is technically in a recovery, a growing sector of the working class is facing desperation. As during the Great Depression, class struggle points the only way forward today.</P>


