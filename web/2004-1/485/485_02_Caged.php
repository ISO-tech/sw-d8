<P><font face="Times New Roman, Times, serif" size="4">Barbaric treatment for juvenile offenders in California</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Caged like animals</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CAGED AND drugged like animals. That's what the California "justice" system considers proper care for juvenile offenders.</P>
<P>On its Web site, the California Youth Authority (CYA) claims to administer "treatment and training" to 4,600 young prisoners in an effort to reform and rehabilitate them. Yet a state-funded study issued in December--but not released until last week--found that kids imprisoned in the CYA are often punished by being isolated in wire cages. </P>
<P>The report also found that juveniles suffering from mental illness and substance abuse are being treated with prescription drugs, instead of other forms of treatment. CYA officials didn't even bother to deny the charges made in the report. "The observations of the state experts in these areas are substantially correct, and our department is reviewing each of these reports to develop a plan to correct the issues raised," CYA spokeswoman Sarah Ludeman told the <I>San Jose Mercury News</I>.</P>
<P>But that admission didn't come quick enough for two teenagers who committed suicide at Ironwood Lodge, a CYA facility in Ione, Calif., on January 19. Ironwood houses kids in a 60- to 90-day special management program. While there, they receive just an hour a day of education outside their cells.</P>
<P>Ironwood was specifically singled out in the December report. Investigators found that guards using pepper spray were "exacerbating symptoms of mental illness" and youths were kept "isolated and away from staff observation or interaction." </P>
<P>Studies show that as many as 65 percent of CYA prisoners suffer from mental illness, and 85 percent have drug and alcohol addictions. Yet according to the report, "The vast majority of youths who have mental health needs are made worse instead of improved by the correctional environment. The California Youth Authority continues to fall short of meeting many recognized standards of care for youth with mental health and substance abuse disorders."</P>
<P>Some kids receive between three and eight different psychiatric drugs without "adequate justification," the report found--while others were given no medicine when they needed it. Nighttime medications were not available in some facilities, a practice the report states is "especially egregious because needy youths are deprived of appropriate care."</P>
<P>Worse, according to the study, kids who misbehave are frequently administered drugs in order to "chemically restrain" them, even if they are of no danger to themselves or others. "In a number of facilities, psychiatric evaluations are cursory and do not meet accepted professional standards," the report commented.</P>



