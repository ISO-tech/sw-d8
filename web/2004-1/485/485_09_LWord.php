<P><font face="Times New Roman, Times, serif" size="5"><b>TV's way of breaking lesbian stereotypes</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>The "L" Word,</B></I> starring Jennifer Beals, Laurel Holloman and Mia Kirshner, airs Sundays on Showtime.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE LATEST in a string of gay-oriented TV shows, Showtime's <I>The "L" Word </I>is the first program to revolve around lesbians and bisexual women. That it exists--to mostly positive reviews--is not an unwelcome contrast to the fate of out lesbian Ellen Degeneris' sitcom <I>Ellen, </I>which ABC promptly canceled in 1997 when her character came out as a lesbian.</P>
<I><P>"L" Word </I>is set in posh West Hollywood, and its characters are products of their surroundings--they are rich, white and share impeccable fashion tastes and haircuts. The storylines aren't dissimilar from other soap opera themes--relationships between friends, the pursuit of love and intimacy, the sting of infidelity, and then of course there's the sex.</P>
<I><P>"L" Word </I>has received tons of publicity because the producers went out of their way to portray the show as breaking stereotypes of typical lesbians. The <I>New Yorker </I>ran a cover story featuring the cast naked with a banner that read, "Not Your Mother's Lesbians."</P>
<P>But if <I>"L" Word </I>is supposed to challenge stereotypes, it only does so by perpetuating the ridiculous caricature of gay life that Hollywood has recently championed. This is the view that gays and now lesbians are rich, white, supermodel beautiful and lead fabulous lives.</P>
<P>That <I>"L" Word </I>rehashes unrealistic popular culture stereotypes about the lives of gays and lesbians isn't a reason in and of itself to criticize the show. Just because the show is about a group of people that are oppressed doesn't mean that it should be judged by a different standard than the rest of Hollywood's drivel. Whenever dealing with the entertainment industry, one has to temper their expectations.</P>
<I><P>The "L" Word </I>is only another example of the yawning gap between reality and what the TV industry wants us to believe is reality. Still, there's something to be said about a show that approvingly portrays relationships--and sex--between women as normal, even if it chooses to focus on upper-class lesbians.</P>


