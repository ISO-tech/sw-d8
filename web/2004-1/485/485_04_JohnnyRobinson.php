<P><font face="Times New Roman, Times, serif" size="4">Florida's plan to execute Johnny Robinson</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>This is murder, not justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 6, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I></B><br>
A major miscarriage of justice is about to be carried out in Florida on February 4, if Johnny L. Robinson is executed. In the words of sociologist Michael Radelet, an expert on capital punishment, if the planned execution takes place, it will be "one of the very most horrendous miscarriages of justice that I have seen anywhere in my 25 years in the business."</P>
<P>In 1986, Robinson was tried and convicted by an all-white jury for the murder of a white woman, Beverly St. George. The prosecutor, Steve Alexander, a racist by reputation, spoon-fed his desired version of the murder to a then-16-year-old mentally retarded child named Clinton Fields, who was with Robinson at the time of the killing. </P>
<P>Despite being told by Fields that the death was an accident, Alexander refused to listen. He allowed his officers to coerce Fields--with an IQ of 50--into changing his story and signing Alexander's version of the killing. </P>
<P>In return for his signature on the prepared statement came the promise that once the state's version of the "facts" was signed, Fields would be allowed to go home to his mother. On January 19 of this year, Clinton Fields took the stand and recanted the testimony that he was forced into in 1985, describing how he was pressured and bullied into signing what he knew was not the truth. </P>
<P>In 1985, as a 16-year-old mentally retarded boy, Clinton Fields was believable. As a man in 2004, his recantation was dismissed by prosecutors. The sad reality is that Beverly St. George is dead at the hands of Johnny Robinson. </P>
<P>Sadder yet is the fact that in 1985, Florida was a racist environment where fear of repercussion drove Johnny Robinson to panic at the accidental shooting of a woman not of his race. The tragic reality is that discrimination is still alive and well in our country. </P>
<P>To base a death penalty conviction solely on the words of a retarded witness who is under pressure should not be tolerated. It is time for Florida to rethink.<br>
<B>Dianne Abshire, </B>From the Internet</P>
