<P><font face="Times New Roman, Times, serif" size="4">Mass. unions join the battle for gay marriage</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A fight for all workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Steve Trussell</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 6, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MASSACHUSETTS unions representing more than 200,000 workers across the state have come out in support of same-sex civil marriage. Labor's campaign comes several months after the Massachusetts Supreme Judicial Court struck down a state ban on gay marriage in its <I>Goodridge </I>decision.</P>
<P>Since then, the right wing has tried to take the offensive. State lawmakers, both Republicans and Democrats, have come up with a range of proposals--from an amendment to the state constitution banning same-sex marriage, to a "compromise" offering gays and lesbians rights under civil unions rather than marriage.</P>
<P>But many people are standing up to the bigots. On January 8, more than 1,000 people poured into the state house for a rally to support same-sex marriage. Later in the month, "pro-family" demonstrations organized by the Catholic Church were met by spontaneous counterprotests in favor of gay rights in Worcester and Springfield.</P>
<P>Now, several major unions in the state are taking sides. The Massachusetts Teachers Association, SEIU Locals 509 and 2020, the Massachusetts Nurses Association, the National Association of Government Employees, IBEW Local 1505 and the statewide United Auto Workers union have all passed resolutions supporting the <I>Goodridge </I>decision. The unions are planning to lobby lawmakers and participate in a state house rally on February 11.</P>
<P>This is a very important development in the fight for gay rights. From silence on the issue, the unions are stepping forward and recognizing the importance of demanding equality among their members as an important part of defending their interests. "Our union has taken this position in order to protect the civil rights of our members," wrote the UAW's political caucus in a letter to state lawmakers. "Ending marriage discrimination is also a critical union issue."</P>
<P>This stand is a step forward for the labor movement itself. Unions that are willing to address the needs of <I>all</I> their members will find themselves much better positioned to win new layers of the unorganized. And a labor movement ready to take an active stance against discrimination will be stronger and better prepared to wage the struggles ahead.  </P>
<P>Plus, organized labor has both the interest and the social power to challenge discrimination in all its forms--making it an important ally for a gay rights struggle that has too often floundered because it remained on the margins. This represents the real way forward for gay rights--and the wider fight against injustice in society.</P>



