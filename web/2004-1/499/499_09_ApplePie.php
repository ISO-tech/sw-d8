<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"It's as American as apple pie"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WILLIAM BLUM'S <I>The CIA: Forgotten History</I> tells many stories of direct U.S. employment of torture, as well as stories of how U.S. agencies provided funding and training to torturers. It's impossible to read these stories and not conclude that U.S. support for and involvement in torture is routine rather than the work of a few "bad apples."</P>
<P>He notes that training in torture techniques is taught in various U.S. military schools under names like "countermeasures to hostile interrogation." Former Green Beret Donald Duncan recounts a conversation between teacher and student during one of these training sessions:</P>
<P>"'Sergeant Lacey, the name of this class is "Countermeasures to Hostile Interrogation," but you have spent most of the period telling us there are no countermeasures. If this is true, then the only reason for teaching them [the torture methods], it seems to me, is so that we'll know how to use them. Are you suggesting we use these methods?'</P>
<P>"The class laughs, and Lacey looks down at the floor creating a dramatic pause. When he raises his head, his face is solemn but his deep-set eyes are dancing. 'We can't tell you that, Sergeant Harrison. The Mothers of America wouldn't approve.' The class bursts into laughter at the sarcastic cynicism. 'Furthermore,' a conspiratorial wink, 'we will deny that any such thing is taught or intended.'"</P>
<P>Chapter 33, subtitled "Torture--As American as Apple Pie" tells the story of former Richmond, Ind., Police Chief Dan Mitrione, the head of the Office of Public Safety (OPS) mission in Montevideo, Uruguay, between 1965 and 1970. The OPS was a U.S. agency that supplied equipment, weapons and training to various police forces in Latin America and elsewhere.</P>
<P>Though Uruguayan officials had used torture in the past against dissidents, the OPS, according to an interview given in 1970 by the former Uruguayan Chief of Police Intelligence Alejandro Otero, Mitrione had made torture a more frequent and "refined" part of the state's repressive tools. Otero was demoted when he complained after he discovered that Mitrione had personally assisted in torturing of one of Otero's female friends.</P>
<P>The Uruguayan Senate set up a commission to investigate torture and found that torture had become a "normal, frequent and habitual occurrence" used against Tupamaros guerrillas and other activists. The torture techniques used were electric shocks to the genitals, electric needles under the fingernails, burning with cigarettes, the slow compression of testicles and the daily use of psychological torture.</P>
<P>Mitrione built a soundproofed room in the cellar of his home where he invited Uruguayan police officers to improve their torture techniques, working on beggars whose disappearances would go unnoticed. A CIA-Cuban double agent, Manuel Hevia Cosculluela, who observed one of these sessions reported that Mitrione "personally tortured four beggars to death with electric shocks."</P>
<P>According to Hevia, Mitrione explained his philosophy of torture thus. First comes the "softening up" period, in which the prisoner is humiliated and made to feel helpless and cut off from the outside world. The images coming out of Iraq seem to show prisoners going through this "phase" of torture.</P>
<P>After this comes the interrogation. "The precise pain, in the precise place, in the precise amount, for the desired effect," was Mitrione's motto. Mitrione suggested that after the information was extracted from the victim, it was good to apply a little more torture "as a political measure, to create a healthy fear of meddling in subversive activity."</P>
<P>In July 1970, Mitrione was kidnapped by Tupamaro guerrillas, who demanded the release of 150 prisoners in exchange for Mitrione's life. The Tupamaros did not torture him, and he was killed only after the Uruguayan government refused to meet the demands.</P>
<P>At Mitrione's funeral, White House spokesman Ron Ziegler declared that "Mr. Mitrione's devoted service to the cause of peaceful progress in an orderly world will remain as an example for free men everywhere." Today we hear the same rhetoric covering up the same atrocities.</P>


