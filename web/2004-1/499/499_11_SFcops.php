<P><font face="Times New Roman, Times, serif" size="4">S.F. cops murder an unarmed Black man</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"His arms were raised, but they kept shooting"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kim Rabuck</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--On May 5, police shot and killed Cammerin Boyd, an unarmed 29-year-old African American man with wooden legs. The San Francisco Police Department (SFPD) claims that Boyd fired on them, but this is flatly contradicted by many witnesses who say Boyd was trying to surrender.</P>
<P>Boyd was holding his shirt up with one hand to show he was unarmed, according to witnesses, and was using the other arm to take off his prosthetic legs when he was shot. "His arms were raised up, he was saying, 'I don't have nothing,' but they kept shooting," said Antonette Allen, who witnessed the shooting with her 5-year-old son. "They kept telling him to get down, but he had no legs. How could he?"</P>
<P>Last month, San Francisco police officer Isaac Espinoza was shot and killed while on duty--and the SFPD is out for blood. When San Francisco District Attorney Kamala Harris announced that she would seek life without parole for David Hill, the 21-year-old Black man that police say is the perpetrator, the Police Officers Association threatened to march on her office unless she agreed to call for the death penalty.</P>
<P>Harris was recently elected on an anti-death penalty platform. Several groups, including Death Penalty Focus, Murder Victims' Families for reconciliation and the Campaign to End the Death Penalty, held a press conference to support Harris' decision on May 5, and more than 150 anti-death penalty activists and local politicians protested on the steps of city hall.</P>
<P>Speakers included City Supervisor Tom Ammiano, actor and activist Mike Farrell and many others. "Ever since [San Francisco police officer Isaac Espinoza] died, they've been jumping out on everybody," said Tanya Fowler, another witness to Boyd's murder. "The police are supposed to protect, but what they did here, that's off the hook."</P>

<P><I>Community activists are planning a May 14 press conference at 2 p.m. at the Green House, 4929 3rd St. The Idriss Stelley Foundation, an organization that supports families whose children have been beaten, disabled or killed as a result of police brutality, is sponsoring the event. All opposed to police brutality are welcome to attend.</I></P>

