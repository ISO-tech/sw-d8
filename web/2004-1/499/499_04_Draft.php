<P><font face="Times New Roman, Times, serif" size="4">Government could have the system ready by 2005</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bringing back the draft</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 14, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I have read that the current agenda of the U.S. government is to reinstate the draft. Pending legislation in the House and Senate (twin bills S89 and HR163) would time the program so the draft could begin as early as spring of 2005.</P>
<P>If voters who currently support U.S. aggression abroad were confronted with the possibility that their own children or grandchildren might not have a say about whether to fight, many of these same voters might change their mind. </P>
<P>A total of $28 million has been added to the 2004 Selective Service System (SSS) budget to prepare for a military draft that could start as early as June 15, 2005. SSS must report to Bush on March 31, 2005 that the system, which has lain dormant for decades, is ready for activation. </P>
<P>The Pentagon has quietly begun a public campaign to fill all 10,350 draft board positions and 11,070 appeals board slots nationwide. Though this is an unpopular election year topic, military experts and influential members of Congress are suggesting that if Rumsfeld's prediction of a "long, hard slog" in Iraq and Afghanistan (and a permanent state of war on "terrorism") proves accurate, the U.S. may have no choice but to draft.</P>
<P>Dodging the draft will be more difficult than those from the Vietnam era remember. In December 2001, Canada and the U.S. signed a "Smart Border Declaration," which could be used to keep would-be draft dodgers in. Signed by Canada's Foreign Affairs Minister John Manley and U.S. Homeland Security czar Tom Ridge, the declaration involves a 30-point plan that implements, among other things, a "pre-clearance agreement" of people entering and departing each country. </P>
<P>Reforms are being made to make the draft more equitable along gender and class lines and to eliminate higher education as a shelter. Underclassmen would only be able to postpone service until the end of their current semester. Seniors would have until the end of the academic year.</P>
<P>Millions of people protested the war in Iraq, and we need representation. I don't believe that the draft is going to be equitable in selecting a cross-section of wealthy, middle class and the poor. I don't believe the assignments to civilian capacity will be equitable. I am afraid the new wave of employment will come in the wave of forced military service for men and women.<BR>
<B>Linda Zoblotsky, </B>New York City</P>


