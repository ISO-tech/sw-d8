<P><font face="Times New Roman, Times, serif" size="5"><b>Nader campaign deserves a serious look</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 14, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Ben Dalbey and Bill Linville raise two objections to endorsing Ralph Nader's campaign: Nader's appeal to "conservatives" and his support for some sort of United Nations (UN) "security force" replacing the U.S. in Iraq. These are real problems that will weaken Nader's campaign, and socialists should be very critical of Nader for taking these positions. But I don't believe they necessarily preclude an endorsement.</P>
<P>First, Nader's appeal to "conservatives" is just plain foolish. It is one thing to appeal to workers who voted for Bush because they were so disgusted with Clinton that they wanted "change." It is another thing, as Nader does, to appeal to ideologically committed Republicans on the basis of "fiscal responsibility," which is their main rallying cry to cut social spending. If Nader's appeal to conservatives becomes a central part of his campaign, then Ben and Bill are correct, and we cannot endorse him.</P>
<P>Second, as socialists, we support the right of Iraqis to resist the colonial occupation, their right to self-determination and the immediate withdrawal of all U.S. and foreign troops be they from NATO or the UN. Nader does not agree fully with this position.</P>
<P>He is putting forward a plan to withdraw all U.S. troops in six months and replace them with some sort of UN security force while free elections are organized. But he also supports ending all U.S. corporate contracts in Iraq and providing Iraqis economic and humanitarian support as reparations for all the damage that the U.S. has committed. Achieving even these partial victories would require an enormous retreat for American imperialism.</P>
<P>Let's be clear. Nader is not a socialist, and we should be openly critical of his proposed half-measures in Iraq. But we should consider endorsing him if he galvanizes a significant layer of students and workers to challenge the Democrats from the left.</P>
<P>The torrent of abuse heaped on Nader from liberal Democrats like Howard Dean and their supposed "independent" supporters such as Michael Moore and The Nation magazine shows just how hard it is to stand up against the two-party system. Today, that means opposing the bipartisan occupation of Iraq and U.S. domination of the Middle East. Unlike Dennis Kucinich who uses his stated opposition to the occupation to stump for votes for John Kerry, Nader is trying to convince people to vote against Kerry.</P>
<P>No one has a crystal ball, but imagine a situation in October where the Iraqi resistance is surging, American combat deaths reach 1,000, even more U.S. torture scandels are exposed, and the economy remains stagnant. John Kerry continues to "out Bush Bush" by calling for more troops and more tax breaks for the rich. Nader is saying "Get the U.S. out in six months, cancel all of Halliburton's contracts, raise taxes on the corporations, support gay marriage and abortion rights, and repeal Taft-Hartley." </P>
<P>That is a campaign that could rally hundreds of thousands of people who are sick of Democrats supporting the wars we march against, and then demanding our votes. That is why the Democrats are trying so hard to keep Nader off the ballot--and that is why we should support his right to be on all state ballots, and then prove in practice that he deserves our votes.<BR>
<B>Todd Chretien, </B>Oakland, Calf.</P>


