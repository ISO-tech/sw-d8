<P><font face="Times New Roman, Times, serif" size="5"><b>Setback in Bush's overtime scam</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN THE latest skirmish in the yearlong battle over workers' right to overtime, the Bush administration suffered a setback on May 4. Five Republican senators jumped ship to support a measure that would amend the administration's planned new overtime rules so that they wouldn't reclassify workers who are currently eligible.</P>
<P>But the fight to stop the regulations--which could leave some 8 million workers without overtime rights--still has a long way to go. Last week's Senate vote is only a first step in blocking them. Next, the amendment, sponsored by Sen. Tom Harkin (D-Iowa) and attached to the Foreign Sales Corporation tax bill, must pass the House of Representatives before it goes to the president for a veto.</P>
<P>The new rules were released last week and are scheduled to go into effect August 23, unless Congress takes action. According to estimates, literally millions of workers could be reclassified as "professional" or "managerial" in order to deny them the right to time-and-a-half pay after 40 hours of work.</P>
<P>Unfortunately, the AFL-CIO is looking to the November election as the focus for overturning these anti-worker rules--even though the changes are scheduled to go into effect in August! We can't wait until November. Our unions must take on Corporate America's attack on our overtime--now!</P>


