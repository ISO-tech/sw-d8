<P><font face="Times New Roman, Times, serif" size="4"><b>April 25 was an inspiring step</b></font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I was pleased with your analysis of the April 25 March for Women's Lives ("A march for women's lives or Democrats' votes?" <I>SW,</I> April 30). I certainly shared in the disgust when, watching coverage from San Diego, I saw war hawk Madeleine Albright take the stage.</P>
<P>I'm concerned, however, that we could be too pessimistic about a march undoubtedly dominated by "Anybody But Bush" politics and ultimately miss the overriding message of the day: Women are not going to accept attacks on our rights--and we will mobilize in historic numbers to prove it.</P>
<P>Everyone I spoke to about the march here spoke with enthusiasm, from activists who went to D.C. to those who were here at the local march in San Diego. Overwhelmingly, I believe people will be more inspired to fight for their rights after that historic event.</P>
<P>The march of 1 million should have been on the front cover of <I>SW</I>, highlighting a promising first step for a new fighting abortion rights movement and offering a socialist analysis of the way this spirit of resistance can be carried forward.<BR>
<B>Rebecca Anshell,</B> San Diego</P>

