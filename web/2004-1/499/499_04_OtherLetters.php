<p><a name="voting"></a><font face="Times New Roman, Times, serif" size="4"><b>Voting Democrat won't bring change</b></font></P>
<B>
<P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In what might be a surprise to many left-wing activists, Noam Chomsky, renown and revered critic of U.S. politics, recently came out with support for Democratic candidate John Kerry. As reported in the British <I>Guardian</I>, Chomsky said that Bush and Kerry are "two factions of the business party," but that the Bush administration is so "cruel and savage" that it is "important to replace it."</P>
<P>Chomsky is not the only prominent and respected left-wing figure to suffer from the "Anybody But Bush" syndrome. Norman Solomon and Michael Moore have argued in favor of voting Democrat. </P>
<P>This fear of Bush's reelection is something that is shared among average activists as well. As this unfounded and irrational support of Kerry and the Democrat Party grows, we need to argue strongly against feeding this irrational disease.</P>
<P>Socialists need to remind people that Democrats are not the party of the working class. We need to bring up Clinton's failed promises on gay rights, the environment, welfare reform and other issues that leftists put their faith in as a hope to get rid of Bush Sr. in 1992.</P>
<P>Also, we need to tell others suffering from this Democratic delusion about Kerry's equally flawed record on the issues. Kerry voted for the USA PATRIOT Act, he voted for the war in Iraq and supports a continued occupation, promising to send 40,000 more troops. He supports the continued "war on terror" as well as our occupation of Afghanistan. He supports more tax breaks for the rich and opposes gay marriage.</P>
<P>Voting for a Democrat on Election Day is not going to bring about real social and political change. Only through strong organization and protest against the system will we win the day, despite whichever of the two business parties are in the White House.<BR>
<B>Jeremy Hobbs,</B> Riverside, Calif.</P>

