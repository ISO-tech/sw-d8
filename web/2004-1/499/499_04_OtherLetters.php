<p><a name="hopeless"></a><font face="Times New Roman, Times, serif" size="4"><b>Kucinich isn't a hopeless cause</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I agree wholeheartedly with Lance Selfa ("Why is Kucinich still in the race?" <I>SW</I>, April 23) that the Democratic Party is "a bosses' party that occasionally (and less frequently) uses populist rhetoric to get enough workers, women and racial minorities to vote for it to help it win elections." Personally, I think that the Democratic Party is a hopeless cause. </P>
<P>However, at this moment in American history, I see no "real political alternative" to the powerful Republocrat duopoly. Trying to fight money and entrenched political power structure from "outside the Democratic Party" is like trying to fight tanks and gunships with stones.</P>
<P>In my opinion, Dennis Kucinich's continued candidacy can make a (slight) difference and is therefore worth the effort.  As long as the Democratic Party gets away with taking progressives for granted, life for the Average Joe and Jane will only get worse and worse. </P>
<P>Without continual pressure, the Democratic Party power brokers can rightly claim that there is no progressive wing of the party, at least none worth worrying about, and that the party's shift to the right is in tune with the overwhelming majority of its members.</P>
<P>Unfortunately, too many progressive voters have succumbed to "anybody but Bush" fever this year, and they prematurely jumped on the (Bush-lite) Kerry bandwagon. At least those who stand with Kucinich are showing some backbone by voting their principles--and ultimately their own best interest--in the primaries and caucuses. I applaud them for realizing that there will be plenty of time to settle for the "lesser evil"--or to jump ship--after the Democratic convention.<BR>
<B>Mary Johnson,</B> From the Internet</P>

