<p><a name="terror"></a><font face="Times New Roman, Times, serif" size="4"><b>The real purveyors of violence</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In his article, "The greater terror of U.S. power," (<I>SW</I>, April 9) Paul D'Amato pins down the two key truths that expose the so-called "War on Terror" for the lie that it is. Firstly, as D'Amato points out, the whole concept of a "war on terror" is logically absurd. </P>
<P>Terrorism is a tactic, not a political movement or ideology in its own right. It is used by a variety of groups for a variety of reasons, and does not constitute some kind of monolithic "force of evil" at loose in the world. Secondly, the greatest purveyors of terrorism in the world today are not paramilitary organization like Hamas or Hezbollah, but nation states. </P>
<P>The fact that al-Qaeda is recognized as a terrorist organization while the U.S. military is not results from the fact that the U.S., as a nation state, can claim a monopoly of violence, and therefore pass off its use of terror as legitimate.</P>
<P>I also appreciated the irony of that fact that while Islamic fundamentalists who set off bombs on airplanes are considered "terrorists," Christian fundamentalists who blow up women's health clinics are not. D'Amato's humane, clear-headed article provides a good example of why alternative media like <I>Socialist Worker</I> are so desperately needed today.<BR>
<B>Dennis Fritz,</B> Austin, Texas</P>

