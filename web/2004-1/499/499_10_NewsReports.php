<p><a name="Crusade"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop bigotry at Southern Connecticut</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Rebecca J. Lewis </b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--Last week on the campus of Southern Connecticut State University (SCSU), the student leader of the Black Student Union was attacked twice by anti-gay bigots. Leading up to this incident, several other students had been harassed by members of Campus Crusade for Christ (CCC).</P>
<P>Members of CCC have felt confident enough in recent weeks to publish articles in our school newspaper entitled "Homosexuals are Mutants," and "Women are Subservient to Men," creating an actively sexist and homophobic environment and lending confidence to the gay-bashers. Incredibly, CCC has received $500 from SCSU's Student Government Association for being the "Most Active Club on Campus."</P>
<P>An emergency rally is being organized by leaders from the NAACP and others for May 13 to show our outrage at this incident of violence and give confidence to people to stand up to the bigots. We will send a message to the bashers, the CCC, and our administration that these attacks will not be tolerated. </P>
<P>Our campus will not be a safe haven for gay-bashers, and we won't put up with this harassment. We will stand up to the bigots and let them know that their harassment and violence are not welcome on our campus.</P>

