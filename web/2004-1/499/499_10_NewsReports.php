<P><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage is a civil right</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By David Alt and David Thurston</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">SUPPORTERS OF gay marriage rights held lively meetings last week to build awareness of the upcoming legalization of gay marriage in Massachusetts. In San Francisco, New Paltz, N.Y. Mayor Jason West, who performed gay weddings before being forced to stop by officials was among the speakers of a meeting of 65 people. Kathryn Lybarger, recently married in San Francisco, said of her experience, "It's like coming up from under water to take a breath of air for the first time.</P>
<P>Mayor West spoke of having married people who had waited 30 years to put on their wedding rings. The breadth of the showing of support was summed up by Green Party candidate for governor, Peter Camejo, who said, "This issue belongs to everyone, so let's work together," and identified this struggle as a "turning point in history."</P>
<P>In New York, about 70 people came to a town hall meeting May 6 on gay marriage held at Hostos Community College in the Bronx. A member of the U.S. Congress and several city councilmembers spoke in favor of gay marriage and condemned George W. Bush's drive for a constitutional ban. The meeting was organized by the Empire State Pride Agenda, the leading statewide lesbian, gay, bisexual and transgender lobbying group, and by local Latino activists.</P>
<P>It was conceived in part as a direct response to a recent 5,000-strong antigay rally in the Bronx. Unfortunately, the event seemed more focused on turning out the vote for Democrats in November, than on building an ongoing movement.</P>

