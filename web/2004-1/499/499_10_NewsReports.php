<p><a name="Teens"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend lesbian and gay teens</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Leighton Christiansen</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">IOWA CITY, Iowa.--Raging homophobe Fred Phelps is coming to Iowa to picket two high school graduation ceremonies this month. Phelps and his Westboro Baptist Church have become infamous among gay rights activists for protesting at Matthew Shepard's funeral.</P>
<P>Shepard was killed in a brutal gay bashing outside Laramie, Wyo., in 1998. Phelps will be protesting two Iowa recipients of the Matthew Shepard Scholarship, awarded to openly lesbian or gay high school seniors who are involved in the struggle for gay rights in their schools.</P>
<P>Besides picketing the May 29 graduation ceremony, Phelps is planning to bring his bigot brigade to picket outside City High School Tuesday May 18. And he will picket the May 22nd graduation ceremony of Roosevelt High School in Des Moines, where another gay teen won a Matthew Shepard Scholarship.</P>
<P>Sixteen-year-old Ilse Bendorf of City High is the 26th Iowa senior to win the scholarship. Ilse, a member of GLOW (Gay, Lesbian, Or Whatever), told <I>Socialist Worker </I>that since news of the Phelps announcement became public, "support has been overwhelming. So many people are eager to do something."</P>
<P>"Phelps has such an extreme point of view," Ilse said, "that it's rallying support in my school. My family and I want any response to be a strong but peaceful statement that our community does not tolerate any kind of hate." We need hundreds of Iowans to turn out and show Phelps his bigotry is not welcome here.</P>

