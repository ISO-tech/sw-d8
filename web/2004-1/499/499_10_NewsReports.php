<p><a name="SF"></a><font face="Times New Roman, Times, serif" size="4"><b>San Francisco State budget cuts</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Levine and Leigh Smith</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--At San Francisco State University, a meeting May 6 to organize against Gov. Arnold Schwarzenegger's attack on education drew a crowd of 75 furious students--from dance to holistic health majors--whose futures are being threatened by budget cuts. Various departments and clubs, including the social work department, Students against War, the Campaign to End the Death Penalty and the California Faculty Association, gathered to focus frustration and anger into an organized movement with one strong point of unity: "No cuts to education,"</P>
<P>While the majority of students recognized the part Schwarzenegger plays in deciding what little money will be given to our schools, there was a nearly unanimous vote to stay on campus and target the "Governator" while simultaneously challenging President Robert Corrigan, who ultimately decides how the money will be spent.</P>
<P>Students plan to walk out of class May 12 for a strike and rally on the campus quad in protest. But this is just the first step in confronting the cuts. We need a series of strong rallies, sit-ins and strikes that make it impossible for Corrigan and Schwarzenegger to ignore our demands.</P>


