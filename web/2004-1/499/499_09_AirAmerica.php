<P><font face="Times New Roman, Times, serif" size="4">Air America radio has a long way to go</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Liberals on the AM dial</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE OF the building blocks of the current Republican control of the government has been conservative domination of the talk radio airwaves. But starting in late March, liberals got their spot on the AM dial with the launch of Air America Radio.</P>
<P>Air America, a venture of Democratic Party-affiliated investors including former Vice President Al Gore, currently broadcasts on 11 AM stations in major media markets like New York, Chicago and Southern California and on XM and Internet radio. In May, it plans to add 15 more stations to its network.</P>
<P>Since going on the air, Air America has been besieged by management woes, the latest being the resignation of its chair and vice chair. Conservatives have tried to make hay out of these difficulties, but it's easy for them to make fun, since they're so well-funded, they can just throw money at any problem.</P>
<P>It's refreshing to hear a voice on the radio that goes after the lying thugs in the White House and their abettors in the media day after day. The tone of most Air America programs departs from the sleep-inducing "centrism" of National Public Radio. The typical Air America day leads off with <I>Morning Sedition,</I> a drive-time talk show hosted by Mark Riley, Sue Ellicott and Marc Maron.</P>
<P>Air America's attempts to reach its audience through the medium of comedy and pop culture--featuring figures like comedy writer and author Al Franken co-hosting <I>The O'Franken Factor,</I> hip hop pioneer Chuck D or comedian Janeane Garofalo--is commendable. Some of the best content on Air America comes in the form of satirical pieces, like the parody advertisement for right-wing ABC <I>20/20</I> correspondent John Stossel: "Before John Stossel came along, American corporations had no one in the media to stand up for them."</P>
<P>Unfortunately, the talk radio pressure to be "funny" and the network's determination not to be "politically correct" also produces a lot of clunkers--and some downright objectionable material. One morning in April, the <I>Morning Sedition</I> team spent a half-in-jest segment discussing whether feminist women were ugly or not.</P>
<P>The female host sneered about feminists' successful opposition to a sexist beer ad "jokingly" asking, "Were the women ugly fat dykes?" "[Saudi] Prince Bandar has made the Bushes his bitches," said humorist Maron on April 28.</P>
<P>Another annoying trait of almost all of Air America's hosts is their penchant to describe ordinary Americans who support Bush as dumb, lazy or duped by Bush's media spin-doctors.</P>
<P>But the low points on Air America aren't just a question of style. They are questions of politics. As a liberal venture set up, in part, to promote the Democratic ticket in November, it's firmly committed to expressing a politics acceptable to the liberal end of the status quo.</P>
<P>For example, one of the lowlights of the network's first week was Randi Rhodes hectoring, Bill O'Reilly-like phone interview with independent presidential candidate Ralph Nader. As Nader tried to explain his reasons for running, Rhodes continually shouted "We can't afford you, Ralph" until Nader hung up on the interview.</P>
<P>Its critique of the war in Iraq and the "war on terrorism" emphasizes, as Democratic presidential candidate John Kerry does, Bush's bungling--without calling for the U.S. to get out of Iraq. The day after crowds in Falluja killed four U.S. mercenaries, former NATO commander and presidential candidate Wesley Clark appeared on the network to urge a strong military response--with fawning agreement from his <I>Morning Sedition</I> hosts.</P>
<P>In contrast, the more politically radical Pacifica Radio show, <I>Democracy Now!</I>, interviewed Iraq-based activists like Rahul Mahajan and Naomi Klein, who described the reality of the U.S. attacks from the point of view of Iraqi civilian victims.</P>
<P>As a challenger to the right-wing domination of the airwaves--where Rush Limbaugh infests more than 500 radio stations across the country--Air America has a long way to go. But breaking the right-wing stranglehold on politics depends on what happens in the streets and the workplaces, not on what's playing on the radio.</P>


