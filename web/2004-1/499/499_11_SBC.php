<font face="Times New Roman, Times, serif" size="5"><b>CWA ducks SBC strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">UNIONS REPRESENTING more than 110,000 workers at telecommunications company SBC agreed to work beyond a May 8 strike deadline with a day-by-day contract extension during negotiations brokered by a federal mediator. SBC, formerly known as Southwestern Bell, has absorbed the former Pacific Bell in California and Ameritech regional phone company in the Midwest to become an industry giant.</P>
<P>Having promised job security to win union support and regulatory approval for those mergers in the late 1990s, the company has eliminated 20,000 jobs over the last three years through layoffs, outsourcing and attrition. In Illinois, for example, where the International Brotherhood of Electrical Workers (IBEW) Local 21 represents 11,300 workers, the company pushed through 600 layoffs in late 2002 alone.</P>
<P>The dominant union at SBC is the Communications Workers of America (CWA), which represents more than 102,000 workers at the company. CWA President Morton Bahr announced that because SBC had backed off demands to force retirees to pay between $300 million and $600 million for their health insurance, the union would continue to negotiate "as a show of good faith" rather than walk out.</P>
<P>That preliminary agreement came during SBC's national meeting in Columbus, Ohio, where 1,000 SBC workers were set to protest the company's top management. Instead, the workers attended a CWA-organized presentation by talk show host Jerry Springer at a nearby hotel.</P>
<P>Union leaders' decision to work through the strike deadline is a repeat of the CWA's and IBEW's tactics at Verizon a year ago, when the union ordered members to stay on the job and kept them in the dark while a deal was cut. The Verizon agreement preserved job security won in an earlier arbitrator's decision, but excluded new hires from that provision and included givebacks on health insurance coverage--concessions worth $1 billion, according to Verizon executives. At SBC, the unions don't have the leverage of the job security agreement, making the fight against outsourcing that much tougher.</P>
<P>Health care costs are the other major issue. SBC wants to double or more the co-pays paid by employees to the level of 12 percent of total medical expenses. While the employers would continue to pay insurance premiums, workers would see their out-of-pocket expenses jump dramatically--particularly for prescription drugs, where management is demanding a three-tiered system.</P>
<P>Though the CWA and IBEW mobilized for a strike, action has now been put on hold in the hope that management will withdraw its harshest demands. This approach risks a replay of Verizon, where management squeezed workers for weeks until union leaders agreed to much of what management wanted--even though the company was making record profits.</P>
<P>By working through the contract deadline once again, the unions are undermining their biggest weapon. A strike against--or lockout by--SBC would be a long struggle, given automation and management's certain attempts to run a scabbing operation. A failure by the union to draw the line now, however, will only open the way for further decline in union membership and leverage at the company.</P>


