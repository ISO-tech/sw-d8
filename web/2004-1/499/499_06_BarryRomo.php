<P><font face="Times New Roman, Times, serif" size="5"><b>"Trained to kill for a political purpose"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 14, 2004 | Pages 6 and 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BARRY ROMO is a former infantry lieutenant who served in Vietnam from 1966 to 1968. He returned home and became an antiwar activist as a leading member of Vietnam Veterans Against the War.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>HOW IS it that soldiers commit crimes like the torture at Abu Ghraib prison in Iraq?</B></P>

<P>I THINK that the level of frustration when they believed their politicians and religious leaders and the people on television is something that grows within them--when they think they're doing something right, and then the population itself rejects them. So the question comes down to how do you survive your tour of duty?</P>
<P>The question of training goes without saying. If you get in a fight with the guy next door, your mom and/or your father are going to deal with you for getting in a fight. You're not allowed to fight in the schoolyard grounds. You're supposed to work with other people and not interfere with them.</P>
<P>But when you go into the military, you have to be retrained--you have to be reoriented. You have to put all that stuff behind you--about "what are the norms of the civilized person"--because you have to be put in a situation where you're willing to kill other people for a political purpose, not because you're actually defending your "homeland."</P>

<P><B>FROM YOUR own experiences in Vietnam and dealing with veterans of wars since then, how are U.S. soldiers trained to view the population of the country they're at war with?</B></P>

<P>THERE'S TWO things. One, these troops weren't told that they were going into Iraq to liberate Iraq. They were told they were going into Iraq because Saddam Hussein had weapons of mass destruction and was an immediate threat to its neighbors and to the United States.</P>
<P>Then, after they took Baghdad, the justification sort of changed--when there were no weapons of mass destruction and no anthrax, and it became clear that he couldn't have invaded anybody. We're there now for "freedom," but we're also there now because Americans have died, and "we can't let them die in vain."</P>
<P>As far as how you view the people, you're trained to be assault soldiers--or trained to be supporters of those assault soldiers--and that means fighting an enemy army. Most of the people who are doing military police work aren't trained to be military police. They're trained to be anything from combat engineers to artillerymen.</P>
<P>The other thing is that the military wasn't trained to deal with a civilian population. They were trained in conventional warfare to deal with Saddam Hussein's army. Now, they're being asked to fulfill a task which is one of insurrection and guerrilla warfare, and the overwhelming majority of those people have no training in that.</P>
<P>That means that they have to walk down the street and get shot at. So they start to look at the entire population as a threat to them and their buddies. That's where you start to reach that thing of unit cohesion, and think, "I'd rather make a mistake and shoot a civilian, and have my friends to the right and left of me come home, than to live the rest of my life knowing that I could have saved Johnny or Betty's life, and I didn't do it."</P>

<P><B>GEORGE MCGOVERN said that the My Lai massacre "ripped the mask off" Vietnam. Do you think that this scandal has the potential to do that for the war in Iraq?</B></P>

<P>I THINK that's true. They've already announced that they're going to put people on trial in just a couple of weeks. I think that's going to lead to a whole bunch of demoralization among the troops. You're going to have a lot of families asking about their little boy and girl next door and saying, "We gave them to the military, and look what they ended up doing."</P>
<P>I think that the potential is there, especially when there's daily bombings and killings of American troops. I'm amazed at the level of rejection of the war in such a short amount of time. </P>
<P>Most people are like, "Oh my gosh, the movement isn't that big." Well, the movement wasn't that big when Vietnam started, and the movement wasn't that big when there was this many casualties, and the movement wasn't that big when there was this many troops that were overseas in Vietnam.</P>
<P>So, for me, it's kind of an amazing thing to watch the amount of outrage. Vietnam Vets Against the War was formed in 1967, but it didn't become a massive national organization with impact until 1971. And never during the Vietnam War was there anything like Military Families Speak Out. Most of the parents whose kids were killed joined VVAW. In this, we see 1,500 families in less than two years. What an amazing number to have by this time.</P>

