<P><font face="Times New Roman, Times, serif" size="4">Morning-after pill rejected</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>FDA caves in to bigots' pressure</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 14, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THEY SAID it was in the interest of young women's health. Overruling a decision made just five months ago, the Food and Drug Administration (FDA) announced May 6 that it was rejecting over-the-counter sales of the morning-after pill.</P>
<P>The morning-after pill--emergency contraception that is also called "Plan B"--is a higher dose of regular hormonal contraception that, if taken within 72 hours after unprotected intercourse, can cut a woman's chances of pregnancy by up to 89 percent. It has been available by prescription since 1999.</P>
<P>In December, the FDA's scientific advisers voted 23-4 to make the pills available without a prescription. Emergency contraception advocates say that easier access to "Plan B" could reduce the country's 3 million unintended pregnancies each year by as much as half. But the FDA caved, under pressure from right wingers.</P>
<P>The FDA now claims it is worried that research hadn't proven the drug was safe for teenagers. In a letter to Barr Pharmaceuticals, the company that planned to make the drug available over the counter, the FDA said that Barr hadn't provided "adequate data to support a conclusion that Plan B can be used safely by young adolescent women for emergency contraception without the professional supervision of a practitioner."</P>
<P>Anti-choice bigots were delighted to see the FDA collapse. "The FDA is right to be cautious about having a potent drug that can harm women next to candy bars and toothpaste," said Wendy Wright of the anti-abortion group Concerned Women for America.</P>
<P>But Dr. Paul Blumenthal, an obstetrician-gynecologist at Johns Hopkins Hospital explained to the <I>Chicago Tribune </I>that Plan B wasn't toxic or addictive--and that there's no need for medical screening to use it. "A teen knows she has had sex with a broken condom or an absent condom just as well as an older person," Blumenthal said.</P>
<P>In fact, in 2000, France passed a law allowing high school nurses to provide emergency contraception--and later ordered pharmacies to give it to minors free and without parental permission. Since then, almost 1.5 million non-prescription treatments have been sold, and there have been no reports of medical complications or complaints, according to the French Association for Contraception.</P>
<P>Emergency contraception is available without a prescription in 33 countries, and five states--California, Washington, Alaska, Hawaii and New Mexico--allow women to buy morning-after pills from certain pharmacists without a prescription.</P>
<P>Contrary to the claims of the anti-abortion fanatics in the Bush administration, this isn't about protecting young women. The FDA's decision is about limiting their reproductive choices. Women--no matter what their age--should have easy access to emergency contraception. The FDA should be ashamed of itself.</P>


