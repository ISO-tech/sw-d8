<P><font face="Times New Roman, Times, serif" size="4">Cuts come on eve of union elections</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will the CTU fight layoffs?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 4, 2004 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Arne Duncan, CEO of Chicago Public Schools, announced last week that the district plans to ax 1,600 employees. </P>
<P>Hardest hit will be security guards and classroom aides who are paid out of discretionary funding. Because most discretionary funding goes to the lowest performing schools--probationary schools--these cuts will have the most devastating impact on the poorest schools. Only 130 teachers will be cut, but the impact on the educational environment--especially at schools with large special education populations or security issues--will be significant.</P>
<P>Chicago Teachers Union (CTU) President Deborah Lynch said, "In a cruel hoax, CPS has raised [the number of] probation schools. Instead of providing help to the neediest schools, they are using their new powers to cut positions." The <I>Chicago Tribune </I>quoted Lynch as saying, "This is the end of labor peace."</P>
<P>But it remains to be seen whether this tough talk is simply election rhetoric--coming just days before the June 11 run-off vote for leadership of the union. Lynch, who was elected in 2001 as a reformer, failed to win a majority in the first round of the vote as nearly 60 percent of union members backed other candidates. Abstention was high. The election results are being driven by last years' contract fight, in which the rank and file voted to reject Lynch's proposed contract before voting to accept a deal that contained givebacks.</P>
<P>Now the militant talk is partly designed to show that Lynch has learned a lesson. For example, the union has put out a six-point plan for combating layoffs which includes meeting with other affected unions and picketing a school board meeting. Unfortunately, none of the plans involve rank-and-file teachers or even occur at times when teachers can attend! Lynch's rival from the union's old guard, Marilyn Stewart, has no plan to fight the layoffs, either.</P>
<P>Rank-and-file teachers are organizing to mobilize pickets against the layoffs without backing from any presidential candidate. It appears that what's really needed to bring the union forward won't be on the ballot June 11.</P>

