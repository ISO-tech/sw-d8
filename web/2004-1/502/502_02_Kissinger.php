<P><font face="Times New Roman, Times, serif" size="5"><b>How they cover up atrocities</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 4, 2004 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>BLAMING WARTIME atrocities on a few low-ranking "bad apples" is standard operating procedure for the U.S. government. The Nixon administration did the same when the truth emerged--in the form of gruesome photos published by the media--about the My Lai massacre during the Vietnam War.</P>
<P>According to documents obtained by the National Security Archive--a nonprofit research center at George Washington University--Nixon's chief foreign policy fixer, National Security Adviser Henry Kissinger, personally concocted the strategy to respond to My Lai.</P>
<P>According to the transcript, Kissinger and Defense Secretary Melvin Laird initially discussed trying to suppress photos of the U.S. slaughter of 347 men, women and children in the Vietnamese village of My Lai. But the media already had the pictures. Laird, according to a memo summarizing the conversation, "said about a game plan, he'd like to sweep the whole thing under the rug, but you can't do that." </P>
<P>So Kissinger and Laird decided that the administration should act outraged--and blame the low-ranking officers.</P>

