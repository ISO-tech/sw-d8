<p><a name="Antigay"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the antigay bigots</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>IOWA CITY, Iowa--More than 200 lesbians, gays and their supporters came from across eastern Iowa to protest antigay bigot Fred Phelps on May 29. Twenty-two members of Phelps' Westboro Baptist Church came to protest at City High School's graduation ceremony because a lesbian student was awarded a Matthew Shepard Scholarship.</P>
<P>While the turnout was great, it could have been larger. Before the event, debate raged among gay rights supporters about how to respond. One group, Hate Acts Rapid Response Network (HARRT), asked people to wear school colors, bring non-political signs congratulating the graduates and encouraged people to turn their backs on Phelps' gang. When other activists tried to chant over Westboro's hate messages; HARRT leaders shushed and gay-baited them.</P>
<P>Others argued for showing respect for the graduation ceremony by staying away. This meant that leading members of the University of Iowa's Gay, Lesbian, Bisexual, Transgender and Allies Union didn't show at all.</P>
<P>Nevertheless, as one graduating senior, Stephanie, told <I>Socialist Worker</I>, she was surprised by the large number of gay rights supporters. "This is a great thing for all of them to do," she said.</P>
<P>Supporters of a more vocal response were accused of not considering the wishes of the students and their families. But when antigay bigots like Phelps come to town with their "God Hates Fags" signs, it becomes more than an attack on one lesbian student. If Phelps is allowed to appear while gay rights supporters stand silently by, it only encourages gay-bashers.</P>
<P>If gays and lesbians are to gain equal rights--including the right to marry--we have to be prepared to face to face with hate mongers and force them to back down.</P>

