<P><font face="Times New Roman, Times, serif" size="4"><b>Providence, R.I., public schools</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Chidester, Bristol-Warren Education Association</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>PROVIDENCE, R.I.--About 200 people rallied in front of the State House on May 25 to demand better funding for Providence Public Schools and to celebrate the governor's retreat on cuts to state education spending. </P>
<P>The rally was sponsored by Save Our Schools (SOS), a parent organization, and the Association of Community Organizations for Reform Now (ACORN). Speakers included parents, students, teachers, and politicians, including Providence Teachers' Union president and state representative Steven Smith. </P>
<P>In February, Rhode Island Governor Don Carcieri--a hard right Republican--proposed $8 million in cuts to state education spending, while increasing state aid to charter schools by an almost equal amount. On the chopping block: music, arts, foreign languages, and elementary school counselors. On the day of the rally, Carcieri magically "found" $45 million extra in the state budget and rescinded the cuts--but then proposed ridiculous new "fiscal accountability standards" for local school districts. </P>
<P>The rally was a good step toward building a movement for better public schools. Now we need to demand the repeal of George W. Bush's No Child Left Behind law, and put pressure on both Republicans and Democrats to fully fund our public schools.</P>

