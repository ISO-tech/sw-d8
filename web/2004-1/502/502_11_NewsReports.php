<p><a name="CSU"></a><font face="Times New Roman, Times, serif" size="4"><b>California State University budget cuts</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Scott Johnson</b></font></P>
<font face="Times New Roman, Times, serif" size="3">
<P>HAYWARD, Calif.--For the second time in less than a month, students at a Bay Area university walked out of classes to protest state budget cuts. </P>
<P>Inspired by the walkout of 1,500 students at San Francisco State University the week before, about 400 students at California State University (CSU)-Hayward rallied on May 26. This action came after the CSU system Chancellor Charles Reed agreed to raise tuition by 14 percent next year to offset the budget cuts. This follows a 40 percent tuition increase over the last year. It was also announced that thousands of students who qualified for admission would be turned away because the system couldn't afford to accept them. </P>
<P>Reed agreed to the budget cuts on Gov. Arnold Schwarzenegger's "promise" that there would be no cuts next year. But Sacramento has a history of making--and then breaking--these "promises."</P>
<P>Already, the budget has fallen short for this year. The work-study program ran out of money the week before the walkout, resulting in hundreds of layoffs of part-time work-study students. Fearing that financial aid would be cut too, a student at the rally commented, "Without financial aid, there is no way I am going to be here next year."</P>
<P>"Education is right, not a privilege for the rich," Diane Peterson from the Equal Opportunity Program told the crowd. After the rally, many students marched off campus to the main street in Hayward, where dozens of drivers honked their horns in support. The next event is a forum against budget cuts with Arianna Huffington, <I>American Idol </I>judge Randy Jackson and State Treasurer Phil Angelides.</P>

