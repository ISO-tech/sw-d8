<p><a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>DAVIS, Calif.--Nearly 60 women and men rallied here for a woman's right to choose on May 26. Demonstrators carried signs calling to "Keep Abortion Safe and Legal" and "Reproductive Justice For All."</P>
<P>University of California sophomore Sarah Barnes denounced restrictions placed on abortion access that disproportionately effect the poor and women of color. Barnes called for "an end to attacks on affordable health care and women's reproductive rights."</P>
<P>Choice USA, the International Socialist Organization (ISO) and the National Organization sponsored the rally for Women. That night, 30 students and community members gathered on campus for a panel meeting to discuss strategies for saving abortion rights in the U.S. Many debated the merits of the Democrat John Kerry campaign in the context of preserving access to abortion. The panel featured Jenny Olson of the ISO and Solana Phillips of Choice USA.</P>

