<P><font face="Times New Roman, Times, serif" size="4">Georgia State suspends group for opposing bigots</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We won't "get over" racism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 4, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</B></I> <BR>
In January 2004, Pi Kappa Alpha at Georgia State University (GSU) held a so-called "hip-hop theme party" titled "Straight Outta Compton." Members of the mostly white, conservative fraternity showed up to represent every "hip-hop" stereotype. The party itself was racist enough, but what made it infamous was the appearance of two white Pi Kappa members in black face, supposedly dressed as their favorite rappers.</P>
<P>Afterwards, there was an outburst of antiracism on campus, with rallies and speakouts against the racist party. The frat was later convicted of violating university rules, but was only suspended until the end of fall semester 2004.</P>
<P>Then, the lone Black member of the fraternity filed suit against GSU's Black Student Alliance (BSA). The BSA was convicted of "discriminatory harassment," and "intent to incite others by making a misleading flyer." The unbelievable charge of "discrimination" was the result of students allegedly calling the Pi Kappa member "Uncle Tom" at an open university forum. But it's not even clear who made the statement, or if they are even members of the BSA. </P>
<P>The "misleading" flyer featured a picture of white men wearing black face to demonstrate what black face is. The university cites a BSA member's statement of wanting to "push the issue" as its strongest evidence of intention to "incite."</P>
<P>In April, the administration recommended that the BSA be suspended until the end of fall semester 2004--the same suspension that Pi Kappa received. Adding to the hypocrisy, the administration ruled that the BSA has to do "community service" and participate in a "diversity education" program.</P>
<P>The frat party itself has been increasingly trivialized on campus. Conservative students have been able to write demeaning, paternalistic articles in the school newspaper with little opposition. One such article was titled "The Black Student Alliance Just Can't Get Over It."</P>
<P>We can't get over the fact that the GSU student body is over 35 percent Black, and yet fewer than 10 percent of professors are Black. We can't get over the fact that there are only 18 Hispanic professors at GSU out of 1,100 total.<BR>
<B>Desmond Gardfrey, </B>Atlanta</P>

