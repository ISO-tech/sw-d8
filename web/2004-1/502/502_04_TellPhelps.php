<P><font face="Times New Roman, Times, serif" size="5"><b>Tell Phelps that he can't spew his bigotry</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 4, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I> <BR>
In 18 years of organizing in the Midwest, I have seen rallies rained out, snowed out and wind-chilled out. But May 21 was the first time I've seen one "tornadoed" out.</P>
<P>The Iowa City branch of the International Socialist Organization had put out a call for a rally to show solidarity with gay and lesbian couples marrying in Massachusetts, and to start to rebuild a fight for gay rights in general. We also wanted to get the word out that antigay fanatic Fred Phelps is coming to Iowa City High School's graduation on May 29 to spew his hate--and we want to build a protest against him.</P>
<P>Just as activists were assembling for the rally, the city's tornado warning sirens went off, forcing us to take shelter at the nearby recreation center. About 20 of us turned the weathered-out rally into a planning meeting to counter Phelps.</P>
<P>Though there was some debate about tactics, nearly everyone at the meeting felt that we must have a counter demonstration to confront Phelps. Unfortunately, other organized gay rights groups ignored a call to cosponsor the rally, and are planning to counter Phelps by silently standing with their backs to him and holding signs congratulating the graduates. They argue that a counter demonstration against Phelps with chants and political signs will ruin the spirit of graduation.</P>
<P>But it is Phelps' message that all gays and lesbians must die that will cast a dark cloud over the ceremonies.</P>
<P>Phelps is bringing his Westboro Church from Topeka, Kan., to protest City High's graduation ceremony because Ilse Bendorf, a lesbian student, was awarded the Matthew Shepard Scholarship this year in recognition of her activities to build a gay-positive atmosphere at City High. Phelps has turned Matthew Shepard's brutal death at the hands of anti-gay bigots into a cottage industry of hate, even marching outside Shepard's funeral with signs proclaiming "God hates fags."</P>
<P>We can best show our support for all lesbian and gay teens--and all fighters for lesbian and gay rights--by showing up in the largest possible numbers and countering Phelps' message.<BR>
<B>Leighton Christiansen, </B>Riverside, Iowa</P>

