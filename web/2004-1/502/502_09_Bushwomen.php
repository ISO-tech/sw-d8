<P><font face="Times New Roman, Times, serif" size="5"><b>Behind every Bushman, there's a Bushwoman</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 4, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Laura Flanders, <B><I>Bushwomen: Tales of a Cynical Species. </B></I>Verso, 2004, 160 pages, $22.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>A SUCCESSION of tell-all books have roundly exposed the Bushmen--George Bush, Dick Cheney and Donald Rumsfeld--as masters of the art of lying. Often overlooked are the whoppers told in "kinder, gentler" voices--Bush's female appointees.</P>
<P>Laura Flanders' <I>Bushwomen: Tales of a Cynical Species</I> lays bare deceptions that would make Goebbels blush. While Bush is busy butchering women's rights, they attempt to capture women voters by putting a "media-friendly face" on what Flanders calls "an extremist administration." These women owe their positions to affirmative action and other benefits from the women's and civil rights movements of the 1960s, a debt they repay by fighting for "freedom for corporations and liberty for Americans like themselves."</P>
<P>The Condoleezza Rice and Karen Hughes chapters are fascinating studies in hypocrisy. Rice is not only the first female national security advisor, but the first national security advisor to have a <I>New York Times </I>article begin with her choice of breakfast food and hemlines. </P>
<P>Hughes keeps Bush ruthlessly on message, and in her capacity as "counselor to the president"--a job created just for her--she literally serves as Bush's chief liar. Not only did Bush not say what he just said, Hughes tells the press, but he meant something entirely different.</P>
<P>Bush's female cabinet secretaries, as well as wife Laura and Lynn Cheney, all stand by their man by soft-peddling his true agenda and taking part in his excesses against humanity.</P>

