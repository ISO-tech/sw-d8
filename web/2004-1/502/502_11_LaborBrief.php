<p><a name="Madison"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Wisconsin-Madison</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--After a successful two-day strike in late April, the Teaching Assistants' Association (TAA) at the University of Wisconsin-Madison will head into summer without a new contract. </P>
<P>At an early May bargaining session, the state's bargaining team declared that negotiations had reached an "impasse" and left the table. But under Wisconsin law, "impasse" has no legal definition, and the state cannot arbitrarily declare an impasse. What's more, the state has not retracted its latest offer, which includes a 4.6 percent pay increase in the second year of the contract, but would force TAA members to pay health insurance premiums for the first time.</P>
<P>The TAA's previous offer had teaching and project assistants paying $1 monthly health care premiums and a 5.7 percent pay increase in the second year of the contract. Some union members think that the state's action is the beginning of a legal strategy to break the union, but most think that it was a stall tactic to halt the TAA's momentum after the strike. In fact, the state's action amounts to a de facto extension of the previous contract that includes no health insurance premium.  </P>
<P>This summer, the TAA is planning to build coalitions with other state unions and to pressure the state legislature. State unions still without a contract should take a page from the TAA's playbook. The threat of a strike won concessions that no other state unions have won. "We noticed that the TAs were offered [a] 4.6 percent [pay increase]," one state union leader told a reporter. "That's not on our table."</P>

