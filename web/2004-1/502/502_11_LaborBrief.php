<p><a name="CUNY"></a><font face="Times New Roman, Times, serif" size="4"><b>City University of New York</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Wallis, PSC CUNY, AFT Local 2334</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than 1,300 professors from the City University of New York (CUNY) rallied outside a Board of Trustees meeting to protest having to work without a contract for the last three years. Their demands, to name only a few, include better salaries, smaller class sizes, benefits for adjunct faculty, office space, restoration of the drastically reduced dental plan and an end to secretarial duties. </P>
<P>One adjunct pointed out that, though the union had won health insurance for him, actually qualifying to receive the benefit was impossible. Many protesters agreed that PSC CUNY will have limited negotiating power as long as it continues to respect the Taylor Laws, which prohibit CUNY professors and other city employees from exercising their right to strike.</P>
<P>Members of the Transport Workers Union rallied in solidarity with PSC CUNY, as did Columbia University graduate student instructors from GSEU, janitorial workers from SEIU Local 32BJ, the International Socialist Organization, the Internationalist Group and some CUNY students. </P>
<P>A graduate student who teaches chemistry at Queens College said that, although PSC leader Barbara Bowen gave an excellent speech, a protesting adjunct should have addressed the crowd. A faculty member from City College pointed out that the union could do more to build support among students.</P>
<P>Nonetheless, a professor from Brooklyn College said the rally was the largest he had seen in his five years on the faculty--and that the show of support from other unions was unusually strong.</P>

