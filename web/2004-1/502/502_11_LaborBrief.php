<P><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles Unified School District</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Over 600 members and supporters of United Teachers Los Angeles (UTLA) picketed and rallied outside the School Board meeting on May 25. The protest targeted the Los Angeles Unified School District (LAUSD) with four major demands: no health care cuts, no job cuts, reduce LAUSD bureaucracy, and lower class sizes. The School Board plans to slash $60 million from employee health care and eliminate hundreds of school site jobs.</P>
<P>This protest represents the boldest independent initiative of rank-and-file UTLA members yet this year. Dissident union activists in Progressive Educators for Action (PEAC) were the driving force behind the successful mobilization and electric atmosphere at the protest.</P>
<P>Instead of waiting for approval from union officials, PEAC members took proposals for the protest to the more democratic UTLA Area Assemblies, several of which passed the proposals enthusiastically. Then PEAC members hit the ground running--publicizing the rally and organizing buses to get a large turnout.</P>
<P>At the rally we distributed stickers with slogans like "Tax the Rich!  Fund our schools!" and leaflets calling on teachers to build a movement to defend public education. All this pushed UTLA's conservative President John Perez to eventually endorse the action and speak at the rally.</P>
<P>Now Perez is now calling for a mass mobilization at the next school board meeting June 8. Let's bring even more people out to show LAUSD that we will not accept their cutbacks!</P>

