<P><font face="Times New Roman, Times, serif" size="5"><b>Crisis in Dominican Republic deepens</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 8</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANOTHER FREE-MARKET miracle in Latin America has unraveled into economic collapse, mass protest and repression--this time in the Dominican Republic. Two people were killed and at least 150 were arrested in a two-day general strike last month in an action called to protest skyrocketing inflation and austerity measures that have led to electricity blackouts in parts of the country for as much as 20 hours a day.</P>
<P>And if Dominican President Hip&oacute;lito Mej&iacute;a has his way, there's more misery to come under terms of an emergency bailout by the International Monetary Fund (IMF). The IMF aims to make workers and the poor pay for the crisis by raising taxes on alcohol and tobacco, cutting the government budget and limiting public-sector wage increases to 9 percent despite an inflation rate of 43 percent last year.</P>
<P>In the late 1990s, the Dominican Republic was deemed an economic model for Latin America, with growth hitting 7 percent a year under former President Leonel Fern&aacute;ndez of the Dominican Liberation Party (PLD). Wealthy, crooked government officials and the middle class prospered as never before. But workers--whose labor fueled the booming sweatshop economy--were shut out.</P>
<P>The poor, disproportionately Black and of Haitian origin, continued to suffer racist discrimination and squalor as social spending totaled just 7 percent of the Gross Domestic Product (GDP)--half the average in Latin America.</P>
<P>As the 2000 presidential candidate of the nominally left-of-center Democratic Revolutionary Party (PRD), Mejia waged a successful campaign based on the slogan "People First" and an end to corruption. Once in office, he cozied up to leading businessmen like Ram&oacute;n B&aacute;ez Figueroa, the head of Banco Intercontinental, and Baninter, the country's second-largest bank, which gave Mejia two SUVs.</P>
<P>But Baninter went bust when regulators discovered that hundreds of millions of dollars had been siphoned out of the bank. The government stepped in to bail out depositors--including those of the wealthy in offshore accounts--doubling the Dominican Republic's debt overnight.</P>
<P>The result was a free fall in the country's currency, the peso. Prices surged out of control, and blackouts ensued because the government can't afford to pay the subsidies that keep power affordable.</P>
<P>Elections are set for May, with Fern&aacute;ndez set for a comeback in a contest with Mej&iacute;a--so more austerity policies are in the offing whoever wins. The potential for a different kind of politics in the Dominican Republic lies in the struggles and strikes of recent weeks.</P>


