<P><font face="Times New Roman, Times, serif" size="4">Striking LA grocery worker speaks out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Hit them where it hurts"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 20, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Labor movement criticism of the United Food and Commercial Workers (UFCW) leadership in the strike and lockout of 70,000 grocery workers in Southern California has spilled over into the media. AFL-CIO officials, who in late January began coordinating strategy for the struggle, told reporters that they were surprised when the UFCW made a sudden offer to the three chains--Safeway Inc., Albertsons Inc. and Kroger Co.--for binding arbitration.</P>
<P>It was the latest in a series of erratic steps by the UFCW, which first told the AFL-CIO to hold off when the struggle began in October, and pulled picket lines at the Kroger-owned chain, Ralphs. The struggle got a boost in November when the Teamsters honored picket lines--but the lines were pulled a month later.</P>
<P>The struggle has won widespread support from the LA labor movement, with several big marches and a dockworkers' solidarity rally that shut the ports for a night. Yet as the battle entered its fourth month, picket lines have dwindled, and many workers are demoralized by the union's passive strategy.</P>
<P>An exception to this is UFCW Local 1442, based in Santa Monica, west of LA. The local has organized regular large pickets, often involving hundreds of workers and community supporters--and has succeeded in forcing stores to close on several occasions.</P>
<P>Here, <I>Socialist Worker's </I>ALISON McKENNA interviews Robert, a rank-and-file worker and member of Local 1442. In his 33 years on the job, Robert--who asked that his last name not be used--has worked as box person, store director, checker and liquor manager.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>WHAT ARE the most important issues for you in this strike?</B></P>
<P>THE DOUBLE-tier [wage] system. These companies have made it mandatory to get this system in so they can erode union solidarity. On the tails of that are health care and pensions.</P>

<B><P>WHAT ACTIONS have you been taking within Local 1442 to put pressure on the companies?</B></P>
<P>A LOT of what we have done has been misconstrued by the public because of [what] the chains have led them to believe. Our primary target is to become highly visible, and the rallies have been important for this.</P>

<B><P>WHAT IMPACT do you think the rallies are having on workers, the community and the company?</B></P>
<P>FOR WORKERS, it keeps up morale, gets them involved in the union, and we are able to give out information about what we are doing. Every community is different. When we go to working communities, we have a lot of local support. When we go to communities by the beach where people have more money, they sympathize, but they don't have the same kind of support. I don't know if we are having the impact [on the companies] that we would want to have. It's not the same as if we planned [the strike] years ago. </P>

<B><P>WHAT IS your assessment of the strike?</B></P>
<P>WE SHOULD have solved this a long time ago. [We could have] if we had been more aggressive in the beginning. When I say this, I am thinking of the janitors' strike [which was known for effective civil disobedience tactics]. We should have become more militant months ago. But I am still hoping that we will be able to settle in the near future.  They are still talking and hopefully some good will come from that, although the talks are behind closed doors.</P>

<B><P>WHAT DO you think it will take to win this strike?</B></P>
<P>WE NEED to take it up a few notches. We need to really hit [the corporations] where it hurts, their bottom line. We need to get them to realize what they are doing to the entire workforce in Southern California. It's not just the grocery stores. This is a lot bigger than we are. This is an impetus for the labor work force. It's time to start looking ahead, to start strengthening, recruiting, crossing national boundaries, maybe even using civil disobedience.</P>

<B><P>DO YOU think other locals are going to take up Local 1442's strategy?</B></P>
<P>IT IS hard for me to say...many individual workers from Local 770 [the main local in LA] want to be a part of it, but the other locals have not endorsed the rallies officially. A lot of these workers from other locals have asked, "Why can't we do this?" We're discussing more militant strategies so we can raise national attention to this struggle.</P>

