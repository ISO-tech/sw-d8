<P><font face="Times New Roman, Times, serif" size="4">Ashcroft's goons go after hospital records</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Anti-abortion fanatics</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IF ANTI-ABORTION fanatic John Ashcroft has his way, the U.S. Justice Department will pore over the private medical records of hundreds of women who had abortions. In the Bush administration's crusade to crack down on a late-term abortion procedure that abortion opponents have misnamed "partial-birth" abortion, Ashcroft's fanatics last week issued subpoenas ordering at least six hospitals in New York City, Philadelphia and elsewhere to turn over medical records on abortions they performed.</P>
<P>In November, Congress passed a ban on the late-term abortion procedure known medically as "intact dilation and extraction"--or D&amp;X--and George W. Bush eagerly signed it into law. The law was blocked immediately by federal judges in San Francisco, New York and Lincoln, Neb.</P>
<P>Trials are scheduled to start March 29 to decide whether the law's lack of an exception for abortions to protect a woman's health violates standards set by the U.S. Supreme Court in 2000 when it struck down a similar ban--and whether the ban is so broad that it potentially covers all abortion procedures commonly performed in the second 12 weeks of pregnancy. </P>
<P>.Now the Justice Department says that it needs doctors to turn over women's medical records so that it can defend its draconian law. The department is specifically going after doctors involved in the three legal challenges to the ban--to see if the procedures were medically necessary or "just the doctor's preference."</P>
<P>A chief federal judge in Chicago threw out the subpoena against the Northwestern University Medical Center because he said it was a "significant intrusion" on patients' privacy. Judge Charles Kocoras argued that a woman's relationship with her doctor and her decision on whether to get an abortion should remain confidential "without the fear of public disclosure."</P>
<P>But a federal judge in Manhattan allowed the subpoenas to go forward and even threatened to lift a temporary stay that he'd imposed on the government's new abortion restrictions if the records were not turned over. "We're going to get to those records one way or the other," said Judge Richard Conway Casey, who was appointed in 1997 by Bill Clinton. "It's troubling," Bruce Merlin Fried, a medical privacy expert, told the <I>Chicago Tribune. </I>"Here's something that no woman is going to announce to anyone, maybe not even her family. And all of a sudden, the government is poking around her life."</P>
<P>Activists are already planning a demonstration to protect women's right to abortion on April 25 in Washington, D.C. We have to demand that Bush's Big Brother stop snooping into women's private lives.</P>


