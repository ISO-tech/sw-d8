<p><a name="Tootsie"></a><font face="Times New Roman, Times, serif" size="4"><b>Tootsie Roll</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--After previously rejecting two contracts by large margins, workers at Tootsie Roll Industries Inc. overwhelmingly approved a new contract last weekend. The new four-year deal contains increases in pay, pension and health care benefits, according to union officials.</P>
<P>The $1.90-an-hour wage increase will be phased in over the life of the contract, and pension benefits will also improve in three steps. The new contract also puts a cap on out-of-pocket health care expenses that workers must pay and doubles the lifetime maximum health benefits to $2 million, according to Jethro Head, president of the Bakery, Confectionery, Tobacco Workers and Grain Millers Union Local 1.</P>
<P>About 500 full-time workers are covered by the contract. Their last contract expired September 30, but workers continued under the old contract until the company terminated it last month. Last weekend, workers overwhelmingly authorized a strike, and a few days later there was a breakthrough in negotiations.</P>

