<p><a name="NYU"></a><font face="Times New Roman, Times, serif" size="4"><b>New York University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Mitch Day</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Adjunct professors at New York University (NYU) are fighting for their first contract since joining Adjuncts Come Together-United Auto Workers (ACT-UAW) in 2002. Part-time faculty, called adjuncts, make up close to 70 percent of NYU's faculty. This follows a national trend in which almost 50 percent of all U.S. faculty are adjuncts--and therefore receive lower wages, little or no benefits and little job security.  </P>
<P>Contrary to NYU's motto of "a private university in the public service," the administration increased undergraduate tuition by 7 percent last year, making the average yearly undergraduate tuition $33,000. Outrageously, adjuncts earn on average $2,700 per course, and almost all are limited to teaching two courses per term.  </P>
<P>In their new contract, adjuncts are asking for substantial wage increases, health and retirement benefits, participation in faculty governance, adequate space to prepare and meet with students and--most of all--job security.  </P>
<P>But it will take a fight to squeeze these basic rights from NYU. A student group has recently formed to build student solidarity with adjuncts, with a teach-in being planned for early March.</P>

