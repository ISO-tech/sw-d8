<P><font face="Times New Roman, Times, serif" size="4"><b>Stop and Shop</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--With 70,000 of their fellow grocery workers engaged in a bitter strike battle in Southern California, the United Food and Commercial Workers (UFCW) in New England narrowly averted a strike last weekend. On Sunday morning, roughly 43,000 grocery workers employed by Stop &amp; Shop in Connecticut, Rhode Island, and Massachusetts agreed to a last-minute contract that fended off attacks on health care and wages by the company.</P>
<P>Stop &amp; Shop had originally proposed that employees pay 20 percent of health care costs while eliminating time-and-a-half pay for new part-time workers on Sundays and holidays as well as delaying medical benefits for three years for newly hired employees. But the new contract calls for the company to continue paying all employee health care costs while both full-time and part-time employees receive modest wage increases and some health benefit reductions. </P>
<P>However, workers will continue to make co-payments for doctor visits and pay 20 percent of major medical costs. "The company contributes all of our pension and insurance premiums," said Donna Boyington, a 15-year Stop &amp; Shop employee from Glastonbury, Conn. "That's a wonderful thing in this day and age. We held our ground."</P>
<P>The key to maintaining these previous gains was the threat of a region-wide strike. "What turned the tables here was that we stuck together as unions," said Local 371 President Brian Petronella. "Stop &amp; Shop tried to pick us off. We had to hold our ground on health care." But, although Stop &amp; Shop workers didn't face the same harsh demands as their counterparts in California, a separate settlement has weakened the UFCW's attempt to carry out an industry-wide fight on these issues.</P>

