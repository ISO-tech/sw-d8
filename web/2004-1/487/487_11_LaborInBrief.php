<p><a name="Powells"></a><font face="Times New Roman, Times, serif" size="4"><b>Powell's</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--Up to 400 people rallied in support of members of International Longshore and Warehouse Union (ILWU) Local 5 who work at Powell's Bookstore and in solidarity with striking Safeway workers here. Safeway management is going after workers' benefits--similar to Safeway's attack on grocery workers in Southern California. At Powell's, ILWU Local 5 has been trying to negotiate a new contract for months, yet management wants to make workers pay for more profits.</P>
<P>Protesters marched through Powell's as dozes of workers walked off the job to demand a fair contract. The marchers then went on to a Safeway store, where some went inside to leaflet shoppers about the plight of the California's grocery workers. </P>
<P>One marcher was roughly arrested and handcuffed by security guards for trespassing. The crowd outside grew angry outside when they heard the news and demanded his release, chanting "Let him go!"</P>
<P>After a short while, and probably under police advice, Safeway security released him without pressing charges. Safeway and their hired goons might want to intimidate pickets, but the message from activists is "We'll be back!"</P>

