<p><a name="UT"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Texas shuttle bus drivers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Dana Cloud</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--More than 50 University of Texas (UT) shuttle bus drivers, mechanics, and supporters held a spirited rally and picket at the campus statue of Martin Luther King February 9. The rally was designed to win the solidarity of students, faculty and staff with the workers' campaign to win a decent contract from ATC, a huge subcontractor with Austin's Capital Metro Transportation Authority.</P>
<P>The current contract expired more than two years ago, during which time ATC has reduced health benefits. Senior drivers and mechanics have not had raises in three years. The workers also have safety concerns.</P>
<P>Picketers chanted "Support UT Shuttle Workers" as other participants handed informational brochures to passing students. Speakers at the rally emphasized the need to win the support of students. "Without students' support, the university will just divide and conquer, telling the students that their fees will go up if we get paid what we deserve," said union member Glenn Gaven. </P>
<P>Union vice president Norm Couture said that although student shuttle fees have risen, the workers have seen none of that money. "He said that an injustice anywhere is a threat to justice everywhere," said UT student Courtney Morris, pointing to King's statue. "Students need to find out that an injustice to anyone is an injustice to everyone. We have to build broad support, and we have begun that work today."</P>


