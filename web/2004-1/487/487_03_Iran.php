<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is Iran headed for a political crisis?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 20, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THIS WEEK'S elections in Iran will mark a turning point in the stalemate between the conservatives and reformers who make up Iran's political establishment. In 1997, Iranians turned out in massive numbers to elect reformer Mohammad Khatami as president in a landslide. Khatami's popularity stemmed from his pledge to loosen the grip of Iran's conservative Islamic clerics, who use their stranglehold on political power to dominate everything from Iran's economy to enforcing a restrictive dress code.</P>
<P>In the run-up to this week's elections, however, the conservative Guardian Council--an unelected body of 12 hard-line Islamic clerics with broad powers to veto legislation--barred 2,500 reformers from running for office, including 80 sitting members of Parliament. This means that conservatives will face no reform challenger in 132 of the 290 seats up for a vote. </P>
<P>The biggest reform party, the Islamic Iran Participation Front, led by Khatami's brother, is boycotting the election after all its candidates were banned. In solidarity, another 550 reform politicians are boycotting the vote by refusing to stand for office--a move that they hope will rob the elections of any legitimacy. In contrast to Khatami's 2000 re-election, when voter turnout hit 70 percent, turnout this Friday isn't expected to get above 10 or 15 percent.</P>
<P>But the expected victory for conservatives doesn't herald a return to the old days. For one thing, the loosening of social restrictions, growing access to the Internet and other sources of political dissent can't be easily reversed.</P>
<P>Second, the conservatives success in banning reform candidates doesn't reflect an upsurge in support for the conservative cause, but rather the widespread disillusionment of supporters of the reformers--who have been unwilling to stand up to the conservatives. Khatami and the other reformers fear that mobilizing Iranians to challenge the conservatives' grip on power could uncork a movement that goes beyond the narrow limits of change that they seek.</P>
<P>Khatami's wing of the Iranian ruling establishment is thoroughly committed to a neoliberal, anti-working class agenda--the source of strikes and workers' demonstrations in recent months. Thus, Khatami didn't wage a fight against the Guardian Council when it used its constitutional position to shut down the pro-reform media. </P>
<P>Despite U.S. rhetoric about its opposition to Islamic fundamentalism, the U.S. government tends to be hostile toward Khatami because of his government's close ties to Washington's competitors in Europe, Russia and several Asian countries. Whatever shape Iranian politics takes in the coming months, Washington's main concern will be keeping up the pressure on the government to bow to U.S. aims to dominate the Middle East.</P>


