<P><font face="Times New Roman, Times, serif" size="5"><b>Thousands rally to demand school funding in Maryland</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sheri Pegram</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANNAPOLIS, Md.--Thousands of teachers, administrators, parents and students from schools across Maryland converged at the State House February 9 to rally for full funding of a promised school improvement plan. Instead of increasing spending on the plan, Republican Gov. Robert Ehrlich has proposed cutting $50 million from the program--and he also has talked about spreading the final installments over several more years, a suggestion that has irked many politicians throughout the state. </P>
<P>As about a dozen speakers addressed the crowd, protesters chanted, "Fund our schools," while others waved signs saying, "Show us the money." Prince George's County officials dismissed students two hours early to free up buses in order to take about 3,000 employees to the rally. Rally organizers estimated the turnout at between 10,000 and 15,000. </P>
<P>Faced with a $700 million budget deficit, Ehrlich has said that the state can't afford to pay for the entire school-funding program, approved by the General Assembly in 2002, unless legislators vote to legalize slot machine gambling. His budget for fiscal 2005 includes $326 million more money for schools--about 90 percent of what educators had anticipated. </P>
<P>Nevertheless, several rally participants criticized Ehrlich's efforts to tie full funding of the $1.3 billion promised for schools to approval of the slot machines. The school funding plan--developed by a commission led by former Prince George's County school board chief Alvin Thornton--had became the talk of Annapolis. </P>
<P>Two years ago, the General Assembly approved the so-called Thornton plan, which aims to boost student performance by closing the spending gaps between rich and poor school districts. The Assembly also agreed to finance two years of the program with a tobacco tax increase--and to decide later how to pay for the program through 2008. </P>
<P>But teachers are demanding funding now. "It's time to end the rhetoric," Maryland State Teachers Association President Patricia Foerster told the crowd. "A promise is a promise."</P>


