<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What we mean by the working class</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CLASS IS commonly viewed as something based upon income level: upper, middle, lower. The other popular idea is that we are all basically "middle class"--with perhaps a minority "underclass" of poor people.</P>
<P>The use of these superficial and misleading terms helps to obscure rather than clarify what kind of society we live in, and deliberately so. They are meant to direct us away from the fact that the real parasites in our society are not some so-called "underclass," but the extremely rich financiers and industrialists.</P>
<P>Income doesn't tell us everything we need to know about a person's place in the economic system--for example, whether they <I>worked</I> for it or not. Even the concept of "haves" versus "have nots," while an improvement, is still only a descriptive device that tells us nothing about why some "have" and others "have not."</P>
<P>Classes are defined by their relationship to the production process in any given society, and in particular, the production and control of the <I>surplus</I>. As Marx writes in <I>Capital</I>:</P>
<P>"Wherever a part of society possesses a monopoly of the means of production, the worker, free or unfree, must add to the labor-time necessary for his own maintenance an extra quantity of labor time in order to produce the means of subsistence of the owner of the means of production, whether this proprietor be an...Etruscan theocrat, a <I>civis Romanus</I>, a Norman baron, an American slave-owner, a Wallachian Boyar, a modern landlord or a capitalist."</P>
<P>All class societies are societies that center around a specific form of exploitation, whereby a small group (the ruling class, which "possesses a monopoly of the means of production") appropriates the surplus labor of the majority who work.</P>
<P>Under capitalism, there are two main classes--the class of capitalists (or ruling class), who own the vast means of production (banks, hospitals, factories, office buildings, grocery stores, etc.); and the class of wage workers (or working class), who because they lack means of production, must hire themselves out hourly, weekly or monthly to these same capitalist owners. The capitalist class, because of its ownership of the means of production, can command the labor of others, who for them are merely "inputs" for production.</P>
<P>Capitalists appropriate the surplus produced by wage workers by selling goods and giving back to workers only a portion of the value of what they produce in commodities. What this means is that a skilled auto mechanic who works for Firestone might make a great deal more money than a strawberry picker--but both are part of the working class because both perform <I>surplus labor</I> for capitalists.</P>
<P>Economics professor Michael Zweig estimates that the class of wage workers in the U.S. constitutes about 62 percent of the population, the capitalist class about 2 percent. In between are, broadly speaking, a "middle class" that consists of people whose class position is intermediate between the two poles--managers, professionals, small businesspeople.</P>
<P>Calibrating class by income can be very misleading. For example, a small-business owner who exploits 10 employees might in a given year have a smaller income than a skilled carpenter. If we were to determine class by income, the carpenter would be "above" the businessperson, completely obscuring the fact that the carpenter's labor is exploited for the profit of the builder, whereas the businessperson is in fact a small-scale <I>exploiter.</P>
</I><P>Marx continues: "What distinguishes the various economic forms of society--the distinction for example between a society based on slave-labor and a society based on wage-labor--is the form in which this surplus labor is in each case extorted from the immediate producer, the worker."</P>
<P>The only difference under capitalism is that the aim of production changes, so that the limits of exploitation are no longer limited by, to paraphrase Marx, the size of the lord's stomach. It is a system of limitless expansion. And that expansion happens on the backs of the working class.</P>


