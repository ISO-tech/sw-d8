<P><font face="Times New Roman, Times, serif" size="5"><b>What a fraud!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 20, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><I>THEN: He used his family's influence to stay of the Vietnam War.<br>
NOW: He sends Iraqis and U.S. soldiers to their deaths.</I></font></P>
<P><font face="Times New Roman, Times, serif" size="3">Hypocrite. That's the only word that can describe a man so quick to send poor and working class men and women to their deaths in Iraq and Afghanistan--but who bought his way out of serving in the military during the Vietnam War.</P>
<P>But George W. Bush didn't only use his family's influence to get a prized spot in the Texas Air National Guard's "Champagne Unit"--where rich families sent their brats to play fly boy. Once there, he skipped out altogether on more than a year of service. That didn't stop Bush from suiting up to play GI Joe for a day last year, when he declared that the war in Iraq was over as he stood aboard the <I>USS Abraham Lincoln.</I></P>
<P>And the cost of his unrestrained arrogance? Tens of thousands of Iraqis dead, and millions more thrown into chaos and despair. Hundreds of U.S. soldiers killed, and reservists deployed indefinitely. All in the name of U.S. oil and empire.</P>
<P>The allegations about Bush's unauthorized absence from the National Guard have been known about for years--but were largely ignored by the mainstream media. Now, though, fueled by other scandals facing the Bush administration--from nonexistent weapons of mass destruction in Iraq, to the leak of an undercover CIA agent's name, to the sweetheart Iraq deals handed out to politically connected companies like Halliburton and Bechtel--the media are taking another look at Bush's "vacation" from the Guard.</P>
<P>The White House spin doctors are using everything from Bush's dental records to a few pay stubs to try to explain it all away. But it isn't working. A recent ABC News/<I>Washington Post</I> poll found that just 52 percent of the population believes that Bush is "honest and trustworthy."</P>
<P>The big question, though, is why the administration hasn't yet paid a higher price for its lies, cover-ups and corruption? The main answer is because the official "opposition" to Bush comes in the lukewarm form of the Democratic Party. Democrats may criticize the Republicans in the heat of the 2004 campaign, but the two parties have far more in common with each other--from supporting the war on Iraq, to cuts in social services at home--than they care to admit.</P>
<P>A real challenge to Bush won't come from inside the Washington establishment. We need to build a real movement for change--in our workplaces, on our campuses and in the streets.</P>


