<P><font face="Times New Roman, Times, serif" size="4">Farouk Abdel-Muhti detained</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why is this man still behind bars?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. government announced its intention earlier this month to continue the nearly 2 year detention of New York-based Palestinian immigrant Farouk Abdel-Muhti. The Feds claim that Abdel-Muhti has not "cooperated"...with his own expulsion from the U.S.! </P>
<P>The government's announcement came in response to a January 21 order by a U.S. district court judge that the Feds immediately explain their rationale for continuing Adbel-Muhti's detention. Abdel-Muhti is a long-time and prominent activist in the struggle for Palestine. He was arrested in April 2002, one month after he began working regularly at the New York radio station WBAI-FM arranging interviews with Palestinians in the Occupied Territories.</P>
<P>"The government is stalling because they have no case," David Wilson, of the Committee for the Release of Farouk Abdel-Muhti, told <I>Socialist Worker. </I>Abdel-Muhti's attorneys are seeking a court order for their client's immediate release, under a U.S. Supreme Court ruling requiring that detainees whose deportation orders cannot be carried out in a reasonable period of time be freed.</P>
<P>The government has been unable to deport Abdel-Muhti because he is a stateless Palestinian. Under Israeli law, he isn't permitted to return to the West Bank, and no other country has agreed to accept him. </P>
<P>Abdel-Muhti is 56 years old, and his health has been deteriorating during his 21 months in detention. "Although we believe that Farouk was targeted for political reasons," Wilson said, "there are hundreds of other immigrants being held in the same situation. There is a pattern of the government stalling in order to not have to release these people."</P>

