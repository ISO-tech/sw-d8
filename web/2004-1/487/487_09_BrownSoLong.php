<P><font face="Times New Roman, Times, serif" size="5"><b>The Democrats vs. the environment</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by John Green</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Jeffrey St. Clair, <B><I>Been Brown So Long It Looked Like Green to Me: The Politics of Nature. </B></I>Common Courage Press, 2004, 407 pages, $20.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFREY ST. Clair, co-editor of <I>CounterPunch, </I>has delivered his hard-hitting book <I>Been Brown So Long It Looked Like Green to Me </I>just in time to stem the amnesia over the Democratic Party's environmental record. In this collection of essays, St. Clair pulls no punches criticizing big corporations, Democrats and Republicans alike and "institutionalized environmentalist" groups such as the Sierra Club and the Natural Resources Defense Council.</P>
<P>Many essays deal with the Republicans running roughshod over the environment, skewering nutcases such as Reagan's Secretary of the Interior James Watts. Watts famously defended super-exploiting the environment by declaring, "I do not know how many future generations we can count on before the Lord returns."</P>
<P>But St. Clair is careful to remind the reader, "an equal threat comes from the leadership of the Democratic Party...and enviro bureaucrats." Bill Clinton and toadies like his interior secretary Bruce Babbit receive no quarter for their betrayals on the Headwaters forest deal, the repeal of the Delaney Clause on pesticide reform and gutting the Endangered Species Act.</P>
<P>Many on the left argue that the giveaways by Clinton--while bad--were preferable to George W. Bush's handling of the environment. But, as St. Clair explains, not only is it hard to tell who's worse but Clinton's actions set the stage for his successor's assaults.</P>
<P>Take the recent spate of blackouts on both coasts: "[T]he electric power safety net, erected after the power company scandals of the 1920s, was giddily cut loose during Clintontime," he explains. "Like welfare, once the regulatory framework is dismantled it's gone for good. Score another one for Bill."</P>
<P>St. Clair also takes issue with the leading Democrats' hypocritical "free market" approach to environmentalism. Their foremost theoretician, Al Gore, "stresses environmental discipline for the Third World, while gliding over corporate looting of North America's forests, rivers and mountains."</P>
<P>Nor do mainstream environmentalists escape scrutiny. Acting as "political pimps for the Democratic Party," groups like the Sierra Club provide green fig leaves for the terrible legislation and backroom "compromises" that actually hurt the environment.</P>
<P>Don't call St. Clair a cynic, however. He also covers successful challenges to despoliation and anecdotes of poetic justice. Take for example, the University of Arizona's modern-day theft of Native American land to construct an observatory for the Vatican...<I>on a cloudy mountain.</P>
</I><P>This book largely focuses on the destruction of the great outdoors, and more could be said about the problems facing urban and suburban ecologies where most people live. Despite this, <I>Been Brown So Long </I>is a refreshing look by a radical ecologist at the politics of nature in America today. This is an eye-opening choice for anyone intending to vote Democrat in 2004 out of concern for the environment.</P>


