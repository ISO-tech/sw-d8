<P><font face="Times New Roman, Times, serif" size="4">Seattle police settle with anti-WTO demonstrators</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>It's our right to protest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 20, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
The chaos in the streets at protests against the World Trade Organization (WTO) in Seattle in December 1999 shook the world. The media and the city of Seattle were quick to lay the blame on "crazy anarchists" and "rioting protesters." </P>
<P>But the truth was that the vast majority of violence came from the cops of Seattle, who went completely out of control. The police aggressively herded people into a blocked-off area. They used pepper spray and tear gas indiscriminately on hundreds of people. Over the course of the weekend, 600 people were arrested. Many of them were kept in jail for three to five days. </P>
<P>The brutal methods used by the Seattle police during the WTO protests set the standard for police forces across the country. Since 1999, pepper spray, teargas, rubber bullets and mass arrests have been used against demonstrations across the country.</P>
<P>After the Seattle protests, it seemed to many activists like the police were "untouchable." But global justice activists won an important victory recently when the city of Seattle agreed to pay $250,000 to more than 150 people arrested during the WTO protests. A federal judge had declared that the Seattle police had no probable cause for the arrests and called their record-keeping "atrocious," because of their use of photocopied arrest warrants and a signature on warrants from a policeman who admitted that he had not made any of the arrests. </P>
<P>After the decision, the city agreed to settle the class-action lawsuit in order to avoid a full trial. This payment represents an admission of wrongdoing by the city and its police force. </P>
<P>But the settlement presents challenges for activists, too. By settling out of court, the city did not have to officially accept liability for its mistakes and has been able to avoid a public trial and further legal actions. No police officer, or the city officials who approved of their Gestapo tactics, has had to face any criminal charges. </P>
<P>The amount of money--about $1,500 per person after lawyers' fees--is not nearly enough restitution for people who were locked up for days "until the WTO was over." The length of time it took for the city to pay up is also a problem. </P>
<P>While some important decisions may go our way, the "injustice" system is overwhelmingly biased towards protecting the police. More pressure from outside the courts is needed to hold them truly accountable, and to protect our basic rights of freedom of speech and assembly. <BR>
<B>Ellie Fingerman, </B>Seattle</P>


