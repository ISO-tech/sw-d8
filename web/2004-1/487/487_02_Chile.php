<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. plan for regime change in Chile</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration may not believe in recycling environmental resources. But when it comes to the policy of "regime change," that's exactly what they did. A newly released 34-year-old memo by Henry Kissinger, the national security adviser to the Nixon administration, documents the U.S. government's policy of pre-emptive "regime change" in Chile--30 years before George W. Bush sought the same thing in Iraq.</P>
<P>In the eight-page text, Kissinger argues that the U.S. should adopt a posture of "covert hostility" to the government of Chile's elected president, Salvador Allende. Allende was overthrown by Gen. Augusto Pinochet in a CIA-backed coup in 1973.</P>
<P>Some 20,000 Chilean workers and socialists were murdered in the aftermath of the coup, and for the next 17 years, Pinochet ran Chile with an iron fist. And that was exactly the outcome that Kissenger had hoped for.</P>
<P>"The election of Allende as president of Chile poses for us one of the most serious challenges ever faced in this hemisphere," wrote Kissinger. The fate of Allende's regime "will have an effect on what happens in the rest of Latin America and the developing world; on what our future position will be in the hemisphere; and on the larger world picture, including our relations with the USSR. They will even affect our own conception of what our role in the world is."</P>
<P>"I recommend, therefore, that you make a decision that we will oppose Allende as strongly as we can and do all we can to keep him from consolidating power, taking care to package those efforts in a style that gives us the appearance of reacting to his moves."</P>
<P>Time and again throughout the last century, U.S. officials have shown that no price is too high to make an example out of a country that poses a challenge to U.S. power--not the murder of 20,000 Chileans, nor the deaths of 500,000 Iraqi children because of U.S.-backed sanctions on Iraq.</P>


