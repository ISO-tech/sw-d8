<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SOUTH KOREA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Daewoo autoworkers face brutal police attack</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By  Scott Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">POLICE IN South Korea stepped up their assaults on autoworkers outside of Daewoo's Bupyong auto plant in Inchon in early April. Regular protests have taken place outside the plant since 1,700 workers were laid off in mid-February. Several hundred workers and their families occupied the plant before they were forcefully expelled with clubs and fire hoses.</P>
<P>Since then, police have surrounded the factory to keep protesters away--barring entrance to a union office on the premises. When the union won a court order allowing them to enter their office, police refused to budge as a peaceful demonstration of several hundred Daewoo workers formed outside the plant.</P>
<P>Eventually, protesters laid down on the asphalt in a show of protest. Just after several members of South Korea's National Assembly left the site, police suddenly charged the men lying on the ground and beat them mercilessly.</P>
<P>Videos of the incident distributed on the Internet showed dozens of bloodied demonstrators screaming in pain. Several were beaten unconscious, while another suffered broken ribs and a punctured lung.</P>
<P>The next day, President Kim Dae Jung, taking a page from George W. Bush's book, offered "deep regret"--but no apology since he claimed to have asked police not to use violence. This is particularly unbelievable, since South Korea's cops regularly use force against protesters--even recently beating a pregnant woman, causing her to lose the fetus.</P>
<P>Kim and his thugs may soon find that the price for crushing protests is too high. The autoworkers union has begun a campaign for international solidarity--putting pictures of Nobel Peace Prize laureate Kim with a bloodied Nobel medal on its Web site. The pressure has forced the government to fire the head of the Inchon Metropolitan Police Agency and punish several officers involved in the attack.</P>

