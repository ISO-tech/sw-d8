<P><font face="Times New Roman, Times, serif" size="5"><b>How the Bush gang got down and dirty in Florida</b></font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Book: Jake Tapper, <I>Down and Dirty: The Plot to Steal the Presidency</I>. Little, Brown and Co., 2001, 514 pages, $24.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A FEW days into the post-election fiasco in Florida last year, an obscure Republican Party hack held a news conference to talk about overseas absentee ballots. Strict deadlines should be observed, and no undated or unsigned ballots should be tolerated, former Florida Secretary of State Jim Smith told reporters.</P>
<P>At this point, Republican supporters of George W. Bush thought the majority of overseas ballots would arrive from Israel--and favor the Gore-Lieberman ticket. Then the GOP figured out that the ballots would come mostly from military personnel overseas--and would probably be for Bush.</P>
<P>So Republicans did a 180-degree reversal. They began roasting the Democrats for a memo, drafted by a lawyer working for Al Gore, which made precisely the same points that Jim Smith had a few days earlier. The issue turned into a public relations disaster for the Democrats.</P>
<P>Yet even as the Republicans were spinning the ballot issue one way, their operatives were seriously discussing how to get Bush supporters at overseas military bases to send in ballots after Election Day, according to author Jake Tapper. Publicly, the Republicans were hammering the Democrats--while secretly, they were the ones plotting vote fraud.</P>
<P>Tapper isn't sure whether the GOP carried out this plot. But he's sure it was discussed. This GOP scheme is arguably the most explosive revelation in Tapper's <I>Down and Dirty</I>, one of the first journalistic accounts of Bush's election theft in Florida to hit bookstore shelves.</P>
<P>Tapper is one of the few Washington-based mainstream journalists who could produce a book on Florida worth reading. As the Washington correspondent for Salon.com, Tapper brings his publication's muckraking style to the story. And he doesn't show any favoritism to Bush or Gore. In fact, he seems to hate them and their toadies equally.</P>
<P>He calls Mark McKinnon, Bush's main media adviser, "one of the only decent guys in the higher echelons" of Bush's staff--which makes you wonder what the rest are like. And he expresses disgust with "Strep Throat," a high-level Gore adviser who repeatedly tries to pitch him a story about an alleged affair between Florida Gov. Jeb Bush and Secretary of State Katherine Harris. "This is how a senior Gore adviser is spending his time, peddling this filth," Tapper writes. "Filth that isn't even remotely true."</P>
<P>Tapper skewers the hypocrisy and ruthless calculation that dominated both campaigns during the post-election brouhaha. He blasts the Republican effort as "a goulash of truth, lies and innuendo--most offensively against the judiciary--and served to the American people with a sprinkle of concocted moral outrage."</P>
<P>Tapper denounces the ugly GOP-staged riot that pressured Miami-Dade County election officials to stop a crucial recount. He calls subsequent Republican attempts to characterize the incident as an expression of concerned citizens as "unadulterated bullshit."</P>
<P>Tapper also criticizes Gore for hypocrisy--for talking about "counting every vote" in Florida, but focusing on technical haggling in a few Democratic South Florida counties. For weeks, Democrats in Duval County--where more than 20,000 ballots were rejected, most of them in African-American precincts--called on Gore to take up their cause. Gore turned them down. Tapper shows that the Bush forces were willing to play more "down and dirty"--and that's why they won.</P>
<P>At times, <I>Down and Dirty</I> is almost too detailed in its descriptions of the legal and political battles between the Bush and Gore teams. And because it focuses mostly on the legal controversies and exposing the politicians' lies, the book misses out on the bigger story of Black disenfranchisement that hangs over the whole Florida fiasco. So he tends to dismiss the demonstrations protesting Bush's election theft as Democrat-instigated background noise.</P>
<P>After spending so much time around the professional liars in the Bush and Gore campaigns, Tapper seems unable to separate "spin" from real outrage. So when he concludes <I>Down and Dirty</I> with the message that ordinary Americans deserve the blame for the Florida disaster--because we've never really cared about voting rights and because we put up with losers like Bush and Gore--Tapper sounds just plain foolish. This blame-the-victim conclusion mars an otherwise worthwhile book.</P>


