<P><font face="Times New Roman, Times, serif" size="5"><b>May Day events focus on immigrant rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart and Geoff Bailey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 14</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS ACROSS the country are preparing for events to celebrate May Day, the international workers' holiday.</P>
<P>--Los Angeles immigrant rights, community and labor groups are planning a May Day march to call for a broad legalization program for immigrant workers. More than 6 million undocumented immigrants in the U.S. face abuse, exploitation and rotten living conditions.</P>
<P>Last year, the Korean Immigrant Worker Association (KIWA) organized a May Day march of 400 against the Elephant Snack restaurant in Koreatown. "The march woke Koreatown up," KIWA organizer Elizabeth Sunwoo told <I>Socialist Worker</I>. "That demonstration laid the basis for this year's mobilization."</P>
<P>KIWA joined with the Filipino Workers Center and the Coalition for Humane Immigration Reform-LA to create the Multi-ethnic Immigrant Worker Organizing Network. The network brought together other organizations representing around 3,000 members, potentially making this year's demonstration the largest LA has seen in decades.</P>
<P>--In Boston, a historic coalition has come together to organize this year's May Day March for Workers' Rights. As part of the national amnesty campaign for undocumented workers, the Massachusetts AFL-CIO, the Joint Council of Painters, Jobs with Justice and the General Amnesty Coalition have joined forces to organize a March for Immigrant Rights. The AFL-CIO Executive Council, which last year came out in support of the national amnesty call, is sponsoring the event, and AFL-CIO President John Sweeney is expected to attend.</P>
<P>General Amnesty Coalition member Katia L&eacute;mos described the confidence given to undocumented Brazilian workers at a recent amnesty rally in New York: "We had 25 Brazilians in the bus back--all undocumented...And they started to sing, 'We are not afraid. We are not afraid. We shall overcome someday.'" The May 1 march will kick off a statewide campaign to pressure the legislature to pass a resolution in favor of amnesty.</P>
<P>--The Organizing Committee for Workers Rights in New York City is mobilizing for a rally and concert in Union Square on May 1 with the demand "Amnesty for all immigrants--present and future!" Protesters will march through the garment district and then take their demand for international workers' rights to the International Monetary Fund headquarters.</P>
