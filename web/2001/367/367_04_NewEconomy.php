<P><font face="Times New Roman, Times, serif" size="5"><b>"New economy" workers are taking it on the chin</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>I work at Cnet.com, an Internet company which is unique in the industry because it actually makes a profit. But in early February, Cnet announced it was laying off 10 percent of its workforce--about 190 people. Those were "the easy ones," the company is saying, because the departments were made redundant when Cnet bought out its only serious competition late last year.</P>
<P>For those of us who got through the first round of cuts, we've been told that there are "no guarantees"--and that anyone could be among the next 100 to lose their job. We're also told that these layoffs are good because they will curb future cutbacks--when the recession really hits us. Not much consolation for those who got fired or those of us who remain behind to face an uncertain future.</P>
<P>People in my office are worried. While many make above-average incomes, rents in San Francisco are astronomical. </P>
<P>But what about making millions off stock options? I have the option to buy stock from Cnet--at a price of $50 per share! For several months, the stock has hovered around $15, which makes my options less than worthless.</P>
<P>Recently, there was a glimmer of hope as customer service workers at Amazon.com began organizing a union to protect themselves against cutbacks. Then, a union drive was launched at Etown.com in San Francisco, which promised to be the first union election at an Internet company. </P>
<P>But in the past month, the entire customer service department at Amazon that was organizing got laid off. And Etown has shut down completely--but not before trying to bust the union. This shows exactly why we need unions in this industry--to protect us from layoffs in the coming recession and to guarantee that our interests are not put last.</P>

<B><P>Scott Johnson , </B>Oakland, Calif.</P></FONT></BODY>

