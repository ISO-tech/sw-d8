<P><font face="Times New Roman, Times, serif" size="4">Companies drop South Africa suit</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Drug giants cave in</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">PRETORIA, South Africa--The giant drug multinationals suffered a humiliating defeat in April when they were forced to drop their suit against the South African government. Some 39 drug makers had filed suit against a South African law that allows the government to buy cheaper generic versions of AIDS drugs. The drug bosses wanted their patents to come before millions of South Africans who suffer from HIV and AIDS.</P>
<P>But when the trial began in March, thousands mobilized in protest in Pretoria and other South African cities. Activists held solidarity demonstrations in the U.S., Europe, Australia and Canada.</P>
<P>At the end of the first phase of the trial, the judge ruled that activists and people with HIV would be able to testify--to expose how, even at "discounted" prices, the corporations were asking more for a month's supply of drugs than most South Africans make. The judge also ordered the drug giants reveal closely guarded secrets about their business practices.</P>
<P>In April, the drug companies threw in the towel. "We don't exist in a vacuum," SmithKleinGlaxo CEO J.P. Garnier admitted to the New York Times. "We're not insensitive to public opinion." But SmithKleinGlaxo was forced to be sensitive--by the outraged protests of South Africans and people around the world.</P>
<P>Newspaper pictures of activists in South Africa showed a joyous celebration in the courtroom and on the streets outside. The drug giants have products that could lift the death sentence faced by some 25 million HIV sufferers in Africa. But we'll have to fight to make them open up access.</P>


