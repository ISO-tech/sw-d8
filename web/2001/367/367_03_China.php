<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Tensions between China and the U.S.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Headed for a showdown?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">APRIL'S SPY plane crisis with China exposed the hypocrisy of the U.S. government. The spectacle of Bush White House officials trying to explain the difference between expressing regret and making an apology--while ignoring the question of why the U.S. claims the right to spy on China--was laughable. But the furor also cast a spotlight on growing tensions between the two countries.</P>
<P>There are two trends in U.S. policy toward China. On the one hand, U.S. business wants to maintain its relationship with China--to exploit its enormous consumer market and cheap labor. But the two countries' long-term strategic goals are increasingly at odds.</P>
<P>Since the 1999 NATO war over Kosovo--when the U.S. "accidentally" bombed the Chinese embassy in Belgrade --Beijing has come to see the U.S. government as out of control.</P>
<P>The new Bush administration's fanatical commitment to building a "Star Wars" missile defense system only heightened this concern. A missile defense system would leave China vulnerable to a U.S. nuclear first strike. "The United States says this is a defensive system, but everyone knows it will be used to strengthen your offensive capability," Yan Xuetong, of the Institute of International Affairs at Qinghua University, told the <I>New York Times</I>. "It protects your troops so you can attack any time without fear of retribution."</P>
<P>The flash point between the U.S. and China is shifting to the issue of Taiwan. The Taiwanese government is asking the U.S. to sell it more advanced weaponry, and China sees these sales--to what it regards as a breakaway republic--as a direct challenge. In response, Beijing announced in March that it was increasing its military budget by nearly 18 percent.</P>
<P>The more aggressive posture of each side isn't simply the result of short-sighted leaders or U.S. defense industry lobbying. The U.S. and China are competing for influence in Asia, an area that the U.S. has dominated for the past half-century.</P>
<P>"Both countries are headed for additional military showdowns," one mainstream analyst concluded. As <I>Progressive</I> magazine editor Matthew Rothschild pointed out: "The last time the air chilled so noticeably was when Ronald Reagan came to power." But Reagan's "bellicosity galvanized the peace movement... A revitalized opposition to war-making is what it will take to rein in Bush and his baying dogs."</P>

