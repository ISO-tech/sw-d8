<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BOLIVIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Workers threaten May Day general strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">UNREST IS spreading in Bolivia as workers prepare for a planned May Day general strike against government attacks. The strike call loomed as two separate mass marches, involving peasants and union activists, defied police repression to continue toward the capital city of La Paz.</P>
<P>The marches were due to converge on La Paz April 23 as <I>Socialist Worker</I> went to press. Protesters have vowed to block roads throughout the country by April 25 if the government continues to ignore demands for an end to privatization schemes and a slowdown in a U.S.-backed campaign to eradicate coca plants. The Bolivian Workers Union (COB) says it will join the roadblocks May 1 if its own negotiations with the government don't progress.</P>
<P>Fearing that growing protests will topple the government of President Hugo Banzer, Bolivian authorities launched a crackdown. Police moved in against the mass march that began from Cochabamba, arresting some 60 people.</P>
<P>Among those detained were Oscar Olivera, who helped to lead last year's successful struggle against privatization of the water system in Cochabamba, and Silvia Lazarte, a member of the National Federation of Peasant Women Bartolina Sisa. Olivera and Lazarte were released, but government officials are openly talking about imposing martial law.</P>
<P>April began with strikes by teachers and health-care workers. The government attacked these, too, teargassing a demonstration of hospital workers in La Paz in mid-April.</P>
<P>Authorities have had some success using "divide-and-conquer" strategies. Some peasant groups recently called for postponing the late April blockades, saying that the harvest season should not be interrupted. And COB officials also agreed to end the health-sector strike after some government concessions.</P>
<P>But the federation says it's still behind the May 1 general strike. If they organize to support one another, Bolivia's workers and peasants have the strength to beat government repression.</P>


