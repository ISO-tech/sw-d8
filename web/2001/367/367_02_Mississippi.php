<P><font face="Times New Roman, Times, serif" size="5"><b>Why did Mississippi vote on state flag lose?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">JACKSON, Miss.--An attempt to remove the Confederate flag symbol from Mississippi's state flag failed in a referendum held in mid-April. The vote, which went by a wide margin in favor of keeping the old state flag, was a disappointment to anti-racists who have organized to get rid of the Confederate flag in several Southern states in recent years.</P>
<P>The media claimed that voters were respecting Mississippi's "heritage." But the real reason that the campaign against the flag failed was because it was wrong-headed.</P>
<P>In South Carolina, where the legislature voted last year to lower the Confederate flag from the state capitol, anti-racists organized marches and demonstrations of tens of thousands to put pressure on the politicians. In Georgia, where the Confederate symbol was reduced on the state flag earlier this year, Martin Luther King Day was the setting for a thousands-strong march.</P>
<P>But there were no protests in Mississippi during the run-up to the referendum. The campaign was led by big business officials whose TV ads focused on how changing the flag would attract investment. They never made the case for why the Confederate flag was a symbol of racism and injustice.</P>
<P>So it's little wonder that a huge majority of whites and--even more tellingly--one in three African Americans voted to keep the old state flag. We can bring down this symbol of hate once and for all. But it will take a fight.</P>


