<P><font face="Times New Roman, Times, serif" size="5"><b>Youth crime frenzy created by media</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Snehal Shingavi</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Anyone watching the news would think that crime is on the rise--especially among minority youth referred to by the media as "super-predators." But a recent report proves that this is a myth--created by the media itself.</P>
<P>The study, authored by Lori Dorfman and Vincent Chiraldi for the Building Blocks for Youth Initiative, found three patterns: "First, and most consistent over time, is that newspapers and television emphasize violent crime. Second, the more unusual the crime, the greater the chance that it will be covered. Third, the rate of crime coverage increased while real crime rates dropped."</P>
<P>In fact, though homicide rates dropped by 33 percent between 1990 and 1998, media coverage of homicides increased by a ridiculous 473 percent! No wonder 62 percent of poll respondents thought that juvenile crime was on the rise--even though violent crime by youth is at its lowest point in more than two decades.</P>
<P>The report also uncovered a bias in the media coverage of crimes by people of color and whites. In fact, the media are reflecting the interests of rulers who drum up a fear of crime to deflect the blame for poverty from themselves.</P>


