<font face="Times New Roman, Times, serif" size="5"><b>Washington state workers begin rolling strikes</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OLYMPIA, Wash.--After days of fruitless talks, 19,000 members of the Washington Federation of State Employees started rolling strikes across the state April 18. The workers are demanding raises that keep up with the cost of living, caps on health insurance increases and no cuts in vital social programs.</P>
<P>On the first day, workers struck the Department of Social and Health Services Offices and many Washington state ports. The longshore workers' union vowed to honor picket lines at the ports, which shut some operations. A different set of state offices was due to be hit each day over five days.</P>
<P>On April 21, 1,500 workers rallied in Olympia to show their determination to win. The legislature and Democratic Gov. Gary Locke claim that there's no money to meet the federation's demands.</P>
<P>But state worker Kim Vincent says Locke has been saying that for the last four years. "Enough is enough," Vincent said. "We've been patient too long." In fact, the union says that its contract proposal would cost $160 million over two years--and that in the next two years, the state will lose $2.4 billion from tax cuts, mostly for businesses.</P>
<P>The state government has enough money to spend $1 billion on sports stadiums and to build new prisons--one of the few areas where its budget has expanded. But legislators claim there isn't enough money to continue funding dental and vision care for poor people--or to give state employees a raise that keeps up with inflation. Statewide, 79 percent of federation members voted to strike. At the University of Washington, the strike vote was 94 percent.</P>
<P>Other state workers' unions are supporting the federation. Service Employees International Union (SEIU) Local 1199, which represents nurses, voted to strike by 80 percent. At the University of Washington, SEIU-CSA-925, which represents office workers, voted to honor the federation's picket lines. The King County Labor Council sanctioned the strike, which means that Teamsters and bus drivers will respect pickets.</P>
<P>The rolling strikes are a step forward. But federation leaders see the strike as a short-term pressure tactic, not an all-out fight. This will limit our effectiveness.</P>
<P>Everyone who cares about humane policies in state government should support the state workers. And rank-and-file state workers will need to organize for more effective action.</P>


