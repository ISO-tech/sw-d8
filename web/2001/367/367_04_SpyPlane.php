<P><font face="Times New Roman, Times, serif" size="4">Cold Warriors in patriotic frenzy over China</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Spy plane hypocrisy</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>Washington's response to the crash-landing of its EP-3 Navy spy plane reeked of hypocrisy. In so many words, Pentagon officials have made it plain that they will spy on whoever they feel like. "If a Chinese spy plane crash-landed in California after knocking down one of our jets, I'm sure we'd give it right back," teased the satire newspaper the <I>Onion</I>. </P>
<P>Having failed in their attempt to railroad Chinese-American scientist Wen Ho Lee on spying charges, congressional Cold Warriors rushed to whip up a frenzy over this latest incident. The mainstream media were happy to oblige them. <I>Newsweek</I> devoted nearly 30 pages in two issues to the standoff. </P>
<P>Other important events --like the rebellion in Cincinnati--practically dropped from view. But then, urban riots don't do such a hot job of cultivating reverence for the "Greatest Country on Earth." </P>
<P>Not to be outdone by the hawks, a host of born-again loonies saw the spy plane collision as a golden opportunity to rouse their minions. Upon the return of the U.S. spies to their base, pilot Shane Osborne stretched credulity by announcing that "God flew our plane." Pastor Ron Willis, of the Family Bible Church attended by the EP-3 crew, agreed. "God can and does on occasion intervene, and he does give victory over physical death," he told reporters. </P>
<P>In a tacit endorsement of this miracle theory, <I>Newsweek</I> spotlighted cryptologist Rodney Young's statement that "it was as if angels had taken hold of the wings." Unfortunately for the Chinese pilot killed in the collision, it seems that God only helps the Americans.</P>

<B><P>Matt Nichter, </B>Chicago</P>



