<P><font face="Times New Roman, Times, serif" size="4">CEO pay still shooting up as layoffs start to hit</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Living like kings</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The gap in pay between U.S. workers and their bosses has grown so big that <I>Business Week</I> is running out of ways to describe it. "We're back to serfs and royalty in the Middle Ages," Edward Lawler, a management professor at the University of Southern California's Marshall School of Business, told the magazine for its annual CEO pay issue. </P>
<P>According to the survey, CEOs at 365 of the largest public U.S. companies earned an average of $13.1 million last year. That's 531 times more than their average employee. These bosses raked in $50,384 for every "working" day--almost twice what an average worker makes in a year.</P>
<P>With layoff announcements hitting right and left, U.S. workers are expecting tougher times ahead. But "don't expect CEOs to share the pain," says <I>Business Week</I>. The magazine predicts that the ultra-rich will actually do even better in the coming years--thanks to George W. Bush's tax-cut giveaway, which will mainly benefit the richest 1 percent of households.</P>
<P>Indeed, the economic slowdown and plummeting stock market had little impact on CEO compensation, which is up by 434 percent since 1991, according to <I>Business Week</I>. If the minimum wage had matched that pace of growth, it would now be more than $24.50 an hour.</P>
<P>The winners of the 2000 CEO greed and sloth award included:</P>
<P>--Citigroup CEO Sanford Weill, who took home $224 million.</P>
<P>--Disney's Michael Eisner, who "earned" a mere $72 million in salary and long-term compensation.</P>
<P>--Apple CEO Steven Jobs, whose bonus this year was his own $90 million Gulfstream V jet.</P>
<I><P>Business Week</I> had to admit that CEOs were paying themselves at "indefensibly high levels." Why is this shamelessly pro-corporate magazine suddenly concerned? Because it's worried about what will happen in the recession, when workers lose jobs at companies that pay their executives like kings. As <I>Business Week</I> concluded, "Let the pay that CEOs earn keep rising--and watch the economy--and execs might well remember the fate that has awaited royalty in the past."</P>


