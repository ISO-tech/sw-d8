<P><p><font face="Times New Roman, Times, serif" size="4"><b>Gas shutoffs delayed in Chicago</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--One hundred people came out on a frosty April 16 morning to stop Peoples Gas from shutting off heat for 14,000 poor customers. Protesters, furious with the bloated gas bills that Chicagoans were sent for heating this winter, were prepared to sit down and blockade company gates to stop trucks from leaving. </P>
<P>Rev. Jesse Jackson called on Peoples Gas CEO Richard Terry to accept a deal in which the company, the city of Chicago and the state of Illinois each paid one-third of the overdue bills. If the company refused, protesters planned to keep the gates shut. </P>
<P>But after a few hours, the normally cold-hearted Peoples Gas announced for the second time in April that it wouldn't shut off customers. The next day, the company agreed to Jackson's plan and called off the shutoffs.</P>
<P>The protests brought a victory for Peoples Gas customers. Now we need to show our support for union workers at Peoples Gas, who are in a tough battle for a fair contract and are threatening to strike. They plan to picket Peoples Gas corporate headquarters April 26 if contract negotiations aren't resolved.</P>

