<p><a name="Solid"></a><font face="Times New Roman, Times, serif" size="4"><b>Solidarity with the Palestinians</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Aaron Hess</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Some 5,000 marched from Times Square to lower Manhattan April 7 to demand justice and the right of return for 4 million Palestinian refugees. The spirited march specifically targeted U.S. support for Israel--and its reign of terror against Palestinians in the Occupied Territories.</P>
<P>At Union Square, demonstrators heard speeches from a range of speakers, including scholar and activist Edward Said, who called on people to tell the truth about the Middle East conflict. "When men, women and children go out into the streets to fight for what has been stolen from them--for water and education and land--the media calls this violence," Said said. "They say, 'Palestinians must end the violence.' But this is not violence. It is resistance. And our resistance must not end."</P>
<P>The huge march followed a conference held a week earlier at the Columbia University Law School which drew more than 300 supporters of Palestinian rights. A diverse group of speakers took the podium to talk about building the solidarity movement.</P>

