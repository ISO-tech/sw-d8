<p><a name="Charter"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop charter schools</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Benoit and Petrino DiLeo</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--On March 29, parents voted down an attempt to put the for-profit Edison Schools in charge of five public schools. But public education supporters have a fight on their hands--because Edison and its pals in the administration of Mayor Rudolph Giuliani are looking for ways to get what they couldn't from a democratic vote.</P>
<P>Edison wanted to take over five "failing" public schools and run them as charter schools--with a fat subsidy from the New York City Board of Education (BOE). Opponents of the deal say that the company would have gotten more money per student than the board currently spends at the schools.</P>
<P>Parents saw through the scam. "Like all companies, they're scavengers, and they feed off people," Elton Wegman, the parent of a seventh-grader at one of the schools, told <I>Socialist Worker. </I>In fact, Edison's top executives make a pretty penny running schools--with CEO Chris Whittle holding stock options worth more than $22 million.</P>
<P>And there's no evidence that the company produces better schools. In fact, an American Federation of Teachers study of 40 Edison schools in eight states found that most children performed as well or worse than students in comparable public schools.</P>
<P>The months that parents and activists spent mobilizing against Edison with protests and petitions paid off in the March 29 vote. But the fight isn't over.</P>
<P>"I wouldn't be surprised if they keep coming and coming if they fail here," Wegman said. "But it's not just about that. It's about keeping the city accountable for managing the schools that are failing. The BOE's job isn't to give up control. This school isn't theirs to give up. It's ours."</P>

