<p><a name="Gay"></a><font face="Times New Roman, Times, serif" size="4"><b>Madison gay rights</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--More than 500 people rallied April 2 to oppose the homophobic Rev. Fred Phelps and his 10 hate-spewing followers. Hiding behind barricades and a line of 20 cops, Phelps hurled insults at Madison community, with its openly gay elected politicians and a recent school board decision to hire a counselor for gay students. Counter-protesters followed Phelps as he picketed the University of Wisconsin-Madison student union, local gay bars and the school district building. </P>

