<p><a name="DC"></a><font face="Times New Roman, Times, serif" size="4"><b>D.C. General</b></font><br>
By Juliana Karr and Jeff Bale</P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Four hundred people again packed the pews of Union Temple Baptist Church to show their opposition to the privatization of D.C. General Hospital. Opponents say the privatization scheme will make health care less accessible for poor residents and hit hospital workers hard.</P>
<P>While the D.C. City Council voted unanimously against the plan, Mayor Anthony Williams and the federal government's Control Board have the authority to ram the deal through. Details of the privatization deal with Doctors Community Healthcare Corp. (DCHC) have finally come out. </P>
<P>D.C. General will be pared down to an emergency room, with most services shifted to Greater Southeast Community Hospital. The cuts leave D.C. General vulnerable to being shut down for good--which is what hungry real estate developers, after the hospital's riverfront property, want.</P>
<P>In return, DCHC would be paid get more than $100 million in the first year. That's well over twice what D.C. General gets to operate.</P>
<P>Details of the privatization deal have given new energy to those defending the hospital.  Unions, ministers and other activists are planning a march in mid-May to save D.C. General.</P>

