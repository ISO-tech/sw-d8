<p><a name="Pow"></a><font face="Times New Roman, Times, serif" size="4"><b>California power protest</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Seventy-five people came out in April to express their anger to Public Utilities Commissioner Jeffery Brown about proposed 40 percent hikes in electricity rates. Person after person voiced their disgust with the massive profits being made by energy giants like Enron and Dynergy.</P>
<P>That very day, Enron had reported a fourfold increase in profits. Meanwhile, residents say that their utility bills have tripled--even before the latest proposed increase. "The state spends $58 million a day to buy electricity on behalf of PG&amp;E," said community activist Don Paul. "That money could be spent for badly needed improvements in public housing."</P>
<P>In the face of rising anger, Brown was forced to admit, "Public power has a respectable place in the discussion. But when activists talked about an initiative on the ballot that would create a local municipal utility district to take over from PG&amp;E in San Francisco, Brown insisted that it was important to "go slow."</P>


