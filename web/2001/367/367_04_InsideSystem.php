<P><p><font face="Times New Roman, Times, serif" size="4"><b>Wrong time to do the right thing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">SOMETIMES IT'S the wrong time to do the right thing. At least that's the opinion of the <I>Union Leader</I>, the reactionary New Hampshire newspaper.</P>
<P>A plaque was erected in February in honor of 12 New Hampshire citizens who fought with the Abraham Lincoln Brigade against the rise of fascism in Spain. Hundreds of people from the U.S. volunteered to fight in Spain against Gen. Francisco Franco and his fascist allies, Germany's Adolph Hitler and Italy's Benito Mussolini.</P>
<P>But the <I>Union Leader</I> ran an editorial denouncing the plaque--and convinced some legislators to have it removed. "The fact that America fought fascism later does not validate the defiance of those who fought in Spain," said Rep. Tony Soltani. "These people were fighting fascism at the wrong time."</P>
<P>--Associated Press, February 18, 2001</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>The Mercedes gap</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">THE NEW chair of the Federal Communications Commission (FCC) isn't too interested in communications. Michael Powell has long downplayed the need to equip public schools with up-to-date computer equipment--so that poor and working-class students can learn how to use the Internet and all the information it provides access to. Powell recently compared the "digital divide," the huge gap in Internet access between rich and poor households, to a term he himself coined: the Mercedes divide.</P>
<P>"I cannot say that I understand the urgency," Powell wrote in 1999. "The rhetoric is powerful: 'A digital divide,' 'information haves and have-nots,' 'affluent suburbs versus poor urban children.' They certainly are clever sound bites that give the sense of urgency and crisis." </P>
<P>Powell recognizes that only 51 percent of classrooms are wired, but says it isn't really a cause for alarm. But if they get computers at all, poorer schools often rely on donations of outdated computer equipment from corporations--which then take a tax break for their "generosity."</P>
<P>Even industry spokespeople see the point that Powell misses. "There's a point where you have to make an investment and not run our schools on leftovers," Robin Willner, director of IBM corporate-community relations, told Congress last fall.</P>
<P>--<I>Miami Herald</I>, April 2, 2001</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>It's breakfast time with Big Brother</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">"WE'RE REACHING people from the moment they wake up until they fall asleep." In case it wasn't obvious, those words gushed forth from international media boss Rupert Murdoch.</P>
<P>"We give them their morning weather and traffic reports through our television outlets around the world," he wrote in his 1999 annual report. "We enlighten and entertain them with such newspapers as the <I>New York Post</I> and the <I>Times</I> [of London] as they have breakfast. </P>
<P>"We update their stock prices and give them the world's biggest news stories every day through such news channels as Fox or Sky News. And when they get home in the evening, we're there to entertain them...Before going to bed, we give them the latest news, and then they can crawl into bed with one of our best-selling novels from HarperCollins."</P>
<I><P>Extra!</I>, November/December, 2000</P>

