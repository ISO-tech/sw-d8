<font face="Times New Roman, Times, serif" size="5"><b>Don't let Bush take our abortion rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 16</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH SCHULTE reports from the Emergency Action for Women's Lives</b></font></P>

<P>WASHINGTON--Some 15,000 women and men turned out April 22 for the first national abortion-rights demonstration in almost a decade. Many had never been to a protest before--and traveled from up and down the East Coast and around the Midwest to attend the "Emergency Action for Women's Lives," called by the National Organization for Women (NOW).</P>
<P>One group of students who just started a pro-choice group at their campus in Las Vegas traveled nearly 2,500 miles for the rally and march. "We're a minority at Boston College, so it's nice to be here," said Samantha Alvarez, who drove to Washington with about a dozen students from her feminism class at Boston College, a Catholic school. "We've always lived in a time when abortion has been legal. It's strange to think of something that was a given and now is in jeopardy of being destroyed."</P>
<P>"It's good to see people come together," agreed Kelly Dugan, also from Boston College, "but I don't think it should be necessary to do this. Abortion should just be a right."</P>
<P>There were many longtime activists, too. "It's encouraging to see young people coming out," said Audrey Burns, a grandmother who became active around abortion and women's rights some 20 years ago. "They're beginning to see that they better speak up."</P>
<P>They better speak up because of George W. Bush. From his gag order barring international family-planning organizations from talking about abortion to his plans to take away women's access to the abortion pill RU-486, Bush wants to drive a steamroller over women's right to choose. Meanwhile, congressional Republicans have other restrictions in store.</P>
<P>The three-hour rally turned into a march that wound past the Supreme Court building. Along the way, a couple dozen anti-abortion protesters held gruesome photos of what they called aborted fetuses. These hateful fanatics--who had to hide behind police--were drowned out by pro-choice marchers chanting, "Racist, sexist, anti-gay! Born-again bigots, go away!"</P>
<P>Before the march, Dr. James Pendergraft, an abortion provider from Florida, described his ordeal facing these extremists at his clinic. Until Pendergraft opened his offices in 1997, Ocala, Fla., had no abortion clinic--the last one was burned down in 1989. Outrageously, local officials and the FBI are charging Dr. Pendergraft with "extortion" because he filed a lawsuit requesting protection for his clinic from the anti-abortionists. He could face up to 30 years in prison.</P>
<P>"I think with Bush in office, it's easier for them to think that they can hack away at women's rights," Dr. Pendergraft told <I>Socialist Worker</I>. "It's time for everyone to step up and be counted--to be as loud and visual as possible." This is exactly what it will take to keep Bush--and the anti-abortion fanatics--from destroying abortion rights.</P>
<P>But it would be wrong to think that the attack on choice began with Bush. "Let's be brutally honest--this didn't happen just in the last 100 days," Sherry Wolf, of the International Socialist Organization, told demonstrators. "The erosion of abortion rights, from parental consent laws to late-term abortion bans, has staggered on for the last eight years under Clinton."</P>
<P>It was a huge step forward for NOW to call the April 22 action after eight years of relative inactivity. But where the fight goes next is the question.</P>
<P>Many speakers at the opening rally --which included NOW President Patricia Ireland and other representatives of women's groups--voiced the fear that Bush could have an opportunity to appoint a Supreme Court justice. Sandra Day O'Connor, a right-winger who nevertheless has voted to uphold the court's Roe v. Wade decision legalizing abortion, is considering retirement.</P>
<P>But the march itself showed the potential to build a dynamic new movement to stop Bush from pushing through an anti-choice justice. Judging from the enthusiasm of the crowd--and the way many people mobilized themselves to get there--the march could have been much larger had NOW devoted more resources to organizing.</P>
<P>Abortion became legal in the first place under the Republican Nixon administration because a strong movement relied on protest, not lobbying efforts. Building a new fighting movement is the way to defend abortion rights today.</P>


