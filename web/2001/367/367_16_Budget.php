<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's budget rip-off</b></font><p><font face="Arial, Helvetica, sans-serif" size="2">April 27, 2001 | Page 16</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">PRESIDENT BUSH promised that his budget would make his $1.6 trillion tax cut "affordable." And it will--but only by stealing billions from the poor, the sick, the elderly and kids. The candidate who promised to "leave no child behind" would kick 2,500 children out of the Head Start program, eliminate the $20 million Early Learning program and slash $15.7 million from a child abuse prevention program.</P>
<P>Bush's budget also eliminates the supplemental welfare grants to 17 states that are designed to help families who are cut off by time limits imposed by welfare "reform." Social Security and Medicare also face major cuts, as does public housing.</P>
<P>"The pressure comes from the effort to make room for the president's tax cut, which would heavily benefit the better-off," the <I>Washington Post</I> reported. In fact, the largest 4,500 family estates would get as much of a tax break as 142 million people, according to the Center on Budget and Policy Priorities.</P>
<P>"The idea is to so lower federal revenues and to run up military spending, so that, in time, discretionary domestic spending--the social programs that work at the common good--will be squeezed out," newspaper columnist Tom Teepen explained. Congressional Democrats have criticized Bush budget--not because it hammers working people and the poor, but because it's "fiscally irresponsible."</P>
<P>"A few years ago, Democrats championed such things as universal health care," wrote Robert Reich, former labor secretary under Bill Clinton. "Now that there's money to pay for it, they're rooting for the smaller of two huge tax cuts instead." It's time to tell Washington that we won't let Bush rob working people to keep his wealthy pals fat and happy.</P>


