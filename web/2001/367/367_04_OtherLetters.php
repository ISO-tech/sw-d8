<p><a name="Ugly"></a><font face="Times New Roman, Times, serif" size="4"><b>The ugly reality of welfare "deform"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>Thank you for publishing <I>Socialist Worker</I> and the <I>International Socialist Review</I>.</P>
<P>I am writing to you about a group of women who receive public assistance or have in the past. They are challenging the corruption of welfare "reform" and Tommy Thompson, who was recently promoted from governor of Wisconsin to Secretary of Health and Human Services.</P>
<P>As usual, women with a child or two are expected to conform to draconian rules and regulations in order to get a pittance, while those at the top go on their merry way. While Milwaukee's infant mortality rate among Blacks increased by 37 percent since 1997, the welfare system's top private administrators gave themselves bonuses.</P>
<P>William Martin, a former aide to Thompson, was paid $98,000 in bonuses over three years. In 1999 alone, Martin got more than $170,000 when you count his nearly $20,000 bonus!</P>
<P>This exposes the negative impact of the privatization of government services. Our objective should be the collective ownership and control of resources to satisfy the needs of people, not profit. As Marx said, "History does nothing...History is rather nothing but the activity of humanity in pursuit of its ends," and "human power is its own end."</P>
<B>
<P>Frank Roemhild, </B>Bayfield, Wis.</P>


