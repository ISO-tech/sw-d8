<p><a name="Voucher"></a><font face="Times New Roman, Times, serif" size="4"><b>Bush's voucher plan would hurt teachers and students</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B></I>, </P>
<P>The wimpiness shown by the Democratic Party in opposing the backward policies of President Bush is infuriating. Many of the important things that need to be said or emphasized are neglected in their replies to Bush's attacks. </P>
<P>One important example is Bush's attempt to impose a school voucher system. School vouchers are a serious threat to the education of working-class children--and to the workers who provide this education.</P>
<P>For a moment, leave aside the fact that the voucher money will be used to subsidize the teaching of pious bigotry. Leave aside the fact that poor families who can't afford the tuition fraction that the vouchers won't cover will be left out. Leave aside the fact that the sickening standardized tests--which Bush couldn't pass himself--single out schools for voucher-led financial ruin based on race, gender and class bias. </P>
<P>The most basic problem is that vouchers are a non-solution to the crisis in our schools because they can't be implemented on a significant scale--and any attempt to do so would create havoc. It doesn't take a rocket scientist to envision the impossibility of absorbing into parochial schools the millions of children attending the dilapidated schools of the inner cities. </P>
<P>If Bush really wants to improve education, he would use the alleged budget surplus to restore funding to all the school programs that have been cut over the past two decades. Only then can we begin to correct the savage inequalities in our schools that have forced some desperate parents--many of them Black--to buy into Bush's voucher plan, charter schools and similar schemes. </P>
<B>
<P>Hector Reyes, </B>Chicago</P></FONT></BODY>

