<p><a name="GPigs"></a><font face="Times New Roman, Times, serif" size="4"><b>Making profits from human guinea pigs</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>Not even Nobel Prize-winning doctors at world-class institutions are immune from corporate greed. Three doctors at the Fred Hutchinson Cancer Research Center in Seattle carried out a study of Graft-vs.-Host Disease, an immune system reaction to bone marrow transplant operations.  </P>
<P>The study started in 1981 and ran 12 years. First, the very basis of the study was dubious--a human subjects review board initially failed to approve it because of lack of animal testing.</P>
<P>Second, there was the lack of information given to patients who participated in the study. Patients weren't informed of the many risks involved nor told about more successful alternative treatments--taking place right down the hall! Lastly, patients weren't told that the Hutchinson clinic, its cofounder and two other doctors got stock options as well as lucrative consulting fees and board positions from a company that owned some antibodies used in the study.  </P>
<P>Not one success was reported from the study. Today, 80 out of 82 people enrolled are dead. These patients were among those with the greatest chance of surviving with other treatments.  </P>
<P>But the Hutchinson clinic and these doctors are now wealthy.  Those stock options today are worth millions of dollars. In the end, corporate dollars proved more powerful then the Hippocratic oath.</P>

<B><P>Ken Matsumura, </B>Seattle</P>


