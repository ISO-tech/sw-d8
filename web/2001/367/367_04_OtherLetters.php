<P><p><font face="Times New Roman, Times, serif" size="4"><b>Washington budget crisis hits poor hardest</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>The state of Washington faces a budget crisis. The legislature repealed a vehicle tax that provided $800 million per year. It also gave $6 billion in tax breaks to business during the last 10 years.</P>
<P>Now Democratic Gov. Gary Locke and the predominantly Democratic legislature are talking about major budget cuts. State employees will only get a 2.2 percent raise under Locke's budget--less than the inflation rate of 3.7 percent. Worse yet, medical services for poor people--such as vision and dental coverage--are being drastically cut.</P>
<P>The cuts are cruel and shortsighted. As Joe Martin, a social worker at the Pike Market Clinic, says, "Teeth that look more like tree stumps" can be a major obstacle to finding a job. Marlene Johnson, a single mother of three, suffers from multiple sclerosis and failing eyesight. Yet she's in danger of losing vision coverage due to the cuts.</P>
<P>But the budget crisis is not hitting all agencies equally. Prison spending is actually going up--and so is spending for the lottery and horse-racing commissions.</P>
<P>Washington has one of the most discriminatory tax structures in the U.S. The poor pay a higher proportion of their income in taxes than the rich.</P>
<P>State employees are organizing rolling strikes against the budget--not just to get higher wages but to stop the erosion of services to the poor. They are beginning to raise the issue of taxing the rich. A successful strike could help change the priorities of state government to the benefit of all poor and working people.</P>

<B><P>Steve Leigh, </B>Seattle</P>


