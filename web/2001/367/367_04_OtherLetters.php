<p><a name="Abuse"></a><font face="Times New Roman, Times, serif" size="4"><b>"I've seen abuse in N.Y. prisons"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</P>
</B><P>As I was reading the <I>Daily New Express</I> recently, I was deeply angered at an article that described the intentional degradation of people in the criminal injustice system. Inmates at the Chantanqua County Jail have been wearing bright coveralls for years. Now prison officials want to embarrass them even more by suiting them up in black-and-white striped outfits--just like the chain gangs in the South during slavery days.</P>
<P>What's worse is the bad treatment, day in and day out, by correction officers at this small facility. I'm talking about daily attacks from officers who spit in your face and use you as a punching bag for the purpose of "correcting" you.</P>
<P>I was once incarcerated in this upstate jail, so I know what it's like. I think the public needs to know about these abuses, so people can see that prisons aren't about right or wrong but the hypocrisy of the so-called justice system.</P>
<B>
<P>Charles X, </B>New York City</P>


