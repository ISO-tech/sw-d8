<p><a name="bring democracy"></a><font face="Times New Roman, Times, serif" size="4"><b>U.S. won't bring democracy</b></font></P>

<B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Many of us in the antiwar movement recently have been learning about the incredibly harsh conditions faced by the people of Afghanistan under the Taliban government. </P>
<P>We've been learning about the history of U.S. and Soviet terror in Afghanistan, which helped produce the terror of the Taliban. </P>
<P>We've also been learning about the humanitarian violations of the Northern Alliance, which the U.S. has decided to back.</P>
<P>Recently, I have heard some people argue that if the U.S. overthrows the Taliban, it will help or even liberate the people currently under its rule. This is similar to the argument that the Bush administration used during the 1991 Gulf War: that since Saddam Hussein was a brutal dictator, the U.S. war against him would help free the Kurds or other oppressed groups in Iraq.</P>
<P>This turned out to be the cruelest of lies. The U.S. cynically manipulated the legitimate claims of the Kurds to self-determination and then gave support to a brutal crackdown by Saddam's troops. </P>
<P>The U.S. never ousted him, but it did kill hundreds of thousands of ordinary Iraqis with bombs and sanctions that have left the country in a pre-industrial state.</P>
<P>People are right to have deep concerns about the Afghan people. But we can't rely on the U.S. government to help them--or to bring a democratic regime to power. </P>
<P>Bringing a halt to U.S. intervention in the region will do far more to aid the liberation of the people of Afghanistan.</P>
<B><P>Meneejeh Moradian,</B> New York City</P>

