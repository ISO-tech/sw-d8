<p><a name="coverage"></a><font face="Times New Roman, Times, serif" size="4"><b>I agree with your antiwar coverage</b></font></P>

<B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I just read your September 28 issue, and I agree totally with your "No to War" coverage. I am wasting no time in giving this information to others.</P>
<P>I think it is also very significant that the military forces of Britain were on the move to the Middle East before September 11. There's no doubt in my mind that Bush's war drive began well before the tragedy of September 11.</P>
<P>In 1989, after the murder of 7,000 Panamanians, I chose not to reenlist in the Iowa Army National Guard. Today, I fear we have nearly as many victims in the disaster in New York.</P>
<P>I'm sending you a donation for a subscription to your paper, and if you can, I'll take 10 of your antiwar buttons.</P>
<P>I'm sending this even though I'm on strike here at Maytag-Amana, where I work.</P>
<P>Thank you and God bless.</P>
<B><P>Fred M.,</B> Marengo, Iowa</P>

