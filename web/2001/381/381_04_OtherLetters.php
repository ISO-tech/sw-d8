<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's double standards</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I was sickened to read recent reports that the Bush administration is trying to tar the Sandinista party in Nicaragua--which may return to power in elections next month--as a "terrorist" organization.</P>
<P>State Department officials are accusing the Sandinistas of having links to Iraq and Libya, as well as to the FARC rebels in Colombia and the ETA separatist movement in Spain. They also claim that leading Sandinistas "have long histories of grossly violating civil and human rights and suppressing democratic activities."</P>
<P>What makes this lie so outrageous is that it was the U.S. government that organized terrorism against the Sandinistas when they ruled Nicaragua in the 1980s.</P>
<P>In 1986, the International Court of Justice (commonly known as the "World Court") condemned the United States for mining Nicaragua's harbors and funding and training the contra rebels--who tortured and butchered more than 10,000 Nicaraguan civilians. But Washington simply ignored this ruling, refusing to recognize the Court's jurisdiction.</P>
<P>As the U.S. government prepares to use its "war on terrorism" as an excuse to attack any country or group that it doesn't like, its hypocrisy and double standards have never been more obvious.</P>
<B><P>Phil Gasper,</B> San Leandro, Calif.</P>

