<p><a name="tribunal"></a><font face="Times New Roman, Times, serif" size="4"><b>Could a tribunal bring justice?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Recently, some antiwar activists have been calling for an international tribunal to apprehend and prosecute those responsible for the attacks on September 11. They argue that in such a tribunal, justice would be served.</P>
<P>I disagree. In the various systems of the developed world, "justice" is reserved for the poor--while white-collar criminals, brutal cops and politicians remain exempt.</P>
<P>The same injustice is clear in international bodies such as the United Nations. The UN regularly condemns acts of terror against civilians in the developed world, while at the same time continues to impose its own terror on the civilians of Iraq through economic sanctions. Meanwhile, the UN ignores the terrorism carried out by the U.S. and Israeli governments in the Middle East. </P>
<P>In this way, the UN--the largest instrument of international law--is itself an instrument of war and terror.</P>
<P>My message for the advocates of an international tribunal is this: No international court can be established that will be free of the racism and double standards of the UN and the U.S.</P>
<P>If you should go ahead and try to set up your imaginary court, then you must first prosecute well-known, proven terrorists such as Henry Kissinger, Ariel Sharon, George Bush, George W. Bush, the UN officials who continue to murder 5,000 Iraqi civilians a month and the agents of terror in the CIA and Israeli Defense Forces. </P>
<P>The list is endless. Osama bin Laden--the unproven mastermind behind the September 11 attacks--will have to wait in line.</P>
<B><P>Suzanne Adely,</B> New York City</P>

