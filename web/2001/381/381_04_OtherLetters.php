<p><a name="could help"></a><font face="Times New Roman, Times, serif" size="4"><b>He could help kids by not bombing</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The man illegally occupying the White House made the following announcement a few days ago: "We are asking every child in America to earn or give a dollar that will be used to provide food and medical help for the children of Afghanistan. You can send your dollar in an envelope marked 'America's Fund for Afghan Children' right here to the White House, 1600 Pennsylvania Avenue, Washington, D.C."</P>
<P>I suggest that we send the envelopes, but not with any money--instead with a note saying something like, "You could help the children of Afghanistan a great deal more if you'd stop bombing their country, killing them and their parents, and making it impossible for real charitable organizations to bring food, medical and other aid into the country."</P>
<P>It would be better if you signed your name and address, but you must realize in the expanded police state that is now looming, this could likely get you put on a list, if not worse.</P>
<B><P>William Blum,</B> Washington, D.C.</P>

