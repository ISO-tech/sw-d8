<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pushing patriotism for profits</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH has a message for workers suffering from the recession and the economic impact of the September 11 attacks: Suffer some more. </P>
<P>Consider what the <I>New York Times</I> said about the White House "economic stimulus" plan: "Apart from its economic flaws, the measure is flat-out unfair. Of the $54 billion in accelerated tax cuts, every penny would go to the top 30 percent of taxpayers. Half would go to the top 5 percent. Eighty percent of the benefits from the capital gains tax cuts would go to the top 2 percent of households."</P>
<P>Just $2.3 billion of the $100 billion in the White House's proposal would go to extended benefits for unemployed workers. But Republicans in Congress--who already blocked measures to aid laid-off airline and aerospace workers--have vowed to vote against extra jobless benefits. And just 39 percent of all employees are even eligible to receive unemployment compensation.</P>
<P>So much for "national unity." And with job security disappearing for tens of millions of people, Bush's appeals for patriotic shopping sprees to boost the economy seem obscene.</P>
<P>Bush even had the gall to use last week's summit for Asia-Pacific Economic Cooperation to declare that the U.S. is "committed to opening the doors of trade and opportunity and therefore [improving] the lives of its citizens, versus the terror network, which has a dark view, an oppressive view and no regard for human life."</P>
<P>In other words, Bush is using the war against terrorism to push through a law granting him fast-track trade negotiating authority for more corporate trade deals.</P>
<P>Unions are under pressure to back off their demands. In New York City, SEIU/1199 agreed to a five-month extension on a contract with private hospitals that expired October 31.</P>
<P>But employers have only become more aggressive about pushing their program. Airline bosses have used the threat of bankruptcy to announce mass layoffs and drive through corporate restructuring.</P>
<P>And New York Gov. George Pataki plans to cut 5,000 state jobs through early retirements and to impose a hiring freeze--just when tens of thousands of New Yorkers urgently need employment. </P>
<P>Fortunately, groups of workers--from state employees in Minnesota to tank makers at General Dynamics in Michigan and Ohio--have shown the courage to strike to defend their interests. Asked why United Auto Workers Local 12 was willing to strike a defense plant during a war, Al Logie, a mechanic, told reporters, "They should have thought about that when they gave our benefits away."</P>
<P>By standing up for their demands, workers can send a message to employers that they can't use the war to ram through an anti-labor agenda.</P>

