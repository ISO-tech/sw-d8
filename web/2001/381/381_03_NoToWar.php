<P><font face="Times New Roman, Times, serif" size="5"><b>Saying no to war around the world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH claims that people around the world support his "war against terrorism." But an attempt to estimate the number of people who've attended antiwar demonstrations across the globe since September 11 comes up with close to 1 million.</P>
<P>In the U.S., despite the continual pro-war drumbeats from the mainstream media, thousands have turned out for demonstrations.</P>
<P>San Francisco saw a march of several thousand people October 20--the latest in a series of antiwar actions in the Bay Area. A few days before, the Berkeley, Calif., City Council braved the likelihood of a witch-hunt and passed a resolution calling on the U.S. to stop the war.</P>
<P>Across the country, in Burlington, Vt., 1,200 people came to hear historian Howard Zinn speak out against the bombing. When Zinn asked the crowd who was in favor of military action, less than a dozen people raised their hands.</P>
<P>Other teach-ins and public forums have also been popular. When Noam Chomsky, a longtime opponent of U.S. militarism, spoke at the Massachusetts Institute of Technology, nearly 3,000 people attended. A march in Boston the same day drew 1,000.</P>
<P>As the U.S. stepped up its bombing of Afghanistan, a wave of protest spread across southern Asia, northern Africa and the Middle East.</P>
<P>In Calcutta, India, more than 70,000 people attended a peace march, chanting, "Stop the terrorism against Afghanistan." And in Pakistan, Muslim leaders opposed to the government's support of the U.S. war called for a general strike.</P>
<P>U.S. politicians dismissed the demonstrations as "anti-American" outbursts by hard-line Islamists. But opposition to the U.S. war spread far beyond the Muslim world.</P>
<P>In South Korea, thousands attended an antiwar protest endorsed by more than 700 organizations, including trade unions, political parties and human rights groups. In Italy, some 150,000 people joined a 13-mile march for peace from Perugia to Assisi.</P>
<P>And 50,000 antiwar demonstrators took to the streets of London October 13. "We expected a lot of people, but this just shows that there really is a big upsurge of people who are opposed to the conflict in Britain," said Carol Naughton, the chair of Campaign for Nuclear Disarmament, which called the demonstration.</P>
<P>Washington's war makers may believe they call the shots around the globe. But growing numbers of people are saying no to this war!</P>

