<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is the U.S. waging a "just" war?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2001 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is calling "Operation Enduring Freedom"--the U.S. war on Afghanistan--a "just" war.</P>
<P>But the ruling classes of all countries always declare that their side is conducting a just war. To listen to them, wars are never fought for profits, to demonstrate who is boss or for other such crudities.</P>
<P>The current war is "just," according to some, because the U.S. is acting in "self-defense" against terrorism. But by this logic, Afghanistan has the more legitimate claim of self-defense. And after a decade of debilitating sanctions and endless bombing raids, so does Iraq.</P>
<P>This begs the question: What is the criterion for judging such things?</P>
<P>Since the turn of the last century, most wars have been the outgrowth of imperialism--the spillover of economic conflicts between the world's richest and most powerful states into military conflicts.</P>
<P>Each time a temporary "balance of power" is established between the powers, new developments--the rise of new pretenders to world-power status, the onset of economic depression--upset that power balance and give rise to wars in which there are new winners and losers in the struggle for world hegemony.</P>
<P>There are also, within this framework, wars conducted by oppressed colonies and weaker nations against the domination of the stronger states. The war waged by the Vietnamese people against French colonialism, and later U.S. imperialism, was a just war of national liberation--every bit as much as America's War of Independence.</P>
<P>There are also civil wars fought between classes. The U.S. Civil War--as terrible and bloody as it was--was a just war waged for the liberation of Black slaves.</P>
<P>The Russian civil war after the 1917 revolution--in which the organized and armed workers of Russia fought to defend their fledgling workers' state against military invasion and Tsarist reaction--was a just war of defense.</P>
<P>Whether a war is just or not doesn't depend on who fired the first shot. Japan "fired the first shot" in the Second World War at Pearl Harbor. But it later turned out that the U.S. deliberately maneuvered Japan into attacking--even knowing where Japan was going to attack. Neither side of the war had "justice" in mind--but rather who would control the Pacific.</P>
<P>In the early 1960s, according to documents from the National Security Agency, the Joint Chiefs of Staff proposed to President Kennedy a plan to commit acts of terrorism on U.S. soil and then blame them on Fidel Castro, thus tricking Americans into supporting war against Cuba. That the plan was rejected doesn't change the fact that it was supported by America's top military brass as a legitimate means to provoke an unjust war.</P>
<P>"The philistine does not realize that war is 'the continuation of policy,'" wrote the Russian revolutionary Lenin, "and consequently limits himself to the formula that 'the enemy has attacked us,' 'the enemy has invaded my country,' without stopping to think what issues are at stake in the war, which classes are waging it, and with what political objects."</P>
<P>In 1935, Italy--a fascist dictatorship under Mussolini--invaded Ethiopia, which was run by a backward monarchy. Leon Trotsky attacked those on the left who condemned both sides. For Trotsky, the issue wasn't the political character of the regimes involved, but Italy's war aims: colonial conquest. He argued that every socialist had to support Ethiopia against Italy--the oppressed nation against the oppressor.</P>
<P>The U.S. government is using the September 11 attacks as a pretext to establish American power in Central Asia and to create an open-ended excuse ("fighting terrorism") to justify its every military adventure.</P>
<P>It is a policy involving the conquest of a weak nation, already debilitated by endless war and famine, by the world's "sole" superpower. It is therefore an unjust war.</P>

