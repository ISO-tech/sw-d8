<p><font face="Times New Roman, Times, serif" size="5"><b>Does labor need more Hoffas?</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS: </B>Thaddeus Russell, <B><I>Out of the Jungle: Jimmy Hoffa and the Remaking of the American Working Class.</B></I> Alfred A. Knopf, 232 pages, 2001, $26.</P>
</font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Don Schraffenberger, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LAST WEEK, former Teamsters president and reformer Ron Carey was acquitted of all charges in a federal perjury trial.</P>
<P>The decision exposed the frame-up that the government used to throw the 1997 UPS strike leader out of the union. It will give confidence to members everywhere who are fighting for democracy and rank-and-file power.</P>
<P>But in his new book <I>Out of the Jungle,</I> Thaddeus Russell poses a completely different strategy. In an attempt to challenge New Left labor historians who emphasize rank-and-file struggles, Russell pushes workers to the background.</P>
<P>Instead, he shines the spotlight on former Teamsters leader James R. Hoffa and his personal struggle to organize the unorganized. What he ends up with is an apology for backward, self-serving union leaders who live like parasites off members.</P>
<P>Russell claims that it was the competition between the Teamsters and the CIO unions to organize workers in the 1930s and 1940s that delivered the best contracts.</P>
<P>True, Hoffa stole from the membership. But Russell claims that truck drivers idolized him anyway--because, no matter how corrupt, he delivered the goods.</P>
<P>Hoffa hated government interference in the union, yet he routinely bribed politicians and cops. And he ordered dissidents beaten out of the Teamsters. Russell argues that the "jungle" of competition--against the bosses, but mostly against other unions--made Hoffa a brutal but successful union leader.</P>
<P>What's more, Russell concludes that it was better that Hoffa's son, current Teamsters president James P. Hoffa, beat reform candidate Tom Leedham in 1998.</P>
<P>The problem, Russell says, is that power corrupts reformers, too. But he uses Ron Carey as an example--without ever acknowledging the double standards and outright lies that were used to take Carey down.</P>
<P>Russell even absurdly claims that it was the Teamsters old guard and the memory of Jimmy Hoffa that won the 1997 UPS strike--because Carey was forced to prove he could take on the company. Nothing could be further from the truth.</P>
<P>The old guard tried to sabotage the strike. In Chicago, old-guard leader Frank Wsol refused to call his local out on strike. It was Hoffa's son who bad-mouthed the strike in the national media.</P>
<P>Russell believes that reform-minded Teamsters, like Teamsters for a Democratic Union (TDU) members, are better off not winning union office. "And so it appears that if TDU maintains its dissident stance, and if members continue to demonstrate their desires," he writes, "the junior Hoffa will be forced to deliver the goods."</P>
<P>True, members need to pressure their leaders no matter who holds office. But it will be easier to fight back with Leedham as president than with Hoffa.</P>
<P>We need a union movement committed to rank-and-file power and not to the bloated expense accounts of Teamster officials living off a false memory of Jimmy Hoffa.</P>

