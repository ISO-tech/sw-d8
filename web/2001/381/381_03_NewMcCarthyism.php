<p><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to the new McCarthyism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 830 people have been arrested in the criminal investigation of the September 11 air attacks. But in almost no case have officials discovered any tie with the hijackers&#150;except for skin color and unlucky coincidences.</P>
<P>On October 11, Tarek Albasti was arrested at his Indiana restaurant, along with his uncle and seven others. The reason? Albasti, who is Egyptian, had received flying lessons as a gift from his in-laws.</P>
<P>"This is really a dark time for America," said Albasti's mother-in-law. "I don't think we need to increase the darkness by having the law stretched to these lengths by the attorney general."</P>
<P>While the hundreds of arrests and detentions have failed at their stated intention of "rooting out terrorists," they have succeeded at their unstated aim--whipping up a climate of fear and suspicion.</P>
<P>Since September 11, the FBI has received 365,000 tips from people reporting "suspicious behavior." Incidents of airline crews and passengers refusing to fly with Middle Easterners on board are commonplace.</P>
<P>George W. Bush and Attorney General John Ashcroft may mouth caution and calm. But they're the ones responsible for spreading the paranoia--with their daily predictions that more attacks are on the way.</P>
<P>And they're using the recent anthrax outbreaks, which have understandably scared people, to stoke hysteria and create a climate where anything goes in the name of "security." Anything, of course, includes antiterrorism legislation passed in Congress last week that expands law enforcement's power to wiretap, monitor e-mails and detain people.</P>
<P>Politicians who have for years pushed for more restrictions on immigration and fewer civil liberties are getting policies they've always dreamed of.</P>
<P>Before September 11, even the Bush administration was considering proposals to grant amnesty for immigrants. Now the talk is about militarizing the border and limiting student visas.</P>
<P>"How do you defend your home if your front and back doors are unlocked?" Rep. James Traficant Jr. (D-Ohio) declared last week. "If 300,000 illegal immigrants can gain access to America every year, trying to find a better life, do not doubt for one moment that a larger contingent of people with evil intentions could gain entry into America and continue to kill American citizens."</P>
<P>But these sleazy attacks don't end with immigrants. The Washington establishment has gone on the offensive against all opponents of its right-wing agenda.</P>
<P>When author Susan Sontag wrote a <I>New Yorker</I> essay that challenged Washington's flag-waving after September 11, she was savaged. "I have been amazed by the ferocity of how I've been attacked," Sontag told <I>Salon</I> magazine. "One article in the <I>New Republic,</I> a magazine for which I have written, began: 'What do Osama bin Laden, Saddam Hussein and Susan Sontag have in common?'"</P>
<P>The FBI has threatened Women in Black, an international women's peace network, with a grand-jury investigation. And antiterrorism legislation passed by the House is so vaguely worded that many left-wing groups would be considered "terrorist."</P>
<P>The atmosphere smacks of the political crackdown of the late 1940s and 1950s--the era that came to be known as McCarthyism. The anticommunist witch-hunts led by Sen. Joseph McCarthy targeted thousands of people who were fired from their jobs, spied on by their neighbors and harassed in every area of their lives.</P>
<P>The fight against the "red menace" gave the U.S. government and the bosses the authority to attack leftists, union activists and immigrants. This time around, it's being done in the name of "the fight against terrorism."</P>
<P>The lesson of McCarthyism is clear for today--we have to take these attacks head-on.</P>
<P>Most people remember McCarthyism as a shameful period in U.S. history, and we can't let Bush and Ashcroft bring it back. We have to stand up against the attack on our rights in the name of "security."</P>

