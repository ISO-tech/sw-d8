<P><font face="Times New Roman, Times, serif" size="5"><b>IMF-World Bank set new meeting</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE IMF and World Bank are getting back to business as usual. The terrible twins of the international financial system announced that they plan to hold a scaled-down version of their annual meetings November 17-18 in the Canadian city of Ottawa.</P>
<P>The meetings were originally scheduled for the weekend of September 29-30 in Washington, D.C., but were postponed following the September 11 attacks.</P>
<P>But the IMF and World Bank didn't postpone causing misery around the globe. According to the <I>Wall Street Journal</I>, the U.S. political leaders who pull the strings at the IMF and World Bank were pressuring the institutions to help out in the war on Afghanistan. "Treasury Department officials are considering how the International Monetary Fund and World Bank can help in the effort with loans to allies, and possibly by denying aid to nations that defy Washington," the <I>Journal</I> wrote.</P>
<P>The Ottawa meetings will give government officials and bankers the opportunity to hash out policies that cause poverty and desperation around the globe.</P>
<P>In some ways, the delay in its meetings was good for the IMF and World Bank. Tens of thousands of protesters had been planning to travel to Washington, D.C., to demonstrate against the meetings. Now the Ottawa summit will take place on the same weekend as the annual demonstration against the U.S. Army's School for Assassins in Fort Benning, Ga.</P>
<P>Global justice activists say that demonstrations in Ottawa will be smaller and less confrontational than those planned for Washington. But protesters are still determined to speak out.</P>
<P>"The movement isn't going to soft-pedal its agenda," said Susan George, of the French-based global justice group ATTAC. "We have to be present wherever they meet to make sure they are being monitored."</P>

