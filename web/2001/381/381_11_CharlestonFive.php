<P><font face="Times New Roman, Times, serif" size="4">International day of action set for November 14</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Charleston Five face trial</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CHARLESTON, S.C.--It's time to mobilize for the Charleston Five with an International Day of Action set for November 14. AFL-CIO President John Sweeney has sent a letter to all union leaders calling on them to support the day of action, timed to coincide with the trial of the five members of the International Longshoremen's Association Locals 1422 and 1771.</P>
<P>"We are calling on you to give your support," ILA Local 1422 President Kenneth Riley told an audience of nearly 100 union members and labor activists at UAW Local 600 in Dearborn, Mich., October 18.</P>
<P>The Charleston Five face charges of conspiracy to riot for the "crime" of walking a picket line against a nonunion operation on January 20, 2000.</P>
<P>Racial justice is central to this struggle. Four of the five are Black. And the picket-line attack came just three days after the ILA--which is overwhelmingly African American--led a 47,000-strong march to take down the Confederate flag at the state capitol building.</P>
<P>The five were placed under house arrest for 20 months and confined to their homes between 7 p.m. and 7 a.m. unless they were working or attending union meetings.</P>
<P>But an international defense campaign this month forced state Attorney General Charlie Condon--the prosecutor in the case--to turn the matter over to a local attorney. A judge soon afterward ordered the release of the five, pending trial.</P>
<P>Actions and protests are planned around the U.S. and in several countries the day that opening testimony in the trial is expected. Rallies and protests will be held on the docks on the coasts, with labor unions and local defense committees organizing rallies, pickets and protests. Dockworkers across Europe have vowed to take action as well.</P>
<P>Condon's retreat and the lifting of the house arrest are important steps for this struggle. But we can't rest until this attack on labor and civil rights is completely defeated.</P>
<P>All out on November 14--and victory to the Charleston Five!</P>

