<font face="Times New Roman, Times, serif" size="3">George W. Bush is calling "Operation Enduring Freedom"--the U.S. war on Afghanistan--a "just" war. But the ruling classes of all countries always declare that their side is conducting a just war. To listen to them, wars are never fought for profits, to demonstrate who is boss or for other such crudities.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Kill innocent victims and blame Cuba</b></font><BR>
