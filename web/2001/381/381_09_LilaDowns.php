<P><font face="Times New Roman, Times, serif" size="5"><b>A voice for the undocumented</b></font></P>

<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MUSIC:</B> Lila Downs, <B><I>Border/La L&iacute;nea,</B></I> distributed by Narada Productions, $16.97.</P>
</font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE GROWING movement to demand amnesty for undocumented immigrants has found a strong and inspiring voice in Lila Downs. Her latest CD, <I>Border/La L&iacute;nea,</I> is an amazing expression of the sorrows, hopes and determination of those who leave their countries to try to build a better life in the U.S.</P>
<P>Downs sings in four languages--Spanish, Zapotec, Mayan and English--not only about the plight of those who come to pick the vegetables and fruits that we serve on our tables, but about the violence and pain they suffered in the lands they left.</P>
<P>In "Smoke" ("Acteal"), Downs denounces the massacre of 40 Mayan peasants by paramilitaries in Chiapas, Mexico, six years ago. In "La Ni&ntilde;a," she describes the toll that dehumanizing working conditions in a maquiladora factory have taken on a teenage woman.</P>
<P>I've heard the human voice referred to as an instrument before, but it wasn't until I listened to Downs that I understood what this meant. In "Border," her voice modulates in so many different ways that at times you think you're listening to the great Argentinean folk singer Mercedes Sosa, while at others, you think it is Soledad Bravo or salsa and pop singer La India.</P>
<P>Perhaps the most stirring track is "Land," a medley of two Woody Guthrie songs--"Pastures of Plenty" and "This Land Is Your Land." Downs ends the medley with a crescendo that has the feel of a chant at a picket line: "This land I'll defend with my life, if it need be/'Cause my pastures of plenty must always be free."</P>

