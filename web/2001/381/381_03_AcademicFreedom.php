<P><font face="Times New Roman, Times, serif" size="5"><b>They can't stifle dissent</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PROFESSORS and other academics against the war are taking a stand against the campaign of intimidation carried out by pro-war media and college administrators.</P>
<P>"Colleges and universities are being pressured by agencies of the federal government to hand over confidential information from student files," the Campaign to Defend Academic Freedom said in its initial statement.</P>
<p>"And there are moves in Congress to limit visas for students from abroad. We call on all members of the academic community to speak out strongly in defense of academic freedom, not just as an abstract principle but as a practical necessity at a time when anger, fear and patriotic fervor may play right into the hands of politicians eager to seize upon the present crisis as an opportunity to the give the U.S. military a free hand."</P>
<P>The statement, initiated by Brown University professor William Keach and Notre Dame de Namur University associate professor Phil Gasper, is circulating by e-mail. The effort will include a fundraising drive to pay for a newspaper ad. </P>
<P>Other initial endorsers include Edward Said of Columbia University, Tom Lewis of the University of Iowa and Dana Cloud of the University of Texas at Austin.</P>

<I><P>To get a copy of the statement or for information on the campaign, contact AcademicFreedomNow@hotmail.com.</P></I>

