<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Iraq: Phase Two of Washington's war?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2001 | Page 7</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>"HEADS UP, Hussein: You're Next," screamed a headline in the <I>New York Post</I>, joining the growing chorus clamoring for a U.S. military strike against Iraq. The <I>Washington Post</I> was no less vitriolic, running an op-ed piece that concluded, "Saddam and his bloody bugs have to go."</P>
<P>From the <I>New York Times</I> to CNN, major media outlets advanced the theory of former UN weapons inspector Richard Butler that Iraq is the number one suspect behind the anthrax attacks.</P>
<P>Butler's case, as laid out in an op-ed piece in the <I>New York Times</I>, is based upon the following bit of psychobabble: "I found one rule of thumb to have merit: The vigor with which Iraq conspired to defeat any given step toward arms control was a good indicator of how interested Mr. Hussein was in the weapons system at issue. I concluded that biological weapons are closest to President Hussein's heart because it was in this area that his resistance to our work reached its height."</P>
<P>There is, in other words, no evidence that Iraq is behind the anthrax attack.</P>
<P>That's the view of another former UN weapons inspector, Scott Ritter, who says that it's "irresponsible to speculate about a Baghdad involvement" in the anthrax attacks. "Under the most stringent on-site inspection regime in the history of arms control, Iraq's biological weapons were dismantled, destroyed or rendered harmless during the course of hundreds of no-notice inspections," Ritter wrote in Britain's <I>Guardian</I> newspaper. But no U.S. major newspaper or broadcast network bothered to carry Ritter's opposing viewpoint.</P>
<P>With a membership that reads like a Who's Who of hard-line conservatives--from Henry Kissinger to Dan Quayle--the Pentagon's Defense Policy Board has dedicated itself to pushing the U.S. to bomb Iraq as "Phase Two" of its war against terrorism.</P>
<P>Former CIA director James Woolsey has been in London for weeks, at the behest of the board, trying to dig up evidence of Iraq's involvement with the September 11 attacks. Having come up empty, he argued in the <I>Wall Street Journal</I>, "If we define the problem in such a way as to require proof (and make it proof beyond a reasonable doubt) of state involvement in the September 11 attack itself, we will quite likely define ourselves out of being able to understand who is at war with us."</P>
<P>The media has made much of the division in the Bush administration between the "hawks" grouped around Deputy Defense Secretary Paul Wolfowitz and the "doves" around Secretary of State Colin Powell.</P>
<P>But the division is between those arguing for an immediate strike against Iraq and those who prefer to wait. On CBS's <I>Meet the Press </I>last Sunday, Powell said of the anthrax attacks, "I wouldn't put it past Iraq." </P>
<P>Striking Iraq has never left the Bush administration's agenda. The U.S. has been bombing Iraq's "no-fly zone" regularly for the last 10 years, killing many hundreds of Iraqi civilians. And the U.S.-sponsored sanctions against Iraq have killed well over a million people since the end of the Gulf War.</P>
<P>Well before September 11, the Bush administration was looking for a reason to escalate the attack. In August, National Security Adviser Condoleezza Rice warned that "Saddam Hussein is on the radar screen for the administration"--and said that Bush was already considering "military force in a more resolute manner and not just a manner of tit-for-tat with them every day."</P>
<P>The war against terrorism may provide the U.S. with just the excuse it has been looking for--with the mass media dutifully whipping up anti-Iraq hysteria to justify it.</P>
<P>But Defense Policy Board member Newt Gingrich (whose failing career has revived since September 11) stated the real reasons why the U.S. would attack Iraq in <I>Newsweek</I> magazine--a week before the anthrax scare surfaced.</P>
<P>Gingrich said the U.S. should strike against Iraq simply because bombing Afghanistan is not an adequate U.S. response to September 11. "There's a feeling we've got to do something that counts--and bombing caves is not something that counts," said Gingrich.</P>

