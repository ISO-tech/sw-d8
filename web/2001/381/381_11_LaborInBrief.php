<p><a name="Music"></a><font face="Times New Roman, Times, serif" size="4"><b>Musicians' union</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Local 802 of the American Federation of Musicians is asking people to boycott the play <I>Reefer Madness.</I> <I>Reefer Madness</I> is a musical spoof of a 1930s film about the dangers of marijuana.</P>
<P>But union members were discriminated against in hiring musicians for the show, and the show--produced by the Nederlander family at the Variety Arts Theatre--is paying below-standard wages and benefits.</P>
<P>Local 802 is filing charges with the National Labor Relations Board accusing the producers of unfair labor practices.</P>
<P>Support the boycott of <I>Reefer Madness</I> and help stop the unfair treatment of union musicians.</P>

