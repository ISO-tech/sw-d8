<p><a name="Carey"></a><font face="Times New Roman, Times, serif" size="4"><b>Support Ron Carey</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch, Teamsters Local 804</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--When former Teamsters President Ron Carey won an acquittal in federal court, Local 804--Carey's former local--held a celebration. The verdict also gave a boost to campaigners for Tom Leedham's Rank-and-File Power slate, which is trying to continue Carey's legacy of fighting corruption and taking on employers.</P>
<P>Rich Barrett, an air driver at UPS for six years, said that Carey's trial was about revenge for the 1997 strike victory against UPS while Carey was president of the Teamsters. "It sent a message to unions in general that if you strike against Corporate America, they'll get back at you in some way."</P>
<P>The case against Carey was a joke. He was removed from office and later expelled from the union by the union's federal overseers for allegedly taking part in an illegal fundraising scheme. </P>
<P>Yet there was so little proof of this that a federal prosecutor could only charge Carey with perjury--on seven counts, one for each time he denied the corruption charges.</P>
<P>At five years per count, Carey faced up to 35 years in jail for consistently maintaining that he did not know about the illegal scheme!</P>
<P>Marilyn Vogt-Downey, an activist and writer who attended the entire trial, told <I>Socialist Worker</I> that the jury's decision was almost automatic. "All three of the government's main witnesses admitted under cross-examination that Ron Carey didn't know about the scheme&#133;One of the jurors told me afterward, 'We knew it was a frame-up from early on.'"</P>
<P>Most members of Local 804 knew it was a frame-up, too. Finally, we're being vindicated. </P>
<P>"This was a long time coming and I never had any doubt that he was innocent of all charges," Neil O'Brien, a UPS driver who ran on the Members First slate for president of Local 804, told <I>Socialist Worker</I>.</P>
<P>Amazingly, Carey still needs to appeal the Internal Review Board ruling that expelled him from the union before he can have any contact with active Teamsters.</P>
<P>But for Teamster members in Local 804 and elsewhere, the next steps are clear. We should pass resolutions condemning all those like Teamsters President Jimmy Hoffa Jr.--who backed the witch-hunt of Carey. </P>
<P>And we should make the final push to get out the vote for the Tom Leedham slate, Hoffa's opponents in the current election.</P>

