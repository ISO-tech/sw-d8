<P><font face="Times New Roman, Times, serif" size="5"><b>InterPark</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON, D.C.--About 150 people picketed an InterPark garage on October 19 to protest the company's exploitation of the September 11 tragedy to crush a union organizing drive.</P>
<P>Citing "security risks," the company reported to police detectives that a woman had made a bomb threat at the garage. But, as a detective quickly determined, the woman never made this threat. </P>
<P>In fact, she is an organizer for Hotel Employees and Restaurant Employees Local 27, Parking and Service Workers' Union, and InterPark had been unable to keep her from meeting with workers. </P>
<P>The incident has intimidated workers there--most of whom are immigrants from Ethiopia, Eritrea and Latin America--who fear exercising their rights on the job.</P>
<P>Many students, community activists and representatives from other unions joined the protest, including AFSCME, American Federation of Government Employees, and Iron Workers Local 5.</P>
<P>"We're very concerned with any employer taking advantage of a national tragedy and using it against workers in an organizing drive," said Carl Goldman, director of AFSCME Council 26. "We've been seeing it in our union, too."</P>
<P>Rev. Graylan Hagler, a prominent community and antiwar activist, also came out to protest these tactics. "Post-September 11, employers have been&#133;attempting to profile people racially and ethnically, to isolate and remove them," he told picketers. "I'm standing up for workers here and against profiling across the country."</P>
<P>Unfortunately, protest organizers handed out leaflets emphasizing "national unity" and urged participants to wear patriotic ribbons and sing national anthems in addition to labor and civil rights songs. </P>
<P>It's important to take a stand, but our movement can't adopt symbols that are being used to attack labor in the name of "national unity."</P>

