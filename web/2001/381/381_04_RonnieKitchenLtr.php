<P><font face="Times New Roman, Times, serif" size="4">The U.S. government shows how cruel it is</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>United <I>they</I> stand</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 26, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P></B>
<P>"United we stand, divided we fall." That's the slogan that the hypocrites are using for this war. They use this slogan only when they feel they can make money or war off of it. </P>
<P>During the Civil War, they also said, "United we stand." But Black soldiers couldn't ride their horses or salute white civilians when they entered a white town, even though they fought for this country.</P>
<P>It was the same when Pearl Harbor was bombed. What did the U.S. government do? It put Asian Americans into concentration camps, even though they had loved ones who fought for the U.S.--and still, a Black man couldn't walk into an all-white town or neighborhood.</P>
<P>But "united we stand," right?</P>
<P>During the Vietnam War, we heard it again. Black men fought and died, but as soon as the war was over, the Black man fell again, "divided" by racism.</P>
<P>Here we come to this same crossroad. This government wants us to believe that they're standing on truth, but they are not. They are standing on propaganda.</P>
<P>The U.S. government is showing just how cruel and vindictive it is. This war is not about peace and it's not about protecting lives. It's about destroying souls--the souls of the Afghan people.</P>
<P>This is the time for this so-called great country of ours to cry, not cheer. When it's all finished, the dead will still be dead, but the Black man will still not be able to walk the streets in peace.</P>
<P>But hey, united THEY stand.</P>
<P>To the exiled brothers and sisters of Afghanistan, I say that the Black man has felt the same thing. It is called hatred.</P>
<P>If there is one thing this government does well, it is lie.</P>
<B><P>Ronald Kitchen,</B> Illinois death row, Pontiac, Ill.</P>

