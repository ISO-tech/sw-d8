<p><font face="Times New Roman, Times, serif" size="4">Leedham backers defy old guard at Teamster convention</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Step up the fight to kick out Hoffa</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by DARRIN HOOP, Teamsters Local 174</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 14</font></p>

<p><font face="Times New Roman, Times, serif" size="3">LAS VEGAS--The two opposing sides of the International Brotherhood of Teamsters were on full display from June 25-29 at the union's 26th convention at the Paris Hotel and Casino here.</P>
<P>On one side, there was the current President James Hoffa, son of the notorious Jimmy Hoffa.</P>
<P>Hoffa Junior had 90 percent of the delegates supporting him--hundreds were local officials who won election unopposed. On the other side was Tom Leedham and his supporters, mostly affiliated with the reform group Teamsters for a Democratic Union (TDU).</P>
<P>The convention's purpose was to determine which candidates will be able to run in the November election for the Teamsters president and general executive board and to update or change the union's constitution.</P>
<P>The roughly 1,600 delegates clad in yellow vests with "Hoffa" in big black letters across the back were full of false talk about excellent contract gains and union democracy. The 100 or so Leedham delegates were subjected to constant verbal abuse and were frequently drowned out by chants of "Hoffa! Hoffa!" or "TDU sucks!"</P>
<P>As Kristi Valenzuela, a flight attendant at Northwest Airlines in Local 2000 said, "With the intimidation, it was a kangaroo court. It clearly is a sham to our justice process. It was a clear demonstration of the good old boys club."</P>
<P>The delegates passed a resolution in favor of dissolving the consent decree set up with a federal judge in 1991. This is a step towards rolling back reforms tied to the decree--such as the right to vote for the union's president.</P>
<P>Amendments calling for elected shop stewards, a members' bill of rights and limits on union officials' salaries to $150,000 were defeated. Hoffa also passed resolutions endorsing Bush's energy plan, including calling for oil drilling in the Arctic National Wildlife Refuge, and continued support for keeping the border closed to Mexican truckers.</P>
<P>Hoffa highlighted the Overnite strike for union recognition and the Basic Vegetable/Conagra strike in King City, Calif.--both which are nearly two years old--as examples of the union's tenacity. But the union has lost 11,000 members since Hoffa took office in 1998.</P>
<P>Incredibly, Hoffa claimed that there is no more corruption in the union. Yet William Hogan Jr., president of Joint Council 25 in Chicago, among other positions, and Dane Passo, Hoffa's special assistant, have been accused of diverting jobs from Teamster members in Las Vegas to a nonunion temporary labor firm where Hogan's brother is an officer.</P>
<P>Leedham received the necessary 5 percent of votes to run in the November election. "This convention shows how important it is to win this election," Leedham told <I>Socialist Worker. </I>"With the lack of democracy and the changes to the [union] constitution, these changes will make it virtually impossible to have a contested election in the future. Hoffa has a lot of money. He'll finance a flashy campaign. We need rank-and-file Teamsters who believe in the union to campaign for us every day on the shop floor."</P>

