<p><font face="Arial, Helvetica, sans-serif" size="2"><b>NORTHERN IRELAND</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Unionist bigots escalate violence</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 7</font></p>

<p><font face="Times New Roman, Times, serif" size="3">NORTHERN IRELAND was facing a new political crisis at the beginning of July in the wake of stepped-up violence against the Catholic minority.</P>
<P>David Trimble, the leading Protestant politician in Northern Ireland, resigned as first minister from a power-sharing government involving both Protestants and Catholics that was formed last year after years of negotiations.</P>
<P>Trimble said that he was resigning to protest the Irish Republican Army's (IRA) delays in handing over weapons.</P>
<P>In fact, the IRA--which fought a decades-long armed struggle against occupying British troops and the Protestant-dominated state machine that the soliders propped up--has put its weapons "beyond use," in stockpiles monitored by international observers. But Trimble and other Protestant politicians--known as Unionists or Loyalists, because they want Northern Ireland to remain part of Britain--say the IRA's guns must be "decommissioned."</P>
<P>Yet this demand comes even as Loyalist thugs have increased their violence against Catholics.</P>
<P>In Belfast last month, a Loyalist mob blocked Catholic parents from taking their children to a parochial school in a Protestant neighborhood, sparking days of rioting. At the end of the month, a Catholic man was gunned down in front of his family--the victim of Loyalist paramilitaries, who certainly haven't decommissioned their weapons.</P>
<P>What's more, July is the beginning of Orange marching season--a series of parades organized by Loyalists that have traditionally been an excuse for anti-Catholic violence. The first of the Orange marches is July 8, in the Belfast neighborhood of Portadown, the scene of bloody clashes in recent years.</P>
<P>Trimble's resignation has nothing to do with peace. He issued his threat to resign two months ago--in the middle of an election campaign, in a desperate attempt to unite his Protestant followers and whip up support. But the threat only gave more momentum to Unionist hardliners like Ian Paisley, who don't want any compromises.</P>
<P>Among ordinary Catholics and Protestants, the "peace" process that ended decades of violence is still very popular. But the power-sharing arrangement put in place last year doesn't answer the questions that led to the violence.</P>
<P>In fact, it has only solidified sectarian divisions, with Catholic and Protestant politicians vying to represent "their" communities.</P>
<P>There is an alternative to the conflict--a working-class alternative that unites Catholics and Protestants in a fight for better conditions against the bosses of both communities.</P>

