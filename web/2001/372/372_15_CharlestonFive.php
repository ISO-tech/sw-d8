<p><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The fight to defend the Charleston Five</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 15</font></p>

<p><font face="Times New Roman, Times, serif" size="3">THE CAMPAIGN to defend the Charleston Five might seem like a throwback to another era. A race-baiting Southern politician, running for governor, tries to send five members of an overwhelmingly Black union to prison for the "crime" of walking a picket line.</P>
<P>But this is a high-stakes fight for the future--not just for dockworkers in Charleston, S.C., but for the entire labor movement.</P>
<P>To be sure, the struggle in Charleston does recall some of the worst racism of the Old South. It was certainly no coincidence that South Carolina Attorney General Charlie Condon unleashed 600 riot cops against members of the International Longshoremen's Association (ILA) just three days after the union helped lead a mass march against the Confederate flag that flew over the state capitol.</P>
<P>But Condon's attack is the product of corporate globalization as well. In recent years, some of the biggest multinational companies in the world--including BMW and Robert Bosch from Germany, Hitachi from Japan and Michelin from France--have all set up shop in South Carolina. The state is no longer an economic backwater, but one of the fastest-growing centers of manufacturing in the U.S.</P>
<P>As a result, the Port of Charleston is expanding rapidly, too. That gives ILA Local 1422 enormous potential power--something that Condon and his corporate backers can't tolerate. So they're determined to break the union by jailing its activists and draining its treasury.</P>
<P>The impact of the struggle goes far beyond South Carolina. Today, the South has 32 percent of all U.S. manufacturing jobs--more than any other region.</P>
<P>It isn't difficult to see the attraction for employers. In South Carolina, wages are 20 percent below the national average and union membership rates are less than 4 percent.</P>
<P>If the South is a haven for corporations, it's because organized labor has historically failed to take on racism, which has been used by the local rulers to divide and rule since slave days.</P>
<P>When unions tried to organize the South in the 1930s and 1940s, politicians and bosses were quick to accuse union organizers of being "communists." Who else, they said, would take up the rights of both white and Black workers?</P>
<P>In the 1950s, that was enough to scare the increasingly bureaucratic unions into scrapping their organizing drives. Rather than organize the South, Cold War-era union leaders drove virtually all the communists and socialists out of the labor movement. Never mind that they were many of the people who had literally risked their lives to organize steel mills in Alabama and textile plants in North Carolina.</P>
<P>The struggle to defend the Charleston Five presents organized labor with a tremendous opportunity to heal its self-inflicted wounds.</P>
<P>To his credit, AFL-CIO President John Sweeney has endorsed this struggle--and signed a statement along with NAACP Chair Julian Bond denouncing Condon's use of the words "comrades" to describe Charleston Five supporters.</P>
<P>"By using code words like 'sympathizers' and 'comrades,' Condon is engaging in the lowest form of race-mongering and union-bashing," they wrote. "It's called 'redbaiting,' and it recalls the words of Dr. Martin Luther King Jr., 'The labor-hater and the labor-baiter is virtually always a twinheaded creature spewing anti-Negro epithets from one mouth and anti-labor propaganda from the other mouth."</P>
<P>They concluded: "At risk in this trial are the inalienable rights of every American to speak freely without fear of government censure, the right to join or form a union, and the right to participate in an open and democratic political process. These rights are at the core of our free society and the American labor and civil rights movements will not rest until justice is served and the Charleston Five are free."</P>
<P>The fight for the Charleston Five is a fight for workers' rights and racial justice everywhere. Every union member--and everyone committed to justice--should get involved.</P>
<P><I>Defense campaign funds are urgently needed. Send donations to Dockworkers Defense Fund, 910 Morrison Drive, Charleston, S.C. 29403, Attn: Robert J. Ford.</I></P>

