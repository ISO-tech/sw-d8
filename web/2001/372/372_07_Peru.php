<p><font face="Arial, Helvetica, sans-serif" size="2"><b>PERU</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Convicted again by a corrupt system</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Free Lori Berenson!</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by TODD CHRETIEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 7</font></p>

<p><font face="Times New Roman, Times, serif" size="3">LORI BERENSON, a 31-year-old New Yorker, was convicted for a second time last month and sentenced to 20 years in a Peruvian prison for supposedly collaborating with left-wing Peruvian rebels. This second conviction shows what has changed in Peru since the dictator Alberto Fujimori was overthrown last year--and what hasn't.</P>
<P>Berenson was originally convicted in 1996 after a military trial, presided over by hooded judges, which was so corrupt that even the U.S. government complained.</P>
<P>Fujimori's downfall after he ruled Peru with an iron fist for a decade opened the way to Berenson's retrial. This time, the judges didn't conceal their identity. But Berenson didn't have the right to a jury trial.</P>
<P>The main evidence against her came from witnesses who, Berenson says, "told lies to save [their] own skin." And the presiding judges declared her guilty in press interviews before the trial was finished. </P>
<P>Berenson was acquitted of actually being a member of the rebel Tupac Amaru Revolutionary Movement. But she was sentenced to 20 years for "collaborating" with the group.</P>
<P>Meanwhile, Fujimori is living it up in a plush exile in Japan, where conservative politicians pay his expenses and hold him up as a hero.</P>
<P>"I said my case has been used as a smokescreen, that it's a political trial," Berenson said in a June 20 statement. "There's a very simple reason: the existence of insurgent or rebel movements in Latin America and many other places in the world has a lot to do with social and economic conditions. The government responds through state policy...to draw attention away from these conditions."</P>
<P>During a recent visit to Washington, D.C., Peru's newly elected President Alejandro Toledo was asked by the Bush White House to consider the "humanitarian" aspects of Berenson's case. Toledo said that he would respect the "independence" of Peruvian judges.</P>
<P>But Bush and Toledo shouldn't be allowed to weasel out of their responsibility for Berenson's unjust conviction.</P>
<P>While Bush has tried to appear "compassionate," the U.S. government clearly backs Peru's decision to keep Berenson in jail. "I don't know how anybody could look at the evidence and arrive at a different conclusion than she knew more than she's admitting to," Dennis Jett, U.S. ambassador to Peru from 1996 to 1999, told the Associated Press.</P>
<P>The Bush administration--like the Clinton White House before it--accepts at face value the "evidence" against Berenson rigged up by an ex-dictator's corrupt injustice system.</P>
<P>Meanwhile, Toledo's defense of the Peruvian judiciary's supposed "independence" is a joke. After all, the country's judges were largely handpicked by Fujimori.</P>
<P>In fact, Fujimori's former intelligence chief, Vladimiro Montesinos, was apprehended last month and extradited to Peru, where he now faces charges of bribery, extortion, arms dealing and worse. Hoping for a lighter sentence, Montesinos is threatening to turn over 30,000 videotapes that he says prove corruption at the highest levels of Peru's government, including the judiciary.  </P>
<P>And yet, Lori Berenson is facing another 15 years in prison for allegedly participating in activities organized to oppose an exiled ex-dictator and his imprisoned CIA-backed security chief.  </P>
<P>What a terrible travesty of justice. Lori Berenson must be freed now!</P>
