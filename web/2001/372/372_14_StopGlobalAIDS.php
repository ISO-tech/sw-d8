<p><font face="Times New Roman, Times, serif" size="5"><b>Stop global AIDS</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by REBECCA KURTI</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 14</font></p>

<p><font face="Times New Roman, Times, serif" size="3">NEW YORK--"Pills cost pennies, greed costs lives!" That chant rang out on the streets of midtown Manhattan as activists kicked off a demonstration and march June 23 as part of the fight to stop global AIDS.</P>
<P>Despite rainy weather, more than 2,000 people came out for the protest, which was called to put pressure on the United Nations (UN) conference on AIDS meeting nearby.</P>
<P>The multiracial crowd came from cities across the country and countries around the world. A wide range of organizations worked together to build the event, including African Services Committee, ACT UP, Health GAP Coalition and Jubilee USA Network. Protesters took inspiration and energy from the diversity of the march--and links between AIDS activists and antiglobalization protesters. </P>
<P>The demonstration focused especially on the issue of AIDS in Africa --which now accounts for two-thirds of HIV-infected people. Shelia Debuka from Hope for Africa told the crowd that 3 million Africans would die of AIDS this year.</P>
<P>Demonstrators called on the UN and advanced countries like the U.S. to increase funding for the worldwide fight against AIDS, give poor countries greater access to AIDS drugs and abolish the debt of poor countries so they can better deal with AIDS. "It's not fair," said two marchers from Philadelphia. "People in Africa should be able to get the same medications as people in the United States."</P>
<P>March organizer Asia Russell, of Philadelphia ACT UP, pointed to the recent racist remarks of Andrew Natsios, head of the U.S. Agency for International Development, who said AIDS drugs would be wasted in Africa because Africans couldn't understand "Western time." As Russell argued, Natsios is willing to trade the lives of millions of people to protect the profits of drug companies.</P>
<P>Earlier this year, drug companies were pressured into dropping a lawsuit against the South African government's law allowing the manufacturing of cheaper generic AIDS drugs. But there are more battles to be fought. We need to keep up the pressure on the U.S. government and the drug company giants alike--and show them that we want patients before profits!</P>

