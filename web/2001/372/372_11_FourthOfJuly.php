<p><font face="Times New Roman, Times, serif" size="4">Who made the American Revolution?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The real story of the Fourth of July</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2"><B>BOOKS:</B> Ray Raphael, <B><I>A People's History of the American Revolution: How Common People Shaped the Fight for Independence. </B></I>The New Press, 386 pages, 2001, $25.95.</font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>Review by REBECCA WESTON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="3">JUST IN time for the Fourth of July, Ray Raphael's <I>A People's History of the American Revolution </I>is in bookstores. The first installment in a new "People's History" series from the New Press--which will be edited by radical historian Howard Zinn--Raphael's book is outstanding.</P>
<P>In contrast to the countless top-down histories of the revolution, Raphael shows the many and contradictory ways in which the War for Independence from British rule both unleashed and frustrated the hopes of the oppressed and exploited in colonial America. </P>
<P>He draws on countless primary and secondary sources--diaries, letters, contemporary newspaper articles--all of which provide the kinds of details that make you want to read parts of this book out loud to friends.</P>
<P>But the beauty of Raphael's book isn't merely that he pays attention to details that other historians don't. Raphael's history lets us see how the political consciousness of ordinary people changed through struggle--as initial hopes for adventure or a job or a yearning for liberty and freedom came up against the realities of a protracted war fought and won mainly by the poor.</P>
<P>Instead of Thomas Jefferson or Benjamin Franklin, we hear other voices--the poor, mostly young patriots who filled the Continental Army; the apprentices, tenants and farmers who formed "Nonimportation" committees and organized against British officials; the women who felt the burden of a war-torn economy; the frequently impoverished "loyalists" who fought for the king to strike a blow against their wealthy patriot landlords; and the slaves and Native Americans who were forced to make heroic and often tragic choices in a war that opened momentary possibilities for their liberation. </P>
<P>Without minimizing the achievements of the Revolution, Raphael's history shows how the aspirations of ordinary people threatened to go beyond the more narrow goals of the "Founding Fathers"--that is, the wealthy colonial landowners. As Raphael writes, "Whigs and lower-class rioters vied for control: Who would define the issues? Whose revolt was this anyway?" </P>
<P>Caught up by the energy and politics of the early revolutionary period, masses of people from all sections of society initially volunteered for the colonial militias as "Minutemen." But as the war dragged into winter, as harvest time came and went and as disease spread through the army, the numbers of volunteers dwindled.</P>
<P>George Washington, the head of the Continental Army, resorted to conscription. This enabled wealthy merchants to pay young, poor and unemployed men to substitute for their own tour of duty. As a result, "poor soldiers carried the burden not because they were more patriotic than the rest but because they were more available," Raphael writes.</P>
<P>At the Battle of Fort Wilson, hundreds of militiamen protested outside the home of a wealthy merchant --James Wilson. Demanding democracy in the army, payment, food and conscription for the merchant class, the mutiny was put down with bloody repression.</P>
<P>Summing up the mood of those protests, one soldier said, "The common soldiers have nothing to expect, but that if America maintain her independendency, they must become slaves to the rich."</P>
<P>There are some problems with Raphael's book. Because the chapters are organized according to social group ("women," "Native Americans" and so on), it's difficult to see the connections between different exploited and oppressed groups. Also, in the chapter "Loyalists and Pacifists," Raphael's insights regarding class and oppression take a backseat to his discussion of coercion and revolutionary violence against those who refused to support the fight.</P>
<P>Still, the strengths of the book far outweigh these drawbacks. For anyone wanting an entertaining read, anecdotal gems and--more importantly--an opportunity to reclaim the history of the American Revolution, Raphael's book should be at the top of your summer reading list.</P>

