<p><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What's Cheney afraid of?</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 2</font></p>

<p><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Not even Congress can get a straight answer out of Dick Cheney.</P>
<P>Flabbergasted investigators from the nonpartisan General Accounting Office (GAO) run by Congress are trying to figure out what to do after they were blown off by Cheney's office in their attempt to find out which corporate honchos helped the White House come up with its energy plan.</P>
<P>Reps. Henry Waxman (D-Calif.) and John Dingell (D-Mich.) asked the GAO in April to find out who served on the energy task force that Cheney headed. Figuring the request was routine, the GAO passed it on to the White House.</P>
<P>Cheney's response: The vice president and his staff would take this secret to their graves.</P>
<P>Now the GAO is considering going to court to pry the information out of the White House--which would be the first time in its history that it's had to go to this length.</P>
<P>Of course, it's not hard to figure out who helped Cheney draw up a plan to dismantle any government regulation that stands in the way of more oil, more coal and more nuclear power plants. But what could be so embarrassing that Cheney won't even allow the information to see the light of day?</P>
<P>After all, it's well known that Kenneth Lay, CEO of the oil and gas giant Enron and one of George W. Bush's biggest campaign contributors, has veto power over White House energy policy. Earlier this year, Lay told the acting head of the Federal Energy Regulatory Commission that he'd better announce his support for electricity deregulation if he wanted to keep his job.</P>
<P>What more does Dick Cheney want to keep under wraps?</P>

