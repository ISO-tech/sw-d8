<p><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">What the Patients' Bill of Rights won't fix</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Health care disaster</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by SHARON SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 6</font></p>

<p><font face="Times New Roman, Times, serif" size="3">DEMOCRATS AND Republicans are fighting a pitched battle over the Patients Bill of Rights legislation pending before Congress for the fifth straight year. </P>
<P>Judging from the level of acrimony, one would think that a piece of sweeping workers' rights legislation is being debated. "Employers beware. You are going to be sued," warned Sen. Don Nickles (R-Okla.). "We're making very clear that this legislation cannot be fixed, and they are to be opposed," huffed Kate Sullivan, director of policy at the U.S. Chamber of Commerce.</P>
<P>George W. Bush says the bill could lead to an onslaught of "frivolous lawsuits" against well-meaning companies and has threatened to veto it. </P>
<P>But the Patients Bill of Rights is a long way from the far-reaching manifesto that its name implies.</P>
<P>Its list of "patients' rights" covers the already insured--while ignoring the rights of the more than 40 million people who have no medical coverage.</P>
<P>The bill guarantees the already insured the right to emergency room care; access to specialists, such as obstetricians and gynecologists; coverage for breast cancer treatment; access to prescription drugs; and the right to sue if care is denied.</P>
<P>The fact that a law is needed to mandate these most basic medical rights serves only to expose the severe crisis of the U.S. health care system.</P>
<P>The U.S. spends more per person on health care than any nation in the world--some $1.3 trillion in 2000, or 13.5 percent of the country's gross domestic product. Yet the U.S. is also the only industrialized country that doesn't offer universal health care, and it ranks 37th in the overall quality of the health care that it provides, according to the World Health Organization.</P>
<P>Today in the U.S., the vast majority of workers who receive medical coverage from their employers are enrolled in for-profit health maintenance organizations (HMOs), which long ago surrendered medical considerations to the quest for profits.</P>
<P>According to Morningstar financial analysts, "In 2000, managed care stocks had a spectacular run. Triple-digit returns were quite common in the group." The key to their success? "As a group, managed-care companies have hiked premium yields 10 to 12 percent per year in 1999 and 2000," wrote Morningstar. "[E]ach company in the group has shown that it's not afraid of cutting loose members who are unwilling to pay for higher premiums."</P>
<P>These companies make their profits by routinely denying necessary health care for the people they insure--oftentimes, emergency care or treatment for serious illness.</P>
<P>Several years ago, for example, MetroHealth, a Missouri-based managed care company, told patients not to go to emergency rooms for "non-emergency treatment such as high fevers in infants" or "broken bones."</P>
<P>Yet Missouri HMO lobbyist, Randy Scherr, argued that patients who complain expect too much from HMOs. "Although they are paying less, they think they're buying a Cadillac, and that's what they should get," Scherr said.</P>
<P>Families U.S.A. estimates that, every year, 18.1 million people are denied or forced to delay needed medical treatment by their HMOs.</P>
<P>And patients denied coverage have no legal recourse, thanks to a law passed by Congress in 1974. The Employee Retirement Income Security Act (ERISA) protects all employee benefit plans provided by private employers from medical malpractice lawsuits. Congress also included in the law a provision that actually prevents states from regulating employer health or benefit plans.</P>
<P>The lack of any government regulation goes a long way toward explaining how the health care industry has become such a nightmare. </P>
<P>That's why the American Association of Health Plans, National Association of Manufacturers and U.S. Chamber of Commerce have joined forces to fight against the Patients Bill of Rights. All of them have a stake in preventing patients from winning the right to sue over denied medical care.</P>
<P>But on this matter, too, the bill comes up short. The Democrats made a last-minute compromise with Republicans that leaves only 6 percent of businesses open to possible lawsuits, signaling that they're open to further compromises to appease Bush.</P>
<P>Whatever its shortcomings, however, public support for the bill is overwhelming. According to a June 20 Gallup poll, 77 percent of those surveyed said passing the Patients Bill of Rights is "extremely" or "very" important. And in a Gallup poll last year, 64 percent said that it was the responsibility of the government to make sure all Americans have health coverage.</P>
<P>Most people want what Bill Clinton promised--and failed to deliver--nine long years ago: health care as a right for all people.</P>

