<p><font face="Times New Roman, Times, serif" size="5"><b>Cicero mayor gets what she deserves</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 4</font></p>

<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>The FBI arrested Betty Loren-Maltese, the mob-connected town president of Cicero, Ill., June 16. Loren-Maltese and nine others were charged with looting $10 million that was earmarked for town employees' health insurance payments.</P>
<P>In a scheme cooked up by her late husband, convicted mobster Frank Maltese, Specialty Risk Consultants Inc. was handed the administration of town workers health insurance claims in 1992. As town president, Loren-Maltese signed off on huge weekly payments to Specialty Risk&#150;whether there were health claims to cover or not.</P>
<P>Of the $33 million Loren-Maltese authorized that ran through Specialty Risk's sticky fingers, only $18 million ever went to cover health claims. The rest went into a Wisconsin golf course, a horse farm, a vacation home and other mob investment schemes.</P>
<P>Cicero is no stranger to organized crime. Al Capone took over suburban Cicero in the 1920s after Chicago cops chased his bootlegging and gambling rackets out of town. And even before Loren-Maltese's arrest, the feds were already investigating whether Cicero was illegally towing and selling cars and taking kickbacks on parking tickets.</P>
<P>Readers of <I>Socialist Worker </I>may remember Loren-Maltese from 1998. That's when she tried to broker a deal with a Ku Klux Klan chapter to mail out&#150;at taxpayer expense&#150;Klan literature to all 70,000 residents, if the Klan agreed to cancel a rally.</P>
<P>Cicero&#150;once a bastion of white flight from Chicago and the scene of racists throwing rocks and bottles at Martin Luther King during a housing march&#150;is now majority Latino.</P>
<P>When activists called for a demonstration against the racist mailing, Loren-Maltese tried to stop us, and Police Chief David Niebur threatened to arrest anti-racists. But Loren-Maltese caved in, the KKK stayed home, and antiracist activists marched through the suburb instead.</P>
<P>Loren-Maltese fired Niebur not long after the debacle. Niebur returned the favor by going to the feds with the dirt on Loren-Maltese. Though Loren-Maltese is currently out on bail, she may finally be headed to the big house.</P>
<P><B>Leighton Christiansen, </B>Chicago</P></FONT></BODY>

