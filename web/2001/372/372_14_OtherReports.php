<p><a name="Village"></a><font face="Times New Roman, Times, serif" size="4"><b>Little Village protests in Chicago</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by VANESSA ERAZO</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">CHICAGO--Thirty picketers chanted, "Daley, Vallas, keep your promise!" as mothers and students demanded that the Board of Education begin construction on a high school in Little Village.</P>
<P>Prior to the action, a group of mothers went on hunger strike for three weeks after the Chicago Public Schools reneged on its commitment to build a high school in this Mexican-American neighborhood. </P>
<P>Even though state funds are available, the Chicago Board of Education has not yet allotted the money towards the much-needed high school.</P>
<P>Community members organized a bus to picket the last board meeting for President Gery Chico and schools' Chief Executive Paul Vallas, who are stepping down from office. Organizers have planned other events, including a meeting with Mayor Richard Daley, to keep up the pressure.</p>
