<p><a name="Immigrants"></a><font face="Times New Roman, Times, serif" size="4"><b>Chicago immigrant rights demonstration</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">CHICAGO--About 500 immigrants' rights activists and unionists took to the streets of the largely Mexican neighborhood of Little Village to demand amnesty for immigrants. </P>
<P>Unions and politicians spoke about the need to push for passage of legislation called H.R. 500, which would allow permanent legal residency to any immigrant who has been in the country since February 6, 1996. Those who arrived in the U.S. by February 6, 2001, would be allowed to apply for legal residency after five years. </P>
<P>Union leaders also called for more organizing among undocumented workers.</P>
<P>Protesters chanted, "What do we want? Amnesty!" and "Boricua y mexicano, luchando mano en mano" (Puerto Ricans and Mexicans, fighting together). The crowd marched to the local Social Security Administration building and threw bags of balloons marked "$1 billion" at the door to remind the government of how much immigrant workers contribute to the system.</P>
<P>Activists plan to head to Washington, D.C., in September to pressure Congress to pass the new amnesty bill.</p>
