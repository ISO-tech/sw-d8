<p><a name="Harlem"></a><font face="Times New Roman, Times, serif" size="4"><b>Harlem housing protest</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by CANDICE RIVAS</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">NEW YORK--About 120 people held a spirited protest June 13 outside the Harlem Schomberg Center against new rent increases for public housing.</P>
<P>Mayor Rudolph Giuliani's Rent Guidelines Board voted on suggested rent increases of 3 percent for one-year leases and 5 percent for two-year lease renewals. But outside, people voiced their desire for affordable housing--with many protesters staying at the demonstration after they walked by.</P>
<P>The demonstration followed a town hall meeting in Harlem a few weeks earlier on gentrification and globalization. "Housing is a small example of the barrage of attacks that are committed against working class people," said Tom DeMott, an activist in Harlem.</P>

