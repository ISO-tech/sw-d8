<p><a name="Nazis"></a><font face="Times New Roman, Times, serif" size="4"><b>Confront the Nazis in New Jersey</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by NICOLE COLSON</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">MORRISTOWN, N.J.--Hate-monger Richard Barrett and his band of neo-Nazi thugs plan to hold their second "Independence from Crime Day" parade here July 4.</P>
<P>Barrett is the leader of the white supremacist Nationalist Movement. He recently held "press conferences" in Philadelphia and Cincinnati to voice support for police after the shooting of an unarmed Black man, Timothy Thomas, in Cincinnati.</P>
<P>At this year's Morristown "parade," Barrett plans to honor Officer Steven Roach--the cop who shot Thomas--and York, Pa., Mayor Charlie Robertson, who is currently under indictment for the brutal murder of an unarmed Black woman in 1969.</P>
<P>Last year, the Nationalist Movement's rally focused on racial profiling by New Jersey cops. "I am pro-profiling because I am against crime," said bigot Steve Ucci, a Nationalist Movement supporter who until recently worked as a guard at the Morris County Courthouse. Ucci is this year's Grand Marshal of the Nazi parade.</P>
<P>Fortunately, antiracist activists from across the state and beyond are fighting back. Last year, more than 350 people turned out to show Barrett and his supporters that they can't get away with spouting racist garbage. This year, activists are again mobilizing. </P>
<P>Some local politicians have advocated staying away from the Nazi march. But as Lawrence Hamm, of the People's Organization for Progress, said, "We have to come out. We have to speak out against it, because if we don't, it goes unchecked and it spreads, and the groups become more powerful."</p>
