<p><font face="Times New Roman, Times, serif" size="4"><b>BioJustice 2001</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by CHUCK STEMKE</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">SAN DIEGO--"Disease and starvation will not be solved by corporations! It's bullshit, get off it, the enemy is profit!" So went a popular chant at the BioJustice 2001 protests in San Diego against an annual convention of biotech companies.</P>
<P>The march and protest on June 24 was the high point of a five-day series of teach-ins and protests to point out the dangers of the genetic engineering of foods and other organisms. Although fewer people attended than expected--1,000 as opposed to pre-event estimates as high as 10,000--the protests were successful in creating public debate on several fronts.</P>
<P>Police whipped up a massive scare campaign prior to the event. Downtown businesses were told to board their windows and residents to stay clear of "violent" protesters. Cops received $2 million for overtime and new equipment to lock down the city.</P>
<P>Despite this, many protesters came out.</P>
<P>At teach-ins before the protests began, dozens of meetings were held by scientists, writers, farmers and others. Vandana Shiva, an advocate for poor farmers in India, spoke out, as did a Canadian farmer who was sued by Monsanto when his neighbor's genetically patented crops cross-pollinated with his.</P>
<P>Those who have been fighting biotech on their own for years were re-energized by the energy of the new generation of activists associated with the antiglobalization movement who made up the bulk of this protest.</p>
