<p><font face="Times New Roman, Times, serif" size="4">Whistleblowers tell how energy giant forced up prices</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gouged by Duke Energy</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by JESSIE MULDOON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 2</font></p>

<p><font face="Times New Roman, Times, serif" size="3">SACRAMENTO, Calif.--Three former employees are blowing the whistle on the power bosses who've made out like bandits during California's energy crisis.</P>
<P>Ed Edwards, Glenn Johnson and Jimmy Olkjer worked at the Chula Vista plant in southern California--which has been leased and managed by power giant Duke Energy since 1998 under the terms of a deregulation law passed by the state legislature two years before.</P>
<P>In less than three years, prices for electricity have skyrocketed--as have Duke's profits. In fact, this year, Duke has charged up to $3,800 per megawatt hour of electricity--up more than 100 times from the price of $35 per megawatt hour before the power pinch. Even the Federal Energy Regulatory Commission said in June that Duke's prices "can in no way be found to be just and reasonable."</P>
<P>Now, three whistleblowers are telling how Duke managers manipulated production at the Chula Vista plant to cause artificial shortages that gave the company an excuse to boost prices.</P>
<P>According to Olkjer, who is now retired, plant managers--under orders from corporate honchos in Salt Lake City--told workers to shut or slow down functioning generators, even when demand for electricity was high. "We noticed that a lot of times when the price was down, megawatts would go down," Olkjer told reporters.</P>
<P>Electricity supplies were kept dangerously low--even when the state was facing rolling blackouts. As a result, Duke was able to charge virtually any price it wanted.</P>
<P>Gov. Gray Davis is seizing on the testimony of the three whistleblowers to bolster his demand that power producers return $9 billion in rebates to ratepayers. </P>
<P>Davis and other politicians have lambasted the so-called "Confederate Cartel"--energy giants like Duke, Enron and Reliant that are based in the South--for skyrocketing electricity prices.</P>
<P>But no one should let California's power producers off the hook. While the state's largest utility, Pacific Gas and Electric (PG&amp;E), has filed for bankruptcy, its parent corporation is highly profitable--because its power-producing subsidiary has gouged consumers, too.</P>
<P>Meanwhile, Davis and the other politicians are wary about shining too much light on the deregulation law of 1996. Lawmakers of both parties voted unanimously for the disastrous legislation. Davis has refused to use his power as governor to take over power plants. In fact, he's still pouring billions from the state budget into a utility bailout.</P>
<P>Meanwhile, PG&amp;E customers are facing another 36 percent rate increase in their July bills. With this latest hike, many poor and working people will face a grim choice between paying for rent and food--and having their electricity shut off.</P>
<P>The crisis in California has shone a spotlight on the upside-down priorities of a system that puts profits first.</P>
<P>But San Francisco residents will have an opportunity in November to vote for a referendum to create a Municipal Utility District with the ability to seize power plants and transmission lines. This would be a great first step toward publicly owned utilities.</P>

