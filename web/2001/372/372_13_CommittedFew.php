<p><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can change be made by a committed few?</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 13</font></p>

<p><font face="Times New Roman, Times, serif" size="3">THERE ARE two opposing ideas that, strangely enough, are both attributed to Marxism. One is fatalism, the idea that history takes an inevitable course independent of what people do. The other is voluntarism, the idea that sheer will power can achieve anything.</P>
<P>In this first category is the belief that socialism is inevitable--that developments under capitalism will automatically bring about its downfall and the victory of socialism. In the second is the belief of some anarchists and other activists that small deeds by a few can spark a revolt of the many--what is sometimes called "propaganda of the deed."</P>
<P>Fatalism is crudely materialist in that it attributes no power to human ideas. We are merely playthings of history. Voluntarism is purely idealist in that it attributes any and all possibilities to human ideas.</P>
<P>Marxism rejects both of these approaches.</P>
<P>Fatalism is like the arguments of the 17th-century physicist Isaac Newton, who believed that if we knew the cause and effect of every action and reaction in the universe, then the future could be predicted with perfect accuracy. In this view, no matter what individuals do or say, conditions not of their own choosing compel them to move along a predetermined course. Of course, if this were true, then I might as well not bother writing this!</P>
<P>The voluntarists say the opposite. Whatever someone can dream up in their head, they can achieve, no matter what material and social conditions prevail.</P>
<P>Fatalism forgets that history is made by human beings, who think, act and interact with each other and their environment. But voluntarism forgets that, to paraphrase Karl Marx, while people do make history, they don't make it in conditions that they choose, but in conditions that they inherit from the past.</P>
<P>This voluntarist view can't explain why we humans first tolerated inequality when class societies began to appear about 10,000 years ago.</P>
<P>"Grace was among them, because none suffered lack," goes a late fourth century homily, "for they did not give one part and retain another for themselves&#133;They abolished inequality and lived in great abundance."</P>
<P>Such fragments of verse show that socialist ideals of equality and freedom existed long before Marx was born. But in the fourth century, it was only possible to <I>dream </I>of socialism. </P>
<P>To put it crudely, the productive level of the rural peasant economy of the time was insufficient to support a society of sharing and abundance. No human action--whether by the few or the many--could have changed this.</P>
<P>Voluntarism is often closely linked with another "ism"--substitutionism, which means a small group of people trying to substitute themselves for larger social forces. One example of substitutionism would be the Latin American revolutionary Che Guevara and his small band of fighters. Without real roots in society or knowledge of the historical conditions, Che's band tried to spark a guerrilla war for liberation in Bolivia in the 1960s--even though that country had already seen large-scale working-class organization and struggle.</P>
<P>What both voluntarism and substitutionism forget is that major social changes--from the end of apartheid to desegregation in the U.S. South to the fall of any hated tyrant--are the result of mass mobilizations. Individuals and groups can have an impact on history, but only if their ideas and organization fit developed material conditions that make it possible for their goals to become reality.</P>
<P>Both because of the abundant wealth and the abject misery it produces, capitalism has created the material conditions for a socialist society. But even so, socialist ideas have to be embraced by millions for them to become a material force capable of bringing real revolutionary change.</P>
<P>"The mystics of the Middle Ages who dreamed of the coming millennium were already conscious of the injustice of class antagonisms," wrote Frederick Engels. But a mass movement to create a just society, Engels argued, was only possible because "modern large-scale industry has called into being&#133;a proletariat, a class which for the first time in history can demand the abolition, not of this or that particular class organization, or of this or that particular class privilege, but of classes themselves."</P>

