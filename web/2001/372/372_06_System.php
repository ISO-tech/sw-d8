<font face="Times New Roman, Times, serif" size="5"><b>Learning from Cuba</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 6</font></p>

<p><font face="Times New Roman, Times, serif" size="3">THE WORLD Bank is notorious for its religious devotion to the free market as a cure for any and every social problem. Whether it's low wages or high inflation, a strong dose of free-market measures will do the trick.</P>
<P>So it was entirely surreal to hear World Bank President James Wolfensohn praising the government of Cuban President Fidel Castro for doing "a great job" in providing for the social welfare of the Cuban people. </P>
<P>His comments followed publication of the Bank's 2001 edition of World Development Indicators, which showed Cuba topping nearly every country in the developing world in health and education statistics. The report also showed that Cuba has actually improved its performance in both areas despite the U.S. embargo and the end of Soviet aid and subsidies more than 10 years ago.</P>
<P>"Cuba has done a great job on education and health," Wolfensohn told reporters at the conclusion of the annual spring meetings of the Bank and the International Monetary Fund (IMF). "They have done a good job, and it does not embarrass me to admit it." </P>
<P>Havana's economic policies are virtually the antithesis of the "Washington Consensus"--the neoliberal orthodoxy that has dominated the Bank's policy advice and its structural adjustment programs for the last 20 years. Yet some senior Bank officers go so far as to suggest that other developing countries should take a very close look at Cuba's performance. </P>
<P>"It is in some sense almost an anti-model," according to Eric Swanson, the program manager for the Bank's Development Data Group. In fact, he continued, Cuba is proof that the Bank's dictum that economic growth is a precondition for improving the lives of the poor is over-stated, if not plain wrong.</P>
<P>--InterPress Third World News Agency, June 7, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Menace to society?</font></p>
<p><font face="Times New Roman, Times, serif" size="3">A PROGRAM aimed at cracking down on young offenders has ensnared another "menace to society." A 7-year-old boy from North Dakota was charged with criminal theft after police cited him for stealing $6 from his mother.</P>
<P>The boy's mother discovered $6 missing from a pile of cash she had left lying on her kitchen counter. When she questioned her two sons, the 7-year-old admitted to taking the money.</P>
<P>Upon questioning by police, the boy said he took the money because he wanted to buy a Beanie Baby toy. The boy was given a criminal citation and released to his mother's custody. The matter now heads to juvenile court for sentencing by youth counselors.</P>
<P>"The reason we determine consequences is to help them avoid this type of trouble in the future," said Bismarck Police Lt. Nick Sevart. "We believe in early intervention."</P>
<P>--Reuters, May 10, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">O'Neill: Robin Hood in reverse</font></p>
<p><font face="Times New Roman, Times, serif" size="3">PAUL O'NEILL built his career on cheating the U.S. Treasury. Now, he's the Secretary of the Treasury.</P>
<P>During his last "job" as chief executive at Alcoa, O'Neill wasn't the worst of the looters, but he still did pretty well at thwarting the tax collectors. </P>
<P>In 1996, Alcoa raked in profits of $399 million--and paid nothing in taxes. In fact, Alcoa received a rebate of $17.6 million from the Feds--which works out to a tax rate of minus 4.4 percent--derived from accounting ploys. </P>
<P>For the three-year period from 1996 to 1998, Alcoa paid an effective tax rate of only 15.9 percent on $1.7 billion in profits. This is less than half the statutory rate of 35 percent and roughly what workers in the U.S. must pay--even though their income is a tad shy of Alcoa's billions.</P>
<P>Meanwhile, O'Neill himself "earned" $59 million in his last year at Alcoa, taking advantage of the stock options that are one of the devices Alcoa uses to reduce its taxes.</P>
<P>But if you ask O'Neill, this of course isn't cheating. The real cheaters are elderly people who don't save enough for their retirement. "Able-bodied adults should save enough on a regular basis so that they can provide for their own retirement and for that matter for their health and medical needs," said O'Neill.</P>
<P>Otherwise, he explained to the <I>Financial Times, </I>elderly people are just dumping their problems on broader society.</P>
<P>--<I>The Nation,</I> July 16, 2001<br>
