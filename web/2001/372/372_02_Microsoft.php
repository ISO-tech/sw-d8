<p><font face="Times New Roman, Times, serif" size="4">Judges overturn breakup decision</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Microsoft off the hook</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by MATT NICHTER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 2</font></p>

<p><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The champagne was flowing at Bill Gates' estate following a decision by federal judges last month to stop the breakup of his Microsoft empire.</P>
<P>"Anyone from Seattle knows how it feels when the sun breaks through the clouds," beamed Microsoft CEO Steve Ballmer, Gates' second-in-command. "That's what this is like."</P>
<P>Last year, Judge Thomas Penfield Jackson ordered the breakup of the software giant, agreeing with Justice Department prosecutors that Microsoft had violated anti-trust laws. Translation: The Microsoft mob used its dominant position in the software industry to force competitors to cooperate--or be driven out of business. </P>
<P>But a federal appeals court overturned Jackson's order. Their main complaint seems to be that Jackson violated a "code of conduct" that bars judges from making public comments on cases they preside over. Following his ruling last year, Jackson told reporters that Gates had a "Napoleon complex"--and had been so arrogant, both in and out of court, that he needed to be "whopped upside the head."</P>
<P>The appellate judges <I>don't</I> deny that Gates and his henchmen are monopolists. In fact, they upheld almost all of Jackson's specific findings, including charges of price gouging and collusion. But they say that Jackson gave too little consideration to the "economic ramifications" of a breakup. </P>
<P>More than anything, the decision buys time for Microsoft to lobby Washington lawmakers in the hopes of getting an out-of-court settlement with the Justice Department. Gates has paid top dollar to assemble a "dream team" of lobbyists with close connections to the Bush administration--including former Republican National Committee chair Haley Barbour and former Christian Coalition director Ralph Reed.</P>
<P>And he's generously dumped cash into the campaign coffers of Senate and House members. "[W]hen the case began seven years ago, the company and its senior executives had given a total of $10,000 to the two political parties and $33,000 to federal candidates and had one lobbyist on the company payroll," wrote the <I>New York Times. </I>"In the last two-year election cycle, the company and its employees were the fifth-largest political donors in the nation, giving some $4.66 million."</P>
<P>Gates' bribery scheme seems to be working. Attorney General John Ashcroft--who's now in charge of the government's case against Microsoft--described the appellate court ruling as "a significant victory."</P>

