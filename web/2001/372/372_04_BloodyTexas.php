<p><font face="Times New Roman, Times, serif" size="4">Mentally ill and railroaded onto death row</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Deep in the heart of bloody Texas"</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 4</font></p>

<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>I am an African American man on death row in Texas. Very soon, I'll be given an execution date and likely murdered--like so many others that I've seen since I've been here--for a crime that I was convicted of and "confessed" to in a sham trial. </P>
<P>The trial was a mockery of justice, aided and abetted by my court-appointed lawyer, who was friends with the judge and prosecutor.</P>
<P>The only evidence that they had to sentence me to die was my "confession," which might seem to be enough. The problem, however, is that police officers from Texas came to California, where I was serving time in prison, and paid me $500 to get my confession--even though they knew that I had been institutionalized twice after courts in Colorado and California had found me to be suffering from a chronic mental illness.</P>
<P>So even if I was guilty, there would be mitigating factors suggesting that they wouldn't have to murder me.</P>
<P>But the main reason I am writing you is that when I was locked up in California, I was in notoriously bad prisons, like Pelican Bay and Folsom. Due to problems caused by my illness, I was routinely brutalized by guards and was often set up to assault or be assaulted by other prisoners--until it drove me to attempt suicide several times.</P>
<P>Ultimately, I confessed to a murder while in this deranged, depressed suicidal condition. </P>
<P>My court-appointed attorney is working to keep this all quiet and has resisted my attempts to have him appeal my conviction on the grounds that my false confession was only a product of prolonged abuse at Pelican Bay and other California prisons where I underwent seven years of pure hell.</P>
<P>I have seen innocent men sent to their death in Texas--men like David Spence and the mentally ill, like Oliver Cruz, who are not really responsible for what they did. </P>
<P>In a month or two when they give me a date, I might be next, since tragically and unbelievably, Texas' governor just vetoed legislation banning the execution of the mentally retarded.</P>
<P>I have written down these facts of my case in order to penetrate the smokescreen of the criminal justice system that wants to keep people like me silent and muzzled--because as long as we remain numbers or statistics without names and faces, then people won't care or know the truth. Then the slow "genocide" and systematic injustice can continue here deep in the bloody heart of Texas.</P>
<P>I may yet be murdered. </P>
<P>But I hope you will expose my case, which is a glaring example of the mockery of justice that plagues the death penalty in general and taints the Texas courts in particular.</P>
<P><B>Jermarr Arnold,</B> Terrell Unit, Death Row, Livingston, Texas<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Still executing the mentally retarded</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>On June 17, Texas' new governor, Rick Perry, vetoed legislation that would have banned the execution of the mentally retarded. Civil rights groups, including the Campaign to End the Death Penalty's Austin chapter, advocates for the mentally disabled, and religious organizations, supported the bill.</P>
<P>Perry stunned the nation by vetoing the bill, claiming that while Texas had never executed a mentally retarded person and doing so would be immoral, the current legal system protected defendants' rights adequately. Yet five state legislatures have passed similar bills in recent months, recognizing that more protections are needed. Even Florida Gov. Jeb Bush signed legislation similar to that rejected by Perry.  </P>
<P>By refusing to sign the bill, Gov. Perry has shown his callous disregard for the human rights of the disabled and has bucked the growing trend of recognizing such punishment as cruel and unusual.  </P>
<P>Supporters of Perry's decision call his veto "an act of courage" in support of victim's families. But vetoing a bill passed democratically by the Texas legislature on the last day before it becomes law is not courageous and only creates more pain through state-sanctioned murder.  </P>
<P>Perry argued that the bill took too much power out of the hands of juries, but in reality, it only required that defendants who are mentally retarded spend the rest of their lives in jail, rather than face execution. Instead, Perry has chosen to wait and see if the U.S. Supreme Court forces him to change Texas law when the court revisits the issue of whether executing the mentally disabled is unconstitutional.</P>
<P>Such a ruling would be a step in the right direction, but we'll need to keep up the fight for abolition of all capital punishment.</P>
<P><B>Daniela Dwyer-Grove,</B> Chicago</P>

