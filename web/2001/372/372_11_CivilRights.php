<p><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A museum for the civil rights struggle</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by DONNY SCHRAFFENBERGER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2001 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="3"><B>WITH THIS article, <I>Socialist Worker </I>begins a summertime series on places that readers might like to visit during their next vacation. Send us your ideas for future columns.</B></P>
<P>IN EARLY April, I visited Memphis, Tenn. Although I gladly took in the sights--like Beale Street and Sun Records, where Elvis Presley recorded his first records--and ate my fill of catfish and ribs, the high point was a trip to the National Civil Rights Museum.</P>
<P>The museum is located in the Lorraine Motel, where Martin Luther King Jr. was gunned down in 1968.</P>
<P>I expected a half-hearted tribute to the struggle. I was pleasantly surprised to find an engaging look at the turbulent and long battle for equality in the U.S.</P>
<P>One part of the museum focuses on the brutality of slavery and the fight against it. There were exhibits about the abolitionists of the 19th century, and the former slaves who fought for the Union Army to wipe out their former masters' evil system.</P>
<P>But the main focus is on the civil rights movement after the Second World War.</P>
<P>The history featured here doesn't just record the names of famous civil rights leaders, although they're important to know and understand. Rather, the museum shows that ordinary people fought by the hundreds of thousands for their rights.</P>
<P>One exhibit reproduces the bus where Rosa Parks refused to take a back seat--sparking the Montgomery Bus Boycott in 1955. When you sit down, the bus driver warns you that you have to go to the back of the bus or be arrested.</P>
<P>There are also replicas of the segregated Southern lunch counters where protesters sat in--and the jail cell in Birmingham, Ala., where King wrote his eloquent letter against segregation.</P>
<P>The museum doesn't hide the fact that the civil rights movement had different tendencies and politics. The groundbreaking work of the Communist Party of the 1930s is rightfully mentioned, as are more radical 1960s organizations like the Black Panther Party for Self-Defense.</P>
<P>But the high point of the museum is the room and balcony where Martin Luther King was assassinated. Few people I was with could hold back the tears when they came to this moving place. If you're anywhere near Memphis, I highly recommend a visit to the National Civil Rights Museum.</P>
