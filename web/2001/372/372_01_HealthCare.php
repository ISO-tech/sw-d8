<p><font face="Times New Roman, Times, serif" size="4">Enough of Washington's empty words</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We want health care for all!</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 1</font></p>

<p><font face="Times New Roman, Times, serif" size="3">DECENT HEALTH CARE. That's the dream of millions across the U.S.</P>
<P>And that's why the long-awaited, much-debated Patients' Bill of Rights--now being considered by Congress--was seen as "extremely" or "very" important to 77 percent of those surveyed in a June 20 Gallup poll.</P>
<P>But when all the grandstanding is over in Washington, millions of Americans will be as far away as ever from decent health care. Even the Senate Democrats' version of the legislation, which George W. Bush is promising to veto, would do nothing for the 44 million Americans without health insurance--and would leave those with insurance with precious few new benefits.</P>
<P>But when you get down to it, some Americans just get better health care.</P>
<P>When Vice President Dick Cheney went into the hospital for tests on his heart last month, he came out with what the <I>New York Times</I> called "His Own E.R." implanted in his chest.</P>
<P>Cheney gets the best care available--because he's a millionaire. And besides, he along with every other Washington politician is covered by one of the most generous health plans in the country.</P>
<P>But when Dianna Oden gets sick, she doesn't get state-of-the-art technology. She doesn't even go to the hospital.</P>
<P>Oden, a 52-year-old single grandmother who works full time as a waitress, suffers from chronic pain due to a disorder called fibromyalgia. Her $6.50-an-hour wage disqualifies her from being covered under the Oregon state health plan--but leaves her too poor to afford private insurance. So she stretches her medication.</P>
<P>When Oden's gynecologist suggested recently that she go into the hospital to have a cyst removed, she said no. "When I heard the word 'hospital,' I thought, 'bills,' and asked him, 'What else can you do?'" Oden said.</P>
<P>Treasury Secretary Paul O'Neill doesn't have any sympathy for people like Oden. "Able-bodied adults should save enough on a regular basis so that they can provide for their own retirement and, for that matter, for their health and medical needs," O'Neill sniffed to the <I>Financial Times.</I></P>
<P>Easy to say if you made $59 million last year, as O'Neill did as CEO of Alcoa. But it's a different world if you work for a living in Bush's America.</P>
<P>"Over the last decade or two, the health care system has come ever-increasingly under the control of big corporate interests, giant hospital chains, drug companies," Dr. Quentin Young of Physicians for a National Health Program told <I>Socialist Worker. </I>"Our resources could give us the best health system in the world. We have the money, we have the workforce--9 million highly skilled health workers--we have the facilities, adequate hospital beds&#133;We have all the elements of a decent system, but we organize it on a profit-making basis, and the results are disastrous."</P>
<P>What a sick system--that denies people access to health care resources that should be available to all! We need to fight for an alternative--where people come before profits.</P>

