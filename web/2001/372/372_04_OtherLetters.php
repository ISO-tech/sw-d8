<p><a name="Bread"></a><font face="Times New Roman, Times, serif" size="5"><I>Bread and Roses </I>is an inspiring story</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>A local movie theater used a June 5 showing of the new film <I>Bread and </I>Roses as a starting point for an audience discussion of immigrant rights, union organizing and ethnic stereotypes. The post-film discussion at the not-for-profit Cinema Arts Centre in Huntington was led by immigrant labor organizers Zoila Rodriguez and Carlos Canales.</P>
<I><P>Bread and Roses </I>is a dramatic portrayal of the Justice for Janitors campaign in Los Angeles. The successful 1990s struggle by the city's building cleaners for union recognition and living wages was fought by a largely immigrant workforce.</P>
<P>Rodriguez and Canales are staff members of the Workplace Project in Hempstead, a Long Island group that helps immigrant day laborers, building cleaners and other low-wage workers fight for their rights. Rodriguez, a former undocumented immigrant and cleaning worker herself, said she admired the film's uncompromisingly pro-immigrant and workers' rights stance.</P>
<P>"We have the same rights as any other human being--with or without papers," Rodriguez told the audience.</P>
<P>Audience member Enzo Bard agreed. "When you think about it, we've all been illegals in this country since 1492," he said.</P>
<B><P>Kevin O'Neill,</B> Long Island, N.Y.</p>
