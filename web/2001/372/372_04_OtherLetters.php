<p><a name="Heal"></a><font face="Times New Roman, Times, serif" size="5">U.S. spends more to kill than to heal</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>Last week, the U.S. government promised $200 million to the United Nations to help fight AIDS.</P>
<P>Compare this with the $1.3 billion for weapons for Plan Colombia, $5.5 billion for Israel's war machine, $15 billion for NASA space missions, $29 billion for domestic police programs and some $300 billion in overall military spending.</P>
<P>AIDS kills millions--but if you look at the figures, you can see it's not about curing AIDS.</P>
<B><P>Mitch Lewis, </B>Cambridge, Mass.</P>

