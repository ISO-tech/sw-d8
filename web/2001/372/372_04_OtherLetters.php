<p><font face="Times New Roman, Times, serif" size="5">A juror's view of the justice system</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B> </P>
<P>I was recently called on to do my "civic duty"--and so I reported for jury duty at the Kings County Supreme Court. </P>
<P>I did get a civics lesson from the whole experience. I learned why it is that my brothers and sisters in Brooklyn are rightfully cynical about the criminal justice system.</P>
<P>First, the judge informed us that the jury system is the pillar of our democracy and that each defendant's guilt or innocence is judged by a jury of peers. </P>
<P>The judge then informed us that, while we would consider questions of fact, we would have no say over what the punishment would be. "No, no," we were informed, that was a matter of "the law" and therefore no concern of ours.</P>
<P>Some people in the jury pool had a little problem with that--of course, they were not selected for the jury.</P>
<P>The prosecutor told us that we couldn't have any sympathy for the skinny, 16-year-old hearing-impaired defendant who was peering around nervously at the people who would decide his fate. People who expressed concern about sending this kid to jail apparently weren't fit for jury service either.</P>
<P>In another telling moment, a police officer in the jury pool was asked if he ever knew another cop to do something wrong. As he paused before answering the question, a group of us broke out laughing, and a woman behind me murmured, "I take the fifth!"</P>
<P>My experience proved to me that the justice system is a farce. </P>
<P>First, anyone who is cynical about police is weeded out of the jury. Then the prosecution, which has far more resources than the defense, gets to line up a string of cops and present its one-sided case.</P>
<P>While it was good to see that many of my Brooklyn neighbors are very aware of these problems, the whole process was sickening.</P>
<B><P>Lucy Herschel,</B> Brooklyn, N.Y.</p>
