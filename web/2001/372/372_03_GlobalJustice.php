<p><font face="Times New Roman, Times, serif" size="5"><b>Mobilizing the fight for global justice</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 6, 2001 | Page 3</font></p>

<p><font face="Times New Roman, Times, serif" size="3">THE OUTRAGE over the shooting of protesters at the European Union summit in Gothenburg, Sweden, was still fresh last month when Spanish authorities viciously attacked a peaceful demonstration for global justice in Barcelona, Spain.</P>
<P>Originally timed for a World Bank meeting, the protest in a downtown park was expected to be uneventful because the Bank had cancelled its gathering.</P>
<P>But police had other plans. "Thousands of screaming demonstrators, some with small children, fled in panic as the police pushed into the crowd behind shields, wielding nightsticks and firing blanks," Associated Press reported.</P>
<P>The cops' excuse? Protesters had supposedly broken some store windows during an earlier march. But Jordi Pedret, a Socialist Party member of parliament, told reporters that undercover cops broke the windows.</P>
<P>Now Silvio Berlusconi, the newly elected right-wing prime minister of Italy, is threatening a bigger crackdown in Genoa, Italy--where a Group of Eight summit meeting will take place later this month. Russian President Vladimir Putin has already ordered an elite "anti-terrorist" unit to Genoa--the same forces that routinely use rape, torture and mass executions in Russia's war in Chechnya.</P>
<P>There's even talk that the summit could be moved from a medieval castle to a luxury ocean liner--or even the USS Enterprise aircraft carrier!</P>
<P>All this shows just how far the world's heads of state will go to silence opposition to their policies.</P>
<P>Unions and activists from across Europe are mobilizing for mass protests and a peoples' counter-summit in Genoa. By building the biggest possible demonstration, they can not only forestall a crackdown, but show the world that there's a large and growing resistance. Deepening this resistance means connecting the struggle for global justice to the force that no multinational corporation can do without--the working class.</P>
<P>The same is true for the September 28-30 protests against the International Monetary Fund and World Bank, set for Washington, D.C.</P>
<P>From Sweden to Italy to the U.S., the underlying issues are the same--whether a handful of powerful politicians and unaccountable bureaucrats will continue to run the world in the interests of their corporate masters.</P>

