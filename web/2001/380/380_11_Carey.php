<P><font face="Times New Roman, Times, serif" size="4">Federal court acquits Teamster reform leader</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Ron Carey cleared</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Former Teamsters President Ron Carey was acquitted of all charges in a federal perjury trial here last week, a decision that exposed the frame-up that the government used to force him from the union.</P>
<P>Federal prosecutors--egged on by the Bush administration--also hoped to use the trial to take down AFL-CIO Secretary-Treasurer Rich Trumka and possibly AFSCME President Gerald McEntee. While the government could still proceed with indictments before November 1, this is unlikely.</P>
<P>However, the damage has already been done to Carey and the Teamsters.</P>
<P>Following the Teamsters' victorious strike against United Parcel Service in 1997, investigations into the 1996 Teamsters election uncovered a fundraising operation in Carey's campaign that allegedly violated union campaign laws. The plan--hatched by longtime Democratic political consultants Jere Nash and Martin Davis with the help of Bill Clinton's top fundraiser, Terry McAuliffe--involved Teamsters' donations of $885,000 to liberal groups in exchange for donations to Carey's campaign.</P>
<P>Of course, this pales besides the sleazy fundraising in Washington, from Clinton's rental of the Lincoln Bedroom to Bush's mountain of corporate cash. But it started an anti-union witch-hunt in Congress--and gave ammunition to the government officials who oversee the union as part of a federal consent decree. They banned Carey from running in 1998.</P>
<P>The evidence against Carey? Court testimony by Nash--who was convicted. Yet even Nash admitted that he informed Carey of the convoluted plan in a 15-second phone call--and never mentioned any cash swap.</P>
<P>The jury in Carey's trial concluded that Nash was lying about Carey's role. But the union's Internal Review Board had already used the same accusations to expel Carey from the union.</P>
<P>With Carey's ouster, James P. Hoffa won the 1998 election and is now running for re-election. Hoffa promised to "restore Teamster power." Instead, he restored the power of self-serving bureaucrats and crooks. Hoffa has negotiated a series of weak contracts from Northwest Airlines to Anheuser Busch and has failed to organize new members.</P>
<P>Carey's court victory is also a vindication of the movement to chase Hoffa out of union office and democratize the union. That's why supporters of reform should send Hoffa another message--and vote for the Tom Leedham slate.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Why they went after Ron Carey</font></P>
<P>IT ISN'T hard to see why employers wanted to get Ron Carey.</P>
<P>"Some politicians ought to wear the logos of their corporate sponsors on their suits, just like athletes wear them on their uniforms," Carey said in a speech a few weeks after the victorious UPS strike of 1997.</P>
<P>From the employers' point of view, such speeches--and Carey's leading role in the strike--was Carey's real crime. They also hated Carey for taking the Teamsters out of the Republican camp. The wrongdoing by Carey's aides was used as a pretext to oust Carey himself.</P>
<P>The labor movement should have rallied to Carey's defense. Instead, he was abandoned by the entire AFL-CIO leadership--even when federal prosecutors used the indictment to pressure other top union leaders under suspicion. </P>
<P>Even Teamsters for a Democratic Union, the reform caucus that backed Carey, refused to defend him. Nor did Tom Leedham, the current reform candidate for Teamster president, take up the defense of Carey. Some reform leaders argued that abandoning Carey was the only way to protect the reform movement from the taint of scandal. In reality, it only made Carey supporters cynical about reform of the union.</P>
<P>It fell to a handful of left-wing publications like <I>Socialist Worker </I>to take up the argument that the Carey was the target of a politically motivated attack.</P>
<P>Carey's personal vindication can never undo the damage that the government caused by opening the door for Hoffa. But it can serve as a reminder to rank-and-file Teamsters--and union members everywhere--of the need to stand up against government attack on our unions.</P>

