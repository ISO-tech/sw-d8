<P><font face="Times New Roman, Times, serif" size="4">Crimes of the IMF and World Bank exposed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Life and debt</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><b>MOVIES: <i>Life and Debt,</i></b> produced and directed by Stephanie Black. Now showing in New York, Washington, D.C., and other cities. Visit www.lifeanddebt.org for other showings.</font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IF YOU want to understand the impact of IMF and World Bank policies, you only have to ask a farmer or sweatshop worker in Jamaica. And this is what Stephanie Black does in her powerful film <I>Life and Debt.</I> </P>
<P>The documentary contrasts the view of Jamaica as an "island paradise" for tourists with the poverty conditions for people who live there. It cuts to the heart of the crisis--the enormous debts that countries like Jamaica owe to global financial institutions, and the conditions that these institutions impose on poor countries.</P>
<I><P>Life and Debt </I>includes interviews with Jamaica's former prime minister Michael Manley, as he describes what it's like to find you have "two nooses around the neck"--the IMF and World Bank. Manley's words are interspersed with an interview with IMF Deputy Director Stanley Fischer, who coldly explains the goals of the global loan sharks.</P>
<P>But the most eloquent analysis comes from Jamaican farmers and workers. As one dairy farmer explains IMF and World Bank policies: "If we lend you $50 million, you have to lower your trade barriers, and you have to compete with us on a level playing field. This is double talk&#133;America subsidizes some of its exports by 137 percent. Nobody can compete with that."</P>
<P>The film then shows farmers dumping milk that they are unable to sell because of the glut of imported powdered milk.</P>
<P>A man describes a shipment of poultry brought in for sale in Jamaica that was found to be 20 years old. The company admitted that this was a mistake. The shipment was supposed to go to Haiti.</P>
<P>The film interviews workers in the Kingston "free enterprise zone," where clothing for companies like Tommy Hilfiger is stitched together--free of taxes and duties, and especially free of unions. "It's like working under slavery," says one woman, describing poverty wages and intense speedups without bathroom breaks.</P>
<I><P>Life and Debt </I>is a damning indictment of the global free market--and a stunning film to watch. It has played to sold-out audiences in New York City because of word of mouth alone.</P>
<I><P>Life and Debt </I>is a great resource for activists organizing against the IMF and World Bank--and an excellent way to convince others why they should join the fight.</P>

