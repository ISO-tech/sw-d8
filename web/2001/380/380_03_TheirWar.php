<P><font face="Times New Roman, Times, serif" size="4">Washington sends the message that it will call the shots</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Their war at home and abroad</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH promised a war that would bring "justice" and "end terrorism." He vowed to round up the "prime suspect" in the September 11 attacks--Osama bin Laden--"dead or alive."</P>
<P>But as <I>Socialist Worker </I>went to press, the dead in Afghanistan included hundreds of innocent civilians--with millions at risk of famine. And Bush was no longer even bothering to conceal the real aim of the operation.</P>
<P>"It would be a useful function for the United Nations to take over the so-called nation building--I would call it the stabilization of a future government--after our military mission is complete," Bush said.</P>
<P>With undisguised racism and contempt, Bush was parroting what his advisers and media pundits had been telling him--that Afghans and other people in the Middle East and Central Asia are unfit to rule themselves. </P>
<P>"I suspect the best medium-term solution will be to revive the old League of Nations mandate system, which served well as a 'respectable' form of colonialism between the [world] wars," wrote author Paul Johnson in the <I>Wall Street Journal.</I> </P>
<P>Columnist Mark Steyn made the point more crudely in the <I>Chicago Sun-Times.</I> "If neo-colonialism makes you squeamish, give it some yuppie Clinto-Blairite name like 'global community outreach,'" he sneered in an article headlined "Imperialism is the answer."</P>
<P>Such statements make it impossible to hide what this war is really about--asserting the U.S. government's right to conquer and dominate weaker nations.</P>
<P>After all, the world's most advanced weapons are hardly needed to defeat Afghanistan's paltry military. But this is an effective way for the U.S. to stake its claim as the new boss of Central Asia.</P>
<P>The other major powers want a piece of the action, too. German Prime Minister Gerhard Schr&ouml;der and his Japanese counterpart, Junichiro Koizumi, are using the war to escape historic restrictions on deployment of their militaries. Meanwhile, British Prime Minister Tony Blair fronted for the U.S. agenda on his trips to the Middle East and South Asia.</P>
<P>With the new colonial war abroad has come a new war at home--on our civil rights and unions. The FBI and government officials were using the anthrax scare--though it hasn't been linked to bin Laden--to intimidate critics of Bush's war and justify a crackdown on civil liberties. Attorney General John Ashcroft has even appealed to people to spy on their neighbors and report "suspicious activity"--a tactic straight out of the 1950s red scare.</P>
<P>After a week of bombing, it seemed as if the Bush White House was having its way--both with the military campaign and maintaining domestic support. But Washington's plans are full of contradictions--at home and abroad.</P>
<P>In Pakistan--which had to be pressured into breaking with its Taliban allies to back the U.S.--protests and strikes greeted Secretary of State Colin Powell's visit this week. Also during Powell's stay, Pakistani troops exchanged fire with the Indian army--even though the two are both supposed to be part of the U.S. "coalition against terrorism." Protests against the bombing swept the Muslim world--from Indonesia to Palestine--and many of the demonstrations were put down by force.</P>
<P>The war at home has problems, too. </P>
<P>While Washington appealed to patriotism, Corporate America forced through layoffs in the airlines and other industries. Congress repeatedly refused to give a dime of extra benefits to laid-off workers. And after years of denying spending increases to "protect the budget surplus," Congress is shoveling tens of billions into military spending and--if Bush gets his way--more tax cuts for the rich.</P>
<P>This is a slap in the face to working people. "Today, in the wake of the September 11 attack on America, some in the Congress are using the nation's tragedy to engage in tax profiteering," AFL-CIO President John Sweeney--who supports the war--said last week. "They aim to exploit the nation's desire to stand together to push through an agenda that predates the nation's crisis and would do nothing to solve it."</P>
<P>This resentment is shared by millions of people who may support the war today--and it can lead them to an antiwar position as they see Bush's real agenda of imperialist war abroad and an attack on workers at home.</P>
<P>That's why the movement against the war has to be rooted in workplaces, unions communities and campuses across the country. We can build a movement to end this war--and for an alternative to the society that produced it. </P>

