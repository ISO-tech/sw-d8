<P><font face="Times New Roman, Times, serif" size="5"><b>Stop gay bashing!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 10</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Hundreds marched through Chicago's Boys Town neighborhood in memory of Matthew Shepard, the 21-year-old college student who was tortured and beaten to death three years ago in Laramie, Wyo.</P>
<P>Protest organizers, the Chicago Anti-Bashing Network (CABN), linked their opposition to gay bashing to a wider antiwar and anti-hate message. The most popular chant on the march was "Gay, straight, Arab, white--one struggle, one fight." One speaker, Ali Abunimah from the Arab American Action Network, spoke out against the hateful climate that is developing against Arabs and Muslims as a result of the war drive. </P>
<P>In response to the Salvation Army's refusal to hire gays and lesbians, protesters marched to their neighborhood headquarters to initiate a new campaign. When Salvation Army workers ring bells and collect donations this holiday season, CABN will be dropping anti-hate messages into their collections and organizing other actions against them.</P>

