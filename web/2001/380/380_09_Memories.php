<P><font face="Times New Roman, Times, serif" size="5"><b>"Memories of racial profiling"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 9</font></p>

<P><font face="Times New Roman, Times, serif" size="3">After the Japanese bombing of Pearl Harbor in 1941, ROGER SHIMOMURA's family was taken from their home and put in an internment camp in Idaho. Roger's first memories of life were of the camp--where his sister was born and his grandmother died a prisoner.</P>
<P>Now an artist and teacher at the University of Kansas, Roger spoke with <I>Socialist Worker's </I>Dennis Kosuth about his latest work "Not Pearl Harbor." </P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>TELL US what happened to your family after Pearl Harbor.</b></P>
<P>PRESIDENT ROOSEVELT signed the executive order, and the FBI made the first sweep, rounding up community leaders. The FBI searched homes for materials that might implicate them as spies or informers of Japan.</P>
<P>If a family happened to have a compass, for example, that became proof of spying for the Japanese. My grandmother's diaries--she ended up burning many of them--or any kind of sentimental reference were interpreted as sympathy for the old country. </P>
<P>It's worth remembering that Japanese American soldiers fought in Europe--and 95 percent of the parents of the men who went to fight were in camps.</P>
<P>My new painting has the face of a World War Two-stereotype Japanese person with slanted eyes, yellow skin and buck teeth--except this person is wearing a turban and a beard like a member of the Taliban. In the background, a Japanese zero fighter plane is crashing.</P>
<P>The accompanying statement reads: "The comparison of the tragedy at the World Trade Center to Pearl Harbor brings back memories of racial profiling in World War Two when innocent Japanese Americans were placed in internment camps, and now Arab Americans are suffering the same indignity."</P>
<P><i>An exhibit of Shimomura's paintings opened October 12 in Washington, D.C. at the Anton Gallery. Much of his work is on display at the Greg Kucera Gallery in Seattle, or visit his Web site at www.gregkucera.com/shimomura.html</i></P>

