<font face="Times New Roman, Times, serif" size="5"><b>Why the U.S. can't bring justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 7</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>TODD CHRETIEN, who worked for Ralph Nader's presidential campaign last year, challenges Nader's confused attitude to the U.S. war on Afghanistan.</b></P>
<P>AFTER THE first few days following the September 11 attacks, I got used to supposedly "progressive" Democrats lining up behind George W. Bush. But I was surprised to hear Ralph Nader--the veteran consumer advocate who got 2.7 million votes running for president on the Green Party ticket last year--sound like a supporter of war during an interview on a Bay Area morning radio show October 9.</P>
<P>"If we're gonna do what we're gonna do, we should do it in a very constitutional way," Nader said. "If we're going to make that move, we've got to accept certain responsibilities. Just as [when] we declared war against Japan, defeated Japan and had General Douglas MacArthur have a peaceful occupation of Japan until he brought it around to a more peaceful and stable country and economy." </P>
<P>Nader even praised Secretary of State Colin Powell.</P>
<P>Needless to say, those of us organizing for a Nader super rally a couple days later couldn't believe that he failed to mention that the atomic bombing of Hiroshima and Nagasaki that preceded the "peaceful occupation" of Japan. And how could anyone for peace have anything good to say about Colin Powell?</P>
<P>I supported Nader during the elections and was California student coordinator for the campaign. Nader put forward a serious alternative to the two mainstream parties--including his critique of U.S. foreign policy, especially sanctions against Iraq that have killed more than 500,000 children.</P>
<P>After hearing the radio interview, I was pretty disgusted. I got on the phone and spoke to one of Nader's aides, asking for clarification.</P>
<P>Fortunately, at the San Francisco rally on October 11, Nader gave a speech that clearly condemned the U.S. bombing.</P>
<P>"We can't just bomb our way to justice," Nader said. "Let's talk about the people of Afghanistan&#133;In four weeks, it will be winter, and 7.5 million people are on the verge of starvation." </P>
<P>Nader also rightly criticized "corporations that are trying to turn this tragedy into a pretext for handouts and to increase their power over the rights of the people. The airlines got $15 billion just like that and then laid off 85,000 workers."</P>
<P>Yet as happy as I was that Nader stopped praising Colin Powell, he went on to say things that I believe will hurt the anti-war movement.</P>
<P>First, Nader's idea that the war would be any different if Bush had asked the Senate for a declaration of war is foolish. I doubt that the people who are being hit by cluster bombs care much about the niceties of American constitutional law.</P>
<P>U.S. politicians and their rich backers have never cared much about following the constitution. Nader should remember his campaign slogan from last year: "The only rights you have are the ones you fight for." </P>
<P>Second, Nader still seemed to accept the idea that the U.S. military has the right to overthrow and replace Afghanistan's government. This leaves the door open to a different type of intervention. </P>
<P>In other words, while Nader opposes Bush's bombing raids, it isn't so clear that he opposes U.S. Special Forces invading Afghanistan to overthrow the Taliban and capture Osama Bin Laden. </P>
<P>The anti-war movement needs to oppose all U.S. efforts to dictate what happens in other countries. </P>
<P>The Taliban government is a terrible right-wing regime. But it only came to power with the support of U.S. allies like Pakistan and Saudi Arabia--in the interest of creating "stability" for U.S. oil companies. The surest way to get rid of the Taliban and bin Laden is for the U.S. to stop interfering and get its military out of the area.</P>
<P>Finally, Nader ended his speech by saying, "You shouldn't let anyone take that flag away from you. The flag represent liberty and justice for all, not a gag on your First Amendment rights."</P>
<P>The idea that opponents of Bush's war should take back the American flag is deeply flawed. Of course, millions of people put the flag on their cars or homes in an expression of solidarity with the people who died in the World Trade Center and Pentagon attacks. We shouldn't automatically assume that everyone who displays a flag is pro-war.</P>
<P>But we must remember that the flag is painted on the cluster bombs and Tomahawk cruise missiles that falls on Afghanistan. </P>
<P>Patriotism means loyalty to your country. But all wars are fought between working people from different countries. In other words, patriotism is the ideology of war. It's the explanation for why we should accept that hundreds and thousands of Afghans--men, women and children--will "unfortunately" die in the U.S. bombing as "collateral damage."</P>
<P>Would anyone in their right mind accept that hundreds of people from, say, California must "unfortunately" die at the hands of the U.S. military in order to "catch terrorists?" </P>
<P>Patriotism and the flag are an excuse for wars--not a means to stop them. Our movement should reject the idea that there are some circumstances where U.S. military action against Afghanistan would be justified.</P>
<P>Bush's war--in all its forms--is only causing more misery and suffering. We have to stand up and oppose it.</P>

