<P><font face="Times New Roman, Times, serif" size="5"><b>Giving a green light to racial profiling</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yahmatta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RACIAL PROFILING is perfectly acceptable. That was the message that the U.S. Supreme Court sent in early October when it refused to hear a case accusing police in an upstate New York town of singling out African Americans.</P>
<P>The case stems from a 1992 incident in which a white woman from Oneonta, N.Y., said that she was attacked by a Black man in her home. The woman never saw the face of her attacker and only caught a "glimpse" of his forearm. But that was enough for Oneonta cops to demand that the local college provide a list of all Black male students on the campus.</P>
<P>The cops interrogated all the Black male students--and then proceeded to stop and question another 200 Black men who lived in the town.</P>
<P>If that isn't racial profiling, then what is? But the Supreme Court wasn't interested in hearing the case--because a ruling in favor of the former Black students who sued would have helped establish that racial profiling is illegal.</P>
<P>That's a problem--because racial profiling has become the number one domestic strategy in Bush's "war against terrorism." Only now, it's not only African Americans and Latinos who are harassed, but Arabs, Muslims and anyone who looks Middle Eastern.</P>
<P>We have to oppose this green light for racism. Racial profiling will never be justified!</P>

