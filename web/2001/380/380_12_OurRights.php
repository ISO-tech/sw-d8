<P><font face="Times New Roman, Times, serif" size="5"><b>Don't let them take our rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 12</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON reports on the U.S. government's assault on civil liberties.</b></P>
<P>IT'S OPEN season on immigrants. That was the message the Bush administration was sending October 10 when the FBI unveiled its "Most Wanted Terrorist" list.</P>
<P>Every single one of the 22 men is of Middle Eastern descent. Apparently, the Bush gang forgot that the last major terrorist attack in the U.S.--the bombing of the Oklahoma City federal building in 1995--was carried out by homegrown Nazis.</P>
<P>The feds even announced a deal with the TV program <I>America's Most Wanted </I>to publicize the list--and solicit tips to track them down. The program is unlikely to produce any leads about the 22 suspects--since none are thought to be in an English-speaking country, much less the U.S. But it's almost certain to lead to more intimidation, harassment and physical attacks on people who "look like" them--in other words, young men with Middle Eastern features.</P>
<P>The <I>America's Most Wanted </I>stunt fits in perfectly with the vigilante climate that George W. Bush whipped up after the September 11 air attacks, when he declared that Osama bin Laden was wanted "Dead or Alive." Now, says Bush, with the help of a tabloid TV show, "We're going to shine the light of justice" on those responsible for the attacks.</P>
<P>In fact, "justice" so far has meant throwing more than 700 people--almost all immigrants or foreign nationals--behind bars in connection with the investigation into September 11. But so far, the FBI has only charged one person with a crime--perjury--directly related to the case.  The rest are being held as potential "material witnesses"--or on minor immigration and even traffic violations.</P>
<P>Lawyer Stanley Cohen says the "material witness" orders that the feds are using to detain people would never be granted before September 11. "Now there isn't a federal judge in the United States&#133;that's going to say 'denied.'" Cohen said. "They just roll over and say, 'Give me your hungry, your tired, your poor, your huddled masses yearning to be jailed.' That's what's going on."</P>
<P>To justify such repression, the Bush gang has stoked people's fear of terrorist attacks to keep the climate of suspicion and scapegoating at a fever pitch.</P>
<P>Last weekend, the FBI predicted that the likelihood of another terrorist attack on the U.S. was "100 percent."</P>
<P>Administration officials followed up with a crackpot story that bin Laden was communicating with terrorist "sleeper" cells through coded messages in his videotaped statements. The point of this absurd story? To bully TV networks into keeping bin Laden's statements off the air.</P>
<P>There's a reason for the feds' hysteria: They want to take our rights away for good.</P>
<P>On October 12, the House of Representatives passed the Uniting and Strengthening America (USA) Act by a vote of 337 to 79. The legislation is a nightmare of restrictions on civil liberties and Big Brother-style surveillance provisions. Among other things, it would expand the ability of law enforcement agencies to conduct wiretaps and searches, including so-called "sneak and peek" searches--where authorities can enter and search homes or offices while the occupants aren't there and without having to ever inform them. </P>
<P>The USA Act would also give the CIA the power to spy on Americans, something which it's currently barred for doing. Plus, the bill broadens the definition of "terrorism" to such an extent that basic acts of political protest will easily qualify. According to the ACLU, a student demonstrator breaking a window could be tried as a terrorist.</P>
<P>"This could be the Gulf of Tonkin resolution for civil liberties," said Rep. Peter DeFazio (D-Ore.), one of the few lawmakers to oppose the USA Act.</P>
<P>Washington's warlords aren't content with bombing a defenseless country into rubble. They want to steal our rights and silence all opposition. Don't let them get away with it.</P>

