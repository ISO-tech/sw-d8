<P><font face="Times New Roman, Times, serif" size="5"><b>House arrest lifted for Charleston Five</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 10</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE CHARLESTON Five were freed from house arrest as <I>Socialist Worker </I>went to press, thanks to a defense campaign that has stretched from the U.S. to Europe to South Africa. The release came less than a week after South Carolina Attorney General Charles Condon withdrew from the case after mounting pressure for him to stop this politically motivated attack on the five dockworkers and their union.</P>
<P>The five--members of International Longshoremen's Association (ILA) Locals 1422 and 1771--spent more than 20 months confined to their homes between 7 p.m. and 7 a.m. except to work and attend union activities. This harsh treatment followed a police attack on ILA picket lines against a nonunion operation on January 20, 2001.</P>
<P>The retreat by the prosecution marks a major step for the defense campaign. However, trial is scheduled to proceed next month--with an International Day of Action set for November 14. And the struggle won't be over until all the charges--which carry sentences of up to five years in prison--are dropped.</P>
<P>ILA Local 1422 President Kenneth Riley has continued his speaking tour--including a stop October 12 in New York City that drew 150 people and raised more than $5,000 for the Five's legal defense, including large contributions from several union locals. All out November 14!</P>
<P><I>For more information about the Day of Action, contact the South Carolina AFL-CIO at 803-798-8300.</I> </P>

