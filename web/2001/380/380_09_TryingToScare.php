<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Trying to scare the hell out of us</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN 1947, President Truman was having a hard time convincing war-weary Americans that they should get behind his plans for a Cold War with the Soviet Union. He received a friendly piece of advice from Republican Sen. Arthur Vandenburg, who told Truman he "would have to scare the hell out of the country."</P>
<P>Days after announcing his military crusade against the USSR, Truman launched the first postwar witch-hunts against "subversives."</P>
<P>It's worth remembering this when we try to make sense of the Bush administration's doublespeak.</P>
<P>On the one hand, it calls on Americans to "get back to normal" so that the terrorists "won't win." On the other hand, Attorney General John Ashcroft and other officials issue daily warnings of terrorist threats.</P>
<P>President Bush's photo-op at Chicago's O'Hare International Airport last month illustrated this doublespeak best. While Bush urged Americans to fly again, the government announced that it had given the military authority to shoot down hijacked or off-course airliners.</P>
<P>Now that's the way to inspire confidence in the traveling public!</P>
<P>Since the September 11 attacks, every "anti-terrorism" crackpot with an outlandish theory about "cyberterrorism" or "bioterrorism" has gotten a hearing in the major media. The cable shows that spent the summer hyping the dangers of shark attacks are now devoting hours to speculation about whether major cities could survive a chemical weapons assault.</P>
<P>The September 11 attacks were shocking and horrifying. They made ordinary people more anxious and fearful. But politicians and pundits dwelling on outlandish scenarios--or rushing to judge perfectly explainable incidents as evidence of terrorism--simply increase these fears.</P>
<P>Even after September 11, ordinary Americans are less likely to be killed or hurt in a terrorist attack than they are to be struck by lightning. At the same time, an American dies in a car accident every 13 minutes, according to the National Safety Council. That's more than 43,000 killed each year.</P>
<P>Then there's the way that the everyday workings of the system put people at risk. More than 6,000 people die each year in work-related accidents, according to the Occupational Safety and Health Administration (OSHA). Another 50,000 to 70,000 die each year from diseases contracted in the workplace. But you won't hear Bush declaring a "war on occupational injury and disease." </P>
<P>An estimated 817,000 households experience severe hunger each year, according to the U.S. Department of Agriculture. This kind of hunger has caused more disease in the U.S. than bioterrorism ever will.</P>
<P>So when you ask what really threatens the health and safety of ordinary Americans, the reality is that corporate greed, unsafe workplaces and poverty far outstrip any possible threat from terrorism.</P>
<P>But all the scare talk serves the Bushes and Ashcrofts in their drive to push through their agenda. They, like their predecessors in the Truman administration, want to "scare the hell out of the country" enough to ram their military spending hikes and attacks on civil rights through Congress. These measures won't make Americans safer. But they will take away freedoms. </P>
<P>The absurdity of all of this became clear when leaked congressional testimony quoted a government official asserting a "100 percent chance" of terrorist attacks in retaliation for the U.S. war in Afghanistan. The leak infuriated the Bush administration, leading it to clamp down further on "counterterrorism" information disclosed to Congress.</P>
<P>It's not hard to see that Bush and his cronies objected for reasons other than "compromising intelligence." The leak gave the lie to the administration's entire crusade. The war they said would protect Americans will actually endanger them.</P>
<P>But for a president determined to show that American military might makes right, that's a risk worth taking.</P>

