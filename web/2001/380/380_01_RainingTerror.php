<P><font face="Times New Roman, Times, serif" size="4">And they call this justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Raining terror on Afghanistan</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IN THE hills west of the Afghan city of Jalalabad, Aiman Malai watched as U.S. jets approached the village of Kadam "like two black darts shooting through the air&#133;They came low over it, and then there was a huge explosion and flames reaching high into the air," Aiman told Britain's <I>Observer.</I> </P>
<P>But that wasn't enough. Without a "military target" in sight, the warplanes circled back for <I>two more </I>runs at the defenseless village.</P>
<P>Kadam was obliterated--with the bodies of as many as 200 people buried beneath the rubble. "All the women and children were still in the houses," said a man whose nephew was injured in the attack. "They had no chance."</P>
<P>These are the victims of a war that the U.S. government is waging in the name of "justice." Hundreds of Afghans--with no connection at all to Osama bin Laden or the Taliban government--have been killed by U.S. not-so-smart weapons.</P>
<P>All this so American generals could gain "air superiority" over Afghanistan--a country whose "air force" consisted of 20 ancient MIG jets, most of which couldn't fly.</P>
<P>Pentagon brass bragged about bombing Osama bin Laden's "terrorist camps." They had no excuse for missing their locations--since the CIA built many of them, a fact conveniently forgotten during Pentagon briefings.</P>
<P>And for every Afghan who dies beneath a U.S. bomb, many more will suffer a long, drawn-out, horrific death--from starvation.</P>
<P>Afghanistan was already facing famine conditions before Bush's war. Now the UN says it had about 9,000 tons of food in Afghanistan as of mid-October--less than one-fifth of what it estimates is needed every month to stop millions of Afghans from starving.</P>
<P>Plus winter will close the roads in a month's time. Relief workers say they need a five-month stockpile of food in place by then. No one believes it will be.</P>
<P>So George W. Bush has handed a death sentence to an estimated 1.5 million Afghans.</P>
<P>And Washington's warmakers are talking about lashing out at other convenient targets. 0Like Iraq, a country already devastated by a decade-long U.S. war. Like Indonesia and the Philippines, where the only conceivable excuse for intervention is that Islamist organizations in these countries are critical of the U.S. government.</P>
<P>Washington's message is clear: Opposing the U.S. is justification enough for raining death from the skies.</P>
<P>This is a sick slaughter that we have to stand up against. We won't let Washington use the September 11 attacks as an excuse for causing far worse around the world. Stop this war now!</P>

