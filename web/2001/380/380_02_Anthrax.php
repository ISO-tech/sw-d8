<P><font face="Times New Roman, Times, serif" size="4">Pentagon hardliners blame outbreaks on Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stoking panic about anthrax</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. was gripped by a "bioterrorism" scare after a dozen people in Florida, New York and elsewhere were exposed to anthrax apparently sent through the mail.</P>
<P>National and local news shows were dominated last week by reports of new "outbreaks"--though usually, these amounted to someone finding traces of harmless white powder. Pharmacies sold out of the antibiotic Cipro, the most effective drug for treating anthrax--despite warnings from health care professionals that people treating themselves would likely cause more harm than good.</P>
<P>The anthrax scare is understandable. The idea that such a deadly disease could be spread so easily is horrifying.</P>
<P>But the panic has only added to the climate of fear that followed the September 11 air attacks. Terrorism "experts" can now spin the wildest stories about who's to blame--and get an enthusiastic hearing in the media.</P>
<P>After FBI officials stated that there was no evidence to connect the anthrax cases in Florida and New York with Osama bin Laden, right wingers began pointing their fingers at the next most convenient target--Iraq. The "proof"? Producing the airborne form of anthrax that struck the three victims in Florida requires millions of dollars worth of equipment and sophisticated lab facilities. "This is prima facie evidence of the involvement of a state intelligence agency," said an anonymous CIA official. "That leaves Iraq."</P>
<P>What a bunch of bunk! Hard-liners in the Pentagon who want to make Iraq the next target of the U.S. war on terrorism are obviously exploiting the anthrax scare to build their case.</P>
<P>If there's one organization capable to producing anthrax in large quantities, it's the U.S. government. The U.S. and Russia have the vast majority of the world's germ weapons--enough between them to kill everyone on the planet.</P>
<P>Yet the U.S. has reportedly embarked on a series of secret research programs to make its biological weapons arsenal even more deadly--including a new, more lethal strain of anthrax. That's why U.S. representatives wrecked an international effort in July to come up with tighter enforcement of a worldwide ban on germ weapons.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">One Nazi's germ warfare crusade</font></P>
<P><font face="Times New Roman, Times, serif" size="3">IN THE rush to blame Middle Eastern terrorists, the media conveniently forgot that the last people connected to bioterrorism in the U.S. were American-born Nazis.</P>
<P>In 1998, federal agents arrested two men in Las Vegas for possessing anthrax. One of the two, Larry Wayne Harris, was a former member of the Aryan Nations. An FBI official told reporters that Harris and William Leavitt were building a germ weapon.</P>
<P>Harris is an old hand at the bioterrorism game. He says he worked for the CIA in the 1980s training Iraqi scientists in biological warfare--back when Iraq was an ally of the U.S. In 1995, Harris was caught by the feds for obtaining bubonic plague bacteria through the mail.</P>
<P>He told <I>U.S. News and World Report </I>that his personal germ warfare crusade was "deterrence. If you know that everyone in the neighborhood has an atomic bomb, civility returns to the neighborhood very quickly."<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Is the vaccine worse than the disease?</font></P>
<P><font face="Times New Roman, Times, serif" size="3">THE U.S. military wants 2.4 million soldiers and reservists to take a vaccine against anthrax. But given the track record of the company making the vaccine, it's no wonder that hundreds of soldiers have refused to be inoculated.</P>
<P>"BioPort is the industry equivalent of Valujet," one analyst told National Public Radio.</P>
<P>Shipments of BioPort's anthrax vaccine have been suspended by the Food and Drug Administration (FDA) since 1998 because of poor quality. Officials say the company has a hard time making the same vaccine twice. Plus 30 percent of soldiers given the vaccine have reported mysterious side effects after taking the drug, including serious ones that may have caused six deaths.</P>
<P>But BioPort's team of owners--which includes Adm. William Crowe, former chair of the Joint Chiefs of Staff under Ronald Reagan--have a lot of pull in Washington. The anthrax scare may help the company win FDA approval to resume shipments to the Pentagon--so U.S. soldiers can again become guinea pigs for BioPort.</P>

