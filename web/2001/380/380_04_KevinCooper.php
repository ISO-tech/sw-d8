<P><font face="Times New Roman, Times, serif" size="4">An innocent man speaks out from death row</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This country was founded on violence"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 19, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">KEVIN COOPER is an innocent man on death row at San Quentin prison. Kevin has been an outspoken opponent of the death penalty--and also in the struggle for a better and more just world.</P>
<P>Members of the Campaign to End the Death Penalty visited Kevin after the September 11 attacks and asked him to put his thoughts into words. <I>Socialist Worker </I>prints excerpts of his statement here.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WAR IS defined as: a state or period of conflict or armed conflict between nations or states; a determined struggle or attack; a condition of antagonism or conflict; the techniques or procedures of war, military science; a concerted effort to put down, reduce, or exterminate a group of people. </P>
<P>This country called America was founded on war. There have only been a few brief times in the history of this country when it has not been at war, whether within its borders or with another country. </P>
<P>I, Kevin Cooper, and the thousands of men, women and children who are on death row across this country have had war declared on us by the same government that is now declaring war on the different peoples around the world. </P>
<P>The very same statements that are being made by the government against those who they call "terrorists" have been made against me and others in my situation. We all have been told that we are animals; that we deserve to be eliminated; that we are expendable; that the world would be better off without us. </P>
<P>Throughout this country's history, war and the death penalty have coexisted. The people who declare war and impose the death penalty do not simply inflict their will upon certain groups. First, they must dehumanize the people who they want to attack in war and they want to execute. They must put them down to lift up the people in whose names war and execution are carried out. </P>
<P>America was founded on violence, especially violence within its own borders, inflicted upon its own people. Instead of taking a step back, learning from its past and seeing what violence and war truly mean, this country is about to ignore what it has learned in favor of doing what it has historically done: Create violence against poor people, and people of color. </P>
<P>The 1970s lyrics to a song titled "War" by Edwin Starr say: "War, what is it good for? Absolutely nothing!" I must agree. </P>
<P>In struggle from death row at San Quentin Prison, <B>Kevin Cooper</B></P>

