<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bosses cash in on U.S. war drive</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AT A recent regional United Auto Workers event, a top official made it clear in his closing speech that, as a military veteran, he supports the U.S. war on Afghanistan. But, he added, "Corporate America is waving the flag with one hand and stuffing their pockets with the other--at the expense of working people."</P>
<P>There are plenty of examples of this. Congress's $15 billion airline industry bailout passed soon after the September 11 attacks, with the promise that relief for workers would come later.</P>
<P>But it hasn't--and House Majority Leader Dick Armey (R-Texas) explained why. "The model of thought here, and quite frankly, the model of thought that says we need to go out and extend unemployment benefits and health insurance benefits and so forth is not one that is commensurate with the American spirit here," Armey told reporters.</P>
<P>AFL-CIO President John Sweeney denounced Congress for having "stiffed airline industry workers in the bailout bill" while protecting executives' "golden parachutes." Sweeney then appealed to Congress for a separate bailout in the name of national unity. "Americans are hungry for a continued, positive response to the aftermath of the September 11 tragedy, " he said. "They want to see fairness in a time marked by injustice, to see balance and calm in a time of suffering--and that's what we're calling for here today."</P>
<P>Yet the airlines even tried to use a clause in their contract to avoid paying severance packages to laid-off workers--until pressure from unions and bad publicity forced them to pay up.</P>
<P>When the U.S. bombing campaign against Afghanistan began October 7, Sweeney issued a statement that combined support for the war with another appeal for relief for laid-off workers. But Congress last week stiffed labor again when the Senate blocked extended jobless and related benefits.</P>
<P>As a consolation, George W. Bush claimed that his economic stimulus package would contain some relief for the unemployed--if it gets through the Republican House. But as Sweeney pointed out, Bush's plan "will lavish rich new tax breaks on corporations and speed up tax cuts for individuals."</P>
<P>What's more, employers are using the September 11 tragedy to justify further attacks on unions. The <I>Chicago Tribune </I>reported October 5 that Wall Street wants the Big Three automakers to demand that the UAW to reopen labor contracts and agree to plant closures.</P>
<P>Workers who dare to fight for their own interests face relentless criticism. "Now is not the time to press for wages or grind industry to a halt," the <I>San Francisco Chronicle </I>lectured grocery store workers in the Bay Area. </P>
<P>Yet congressional Republicans are still trying to ram through their anti-union agenda. Here again, labor appealed to patriotism. "We are united in combating terrorism at home and around the world," a UAW newspaper ad said, adding that "fast track legislation&#133;threatens to divide our nation." But at the same time as Congress blocks unemployment relief for laid-off workers, it's getting ready to ram through a vote on "fast track" trade authority for Bush.</P>
<P>Dick Armey is right. The "American spirit" means, and always has meant, that workers make sacrifices during both war and peace--while the employers cash in either way.</P>
<P>Bush's war will kill enormous numbers of innocent Afghans. And U.S. soldiers who die on the battlefields of Asia won't be the only working-class people in this country who suffer.</P>
<P>The employers will use the war to attack our unions and push down the living standards of working people across the U.S. Organized labor should oppose this brutal war on a shattered and defenseless nation--and stand up for workers' interests against Corporate America's union-busting patriots.</P>

