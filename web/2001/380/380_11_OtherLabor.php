<p><a name="Local743"></a><font face="Times New Roman, Times, serif" size="4"><b>Teamsters Local 743</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Reformers at the second-largest Teamsters local in the city are giving James Hoffa supporters a run for their money in local elections--if there's any money left, that is.</P>
<P>For decades, Local 743 has been the poster child for old-guard corruption. In 1994, then-national president Ron Carey put the local under trusteeship after it was uncovered that local president Robert Simpson still had former president Donald Peters on the payroll.</P>
<P>Peters was local president for 40 years before the federal government forced him to resign. He had close ties to leading mob figures like Joey "The Clown" Lombardo and the late Allen Dorfman.</P>
<P>Today, reformers running in local elections on New Leadership Slate are asking what happened to the health and welfare fund. Some $16 million vanished under the presidency of Chester Glanton, who died last month. In June, hundreds of rank-and-filers rallied to call for the executive board's resignation.</P>
<P>Glanton was succeeded by Robert Walston, who's running for local president on the Hoffa Unity Slate and was chair of the health and welfare fund when the money disappeared. </P>
<P>The old-guard cronies are responsible for several rotten contracts, including a 1997 contract at University of Chicago Hospitals. Due to several voting irregularities, members voted a second time on the contract--overwhelmingly rejecting it. But Glanton and Co. instead recognized the first vote, and signed the sell-out contract.</P>
<P>Local 743, with some 15,000 members, represents clerical and support workers, many of and at Chicago's main hospitals. "Many members have lost interest in the union and we're trying to get them involved again," reform activist William Jenkins, who has worked at Rush-Presbyterian Hospital for 21 years, told <I>Socialist Worker.</I> </P>
<P>Local 743 has a diverse membership, so the NLS has translated their campaign materials into Polish and Spanish. The ballots are out and activists are phone-banking to get out the vote--most of them pulling double-duty as they campaign for national Teamster presidential candidate Tom Leedham. Local 743 member Christine Royster is running in national elections on Leedham's Rank and File Power Slate. </P>
<P>Reformers in Local 743 and across the country will work hard in the coming weeks to scrub the dirt out of the union. Local ballots are due by November 2.</P>
