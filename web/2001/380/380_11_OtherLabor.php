<P><font face="Times New Roman, Times, serif" size="4"><b>Minnesota state workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">MINNEAPOLIS--Some 29,000 state employees returned to work on October 15 after making gains in the largest strike in Minnesota's history.</P>
<P>Workers, represented by the AFSCME Council 6 and the Minnesota Association of Professional Employees (MAPE), struck for two weeks for better wages and to stop increases in health care costs.</P>
<P>Gov. Jesse Ventura tried to shame the unions into settling for an unfair deal by implying that they were unpatriotic for striking in the wake of the September 11 attacks. But the workers weren't buying it. "This doesn't have anything to do with that terrorism attack," said striker Lisa Maidl. "He's using that to make us look bad."</P>
<P>Ventura called in 1,000 National Guard members to do workers' jobs and threatened to hire thousands of replacement workers to scab. In response, the unions staged a series of solidarity rallies featuring New York City public employees.</P>
<P>Izzy Miranda, an emergency medical technician from AFSCME Local 2507 in New York, told hundreds of strikers in St. Paul that he was in Minnesota because he believed in the power of a union. "After all the working men and women in this country have been through helping New York City dig out from the rubble, it's important that we all stand behind you," he told the crowd.</P>
<P>The state originally offered AFSCME workers 3 percent wage increases each year over two years, and MAPE workers a single 4 percent increase over two years. Under the new contract, AFSCME workers will receive a 3.5 percent wage each year. MAPE workers will get 3 percent increases for each of two years. AFSCME members' health insurance co-payments will rise, but not as much as the state wanted.</P>
<P>AFSCME Council 6 Executive Director Pete Benner stated that the new contract acheived "many of the goals that lead to this strike&#133;I consider this a win--a clear win--for our members."</P>
<P>But it looks like state workers have more fights ahead. Ventura is already hinting that he may have to start laying off workers to pay for the increases.</P>
