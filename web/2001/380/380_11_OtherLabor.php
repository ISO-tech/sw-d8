<p><a name="Orange"></a><font face="Times New Roman, Times, serif" size="4"><b>Orange County sanitation workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Federico Moreno</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">ORANGE COUNTY, Calif.--Disappointed trash haulers ended their five-day countywide strike on October 6, accepting a contract they'd rejected a week earlier.</P>
<P>On October 1, about 800 workers--represented by Teamsters Local 396--walked out at four waste management companies against their union leaders' advise.</P>
<P>Workers' main concerns were wages and working conditions. "We don't get sick days," Jos&eacute; Gardu&ntilde;o told <I>Socialist Worker. </I>"And if we take paid holidays, we have to make them up."</P>
<P>The companies offered a five-year raise scale that topped at $14.15 an hour. Workers wanted a $1 raise per year, which would top at $16.05. "This is a dangerous and difficult job," said one striker. "We have to demand what we deserve."</P>
<P>But the companies would not budge on their offer, which is $1.20 below the county average, and threatened to permanently replace strikers.</P>
<P>In the end, fear of losing their jobs in a faltering economy and the lack of medical insurance forced workers to accept the contract. "We felt like our backs were up against a wall," said driver Miguel Angelo.</P>
<P>Jos&eacute; Beltran's wife suffers from cancer. "I need the insurance," said the father of two. "I have to take care of my wife."</P>

