<P><font face="Times New Roman, Times, serif" size="4">Thousands rally to protest Bush's war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"How many more people have to die"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"I DON'T want to see any more bloodshed." That's what Matthew Lasar, whose uncle, Abe Zelmanowitz, was killed in the September 11 attack on the World Trade Center, has to say to George W. Bush.</P>
<P>Thousands of protesters have been sending Bush the same message since he began bombing Afghanistan October 7.</P>
<P>On that day, more than 10,000 people rallied in New York City in a demonstration organized by a newly formed coalition representing more than 100 organizations. Chanting, "It's not justice, it's a lie&#133;How many more people have to die?" the crowd marched from Union Square to Times Square to send the message that our grief is not a cry for war. </P>
<P>Less than a week later, on October 13, activists in dozens of cities held an "International Day of Protest Against the Militarization of Space." In Chicago, some 100 people came out in the pouring rain to protest.</P>
<P>"We will not be silent while this superpower nation is abusing its power, causing more suffering, starvation, casualties and havoc in Afghanistan and elsewhere," said Rev. S. Michael Yasutake of the U.S.-Japan Committee for Racial Justice.</P>
<P>In New York, at least 800 protesters gathered that day to hear Rev. Al Sharpton, journalist Amy Goodman, Father Daniel Berrigan and others speak out against the war. In Oakland, Calif., 200 people marched against the war in a protest called by the East Bay Coalition Against the War and Lake Merritt Neighbors Organized for Peace. Another 450 protesters gathered in San Diego that day. </P>
<P>"Some of us have come to protest with a raised fist, while others came to dissent with lit candles, but everyone here today is united in opposition to this unjust war," said John Patel from the International Socialist Organization.</P>

