<P><font face="Times New Roman, Times, serif" size="5"><b>Giuliani plans for new budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK Mayor Rudolph Giuliani is back at work slashing social services.</P>
<P>Earlier this month, he announced plans for a 15 percent budget cut from almost every city agency. Despite the massive aid package from the federal government, Giuliani says the city won't have enough money to pay its workers.</P>
<P>But Giuliani is using the September 11 tragedy as a cover for carrying out the same old attacks. As the <I>New York Times</I> put it, "Mr. Giuliani appeared to be using the new moral context created by the World Trade Center attack to pursue the wish list of initiatives he has had for much of his second term."</P>
<P>First on the chopping block: teachers and workers at the Board of Education. Contract negotiations for teachers have been dragging on for months, making them especially vulnerable to pay cuts and a hiring freeze.</P>
<P>Meanwhile, much of the federal money will end up in the pockets of the rich--thanks to schemes like Sen. Hillary Clinton's (D-N.Y.) plan for tax-exempt bonds for developers.</P>
<P>The rhetoric of unity in the face of the September 11 attacks has gone out the window. Working people will have to resist these hypocritical attacks.</P>

