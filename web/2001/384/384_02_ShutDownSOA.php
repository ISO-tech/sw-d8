<P><font face="Times New Roman, Times, serif" size="4">Shut down the School for Assassins!</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>America's own terrorist camp</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH says that he wants to wipe out terrorism. If he were serious about it, he would start in his own backyard.</P>
<P>At the School of the Americas (SOA) in Fort Benning, Ga., the U.S. trains military personnel from Latin America in torture, assassination and other human rights abuses.</P>
<P>The list of the school's graduates is a who's who of human rights violators. For example, in the 1980s, SOA grad Col. Byron Lima Estrada ran D-2, the Guatemalan military intelligence agency that coordinated massacres in 448 Mayan Indian villages. Then there's Roberto D'Aubuisson, leader of El Salvador's death squads and one of the men responsible for the 1980 assassination of Archbishop Oscar Romero.</P>
<P>Last year, under growing pressure, Congress decided to "close" the SOA&#150;only to reopen it under a new name: the Western Hemisphere Institute for Security Cooperation. While the camp has a new name, it has the same old faculty, teaching the same old classes.</P>
<P>That's why activists from SOA Watch and other human rights groups will gather at the gates of Fort Benning on November 17 and 18&#150;as they have for the last 10 years. Recent demonstrations have drawn upwards of 10,000, many of whom "cross the line" onto SOA property in a mass showing of civil disobedience.</P>
<P>But with the U.S. waging a bloody war in Afghanistan, the nearby city of Colombus, Ga., is playing hardball. After initially giving written permission for a Saturday vigil in a park a mile-and-a-half from the base's main gate, city officials announced last week that demonstrators had to gather in a baseball stadium five miles away.</P>
<P>They said that they wouldn't allow a procession to the base and threatened SOA Watch leaders with legal action. "Regardless of the political situation, we have a right to voice our opposition to the terrorism training that's happening at the SOA," SOA Watch staff member Ally Styan told <I>Socialist Worker</I>. </P>
<P>Many activists feel that now is the perfect time to protest the SOA. "Listening to Bush's speeches, you have to call it flat-out hypocrisy to say, 'If you're not with us, you're with the terrorists,' while he's continuing to support the SOA," said Styan. "If he's going to talk about terror, then the first thing to do is to look at his own action and to realize that he's fully supporting terrorism."</P>
<P>Solidarity actions are also planned for several cities across the country and internationally, including Santiago, Chile. On November 17 and 18, tell Bush to shut down the original terrorist training camp.</P>

