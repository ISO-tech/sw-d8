<P><font face="Times New Roman, Times, serif" size="5"><b>The lies behind the U.S. war on Iraq</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Hidden Wars of Desert Storm,</B></I> directed by Gerard Ungerman and Audrey Brohy, narrated by John Hurt, distributed by Free-Will Productions.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><I><P>Hidden Wars of Desert Storm</I> captures the real horror and outrageous lies of the 1991 Gulf War, which rages to this day through periodic bombing raids and sanctions that have killed more than a million civilians.</P>
<P>As the Bush administration and U.S. media prepare the ground for a renewed assault on Iraq as phase two in its "war on terrorism," this 60-minute documentary video should be required viewing.</P>
<P>Gerard Ungerman, a former French infantry platoon leader, and his partner Audrey Brohy interview an impressive array of figures. Investigative reporter Jean Heller asks where the photographic proof of tank tracks in the sand and the other evidence of 250,000 Iraqi soldiers at the Kuwaiti border was that the U.S. used to justify its largest ground war since the Second World War. There was none.</P>
<P>Retired Desert Storm Commander General Norman Schwartzkopf chuckles at the "coincidence" of American forces amassed in the Gulf for war games in the summer of 1990 when Saddam Hussein's troops invaded Kuwait. And a young marine explains that for weeks before the invasion, they were being prepared for war on Iraq.</P>
<P>When ground troops finally arrived after weeks of the most intense bombing campaign in history, soldiers discovered a skeletal force in tatters&#150;not the well-trained fighting machine they had been told to expect. "The mosquitoes put up a bigger fight than they did," the marine explains.</P>
<P>The film provides damning evidence that the Reagan and first Bush administrations supplied Saddam Hussein with the weapons of mass destruction&#150;including biological and chemical arms&#150;that he used against his own population.</P>
<P>The words of Denis Halliday, who quit the UN oil-for-food program, powerfully condemn the U.S.'s genocidal practices against Iraqi civilians. The sudden cease-fire that left Hussein in power is exposed as a ploy to stop a popular mass movement from taking control of Iraqi oil.</P>
<I><P>Hidden Wars</I> pulls no punches. It shows graphic images of Iraqi infants with horrifying deformities as a result of the American depleted uranium-238 ammunition made from nuclear waste that still litters Iraq and will remain toxic for the next 4 to 5 billion years.</P>
<P>Uranium-238 is now acknowledged to cause the Gulf War syndrome that has affected more than 150,000 of the nearly 700,000 troops deployed in the region. More than 9,500 American soldiers are now dead as a result of poisoning by their own government's ammunition.</P>
<I><P>Hidden Wars</I> has earned awards and accolades around the world. The film's only real flaw is its contradictory ending calling for a solution from the very body that's been waging war and imposing crippling sanctions on Iraq for more than a decade&#150;the UN.</P>
<P>Since no major theaters have stepped forward to show this excellent film, folks should buy a copy to show on their campus or in their neighborhood.</P>
