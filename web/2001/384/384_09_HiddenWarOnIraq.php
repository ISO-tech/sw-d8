<P><font face="Times New Roman, Times, serif" size="5">More to this than meets the eye</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Filmmaker GERARD UNGERMAN spoke to <I>Socialist Worker</I> about <I>Hidden Wars.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

</I><P>AS A former French infantry platoon leader, why did you make this antiwar film?</P>
</B><P>ONE thing that motivated me was the fucking hypocrisy and lies. It was clear that going to the Gulf for freedom and democracy was pure bull. If nothing else, it was because of big oil interests.</P>
<P>Due to the pitiful condition of the people of Iraq, we felt that it was urgent to do something. And we thought it would have more impact to not just do a piece about what happened, but the reasons behind it&#133;and to expose a long pattern of war profiteering and embargo profiteering as well.</P>
<B><P>WHAT DO you want people to take away from this film?</P>
</B><P>IF NOTHING else, the nice thing for a large spectrum of people&#150;those who have doubts or even those who don't have any doubts&#150;[is] to know that there's more to this thing than meets the eye. Some people with big smiles and nice ties and with nice big words in their mouths like "peace," "freedom" and "justice" actually are lying and following other agendas. </P>
<B><P>HOW DOES your film relate to the current war?</P>
</B><P>THE MOST important thing to bring home to many people is that whatever one may think of the horror of what happened on September 11&#133;it didn't come as an attack from Mars. In however sick a way, it was seen as revenge by the people who did it. At least we can put in people's minds that you fought with people and they fought back with you.</P>

