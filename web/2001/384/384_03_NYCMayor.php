<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How a billionaire buffoon took NYC</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER EIGHT miserable years of Rudolph Giuliani, the question wasn't <I>whether </I>the next mayor of New York City would be a Democrat but which one. But on November 7, Republican billionaire Michael Bloomberg walked away with a victory in one of the nastiest and most expensive mayoral elections ever. </P>
<P>Bloomberg, a media mogul who has never held public office, trailed by double digits in opinion polls a few weeks before the vote. Yet the arrogance, incompetence and racism of Democratic candidate Mark Green handed the election to this buffoon.</P>
<P>Green&#150;who in a former life was a liberal activist trained by Ralph Nader&#150;was the early favorite to win the Democratic nomination. Bronx Borough President Fernando Ferrer jumped in the polls after winning the endorsement of civil rights leader Rev. Al Sharpton.</P>
<P>After the endorsement, racist fliers began appearing, depicting Ferrer as Sharpton's puppet. The Green campaign promised to investigate where the fliers came from, but never did&#150;and meanwhile continued to press a message designed to play on the fears of conservative white voters. Green eked out a primary victory over Ferrer, but Black and Latino leaders sat on their hands for the general election.</P>
<P>Bloomberg took full advantage of the opening. He appeared almost daily on Black radio&#150;while Green refused invitations for on-air interviews.</P>
<P>Giuliani's long-delayed endorsement of Bloomberg wouldn't have been much help before September 11. But transformed into a hero by the media, Giuliani was able to turn out voters.</P>
<P>Bloomberg spent $60 million of his $4 billion fortune on the race, prompting <I>Newsday </I>columnist Jimmy Breslin to write that Bloomberg "bought the office like a cup of coffee." But Bloomberg's money was only decisive because of Green's self-destruction.</P>
<P>That a Republican billionaire could get away with posing as an antiracist and friend of working New Yorkers is an indictment of Green's campaign&#150;which mirrored the conventional wisdom among mainstream Democrats that candidates have to suck up to big business and slap down "special interests."</P>
<P>Democrats won governor's races in New Jersey and Virginia on Election Day, prompting happy talk from party leaders. But the real lesson was in New York.</P>
<P>Green's miserable campaign shows why the Democrats aren't the "friends of working people" that they claim to be&#150;and why we need a real alternative to the two parties of big business.</P>

