<P><font face="Times New Roman, Times, serif" size="5"><b>New York City Labor Against War</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>MICHAEL LETWIN of New York City Labor Against War (NYCLAW)&#150;who is also a member of the Association of Legal Aid Attorneys/UAW 2325&#150;spoke to <I>Socialist Worker</I> about mobilizing union members to oppose the U.S. war drive.</b></P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="3"><b>WHY SHOULD organized labor oppose the war in Afghanistan?</P>
</B><P>WE'RE CONCERNED with how the war will affect people in Afghanistan&#150;civilians in particular&#150;who obviously had nothing to do with what happened to the World Trade Center.</P>
<P>And we're concerned with the impact of the war more broadly, both in terms of American foreign policy&#150;such as U.S. support for dictatorships abroad&#150;and in terms of generating further terrorism by attacking countries and civilians who have no responsibility for September 11. War endangers people in this country.</P>
<P>And then there's the domestic fallout&#150;attacks on civil liberties, racism, the backlash against immigrants. Politicians are using the war as a pretext to push through huge government handouts to multinationals and killing legislation that might do the opposite, such as protect Social Security or anything else that might benefit working people.</P>
<P>It's important for labor to speak out, but so far labor has been scared. There's the sense that there's no room to speak out, no room for debate or criticism of any sort&#150;except for maybe on the campuses, and even there not so much in some places.</P>
<P>What we're hoping to do is create room for labor leaders and rank-and-file people who want to be critical of the war. Although we have no illusions that we represent a major force, it is nonetheless notable that&#150;coming out of New York City in particular&#150;hundreds of union members, some of whom are union presidents, have signed on to our statement.</P>
<B><P>HOW DOES NYCLAW address rising unemployment since the September 11 attacks?</P>
</B><P>SEPTEMBER 11 really had a major impact, but it's also a pretext for companies or the government to do what they've wanted to do for many years in terms of pushing workers back.</P>
<P>What's important for us to do is to link up with those issues on a day-to-day level. When the firefighters two weeks ago were arrested by the cops, for example, and suddenly realized that Giuliani might not be in their interest, we took their side in that and tried to come to their assistance. And the same thing goes for people who lost jobs and the various battles that are coming up around those issues.</P>
<P>I think this is critical not only because these are the right things to do, but also because this is how people who may not be prepared to oppose the war yet will see that they need to fight for themselves.</P>
<P>This will mobilize opposition to the kinds of repression and other attempts to push workers back, but starting from a much more concrete position than perhaps the war itself.</P>
<B><P>AND THIS might politicize people in the process.</P>
</B><P>YES, THAT'S certainly something that happened during the Vietnam War. People organizing in labor certainly tried to make the links between what was going on with the war abroad and the war at home, so I don't think we're breaking new ground in that regard. But I think we're learning from that experience&#150;and many of us were a part of that movement then.</P>
<P>There's a potential for people in labor to come together against the war and to build a more activist, more rank-and-file based movement within labor generally around particular issues that the war has clearly exacerbated&#150;like unemployment and racism.</P>
<P>So the war will be one issue, but it will lead into other kinds of activism within the union.</P>
