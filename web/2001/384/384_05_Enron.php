<P><font face="Times New Roman, Times, serif" size="5"><b>Collapse of Enron's house of cards</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ONE OF Corporate America's highest fliers bit the dust this month. Natural gas giant Enron was bought by its much smaller rival Dynegy after a federal investigation into financial misdeeds sent the company's stock price plunging.</P>
<P>Over the 1990s, Enron became one of the 10 biggest companies in the U.S., with more than $100 billion in revenues last year. The key to its growth was exploiting government deregulation of the energy industry.</P>
<P>Enron, for example, was one of the corporations that made billions in profits by driving up electricity prices to take advantage of the power pinch in California&#150;brought on by a 1996 deregulation plan written by the utility companies.</P>
<P>To get away with this, Enron depended on its friends in Washington. Chairman Kenneth Lay was the single biggest contributor to the Bush campaign. When the Texas oil boys took over the White House, Lay got to call the shots on energy policy&#150;so the Bush gang's response to the California power crisis was to call for more deregulation.</P>
<P>Apparently, though, the company's success also depended on creative accounting. Earlier this month, Enron admitted that its profits for the last five years were vastly overstated&#150;because it had hidden billions of dollars in debt in supposedly independent partnerships controlled by Enron executives.</P>
<P>The partnerships kept Enron's reported bottom line looking good. Plus, executives pocketed hefty management fees for "running" the partnerships&#150;on top of their fat salaries and stock options from Enron.</P>
<P>The whole house of cards began to collapse over the past several months, and the company had to grovel to find a buyer that would assume as much as $23 billion in corporate debt.</P>
<P>But don't shed any tears for Enron executives. They became millionaires and billionaires when Enron was the toast of Wall Street. And they know there's almost no chance that they'll be punished.</P>

