<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's plan to eavesdrop on lawyers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ATTORNEY GENERAL John Ashcroft has decided that he can eavesdrop on confidential conversations between prisoners and their lawyers. A new federal rule&#150;published on October 31, a day after it went into effect&#150;says that the Justice Department can monitor conversations between "terrorist suspects" and lawyers if the attorney general concludes that there is "reasonable suspicion" to believe that further terrorist acts will be discussed.</P>
<P>Lawyers and legal experts alike were stunned by this latest attack on civil liberties. Robert Hirshon, president of the American Bar Association, said that "attorney-client privilege" dates back to the reign of Elizabeth I in Britain. "No privilege is more indelibly ensconced in the American legal system," he said.</P>
<P>Ashcroft claimed that he only wants to stop terrorism from being initiated from inside prison walls. So-called "privilege teams" are supposed to sift through the information and keep prosecutors from seeing anything they shouldn't. But does anyone trust John Ashcroft and the FBI to keep their eyes and ears closed at the right times?</P>
<P>Ashcroft has already exploited September 11 to win support for draconian "antiterrorism" legislation signed into law by George W. Bush last month. But he isn't satisfied.</P>
<P>These bullies will try to take away any rights that they can&#150;until we stand up and say no!</P>

