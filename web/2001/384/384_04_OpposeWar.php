<P><font face="Times New Roman, Times, serif" size="5"><b>Socialists have a duty to oppose this war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Last week, you printed a letter by a socialist who supports the war. He says that there is proof that Osama bin Laden was behind the September 11 attacks. If so, why wasn't this evidence turned over to the Taliban? They asked for evidence, but Washington sent B-52s instead.</P>
<P>He says that the U.S. "sat on its hands" after the embassy bombings in Africa. What does he call the U.S. destruction of a medicine factory in the Sudan that produced half the country's anti-malaria drugs?</P>
<P>He argues that bombing will make the world safer. Yet already, Israel has used it as an excuse to step up its war on the Palestinians. Bombing is making the world a much more dangerous place, for Americans and for everyone else.</P>
<P>Contrary to what he thinks, the U.S. isn't out to get bin Laden. "If Osama bin Laden was gone today, the war would continue tomorrow," said George W. Bush's press secretary Ari Fleischer.</P>
<P>The U.S. is trying to install a government in Afghanistan that will toe the U.S. line&#150;even if this government is headed by the warlords, terrorists and gangsters of the Northern Alliance. Meanwhile, the U.S. is raining terror on ordinary Afghans with an arsenal of bombs that bin Laden could only dream of.</P>
<P>The only way to prevent any more innocent people from dying is by building a movement against the war. If we do this, oppressed people all over the world will see that the U.S. government doesn't speak for ordinary Americans. </P>
<P>As socialists, we have to expose the lies of Washington's warlords and understand their real aims. We have a duty to oppose this war&#150;and every war waged by the U.S. Remember, the real enemy is at home.</P>
<B><P>Pham Binh,</B> Geneseo, N.Y.</P>

