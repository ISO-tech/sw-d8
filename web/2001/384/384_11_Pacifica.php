<P><font face="Times New Roman, Times, serif" size="5"><b>Pacifica</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kevin O'Neill</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PACIFICA Foundation board may be backing out of an expected agreement that was being hailed as a victory by listener groups and employees victimized by management.</P>
<P>Plaintiffs in lawsuits against the Pacifica board&#150;which runs the left-wing Pacifica Radio Network&#150;announced November 3 that they were on the verge of a settlement. But more than a week later, the board had yet to sign the deal.</P>
<P>Listeners groups and supporters of fired workers were continuing to build for demonstrations at the Pacifica board meeting in Washington, D.C., in mid-November.</P>
<P>Over the past several years, the board&#150;which is dominated by a mix of business representatives and Democratic Party heavyweights&#150;has been pressuring Pacifica member stations to adopt more corporate-friendly, politically "mainstream" programming. At New York City's WBAI, some two dozen producers were driven out over the past year alone as a result of the board's attack on station leftists.</P>
<P>But radio workers and station listeners organized a fight to put pressure on the board. The apparent settlement was hailed at a November 3 benefit for <I>Democracy Now!</I>, the network's flagship news program that was removed from the airwaves as part of the board's crackdown. </P>
<P>Now, the pro-corporate board has started cracking. On October 31, board member Ken Ford, a housing industry lobbyist, announced his resignation&#150;only a week after attacking the board's opponents in the media.</P>
<P>And the fight to save Pacifica has grown, despite the board's decision to spend hundreds of thousands of dollars to hire anti-union law firms, a security company and a PR firm to polish its image.</P>
<P>Busloads of people are planning to rally at the board's November 17-18 meetings. "It's very important to get as many people as possible out there," organizer Denis Moynihan told <I>Socialist Worker</I>.</P>
