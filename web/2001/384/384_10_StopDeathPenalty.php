<p><font face="Times New Roman, Times, serif" size="5"><b>Stop the death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Frank Edwards</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AUSTIN, Texas&#150;More than 300 people from across Texas and the country marched for a moratorium on executions. After a rally at the Capitol, demonstrators surrounded the governor's mansion with crime-scene tape.</P>
<P>The second annual March for a Moratorium was sponsored by several organizations, including the Campaign to End the Death Penalty, the Texas Moratorium Network, the International Socialist Organization, the National Organization for Women-Texas and many others.</P>
<P>Texas has overseen more executions than any other state, and its reputation for lynching is well-known around the world. But there's a growing opposition to Texas's death machine. </P>
<P>The last legislative session in Texas saw a bill that requires DNA testing in all capital cases, and two moratorium bills were tabled&#150;but even the consideration of such proposals in Texas is encouraging.</P>
<P>And the legislature passed a bill prohibiting the execution of people with mental retardation. But Gov. Rick Perry&#150;who took over after George W. Bush was "selected" president&#150;showed his devotion to Bush's legacy of death by vetoing the legislation.</P>
<P>Speakers at the rally included family members of death row prisoners and a former Texas death row inmate released when his innocence was later proven. The crowd of diverse protesters chanted "No justice, no peace! Moratorium now!" and speakers addressed the barbarism and racism of the death penalty.</P>
<P>A moratorium won't be easily won, but the March for a Moratorium represents a unified activist approach to achieving the unthinkable in a state whose name is synonymous with executions.</P>

