<P><font face="Times New Roman, Times, serif" size="4">Management ignores the anthrax threat</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>UPS puts profits first</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><p><B>Dear <I>Socialist Worker</I>,</P></B>
<P>Although most of the anthrax deaths and infections have occurred at U.S. Post Offices, any parcel company could be affected just as easily.</P>
<P>I work at UPS and drive in downtown Chicago four hours a day. After that, I sort packages for another four hours.</P>
<P>UPS management clamors about "safety"&#150;though it's one of the most unsafe companies in the country. They like to pretend that they care, so they can fulfill the Occupational Safety and Health Administration's rubber-teeth safety requirements.</P>
<P>In the meantime, the biggest scare in our industry is being ignored. We've yet to hear the word "anthrax" fall from the lips of our employer.</P>
<P>Most workers are upset by UPS management's silence. We're not alarmist, but almost every day, there have been more reports of anthrax contamination in the news.</P>
<P>That's why we've started a petition demanding that UPS have a policy dealing with anthrax. We're demanding that gloves, masks, proper medicine and spot-checking of the facility become procedure.</P>
<P>The petition has been an enormous success. Workers quickly sign their names. People signing the petition say that UPS sees us as expendable, just like the postal workers that have died.</P>
<P>A fellow UPS worker went to the hospital last week. He wasn't sure if he had the flu or possible anthrax contamination, since many hazardous materials break open at UPS on a daily basis. The doctor told him that he couldn't be tested for anthrax unless the company admitted that there was anthrax at the facility.</P>
<P>Knowing UPS, even if there was anthrax, they would try to cover it up as long as possible. Once again, we see our corporate terrorists&#150;oh, sorry, I mean <I>leaders</I>&#150;putting profits before people.</P>
<B><P>Donny Schraffenberger,</B> steward, Teamsters Local 705, Chicago</P>

