<P><font face="Times New Roman, Times, serif" size="5"><b>Old guard steals vote at Teamsters Local 743</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Joe Allen, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO&#150;"They didn't win the election&#150;they stole it," said Richard Berg, the reform candidate for president of Teamsters Local 743, describing the results of the local election. The corrupt, old-guard slate led by Bob Walston, the current local president, won by 239 votes out of some 3,000 cast.</P>
<P>Many irregularities plagued voting. Among them was the fact that the current secretary-treasurer had a key to the postal box that contained the returned ballots, in violation of Local 743 election rules! Berg believes that hundreds of ballots were stolen and destroyed.</P>
<P>Teamsters Local 743 was, at one time, one of the largest locals in the Teamsters. It has more than 20,000 members, most of them concentrated in the health care industry, retail and manufacturing.</P>
<P>It has lost 7,000 members in the last six years. At the same time, the local's health and welfare fund and credit union have gone bankrupt.</P>
<P>Berg and other members of the reform New Leadership Slate have filed a formal protest calling for the election results to be overturned.</P>
<P>While the old guard was charged with stealing the election in Local 743, their pals in Local 710, lead by Frank Wsol, suffered an important defeat.</P>
<P>Wsol tried to deny ballots to 3,500 members of his local to vote in the International Teamsters election contest between reformer Tom Leedham and current General President James P. Hoffa. These 3,500 members, who formerly belonged to the independent Chicago Truck Drivers Union (CTDU), merged last February with the Teamsters.</P>
<P>Richard DeVries, the Chicago-area Leedham campaign coordinator, recently filed a protest on behalf of the excluded Local 710 members, and the election officer ruled in his favor. Ballots for the ex-CDTU Local 710 members were mailed out a week and a half ago.</P>
<P>In the past two International elections, in 1996 and 1998, Local 710 voted by a 2-1 margin for Hoffa. Local 710's officers are clearly worried that the new members' vote could change that pattern in this election.</P>
