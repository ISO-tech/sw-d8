<font face="Times New Roman, Times, serif" size="4">Media scrambles to give Florida recount a pro-Bush spin</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Commander-in-thief</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER MONTHS of research and delays, the results of an investigation into the disputed Florida vote from last year's presidential election finally emerged this month. But the media made it as hard as possible to figure out what happened.</P>
<P>The headlines of almost every newspaper declared George W. Bush "still the winner" over Al Gore. But a different verdict showed up a few paragraphs into the stories. "The results plainly suggest that more Floridians intended to cast votes for [Gore] than Bush, and that under most standards for counting ballots by hand, [Gore] would have won in a statewide recount," the <I>Washington Post</I> wrote.</P>
<P>In other words, Bush should have lost to Gore in Florida&#150;if you care about who people "intended to cast votes for." In fact, Gore beat Bush in six of nine recount scenarios tested by independent researchers hired for the investigation of disputed Florida ballots by a consortium of major newspapers and media outlets.</P>
<P>Despite the corporate media's scramble to spin the results Bush's way, the report adds to a mountain of evidence showing that Dubya is only in the White House because of outright fraud, the disenfranchisement of Black voters and election manipulation by state officials answering to Florida Gov. Jeb Bush.</P>
<P>Though it's rarely mentioned anymore, Bush lost the nationwide popular vote by 540,000. But because of the undemocratic Electoral College&#150;an 18th-century institution designed to protect the influence of slave states&#150;Bush became president by eking out a 537-vote victory in Florida. Meanwhile, the Supreme Court decision that handed Bush the White House openly declared that the U.S. Constitution doesn't give Americans the right to choose their president.</P>
<P>Throughout the Florida fiasco last year, Bush and his handlers used every trick in the book to stop a recount&#150;mobilizing a mob of Republican congressional staffers to intimidate local Florida election officials and spending millions on lawyers to make sure that the result went their way.</P>
<P>Yet since Bush took office, the Washington establishment&#150;Republican and Democrat alike&#150;has been content to "forgive and forget."</P>
<P>The consortium report was due out in mid-September, but media executives held up the release. With every major institution in U.S. society preaching the need to "stand behind our president" after September 11, the media apparently didn't want to remind Americans that Bush wasn't really "our" president.</P>
<P>The Washington elite remains united in wanting to "move on." Which shows that the so-called defenders of democracy in the U.S. "war against terrorism" don't care that much about democracy.</P>
<P>Last year, they were willing to subvert it to put Bush in the White House. This year, they've shredded dozens of civil liberties in the name of "security."</P>
<P>They pull out the talk about "democracy" and "freedom" when they want to win support for war. But don't expect them to practice what they preach.</P>

