<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's war on Arab immigrants</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P></B>
<P>Attorney General John Ashcroft and the FBI are waging a war right here at home&#150;against Arab immigrants. So far, they've detained more than 1,182 mostly Arab immigrants in connection with the September 11 attacks.</P>
<P>Both the <I>Washington Post</I> and the <I>Los Angeles Times</I> have reported that the detainees have limited access to lawyers and no contact with their families. There have been multiple reports of physical mistreatment of detainees.</P>
<P>Ashcroft and the FBI aren't even telling us who they've detained or where they're being held, let alone what "crimes" they've been accused of committing. These people are being held hostage indefinitely.</P>
<P>On November 1, I attended a demonstration with 80 other New Yorkers outside of the Immigration and Naturalization Service headquarters in downtown Manhattan to send the message that people in this country are not going to stand for this abuse.</P>
<P>The protest was called by New Yorkers Say No to War, and our demands included full disclosure of who is being detained, a legal process for all detainees and an investigation of all allegations of abuse.</P>
<P>As one of the protest organizers told the crowd, "There's targeted racial profiling going on right now. We're calling this kidnapping&#150;that's what it is. They're kidnapping people from their homes and workplaces, without any evidence."</P>
<P>What kind of system gives one man the power to judge the innocence or guilt&#150;in some cases literally the life or death&#150;of anyone who happens to fit his idea of a terrorist profile?</P>
<P>"I hope this demonstration is the first step of a broader movement to disclose the names, to release the detainees without charges and to stop the rounding up of people based on skin color, religion and last name," said one protester.</P>
<P>I couldn't agree more.</P>
<B><P>Sarah Hines, </B>New York City</P>

