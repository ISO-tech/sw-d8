<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the U.S. won't wage a "just" war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH White House doesn't even try to hide it anymore. "If Osama bin Laden was gone today, the war would continue tomorrow," press secretary Ari Fleischer declared. But at the liberal <I>Nation</I> magazine, the editors are still recommending a "just war."</P>
<P>"We believe that America has a right to act in self-defense, including military action," reads a recent <I>Nation</I> editorial. "But acknowledging a right of response is by no means an endorsement of unlimited force. We must act effectively, but within a framework of moral and legal restraint." </P>
<P>Many liberals&#150;and even some radicals with reputations as committed opponents of imperialism&#150;have echoed this argument since the U.S. war on Afghanistan began.</P>
<P>Now it is true that the right wing is on a rampage. Pundits like Andrew Sullivan label even hesitation about the war as treasonous. But can right-wing tirades justify veteran activist and author Ellen Willis's apparent call for a U.S. ground invasion in a recent letter to the <I>New York Times</I>?</P>
<P>"The issue [in Afghanistan]," Willis wrote, "is how best to combat the Taliban and Al Qaeda with the least possible damage to Afghan civilians&#133;If we are unwilling to commit American troops to this battle, we should declare defeat and go home."</P>
<P>Certainly Attorney General John Ashcroft is riding a wave of support in pushing through his Big Brother antiterrorism legislation. But is this any excuse for lawyer Alan Dershowitz&#150;though not a radical, a longtime advocate of civil liberties&#150;to tell a <I>Nation</I> interviewer, "I've changed my mind" on roving wiretaps, facial recognition technology and even a national identity card?</P>
<P>It's hard to take seriously any talk about a war fought with "moral and legal restraint" when it becomes more and more obvious each day that the Pentagon's air war knows no bounds.</P>
<P>At the end of October, the U.S. began dropping "daisy cutters"&#150;a single 15,000-pound bomb with nearly as much destructive power as a nuclear weapon. "As you would expect, they make a heck of a bang when they go off, and the intent is to kill people," said Gen. Peter Pace, vice chair of the Joint Chiefs of Staff. It doesn't take a military genius to realize that "daisy cutters" have little to do with targeting Osama bin Laden&#150;and everything to do with terrorizing the Afghan population.</P>
<P>With atrocities like this taking place, talking about how a "just war" would be waged is like debating the number of angels on the head of a pin.</P>
<P>Likewise, the frightening new powers of repression that are now the law of the land can be only justified if you trust right-wing crazies like John Ashcroft&#150;an anti-abortion fanatic, Confederate flag lover and opponent of school desegregation&#150;not to abuse them. Why should we trust him&#150;any more than we should trust the Pentagon not to use mass terror, as it has so many times in the past?</P>
<P>You can only accept the idea of a just war if you believe that the U.S. government can act in the interest of justice. Magazines like the <I>Nation</I> ultimately rest on this belief&#150;in a kinder, gentler capitalism, with the hard edges of repression, injustice and super-exploitation taken off.</P>
<P>The socialist view starts from an understanding that society is run by a small minority whose main goal is to preserve and extend their own wealth and power. It follows that their actions will be shaped by their goals.</P>
<P>This is why U.S. military power can't be used in the cause of justice. The U.S. government and the corporate masters it serves depend on perpetuating injustice in order to expand their power.</P>
<P>The hope for stopping the slaughter in Afghanistan and confronting the fanatics in Washington lies not in coming up with utopian schemes for how the U.S. might fight a just war. It lies in fighting to stop the war&#150;as part of a movement that aims to put an end to injustice once and for all.</P>

