<P><font face="Times New Roman, Times, serif" size="5"><b>What they drop on Afghanistan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PENTAGON is dropping some of the most deadly weapons of mass destruction ever known on Afghanistan.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="3"><STRONG>BLU-82</STRONG><BR>
<STRONG>Daisy cutter</STRONG></P>
<P>THE PENTAGON'S "daisy cutters" are the most destructive nonnuclear weapons in the world. One bomb is the size of a small car and weighs 15,000 pounds. </P>
<P>Daisy cutters are dropped from specially fitted transport planes and release several chemicals that are detonated just over the ground. The resulting firestorm obliterates anything within a half-mile area. The shock wave and vacuum pressure created by the blast destroy the internal organs of anyone within range.</P>
<P>"The sound split the air," says a reporter for a British newspaper, describing a daisy cutter explosion. "It was like a thunder clap directly overhead at the height of a ferocious storm. "I could see the massive oil black cloud of the explosion as it rolled across the hillside, a mixture of thick smoke, chunks of earth and debris."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="3"><STRONG>CBU-87<BR>
Cluster bomb</STRONG></P>
<P>CLUSTER BOMBS each contain 202 bomblets that are designed to scatter over an area the size of several football fields when the bomb is released.</P>
<P>On impact, the bomblets explode into 300 fragments of jagged shrapnel that can "defeat light armor and personnel." In other words, tear any human being within 100 feet to pieces.</P>
<P>At least one in 20 bomblets doesn't detonate on impact. They remain left behind as a kind of super-land mine&#150;to explode on impact when touched.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="3"><STRONG>GBU-29<BR>
Bunker buster</STRONG></P>
<P>THE PENTAGON is using so-called "bunker buster" bombs to destroy caves where Osama bin Laden is supposedly hiding. The 5,000-pound bombs can burrow through as much as 20 feet of rock before exploding underground.</P>
<P>Witnesses say the blast is like an underground nuclear test&#150;and "a significant emotional event for anyone within a square mile," said one U.S. officer.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="3"><STRONG>Food packages</STRONG></P>
<P>THE U.S. had dropped about 1 million food packages in Afghanistan by early November. Each package contains one day's worth of food for one person&#150;at a cost of $3.95.</P>
<P>An estimated 1.5 million people are suffering from malnutrition now. So the food drops haven't even met the needs of everyone at risk for just one day out of the month.</P>

