<P><font face="Times New Roman, Times, serif" size="5"><b>West Coast longshore workers face new attack</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>JACK HEYMAN, an Executive Board member of International Longshore and Warehouse Union (ILWU) Local 10 of San Francisco and chair of the Northern California Committee to Defend the Charleston Longshore Workers, discusses the challenges facing the ILWU.</P>
</B><P>IN THE upcoming contract negotiations, the ILWU will be facing an all-out attack by the Pacific Maritime Association (PMA).</P>
<P>In a statement before a congressional committee earlier this year, PMA head Joe Miniace openly declared that employers want to take away our hiring hall and replace it with an automatic telephone dispatch system that will be used to supplement a small, steady group of longshore workers for each company.</P>
<P>He said this would eliminate the current practice of gathering workers in a central location within the harbor area. The PMA is also demanding a free hand in using electronic technology. These proposals would render longshore jurisdiction meaningless and allow the elimination of jobs.</P>
<P>Of course, Miniace is trying to sell this scheme to our union officials under the guise of a spirit of cooperation. He praises the 1960 Mechanization and Modernization Agreement, which gave huge profits to the PMA in a losing exchange of jobs for money. We can't afford to make that mistake again.</P>
<P>It is instructive that Miniace uses the Port of Houston, organized by the International Longshoremen's Association, as a model for his "steady workforce" scheme. What he forgot to mention is that 80 percent of longshore work in Houston is nonunion!</P>
<P>We need to protect the hiring hall, which was won in the 1934 strike with the blood of labor martyrs. It remains the basis of our strength today by providing the union with some control over our jobs. Without the hiring hall or the right to strike coast-wide, we have very little power.</P>
<P>Over the years we experienced fractures in the integrity of the structure of our dispatch hall system, whose key principle is the equal opportunity of work for all longshore workers. Now Miniace wants to further undermine the hiring hall to divide us during difficult contract negotiations.</P>
<P>To be sure, electronic technology and computerization will continue to develop and expand in the global economy. Just as ILWU favors fair world trade over protectionism, we must favor technological advancement over stagnation.</P>
<P>However, under capitalism workers get the short end of the stick. Labor-saving devices mean unemployment for workers. If the trade union movement is to survive and grow, we need to gear our contract demands to the new technological gains.</P>
<P>The American trade union movement was built 130 years ago on the struggle for the eight-hour day. Now we must fight for a shorter workday with no loss in pay. We could have four shifts of six hours of work instead of the present three shifts to provide more work for new members.</P>
<P>Miniace and the PMA are trying to portray us to the press as a bunch of lazy, overpaid workers with our feet stuck in the mud as far as modern technology is concerned. By showing that we want all workers to share the benefits of technology, not just the rich, we gain the support of a working class hard-hit with unemployment.<br>
<br>

<P><font face="Times New Roman, Times, serif" size="4"><b>Why the ILWU should oppose the war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FIRST and most direct victims of the war in Afghanistan are not Osama bin Laden or the Taliban, but innocent Afghan civilians who are dying from U.S. and British bombing raids. Secondly, the U.S. labor movement is being targeted under the pretext of heightened national security.</P>
<P>The so-called Port and Maritime Security Act of 2001 introduced by Sen. Hollings of South Carolina would, if passed, militarize the docks. It would impose background checks on longshore workers.</P>
<P>It would undermine our hiring hall and our union solidarity by denying the right of employment to those who have been in prison or are deemed national security risks (like ILWU founder and longtime president Harry Bridges).</P>
<P>ILWU members who voice political opposition to the war in Afghanistan could be de-registered and effectively barred from working. The right to strike will be dumped overboard, leaving unions with virtually no negotiating power.</P>
<P>When anti-labor forces tried to pass this kind of bill during the war against drugs, it fizzled. But now, in the war hysteria, with Democrats and Republicans singing the same marching tune, maritime employers see a golden opportunity. Miniace's strategy has always been to seek government intervention in union affairs, using the courts against the ILWU whenever possible.</P>
<P>The ILWU should oppose the war in Afghanistan and defend the working people of that country against imperialist attacks, just as we defend workers' rights here at home. The ILWU could be next.</P>
<P>Now more than ever workers need their own party, a workers' party to represent working-class interests. For now, unions must rely on labor solidarity to ensure social justice.</P>

