<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Red Cross exploits tragedy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>YOU COULD expect Corporate America to cash in on the September 11 tragedy. But so have so-called nonprofit charity organizations.</P>
<P>In less than two months since the disaster, leading charities like the Red Cross and the United Way have collected $1.3 billion. This outpouring of support, in which as many as 70 percent of Americans contributed, aimed to provide aid to families of the September 11 attack victims. The response says a lot about the generosity of ordinary people&#150;in contrast to the greedy airline bosses who used September 11 to line their own pockets.</P>
<P>Yet seven weeks after the disaster, only about one-sixth of the money collected has been distributed. But a bigger scandal has developed.</P>
<P>The American Red Cross has come in for well-deserved criticism for pocketing much of the money it collected for the victims. Specifically, the Red Cross announced plans to divert nearly half of the $550 million it collected to fund its own internal priorities. It plans to spend $109 million on telecommunications and databases and $55 million on "community outreach" and "administrative costs."</P>
<P>No wonder the Red Cross rejected New York Attorney General Elliot Spitzer's proposal to administer all funds collected through a single centralized database. It had other plans for the money.</P>
<P>By the end of October, the Red Cross had delivered only $40 million to victims' family members. "The question is one of false advertising and whether people were led to believe something that wasn't true," Stacy Palmer, editor of the <I>Chronicle of Philanthropy,</I> told the <I>Los Angeles Times</I>. "This is no good," said the stepmother of a paramedic killed at the World Trade Center. "It's really shameful. People should be ashamed of themselves." </P>
<P>After the scandal came to light, Red Cross President Bernadine Healy resigned under pressure.</P>
<P>The September 11 charity debacle raises some fundamental questions beyond the issue of Red Cross corruption. These questions are especially important to ask when political leaders have boosted charity and volunteerism as the answer to social needs.</P>
<P>First, we have to ask about the resources these organizations can command. The extent of the September 11 fundraising campaign was larger than anything seen before.</P>
<P>Of course, contributing to September 11 relief funds was hardly a "controversial" cause, as AIDS fundraising in the 1980s was. Yet this unprecedented fund drive is a drop in the ocean if you measure it against real social needs.</P>
<P>In their most ambitious program yet, Habitat for Humanity will build fewer than 1,000 houses by the end of this year. This will hardly make a dent in homelessness, even if you accept the Census Bureau's absurdly low estimation of 170,000 homeless.</P>
<P>Before September 11, food charities reported difficulty keeping up with demand. And if major charities are having trouble figuring out how to distribute help to a fairly limited number of people, why should we expect them to be able to take over huge programs, as Bush's "faith-based initiative" does?</P>
<P>Democrats and Republicans persist in the delusion that private charity will provide social needs because it fulfills their ideological agenda. For more than two decades, they have used the rhetoric of "personal responsibility" to abandon millions of poor people even as they spend billions on corporate welfare.</P>
<P>They insist that "we're all in this together" in the "war on terrorism," but their message to working people is "you're on your own."</P>
<P>Both parties collaborated in the destruction of the welfare system five years ago. Today, with recession spreading and millions losing their jobs, the bill for that human-made disaster is coming due.</P>
<P>For that reason, we need a movement demanding that the government provide jobs, housing and health care more than ever.</P>

