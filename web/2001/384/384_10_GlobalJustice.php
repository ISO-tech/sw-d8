<p><font face="Times New Roman, Times, serif" size="5"><b>Fight for global justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ACROSS THE country, hundreds turned out this past weekend to protest the November 9-13 World Trade Organization (WTO) meeting in Doha, Qatar.</P>
<P>In the U.S., global justice events were held coast-to-coast as part of a November 9 day of action against the Qatar meeting. Protests also took place in at least 36 other countries.</P>
<P>WTO officials chose the small desert island of Qatar&#150;where civil liberties and the right to protest are essentially nonexistent&#150;to discourage protests.</P>
<P>But more than 400 demonstrated in San Francisco, where speakers addressed economic inequality and environmental racism&#150;and linked the drive for profit to the war in Afghanistan. "We have to look at the role of oil," global justice activist Starhawk told the crowd. "Oil is a big factor in the U.S.-led war in Afghanistan."</P>
<P>In New York City, about 200 people marched a "Walk of Corporate Shame" to learn about how corporate globalization has meant putting profits over human needs, attacking working people throughout the world and destroying natural resources.</P>
<P>At the headquarters of Bristol-Myers Squibb, for example, protesters chanted "Pills cost pennies, greed costs lives," and criticized how drug companies have blocked the production of cheap AIDS drugs that could save millions of lives in Africa. </P>
<P>David Hylend, one of the event's organizers, said the march "was a good way to say that [the global justice] movement had not disappeared" and that, in light of the September 11 attacks, it was "significant that it happened at all, particularly in New York."</P>
<P>In Washington, D.C., 150 protested at the office of the U.S. trade representative at a march sponsored by the Mobilization for Global Justice. UNITE union members, Global Exchange and others spoke about the fight for workers' rights and civil liberties while a 70-foot dragon puppet caught the attention of many passers-by.</P>
<P>In Chicago, about 100 marched, and two were arrested for trying to hang a banner on a downtown bridge. In Madison, Wis., a rally of 20 to 30 was followed by a teach-in that drew roughly 80 people.</P>
<I><P>Sofia Alvarez and Mike Estrada contributed to this report.</P>
