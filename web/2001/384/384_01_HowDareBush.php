<P><font face="Times New Roman, Times, serif" size="4">Victim of the U.S. war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How dare Bush talk of justice</b></font></P><p><font face="Arial, Helvetica, sans-serif" size="2">November 16, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THIS NEW enemy seeks to destroy our freedom and impose its views," George W. Bush declared in a speech last week in Atlanta. "We value life."</P>
<P>But not if you live in Afghanistan&#150;where the U.S. government is using some of the most hideous weapons of mass destruction ever known in its so-called "war against terrorism." Weapons like the 15,000-pound BLU-82, which gives new meaning to the word "terror." The world's most destructive nonnuclear bomb, it obliterates everything in a half-mile area.</P>
<P>And for every Afghan killed by a U.S. bomb, hundreds and perhaps thousands more will die a slow death from starvation&#150;part of a massive humanitarian crisis that relief agencies say is only weeks away.</P>
<P>Then there's the random vengeance of the U.S. government's cutthroat allies as they took control of the Afghan capital of Kabul in mid-November. Britain's <I>Guardian</I> newspaper reported that the warlords of the Northern Alliance had massacred hundreds of people after capturing the city of Mazar-i-Sharif days earlier.</P>
<P>Soldiers trapped some 1,200 pro-Taliban Pakistani fighters in a school on the outskirts of the city&#150;and shot 200 of them. Other Northern Alliance soldiers seized 10 trucks carrying relief supplies, said Chulho Hyung of UNICEF.</P>
<P>As <I>Socialist Worker</I> went to press, Taliban troops were fleeing Kabul as Alliance forces moved into the city. That was the cue for U.S. warplanes from the USS Theodore Roosevelt to target Taliban vehicles and tanks as they retreated south.</P>
<P>All the while, Washington's war makers talked about "honoring" those who died September 11&#150;and bringing "democracy" to Afghanistan. Yet it's plain to see that the U.S. government is backing one set of warlords against another&#150;and using September 11 to justify the killing, no matter what the cost in human lives.</P>
<P>The White House even tried to win more support for its billion-dollar-a-day war drive when an American Airlines plane crashed in New York November 12&#150;though federal officials reported that the crash appeared to be the result of an equipment malfunction, not terrorism.</P>
<P>Leave it to the Bush gang to exploit a tragedy to fuel its war for "justice."</P>
<P>We have to stand up and speak out for what's right. Stop this war now!</P>

