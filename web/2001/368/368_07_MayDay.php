<P><font face="Times New Roman, Times, serif" size="5"><b>May Day protests around globe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 7</font></p>

<P><font face="Times New Roman, Times, serif" size="3">MAY DAY protests from France to Indonesia and Turkey to Tawian highlighted workers' fights for job security and the growing opposition to corporate globalization. Some of the most dramatic protests came in Seoul, where South Korean workers stood up to police attacks on their march to defend thousands of workers laid off from Daewoo auto plants.</P>
<P>The Western media focused mainly on supposedly "violent" protests in Berlin and London. But it was London cops who surrounded protesters for hours, preventing anyone from even leaving the march--while Berlin police tried to ban one protest. Cops in Australia used similar tactics against mostly young protesters.</P>
<P>In France, tens of thousands of workers marching in 70 cities highlighted layoffs at a Danone food factory, the Marks &amp; Spencer department store chain and the AON airline. "This May 1 is particularly important because of the string of layoffs organized by companies whose profits are rising," said Marcel Karbasse, a member of the CGT union federation.</P>
<P>Turkey saw more than 20,000 march against austerity measures imposed by the government as part of an International Monetary Fund bailout plan (IMF). The IMF was also a target of May Day protests in Indonesia.</P>
<P>In Taiwan, about 20,000 workers fighting for better conditions marched on the presidential palace--until they were turned away with barbed wire and thousands of riot cops. And in Zimbabwe, about 200 pro-government, antilabor thugs were kicked out of a May Day rally that drew 7,000 unionists and supporters.</P>

