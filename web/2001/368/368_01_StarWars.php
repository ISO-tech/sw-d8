<P><font face="Times New Roman, Times, serif" size="4">Bush's gang triggers new arms race</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Star Wars madness!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. Bush had a message for the rest of the world. The U.S. is in charge, and you better do what we say.</P>
<P>He didn't exactly put it that way. Instead, he said, "We need a framework that allows us to build missile defenses to counter the different threats of today's world." But that was the real meaning of Bush's May 1 speech at National Defense University, where he unveiled his administration's scheme for "national missile defense"--a new name for the same "Star Wars" fantasy that Ronald Reagan pushed in the 1980s.</P>
<P>In Reagan's dreams, the U.S. was going to develop a system of missiles and satellites that could shoot down nuclear missiles launched at the U.S. Of course, scientific experts --then and now--say this can't work. And every Pentagon test of missile defense prototypes--even ones rigged to work--have failed.</P>
<P>But Bush and his top military advisers don't care. They're willing to spend $120 billion over the next 25 years on a program that won't work.</P>
<P>Why? For one thing, military contractors like Boeing, Raytheon and TRW stand to make out like bandits.</P>
<P>In announcing his intention to go ahead with the scheme, Bush deliberately stiff-armed every other major power in the world--from China to Russia to the U.S.'s European allies. The Chinese government, in particular, opposes Dubya's missile madness because it thinks--rightly--that, as the rising power in Asia,  China is the main target.</P>
<P>It's no secret that hawks like Defense Secretary Donald Rumsfeld are itching for a fight with China. Rumsfeld's May 3 order ending military cooperation with the country--taken back only an hour after he issued it--tells what the ex-Cold Warriors inside the Bush administration really think. "The [missile defense] scheme...will touch off a nasty arms race in Asia," Lee Jong Seok of the Sejong Institute in Korea told Business Week. </P>
<P>In other words, far from making the world safer--as Bush claims--his Star Wars madness will make the world more dangerous. In fact, the U.S. wants a missile shield to make itself invulnerable to attack--which would make it more likely to consider using nuclear weapons.</P>
<P>For a half century, the threat of "mutually assured destruction" (MAD) kept superpowers like the U.S. and Russia from starting a nuclear war. Today, Bush and Rumsfeld are listening to "nuclear use theorists" (NUT) who think it's possible to fight a limited nuclear war. That's why military analyst William Hartung says that, under Bush, U.S. nuclear policy has gone from "MAD to NUTs."</P>
<P>What a nightmare! We have expose Bush's missile madness--and organize to stop him.</P>

