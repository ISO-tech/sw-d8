<P><font face="Times New Roman, Times, serif" size="4">Students demand living wage for workers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Harvard should be proud of them"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Geoff Bailey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 14</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CAMBRIDGE, Mass.--More than 30 Harvard University students entered the third week of a sit-in in May in their fight get the country's richest university to pay a living wage to all its workers. The Harvard Living Wage Campaign, a coalition of student groups and campus unions, has been demanding that Harvard pay all employees at least $10.25 an hour-which is barely enough to scrape by with the Boston area's high prices. "An institution with $19 billion in the bank" can afford that, said one sit-in organizer.</P>
<P>Currently, more than 400 workers employed by Harvard make less than a living wage. And another 1,600 campus workers work for subcontractors that pay only the minimum wage. Dan O'Connell, a Harvard custodian who makes $11.75 an hour, says his paycheck "pays the bills, that's about it. But you can't get anywhere on it."</P>
<P>The coalition has received support from all over the area-including the Cambridge City Council, whose members led a march last month from the city hall to the gates of Harvard. Actors Matt Damon and Ben Affleck have given their backing, as has historian and author Howard Zinn. </P>
<P>And the AFL-CIO has gotten involved. Federation President John Sweeney spoke to an April 30 rally of 500 students and union workers.</P>
<P>But so far, the administration has refused to negotiate, cynically claiming that to give workers a raise would violate union contracts signed last year. Administrators say they plan to wait out the students, who will begin final exams next week.</P>
<P>This has left activists looking for ways to step up the pressure. More than 1,000 people attended rallies in the final week of April, and union workers on campus have begun playing a more visible role.</P>
<P>Janitors have even begun discussing plans for civil disobedience of their own to force the administration to the negotiating table. These actions need to be expanded if the sit-in is to succeed before school ends.</P>
<P>But Danny Meagher, a part-time museum guard, said that the students have already made a difference. "These students have chosen to stand up and take what I consider to be an incredible moral stand on behalf of people whose backgrounds are different from theirs," Meagher said. "I think that's a fine thing. To me they're a reflection of what the university claims to be about. Harvard should be proud of them."</P>



