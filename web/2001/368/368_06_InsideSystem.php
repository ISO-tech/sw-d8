<P><p><font face="Times New Roman, Times, serif" size="4"><b>Human wrong righted</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. finally lost its seat on the United Nations (UN) Human Rights Commission.</P>
<P>Since the UN was founded in 1947, the U.S. has held a seat on the body charged with probing human rights abuses around the world. The U.S. came in fourth behind France, Austria and Sweden in balloting for three seats allocated to Western nations that were up for re-election. </P>
<P>Republicans scampered to attack the UN--and deflect blame from President Bush's delay in appointing a U.S. ambassador to the UN. "This is emblematic of the increasing irrelevancy of some international organizations," said Rep. Henry Hyde (R-Ill.). Rep. Christopher Smith (R-N.J.) explained the vote as revenge for U.S. attempts to pass a resolution critical of China at this year's meeting of the commission.</P>
<P>But resentment against the U.S. in the UN has been growing for many reasons. First, there's the $1.7 billion in back dues that the U.S. has owed the UN for years. The Bush administration's opposition to the Kyoto climate treaty and plans for a missile defense shield also contributed to the vote against the U.S., according to several Western diplomats.</P>
<P>Joanna Weschler, the UN representative of the New York-based Human Rights Watch, said both Western and developing countries bore grudges against the United States. "In recent years, the United States often failed to support important human right initiatives," she said. "These include a measure calling for AIDS drugs to be made available to all, the treaty to ban land mines and the International Criminal Court."</P>
<P>--Reuters, May 4, 2001</P>

