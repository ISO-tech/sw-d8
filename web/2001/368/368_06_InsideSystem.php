<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Bush the bauble</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">A NEW digital craze is sweeping the world's most populous country. Released in April to little interest in the U.S., Eruptor Entertainment's PortaBush software toy hit big in China. Players feed their PortaBush, keeping him happy, and help him to make vital national decisions like whether to bomb teen singer Britney Spears.</P>
<P>The software's designers based in California noticed a spike in traffic coming from China to their Web site, at one point reaching about 2,000 downloads an hour and threatening to overwhelm their servers. "About 80 percent of the downloads were coming from China," said Eruptor Entertainment President Brad Foxhoven. "It's just incredible that it's taken off like this," he said.</P>
<P>The interest in ridiculing Bush might have something to do with the U.S. spy plane that collided with a Chinese jet, killing the pilot. The White House had no immediate comment.</P>
<P>But Bush can at least be relieved that he's in good company. The company has created similar virtual pets based on a fish, a monkey, a kitten and former Baywatch star Pamela Sue Anderson.</P>
<P>--Associated Press, April 14, 2001</P>

