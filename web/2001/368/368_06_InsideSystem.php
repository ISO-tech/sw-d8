<p><a name="Bugs"></a><font face="Times New Roman, Times, serif" size="4"><b>Bugs the bigot</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ALL THE Bugs Bunny cartoons will air this June on the Cartoon Network as part of a retrospective on the longstanding series--well, almost all of them. Network executives decided not to air a dozen Bugs Bunny that were simply "too racially charged."</P>
<P>In one episode, Bugs is featured parodying a black-faced Al Jolson. In another short, he calls an oafish, bucktoothed Eskimo a "big baboon." And in yet another, he distracts a Black rabbit hunter by rattling a pair of dice. </P>
<P>Executives decided against airing the shorts, citing concern that the episodes might hurt their merchandising ventures. Warner Bros. began pulling the cartoons lampooning Blacks in the 1960s under pressure from the movement for civil rights, according to animation expert Jerry Beck.</P>
<P>Cartoons featuring stereotyped American Indians were taken out of circulation about five years ago. </P>
<P>--Associated Press, May 4, 2001</P>

