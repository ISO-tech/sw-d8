<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Media cheers Bush's 100 days</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dubya and the do-nothing Democrats</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">PRESIDENT BUSH took office in January as the first president in 112 years to win the presidency while losing the popular vote. His 537-vote margin of victory in Florida depended on right-wing justices of the U.S. Supreme Court stopping a hand recount--not to mention the systematic disenfranchisement of Black voters.</P>
<P>For the first time since 1973, thousands of people protested a presidential inauguration when Bush took the oath of office January 20. But when the media marked the Bush's "first 100 days" in office earlier this month, all that was forgotten. Commentators rushed to proclaim Bush's White House a stirring success that had won over doubters among the public.</P>
<P>Yet simply to summarize the administration's agenda is to list a series of attacks on working people. The reversal of a series of regulations ordered by Bill Clinton--from standards on arsenic levels in drinking water to workplace safety rules--got the most attention. But Bush is out to turn back the clock on federal policy on every front. Tax cuts for the rich, the repeal of abortion rights, privatization of Social Security and Medicare, the building of a reckless "missile defense" system--are all Bush cornerstones.</P>
<P>It's not like these policies are popular. An ABC News/<I>Washington Post</I> poll taken in late April found that less than half of the public thinks Bush understands their problems. By a 2-to-1 margin, they think he supports corporations over ordinary people. And ordinary Americans consistently say that they'd rather the government spend money on needed social programs than hand out a tax cut to the rich.</P>
<P>Yet Bush is continuing with his agenda--and getting away with it. There's one reason for this: the pathetic response of the Democratic "opposition."</P>
<P>Senate Democrats last month congratulated themselves for slicing $300 billion off Bush's tax cut plan. But they still gave Dubya what he wanted--a huge giveaway to the rich that will prevent lawmakers from spending the government's budget surplus on anything that will help ordinary people. What's more, neither unions nor liberal organizations have done much to build an opposition to Bush.</P>
<P>The sentiment to fight back is there.  On the third weekend of April, tens of thousands demonstrated against Bush's "free trade" agenda in Quebec City and his plans to roll back abortion rights in Washington, D.C. Yet the turnout at these events could have been larger had labor and the National Organization for Women really put muscle behind their rhetoric. Still, in demonstrations across the country, you can see thousands of people who hate what Bush stands for--and want to do something about it.</P>
<P>We can turn the tide against Bush. But it will require organizing in workplaces, schools and communities.</P>
<P>Press releases from the Democrats and liberal groups won't stop Bush. It's up to our side to mobilize the kind of force that puts Dubya on the defensive, where he belongs--and pressures all the politicians to deal with the issues, like health care and education, that matter to ordinary people.</P>

