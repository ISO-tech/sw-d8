<p><font face="Times New Roman, Times, serif" size="4"><b>Hawaii teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">HONOLULU--On April 25, 13,000 teachers throughout Hawaii settled a 20-day strike that shut down the public school system, from kindergarten to college. Teachers and professors had worked the previous two years without a contract.</P>
<P>The settlement came after teachers were guaranteed a 4 percent pay hike during the 2001-2002 school year and a 6 percent raise the following year. Additionally, teachers will receive a one-time payment of $1,100 for each of the two years that they worked without a contract. Teachers with master's degrees would receive an additional 3 percent raise, and those with doctorates would get an additional 6 percent. Professors ended their strike two days before the teachers when they agreed to 12 percent salary raises over the next two years. </P>
<P>Hawaii's teachers rank 18th among the 50 states when it comes to pay. This is much too low to keep up with the cost of living in Hawaii, which is 20 to 30 percent higher than in the mainland U.S.</P>
<P>The wage issue helped to generate an enormous amount of community support for strikers. According to news reports, teachers received cash donations, bottled water, umbrellas and lots of food on the picket line.</P>
<P>That support put pressure on Gov. Ben Cayetano to seriously negotiate. A few days into the strike, Cayetano ordered the state to stop payment on its share of premiums for the teachers' health plan.</P>
<P>But the solidarity of the teachers--99 percent of teachers and 91 percent of professors went out and stayed out--forced the state to concede. Even Cayetano had to admit, "I was impressed with the resolve of our teachers."</P>

