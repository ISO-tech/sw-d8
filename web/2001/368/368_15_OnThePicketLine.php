<p><a name="Vermont"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Vermont faculty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Helen Scott, member, UAV</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Faculty at the University of Vermont (UVM) voted in favor of forming a union in mid-April. The faculty will join United Academics of Vermont (UAV), which is affiliated with the American Federation of Teachers and the American Association of University Professors.</P>
<P>The election was close, with 53 percent casting a ballot for collective bargaining. The victory followed an intensive yearlong campaign--and before that, two previous union drives. Support for a union grew out of recent unpopular moves by the administration, including attacks on health care benefits and a faculty "buyout" that left many departments understaffed--and remaining faculty overworked.</P>
<P>"I became interested in a union when I realized that the compensation package and working conditions under which I was hired weren't guaranteed," said faculty member Nancy Welch. "When I realized that the administration could unilaterally cut our health care benefits, I began to pay attention to how little voice faculty have and how much the university was acting like a corporation."</P>
<P>UVM service and maintenance workers are already represented by the United Electrical Workers (UE). Andrew Tripp of the UE said faculty unionization was a "huge step forward" because "what's bargained by one group gets passed on to everyone else."</P>
<P>Two other union drives are likely in the near future--among part-time faculty, who are excluded from the UAV bargaining unit, and among clerical and technical workers. UAV members realize that they still have a fight on their hands. The closeness of the votes means, as Welch put it, "We need to keep convincing people that a union is in our best interests."</P>

