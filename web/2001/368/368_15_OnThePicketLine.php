<p><a name="UW"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Wisconsin</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike McHugh, AFSCME Local 171</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.-- Nearly 100 workers and students rallied on Cinco de Mayo to support 24 Latino workers who were fired by the University of Wisconsin (UW) in April. The UW administration has admitted that the employees were singled out because their names "sounded Latino." The workers were sent letters demanding that they provide documentation of their legal immigration status.</P>
<P>When union leaders went to the administration to ask for an extension on the deadline, the workers were fired the next day. The firings happened just days after hundreds of state workers and UW students packed the State Capitol to protest Gov. Scott McCallum's proposed budget.</P>
<P>The plan calls for increasing tuition while offering state workers a measly 1 percent "raise."</P>
<P>At the rally, Mark Thomas, president of AFSCME Local 171, which represents the fired workers, accused UW of "racial profiling." "It's the public support we've received that is forcing the university to deal with this at all," Thomas said. "We're gonna have to keep pressure on."</P>
<P>"As long as workers live in fear of deportation, unscrupulous employers will abuse their rights," South Central Federation of Labor President Jim Cavanagh said.</P>

