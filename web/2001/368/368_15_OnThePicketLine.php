<p><a name="LA"></a><font face="Times New Roman, Times, serif" size="4"><b>Screenwriters</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--A possible strike by movie and television screenwriters was narrowly averted by a last-minute agreement May 4. Negotiators for the Writers Guild of America, which represents some 11,000 members, locked horns with the Alliance of Motion Picture and Television Producers in contract talks that lasted several weeks.</P>
<P>The deal will bring in $41 million more for writers over the next three years. This falls short of the $100 million the union wanted, but the Guild won other important gains. Screenwriters won increased payment for foreign sales and forced the Fox network to pay the same rate as other major networks.</P>
<P>The union failed to win two critical demands: residuals for reruns on cable of network shows like <I>ER </I>and a revision of the payment formula used for work sold on videocassette and DVD. Only the threat of a strike forced producers to negotiate at all. In fact, industry bosses were so afraid of a strike that they stockpiled shows and filled program schedules with writer-free, reality-based shows like <I>Survivor.</P>
</I><P>Guild members will vote on ratification of the contract June 4. And Tinseltown is sure to face more fights, with the contract for 135,000 movie and television actors set to expire July 1.</P>

