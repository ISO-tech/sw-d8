<P><font face="Times New Roman, Times, serif" size="5"><b>"We'll pull together to fight the coup at WBAI"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 15</font></p>

<P><font face="Times New Roman, Times, serif" size="3">MEDIA WORKERS and listeners have been fighting management's attack on progressive programming and union rights at Pacifica radio network stations. The network's latest attacks have fallen most heavily at WBAI, the Pacifica station in New York City. Veteran civil rights and AIDS activist BOB LEDERER is a producer of health programming at WBAI--and a member of the community group Concerned Friends of WBAI. ANTHONY MACKALL is a listener who works with the Campaign to Stop Corporate Takeover of Pacifica. They talked to <I>Socialist Worker's</I> PETER LAMPHERE about their fight.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<P><b>WHAT'S BEHIND Pacifica management's attack?</b></P>
<P><b>Lederer</b> When Pacifica began airing the commentaries of Mumia Abu-Jamal in 1994, the pressure escalated from the Corporation for Public Broadcasting in Washington to purge leftist programmers and tighten the reins of control. Pacifica moved in 1999 against its Berkeley station, KPFA. It fired the manager and any dissenting staff and later shut down the station completely for more than two weeks.</P>
<P>The staff and community rebelled, and after a five-month struggle, they forced Pacifica to back down. Limited autonomy was restored, and most--but not all--staff rehired. Finally, in 2000, it was WBAI's turn--as Pacifica's last bastion of radical programming that hadn't been attacked.</P>

<P><b>Mackall</b> We feel this is a national as well as a local issue. In Washington, D.C., programming was significantly watered down. The Houston station used to have 11 languages on air, and now it only broadcasts in English.</P>

<P><b>Lederer</b> At WBAI, Amy Goodman and Juan Gonzalez are award-winning, radical journalists who for five years used their national daily program on Pacifica, "Democracy Now!" to provide in-depth discussions with progressive activists. Goodman has faced constant pressure to stop covering or water down regular topics like Mumia Abu-Jamal and East Timor. As a result, last October, she and her union, AFTRA, filed charges of harassment, gender harassment and censorship against management. It was this climate--including a ban on discussing station policy on-air--that spurred co-host Juan Gonzalez to resign on-air on January 30.</P>

<P><b>WHAT'S AT stake in this fight?</b></P>
<P><b>Lederer</b> For 50 years--through the Cold War, the war against Vietnam, the civil rights and Black Power movements, the movement against the Gulf War, right through to the current movements against police brutality, the death penalty and corporate globalization--WBAI and Pacifica have been precious resources, playing a critical role in keeping radical information flowing.</P>
<P>Pacifica management has consistently violated existing union rules in harassing, defaming and firing both paid and unpaid staff in violation of all due process. More than 300 staff members have been summarily fired in the past decade. The current chair of the Pacifica Board's Governance Committee, John Murdock, is even part of a law firm which brags about its expertise in assisting employers in "maintaining a union-free workplace." </P>

<P><b>Mackall</b> We're trying to mobilize on two levels. We're encouraging people to put pressure on the board of directors by picketing, demonstrations, passing out leaflets, etc. But also, with regards to Pacifica's fund drive [that begins in mid-May], we're trying to get people to realize that the board has been wasting money and not spending it on progressive programming. </P>
<P>Since the coup at WBAI last December, there've been numerous pickets, marches and teach-ins, as well as an act of civil disobedience at the station. Every organization and individual who ever appeared on the airwaves of Pacifica or WBAI must pull together to fight this theft-in-progress.</P>

<I><P>For more information, contact Concerned Friends of WBAI at 800-825-0055 or the Campaign to Stop the Corporate Takeover of Pacifica at 646-230-9588.</P>



