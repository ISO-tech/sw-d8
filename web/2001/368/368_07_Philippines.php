<P><font face="Arial, Helvetica, sans-serif" size="2"><b>PHILIPPINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What's behind the rebellion in the Philippines?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eduardo Capulong</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE PHILIPPINES capital of Manila remained in a "state of rebellion" as <I>Socialist Worker</I> went to press following an April 30 siege on the presidential palace by 20,000 protesters loyal to former President Joseph "Erap" Estrada.</P>
<P>The siege laid bare the deep divide between the country's poor and the newly installed government of Gloria Macapagal Arroyo--and also among the various factions of the Philippines' ruling elite. The palace siege--which lasted six hours and left six people dead, hundreds injured and downtown Manila in a shambles--grew out of a weeklong mass rally sparked by Estrada's arrest on corruption charges April 25. Estrada was overthrown last January by a popular uprising--dubbed "People Power II" after the 1986 revolution that ended the dictatorship of Ferdinand Marcos--after evidence presented at his Senate impeachment trial showed his control of a vast criminal empire.</P>
<P>A former movie actor famous for his portrayal of Robin Hood-like characters, Estrada ran on a populist campaign in 1998. Though Estrada himself is wealthy, he won a plurality of the vote in an election that revealed the deep class anger against trapos--or traditional politicians--many of whom are back in power under the Arroyo administration.</P>
<P>Leaders of the April 30 siege include former coup plotters who repeatedly tried to overthrow the first "People Power" administration of Corazon Aquino in the late 1980s. Among them are Sen. Juan Ponce Enrile, former defense minister under Marcos; Sen. Gregorio Honasan, a former army colonel; and Ernesto Maceda, Estrada spokesperson and a former ambassador to the U.S. </P>
<P>At a rally that reportedly reached a peak of 300,000 people, these thugs exploited protesters' complaints of poverty and political exclusion. But Arroyo, an affluent daughter of a former president and college classmate of Bill Clinton, is an easy target.</P>
<P>She has put together a new government filled with trapos, corporate heads and military men. It's similar in many ways to the administration of former President Aquino--herself a part of one of the country's largest landowning families--who came to power with the overthrow of Marcos but turned her back on workers and the poor.</P>
<P>Estrada's backers are appealing to the one-third of the Philippines' 76 million people who live on less than a dollar a day. Sixty percent define themselves as poor, and as many as 40 percent are unemployed or underemployed.</P>
<P>The crisis is far from over. Despite a nationwide manhunt, many leaders of the rebellion remain in hiding. Their supporters are campaigning in regional and local elections this month.</P>
<P>While it's necessary to defend the Arroyo government from a right-wing coup, the class and mass character of the rebellion are--unlike the coup attempts against Aquino--evidence of the anger and frustration of Manila's poor. Various left-wing organizations, such as Buklurang Manggagawa ng Pilipinas (Philippine Workers Federation), have pointed out that Estrada and his allies are exploiting popular grievances for their own purposes. These groups have called for the Arroyo government "to drop the charges against the ordinary people who marched" to the presidential palace.</P>
<P>The key to the future lies with building an independent working-class alternative to the trapos that can marginalize the right-wing populists and challenge a corrupt system.</P>

