<P><font face="Times New Roman, Times, serif" size="4">Killed in skies over Peru</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victims of the war on drugs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 8</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LAST MONTH, three CIA pilots flying a U.S. Air Force spy plane over Peru began tracking a twin-engine Cessna aircraft. Minutes later, Veronica Bowers and her 7-month-old baby Charity were dead.</P>
<P>After briefly trailing the Cessna, which was carrying a family of American Baptist missionaries, CIA pilots--who regularly track aircraft as part of the so-called "war on drugs"--radioed Peru's Air Force, requesting that a jet intercept the plane. The Peruvian jet arrived on the scene, got the order to fire--and blasted the Cessna with 50 bullets. One of the shells tore a hole through Bowers' heart and ended up in her child's head. </P>
<P>The CIA's pilots never told the Peruvian Air Force to back off--even though they now claim to have doubted that the Cessna was smuggling drugs. When the Peruvian jet got the authority to fire, the lead CIA pilot simply muttered, "Jeez!" A White House spokesperson claimed that the pilots mounted "vigorous" objections to the shootdown--only to add, "Different people can define vigorous in their own way."</P>
<P>These two tragic deaths have exposed a little-known aspect of the U.S. government's escalating military intervention in the Andes region of South America. In 1994, the Peruvian Air Force began shooting down civilian aircraft suspected of being used for drug trafficking. This was a clear violation of U.S. and international law, leading some members of Congress to balk at approving CIA assistance for the notoriously corrupt and brutal Peruvian military. </P>
<P>But Democratic Sen. John Kerry (D-Mass.) came to the rescue. As the <I>Washington Post</I> reported, "Within weeks, Congress overwhelmingly passed a measure sponsored by Kerry to provide immunity for 'employees and agents of the United States and foreign countries engaged in interdiction of aircraft used in illicit drug trafficking.'" </P>
<P>To cover their tracks, the CIA ran its spy plane operation out of a front company called Aviation Development Corp., a 45-employee business based at Maxwell Air Force Base in Montgomery, Ala. This joint Air Force-CIA operation helped Peru to shoot down 30 civilian aircraft in the past six years, killing an unknown number of people.</P>
<P>Meanwhile, Bill Clinton and Congressional leaders agreed last year to dramatically escalate U.S. military intervention in the region--by approving "Plan Colombia," which authorized $1.3 billion in military aid for Colombia, Peru and Ecuador, supposedly to fight the "war on drugs." In reality, U.S. military supplies are helping the Colombian government continue its bloody war against left-wing guerrillas--and upgrade its security forces, which regularly target union, student and peasant activists.</P>
<P>Nearly 30,000 civilians have died in Colombia's dirty war--nearly all of them victims of the Colombian military or right-wing death squads. Now, far from "making the region safe for democracy," the U.S. military is helping to spread Colombia's war to neighboring countries. </P>
<P>According to Adam Isacson of the Center for International Policy, "Last year, the only money in Plan Colombia for Peru was about $32 million for helicopters. Since [former Peruvian President Alberto Fujimori] had just stolen an election, it wasn't really politically palatable or possible to give it to them. But they're going to get more next year. A lot of people are speculating that most of the aid for Colombia's military is being concentrated in an area right along Colombia's border with Ecuador and Peru... That spillover is a big worry about Plan Colombia--that's probably one reason why they're proposing $90 million in military aid for Peru now."</P>
<P>Mass demonstrations and strikes toppled Fujimori last year--yet another motivation for a stepped-up U.S. military presence in Peru. The U.S. government is worried that Peru could go the way of Ecuador, where mass strikes and semi-insurrections have ousted five presidents in as many years--and stopped the imposition of International Monetary Fund-backed austerity measures.</P>
<P>So after a few weeks, you can expect the flights from Montgomery, Ala., to resume. Veronica Bowers and her daughter weren't the first victims of the U.S. war against South America's poor. And they won't be the last.</P>



