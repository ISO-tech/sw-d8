<P><font face="Times New Roman, Times, serif" size="5"><b>May protests across the U.S.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 14</font></p>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Some 1,500 celebrated May Day by marching down Wilshire Boulevard to demand amnesty for undocumented workers. People carried signs that said, "We are workers without borders."</P>
<P>The demonstration was organized by the Multi-ethnic Immigrant Workers Organizing Network, a coalition of Latino, Filipino and Korean immigrant groups. One speaker who works in a toy factory described the sweatshop conditions and overcrowded housing that immigrants endure.</P>
<P>Mike Garcia of the Service Employees International Union, noting the one-year anniversary of LA's janitors' strike, said, "We set fire to strikes by other immigrant worker janitors across the country." But there's still more to do. "The majority of janitors have to work in the darkness of their undocumented status," Garcia added. "We will lead the nation in the fight for legalization."</P>

<P><b>Boston</b></P>
<P>A 700-person-strong May Day march in Boston brought together local unions and immigrant rights groups in a historic coalition. The march was endorsed by the Massachusetts AFL-CIO and was addressed by AFL-CIO President John Sweeney. "Undocumented workers should not live in fear. We are united. The only thing that is just is a general amnesty," Sweeney said.</P>
<P>"We fought our revolution for all the people," Massachusetts AFL-CIO Bobby Haynes told the crowd in front of a memorial to the American Revolution. "But we have had a lousy record of discriminating against Blacks, immigrants, women. It is time for amnesty in this country. It is time for justice!"</P>
<P>The demonstration will kick off a statewide campaign to demand passage of a general amnesty bill in the state legislature.</P>

<P><b>New York City</b></P>
<P>More than a thousand protesters marched through Manhattan's garment district to the International Monetary Fund's (IMF) offices on May 1 to demand amnesty and equal rights for immigrant workers. Marchers first targeted two green grocers where store owners are trying to prevent their largely immigrant workforces from gaining union recognition.</P>
<P>UNITE Local 169 President Jerry Dominguez, dressed as Mexican wrestler and progressive figure "Super Barrio," took on the green grocers in a street theater skit.</P>
<P>The police then used an arcane city law prohibiting the wearing of masks in public demonstrations to attack the peaceful march, clearly targeting "anarchist types" by their dress. Four were arrested.</P>
<P>The multiracial march, called by the Organizing Committee for Workers Rights, then fed into the Rally for Dignity and Amnesty at the doorstep of the IMF, which has fostered austerity programs in immigrants' countries of origin.</P>
<P>The demand for amnesty is crucial for the U.S. labor movement, which for too long allowed corporate bosses and politicians to pit foreign-born workers against native-born. The march's spirit of international solidarity is what's needed to broaden the fight against globalization and the FTAA.</P>

<P><b>Chicago</b></P>
<P>More than 500 protesters marched through downtown Chicago May 1 to expose corporate greed and call for an alternative to the system.</P>
<P>The protest, endorsed by a range of activist, socialist and anarchist groups, began at the Chicago Board of Trade and included stops at Gap and Disney stores, where dozens of activists risked arrest by blocking the entrances. The march ended at the site of the old Cook County Jail where the Haymarket Martyrs were hanged in 1887.</P>
<P>Some protesters then traveled a few miles away to a later demonstration for immigrant rights. Endorsed by local labor unions, the evening protest attracted several hundred.</P>
<P>The action merged with another protest against the U.S. Navy's bombing of the Puerto Rican island of Vieques. Rep. Luis Guti&eacute;rrez (D-Ill.) of Chicago, who was arrested and imprisoned in a dog kennel on Vieques, addressed the crowd.</P>

<P><b>San Diego</b></P>
<P>A May Day concert at the University of California-San Diego (UCSD) by the popular radical band Ozomatli turned into a march for workers rights. The concert, sponsored by the Raza Awareness Week Committee, provided a platform for students to tell the crowd about an upcoming contract for campus janitors.</P>
<P>Activists in MEChA and Students for Economic Justice set up hundreds of tiny crosses on the lawn in front of the stage to represent those who have died trying to cross the border. After the concert, more than 200 janitors and students marched to UCSD's Human Resources building, where they taped a giant petition on the door to protest the firing of a union organizer.</P>
<P>Custodian Loretta Phillips said she was "surprised by the awareness among students of what's happening." "It's called numbers," said custodian John Palmer. "We will win if we have unity and work together."</P>

<P><b>Other cities</b></P>
<P>In Portland, Ore., more than 1,000 people attended this year's May Day, triple the size of last year's. Police attacked a demonstration of about 200 people in Long Beach, Calif. Eyewitnesses say the protest was nonviolent, yet police shot tear gas and rubber bullets and savagely beat some demonstrators.</P>
<P>Many of 95 arrestees--a quarter of them juveniles--were released on $500 bail. But as of late May 2, some half a dozen people were still being held on bails ranging from $10,000 to $35,000. One protester, who is an immigrant, is in danger of deportation.</P>
<P>Some 300 people marched on the Rhode Island Statehouse in Providence in support of general amnesty for undocumented immigrants. Local activists are mobilizing for a national march for general amnesty in Washington, D.C., on September 18.</P>
<P> </P>
<P><i>Evan Kornfeld, Chuck Stemke, Rebecca Anshell, Michael Ware, Geoff Bailey, Paul Dean and Brian Chidester contributed to this report.</i></P>

