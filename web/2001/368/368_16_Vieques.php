<P><font face="Times New Roman, Times, serif" size="4">Start of a new wave of the struggle</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. Navy out of Vieques!</b></font><br>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 16</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>ROBERTO BARRETO reports on the fight to kick out the U.S. Navy.</b></P>

<P>SAN JUAN, Puerto Rico--Thousands of Puerto Ricans gathered in front of the capitol building in San Juan April 27 to inaugurate a new wave of struggle against the U.S. Navy's bombing practice on the island of Vieques. From the capitol, a car caravan carrying more than 500 people willing to illegally trespass onto the bombing range went to Fajardo, where a ferry waited to take them to Vieques.</P>
<P>Five hundred U.S. marshals had been imported from the continental U.S. to defend the range.</P>
<P>But that didn't stop hundreds of demonstrators gathered in front of Camp Garcia from tearing down the fence-in the face of the heavily armed marshals. Protesters cut hundreds of feet of fence and built barricades-as groups entered the range, forcing the Navy to stop bombing several times. </P>
<P>"That fence represents all the sick children of Vieques," said one woman, whose grandson suffers from a condition caused by noise from the bombing.</P>
<P>The marshals used rubber bullets and tear gas to defend the fence. Many demonstrators were wounded by the bullets-including the Catholic priest of Vieques-but entered the range anyway. Groups of people with T-shirts covering their faces threw rocks at the marshals.</P>
<P>The Navy also used a neuro-toxic gas that attacks the extremities and causes shaking and a burning sensation. So much gas was used that a child living two blocks away from Camp Garcia had to be taken to the hospital with a severe attack of asthma. And an infant traveling in a car lost consciousness and stopped breathing before being resuscitated.</P>
<P>Some groups of demonstrators reached the impact zone, but the Navy ignored reports that there were activists hidden on the range and kept bombing. Demonstrators included Puerto Rico Sen. Norma Burgos as well as the Vieques Mayor D&aacute;maso Serrano. Rub&eacute;n Berrios, leader of the Puerto Rican Independence Party, was also hidden on the range.</P>
<P>About 160 demonstrators were arrested-and received brutal treatment from federal authorities. Some-like U.S. Rep. Luis Guti&eacute;rrez (D-Ill.) of Chicago-were put into dog kennels.</P>
<P>Marshals put their boots over Guti&eacute;rrez's neck while his face was on the ground, forcing dirt into his mouth. He was later dragged on the ground.</P>
<P>Guti&eacute;rrez was only one of many public figures to participate in the actions. "When a law is unfair, we need to break it," Guti&eacute;rrez said. "That's why we are here." Rev. Al Sharpton and environmental lawyer Robert F. Kennedy Jr. also participated. "We have come to put our bodies on the line of fire," Sharpton said. </P>
<P>A week before the Navy practice bombing was scheduled to start, the Puerto Rican legislature approved a law regulating maximum noise levels-in an attempt to stop the bombardments. Many people of Vieques suffer from a vibro-acoustic disorder, that causes damage to heart valves, thickening of the arteries, lung tumors and bio-genetic damage. Vieques also has the highest cancer rate in all of Puerto Rico.</P>
<P>Since David Sanes, a security guard, was killed in practice bombing during the Kosovo war in 1999, more than 700 people have been arrested in protests to stop the Navy. This time, politicians from all of Puerto Rico's political parties-feeling the pressure from below-decided to participate. Although the Navy tried to deny it, the demonstrations were successful in canceling most of the practices. One Navy boat even ran aground after being tricked by fishermen.</P>
<P>The Navy has announced new practice bombing for July. But after days of battles, people in Puerto Rico are more convinced than ever that resistance can defeat the Navy. "We will always find a way," said one masked student. "This is our land, and we can step on it when ever we see fit."</P>



