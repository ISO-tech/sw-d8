<P><p><font face="Times New Roman, Times, serif" size="4"><b>Protests expose fraud of Bush's  "compassion"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B></I>,</P>
<P>Now is an exciting moment in the fight for immigrant rights. People have realized that activism and protest are what bring about real change. That was learned after Congress passed Section 245i late last year. </P>
<P>It granted amnesty to thousands of immigrants--in a matter of days instead of years. But the bill expired April 30, and many people were left out.</P>
<P>But protests have begun. On the night that Section 245i expired, the federal building in downtown LA was still turning people away at 1 a.m., but some people didn't leave quietly. </P>
<P>There is pressure to extend the bill--and even President Bush got involved, asking Congress to extend the time period. It's not because Bush is "compassionate" towards immigrants--in fact, he has opposed Section 245i all along. Bush is responding to the growing fight for immigrant rights.</P>
<P>Last year, the AFL-CIO along with many other organizations held a rally of 20,000 people calling for general amnesty. Since then, there have been demonstrations across the country--from LA to Chicago to outside the White House--in favor of Section 245i.</P>
<P>Last week, Illinois Congressman Luis Guiti&eacute;rez introduced another bill that would grant amnesty to more immigrants. But we can't depend on politicians to get amnesty for us. A workers' organization and the power of protest is what will move us forward!</P>
<B><P>Tony Herrera</B>, Los Angeles</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Challenging David Horowitz and his pack of racist lies</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B></I>,</P>
<P>The 1960s ex-radical David Horowitz visited the University of Washington on May Day. He addressed a crowd of mostly older white couples and young men on his belief that descendants of slaves should not receive reparations for their ancestors' suffering.</P>
<P>While protesting with the International Socialist Organization outside, I saw a group of about 20 mostly African American students with signs enter and decided to follow them. We were informed that we would have to sit in the chairs provided and not speak--or the police would remove us. After listening to him ramble about his background as a Marxist radical with ties to the Black Panthers and Ramparts magazine, he began trashing everyone to the left of Trent Lott. </P>
<P>Throughout his talk, the students I followed in taunted him as the rest of the crowd applauded. Throughout his speech, he used half-truths and veiled racism to stir up his audience. </P>
<P>His very point that those "damn liberals" play the race card was undermined by his constant use of the phrase "reverse racism." And he once hinted that the students must be anti-Semitic for no reason I could understand.</P>
<P>His whole speech was just a cunning form of race baiting. He played off the original audience against his detractors--all for the purpose, in my opinion, of distracting both sides from more tangible issues of class, wealth and power.</P>
<B><P>Robert Ehl</B>, Seattle</P>



