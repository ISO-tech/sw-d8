<P><font face="Times New Roman, Times, serif" size="4">Writing opera about the fight for liberation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Verdi's ''music of the cannon''</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>THIS YEAR marks the 100th anniversary of the death of Giuseppe Verdi, composer of some of the world's greatest operas. NICOLE COLSON reviews Verdi's life, showing that the spirit of resistance to tyranny filled his work.</b></P>

<P>FOR EVEN the most die-hard music lover, going to the opera can be tedious. Paying through the nose for an evening of being sniffed at by blue-haired society matrons and Armani-clad yuppies isn't a good time. But for 19th-century Italians, the opera was an arena of political and cultural life for men and women of various classes.</P>
<P>And at the heart of this world for more than 50 years was Giuseppe Verdi. Verdi was born in 1813 in Parma. </P>
<P>At the time, "Italy" didn't exist as a country. It was a collection of small statelets, governed by various foreign armies. Throughout much of the 19th century, the movement known as the "Risorgimento" ("Reawakening") gave voice to the desire for a unified Italy, free from foreign rule.</P>
<P>A radical opponent of the monarchy as well as Italy's hugely powerful Catholic church, Verdi became the artistic voice of the Risorgimento almost as soon as he began writing operas at the end of the 1830s. He frequently drew on themes of national liberation, self-determination and hatred of tyranny.</P>
<P>With his third opera Nabucco in 1842, Verdi used the biblical story of the Hebrews' captivity in Babylon to raise a cry for freedom for Italy. Italian patriots adopted "Va Pensiero" from Nabucco as their anthem.</P>
<P>For Verdi, musical and political revolution went hand in hand. Rather than create operas that contained a few outstanding arias surrounded by musical filler--the standard for the time--Verdi developed real characters that sang intensely throughout.</P>
<P>Because his operas blasted the church and dramatized assassination plots against royalty, state censors hacked his work to pieces. Yet he still chose to write about themes of liberation.</P>
<P>In the spring of 1848, Italy--and Europe as a whole--erupted in revolution. Across the Italian peninsula, tyrannical rulers were driven out, and free representative governments took their places.</P>
<P>Living in Paris at the time, Verdi hurried back to Italy. Writing to Francesco Maria Piave, who wrote the words to his operas, Verdi declared, "I left the moment I heard the news, but I could see nothing but these stupendous barricades...The hour of [Italy's] liberation has sounded...You speak to me of music!! What's got into you?...Do you believe I want to concern myself now with notes, with sounds?...There must be only one music welcome to the ears of Italians in 1848. The music of the cannon!"</P>
<P>That year he composed the battle hymn "Suona la tromba" ("Sound the Trumpet") for the revolutionary leader Giuseppe Mazzini--and conducted the openly pro-revolutionary opera La Battaglia di Legnano.</P>
<P>In 1849, when Italy's revolution was defeated and Austria's rule was reimposed, Verdi despaired. "Italy is now only a vast and beautiful prison," he wrote. But his operas continued to express sympathy with the oppressed and outcast.</P>
<P>His most memorable and moving characters are the ones that "respectable" society is the most critical of: Violetta, the dying prostitute of La Traviata, who gives up her lover Alfredo for the sake of his family's name; Rigoletto, the deformed court jester who rails against the hypocrisy and corruption of the Duke of Mantua and his court; and Azucena, the peasant gypsy of Il Trovatore, whose mother is burned at the stake by the cruel nobleman Count di Luna.</P>
<P>By the end of the 1850s, national unification was again on the agenda for Italy. Under the direction of Gen. Giuseppe Garibaldi, Italian forces made inroads against Austrian rule. Verdi contributed to Garibaldi's "Fund for a Million Rifles," and bought nearly 100 guns for his own local militia. By this time, Verdi's nationalism had pushed aside his republicanism. He became convinced that Italian forces had to unite behind King Victor Emanuel to defeat the Austrians.</P>
<P>When Verdi died in 1901, more than 250,000 mourners gathered for a state funeral held in his honor a month after his death. As one senator commented at the time, Verdi didn't merely live in the 19th century--the 19th century had lived in him.</P>


