<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The airline bosses' pal in the White House</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Leighton Christainsen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS THE new year began, it looked like airline workers would be setting the pace for labor fightbacks in 2001. In the early 1990s, all the major airlines cried financial problems--and won wage and benefit concessions from unions. After years of record profits, airline workers were looking to get back what they gave away.</P>
<P>In October 2000, United pilots won wage increases of around 25 percent after they refused to work overtime. Pilots, mechanics and flight attendants at the five major U.S. airlines--United, American, Delta, Northwest and Continental--were all talking slowdowns, picketing and possible strikes. This despite being handcuffed by the Railway Labor Act (RLA) that restricts airline workers' right to take action on the job.</P>
<P>But now airline bosses are reaping the rewards of their well-placed campaign contributions to George W. Bush and the Republican Party. In early March, Bush let everyone know that he's clearly on the side of the airline bosses. During a speech in Sioux Falls, S.D., Bush announced a Presidential Emergency Board would take over stalled negotiations between Northwest and its mechanics--heading off a threatened March 12 strike. "Several other negotiations involving other national carriers face deadlines within the next few weeks," Bush said. "And I am concerned about their impact--concerned about what it could mean to this economy. And I intend to take the necessary steps to prevent airline strikes from happening this year."</P>
<P>Airline bosses invested big time in Election 2000--with all of the top four showing up on Bush's list of major contributors. Northwest is probably the best represented in Republican-run Washington.</P>
<P>Labor Secretary Elaine Chao actually sits on Northwest's board of directors. Northwest Chair Gary Wilson has given the Republicans nearly $360,000 since 1992. And Northwest lobbyist Andrea Fischer Newman is a longtime Washington insider who worked in the Reagan White House for Dubya's chief of staff, Andrew Card.</P>
<P>But the rest of the airlines have their connections, too. American CEO Donald Carty is a Bush fundraiser who kicked in $100,000 of his own money for Dubya's inaugural committee. United's lobbyist Shelley Longmuir worked for Card when he was transportation secretary. And former Republican National Committee chair Haley Barbour has put his clout to work as a lobbyist for Delta. </P>
<P>In the weeks before the Sioux Falls speech, Delta CEO Leo Mullin used Barbour to gain access to the White House to make his case for presidential intervention. The lobbying paid off. "The day the president made his announcement, Delta stopped bargaining," Capt. Duane Woerth, president of the Air Line Pilots Association (ALPA), told reporters.</P>
<P>ALPA accepted a tentative 5-year contract at Delta days after AMFA reached a deal with Northwest. Leaders from both unions, while recommending the new contracts to their members, said Bush's actions made it harder to get what they wanted.</P>
<P>But the labor-haters could overplay their hand. At American, the 23,000-strong Association of Professional Flight Attendants (APFA) voted to strike by 96 percent in late February.</P>
<P>Because of Bush, APFA was sounding a more cautious tone in April. But early May negotiations hosted by the National Mediation Board (NMB) angered union leaders--because the board continued to refuse union requests for a release from talks and a 30-day pre-strike cooling-off period for months.</P>
<P>Meanwhile, flight attendants at United picketed major airports May 1 to underline their commitment to invoke CHAOS--unannounced wildcat strikes--if United goes ahead with its plan to buy US Airways without getting a waiver from the union.</P>
<P>The airline bosses clearly expect their friends in the White House to ensure a summer of labor peace and big profits. But airline workers are still willing to fight--despite further restrictions of their rights. Given Bush's promise to prevent an airline strike, workers will have to be willing to fight outside the rules to win the contracts they deserve.</P>

