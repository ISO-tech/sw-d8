<P><font face="Times New Roman, Times, serif" size="5"><b>Books to expose the IMF and World Bank</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 11</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">BOOKS: Kevin Danaher, editor<I>, Democratizing the Global Economy: The Battle Against the World Bank and the IMF</I>. Common Courage Press, 2001, 221 pages, $15.95.</font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2">Jeremy Brecher, Tim Costello and Brendan Smith<I>, Globalization from Below: The Power of Solidarity</I>. South End Press, 2000, 164 pages, $13.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVER SINCE the protests against the World Trade Organization (WTO) meeting in Seattle in 1999, well-paid corporate media hacks like Thomas Friedman of the <I>New York Times</I> have tried to paint global justice activists as either industrial union dinosaurs or kids looking for a "60s fix."</P>
<P>"By inhibiting global trade expansion, they are choking the only route out of poverty for the world's poor," Friedman sneered following the protests against the Free Trade Area of the Americas in Quebec City last month. "Which is why these 'protesters' should be called by their real name: The Coalition to Keep Poor People Poor."</P>
<P>Never mind that the number of people in the world living on $1 a day increased to 1.3 billion as a result of the East Asian financial crash of 1997-98--and the International Monetary Fund (IMF) austerity policies that followed. Since then, protests from Washington, D.C., to Prague forced the IMF and its sister organization, the World Bank, to undertake a massive PR campaign.</P>
<P>But the ugly truth is brought to light in the essays collected in <I>Democratizing the Global Economy: The Battle Against the World Bank and the IMF</I>. "In South Korea, a country whose income was approaching European levels, unemployment skyrocketed from approximately 3 percent to 10 percent," writes Robert Weissman, editor of <I>Multinational Monitor</I> magazine.</P>
<P> "'IMF suicides' became common among workers who lost their jobs and dignity. "In Indonesia, the worst hit country, poverty rates rose from an official level of 11 percent before the crisis to 40 to 60 percent in varying estimates."</P>
<P>South African author and activist Patrick Bond shows how the IMF and World Bank are both creatures of U.S. imperialism--and calls for their abolition. Editor Kevin Danaher--a widely published author, activist and cofounder of Global Exchange--devotes nearly two-thirds of the book to assessments of last year's April 16 protest in Washington and the future of the movement.</P>
<P>But while the articles reflect the diversity of ideas and views in the movement, none devotes attention to the biggest controversy at "A16"--the decision of organized labor to mobilize for a separate rally against normal trade relations with China. That event--a throwback to Cold War anticommunism--figures prominently in <I>Globalization from Below: The Power of Solidarity</I>.</P>
<P>Jeremy Brecher, Tim Costello and Brendan Smith argue that global justice activists must combat the illusion of "national economic sovereignty" and protectionism seen at the China-bashing rally--through broader engagement with union members and working people. And like Danaher, the authors argue that the movement can't stop at abolishing the IMF, World Bank and WTO. "An adequate strategy must address not only international institutions, but the race to the bottom, the power of global corporations, the destructive volatility of global capital and the dominant role of the rich governments and the interests they serve," they write.</P>
<P>The book is less successful, however, at developing such a strategy. One of the most engaging passages compares today's movement discussions with the debate on reform, revolution, socialism and anarchism in the era of the First World War. But it's confined to a footnote. And the authors are unclear on how the movement can achieve the aims they put forward in their "draft global program."</P>
<P>It's too bad the authors didn't include examples from Brecher's classic labor history book <I>Strike!,</I> which is full of stories of how the working class has the social power to transform the world. Of course, the question of how the movement will go forward will be decided through ongoing debates--and the struggle itself.</P>
<I><P>Globalization from Below</I> and <I>Democratizing the Global Economy</I> are valuable contributions.</P>



