<P><font face="Times New Roman, Times, serif" size="4">Laid off at Chicago Steel</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>''They just shut the door''</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 16</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Workers at Chicago Steel &amp; Pickling Co. thought their futures were secure. The media was full of happy talk about how the U.S. might avoid a recession after all. Management didn't issue a warning of a plant closure--as it's required to by federal law.</P>
<P>But one day last month, Chicago Steel &amp; Pickling closed its doors--for good. And 126 members of United Steelworkers of America (USWA) Local 5544 were out on the street. "They sold out to our competitors," said Jesse Bareda, one of the laid-off workers. "They just shut the door." </P>
<P>This isn't the first time Chicago Steel &amp; Pickling has wrecked lives for the sake of profit. In 1998, the company appealed a lawsuit filed by Citizens for a Better Environment all the way to the U.S. Supreme Court to defend its "right" to dump hazardous chemical waste in the surrounding South Side community. And thanks to its high-powered industry lawyers, it won its case.</P>
<P>Now the company is dumping workers who spent their entire adult lives at the plant. "Big business is knocking the union out of the picture," said Alan Progar, one of the laid-off workers and a third-generation member of the USWA.</P>
<P>Progar was one of several union members from Chicago Steel &amp; Pickling who came to an April 21 demonstration against the Free Trade Area of the Americas (FTAA) outside Hendrickson Spring, another USWA-organized plant slated for closure. "We had people with 25 years of experience, and our company didn't give us any respect for them--to say, 'Hey, we're going to close,'" Progar said. "That was something that was really hard to take. There's a lot of people with houses and bills to pay, and they just totally forgot about us. After 25 years, you'd think they would remember us a little bit."</P>
<P>Chicago Steel &amp; Pickling workers were among the 223,000 people who lost their jobs in April--the highest total since the recession year of 1991, according to the Bureau of Labor Statistics (BLS). Nearly half the layoffs came in manufacturing, which has seen 550,000 jobs wiped out since last June--most in just the last four months.</P>
<P>While official unemployment remains historically low at 4.5 percent, the truth is that bosses are slashing jobs to protect their falling profits. "The bottom line is that a profit recession is leading to a higher jobless rate," said Sung Won Sohn, chief economist at Wells Fargo. As Joel Naroff of Naroff Economic Advisors said of the BLS unemployment report, "How do you spell ugly? How about horrible? It doesn't get much worse than this, I hope."</P>
<P>But if the bosses have their way, more layoffs are coming. According to Challenger, Gray and Christmas, a firm that tracks layoffs, job cuts announced in April totaled 165,564--the highest since its survey began in 1993.</P>
<P>The layoffs underscored the importance of the demonstrations against the FTAA--and of building a fight to defend jobs. As USWA President Leo Gerard said at the Chicago protest, "These are workers that have kept these companies together while bad management pissed away the profits and destroyed their communities and destroyed their lives. "All while workers in steel kept those companies healthy and profitable and productive."</P>
<P>It's time for our unions to take a stand--and show management that they can't get away with tossing workers on the scrap heap.</P>



