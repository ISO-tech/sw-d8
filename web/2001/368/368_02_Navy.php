<P><font face="Times New Roman, Times, serif" size="4">A slap on the wrist for Navy captain</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Deadly joyride on a nuclear sub</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Matt Nichter</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Scott Waddle's reckless joyride on a nuclear submarine cost nine people their lives. For that, he received a slap on the wrist--and even some pats on the back.</P>
<P>Waddle was the captain of the <I>USS Greeneville</I>, which rammed and sank a Japanese fishing boat off the coast of Hawaii February 9. Following an investigation into the causes of the collision, top Navy officials found Waddle guilty of "dereliction of duty" and operating the sub "in an unsafe manner."</P>
<P>According to Thomas Fargo, commander of the Pacific Fleet, Waddle was so busy showing off for a boatload of Republican VIPs that he failed to take elementary safety precautions. But fearing that a criminal trial would jeopardize their "distinguished visitors' program," Navy brass decided not to pursue a court-martial. Instead, Waddle faced an internal review.</P>
<P>And his punishment for playing around with a nuclear sub and killing nine people? Not jail time. Not community service. Not even a fine. Waddle agreed to retire--with a full pension and benefits. "I cannot find words to express my anger," said Teruo Terata, whose 18-year-old nephew, Yusuke Terata, was among those lost at sea. </P>
<P>Waddle, on the other hand, was quite pleased with the decision. "They were absolutely fair," he told CNN's <I>Larry King Live</I>. "If I were in their position I would make the same decision."</P>
<P>George W. Bush--who had several oil-industry pals and big-time campaign contributors on board the <I>Greeneville</I>--went a step further, calling Waddle a "good commander" and "a fine American patriot." And Waddle's lawyer told reporters that his client deserves a megabucks movie deal as compensation for his suffering. "It would be an appropriate way for Scott to be rewarded for what has been an incredibly difficult time," said Charles Gittens.</P>
