<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Are we to blame for global warming?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Newman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SINCE THE first Earth Day 30 years ago, the Earth's average temperature has gone up by 1 degree Fahrenheit--and scientists believe that it could rise by 2 to 10 degrees over the next century. This "global warming"--which is mainly caused by increased emissions of "greenhouse gases," especially carbon dioxide, that trap heat in the Earth's atmosphere--will have a devastating impact.</P>
<P>The U.S. produces one-quarter of global carbon dioxide emissions--and much of that comes from the gasoline we burn in cars. </P>
<P>Cars aren't just dirty. People waste hours driving each day that we could devote to more important things. Cars make our lives lonelier and more stressful. They even make them more violent--not from road rage as much as the far more brutal violence of wars, fought by the U.S. military when it sets out to protect the interests of big oil corporations. For example, more than one million Iraqis have died because of the 1991 Gulf War and the economic sanctions that the U.S. has maintained ever since--all to protect U.S. control over Middle East oil.</P>
<P>So cars seem to be at the root of many of the world's worst problems. Naturally, people want to do something about this. But what? </P>
<P>Many people involved in the movement against corporate globalization believe that we can have a big impact as consumers. We should evaluate what we consume everyday and cut back to the necessities, goes the argument. Small changes in our lives may be drops in the bucket, but enough drops will add up to a solution.</P>
<P>It's certainly true that driving a car to work is the most polluting act that most of us make in any given day. There are vehicles that use alternative fuels which are five or six times more efficient than the average car. And bicycles, of course, don't use any gas at all.  But is this realistic?</P>
<P>Most working-class people don't have the luxury of deciding to bike to work. Most of time, only "environment-friendly" choice they could make is to use public transportation--and given the state of public transportation, this is often no choice at all. Moreover, the puzzling over what to consume and not consume has no effect at all on the corporations that are truly responsible for pollution.</P>
<P>General Motors and Ford bought public trolley and bus systems in cities across the U.S. in the 1930s and 1940s--so they could close them down and assure themselves a market for private cars. These same corporations today have the technology to produce alternative-fuel vehicles.</P>
<P>rather pay But they would rather pay millions to lawyers and lobbyists to avoid laws that set quotas for the production of cleaner cars.</P>
<P>And just in case their lawyers don't deliver, U.S. corporations have also bought an insurance policy--named George W. Bush. It's no accident that Bush is pushing for opening pristine wilderness to oil exploration--or that he just abandoned the 1997 Kyoto Protocal that calls for worldwide reductions in emissions of greenhouse gasses. </P>
<P>If lifestyle decisions can't challenge the corporations and their politicians, what can? The most important anti-pollution legislation in U.S. history was the Clean Air Act of 1970. </P>
<P>It was signed into law by Richard Nixon. He signed it not because he was any more of an environmentalist than Dubya--but because he was forced to sign by social movements fighting on a range of fronts.</P>
<P>We've seen the signs of a growing movement that's ready to fight for the future of the environment. And the highest points of the antiglobalization movement have come when it has linked up with organized labor, which has the power to shut down the biggest companies.</P>
<P>We can stop the corporations that are destroying our world. But it will be through the power of protests and strikes--not through the self-policing of consumers deciding what to buy and what not to.</P>
