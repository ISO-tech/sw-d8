<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ITALY</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Berlusconi expected to win election</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Where is Italy going?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RIGHT-WING parties led by billionaire Silvio Berlusconi were expected to triumph in Italy's general election as Socialist Worker went to press. If the right-wing coalition prevails, it would bring into office not only Berlusconi--who controls nearly half of Italy's television networks--but also the far-right Northern League and the fascist National Alliance.</P>
<P>Berlusconi was elected prime minister in 1994, but he was driven out of office after widespread opposition to his conservative policies and evidence of his corruption. Evidence of his Mafia ties and corruption have been featured in the Western European media. </P>
<P>But Berlusconi, who is worth an estimated $14 billion, has used his media empire to hammer away at his election competitors--Italy's moderate center-left government. He's denounced his opponents as "communists" and "terrorists" while promising to run Italy like an efficient corporation.</P>
<P>Meanwhile, his ally Umberto Bossi of the Northern League has campaigned on a racist anti-immigrant platform modeled after the Austrian far-right thug J&ouml;rg Haider And while the National Alliance of Gianfranco Fini has supposedly "modernized" itself, its roots in Benito Mussolini's fascist dictatorship are all too clear. </P>
<P>The fascists have taken this as a signal for attacks on immigrants and other violence--including the bombing of the liberal Il Manifesto newspaper. The right-wing comeback was fueled by widespread disillusionment with the Olive Tree coalition that has governed Italy since 1996.</P>
<P>The biggest party in the coalition, the Democrats of the Left (DS), is composed mainly of Italy's old Communist Party, the largest in Western Europe, with deep roots in the unions. But when DS leader Massimo D'Alema became prime minister in 1998, he became an enthusiastic backer of Bill Clinton's "Third Way" between left and right.</P>
<P>D'Alema slashed government spending to keep Italy on track to merge its currency into the Euro and backed NATO's war over Kosovo despite massive popular opposition. D'Alema's government also opposed a series of strikes across the country in 1999.  Local elections a year ago put the right wing in control of eight of 15 regional governments and led to D'Alema's resignation.</P>
<P>While Italy's economy has grown--unemployment recently dropped below 10 percent for the first time in eight years--joblessness remains above 20 percent in the South. But rather than challenge Berlusconi by championing the demands of working people, the Olive Tree has moved even further to the right. Its candidate for prime minister, Rome Mayor Francesco Rutelli, even hired Al Gore's adviser Stanley Greenberg.</P>
<P>"The candidates are not that far apart on the issues," the <I>Philadelphia Inquirer</I> noted. "Both call for tax cuts and free capitalism." The <I>Economist</I> agreed: "Both the main coalitions' programs are quite alike, especially on economics."</P>
<P>The Communist Refoundation Party did run a left-wing campaign, but it had been compromised by its previous support for the Olive Tree in parliament.</P>
<P>Still, a Berlusconi victory won't mean easy going for the right. His earlier attempts as prime minister to cut pensions sparked massive protests. A revival of such struggles will be needed to fight the pro-business policies that will be pursued by whoever holds office.</P>

