<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GREECE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>General strike paralyzes country</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By George Vouros</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MORE THAN 200,000 angry workers from both public and private sectors went on a massive one-day general strike in Greece April 26 to protest the free-market policies of the government. In the biggest workers' action since the fall of the junta in 1974, the strike shut down factories and workplaces and paralyzed much of the country. Bus, rail, port and airline workers joined forces with teachers, civil servants, bank workers, shipyard workers and journalists in the mass media.</P>
<P>The ruling PASOK social democratic party wants to change the minimum retirement age to 65, replacing the current system that allows much earlier retirement for many workers. The plan would also funnel money to private insurance companies and put surplus funds into the stock market. The attack is in part the result of pressure to impose budget cuts needed to meet the standards for the Euro currency of the European Union.</P>
<P>The assault sparked fury among Greek workers. Trade union leaders who hold prominent positions in PASOK were pressured into calling for action, leading to a crisis in the party.</P>
<P>Some 66 members of PASOK's executive committee demanded to meet with Prime Minister Costas Simitis because they were worried about "the growing gap between the party and its social base." PASOK then announced a few hours before the strike that it would freeze its plans and seek a "dialogue" with the unions. </P>
<P>Now unions are calling for a general strike May 17 to try to press the government to adopt labor's plan for pensions. Another massive protest on May Day again showed workers' anger --and the potential to challenge the government's policies and to rebuild a fighting labor movement from the bottom up that will fight for people and not for profits.</P></FONT></BODY>


