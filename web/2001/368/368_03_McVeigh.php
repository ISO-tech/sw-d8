<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Politicians want to use McVeigh case</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop all executions!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">LOCAL OFFICIALS estimate that more than 3,000 people will descend on Terre Haute, Ind., for the May 16 execution of Timothy McVeigh. The media are planning round-the-clock live coverage, and town businesses expect a boon--from the sale of souvenirs, such as t-shirts bearing slogans like "Hangin' Time." But beneath this circus lies a real debate about the death penalty.</P>
<P>Some family members of McVeigh's victims in the 1995 Oklahoma City bombing have refused to be stampeded into calling for McVeigh's execution. Bud Welch, whose daughter Julie was killed in the bombing, became a leading voice against the death penalty after her death. He's called several times a week to console McVeigh's father since federal officials announced the execution date.</P>
<P>Compare that to Attorney General John Ashcroft's cold-blooded, politically calculated decision to broadcast McVeigh's execution on closed-circuit television. All Ashcroft and Co. care about is using McVeigh's execution to regain momentum for the death penalty.</P>
<P>Over the last few years, public opinion has shifted away from what was once overwhelming support. In Illinois, years of pressure from activists and several high-profile cases of innocent people freed from death row forced Republican Gov. George Ryan to declare a halt on executions last year.</P>
<P>Meanwhile, a steady stream of news reports and studies have further exposed how the death penalty unfairly targets poor and working-class people, particularly Blacks and Latinos. Thus, just two weeks before McVeigh's scheduled execution, an ABC News/ <I>Washington Post</I> opinion poll showed that support for capital punishment has continued to decline.</P>
<P>Just over half favored halting all executions until a commission is established to determine whether the death penalty is being administered fairly. Politicians are feeling the heat--which is why 19 states are currently considering moratoriums in some form.</P>
<P>Ashcroft and the others hope that the McVeigh case will give the death penalty a boost. But McVeigh is by no means an ordinary death row inmate. After him on federal death row sit 23 other people--and 20 of them are Black or Hispanic.</P>
<P>McVeigh's death will do nothing more than help the politicians regain the ground they've lost on this issue. That's why everyone who cares about justice should oppose the execution of Timothy McVeigh--and fight to end the death penalty, once and for all.</P>
