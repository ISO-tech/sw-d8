<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Racism from ''Bombingham'' to today</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The struggle continues</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 11, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">JUSTICE TOOK a long time catching up with ex-Klansman Thomas Blanton Jr. But in early May, Blanton was finally convicted for the church bombing that took the lives of four little girls in Birmingham, Ala., 38 years ago.</P>
<P>The 1963 bombing was one of many that rocked Birmingham-earning it the nickname "Bombingham." Birmingham authorities couldn't be trusted to do anything-since Klan sympathizers and members permeated the police force and political establishment. And the FBI was run by the notorious J. Edgar Hoover, who was more concerned with investigating civil rights leader Martin Luther King Jr. than tracking down killers. </P>
<P>The church bombing case was closed in 1968 without any charges filed. In later years, Alabama officials begged the FBI to turn over evidence related to the case. The FBI did nothing.</P>
<P>Only recently did the feds come forward with hundreds of hours of audio tapes that contained evidence critical to Blanton's conviction. The FBI's excuse for why it didn't hand over this evidence in the first place? "It was just a different time," explained FBI spokesperson Craig Dahle.</P>
<P>But while the civil rights movement succeeded in uprooting legal segregation, times haven't changed nearly enough. Racism remains a cancer in U.S. society.</P>
<P>Just look at Cincinnati, where police killings of 15 Black men in five years provoked a rebellion in April. By any social measure-poverty, unemployment, infant mortality rates-Blacks still fare worse than whites. And racist violence is still a threat everywhere in society.</P>
<P>Ask African Americans at Penn State University. Black students and administrators have received a string of death threats since October-with the racists declaring in one letter that they had already committed murder. Days later, the body of a Black man was found in the woods 20 miles from campus.</P>
<P>Yet school officials did nothing to improve security-and tried to downplay connections to the victim.</P>
<P>"The university is hiding a lot of information to preserve its image," Sharleen Morris, vice president of the Black Caucus, told <I>Socialist Worker</I>. "I think local authorities are also involved in the cover-up, just like what happened in Birmingham. It shows you that we haven't really come that far."</P>
<P>But students took up the fight when the university wouldn't. Thousands turned up for a march against hate-which turned into a two-week sit-in at the student union building.</P>
<P>"This was the first event on campus I've seen where students of color and white students worked together in a sincere way for a common cause," explained Morris. "It was beautiful." This kind of unity can put the racists on the defensive-and win a better future for Blacks and whites.</P>

