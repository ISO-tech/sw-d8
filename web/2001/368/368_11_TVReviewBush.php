<P><font face="Times New Roman, Times, serif" size="5"><b>Mocking the commander-in-thief</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Kaffen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 11</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">TELEVISION: <I>That's My Bush</I>, on Comedy Central, Wednesday nights at 10:30 p.m. (Eastern Time)</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE PREMISE of <I>That's My Bush</I> sounds great. It portrays the commander-in-thief (Timothy Bottoms) as an immature, bumbling, stammering idiot. The life of the president of the United States has become the subject of a typical TV sitcom, complete with wacky antics, a sassy housekeeper and a nosy neighbor.</P>
<I><P>That's My Bush</I> creators Matt Stone and Trey Parker, who also created the animated hit <I>South Park</I>, said they wanted to spoof sitcoms while commenting on controversial issues like abortion and the death penalty.</P>
<P>In one of the show's better episodes, Dubya tries to prove to his obnoxious fraternity buddies from Yale that he's not afraid to witness an execution. Through a series of typical sitcom misunderstandings, he's shocked to find that he actually kills the condemned prisoner. But his ever-loving wife Laura reassures him that it's really no different than the 152 others he killed in Texas--except that this time he did it himself.</P>
<P>Still, the important lesson that George learns isn't that executions are wrong, but that he shouldn't show off in front of his friends. While it's certainly fun to see George W. Bush mocked, viewers expecting a left-wing show will be disappointed.</P>
<P>The show's overall tone is critical of right-wingers, but its obvious effort to take on "political correctness" at the same time makes it pretty awful at times.</P>

