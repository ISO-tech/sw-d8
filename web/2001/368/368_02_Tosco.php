<P><font face="Times New Roman, Times, serif" size="5"><b>Tosco risks workers' lives for profits</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michal Myers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CARSON, Calif.--Most people heard about the April 23 fire at a Tosco refinery here when oil companies used it as an excuse to jack up gas prices. But the disaster had a more immediate effect on 16 workers--who narrowly escaped death.</P>
<P>Eleven workers had reached a secured control room and five others were about to enter the facility when the fire broke loose during a shift change. Had anyone been caught between the two points, they would have been incinerated. Black smoke billowed half a mile into the air and choked neighboring communities.</P>
<P>And just days later, Tosco's Wood River refinery in Illinois caught fire. Fortunately, no one was hurt in these blazes. But only two years ago, four workers were killed in an explosion and fire at a Tosco facility in Avon, Calif.</P>
<P>The California Occupational Safety and Health Administration (Cal OSHA) fined the company $1 million for safety violations. But in an ironic twist, Cal OSHA chose the day after the Carson fire to announce that it might settle the previous fine for $400,000.</P>
<P>"I think it's a slap on the hand to them," said Genesa Duncan, the wife of a Tosco worker injured at Avon. Genesa's husband has undergone 40 operations and still hasn't recovered from the blast. "It's giving them the go-ahead that it's alright, that a few lives doesn't matter, that it happens all the time, that it will happen again," she said. </P>
<P>The cause of the Carson fire is being investigated. But Dave Campbell, of the oil workers' union PACE, said Tosco runs a "bare-bones operation. "When Tosco took over the refinery from UNOCAL, they fired 20 percent of the workforce, including all their safety personnel. "The top dogs' bonuses depend on meeting production goals, and the maintenance managers' job is to stay within budget, so no one in power has safety as their guiding concern."</P>

