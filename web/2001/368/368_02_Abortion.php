<P><font face="Times New Roman, Times, serif" size="5"><b>Democrats join GOP in new attack on abortion</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON-Fifty-three House Democrats joined almost 200 Republicans in voting for the anti-choice Unborn Victims of Violence Act last month. Encouraged by President Bush's opposition to abortion rights, the anti-abortionists finally won House approval for a bill that, for the first time, would define any fertilized egg as an unborn child. The legislation now goes to the Senate for debate.</P>
<P>As National Organization for Women Executive Vice President Kim Gandy said, "Today's vote was not meant to protect women; it was meant to lay the groundwork for dismantling Roe v. Wade," the 1973 Supreme Court decision that legalized abortion. </P>
<P>The bill's sponsors had the gall to package their attack as pro-child. But this attempt to legislate "fetal rights" is really a backdoor measure to restrict women's rights. </P>
<P>Naturally, anti-choice Republicans led the way in pushing for the bill. But Democrats should share the blame. Not only did 53 of them vote for this latest assault, but the leading Senate Democrat, Minority Leader Tom Daschle (D-S.D.), has introduced "fetal rights" legislation in the past.</P>
<P>Abortion rights supporters have to take action to stop the anti-choice bigots of both parties.</P>


