<font face="Times New Roman, Times, serif" size="5"><b>Airline boss grounded</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MAYBE JEFFREY Erickson will think twice before he issues another pink slip. The former TWA executive was brought in by Air Afrique earlier this year to supervise a massive restructuring plan--which included cutting the airline's 4,200-person workforce in half.</P>
<P>Air Afrique workers weren't so keen on the downsizing plan. So when Erickson got on an Air Afrique plane last month to fly from the Ivory Coast to Paris, the plane's flight crew refused to take off until the chief executive got off. The flight finally left more than three hours late--without Erickson on board.</P>
<P>Erickson tried to leave again the next day. But Air Afrique staff found out about it--and blocked the runway until he again got off the plane.</P>
<P>--Reuters, July 23, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">White history month in Virginia</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EARLIER THIS year, Virginia Gov. James Gilmore III honored a request from the National Organization for European-American Rights and declared May to be European-American Heritage and History Month.</P>
<P>The official proclamation from Gilmore's office recognized "Americans and Virginians of European descent for the significant contributions they have made to our nation and to the world."</P>
<P>The next day, reporters were on the phone with a question for the governor. Did Gilmore realize that the National Organization for European-American Rights was a neo-Nazi group--headed by none other than David Duke, the former Ku Klux Klan leader?</P>
<P>Gilmore withdrew the proclamation--but not before insisting that there was "nothing wrong with celebrating the accomplishments of thousands of European Americans."</P>
<P>&#150;<I>New York Times,</I> May 11, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Britain's royal ass</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BRITAIN'S PRINCE Philip must think that it's his divine right to say anything at all. Last month, Philip--who's the husband of Queen Elizabeth--told a schoolboy that he was too fat to be an astronaut.</P>
<P>But Philip is notorious for his asinine remarks. While visiting China in 1986, he told several British students, "If you stay here much longer, you'll all be slitty-eyed."</P>
<P>In 1995, he asked a Scottish driving instructor, "How do you keep the natives off the booze long enough to pass the test?"</P>
<P>And in 1981--as Britain suffered through a harsh recession--he declared, "Everybody was saying we must have more leisure. Now they are complaining they are unemployed."</P>
<P>&#150;Reuters, July 27, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Keeping it all inside the family</font></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is one of two presidents to be the son of a former commander-in-chief. Maybe that explains his soft spot for the kids of other well-known Republicans.</P>
<P>There's Michael Powell, the son of Secretary of State Colin Powell. Bush picked him as chair of the Federal Communications Commission--where he's expected to pave the way for more media mergers.</P>
<P>Janet Rehnquist, the daughter of Supreme Court Chief Justice William Rehnquist, was named inspector general for the Department of Health and Human Services. </P>
<P>And Bush picked Supreme Court Justice Antonin Scalia's son Eugene as solicitor general of the Department of Labor.</P>
<P>Plus, there's Strom Thurmond Jr.--the 28-year-old son of 98-year-old Sen. Strom Thurmond (R-S.C.)--who got a federal prosecutor's job.</P>
<P>--<I>Chicago Tribune,</I> August 5, 2001<br>
