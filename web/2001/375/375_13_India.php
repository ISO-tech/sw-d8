<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Workers strike over privatization program</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by GANESH LAL</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEARLY 10 million workers in India held a one-day strike July 25 against the effects of globalization on workers and the poor. The nationwide strike was the first time that state and central government employees have struck together.</P>
<P>The strike was called to protest privatization, layoffs and antilabor legislation. It followed mass demonstrations a week earlier that were organized in a number of cities to celebrate "anti-privatization day."</P>
<P>Over the last decade, living standards for the vast majority of Indian workers have dropped--while prices have soared after government subsidies on essential goods were slashed. Millions of workers now live in slums and shantytowns, while their bosses drive posh foreign cars.</P>
<P>Worst hit of all are workers in public-sector enterprises, which are being shut down in line with International Monetary Fund (IMF)-sponsored austerity measures. "Globalization has brought disaster to the working class," said Debkumar Ganguli, deputy general secretary of the World Federation of Trade Unions (WFTU). </P>
<P>But "if you want to force these institutions [like the IMF and the World Bank] to retreat, our present strength isn't enough," Ganguli told <I>Socialist Worker</I>. "We need unity within the labor movement--we need a broader coalition, including all the labor organizations as well as the [nongovernmental organizations] and others who are fighting back."</P>

