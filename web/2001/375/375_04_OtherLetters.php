<p><a name="anti-Asian"></a><font face="Times New Roman, Times, serif" size="4"><b>New movie promotes anti-Asian humor</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I've been disgusted by ads for the new movie <I>Rush Hour 2</I>. But unfortunately, anti-Asian humor is far too often ignored in the corporate media.</P>
<P>Previews for this movie show actor Chris Tucker throwing racial slurs at his costar Jackie Chan on a regular basis. For instance, he hits Chan by accident during a big fight scene--and then says, "All y'all look alike."</P>
<P>It's disturbing to see this kind of humor promoted daily, but nothing less is to be expected from the same corporate-controlled media that shouted anti-Chinese remarks from the rooftops when a U.S. spy plane was forced down earlier this year.</P>
<P>Presentations of Asians in the corporate media are inevitably full of stereotypes. But we can't just sit by and laugh.</P>
<P>I, for one, won't be watching <I>Rush Hour 2</I> anytime soon, and I would encourage others to join an active fight against racism in an unjust society.</P>
<B><P>Rebecca Anshell,</B> San Diego</P>

