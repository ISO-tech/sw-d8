<p><a name="IMF"></a><font face="Times New Roman, Times, serif" size="4"><b>IMF's victims in Guatemala</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>We've seen two weeks of protest here in Guatemala against a regressive value added tax (VAT) proposed by the government of President Alfonso Portillo and--not surprisingly--heartily supported by the International Monetary Fund (IMF) and World Bank.</P>
<P>The VAT is a consumption tax that would increase the price of every basic good by 12 percent--all food and all clothing, every tortilla and every pair of shoes. It would obviously hit Guatemala's numerous poor the hardest.</P>
<P>Every major city has seen protests, ranging in size from 100 to 10,000. Of all the workers I've talked with, none supports the tax. Most everyone I talked to is also aware of the direct connection between Portillo's plan and the IMF and World Bank.</P>
<P>In my discussions, I've been thinking a lot about the importance of the September 28 protests against the IMF and World Bank in Washington, D.C., to workers' movements all over the world. That includes movements like this one in Guatemala--about which we hear very little, if anything, in the U.S.</P>
<B><P>Keith Danner,</B> Quetzaltenango, Guatemala</P>

