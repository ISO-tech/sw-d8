<P><font face="Times New Roman, Times, serif" size="4"><b>Creating a class system for cyberspace</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>There's something wrong with the Internet. At least according to a growing number of business leaders.</P>
<P>"The Internet is an important cultural phenomenon, but that doesn't excuse it's failure to comply with basic economic laws," telecommunications consultant Thomas Nolle argued recently. </P>
<P>"The problem is that it was devised by a bunch of hippie anarchists who didn't have a strong profit motive. But this is a business, not a government-sponsored network."</P>
<P>What's Nolle's gripe? Currently, information transferred over the Internet--whether a Web page or e-mail or a music file download--is broken up into "packets." These data packets are passed along the Internet by specialized machines called "routers."</P>
<P>Here's the problem, as the businesspeople see it--all packets on the Internet are created equal. A router treats each one the same, whether it's from Microsoft or your aunt.</P>
<P>The lords of online commerce want this "hippie anarchist" technology changed. Ideally, for them, routers would be able to distinguish between packets with lower or higher priorities.</P>
<P>And how do you get to send "high priority" packets? Like everything else, you'd have to pay. Big entertainment companies doing video "Webcasts" or corporations with large data transfers would be first in line. And that means your e-mail or Web search would wait.</P>
<P>There's already a "digital divide" in terms of access to the Internet. Now the bosses want it built into the structure of the Internet itself!</P>
<B><P>Stuart Easterling,</B> Los Angeles</P>
