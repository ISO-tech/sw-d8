<p><a name="upstate"></a><font face="Times New Roman, Times, serif" size="4"><b>Fighting oil drilling in upstate New York</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In July, some 200 angry residents of upstate New York turned out to protest proposed oil and gas drilling in Finger Lakes National Forest. A week later, 150 turned out again when the U.S. Forest Service held an open house.</P>
<P>The Finger Lakes area is widely renowned for its natural beauty. But corporations are planning to build between 15 and 32 gas wells, as well as access roads and pipelines.</P>
<P>Possible environmental damage would include blowouts (where gas or oil jets catch on fire), erosion and contamination of soil and contamination of drinking water wells. </P>
<P>Meanwhile, some lucky private oil company would rake in 87.5 percent of the royalties, while local governments would get only 3 percent. Meanwhile, towns in this rural, economically depressed area would have to foot the bill for road maintenance. </P>
<P>But area residents aren't about to let this happen. By speaking out, they won "no drilling" resolutions in the towns of Lodi and Hector and in the Schuyler and Seneca county councils.</P>
<P>As Jesse Stock of the Finger Lakes Forest Watch Congress said, "We're here to tell them that this is our land. We don't want it destroyed, we want it preserved."</P>
<B><P>Kyle Gilbertson,</B> Hector, N.Y.</P>

