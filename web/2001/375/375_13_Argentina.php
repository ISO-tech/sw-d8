<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ARGENTINA</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Strikes and protests spread against austerity measures</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Argentina on the brink</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A WAVE of strikes and protests continued across Argentina this month as the government begged for a new $9 billion bailout from the International Monetary Fund (IMF).</P>
<P>The latest strike was a 48-hour walkout by public-sector employees and transportation workers. The action was supported by the unemployed, who threw up road blockades that shut down virtually the entire country.</P>
<P>Teachers, civil servants, college students and the jobless turned out for a massive rally August 8 outside the presidential palace in Buenos Aires. Another 100,000 gathered to hear a speech by Cardinal Jorge Bergoglio, who spoke of "poor people who are persecuted for demanding work, and rich people who are applauded for fleeing from justice."</P>
<P>The strikes and protests have been sparked by IMF-imposed austerity measures that will slash salaries and pensions of public-sector workers by at least 13 percent. Meanwhile, unemployment is already at least 16 percent, and a third of the population lives in poverty.</P>
<P>"They're always cutting back on us, on those who suffer," Christian Valcalda, who works for a government agricultural agency, told reporters. "They never hit the multinational bankers and those that have made lots of money in the last few years."</P>
<P>The key figure in the strikes is union leader Hugo Moyano, who split from the country's main labor federation CGT last month after top officials blocked his election to the federation's top post. "Our country is being punished more and more by these economic measures," Moyano said. "The strikes are the only way of protesting." </P>
<P>President Fernando de la R&uacute;a claims that cuts are necessary to eliminate a budget deficit and prevent a default on Argentina's $130 billion foreign debt. In fact, the country's 2 percent deficit and debt levels are no worse than those of the U.S. a decade ago.</P>
<P>But with a world recession spreading, the IMF and Western bankers are determined to squeeze as much as they can from Argentina out of fear of the consequences of a default.</P>
<P>Besides charging extortionate interest rates for new loans, they are demanding that the Argentine currency, the peso, continue to trade on a one-to-one basis with the U.S. dollar. But it was just this policy that led to the crisis.</P>
<P>During the world financial panic of 1998, Argentina's neighbor and top trading partner, Brazil, devalued its currency, making the costs of Brazilians goods much cheaper compared to Argentina.</P>
<P>Since then, the dollar--and with it, the Argentine peso--has remained strong in comparison with other major currencies. As a result, Argentina's exports became relatively more expensive and impossible to sell. Foreign investment in the country collapsed.</P>
<P>After three years of severe recession, a financial panic broke out last December over doubts about Argentina's ability to repay its loans. The IMF agreed then to a $39.1 billion bailout loan in exchange for austerity.</P>
<P>But when the financial crisis worsened last month, the IMF announced a new $15 billion loan--not to Argentina, but to neighboring Brazil. Currency speculators concluded that Washington and the IMF were prepared to let Argentina go under, and near-panic spread through world financial markets.</P>
<P>So Washington and the IMF were forced to discuss new aid to Argentina as <I>Socialist Worker</I> went to press. In exchange for new loans, the IMF and Western banks will demand even more sacrifices from Argentine workers.</P>
<P>But the resistance to cutbacks is building, admitted Daniel Artana, a pro-business Argentine economist. "If Argentina collapses, we're not talking about just an economic contagion in emerging markets, but a political one," he said.</P>
<P>"The real danger is that rather than see it as just one nation's failure, restless left-wingers will point to Argentina, a country that went full thrust with the free market, and say it is evidence that capitalist reforms simply don't work."</P>

