<P><font face="Times New Roman, Times, serif" size="5"><b>Unionists and students unite against sweatshops</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by MONIQUE JEANNE DOLS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 15</font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Despite a staggering heat wave, more than 300 people gathered for a rally at Jedson Memorial Church in Manhattan to kick off a new anti-sweatshop campaign.</P>
<P>The panel discussion featured representatives from Canada, Mexico, Thailand, Nicaragua, Hong Kong, Guatemala, Honduras, the Dominican Republic and the U.S.</P>
<P>The rally was the kickoff to a new effort to target retailers and hold them responsible for inhumane conditions faced by garment workers worldwide. The campaign is an important step by the textile workers union UNITE--led by new President Bruce Raynor--to unite labor's fight with student anti-sweatshop and global justice activists and civil rights veterans.</P>
<P>"Despite years of public pressure against sweatshops, today's global retailers are greedier than ever, and more workers around the world are toiling in sweatshops to make their goods," Raynor said. "It's time for retailers to stop hiding behind high-priced PR firms and industry created cover-ups and finally sit down at the table with workers to develop concrete ways of ending the global sweatshop crisis." </P>
<P>Rev. Timothy McDonald of Atlanta told the overheated crowd: "You think it's hot in here! It's nothing compared to the heat that we're gonna put on the retailers..."</P>
<P>Longtime anti-sweatshop activist Charles Kernigan of the National Labor Organization held up a water sample and said that it was taken from a drinking tap in a sweatshop in El Salvador where workers make Gap clothing.</P>
<P>A biologist who tested the water found it contained 123 times more bacteria than safe limits allow. "It's a problem when you can't tell the difference between the retailing industry and organized crime," Kernigan said. </P>
<P>The rally was followed by a spirited march of about 500 activists that ended with protests at the New York City outlets of international retailers Banana Republic, Eddie Bauer and Ann Taylor.</P>
<P>"What we are doing is very simple," a UNITE Local 155 organizer told <I>Socialist Worker</I>. "We're launching a struggle against the retailers who make money off of our work. Our goal is that the workers will earn enough to survive on, because what we are making now is just pennies."</P>
