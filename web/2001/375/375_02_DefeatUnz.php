<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="5"><b>Bilingual education targeted in Massachusetts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by FRAN&Ccedil;OIS LaFORGE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--Two years ago, school officials here eliminated busing. Now bilingual education is under attack. Ron Unz, the Silicon Valley millionaire who bankrolled anti-bilingual education referendums that passed in California and Arizona, has set his sights on Massachusetts.</P>
<P>Unz has won support from minority figures like Wilfredo Laboy, the school superintendent in Lawrence, Mass., and Lincoln Tamayo, the Cuban-born principal of Chelsea High School who resigned from his position to lead Unz's push on the ballot measure.</P>
<P>Unz is a cynical right-winger who uses the horrible state of U.S. schools as a pedestal for his anti-bilingual propaganda. But unfortunately, the referendums that he's backed have passed by sizeable majorities.</P>
<P>That's because the people who could have put up a real fight--most importantly, Democratic Party officials--avoided taking a hard stand for fear of alienating conservative voters.</P>
<P>We need to expose Unz as a racist phrasemonger, even when he uses minority superintendents and principals to lead his cause.</P>
<P>Beating this ballot measure will mean making a clear defense of bilingual education--and appealing to both immigrant and native workers to oppose Unz's attack as scapegoating that draws attention away from the real problem, the rotten state of our schools.</P>

