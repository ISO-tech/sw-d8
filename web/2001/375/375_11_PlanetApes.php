<P><font face="Times New Roman, Times, serif" size="5"><b>Tim Burton's <I>Planet of the Apes</I>: New but not improved</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><B>MOVIES: <I>Planet of the Apes</B></I>, directed by Tim Burton, written by William Broyles, Jr., starring Mark Wahlberg, Helena Bonham Carter, Tim Roth and Michael Clarke Duncan.</P>
</font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by DONNY SCHRAFFENBERGER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TIM BURTON'S new--but not improved--<I>Planet of the Apes</I> is as thrilling as any Hollywood action movie.</P>
<P>The original <I>Planet of the Apes,</I> starring Charlton Heston and featuring the brilliant writing of the socially conscious "Twilight Zone" creator Rod Serling, debuted in the radical year 1968.</P>
<P>The civil rights and Black Power movements and anti-Vietnam War demonstrations had exposed a corrupt system--led by men in tuxedos (a.k.a. "monkey suits").</P>
<P>Based on a Pierre Boulle novel, <I>Planet of the Apes</I> was an allegory about racial and class oppression--as well as a critique of the nuclear madness of the U.S. Cold War with the ex-USSR.</P>
<P>The original movie shows a world where humans live like animals and apes rule. But the ape ruling class has to keep a secret from the humans and most of the other apes--that thousands of years before, humans had ruled the planet, but destroyed their civilization in a nuclear war.</P>
<P>The indigenous humans look primitive and are mute--all except for astronaut Heston, whose ship crash lands on the future Earth.</P>
<P>In Burton's remake, the humans all talk and wear pretty makeup. But the apes still treat them as slaves. Burton's movie has realistic ape suits and special effects, but it lacks the original's suspense.</P>
<P>Mark Wahlberg, as understated an actor as Heston was over the top, plays the stranded astronaut. He becomes a reluctant leader who defies the apes and gives humans hope that they can fight back.</P>
<P>Helena Bonham Carter, as an ape scientist and animal rights (or should I say human rights) activist, contributes some amusing bits of role-reversal humor.</P>
<P>The most improbable role reversal, though, is left to National Rifle Association President Heston, who makes a cameo as an old dying ape. Heston tells his son, the ruthless General Thade (Tim Roth), that one thing above all others makes the humans so treacherous--a gun!</P>
<P>As funny as it might seem, Heston's anti-gun diatribe doesn't have the same impact as the original movie's anti-Cold War stance. Burton's <I>Planet of the Apes</I> gains in slickness and spectacle, but it lacks the original's sharp-edged political punch.</P>

