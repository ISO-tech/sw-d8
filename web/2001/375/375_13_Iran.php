<P><font face="Arial, Helvetica, sans-serif" size="2"><b>IRAN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind Iran's power struggle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SAMAN SEPEHRI</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IRANIAN PRESIDENT Mohammad Khatami was finally sworn in for his second term in office August 8 after a four-day standoff between reformist and conservative forces vying for power over the country.</P>
<P>Khatami's inauguration was held up by his conservative opponents after the president's reformist allies, who control Iran's parliament, refused to be bullied into confirming the right's candidates for the powerful Guardian Council.</P>
<P>The Council is Iran's equivalent to the Supreme Court and has long been dominated by conservatives who have used it to undo many of Khatami's reforms. The Council has reversed laws liberalizing the press, overturned legislation limiting arbitrary arrests and even struck down parts of the budget proposed by reformists.</P>
<P>The latest deadlock was resolved when the conservative nominees for the Guardian Council were forced through the parliament on a technicality. But this has put in question the legitimacy of these candidates--as well as that of the Guardian Council and the conservatives in general.</P>
<P>Khatami won his second term in an election June 8, where he got 77 percent of the vote, repeating his landslide victory of four years ago. Khatami's goal is the introduction of free-market measures, privatization of state firms, cuts in government subsidies and labor law "reforms" as part of a bid to open Iran's economy to foreign investment.</P>
<P>But such measures would hurt Khatami's own supporters--large sections of the working class and youth. So Khatami has tried to popularize his program by calling for "democracy" as a means to mobilize public sentiment and pressure against the conservatives, who control the courts, intelligence service, police and armed forces.</P>
<P>This time, the conservatives decided not to contest the presidential election directly, retreating to their strongholds of the police and the judiciary.</P>
<P>This will be Khatami's final term as president, leaving the reformists just four years to break the control of the conservatives. The stage is set for major political battles that will determine the future of Iran.</P>

