<P><font face="Times New Roman, Times, serif" size="4">Texas wants to kill Napolean Beazley</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop the racist death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by MIKE STARK</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HUNTSVILLE, Texas--The state of Texas is ready to kill Napolean Beazley. As <I>Socialist Worker</I> went to press, Napolean was due to go to the execution chamber August 15, for a crime he committed when he was only 17 years old.</P>
<P>If Napolean is killed, it would be the 19th time that the U.S. has executed a juvenile offender since the death penalty was reinstated in 1976. That barbaric statistic sets the U.S. apart from almost every country in the world.</P>
<P>According to Amnesty International, Texas alone accounts for almost one-third of the world's known executions of child offenders.</P>
<P>Napolean, who is Black, was accused of shooting a white victim during a botched carjacking. He was given the death sentence by an all-white jury, after a trial during which prosecutors referred to him as an "animal" and one juror was heard to say, "The nigger got what he deserved."</P>
<P>The victim, John Luttig, was a well-known figure in the east Texas town where the trial took place. His son, Michael Luttig, is a judge who sits on the ultraconservative, pro-death penalty 4th Circuit Court of Appeals, based in Richmond, Va.</P>
<P>Luttig, who has never ruled in favor of a death penalty defendant in the 10 years since he became a federal judge, played a hands-on role in pushing prosecutors to seek a death sentence for Napolean.</P>
<P>In Texas, prosecutors are supposed to prove that a defendant is a continuing threat to the public before he or she can get a death sentence. Beazley has no prior criminal record or history of violence.</P>
<P>He was president of his senior class, the starting running back for his high school football team, a member of the track team and had just graduated 13th in his class when he was arrested.</P>
<P>Dozens of community members described Napolean as a respectful, helpful teenager and said they were shocked that he got mixed up in the carjacking.</P>
<P>But that didn't stop the prosecutors. They scrounged up a psychiatric "expert" who--without ever interviewing or even meeting Napolean--testified that he was a continuing threat.</P>
<P>Even the district attorney in Napolean's home county has called for Napolean's execution to be stopped. The ACLU, Amnesty International, the American Bar Association and other groups are also campaigning for clemency.</P>
<P>A few years ago, there would have been little hope of this in Texas--even in a case with as many outrageous injustices as this one. But since the national explosion of protest over the execution of Gary Graham--another juvenile offender--last summer, the Texas killing machine has been under intense scrutiny. </P>
<P>Even the state's pro-death penalty politicians are having doubts. For example, C.C. Cooke, a Texas judge who helped craft the state's death penalty law when he was a legislator, recently said that "from a judge's perspective and taking care of people's rights, I think it has a lot of flaws."</P>
<P>Graham's execution--which came in the midst of then-Texas Gov. George W. Bush's campaign for the White House--added momentum to the growing opposition to capital punishment.</P>
<P>Pressure from death penalty opponents has led more and more people to question a system that reserves the ultimate punishment exclusively for the poor, especially poor minorities.</P>
<P>Recent opinion polls show that a majority of people want executions halted nationwide until the fairness of the death penalty system can be studied. Napolean Beazley's case shows why they're right.</P>
<P>We have to oppose this legal lynching. Stop the execution of Napolean Beazley!</P>
