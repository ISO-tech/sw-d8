<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="5"><b>Study shows need for AIDS drugs in Africa</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LONDON--Remember Andrew Natsios? The head of the U.S. Agency for International Development told a congressional committee that sending life-saving AIDS drugs to Africa was a waste--because Africans "don't know what Western time is."</P>
<P>"You have to take these drugs a certain number of hours each day, or they don't work," he said. "Many people in Africa have never seen a clock or a watch their entire lives."</P>
<P>Natsios' remarks were appallingly racist. And now one of the world's most respected medical journals has shown that he's ignorant about AIDS drugs, too.</P>
<P>Two articles in the latest issue of the <I>Lancet</I> say that people suffering from HIV-AIDS in poor countries can be safely and effectively treated with so-called "drug cocktail" therapies--and at a relatively low price.</P>
<P>One article describes a successful pilot program using cutting-edge AIDS drugs in the Haitian countryside--and concludes that if the program "can be implemented in the devastated central plateau of Haiti, it can be implemented anywhere."</P>

