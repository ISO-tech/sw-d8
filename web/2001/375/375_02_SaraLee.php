<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="5"><b>Slap on the wrist in Sara Lee food poisoning case</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEIGHTON CHRISTIANSEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Hundreds of people have fallen ill and at least 21 are dead from eating Ball Park franks. But the Chicago-based food giant Sara Lee, which produces the hot dogs, has gotten away with a slap on the wrist.</P>
<P>The hot dogs caused cases of food poisoning from the deadly <I>Listeria monocytogenes</I> bacteria. In the summer and fall of 1998, the Centers for Disease Control (CDC) began receiving calls from state health departments about an outbreak of Listeria.</P>
<P>CDC scientists traced the bacteria to a package of Ball Park franks, and testing proved that a hot dog factory in Zeeland, Mich., was the source.</P>
<P>Sara Lee recalled the contaminated products in December 1998, but 21 people had already died from eating the franks.</P>
<P>Why didn't testing at the Zeeland plant itself catch the problem? In fact, workers began detecting a sharp increase in cold-loving bacteria shortly after maintenance was done near the Ball Park franks production line.</P>
<P>Sara Lee's response? Stop testing. "They knew they had a problem with bacteria in the plant," said Caroline Smith DeWaal, of the Center for Science in the Public Interest. "But instead of solving it, they chose to ignore it."</P>
<P>When lawsuits began to pile up, Sara Lee called in its chips with government officials. Meat processors have long had a cozy relationship with Washington, keeping regulations limited and inspectors from digging up violations. But Sara Lee has a big hot dog contract with the Department of Defense to worry about, too.</P>
<P>The Feds refused to bring felony charges against Sara Lee, and this June, the company pled guilty to two misdemeanors and paid a $200,000 fine. Thanks to its friends in Washington, Sara Lee has gotten away with murder.</P>

