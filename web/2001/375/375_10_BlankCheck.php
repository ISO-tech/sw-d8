<P><font face="Times New Roman, Times, serif" size="5"><b>Why Israel gets a blank check from the U.S.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL'S LICENSE to kill is written on U.S. government stationery.</P>
<P>Year in and year out, Israel receives more economic and military aid than any other country in the world. Thanks to its American support, Israel was able to develop a nuclear arsenal with more than 200 warheads--and illegally occupy southern Lebanon for 23 years and the West Bank, Gaza and the Golan Heights for more than 30 years. </P>
<P>Why does Israel get a blank check from the U.S.?</P>
<P>The standard story is that Israel is "the only democracy in the Middle East." "There exists a deep friendship between Israel and the U.S." Israel Prime Minister Ariel Sharon claimed recently. "The basis of this friendship is common values, a commitment to democratic values, freedom, peace."</P>
<P>Sharon's "commitment to democratic values" involved overseeing the massacre of thousands of innocent Palestinians at the Sabra and Shatila refugee camps in Lebanon in 1982. In fact, that massacre says much more about why the U.S. government supports Israel than all the PR hot air about "democracy." </P>
<P>U.S. support for Israel is based on the country's strategic value in the oil-rich Middle East. The Middle East is home to two-thirds of the world's oil reserves. Control over Middle Eastern oil gives the U.S. economic leverage over its economic rivals, especially Japan and Germany.</P>
<P>As Gershom Shoken, editor and publisher of the Israeli newspaper <I>Ha'aretz, </I>said in 1951: "Strengthening Israel helps the Western powers to maintain equilibrium and stability in the Middle East. Israel is to become the watchdog."</P>
<P>During the June 1967 war against neighboring Arab countries, Israel proved its value as an outpost of U.S. imperialism--and not for the last time. "As an indirect beneficiary of the Israeli blitz," <I>Newsweek </I>magazine wrote at the time, "the U.S. should at least be in a position to neutralize the Middle East, so that its oil can be profitably marketed and its waterways used for the benefit of world commerce."</P>
<P>Israel serves not as an outpost of democracy--but as a defense against the threat of democracy. The same goes for the regimes of Arab countries--like Egypt, Saudi Arabia and the Gulf monarchies--which also receive huge quantities of U.S. aid and military supplies.</P>
<P>The real threat to U.S. interests is that ordinary Arabs will take on both Israel and the corrupt right-wing regimes throughout the region--and fight for a world where resources are used to meet human need, not oil company profits.</P>
