<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>When a refund isn't a refund</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--Did you think that check from the government was a tax refund? Not exactly.</P>
<P>Though Washington is not talking much about it, the $300 or $600 checks mailed to taxpaying individuals or families this summer aren't refunds on last year's taxes--but an advance on what you may or may not get back when you file next April.</P>
<P>If you get a refund on your taxes next year, it'll be $300 or $600 smaller because of the advance. And if you owe money, this summer's supposed "rebate" will be tacked onto the bill.</P>
<P>Obviously, the Bush gang has little interest in clearing up the confusion. And they plan to keep it that way.</P>
<P>Next year's tax forms will be designed to hide the fact that your final tax payment or refund is off. Refund? Better to call it a shell game.</P>

