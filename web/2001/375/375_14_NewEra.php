<P><font face="Times New Roman, Times, serif" size="5"><b>Interview with New Era Cap striker</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 14</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DERBY, N.Y.-Workers went on strike at New Era Caps on July 16 against company attempts to cut wages and sick days and increase speedups. New Era refuses to further negotiate with members of Communication Workers of America (CWA) Local 14177.</P>
<P>Workers--previously represented by an "independent" pro-company union--voted in the CWA in 1997. Since then, management has cut jobs at the Derby plant from 500 to 376, moving the work to plants in Alabama. Workers also suffer continuous harassment from management.</P>
<P>Local 14177 President <I>JANE HOWALD</I> attended the United Students Against Sweatshops conference, where she spoke with <I>Socialist Worker</I>'s <I>GLENN ALLEN</I>.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>WHAT WAS it like organizing with the CWA?</P>
</B><P>I've been at New Era 19 years, and in 19 years I have tried organizing four different unions. And the company held captive audience [meetings], where the company sits you down and tells you that if you bring in a union you lose your job. So people didn't vote them in.</P>
<P>Then, in '97, I was elected secretary of the existing union. Our president wanted to get a new union in, because with the independent union, the company got away with murder.</P>
<P>In 1997, we won by 26 votes. And it took a lot--phone calling, constant talking one on one, telling them not to be afraid to get a union in.</P>
<P>A union isn't some outsider. It's you and me with the ability to sit down at the table with the company and know that someone is going to help us.</P>
<B><P>WHAT IS the company's current offer?</P>
</B><P>[It] caused 77 percent of our members to lose money, up to $5 an hour on a $12-an-hour wage. We used to have four unpaid sick days--they wanted only one.</P>
<P>We paid close to $349 per month in out-of-pocket medical expenses. A lot of people can't afford it. One woman who has five kids had to decide to pay her rent instead of her medical.</P>
<P>So the offer on the table was the most insulting offer. They thought we would vote the contract in and didn't think we'd go on strike. They told the mediator that if our people don't want to work here, tell them to get out. That insulted our members even more.</P>
<P>We want to work there; we just don't want to work there for $5.15 an hour.</P>
<B><P>TELL ME about the injuries at New Era.</P>
</B><P>I have carpal tunnel in both wrists and a ruptured disk in my back. Everybody I know is having surgery. My vice president is going out for a third or fourth surgery for rotor-cuff tears.</P>
<P>Yet the company was saying that our people are faking it. You don't fake it and have surgery. It's not something you get rich off of, and it's not something you enjoy.</P>
<B><P>WHERE IS the strike at now?</P>
</B><P>Negotiations have stalled. They said their final offer is their final offer. Our members have said, in no uncertain terms, "no." Federal mediators are coming in.</P>
<P>It's solid on the line. Our people are out there every day, even in the heat. They won't even go home when I tell them to. They were putting in 16 hours.</P>
<P>I thought, the first week's one thing, the second week's another, but the third week, it got stronger.</P>
