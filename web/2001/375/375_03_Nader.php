<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="4">Nader and friends hit the road for a series of super rallies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The return of Ralph</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>YOU OUGHT to be ashamed of yourselves. That's the Democrats' message to Ralph Nader and those of us who supported his presidential campaign.</P>
<P>But Brian Harrison has a message for them. "Back in Texas, we have a bumper sticker that says 'Unrepentant Nader Voter,'" Harrison, a Campus Green member who attended a Nader super rally in Chicago, told <I>Socialist Worker</I>. "Sometimes you've got to make a principled stand-and certainly an election is one of those times."</P>
<P>Nader feels the same way--and he's back again, touring with many of the big-name supporters, like actor Danny Glover, singers Ani DiFranco and Patti Smith and author and activist Cornel West, who appeared with him during his campaign.</P>
<P>Nader and friends drew 7,500 to a super rally in Portland to support his new effort, called Democracy Rising. That rally--and another of nearly 3,000 in Chicago--reflected the enthusiasm and incredible thirst for change felt during the campaign.</P>
<P>Tens of thousands were inspired by Nader's message against greedy corporations and the two parties that do the bosses' bidding. Nader raised the bar for what voters should expect from candidates--calling for universal health care, the repeal of anti-union laws, a $10-an-hour minimum wage, abolition of the death penalty and an end to environmental destruction.</P>
<P>Hundreds of people, many of whom had never considered themselves political before, organized Nader committees across the country. Nader disappointed his supporters when he all but disappeared from the political scene after the election. They're rightly glad that Nader is back.</P>
<P>Democrats and their supporters still want to blame Nader for "stealing" votes from Al Gore. Some even picketed Nader's Portland rally. They're the ones who should be ashamed.</P>
<P>The Democrats haven't put up any fight against Bush's right-wing agenda. All this proves once again that it will take organizing from below to make real change, not depending on liberal Democrats.</P>
<P>As actor Tim Robbins, a Nader supporter, put it recently: "The young people who have helped launch a quest for an alternative party, one that will not compromise this planet's future for campaign donations from corporate sugar daddies, believe the Democratic and Republican parties are united on the major issues of our time...it is grassroots movements that create real change, and no grassroots movement ever got anywhere compromising its ideals.</P>
<P>"Real change won't happen at Washington cocktail parties or in the Lincoln Bedroom...this movement is in its infancy, but it is alive, and it's not going away. Its door is wide open to you. It's a frightening threshold to cross, but an essential one."</P>
<P>The big crowds that came out this month to hear Nader show that there's a large audience for a political alternative to the status quo. We have to make sure that this sentiment is mobilized into action in all the struggles in our workplaces, campuses and communities.</P>

