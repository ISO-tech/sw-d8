<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Iraq still faces sanctions and bombs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The never-ending war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SHARON SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PERSIAN Gulf War ended more than 10 years ago, but the U.S. has continued to kill Iraqis ever since--both with United Nations (UN) sanctions and regular bombings.</P>
<P>Now George W. Bush has said that the Pentagon is drawing up plans to escalate the bombings. "Saddam Hussein is on the radar screen for the administration," National Security Adviser Condoleezza Rice recently warned on CNN's "Late Edition," and Bush is considering "military force in a more resolute manner and not just a manner of tit-for-tat with them every day."</P>
<P>In the short term, Bush is likely to win the support of a majority of people in the U.S. if he orders a new round of bombings, like the one that Bill Clinton ordered in December 1998.</P>
<P>But few people in the U.S. yet know the truth about U.S. policy toward Iraq--thanks to a virtual media blackout and widespread acceptance of the ludicrous claim that Saddam Hussein poses a threat to the rest of the world.</P>
<P>For example, when U.S. military forces bombed an Iraqi soccer field on June 19, killing 23 people--21 of them younger than 17--it barely warranted a mention in the mainstream press.</P>
<P>The U.S. and Britain declared "no-fly zones" over northern and southern Iraq at the end of the war in 1991, banning Iraq from flying any aircraft, including helicopters, in these areas.</P>
<P>U.S. and British warplanes have been patrolling the "no-fly zones" ever since, dropping bombs on a regular basis. But the mainstream U.S. press has yet to question the U.S. government's right to do so.</P>
<P>In contrast, an Arab Emirate government daily newspaper wrote recently, "One has to ask what these planes are doing in Iraq's airspace&#133;which they are violating without justification." </P>
<P>And in the September issue of the <I>Progressive </I>magazine, Thomas Nagy shows that the U.S. government intentionally used the sanctions against Iraq to degrade the country's water supply--knowing from the beginning that a massive death toll would result, especially among children.</P>
<P>A Defense Intelligence Agency (DIA) document, titled "Iraq Water Treatment Vulnerabilities" and dated January 22, 1991, makes it clear that U.S.-imposed sanctions would "result in a shortage of pure drinking water for much of the population. This could lead to increased incidences, if not epidemics, of disease."</P>
<P>The document admits that chlorine was an item that Iraq was banned from importing under the sanctions--and that "recent reports indicate the chlorine supply is critically low."</P>
<P>It goes on to acknowledge that food and medical supplies would also be affected by the lack of clean water, stating: "Food processing, electronic, and, particularly, pharmaceutical plants require extremely pure water that is free from biological contaminants."</P>
<P>Another document later in 1991 spelled out that "there are no operational water and sewage plants" in Iraq--all had been destroyed by bombing during the war.</P>
<P>These documents presaged the epidemics of disease and hunger that have afflicted the mass of the Iraqi population over the last decade--and the hospitals without medicine that are the last refuge for those waiting to die.</P>
<P>The UN estimates that 500,000 children under the age of five have already died because of the sanctions, and 5,000 more children die with each passing month.</P>
<P>But Nagy shows not only that the U.S. government knew in advance the scale of human destruction that the sanctions would cause, but that the mainstream U.S. media has taken no interest in the issue.</P>
<P>The first document issued for the DIA was partially declassified in 1995 and is posted on the Pentagon's Web site. "I disclosed this document last fall," Nagy explains. "But the news media showed little interest in it."</P>
<P>Furthermore, mainstream media outlets have not drawn the obvious conclusion that U.S. sanctions toward Iraq are in direct violation of the Geneva Convention.</P>
<P>As Nagy points out, the Geneva Convention states explicitly in a 1979 protocol: "It is prohibited to attack, destroy, remove or render useless objects indispensable to the survival of the civilian population, such as foodstuffs, crops, livestock, drinking water installations and supplies."</P>
<P>Yet the U.S. government has been doing just that--and getting away with it.</P>
<P>Those of us who live in the heart of the beast must build a movement against the U.S.'s murderous aims in Iraq--and to help make more people in the U.S. aware of the truth about American foreign policy.</P>

