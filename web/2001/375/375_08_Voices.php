<P><font face="Times New Roman, Times, serif" size="5"><b>Voices from Palestine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Pages 8 and 9</font></p>

<p><font face="Times New Roman, Times, serif" size="4"><b>"Israel is founded on discrimination"</b></font></p>
<P><font face="Times New Roman, Times, serif" size="3">ONE IN five Israeli citizens is Arab. But Arabs don't even have formal equality with Jews under Israeli law, and discrimination is widespread.</P>
<I><P>NASEER,</I> an activist in the West Bank, talked about the conditions that he faces.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL IS defined as the land of the Jews. Now if the state is defined as the land of the Jews, how can you even expect that there will be political equality?</P>
<P>By definition, it's a discriminating state. As an individual, I may have some privileges of citizenship, like the right to vote, the right to work.</P>
<P>But the more important form of discrimination occurs when you talk about collective rights. As a Palestinian inside of Israel, you always feel that you don't belong.</P>
<P>Eighty percent of Palestinian lands inside Israel--that is, land that was owned by Palestinians--was expropriated by the Israeli state. </P>
<P>The most basic way of showing the discrimination is to point out that, from 1948 until now, no Palestinian village or city was built for the Palestinian minority inside Israel--although we increased from half a million to a million or more.</P>
<P>We aren't allowed to build new villages, and if we try to build homes, they're demolished.<br>

<br>
<p><font face="Times New Roman, Times, serif" size="4"><b>"They open their files and pick one"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">ONE WEAPON in Israel's campaign of terror against Palestinians has been home demolitions--where Israeli forces bring in bulldozers and equipment to destroy Palestinian settlements.</P>
<P><I>TAYSEER AROURI,</I> a physics professor at Bir Zeit University and board member of the Ramallah-based Jerusalem Legal Aid and Human Rights Center, explained the logic behind the demolitions.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WHENEVER THE Israelis want to put some pressure either on the Palestinian Authority or local activists, they open the files and say, "Okay, those homes were built without permits, and they'll be demolished."</P>
<P>Officially, Palestinians can apply for a permit to build homes, but you have to wait somewhere between three to five years--sometimes even seven years--to get the permit, if you get it at all.</P>
<P>The cost of a permit is tens of thousands of dollars. For some buildings, this is almost equal to the value of the building itself. The rationale is that Israel wants to kick the Arabs out of Jerusalem.</P>
<P>If you're a Jew living in Israel, then you can get the permit within a week. But most Jewish settlements in the Jerusalem area are public housing projects, so there's no question of needing building permits.</P>
<P>The settlements are expanding all the time. Officials within the Israeli state say that they don't have the right to expand or build new homes. But they never demolish them.<br>

<br>
<p><font face="Times New Roman, Times, serif" size="4"><b>"They're treated like slaves"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">SINCE IT sealed off the Occupied Territories, Israel has moved to replace Palestinian workers who used to travel from the West Bank and Gaza each day with foreign workers.</P>
<P><I>EHUD EIN-GIL,</I> a volunteer at the Workers' Rights Hotline in Tel Aviv, explained the process.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THERE'S A large concentration of Romanians, who are mainly employed in the construction business. Lately, we have Chinese workers also coming in, because Romanians are already too [class] conscious for Israeli employers.</P>
<P>In agriculture, most of the people are from Thailand. Then there's a large contingent of Filipinos, who are mainly employed in professional nursing.</P>
<P>There's a law in Israel where families get a sum of money each month for every elderly or disabled person who can't attend to themselves. So often, the families bring in a domestic servant.</P>
<P>These workers stay in the home 24 hours a day, but they're paid as if they worked eight hours a day. In reality, they're half slaves.</P>
<P>The reports of abuse are horrible. We've heard stories about some families that lend these domestic workers to other families and collect the money.</P>
<P>We've also had cases of people who take passports from workers, so they can't leave the house.<br>

<br>
<p><font face="Times New Roman, Times, serif" size="4"><b>"Honest observers say the big blow is coming"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3"><I>TIKVA HONIG-PARNASS</I> and <I>TOUFIC HADDAD</I> are coeditors of the journal <I>Between the Lines</I>.</P>
<P>During the 1948 war that established the Israeli state by expelling 800,000 Palestinians, Tikva fought in the Palmach--a kibbutz-based strike force of the Zionist militia Haganah. Today, she is an outspoken critic of Zionism and firm advocate of Palestinian rights.</P>
<P>Toufic is a Palestinian American activist based in the West Bank.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>IS ISRAEL preparing for an all-out war?</P>
<P>Tikva:</B> Sharon is waiting for the pretext to gain consent from the United States, Europe and the rest of Israeli society. The honest observers are saying that the big blow is coming.</P>
<P>I think the optimum aim is to get rid of Yasser Arafat and put in his place Palestinian collaborators and security people, who Israel assumes will cooperate with their aims more.</P>
<P>They want to leave the settlements and continue with Israel controlling the whole situation. </P>

<B><P>HOW IMPORTANT is U.S. aid to Israel?</P>
<P>Toufic:</B> It's the linchpin that allows what goes on here to continue going on. And it's been unanimous throughout various administrations, going back at least to the 1950s.</P>
<P>You have $5 billion of U.S. aid above the table--not to mention, of course, the intelligence connections and the money under the table. The political backing that the U.S. gives Israel in the United Nations Security Council and in the General Assembly is essential.</P>
<P>There also seems to be close military cooperation between the Israelis and the Americans on how best to suppress the Intifada. These plans were drawn up as early as 1996, when there were rising feelings that they might not be able to push through the Oslo process.</P>
<P>Anthony Cordesmann, who's the head of the Center for Strategic and International Studies and has close relations to the CIA, wrote a document on what a second Intifada would mean. He's very clear about it--that we're going to have to have torture, imprisonment and administrative detentions. </P>
<P>Everything Israel does is done with American consent. That's why I think it's very important to stress that when we think strategically about Palestinian solidarity in America, we shouldn't ally ourselves with the two U.S. parties that historically have been the problem. We should look instead to the social forces within America that are building an alternative.</P>

<B><P>HOW DO you characterize what's been called the peace camp in Israel? Is there any opposition to the drive to war?</P>
<P>Tikva:</B> It's a total collapse. It collapsed because they didn't have any alternative to the apartheid plan of Oslo.</P>
<P>They thought the Oslo apartheid regime could really work. Once Oslo didn't work, they were left without any alternative, and a vacuum was created in which the right came and started pulling most of Israeli society to its side.</P>
<P>So we've seen a return to the rhetoric of 1948, as if Israel were a small state fighting for its existence.</P>
<P>The problem with the Zionist left is that it's deep-rooted in accepting the notion of a Jewish state. Once you accept this idea, of course, you're not for a full right of return for Palestinians.</P>
<P>Support for the Jewish state, by definition, denies the possibility to achieve a just settlement.</P>
