<P><font face="Times New Roman, Times, serif" size="5"><b>Paying for the weapons that murder Palestinians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government gives billions of dollars in aid to Israel every year. "[The] request of $1.98 billion will enable the Israeli government to meet cash flow requirements associated with the procurement of U.S. origin systems such as F-16 and F-15 fighter aircraft, Apache Longbow upgrades, field vehicles and advanced armaments," reads the State Department's "Congressional Budget Justification for Foreign Operations for Fiscal Year 2001."</P>
<P>The report notes that military aid to Israel is "expected to increase incrementally (by $60 million each year) to a level of $2.4 billion in FY 2008."</P>
<P>Why so much money? Because, the State Department explains, the U.S. had "a significant interest in [an]...economically and militarily strong Israel" and in "maintaining the qualitative edge of the Israeli Defense Forces."</P>
<P>All told, U.S. military aid "represents about 26 percent of the Israeli defense budget and is crucial to Israel's multi-year defense modernization plan," the State Department admits.</P>
<P>And for all the military aid, Israel gets billions more in loan guarantees, export credits and other assistance. This support is essential to Israel's ability to build settlements, pay for soldiers and purchase the weapons that it uses to murder Palestinians.</P>
<P>When the Israeli government sends helicopter gunships or jet fighters to bomb buildings in the Occupied Territories, it's using weapons of terror that were made in the USA. We have to push for a cutoff of all U.S. aid to Israel. </P>
