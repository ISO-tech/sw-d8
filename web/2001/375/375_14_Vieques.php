<P><font face="Times New Roman, Times, serif" size="5"><b>Get the Navy off Vieques!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by HECTOR REYES</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>VIEQUES, Puerto Rico--Finally, after 60 years of abuse, the residents of Vieques were allowed to express their opinions about the U.S. Navy's use of their island as a bombing range.</P>
<P>In a local government-sponsored referendum on July 29, residents voted overwhelmingly--68 percent--for the Navy to leave immediately.</P>
<P>While a referendum agreed upon between former President Clinton and former Gov. Pedro Rosell&oacute;, and scheduled for November, had only two options--the Navy stays indefinitely or goes in 2003--the local referendum included the demand that the Navy leave immediately.</P>
<P>The option allowing the Navy to stay indefinitely barely received 30 percent of the vote.</P>
<P>News of the referendum traveled the world, with newspapers from London to New York acknowledging the results as an "unequivocal act of self-determination" and a defeat for the Navy.</P>
<P>Meanwhile, blowhards in the U.S. Congress threatened to cancel the November referendum and void President Bush's decision to remove the Navy from Vieques by May 2003.</P>
<P>Once the results were made public, the people of Vieques took to the streets in the rain and spent all night dancing, singing and shouting anti-Navy chants. The next day, more than 300 people marched to the Navy's Camp Garc&iacute;a gates, carrying a black coffin stuffed with pro-Navy referendum propaganda. The crowd then destroyed the coffin.</P>
<P>Vieques' new mayor, D&aacute;maso Serrano, handed a Navy official a symbolic eviction notice that summoned the Navy to stop its military practices immediately and provide a timetable for cleaning up contaminated land.</P>
<P>"To not satisfy these demands would put in question in the world's eyes the values of freedom and democracy that rule in Vieques and Puerto Rico," Serrano told the crowd.</P>
<P>That same day, the Navy began a new round of military exercises, which protesters once again met with civil disobedience. Nearly two dozen activists from all walks of life were arrested--after successfully disrupting the exercises.</P>
<P>Activists are already strategizing about a response to the next round of exercises, which is rumored to begin in September and include NATO forces.</P>

