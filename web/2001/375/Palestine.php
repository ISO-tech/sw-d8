<p><font face="Times New Roman, Times, serif" size="5"><b>Eyewitness report from Palestine</b></font></p>
<p><font face="Times New Roman, Times, serif" size="3"><I>Socialist Worker's ANTHONY ARNOVE</I> and <I>AHMED SHAWKI</I> visited Palestine in July--where they witnessed firsthand the horrors of Israel's bloody war. In this special section, they describe Israel's apartheid, talk to some of its leading opponents--both Palestinians and Israelis--and report on the struggle against it.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_07_IsraelApartheid.shtml">Israel's apartheid </a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">People around the world look back on South Africa's racist apartheid system with horror and disgust. But you don't have to go to the history books to find out what apartheid is like. You only need to visit Palestine.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_07_TornDown.shtml">"They came and just tore them down"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">ISRAEL HAS escalated its campaign of demolishing the homes of Palestinians in the Occupied Territories. </P>

<p><font face="Arial, Helvetica, sans-serif" size="2"><a href="#Top">Back to the top</a></font></p>

<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>BACKGROUND AND ANALYSIS</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3"><b>As army prepares for all-out war&#133;</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="375_08_IsraelTightens.shtml">Israel tightens the noose</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The Middle East stands on the edge of all-out war. According to news accounts published both inside and outside Israel, the Israeli Defense Forces have plans for a "major military operation that will be launched if politicians give the green light," wrote <I>Ha'aretz </I>newspaper.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_08_Hijacked.shtml">How Israel hijacked Palestine</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Israel has been called "a land without people for a people without a land." But to claim this means insisting, as Zionist leader Golda Meir famously declared, that "Palestinians do not exist."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_09_OsloPeace.shtml">Oslo's "peace" without justice</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">WHEN ISRAEL and the Palestine Liberation Organization (PLO) signed the Oslo peace accords in 1993, it was hailed as a "historic achievement." But Oslo was never about a just peace. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_09_Arafat.shtml">Did Arafat "reject peace"?</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Every day, the mainstream newspapers blame the current round of violence in Palestine on Yasser Arafat, who, they say, "rejected peace" at the Camp David meeting orchestrated by Bill Clinton last summer.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_09_Map.shtml">A Palestinian bantustan</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">A map of the West Bank and Gaza, showing how Israeli settlements and bypass roads have carved up the land "controlled" by the Palestinian Authority.</P>

