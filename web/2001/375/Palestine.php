<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>INTERVIEWS</b></font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_08_Voices.shtml">Voices from Palestine</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3"><I>Socialist Worker </I>talked to dozens of people committed to the fight for Palestine--both Palestinians and some of the courageous Israelis who have spoken out against their government's crimes. </P>

