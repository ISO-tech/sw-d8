<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>THE U.S. CONNECTION</b></font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_10_BlankCheck.shtml">Why Israel gets a blank check from the U.S.</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">ISRAEL'S LICENSE to kill is written on U.S. government stationery. Year in and year out, Israel receives more economic and military aid than any other country in the world.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_10_PayingForWeapon.shtml">Paying for the weapons that murder Palestinians</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">THE U.S. government gives billions of dollars in aid to Israel every year. This support is essential to Israel's ability to build settlements, pay for soldiers and purchase the weapons that it uses to murder Palestinians.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="375_10_NoUSAid.shtml">No U.S. aid for Israel</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. government has poured almost $100 billion into Israel--more than it has given throughout its history to the whole of Africa--to cement the "unbreakable" relationship. But some cracks are beginning to show. </P>

