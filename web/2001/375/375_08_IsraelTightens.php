<font face="Times New Roman, Times, serif" size="5"><b>Israel tightens the noose</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE MIDDLE East stands on the edge of all-out war. According to news accounts published both inside and outside Israel, the Israeli Defense Forces (IDF) have plans for a "major military operation that will be launched if politicians give the green light," wrote <I>Ha'aretz</I> newspaper.</P>
<P>Israeli troops would reoccupy areas of the West Bank and Gaza now controlled by the Palestinian Authority (PA)--and target Yasser Arafat and the core of PA officials around him for assassination.</P>
<P>Some reports said that the Israeli government was waiting for a pretext--for example, another Palestinian suicide bombing like the one carried out August 9 in Jerusalem, which killed 15 people.</P>
<P>An invasion of PA territory would be the culmination of Israel's massive escalation of violence against Palestinians since the second Intifada--or uprising--began last September.</P>
<P>As usual, the Intifada was sparked by Israeli provocation--the storming of the al-Aqsa mosque in Jerusalem by Ariel Sharon and a small army of heavily armed security guards.</P>
<P>But at every step of the way, Israel has responded to Palestinian outrage by raising the stakes. The latest tactic is assassination.</P>
<P>Israeli forces have increasingly targeted Palestinian leaders--both PA officials and militant opponents of the PA--for cold-blooded murder.</P>
<P>The favored method is aerial attacks with U.S.-supplied helicopter gunships--and even an F-16 fighter jet in one case in Ramallah. But the Israelis have also resorted to more common terror tactics--like planting bombs in cell phones and then triggering them by remote control.</P>
<P>During our visit, we saw the rubble left behind by the F-16 attack on Ramallah. The wreckage lay 30 yards away from a Palestinian hotel with an outdoor playground for children.</P>
<P>Yet U.S. politicians and the media claim that Israel's assassinations are "defensive actions" or "preemptive attacks." "I think there's some justification in their trying to protect themselves by preempting," Vice President Dick Cheney declared earlier this month.</P>
<P>In these circumstances, it's little wonder that some Palestinians believe the only hope of striking back at Israel is through suicide bombs.</P>
<P>In fact, the August 9 explosion in Jerusalem was a direct response to an Israeli attack a week earlier aimed at leaders of the group Hamas that killed eight people, including two children.</P>
<P>Israel's violence and repression will only add to the ranks of desperate young Palestinians willing to die if they can lash out just once at their oppressors.</P>
<P>Yet the cries for blood in Israel are growing ever louder. Former Prime Minister Benjamin Netanyahu even attacked his successor, veteran war criminal Ariel Sharon, for being too soft. Netanyahu says he would send in the army to recapture major Palestinian towns such as Jenin, Nablus and Hebron.</P>
<P>Top Israeli leaders openly speak of "creating" a new Palestinian leadership--a not-so-veiled reference to the expectation that Yasser Arafat will be killed. "Arafat did not prove to be a partner for peace and quite probably will not be one in the future," former Prime Minister Ehud Barak--the leader of the supposedly liberal Labour Party--wrote in the <I>New York Times</I>.</P>
<P>One Israeli woman interviewed by the <I>Times</I> spoke of a "final solution" to the conflict with the Palestinians--a chilling phrase used by the Nazis to refer to the Holocaust of Jews during the Second World War.</P>
<P>An all-out war on the Palestinians isn't without risks for Israel. It would provoke fury across the Middle East--potentially leading to revolts by the mass of the Arab population that sympathizes with the Palestinian cause. This is something that the U.S. government--Israel's most important ally--fears.</P>
<P>But Israel's fanatical right wing--which opposes even the limited concessions to Palestinians resulting from the Oslo peace process--is calling the shots today in Israel.</P>
<P>An invasion of PA-controlled territory would have horrific consequences. Thousands of people would die. And an offensive could spark a broader Middle East war--one in which Israel would be tempted to use its more than 200 nuclear warheads.</P>
<P>We can't allow Israel's drive to war to go unopposed. We have to stand up to demand justice for Palestinians--and call on the U.S. government to cut its ties to its watchdog in the Middle East.</P>
