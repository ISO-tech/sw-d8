<P><font face="Times New Roman, Times, serif" size="4">Telling the story of the mid-1990s labor battles in central Illinois</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The War Zone</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><B>BOOKS:</B> Stephen Franklin, <b><i>Three Strikes.</i></b> Guildford Press, 2001, 308 pages, $23.95.</font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by LANCE SELFA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THREE years in the mid-1990s, labor activists described the central Illinois factory town of Decatur as a "war zone." With a population of 81,000, Decatur was the site of three major industrial disputes between union workers and world-leading employers.</P>
<P>Two strikes pit the United Auto Workers against heavy equipment maker Caterpillar, Inc., and the United Rubber Workers against tire manufacturer Bridgestone-Firestone.</P>
<P>Meanwhile, A.E. Staley Co., a division of the British multinational sugar giant Tate &amp; Lyle, locked out its more than 700 workers in Decatur. At one point in 1994, about one in 10 Decatur workers was involved in a strike or lockout.</P>
<P>The War Zone battles marked an important departure for the labor movement. In the end, all three ended in defeat for the unions.</P>
<P>Yet the War Zone struggles showed that an increasing number of workers were in no mood to simply accept concessions, as many unions had during the 1980s. War Zone activists revived many of the weapons of class struggle--from the "work to rule" to international solidarity action--that brain-dead AFL-CIO honchos had long since forgotten.</P>
<P>Now, some years after Decatur's labor wars ended, <I>Chicago Tribune</I> reporter Stephen Franklin has produced the first book-length account of them. Franklin's <I>Three Strikes</I> is a rare attempt to show the real effects of corporate greed on ordinary people--and their determination to resist it.</P>
<P>Franklin frames his history around the battle at Caterpillar, whose determination to use any means, including a scab workforce, to defeat the UAW had the greatest impact on the labor movement as a whole.</P>
<P>Over the course of four years, the UAW surrendered to Caterpillar twice. Only the determined efforts of rank-and-file activists in Decatur and Peoria, Ill.--who continued to resist union leaders' attempts to impose a settlement on them--salvaged a shred of union dignity when Caterpillar finally signed a contract in 1998.</P>
<P>I took special interest in <I>Three Strikes</I> because I helped to found the Staley Workers Solidarity Committee (SWSC) in Chicago in the summer of 1993. For the next two-and-half years, we in SWSC worked closely with--and got to know--many War Zone activists, including a number that Franklin profiles in his book.</P>
<P>While the book is sympathetic to the workers and largely accurate in the broad strokes of its account, I found it incomplete.</P>
<P>For instance, it barely mentions the June 1994 demonstration of more than 5,000 workers and their supporters, which police dispersed with pepper gas. The protest marked a turning point in the three disputes.</P>
<P>Moreover, Franklin's attempt to sum up the experience of the War Zone leaves readers with the impression that Decatur workers were doomed to lose. "If anyone had doubted it before, it was now clearer than ever that only fools or brilliant strategists led unions into strikes," he writes.</P>
<P>My conclusions are different. First of all, the War Zone struggles--especially the fight at Staley--had a profound impact on labor activists across the U.S. Rank-and-file "Road Warriors" who traveled the country to build solidarity for their struggle were a shining example of how the labor movement could turn itself around.</P>
<P>The War Zone struggles not only put pressure on Staley, Cat and Bridgestone-Firestone, but they shook up the labor movement--foreshadowing the victory of John Sweeney's New Voices slate in winning the top offices of the AFL-CIO.</P>
<P>If the Decatur fights lost, it wasn't because they were too militant--but because International union leaders never gave them the support they deserved and local union leaders discouraged mass picketing in favor of more moderate strategies like a "corporate campaign" boycott.</P>
<P>Franklin describes a delegation of Decatur rank and filers trying to take their case to the 1995 AFL-CIO winter meeting at the posh Bal Harbour resort in Florida. The image of the Decatur workers wandering around the resort, trying to buttonhole union officials who were more interested in their tee times at the golf course, says a lot about labor's problems.</P>
<I><P>Three Strikes</I> is a good place to start learning about the War Zone struggles. But it doesn't tell the whole story.</P>

