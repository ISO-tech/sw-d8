<P><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Remembering the Matewan massacre</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MANY READERS of <I>Socialist Worker</I> will have seen the excellent John Sayles film <I>Matewan,</I> about a group of miners in West Virginia who go on strike against the coal bosses in the early 1920s.</P>
<P>In the film's climactic scene, striking miners--led by the town sheriff, Sid Hatfield, who supports them--square off against a gang of Baldwin-Felts guards hired by the company, leading to a deadly gun battle.</P>
<P>What you might not know is that the film is based on a true story--and the real setting still exists.</P>
<P>Matewan is a small town near the Tug River that divides Kentucky and West Virginia. The rugged green mountains of the region are beautiful--where the tops haven't been blasted off by strip mining.</P>
<P>I won't tell you that the place hasn't changed. A big concrete floodwall cuts across the town, and there's now a road where the railroad tracks from the film's climactic scene used to be.</P>
<P>But quite a few old storefronts along the main street have been preserved. One of them has been converted into a one-room museum, with historical photographs and other items related to the region's history.</P>
<P>The old brick bank building at one end of the street still has bullet holes in it from the "Matewan massacre." Press a button on the wall of the bank, and a recorded message--surprisingly sympathetic to the miners--retells the story of the Matewan massacre and the efforts of miners to unionize in West Virginia.</P>
<P>As you listen, you can look out on the scene where the gunfight took place--which still looks close enough to what it must have then to let your imagination go a little bit.</P>
<P>The Tug River region is also the locale of the old and bloody Hatfield and McCoy feud. When my partner and I visited Matewan, we ran across a couple who was also listening to the taped recording describing the union's battles. It turned out that one was actually a Hatfield--related to old Sheriff Sid Hatfield.</P>
<P>Matewan, we were told by the owners of the local gift shop, regularly gets visitors, some even from overseas, because of the film. But as of a few years ago, the town still didn't have a place to stay.</P>
<P>If you spend the day there, you'll have to get a room in the nearby coal town of Williamson--whose main attraction is a small city hall building made entirely of coal bricks.</P>

