<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="5"><b>How Italy's Berlusconi was beaten last time</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I></P>
<P>Thank you for your excellent and wide-ranging coverage of struggles for justice, workers' rights, and real democracy at the local, national and international levels. </P>
<P>I was especially impressed with David Zirin's compelling eyewitness report from the antiglobalization protests at the Group of Eight (G8) meeting in Genoa last month.</P>
<P>His article conveyed well both the brutality of the Italian <I>carabinieri</I>--who assassinated a demonstrator at the behest of George W. Bush's good friend, Prime Minister Silvio Berlusconi--and the courage and humanity of the hundreds of thousands of protesters and average Genoese citizens who spoke out against this brutal act and, more broadly, against the ravages of globalization. </P>
<P>As Zirin points out so eloquently, even the seemingly passive and certainly hilarious statement of hanging underwear from outdoor clotheslines in Genoa bespoke a deep rage at the arrogance of Berlusconi and the other G8 leaders.</P>
<P>I experienced Berlusconi's earlier reign as prime minister firsthand while living in Rome in 1994-95. During his short term of office, this media magnate and coalition partner of neo-fascists tried to force an unpopular austerity program on workers.</P>
<P>Their response was to converge on Rome in November 1994 for a demonstration of over 1 million people. Sadly, the Italian parliamentary left made no real effort to build on that burgeoning movement and squandered its first-ever chance to govern Italy by presenting a watered-down version of the same plan as "realpolitik."</P>
<P>This time, however, Italian workers, students and other activists have been joined by hundreds of thousands from around the world in an increasingly global and confident movement to fight the economic status quo, environmental degradation and exploitation everywhere.</P>
<P>I know that I can look forward to <I>Socialist Worker</I> keeping me informed of future struggles and encouraging effective activism.</P>
<P>Viva Hanging Underwear!</P>
<B><P>Jennifer Selwyn,</B> Kittery, Maine</P>

