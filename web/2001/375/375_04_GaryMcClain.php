<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="4">Locked up in an institution for talking union</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why they don't want Gary to tell his story</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON A July morning two years ago, Gary McClain was on his way to work when he was arrested and taken to a doctor, who declared him mentally ill and had him institutionalized.</P>
<P>What were Gary's "symptoms"? Talking union at Tenneco, a plastics company in Aiken, S.C., where he'd worked for 17 years.</P>
<P>Gary was pulled out of his car by a heavily armed SWAT team, taken to a local hospital and then transported to a mental institution. He spent two weeks there and was forced to take anti-psychotic drugs.</P>
<P>Gary, who filed a lawsuit over the harassment, told <I>Socialist Worker</I> at the time: "Workers know the truth of what they did to shut me up. But I won't be quiet. I will still talk union, because we need one."</P>
<P>Last month, <I>SW</I> received this update on the case from a friend of Gary's.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I></P>
<P>Gary McClain is a close friend of mine, and I just wanted to give you an update on his situation. Recently, the local newspaper reported that a district judge had ruled against him in his lawsuit, saying that he didn't have enough evidence to go to court.</P>
<P>Actually, he has so much evidence that a blind man could see it.</P>
<P>Gary McClain's former employer, Tenneco (now Pactiv), is a multimillion-dollar company out of Chicago. My point is that with so much money on their side, could Tenneco possibly have used other resources besides the legal system to get their way?</P>
<P>There are powerful forces at work here. Why doesn't the opposition want Mr. McClain to tell his side of the story to a jury? Because if the truth is known, a lot of corporate fat cats and their sycophants are going to get burned.</P>
<P>I hope that, in the end, Gary wins this one.</P>
<B><P>Steven Buchmiller, </B>Jackson, S.C.</P>

