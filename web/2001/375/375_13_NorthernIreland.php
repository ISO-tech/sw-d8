<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NORTHERN IRELAND</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protestant leaders trigger new crisis</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 13</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE IRISH peace process hung in the balance this month after Britain briefly suspended Northern Ireland's government.</P>
<P>The latest crisis was triggered when David Trimble, leader of the main pro-British party, the Ulster Unionists, resigned as Northern Ireland's first minister in June over the alleged failure of the Irish Republican Army (IRA) to decommission all of its weapons.</P>
<P>Britain this month suspended the government for the second time in 18 months despite an IRA offer to put its weapons "beyond use." When the IRA threatened to withdraw its offer on arms, the British government reinstated the Northern Ireland administration just 24 hours later.</P>
<P>The suspension creates a six-week period of negotiations, which the British government will use to try to push through a final settlement of the 1998 Good Friday peace accord.</P>
<P>The truth is that the question of IRA arms is a pretext for the Unionists to keep from making concessions to the Catholic minority, large numbers of which support the IRA and its allied political party, Sinn Fein.</P>
<P>An international inspection team has already examined the IRA weapons cache and stated that they could not be distributed without detection. But the gains of a right-wing Unionist party in recent elections pressured Trimble into demanding still more concessions from the IRA and Sinn Fein.</P>
<P>What's more, a final settlement based on the Good Friday accord will do nothing to rid the sectarianism and anti-Catholic discrimination that is entrenched in Northern Ireland.</P>
<P>Republican opposition to the deal has seen small, dissident groups such as the Real IRA turn to disastrous tactics like the August 3 bombing outside a crowded London bar, which injured seven.</P>
<P>But the most violent opposition to the deal comes from Unionist paramilitary thugs, who have continued their anti-Catholic violence, including the murder of a 19-year-old last month.</P>
<P>The fight against anti-Catholic bigotry and Northern Ireland's sectarian state has to be linked to struggles that can unite Protestant and Catholic workers against the poverty, unemployment and low wages that afflict them both.</P>

