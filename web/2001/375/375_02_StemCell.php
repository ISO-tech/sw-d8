<FONT FACE="Courier New" SIZE=2><P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Strict limits on stem cell research</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush backs the fanatics</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PHIL GASPER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--In his first prime time speech since becoming president, George W. Bush announced severe restrictions on federal funding for research on embryonic stem cells earlier this month.</P>
<P>Bush's decision represents a victory for religious right and anti-abortion extremists--and a defeat for millions of people who could benefit from the research.</P>
<P>Media analysis of the decision focused on conservatives who said they were disappointed that Bush didn't prohibit all research. But Bush's new rules are the most restrictive possible short of an outright ban.</P>
<P>Stem cells are the body's master cells. If they come from embryonic tissue in its first few days of development, stem cells can be transformed into any of the body's 200 different cell types.</P>
<P>Scientists hope that stem cells can be used to regenerate tissue and organs damaged by injury or disease--offering potential cures or treatments for Alzheimer's, Parkinson's, multiple sclerosis, juvenile diabetes, heart disease, severe burns, spinal cord injuries and other conditions.</P>
<P>The controversy is over research that involves removing material from--and thus destroying--embryos in the very early stages of development, when they are no more than clumps of about 100 cells called "blastocysts." Fertility clinics routinely store these tiny embryos for couples undergoing in-vitro fertilization. Many aren't used and deteriorate or are discarded.</P>
<P>Yet anti-abortionists--who claim that destroying an embryo, even in the first few days of its existence, is murder--have raised vocal opposition to stem cell research. Bush's decision accepts their absurd assumptions.</P>
<P>Under his new rules, federally funded researchers won't be allowed to extract new stem cells from embryos--although they'll be permitted to experiment on already existing "cell lines," reservoirs of stem cells previously derived from an embryo.</P>
<P>Bush claims that there are 60 existing cell lines. But most researchers think the number is half that. In any case, most are privately owned and might not be available to public researchers. More importantly, even if 60 lines do exist, this isn't nearly enough to provide the genetic diversity that researchers need to develop effective therapies.</P>
<P>Bush's decision on stem cell research followed a vote by the House of Representatives last month to ban cloning involving human cells, not just for reproductive purposes, but for therapeutic purposes, too. If the bill becomes law, it would effectively end stem cell research, which relies on such techniques.</P>
<P>Incredibly, this backward legislation was supported by many liberal Democrats. Some explained their vote as opposition to the "commodification of life" and the power of biotechnology corporations.</P>
<P>It's certainly true that the profit system distorts scientific development for its own purposes. But blocking basic research, which has already made promising advances toward combating disease, is the wrong way to challenge corporate power.</P>
<P>Meanwhile, Bush's decision is likely to increase private control over science. As Quentin Young of Physicians for a National Health Program put it: "Bush's statement on stem cell research is the latest in a series of doublespeak pronouncements where the fig leaf of high morality-the sanctity of life-poorly conceals his fundamental ethic: a commitment to corporate exploitation and commodification of everything."</P>

