<P><font face="Times New Roman, Times, serif" size="5"><b>"They came and just tore them down"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL HAS escalated its campaign of demolishing the homes of Palestinians in the Occupied Territories. </P>
<P>On one day during our visit, the Israeli army destroyed 14 homes in the Shufat refugee camp in East Jerusalem. Residents were given a day's notice that their homes would be demolished--because of "permit" violations.</P>
<P>It's almost impossible for a Palestinian to get a building permit from Israeli authorities. Israelis, of course, expand their homes and settlements with the government's encouragement.</P>
<P>"They came with bulldozers and just tore them down," said Hassan, as he told us how his home in Shufat was destroyed. When Hassan, his family, neighbors and others tried to stop the soldiers, they were beaten. Journalists covering the demolition and lawyers trying to stop it were pushed aside.</P>
<P>Hassan, who walks on crutches because of a childhood case of polio, now has difficulty making his way through the rubble that remains of his home. Because of the demolition, Hassan and his brothers share a small room with their mother and her grandchildren.</P>
<P>That same week, the Israelis tore down 22 Palestinian homes in Rafah, a southern town in Gaza, near the border with Egypt. "The only thing I have left is the red shirt I'm wearing," said Mohammed Abu Lideh, whose home was destroyed. "I spent all my savings to build this house."</P>
<P>And in an even larger demolition during our visit, Israel destroyed the homes of hundreds of Palestinian shepherds in the hills of South Hebron. "The destroyers blocked up the wells--the source of life for these families that have neither running water nor electricity," <I>Ha'aretz Magazine</I> reported.</P>
<P>"Now hundreds of children have no roof over their heads in the midsummer sun and nowhere to go."</P>
