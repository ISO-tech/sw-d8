<P><font face="Times New Roman, Times, serif" size="4">Thousands attend rallies in Portland and Chicago</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Ralph Nader is back</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL DEAN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND--There was electricity in the air at the Rose Garden arena on August 4 as 7,500 people cheered Ralph Nader.</P>
<P>Last October, during the presidential campaign, Portland was the kickoff of Nader's "People Have the Power" tour, which included super rallies in Chicago, Seattle and New York City.</P>
<P>Then, 10,000 people showed up in Portland to hear the Green Party candidate for president. This time, a few Democrats who blame Nader for Bush's taking of the White House picketed the super rally, with signs that read "Defense Contractors for Nader" and "Citizens Against Tundra."</P>
<P>But Nader's message today was the same as during the campaign for president: Raise your expectations and don't settle for "Tweedledee-Tweedledum" politics!</P>
<P>"Believe that you can alter the course of events, and in one gigantic arena after another, Americans are going to come," Nader told the August 4 crowd. "They're going to learn there are a lot of people like themselves who believe that things can change."</P>
<P>Nader announced the goals of his new "Democracy Rising" campaign--for 1 million people each to give $100 and volunteer 100 hours to grassroots causes.</P>
<P>Before the rally, hundreds attended a "Progressive Action Conference," which featured 37 meetings throughout the day. Speakers included Ramon Ramirez, president of Pineros y Campesinos Unidos del Noroeste, who explained the historic struggles of farmworkers in Oregon and Bush's new bracero program.</P>
<P>Nader's rally featured actor Danny Glover and singer Eddie Vedder. Nader started off his speech by saying there's a "thirst for fun." The rally showed that there's also a thirst for change.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--"Who said 'no' to women's rights, to the environmental movement, to equal pay for equal work, to civil rights? It was the business commercial mentality that kept saying 'no'--until they were confronted with an overwhelming populist civic movement that told them, Whether you say 'no,' from now on you're going to obey yes!" </P>
<P>That's what Ralph Nader told some 3,000 people who packed into Chicago's Congress Theatre August 10. The "Super Rally for Radical Change" featured music by Patti Smith and Ani DiFranco. Other speakers included Nader's running mate and Native American activist Winona LaDuke, author Cornel West, the Green Party California Senate candidate Medea Benjamin, artist and activist Jello Biafra and Cheri Honkala of the Kensington Welfare Rights Union.</P>
<P>Nader focused his talk to the mostly young crowd on building a successful movement by learning from the struggles that came before. He urged audience members to become activists "for life."</P>
<P>The crowd was clearly glad to have Nader back again--after he practically dropped out of the public eye since the election. The event was sponsored by the Campus Greens, which held their national founding convention at the University of Illinois-Chicago that weekend.</P>
<P>Literature tables representing not only the Greens but a wide of range struggles lined the walls of the theater lobby--activists who oppose nuclear power, sweatshops, the death penalty, animal testing and restrictions on hemp use. Groups organizing buses to protest the IMF and World Bank in late September, as well as supporters of the Charleston Five, passed out leaflets.</P>
<P>Many said that Nader's 2000 campaign was what got them interested in political activism--and they remained proud of their vote for Nader. "Nothing's going to change if we vote for the lesser evil," said Nicole from Minneapolis.</P>
<P>"He knows what's up. He's for the people," agreed Benjamin Chang. "All the other parties are for the corporations, and they don't have humanity in mind."</P>

