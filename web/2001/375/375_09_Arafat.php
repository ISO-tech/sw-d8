<P><font face="Times New Roman, Times, serif" size="5"><b>Did Arafat "reject peace"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVERY DAY, the mainstream newspapers blame the current round of violence in Palestine on Yasser Arafat.</P>
<P>Arafat, they say, "rejected peace" at the Camp David meeting orchestrated by Bill Clinton last summer, in the final days of his administration. But not even the <I>New York Times</I> believes that one.</P>
<P>When it comes to the Middle East, the <I>Times</I> has always been a willing mouthpiece for the U.S. State Department and the Israeli establishment. So when it casts doubt on the common wisdom about Arafat, we should pay attention.</P>
<P>"A potent, simplistic narrative has taken hold," wrote <I>Times</I> reporter Deborah Sontag. "Mr. Barak offered Mr. Arafat the moon at Camp David...Arafat turned it down, and then 'pushed the button' and chose the path of violence."</P>
<P>But it just isn't true. "Barak did not offer Mr. Arafat the moon," Sontag reported. "Palestinians did not believe [Barak's proposal] would leave them with a viable state."</P>
<P>The Camp David proposal would have denied millions of Palestinians the right of return to their homeland and allowed a number of Israeli settlements to remain in the West Bank and Gaza, leaving the resulting "Palestinian state" carved into tiny pieces.</P>
<P>As lousy as the deal was, Arafat made it clear that he was willing to sign away key historic demands of the Palestinian people--if only Israel would be somewhat flexible.</P>
<P>"No other Arab party that has negotiated with Israel...ever came close to considering such compromises," says Gilad Sher, one of Israel's lead negotiators at Camp David. Instead, it was the Israeli government that walked away from the "peace" process.</P>
