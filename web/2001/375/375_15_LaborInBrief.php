<p><a name="Taco"></a><font face="Times New Roman, Times, serif" size="4"><b>Taco Bell Truth Tour</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by FIDEL BELMONT</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Activists here are spreading the word about the Coalition of Immokalee Workers' (CIW) fight for justice. The CIW is an organization of farmworkers who work for Immokalee, Fla.-based Six Ls Packing Co., one of the nation's largest tomato producers and a contractor for Taco Bell.</P>
<P>They launched a national boycott of Taco Bell on April 1. Workers get no overtime pay, health insurance, sick leave, holiday or vacation pay, or retirement.</P>
<P>They must pick two tons of tomatoes to earn $50. Yet in 1999, Taco Bell reported earnings of more than $5 billion.</P>
<P>LA activists have organized numerous pickets at Taco Bells around the city to inform the public about the conditions of these workers.</P>
<P>Students and other local activists showed up each week at one Taco Bell location in East Los Angeles, carrying tomato-shaped signs that read, "Support Farm Workers" and "Boycott Taco Bell." They've also hung a 10-foot banner with an angry Chihuahua saying, "Yo No Quiero Taco Bell!"</P>
<P>Most passersby sign the CIW's petition without hesitation, and it's common for people driving by to honk and shout, "Boycott Taco Bell!"</P>
<P>CIW members are starting a "Taco Bell Truth Tour" in September, which will culminate in a September 23 march in LA and a September 24 demonstration at Taco Bell's headquarters in Irvine, Calif.</P>
