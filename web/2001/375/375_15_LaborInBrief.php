<p><a name="Fast"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop Fast Track</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JEFF SKINNER and JOHN COURSEY</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--Some 300 unionists and activists demonstrated in front of the U.S. Chamber of Commerce offices July 30 against Bush's attempt to gain fast-track authority over trade agreements.</P>
<P>Bush wants fast-track authority so he can push through trade deals, even if they ignore protection for workers' rights and the environment.</P>
<P>The AFL-CIO organized the rally as part of its campaign against fast track, which will come up for a vote in Congress later this year. The demonstration showed that workers are fed up with politicians of both parties trying to ram trade deals down our throats--and determined to say no to fast track.</P>
