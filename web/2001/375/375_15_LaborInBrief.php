<P><font face="Times New Roman, Times, serif" size="4"><b>Brockton nurses</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ALPANA MEHTA</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BROCKTON, Mass.--Some 450 nurses at Brockton Hospital are holding firm in their strike after walking the picket lines all summer. The nurses, who are members of the Massachusetts Nurses Association (MNA), walked out May 25 after working without a contract since last October.</P>
<P>The main issue in the strike is mandatory and forced overtime. Nurses say they receive late-night calls from the hospital, demanding that they come to work--often in the intensive care unit and emergency room, both areas where safety is of utmost importance.</P>
<P>This year alone, nurses have reported 49 incidents where low staffing put patients' safety at risk. </P>
<P>Brockton Hospital CEO Norman Goodman--who the nurses call "Norman the Very Badboy"--has been leading a vicious attack on the MNA since he took over six years ago.</P>
<P>Back then, Goodman pleaded hard times during negotiations with the nurses, convincing the MNA to agree to concessions, like part-time benefits for full-time work.</P>
<P>Now the hospital is raking in record profits, and Goodman has given himself big pay raises that make him the highest-paid hospital administrator in the state. But he's abandoned promises to restore benefits and is stonewalling negotiations with the nurses, a majority of whom have worked at Brockton Hospital for more than 20 years.</P>
<P>"Nurses here have been the backbone," said Anne, a 30-year veteran of the hospital. "We've been here through the lean times. What we did six years ago, we did for the patients."</P>
<P>Federally mediated negotiations held July 25 ended the same way that other talks have--with the hospital conceding nothing and insisting on its original proposal. Management's pathetic response to the forced overtime issue is a toothless advisory committee to "review" staffing issues.</P>
<P>"They've never given anything on mandatory overtime," Cathy, a nurse at Brockton for 22 years, told <I>Socialist Worker</I>. "They deny there's a problem in the first place."</P>
<P>Nurses are also battling an injunction against their picket line. "The injunction was uncalled for," says Anne. "On the first day, we blocked cars. But we didn't block the ambulances. They know that."</P>
<P>Strikers have won support from MNA members and other unionists, who have come out to walk the picket lines. "It's about people coming together in a community for what's right," said Anne.</P>
<I><P>Send messages of support and contributions to: Brockton Hospital Nurses Strike Fund, 707a Centre Street, Brockton, MA 02302.</P></I>
