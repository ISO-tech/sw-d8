<p><a name="Berlin"></a><font face="Times New Roman, Times, serif" size="4"><b>Berlin Health and Rehabilitation</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by MARY McBRIDE</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BURLINGTON, Vt.--More than 200 people came together July 28 for a Right to Organize march to support workers at the Berlin Health and Rehabilitation Center. </P>
<P>The march marked nearly one year since the nursing home workers voted to be represented by United Electrical workers union Local 254 and began their struggle for a fair first contract.</P>
<P>The Berlin home is owned by CPL SubAcute, a Canadian-based for-profit company that has spent huge sums on union-busting lawyers to try to stop the union.</P>
<P>Meanwhile, 80 percent of the workforce at the center is paid less than the living wage standard set by the Vermont state legislature. "A company that spends $400 to $500 an hour on lawyers can afford to pay us a livable wage," said one worker.</P>
<P>The other key demand of the nurses' aides and kitchen and maintenance workers is an end to short staffing.</P>
