<p><a name="South"></a><font face="Times New Roman, Times, serif" size="4"><b>BellSouth</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by a CWA Local 1109 steward</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ATLANTA--Regional phone giant BellSouth and the Communications Workers of America (CWA) reached a tentative contract deal August 4.</P>
<P>As negotiations deadlocked in the run-up to the August 4 contract deadline, 96 percent of the CWA's 44,000 members working at Bell South voted to authorize a strike.</P>
<P>The sentiment to strike persuaded management to back off of giveback demands like elimination of double time and increased employee contributions for health care.</P>
<P>More than 200 members of CWA Local 3204 and their supporters marched and rallied in Atlanta August 3 to demand a fair contract. "We had UAW, the painters' union, even pilots out today!" said steward James "Gadget" Wallace. "And we'll stand alongside any union that's fighting for its rights. People are going to see that the union is the only way the workers can win."</P>
<P>The three-year deal, which covers nine southeastern states and Puerto Rico, contains a 12.35 percent raise, improved rules to deal with job stress for call center workers, health benefits for same-sex domestic partners, pension increases and increased vacation time and days off. </P>
<P>But the agreement failed to resolve the important issue of organizing workers in BellSouth subsidiaries. </P>
<I><P>Craig Michael Johnson contributed to this report.</P></I>
