<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="4">Union heads back Bush's energy plan</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Which side are they on?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 17, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE BUSH found some unexpected allies last month when the U.S. House debated his bill to allow oil and gas exploration in the Alaska National Wildlife Refuge (ANWR). Twisting arms to get the Democrats in line for Dubya was none other than the International Brotherhood of Teamsters and the AFL-CIO.</P>
<P>"It makes us more independent of Saddam Hussein and other people who control our oil," Teamsters President James Hoffa said in defense of ANWR drilling. "It creates jobs."</P>
<P>Never mind that the U.S., having imposed murderous sanctions on Iraq for a decade, doesn't rely on Saddam Hussein to fill up the gas tank. Or that oil in Alaska is limited and expensive to obtain--not to mention the environmental destruction involved.</P>
<P>An environmentally responsible national energy plan that promoted renewable resources would create far more jobs than ANWR.</P>
<P>It may not be surprising that the conservative Hoffa would forget about "Teamsters and Turtles"--and cozy up to Bush and the oil barons instead. Yet the AFL-CIO supported ANWR drilling, too.</P>
<P>That move shocked some in the labor movement. But it reflects labor's strategy of partnership with employers--and, by extension, the Bush administration that serves them.</P>
<P>For example, the United Auto Workers last month worked with automakers and the White House to lobby dozens of Democrats to vote down higher fuel efficiency standards for SUVs. Meanwhile, International Association of Machinists President Thomas Buffenbarger devoted the cover of his union's magazine to supporting Bush's Star Wars missile defense program--with the blaring headline "Bombs Bursting in Air."</P>
<P>"Which of our cities will they target?" Buffenbarger wrote. "Anchorage? Honolulu? Seattle? Portland? San Francisco? Los Angeles? Machinists build many of the weapons systems that defend us today. And we want to be darn sure that Americans living in the cities most at risk...can rest easy in the future."</P>
<P>With tens of thousands of union jobs being eliminated in Boeing's commercial airplane division, Buffenbarger wants new union jobs in the defense industry--even at the cost of a terrifying new arms race.</P>
<P>Apparently, union leaders are willing to forgive Bush for abolishing workplace safety standards, preempting major airline strikes, eliminating pro-union policies in federal contracting and trying to restrict unions' political activities--all in the name of "realism."</P>
<P>Unfortunately, labor's biggest political blow to Bush so far is the Senate's opposition to so-called "NAFTA trucks"-Mexican trucks that would be driven more than 20 miles into the U.S. The Teamsters' fear-mongering campaign over "unsafe" Mexican trucks undercuts international labor solidarity and slides backward into immigrant bashing.</P>
<P>The AFL-CIO Executive Council did take a step in the right direction this month when it endorsed the demonstrations against the International Monetary Fund and World Bank in late September. That's the kind of action that can contribute to building a real fight against Bush and his corporate backers.</P>

