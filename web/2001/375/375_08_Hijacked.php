<font face="Times New Roman, Times, serif" size="3"><P>THE FOUNDING of Israel in 1948 is celebrated as a historic achievement of the Jewish people in their long fight against anti-Semitism. Israel has been called "a land without people for a people without a land."</P>
<P>But to claim this means insisting, as Zionist leader Golda Meir famously declared, that "Palestinians do not exist."</P>
<P>In fact, some 800,000 Palestinians were expelled from their homeland during the Zionist war of terror that led to the creation of Israel.</P>
<P>There are many myths about the founding of Israel. For example, Palestinians supposedly fled because they were told to do so by radio broadcasts from other Arab countries.</P>
<P>This is a complete lie. And thanks to Israel's recent release of secret intelligence files, there's even more evidence of it.</P>
<P>The number one reason that Palestinians fled, Israel concluded, was "direct, hostile Jewish operations against Arab settlements."</P>
<P>In its 1967 war, Israel stole more Palestinian land--the Occupied Territories of Gaza and the West Bank.</P>
<P>Jews anywhere in the world can settle in Israel. But Palestinians are denied this same right.</P>
