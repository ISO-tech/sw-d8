<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The fighting legacy of Harry Bridges</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by RANDALL CHILDS, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE 100th anniversary of the birth of legendary International Longshore and Warehouse Union (ILWU) leader Harry Bridges was celebrated July 28. That day, the ILWU organized a march of thousands of union members and supporters in the Los Angeles harbor to celebrate his legacy.</P>
<P>This celebration was noteworthy because Harry Bridges was known for advocating militant strategies that won major gains. However, Bridges' socialist politics were not mentioned at the July 28 celebration.</P>
<P>Typical is a leaflet from the ILWU-sponsored Harry Bridges Institute, which reads in part: "Through Bridges' leadership, union members can look back through the years and say they brought home good wages, sent their children to college and received pensions with health and welfare benefits...This is all because of the leadership of Harry Bridges, the greatest labor leader in the whole country."</P>
<P>Ironically, Bridges himself would have disagreed. In a 1936 interview, he said, "I prefer not to be publicized as an individual. My personal background and life are unimportant. The movement is important."</P>
<P>The reality is somewhere in between these two statements. Unionism took root among West Coast dockworkers in the early 1930s because of low wages, dangerous conditions and the humiliating "shape-up" system that forced longshoremen to literally beg for jobs.</P>
<P>In the 1934 West Coast waterfront strike and San Francisco General Strike, the union shut down the ports with mass pickets to keep scabs off the docks, at times violating court injunctions to do so. They even battled the cops repeatedly to defend their strike despite the police killing of six strikers.</P>
<P>The union won longshoremen better pay, health care, better job safety, and a union-controlled hiring hall to prevent the favoritism and discrimination of the "shape-up" system.</P>
<P>This couldn't have happened without the conscious, united action of thousands of workers. That said, the leadership provided by Harry Bridges also played an important role.</P>
<P>Bridges moved to this country from his native Australia in 1920 and joined the Industrial Workers of the World in 1921, participating in a nationwide seamen's strike. Eight years later, Bridges was part of a rank-and-file longshoremen's group more or less under the leadership of the Communist Party (CP) and was a key contributor to <I>The Waterfront Worker </I>newsletter.</P>
<P>Bridges' experience allowed him to give a voice to the militancy that exploded among waterfront workers in 1934.</P>
<P>Every step of the way, the rank-and-file longshoremen butted heads with conservative officials of the International Longshoremen's Association (ILA). When ILA officials told the longshoremen that government hearings would get them unionized, Bridges argued that workers should rely on the power of a strike.</P>
<P>"Those hearings were a lot of shit," he said. Bridges led the push for a coast-wide contract to keep shipping bosses from playing one port off against another. He also argued for organizing Black and white workers together in the same union. </P>
<P>In 1937 the ILWU was formed out of a split from the conservative East Coast-based ILA. The ILWU joined the Congress of Industrial Organizations (CIO). But amid the Cold War anti-Communist witch-hunts, the CIO expelled the ILWU in 1950.</P>
<P>The union was to remain outside the AFL-CIO for 40 years because mainstream union leaders hated the ILWU's left-wing politics.</P>
<P>As a close collaborator with the Communist Party, Bridges' politics were distorted by the CP's support for Stalin's Russia. And as a union president, he carried out policies that he would have opposed as a young rank-and-file militant.</P>
<P>But his life shows how important role socialists can play in building rank-and-file organization that can lead the struggle forward when union officials fail to do so--and the possibility of building a fighting union. And that legacy holds plenty of lessons for us today.</P>

