<P><font face="Times New Roman, Times, serif" size="5"><b>I get 22 years and the cops go free</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I></P>
<P>I'm in prison in the state of Rhode Island because I was scapegoated by the Providence police and the state attorney general's office. I was defending myself and people that I was with against a deadly attack by several people armed with box cutters, pipes and broken bottles.</P>
<P>I was assaulted several times, and that's when I grabbed a firearm that belonged to my girlfriend at the time. The police were called, and when they arrived, an off-duty police officer came out of the diner, and the cops shot him to death (the off-duty officer was Black, and the cops that shot him were white).</P>
<P>After that, I was charged with felony murder, possession of a firearm, creating a crime of violence while armed and two assaults. I was given 12 years in prison for violating my parole for the gun.</P>
<P>The Feds indicted me for the gun, too (felon in possession of firearm), and they gave me the maximum penalty of 10 years.</P>
<P>I had to plead no contest for the gun again in state court, in return for the other charges being dropped. They dropped the felony murder charge when they cleared the cops in the shooting.</P>
<P>So I'm serving 22 years for defending myself--and the cops go free. Where is the justice in Rhode Island?</P>
<B><P>Aldrin Diaz,</B> Cranston, R.I.</P>
<I><P>To find out more about Aldrin's case, contact the Campaign Against Wrongful Convictions at 401-273-8312.</I></P>

