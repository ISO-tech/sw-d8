<P><font face="Times New Roman, Times, serif" size="5"><b>Arab students at UC-Davis targeted</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SNEHAL SHINGAVI</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DAVIS, Calif.--The FBI and local authorities launched a campaign of persecution against Arab and Arab American students after an anti-Semitic attack near the campus of the University of California (UC)-Davis.</P>
<P>Hillel House, a Jewish community center, was vandalized on May 9. Davis and nearby Sacramento have been the scene of hate crimes in the past. Last year, two members of a white supremacist group were convicted for their involvement in synagogue burnings. </P>
<P>But when the FBI and Davis police began their investigation, they immediately started profiling Palestinian American students. Several students were met by federal agents at their dorm rooms and questioned about their involvement.</P>
<P>Despite the fact that police had eyewitness statements saying that four white youths were seen fleeing from the scene, local cops and campus administrators issued stern warnings to Arab, Muslim and pro-Palestinian groups at UC-Davis. The local media even published rumors that Palestinian American students had been overheard plotting the attack. </P>
<P>This racial profiling is disgusting enough. But Palestinian solidarity activists believe that it was retaliation.</P>
<P>Students at Davis and Berkeley have been organizing to pressure the UC system to divest more than $6.4 billion from the Israeli economy.  Just a few days before the incident, Students for Justice in Palestine organized a sit-in at UC-Berkeley. Thirty-two people were arrested in a civil disobedience action.</P>
<P>We need to send a clear message that we won't tolerate the scapegoating of Arabs.</P>

