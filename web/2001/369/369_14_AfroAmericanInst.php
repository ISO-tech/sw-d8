<P><font face="Times New Roman, Times, serif" size="4">Northeastern student occupation for African American Institute</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We've been lied to for so many years"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by MITCH LEWIS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--Hundreds of Northeastern University (NU) students occupied the intersection of Huntington and Massachusetts Avenues on May 10, blocking traffic for hours. The protest came in reaction to University President Richard Freeland's decision to tear down the African American Institute and recreate it within a new, larger building.</P>
<P>"I believe that the new home of the John D. O'Bryant African American Institute demonstrates Northeastern's continued commitment to the African American community and the diversity goal of the university," Freeland said in a statement.</P>
<P>The university administration has long cited a need to tear down the Institute in order to "make the best use of space" on campus. But students aren't falling for Freeland's empty rhetoric.</P>
<P>"We are upset because we've been lied to for so many years," said Ibiere Seck, the president of the campus's Black Student Association. "They did not meet our key demand: that the institute remain a free-standing building."</P>
<P>"We've seen how African American institutes have been shrunk to near nothing in universities all over the country through 'plans' to 'incorporate' them into new buildings," NU sophomore Justin Brown said at the rally. "If there's one thing that's clear, Northeastern University doesn't give a shit about Black people."</P>
<P>Students had occupied the Institute for a month in the run-up to the protest. While other university buildings are renovated regularly, the Institute has never undergone much more than a repainting in its 30-year existence.</P>
<P>Until recently, the active opposition had been largely confined to a small number of the university's Black students. The May 10 protest, however, drew between 500 and 1,000 students of all backgrounds. "This is not just a Black issue, this is a student issue," one African American student told the crowd on a bullhorn, to wild applause.</P>
<P>Students have vowed to keep fighting, even through the final exam period if necessary. The Northeastern protests--and recent student sit-ins to demand a living wage for Harvard workers--show the tremendous eagerness for students to take action.</P>

