<P><font face="Times New Roman, Times, serif" size="4">Warplanes bomb Palestinian town</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's terror from the skies</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LANCE SELFA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL'S WAR against the Palestinians took a huge leap in firepower as Israeli warplanes bombed a Palestinian police station May 18. This was Israel's first use of jet fighters against Palestinian targets since the 1967 war.</P>
<P>The attack leveled the main target--a three-story Palestinian police station and jail in the town of Nablus. Eleven people died in the bombardment.</P>
<P>But the target of the attack, Mahmoud Abu Hanoud--a leader of the Palestinian group Hamas who was behind bars in the Palestinian Authority's jail--escaped Israel's aerial assassination attempt with an injury.</P>
<P>Meanwhile, Israeli forces used missiles and helicopter gunships to strike at other targets, injuring at least 50 Palestinians in Jenin and Tulkarm.</P>
<P>Israel claimed that the onslaught was retaliation for a May 18 suicide bombing that killed five Israelis at a shopping mall in Netanya.</P>
<P>But well over 90 percent of the casualties since the beginning of this round of violence in September have been Palestinian. Israel's brutal and cold-blooded repression has produced such a climate of despair that some Palestinians see suicide bombings as the only way to strike back.</P>
<P>One day after Israel's warplane attack, representatives of the Arab League, meeting in Cairo, Egypt, called on member states to stop contacts with Israel until the Israeli government declares a cease-fire. The action caught the attention of the U.S. government, prompting a plea for a cease-fire from Secretary of State Colin Powell, which Israel ignored.</P>
<P>Whether Arab governments will act on the call for a boycott of Israel remains to be seen. For decades, Arab governments have pledged their solidarity with the Palestinians--but done little in real terms to support them.</P>
<P>None of the diplomatic intrigue meant much to ordinary Palestinians. To them, there's no point in restarting a "peace process" whose intent is to force Palestinians to give up their land and rights at the bargaining table instead of at gunpoint.</P>
<P>Since taking office in March, hard-line Israeli Prime Minister Ariel Sharon has escalated the military terror, assuming that more repression will force Palestinians to accept his terms for the surrender of their homeland.</P>
<P>But the violence is causing the opposite--leaving Palestinians more determined to resist. As if to underscore this reality, thousands filled the streets of Nablus May 19 to protest Israel's attacks.</P>
<P>"Tens of thousands of people jammed the city center, squeezing shoulder-to-shoulder in the Martyrs' Square; they lined the rooftops, and peered out of every office window," reported Phil Reeves of Britain's <I>Independent</I> newspaper.</P>
<P>"Everywhere there were weapons and wild gunfire, mostly Kalashnikovs blasting away at the heavens. Every main Palestinian faction seemed to be there, from the mainstream Fatah to the Popular Front for the Liberation of Palestine to the hooded young men of Hamas&#133;A pulsing sea of young men followed in their wake, chanting 'revenge, revenge.' There were no signs here of the Intifada fatigue that the Palestinians complained of a few months ago. No one wanted to talk any more about compromise or 'concessions.'"</P>

