<P><font face="Arial, Helvetica, sans-serif" size="2"><b>PHILIPPINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Election setback for Arroyo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by EDUARDO CAPULONG</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CONGRESSIONAL ELECTIONS held May 14 produced no clear winner after one of the bloodiest campaigns in the history of the Philippines.</P>
<P>An estimated 85 percent of voters went to polls in an election to pick 13 of 24 senators, all 262 members of the House and more than 17,000 local officials. According to official records, 98 people--including one outgoing member of Congress and four candidates--were killed and 138 people wounded during the run-up to the election. </P>
<P>Official and unofficial reports documented systematic ballot tampering, vote buying, violence and widespread disorganization. "This is probably the worst election in recent memory," said Raymond Villanueva of the Alliance of Concerned Teachers union, which staffed the polls. "Hundreds of thousands of people were denied the right to suffrage. What they gave us was the right to suffer."</P>
<P>The elections failed to give President Gloria Macapagal Arroyo and her ruling People Power Coalition--which was swept to power by a popular uprising in January--the solid mandate she wanted.</P>
<P>Last month, the Arroyo government was rocked by a rebellion led by allies of former President Joseph "Erap" Estrada. Arroyo faces the embarrassing prospect of a Senate that will continue to include instigators of the rebellion she barely survived. </P>
<P>According to some reports, Sens. Juan Ponce Enrile and Gregorio Honasan, who have been charged with sedition, are among the "quick-count" front-runners. Enrile and Honasan are right-wing thugs. But Arroyo's setback at the polls is further evidence of the frustration of the country's poor.</P>
<P>Left-wing parties that fielded their own candidates picked up seats in the House. Coming in the wake of last January's popular uprising, this shows the potential for building a political alternative to both Estrada and Arroyo.</P>

