<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is it enough to curb the system's excesses?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN SOMEONE is described as having "liberal" ideas about, for example, women's rights or racial equality, that's positive. "Liberal" in this sense means simply the opposite of conservative.</P>
<P>But in the world of politics, "liberalism" has a more definite meaning. Marxists are for women's rights and against racial discrimination. But Marxists are not only not liberal; we are, in certain ways, opposed to liberalism.</P>
<P>Liberals criticize some aspects of the system. But they do so with the aim of finding ways of improving the institutions they criticize.</P>
<P>The so-called progressive movement of the early 20th century was a liberal reform movement. It tried to curb the excesses of big business, break up monopolies and trusts and eliminate corruption. </P>
<P>The reformers saw themselves as saving the system from its own excesses--making improvements in order to prevent more drastic revolt. They were very clear about their moderate aims--as this passage that prefaced a 1912 muckraking expose of judicial corruption makes clear:</P>
<P>"This is presented as the first step in the important work of making conditions wholesome enough to restore people's confidence. It is our hope that the publication of the truth will lead to that reform of the judiciary which is necessary to remove the causes of discontent. Because we respect judges as a class, we insist upon exposing those who are bringing them into discredit."</P>
<P>What would a Marxist version of this paragraph look like? "This is presented as a first step in the important work of removing people's confidence in the judiciary. Because money and power grease the judicial machinery, an honest judge is the exception rather than the rule. It is our hope that the publication of the truth will, in provoking a movement for judicial reform, lead activists to conclude that the whole judicial system is rotten."</P>
<P>Liberals want to "remove the causes of discontent" in order to "restore confidence" in the system--whereas socialists want to destroy confidence in the system because the system is fundamentally flawed.</P>
<P>So, for example, where a liberal might encourage capitalists to invest more "responsibly," Marxists attack the whole profit system for putting greed over human need. Liberals fear collective struggle and working-class revolt, whereas socialists welcome it.</P>
<P>That doesn't mean that liberals won't sometimes participate in mass movements. But when they do, they do so in order to ensure that things don't "get out of hand."</P>
<P>The fundamental political difference between liberalism and Marxism is that liberalism sees a social problem--such as America's role as the world's bully--as bad policy, not as something that flows out of U.S. economic and military interests.</P>
<P>For example, liberal commentator Robert Borosage, in a book critical of high military spending, argues that with higher taxes the U.S. government could afford to "police the world&#133;and invest in vital social and economic needs at home." In the same book, another writer, William Hartung, argues that the U.S. military budget "sends the wrong signal by implying that force is still the ultimate arbiter of international disputes."</P>
<P>But what other signal could the U.S. possibly send? Force is indeed the final arbiter of international disputes in a system of states that compete economically and militarily for control of the world's resources.</P>
<P>Hartung and Borosage want the U.S. government to carry out a nicer foreign policy--one that doesn't murder thousands of Iraqi children every month through economic sanctions, one that doesn't slaughter 2 million Vietnamese people, one that doesn't provide aid to a Colombian military that works hand in glove with murderous, drug-running death squads.</P>
<P>But imperialism with a human face is a contradiction in terms. The problem with the liberal approach is that it seeks, to paraphrase the Polish Marxist Rosa Luxemburg, surface modifications in the existing society rather than a fundamental transformation. </P>
<P>So no matter how well they criticize aspects of the system, they end up apologizing for it. Surface modifications leave the basic structures intact. And it's the basic structures that need to be dismantled.</P>

