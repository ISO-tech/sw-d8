<P><font face="Arial, Helvetica, sans-serif" size="2"><b>HAITI</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Orange workers fight anti-union violence</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 13</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ORANGE WORKERS and local farmers who occupied a plantation in northern Haiti are appealing for support in their fight against anti-union harassment and violence.</P>
<P>Workers at a plantation run by the Haitian company Guacimal, which supplies oranges used by the French firm R&eacute;my Cointreau to make its famous liqueur, unionized in October of last year. But management refused to negotiate, sparking a strike.</P>
<P>The company and local government officials used violence and intimidation to break the strike, and the plantation's overseer tried to bar union members from returning to work.</P>
<P>At the beginning of the April-to-August "off season"--when oranges aren't harvested, and laid-off plantation workers and local farmers grow their own crops on small parcels of plantation land--the overseer barred union members from getting plots.</P>
<P>After a series of protests, a plantation guard beat a child for picking oranges. This sparked the occupation at the end of April.</P>
<P>The workers and farmers say that they won't share their harvest with the overseer or guards until the violence and intimidation ends.</P>
