<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Lying lawyer in hot water</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ALAN MAASS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--The Bush administration was scrambling to save Theodore Olson's nomination as <I>Socialist Worker</I> went to press.</P>
<P>Olson is George W. Bush's choice for solicitor general, the lawyer who represents the U.S. government before the Supreme Court.</P>
<P>No one's ever thought of Olson as anything other than a hardened right-winger. But like John Ashcroft and Tommy Thompson before him, Olson was expected to sail through confirmation hearings.</P>
<P>Then came Olson's straight-faced claim before the Senate Judiciary Committee that he knew nothing about the Arkansas Project, the $2.4 million campaign organized in the 1990s to smear Bill Clinton.</P>
<P>Press investigations have exposed Olson as the chief lawyer and consigliere for the Arkansas Project. This was too much even for meek Senate Democrats to swallow. </P>
<P>The Senate Judiciary Committee deadlocked on party lines in a 9-9 tie on confirmation--which normally would be enough to kill a nomination. But Majority Leader Trent Lott (R-Miss.) is expected to use a parliamentary loophole to force a vote in the full Senate, where he can hope for support from Bush-loving renegade Democrats like Sen. Zell Miller (D-Ga.).</P>
<P>Sen. Patrick Leahy (D-Vt.), the ranking Democrat on the committee, says that Olson's testimony "leaves many, including myself, wondering whether the whole truth has come out." Translation: The man who Bush wants to be the country's top lawyer lied through his teeth.</P>
<P>Olson was hired by <I>American Spectator</I> magazine--the main publication where dirt from the Arkansas Project showed up in print--as its lawyer in 1994. He even coauthored--under a pen name--some of the magazine's anti-Clinton diatribes.</P>
<P>David Brock, a former investigative reporter for the magazine, said Olson participated directly in discussions of Arkansas Project stories. What's more, there's continuing speculation that witch-hunter-in-chief Ken Starr used Arkansas Project research to further his inquisition of the Clintons.</P>
<P>But Olson isn't trying to dance around the issue. He denies any involvement in the Arkansas Project--and Republicans are defending him to the hilt.</P>
<P>No wonder. For them, Olson's right-wing credentials are to die for. He got his start in politics as a lawyer for the Reagan administration, where he came up with the legal reasoning to fire striking air traffic controllers. In private practice, Olson led the legal fight to get affirmative action overturned at the University of Texas among many other high-profile cases.</P>
<P>But he earned the Bush gang's undying gratitude last year as their lead lawyer in <I>Bush v. Gore,</I> arguing for the Supreme Court to stop the recount of disputed ballots in Florida and install Dubya in the White House.</P>
<P>No wonder they want Olson as their lawyer.</P>

