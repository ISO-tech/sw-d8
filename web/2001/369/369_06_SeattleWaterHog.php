<font face="Times New Roman, Times, serif" size="5"><b>Seattle's water hog</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE CITY of Seattle is experiencing a drought emergency. So Seattle Public Utilities has asked its residents to reduce water use by 10 percent.</P>
<P>But it turns out that the 5-acre estate of Microsoft billionaire Bill Gates uses 60 times as much water as a typical homeowner--so cutting back 10 percent would supply about six homes with water for a year.</P>
<P>Last year, Gates' home--with its 60-foot pool, sauna and indoor-outdoor spa--used 4.7 million gallons of water and racked up a $24,828 water bill. Groundskeepers at the $109 million estate surmise that most of the water went for irrigation and the heating and cooling system.</P>
<P>"When they were made aware of the problem, Bill and Melinda were very surprised and concerned," said family spokesperson Trevor Neilson. "Immediately they asked the staff to look into the cause of the problem."</P>
<P>The <I>Salt Lake Tribune</I> headline on the story captured the issue best: "Billionaire Gates goes through water like it's money." </P>
<I><P>--Salt Lake Tribune,</I> April 27, 2001</P>

<P><font face="Times New Roman, Times, serif" size="4">He was always spoiled</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IF YOU ever wondered if George W. Bush was always such a smug twit, the answer is yes. A former professor of Bush at Harvard Business School remembers him as a "spoiled brat."</P>
<P>"It's safe not to have a romantic illusion about President Bush," warned Yohihiro Tsurumi, who is now a professor of international business at Baruch College of the City University of New York. Tsurumi taught Bush macroeconomic policy and international business at Harvard in the mid-1970s.</P>
<P>Bush, the first U.S. president with a business degree, hasn't changed a bit since his school days, Tsurumi says. "He is more conservative than his father," said Tsurumi, comparing Dubya to former President George H.W. Bush.</P>
<P>He recalled a conversation with the younger Bush about a summer job at a time when his father headed the U.S. liaison office in Beijing. "I told him then, 'You could become a fraternity president, but not a CEO,'" the Japanese professor said. "He has been totally dependent on his father."</P>
<P>--<I>Japan Today News,</I> May 17, 2001</P>

<P><font face="Times New Roman, Times, serif" size="4">Trying to eliminate Arafat through melting</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK politicians want to melt Palestinian Authority President Yasser Arafat.</P>
<P>It turns out that Madame Tussaud's Wax Museum in Manhattan has a statue of Arafat. But a group of New York state legislators--including Gov. George Pataki--are calling for its removal.</P>
<P>Legislator Dov Hikind--a fanatical supporter of Israel and a vocal opponent of Arafat--held a demonstration outside the museum. Hikind said the wax figure "glorifies Arafat and what he does."</P>
<P>"Arafat has orchestrated a campaign of premeditated violence against Israeli citizens," said Hikind.</P>
<P>But the museum's general manager, Janine Scarpello, said the Arafat figure wasn't going anywhere. "The decision to portray an individual is irrespective of any political or religious stance," said Scarpello.</P>
<P>Hussein Ibish--a spokesperson for the American-Arab Anti-Discrimination Committee--described Hikind's "campaign" as "depressing nonsense" that shows support for Israel's brutal repression of Palestinians is "infinitely more extreme" than anywhere else.</P>
<P>"Politically and psychologically, they pretend Palestinians don't exist," said Ibish. "They think that if you just melt the wax figure of a Palestinian, then everything would be okay."</P>
<P>--Reuters, May 17, 2001</P>

