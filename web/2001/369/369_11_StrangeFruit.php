<P><font face="Times New Roman, Times, serif" size="4">Telling the story of the song "Strange Fruit"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A song that changed the world</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> David Margolick, <B><I>Strange Fruit: The Biography of a Song</B>,</I> Ecco Press, 2001, 144 pages, $12.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by NICOLE COLSON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P><i>Southern trees bear a strange fruit/<BR>
Blood on the leaves and blood at the root/<BR>
Black body swinging in the Southern breeze/<BR>
Strange fruit hanging from the poplar trees.</i></P>
<P>IT'S EASY to see why <I>Time</I> magazine voted "Strange Fruit" the Best Song of the Century, and why the British magazine <I>Q</I> called it one of "10 songs that actually changed the world."</P>
<P>Listening to a recording of Billie Holiday sing "Strange Fruit" is an electrifying experience. Written by schoolteacher Abel Meeropol, the song--an agonizingly poetic description of a Southern lynching--may be the most compelling antiracist song ever recorded. </P>
<P>David Margolick's fascinating book <I>Strange Fruit: The Biography of a Song</I> tells the history of the song's creation--and how it's been received over the past 60 years.</P>
<P>Between 1889 and 1940, 3,833 Blacks were lynched in the U.S., according to official records. Unofficially, the number was much higher. A 1939 poll found that 60 percent of Southerners believed that lynching was acceptable.</P>
<P>This barbarism inspired Meeropol to pen "Strange Fruit." Meeropol wrote the lyrics in 1937 and published them in his union newsletter after seeing pictures of the lynching of two young Black men from Marion, Ind. He later composed the music, and the song became a standard at left-wing events.</P>
<P>Today, Meeropol is better known for having adopted the children of Ethel and Julius Rosenberg following their execution at the height of McCarthy-era hysteria. But in the 1930s, he was a prolific poet and songwriter with a gift for crafting political songs. Contemporary composers Ira Gershwin and Kurt Weill admired his work.</P>
<P>In 1939, Meeropol played the song for Billie Holiday. He persuaded her to add it to her act at New York's Caf&egrave; Society, one of the only racially integrated, left-wing clubs in the city at the time.</P>
<P>Margolick's book conveys the impact of the song. "Even now, as I think of it, the short hair on the back of my neck tightens, and I want to hit somebody, and I think I know who," wrote <I>New York Post</I> columnist Samuel Grafton in a 1939 review. "If the anger of the exploited ever mounts high enough in the South, it now has its 'Marseillaise.'"</P>
<P>Margolick notes that "Strange Fruit" wasn't universally praised. Performer and political activist Paul Robeson apparently felt the song portrayed Blacks as victims, and record producer Jerry Wexler said that "it's got too much of an agenda."</P>
<P>But Margolick shows that the negative reviews were a minority. He rightly argues that the politics of the song--combined with Holiday's unique style--make it so gripping and moving.</P>
<P>So it's disappointing that the book doesn't provide a lot of information about how the song was used in a political context. Margolick mentions in passing that "Strange Fruit" was sung at political events--including a 1939 fundraiser for antifascists during the Spanish Civil War and a 1943 benefit for Ben Davis Jr., a Black Communist Party member elected to the New York City Council.</P>
<P>For the most part, however, these details are skimmed over--in favor of describing Holiday's artistry and the personal impact that the song had on audience members. But that's a small flaw in an otherwise outstanding book.</P>

