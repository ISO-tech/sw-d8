<P><font face="Times New Roman, Times, serif" size="4">McVeigh case latest in a series of high-profile fiascoes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>FBI blunders exposed</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ERIC RUDER on the missing documents in the McVeigh case.</P>
</B><P>WASHINGTON--Less than a week before the scheduled execution of Oklahoma City bomber Timothy McVeigh, Attorney General John Ashcroft revealed that the FBI bungled the case. The Feds failed to turn over more than 3,000 pages of documents to McVeigh's defense team, a clear violation of the right to a fair trial.</P>
<P>As a result, Ashcroft was forced to delay McVeigh's execution for a month--and admit that the government messed up one of the highest-profile criminal cases in U.S. history.</P>
<P>Danny Defenbaugh, an FBI agent on the McVeigh case, told a Senate committee that he knew for months about the documents--but didn't tell his superiors because he was "trying to determine how bad the problem was."</P>
<P>In his testimony before the Senate, FBI Director Louis Freeh said the buck stopped with him. But he also pointed a finger at "a cultural defect in his agency when it comes to following commands," according to CNN.</P>
<P>The blunder further undermines the FBI's credibility as it struggles to bury the memory of other recent fiascoes--like the incineration of more than 80 people during its assault on the Branch Davidian compound in Waco, Texas, and the violent end to the agency's Ruby Ridge standoff with white supremacists in Idaho.</P>
<P>Incredibly, the setback has transformed the McVeigh case--which was held up as a prime example of why the death penalty is needed--into an embarrassing reminder of everything that's wrong with capital punishment.</P>
<P>Oklahoma City, for example, is the setting for another death penalty scandal. Authorities now admit that police lab worker Joyce Gilchrist falsified medical results and gave perjured testimony to help prosecutors make convictions.</P>
<P>During her 20-year career, Gilchrist helped put 23 people on death row--12 of whom have already been executed. Oklahoma Gov. Frank Keating has ordered a review of all 3,000 felony convictions that Gilchrist played a role in.</P>
<P>The McVeigh case offers further proof of the injustices of the criminal justice system. The difference is only that its usual victims are poor and Black--and the mistakes, errors and violations of due process in their cases rarely get corrected.</P>
<P>In fact, the federal government's death row is more racist than any state's--fully 87 percent of inmates are nonwhite. How many of their cases were marked by the same incompetence--or even intentional foul play? How many never even had a shot at a fair trail?</P>
<P>That the argument against capital punishment has been strengthened by the McVeigh case is testament to the growing questioning of America's broken death penalty system. We need to put an end to the death penalty.</P>

