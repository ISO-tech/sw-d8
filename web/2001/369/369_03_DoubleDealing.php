<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">White House con men sell their tax and energy schemes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Double-dealing by Dubya</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DUBYA WANTS working people to know that he's concerned about the high price of gas and skyrocketing electric and heating bills.</P>
<P>His solution? Get Congress to pass his $1.35 billion tax cut right away. "If I had my way, I'd have it in place tomorrow, so that people would have money in their pockets to deal with high energy prices," Bush said at a May 11 news conference.</P>
<P>One reporter pointed out that a tax cut which still hasn't passed won't be much help with $3-a-gallon prices expected this summer. He could have added that if people spend their tax cut at the pumps, it will only put more money into oil company bank accounts.</P>
<P>And of course, most of the tax cut will go to the superrich--who don't exactly need extra cash to fill up their Jaguars and Porsches.</P>
<P>But Bush, as usual, didn't let the facts get in the way. "Let me say it again, see if I can be more clear," he stammered. "To the Congress, who is interested in helping consumers pay high gas prices: Pass the tax relief as quickly as possible."</P>
<P>Of course, Bush's tax cut proposal isn't about helping consumers. Neither is the energy plan. "You can summarize the president's energy policy as real men dig, drill and burn, and conservation is for wimps," said Philip Clapp, president of the National Environmental Trust. </P>
<P>And Democrats, as usual, are caving. Not only are they letting Bush get away with packaging his energy plan as pro-consumer, they're giving in on the tax cut.</P>
<P>House Minority Leader Dick Gephardt (D-Mo.) claims that the Democrats "forced" Bush to drop $450 million from his proposal. But the final figure is $1.35 billion--more than what Bush promised during his campaign!</P>
<P>"The tax cut cannot be paid for except by raiding the Social Security and Medicare trust funds," wrote the <I>New York Times</I> in an editorial that estimated its real cost at $4 trillion. "It is a scheme that seems deliberately aimed at wrecking the basic American retirement programs, perhaps to force their dismantling or privatization."</P>
<P>It's no surprise that the Democrats, who've rolled over for every Bush attack so far, would surrender. More alarming, though, is the fact that some top union leaders are climbing on the bandwagon.</P>
<P>Conservative International Brotherhood of Teamsters President Jimmy Hoffa might be expected to back Bush's energy plan. But it was shocking to see 30 union leaders flock to the White House May 14 to hear about jobs that would supposedly be created by the plan.</P>
<P>Bush's real aim is to split the labor-environmental coalition that came together in the protests against the World Trade Organization in Seattle in 1999. But Bush isn't just pitting "Teamsters and turtles" against one another. He's cultivating Carpenters' President Doug McCarron--whose union just left the AFL-CIO--to split the labor movement itself.</P>
<P>Yet labor leaders who cozy up to Bush in hopes of getting some new union jobs today are only giving him the political cover to smash entire unions tomorrow. Bush has already blocked new workplace safety rules, scrapped project labor agreements for government construction projects, pushed for "paycheck protection" to stifle unions' political voice and preemptively banned all major airline strikes.</P>
<P>Labor leaders shouldn't pal around inside the White House. They should call protests outside.</P>
<P>Opinion polls consistently show that big majorities oppose Bush's agenda--from taxes to the environment to abortion rights. And a recent Pew Center poll shows that more than one in four union members "strongly disapprove" of Bush--more than any other group polled.</P>
<P>It's time to turn that sentiment into a fighting opposition to Bush and his corporate sponsors.</P>

