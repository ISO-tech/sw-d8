<P><font face="Arial, Helvetica, sans-serif" size="2"><b>TURKEY</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Huge protests against harsh cuts</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Turkey rocked by new unrest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 13</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE POLITICAL and economic crisis in Turkey sparked new unrest in May, while a hunger strike by hundreds of political prisoners took more victims.</P>
<P>As many as 800 of Turkey's 12,000 political prisoners and their relatives are on hunger strike to protest plans to move them into special isolated units, known as "F-type prisons," where they can be more easily tortured or murdered.</P>
<P>Some 22 hunger strikers have already died, and another 80 were reported near death as <I>Socialist Worker</I> went to press. The vast majority of the political prisoners are members and supporters of left-wing political parties--including the Kurdish Workers Party (PKK), which has been fighting for decades for civil rights and a separate state for the Kurdish people.</P>
<P>Turkey's prisons are nightmarish. Even the Turkish parliament's own investigation found that 13-year-old children in Istanbul had been subjected to electric shock "therapy."</P>
<P>As the hunger strike continued, the government faced opposition to economic "reforms" pushed through since the crash of the Turkish currency last fall. In return for an emergency loan from the International Monetary Fund (IMF), the Turkish government accelerated its program of privatizing state-owned industries and opening up to foreign investors. The plan included jacking up interest rates, which forced the shutdown of hundreds of businesses and a wave of layoffs.</P>
<P>More than 1 million workers marched in protest in December under signs and slogans condemning the IMF--the biggest demonstration since the military coup in 1980. Then another financial crash in February drove the value of the Turkish currency, the lira, down by another 40 percent.</P>
<P>Western corporations are salivating over gaining new access to the Turkish economy. If Turkey someday succeeds in its long campaign for admission to the European Union (EU), the country would provide a pool of low-wage workers and free access to the EU market.</P>
<P>But workers aren't surrendering to the government's attacks. On April 14, tens of thousands of members of the four main union federations marched in Istanbul. On May Day, another 40,000 demonstrated in the capital, while other protests took place in 44 different localities.</P>
<P>Some demonstrators carried signs and banners expressing sympathy for the political prisoners--a major development in a country where Kurds face terrible oppression and left-wing prisoners are constantly vilified as "terrorists."</P>
<P>Activists in the U.S. should work to expose the IMF's anti-working class program--and support the campaign for the rights of Turkish political prisoners.</P>

