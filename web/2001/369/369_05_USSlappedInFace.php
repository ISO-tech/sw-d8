<P><font face="Times New Roman, Times, serif" size="5"><b>The bully gets a slap in the face</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S ALWAYS nice to see someone get what they deserve. And it was especially nice when the U.S. government was voted out of its seat on the United Nations (UN) Human Rights Commission. A week later, the U.S. lost its seat on a second UN panel, the International Narcotics Control Board--which the U.S. has used to promote its "war on drugs" hysteria.</P>
<P>House Republicans were quick to lash out. Rep. Henry Hyde (R-Ill.) had the gall to declare: "This is a deliberate attempt to punish the United States for telling the truth when it comes to human rights violations around the globe."</P>
<P>Tell that to the victims of the paramilitary death squads in Colombia, which are linked to a government that the U.S. has propped up with billions in military aid. Or Palestinians whose homes were destroyed during the latest onslaught by the U.S.'s Middle East watchdog, Israel. Or the women counseled by international family-planning groups now barred from talking about abortion if they want U.S. aid.</P>
<P>Hyde and his friends in Congress voted to withhold the $244 million that the U.S. owes in UN back dues until the U.S. gets its seat back. Republican goons were joined by leading Democrats, including House Minority Leader Richard Gephardt (D-Mo.), who said that Hyde's blackmail scheme was "the best way to get back on the commission."</P>
<P>But the vote to kick out the U.S. underlines the fact that the U.S. is viewed around the world as the biggest bully on the block.</P>

