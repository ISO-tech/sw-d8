<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Exposing the myth of youth predators</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by KIRSTIN ROBERTS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NATHANIEL BRAZILL, the 14-year-old Florida boy found guilty in May of killing his teacher, is awaiting sentencing. He could spend the next 25 years of his life--almost twice the amount of time he's been alive--in an adult prison.</P>
<P>And that's only because he was convicted on a lesser charge of second-degree murder. Nathaniel could have been found guilty of first-degree murder and sent to prison for the rest of his life with no chance of parole--which is what happened to Lionel Tate, another Florida 14-year-old, earlier this year. </P>
<P>With the media focusing so much attention on cases of juvenile crime, you might think that youth today are more violent and dangerous than ever. In the early 1990s, a researcher from the influential Brookings Institution, John Dilulio, even came up with a theory to explain this supposed teenage crime wave. Dilulio talked about a new brand of "superpredator" youth criminals--"a new horde from hell that kills, maims and terrorizes merely to become known, or for no reason at all," he told Congress.</P>
<P>The mainstream media overwhelmingly bought Dilulio's argument. In a typical example from a 1996 <I>USA Today</I> article, titled "Violent Kids Can't Be Reformed," author Susan Estrich wrote, "There are 40 million children in the United States under the age of 10&#133;People wonder whether there is anything that can be done to stop the potential super-predators among them before it's too late."</P>
<P>Lawmakers also promoted the superpredator hype. Chief among their efforts was legislation to put child offenders on trial as adults--and to throw them in adult prisons if convicted.</P>
<P>But the truth is that the superpredator "theory" is based on lies and racism. First of all, youth violence isn't on the rise--despite the media's frantic coverage of the issue. According to the U.S. Justice Department, despite a steady growth in the juvenile population over the past decade, juvenile violent crime arrests have dropped by 23 percent since 1995. </P>
<P>Second, because the superpredator myth is racist to its core, Black and Latino children have suffered the brunt of the tough-on-youth-crime hysteria. According to the American Civil Liberties Union, three out of four youths standing trial in adult courts are children of color--despite the fact that white youths commit most juvenile crimes.</P>
<P>For those charged with drug offenses, Black youths are 48 times more likely than whites to be sentenced to juvenile prison. And for children charged with violent crimes, white youths averaged 193 days in detention, compared to 254 days for Black youths and 305 days for Latino youths.</P>
<P>One example is Anthony Lester, a 15-year-old mentally disabled boy in Florida who was tried as an adult for armed robbery and extortion, charges that could lead to up to 30 years in an adult prison. His "crime"? Stealing $2 to buy food.</P>
<P>As the politicians have hyped their get-tough laws, programs that have been proven to reduce youth crime--after-school programs, summer-job programs, public mental health services--are being scrapped.</P>
<P>This is the real crime. The resources that could help youths to develop into healthy and happy adults are being wasted on tax cuts for the rich and bigger prisons to lock up the victims of an unjust society.</P>
<P>Meanwhile, Bush claims Washington's cutbacks in social services for the poor can be made up for by faith-based charities.</P>
<P>And guess who he's gotten to run his faith-based initiative? None other than superpredator theorist John Dilulio.</P>
<P>With Bush in the White House, it appears that more scapegoating of America's youth is on the agenda. But public discontent with treating youth offenders as adults is on the rise.</P>
<P>We need to keep challenging the politicians who are prepared to condemn an entire generation of young people to the wasteland of the American injustice system--all for political gain.</P>

