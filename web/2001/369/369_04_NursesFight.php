<P><font face="Times New Roman, Times, serif" size="5"><b>Nurses need to fight back!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Nursing is plagued with a social disease. It's called capitalism--with pharmaceutical companies and HMOs making billions of dollars in profits.</P>
<P>Meanwhile, nurses are exploited, overworked, stressed out, burnt out, devalued and underpaid. Every year, nurses work an average of two months overtime, and job turnover rates in critical care areas are as high as 50 percent. </P>
<P>One in five nurses is considering quitting nursing altogether because of poor working conditions. In fact, nearly half a million of the nation's 2.6 million nurses weren't using their licenses in 2000. </P>
<P>We are in the midst of a public health crisis--a corporate-planned nursing shortage that values profits before people's health and well-being.</P>
<P>But a recent fightback in Madison, Wis., can offer a glimmer of hope. Angered by Meriter Hospital's stubbornness and union leaders' sluggishness, rank-and-file union members took matters into their own hands. </P>
<P>Fifty nurses confidently carried signs that showed they meant business. "These [surgical] booties are made for walking," read one sign.</P>
<P>As a result, we won a 14 percent wage increase over two years and better shift pay differentials. We didn't win all our demands--but we showed we can win more in future contracts by fighting together.</P>
<B><P>Bill Schmitz,</B> SEIU 1199W/UP, Madison, Wis.</P>

