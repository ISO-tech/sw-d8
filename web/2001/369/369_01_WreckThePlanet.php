<P><font face="Times New Roman, Times, serif" size="4">Big Oil pulls their strings</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They want to wreck the planet</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"BLESS HIS heart." That's how Bill Raney, president of the West Virginia Coal Association, reacted to the Bush administration's energy plan unveiled in mid-May. The plan was weeks in the making, with the White House hushing up the identities of the corporate bigwigs who attended meetings to draft it.</P>
<P>When it was finished, Dick Cheney sang the praises of drilling for oil in the Arctic National Wildlife Refuge--even if it means destroying a pristine natural habitat. He also called for building new nuclear plants, which he called "a safe, clean and very plentiful energy source."</P>
<P>Except, of course, for the radioactive waste that remains deadly for thousands of years. Cheney acknowledged to reporters that the waste problem was "a tough one." But that isn't stopping the Bush gang from plunging ahead.</P>
<P>From top to bottom, the energy plan follows the same pattern--dismantle or disregard environmental regulations so that the administration's friends in the energy industry can drill for oil, mine for coal and build nukes to their hearts' content.</P>
<P>But industry executives wouldn't expect anything less--considering the millions they contributed to help Bush get elected. In all, the Republicans received $48 million from the energy industry in 2000. You could be excused for thinking that the party's nickname, GOP, really stands for Gas, Oil and Plutonium.</P>
<P>But the power bosses are only the tip of the iceberg. Plenty of other stooges for Corporate America have weaseled their way into the Bush administration.</P>
<P>For example, Linda Fisher, picked by Bush to hold the number two post at the Environmental Protection Agency (EPA), was previously a lobbyist for Monsanto who fought regulations on genetically engineered food. James Connaughton, nominated to be chair of the Council on Environmental Quality, represented General Electric in its fight with the EPA over toxic waste sites.</P>
<P>With Big Oil and other corporate giants so obviously pulling the strings at the Bush White House, people might look back at Bill Clinton's presidency as a green utopia.</P>
<P>It wasn't. During his eight years in office, Clinton did nothing to require that the Big Three automakers improve the efficiency of gas-guzzling sport utility vehicles. And the U.S. continued to drag its feet on international treaties requiring cuts in the emission of greenhouse gases that cause global warming.</P>
<P>In fact, much of Bush's plan was once pursued by the Clinton administration. "Imitation is the sincerest form of flattery," said Dan Reicher, who headed the Department of Energy's energy-efficiency office for Clinton.</P>
<P>Bush is willing to destroy the planet to help his corporate cronies line their pockets. We can't let him get away with it.</P>

