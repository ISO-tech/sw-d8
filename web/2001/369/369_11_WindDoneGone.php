<P><font face="Times New Roman, Times, serif" size="4">Court ruling okays censorship</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Silencing a parody of <I>Gone with the Wind</b></font></P></I>
<P><font face="Times New Roman, Times, serif" size="2"><b>by KEEANGA-YAMAHTTA TAYLOR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PUBLISHING world is in an uproar over Alice Randall's novel<I> The Wind Done Gone</I>. Randall, an African American, wrote <I>The Wind Done Gone</I> as a parody of Margaret Mitchell's 1936 novel <I>Gone with the Wind.</P></I>
<P>A U.S. District Court last month ruled that <I>The Wind Done Gone</I> can't be published because it infringes on <I>Gone with the Wind</I>'s copyright.</P>
<P>Mitchell's estate is behind the lawsuit that led to the court decision. It claims that <I>The Wind Done Gone</I> is really a sequel to <I>Gone with the Wind, </I>making the estate eligible to collect profits from Randall's sales.</P>
<P>The claim--and the district court's decision--is ridiculous. But Mitchell's estate has a lot at stake. It wants to protect the millions it makes from sales of <I>Gone with the Wind</I> paraphernalia and movie-related tourism in Atlanta.</P>
<P>No doubt, there's also unhappiness with Randall's take on <I>Gone with the Wind</I>, too. <I>The Wind Done Gone</I> is a rebuttal to the racist stereotypes that filled Mitchell's book--and the 1939 Hollywood blockbuster adapted from it.</P>
<I><P>Gone with the Wind</I> romanticized the "Southern way of life" during slavery. The Civil War that ended slavery is portrayed as an atrocity against the South--for disrupting the lives of the Southern gentry and their slaves.</P>
<I><P>The Wind Done Gone</I> turns <I>Gone with the Wind</I> on its head. It's told from the perspective of the slaves who lived on Scarlet O'Hara's plantation. Randall's central character is Cynara, the child of O'Hara's father and a slave--making Cynara the half sister of Scarlet.</P>
<P>Mitchell's book depicted slaves as childlike imbeciles who were quite content with their lives on the plantation--and saddened by the outcome of the Civil War. This racist rewriting of history came to life in the caricatures of African American slaves in the Hollywood epic.</P>
<P>But in Randall's book, the slaves are only too happy to be rid of the likes of O'Hara after the Civil War.</P>
<P>Randall has a longer fight on her hands so her novel can see the light of day--against the Mitchell estate that wants to preserve <I>Gone with the Wind</I>'s racist mythology. "Once upon a time in America, African Americans were forbidden by law to learn to read and write," Randall said recently. "It saddens me and breaks my heart [that] there are those who would try to set up obstacles for a Black woman to tell her story of her people, with words and writing."</P>

