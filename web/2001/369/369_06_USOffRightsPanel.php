<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">U.S. kicked off UN human rights panel</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why did it take so long?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SHARON SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. CONSERVATIVES are up in arms because the U.S. has been voted off the United Nations (UN) Human Rights Commission--a seat that it's held since the body's founding in 1947.</P>
<P>A flustered Defense Secretary Donald Rumsfeld blamed the vote on other countries that no longer "feel grateful" toward the U.S. now that the Cold War is over.</P>
<P>National Security Adviser Condoleezza Rice complained that the U.S. was voted off, not because it's delinquent in its dues, but because it's so diligent in enforcing human rights throughout the world. "Obviously, the United States has been too strong on the human rights agenda," she said. "I suspect that this was a backlash of those who don't like being judged."</P>
<P>Conservatives in the House of Representatives expressed their "outrage" by voting to withhold $244 million in back dues owed by the U.S. to the UN--money that Congress only recently agreed to pay--unless the U.S. regains its seat on the commission.</P>
<P>But their words of outrage can't hide the U.S.'s shameful record on human rights around the world. Recent headlines have exposed, for example, the role that former senator and presidential candidate Bob Kerrey played in slaughtering more than a dozen Vietnamese women and children during the Vietnam War--before being awarded the Medal of Honor in 1970. </P>
<P>Such massacres--masterminded by the CIA--were commonplace during the Vietnam War. Former CIA official William Colby estimated before Congress that 20,587 Vietnamese were killed by U.S. death squads during the war; others put the figure at 41,000.</P>
<P>The question of human rights does not even enter the equation for U.S. foreign policymakers. That's why the U.S. can, on the one hand, condemn the "state-sponsored terrorism" of countries like Iraq or Libya, yet invade and bomb other countries at will.</P>
<P>Over the last decade alone, the U.S. invaded Panama and carpet-bombed Iraq and Serbia--not to mention its slow starvation of Iraqi civilians during more than a decade of economic sanctions, its funding of Colombian death squads or the one-off bombing of a pharmaceutical factory in Sudan.</P>
<P>The U.S. backs Israeli leader and well-known war criminal Ariel Sharon, who denounces Palestinian "terror" but who personally oversaw the massacre of hundreds of Arab civilians and engineered the Israeli invasion of Lebanon in 1982.</P>
<P>Without massive U.S. funding, the Israeli army couldn't continue to mow down Palestinians using U.S.-made Apache helicopters and other high-tech military equipment. And without U.S. backing, Israel couldn't continue to ignore the countless UN resolutions condemning its expulsion of more than 3 million Palestinians during the 1947-48 war and its 34-year brutal occupation of the Occupied Territories.</P>
<P>But the U.S.'s inhumanity extends far beyond the military front. During the last session of the UN Human Rights Commission, the U.S. stood virtually alone in opposing resolutions for access to low-cost medicines for the millions of people dying of AIDS in poor countries.</P>
<P>And at last week's World Health Assembly to discuss increasing the availability of AIDS treatments, Health and Human Services Secretary Tommy Thompson appeared with an entourage of representatives of the International Federation of Pharmaceutical Manufacturers Associations. "I want you to understand I'm fighting for you," Thompson assured them--as he went on to oppose resolutions calling for cheaper AIDS drugs.</P>
<P>After pushing through a trillion-dollar tax cut for the rich, Bush has announced that the U.S. will contribute a mere $200 million to the new UN AIDS trust fund--a tiny fraction of the $4 billion to $10 billion needed to even begin to curtail the world AIDS epidemic.</P>
<P>The U.S., along with Somalia, is one of only two nations that refuses to sign on to the UN Convention on Children's Rights, which provides basic protection against economic exploitation and compulsory education.</P>
<P>The U.S. has also stood firm against UN initiatives to ban land mines, to declare a human right to adequate food and for an international moratorium on the death penalty.</P>
<P>And Bush has already killed the Kyoto accords on global warming--while banning U.S. funding for any global organization that includes abortion counseling in its family-planning services.</P>
<P>These examples show that the U.S. is the world's leading human rights abuser and is single-handedly responsible for the deaths of millions of people around the world. The surprise isn't that the U.S. has lost its seat on the UN Human Rights Commission--only that it's taken so long.</P>

