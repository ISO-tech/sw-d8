<P><font face="Times New Roman, Times, serif" size="5"><b>Will the U.S. lift Iraq sanctions?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ANTHONY ARNOVE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--Isolated and on the defensive about economic sanctions that have strangled Iraq, the U.S. and British governments are proposing a restructuring of the nearly 11-year-old embargo.</P>
<P>But opponents of sanctions have good reason to fear that the changes will be more about spin than ending the suffering of millions of Iraqis.</P>
<P>The embargo on Iraq has caused the deaths of more than 1 million people--including more than 500,000 children under the age of five, according to the United Nations Children's Fund.</P>
<P>The U.S. and Britain plan to introduce a new resolution in the UN Security Council, in the hopes of ending "the widespread perception that sanctions cause great suffering for ordinary Iraqi people," the BBC reported. The resolution would impose so-called "smart sanctions" that target the government of Saddam Hussein rather than the people of Iraq.</P>
<P>But this is exactly what the U.S. and Britain have claimed about the current embargo for the last decade. Pressure from activists and opposition across the Middle East have exposed that lie, putting leading U.S. and British officials on the defensive.</P>
<P>Under the "smart sanctions" proposal--which could go into effect as early as June if approved by the Security Council--so-called "dual-use" goods would still be restricted. Such restrictions have prevented Iraq from importing essential items like chlorine, vaccines and ambulances--since they all have potential military applications.</P>
<P>"UN diplomats remain skeptical that the amended rules would bring a substantial change," the <I>Financial Times</I> reported. "According to UN diplomats, the U.S. wants to include chemicals necessary for water sanitation and computing and telecommunications equipment" on the list of barred dual-use items.</P>
<P>Lack of properly treated drinking water has led to widespread disease in Iraq and is a major reason for the doubling of the mortality rate for children. We need to keep fighting for an end to the sanctions--once and for all.</P>

