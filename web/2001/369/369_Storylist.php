<font face="Times New Roman, Times, serif" size="3">Dubya wants working people to know that he's concerned about the high price of gas and skyrocketing electric and heating bills. His solution? Get Congress to pass his $1.35 billion tax cut right away.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="369_03_DrugWarriors.shtml">Drug warriors march on</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">It seemed like the U.S. "war on drugs" might be in for a dose of sanity. But in a unanimous decision, the Supreme Court ruled against the medicinal use of marijuana for AIDS and cancer patients. And Bush nominated John Walters, a right-winger whose views on drug policy verge on the fanatical, to be his new drug czar.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="369_03_StandardizedTest.shtml">Time to flunk standardized testing</A></b></font><BR>
