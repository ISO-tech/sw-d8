<P><font face="Times New Roman, Times, serif" size="4">Using McVeigh to "save" the death penalty</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why we need to stop executions</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 25, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P></B>
<P>Yes, he's guilty of murder--mass murder in fact. Yes, he's a right-wing, neo-fascist scumbag. But we must nonetheless oppose his execution.</P>
<P>Why? Because the Bush administration wants to use McVeigh's blood to oil the federal death penalty machine with all of its racism.</P>
<P>During the last year, the death penalty has lost credibility throughout the country as more and more cases of innocent people on death row, judicial racism and class bias have been publicized. </P>
<P>Ever since activists won a moratorium on executions in Illinois, the death penalty's validity, effectiveness and fairness are being questioned by millions of people across the nation.</P>
<P>Now Bush--who is himself responsible for the murder by execution of more than 150 in Texas--wants to restore the death penalty's shrinking credibility. For the "Texecutioner," this is going to be extremely difficult, considering that more than 75 percent of those on the federal death row are Black or Latino. This makes it the most racist death row in the entire country--and now the country's leading executioner is behind the trigger.</P>
<P>Another reason to oppose McVeigh's execution is that McVeigh wants to go down in the history books as an antigovernment martyr. He wants to die so more people will be moved to join the forces of the far right.</P>
<P>Instead of allowing Bush to exploit this in order to do their dirty work, instead of allowing McVeigh to become the focal point for the far right, we need to point to McVeigh's case as the exception to the rule in a racist death penalty system that doesn't really give a damn about justice.</P>
<B><P>Pham Binh,</B> Rochester, N.Y.</P>

