<P><font face="Times New Roman, Times, serif" size="4">The struggle to save nine young Black men from lynch mob justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The story of Scottsboro</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Marlene Martin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 15</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><I>Scottsboro: An American Tragedy</I>, produced and directed by Daniel Anker and Barak Goodman.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN MANY ways, the story of the Scottsboro Boys is a terrible tragedy--as the title of Daniel Anker and Barak Goodman's Oscar-nominated documentary acknowledges. In 1931, nine young Black men, aged 13 to 19, were dragged off a train in northeastern Alabama, accused of raping two white women, put on trial and railroaded to the death house.</P>
<P>The nine didn't stand a chance at justice in the racist South--where an accusation of a Black man raping a white woman was an automatic death sentence, carried out in prison or, more often, by a lynch mob.</P>
<P>Still, the story of the Scottsboro Boys is so much more than a tragedy. It is also the story of how ordinary people fought back and won. They challenged a seemingly all-powerful racist Southern state and ultimately gained freedom for the Scottsboro Boys.</P>
<P>Unfortunately, that part of the story is distorted in Anker and Goodman's documentary. Their film, which aired on PBS's "American Heritage" series in early April, does a good job of portraying the racist system that the Scottsboro Boys were up against.</P>
<P>It describes the series of trials in the case, tearing apart the testimony of the alleged victims and exposing how prosecutors exploited hatred for political gain, with one declaring that he planned to "ride their Black asses into the governor's mansion." But this documentary shouldn't be considered the last word on Scottsboro.</P>
<P>For one thing, it annoyingly gives voice to those who try to explain white racism from the racists' point of view--that "the South" was reacting to snubs by "the North." Worse, the filmmakers are suspicious about the struggle to save the nine--especially about the motives of the main organizing force behind the fight, the Communist Party (CP).</P>
<P>Over and over, the CP is described as determined to "take control" of the Scottsboro defense--in order to exploit the case for its own "agenda." The filmmakers think they have a particularly damning quote from former CP member Lloyd Brown. "We were propagandizing, there's no doubt," Brown says. "We were using the Scottsboro case to expose what was going on in the South."</P>
<P>But this isn't cynical at all. To its credit, the CP recognized that the Scottsboro case couldn't be won in an Alabama courtroom. As the historian Dan Carter wrote, the party's strategy was to "give the Boys the best available legal defense in the capitalist courts, but at the same time to emphasize that the Boys could be saved only by the pressure of millions, colored and white, behind the defense in the courts."</P>
<P>The CP organized impressive demonstrations, uniting Blacks and whites. And it toured the mothers of the nine around the world to speak out for their sons' freedom.</P>
<P>It didn't take much to convince family members to make the political connections that the CP was arguing for. "I am with the party as long as I live," said one of the moms, Viola Montgomery. "I don't care who likes it or who doesn't like it...I tell the world I want to be somebody, but I can't under this government. This so-called government has put many a good woman in the garbage can and put the lid on it. But I tell the world I will fight like hell to stay out, and I want the rest of my comrades to do the same."</P>
<P>In the end, Anker and Goodman conclude that neither protest nor courtroom efforts saved the Scottsboro Boys. Only the passing of time and the fading of the case from the news, they claim, gave Alabama authorities the room to back down and eventually free the nine.</P>
<P>This is dead wrong. If the CP hadn't made the Scottsboro Boys a national political issue, the nine would have suffered the same fate that thousands of other Blacks did under the Southern system of lynch-mob justice.</P>
<P>We should remember Scottsboro not only as a tragedy of racism but as one of the most extraordinary and inspiring chapters in U.S. working-class history.</P>



