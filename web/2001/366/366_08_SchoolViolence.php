<P><font face="Times New Roman, Times, serif" size="5"><b>Don't buy media hype about school violence</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 8 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>The recent school shooting in San Diego and other shootings across the country have gotten a lot of press. To be sure, what happened in San Diego was a tragedy that no one would wish on any school.</P>
<P>But the press coverage has covered up two important points. One is that the level of juvenile violent crime&#151;especially school violence&#151;has dropped significantly in this country. It doesn't make the horrific cases go away, but it says that the overwhelming majority of public schools actually is safe.</P>
<P>More importantly, the obsession with school shootings in the media puts the source of that violence in the wrong place. I walked into work the week after the San Diego shooting like usual. I saw high schoolers in the Junior ROTC, the army training program, practicing in our gym.</P>
<P>But this time, the JROTC students weren't just in their uniforms. They were flinging fake wooden rifles from shoulder to shoulder! I was dumbfounded. </P>
<P>When a student brings a gun to school, it is decried as proof of our rotten, violent public schools. But when the government gives our children guns&#151;fake or not&#151;to train with for war, they are given medals and scholarships to college!</P>
<P>What insanity! Instead of accepting the hysteria against those disturbed teens who do lash out at school, we should point the finger at the real problem&#151;those at the top of society who depend on the violence of poverty and war to keep their power.</P>
<B><P>Jeff Bale, </B>AFT Local 6, Washington, D.C.</P>


