<P><font face="Arial, Helvetica, sans-serif" size="2"><b>YUGOSLAVIA</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bloody record of U.S. proves that it doesn't care about justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington hypocrites cheer Milosevic arrest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 9 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">FORMER YUGOSLAV President Slobodan Milosevic was arrested in Belgrade earlier this month. Yugoslav authorities say they will put him on trial for corruption and political violence. But the U.S. and other Western countries are demanding that Milosevic be sent to The Hague in the Netherlands to stand trial before the United Nations (UN) International War Crimes Tribunal for the actions of Serbian troops in Kosovo in 1999. LEE SUSTAR explains why the U.S.'s tough talk about Milosevic has nothing to do with justice.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THERE'S NO doubt that Slobodan Milosevic committed war crimes during the nightmare that followed the breakup of Yugoslavia in the early 1990s. But the Western countries that want to put him on trial are guilty, too.</P>
<P>When the old Yugoslavia began to fall apart, the U.S. initially backed Milosevic as a strongman who could hold the country together--even though he rose to power by repressing Albanians in Kosovo. Then, when Milosevic backed ethnic Serbs in the civil war in neighboring Bosnia, the U.S. and Western European governments switched sides to back a different war criminal--President Franjo Tudjman of Croatia.</P>
<P>Today, the West wants Milosevic put on trial for "ethnic cleansing" in Kosovo. Yet it was Washington that organized the biggest ethnic cleansing of the Balkans wars.</P>
<P> In 1995, the U.S. trained Tudjman's army to expel 200,000 Serbs from territory in Croatia where they had lived for centuries. At that point, the U.S. was still willing to embrace Milosevic as a "peacemaker" during the Dayton negotiations that ended the war in Bosnia. "Milosevic could now present himself to the Serbian people as a man who had brought Serbia peace, relief from the sanctions regime and acceptance back into the international community," wrote author Robert Thomas.</P>
<P>When mass protests led by students in the winter of 1996-97 forced Milosevic to recognize opposition victories in local elections, the U.S. continued to support him. Washington said nothing as Milosevic's cronies amassed huge fortunes through Mafia networks and state corruption.</P>
<P>But by 1999, a guerrilla war by the Kosovo Liberation Army (KLA) to win independence for ethnic Albanians threatened to trigger a wider conflict that could have pitted NATO members Greece and Turkey against one another. Washington's response was to lead NATO's "humanitarian" terror bombing against Serbia--and declare Milosevic the "new Hitler." </P>
<P>In fact, the war actually helped Milosevic consolidate his control. He was able to pose as a nationalist defender of Serbs against aggression by the world's most powerful militaries.</P>
<P>In the end, it wasn't NATO's bombs that ousted Milosevic. A revived opposition of students and a revolutionary uprising by workers finally toppled Milosevic last September, forcing him to accept his election defeat. But NATO wants to bury the memory of that mass revolt--and show who's boss in the Balkans. "Justice" has nothing to do with it.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>The West's "peace" at gunpoint unraveling</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE HYPOCRISY of Washington's demands that Milosevic be put on trial can be seen anywhere across the former Yugoslavia. In Macedonia, NATO is backing the Slav-dominated government in its guerrilla war against an offshoot of the Albanian KLA--the very force that Washington backed in its war against Serbia.</P>
<P>And while NATO allowed the KLA to ethnically cleanse Serbs from Kosovo during the occupation that followed the 1999 war, today they're helping the Yugoslav Army crack down on Albanian militants.</P>
<P>In Bosnia, the government created by the Dayton Accords--and backed up to this day by 21,000 NATO troops--is unraveling. The UN bureaucrat who effectively runs the country recently removed the Bosnian Croat president from office. Croat nationalists responded this month with an armed demonstration, leading to the worst violence in Bosnia since the civil war ended in 1995.</P>
<P>Washington's warlords don't care about the enormous death and suffering caused by the ethnic cleansing and war crimes in the former Yugoslavia. They only want "stability" to preserve their interests--and they're all too willing to commit war crimes of their own to achieve it.</P>

