<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Holes in the media's Bush victory story</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Commander-in-thief</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 6 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A NEWSPAPER analysis of Florida election ballots released earlier this month claims to have answered the question everyone's been asking since November's presidential election. If all of Florida's votes had been counted, would Gore or Bush have won?</P>
<P>The report, issued by the <I>Miami Herald, USA Today</I> and Knight Ridder, and publicized widely by the rest of the media, concluded that Bush's victory in Florida "almost certainly would have endured" if the U.S. Supreme Court had not stepped in and halted a recount, effectively handing the presidency to Bush.</P>
<P>Readers are presumably meant to believe that, despite all the fuss in November, democracy carried the day after all--and George W. Bush earned the right to occupy the White House. But the study actually measured only a small sampling of the Florida voters whose intended votes were rejected or ignored. </P>
<P>It recounted only "undervotes"--ballots in which a machine didn't record any vote for president. The study left out all the "overvotes"--those in which a machine recorded more than one vote.</P>
<P>What's more, this incomplete study can't be trusted because scores of uncounted ballots have gone missing since Election Day. In some counties, one-third or more of uncounted ballots have disappeared. Only eight of Florida's 67 counties were able to produce the exact number of ballots they reported missing on election night.</P>
<I><P>Miami Herald</I> editor Mark Seibel even created a name for the missing ballots--the "ballot twilight." All told, 180,000 votes went uncounted in Florida--most of which weren't touched by the <I>Herald's</I> analysis. Yet the study claimed that Bush's lead over Gore would have risen from 537 to 1,665 if the U.S. Supreme Court had allowed the recount to continue.</P>
<P>Even the study's authors admit, however, that, had they used a different method of counting, Gore would have gotten more votes than Bush. If every dimpled chad or hanging chad were considered a valid vote, Gore would have won by 393 votes. If dimples were counted as valid only on ballots that had similar dimples in other races, Gore would still have won by 299 votes in the counties measured.</P>
<P>A separate media recount of Florida's election results is scheduled to be released within the next month. This one, conducted by the <I>Washington Post</I>, CNN, the <I>New York Times</I> and other media outlets, will count the "overvotes"--which have thus far favored Gore at a rate of three to one--as well as the "undervotes."</P>
<P>But even if all the uncounted votes in Florida were counted, no one can accurately measure what the outcome would have been. That's because thousands of Black Florida voters were denied the opportunity to even cast a ballot--because of police roadblocks in Black neighborhoods and other forms of racist harassment.</P>
<P>The state of Florida also compiled a list of 56,000 convicted felons from across the U.S. to bar from voting. Many of these people were legally entitled to vote, because their convictions happened in one of the 37 states that, unlike Florida, allows ex-felons to vote. Roughly half of the list was Black.</P>
<P>At Florida's polling stations, attendants refused to allow Blacks to vote whose names--but not their social security numbers--matched any of the names on the massive list. According to British reporter Gregory Palast, "The U.S. Civil Rights Commission said this was the most egregious case of systematic racial disenfranchisement they'd ever seen."</P>
<P>And Florida isn't the only place where Black voters were racially harassed at the voting booth. In Tennessee, Black voters were ordered to get behind white voters in the line to vote. Many were told to remove NAACP stickers from their cars if they wanted to vote.</P>
<P>Lines at polling stations in Black, Latino and poor neighborhoods stretched a mile long, forcing voters to wait up to five hours to cast a ballot. Some stations in Black areas opened hours late or closed hours early--while others mysteriously changed location at the last minute. According to writer Catherine Danielson, "[T]here is massive evidence that thousands--and perhaps tens of thousands--of people were disenfranchised, the vast majority of whom were Black."</P>
<P>All told, similar voting "irregularities" occurred in at least 21 states on Election Day. No amount of media manipulation can legitimize Bush's stolen presidency.</P>


