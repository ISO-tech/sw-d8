<P><font face="Times New Roman, Times, serif" size="5"><b>Opponents of racist ad face threats at Brown</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Shaun Joseph</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--The controversy over right-winger David Horowitz's racist ads about reparations continued at fever pitch this month at Brown University. Horowitz's ad, which he submitted to numerous campus newspapers, denounces proposals to pay reparations to African Americans for the crime of slavery.</P>
<P>Horowitz dared to claim that Blacks "owe a debt" to America--because they benefited from slavery! When antiracists protested his disgusting ad, Horowitz whined that he was the victim of an attack on his free speech rights.</P>
<P>But Brown students and faculty are learning who the real victims are. In late March, a leading member of the Coalition of Concerned Students--which formed to challenge the Brown Daily Herald's decision to print Horowitz's bigoted ad--received a hate letter through the mail. Lewis Gordon, the chair of the Afro-American Studies program and a supporter of the coalition, got threatening phone calls, and many Black students say they've been confronted in person and told to "go back to Africa."</P>
<P>University administrators had to arrange for extra police protection for the Afro-American Studies department and the minority community center. But they only made a public statement condemning the racist abuse after a group of students confronted Brown President Sheila Blumstein. Apparently, administrators have put a higher priority on punishing activists for committing an act of civil disobedience by taking a print run of the free newspaper that printed Horowitz's racist garbage.</P>
<P>Antiracists here won't back down. The harassment and threats they've faced show that the problem of racism on campus goes beyond one ad in a school newspaper.</P>



