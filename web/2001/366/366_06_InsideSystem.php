<P><font face="Times New Roman, Times, serif" size="5"><b>Inside the system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 6 </font></P>


<P><p><font face="Times New Roman, Times, serif" size="4"><b>The right's right to kill</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IT'S O.K. for anti-choice terrorists to publish hit lists of abortion providers on the Internet. That's what a U.S. Federal Appeals Court in San Francisco ruled in late March.  </P>
<P>But even more outrageous was the logic they used in the decision. The Federal Appeals Court said anti-choice Web sites that advocate murdering abortion providers are protected as "free speech" under the First Amendment.  </P>
<P>But the court had the gall to compare these right-wing nutcases to labor activists, anti-war organizers, abolitionists like John Brown, and colonists who fought against British rule! "Extreme rhetoric and violent action have marked many political movements in American history," wrote Judge Alex Kozinski. "Patriots intimidated loyalists in both word and deed as they gathered support for American independence. </P>
<P>"John Brown and other abolitionists...committed murder in pursuit of their cause. In more modern times, the labor, antiwar, animal rights and environmental movements all have had their violent fringes. As a result, much of what was said, even by nonviolent participants in these movements, acquired a tinge of menace."</P>
<P>The day after the ruling, the anti-choice Web site was back up and running, calling for new volunteers to harass abortion providers and protect "God's little babies."<br>
<I>-- Los Angeles Times, </I>March 29, 2001</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Government 101</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE OF the seldom discussed benefits of Florida's election slugfest was the crash course in civics that the whole country enjoyed. For example, Florida Secretary of State Katherine Harris recently recalled the visit of some fourth graders to her office.</P>
<P>Harris asked the kids what tasks fall to the secretary of state. "I called on one beautiful little girl and asked, 'What do you think the secretary of state does, what are her responsibilities?'" Harris remembered. The girl replied, "You get to choose the president of the United States."<br>
<I>-- Chicago Tribune, </I>April 2, 2001</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>They call this belt tightening?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE INVESTMENT bank Credit Suisse First Boston (CSFB) has asked its bankers to tighten their belts. "Given current market conditions, try to keep dinners below $10,000, particularly when no travel is involved," wrote Tony James and Chuck Ward, co-heads of investment banking at CSFB, in a memo to colleagues.</P>
<P>This noble self-denial and restraint follow sharp declines in CSFB business. CSFB executives are also considering a smaller budget for "weekend-long celebrations once deals have been completed," according to the Financial Times. </P>
<P>CSFB bankers have also called for a lighter touch when ordering trophies, known as lucites, which are given away to mark deals. Bankers treasure these trinkets as a sign of their "skill." "Keep costs of lucites below $2,500 and do not order for senior bankers," said the CSFB memo said. <br>
<I>-- Financial Times, </I>April 6, 2001</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>U.S. says play nice</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. has warned Qatar not to beat up WTO protesters. "We've been telling Qatar there are certain lines they cannot cross," one U.S. official says.</P>
<P>They fear the undemocratic emirate might turn protests at November's World Trade Organization meeting there into a public-relations disaster. In Qatar, there is no constitution, and all demonstrations are illegal.</P>
<P>If only the U.S. spent less time lecturing, maybe the cops wouldn't have turned the Seattle WTO meeting into such a debacle.<br>
<I>-- Wall Street Journal, </I>February 8, 2001</P>

