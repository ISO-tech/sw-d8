<P><font face="Times New Roman, Times, serif" size="4">Stop the FTAA!</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Our world is not for sale</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 1 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">FOUR THOUSAND heavily armed cops from four different police forces. Concrete barriers topped with chain-link fences to seal off the city. Passes restricting the free movement of residents. Bans on unauthorized protests. Limits on independent journalists. Exclusion of "undesirable aliens" at the border.</P>
<P>If this were taking place in Iraq under Saddam Hussein or Serbia under Slobodan Milosevic, Washington would be screaming about such outrageous violations of human rights and freedoms. But it's happening right across the border in Quebec City, Canada--and it's being done in the name of freedom.</P>
<P>Freedom for trade, that is. The massive crackdown is to protect President George W. Bush and 33 other heads of state at the Summit of the Americas.</P>
<P>If Bush has his way, the summit will speed up efforts to create the Free Trade Area of the Americas (FTAA), a scheme to expand NAFTA from the Arctic to Argentina's southern tip--except for Cuba. If it goes through, the FTAA would give huge multinational corporations the right to squeeze small countries in Central and South America.</P>
<P>And it would encourage the privatization of almost everything--even schools and libraries--while gutting environmental protections and public health laws as "barriers to trade." It was the widespread anger over such policies that led to the demonstration of 40,000 unionists, students and environmental activists at the World Trade Organization summit in Seattle in 1999.</P>
<P>Then, a vicious police crackdown exposed just how far world leaders would go in trampling democracy to defend their pro-corporate policies. In the months since, a series of protests held in cities around the world against international financial institutions have put the free traders on the defensive.</P>
<P>That's why Bush and friends will have to hide in a "security zone" as they discuss how best to serve their corporate masters. As Canadian journalist Dalton Camp put it, these leaders "enjoy the same corporate sponsors, their election purchased by the same money bringing the same all-embracing endorsements, holding the same distinguished credentials: anti-Communist, anti-Castro, anti-union, anti-environment, anti-liberal...This explains the contempt for people on the street."</P>
<P>But they won't go unchallenged. Thousands of activists will defy police to hold direct-action protests in Quebec City on April 20. And many thousands more will join a permitted labor march the following day.</P>
<P>Other protests will take place at the U.S.-Canada border. And demonstrators from Mexico and the U.S. will gather for a historic joint demonstration on the border near San Diego.</P>
<P>All the concrete barriers and cops in Quebec City won't stop the growing movement to stop the FTAA--and the other corporate trade deals that serve corporate greed over human need. We'll make them hear our message: Our world is not for sale!</P>



