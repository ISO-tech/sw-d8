<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NICARAGUA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Chentex ordered to rehire fired workers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 9 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE FIGHTERS against sweatshops won a major victory in early April when an appeals court in Nicaragua ruled that nine union leaders at the Chentex textile plant were fired illegally last year. The court ordered management to hire back nine union supporters who had been fired for organizing a strike last May--and to pay back wages for the time that they were locked out.</P>
<P>The decision is unprecedented, said Charles Kernaghan of the National Labor Committee (NLC). "This is the first time a court in Nicaragua or anywhere in Central America has ruled against a foreign multinational like this," Kernaghan said.</P>
<P>But Chentex is hitting back. Company officials say they will defy the court's orders--even if they have to shut down the factory and lay off all 1,800 workers. And activists fear that the reinstated workers could be attacked by thugs when they return to work.</P>
<P>The struggle at Chentex grew to prominence after the NLC began organizing support for the workers. Representatives of unions and groups like United Students Against Sweatshops joined delegations to investigate conditions at Chentex and other factories in Nicaragua's free-trade zone.</P>
<P>Chentex workers earn about $60 a month making jeans that sell in the U.S. for $20 and up at stores such as Kohl's and Wal-Mart. Living conditions are squalid in the residential areas--with many people living in shacks made of scrap metal and cardboard. The court decision could set an important precedent in the fight against sweatshops--but only if we keep up the pressure.</P>

