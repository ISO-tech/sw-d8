<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Democrats bow to Bush's agenda</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">AFTER THE U.S. Senate passed a budget outline slashing President Bush's $1.6 trillion tax cut, Minority Leader Tom Daschle (D-S.D.) mocked Republican efforts to claim victory. "If this is a victory for them, then we want more victories just like it," Daschle bragged.</P>
<P>But with "victories" like this, who needs defeats? The Democrats are crowing that they've stood firm--and reduced Bush's tax cut package from $1.6 trillion to $1.2 trillion, with more money diverted to increase funding for education and to reduce the federal debt.</P>
<P>Coming a week after the weak-kneed McCain-Feingold campaign finance bill passed the Senate over Bush's opposition, the Democrats were claiming that they'd stopped rolling over for Bush. In fact, they helped Bush fulfill a campaign promise. During last fall's campaign, Bush promised a $1.3 trillion tax cut, while the Democrats pushed for $500 billion.</P>
<P>Seems like the Democrats moved a lot closer to Bush than Bush moved toward them. That's the kind of "bipartisanship" Bush likes. The peanuts that the Democrats salvaged for education won't make up for the billions that will end up in the pockets of the rich.</P>
<P>As even mainstream economist Paul Krugman pointed out in the New York Times, the tax plan that the Senate passed will force huge cuts in essential programs and a raid on future Medicare spending. "Mr. Bush's tax plan shouldn't be scaled back; it should be abandoned," Krugman wrote.</P>
<P>Since Bush took office, he's pushed on every front to do the bidding of big business and the Christian Right. Whether it's allowing unsafe levels of arsenic into drinking water or repealing workplace safety rules, Bush has done the job Corporate America hired him to do.</P>
<P>He's gotten away with most of it because the so-called Democratic "opposition" in Congress has barely put up a fight. And in cases like the repeal of workplace safety rules, Democrats were the margin of victory.</P>
<P>The Democrats may want to get along with Bush, but the people who'll bare the brunt of his attacks don't have to. It's time to draw the line against Bush's attacks.</P>


