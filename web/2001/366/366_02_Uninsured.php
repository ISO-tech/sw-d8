<P><font face="Times New Roman, Times, serif" size="5"><b>Bush goes after the uninsured</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--It's slash-and-burn time at the Bush White House. Dubya and the gang want to ram through tax cuts for the wealthy before they reveal the details of their budget plans. But as time goes on, a few items have slipped out.</P>
<P>One whopper came to light earlier this month. Bush's budget will virtually eliminate funding for health programs to help people without health insurance.</P>
<P>These programs are already far too small. This year's budget of $200 million works out to a little more than $5 for every uninsured person in the U.S. But even that's too much for Dubya.</P>
<P>"Creating new federal grants is not the best way to address health care access," sniffed a White House spokesperson. Apparently, the Bush gang thinks it would be better for the uninsured to get no help at all.</P>
<P>Dubya has plenty of other outrages in store. There'll be less money to care for people with AIDS after inflation is factored in. The administration plans a 60 percent cut in federal spending for the training of doctors, dentists, nurses, pharmacists and other health professionals.</P>
<P>Bush has promised to build more community health centers for the poor. But as one pediatrician pointed out, "You can build a lot of health centers, but if you don't have anybody to work in them, it's a problem."</P>
<P>Overall, the budget for the Department of Health and Human Services is slated to grow by 5 percent. But all of the net increase will go to the National Institutes of Health--to support biomedical research.</P>
<P>Guess who benefits? That's right, biotech giants like Monsanto and other needy multinational corporations.</P>


