<p><a name="Cianci"></a><font face="Times New Roman, Times, serif" size="4"><b>Providence mayor is in trouble again</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>Providence Mayor Vincent "Buddy" Cianci was indicted April 2 on federal charges of bribery and racketeering in what has been a year-long investigation by federal officials. Cianci's chief aide and other high-ranking officials have also been indicted. </P>
<P>The specific crimes range from taking bribes to giving local contractors choice jobs. Cianci has been trying to avoid an indictment since the beginning of what the federal officials have labeled "Operation Plunder Dome."</P>
<P>In a recent television poll, 40 percent of Providence residents think Cianci should resign. But Cianci won't listen to them, the governor or anyone else who has asked him to leave office. </P>
<P>If only ordinary people had the rights that Cianci's money has bought him! Cianci has a violent criminal record. He was convicted of assault after kidnapping a man, hog-tying him, torturing him with cigarette burns and urinating on him--all with the help of his police goons. </P>
<P>Cianci--unlike the hundreds of people arrested in Providence each year--has been able to buy his way through the court system and largely avoid facing up to his crimes. It is sheer hypocrisy for Cianci to talk about being innocent until proven guilty when innocent men like Derick Hazard and Aldrin Diaz sit rotting in prison in Rhode Island, put there by Cianci's corrupt cops.</P>
<B><P>Dana Blanchard, </B>Providence</P>

