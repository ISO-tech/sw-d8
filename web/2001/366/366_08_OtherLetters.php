<p><a name="Lapdog"></a><font face="Times New Roman, Times, serif" size="4"><b>FDA has gone from watchdog to lapdog</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,<br>
</B></I>In February, the Food and Drug Administration (FDA) announced that five drug companies were producing nine vaccines using ingredients from cattle from countries where mad cow disease is a risk. Eight years ago and repeatedly thereafter, the FDA issued instructions to drug companies not to use ingredients made from European cattle.</P>
<P>Among the companies that ignored the FDA are some of the biggest names in drug manufacturing--GlaxoSmithKline and Aventis, for example. Now they say they'll behave.</P>
<P>But why did it take the FDA so long to discover that their directives were being ignored? The answer can be found in how the FDA has evolved from a watchdog agency to a "partner" of the pharmaceutical and food industry in the 1990s.</P>
<P>The FDA used to emphasize safety even if it meant drugs were slow to get to market. Now the FDA rushes drugs to market, and its safety record is last among industrialized countries.</P>
<P>In the past three years, 10 drugs have been pulled for safety reasons--after 22 million people took those drugs. Worrying about E. coli contamination in our hamburgers is bad, but now we also have to worry that the medicine we take to cure our food poisoning might kill us, too.</P>
<B><P>Bill Roberts, </B>Chicago</P>

