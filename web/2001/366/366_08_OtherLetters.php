<p><a name="Guess"></a><font face="Times New Roman, Times, serif" size="4"><b>You wouldn't guess they're Democrats</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>Massachusetts is arguably one of the most liberal states in the union. Yet Massachusetts Democrats continually mimic right-wing Democrats and moderate Republicans.</P>
<P>For example, House Speaker Tom Finneran has gutted bills intended to regulate HMOs and ban ATM surcharges. Now he's trying to destroy a campaign finance reform bill that 67 percent of voters supported in a 1998 referendum. And all the "leftish" Democrats in the state house are keeping quiet while Finneran struts about with impunity. </P>
<P>And while big developers built luxury apartments in the city, Boston Mayor Tom Menino--a Democrat--stood by without even considering a public housing clause. Now housing costs are so outrageous that it's not uncommon for people to commute from out of state.</P>
<P>Legislative pressure doesn't work. Instead, we should look to an alternative from below, like the 1968 protests for affordable housing which eventually culminated in Tent City--one of the few bastions of affordable housing still standing near central Boston.</P>
<P>The Democrats have sold us out to big business too often. It's time to get our share of the wealth--our way.</P>
<B><P>Mitch Lewis, </B>Cambridge, Mass.</P>

