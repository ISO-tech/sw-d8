<p><a name="Cops"></a><font face="Times New Roman, Times, serif" size="4"><b>Campus cops caught spying on anti-sweatshop activists</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>It seems that the police have taken a keen interest in the student movement against corporate greed. Keith Rosenthal's letter in <I>Socialist Worker</I> last month showed that the cops are willing to intimidate antiglobalization activists even as they study in their dorm rooms.</P>
<P>But Michigan State University (MSU) police have gone them one better. Last year, MSU police officer Jamie Gonzales posed as elementary education junior "Samantha Volare," so she could infiltrate the campus chapter of United Students Against Sweatshops.</P>
<P>She was only recently discovered after she was seen on campus wearing her uniform. The student government and even the head of MSU's College Republicans have condemned the police.</P>
<P>But the administration sees it differently. MSU Trustee David Porteous argued that spying on student groups was necessary and wouldn't "have any sort of chilling effect on students and their First Amendment rights."</P>
<P>Hopefully he'll be proved half right, and the anger over this police abuse will stir up more activism against corporate greed and pro-corporate university hacks like Porteous. The whole thing points to the hypocrisy of what's considered freedom of speech and assembly in our society.</P>
<P>The media was more than willing to make right-wing nut David Horowitz a "First Amendment" hero when students confiscated newspapers carrying his racist tirades last month. There has hardly been a murmur about what went down at MSU.</P>
<B><P>Adam Turl, </B>Chicago</P>

