<P><p><font face="Times New Roman, Times, serif" size="4"><b>Nonprofits aren't kinder and gentler</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>In the face of an oncoming recession, it's easy to focus on the bursting bubble in the high-tech sector. But employees at non-profit companies can also feel the sting.</P>
<P>Since I began working at a nonprofit company four years ago, my bosses have told my coworkers and me that our nonprofit status makes us a "kinder, gentler company." What a load of garbage.</P>
<P>Recently, the company president called the employees together for a meeting. Amid a polished slide presentation filled with executive double-speak and indecipherable jargon about "dysfunctional matrices," "functional stovepipes," and  "process tunnels," we were told that we were "failures at innovation" and not "efficient enough."</P>
<P>We were told that we may have to reapply for our jobs, be reassigned to different jobs without our consent and our salaries may be frozen. As you might imagine, employee stress levels have skyrocketed during the past several weeks.  </P>
<P>What's management's solution? They've offered us the chance to sign up for a five-minute "mini-massage" and ordered a few books on stress management for our library--including one that's a parable about cheese?!</P>
<P>At one point in his presentation, our president summed up Marxist economics for me better than I thought possible. "For those of you who think that a nonprofit company doesn't need to make a profit, you're wrong," he said.  "A nonprofit company must make a profit like any other company, in order to compete."</P>
<P>Although we don't have a union, many people who have been hostile to union ideas in the past are beginning to see why unions are necessary to protect workers on the job.</P>
<B><P>Nicole Colson, </B>Philadelphia</P>

