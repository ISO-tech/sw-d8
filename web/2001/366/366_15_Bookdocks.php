<P><font face="Times New Roman, Times, serif" size="5"><b>A razor-sharp satire on race relations in the U.S.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Alexander Ross</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 15 </font> </P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Aaron McGruder, <B><I>The Boondocks: Because I Know You Don't Read the Newspaper</B></I>. Andrews McMeel Publishing, 2000, 128 pages, $9.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FEW COMIC strips have generated as much controversy as Aaron McGruder's "The Boondocks." With a writing style influenced by cartoonists Bill Watterson and Garry Trudeau, a visual style inspired by Japanese animation and politics learned from socialist Angela Davis and Black nationalist Frantz Fanon, McGruder has come up with a politically sharp satire on race relations in the U.S.</P>
<P>Just three years after it first ran in the University of Maryland student newspaper, "The Boondocks" debuted in mainstream newspapers in April 1999. It had one of the largest comic strip launches ever, appearing in more than150 publications in its first week.</P>
<P>It's easy to see why "The Boondocks" did so well.  It manages to discuss serious political issues in a funny and provocative way. The strip centers around the life of a Black family, the Freemans, who've recently moved from Chicago's South Side to the middle-class, predominantly white suburb of Woodcrest.</P>
<P>Huey Freeman is the older brother of the family, a quick-witted, elementary school socialist. His younger brother Riley is a self-styled gangster rapper, and the two are being raised by their traditionalist grandfather.</P>
<P>Huey and his family deal with yuppie neighbors, a classmate who thinks that all African Americans are either rappers or basketball players and a school principal whose idea of racial sensitivity training involves having teachers watch 1970s blaxploitation films.</P>
<P>The strip is frank and honest in dealing with political issues. In one installment, Riley complains about having missed the neighborhood Fourth of July barbecue. "I can't believe Independence Day came and went and nobody told me," he says. Huey responds: "I bet millions of African slaves said the same thing a couple hundred years ago."</P>
<P>In another, Huey convinces his neighbor that the story of Santa Claus is based on a Black political prisoner named Jolly Nick Jenkins. "As long as we're lying to kids, we might as well add some moral substance," he says. His liberal neighbors plan a "Free Jolly Jenkins!" rally.</P>
<P>Meanwhile, the Star Wars character Jar-Jar Binks has become a Black nationalist, declaring that "It's revolution time!" and promising that he will no longer "speak ignorantly for the man, act like a buffoon for the man and degrade myself and my people for the man." </P>
<P>Naturally, conservatives have railed against "The Boondocks," causing many newspapers to cancel or refuse to run it. Yet its overall popularity has grown steadily.</P>
<P>If your local newspaper doesn't carry "The Boondocks," this book--which collects all the strips from the first year-and-a-half of publication--is a great introduction. There's a second book slated for May and a TV cartoon in development--which means that more Americans will be exposed to McGruder's biting wit.</P>


