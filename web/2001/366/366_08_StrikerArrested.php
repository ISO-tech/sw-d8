<P><font face="Times New Roman, Times, serif" size="4">Cops in LA go to bat for corporate greed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Arrested at the bosses' whim</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 8 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<br>
</B>Workers at two Hollander Home Fashions factories have been on strike for a month here in Los Angeles. The 450 workers are demanding a retirement plan, a raise and an end to abusive working conditions. </P>
<P>Workers who've been there 20 years still make less than $7 per hour. When they asked for a retirement plan, the company said they'd bring in an expert to teach them how to save money! </P>
<P>Hollander made $205 million in profits last year. The Hollander strikers are the biggest group of workers represented by the Union of Needletrades, Industrial and Textile Employees (UNITE!) in Los Angeles, the sweatshop capital of the U.S. </P>
<P>In an industry with such low unionization, the Hollander workers' fight is clearly a crucial battle. I have been going to the pickets--along with other socialists--from the beginning. We've taken strikers to our union halls, campuses and anywhere else we could build solidarity for their fight. </P>
<P>The company doesn't like this, and yesterday the company had the police take me into custody along with seven workers and an organizer. We had been legally picketing in front of the factory gates. </P>
<P>Police broke up the picket to let a scab bus through. After the scabs got on the bus, someone from the company went to the police and began pointing out people. </P>
<P>Whoever the company pointed out, the police arrested with no explanation. They held us in a dirty, cold jail for nine hours. They said we had violated an injunction--one that was passed that day and no one knew of or was warned about! </P>
<P>The only people who belong behind bars are Jeff Hollander and his scab agency who are robbing the workers of a retirement and a raise they deserve!</P>
<B><P>Karl Swinehart</B>, Los Angeles </P>
