<P><font face="Times New Roman, Times, serif" size="5"><b>Right-wingers take aim at affirmative action</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 13, 2001 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">DETROIT--A federal judge last month ruled against the University of Michigan Law School's affirmative action program in admissions. The decision was a victory for the Center for Individual Rights (CIR), the right-wing group that has filed anti-affirmative action lawsuits against colleges across the country.</P>
<P>But last month's decision is directly at odds with a ruling in December by another federal judge in Detroit, who upheld the affirmative action program for University of Michigan undergraduates. Michigan is at the center of the controversy in part because school administrators have fought back against the CIR.</P>
<P>Many universities apologized for affirmative action in the face of the CIR's argument that using race and gender as a factor in admissions discriminates against white men. But Michigan officials defended affirmative action as necessary to promote diversity and break "patterns of racial segregation and separation historically rooted in our national life." Now the U.S. Supreme Court is expected to resolve the two conflicting decisions.</P>
<P>During his confirmation hearings, Attorney General John Ashcroft promised to remain neutral on affirmative action. Anyone believe him? </P>
<P>People who want to stop the racists' attack on affirmative action will need to mobilize to show the politicians and judges that we won't stay quiet. Last month, more than 3,000 students rallied at UCLA in defense of affirmative action. This turnout shows the potential for rebuilding the fight against racism.</P>


