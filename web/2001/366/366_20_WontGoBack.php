<P><font face="Times New Roman, Times, serif" size="4">Tell Bush we won't go back</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Abortion is every woman's right</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte and Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 20 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--A federal court here ruled this month in favor of anti-abortion fanatics who publish "hit lists" of abortion providers on the Internet. The ruling is the latest blow in the right wing's war against a woman's right to choose abortion.</P>
<P>It protects a Web site called the "Nuremberg Files," which features "wanted" posters and hit lists of abortion providers, clinic workers and abortion rights advocates. When one is killed, his or her name is crossed off the list.</P>
<P>One of the people listed is Bernard Slepian, a Buffalo, N.Y., doctor who was shot in his kitchen in 1998. His alleged killer, James Kopp, was recently arrested by French police and is awaiting extradition. Within hours of Slepian's murder, the "Nuremberg" Web site had drawn a line through his name.</P>
<P>The judges ruled that the Web site constituted "free speech." So advocating the murder of abortion providers is a legitimate form of political expression these days. The ruling is a slap in the face to women across the country--and paints a target on the back of every abortion provider and clinic worker.</P>
<P>But the attack on abortion rights goes beyond the small number of anti-abortion terrorists. After all, they're taking their lead from Washington--where the new commander-in-thief celebrated his first days in office by announcing new restrictions on the right to choose.</P>
<P>He even restated his opposition to the Roe v. Wade Supreme Court decision that legalized abortion. "I've always said that Roe v. Wade was a judicial reach," Bush stammered shortly after taking office.</P>
<P>Meanwhile, anti-abortion Republicans in Congress are anxious to introduce new bills that will further limit abortion rights--and Democrats are prepared to let them get away with it. Women can't afford any more restrictions.</P>
<P>Abortion is already unavailable in 86 percent of counties across the U.S. And the cost of obtaining an abortion puts a heavy burden on poor and working-class women. What's more, state legislation passed during the Clinton years--such as parental consent laws, mandatory waiting periods and so-called "partial-birth" abortion bans--further limited access to abortion.</P>
<P>The politicians' anti-choice agenda stands in sharp contrast to the opinions of ordinary people. A Gallup poll taken in October showed that 67 percent of Americans support the Roe v. Wade decision.</P>
<P>Tens of millions of people can't stand Bush and his Neanderthal pals' attempts to control women's lives. We need to take that anger to the streets--and organize a protest movement that takes on Bush and his anti-abortion friends in Congress.</P>
<P>But a movement has to be built. For the last eight years, little action was organized in defense of abortion. Mainstream women's organizations hoped a Democrat in the White House would guarantee the right to choose.</P>
<P>Instead, abortion rights were steadily eroded. We need to build a movement that beats back Bush's assault--and takes back the ground we lost during the Clinton years.</P>
<P>The National Organization for Women's (NOW) April 22 "Emergency March for Women's Lives" can be the first step toward building such a movement. "Abortion is the kind of issue that people feel passionately about and will mobilize for," NOW President Patricia Ireland told <I>Socialist Worker</I>.</P>
<P>"This is a way to mobilize a lot of new activists out there who were turned off by the blatancy of the election theft and want to find a place to plug in. We can help build a movement to fight on all fronts."</P>
<P>Women--not the government, not the church, not our husbands, boyfriends or families--should have the right to control our own bodies, without a single restriction. On April 22, let's send a message to Bush and the anti-choice bigots that we won't go back.</P>
<p><font face="Times New Roman, Times, serif" size="4"><b>Bush's assault on our right to choose</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS SOON as he moved into the White House, George W. Bush went after a woman's right to choose. Within his first few months in power, Bush:</P>
<P>--Imposed a gag rule on international family-planning organizations that receive U.S. aid, barring them from using the word "abortion."</P>
<P>--Called for the Food and Drug Administration to re-examine RU-486, the much-awaited abortion drug that only recently became available in the U.S.</P>
<P>--Installed right-wing anti-abortion fanatic John Ashcroft as U.S. Attorney General.</P>
<P>--Announced his eagerness to have Ashcroft reexamine Roe v. Wade.</P>
<P>--Discussed a ban on the use of fetal tissue for medical testing and research.</P>
<P>That's why the Emergency March for Women's Lives on April 22 is so important--and why women and men are traveling from hundreds and even thousands of miles away. It's time to build the fight to defend abortion rights.</P>

