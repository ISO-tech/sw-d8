<P><font face="Times New Roman, Times, serif" size="4">Spy plane furor sends politicians into frenzy of hypocrisy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's China bashing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Phil Gasper</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The hypocrisy of U.S. leaders shot up to frantic levels following the April 1 collision between a U.S. spy plane and a Chinese fighter jet over the South China Sea. From the Bush administration to congressional leaders to establishment media mouthpieces, almost everyone in Washington was heaping abuse on the Chinese government.</P>
<P>You'd never know to hear them that the Navy's EP-3 surveillance plane was intercepting Chinese military and commercial communications at the time of the collision. And even though a Chinese pilot died in the incident, it was days before Washington would even express regret about what happened.</P>
<P>No one knows exactly how the collision took place. But one thing is clear--it would never have occurred if the U.S. didn't fly regular intelligence missions along the Chinese coast.</P>
<P>Pentagon spokesperson Rear Adm. Craig Quigley claimed with a straight face that the EP-3 wasn't spying but carrying out "overt, routine surveillance and reconnaissance." But the plane was using the latest electronic and cryptological eavesdropping technology--and flew close to a number of sensitive Chinese military bases. Imagine the U.S. response if the tables were turned, and a similar Chinese spy aircraft flew up and down the West Coast.</P>
<P>Washington claims that the collision took place outside the 12-mile boundary that it recognizes as China's territorial airspace. But according to the Straits Times newspaper in Singapore, "U.S. planes occasionally nudge Chinese airspace or fly close enough to test the reaction of the other side." For its part, the U.S. government insists on a 200-mile air-defense intercept zone around its own coastline.</P>
<P>After the EP-3's emergency landing at a Chinese base on Hainan island, the U.S. government tried to claim that the plane was "sovereign" territory, like an embassy--so it would be a violation of international law for Chinese authorities to inspect it. That provided a good laugh for international law experts. "There is no authority for the proposition that a spy plane is like the sovereign territory of the United States," Francis Boyle of the University of Illinois told reporters.</P>
<P>Under different circumstances, the U.S. hasn't been so touchy about such matters. When a pilot from the former USSR landed his MIG in Japan and defected in 1976, the U.S. spent weeks taking the plane apart--despite repeated requests from Moscow for the return of its property. The plane was eventually shipped back to Russia in crates.</P>
<P>In recent years, U.S. policy towards China has balanced uneasily between seeing the country as a huge trade and investment opportunity on the one hand, and as a military threat on the other. Under George W. Bush, whose administration is stuffed full of former Cold Warriors, there's been a sharp shift to the hawk end of the spectrum.</P>
<P>As one commentator put it, Washington thinks it has "a license to spy on and otherwise invade the world, killing and maiming whenever the time seems right." As this latest episode shows, being the world's main superpower means never having to say you're sorry.</P>


