<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Labor's challenges--and potential</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 19 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"DO OR DIE." So said AFL-CIO President John Sweeney to the labor federation's executive council last month following reports of another decline in union membership to 13 percent overall and less than 10 percent in the private sector. And that was before President Bush launched a series of anti-union measures--and before the most recent spike in job losses in manufacturing, the worst in 10 years.</P>
<P>What's more, Bush is seeking to accelerate the move toward the Free Trade Area of the Americas, an effort to expand NAFTA to the entire Western Hemisphere except Cuba. So Sweeney's sense of urgency is well founded.</P>
<P>Yet big questions hang over every aspect of labor's strategy--on organizing, challenging the employers, politics and trade. On the FTAA, the AFL-CIO has backed the internationalism on display in the 1999 Seattle demonstrations against the World Trade Organization.</P>
<P>But the federation hasn't mobilized. And its statements of cross-border solidarity have been undercut by the International Brotherhood of Teamsters President James Hoffa's campaign to bar "unsafe" Mexican trucks from the U.S.</P>
<P>But U.S. highways are already full of dangerous trucks driven by nonunion workers--not because of NAFTA, but as a result of deregulation of the industry in the late 1970s. Hoffa's position--like labor's opposition to normal trade relations with China last year--is a throwback to the unions' old anti-immigrant and protectionist policies.</P>
<P>Labor's new internationalism will be further tested as job losses mount in manufacturing, especially steel. In 1998, the United Steelworkers of America (USWA) responded to a sharp rise in steel imports with a "Stand Up for Steel" campaign alongside steel company executives.</P>
<P>But collaboration and concession with steel employers in the 1980s didn't save jobs--and inevitably pits U.S. steelworkers against their counterparts in other countries. In fact, Sweeney's strategy of "partnership" allowed employers like Boeing, General Motors and DaimlerChrysler to keep slashing jobs even during the boom.</P>
<P>With recession looming, some union leaders are already in retreat. The Carpenters' union pulled out of the AFL-CIO this month in order to make sweetheart deals with construction companies. And United Auto Workers (UAW) President Steve Yokich this month defended DaimlerChrysler management's  26,000 job cuts on the grounds that the old Chrysler might have gone out of business.</P>
<P>Labor's reliance on the Democrats is another dead weight on the unions. Bill Clinton promised pro-labor policies and delivered NAFTA instead. And now the Republicans control Congress and the White House.</P>
<P>Some observers compare the situation to that of the early 1980s, when Ronald Reagan smashed the air traffic controllers' union PATCO and the employers used a recession to extract billions of dollars in concessions. But there are big differences in the labor movement today.</P>
<P>Despite some serious defeats, unions have made the strike a weapon again, beating powerful employers like United Parcel Service and Verizon. Moreover, the AFL-CIO has demanded amnesty for undocumented immigrants--a big change over anti-immigrant policies of decades past.</P>
<P>All this is a reflection of a much broader shift in working peoples' consciousness. In the early 1980s, Reagan, the employers and union leaders alike were able to convince large numbers of workers to take cuts to "help America compete with Japan." Today, Japan is in a mess and the U.S. is the world's dominant economic power. And still the employers demand cuts.</P>
<P>Workers' bitterness at Corporate America's arrogance fueled the big union protest in Seattle. The demonstration showed the possibility of a labor alliance with the new global justice movement.</P>
<P>And in recent years, rank-and-file workers have tossed out do-nothing union officials in important locals in the UAW, the Teamsters and the Transport Workers Union. This new spirit of resistance and solidarity shows the potential to challenge the dead-end strategies of labor's top leaders--and to rebuild a fighting labor movement from the bottom up.</P>



