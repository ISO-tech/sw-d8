<P><font face="Arial, Helvetica, sans-serif" size="2"><b>PERU</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Where is Peru headed after Fujimori?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2001 | Page 9 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LAST FALL, masses of Peruvians drove President Alberto Fujimori out of office with mass strikes and huge street protests. Fujimori's dictatorial regime had seemed unshakable months earlier. But it collapsed all the same--amid charges of rampant corruption, election tampering and systematic violations of human rights in its dirty war against leftist rebels, student dissidents and unionists.</P>
<P>As Socialist Worker went to press, Peru's voters were headed to the polls to elect Fujimori's successor. But the election won't produce a real alternative.</P>
<P>The leader in opinion polls was Alejandro Toledo, who probably beat Fujimori in elections last spring but was denied the presidency by massive vote fraud. Toledo, who is part Indian, has strong support among Peru's indigenous population and poses as a "man-of-the-people" populist.</P>
<P>But he's squandered much of his popularity by promising to continue Fujimori's most unpopular IMF-backed austerity programs. Trained at Harvard to be an economist at the World Bank, Toledo has done his best to assure international investors and Peru's rich that he means them no harm.</P>
<P>Lourdes Flores, a right-wing congresswoman, was Toledo's main challenger going into April. But former President Alan Garcia, of the left-of-center APRA party, was surging in the polls as the election approached.</P>
<P>As president from 1985 to 1990, Garcia oversaw the virtual collapse of the Peruvian economy, with inflation hitting 7,500 percent. Nonetheless, he's made gains by promising to spend money on job programs and to reduce poverty.</P>
<P>These mild attacks on the free market struck a chord--for obvious reasons. A worsening recession has driven more than half of Peru's population into dire poverty, and unemployment is nearing record highs. But whoever comes out ahead in the vote, ordinary Peruvians can expect no relief from the next president.  </P>
<P>Peru's working class has a militant history. Mass strikes, student protests and peasant mobilizations brought down military dictatorships in the late 1970s and 1980s. Unfortunately, the mass of the population didn't benefit from this.</P>
<P>If the huge struggles that brought down Fujimori are to lead to radical change, a new left will have to be created. This question won't be answered on April 9--but in the coming months and years of struggle.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Free Lori Berenson now!</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE NEW trial for American human rights worker Lori Berenson, who was convicted of treason in 1996 by hooded military judges, began in Peru in late March. Berenson has rotted in Peru's miserable prisons since a military court--operating on the principle that the accused are presumed guilty--found her guilty of terrorism.</P>
<P>In fact, her only "crime" was to defend Peru's left-wing MRTA rebels and their struggle against Fujimori's dictatorship. Fujimori's decision last summer to order a new trial was the result of pressure organized by Berenson's family and the growing unrest in Peru that eventually toppled him.</P>
