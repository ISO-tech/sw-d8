<P><font face="Times New Roman, Times, serif" size="4">Feds renew persecution of Mazen Al-Najjar</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Facing deportation on secret evidence</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER THREE and a half years of detention, Mazen Al-Najjar was freed last year when the "secret evidence" used to convict him didn't hold up. But now the 44-year-old Palestinian man is back in prison--and threatened with deportation--based on the same evidence.</P>
<P>In 1997, secret evidence was used to jail Al-Najjar. A year ago, Al-Najjar was freed after Attorney General Janet Reno admitted that there was no basis for holding him.</P>
<P>Then, on September 24, as part of its massive federal crackdown on civil liberties, Immigration and Naturalization Service (INS) agents arrested Al-Najjar outside his home in Tampa, Fla. Al-Najjar is being held in solitary confinement 23 hours a day and isn't allowed to call his family.</P>
<P>The INS claims that it is deporting Al-Najjar because he overstayed his student visa in the 1980s. But in their public statements, officials have dredged up charges that Al-Najjar has ties to terrorist organizations--charges that were dismissed earlier in court. "The whole case happened as a government reaction to an individual practicing his first amendment freedom of expression," said Al-Najjar, a prominent Palestinian rights advocate.</P>
<P>The government hopes to deport Al-Najjar to the United Arab Emirates, even though he has no residence or family there. It wants to deport his wife, Fedaa, to Saudi Arabia. Their three American-born daughters are ages 6, 11 and 13.</P>
<P>"My family really suffered a lot, especially my children," said Al-Najjar. "The youngest of them, when I left her, was barely 2 years old...This is really the most beautiful part of the child's life--and I missed it."</P>
<P>The Bush administration is using this case as a test for its new broad federal powers to detain and deport immigrants based on secret evidence. "I believe that the government will be successful in his deportation," Attorney General John Ashcroft boasted in a November 27 press conference. But Joe Hohenstein, one of Al-Najjar's attorneys, said he plans to file a petition for Supreme Court review of the case.</P>
<P>Activism won Al-Najjar's release in 2000, and will be key to stopping his deportation. "Before my detention, I was having second thoughts about activism. Sometimes I felt the fatigue. But after my detention, I am really a different person. I will never quit from activism. I will never be fully satisfied until I see every prisoner of conscience freed from injustice," Al-Najjar said.</P>

