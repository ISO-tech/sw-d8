<font face="Times New Roman, Times, serif" size="5"><b>How the U.S. tramples dissent</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Robert Justin Goldstein, <B><I>Political Repression in Modern America: From 1870 to 1976.</B></I> University of Illinois Press, 682 pages, 2001, $25.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN I read that George W. Bush had called for the creation of a volunteer civil defense force to aid in the domestic "war on terrorism," a shiver ran down my spine.</P>
<P>But since reading Robert Goldstein's <I>Political Repression in Modern America: From 1870 to 1976,</I> I considered for a moment that perhaps Bush had a keener sense of history than I'd given him credit for--but only for a moment.</P>
<P>Bush's proposal isn't consciously modeled on President Woodrow Wilson's American Protective League (APL), I don't think. It's just that Bush--as heir to the family's political dynasty as well as the family fortune--is a product of the longstanding U.S. tradition of using wars and crises to trample on civil liberties.</P>
<P>The official purpose of Wilson's APL--with 350,000 agents by the end of the First World War--was to help the government with food rationing, setting up draft offices and investigating the loyalty of Americans who wished to travel abroad.</P>
<P>In reality, the APL--established in 1917--"quickly became a largely out-of-control quasi-governmental, quasi-vigilante agency which established a massive spy network across the land," writes Goldstein.</P>
<P>APL agents bugged, infiltrated, disrupted and attacked radical organizations and union meetings. Not surprisingly, as Goldstein points out, "[t]he head of the APL urged his chief lieutenants to obtain financial support from leading businessmen 'who usually are the ones benefited in a property sense by the protection afforded by our organization.'"</P>
<P>But the APL was just a warm-up for the Palmer raids--named after Attorney General A. Mitchell Palmer--which, beginning in late 1919, led to the roundup of thousands of immigrants and the forced deportation of hundreds on the grounds that they were "communists."</P>
<P>Palmer laid the groundwork by stoking panic and racist fears of "alien filth." Like U.S. Attorney General John Ashcroft today, Palmer issued warnings of violent attacks against the government--which never materialized.</P>
<P>The Palmer raids happened in the midst of massive strikes in the steel and coal industries and meant that thousands of strikers were arrested on minor charges, such as laughing at police. In the end, they targeted the remnants of the Industrial Workers of the World and the newly founded Communist Party (CP). In one day in 1920, 5,000 to 10,000 CP members were rounded up in 30 cities.</P>
<P>Goldstein's book is an exhaustive accounting of the U.S. government's history of repression--from putting down the great labor uprisings of the 1870s to the FBI infiltration and decimation of Black revolutionary groups and peace organizations in the 1970s.</P>
<P>It shows how politicians justify attacks on civil liberties by appealing to the "need for security," but also how they use their expanded powers to go after anyone who dares to dissent.</P>

