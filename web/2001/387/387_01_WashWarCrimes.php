<P><font face="Times New Roman, Times, serif" size="4">U.S. kills thousands of Afghan civilians</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's war crimes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. OFFICIALS insist the war in Afghanistan is a "war on terrorism" not a war against the Afghan people. But since the Pentagon has dropped 10,000 bombs on one of the world's poorest countries, that story has become harder to sell.</P>
<P>According to a study by University of New Hampshire economics professor Marc Herold, U.S. bombs had likely killed more than 3,500 civilians by early December. His sources weren't Taliban officials--whose claims of civilian casualties were denied by the Pentagon. Herold relied on mainstream news organizations such as the BBC and the <I>Los Angeles Times</I>.</P>
<P>He found that across Afghanistan, whole villages have been destroyed. On October 22, 93 people died under U.S. bombs in Chowkar-Karez. On October 27, at least 16 were killed in the village of Khan Agaha. On November 17, two entire families--30 people--were killed in Charikar. That same day, at least 100 people were killed in Khanabad.</P>
<P>So much for Secretary of Defense Donald Rumsfeld's claim that "it is next to impossible to get accurate information" about civilian casualties. It's only "impossible" if you don't want anyone to find out.</P>
<P>And as <I>Socialist Worker</I> went to press, horrific accounts of civilian deaths were still coming in from Kandahar, the last Taliban stronghold to fall and the focus of intense U.S. bombing. "It was like being inside a nightmare," said Haji Khan after he fled Kandahar and arrived at a refugee camp on the Pakistani border. "Everyone was crying. There were dead people everywhere."</P>
<P>By any measure, these are war crimes--plain and simple. But as far as "official opinion" is concerned, to say this is blasphemy.</P>
<P>"When people decry civilian deaths caused by the U.S. government, they're aiding propaganda efforts," wrote journalist and media critic Norman Solomon, describing the corporate media's double standards. "In sharp contrast, when civilian deaths are caused by bombers who hate America, the perpetrators are evil and those deaths are tragedies."</P>
<P>In addition to the deaths caused directly by U.S. bombs, international aid agencies have warned that one million Afghans face death from starvation, disease and cold. "More than seven million people out of an estimated population of 22 million are classified by aid organizations as being at 'very high risk,'" Britain's <I>Independent</I> newspaper reported December 10.</P>
<P>Even German Foreign Minister Joshka Fischer, who is pushing for Germany to get in on the military action, admitted in mid-December that four million of the five million people needing aid are "inaccessible"--because of interference from Washington's warlord allies in the Northern Alliance.</P>
<P>The U.S. war will take the lives of more innocent civilians than died in the September 11 attacks on the World Trade Center--if it hasn't already. This carnage gives the lie to the claim that George W. Bush and the Pentagon ever meant to fight this war for justice--for anyone, anywhere.</P>
<P>And it shows the urgent need to organize to stop the U.S. military assault in Afghanistan--and to build a movement that can keep Washington from expanding its savage war.</P>

