<P><font face="Times New Roman, Times, serif" size="4">Right-wingers draft an academic blacklist</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Protect free speech</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I suppose you're doing something right when the vice president's wife accuses you of corrupting civilization.</P>
<P>In its ludicrous report, "Defending Civilization: How Our Universities Are Failing America and What Can Be Done About It," the American Council of Trustees and Alumni (ACTA) quotes me and more than 100 other antiwar students and professors around the country in order to "prove" that universities are mis-educating the nation's youth--the assumption being that all really well-educated people naturally fall in line with George W. Bush.</P>
<P>Although any reasonable person would have trouble taking the report seriously, it's worth doing so. ACTA board members include powerful people such as Sen. Joe Lieberman (D-Conn.) and Lynne Cheney, wife of the vice president.</P>
<P>And although it tosses off some fine words about the importance of "academic freedom," its main point is that those who don't swallow the Bush agenda "give aid and comfort" to terrorists.</P>
<P>We should call this shameful report what it is: an academic blacklist and a kindred spirit to the McCarthyist lists that destroyed the lives of hundreds of the country's finest teachers, artists and working-class fighters in the 1940s and 1950s.</P>
<P>We should defend everyone who faces these kinds of attacks and demand that our schools, unions and civic organizations do so as well. The Campaign for Academic Freedom's statement is a big step in the right direction.</P>
<P>As for me, I'm proud to stand with people like Noam Chomsky, Howard Zinn, and my own professor, William Keach, as they speak out against war and racism. I've learned far more from them than those so-called "leaders" and "educators" who do nothing but make excuses for every reeking act of thievery and murder committed by this country's ruling class.</P>
<B><P>Shaun Joseph,</B> Providence, R.I.</P>

