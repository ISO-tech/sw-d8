<P><font face="Times New Roman, Times, serif" size="5"><b>What $1 bought Afghans</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH'S appeal to U.S. schoolchildren to contribute $1 to a fund for Afghan children was always an obvious public relations stunt. Just how obvious became clear in December.</P>
<P>According to <I>Salon</I> magazine, the White House is planning to ship 10,000 "relief packages" to Afghan children, funded by the single-dollar donations. What do they contain? Starburst candy, raisins, two lollipops and a fuzzy plastic ball.</P>
<P>This to a country where 63 percent of children were chronically malnourished--before the war.</P>
<P>The U.S. government refused even to pause its bombing of Afghanistan to allow relief agencies to distribute desperately needed food to an estimated 1.5 million Afghans at risk of starvation.</P>
<P>But when it comes to candy and toys, no expense will be spared.</P>

