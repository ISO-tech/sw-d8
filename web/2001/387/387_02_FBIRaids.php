<P><font face="Times New Roman, Times, serif" size="5"><b>FBI agents raid Muslim charity</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STRIPPING THE rooms clean in raids that went on for hours, federal agents shut down offices of the Muslim charity Holy Land Foundation for Relief and Development on December 4.</P>
<P>The foundation provides aid to Palestinian families struggling under Israel's bloody occupation. But George W. Bush used the "war on terrorism" and attacks by suicide bombers in Israel the week before to seize the charitable organization's records and freeze all of its assets, claiming it is affiliated with the group Hamas.</P>
<P>"Money raised by the Holy Land Foundation is used by Hamas to support schools and indoctrinate children to grow up into suicide bombers," Bush said. Bush also blasted the group for giving donations to the widows and children of Hamas suicide bombers.</P>
<P>"Evidence" used to shut down the charity includes the testimony of unnamed informants and FBI agents. Agents from the FBI, Internal Revenue Service, Immigration and Naturalization Service and the Treasury Department's Office of Foreign Assets Control raided offices in Richardson, Texas; Paterson, N.J.; San Diego, Calif.; and Bridgeview, Ill., near Chicago.</P>
<P>"There's just no way they're involved with terrorists," said Mohammad Ibra of Chicago. "They send medicine and clothes and money to poor people in Palestine. They've built hospitals there. They're just trying to help our people in the holy land."</P>
<P>The raids will only encourage the racist hysteria and suspicion aimed at Muslims whipped up immediately after September 11. A Bridgeview mosque was already the target of an angry mob wielding American flags on September 13. And the night before the federal raid, a Chicago-area Arab community center was the target of arson.</P>
<P>The raids came at a terrible time--during Ramadan, when the foundation receives the bulk of its donations. Foundation officials are outraged.</P>
<P>"If the Holy Land Foundation had violated any U.S. law they would have charged us in a court of law," said Holy Land Foundation President Shukri Abu-Baker, who plans to fight this outrage in court. "They wouldn't need to seize our assets."</P>
<P>As a result of these raids, many Palestinian families will likely go hungry. And Bush will grow more confident to go after other organizations and further slash away at civil liberties.</P>
<P>We have to stop him.</P>

