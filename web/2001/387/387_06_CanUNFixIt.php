<P><font face="Times New Roman, Times, serif" size="5"><b>Can the UN fix Afghanistan?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WITH THE Taliban fleeing its last stronghold of Kandahar, what will happen next in Afghanistan? "[I]n clops the poor old UN donkey, dragged into the pit to undertake the most impossible task ever faced by statesmen in the history of the modern world: to sort out Afghanistan," British journalist Robert Fisk wrote last month.</P>
<P>The White House made it clear all along that it expected the UN to "take over the so-called nation-building" after the war, as George W. Bush put it in October. But this doesn't mean the U.S. is giving up its say-so over Afghanistan's future.</P>
<P>The U.S. government is the most powerful force in the UN and generally gets its way. If the U.S. doesn't want the UN around, the UN isn't around.</P>
<P>Like, for example, when U.S. officials stopped the deployment of UN "peacekeeping" troops in northern Afghanistan last month--supposedly so they didn't "interfere with ongoing military operations."</P>
<P>In reality, the U.S. didn't want UN troops witnessing the murder and mayhem committed by the Northern Alliance--and their U.S. Special Forces "advisers"--against surrendering Taliban fighters or the civilian population.</P>
<P>When the UN is allowed to establish a presence in Afghanistan, it won't be as a neutral force--but as U.S. power hidden under a new mask. That's why Afghanistan's interim prime minister--agreed on at the UN-convened summit of bickering Afghan warlords held in Germany--just happens to be Hamid Karzai, the Pashtun tribal leader with the closest ties to the U.S. government.</P>
<P>Yet many on the left welcome the coming UN presence. For example, a fact-finding delegation to Afghanistan organized by the human rights group Global Exchange recommended "immediate deployment of a UN security force" in place of the U.S. military.</P>
<P>No doubt many people in Afghanistan will welcome UN "peacekeepers" as preferable to the thugs and outlaws of the Northern Alliance--who "rule by rape, robbery and murder," as Britain's <I>Observer </I>newspaper put it.</P>
<P>But the record of UN "peacekeepers" isn't very peaceful. In Somalia, for example, UN troops--including U.S. Marines--killed as many as 10,000 people during their "humanitarian" mission.</P>
<P>One news photo showed Belgian "peacekeepers" roasting a Somali child over a fire.</P>
<P>In Kosovo after the 1999 NATO war, peacekeepers were supposed to protect Albanians who faced ethnic cleansing by Serbian forces from Slobodan Milosevic's government. Instead, Western troops stood by as Albanians committed ethnic cleansing in reverse--driving more than 180,000 Serbs out of Kosovo since June 1999.</P>
<P>More than two years after the war, the UN mission in Kosovo has total authority over the province. It runs the police, the courts and all public services. After much-hyped municipal elections held last year, elected officials were assigned a "co-head"--an international "adviser," answerable to the UN administration, who had veto power on all decisions.</P>
<P>The result of the UN's two-and-a-half years in Kosovo is a society awash in violence, suffering and injustice. "Having watched the international community in Kosovo for two years, I wonder at the naivet&eacute; of those making plans for Afghanistan," Scarlett MccGwire, who works in Kosovo for the Westminster Foundation for Democracy, wrote in an article last month. "The mistakes made in tiny, malleable Kosovo should be heeded: the new colonialism works no better than the old."</P>

