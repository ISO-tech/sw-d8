<P><font face="Times New Roman, Times, serif" size="5"><b>Illinois men exonerated after 15 years behind bars</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>Four Illinois men were exonerated this month when DNA testing cleared them--after spending 12 to 15 years each behind bars. Omar Saunders, Larry Ollins, Calvin Ollins and Marcellius Bradford were teenagers when they were charged with a 1986 rape and murder.</P>
<P>Former Chicago police crime lab analyst Pamela Fish helped to convict them, in testimony that DNA expert Ed Blake describes as "scientific fraud." According to Blake, Fish misrepresented her findings several times, and trial transcripts show that she linked the defendants to the crime even when the testing did not.</P>
<P>At last count, Fish helped to convict seven men who have since been exonerated by DNA testing! Two years ago, Fish was promoted to a $75,900 a year job as a supervisor at the Illinois state police crime lab. Undoubtedly, Fish has been working hand in hand with prosecutors and police to win convictions at any cost.</P>
<P>"If any crime ever cried out for the death penalty, it's this one," said Cook County Criminal Court Judge Christy Berkos at the time of the sentencing. But state law forbids the death penalty for anyone who was under 18 at the time of the crime, so three of the four defendants were sentenced to life without parole.</P>
<P>Bradford escaped a life sentence, receiving 12 years, by pleading guilty and testifying against Larry Ollins. He recently recanted his testimony and said that police forced him to confess by beating him repeatedly during a 24-hour interrogation.</P>
<P>Calvin Ollins, who defense attorneys describe as mildly mentally retarded, said that police tricked him into confessing.</P>
<P>The exonerated men are planning to file a civil rights lawsuit. But as Larry Ollins said, "Nothing can make up for the unjust deprivation of 15 years, half of all our individual lives, in a jungle."</P>

