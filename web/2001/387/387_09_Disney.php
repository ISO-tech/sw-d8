<P><font face="Times New Roman, Times, serif" size="5"><b>Disney's not-so-Magic Kingdom</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CORPORATE GIANT Disney Corp. celebrated the 100th anniversary of founder Walt Disney's birth this month with parades of cartoon animals through the Magic Kingdom's streets.</P>
<P>Disney, who is credited with creating Mickey Mouse, is supposed to be an icon of a world where all your dreams can come true. But these rules didn't apply if you worked for him.</P>
<P>Disney was a vicious union buster and anti-Communist. He demanded ironfisted control over all projects--and sole credit for anything that his team of animators created.</P>
<P>These conditions--plus the hiring of lesser-paid animators in preparation for layoffs and the firing of Goofy animator Art Babbitt for criticizing wage inequality at Disney--led to a strike during the production of <I>Dumbo</I> in 1941. Workers set up picket lines, carrying signs that read "Are We Mice or Men," or "No Strings on Me," with pictures of Pinocchio.</P>
<P>Disney viciously attacked strikers. In one instance, an anti-union goon poured a ring of gasoline around picketers, lit a cigarette and threatened to set them on fire.</P>
<P>During the strike, Disney photographed strikers and sent the pictures to the FBI to aid the McCarthyite witch-hunt. In 1947, Disney testified before the House Un-American Activities Committee, naming several union activists that he thought were reds.</P>
<P>Ironically, a biographer notes that Disney learned to draw by copying the comics out of the socialist newspaper, <I>Appeal to Reason,</I> that his father subscribed to.</P>
<P>During the Second World War, Disney made several films in support of the "war against fascism," many that included racist caricatures of the Japanese. But a few years earlier, Disney rubbed elbows with fascists, when he was the guest of Italian fascist Benito Mussolini, a huge Disney fan.</P>
<P>This true spirit of Disney continues today, with CEO Michael Eisner making money hand over fist while he exploits sweatshop labor in Haiti and fights union drives at the theme parks. That's the reality in the not-so-wonderful world of Disney.</P>

