<p><font face="Times New Roman, Times, serif" size="3">OTHER STORIES BELOW<br>
<font face="Times New Roman, Times, serif" size="3"><a href="#Catholic">New York Catholic school teachers</a><br>
<a href="#Noel">Obituary: Noel Col&oacute;n</a><br>


<br>
<P><font face="Times New Roman, Times, serif" size="4"><b>St. Catherine's Medical Center</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--After working without a contract since May 15, 477 members of the New York State Nurses' Association (NYSNA) struck November 26 at St. Catherine's of Siena Medical Center.</P>
<P>The Christmas spirit seems to have eluded administrators at New York City Catholic schools and hospitals, which have provoked strikes by refusing to address issues of poverty-level pensions and understaffing.</P>
<P>The State Health Department cited St. Catherine's for dangerously low staffing levels in 1998. Nurses are forced to work too much overtime with too many patients. "They have a policy of running the hospital at absolute minimum staffing, so if someone gets sick, you have a huge problem," said union spokesperson Anne Schott.</P>
<P>Nurses also want to switch to the union's health plan, which wouldn't require employee contributions. The hospital claims it can't afford it. And yet the hospital can afford to fly in scab nurses from all over in order to break the strike.</P>
<P>NYSNA held a December 1 rally and a candlelight vigil December 7 to gather support. "Everything the nurses are asking for is reasonable," said Michael Chacon, a nurse and NYSNA representative. "They're not asking for anything outlandish. The nurses just want to be able to create conditions that will improve staffing and enable them to provide the best care possible."</P>

