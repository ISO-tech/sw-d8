<p><a name="Noel"></a><font face="Times New Roman, Times, serif" size="4"><b>Obituary: Noel Col&oacute;n</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, steward, Teamster Local 705</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE TRAGIC death of Noel Col&oacute;n, an International vice presidential candidate on the Tom Leedham Rank and File Power slate, has left union activists saddened.</P>
<P>An emotionally disturbed staff member of Puerto Rico Teamsters Local 901 fatally shot Noel Col&oacute;n after being told he was to be dismissed by order of the executive board. Noel was the local's president, a position he won last year on a reform program.</P>
<P>I recently met Noel at the Teamsters for a Democratic Union Convention in Pittsburgh. He spent an hour of his time talking to me about the Puerto Rican union movement, and also the struggle to get the U.S. Navy out of Vieques.</P>
<P>He proceeded to buy me a beer, and I told him about the Charleston Five's struggle on the docks of South Carolina.</P>
<P>Noel himself began working on the docks of San Juan as a welder and joined the Teamsters in 1976. He was a militant who pushed for more rank-and-file involvement in the union and the struggle against the bosses.</P>
<P>He backed the creation of an elected stewards' council that had the power to approve or deny all of the executive board's major financial and policy decisions.</P>
<P>At the 2001 Teamsters Convention, Noel presented a successful resolution calling for the U.S. government to end its use of Vieques as a bombing range.</P>
<P>The Teamsters and the world's working class have lost a real fighter.</P>

