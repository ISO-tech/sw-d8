<p><a name="Catholic"></a><font face="Times New Roman, Times, serif" size="4"><b>New York Catholic school teachers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Dominic Renda</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--"The Catholic Church is not acting Christian." That's what one anonymous striking New York Catholic school teacher told <I>Socialist Worker</I> about the conduct of the Archdiocese of New York toward her union--the Lay Faculty Association.</P>
<P>Her 450-member union went on strike November 28 over wages, pensions and insurance. "They want us to pay more for our insurance, and that would cancel out an 8 percent raise they offered us," said the teacher.</P>
<P>The teachers at the 10 schools affected by this strike make an average of $31,900. The union is pushing for a raise that would allow them to make an average of $42,000 by 2004.</P>
<P>"We're not asking for huge sums of money," the teacher continued. "I can't get by in the city with this kind of income&#133;I have to work another job."</P>
<P>The union is also asking for an additional pension. Many teachers are leaving these schools because the existing pension is so low that they have to get other jobs when they retire. "[The additional pension] wouldn't cost the Archdiocese anything," the teacher added. "[But] they're not interested."</P>
<P>"What they're trying to do is divide us, and they have succeeded to some extent. There are some teachers that haven't come on strike in the first place and others who haven't even joined the union." She explained that the Archdiocese has told teachers they don't have to join the union or go on strike.</P>
<P>The antiunion campaign has been challenged with solidarity from students, their parents and other teachers' unions. Students have led walkouts in some of the schools. "A lot of the parents support us and most of the kids support us too," said the teacher. "They understand why we're doing this."</P>

