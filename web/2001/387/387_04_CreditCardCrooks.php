<P><font face="Times New Roman, Times, serif" size="5"><b>Seasons greetings from the credit card crooks</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I got a holiday surprise from Capital One.</P>
<P>They gave me a credit card last year with a $300 limit, which I quickly maxed out paying off last winter's gas and electricity bills. I've been paying off the debt at a 22.9 percent interest rate ever since.</P>
<P>But this month, I was shocked to get a bill for $112! I called up to see if there was some mistake.</P>
<P>They said that I was three days late in paying the monthly installment (of $14), so they were tacking on a $25 fee. In addition, November was the one-year anniversary of my "partnership" with Capital One, so they were throwing in another $59 fee for "annual membership services." To top it all off, since these two fees put my balance over the $300 limit, the friendly folks at Capital One were sticking me with an additional $25 "over limit" fee.</P>
<P>So, when all was said and done, I owe $412 (on a $300 card).</P>
<P>I haven't been to church recently, but isn't there something in the Bible about going to hell for charging interest on interest? Maybe the financial theologians at Capital One think they've found a loophole by calling their racketeering "fees" instead of "usury."</P>
<P>I sure hope the devil is keeping a list and checking it twice.</P>
<B><P>Todd Chretien,</B> Oakland, Calif.</P>

