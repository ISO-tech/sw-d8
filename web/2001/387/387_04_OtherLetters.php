<P><font face="Times New Roman, Times, serif" size="4"><b>Bush's snare of patriotic prejudice</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>During wartime, the government of the United States has always tried to generate pro-war propaganda, using patriotism to justify the mass murder of innocent civilians of much smaller and poorer nations all over the world.</P>
<P>Once, the U.S. government castigated Muhammad Ali when he refused to fight in the Vietnam War. Ironically, he became a hero to millions for his bold decision to set his face against the government.</P>
<P>Before 1954, the U.S. government maintained the law of segregation. It doesn't mean that segregation was justified.</P>
<P>The U.S. government also jailed Martin Luther King for his actions. Was that justified? The answer is no.</P>
<P>We should be careful about falling into President Bush's snare of patriotic prejudices, which is based on lies and misinformation. We should ask ourselves before we comply with our politicians and become more patriotic: Is it for the right cause?</P>
<B><P>A student,</B> City College of New York</P>

