<p><a name="Dignity"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend the right to die with dignity</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Attorney General John Ashcroft recently undermined Oregon's Death with Dignity Act. The act allows physician-assisted suicide for patients with terminal illnesses.</P>
<P>Under a new Drug Enforcement Agency policy, physicians would face losing their licenses if they prescribe a lethal dose of drugs for terminally ill patients.</P>
<P>Oregon voters have twice voted in favor of the act, but it has always been under threat from the right wing and religious zealots. More than 70 people have so far taken advantage of the law.</P>
<P>Bush is all too happy to see innocent people murdered by bombs coming from a B-52 bomber. We must defend death with dignity, and we must also fight for a different society where we can live free from wars, racism and unnecessary suffering.</P>
<B><P>Paul Dean,</B> Portland, Oregon</P>

