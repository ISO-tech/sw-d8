<p><a name="disarm"></a><font face="Times New Roman, Times, serif" size="4"><b>We can disarm the supporters of war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Selling <I>Socialist Worker</I> at Hunter College, we've found enormous enthusiasm for the paper and a deep distrust of the U.S. government among a sizable minority of people. At the same time, we've also run into some pretty rabid supporters of the war.</P>
<P>Emphasizing the human cost of this war is crucial. When talking to people about the war, we've been putting a lot of emphasis on the millions of Afghans being threatened with starvation by the war. We've also used posters at every sale with pictures of Afghan children killed by the bombs. These really help attract people who are most disgusted by the war.</P>
<P>Pictures like these also help disarm supporters of the war. People who see this war as a fight for justice are less likely to attack you for opposing the war in the face of pictures of children slaughtered by U.S. bombs.</P>
<P>For copies of pictures, people can e-mail me at david.thurston@att.net.</P>
<B><P>David Thurston,</B> New York City</P>

