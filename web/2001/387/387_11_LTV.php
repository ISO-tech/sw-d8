<P><font face="Times New Roman, Times, serif" size="5"><b>Steelworkers battle shutdown at LTV</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EAST CHICAGO, Ind.--A Federal bankruptcy judge delayed the planned closure of steelmaker LTV after unions and creditors pressed for more time.</P>
<P>Judge William Bodoh will give a committee of LTV creditors and the United Steelworkers of America (USWA) until February 28 to find a buyer for the country's third-largest integrated steelmaker, which employs about 7,500 in Cleveland; Hennepin, Ill.; and East Chicago, Ind.</P>
<P>LTV management--including CEO William Bricker, who resigned in November after collecting a $660,000 "retention" bonus--have literally tried to destroy the company while stuffing their own pockets.</P>
<P>"The workers are the ones who gave concessions to keep these plants going, and now they want to close them and use the assets from the closure to get a tax write-off," said Richard Dowdell, who represents East Chicago coke plant workers and is on the executive board of USWA Local 1011.</P>
<P>In August, the USWA agreed to millions of dollars in concessions in exchange for seats on the company board to keep LTV afloat. The union agreed to more concessions in late November.</P>
<P>But management suddenly ordered workers at the company's Indiana Harbor Works plant in East Chicago to fit pipes into the blast furnaces in order to flood them with water and destroy them. While the judge's December 7 order prohibits management from damaging the equipment, almost all USWA members will be laid off unless a buyer is found.</P>
<P>But the union isn't giving up without a fight. Hundreds of steelworkers and their families will travel to Washington, D.C., to set up a tent city December 11 outside AFL-CIO headquarters.</P>
<P>The collapse of LTV is part of a wider steel industry crisis that has put nearly two dozen companies in bankruptcy court since 1998. Industry leader USX-U.S. Steel is campaigning for the government to boost tariffs on foreign imports and allow consolidation of the industry into one or two giants.</P>
<P>The steelmakers also want the government to pick up $10 billion of underfunded pension obligations to the industry's 700,000 retirees as well as their medical coverage.</P>
<P>The Bush administration is considering an increase of tariffs of up to 40 percent on some types of imported steel. And the government may even take on at least part of the costs of pensions, since they are obligated to do so by the federal Pensions Benefits Guaranty Corp. (PBGC). But PBGC pensions are limited to roughly half the $1,600 that active LTV workers are eligible to receive.</P>
<P>Despite years of protectionist measures, the bosses have succeeded in boosting the companies' bottom line while laying off workers.</P>
<P>A just solution to the crisis would be to tap steel capacity to meet urgent social needs--like rebuilding schools and housing. That's the kind of fight that the USWA needs to undertake--one that could get the backing of the entire labor movement and working people everywhere.</P>

