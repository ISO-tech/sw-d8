<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Democrats cave in to the attack on civil liberties</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's witch-hunt</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"MY NAME is Ali Al-Maqtari, and I want to tell you the story of how I was jailed by the INS for almost eight weeks&#133;Even though I did nothing wrong, and cooperated with the INS, FBI and Army in every way possible, I spent many weeks in harsh jail conditions, cut off from my wife, and my wife had to give up her Army career. I tell you my story in the hope that it will help other innocent people avoid the problems that I had."</P>
<P>So said Yemeni immigrant Ali Al-Maqtari in his testimony before the Senate Judiciary Committee this month. He was arrested September 15 when he drove to an army base with his wife to pick up her orders.</P>
<P>Al-Maqtari's life was nearly destroyed--because he is an Arab. But when U.S. Attorney General John Ashcroft testified before the same Senate committee two days later, he accused anyone who questions such measures of being an ally of "the terrorists."</P>
<P>"[T]o those who scare peace-loving people with phantoms of lost liberty, my message is this: Your tactics only aid terrorists, for they erode our national unity and diminish our resolve. They give ammunition to America's enemies and pause to America's friends. They encourage people of good will to remain silent in the face of evil."</P>
<P>Ashcroft isn't satisfied that he's been able to hold more than 600 people in jail on charges unrelated to September 11. And it isn't enough that he's authorized government eavesdropping on attorney-client conversations, secret phone wiretaps and searches of homes and the offices of political organizations and charities. Ashcroft isn't even content that his boss, George W. Bush, has okayed the use of secret military tribunals for terrorist suspects--where death sentences can be handed down on a two-thirds vote.</P>
<P>He's out to intimidate and silence anyone who simply criticizes the Bush administration's wholesale shredding of the Bill of Rights.</P>
<P>You might think that a Senate Democrat or two would question Ashcroft's outrageous, McCarthy-style attack on civil rights. But Ashcroft's three hours of testimony was a cakewalk.</P>
<P>Sen. Zell Miller (D-Ga.) even released a statement defending the attorney general--the day before the hearing. "Let the attorney general do his job&#133;These nitpickers need to find another nit to pick. They need to stop protecting the rights of terrorists," said Miller.</P>
<P>And when questioned about Ashcroft's rabid characterization of anyone who questions the government as "aiding terrorists," Sen. Patrick Leahy (D- Vt.) could only lamely say that he "didn't understand that from him."</P>
<P>The closest the Democrats could come to real criticism was to point out that Ashcroft has turned down a request made by the FBI to look at the records of gun purchases in order to find terrorist suspects. It seems he's willing to trample over pretty much every freedom--except the ones that are championed by right-wing extremists.</P>
<P>Shamefully, not a single Democrat saw fit to question Ashcroft about his recent plan to allow the FBI to infiltrate religious and political organizations. This despite the fact that the FBI was banned from such activities in the 1970s after it was caught systematically spreading rumors and lies, forging documents and assassinating political opponents.</P>
<P>Then, activists organized to fight back against these attacks--and exposed as a sham FBI claims to be protecting "freedom and democracy." We need to do the same today.</P>
<P>It's time to stand up to Ashcroft's smear tactics--and organize to protect our civil rights.</P>

