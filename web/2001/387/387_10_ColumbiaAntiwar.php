<P><font face="Times New Roman, Times, serif" size="5"><b>Columbia University antiwar protests</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>STUDENTS AT Columbia University won't be silenced by pro-war bigots. In late November, 40 people turned out to hear a U.S. veteran of the war in Bosnia speak against the war in Afghanistan.</P>
<P>Ricardo, an undergraduate who served in the Marines, told the crowd about his experiences. According to Ricardo, his experiences in Bosnia were nothing more than "trampling through other people's backyards," which taught him to question every U.S. military intervention.</P>
<P>"They're bombing innocents and dropping pennies to the starving," said Ricardo about why he opposes the war in Afghanistan.</P>
<P>With that same antiwar spirit, students held a protest of a campus speech by right-wing columnist Ann Coulter on December 5. Coulter was fired from the <I>National Review</I> for writing racist statements, such as her claim that the U.S. should "invade [Muslim] countries, kill their leaders and convert them to Christianity."</P>
<P>Coulter fled the room after a very brief question-and-answer period, with most of the crowd chanting "Don't come back!"</P>
<P>The next day, close to 100 people gathered to speak out against the university's decision to release foreign students' private information to federal authorities.</P>

