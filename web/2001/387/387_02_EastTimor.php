<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. approved attack on East Timor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>RECENTLY RELEASED documents prove that President Gerald Ford and his administration gave the green light for Indonesia's 1975 invasion and annexation of East Timor--one of the most terrible war crimes in history. Indonesia's occupation cost the lives of as many as 200,000 East Timorese--about one-third of the population.</P>
<P>Ford and Secretary of State Harry Kissinger met with Indonesian dictator Gen. Suharto. According to documents collected by George Washington University's National Security Archive, Suharto told the two about his plans to take over East Timor, a Portuguese colony that had won liberation a couple of weeks earlier.</P>
<P>"We will understand and will not press you on the issue," Ford replied. Kissinger, too, offered encouragement. "It is important that whatever you do succeeds quickly," Kissinger said.</P>
<P>The invasion went ahead the day after the meeting--the first step in one of the most horrific genocides in history.</P>
<P>Vice President Dick Cheney was Ford's chief of staff--and therefore certain to have known of the invasion plan. Cheney's mentor, Donald Rumsfeld, was Ford's secretary of defense--the same post he holds today.</P>
<P>The documents provide enough evidence to put Ford and his cronies on trial for complicity in war crimes. But Cheney and Rumsfeld are busy committing more atrocities in Afghanistan.</P>

