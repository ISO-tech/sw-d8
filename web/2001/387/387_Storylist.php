<P><font face="Times New Roman, Times, serif" size="3"><b>Dividing the globe in the name of democracy and freedom</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_08_Imperialism.shtml">Washington's new imperialism</A></b></font><BR>
