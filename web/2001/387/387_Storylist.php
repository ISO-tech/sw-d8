<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>NATIONAL NEWS</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Feds renew persecution of Mazen Al-Najjar</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_02_SecretEvidence.shtml">Facing deportation on secret evidence</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">After three and a half years of detention, Mazen Al-Najjar was freed last year when the "secret evidence" used to convict him didn't hold up. But now the 44-year-old Palestinian man is back in prison--and threatened with deportation--based on the same evidence.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_02_FBIRaids.shtml">FBI agents raid Muslim charity</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Stripping the rooms clean in raids that went on for hours, federal agents shut down offices of the Muslim charity Holy Land Foundation for Relief and Development on December 4.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_02_EastTimor.shtml">U.S. approved attack on East Timor</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Recently released documents prove that President Gerald Ford and his administration gave the green light for Indonesia's 1975 invasion and annexation of East Timor--one of the most terrible war crimes in history. Indonesia's occupation cost the lives of as many as 200,000 East Timorese.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_02_LameDollarProgram.shtml">What $1 bought Afghans</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">George W. Bush's appeal to U.S. schoolchildren to contribute $1 to a fund for Afghan children was always an obvious public relations stunt. Just how obvious became clear in December.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_02_IllinoisMenFreed.shtml">Illinois men exonerated after 15 years behind bars</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Four Illinois men were exonerated this month when DNA testing cleared them--after spending 12 to 15 years each behind bars.</P>


