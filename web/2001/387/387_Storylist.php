<p><font face="Arial, Helvetica, sans-serif" size="2" color="#990000">December 14, 2001 | Issue 387</font></p>

<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>FRONT AND BACK PAGES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>U.S. kills thousands of Afghan civilians</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_01_WashWarCrimes.shtml">Washington's war crimes</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">U.S. officials insist the war in Afghanistan is a "war on terrorism" not a war against the Afghan people. But since the Pentagon has dropped 10,000 bombs on one of the world's poorest countries, that story has become harder to sell.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_12_DefendJobs.shtml">Defend our jobs! Defend our unions!</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">More than 225 teachers of the Middletown school district in New Jersey defied court injunctions against a strike and went to jail in their struggle for a fair contract. Meanwhile, 5,100 members of International Association of Machinists District 91 were on strike against aircraft engine manufacturer Pratt &amp; Whitney.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_12_FastTrack.shtml">Bush's fast-track attack</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Earlier this month, George W. Bush rammed "fast track" free-trade legislation, now called "trade promotion authority," through the House of Representatives by one vote. Outrageously, he used the "war on terror" as an excuse.</P>


<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b><i>SW</i> SPECIAL FEATURES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>The real face of Washington's war</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_06_NewAfghanistan.shtml">Nightmare in the "new" Afghanistan</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">To listen to the Pentagon, the village of Kama Ado is alive and well. Sure, U.S. warplanes dropped bombs nearby, but Pentagon spokespeople dismissed reports of civilian casualties from off-target bombs. However, up to half of the area's residents may have been killed.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Arafat faces mounting pressure from Israel and the U.S.</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_05_Israel.shtml">Israel lashes out</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Casualties continue to mount as Israel steps up its war on Palestinians following three suicide bombings at the beginning of December. In less than a week, more than 100 Palestinians--most of them children--were injured, and two killed.</P>


