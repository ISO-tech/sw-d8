<P><font face="Times New Roman, Times, serif" size="2"><B>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="387_09_EconomistsCant.shtml">Why economists can't explain crisis</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">"I say to my mom, 'Don't worry, there will not be another Depression,' and she says, 'Why not?' And I say I don't really know why not." This comes from an economic historian quoted in the <I>New York Times</I>. Economists seem unable to predict the onset of slumps, their duration, how they came about or how they will end.</P>


