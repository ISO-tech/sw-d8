<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Trying to bring back COINTELPRO</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THERE IS no question that George W. Bush's "either you're with us, or with the terrorists" atmosphere has a certain Cold War ring to it.</P>
<P>Last week, U.S. Attorney General John Ashcroft--visibly basking in the glow of opinion polls showing widespread approval of the Bush administration's civil liberties crackdown--baited critics as traitors. "To those who scare peace-loving people with phantoms of lost liberty, my message is this: your tactics only aid the terrorists," he sneered.</P>
<P>Columnist Bob Herbert observed, while watching Ashcroft testifying, "I experienced the disturbing sense of a 21st century official morphing alternately into J. Edgar Hoover and Joe McCarthy."</P>
<P>There is no question that Bush and Ashcroft would like nothing better than to resurrect McCarthyism, the domestic witch-hunt that accompanied the Cold War. On December 1, Ashcroft announced plans to rid the FBI of restrictions on spying on domestic religious and political organizations that were put in place after widespread abuse against activists in the 1960s and 1970s.</P>
<P>In other words, Ashcroft plans to bring back COINTELPRO, the FBI's counterintelligence program initiated by J. Edgar Hoover against the Communist Party in 1956. By the 1960s, COINTELPRO had mushroomed into a nationwide dragnet that targeted hundreds of organizations--including groups as diverse as Students for a Democratic Society (SDS), the National Organization for Women (NOW), SANE/Freeze, the Southern Christian Leadership Conference (SCLC) and Business Executives Against the War in Vietnam.</P>
<P>Wiretapping--the feature most associated with FBI surveillance--was just a small part of the program. COINTELPRO involved a complex web of infiltration and blackmail, forgeries and lies, imprisonment and assassination of the FBI's "most wanted."</P>
<P>After former Pentagon staffer Daniel Ellsburg leaked papers to the press detailing years of U.S. lies about the Vietnam War, COINTELPRO "plumbers" broke into his psychiatrist's office to gather materials to smear him publicly.</P>
<P>When Martin Luther King Jr. won the Nobel Peace Prize in 1964, Hoover called him "the most notorious liar in the country." When King spoke out against the Vietnam War, the FBI stepped up its campaign, conducting massive surveillance of his personal life, attempting to blackmail him over alleged extramarital affairs.</P>
<P>The FBI's real fears were revealed in an internal document stating that it aimed to prevent King from developing into a Black "Messiah" who could "unify and electrify" the growing Black Power movement.</P>
<P>Hoover called the Black Panther Party the "greatest threat to the internal security of the country" and developed an elaborate plan to "neutralize" the organization. The FBI's plan was to infiltrate and disrupt the party by "promoting internal dissension"--which the agency also referred to as its "Black Propaganda campaign."</P>
<P>Agents forged documents aimed at spreading misinformation to discredit the party and letters aimed at sowing distrust between party leaders. Undercover agents engaged in illegal activities that led to the arrest of Black Panther leaders.</P>
<P>FBI and police agents also assassinated key Black Panther leaders to destroy the organization at its base. Fred Hampton was only 19 years old when the FBI opened a file on him. Two years later, Chicago police entered Hampton's apartment and shot him dead as he slept.</P>
<P>Hoover originally launched the COINTELPRO program as a "national security" measure, directed against the Communist Party, in 1956. But Hoover's obsession with Communism in the 1950s was redirected at the rising movements in the 1960s, especially civil rights and Black Power groups.</P>
<P>Ashcroft's politics are not a far cry from those of J. Edgar Hoover. He is a staunch Christian conservative who opposes desegregation and abortion rights and has links to right-wing militia groups.</P>
<P>Ashcroft said the administration's policies will apply only to "terrorists" who "infiltrate our communities, plotting, planning and waiting to kill again." History teaches us we can't believe him.</P>
<P>The time to fight is now.</P>

