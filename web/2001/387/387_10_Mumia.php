<P><font face="Times New Roman, Times, serif" size="5"><b>Free Mumia Abu-Jamal</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 14, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>POLICE VICIOUSLY attacked a 500-strong march in Philadelphia for death row prisoner Mumia Abu-Jamal December 8.</P>
<P>The attack started when a woman joined the march and began calling for Mumia's execution, according to an eyewitness. She then grabbed a protester and screamed for police.</P>
<P>Police threw the protester on the ground, and as other marchers tried to come to his aid, police overreacted with pepper spray and billy clubs. One cop even pulled a gun and waved it at demonstrators, according to several witnesses.</P>
<P>Cops dragged some of those they arrested for almost a whole block--one woman was in obvious agony as police dragged her.</P>
<P>People from across the country attended the march. Exonerated Illinois death row prisoner Rev. Darby Tillis traveled from Tennessee to join the protest. Tillis said that he believes it's still possible to free Mumia despite the assault on civil liberties since the September 11 attacks.</P>
<P>Many marchers carried signs that read, "Free Mumia--defend dissent."</P>
<P>Dennis Brutus, a prominent global justice activist, spoke at the march and made connections between Mumia's case, the war on Afghanistan and the movement against corporate globalization.</P>
<P>But in order to win justice for Mumia, it will be necessary to reach out and involve larger numbers of people.</P>

