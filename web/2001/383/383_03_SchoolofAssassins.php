<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Shut down the School of Assassins</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>"IF ANY government sponsors the outlaws and killers of innocents, they have become outlaws and murderers themselves." That was George W. Bush's warning to foreign governments that "harbor terrorists" as the bombs began to fall on Afghanistan.</P>
<P>He should take a look in the mirror. For the last half-century, the U.S. military has run its very own terrorist training camp--at Fort Benning, Ga.</P>
<P>Known until last year as the School of the Americas (SOA), the center trained more than 60,000 Latin American military personnel--including some of the Western hemisphere's most notorious human rights abusers and dictators. </P>
<P>U.S. officials claim that the SOA promotes "democracy." But as former director Col. Jos&eacute; Alvarez put it, its purpose is to give the U.S. "a seat at the table with the armies of Latin America&#133;to have a say in how nations conduct themselves."</P>
<P>What kind of conduct? When several SOA training manuals were exposed in 1996, the <I>New York Times</I> reported that they "recommended interrogation techniques like torture, execution, blackmail and arresting the relatives of those being questioned."</P>
<P>Since the early 1990s, activists in the group SOA Watch have organized to shut the school down, holding an annual demonstration at Fort Benning that has drawn more than 10,000 people each time over the last few years.</P>
<P>Faced with this growing pressure, Congress tried to give the SOA a "face-lift" last year, officially changing its name to the Western Hemisphere Institute for Security Cooperation (WHISC). "A new institute for a new century!" proclaims the WHISC Web site.</P>
<P>What a sham! It may have a new name, but this is the same school for assassins and torturers--terrorists trained and funded by the U.S. government to abuse human rights and trample on democracy.</P>
<P>This year's demonstration against the SOA is set for November 16-18. The protests are certain to include many people new to the anti-SOA struggle--activists from the new antiwar movement who are learning for the first time about the crimes of the U.S. government and the thugs it supports and trains. This will be an important way of building links between different struggles--and of adding momentum to the fight against Bush's war.</P>
<P>If the U.S. government wants to wage a "war against terrorism," it can start in its own backyard--with the School of Assassins in Fort Benning.</P>

