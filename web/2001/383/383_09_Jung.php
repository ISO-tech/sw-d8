<P><font face="Times New Roman, Times, serif" size="5"><b>The lives of war-weary Afghans</b></font></p>
<font face="Arial, Helvetica, sans-serif" size="2"><p><STRONG>DOCUMENTARY: <I>Jung (War): In the Land of the Mujaheddin,</I></STRONG> directed by Fabrizio Lazzaretti and Alberto Vendemmiati. Showing in Chicago and New York. Contact Human Rights Watch to arrange a free showing at www.hrw.org/iff/traveling/jung.<BR></font>
<p><font face="Times New Roman, Times, serif" size="2"><b>Review by Kirstin Roberts</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P><I>Jung (War): In the Land of the Mujaheddin</I>--a documentary film that looks at the war-weary people of Afghanistan--is a powerful weapon in the antiwar movement's arsenal.</P>
<P>Filmed by an Italian television crew, the award-winning documentary follows a surgeon and a nurse as they struggle to build a hospital for war and land mine victims near the Pakistani border, in territory controlled by the Northern Alliance, in the late 1990s.</P>
<P>Some scenes are almost impossible to watch. A young boy begging doctors to put him out of his misery after his foot has been destroyed by a land mine is impossible to forget.</P>
<P>There's no way to watch <I>Jung</I> and walk away thinking that U.S. bombs will help the Afghan people. It shows how two decades of war have left Afghanistan's people devastated and prey to despicable rival military factions, some of whom were trained by the CIA. Afghan women describe their impossible plight as war widows in a country where women cannot legally work to support themselves or their children.</P>
<P>Using back-and-forth cuts between rugged, undeveloped countryside and tanks cutting through the land, <I>Jung</I> vividly depicts how the fifth century meets modern warfare in Afghanistan. While the makers of <I>Jung</I> mistakenly portray Northern Alliance soldiers as valiant freedom fighters against the Taliban, this political flaw doesn't undercut the film's power.</P>
<P>As U.S. bombs rain on Afghanistan, anyone who wants to understand what the Afghan people have faced needs to see this film. One theater had to show <I>Jung</I> all day long when more than 1,000 people turned out for a free showing. The antiwar movement should use this film to make the case for why this war is no solution.</P>

