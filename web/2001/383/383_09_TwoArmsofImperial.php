<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The two arms of U.S. imperialism</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>WE HAVE said in the past that the argument, popular in the global justice movement, that corporate globalization undermines the "sovereignty" of states could, if unqualified, lead to nationalism--to the idea that you must rally around your "own" state. The idea, we argued, was wrong and could act as a pull to the right.</P>
<P>The most powerful states could use the institutions of global capitalism to their advantage, using them to pry markets open to investments. In short, countries like the U.S. use their economic leverage to undermine the sovereignty of weaker states, but it would be wrong to say that U.S. "sovereignty" is threatened by institutions that it controls and uses for its benefit.</P>
<P>Arguing that America must protect its "sovereignty" means siding with the most powerful state in the world, whose economic and military policies consist in denying the sovereignty of others.</P>
<P>The result of such politics is now apparent in the wake of September 11 in the musings of William Greider. In a recent <I>Nation </I>piece, Greider, a critic of the ravages of corporate globalization, attacks "borderless" corporations like General Electric for using their leverage during the post-September 11 crisis to get financial handouts from the government.</P>
<P>"Americans will be rightly infuriated," he fumes, "as they see the urgent need for national unity exploited for private gain." Greider hopes that "leading governments," but especially the U.S., will take the opportunity afforded by the current war to use their "neglected sovereign powers" to intervene and impose economic regulations on footloose capital.</P>
<P>Greider is right to point out how "national unity" is being used to bail out corporations at the expense of workers, but he is completely wrong in every other respect.</P>
<P>Greider wants to separate the U.S. state, including its military pursuits, from its economic aspects. But U.S. war aims and corporate economic interest cannot be separated today any more than they could at the turn of the last century.</P>
<P>Then, it was clear that "commerce followed the flag," that along with economic policies designed to open the world's markets to U.S. goods also went military might--"gunboat diplomacy" and sometimes all-out war.</P>
<P>Corporations today may operate internationally, but they still depend on their "home" country to defend their interests. That explains why, for example, Unocal won economic deals with governments in Central Asia in a way that companies without U.S. backing could not.</P>
<P>Looking after the interests of the corporate profiteers is, and always has been, the business of government under capitalism.</P>
<P>Greider's attempt to portray himself as the "true patriot" against corporations that seek to profit from the crisis forgets that this war is run by patriots in the White House who live, think and breathe corporate interests.</P>
<P>Worse, to speak of U.S. "sovereignty" at a time when it is using its overwhelmingly superior military and economic might to impose its policies in Afghanistan and the world means nothing less than supporting the right of the only superpower to impose its will on other nations.</P>
<P>Greider has the gall to appeal to the government to promote a more "humane" and "equitable" society. The bureaucrats, politicians and generals who run the U.S. have no interest in a humane, equitable or democratic society, here or elsewhere. They are in fact busy using this crisis not only to bail out corporations but to shred our basic democratic rights.</P>
<P>Imperialism has different arms. Sometimes it uses its economic arm, as when the U.S. uses institutions such as the WTO and the World Bank to impose its economic policies on weaker nations.</P>
<P>Sometimes, though, its military arm starts swinging. When that happens, we must switch our focus from the economic ravages of capitalism to its military horrors, committed, in a different form, for the same ends.</P>

