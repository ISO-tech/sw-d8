<P><font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>William Jenkins</b></font></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>WILLIAM JENKINS died September 30 of a heart attack while at work. He was 44 years old. He worked for many years at Rush-Presbyterian-St. Luke's Medical Center in Chicago and was a member of the International Brotherhood of Teamsters Local 743.</P>
<P>Over the past several years, Jenkins (as he called himself) could be seen all over the Chicago area videotaping strikes, rallies and forums. He was president of the Committee for Labor Access, which produces the public access TV show <I>Labor Beat</I>, and he was on the board of the Union Producers and Programmers Network, a national group of labor TV and radio producers.</P>
<P>Jenkins was especially proud of his video work. He would leave his third-shift job at the hospital and head to the editing suite to put time into the next <I>Labor Beat</I> show--or to a rally or demonstration where he would often be the only reporter on the scene. Earlier this year, he attended the Teamsters convention, and his video work was put on alternative Internet sites so that thousands could see the fight of Teamsters reformers.</P>
<P>Jenkins was also an activist. A member of Teamsters for a Democratic Union, he was actively supporting the reform slates, both in the national Teamsters election and in his own local union.</P>
<P>About a year ago, Jenkins joined the International Socialist Organization (ISO). His first contact with the ISO was through <I>Socialist Worker</I>, but what brought him into the organization was the <I>International Socialist Review</I> (ISR) magazine.</P>
<P>When Jenkins talked about his union reform work, when he talked about his video work and when he talked about the ISO, his main concern was for working people to understand the world we live in. As an African American, as a worker and as an activist, he found in the ISR an important vehicle for understanding the contradictions in our society.</P>
<P>Jenkins' frustration was that so few workers were in the socialist movement. Our frustration was to lose so soon a gentle friend and comrade.</P>
<B><P>Wayne Heimbach,</B> Chicago</P>

