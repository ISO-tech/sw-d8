<font face="Times New Roman, Times, serif" size="3">Prosecutors in Charleston, S.C., are taking aim at five union dockworkers. And organized labor and civil rights activists will protest this attack across the U.S. and around the world on November 14.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_10_C5DayofAction.shtml">A day of action for the Charleston Five</A></b></font><BR>
