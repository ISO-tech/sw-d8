<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>COLUMNS AND FEATURES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_05_MoratoriumNow.shtml">Moratorium now! Abolition next!</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">For years, Democrats and Republicans tried to outdo each other to show their support for capital punishment. But since 1999, the number of executions has fallen two years in a row--for the first time in 25 years. This is the latest sign of growing doubts about the U.S. killing machine.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_07_USWarofTerror.shtml">U.S. government's war <I>of</I> terrorism</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The looming prospect of mass civilian casualties from slaughter and starvation makes it more difficult for the Bush administration to sustain its claim that "this war is not against the Afghan people." This is not a "war against terrorism"--it <I>is</I> terrorism.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_09_TwoArmsofImperial.shtml">The two arms of U.S. imperialism</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The argument, popular in the global justice movement, that corporate globalization undermines the "sovereignty" of states could, if unqualified, lead to the idea that you must rally around your "own" state. The result of such politics is now apparent in the wake of September 11 in a recent <I>Nation</I> article by William Greider, a critic of the ravages of corporate globalization.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Shadowy world of the secret inner government of the U.S.</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_08_LiesSpiesTricks.shtml">Lies, spies and dirty tricks</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The top U.S. military brass wanted a war. Their solution? Stage an attack on an American military base, followed by a series of bombings and shootings that could be blamed on terrorist opponents of the U.S. If this was submitted as a Hollywood movie script, it would be rejected as too far-fetched. But it's the truth.</P>

