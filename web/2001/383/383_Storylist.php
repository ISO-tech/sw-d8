<font face="Times New Roman, Times, serif" size="3">The village of Chowkar-Karez no longer exists. Neither do most of the people who lived there. But that's what the Pentagon wanted. "The people there are dead because we wanted them dead," an unidentified military official told CNN.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Defend our union rights</b></font><BR>
