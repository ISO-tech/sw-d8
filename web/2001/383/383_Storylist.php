<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>WHAT WE THINK</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Washington's message to allies and enemies alike:</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_03_WhatWeSayGoes.shtml">What we say goes</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">George W. Bush promised that his "war against terrorism" would be based on a coalition of nations. But now that coalition "partners" are beginning to complain, the U.S. has a new message for all of them: We'll escalate the killing when and how we want--whether you like it or not.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_03_SchoolofAssassins.shtml">Shut down the School of Assassins</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">"If any government sponsors the outlaws and killers of innocents, they have become outlaws and murderers themselves." That was George W. Bush's warning to foreign governments that "harbor terrorists" as the bombs began to fall on Afghanistan. For the last half-century, the U.S. military has run its very own terrorist training camp--at Fort Benning, Ga.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_03_Argentina.shtml">Argentina at the brink</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Once a showcase of neoliberal economic "magic," Argentina has become a basket case of neoliberal reality.</P>

