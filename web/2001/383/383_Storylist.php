<P><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b><i>SW</i> READERS SPEAK OUT</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3"><b>"They don't give a damn about the rest of us"</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_04_WorkersAtRisk.shtml">Workers' lives at risk</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Every day, the news is filled with more terrifying rumors about anthrax. But for the past month, the same media that have been whipping up anthrax hysteria have been lying to all of us who work near "Ground Zero" in New York City about the air that we've been breathing.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_04_BerkeleyTeachers.shtml">Berkeley teachers oppose the war</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">I am a teacher in Berkeley, Calif., and a member of the Berkeley Federation of Teachers (BFT). On October 18, those of us on the executive board of the BFT approved a resolution opposing the U.S. response to the tragic events of September 11.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_04_CantBeScared.shtml">We can't be scared into keeping quiet</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">I think I may have been the victim of a terrorist attack. A few weeks back, I came home from an antiwar protest to find that my truck had been burned to the ground.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_04_NoRacistAttacks.shtml">I'm for making sure racists can't attack people</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">A few days after the September 11 attacks, a cartoon ran in the <I>Daily Californian</I>, the student newspaper at the University of California-Berkeley, that showed two turbaned Arab men carrying flight manuals in hell. Antiwar and antiracist students demanded an apology from the editors of the newspaper, but the editor-in-chief refused, calling it "freedom of speech."</P>

