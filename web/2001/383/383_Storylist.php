<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>ANTIWAR NEWS AND ANALYSIS</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Giuliani lashes out at the firefighters he praised as "heroes"</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_02_FirefightersAttack.shtml">Attacked at Ground Zero</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">On November 2, hundreds of firefighters--furious with new restrictions on how many could be at the World Trade Center site to help with recovering the remains of their comrades--marched on "ground zero," clashing with police who tried to bar their way.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>FBI looks for U.S. source of anthrax</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_02_AnthraxandNazis.shtml">Are Nazis to blame?</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">A month after the deadly anthrax attacks hit the headlines, four people are dead, more than a dozen others are infected--and only now have authorities begun to admit that the attacks might well have come from "homegrown" terrorists.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_02_CIABinLadenMeet.shtml">We know what the CIA was doing last summer</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">It's a strange way to treat "Public Enemy No. 1." But a leading French newspaper is reporting that the CIA met with Osama bin Laden when the U.S.-ally-turned-enemy was hospitalized last summer in the Persian Gulf state of Dubai.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>News from our movement</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="383_02_AntiwarConferences.shtml">Students build for conferences</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">With the Bush administration preparing for a long war, student activists are showing that they're prepared to build an antiwar movement from the ground up. November 10-11 is slated as a weekend of student antiwar conferences in cities across the U.S.</P>

