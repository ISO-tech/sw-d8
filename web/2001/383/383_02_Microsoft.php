<P><font face="Times New Roman, Times, serif" size="5"><b>The Microsoft mob got away scot-free</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>THE BEAST from Redmond, Wash., is back. Software giant Microsoft and the U.S. government ended their three-year legal battle with an agreement announced last week.</P>
<P>The Justice Department sued Microsoft in 1998 over antitrust violations, charging that Bill Gates and his boys had acted like a mafia crime family in building up an effective monopoly in the software industry.</P>
<P>The feds won two court decisions in the case--the last one, a 7-0 rejection of Microsoft's appeal by a federal appeals court. But last week's agreement gives up numerous concessions to the Microsoft mob.</P>
<P>The deal effectively allows Microsoft to continue its dominance in the market for Web browsers--even though the government proved that the Gates gang used extortion against rival Netscape.</P>
<P>Some of Microsoft's media minions justified the agreement as a patriotic response to the post-September 11 economic tailspin. But Norman Hawker, a research fellow at the American Antitrust Institute, says that the Bush administration wanted to lose.</P>
<P>"They expected to lose in the Court of Appeals," Hawker said. "When they won, they were put in the embarrassing position of winning a case that they were opposed to from the start&#133;Microsoft, when they saw this settlement, must have thought it was raining pennies from heaven."</P>

