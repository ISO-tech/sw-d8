<P><font face="Times New Roman, Times, serif" size="5"><b><i>Boondocks</I> won't be silenced</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"We'd like to take this time to remind all Americans in this time of hardship that 33 News coverage of 'America Strikes Back' is brought to you by&#133;Nike, who would like to remind all freedom-loving, brave American soldiers to 'Just Do It.'"</P>
<P>In Aaron McGruder's comic strip, <I>Boondocks</I>--distributed by Universal Press Syndicates--Huey Freeman stares, disgusted, at the lies and blind patriotism of the pundits on his TV screen.</P>
<P>Huey is an African American kid struggling to make it through life in suburbia, where he lives with his grandfather. He's been a thoughtful left-wing critic since McGruder started the strip in 1997 as a comment on hip-hop culture, racial diversity and being young in materialist American society.</P>
<P>While <I>Boondocks</I> has always been controversial, some of the 250 newspapers that carry the strip began censoring it after September 11.</P>
<P>A series of strips featured Huey calling the FBI's antiterrorist tip line to convince them that the U.S. government is responsible for the terrorism. He explains that the CIA trained Osama bin Laden and that George Bush gave the Taliban $43 million in May.</P>
<I><P>Newsday </I>and the <I>Daily News</I> have refused to run the strip on days that they determined McGruder's critique of patriotism to be too scathing. The <I>Dallas Morning News</I> moved the strip to a page away from other comics.</P>
<P>McGruder responded defiantly. His new character, Ribbon, trembles to his friend, "Hey Flagee, there's a lot of evil out there!"</P>
<P>"That's right, Ribbon," says the flag. "Good thing America kicks a lot of *#*!"</P>
<P>And then there's the $2 trillion "Anti-Evil Bill." While it will target "Fidel Castro, rappers who threaten police officers, WTO protesters and author/filmmaker Michael Moore," Huey points out that Dick Cheney will unfortunately be left off the Evil List.</P>
<P>"This is one of those critical moments in history, and I didn't want to look back and regret not having said something," McGruder recently told reporters.</P>
<P>Thanks to McGruder, newspaper readers will get a taste of journalism that dares to question the U.S. drive to war--even if they have to go to the funny pages to find it. Although many installments may be censored from your paper, you can read it all on the Web at www.ucomic.com/boondocks.</P>

