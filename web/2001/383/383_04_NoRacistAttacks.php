<P><font face="Times New Roman, Times, serif" size="5"><b>I'm for making sure racists can't attack people</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><p><B>Dear <I>Socialist Worker</I>,</P></B>
<P>A few days after the September 11 attacks, a cartoon ran in the <I>Daily Californian</I>, the student newspaper at the University of California-Berkeley, that showed two turbaned Arab men carrying flight manuals in hell.</P>
<P>Antiwar and antiracist students demanded an apology from the editors of the newspaper, but the editor-in-chief refused, calling it "freedom of speech." Recently, an article appeared in the <I>Oakland Tribune</I> that praised the <I>Daily Cal</I>'s "strong stand" and denounced the antiracist protesters as "neo-fascists."</P>
<P>Fascism is about censorship of the most brutal kind, since it consists of physically eliminating political opponents. But it's also about the promotion of ideas about racial and cultural superiority.</P>
<P>It's dishonest to portray students who are fighting against racism and war as "fascists." The <I>Oakland Tribune</I> should pay more attention to its own columnists. On the paper's opinion pages, Kathleen Parker recently wrote, "There is such a thing as moral and cultural superiority, and we [the Western world] are it."</P>
<P>This war and the justification for it are racist to the core. Racist cartoons like the one in the <I>Daily Cal</I> give courage and justification for anti-Arab and anti-Muslim racists who attack--and even murder--people.</P>
<P>I'm for making sure that doesn't happen, and so are Berkeley students. They haven't waited to get a confirmation that this cartoon has indirectly promoted racism. They've acted.</P>
<P>In France, where I come from, a large number of people have recognized that freedom of speech and hatred don't mix.</P>
<B><P>Frank Laporte, </B>Oakland, Calif.</P>

