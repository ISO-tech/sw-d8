<font face="Times New Roman, Times, serif" size="5"><b>This war only brings more horror</b></font><BR>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>THE VILLAGE of Chowkar-Karez no longer exists. Neither do most of the people who lived there.</P>
<P>The village was obliterated October 22 in a U.S. assault by helicopter gunships that swept overhead for more than an hour, raining death from the skies. Western journalists who visited the site a week later found only huge bomb craters, flattened homes and freshly dug graves.</P>
<P>Chowkar-Karez was a farming village in a remote area near Kandahar. None of the survivors of the attack interviewed by Human Rights Watch could think of a military target nearby.</P>
<P>But Pentagon officials stuck to their story--Chowkar-Karez was a "nest" of Taliban sympathizers. "The people there are dead because we wanted them dead," an unidentified military official told CNN. And the Pentagon wants more.</P>
<P>Some 1,500 people have been killed in U.S. air assaults alone, according to the Taliban government. And some 7.5 million people are at risk of starving to death in the coming months, according to relief workers.</P>
<P>Yet U.S. officials have defied pleas from their allies in the region to halt bombing during the Muslim holy month of Ramadan. "Forget talk of a Ramadan cease-fire," snarled <I>Newsweek</I> magazine. "They fast and fight all the time."</P>
<P>In fact, the U.S. government's willingness to heap suffering on ordinary Afghans has become so blatant that an embarrassed CNN management ordered its anchors last month to end <I>all</I> reports from Afghanistan by "reminding" viewers that the Taliban regime harbors "terrorists who are connected to the September 11 attacks."</P>
<P>CNN said it would be "perverse to focus too much on the casualties or hardship in Afghanistan." The point couldn't be more obvious. </P>
<P>The U.S. government and its media lapdogs are using the deaths of 5,000 innocent Americans on September 11 to justify the deaths of thousands of innocent Afghans. You have to ask the question: How many more?</P>
<P>How many more lives will Washington take in the name of "justice"? How many more villages will be flattened while U.S. politicians talk about "honoring" those who died September 11?</P>
<P>Washington's war makers say that they're waging a war against terrorism. But is there any term other than "terrorism" to describe the war that the U.S. government is conducting--mercilessly, relentlessly, and with the full knowledge that poor Afghans with no connection to Osama bin Laden or the Taliban will pay the price?</P>
<P>This war is obscene. We have to stand up and say: Not in our name.</P>
<P>Stop this war now!</P>

