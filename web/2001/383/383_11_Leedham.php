<P><font face="Times New Roman, Times, serif" size="4">Reformers challenge the Hoffa regime's rotten record</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Leedham battles for Teamster vote</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop,</b> Teamsters Local 174</font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--"We have a rank-and-file network. We don't have a lot of money, but we have a grassroots campaign." That's how Seattle UPS feeder driver Dianne Bolton summed up the campaign for Tom Leedham for Teamsters president. The campaign is in its final week, with ballots due in Washington, D.C., by 9 a.m. November 13.</P>
<P>Much is at stake in the race between incumbent James P. Hoffa and reform candidate Leedham and his Rank and File Power slate. Whoever wins will be negotiating the UPS, freight, carhaul, Anheuser-Busch and other major contracts for some 400,000 Teamsters.</P>
<P>Under Hoffa, membership has shrunk by 11,000 members. His model organizing drive at Overnite is nearly two years old and has no end in sight. Under Hoffa's administration, 141 officials are paid multiple salaries--the most in Teamsters history. While members' strike benefits remain $55 per week, International officials currently receive a $75 daily food allowance. Several of Hoffa's top allies have recently been accused of corruption.</P>
<P>In contrast, Leedham led the drive to eliminate area conferences--a useless layer of bureaucracy that paid 63 multiple salaries to officials. This saved the union $11 million a year.</P>
<P>"Hoffa has been at the helm of the Teamsters for three years," Lorene Scheer, a Seattle campaign organizer for Leedham, told <I>Socialist Worker</I>. "Members in the backbone industries of trucking are seeing a lack of results and promises being broken."</P>
<P>For example, many Seattle-area sand, gravel and construction drivers have switched their support to Leedham after Hoffa signed a Project Labor Agreement for the new light-rail Sound Transit project that excluded Local 174 drivers from the deal.</P>
<P>Bolton said that members in the Northwest have distributed 10,000 copies of the debate video between Leedham and Hoffa's stand-in, Western Region Vice President Chuck Mack, all over Washington state and even into Oregon and Idaho.</P>
<P>It's true Hoffa has 90 percent of the union bureaucracy behind him. Despite this, Leedham supporters must make one final push this week to turn out the vote.</P>
<P>Former president Ron Carey's recent acquittal should inspire Leedham supporters. The 1997 UPS strike Carey led was the biggest victory the labor movement had seen in two decades and showed the importance of having a reform president.</P>
<P>With the economy in recession, having a reformer at the head of the Teamsters could be key in slowing down the bosses' offensive.</P>

