<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Washington's message to allies and enemies alike:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What we say goes</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>GEORGE W. BUSH promised that his "war against terrorism" would be based on a coalition of nations. But now established allies like Indonesia and new ones like Syria are criticizing the bombing for taking innocent lives. And leaders of big European Union countries held a closed-door meeting in London to complain that Washington has excluded their troops from military action.</P>
<P>But the U.S. has a new message for all of them: We'll escalate the killing when and how we want--whether you like it or not.</P>
<P>So Defense Secretary Donald Rumsfeld traveled to Pakistan last weekend to again twist the arm of its military ruler, President Pervez Musharraf. Musharraf, whose country has seen a series of mass antiwar protests, had called for a halt to U.S. bombing during the Muslim holy month of Ramadan--until Rumsfeld shoved him back into line.</P>
<P>Then Rumsfeld looked for help by traveling to a police state where protests are simply banned--Uzbekistan, formerly part of the USSR. Next stop: Tajikistan, another former USSR republic that waged war against Islamists in the early 1990s. Seems the U.S. wants to use the same air bases there that the Russian military used during its savage 10-year war and occupation in Afghanistan in the 1980s.</P>
<P>"There is an increasing disposition toward brute force and the use of whatever allies are at hand, even if that threatens to leave Afghanistan in chaos and the war on terrorism stranded," wrote columnist William Pfaff.</P>
<P>Or as another critic put it: "The U.S. is trying to show its muscle, score a victory and scare everyone in the world. They don't care about the suffering of the Afghans or how many people we will lose." The author of those words: Abdul Haq, a pro-U.S. Afghan guerrilla leader who was captured and killed by the Taliban two weeks ago.</P>
<P>No wonder Washington's latest war stories were confused. Rumsfeld admitted that the U.S. might never catch Osama bin Laden--supposedly the reason for the war in the first place--only to reverse himself a few days later. Then National Security Adviser Condoleezza Rice said the war might last until the next presidential administration--only to have Rumsfeld contradict her with another promise of a quick victory.</P>
<P>This double-talk has led even pro-U.S. foreign leaders to question Washington's war aims. Doubts are growing at home, too. From the government's abandoning of postal workers to anthrax to tax cuts for the wealthy, big business and politicians have brazenly pursued their own interests under cover of national crisis.</P>
<P>Bush and House Majority Whip Tom DeLay (R-Texas) went all out to block a measure that would have made airport security workers federal employees--because it would be easier for them to join a union.</P>
<P>And as the recession produced the biggest one-month increase in unemployment in 21 years, Congress is handing out $25 billion in tax rebates to top corporations--without lifting a finger to save jobs or aid those laid off.</P>
<P>Washington's war makers promised a war for justice--and have delivered only death and destruction. They promised a war to defend ordinary people--but seized the opportunity to push every anti-worker measure on Corporate America's wish list.</P>
<P>This war is against the interests of working people--everywhere in the world. It's time to step up the fight to stop this senseless killing.</P>

