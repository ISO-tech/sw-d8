<P><font face="Times New Roman, Times, serif" size="4">Giuliani lashes out at the firefighters he praised as "heroes"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Attacked at Ground Zero</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>NEW YORK Mayor Rudolph Giuliani lashed out earlier this month at the very people he's praised as heroes many times since September 11.</P>
<P>Giuliani has made sure that he was on the platform at events to mourn the 353 firefighters who died during rescue operations when the World Trade Center collapsed. But when firefighters stepped out of line earlier this month, Giuliani turned on them--returning to the snarling temper tantrums that made him so hated during his eight years in office.</P>
<P>On November 2, hundreds of firefighters--furious with new restrictions on how many could be at the World Trade Center site to help with recovering the remains of their comrades--marched on "ground zero," clashing with police who tried to bar their way.</P>
<P>The media blamed firefighters for the confrontation, and 12 of the marchers were arrested. But firefighters say police provoked the violence. "We were marching peacefully to ground zero, when suddenly, a handful of police officers waded into a line of marchers and began pulling people out," said Peter Gorman, president of the Uniformed Fire Officers Association.</P>
<P>City officials claim that the new restrictions at ground zero are for safety reasons--that firefighters could be exposed to dangerous chemicals or injured by heavy equipment. But firefighters say they think the decision has more to do with serving the mayor's rich pals.</P>
<P>What's more, the announcement of the restrictions came shortly after hundreds of millions of dollars worth of gold, silver and other precious metals were located in the rubble. Finding the vaults of the Bank of Nova Scotia, one of the world's biggest precious metals traders, was obviously a top priority for city officials.</P>
<P>Firefighters now fear that Giuliani will turn the cleanup into a "scoop-and-dump" operation--to get the work done as quickly as possible. The behavior of city officials confirmed firefighters fears.</P>
<P>For example, the Giuliani administration fueled the disgusting rumor that firefighters organized the protest because they want the overtime. Anyone who has heard firefighters talk about the pain they feel at the loss of so many coworkers knows that this is a crock.</P>
<P>But for Giuliani and the corporate fat cats he serves, the most important part of the salvage operation--finding the money--is over. They could care less about the firefighters that they praise as the "real heroes" of September 11.</P>

