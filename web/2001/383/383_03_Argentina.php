<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Argentina at the brink</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>ONCE A showcase of neoliberal economic "magic," Argentina has become a basket case of neoliberal reality.</P>
<P>Less than a decade ago, Argentina pegged its currency to the dollar, borrowed big from international banks, pushed for free-market reforms and let Washington call the shots. But when the value of the dollar soared on international markets, Argentina's goods became too expensive to export, and investment dried up as the big loans came due.</P>
<P>With the economy stuck in recession for four years, unemployment is topping 17 percent, and one-third of Argentines live below the poverty line. And the economy is expected to shrink again this year by 1.4 percent.</P>
<P>Because of the recession, Argentina's tax revenues have plummeted, and although the IMF and other lenders have injected $20 billion of emergency funds into the Argentine economy in the past 12 months, the government is still running out of money.</P>
<P>The IMF's solution: massive cuts in wages and layoffs for public-sector workers. But the austerity measures have only made Argentina's recession worse--which set the stage for a debt crisis.</P>
<P>At the end of October, the government of President Fernando de la R&uacute;a tried to exchange $38 billion in existing debt for lower-interest, longer-term debt. It was a desperate move to avoid an outright default on $132 billion owed to Western banks and institutions.</P>
<P>But the IMF's solution will be still more cutbacks, crushing debt and free-trade measures that favor Western multinational corporations.</P>
<P>Argentina's free-market disaster is a good example of why the global justice movement will hold rallies, protests and teach-ins to coincide with meetings of the IMF, World Bank and World Trade Organization this month.</P>
<P>And in Argentina, workers are on the move. Hundreds of thousands of workers have marched in urban protests in recent months, while masses of the unemployed in rural areas blockaded highways.</P>
<P>It's time for workers to get rid of the de la R&uacute;a government--and to demand cancellation of the debt that's strangling their country.</P>

