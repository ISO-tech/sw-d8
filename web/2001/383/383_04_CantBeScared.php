<P><font face="Times New Roman, Times, serif" size="5"><b>We can't be scared into keeping quiet</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</P>
</B></I><P>I think I may have been the victim of a terrorist attack. A few weeks back, I came home from an antiwar protest to find that my truck had been burned to the ground.</P>
<P>There's no real way to tell who did it or why, but it looks pretty bad. It might have been just a stupid prank, but I live only one block away from a police station. </P>
<P>The arson detective seems to think that it was a calculated attack. Witnesses saw someone break the window to throw in a light, and gasoline had been poured all over the truck.</P>
<P>I was inspired by the letter from Madison, Wis., in last week's <I>Socialist Worker</I>, where the writer talked about making antiwar arguments to a difficult audience.</P>
<P>We have to recognize the will that the other side has to silence us. We should play it safe by leaving protests in groups, for example.</P>
<P>But speaking out now, even when it's unpopular, can make us a pole of attraction as more people become opposed to the war. That can make all the difference in the lives of the people that Washington has in its sights.</P>
<P>It's worth some sacrifices.</P>
<B><P>Chuck Stemke,</B> San Diego</P>

