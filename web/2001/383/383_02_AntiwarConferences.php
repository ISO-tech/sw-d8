<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NO TO WAR! NO TO RACISM!</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Students build for conferences</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson, John Bougie and Erik Wallenberg</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITH THE Bush administration preparing for a long war, student activists are showing that they're prepared to build an antiwar movement from the ground up. November 10-11 is slated as a weekend of student antiwar conferences in cities across the U.S.</P>
<P>In Boston, more than 200 activists from dozens of campuses will attend the Northeast Regional Campus Conference Against War and Racism. Sponsored by the Boston Campus Antiwar Coalition and New York Schools Against the War, the conference already has endorsements from 45 campuses and solidarity greetings from as far away as South Africa.</P>
<P>In Berkeley, Calif., organizers of a California Schools Against the War conference expect some 500 students representing more than 100 different colleges and universities. And in Chicago, the Midwest Student Regional Antiwar Conference is also set for this weekend. Organizers hope that the meeting will lay the groundwork for building a bigger antiwar movement.</P>
<P>As students geared up for the conferences, protesters continued to speak out against the war--even on George W. Bush's home turf. On October 28, 600 antiwar protesters marched to the Federal Building in Austin, Texas.Before the march, a statewide networking meeting drew some 60 people.</P>
<P>Meanwhile, in Burlington, Vt., more than 200 students and community members marched to city hall, the offices of Vermont senators and the local newspaper offices. "We have to stop this war by being in the streets and raising our voices," said one protester. </P>

