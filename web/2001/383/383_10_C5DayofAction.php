<P><font face="Times New Roman, Times, serif" size="5"><b>A Day of Action for the Charleston Five</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 9, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>A TORCHLIGHT rally outside the courthouse in Charleston, S.C., will mark the opening day of the trial of the Charleston Five, followed by an International Day of Action on Wednesday, November 14. Events are planned in cities and campuses across the U.S.</P>
<P><STRONG>Here is a partial list:</STRONG></P>
<B><P>BOSTON</B> Boston Tea Party Ship, Congress St. Bridge, 4:30 pm-6:30 pm (Red line to South Station, walk one block toward the waterfront on Atlantic Ave. and right on Congress St.). Sponsors include Greater Boston Labor Council, North Shore Labor Council, Coalition of Black Trade Unionists, AFT Local 1037 and IUE/CWA Local 201. Contact: 617-524-8778.</P>
<B><P>CHICAGO</B> Indoor rally at Teamster City, 300 S. Ashland, 4:30pm-6:00pm. Sponsors: Illinois AFL-CIO, Chicago Committee to Defend the Charleston Five and Chicago Jobs with Justice. Contact: 312-251-1414.</P>
<B><P>CINCINNATI</B> Location TBA. Sponsors: Cincinnati AFL-CIO Labor Council, Committee to Defend the Charleston Five, UFCW Local 1425. Contact 513-421-1846.</P>
<B><P>LOS ANGELES</B> Beacon Street Park, 7th and Beacon St., San Pedro, noon. Sponsors: LA Committee to Defend the Charleston Five, ILWU Local 13 and Harry Bridges Institute. Contact: 323-221-9128.</P>
<B><P>MADISON</B> Rally at State Capitol, 5 pm, followed by a march to the federal courthouse. Sponsors include South Central Federation of Labor, Madison Charleston Five Defense Committee, Coalition of Black Trade Unionists, International Socialist Organization, Wisconsin Association of Black Public Sector Employees, Student Labor Action Coalition, Progressive Dane. Contact: 608-256-1191. </P>
<B><P>NEW YORK CITY</B> Bethel AME Church, 60 W. 132nd St. at Lenox Ave., 6:00 pm. Sponsors: New York City Central Labor Council, New York City Committee to Defend the Charleston Five and New York Jobs with Justice. Contact: 212-631-0886<B>.</P>
<P>SAN FRANCISCO</B> Harry Bridges Plaza, near Market and Spear, noon. Sponsored by the Northern California Committees to Defend the Charleston Five. Contact: 510-531-4717.</P>
<B><P>SEATTLE</B> 4-6 pm, Rally at Terminal 37, Alaskan Way and Royal Brougham, march to Pier 62-63. Sponsors include Puget Sound Committee to Defend the Charleston Five, King County Labor Council, ILWU Locals 19 and 23 and Teamsters Local 174. Contact: 206-448-1870.</P>
<I><P>For more information, go to the AFL-CIO's Charleston Five Web site at www.charleston5.org.</P>
