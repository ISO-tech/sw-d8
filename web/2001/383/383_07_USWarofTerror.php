<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. government's war <I>of</I> terrorism</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>"DO NOT confuse the cylinder-shaped bomb with the rectangular food bag," warns a new U.S. radio broadcast to the people of Afghanistan. It has been revealed that the food packets and the casings on the cluster bombs that U.S. forces are dropping on Afghanistan are both yellow and roughly the same size.</P>
<P>Hence the radio broadcasts declare, like a sick joke, "We do not wish to see an innocent civilian mistake the bombs for food bags and take it away believing that it might contain food."</P>
<P>From its inception, the U.S. government's "food and bombs" campaign--air-dropping Pop-Tarts and peanut butter to survivors of its round-the-clock bombing raids--has been nothing but a ludicrous public relations exercise.</P>
<P>The Pentagon has awarded a $397,000 contract to the Rendon Group, a public relations firm whose job is to divert public attention from the mounting civilian death toll in the war in Afghanistan while extolling the virtues of the U.S. food drops.</P>
<P>But the food packets dropped thus far amount to no more than 1 percent of what's needed to prevent mass starvation in Afghanistan this winter. And the bombing is blocking food aid needed to prevent the deaths of up to 7.5 million Afghan people this winter. </P>
<P>Despite desperate pleas from humanitarian aid organizations, the U.S. has refused even to pause bombing to allow aid vehicles through to deliver supplies before winter makes the roads impassible. The U.S. has also ignored requests from UNICEF for a three-day suspension in the bombing to allow its volunteers to vaccinate Afghan children at risk for diseases like polio and measles--without which, it says, up to 50,000 children will die this winter.</P>
<P>The U.S.'s "new kind of war" on terror has developed into a conventional war of carpet bombing and cluster bombs hitting residential neighborhoods, hospitals, food warehouses and markets--causing mass civilian casualties.</P>
<P>"I don't think there has ever in the history of the world been a bombing effort that has been done with such precision and care and attention" to avoid civilian casualties, argued Defense Secretary Donald Rumsfeld on Sunday.</P>
<P>Eyewitnesses tell a different story. "From Kandahar come ever more frightful stories of civilians buried under ruins, of children torn to pieces by American bombs," journalist Robert Fisk reported.</P>
<P>On October 22, U.S. bombs killed up to 35 people in the village of Chowkar-Karez, north of Kandahar. After bombing once, the planes returned and attacked again, as terrified villagers ran through the streets. "If there were military targets in this area, we'd like to know what they were," said Sydney Jones, Asia director of Human Rights Watch.</P>
<P>U.S. carpet bombing last Wednesday and Thursday battered a hydroelectricity plant and Kajaki Dam, leaving Kandahar in darkness and the dam in danger of collapsing.</P>
<P>In late October, after U.S. bombs hit a Red Cross humanitarian warehouse for the second time, senior military officials said the U.S. hit the warehouse on purpose--because it had been seized by the Taliban. Yet Red Cross official Christoph Luedi, based in Geneva, said, "This we can confirm is not correct&#133;[W]e had control over this warehouse." </P>
<P>U.S. media outlets have offered only fleeting images of the civilians killed by U.S. bombs in Afghanistan--which now number more than 1,500 according to some estimates.</P>
<P>CNN Chairman Walter Isaacson sent a memo to foreign correspondents instructing them not to focus too much on Afghan suffering. "We must redouble our efforts to make sure we do not seem to be simply reporting from their vantage or perspective," he wrote.</P>
<P>But the media can't hide the fact that, thus far, as journalist John Pilger pointed out last week, "Not a single terrorist implicated in the attacks on America has been caught or killed in Afghanistan."</P>
<P>The looming prospect of mass civilian casualties from slaughter and starvation makes it more difficult for the Bush administration to sustain its claim that "this war is not against the Afghan people."</P>
<P>This is not a "war against terrorism"--it <I>is</I> terrorism.</P>

