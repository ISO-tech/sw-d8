<P><font face="Times New Roman, Times, serif" size="4">FBI looks for U.S. source of anthrax</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Are Nazis to blame?</b></font></p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN THE deadly anthrax attacks hit the headlines in October, U.S. officials didn't let a little thing like evidence get in the way of pointing the finger at Iraq. A month later, four people are dead, more than a dozen others are infected--and only now have authorities begun to admit that the attacks might well have come from "homegrown" terrorists.</P>
<P>Last week, federal officials reported that anthrax sent through the mail to Senate Majority Leader Tom Daschle and media outlets in New York and Florida was missing a coating of bentonite that UN weapons inspectors say was typical to Iraq's biological weapons program. Plus FBI attempts to link anthrax to the cars or apartments of the September 11 hijackers have turned up nothing.</P>
<P>The media leapt to the conclusion that the anthrax letters must have been written by a Muslim since they contain the phrase "Allah is great." But there are inconsistencies. For example, the date written at the top of the letters is "09-11-01"--an American way of writing dates.</P>
<P>In fact, the anthrax attacks have the signature of Nazi terrorism. In recent years, Nazi groups have mailed a rash of anthrax threats to abortion clinics. And the Nazis themselves say that they're thrilled with not only the anthrax threats but the September 11 attacks.</P>
<P>"The enemy of our enemy is, for now at least, our friend," Billy Roper of the white supremacist National Alliance wrote on the Internet. "Anyone who is willing to drive a plane into a building to kill Jews is alright by me." </P>
<P>But Washington isn't concerned about these facts. "Whether or not Saddam is implicated directly in the anthrax attacks or the horrors of September 11, he is, by any common definition, a terrorist who must be removed," Sen. Joe Lieberman (D-Conn.) wrote in the <I>Wall Street Journal</I>.</P>
<P>What Washington really cares about is whipping up hysteria to help them extend their war to Iraq.</P>

