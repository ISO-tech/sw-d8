<P><font face="Times New Roman, Times, serif" size="5"><b>Boston hotel bosses retreat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alpana Mehta</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MEMBERS OF Boston's Hotel Employees and Restaurant Employees International Union (HERE) Local 26 are celebrating a victory. After 97 percent of the union voted to strike, the negotiating board unanimously approved management's drastically revised contract offer.</P>
<P>While Boston hotels--the second-most profitable in the country--had made a record $23.8 billion in profits last year, they were set to use the tragedy of the September 11 attacks to go after workers. Hotel bosses wanted to eliminate all health care provisions from the contract, force overtime as well as part-time status at will and lay off workers while pleading "regret" and "poverty"!</P>
<P>About 30 percent of Boston's 3,000 unionized hotel workers are laid off. While the contract won't automatically restore those workers' jobs, it protects existing jobs by limiting management's ability to impose overtime and part-time work.</P>
<P>The deal also includes other gains--such as full health coverage and the right of HERE to unionize with a card check any new hotel business set up by a major chain.</P>
<P>At five years, the contract is lengthy--which favors management. But according to HERE, wages and benefits will increase 5 percent each year of the five-year contract. And management had to drop almost all of its most extreme demands.</P>
<P>Mobilization for a possible strike was key to making gains. Hundreds of union members came out to rallies.</P>
<P>Nationally, HERE has seen about one-third--or 300,000--of its members laid off. This contract will set an important precedent for the next rounds of negotiations in Hawaii, Chicago and New York.</P>

