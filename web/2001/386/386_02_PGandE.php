<P><font face="Times New Roman, Times, serif" size="4">Was referendum vote bought or stolen?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How PG&amp;E beat public power</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Belknap</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A BANKRUPT corporation spent more than $2 million to defeat two ballot measures in November that would have brought public power to San Francisco.</P>
<P>Measure I would have set up a Municipal Utilities District (MUD) to take over the facilities of Pacific Gas &amp; Electric (PG&amp;E), the state's biggest utility company, and run them with an elected board. And Proposition F would have created a Public Utilities Commission to do the same in case the more far-reaching MUD proposal failed.</P>
<P>But in a flashback to last year's fiasco in Florida, the election may have been stolen instead of just bought.</P>
<P>The two referendums grew out of the California power crisis that drove PG&amp;E into bankruptcy earlier this year. A 1996 deregulation law, which PG&amp;E helped to write, required utilities to buy energy from national power producers that manipulated supplies to jack up prices to obscene levels. State politicians squawked about the power bosses' extortion tactics--but then agreed to increases in already inflated rates charged to consumers.</P>
<P>Low-income households were put in the position of having to choose between food and heating their apartments. Activists called demonstrations against the rate hikes and rolling blackouts--and began organizing for the ballot measures to take the power system back from the profiteers and put it in public hands.</P>
<P>Once public power was on the ballot, the blackouts expected this summer miraculously didn't materialize--and PG&amp;E backed off rate hikes. This, coupled with the millions spent on ads and a scare campaign that cynically appealed to voters' fear following the September 11 tragedy, took the steam out of the campaign for the referendums.</P>
<P>But PG&amp;E--which was prepared "to do anything it takes" to stop public power, a senior company official told reporters--may not have trusted its ad campaign. Election director Tammy Haygood used what she called "the potential threat of anthrax" as an excuse to remove mail-in ballots to a remote location, where the public was denied its right to oversee the vote-counting process.</P>
<P>In addition, thousands of ballots were stored in a waterfront warehouse and left unsupervised for hours. And in late November, the Coast Guard reported that it had found the tops to eight ballot boxes floating near the Golden Gate Bridge. Haygood offered the ridiculous explanation that election workers were washing off the ballot boxes on the pier when their tops blew off.</P>
<P>Exit polls on election day showed Proposition F passing. But when the final results were announced, the referendum went down to defeat--by 533 votes.</P>
<P>"I'm staggered," Rich Waller, director of the San Francisco Labor Council's Labor Neighbor election operation, told <I>Socialist Worker</I>, "and I'm doubly staggered by the irregularities that went on--that's what makes this defeat so difficult." Supporters of public power are planning a Diving for Democracy demonstration at Pier 29 on December 4.</P>
<P>Unfortunately, PG&amp;E got some help from United Electrical workers union Local 1245, which opposed the referendums. Both ballot initiatives would have protected workers' jobs, and union member Robin David was running for the MUD board. But that wasn't good enough for the local's conservative leadership.</P>
<P>Despite the defeat, Waller said a lot of good came out of the campaign. "The effort to bring public power to San Francisco really activated the labor movement," he said. "There were volunteers from more than 60 locals, and I think that this campaign really aligned organized labor with a broader community against the corporate giants."</P>
<P>This fight isn't over. Public power is likely to be on the ballot again next November, and we need to be ready to do what it takes to win.</P>

