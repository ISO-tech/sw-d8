<P><font face="Times New Roman, Times, serif" size="5"><b>Zinn exposes U.S. "democracy"</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><B>BOOKS: <I>Howard Zinn on War,</I></B> Seven Stories Press, 205 pages, 2001, $12.95.</font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FROM THE Second World War to Vietnam to Yugoslavia and the never-ending bombardment of Iraq, Howard Zinn's new collection of essays shows why the U.S. government should never be trusted to fight for human rights or democracy--at home or abroad.</P>
<I><P>Howard Zinn on War</I> begins with several examples of how the U.S. has brutalized its own people. It emphasizes why a government that regularly abuses its own people is incapable of delivering democracy to others.</P>
<P>The most horrifying example is a little-known massacre of African Americans in 1921, in which U.S. planes dropped nitroglycerine bombs on the Black business district of Tulsa, Okla. Thousands of Blacks were killed in the attack and dumped into mass graves to cover up the atrocity.</P>
<P>The bulk of this book, though, deals with American wartime atrocities and how they give lie to any government claims of fighting for freedom. Zinn points out that with the Second World War, the U.S., along with the other imperial powers, made the attacking, maiming and murder of civilians a deliberate war strategy.</P>
<P>"Whatever 'humanitarian' motives are claimed by our political leaders--it is always a war against children: the child amputees created by our bombing of Yugoslavia, the hundreds of thousands of Iraqi children dead as a result of our post-war sanctions&#133;Our leaders cannot be trusted," Zinn writes. "Modern war is inevitably a war against civilians and particularly children&#133;Only a determined citizenry can stop the government when it embarks on mass murder."</P>
<P>Although Zinn's book was written before the latest U.S. venture into mass murder in Afghanistan, his arguments against America's brutal military ventures over the last century are just as important for the antiwar movement today.</P>

