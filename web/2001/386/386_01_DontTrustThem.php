<P><font face="Times New Roman, Times, serif" size="5"><b>Don't trust these men with your rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 1</font></p>
<P><font face="Times New Roman, Times, serif" size="3">"YOUR NAME was brought to our attention because, among other things, you came to Michigan on a visa from a country where there are groups that support, advocate or finance international terrorism." This was the ominous message that more than 700 Middle Eastern men in the Detroit area received in the mail recently. Across the U.S., thousands more will receive similar letters in the coming weeks.</P>
<font face="Times New Roman, Times, serif" size="3"><P>It's all part of Attorney General John Ashcroft's plan to catch terrorists--by "requesting" interviews with 5,000 young men who entered the U.S. on temporary visas. Of course, this is blatant racial profiling--the kind of thing that George W. Bush piously denounced on the campaign trail last year.</P>
<P>But in the "war against terror," anything goes. And Bush and Ashcroft will stop at nothing.</P>
<P>The interviews are the latest in a series of attacks carried out by the Bush administration. More than 1,200 people have been detained in the investigation into the September 11 attacks. Half are still behind bars, but not one has been charged with a crime related to the attacks.</P>
<P>Ashcroft has also ordered eavesdropping on conversations between attorneys and their clients. And his boss authorized the use of military tribunals to try noncitizens suspected of being terrorists--military kangaroo courts complete with secret evidence and practically no appeals.</P>
<P>Now Ashcroft has hatched a plan to allow the FBI to spy openly on religious and political organizations. This is the same agency that systematically harassed the Black Panthers, opponents of the Vietnam War and even civil rights leaders like Martin Luther King Jr.</P>
<P>Spreading rumors, planting media stories, forging documents, infiltrating political groups, organizing assassinations in the guise of police raids--the FBI did it all. And now the Bush administration wants to bring it back--in the name of protecting our "security."</P>
<P>They're exploiting ordinary people's horror at the September 11 attacks to increase their power over us.</P>
<P>Overseas, the Bush gang is waging a war to ensure America's political, economic and military domination around the world.</P>
<P>They're waging a war at home, too--to silence all opposition and take away political rights and civil liberties that ordinary people struggled for decades to secure.</P>
<P>They say they want to protect us from "terrorism." But we can't trust these men. We have to fight for our rights.</P>

