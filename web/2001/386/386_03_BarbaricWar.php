<font face="Times New Roman, Times, serif" size="5"><b>The true face of their barbaric war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>BLOODTHIRSTY <I>WASHINGTON Post</I> columnist Charles Krauthammer is pleased with the way that the U.S. war on Afghanistan has gone. In an article titled "Victory changes everything," Krauthammer gloated that the "psychology in the region is now one of fear and deep respect for American power. Now is the time to use it to deter, defeat or destroy the other regimes in the area that are host to radical Islamic terrorism&#133;</P>
<P>"Now is the time to go for the low-hanging fruit: giving the Philippines assistance in crushing their own al-Qaeda guerrillas. Telling the thugs running Sudan, Syria, Libya and Yemen to cease and desist, to shut down the training camps, to cough up the terrorists--'or else,' as the president so delicately puts it. And then on to Iraq."</P>
<P>No one would expect anything other than this vile ranting from a right-wing crackpot like Krauthammer. But he's not the only one celebrating the war. For example, liberal <I>New York Times</I> columnists Frank Rich and Anthony Lewis threw caution to the wind last month as the Northern Alliance took control in Kabul.</P>
<P>Rich praised George W. Bush's "nuanced and so far effective prosecution of the war," while Lewis celebrated a "military and moral" victory--with the stage-managed "revelations" about the Taliban proving to him that "this is a necessary war, a just war."</P>
<P>A "nuanced" war? Ask Arafa, whose husband, son and six other family members were killed by U.S. "smart" bombs that went off target in the village of Bibi Mahru last weekend.</P>
<P>A "moral" victory? Lewis will have to explain what morality has to do with the string of executions carried out by anti-Taliban forces throughout Afghanistan.</P>
<P>A "just" war? That will be news to the estimated 6,000 desperate refugees who fled the U.S. carpet bombing of Kandahar and now wait, with neither food nor shelter against the freezing temperatures, to cross into Pakistan.</P>
<P>This is the real face of the U.S. war in Afghanistan. It's remained hidden not only because of the tide of propaganda unleashed by Krauthammer and the administration right-wingers he fronts for--but because liberals like Rich and Lewis have jumped on the pro-war bandwagon, too.</P>
<P>The unanimous chorus of support from Washington and the mainstream media can leave antiwar activists feeling isolated. That's why the most important thing that we can do right now is stand up, speak the truth and cut through the lies that they use to advance their agenda.</P>
<P>The initial outpouring of opposition to Bush's war drive following September 11 was more widespread than anyone expected. Now Washington's war makers are riding high after an apparent quick victory in Afghanistan.</P>
<P>But their war is no more just today than it was before. Their barrage of pro-war propaganda hides the truth--from the slaughter of the innocent in Afghanistan to the administration's shredding of civil liberties to Corporate America's assault on workers' living standards.</P>
<P>Every time we speak out, we expose that truth--and give people who doubt Washington's lies the opportunity to stand up with us in opposing a barbaric war.</P>

