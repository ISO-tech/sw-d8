<P><font face="Times New Roman, Times, serif" size="5"><b>Protest against <I>New York Post</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Rudolfo Leyton</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>Students from several New York City colleges and universities showed last month that they won't be intimidated by the <I>New York Post</I>. Close to 100 students and faculty from Columbia University, Hunter College and other schools gathered in front of the <I>Post</I>'s offices to protest the newspaper's vicious attacks on dissent.</P>
<P>In October, the <I>Post</I> denounced an antiwar teach-in held at the City College of New York, implying that faculty who spoke at the event were pro-terrorist and calling students who attended "idiots."</P>
<P>Last month's demonstration was called by New York Schools Against the War, with support from the National Action Network. Protesters voiced their anger with the paper, pointing out the <I>Post</I>'s racism and corporate bias.</P>
<P>One protester referred to the paper's right-wing billionaire owner Rupert Murdoch as "a cancer in the free press." Pro-war fanatics like Murdoch and his henchmen at the <I>Post</I> want to silence all opposition.</P>
<P>But antiwar activists showed that they won't be intimidated.</P>

