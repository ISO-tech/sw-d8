<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Defend Oscar Olivera!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WORKERS IN Cochabamba, Bolivia, scored an inspiring victory against corporate globalization in 2000 when they mobilized to stop the privatization of their city's water supply to a subsidiary of San Francisco's Bechtel Corp. So it was probably only a matter of time before the government and its corporate backers sought revenge.</P>
<P>That day came November 27 with the arrest of Oscar Olivera, executive director of the Federation of Factory Workers and a leader of the Coalition in Defense of Water and Life in Cochabamba. Police jailed Olivera for "sedition, conspiracy, incitement to public disorder, criminal association and other charges."</P>
<P>But within days, an international solidarity campaign forced the Bolivian government to retreat. As <I>Socialist Worker</I> went to press, union leaders at the AFL-CIO convention in Las Vegas were discussing plans to make a statement in support of Olivera.</P>
<P>As the pressure mounted, prosecutors dropped all charges but one. The government also abandoned its attempt to restrict Olivera's movement by forcing him to report to police every 72 hours.</P>
<P>But the remaining charge--sedition--is in itself an outrageous attempt to restrict political speech and action. "I believe that what the government wants is to eliminate the fundamental right to protest," Olivera told <I>Socialist Worker</I>. "It wants to criminalize everything that constitutes social action or mobilization. And it's carrying out a propaganda campaign in the news media to discredit the social movements."</P>
<P>But Bolivian government officials aren't the only ones putting pressure on the workers and peasants of Cochabamba. Bechtel moved the legal headquarters of its subsidiary Aguas del Tunari to Holland in order to sue Bolivia under terms of an investment treaty between the two countries. The lawsuit is a preview of the kind of actions multinational corporations would be able to take across Latin America under the proposed Free Trade Area of the Americas.</P>
<P>And according to Olivera, the country's new president, Jorge Quiroga, is under U.S. pressure to step up a coca eradication program--an attack on the livelihood of peasants in Cochabamba. "All of this means that the government's internal war against the social and popular movements has intensified," he said.</P>
<P>That's why it's urgent to keep building support for Oscar and his comrades--and to send a message that we won't allow the victory in Cochabamba to be taken away.</P>

