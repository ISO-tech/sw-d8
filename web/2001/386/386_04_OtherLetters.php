<p><a name="widens"></a><font face="Times New Roman, Times, serif" size="4"><b>Bush widens Colombia war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The war against the people of Afghanistan isn't enough for George W. Bush. Colombia has now become another target of this "first war of the 21st century."</P>
<P>Under Bill Clinton, "Plan Colombia" deepened U.S. involvement in the 37-year Colombian civil war with a $1.3 billion military aid package.</P>
<P>While the Clinton administration was able to win congressional approval of Plan Colombia, it was forced to disguise the package as part of the "war on drugs" to deflect comparisons to previous U.S. interventions in Latin America. The equipment and weapons given by the U.S. were only supposed to be used against drug traffickers--not against the left-wing guerrilla movements.</P>
<P>But the Bush administration now sees an opportunity to dramatically increase U.S. intervention in Colombia by portraying it as part of a global "war against terrorism." The U.S. ambassador to Colombia, Anne Patterson, recently compared the country's rebel groups to Osama bin Laden and announced that the U.S. will assist the Colombian government's "antiterrorist" forces and help guard its strategically vital oil pipelines.</P>
<P>The U.S. government has more at stake in Colombia than that country's oil reserves, however. Colombia's strategic location is seen as the key to much of South America.</P>
<P>It seems clear that the most recent moves by the Bush administration to escalate the war will only deepen the crisis and bring more suffering to the people of Colombia.</P>
<B><P>John Buttell,</B> Amherst, Mass.</P>

