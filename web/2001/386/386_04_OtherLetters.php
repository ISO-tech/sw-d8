<p><a name="double"></a><font face="Times New Roman, Times, serif" size="4"><b>A double standard of grief</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The crash of Dominican Republic-bound Flight 587 on November 12 was a shock to an already mourning New York City, especially to New York's large Dominican community. What shocked me and fellow students here at Columbia and Barnard Colleges even more, however, was that our university did not even notice.</P>
<P>After the September 11 tragedies, we received a number of carefully crafted e-mails with condolences from the university. But even though there are many Dominican students at Columbia and Barnard, and a sizable part of the workforce is from the Dominican community, Barnard's president Judith Shapiro remarked, "We want to reserve blanket e-mails for special occasions when a truly urgent message is needed."</P>
<P>Students here did not sit still, however. A group of Latino students organized a petitioning drive for recognition of the tragedy on campus. The university finally sent out a campus-wide e-mail--a week later!</P>
<P>As my classmate Diana Gil said, "It took a week of organizing and a week out of our lives to get an e-mail that should have been sent out in a day."</P>
<B><P>Peter Lamphere, </B>New York City</P>

