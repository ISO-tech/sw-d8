<p><font face="Times New Roman, Times, serif" size="4"><b>Why I refuse to pledge allegiance</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I teach at an elementary school in Harlem, and every morning, the teachers and children stand up to say the Pledge of Allegiance. I refuse to do this.</P>
<P>I was working with another teacher one day, and she asked me why I don't salute the flag. I told her that the same flag we salute is on the bombs that are killing innocent people in Afghanistan.</P>
<P>She applauded me for my courage and told me that our students don't even know why they salute the flag. Then, she expressed her outrage about the war and told me that she knows that the U.S. is bombing Afghanistan not to find terrorists, but for some other reason which she wasn't sure of.</P>
<P>At that moment, I took out<I> SW</I> and sold her my paper. Now she wants me to bring her a new issue of<I> SW</I> every week!</P>
<P>To all the antiwar activists out there, remember: There are tons of people out there who are with us, but it is going to take acts of courage for them to join us in our movement.</P>
<B><P>Paul B.,</B> New York City</P>

