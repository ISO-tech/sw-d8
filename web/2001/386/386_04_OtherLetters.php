<p><a name="dominance"></a><font face="Times New Roman, Times, serif" size="4"><b>War for dominance, not for democracy</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Throughout its history the U.S. government has tried to mislead people by saying that the U.S. defends democracy, defends the free world and protects human rights. These words are used to conceal the real war aims of the U.S.--controlling the world's natural resources and reinforcing U.S. dominance all over the world.</P>
<P>Bush recently said, "Either you are with us or against us." This shows how arrogant and violent the U.S. government is when they need to get diplomatic support from a nation for waging a war against humanity.</P>
<P>The U.S. government gets support for its wars by browbeating governments and ignoring popular opposition. In fact, they do not believe in democracy; they believe in dictating to other nations.</P>
<B><P>Hasan,</B> New York City</P>

