<P><font face="Times New Roman, Times, serif" size="5"><b>Protect workers from the profiteers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Since September 11, politicians have exploited war hysteria to hand out billions in bailouts to airline, insurance, and other corporations, while ignoring hundreds of thousands of workers thrown out of their jobs.</P>
<P>We, the undersigned New York City Bellevue Hospital union members, condemn the failure of the government to help union members and other working people who have been laid off all over America.</P>
<P>Congress has refused to extend unemployment benefits or help people pay for their health care after being fired. The best of the stingy proposals in Congress would only extend meager unemployment benefits another few weeks--while making unemployed workers pay for their own health care.</P>
<P>Even worse, under current law, only 33 percent of today's laid-off workers are eligible for any benefits. And just as shameful, nothing at all has been done to help armed forces reserve members called to active duty who lose their incomes.</P>
<P>Congress and the president did hand out the cash to make sure that airlines can keep on giving any laid-off top executives two years' salary at full pay with fully paid health benefits.</P>
<P>We demand that the leadership of the AFL-CIO start fighting for nothing less for working people, including those called up to active duty in the military.</P>
<P>We demand that work begin to organize a Labor March on Washington to get what we need. The best "homeland security" is war at home against the politicians and the corporate profiteers who own them.</P>
<B>Thomas Barton,</B> shop steward and trustee, AFSCME Local 768<BR>
<B>Alma Clark-Chalmers,</B> AFSCME Local 371<BR>
<B>Luis Cruz,</B> AFSCME Local 371<BR>
