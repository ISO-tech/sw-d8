<P><font face="Times New Roman, Times, serif" size="5"><b>UAW threatens to cut off Accuride workers--again</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UAW ACTIVISTS are organizing to prevent the union's International Executive Board from cutting off the strike benefits of locked-out Accuride workers in Henderson, Ky., and revoking the charter of their union, Local 2036.</P>
<P>UAW Solidarity Coalition (UAWSC) members set a demonstration for January 14 at Solidarity House, the UAW's headquarters in Detroit, to protest the cutoff of the 400 workers, who have been locked out since March 1998.</P>
<P>At the same time, a picket will be held outside Local 862 in Louisville, Ky., home local of Ron Gettelfinger, the candidate for UAW president hand-picked by the ruling Administration Caucus. Gettelfinger was the regional director who personally authorized the strike that preceded the lockout in February 1998. But the UAW International turned its back on the members of Local 2036, cutting off strike benefits for 14 months.</P>
<P>But a picket by Accuride workers and supporters outside the UAW International's Solidarity House in May 2000 pressured the Executive Board into restoring benefits four months later at double the normal amount.</P>
<P>The benefits were cut in half in October--and the International forced workers to vote on a contract that they had already rejected five times.</P>
<P>Accuride workers and UAWSC activists held an emergency December 1 meeting hosted by Local 2488 President Justin West in Bloomington, Ill., to plan the January 14 protest and step up the fight.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Ford layoffs raise the stakes</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FORMER UAW Local 2036 President Billy Robinson has a message for UAW members everywhere. "If they can do this to us, you're next," he told <I>Socialist Worker</I>.</P>
<P>The Big Three automakers are planning devastating cuts, with Ford leading the way. Ford--which has already cut 5,000 nonunion white-collar jobs--this week announced the elimination of the second shift at its Edison, N.J., Truck plant, forcing the layoff of 600 workers.</P>
<P>But just days earlier, new CEO Bill Ford got a standing ovation from the UAW Ford Council--even though he spoke of coming "sacrifices."</P>
<P>And Ford's new chief of North American operations, Nick Scheele, is a notorious hard-liner. He was boss of Ford's Cuautitlan assembly plant in Mexico in 1990 when thugs from the state-run union killed an independent union activist and shot eight others. When a strike followed, he sent 5,000 security guards to reopen the plant. More recently, Scheele closed Ford's plant in Dagenham, England.</P>
<P>Instead of mobilizing against concessions, UAW leaders are continuing with the policy of "jointness" that has seen union membership cut in half since the 1970s.</P>
<P>That's why the Accuride struggle will affect workers beyond Henderson, Ky. It's a fight for the principles of union democracy, solidarity and struggle--and to rebuild the UAW as a fighting union.</P>

