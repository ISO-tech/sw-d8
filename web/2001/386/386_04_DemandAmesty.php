<P><font face="Times New Roman, Times, serif" size="4">Facing deportation because of Bush's war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We demand amnesty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I'm 29 years old, and I came to this country legally on July 3, 1988, on a B-2 Holiday Visa at the age of 16 years. I overstayed my visa, which expired on January 1, 1989.</P>
<P>I went to high school here and got my diploma, but because I was undocumented, I couldn't go to college or get a legal job. I worked under the table for a couple of years as a painter and handyman.</P>
<P>Then the job market was really bad, and I was laid off and homeless on the streets. Life got really upsetting, so I sold drugs to survive.</P>
<P>In 1998, I got arrested and convicted on a third-degree felony drug possession charge. I was sentenced to one to three years of jail time in upstate New York. I came home in 2001, luckily without getting deported. I spent three full years behind the wall.</P>
<P>I'm trying to get my life together, but now I may be deported.  The people who make the laws say that if I even attempt to marry a U.S. citizen, I will be deported because of my drug felony--which is the only time I was ever in trouble.</P>
<P>Before September 11, there was growing support for a general amnesty for all undocumented immigrants. Now with all the racism whipped up against Arabs, that has been shoved out of the window.</P>
<P>Bush and his policymakers are using the September 11 attacks as an excuse to crack down on immigration and immigrants in this country very severely. In this backlash, people are being hurt and families are being destroyed.</P>
<P>This is why we need an amnesty now. The only real crime I committed is being poor.</P>
<B><P>Marlon R.,</B> New York City</P>

