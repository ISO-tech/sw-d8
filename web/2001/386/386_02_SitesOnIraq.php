<P><font face="Times New Roman, Times, serif" size="5"><b>Washington sets its sights on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 7, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI PRIME Minister Ariel Sharon used his visit to Ground Zero in New York City last month to help hype the impending U.S. war on Iraq. "Iraq is a state&#133;with the potential and knowledge for weapons of mass destruction," Sharon said. "It may be that within a short time, they will have nuclear weapons."</P>
<P>Nice try. That very same day, the <I>Chicago Tribune</I> quoted Israeli intelligence officials saying that Iraq has no more than a half-dozen missile launchers and 50 missiles.</P>
<P>The claim that Saddam Hussein's regime represents a threat flies in the face of reality. The 1991 Gulf War reduced Iraq--previously one of the most advanced countries in the region--to a "pre-industrial state," according to a UN delegation that visited the country.</P>
<P>Economic sanctions imposed by the UN have continued to strangle the country ever since by barring desperately needed food, medical supplies and materials to rebuild the country's devastated infrastructure.</P>
<P>According to UNICEF, more than 500,000 Iraqi children under the age of five have died as a result of sanctions.</P>
<P>Meanwhile, UN weapons inspectors found no evidence that the Iraqi government maintained any ability to produce weapons of mass destruction, according to Scott Ritter, one of the most gung ho inspectors.</P>
<P>Yet George W. Bush had the gall last month to set up the pretext for a new U.S. war--by demanding that the Iraqi government accept new teams of weapons inspectors, even though the U.S. ordered previous inspectors to pull out of Iraq as a prelude to the last round of devastating bombing in 1998.</P>
<P>Bush and the rest of Washington's war makers aren't about to let the facts get in the way of dropping more bombs. Once again, the Iraqi people will pay the horrific cost.</P>

