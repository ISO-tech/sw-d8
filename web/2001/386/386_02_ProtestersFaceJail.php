<font face="Times New Roman, Times, serif" size="5"><b>Facing prison for protesting</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"OUR GOAL is for the people involved to serve jail time." That's what Hartford, Conn., prosecutor Jeff Lee declared last week --as he sought 10-year prison terms for two student antiwar protesters. "We suspect the defense might try to reach a plea, but we have every intention of taking this case to trial," he sneered.</P>
<P>The incredible charges stem from an October 25 antiwar demonstration of more than 200 activists. Although the march was peaceful, police claim that a few protesters strayed from the sidewalk--and used this as an excuse to attack.</P>
<P>"Halfway through the march, some cop cars started showing up and driving alongside of us," Jason Capell, one of those arrested, told <I>Socialist Worker</I>. Then police began using batons and pepper spray on demonstrators.</P>
<P>A 61-year-old protester had his ribs broken. Another man was forced onto the ground, pepper sprayed and beaten by police until his head was bloody.</P>
<P>"A police officer stepped in front of me--full riot gear, with his baton up in front of him," said Capell. "He asked me where I was going, and I just told him that I was walking down the sidewalk. And he said, 'No you're not.'</P>
<P>"As soon as I asked him where I could go, he put handcuffs on me and started walking me towards the police van. And my charges are inciting a riot, interfering with police conduct and disorderly conduct."</P>
<P>In all, 18 demonstrators were arrested and taken to jail on similar charges. In a clear effort to discourage protest, bail was set ridiculously high, at between $15,000 and $50,000.</P>
<P>According to Capell, police specifically targeted leaders of the demonstration. "A lot of us were asked numerous times, 'Who are the leaders?'" he said.</P>
<P>"It appears that this came down from the higher-ups, whether it be the mayor or the chief of police, to nail us with high charges. Because that sends a message out to people. They're trying to scare people from speaking out against this war and what's going on."</P>
<P>Capell said that it was important for antiwar activists to continue speaking out. "It's definitely scared a lot of other people," he said. "It's a really important time for people to be very vocal and open about where they stand on these issues."</P>
