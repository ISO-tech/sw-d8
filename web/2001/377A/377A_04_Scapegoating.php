<P><font face="Times New Roman, Times, serif" size="4">Media's sickening racism sparks anti-Arab attacks</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No racist scapegoating!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE MEDIA was full of anti-Arab and anti-Muslim rants in the aftermath of the attacks on New York and Washington, D.C.</P>
<P>"Kill the bastards," wrote <I>New York Post </I>columnist Steve Dunleavy. "No, I don't mean hunt them, arrest them, extradite them and prosecute them in a court of law. I mean a far quicker and neater form of retribution for this cabal of cowards. A gunshot between the eyes, blow them to smithereens, poison them if you have to."</P>
<P>And using the cover of sympathy for the attacks' victims, racists across the U.S. took the cue.</P>
<P>In Brooklyn, N.Y., home to many Arabs, a man ran up and down the street, slamming parking meters and screaming that he was going to strap them with C-4 explosives. In Chicago, nearly 1,000 miles away, the Arab Action Network was forced to close its offices early after a car circled several times, with people inside shouting that Arabs were "baby killers."</P>
<P>"This is what happens every time there is a bombing," said Hatem Abudayyeh, the center's director. "I knew right away that the attack would be blamed on Arabs and Muslims."</P>
<P>In the Chicago suburb of Burbank, signs appeared on telephone poles that read, "Kill all Arab terrorists." In nearby Palos Heights, a man was arrested after he attacked a gas station attendant he thought was Arab with a 2-foot-long machete. "I don't want to go outside today," said cabdriver Mohad Khan.</P>
<P>At the University of Illinois-Chicago, students sitting in a lounge watched news reports about the attack. When the scene that networks played over and over--of a handful of Palestinians in the Occupied Territories cheering--came on, a few men at one table started yelling, "We're going to kill you. Bomb 'em!"</P>
<P>"Unfortunately, it's the case that the media leans toward Muslims being at fault," Suhaib Quadri, Muslim Student Association president told <I>Socialist Worker. </I>"And we feel the ramifications of that."</P>
<P>Such intimidation and violence is the inevitable result of the war hysteria whipped up by politicians and the media alike.</P>
<P>We've seen the ugly results before. Arab Americans were chased through the streets by angry mobs following the 1979 seizure of the U.S. Embassy in Iran. In the days following the 1995 Oklahoma City bombing, some 200 incidents of harassment, threats or violence against Arab Americans were reported.</P>
<P>Of course, the terrorist in that bombing turned out to be white American Nazi Timothy McVeigh.</P>
<P>Now Bush, Congressional Republicans and Democrats are urging "national unity"--as they prepare for military attacks on Arab and Muslim countries in the Middle East. At the same time, the government will use the drive to war to attempt to restrict the rights and freedoms for people of Arab descent in the U.S.</P>
<P>Already, a week before the Trade Center attacks, federal agents descended on the offices of the Holy Land Foundation for Relief and Development, in Richardson, Texas--a nonprofit organization that coordinates humanitarian aid to the Middle East.</P>
<P>They also swooped down on the nearby offices of InfoCom Corp., an Internet services business that hosts about 500 Web sites, including the Holy Land Foundation's. The agents copied documents and computer hard drives--and refused to disclose why. Then the U.S. Commerce Department suspended InfoCom's export privileges.</P>
<P>Today, airlines are now considering the merits of "profilers" for domestic flights like those used in international flight--a racial-profiling practice Palestinian activist Rania Masri has called "flying while Arab." And NBC's anchor Tom Brokaw used the attacks as an opportunity to suggest stronger restrictions on immigration.</P>
<P>Where will it end?</P>
<P>"[I]n the back of every American Muslim's mind right now is the camps that Japanese-Americans were thrown into after the [Pearl Harbor] bombing," Omar Ricci of the LA branch of the Muslim Public Affairs Council told the <I>Wall Street Journal.</I></P>
<P>As Rania Masri told <I>Socialist Worker, </I>"I woke up this morning to news that Arab 'cells' have been arrested throughout the country, and I am truly frightened for tomorrow. The local mosque has received two bomb threats&#133;and numerous Arab-looking people and women in Muslim dress have already been insulted and spat on. I fear the worst is yet to happen."</P>
<P>We can't let them get by with this scapegoating. We have to oppose all attacks on Arabs and Muslims--and organize opposition to Bush's drive for war. And we have to recognize that restrictions on civil liberties aimed at "fighting terrorism" will be used against anyone who opposes the government's policies--whether Arab or not.</P></FONT></BODY>

