<p><font face="Arial, Helvetica, sans-serif" size="2" color="#990000">September 14, 2001 | <i>SW</i> special edition</font></p>

<p><a name="FrontAndBackPages"></a><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>FRONT AND BACK PAGES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_01_Tragedy.shtml">Don't turn tragedy into war</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">As people around the world were still grappling with the enormity of the human losses in the attacks on New York and Washington, D.C., George W. Bush and his government were beating the drum for even more death and destruction.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Media's sickening racism sparks anti-Arab attacks</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377A_04_Scapegoating.shtml">No racist scapegoating!</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The media were full of anti-Arab and anti-Muslim rants in the aftermath of the attacks on New York and Washington, D.C. And using the cover of sympathy for the attacks' victims, racists across the U.S. took the cue.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_01_Vigils.shtml">Thousands attend vigils to call for peace</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">While politicians clamor for war, thousands of people turned out for vigils across the country to mourn the victims of the attacks in New York and Washington--and to show their opposition to more violence.</P>

<p><a name="Interviews"></a><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>INTERVIEWS AND REPORTS</b></font><br>

<P><font face="Times New Roman, Times, serif" size="3"><b>NOAM CHOMSKY</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377A_03_Chomsky.shtml">"This will be exploited by the jingoist right"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">With good PR, even the flimsiest arguments will carry some weight among a frightened public. In short, the crime is a gift to the hard jingoist right--those who hope to use force to control their domains. </P>

<P><font face="Times New Roman, Times, serif" size="3"><b>HOWARD ZINN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377A_04_Zinn.shtml">"They perpetuate a cycle of terrorism"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. government is going to respond--and the media will sheepishly go along with calls for military action, increases in the military budget, which is exactly what makes terrorism inevitable.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>NORMAN SOLOMON</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377A_02_Solomon.shtml">"A media that invents history"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">George Orwell's maxim is chillingly appropriate. He wrote, "Those who control the past control the future. Those who control the present control the past."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_02_Witness.shtml">Witness to the tragedy in NYC</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">I work for UPS at 175 Water Street, about eight to 10 blocks from the World Trade Center. At 8:45 a.m., most us were hanging out at the air package center. My route is around 1 Liberty Plaza--across the street from the World Trade Center. But that day, the truck hadn't yet come down with our work. </P>

