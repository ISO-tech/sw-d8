<p><a name="Analysis"></a><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>ANALYSIS AND BACKGROUND</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>The U.S. government:</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377A_02_USViolence.shtml">"The greatest purveyors of violence in the world"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. government is reacting to the attacks in New York and Washington the only way that it ever has--by arming itself to ruthlessly impose its military, political and economic interests around the world. It is this history that explains why millions of people across the globe despise the U.S. government.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_03_Truth.shtml">Truth is the first casualty of war</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. government has a long history of manipulating the truth--even outright lying--in order to win public approval for its military adventures.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_04_PearlHarbor.shtml">The real truth about Pearl Harbor</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">"This is Pearl Harbor for the age of terrorism," wrote one Chicago columnist about the suicide plane attacks. But the story behind Japan's December 1941 air assault reveals more than the media would like us to know.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_03_MiddleEast.shtml">How the U.S. backs violence against Palestine</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Osama bin Laden topped the list of suspects--even though he denied any connection to the attacks. But politicians were quick to pin the blame on Palestinians as well--again, despite the fact that Palestinian Authority leader Yasser Arafat and every Palestinian organization condemned the attack.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="377A_02_BinLaden.shtml">How the CIA trained Osama bin Laden</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Osama bin Laden has been a favorite bogeyman for the Washington establishment for years. But if bin Laden is involved in terrorism, he was taught by the best--the U.S. government.</P>

<P><font face="Times New Roman, Times, serif" size="3"><i>This special bulletin was written by Lance Selfa, Lee Sustar, Elizabeth Schulte, Eric Ruder, Paul D'Amato, Anthony Arnove, Alan Maass, Todd Chretien and Sherry Wolf.</i></P>

