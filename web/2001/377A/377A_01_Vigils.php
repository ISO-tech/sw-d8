<P><font face="Times New Roman, Times, serif" size="5"><b>Thousands attend vigils to call for peace</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WHILE POLITICIANS clamor for war, thousands of people turned out for vigils across the country to mourn the victims of the attacks in New York and Washington--and to show their opposition to more violence.</P>
<P>In Berkeley, Calif., some 4,000 University of California students attended a "free speech candlelight vigil for peace" in the wake of the attacks. </P>
<P>More than 50 students and community members spoke at an open mic. Some talked about people they knew who died in the attacks, and a few supported President Bush's calls for war. But the vast majority spoke against the U.S. responding with violence and the racist backlash against Arabs and Muslims.</P>
<P>After one student called the attacks a "new Pearl Harbor" and urged the crowd to prepare for war, the next speaker said, "Remember how Pearl Harbor ended? With the incineration of 100,000 men, women and children in Nagasaki and Hiroshima." </P>
<P>At the University of Iowa in Iowa City, nearly 1,000 sat silently holding candles and listening to brief statements at a vigil the same night. A handful of students clamoring for war against Arab nations shouted down other speakers. But one activist responded by reading out a nasty message posted on his group's cubicle implying that activists would be happy with the attacks.</P>
<P>"We're not happy, we're horrified," he said, to loud applause. "We're against terrorism, and we're against Bush using this tragedy to scapegoat minorities like Arabs."</P>
<P>Hundreds gathered at vigils held in New York the day after the attacks. And in Chicago, a vigil called by the Rev. Jesse Jackson was attended by some 120 people.</P>
<P>The politicians and pundits will push patriotism. But these turnouts show that not everyone is ready to support Bush's calls for war and the growing racist backlash.</P>

