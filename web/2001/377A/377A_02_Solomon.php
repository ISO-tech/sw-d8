<P><font face="Times New Roman, Times, serif" size="4">NORMAN SOLOMON:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"A media that invents history"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>NORMAN SOLOMON, a syndicated columnist on media and politics, talked to <i>Socialist Worker</i> about the mainstream media's response to the air attacks.</b></P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE MAINSTREAM media have very little use for inconvenient history. We're almost in a history-free zone right now. George Orwell's maxim is chillingly appropriate. He wrote, "Those who control the past control the future. Those who control the present control the past."</P>
<P>To talk about Osama bin Laden without mentioning his relations with the CIA in Afghanistan in the 1980s is to simply put history into the dustbin of history. And watching hour after hour of network TV in the last 24 hours, I couldn't find the slightest such reference. </P>
<P>We have mass media in the United States that simply reinvent history as what's convenient. This is driven largely by the close proximity between press and state--and the fact that many of the same assumptions that hold sway in the Oval Office or the Pentagon or the State Department hold sway in the newsrooms of major media outlets. </P>
<P>I'll give you an example. Throughout the first day of coverage, you had one of the better anchors, Peter Jennings on ABC, in frequent discussion with another ABC news employee named Vincent Cannistraro. </P>
<P>Cannistraro was identified to the millions of viewers only as a news analyst. What the audience wasn't told is that Cannistraro was in charge of the CIA's work with the contras in Nicaragua in the early 1980s. Another way to say it is that Vincent Cannistraro was in charge of implementing terrorism for the CIA less than 20 years ago. </P>
<P>Now he's working for ABC condemning terrorism. </P>
<P>In 1984, Vincent Cannistraro moved on to the National Security Council, and he became a supervisor of the mujahedeen guerrillas in Afghanistan. And we can plausibly assume that Cannistraro worked with Osama bin Laden's buddies in that role under the Reagan administration.</P>
<P>Within hours after the attacks on the World Trade Center and the Pentagon, Colin Powell said, "Once again, we see terrorism, we see terrorists, people who don't believe in democracy, people who feel that with the destruction of buildings, with the murder of people, they can somehow achieve a political purpose."</P>
<P>If you read that and you look at the phrase, "people who feel that with the destruction of buildings, with the murder of people, they can somehow achieve a political purpose," we're reading an apt description of Colin Powell himself, and the people he works with and for. </P>
<P>Specifically, it's a very good description of what he and his colleagues--including Dick Cheney and George Herbert Walker Bush--did during the Gulf War, which after all is the reason why Colin Powell is so prominent today. He became a media superstar supervising a war that--in less than two months, according to the Pentagon itself--killed about 200,000 people with exactly the underlying approach that Powell condemned right after the tragedy took place. </P>
<P>What's on the agenda-setters' agenda is to beat the war drums in the guise of retaliation. They're laying out the rationale for the U.S. to use massive military power for its own terroristic purposes.</P>

