<P><font face="Times New Roman, Times, serif" size="5"><b>The real truth about Pearl Harbor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"THIS IS Pearl Harbor for the age of terrorism," wrote one Chicago columnist about the suicide plane attacks. But the story behind Japan's December 1941 air assault reveals more than the media would like us to know.</P>
<P>U.S. officials had intercepted Japanese messages and knew about the impending attack before it took place--but did nothing because they wanted Japan to fire the first shot. Then-Secretary of State Henry Stimson told Congress after the war that the U.S. government wanted to maneuver Japan to "fire the first shot...in order to have the full support of the American people" for U.S. entry into the war.</P>
<P>After the attack--in which more than 2,000 U.S. soldiers lost their lives--the U.S. government and media whipped up a hysterical campaign against Japanese Americans. President Roosevelt signed Executive Order 9066, which forced more than 120,000 Japanese Americans into concentration camps.</P>
<P>The attack on Pearl Harbor was also used to justify the dropping of atomic bombs on Hiroshima and Nagasaki in August 1945--costing the lives of more than 200,000 people.</P>
<P>President Truman justified the atomic bombing on the grounds that it saved lives by bringing a quicker end to the war. But Japan was already ready to surrender before the bombs were dropped. As Major Gen. Curtis LeMay remarked in September 1945, "The atomic bomb had nothing to do with the end of the war at all."</P>
<P>The real reason for Hiroshima and Nagasaki was to demonstrate to Russia and the rest of the world the awesome power of America's new weapon of mass terror.</P>
<P>The parallels between Pearl Harbor and the recent terrorist attacks lead us to ask the same question posed by Alexander Cockburn and Jeffrey St. Clair. "Are we to believe," they write, "that the $30 billion annual intelligence budget, immense electronic eavesdropping capacity, thousands of agents around the world, produce nothing in the way of a warning?"</P>

