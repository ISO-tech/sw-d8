<P><font face="Times New Roman, Times, serif" size="5"><b>How the CIA trained Osama bin Laden</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">SAUDI MILLIONAIRE businessman Osama bin Laden and his "vast network" of international terrorists were quickly blamed for the air attacks on New York and Washington, D.C.</P>
<P>This is nothing new. Bin Laden has been a favorite bogeyman for the Washington establishment for years. He's been blamed for the suicide attack on a U.S. destroyer in Yeman last year, the bombings of two U.S. embassies in Africa in 1998 and more.</P>
<P>But if bin Laden is involved in terrorism, he was taught by the best--the U.S. government.</P>
<P>Both he and the Taliban in Afghanistan that protects him are products of the 10-year-long, U.S.-backed war against the ex-USSR occupation of Afghanistan. After Russian troops invaded Afghanistan in 1979, the U.S. trained bin Laden and thousands of other Arab men.</P>
<P>Back then, President Ronald Reagan liked to call bin Laden and his cohorts "freedom fighters."</P>
<P>After the USSR was forced out of Afghanistan in 1989, the CIA-trained "freedom fighters" split into rival factions that fought a civil war during the 1990s. With help from the CIA and U.S. ally Saudi Arabia, the small Taliban militia group emerged out of the chaos, taking over the government in 1996.</P>
<P>The U.S. backed the hard-line Islamists of the Taliban because they thought the group would be able to provide stability for big business. But then the Taliban began to shelter bin Laden and other Islamist movements that the U.S. opposes.</P>
<P>Like Iraq's Saddam Hussein before them, bin Laden and the Taliban have moved from U.S. allies to enemies.</P>

