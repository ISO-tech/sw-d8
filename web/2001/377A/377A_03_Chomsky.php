<P><font face="Times New Roman, Times, serif" size="4">NOAM CHOMSKY:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This will be exploited by the jingoist right"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>NOAM CHOMSKY, one of the best-known opponents of U.S. militarism and imperialism, talked about the background to the attacks.</b></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE TERRORIST attacks were major atrocities. In scale, they may not reach the level of many others--for example, Clinton's 1998 bombing of the Sudan with no credible pretext, destroying half its pharmaceutical supplies and killing unknown numbers of people (no one knows, because the U.S. blocked an inquiry at the UN, and no one cares to pursue it). And this is not to speak of much worse cases, which easily come to mind. </P>
<P>But that this was a horrendous crime is not in doubt. The primary victims, as usual, were working people: janitors, secretaries, firemen, etc. It is likely to prove to be a crushing blow to Palestinians and other poor and oppressed people. It is also likely to lead to harsh security controls, with many possible ramifications for undermining civil liberties and internal freedom.</P>
<P>The events reveal, dramatically, the foolishness of the project of "missile defense." As has been obvious all along, and pointed out repeatedly by strategic analysts, if anyone wants to cause immense damage in the U.S., including weapons of mass destruction, they are highly unlikely to launch a missile attack, thus guaranteeing their immediate destruction. But today's events will, very likely, be exploited to increase the pressure to develop these systems and put them into place. </P>
<P>"Defense" is a thin cover for plans for the militarization of space, and with good PR, even the flimsiest arguments will carry some weight among a frightened public. In short, the crime is a gift to the hard jingoist right--those who hope to use force to control their domains. </P>
<P>As to how to react, we have a choice. We can express justified horror; we can seek to understand what may have led to the crimes, which means making an effort to enter the minds of the likely perpetrators. If we choose the latter course, we can do no better, I think, than to listen to the words of Robert Fisk, whose direct knowledge and insight into affairs of the region is unmatched after many years of distinguished reporting. </P>
<P>Describing "the wickedness and awesom cruelty of a crushed and humiliated people," he writes, "This is not the war of democracy versus terror that the world will be asked to believe in the coming days. It is also about American missiles smashing into Palestinian homes and U.S. helicopters firing missiles into a Lebanese ambulance in 1996, and American shells crashing into a village called Qana, and about a Lebanese militia--paid and uniformed by America's Israeli ally--hacking and raping and murdering their way through refugee camps." </P>
<P>And much more. Again, we have a choice: we may try to understand--or refuse to do so, contributing to the likelihood that much worse lies ahead.</P>

