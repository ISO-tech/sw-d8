<P><font face="Times New Roman, Times, serif" size="5"><b>Witness to the tragedy in NYC</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>D.K. is a member of the ISO and contributor to <I>Socialist Worker.</I> He tells what he saw in New York on the day of the air attacks.</b></P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">I WORK for UPS at 175 Water Street, about eight to 10 blocks from the World Trade Center. </P>
<P>At 8:45 a.m., most us were hanging out at the air package center. My route is around 1 Liberty Plaza&#150;across the street from the World Trade Center. But that day, the truck hadn't yet come down with our work.</P>
<P>I never heard the first explosion. I just saw what looked like snow or confetti raining down outside. We ran to the corner and looked to where everybody was pointing. About 15 minutes later came the second strike&#150;a sudden boom and a terrifying ball of fire exploding everywhere. Even though I thought we were a safe distance away, some people started running. Soon, we heard that both buildings had been hit by planes.</P>
<P>That's when we decided to get out of Manhattan. A bunch of us walked inside to grab our stuff. </P>
<P>Our supervisor was there. He told us to stick around until we heard what UPS wanted us to do. My coworker, J., wasn't hearing that. He just kept walking. So the supervisor started barking at him that he'd better stay or turn in his ID card.</P>
<P>It was an absurd moment. The supervisor's yelling at J., I'm yelling at the supervisor, people are yelling at J. not to kill the supervisor, and outside, everybody is screaming. </P>
<P>We just left. He didn't try to stop us. The situation was too crazy for even UPS to control.</P>
<P>About five of us walked up to the Brooklyn Bridge. There were thousands of people already walking over to Brooklyn. On the bridge, we met a guy who was on the 90th floor of one of the twin towers, who was able to evacuate after the plane ran into the other tower. He was saying, "We can't let these people into our country." One of my coworkers, who is an immigrant, was nodding his head. </P>
<P>J. argued the opposite. "You've got to blame the government," he repeated all morning. "They fuck with people around the world, and now it's coming back to us." </P>
<P>About halfway over the bridge, someone pointed behind us and shouted. We turned and saw one of the twin towers collapse. Another UPSer turned to me with tears in him eyes and said, "Do you know how many people just died? All those firefighters and cops?" Then we thought about what would have happened to us if we had listened to the supervisor. </P>
<P>We walked down Atlantic Avenue. This is an Arab neighborhood, and I was afraid we'd see people terrorizing the area, but it was calm. Since then, I've heard about anti-Arab violence in Brooklyn and Westchester&#150;but the news hasn't mentioned it.</P>
<P>There's been a lot of anti-Arab racism whipped up, but J.'s argument made much more sense: Of course we're scared and angry, but let's blame the force that's actually responsible for this situation: the U.S. government.</P>

