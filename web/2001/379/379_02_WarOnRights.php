<P><font face="Times New Roman, Times, serif" size="4">Bush White House plays on fears to gain draconian new powers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The war on our rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the Bush administration offensive against civil liberties.</P></B>
<font face="Times New Roman, Times, serif" size="3"><P>"I DON'T KNOW of any American that hasn't been inspired with the understanding of the willingness of law enforcement to do whatever is necessary to secure the safety of American citizens." That's what Attorney General John Ashcroft told reporters October 4.</P>
<P>Too bad he didn't ask Dr. Albader al-Hazmi.</P>
<P>FBI, INS and U.S. Customs officials showed up at al-Hazmi's San Antonio, Texas, home at 5 a.m. on September 12. "There were six men with guns," said al-Hazmi. "They asked me if I knew Mohammed Atta, and I said that I'd never heard that name." </P>
<P>Al-Hazmi was arrested. After two days of questioning, he was flown to New York--with FBI agents routinely shouting at him and kicking him in the small of his back.</P>
<P>It wasn't until six days after his arrest that al-Hazmi was finally told why he was detained--he shares the same last name as two of the suspected hijackers, and he had contacted a bin Laden family member about a religious educational group.</P>
<P>And it took another week after that for the FBI to admit it had screwed up--and that al-Hazmi had no connection to the September 11 attacks.</P>
<P>Al-Hazmi's release didn't make the headlines that his arrest as an "alleged terrorist conspirator" did. So friends are helping to pay for a security guard to protect him from racist attacks.</P>
<P>But more than vigilantes, al-Hazmi fears the "three-letter agencies that can abuse the law for whatever reason they want," says friend Abdulla Mohammed.</P>
<P>The stories are similar among the hundreds of Arab Americans and foreign nationals detained as part of the government's investigation. And if Attorney General John Ashcroft gets his way, there will be more victims.</P>
<P>Ashcroft's Justice Department put law enforcement agencies across the country on high alert as the war on Afghanistan began. Officials said there was a 100 percent certainty that "international terrorists" would strike again in the U.S. </P>
<P>Meanwhile, newspapers, magazines and TV news programs hyped the threat of "bioterrorism"--complete with advice from health "experts" on what to do in the event of an anthrax attack.</P>
<P>The hysteria about "America's security" grew to such proportions that one lawmaker got on TV to advise people to be more vigilant and report "suspicious activity"--effectively to spy on their neighbors.</P>
<P>It's not hard to see who benefits from this witch-hunt atmosphere. The politicians are playing on fears about terrorism to gain draconian new powers for law enforcement.</P>
<P>For example, Congress is expected to pass the Provide Appropriate Tools Required to Intercept and Obstruct Terrorism (PATRIOT) Act in the coming days. The bill will expand the government's ability to tap phones, monitor the Internet and share information without oversight by courts.</P>
<P>The Bush administration is pushing hardest for a provision allowing the government to arrest and hold foreign nationals for up to seven days--or indefinitely during an "emergency"--without charging them with a crime. </P>
<P>But there's plenty more to fear from the PATRIOT Act. Under the proposal, even small acts of damage to federal property--like a rock thrown through a window during a demonstration--can be considered "terrorism." Under that provision, said a spokesperson for the American Civil Liberties Union, "an organization like the People for the Ethical Treatment of Animals could be investigated as a terrorist group because one of its members hits the Secretary of Agriculture with a pie."</P>
<P>But authorities aren't waiting for any new laws to begin violating civil rights. More than 500 foreign citizens are being held by the federal government as part of the investigation into the September 11 attacks.</P>
<P>Meanwhile, government agencies are rifling through the private records of thousands of foreign students. By law, schools are prevented from disclosing students' personal information without written consent.</P>
<P>But during the past two weeks, the U.S. Department of Education told colleges and universities that the law "doesn't apply" during "a health or safety emergency." Dozens of colleges have confirmed that they've turned over information to the FBI and INS. </P>
<P>The politicians may talk about "security." But restrictions on our rights are aimed squarely at squashing dissent.</P>

