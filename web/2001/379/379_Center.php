<p><font face="Arial, Helvetica, sans-serif" size="2"><b><i>SOCIALIST WORKER</i> SPECIAL FEATURE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can the U.S. bring justice?</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Issue 379</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is prepared to rain death and destruction on one of the poorest countries on earth. And he's doing it in the name of "justice." </P>
<P>Bush and other U.S. political leaders say that their war isn't against the Afghan people, but against the terrorists responsible for the September 11 attacks on New York and Washington, D.C.--specifically against Osama bin Laden and his protectors in Afghanistan's Taliban government. </P>
<P>That will be cold comfort to the country's starving refugees. But there's another question that's gone unanswered in Washington: What gives the U.S. government the right to "end states," topple governments and determine the fate of people around the globe?</P>
<P>To Bush and his buddies, might makes right. Because the U.S. is the world's only superpower, they figure it has the right to do whatever it wants. Naturally, people around the globe are bitter and angry at the U.S. government's self-appointed role as world's top cop. So are many people in the U.S. who oppose Bush's drive to war against Afghanistan.</P>
<P>But the question remains: Will those responsible for the horrific attacks on September 11 be brought to justice?</P>
<P>Some in the new antiwar movement have proposed an international tribunal, possibly overseen by the United Nations, in place of Bush's war. But the U.S. government has enormous power to bend international institutions like the UN to its will. </P>
<P>Here, <I>Socialist Worker </I>offers viewpoints on the question, Can the U.S. bring justice?</P>


<P><font face="Times New Roman, Times, serif" size="3"><b>HOWARD ZINN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_09_HowardZinn.shtml">"Democracy isn't falling into line"</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The writer I.F. Stone used to be asked to speak to journalism classes, and he would say, "If you want to know about governments, all you have to know is two words: governments lie." </P>

<P><font face="Times New Roman, Times, serif" size="3"><b>AHMED SHAWKI</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_08_CanUSBringJustice.shtml">"War will not bring us one step closer to justice"</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The tragedy of September 11 is being used by the government of this country not to honor those who died, not to search for justice, but to advance its agenda. </P>

<P><font face="Times New Roman, Times, serif" size="3"><B>SHARON SMITH</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_08_WhichSide.shtml">U.S. government's double standards</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Since the September 11 attacks, the looming question is how to prevent such terrorist acts in the future. Bush's solution is an all-out war to bring the terrorists to "justice." But what is the definition of "terrorism"--and who should be entrusted to deliver justice?</P>

