<P><font face="Times New Roman, Times, serif" size="4">Cincinnati jury acquits Stephen Roach</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Killer cop walks free</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 12, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE CINCINNATI cop who shot and killed Timothy Thomas--sparking a three-day rebellion against police violence last April--walked out of court free last month.</P>
<P>Officer Stephen Roach was facing relatively minor charges of negligent homicide and obstructing official business, with a sentence of nine months if found guilty. But he didn't even get this slap on the wrist.</P>
<P>In his original statement after killing Thomas, Roach said that his gun "just went off." He later changed his story to claim that Thomas was reaching into his pants--for a weapon that wasn't there.</P>
<P>But Roach wasn't really on trial last month. Timothy Thomas was. Roach's lawyers based their entire defense on Thomas' past criminal record.</P>
<P>Thomas was the 15th Black man killed in encounters with Cincinnati officers since 1995. The anger of residents began to boil over after his murder in April.</P>
<P>When cops attacked protesters gathered for a peaceful vigil at police headquarters, the city exploded into three nights of rioting that captured worldwide attention.</P>
<P>After the verdict last month, Mayor Charlie Luken again declared a citywide emergency--following isolated reports of rock throwing and fires in garbage cans.</P>
<P>Yet Luken has done nothing since April to ease the conditions that led to the rioting.</P>
<P>While residents of poor neighborhoods like Over-the-Rhine remain stuck in poverty, the city has gotten on with the business of enriching big corporations that pull the strings in Cincinnati.</P>
<P>And to add insult to injury, the City Council voted last month to add 75 more cops to the city's police force--at a cost of some $4 million!</P>

