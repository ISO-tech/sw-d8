<P><font face="Times New Roman, Times, serif" size="4">Shot down by strangers for being Arab</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop racist attacks!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Abdo Ali Ahmed lived in the small farm town of Reedley, Calif., for 35 years. He came from Yemen and worked in the fields for years before opening a small convenience store about 10 years ago, typically working 16-hour days. He and his wife had six children--all are under the age of 12. </P>
<P>On September 29, while thousands of people around the country attended antiwar rallies, two strangers walked into Abdo's store and shot him to death. A few days earlier, Abdo had found a death threat on his car windshield but decided to ignore it.</P>
<P>I attended his funeral along with 300 people, mostly from the Muslim community. Several members of the local mosque talked about what a wonderful friend Adbo was and how he didn't have any enemies. </P>
<P>The most heartbreaking part of the funeral was when Yemen's Consul General, Monsoor Ismael, gave Abdo's sons a letter expressing sorrow for the loss of their father. </P>
<P>"Five days ago I spoke to Abdo's three-year-old son," said Ismael. "I asked him if he knew where his father was. His youngest son said, 'My father has gone to the store to get me an ice cream and candy.' Now I have to say to you that there is still ice cream and candy, but no more father. Now I am your father, the people of Yemen are your father, we are all your father." </P>
<P>The littlest boy smiled and waved at the crowd in the mosque, happy to be the center of attention of so many people. He didn't yet understand that his father is gone forever. </P>
<P>Then, Mohamed Rocka from the Fresno mosque spoke. "Today we must add one more victim to the list of September 11, Abdo Ali Ahmed," he said.</P>
<P>And the list keeps growing. Last week, a Muslim woman wearing a scarf walked into a department store and was told by the clerk that she would not be served. </P>
<P>However, many people are stepping forward to speak out against the hate. Farmworkers from the American Friends Service Committee came to the funeral to show their support, and hundreds of people in Fresno are organizing antiwar and anti-hate crime vigils. </P>
<P>Sometimes organizing against the bipartisan war drive can seem overwhelming. But from now on, whenever I get a little weary, I will think of Abdo's son and keep going. </P>
<B><P>Todd Chretien,</B> Oakland, Calif.</P>

