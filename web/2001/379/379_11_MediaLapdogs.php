<P><font face="Times New Roman, Times, serif" size="4">Right-wingers take aim at all dissent from the pro-war chorus</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Wagging the media lapdogs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 12, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN THE weeks since the September 11 attacks in New York and Washington, D.C., right-wingers went on the offensive against critics of George W. Bush--and, in fact, any dissent from the pro-war chorus in the mainstream media.</P>
<P>The best-known incident so far was the campaign against Bill Maher of <I>ABC's Politically Incorrect</I>. The usually smarmy Maher got in hot water when he suggested that President Bush's description of the September 11 hijackers as "faceless cowards" didn't hold up.</P>
<P>Responding to right-winger Dinesh D'Souza's assertion that people who are willing to die for their cause aren't "cowards," Maher agreed and said, "We've been the cowards lobbing cruise missiles from 2,000 miles away. That's cowardly."</P>
<P>Right-wing radio shock jocks called on advertisers to pull their support from <I>Politically Incorrect</I>. Federal Express and Sears did so.</P>
<P>With <I>Politically Incorrect</I>'s future hanging in the balance, Maher had to apologize, groveling for his job on his own show and on other late-night TV programs.</P>
<P>But that wasn't good enough for the White House. Bush spokesperson Ari Fleisher twice attacked Maher--and warned that "people have to watch what they say, watch what they do."</P>
<P>So far, ABC hasn't fired Maher. But the same can't be said for Dan Guthrie and Tom Gutting.</P>
<P>Guthrie, a columnist for the <I>Daily Courier</I> in Grants Pass, Ore., and Gutting, a columnist for the <I>Texas City Sun</I> in Texas City, Texas, were fired after they wrote opinion pieces criticizing Bush's performance on September 11.</P>
<P>The criticisms they made--that Bush's stops at two airbases before returning to Washington didn't project "presidential leadership"--were trivial. But they got the boot anyway.</P>
<P>"We felt [that Guthrie's column] turned into a personal attack as opposed to expanding the concept of the president being on the front line," <I>Daily Courier</I> publisher Dennis Mack told the <I>New York Times</I>.</P>
<P>In fact, the White House was soon exposed for concocting the story that it had evidence that Air Force One was a target on September 11.</P>
<P>But true to form, the media were more intent on attacking anyone who questioned the U.S. war drive.</P>
<P>"It is our obligation to freely debate the direction our country and our leaders are taking," Gutting wrote in the <I>Houston Chronicle</I> after he was fired.</P>
<P>Not exactly the attitude of those big-name "journalists" who gushed over Bush and beat the war drums--channeling the spirit of the state-controlled press of the ex-USSR.</P>
<P>CBS News anchor Dan Rather--a favorite target for right-wing critics of the "liberal" media--was one of the most craven. "George Bush is president," Rather said on <I>Late Night with David Letterman</I>. "He makes the decisions and, you know, as just one American, he wants me to line up, just tell me where."</P>
<P>Had a journalist in Iran or Cuba said something similar, Rather would be slamming them for spouting "government propaganda."</P>
<P>But with all the networks wrapping themselves in red, white and blue, there wasn't even a hint of criticism in the corporate media.</P>
<P>Don't expect Dan Rather or Disney/ABC to truly examine Bush's war drive. That will be the job of the antiwar movement.</P>
<P>As Texas activists Robert Jensen and Rahul Mahajan put it: "The chance to build a genuine antiwar movement is greater than it has been in a very long time--as long as, to take a leaf from George W. Bush, we do not tire and we do not falter."</P>

