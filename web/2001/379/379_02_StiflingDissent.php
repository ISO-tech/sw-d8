<P><font face="Times New Roman, Times, serif" size="5"><b>Stifling dissent on campuses</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RIGHT-WINGERS are using war fever to clamp down on dissent on college campuses. At City College of New York (CCNY), media pundits and school officials joined forces for an old-fashioned witch-hunt.</P>
<P>Columnist Andrea Peyser and her pals at the <I>New York Post</I> got the ball rolling by publishing a distorted account of an antiwar teach-in sponsored by the professors' union at CCNY.</P>
<P>Then Peyser got Jeffrey Wiesenfeld, a trustee of the City University of New York system, to chime in. "They're fortunate it's not up to me," Wiesenfeld told Peyser. "I would consider that behavior seditious at this time." </P>
<P>Wiesenfeld plans to propose a resolution condemning the teach-in at a trustees' meeting.</P>
<P>Opponents of war are under pressure at other schools. At the University of Texas, journalism professor Robert Jensen has become a target for speaking out against the war drive.</P>
<P>But antiwar activists aren't hiding their heads. For example, a group of college professors are putting together a statement defending their right to free speech.</P>
<P>We can't let the right wing silence dissent.</P>

