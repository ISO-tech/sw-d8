<P><font face="Times New Roman, Times, serif" size="5"><b>A rogues' gallery of allies for Bush's war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN ITS "war against terrorism," the Bush administration is embracing a rogues' gallery of governments that are among the most violent and antidemocratic in the world.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>RUSSIA</b></font><br></p>
<font face="Times New Roman, Times, serif" size="3">RUSSIA IS using its place in Bush's "coalition against terror" to extract international support for its own reign of terror--in the rebellious province of Chechnya.</P>
<P>The Russian forces' onslaught in Chechnya targeted hospitals, apartment blocks and schools without distinction. The capital of Grozny was bombed into rubble. </P>
<P>At least 50,000 people--probably many more--were killed in Russia's onslaught on Grozny alone, and many more are refugees. </P>
<P>Vladimir Putin, the ex-KGB spymaster, exploited the war to scramble to power in elections to succeed Boris Yeltsin.</P>
<P>Now he's using the U.S. war drive to head off all criticism of the Chechen War--by demonizing Chechen guerrillas as "Islamist terrorists."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>SAUDI ARABIA</b></font><br></p>
<font face="Times New Roman, Times, serif" size="3">SAUDI ARABIA, long the U.S. government's main Arab ally in the Middle East, is one of the most repressive countries on Earth.</P>
<P>There are no elections, and political parties are banned. The government is a monarchy that has been run by the Saud family for the last century.</P>
<P>Under the same Islamic laws imposed by the Taliban in Afghanistan, the oppression of women and religious minorities in Saudi Arabia is savage. Saudi judges routinely hand out sentences of death, maiming and torture. </P>
<P>One-third of workers are immigrants who have no rights and are denied citizenship. </P>
<P>But the U.S. considers Saudi Arabia a crucial ally--perhaps because the country sits on 26 percent of the world's proven oil reserves. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>PAKISTAN</b></font><br></p>
<font face="Times New Roman, Times, serif" size="3">WHEN GEN. Pervez Musharraf seized power in Pakistan in an October 1999 military coup, George W. Bush--then stumping for support for his presidential bid--seemed pleased. "The new Pakistani general, he's just been elected--not elected, this guy took over office," Bush stammered. "It appears this guy is going to bring stability to the country, and I think that's good news for the subcontinent."</P>
<P>Pakistan has been the main sponsor of the Taliban government in Afghanistan. But under pressure from the U.S., Musharraf flipped after the September 11 attacks.</P>
<P>In exchange for cooperation, the U.S. is offering economic aid and has ended sanctions imposed after Pakistan tested nuclear weapons as part of its ongoing rivalry with India.</P>
<P>But many parts of the regime still support the Taliban, and collaboration with the U.S. could ignite a massive popular upheaval.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>ISRAEL</b></font><br></p>
<font face="Times New Roman, Times, serif" size="3">THE GOVERNMENT of Prime Minister Ariel Sharon lost no time in using the September 11 attacks in the U.S. as an excuse to step up Israel's war on Palestinians.</P>
<P>Since then, the U.S. government has tried to rein in Israel's violence in order to build support for its war drive among Arab regimes.</P>
<P>Sharon and his government have lashed back at Bush in response. But don't think that this is anything other than bickering between old allies.</P>
<P>The U.S. has been--and will continue to be--the main backer of Israel's apartheid.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>CHINA</b></font><br></p>
<font face="Times New Roman, Times, serif" size="3">LONG A favorite target for right-wingers, China makes a strange partner in a supposed war to defend freedom--in more ways than one.</P>
<P>Consider the massacre of pro-democracy demonstrators in Tiananmen Square in 1989. China continues to carry out mass executions of hundreds of people at a time.</P>
<P>Religious sects like the Falun Gong are repressed, and the government is now cracking down on Islamic groups in northwestern China--under the cover of a war on "terror."</P>

