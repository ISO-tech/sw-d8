<P><font face="Times New Roman, Times, serif" size="5"><b>We want justice for the Death Row 10</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 12, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER YEARS of cover-ups and stonewalling, prosecutors in Chicago are offering deals to several members of the Death Row 10.</P>
<P>The Death Row 10 are a group of Illinois prisoners who were convicted and sentenced to death mainly because of "confessions" tortured out of them by Chicago cops under the command of former Lt. Jon Burge. Together, these men have spent more than a century behind bars.</P>
<P>Now, Cook County State's Attorney Richard Devine is offering deals to several of them--but at an enormous cost. The prisoners would have to drop their torture claims and admit "guilt."</P>
<P>This was too much for Aaron Patterson, a Death Row 10 member who rejected a deal that would have not only gotten him off death row, but out of jail altogether in a few years.</P>
<P>"I'm going to stay until we get this thing right," Patterson told the <I>Chicago Tribune</I>. "I'm pretty much certain they know now I didn't do it, but they're trying to wiggle their way out of it."</P>
<P>Devine has a personal interest in sweeping the issue of police torture under the rug. He was assistant state's attorney--under none other than Richard Daley, now Chicago's mayor--when most of the Death Row 10 were tortured and prosecuted. </P>
<P>His offers are the result of the growing campaign to win justice for the Death Row 10, led by the Campaign to End the Death Penalty. </P>
<P>We have to keep up this fight--until the Death Row 10 win the justice they deserve.</P>

