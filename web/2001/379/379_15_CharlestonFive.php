<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ON THE PICKET LINE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Charleston Five trial nears</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHARLESTON, S.C.--The Charleston Five could go to trial as early as November 12, and defense committees are gearing up for an international day of action the following day.</P>
<P>The five--members of International Longshoremen's Association (ILA) Locals 1442 and 1771--have been under house arrest for 20 months following a police attack on their legal picket line.</P>
<P>Racial justice is also at the heart of the case. Local 1422 is overwhelmingly African American, and the case is being prosecuted by the conservative Republican State Attorney General Charlie Condon.</P>
<P>Outrageously, Condon recently compared the ILA pickets with the air attacks on New York and Washington to justify his crackdown.</P>
<P>But meetings and rallies of labor and civil rights organizations across the U.S. have raised funds for defense costs. ILA Local 1422 President Kenneth Riley recently returned from a tour of Europe to build support.</P>
<P>Leonard Riley, Kenneth's brother and a longtime leader in Local 1422, met with union leaders in Chicago last week. He left with the commitment by Illinois AFL-CIO President Margaret Blackshere and top building trade unions to raise funds and organize a rally on the day of action.</P>
<P>Leonard also met with UAW local leaders and spoke to a meeting of regional UAW political activists on a platform alongside Sen. Tom Harkin (D-Iowa) and Rep. Tom Barrett (D-Wis.).</P>
<P>Pre-trial hearings scheduled for mid-October will set the final trial date. But activists should begin preparing for action within the month.</P>
<P>As the members of Local 1422 say, if these five men go to jail, our rights go with them.</P>
<I><P>Kenneth Riley's next tour includes meetings in New York City October 12 at 6 p.m. at the State Office Building on 125th Street, and in Los Angeles at the County Federation of Labor October 15.</P></I>

