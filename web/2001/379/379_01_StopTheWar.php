<P><font face="Times New Roman, Times, serif" size="5"><b>Stop this war!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 1</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. has gone to war against one of the poorest countries in the world. On October 7, U.S. cruise missiles slammed into targets in Afghanistan--followed by waves of U.S. and British planes dropping bombs on an already devastated country.</P>
<P>Frightened people fled Kabul and other cities after the night of horror. "I will sleep under the sky rather than stay in the city for another night," one told a reporter.</P>
<P>And the worst is yet to come. Pentagon officials say the initial attacks are aimed at "softening up" Afghanistan for even heavier assaults to follow.</P>
<P>Naturally, the U.S. claimed that it hit only military targets. But we heard that claim during NATO's 1999 war over Kosovo--and it turned out to be a lie.</P>
<P>So is the supposed "pinpoint precision" of U.S. "smart" weapons. After the 1991 Gulf War against Iraq, the Pentagon admitted that its "smart" missiles missed their targets half the time. Like the one that tore into the entrance to the Amiriyah bomb shelter in Iraq--clearing the way for another "smart" missile to explode inside, incinerating hundreds of people.</P>
<P>Bush proudly declared that the air strikes on Afghanistan would be accompanied by food drops--to help millions of Afghans on the edge of starvation. Except the first day of food drops included only 37,500 packaged meals, far less than the daily needs of even a single large refugee camp.</P>
<P>Meanwhile, the World Food Program and other organizations had to shut down more effective truck convoys that carry hundreds of tons of food. "Air drops make great TV," said Will Day, of the relief agency Care International, "but they often represent a failure to respond to a food crisis."</P>
<P>Before the bombs started to fall, hundreds of thousands of Afghans had fled their homes--in a country that already had one of the largest refugee populations in the world.</P>
<P>"It is a matter of fact that even the threat of military action made the humanitarian situation worse," said Julian Filochowski, director of the Catholic charity CAFOD. "The start of military attacks on Afghanistan, even if limited, will exacerbate problems."</P>
<P>And Bush dares to claim that the U.S. "is a friend to the Afghan people." Bush and Co. don't care about their suffering. Nor do they care about "justice" or "freedom" or any of the other hot air spouted to justify this war.</P>
<P>They want to exploit the tragedy of September 11 as an excuse to promote their agenda of war, repression and U.S. government power around the globe.</P>
<P>Don't let them get away with it. Join the tens of thousands of people who responded to Bush's attack in a matter of hours--and stand up against this cruel war.</P>

