<p><a name="elections"></a><font face="Times New Roman, Times, serif" size="4"><b>Teamsters elections</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Joe Allen, steward, IBT Local 705</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--On October 9, ballots will be sent out to the nearly 1.4 million members of the Teamsters to decide who will lead the union for the next five years. Tom Leedham's Rank and File Power slate is challenging the incumbent General President James P. Hoffa. The ballots will be counted over the next month, and a winner will be announced on November 15.</P>
<P>Leedham is supported by progressive activists in the Teamsters, including reformers in Teamsters for a Democratic Union.</P>
<P>Hoffa represents the most corrupt, backward forces in the union. He is supported overwhelmingly by Teamsters officers, including former supporters of the reform movement.</P>
<P>Despite lopsided support for Hoffa by Teamsters officials and a large campaign war chest, the Leedham camp is optimistic.</P>
<P>Nearly 200 enthusiastic rank-and-file Teamsters from various locals and industries turned out at a Leedham fundraiser on September 30 in Chicago--the largest gathering of reform activists in Chicago in many years.</P>
<P>The disastrous strike against Overnite trucking is the most glaring example of the failure of Hoffa's leadership, along with a series of mediocre contracts.</P>
<P>These, combined with instances of high-level corruption by Hoffa's top aides and his growing alliance with the Republican Party, have alienated many former rank-and-file supporters.</P>
<P>Leedham still has an uphill battle to win the election. Hoffa may be deflated, but he is still a formidable force.</P>

