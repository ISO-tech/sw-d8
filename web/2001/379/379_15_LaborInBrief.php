<p><a name="Safeway"></a><font face="Times New Roman, Times, serif" size="4"><b>Safeway</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Belknap</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Some 28,000 grocery workers at Safeway and Albertson's are set to strike across Northern California on October 8. Workers are represented by eight different locals of the United Food and Commercial Workers.</P>
<P>About 70 percent of the workers are part-time, and the union is fighting to make it 50-50. They also want the weekly minimum number of hours increased to 24 from 20 and raises of between 75 cents and $1.50 per hour each year, with a three-year contract. </P>
<P>Safeway wants to suspend contributions to the pension fund for three years. Safeway has harassed employees and forced them to watch anti-union videos in violation of the National Labor Relations Act. Safeway has further inflamed the situation by suing the union over bogus claims of irregularities in the strike vote.</P>
<P>Safeway is in for a fight. Teamsters Joint Council President Chuck Mack has already pledged his union's solidarity. They will not cross picket lines.</P>
<P>During Safeway workers' strike in 1995, Teamsters refused to cross their picket lines, and the parking lots were empty as customers refused to cross as well.</P>

