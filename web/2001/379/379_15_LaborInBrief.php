<p><a name="Writers"></a><font face="Times New Roman, Times, serif" size="4"><b>National Writers Union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar, NWU member</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE NATIONAL Writers Union (NWU)/UAW Local 1981 is facing the hardest-fought election contest in its history. Challenger Steve Simurda--backed by many longtime leading union members--is trying to unseat NWU President Jonathan Tasini, who has held the position for more than a decade.</P>
<P>Simurda, a veteran freelance writer and teacher, argues that change is needed because Tasini has stifled the development of broader union leadership and has neglected the financial management of the NWU, which led to a $300,000 bookkeeping error in the union's funds.</P>
<P>The fact that Tasini has alienated longtime allies is significant, and the bookkeeping error--while corrected--is a warning sign.</P>
<P>Nevertheless, the NWU has clearly benefited from Tasini's leadership. He was the driving force in this year's victory in the U.S. Supreme Court in a lawsuit against the <I>New York Times</I> that made it illegal for publishers to resell freelance writers' work electronically without permission. Union membership has increased 50 percent to 7,200, and Tasini has worked to build the NWU's relationship with the UAW and the labor movement.</P>
<P>By contrast, Simurda represents the wing of the NWU that argues that writers should act as "professionals." On his Web site, he states that the union's job is "to educate writers, the publishing industry and the public" about why writers should control their work.</P>
<P>This is a major retreat from the goals of the NWU, which formed because writers recognized that they have opposing interests from publishers. Moreover, Simurda cannot be considered a serious candidate when he "campaigns" by sending postcards from his teaching position in Slovenia.</P>
<P>NWU members should vote for Tasini and his NWU Democracy slate to keep the union moving forward.</P>

