<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. Navy out of Vieques!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>VIEQUES, Puerto Rico--On October 4, workers of the Puerto Rican island of Vieques held a three-hour general strike to demand that the U.S. Navy stop bombing their land.</P>
<P>They demanded that the Navy stop its current round of military exercises and that the mayor, D&aacute;maso Serrano, be freed. Serrano is one of two people who remain in jail after entering the Navy's bombing range during the last round of civil disobedience protests.</P>
<P>The strike action was fully supported by unions representing workers at the publicly owned water and energy authorities, where offices were paralyzed. Many other state government offices followed suit, as did the municipal government employees. There were no classes at any of Vieques' public schools.</P>
<P>About 200 people, headed by the mayor's wife, marched to the gates of the Navy's main facilities at Camp Garc&iacute;a. This is an important action because, after the September 11 air attacks in New York and Washington, D.C., the movement to get the Navy out of Vieques lost momentum.</P>
<P>The main groups leading the struggle declared a moratorium on civil disobedience, fearing for the safety of the protesters given the extreme level of alert imposed by the military on all its installations.</P>
<P>However, the October 4 strike clearly signaled that the struggle is not over. The action has served to reignite the movement. The pro-independence Congreso Nacional Hostosiano has scheduled a series of events this month.</P>
<P>The Navy wants to use the air attacks and Bush's war drive to renege on their declared intentions to leave Vieques by 2003.</P>
<P>While Bush prepares for war, supposedly in the name of justice, justice is precisely what the people of Vieques have been denied during 60 years of abuse, expropriation, disease and violence at the hands of the U.S. Navy. </P>
<P>U.S. Navy out of Vieques now!</P>

