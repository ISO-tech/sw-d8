<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Who is the main sponsor of terror?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 12, 2001 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MANY LIBERALS who may have voiced criticism of U.S. foreign policy in the past have enthusiastically signed on to Washington's war in Afghanistan.</P>
<P>The <I>Nation</I> magazine recently came out in favor of "an all-out but carefully targeted effort" against terrorism that "may involve a limited military response." While quite a few <I>Nation</I> writers don't share the editors' views, Christopher Hitchens isn't among them.</P>
<P>In a series of articles, Hitchens denounced critics of Bush's war drive, like Noam Chomsky, Howard Zinn and others.</P>
<P>Hitchens accused anyone who suggested that the attacks might be the unintended consequence of U.S. policies abroad--or that the U.S. government was using the tragedy to advance an agenda that has nothing to do with justice--of "rationalizing" terrorist attacks. Hitchens then proceeds to rationalize Washington's war.</P>
<P>"Did we not aid the grisly Taliban to achieve and hold power?" he asks. "Yes indeed, 'we' did. Well, does this not double or triple our responsibility to remove them from power?" He continues: "This is another but uniquely toxic version of an old story, whereby former clients like Noriega and Saddam Hussein and Slobodan Milosevic and the Taliban cease to be our monsters and become monstrous in their own right."</P>
<P>What Hitchens has done is accept the idea that the "greatest purveyor of violence in the world"--Martin Luther King's 1967 description of the U.S. government--and the government most responsible for supporting "monsters" around the world can be relied on to keep us safe from monsters.</P>
<P>Experience tells us that the U.S. doesn't go to war because it opposes "fascists" or "terrorists" or "monsters." Rather, it labels as "fascist," "terrorist" or "monstrous" whoever it wishes to go to war against.</P>
<P>In the process, as the world's biggest superpower, the U.S. inflicts "state-sponsored" terror that no al-Queda network could possibly match.</P>
<P>A political scientist who visited Iraq after the 1991 Gulf War had this to say: "Air bombardment is state terrorism, the terrorism of the rich. It has burned up and blasted apart more innocents in the past six decades than have all the anti-state terrorists who ever lived. Something has benumbed our consciousness against this reality. In the United States we would not consider for the presidency a man who had once thrown a bomb into a crowded restaurant, but we are happy to elect a man who once dropped bombs from airplanes that destroyed not only restaurants but the buildings that contained them and the neighborhoods that surrounded them."</P>
<P>This is, of course, leaving out the deaths of hundreds of thousands of Iraqis as a result of 11 years of economic sanctions imposed on Iraq by the U.S. and UN.</P>
<P>U.S. bombardment of Korea, Vietnam, Cambodia, Laos, Panama and Iraq has killed and maimed literally millions of people over the decades. The CIA has fomented coups and assassinations in Iran, Chile, Guatemala and elsewhere to put into power "friendly" dictators or to remove "unfriendly" ones. The U.S. has sponsored terrorism and death squads in Cuba, Iraq, Central America and now Colombia in order to defend unpopular regimes and destabilize popular ones.</P>
<P>All of this is very well documented, and Hitchens is aware of it. But that hasn't stopped him from asking that we entrust the government responsible for these mass terrorist atrocities to eliminate the "terrorist threat"--even though he admits that what concerns the U.S. isn't that its enemies are "monstrous," but that they have become "monstrous in their own right," i.e., monsters not under U.S. control.</P>
<P>With solutions like this, who needs problems?</P>
<P>In his speech announcing the beginning of U.S. bombing in Afghanistan, George W. Bush stated that governments that sponsor "outlaws and murderers" become "outlaws and murderers themselves."</P>
<P>Hitchens, in his pro-war zeal, has rightly explained that the U.S. indeed sponsors "outlaws and murderers." Why then does he not turn his criticisms against the hypocrisy of Bush and the government that he represents?</P>
<P>Instead, he's giving them cover.</P>

