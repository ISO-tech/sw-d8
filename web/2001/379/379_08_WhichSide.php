<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. government's double standards</b></font></P>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 12, 2001 | Pages 8 and 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE THE September 11 attacks, the looming question is how to prevent such terrorist acts in the future.</P>
<P>George W. Bush has put forward a straightforward explanation and a simple solution. The attacks represent a clash between "good" and "evil," he says--between the "civilized world" and "terrorism."</P>
<P>Bush's solution is an all-out war to bring the terrorists to "justice," in which all countries must decide if they are "with the U.S. or with the terrorists."</P>
<P>Even many people who oppose an all-out war accept Bush's call to bring the terrorists to "justice." But what is the definition of "terrorism"--and who should be entrusted to deliver justice?</P>
<P>According to the dictionary, terrorism is "a mode of governing, or opposing government, by intimidation." Yet the U.S. government employs a convenient double standard, by explicitly excluding state-sponsored terrorism from its definition--an approach echoed by its compliant media.</P>
<P>The <I>Wall Street Journal</I>, for example, instructs its staff that the word "terrorist" "should be used carefully, and specifically, to describe those people and non-governmental organizations that plan and execute acts of violence against civilian or noncombatant targets."</P>
<P>Were the dictionary definition of terrorism used, the U.S. would be the last choice to dish out justice against terrorists. In fact, the gruesome results of the September 11 attacks pale in comparison to the bloody horrors meted out by the U.S. military all over the world--from Hiroshima and Nagasaki to Iraq to the Sudan to Yugoslavia.</P>
<P>The U.S. military was responsible for the deaths of 3 million Vietnamese, Laotians and Cambodians during the Vietnam War alone. And this doesn't include its sponsorship of terrorism by other states--from the Latin American death squads trained at the CIA's School of the Americas in Fort Benning, Ga., to the Israeli soldiers mowing down Palestinians with U.S. weapons.</P>
<P>The charge of terrorism is conveniently--and hypocritically--aimed by the U.S. government toward its enemies of the moment. As author Edward Herman wrote recently, "U.S. support of the Colombian army (and indirectly, its paramilitaries) is not sponsoring terrorism, despite the thousands killed and scores of thousands displaced each year, because we cannot sponsor terrorism by definition."</P>
<P>This double standard carries over into the realm of international law, where the U.S., as the world's main superpower, has decisive influence. In fact, the U.S. government has refused to ratify a UN call for a permanent International Criminal Court--unless there are guarantees of immunity for U.S. personnel.</P>
<P>When the U.S. carpet bombed Iraq in 1991, killing 200,000 people, it also intentionally destroyed Iraq's water sanitation systems--a violation of the Geneva Convention. The U.S.'s crippling sanctions against Iraq since the war have killed well over 1 million Iraqis--the majority of them under the age of five. </P>
<P>But despite the barbarism that is the norm for U.S. imperialism, no American official has ever been brought before an international court. And the U.S. also shields its allies from being punished under international law, however flagrant their violations.</P>
<P>Israel has faced no consequences for flouting UN resolutions calling on Israel to withdraw from Arab lands occupied in the 1967 war. International law also prohibits Israel from settling civilians on occupied land. Yet more than 200,000 Jewish settlers have done just this.</P>
<P>Those who rightfully reject an all-out war in Afghanistan but seek justice through an international tribunal of some kind must face the fact that no international body exists to impartially dispense justice.</P>
<P>There is only one way to effectively combat terrorism of the sort behind the September 11 attacks, which is fed by bitterness and resentment toward U.S. foreign policy. Like Israel, the U.S. could sharply reduce terrorism by tempering its own thirst for blood.</P>

