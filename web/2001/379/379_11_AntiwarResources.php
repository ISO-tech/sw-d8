<P><font face="Times New Roman, Times, serif" size="5"><b>Resources for antiwar activists</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><B><I><P>SOCIALIST WORKER</I> writers recommend books on the history of the Middle East and Central Asia--and the U.S. military's bloody record of wreaking destruction around the globe.</P></B>
<font face="Times New Roman, Times, serif" size="3"><P>Peter Marsden, <B><I>The Taliban: War, Religion and the New Order in Afghanistan</B></I> (Zed Books, 1998, 162 pages, $19.95)</P>
<P>THIS BOOK begins with a useful history of Afghanistan and the role that Islam and ethnicity have played historically.</P>
<P>Peter Marsden shows that the rise of the Taliban wasn't the result of some blind religious impulse, but was a movement based on specific mujahideen guerrilla factions that won sponsorship from Pakistan and were financed by Saudi Arabia.</P>
<P>Marsden also examines the role of regional politics, showing how the U.S. oil company UNOCAL, Delta Oil of Saudi Arabia and the government of Turkmenistan planned an oil pipeline through Afghanistan in 1997--with the backing of the Taliban.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>John Cooley, <B><I>Unholy Wars: Afghanistan, America and International Terrorism</B></I> (Pluto Press, 2000, 299 pages, $19.95)</P>
<P>FOR MOST journalists in the U.S., Washington's role in arming Osama bin Laden is a footnote. In this book, John Cooley, an ABC News correspondent based in Athens, details the real story. </P>
<P>The author of several books on the Middle East, Cooley chronicles how, following the Russian invasion of Afghanistan, Presidents Jimmy Carter and Ronald Reagan stepped up U.S. aid to new levels. </P>
<P>But Cooley accepts at face value more recent claims of the U.S. government about bin Laden and his "holy warriors"--even though he exposes Washington's lies about Afghanistan in previous pages.</P>
<P>Nevertheless, the book does show how Osama bin Laden's rise was made possible by Washington.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Chalmers Johnson, <B><I>Blowback: The Costs and Consequences of American Empire</B></I> (Owl Books, 2001, 268 pages, $15)</P>
<P>CHALMERS JOHNSON'S book focuses mainly on the impact of U.S. policy in Asia, but the point he makes is more universal.</P>
<P>A term first used by the CIA, "blowback" refers to the unintended repercussions of U.S. arrogance.</P>
<P>Though written before September 11, parts of Johnson's book are prophetic. "The innocent of the 21st century are going to harvest unexpected blowback disasters from the imperialist escapades of recent decades," Johnson wrote.</P>
<P>"Although most Americans may be largely ignorant of what was, and still is, being done in their names, all are likely to pay a steep price&#133;for their nation's continued efforts to dominate the global scene."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Noam Chomsky, <B><I>Rogue States: The Rule of Force in World Affairs</B></I> (South End Press, 2000, 264 pages, $16)</P>
<P>"ROGUE NATIONS must be held accountable" is the refrain used by U.S. politicians to rationalize military interventions around the globe.</P>
<P>In this book, Noam Chomsky holds the U.S. and other superpowers to their own professed standards--as they commit indefensible actions in the name of democracy and human rights.</P>
<P>Chomsky examines the history of U.S. involvement in the Middle East, Southeast Asia and Central America.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><I><P>Eqbal Ahmad: Confronting Empire,</B></I> interviews with David Barsamian (South End Press, 2000, 204 pages, $16)</P>
<P>THIS BOOK puts together several interviews with anti-imperialist activist and scholar Eqbal Ahmad, who died two years ago.</P>
<P>Ahmad was born in India in 1932. As a teenager, he and his family journeyed to Pakistan during partition. Later in the U.S., he became an activist in the civil rights movement of the 1960s and a supporter of the Vietnamese struggle against U.S. imperialism.</P>
<P>In this collection, Ahmad discusses nationalism, ethnic conflict, the nuclear standoff between India and Pakistan, imperialism, and liberation struggles around the world.</P>
<P>Edward Said provides the introduction.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>William Blum, <B><I>Killing Hope: U.S. Military and CIA Interventions Since World War II</B></I> (Common Courage Press, 2001, revised edition, 460 pages, $22.95)</P>
<P>FROM CHINA in the 1940s to Iraq today, William Blum goes through the U.S. government's record of military intervention abroad.</P>
<P>Blum writes in his introduction: "It was in the early days of the fighting in Vietnam that a Vietcong officer said to his American prisoner: 'You were our heroes after the War&#133;a common phrase in those days was to be as rich and as wise as an American. What happened?'</P>
<P>"An American might have been asked something similar by a Guatemalan, an Indonesian or a Cuban during the 10 years previous, or by a Uruguayan, a Chilean or a Greek in the decade subsequent."</p>

