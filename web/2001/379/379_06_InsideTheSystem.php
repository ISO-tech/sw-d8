<font face="Times New Roman, Times, serif" size="3"><P>ULTRA RIGHT-wing Italian Prime Minister Silvio Berlusconi has his own theories about the cause of the September 11 terrorist attacks. To hear him tell it, it's a problem with "inferior" cultures.</P>
<P>"We must be aware of the superiority of our civilization, a system that has guaranteed well-being, respect for human rights and--in contrast with Islamic countries--respect for religious and political rights, a system that has as its values understanding of diversity and tolerance," he said during a press conference in Berlin.</P>
<P>"The West will continue to conquer peoples, like it conquered Communism," he continued on, even if it means a confrontation with "another civilization, the Islamic one, stuck where it was 1,400 years ago." </P>
