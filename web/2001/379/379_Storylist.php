<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>THE WAR AT HOME</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Bush White House plays on fears to gain draconian new powers</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_02_WarOnRights.shtml">The war on our rights</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Hundreds of Arab Americans and foreign nationals were detained in the wake of the September 11 attacks, though most have been cleared of any connection to the suspected hijackers. But if Attorney General John Ashcroft gets his way, there will be more victims.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_02_StiflingDissent.shtml">Stifling dissent on campuses</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Right-wingers are using war fever to clamp down on dissent on college campuses. At City College of New York (CCNY), media pundits and school officials joined forces for an old-fashioned witch-hunt.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Right-wingers take aim at all dissent from the pro-war chorus</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_11_MediaLapdogs.shtml">Wagging the media lapdogs</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">In the weeks since the September 11 attacks in New York and Washington, D.C., right-wingers have gone on the offensive against critics of George W. Bush--and, in fact, any dissent from the pro-war chorus in the mainstream media.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_05_CloseTheSOA.shtml">Close the Army's school for terror</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">One well-documented breeding ground for terrorists isn't likely to become a target in the U.S. "war against terrorism": the School of the Americas in Fort Benning, Ga.</P>

