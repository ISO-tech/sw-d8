<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>THE STRUGGLE AGAINST WAR</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b><I>SW</I> SPECIAL FEATURE</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_Center.shtml">Can the U.S. bring justice?</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">George W. Bush says that his war on Afghanistan is about "justice." In this special feature, Howard Zinn, Ahmed Shawki and Sharon Smith explain why Bush's claim is a lie.</p>

<P><font face="Times New Roman, Times, serif" size="3"><b>The American flag:</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_05_Flag.shtml">"Symbol of a monstrous lie"</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The American flag is a symbol of injustice and oppression. Those of us opposed to Washington's war must march under a different banner--one that stands for international solidarity and justice.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>U.S. lies that justified the Gulf War</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_12_GulfWar.shtml">Operation Desert Slaughter</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">George W. Bush says that he wants to bomb Afghanistan in the name of "freedom and democracy." His father said the same thing when he went to war against Iraq in the 1991 Persian Gulf War.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_12_Hitchens.shtml">Who is the main sponsor of terror?</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Many liberals who may have voiced criticism of U.S. foreign policy in the past have enthusiastically signed on to Washington's war in Afghanistan.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_11_AntiwarResources.shtml">Resources for antiwar activists</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3"><I>Socialist Worker</I> writers recommend books on the history of the Middle East and Central Asia--and the U.S. military's bloody record of wreaking destruction around the globe.</P>

