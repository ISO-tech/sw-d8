<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>FRONT AND BACK PAGES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_01_StopTheWar.shtml">Stop this war!</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The U.S. has gone to war against one of the poorest countries in the world. George W. Bush dares to claim that the U.S. "is a friend to the Afghan people." But Washington doesn't care about their suffering--or about "justice" or "freedom" or any of the other rhetoric used to justify this cruel war.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Thousands rally against the war</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_16_AntiwarMovement.shtml">"We won't be silenced"</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">"The only way they're going to hear us is in the streets. No to war! Yes to peace!" That was the message that Lucas Benitez had for the crowd of some 1,000 Chicago protesters who gathered--as did people in cities around the country--just hours after George W. Bush began raining bombs on Afghanistan.</P>

<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>WHAT WE THINK</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Truth is the first casualty in Bush's war on Afghanistan</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_03_USWarAims.shtml">The real U.S. war aims</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">U.S. bombs had barely begun falling on Afghanistan when George W. Bush announced that "the war against terrorism" could soon target other countries.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_03_SpreadResistance.shtml">Spread the message of resistance</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">As the U.S. government's military assault on Afghanistan began, opinion polls showed wide support for bombing. But the mainstream media's enthusiasm for U.S. military might hid the fact that significant numbers reacted to the war by speaking out against it.</P>

