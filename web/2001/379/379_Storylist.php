<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>THE WAR ON AFGHANISTAN</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>What is the Northern Alliance?</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_07_USNewBuddies.shtml">Bush's new best buddies in Afghanistan</A></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The politicians and their media mouthpieces are now painting the Northern Alliance as heroic and battle-hardened opponents of the Taliban's repressive regime. But the truth is a little different.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="379_07_RoguesGallery.shtml">A rogues' gallery of allies for Bush's war</A></b></font><br>
