<P><font face="Times New Roman, Times, serif" size="4">What is the Northern Alliance?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's new best buddies in Afghanistan</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>ERIC RUDER reports on the U.S. government's allies in Afghanistan and around the globe.</P>
</B><P>THE U.S. government is out to get Osama bin Laden and topple Afghanistan's Taliban regime for "sheltering terrorism." But it had to scramble to come up with an Afghan opposition to the Taliban.</P>
<P>The politicians and their media mouthpieces are now painting the Northern Alliance as heroic and battle-hardened opponents of the Taliban's repressive regime. But the truth is a little different.</P>
<P>The Northern Alliance is a quarreling bunch of warlords and clan leaders who are cut from the same cloth as the Taliban. "When I was in Kabul last year, I was told time and again that the only thing people there feared more than the Taliban were the warlords of the Northern Alliance," Patricia Gossman, an expert on human rights in South Asia, wrote in the <I>Washington Post</I>.</P>
<P>In fact, leaders of the Northern Alliance ruled Afghanistan before the Taliban replaced them in 1996. Their reign was so chaotic and violent that the Taliban was able to sweep across the country and take power in a period of months.</P>
<P>"From 1992 to 1995, fighting among the factions of the alliance reduced a third of Kabul to rubble and killed more than 50,000 civilians," Gossman wrote. "The top commanders ordered massacres of rival ethnic groups, and their troops engaged in mass rape."</P>
<P>The 1992-96 regime was called the Islamic State of Afghanistan (ISA). The ISA took power shortly after the USSR retreated from its military occupation of the country.</P>
<P>After Russia's withdrawal, U.S.-backed Islamist rebels splintered into rival factions, leading to civil war. The ISA was the faction that emerged on top, toppling the pro-USSR government in Kabul in 1992.</P>
<P>During the group's four years in power, there was "no rule of law" in areas it controlled, according to a July report by Human Rights Watch. As they warred with one another, each faction terrorized the local population in the areas it controlled, using summary executions, arbitrary arrests, torture and "disappearances."</P>
<P>In 1995, one faction captured a Kabul neighborhood that had been a former stronghold of another. "[T]roops went on a rampage, systematically looting whole streets and raping women," the U.S. State Department said of the incident.</P>
<P>Now the Northern Alliance has become the U.S.'s on-the-ground ally in the war on the Taliban--in a repeat of the "enemy-of-my-enemy-is-my-friend" logic that helped to propel repressive Islamist regimes like the Taliban to power in the first place.</P>
<P>"[I]t was the CIA's former allies in the anti-Soviet war who subsequently destroyed the country, and our onetime friends have been responsible for many major terrorist attacks of recent years," journalist Ken Silverstein pointed out in <I>Salon</I> magazine.</P>
<P>The U.S. is now set to replay this bloody game. "U.S. officials--who know full well the whole bloody, rapacious track record of the killers in the 'Alliance'--are suggesting in good faith that these are the men who will help us bring democracy to Afghanistan and drive the Taliban and the terrorists out of the country," Robert Fisk wrote in Britain's <I>Independent</I> newspaper.</P>
<P>"In fact, we're ready to hire one gang of terrorists--our terrorists--to rid ourselves of another gang of terrorists. What, I wonder, would the dead of New York and Washington think of this?"<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Will the U.S. back a senile monarch?</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government seems to think that there's nothing like a king to lead a war "against terrorism." So it's toying with backing former king Mohammed Zahir Shah.</P>
<P>Zahir last laid eyes on "his country" in 1973, when his corrupt regime was overthrown in a coup organized by the king's cousin Daud. Ever since, he's lived a quiet existence in his villa in a suburb of Rome.</P>
<P>The U.S. likes the king because, unlike leaders of the Northern Alliance, he's Pashtun --a dominant ethnic group that any new government would need support from. Plus, Zahir is from the powerful Durrani clan that ruled Afghanistan for more than 200 years.</P>
<P>Problem: The 86-year-old king is a bit senile now. Three decades in exile may have left Zahir a little out of touch with ordinary Afghans--not to mention reality.</P>
<P>Still, says the <I>Wall Street Journal,</I> he has a good sense of humor. And, the paper insists, "Monarchy and democracy can be symbiotic."</P>
<P>Huh?</P>

