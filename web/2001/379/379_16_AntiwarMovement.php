<P><font face="Times New Roman, Times, serif" size="4">Thousands rally against the war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We won't be silenced"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the growing opposition to Bush's war, based on reports from <I>Socialist Worker</I> readers around the country.</P>
</B><P>"THE ONLY way they're going to hear us is in the streets. No to war! Yes to peace!" That was the message that Lucas Benitez had for the crowd of Chicago protesters who gathered just hours after George W. Bush began raining bombs on Afghanistan.</P>
<P>Benitez was one of some 1,000 people who took to the streets in Chicago--as thousands more were marching in other cities across the country.</P>
<P>In New York, where nearly 5,000 people died in the attack on the World Trade Center, an inspiring 10,000 people turned out for an emergency protest organized by New York Not in Our Name, a recently formed coalition of more than 100 organizations. </P>
<P>Protesters gathered at Union Square Park--the site of many tributes and shrines to those who died in the World Trade Center nearby--then marched uptown, chanting, "Peace, salaam, shalom." </P>
<P>Reuben Schafer spoke to the crowd about his grandson, Gregory Rodriguez, who died in the attack.</P>
<P>Then he read a letter to Bush from Gregory's parents.</P>
<P>"Your response to the attack does not make us feel better about our son's death," the letter read. "It makes us feel worse. It makes us feel our government is using our son's memory as justification to cause suffering for other sons and parents in other lands."</P>
<P>In San Francisco, activists from the Town Hall Committee to Stop War and Hate had already planned a teach-in featuring journalist Alexander Cockburn and Medea Benjamin of Global Exchange.</P>
<P>As some 2,000 people marched through the Mission District on a separately called emergency reponse protest, about 1,500 turned out for the teach-in. "This is a great start tonight, but we need to reach out to ever-wider layers of people," Benjamin told the crowd. "We all need to become antiwar organizers."</P>
<P>The next day, thousands of students at University of California-Berkeley walked out of classes in a protest against the war, and a late-afternoon demonstration in Berkeley drew more than 2,000.</P>
<P>Antiwar activity began well before the bombs started dropping. Protests drew hundreds, not only in major cities, but smaller ones--from Burlington, Vt., to Olympia, Wash., and from Geneseo, N.Y., to Austin, Texas.</P>
<P>Hundreds came out to teach-ins and forums as well. In Boston, for example, some 400 people packed an auditorium to overflowing on September 25 to hear a panel discussion featuring historian Howard Zinn, Middle East Program Coordinator Souad Dajani, and International Socialist Organization member Anthony Arnove.</P>
<P>"We've seen meetings like this one grow to become great national movements in the past," Zinn told the crowd. "We need another one today. And we may be on the way to one."</P>
<P>The largest shows of opposition came on September 29-30, as activists across the country took to the streets for a coordinated weekend of protests.</P>
<P>In San Francisco, nearly 10,000 turned out for a September 29 protest. "As Arabs and Muslims get kicked off airplanes, as our homes are vandalized, as our children are terrorized, we stand in fierce solidarity with the African Americans who suffered and continue to suffer through the ugly history of racism in this country," Arab American activist Eman Desouky told the crowd.</P>
<P>More than 1,000 antiwar protesters marched in Atlanta, chanting "Peace, not war!" and 2,000 turned out in Los Angeles to demonstrate under the slogan "Don't turn tragedy into war." In Seattle, 1,500 marched through the streets chanting "No racism, no hate, no terrorism by the state!"</P>
<P>In Washington, D.C.--where global justice activists had been planning to demonstrate against an IMF and World Bank meeting that was canceled after the September 11 attacks--some 8,000 marched on Saturday and another 3,000 came out on Sunday. </P>
<P>"This is democracy in the streets," said Rachel Ettling, who traveled from New York City to attend.</P>
<P>Speakers urged protesters to step up their activism. "Even our friends have said this is not a time to speak," said Mara Verheyden-Hillard of the Partnership for Civil Justice at Sunday's demonstration. "But we will not be silenced."<br>
<br>
<font face="Times New Roman, Times, serif" size="5">Students unite to fight Bush's war</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>COLLEGE STUDENTS have been at the forefront of the opposition to Bush's war. </P>
<P>At Wesleyan University in Connecticut, students formed Peaceful Justice, a nationwide student antiwar network, days after the September 11 attacks. After coordinating a National Student Day of Action September 20 with actions on more than 140 campuses, the group called an East Coast organizing conference.</P>
<P>More than 150 people gathered at Wesleyan on September 29-30 for the meeting. Attendees represented more than 20 campuses in Massachusetts, Connecticut and Rhode Island.</P>
<P>On the West Coast, representatives from nearly 20 California campuses met in Berkeley September 29 to form California Schools Against the War (C-SAW).</P>
<P>In the wake of Sen. Dianne Feinstein's (D-Calif.) recent call for a six-month ban on student visas, C-SAW has made protecting the rights of foreign students a priority. The network is calling for a coordinated Campus Day of Action October 11 that will focus especially on protecting student visas and civil rights.</P>
<P>C-SAW has also called a major antiwar conference for November 10-11.</P>

