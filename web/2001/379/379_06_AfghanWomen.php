<P><font face="Times New Roman, Times, serif" size="4">Letter from a <I>Socialist Worker</I> reader</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will U.S. bombs help Afghan women?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 12, 2001 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>NANCY WELCH, an associate professor at the University of Vermont and member of the International Socialist Organization, wrote this as an op-ed article for the UVM newspaper. Here, <I>Socialist Worker</I> prints excerpts.</P>
</B><P>"BUT YOU'RE a feminist," a friend said to me the other day. "You must want the U.S. to take charge of Afghanistan."</P>
<P>The despotic abuse of women by Afghanistan's Taliban regime is well-known and well-documented. They have suffered beatings, disfigurement and murder for such "crimes" as speaking in public. I'm beyond stunned when I read such reports. I find that a response of "I condemn this" isn't nearly strong enough.</P>
<P>Yet I don't support a U.S.-directed military strike against this country. I don't support a covert operation aimed at eliminating the Taliban. </P>
<P>I see no evidence that bombing a country is good for women. And I see no evidence that a covert operation--such as further aid and arms for the Northern Alliance rebel factions--will be good for women either. Instead, I find plenty of evidence that the opposite is true. </P>
<P>Consider the conservative UN estimate that more than 1 million Iraqi men, women and children have died as a result of U.S. bombs and economic sanctions since 1990--actions against Iraq that have only strengthened the standing of its leader. </P>
<P>Consider the systematic abuse of Afghanwomen by the U.S.-championed Northern Alliance. Those abuses--which include rape, the shelling of residential neighborhoods, the use of land mines, restrictions on women's education and actions, and the rebel factions' full participation in the murderous civil war of the early 1990s that left more than 50,000 civilians dead--have been documented by Human Rights Watch, Amnesty International and even the U.S. State Department. </P>
<P>The evidence is clear: U.S. military and economic actions kill women. U.S.-supported "freedom fighters" do, too.</P>
<P>But there's more to my friend's statement than just confusion as to whether war can be good for women. This friend also assumes that our government has women's welfare in mind. Again, the evidence shows otherwise. </P>
<P>Despite the Taliban's open and brutal subjugation of women, the U.S. enthusiastically supported this government through 1998. The U.S. now praises the Northern Alliance factions despite their equal disregard for women and--contradiction of contradictions--despite their ties to international terrorist networks. </P>
<P>The U.S. has also been waging war (there's simply no other way to put it) against its own poor and working-class women with the repeal of welfare and food, health, housing and education assistance programs. So, no, I can't accept the false logic that says the Taliban is bad for women, and therefore, the United States should wage war against them.</P>
<P>I'm not saying that we--you and I--should do nothing at all. We can urge our government to stick to dropping food, massive amounts of it, on drought-stricken Afghanistan. (The idea that we need to bomb the Afghan people first to make it safe to feed them is another bit of bizarre logic that must be abandoned.) </P>
<P>We can also hold the U.S. government, its allies and its agents--including the World Bank and World Trade Organization--accountable for their lack of regard for human rights. </P>
<P>We can tell our government to drop its deadly sanctions against Iraq--and while we're at it, Cuba, too. </P>
<P>We can tell the media to stop exploiting images of shrouded Afghan women and start listening to their voices instead.</P>
<P>And maybe especially, we can come together to call on the U.S. government, its allies and its agents to stop sending arms to the warring factions in Afghanistan, including the Northern Alliance. These so-called freedom fighters are not fighting for the freedom of women.</P>

