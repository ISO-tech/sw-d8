<P><font face="Times New Roman, Times, serif" size="5"><b>Who's leading the war drive?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 6</font></P>

<p><a name="Dick"></a><font face="Times New Roman, Times, serif" size="4"><b>Dick Cheney</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">CHENEY HAS plenty of experience in gearing the U.S. up for war. After all, he was Papa Bush's defense secretary in the run-up to and during the 1991 Gulf War in Iraq.</P>
<P>Cheney reveled in Operation Desert Slaughter. According to one congressional staffer, his briefings to Congress were "a horror show. Cheney tended to dwell on the gruesome efficiency of the bombing campaign and [hyped] the smart-bomb technology with piles of data that later proved bogus."</P>
<P>After being run out of Washington with his boss in the 1992 election, Cheney became CEO of Halliburton, the world's largest oil services company. Halliburton made a killing on contracts in Kuwait after the Gulf War. </P>
<P>And thanks to the Cheney's Pentagon contacts, the company raked in billions in military contracts. Plenty of that trickled up into Cheney's pockets.<br>
<br>
<p><a name="Colin"></a><font face="Times New Roman, Times, serif" size="4"><b>Colin Powell</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">COLIN POWELL has been called the "shining star" of Bush's cabinet. But the "heroic" leader of Operation Desert Storm has a record that's tarnished with a string of U.S. war crimes. </P>
<P>During the Vietnam War, Powell helped in the cover-up of the 1969 My Lai massacre, in which U.S. troops slaughtered some 400 Vietnamese villagers.</P>
<P>In 1989, Powell oversaw the invasion of Panama. An estimated 7,000 Panamanians died during the operation to bring down Manuel Noriega, a dictator formerly backed by the CIA.</P>
<P>Powell headed the Joint Chiefs of Staff during the Gulf War. He explained to reporters his plan of attack. "First, we're going to cut it off; then, we're going to kill it."<br>
<br>
<p><a name="John"></a><font face="Times New Roman, Times, serif" size="4"><b>John Ashcroft</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">HERE'S A man who'll have no objection to carrying out George W. Bush's planned attack on civil liberties.</P>
<P>Ashcroft holds extreme right-wing views on everything from abortion rights to affirmative action to civil rights for minorities and gays and lesbians. As the <I>St. Louis Post Dispatch</I> editorialized, "Mr. Ashcroft has made a career out of opposing school desegregation in St. Louis and opposing African Americans for public office."</P>
<P>An open admirer of the Confederacy, Ashcroft earlier this year signed off on a Justice Department report that claimed the federal death penalty system isn't racist--despite the fact that 80 percent of defendants in federal capital cases from 1995 to 2000 were minorities.<br>
<br>
<p><a name="Donald"></a><font face="Times New Roman, Times, serif" size="4"><b>Donald Rumsfeld</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">"RUMMY," AS Donald Rumsfeld is known to his buddies, never met a weapons system he didn't like. As defense secretary under Gerald Ford, Rumsfeld helped to resuscitate the Pentagon after the U.S. defeat in Vietnam--and backed any proposal for new military spending he could find, including the B-1 bomber, which was so unreliable that the Air Force decided not to use it.</P>
<P>As a corporate executive, Rumsfeld made a name for himself as a tough cost cutter, slashing the workforce at G.D. Searle and the technology firm General Instrument.</P>
<P>Rumsfeld is in charge of pushing through the Bush gang's insane Star Wars missile defense system--a $60 billion waste of taxpayers' money.</P>

