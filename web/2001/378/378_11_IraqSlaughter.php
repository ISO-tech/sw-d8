<P><font face="Times New Roman, Times, serif" size="5"><b>America's hidden slaughter in Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE SEPTEMBER 11 attacks took thousands of innocent lives--a horrific act of violence by any account.</P>
<P>But United Nations (UN) sanctions against Iraq--kept in place since the 1991 Gulf War at the insistence of the U.S. government--take the lives of 5,000 children every month.</P>
<P>Not only do U.S. officials tolerate this "senseless loss of life" inflicted on the Iraqi population. They openly defend it.</P>
<P>As then-Secretary of State Madeleine Albright told a "60 Minutes" correspondent in 1995, "the price" of 500,000 dead Iraqi children--the number of children killed by sanctions up to that time--"we think, is worth it." </P>
<P>During the last decade, the U.S.'s hidden war against Iraq has taken the lives of more than 1 million Iraqis--perhaps as many as 1.5 million.</P>
<P>U.S. officials blame this genocide on Saddam Hussein, claiming that his government withholds food and medicine for its own use. But Denis Halliday and Hans von Sponeck, two former directors of the UN oil-for-food program, resigned in protest against the sanctions. </P>
<P>Both say that the problem lies with the UN embargo and the complete inadequacy of the oil-for-food program, not the Iraqi government. "Oil for food is a stranglehold," according to Halliday, who resigned in fall 1998 after 34 years of working in the UN system. "It's just keeping 22 million people in a refugee camp."</P>

