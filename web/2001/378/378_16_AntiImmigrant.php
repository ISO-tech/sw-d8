<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's attack on immigrants</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"LOCK THEM up and throw away the key" best describes Attorney General John Ashcroft's new approach to immigrants.</P>
<P>On September 18, Ashcroft pushed through new rules allowing the Immigration and Naturalization Service (INS) to detain suspected illegal immigrants for 48 hours before deciding whether to charge them.</P>
<P>The INS has already detained more than 100 people in connection with the investigation into the air attacks in New York and Washington.</P>
<P>But this isn't enough for Ashcroft. Among the new rules in his anti-terrorism proposal put forward to Congress last week is a provision that would allow indefinite detention of foreign "suspects"--and possible deportation.</P>
<P>This would apply to immigrants and foreign visitors who entered the U.S. legally but are suspected of committing crimes in the U.S., overstaying their visas or violating other immigration rules. It also applies to permanent legal residents with "green cards."</P>
<P>And as if the 1996 Anti-Terrorism Act wasn't punitive enough, the new legislation allows for expanded use of "secret evidence" against immigrants or foreign nationals.</P>
<P>Ashcroft and his buddies in Washington are using the anti-terrorist hysteria to chop away at the meager rights that immigrants have in the U.S.</P>
<P>"This proposed legislation is basically making a doormat of the Constitution," said Mike Maggio, an immigration lawyer. "It would permit the INS to serve as prosecutor, judge and jury with no judicial review."</P>
<P>We have to mobilize to fight these attacks on immigrants--and on all of us.</P>

