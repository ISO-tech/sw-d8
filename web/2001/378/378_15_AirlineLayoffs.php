<P><font face="Times New Roman, Times, serif" size="4">Massive airline layoffs in wake of September 11 tragedy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sacrifice for some</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE $15 billion bailout for the airline industry passed by Congress in September will protect shareholders but leave 100,000 laid-off workers in the dust.</P>
<P>"Members of Congress appear poised to stiff airline industry workers in the bailout bill--even as they award protections to airline executives for their golden parachutes," AFL-CIO President John Sweeney said.</P>
<P>Industry executives testifying before Congress claimed their companies urgently needed the money following the September 11 attack on New York and Washington. They claimed that they were willing to "sacrifice," too--so the bill requires airline executives who make more than $300,000 to take a wage freeze this year and next, and limit the value of severance packages to "only" two years' pay.</P>
<P>By contrast, most of the workers who are laid off will collect unemployment insurance for 26 weeks.</P>
<P>Senate Democrats gave lip service to labor's criticisms of the bill--but then voted for it unanimously. "Any member of either party who casts a vote to leave workers out of the relief bill will be voting to betray American workers and the bipartisan spirit of the last week," Sweeney said.</P>
<P>Some promised to sponsor separate bills to aid workers--later. Only one senator--a conservative Republican--voted against the bailout.</P>
<P>The truth is that the airline industry was in dire straits already. "The industry is potentially headed toward its largest loss year ever, and there is absolutely no evidence of fundamental improvement on the horizon," said Samuel Buttrick, an analyst at the investment firm UBS Warburg--the day before the air attacks.</P>
<P>US Airways, for example, was already talking of bankruptcy. And the biggest airlines, United, American and Delta--were already looking for ways to cut costs after being forced to concede significant increases in wages and benefits.</P>
<P>United announced job cuts of 20,000, with Delta expected to follow suit as <I>Socialist Worker</I> went to press.</P>
<P>American Airlines promised that no one would be laid off as the result of its takeover of TWA in order to win union support for the deal. But American seized the opportunity to announce 20,000 job cuts following the air attacks.</P>
<P>"Congress cannot leave out the heroes. It's sad how quickly those who sacrifice to make our great country work, even in these times of tragedy, get left out when corporations go asking for taxpayer money," said Patricia Friend, president of the Association of Flight Attendants. "In announcing layoffs, many in airline management are refusing to follow the contracts they have negotiated, which outline methods for furloughs and layoffs. The tactics of these airlines are un-American. Their greed and antipathy towards workers are needlessly going to hurt thousands more working families."</P>

