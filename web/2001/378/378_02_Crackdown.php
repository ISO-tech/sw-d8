<P><font face="Times New Roman, Times, serif" size="4">Politicians take aim at our civil liberties</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The rights they want to take away</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON looks at Washington's planned assault on civil liberties.</P></B>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH claims that the U.S. was targeted for the September 11 attacks because those responsible "hate our freedoms."</P>
<P>Doesn't make much sense when you think about it. But just to be certain, the Bush gang wants to be sure that there isn't anything left to hate.</P>
<P>White House officials are planning sweeping new attacks on civil liberties as part of their "war on terrorism"--and almost every politician in Washington, Democrat and Republican, is going along for the ride.</P>
<P>Numerous restrictions on the power of government officials are up for grabs--from rules on wiretaps and searches to unlimited detentions during "emergencies."</P>
<P>It used to be that only right-wing kooks talked about this stuff. But now the political mainstream has latched onto the idea that the U.S. was vulnerable to attack because we have too many civil liberties.</P>
<P>Bush even invented a new Cabinet-level position--head of the Office of Homeland Security, a name that could have come from George Orwell's <I>1984</I>.</P>
<P>And the man he's picked to run it? Pennsylvania Gov. Tom Ridge, who has a long history of trampling on civil rights.</P>
<P>Ridge led the drive to speed up executions in Pennsylvania--signing a total of 214 execution warrants in less than seven years, two-thirds of them for African American men.  And during last year's Republican National Convention, he backed Philadelphia police who arrested hundreds of protesters on fake charges and held them in jail for days.</P>
<P>With that kind of record, Ridge will fit right in with Attorney General John Ashcroft. Almost immediately after the attacks, Ashcroft told the Immigration and Naturalization Service to extend the amount of time that immigrants can be held without being charged with a crime from 24 to 48 hours--or "an additional reasonable time, if necessary, under an emergency, or in other extraordinary circumstances."</P>
<P>For the more than 100 foreign nationals and immigrants "detained" while the attacks are investigated, this means that they can be held in jail--on no evidence at all--indefinitely!</P>
<P>Ashcroft even proposed that his Justice Department should be allowed to lock up immigrant suspects and deport them without evidence--though the provision was dropped.</P>
<P>Immigrants--legal and illegal--will be the main victims of the administration's crackdown. But the Bush gang has its sights set on everyone.</P>
<P>Ashcroft wants to allow police to obtain "roving wiretaps," giving cops the authority to tap any phone that a "suspect" uses, regardless of whether or not it's their own phone. Plus his proposal would allow the FBI and other police agencies to seize information from Internet companies without a court order.</P>
<P>Not that the FBI waited for any laws to be passed. In the days following the attacks, agents tried to strong-arm Internet companies into installing the Carnivore program to allow the government to "eavesdrop" on Internet communications.</P>
<P>The Bush gang is clearly exploiting fear of terrorism to gain new powers that it could use at will against political opponents. "This country has a long tradition of responding to fear by stifling dissent, punishing association, launching widespread political spying and seeking shortcuts around the Constitution," Georgetown University law professor David Cole wrote in the <I>Nation</I> magazine.</P>

<P><font face="Times New Roman, Times, serif" size="4">Grim impact of anti-terrorist laws</font><br></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOLLOWING the bombing of the Oklahoma City federal building, Bill Clinton and Republican Congressional leaders united to pass the 1996 Anti-Terrorism and Effective Death Penalty Act.</P>
<P>The law did nothing to stop terrorism. But it has further victimized some of the most vulnerable people in U.S. society.</P>
<P>The act allows federal officials to use secret evidence to detain and deport foreign nationals deemed to be "terrorists." "Neither the detainee nor the attorney gets to see what the prosecutors have, so there's no way for the alien or detainee to defend themselves," said immigration attorney Carol Khawly.</P>
<P>The law also imposed sharp restrictions on rights of death row prisoners to appeal--effectively forbidding federal judges from taking up most issues in death row cases, including new claims of innocence. Clinton's law helped speed up America's killing machine to its record-setting pace at the end of the 1990s.</P>

<P><font face="Times New Roman, Times, serif" size="4">Cracking down around the world</font><br></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. crackdown on civil liberties is being mirrored around the world by governments using the opportunity to crack down not on "terrorists" but on opponents of their own rule.</P>
<P>In France and Germany, press reports describe widespread roundups of Islamists on dubious charges.  In Italy, the government of Prime Minister Silvio Berlusconi--responsible in July for the gunshot killing of a global justice demonstrator--has cracked down on other activists.</P>
<P>And as <I>Socialist Worker</I> went to press, we learned that security forces in Egypt--one of the U.S.'s main allies in the Middle East--had kidnapped Farid Zahran, a leader of the Egyptian People's Committee for Solidarity with the Palestinian Uprising.</P>
<P>These crackdowns have nothing to do with protecting ordinary people. They are an attack on our rights--attacks that we demand must end.</P>

