<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Galvanizing the opposition to war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rhythm of resistance</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SHARON SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 28, 2001 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MANY WAL-MART stores sold out of American flags in September, unable to keep up with the surge of patriotism that has swept the country since September 11.</P>
<P>No longer the subject of popular ridicule, George W. Bush's popularity rating has climbed to 86 percent, according to the latest Gallup poll.</P>
<P>All opinion polls show overwhelming popular support for retaliatory military action against terrorist networks and the countries where they're based.</P>
<P>And a wave of anti-Arab racism has swept across the U.S., already resulting in the murder of three people (one of whom was a Sikh) because they "looked" Muslim.</P>
<P>In a matter of days, the political climate across the U.S. was transformed. Indeed, an evening in front of the television listening to Bush's cronies filling the airwaves with talk of global war, espionage, political assassinations and commando hit squads feels like being transported back in time to the 1950s.  One almost expects "Dragnet" reruns to start appearing.</P>
<P>But it's necessary to look beyond the mass media to get the real picture of the state of politics in U.S. society since September 11.</P>
<P>Many thousands across the U.S. have already shown their determination to organize against Bush's war drive and to defend Arabs and Muslims against racist attacks. More than 100 campuses participated in a day of action against war and racism September 20--with a rally of 5,000 students at the University of California-Berkeley and hundreds more at other schools.</P>
<P>From the beginning, this antiwar movement far surpasses the size of opposition to NATO's 1999 carpet bombing of Serbia--and even the Gulf War against Iraq in 1991. Moreover, many of those who today express support for Bush and a U.S. military response can be won to oppose the war in the weeks and months to come.</P>
<P>Wars are traditionally accompanied by a surge of patriotism at the beginning that melts away as it becomes clear what the real war aims are and who is expected to pay the price with their lives.</P>
<P>Sometimes the patriotic sentiment fades very quickly. The first President Bush learned this when his 92 percent popularity rating at the end of the Gulf War gave way to a landslide defeat in the 1992 presidential election.</P>
<P>Even the surge of patriotism that followed the attack on Pearl Harbor gave way to a wave of wartime strikes--even in the war production industries--despite a no-strike pledge taken by the labor movement.</P>
<P>For several years, opposition to the Vietnam War was limited to the campus-based antiwar movement. But by 1968, the antiwar movement had grown to massive proportions, and a majority of the U.S. population opposed the war in opinion polls. That opposition continued to spread as it became clear to more people not only that the U.S. was going down to defeat but that the war itself was unjust.</P>
<P>Today, we must begin by helping to galvanize the significant minority--numbering in the millions--already opposed to Bush's coming war. And we must help that minority to grow by arguing with others who aren't yet convinced that the war is wrong, but who recognize the hypocrisy of Bush's policies.</P>
<P>Many people who are waving the American flag this week are also repulsed by the witch-hunt against Arabs and Muslims. Among the 86 percent who expressed support for Bush are those who have deep-seated anger about his theft of the election last year amid the mass disenfranchisement of Black voters. Many more were angered by the enormous tax cut handed to the rich just a few months ago--while the Medicare prescription drug benefit and health care spending languished.</P>
<P>Millions of people will oppose Republican attempts to rush through more cuts in social spending to pay for the coming war. And many will be horrified at the slaughter of civilians in their name--and won't want to sacrifice the lives of U.S. soldiers for it.</P>
<P>Opposition to this war can grow--and grow quickly. Bush's own policies may soon come back to haunt him.</P>

