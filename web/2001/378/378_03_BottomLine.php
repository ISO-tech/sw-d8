<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sacrificing for their bottom line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 3</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHILE PEOPLE around the world mourned the victims of the September 11 attacks, big business mourned its bottom line. "The terrible disaster we have had just wiped out more than one year's income for the entire global aviation insurance industry," whined Mark Bayless, spokesperson for an insurance industry group in London.</P>
<P>Feeling the airline industry's pain, Congress and the Bush administration rushed through a $15 billion bailout plan. Not a dime was targeted to the more than 100,000 airline industry workers that the bosses have said will be laid off.</P>
<P>AFL-CIO President John Sweeney was right to denounce this travesty. "Members of Congress appear poised to stiff airline industry workers in the bailout bill--even as they award protections to airline executives for their golden parachutes," he said.</P>
<P>To try to create the illusion of fairness, the legislation barred bonuses to airline executives making more than $300,000 a year! Meanwhile, Congress rushed through a $40 billion "emergency spending" bill, the majority of which will go straight to the Pentagon.</P>
<P>For years, Congress has shot down spending for child care, welfare, health insurance and other programs because they were "too expensive."</P>
<P>Now there's no limit to the money that Washington will pour down the Pentagon drain. So much for Bush's opposition to "big government."</P>
<P>His planned spending increase on his "top priority"--education--amounts to less than one-sixth of what the Treasury will hand over to the airline bosses. And the administration is angling for even more giveaways to the rich--like a cut in the capital gains tax and "fast-track" trade negotiating authority.</P>
<P>Only now, they're being sold as a necessary "economic stimulus" in response to terrorism. "Americans also know that a strong economy is essential to any war effort, and this also gives Mr. Bush a big opportunity," advised the <I>Wall Street Journal</I>.  "In particular, the phony 'trust fund' constraints on fiscal policy have fallen with the Trade Center towers, opening as much as $150 billion a year in surplus for pro-growth tax cuts."</P>
<P>Meanwhile, Boeing seized on the September 11 tragedy as the reason to announce a layoff of 30,000 workers. In reality, most of these cuts were the latest phase of a downsizing effort that has been underway for years.</P>
<P>Big layoffs in other industries are expected soon as the recession deepens. But workers can't expect a bailout.</P>
<P>Instead, they'll hear more calls to "sacrifice for the good of the country." Going along with this will only allow bosses to wrap themselves in the American flag while they keep squeezing us.</P>
<P>We can't allow the politicians to loot essential programs or employers to slash wages and jobs in the name of a "war on terrorism."</P>

