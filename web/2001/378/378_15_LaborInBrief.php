<p><a name="Supremo"></a><font face="Times New Roman, Times, serif" size="4"><b>V&amp;V Supremo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Authorities and V&amp;V Supremo, a Mexican food products company, took advantage of the September 11 attack in New York and Washington, D.C., to arrest and threaten a strike leader.</P>
<P>The International Brotherhood of Teamsters Local 703 member is now prohibited from walking the picket line with other strikers in their 17-week strike against the company because he allegedly made a bomb threat.</P>
<P>The Chicago Interfaith Committee on Worker Issues held a prayer service with strikers on September 17. Striker Max Rodea criticized the Mexican-owned business for its exploitation of the undocumented, mostly Mexican workforce.</P>
<P>In recent negotiations, the company offered a 25-cent raise in the first year, and no raises in subsequent years. It also designed a two-tier pay scale for drivers in hopes of dividing workers.</P>
<P>The majority of strikers have not crossed the picket line, but many are working other part-time jobs to get by.</P>
<P>One major supermarket chain, Dominick's, has agreed not to stock their products, and strike-support organizations are working on other businesses to join the boycott.</P>

