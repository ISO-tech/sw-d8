<p><a name="Boeing"></a><font face="Times New Roman, Times, serif" size="4"><b>Boeing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--A week after the tragedy in New York and Washington, Boeing Co. had more disastrous news for workers.</P>
<P>The company announced that it plans to lay off as many as 30,000 people--some 30 percent of the workforce--in its commercial jet unit by the end of 2002.</P>
<P>After the September 11 air attacks using commercial jets, Boeing slashed its projected delivery number from 510 or 520 to the "low 400s." "It is critical that we take these necessary steps now to size the commercial airplane business to support the difficult and uncertain environment faced by our airline customers," said Alan Mulally, president of Boeing's commercial airplane division.</P>
<P>In fact, Boeing was already preparing to outsource millions of dollars worth of production work to contractors--and announce layoffs--well before the air attacks. Boeing "was already planning to pare the workforce in anticipation of an airline slump, after restructuring factories, embracing lean manufacturing principles and cutting costs," <I>Business Week</I> reported. "For the first time in nearly a decade, the division was producing 10 percent profit margins. Execs were confident they could squeeze even more fat out of a what had been a costly and inefficient airplane production system."</P>
<P>While layoffs will be spread over facilities in Washington, California and Kansas, the greatest pain will likely be felt in Washington's Puget Sound region, where some 60,000 Boeing workers build airliners.</P>
<P>"Every time they need more money, every time they need their stock to go up, they announce layoffs," said Don Gross, who has worked at Boeing for 13 years.</P>
<P>Layoffs--53,000 of them--were a huge issue in the International Association of Machinists (IAM)'s 1999 contract fight with Boeing. And while the IAM's threat to strike won solid wage increases, it left the door open to the job cuts.</P>
<P>So Boeing continued to eliminate jobs by establishing the highest production pace ever. In Renton, Wash., Boeing produces 737s on the first-ever continuously moving production line, with planes parked nose to tail on a moving assembly line. And in August, Boeing announced plans to reduce its three existing production lines to two.</P>
<P>So after September 11, management announced more cuts and blamed them all on the air attacks. But IAM leaders--who have stood by while Boeing outsourced tens of thousands of jobs--don't have a strategy to fight these cuts. "I think at this point, Boeing's management does have an obligation to the region to keep this work in-house. It's an area where we can work together to mitigate job losses," IAM Local 751 President Mark Blondin said.</P>
<P>But Boeing's only obligation is making profits. Whether Boeing is able to use the tragedy to cut more jobs will depend on what kind of fight IAM members organize to stop them.</P>

