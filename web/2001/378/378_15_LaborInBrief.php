<p><a name="Charleston"></a><font face="Times New Roman, Times, serif" size="4"><b>Charleston Five</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--On September 18, the Communications Workers of America Local 1106 unanimously passed a resolution supporting the Charleston Five.</P>
<P>Although most members had not heard of the struggle before the meeting, the original request for $500 was shouted down--and $1,000 was pledged amid cheers.</P>

