<P><font face="Times New Roman, Times, serif" size="5"><b>IBM</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by KEITH ROSENTHAL</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ESSEX, Vt.--Some 125 IBM workers and their supporters gathered at Essex High School on September 7 to rally in support of their union drive. The Alliance@IBM hosted the event and is hoping to win union recognition through the Communications Workers of America (CWA).</P>
<P>IBM is the largest employer in Vermont, and a successful union drive would be a major victory for the labor movement.</P>
<P>The union drive picked up steam late this summer when the company implemented an 18 percent, across-the-board wage cut and fired several hundred workers.</P>
<P>But workers' resentment has been building over the last two years in the face of cuts in health and pension benefits.</P>
<P>"I used to just do my job and then go home," Earl Mongeon, one of the head organizers of the Alliance@IBM, told <I>Socialist Worker</I>. "But IBM has recently deteriorated our benefits so much that I decided I had to do something."</P>
<P>Mongeon went on to point out that IBM CEO Lou Gershner pulled in about $150 million this year, yet some IBM workers have been forced to take second jobs just to stay afloat.</P>
<P>Many workers stood up at the rally and talked about the deterioration of labor standards all over the country. Retirees warned workers that things would only get worse unless they acted. Even those who used to consider themselves anti-union thought that the time had come to fight back.</P>
<P>The rally was also marked by a strong sense of solidarity from members of other unions. Supporters from the AFL-CIO, CWA members from the recently unionized Verizon and members of the new faculty union at the University of Vermont also attended.</P>
<P>Joe Smith, the District 7 vice president of CWA Local 1400 at Verizon, summed up the mood of the rally nicely: "We need to stand together and get the word out to the people--to wake up and see what's going on in this country, because our rights are fading away to corporate greed."</P>

