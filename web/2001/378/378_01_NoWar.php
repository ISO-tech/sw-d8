<P><font face="Times New Roman, Times, serif" size="5"><b>No to war! No to racism!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 1</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH wants to turn your grief into a blank check for war. The U.S. had yet to launch its warplanes and cruise missiles as <I>Socialist Worker</I> went to press. But an attack is almost certain to take place before we publish again.</P>
<P>Bush all but promised military action against Afghanistan in retaliation for the air attacks in New York and Washington, D.C. The reason: Afghanistan's Taliban government refuses to turn over Osama bin Laden, the Saudi millionaire who the U.S. claims is responsible for the attacks.</P>
<P>U.S. officials say that they have evidence against bin Laden to justify a war. But their record leaves little reason to trust them.</P>
<P>And what gives the U.S. government the right to end states, change governments and determine the fate of people around the world?</P>
<P>Remember the last time that the U.S. took action against "terrorism"--in 1998, after the bombing of two American embassies in Africa. U.S. cruise missiles took out a factory in Sudan that was allegedly producing chemical weapons.</P>
<P>In fact, the factory produced half the anti-malarial medicine for that famine-plagued country. Its destruction caused untold misery and death.</P>
<P>This new "war against terrorism" will be no different. Already, hundreds of thousands of ordinary Afghanis--with no connection to either bin Laden or the Taliban--are suffering because the U.S. ordered Pakistan to cut off desperately needed food aid.</P>
<P>But did any U.S. official even pause to consider the "tragic loss of life" that this cruel order is certain to have? The war to come will bring only more of this needless suffering and death.</P>
<P>Meanwhile, Bush and his fanatical Attorney General John Ashcroft have made it clear what the war drive will mean at home--draconian new powers for law enforcement, the use of "secret evidence" and indefinite detention against immigrants and fewer liberties and rights for all.</P>
<P>This is part of Washington's message for everyone--in Afghanistan, in the U.S. and around the globe: What we say goes.</P>
<P>But people around the U.S. have stood up to send a message to Washington: Not in our name.</P>
<P>People like Judy Keane, whose husband was killed in the World Trade Center attack. She organized a vigil of 5,000 people outside her home of Waterfield, Conn., to call for peace.</P>
<P>"The last thing on earth he would want is for his children to avenge his death," Judy told Amy Goodman, host of the radio show "Democracy Now!" "Bombing Afghanistan is not going to end terrorism. It will create more widows and more fatherless children."</P>
<P>There are thousands more people like Judy in every city in the U.S. And they've begun to organize--holding vigils, forums, teach-ins and protests across the country.</P>
<P>Washington's war makers don't speak for us. Join the fight to stop Washington from turning tragedy into war!</P>

