<P><font face="Times New Roman, Times, serif" size="4">Organizing against attacks on Arabs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We won't stand for this racism"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 16</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">ELIZABETH SCHULTE reports on the wave of racist attacks against Arabs and Muslims.</P></B>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>IN DALLAS, shots were fired into a Muslim mosque. In Mesa, Ariz., a Sikh was shot and killed in front of his gas station. In San Francisco, an Indian man was punched and called a "dirty Arab"--and his white friend was stabbed trying to defend him.</P>
<P>Similar stories are being reported across the country--acts of violence and harassment in the wake of the tragedies in New York and Washington, D.C.--that have sent a chill of fear through Arab and Muslim communities.</P>
<P>Individual racists are carrying out their attacks on the street. And some are attacking from their seats in Congress. "If I see someone come in, and he's got a diaper on his head and a fan belt around that diaper on his head, that guy needs to be pulled over and checked," Rep. John Cooksey (R-La.) said in a radio interview.</P>
<P>Cooksey later apologized--but added that a war on terrorism can't be won "if we have to stop every five minutes to make sure we're being politically correct."</P>
<P>The Louisiana lawmaker may be especially crude. But he's following a lead from the top.</P>
<P>While George W. Bush made sure to visit a mosque after the attacks and condemn assaults on Muslim Americans, you have to question his sincerity when, on the very same day, he harkened back to the Wild West days, suggesting posters that read "Wanted Dead or Alive." The <I>New York Post</I> picked up on the president's message, printing their own "Wanted Dead or Alive" posters featuring a photo of Osama bin Laden.</P>
<P>One reason that several Sikhs have been attacked is that their traditional headdress and beards resemble the pictures of bin Laden. "And the media just showed that over and over and over," said Guru Roop Kaur Khalsa, a minister of the Guru Nawak Dwara Sikh temple in Phoenix. "It sets us up."</P>
<P>Attorney General John Ashcroft told reporters that new security rules at airports would mean passengers would be questioned if there's "any suspicion."</P>
<P>"But," he said, "we are not at the place of saying that people are suspects based solely on their race or ethnic origin." </P>
<P>Yet the reality is that racial profiling is standard procedure. Ask the three men who were barred from boarding a flight at the Minneapolis/St. Paul International Airport. They were interrogated and their luggage was searched after they were described to police as "suspicious"--because they were of Middle Eastern descent.</P>
<P>And when victims of racist harassment report incidents to authorities, they're usually ignored--or worse.</P>
<P>Karim Fareh, a 22-year-old Tunisian immigrant, tried to flag down a police officer in patrol car when he saw an angry mob of people coming toward him on a street in Philadelphia's Kensington neighborhood. Fareh said the cop screamed at him, "Go fucking home! You're drunk!" and drove away.</P>
<P>The mob kicked, beat and slashed Fareh with a knife. When the same cop responded to Fareh's 911 call, he screamed some more and drove off--not even taking the bleeding man to a hospital.</P>
<P>Fareh is thinking about going back to Tunisia. "I feel I'm [treated like a] human there," he said.</P>
<P>Yet in the face of such horrible attacks, many Arabs and Muslims are finding concern and solidarity.</P>
<P>Ahmad Ansari worships at the Cleveland mosque that a man smashed his car into last week. "People are constantly leaving flowers, making donations for the repair of the property," Ansari told <I>Socialist Worker</I>. "I came here 40 years ago, and I'm seeing the best of Americans now. I never realized people could be so good."</P>
<P>This instinct of solidarity can be organized and built upon.</P>
<P>When early morning prayers at a Chicago Southwest Side mosque came to a halt after someone threw bricks through the windows, activists got to work. Religious and community groups organized defenses at the mosque.</P>
<P>Sister Margaret brought letters of support from her students. "As an African American&#133;I have experienced racism," wrote a ninth grader, "and I wanted show my support."</P>
<P>As Hakim Husien of the Palestine Aid Society said, "We're here to show that we are all one people, and we will not stand for hate crimes, racism or racial profiling. This is the message that the people here are sending."</P>
<P>We have to organize to stop this racist scapegoating.</P>

