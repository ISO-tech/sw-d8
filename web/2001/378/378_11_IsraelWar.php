<P><font face="Times New Roman, Times, serif" size="4">Sharon government uses tragedy to escalate the violence</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's war on Palestine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>ERIC RUDER reports on Israel's war against the Palestinians.</P></B>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ALMOST IMMEDIATELY after the attacks in New York City and Washington, D.C., Israel's war against the nearly year-old Palestinian uprising took on a new ferocity.</P>
<P>There was little notice in the U.S. media. But the Israeli press could hardly contain its delight.</P>
<P>The Israeli newspaper <I>Ma'ariv </I>urged Israeli Prime Minister Ariel Sharon "to seize the moment and use against terrorism the kind of means which hitherto he did not dare to use for fear of international reaction."</P>
<P>But Sharon didn't need prodding. For four days after September 11, Israeli forces laid siege to the Palestinian town of Jenin. "It is an invasion of the Palestinian territory far longer--and with far more severe consequences--than the April invasion of the Gaza strip which at the time drew a sharp reprimand from the U.S. State Department (nothing of the kind this time, needless to say)," wrote the Israeli human rights group Gush Shalom.</P>
<P>The Israeli military also invaded parts of Beit Jala, Jericho, Rafah, Khan Younis, Hebron and Ramallah--in "the largest number of simultaneous operations since the uprising started," according to Israel Radio's military correspondent.</P>
<P>One week after the attacks in the U.S., Israel had killed at least 26 Palestinians. Among the dead was a 9-year-old girl--and a 71-year-old man shot while trying to go around an earthen barrier that blocked the only access to his village.</P>
<P>Three suspected Palestinian militants in the West Bank who refused to surrender to the Israeli military were "liquidated by missiles and shells," wrote the Israeli newspaper <I>Yediot Aharonot</I>. "A 12-year-old girl was also killed in the shooting on an inhabited building."</P>
<P>One week into Israel's new campaign, the U.S. stepped in to broker a cease-fire agreement between Yasser Arafat's Palestinian Authority and the Israeli government. The deal was part of American efforts to rein in its ally Israel in order to build an "anti-terror coalition" that includes Arab governments.</P>
<P>But the prospects for the cease-fire are dim. Minor skirmishes had already taken place as <I>Socialist Worker</I> went to press.</P>
<P>More fundamentally, the cease-fire, like many before it, doesn't address the underlying sources of the conflict--Israel's occupation of Palestinian land and the apartheid-like conditions faced by more than 2 million Palestinians.</P>
<P>The Washington establishment has done its best to ignore this reality. That's why it was typical for the mainstream media to replay again and again the same footage of a dozen or so Palestinians celebrating the attack on the U.S.</P>
<P>In fact, every Palestinian organization, including the most militant, condemned the attacks in the U.S. If a few celebrated, the overwhelming majority of Palestinians were horrified--as well as fearful, justly it turned out, that the attacks would be used to justify renewed Israeli violence.</P>
<P>In an attempt to counter the media's reporting, Rabbi Arthur Waskow from the Shalom Center in Philadelphia circulated a statement by Palestinians expressing their condolences. "As Palestinians who suffer daily from acts of Israeli aggression against our innocent people, we cannot find the words to express how shocked we were to see the horrific scenes on TV," the statement read. "We condemn such acts, and we do not accept such horrific acts in the third millennium where peace, prosperity and freedom should cover the whole world."</P>
<BR>
<P><font face="Times New Roman, Times, serif" size="5">U.S. watchdog in the Middle East</font><br></P>
<font face="Times New Roman, Times, serif" size="3"><P>THERE'S A simple reason why the U.S. gives Israel billions of dollars a year to wage its war against Palestinians--Middle Eastern oil.</P>
<P>As Lawrence Korb, an assistant defense secretary under Ronald Reagan, said of U.S. preparations for war after Iraq invaded Kuwait in 1990: "If Kuwait grew carrots, we wouldn't give a damn."</P>
<P>Israel has long been central to U.S. aims in the region.</P>
<P>In 1967, after months of provoking its neighbors, Israel launched a war to seize areas of Palestine that it didn't occupy during the 1948 war that drove 700,000 Palestinians off their land. Israel routed the militaries of Egypt, Syria and Jordan--and took the Golan Heights from Syria, the West Bank and East Jerusalem from Jordan and the Gaza Strip and Sinai Peninsula from Egypt.</P>
<P>But the U.S. didn't condemn Israeli aggression. On the contrary, the 1967 war proved to the U.S. that it could use Israel as its watchdog in the region.</P>
<P>Between 1967 and 1972, U.S. aid to Israel jumped by almost 50 percent. The money was well spent, according to foreign policy analyst Stephen Zunes.</P>
<P>"Israel has helped suppress victories by radical nationalist movements in Lebanon, Jordan, and Yemen, as well as in Palestine," Zunes wrote. "The Israeli military has kept Syria, for many years an ally of the Soviet Union, in check, and its air force is predominant throughout the region. Israel's frequent wars have provided battlefield testing for American arms. Israel has also served as a conduit for U.S. arms to regimes and movements--such as apartheid-era South Africa, Iran, Guatemala and the Nicaraguan contras--too unpopular in the United States for overt and direct military assistance."</P>

