<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Politicians and generals aren't interested in justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The mad rush to war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 3</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HANDMADE POSTERS of those missing in the World Trade Center tragedy were still being posted by family and friends of the victims a week after the air attacks. The thousands of leaflets plastered around Manhattan were a heart-wrenching testimony of suffering and loss.</P>
<P>But George W. Bush had a different kind of poster in mind. "I want justice," Bush told reporters during a visit to the Pentagon. "There's an old poster out West, as I recall, that said, 'Wanted: Dead or Alive.'"</P>
<P>Bush was appealing to most people's desire to hold the right people responsible for a terrible loss of life.</P>
<P>But the Bush White House is after something else entirely. Deputy Secretary of Defense Paul Wolfowitz let the real agenda slip when he called for "ending states who sponsor terrorism."</P>
<P>And in his speech before a joint session of Congress, Bush declared the right to make war on any nation that doesn't toe the U.S. line--anywhere and anytime. "Every nation in every region now has a decision to make," Bush said. "Either you are with us, or you are with the terrorists."</P>
<P>Never mind that there is little real evidence linking the "prime suspect," Osama bin Laden, to the attacks. Never mind that bin Laden and his followers were armed and trained by the CIA to drive the Russians out of Afghanistan in the 1980s.</P>
<P>"The resistance of the Afghan freedom fighters is an example to all the world of the invincibility of the ideals we in this country hold most dear, the ideals of freedom and independence." Or so said President Ronald Reagan in declaring Afghanistan Day on March 21, 1983.</P>
<P>Now those "freedom fighters" run Afghanistan's Taliban government. But since they won't follow the U.S. agenda, a country ravaged by two decades of war will face another savage military attack.</P>
<P>Even the U.S.'s allies have grown skittish over Bush's war talk. So to build a coalition supporting action in Afghanistan, the U.S. handed out a few bribes--like accepting Russia's slaughter in Chechnya, Turkey's repression of the Kurds and China's crackdown on dissidents, to name a few. Washington even lifted sanctions on India and Pakistan imposed after they threatened each other with nuclear weapons tests.</P>
<P>Meanwhile, the U.S.-dominated International Monetary Fund promised new loans for Pakistan's military government. But by demanding support from Pakistan--which has ties to the Taliban--the U.S. could trigger a civil war there, and possibly in other Muslim countries as well.</P>
<P>Working people in the U.S. will pay a price, too--both on foreign battlefields and at home. "In order for us to preserve America and our way of life, we're going to have to sacrifice American treasure, and unfortunately, in some cases, perhaps American blood," said Sen. John McCain (R-Ariz.). </P>
<P>But some Americans will sacrifice more than others. Congress' $15 billion bailout for the airline industry will do nothing for the 100,000 workers that bosses plan to lay off. </P>
<P>Don't expect Democrats to object, either. The "defenders of working people" have rolled over for Bush at every turn. Rep. Barbara Lee (D-Calif.) was the only member of Congress to vote against a bill giving Bush a blank check to wage war.</P>
<P>"There's no air, and there's no light between the president and the Congress, and between the Republican and Democratic parties," said House Minority Leader Richard Gephardt (D-Mo.) "We stand shoulder to shoulder." This bipartisan drive to war will only add to the horror unleashed in New York and Washington September 11.</P>
<P>Standing up for justice means opposing this war--and fighting for a different kind of world.</P>

