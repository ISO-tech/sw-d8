<P><font face="Times New Roman, Times, serif" size="5"><b>Israel stages a "celebration"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DURING THE days following the attacks, the TV networks couldn't stop showing footage of Palestinians in East Jerusalem celebrating the disaster in America. Countless commentators fumed about widespread support for terrorism among Palestinians.</P>
<P>But the footage was in contrast to the actual response of Palestinians. Every Palestinian organization condemned the attacks. Within hours of the tragedy, dozens of Palestinians had built a makeshift shrine outside the U.S. consulate in East Jerusalem.</P>
<P>In fact, so out of keeping with the general sentiment were the TV scenes of celebration that many Palestinians wondered whether CNN had dipped into its vaults and showed earlier anti-U.S. demonstrations.</P>
<P>But the footage was real--and orchestrated by the Israeli government. "A team from the Spokesman's Office of the Israel Defense Forces was sent to film the scenes of joy and candy being handed out in East Jerusalem 'for public relations purposes,'" wrote the mainstream Israeli newspaper <I>Ha'aretz</I>.</P>
<P>Turns out this team had stage managed the action. According to the <I>Jerusalem Times,</I> a Palestinian weekly, "A member of the team approached the Juhaina sweets Shop and gave the owner 200 sheckels and asked him to distribute the sweets to the children, according to the owner of the shop."</P>
<P>Then the Israeli team started filming the "celebration."</P>

