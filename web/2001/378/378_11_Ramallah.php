<P><font face="Times New Roman, Times, serif" size="5"><b>"Israel's military is pounding my hometown"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 28, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>WE RECEIVED a forward of this e-mail message, sent by Saifedean Ammous, a Palestinian living in Ramallah in the West Bank, in the midst of Israel's invasion of the city.</b></P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">I AM writing to you now to the sound of Israel's "Defense Forces" pounding the living daylights out of my hometown, Ramallah. As the whole world is busy paying tributes to the victims of terror attacks in New York and Washington, Israel has had a field day in terror attacks of their own on us here. </P>
<P>Every day, they have invaded a few cities using tanks, helicopters and fighter jets. Since the beginning of the Intifada, there hasn't been Israeli aggression like that witnessed since last Tuesday. No one could put this clearer than Shaul Mofaz, Israel's Chief of Staff, who boasted that his forces have killed tens of Palestinians--and no one in the world said anything. </P>
<P>Everyone seems to be more interested in backing that raging cowboy Bush as he declares his country is going to war against an as yet unidentified enemy. </P>
<P>The attack on the World Trade Center and Pentagon is a clear act of terror that I and millions of Palestinians denounce. Yet it's painful to see how the whole world has had nothing to do in the last few days but mourn the victims, while more than a million and a half civilian Iraqis have died since the beginning of the U.S. terror campaign on Iraq. </P>
<P>Why did no one weep for them, why did no one weep for the hundreds of thousands in Rwanda, and why is my people's suffering under the illegal Israeli occupation no longer interesting to anyone? </P>
<P>Excuse me if my thoughts are a bit incoherent or if my sentences seem to make little sense, but it is really hard to concentrate with Apache choppers hovering a few hundred feet above your head, armored tanks unleashing their might on your neighborhood, all sorts of missiles flashing across your window, and a nearby building being obliterated to rubble. </P>
<P>If you don't hear from me anymore, then know that one of those missiles has managed to find its way to my room, turning me into a meaningless number in a meaningless statistic that no one will pay attention to. </P>
<P>Israel must not be allowed to continue its attacks on Palestinians. If terror is to be fought in this world, state terror is the first terror that must be eradicated. American attacks on civilians in Iraq, Sudan and Yugoslavia and Israel's continued terror in Palestine are primary examples. </P>
<P>Finally, my heart goes out to every victim of terror in the world--to the people of Hiroshima, Auschwitz, Rwanda, Baghdad, Belgrade, Vietnam, New York, and D.C.</P>

