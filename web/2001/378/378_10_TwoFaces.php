<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The two faces of U.S. power abroad</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 28, 2001 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FAMOUS war strategist Karl von Clausewitz wrote that "war is a continuation of policy by other, i.e., forceful means."</P>
<I><P>New York Times</I> columnist Thomas Friedman expressed this idea in terms of U.S. interests. "For globalization to work," Friedman wrote, "America can't be afraid to act like the almighty superpower that it is. The hidden hand of the market will never work without a hidden fist. McDonald's cannot flourish without McDonnell Douglas, the designer of the F-15, and the hidden fist that keeps the world safe for Silicon Valley's technology is called the U.S. Army, Air Force, Navy and Marine Corps."</P>
<P>Before September 11, many activists organizing for global justice looked at the free-market policies of the U.S. and Europe and concluded that national states had become increasingly irrelevant--replaced by stateless and rapacious multinational corporations. There was even a fashionable new book called <I>Empire</I> to put forward these arguments.</P>
<P>But the massive buildup for war after the September 11 suicide attacks showed how wrong these ideas are.</P>
<P>Impressively, many activists and groups that were mobilizing for late September demonstrations against the policies of the World Bank and IMF immediately saw the need to start opposing the government's war drive.</P>
<P>These activists feel in their gut that "globalization"--the imposition of policies designed to benefit Western corporations in their pursuit of limitless profits--is connected to Washington's desire to dominate the world militarily. And they're right.</P>
<P>The "Washington consensus"--imposing U.S. economic interests around the world through "free trade" deals and structural adjustment programs--isn't suspended while the U.S. pursues its war. On the contrary, this is its military extension.</P>
<P>This isn't new. Writing just over 100 years ago, Sen. Alfred Beveridge of Indiana described America's new pretensions to economic and military domination. "American factories are making more than the American people can use; American soil is producing more than they can consume," Beveridge said. "Fate has written our policy for us; the trade of the world must and shall be ours."</P>
<P>Every few decades or so, economic rivalries between the biggest imperial powers would give way to political conflicts--and political conflicts to outright war. These wars came in the form of both world wars that pitted the imperial powers against each other, or smaller wars involving powerful states attempting to defend their "spheres of influence" and to send a message to anyone who might challenge them.</P>
<P>What's different today--at least for time being--is that the U.S. stands as the world's "sole" superpower. Its military actions reflect not a desire for revenge, but to demonstrate, both to its allies and enemies, that its military and economic power can't be challenged.</P>
<P>U.S. political leaders don't put it that way. In the wake of the attacks in New York and Washington, they've tried to convince people who are upset and angry that the solution is to expand the role of the U.S. as the world's biggest cop.</P>
<P>But counting on the U.S. government for "justice" is like calling on Al Capone to wipe out a street-corner gang--the solution is worse than the problem.</P>
<P>Of course, the Capones will say that they're fighting for "freedom" against "terrorism." And they will describe their own state-sponsored terror--such as the inevitable bombings of civilians in Afghanistan--as acceptable "collateral damage."</P>
<P>As Palestinian author and activist Edward Said put it, the U.S. is not a "sleeping giant" recently awakened to anger and outrage, but a heavily armed superpower that has been more or less constantly waging war in different forms for decades.</P>
<P>Or as a columnist for the <I>Times </I>of London concluded: "Playing the world's policeman is not the answer to that catastrophe in New York. Playing the world's policeman is what led to it."</P>

