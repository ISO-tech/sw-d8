<P><font face="Times New Roman, Times, serif" size="5"><b>Taking on the heat and winning--twice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</i></b></P>
<P>Last week, my coworkers and I were working in 96-degree heat for APEX--Addiction Prevention Employment Experience--a program run by Washington, D.C. I'm a young man, but my 20 coworkers range in age from 37 to 67.</P>
<P>After finishing our shift two hours early, we wanted to enter our air-conditioned headquarters instead of sitting on a bus in a park for another hour and 45 minutes. After not being able to contact my supervisor, I took the initiative to contact my boss's boss--to make sure we got out of the heat. She said okay, and everyone was pleased. </P>
<P>Later that day, I was shocked to hear that I had been suspended for three days--without pay--for not following the "correct chain of command." </P>
<P>I couldn't believe it! </P>
<P>This is the problem with working in a nonunion workplace. There are no written rules and regulations, and punishment is arbitrary. Many of my coworkers have spoken out for me, and I'm not going to take this lying down. </P>
<P>I have a petition calling for a written code of conduct and due process in our workplace. Winning this could be the first step to winning a unionized workplace, which is what we so clearly need.</P>
<P><b>Jeffrey Ragsdale,</b> Washington, D.C.</P>
<I><P>Since</i> Socialist Worker <i>received this letter, Jeffrey got 22 of his 25 coworkers to sign his petition, and he won full back pay.</P></I>

