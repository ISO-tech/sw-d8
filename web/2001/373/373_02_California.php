<P><font face="Times New Roman, Times, serif" size="5"><b>Consumers pay the price for energy crunch</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JESSIE MULDOON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Customers of Pacific Gas &amp; Electricity (PG&amp;E) are fearing the arrival of their July electricity bills. While the state's energy crunch has eased some, they'll still be hit with a rate increase of up to 36 percent.</P>
<P>Consumers are still paying the price for California's electricity crisis--even though everyone now admits that profit-hungry power producers caused it.</P>
<P>In July, the chief judge of the federal government's energy regulatory agency agreed with California's plea for a rebate from the energy companies that manipulated prices. But the judge limited the rebate to $1 billion&#150;far less than the nearly $9 billion that California asked for. And even that amount might not get to consumers--since the power producers are owed more than $1 billion.</P>
<P>Gov. Gray Davis says that he'll fight for a just settlement. But no one should trust "Give it away Gray," as he's known to his many critics.</P>
<P>For example, three new power plants have gone online in California this summer, to much fanfare. But the companies that run these plants got a sweetheart deal&#150;long-term contracts to sell power to the state at a fixed rate of $70 per megawatt hour.</P>
<P>That sounds good next to the $3,800 per megawatt hour that Duke Energy was able to charge at the height of the crisis. But it's still double the average $35 per megawatt hour from before the crisis.</P>
<P>California's energy crisis is far from over&#150;because power bosses are far from finished with making superprofits off consumers.</P>

