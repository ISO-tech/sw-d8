<P><font face="Times New Roman, Times, serif" size="5"><b>No tolerance policy blames the victims</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by MIKKI SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore--Women's rights groups are challenging a "zero tolerance" policy against violence that could lead to the eviction of victims of domestic abuse.</P>
<P>A new lawsuit, filed against an owner of subsidized housing, stems from a 1999 incident in which Tiffani Ann Alvera informed the property manager for her unit that she had gotten a restraining order against her husband.</P>
<P>She then received notice that she had 24 hours to vacate her apartment. "You, someone in your control, or your pet, has seriously threatened immediately to inflict personal injury, or has inflicted personal injury upon the landlord or other tenants," read the notice.</P>
<P>Never mind that Alvera was the one who was injured! She had been hospitalized with a concussion and fractured cheekbone after her husband assaulted her.</P>
<P>"It wasn't my fault," Alvera told the <i>New York Times</i>. "I lost lots of hours of work [because of the injuries], which meant lots of hours of pay. I didn't feel like I could go looking for a new place to live."</P>
<P>According to the U.S. Conference of Mayors, in most cities surveyed, domestic violence is a primary cause of homelessness. Yet housing authorities in California, Colorado, Louisiana, Massachusetts and Michigan have similar policies to the one in Oregon.</P>
<P>The Oregon lawsuit argues that since women are far more likely to be victims of domestic violence than men, and since one in eight women in Oregon experienced such violence in the prior year, the "zero tolerance" policy amounts to discrimination against women.</P>
<P>We should have zero tolerance for policies that punish victims of domestic violence and poverty.</P>

