<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>War criminals that the U.S. likes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">U.S. GOVERNMENT hypocrisy was on full display in July with the war crimes cases against former Yugoslav President Slobodan Milosevic and ex-Chilean dictator Gen. Augusto Pinochet. </P>
<P>Milosevic was whisked off in handcuffs to face a United Nations war crimes tribunal in The Hague. Meanwhile, a Chilean court declared that Pinochet wouldn't stand trial due to "health concerns"--and released the butcher back to his retirement mansion. </P>
<P>U.S. officials bayed as loud as any about putting Milosevic on trial for war crimes committed against ethnic Albanians in Kosovo. But there wasn't a peep when Pinochet walked free--or, for that matter, when former Secretary of State Henry Kissinger refused to answer a Chilean judge's questions about his knowledge of his buddy Pinochet's war crimes.</P>
<P>The Bush administration essentially held Yugoslavia hostage to get Milosevic turned over to The Hague--by threatening to withhold economic aid from a country that U.S. bombs devastated during NATO's 1999 war.</P>
<I><P>New York Times</I> reporter Steven Erlanger--not exactly a radical critic of American foreign policy--described the U.S. pressure as "a form of blackmail." "The U.S. said, 'What do you want: Milosevic or the money?'" Erlanger said on National Public Radio's <I>Fresh Air with Terry Gross.</I> </P>
<P>Meanwhile, the U.S. was "neutral" about the ongoing prosecution of Pinochet.</P>
<P>What explains the difference? Both Milosevic and Pinochet were responsible for the deaths of thousands of people. </P>
<P>But Pinochet committed his crimes with the full support of the U.S. government--with Kissinger acting as head cheerleader.</P>
<P>The U.S. had a more ambivalent relationship with Milosevic. After he signed the U.S.--imposed Dayton "peace" accords that ended the civil war in Bosnia in 1995, Milosevic became the U.S.'s ally in maintaining "stability" in the Balkans. That's why "a lot of people in Serbia regard Milosevic as 'America's man,'" Erlanger said.</P>
<P>Only when Yugoslavia's war against Kosovar rebels threatened to destabilize the region in 1999 did the war crimes tribunal in The Hague indict Milosevic--in a transparent tactic to build support for NATO's war.</P>
<P>What's more, the indictment was handed down as NATO was committing its own war crimes--unleashing massive bombardments, hammering civilian targets and contaminating the country with depleted uranium.</P>
<P>Why is it a war crime to order the shooting of civilians with a rifle, but not to order their incineration with bombs dropped from 20,000 feet by a stealth bomber?</P>
<P>That question won't be answered anytime soon if the U.S. has its way. The U.S. has single-handedly stopped the UN from setting up a permanent tribunal-for the simple reason that it opposes any body that might hold U.S. officials responsible for their war crimes.</P>
<P>War criminals should be brought to justice. But in a sane world, the likes of Milosevic would have to wait behind a long list of U.S. political and military leaders and their hired thugs around the world.</P>

