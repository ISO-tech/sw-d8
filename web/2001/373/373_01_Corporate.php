<p><font face="Times New Roman, Times, serif" size="4">As workers face more job losses...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Corporate fat cats are cashing in</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 1</font></p>

<p><font face="Times New Roman, Times, serif" size="3">WINIFRED THORNE is nervous. For the first time in 20 years, she doesn't have a job.</p>
<p>Winifred has been a secretary all her life, but the small Boston firm that she worked for recently closed. Now she's scrambling to finish computer training before her unemployment benefits run out next month.</p>
<p>"I feel confident that I'll get a job, I really do," she said. "But I'm not a young girl anymore. It's a very scary time right now."</p>
<p>But Treasury Secretary Paul O'Neill isn't worried. At a July meeting of finance ministers from the world's most powerful industrialized countries, O'Neill squawked about how much the Bush administration has done to combat America's economic slowdown.</p>
<p>"I continue to believe that the prospects for long-term global prosperity are better now than at any time in our history," O'Neill said.</p>
<p>Cold comfort to Winifred Thorne.</p>
<p>The truth is that many U.S. workers--especially the most vulnerable among them--have already been dealt a harsh blow by the slump. Recent jobless figures show a spike in Black and Latino unemployment, and decline in the high-tech sector has led to big layoffs, hitting immigrant workers with visas that only allow them to work in technical fields.</p>
<p>Unemployment figures remain low--at least in part because so many workers have simply stopped looking for work, the government stopped counting them.</p>
<p>But while these "discouraged" workers are invisible in unemployment statistics, their hardship isn't. </p>
<p>According to the National Housing Conference, the number of low-to moderate-income working families who pay 50 percent of their income on rent or who live in substandard housing jumped by 23 percent between just 1997 and 1999.</p>
<p>The Bush administration claims help is on the way--thanks to its $1.35 trillion tax cut. They say that they'll be mailing out tax rebates in August and September. Single people should expect $300 checks and married couples $600.</p>
<p>But some people stand to make a mint on the tax deal--literally.</p>
<p>Take Paul O'Neill. Last year, he "earned" more than $60 million as the CEO of Alcoa. His estimated tax savings over the 10 years of the Bush tax giveaway? A cool $3.5 million, according to Citizens for Tax Justice.</p>
<p>And O'Neill isn't even at the top of the heap. Apple CEO Steve Jobs raked in $872 million last year.</p>
<p>As <i>Fortune</i> magazine reported in an article called "The Great CEO Pay Heist," compensation for top corporate executives is higher than ever--"ballooning into nine-figure totals, almost defying comprehension."</p>
<p>Today, CEOs of major corporations on average rake in 531 times what their employees earn.</p>
<p>Even the bosses' mouthpiece <i>BusinessWeek</i> magazine warned that the gap is getting obscene. "Let the pay that CEOs earn keep rising--and watch the economy continue to sour--and execs might well remember the fate that has awaited royalty in the past," the magazine wrote.</p>
<p>It's time to fight for our share. </p>

