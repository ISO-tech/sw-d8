<P><font face="Times New Roman, Times, serif" size="4">Closing of D.C. General</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Putting profits before people</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JEFF BALE and JULIANA KARR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--D.C. General Hospital is set to close its doors on July 16.</P>
<P>The official shutdown of the city's only public hospital will sharply limit access to health care for the city's poor--and cause the largest layoff in D.C. history.</P>
<P>Despite the opposition of most residents and a determined campaign that united community activists, hospital workers and anti-globalization activists, D.C. Mayor Anthony Williams forced through his plan to privatize the hospital in May.</P>
<P>Two City Council members went to court to ask for an injunction against the plan, but the judge is still sitting on the request months later.</P>
<P>Under the takeover, services provided by D.C. General have been contracted to privately owned Greater Southeast hospital. The city's contract with Greater Southeast contains a host of cuts, according to an analysis by D.C. General workers&#150;no WIC, no HIV treatment, no mental health care, no substance abuse treatment and no obligation to keep six community access clinics open. </P>
<P>And for this, Greater Southeast's corporate parent, Doctors Community Healthcare Corp.--a private company run neither by doctors nor the community--will get more than $110 million this year, twice the annual funding level for D.C. General.</P>
<P>The impact of D.C. General's shutdown has already been felt. Several deaths have been attributed to ambulances being rerouted across town after D.C. General's emergency room closed.</p>
<p>And the worst may be yet to come&#150;health care workers say that Greater Southeast has screwed up the transition. "With a lack of a coordinated plan, [July 16] should prove to be an unmitigated disaster," said Vanessa Dixon, an organizer for the Committee of Interns and Residents at D.C. General.</P>
<P>D.C. General's shutdown shows how far Mayor Williams&#150;and Congress, which has its hand on the city's purse strings--will go to push privatization.</P>
<P>Greater Southeast was exposed as incompetent and profit hungry long ago. But Williams and the D.C. Control Board&#150;the District's federal government overseer established in 1994&#150;showed they were more interested in serving corporate greed.</P>
<P>The fight to save D.C. General was fantastic. Week after week, hundreds of people turned out to meetings and demonstrations to defend their hospital. Activists who had little contact before came together in a multiracial alliance around this struggle.</P>
<P>We may lose D.C. General. But the fight we made to save the hospital will be important for future struggles&#150;struggles for access to health care in the capital of the world's richest country, and against a system that puts corporate profits ahead of peoples' lives.</P>

