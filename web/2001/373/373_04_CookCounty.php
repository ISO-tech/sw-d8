<P><font face="Times New Roman, Times, serif" size="4">Overzealous prosecutors thwart justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>I was set up before my eyes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</i></b></P>
<P>I'm a pre-trial detainee at Cook County Jail in Chicago. I'm also one of five inmates severely beaten by racist guards on July 29, 2000 (<i>Socialist Worker,</i> September 1, 2000).</P>
<P>Fortunately, my time has come for trial so that I may prove my innocence. But unfortunately, a fair trial is unlikely in this unjust system.</p>
<p>Recently I was in court for a morning hearing that was rescheduled for the afternoon. While awaiting my turn, a prosecutor in my case who didn't recognize me came into the waiting room and committed the ultimate criminal act--right in front of my eyes.</P>
<P>He instructed a Chicago cop to lie under oath in order to get my conviction.</P>
<P>I was in a state of shock. Although I'm aware that prosecutors do this to people on a regular basis, it was more devastating to see it before my own eyes.</p>
<p>After the prosecutor and cop finished working out their fabrication against me, the prosecutor wanted to know who I was--and found out that I was the guy! It was their turn to be shocked.</P>
<P>Eventually my attorney came, and I gave him the details of what just happened. My attorney gathered names and asked brief questions of everyone in the room. When my case was called, that prosecutor and police officer were nowhere to be found.</P>
<P>I wonder why. However, it's clear that malicious and sinister prosecutors are a major problem in this "unjustice" system.</P>
<P><b>Edward Mitchell,</b> Cook County Jail, Chicago</P>

