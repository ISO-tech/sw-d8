<P><font face="Times New Roman, Times, serif" size="4">U.S. ignores global warming threat</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dubya fiddles as the world burns</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SHERRY WOLF</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 16</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH is ignoring a plea by 1,800 scientists from 100 countries to "act now to head off potentially disastrous climate change." </P>
<P>Scientists heading to Bonn, Germany, for a summit conference on climate change set to begin July 16 fear that the threat of global warming may have been underestimated, not overestimated. More floods, droughts, storms and disease than originally projected could be on the way--and sooner than expected. </P>
<P>For example, as a result of melting Arctic ice caps, fresh water is flowing into the North Atlantic Ocean, affecting the Gulf Stream current. "That warm current moderates the European climate, and turning it off would make a swath of land from London to Stockholm miserable," according to the <I>Los Angeles Times.</I> </P>
<P>But in defiance of international opposition, the Bush administration rejects the Kyoto climate treaty that is designed to slow global warming. Instead, Bush wants to waste $120 million to study the issue--for three more years. </P>
<P>If the U.S. ratified the international treaty, negotiated in 1997 in Kyoto, Japan, it would have to reduce emissions of carbon dioxide and other greenhouse gases that cause global warming.</P>
<P>No way, says the Bush gang. Backed by oil and gas giants, they say that the treaty is "fatally flawed" and "arbitrary." Why? Because it would require rich countries that produce the most greenhouse gases to cap emissions and explore new technologies for reducing pollution.</P>
<P>The U.S. produces 25 percent of the world's greenhouse gases. But Bush and his energy industry pals want to duck their responsibility for the global warming crisis.</P>
<P>They claim that large but poor countries such as China and India aren't asked to do enough under the Kyoto treaty. But China has already managed to reduce its carbon dioxide emissions by 10 percent since 1997, according to Klaus Toepfer, executive director of the United Nations Environmental Program.</P>
<P>There's growing opposition within the U.S. to Bush's stonewalling. According to an April <I>Time </I>magazine/CNN poll, two-thirds of people want Bush to develop a plan to reduce the emissions of gases that cause global warming. And 69 percent said the government gives in to big business when it comes to the environment.</P>
<P>The United Steelworkers of America and Service Employees International Union are joining forces with the Sierra Club and the Union of Concerned Scientists to support clean energy proposals aimed at halting global warming. And the 20 million-strong International Federation of Chemical, Energy, Mine and General Workers' Unions in Europe is seeking a "just transition" to new technologies. Organized labor's opposition to Bush's energy policy is crucial.</P>
<P>Bush and his corporate cronies have tried to scare workers into thinking that jobs would be lost and the economy would suffer if the U.S. has to develop renewable energy resources, such as solar and wind power. In fact, more--and cleaner--jobs would be created in the transition to these newer industries. </P>
<P>The energy bosses are putting the future of the planet in jeopardy-and their servants in Washington are helping them do it. Don't let them get away with it.</P>

