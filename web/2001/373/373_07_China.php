<P><font face="Arial, Helvetica, sans-serif" size="2"><b>CHINA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Miners and families fight for back wages</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LIMIN YAN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SOME 10,000 Chinese miners and their families blocked a railroad track in the northeastern town of Jishu July 9 in a protest over unpaid back wages. The state-run mine owes workers an incredible 30 months of wages. </P>
<P>The miners began their protest at 1 a.m. and continued until noon, disrupting train service along lines that connect the major cities of the industrial hub of northeast China. </P>
<P>The demonstration was part of a growing number of miners' protests across the country. In a different town in the region in February, 20,000 miners and their families took to the streets to protest layoffs resulting from the closing of many state-run mines.</P>
<P>The mining industry in China has a terrible record. Since the beginning of this year, accidents have claimed more than 1,400 lives, and the government admits that more than 10,000 miners die each year from lung diseases. </P>
<P>A recent official report compared conditions in the mines to those of 18th-century Britain&#150;yet there are only a few thousand safety inspectors.</P>
<P>Coal is China's main source of energy, and the past two decades of economic liberalization have led to an economic boom that increased the demand for coal. Yet government-led restructuring of the mining industry has led to the closure of some 33,000 state-run mines. </P>
<P>China's rulers still use the rhetoric of socialism on occasion. But these attacks on workers show that China's system differs little from the "free market."</P>
<P>Analysts believe that China will lay off millions more workers to stay competitive in the world market. But the miners have shown that Chinese workers don't want to pay for the profits and power of the bosses and bureaucrats.</P>

