<P><font face="Times New Roman, Times, serif" size="5"><b><i>A.I.:</i> Spielberg's feel-creepy movie</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><b>MOVIES: <i>A.I.: Artificial Intelligence,</b></i> written and directed by Steven Spielberg, starring Haley Joel Osment, Jude Law, Frances O'Connor and William Hurt.</font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by DAVID WHITEHOUSE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DIRECTOR STEVEN Spielberg's films often rely on characters that serve only to carry the action along. You don't think of emotional complexity or depth when you remember the characters in <i>Jurassic Park</i> or <i>Jaws</i>--much less in <i>Saving Private Ryan.</i></P>
<P>But for once, wooden characters are just what this Spielberg movie needs.</P>
<P>The boy--robot at the center of <i>A.I.</i> is not only mechanical, but he was actually built to serve as a plot device in the life of his human "mother," Monica (Frances O'Connor).</P>
<P>Monica misses her son Martin, who is in a coma. But a replacement is available. Researcher Allen Hobby (William Hurt) is working on a robot that can love humans.</P>
<P>Once manufactured, David (Haley Joel Osment) turns out to be more than a machine but less than a real boy. This creepy tension is the pivot of the movie. In one moment, David is curious and childlike. In the next, he's machine-like and scary. David's pre-programmed "love" for Monica is endearing in the sappy way that we expect from Spielberg--but also oddly unchanging.</P>
<P>Meanwhile, David's "mother" may be flesh and blood, but she isn't fully human. She uses David just to replace Martin, who rouses unexpectedly from his coma. Monica never tries to understand David on his own terms--and caves in to her husband's panic when David accidentally endangers Martin's life. In a wrenching scene, Monica abandons an uncomprehending David in the woods.</P>
<P>Having learned about Pinocchio's dream of becoming a real boy, David resolves to become "real" in the hopes of winning his mother's love.</P>
<P>He's helped along the way by a robot gigolo named Joe. Jude Law's performance as Joe is the movie's highlight.</P>
<P>As a prostitute, Joe knows how humans regard their robots. "They don't love us," he tells David. "They love what we do for them."</P>
<P>Scenes like this show that <i>A.I.</i> isn't really about machines but about the dehumanized and exploitative relations among people.</P>
<P>Spielberg tries to tack on a happy ending with an improbable reunion between David and Monica. The encounter is pleasant, but Monica remains an empty character despite the way that Spielberg may want us to see her. She never understands David, and David ends up alone--forever.</P>
<P><i>A.I.</i> thus turns out to be more chilling--and much better--than the movie that Spielberg was probably trying to make.</P>

