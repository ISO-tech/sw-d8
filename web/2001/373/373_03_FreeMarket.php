<font face="Times New Roman, Times, serif" size="5"><b>How can they justify this suffering?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WHEN THE United Nations (UN) issued its annual <I>Human Development Report </I>in 1999, its findings were a stunning indictment of the world system.</P>
<P>The richest 225 people in the world had as much wealth as the combined annual income of 47 percent of the world's population, the study found--more than 3 billion people.</P>
<P>The report was blunt in its criticism of the free market: "When the market goes too far in dominating social and political outcomes, the opportunities and rewards of globalization spread unequally and inequitably--concentrating power and wealth in a select group of people, nations and corporations, marginalizing the others...When the profit motives of market players get out of hand, they challenge people's ethics--and sacrifice respect for justice and human rights."</P>
<P>This year's edition is a little different.</P>
<P>It praises technology--courtesy of the world's wealthiest corporations--as the best way to eliminate hunger and poverty. And in promoting the report, UN officials attacked global justice demonstrators who have focused attention on the free market's responsibility for global poverty and inequality.</P>
<P>"At last year's G8 summit [of the eight most powerful countries], protesters mocked international efforts to channel technology towards the needs of the poor," read the UN's press release about this year's report. "Information and communications technology and biotechnology can actually make major contributions to reducing world poverty."</P>
<P>No doubt. But as the report's own figures show, profit-hungry multinational businesses have no interest in using their enormous resources to fight poverty and its catastrophic effects.</P>
<P>In 1998, for example, some $70 billion was spent on health research worldwide. But just $300 million of that was spent on the fight against AIDS.</P>
<P>And although the report hypes the Internet as a path out of poverty, it admits that 2 billion people--one-third of the world's population--don't even have access to electricity. </P>
<P>The new report found that 1.2 billion people still live on less than $1 per day--virtually the same figure as in the 1999 report. Infant mortality rates are lagging or on the rise in countries that have 62 percent of the world's population.</P>
<P>The ugly truth about the system--and its deep-rooted problems--can't be masked by reports.</P>
<P>Free-market mania has plunged whole parts of the globe into chaos in recent years--with the financial panic that has pushed Argentina to the brink of defaulting on its foreign debt serving as the most recent example. </P>
<P>Outrage against these injustices spurred protesters to travel to the G8 summit on a hard-to-reach Japanese island last year. This month, demonstrators will confront the G8 in Genoa, Italy--with an expected 150,000 on the streets.</P>
<P>So when George W. Bush and his counterparts in the advanced countries spout their usual rhetoric about the marvels of the free market, they'll get a very loud response.</P>
<P>As we've seen in a series of international protests that began in 1999 in Seattle with demonstrations against the World Trade Organization, growing numbers of people around the globe are willing to challenge the injustices of the system. And efforts are already underway for another protest against the International Monetary Fund and World Bank in Washington, D.C., this fall.</P>
<P>Spin-doctoring aside, the UN <i>Human Development Report</i> can't hide a world of needless suffering--and the urgent need to build the movement for global justice.</P>

