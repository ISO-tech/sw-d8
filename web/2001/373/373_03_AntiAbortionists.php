<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Enough of these anti-abortion fanatics!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IN GEORGE W. Bush's head, it's okay that 43 million living Americans are without health insurance. But the "pre-born" are first on his list for federally funded health care.</P>
<P>A proposal drafted by the Department of Health and Human Services&#151;which is led by abortion opponent Tommy Thompson--would allow states to classify a fetus as a "targeted low-income child" eligible for coverage under the Children's Health Insurance Program.</P>
<P>"I don't know why anybody would be opposed to that," Thompson said innocently. "Can you imagine anybody that would be opposed to giving prenatal care to an unborn poor child, to give that poor child, especially a minority, a chance to enter this world healthy and ready to develop?" </P>
<P>Yeah right. If Bush and Co. were interested in the health of babies born to low-income women, maybe they would provide health care for women who can't afford it. Maybe Thompson would have thought twice before he drove women and children off welfare with his "reform" scheme as governor of Wisconsin.</P>
<P>The Bush gang has a different agenda. Like the Unborn Victims of Violence Act passed by the House in April, this proposal is about sneaking through legislation that gives fetuses the same rights as human beings. Or in this case, more rights.</P>
<P>Bush has thrown his friends on the Christian right a meaty bone. Many of these same anti-abortion fanatics are counting on their guy in Washington to keep his campaign promise to halt federal funding for embryonic stem cell research.</P>
<P>The cells--which usually come from embryos discarded by fertility clinics--play a key role in medical research into horrible diseases such as Alzheimer's, Parkinson's and heart disease. As <i>Socialist Worker</i> went to press, Bush had yet to make a decision on the question.</P>
<P>But opposition to stem cell research is so absurd that even anti-abortionists are at odds. Nancy Reagan, the wife of anti-choice President Ronald Reagan came out in favor, as did Sen. Orrin Hatch (R-Utah), no friend of a woman's right to choose.</P>
<P>The media focused on this bickering between right-wing cranks. But what's been lost is that 80 percent of the population supports stem cell research--because it could save lives and cure awful diseases.</P>
<P>"Without federal funding&#133;the research will proceed slowly," biologist David Anderson wrote in the <i>New York Times.</i> "And as the solutions are delayed, people will die who might otherwise have been saved." </p>
<p>Similarly, women's lives are at stake every time abortion rights are chipped away by talk about "fetal rights." We have to expose these crackpot proposals for the attacks on our rights that they actually are.</P>

