<P><font face="Times New Roman, Times, serif" size="4">Ruling excludes nurses from union drive</font><br>
<font face="Times New Roman, Times, serif" size="5"><b><I>Supreme Court v. workers' rights</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The U.S. Supreme Court took a shot at workers' right to organize in a decision that excludes some nurses from union certification votes. </P>
<P>Voting by the same 5-to-4 split as in their decision last year that handed George W. Bush the White House, the justices ruled May 29 that some registered nurses who cast a vote for union representation at a nonprofit Kentucky mental-health facility shouldn't have counted.</P>
<P>In its suit, Kentucky River Community Care claimed that six nurses who voted in the election were supervisors because part of their job involved overseeing other workers. </P>
<P>The National Labor Relations Board (NLRB) had decided that the nurses weren't supervisors because they didn't have the power to discipline, fire or hire. This allowed the six to vote with about 100 other workers. </P>
<P>But the Supreme Court's decision upheld an appeals-court ruling that overturned the NLRB.</P>
<P>The decision spells trouble for nurses and doctors who are turning in larger numbers to unionization. Long considered "professionals," they are looking to organize in the face of overstaffing and lack of control over working conditions.</P>
<P>In response to the decision, the American Medical Association (AMA) dropped plans for union drives for doctors at 4,000 hospitals around the U.S. The decision "will almost certainly make it more difficult, if not impossible, for most employed physicians in the private sector to use collective negotiations as an advocacy tool for addressing important patient-care and workplace concerns with their employers," said AMA board member Dr. Donald Palmisano.</P>
<P>The anti-union ruling will especially hit nurses who work at facilities such as nursing homes, where organizing drives involve a broader range of workers. The American Nurses Association--which is considering affiliating with the AFL-CIO--could face problems as a result. </P>
<P>It will take determined organizing to beat back the health care bosses--and their union-busting friends in Washington.</P>

