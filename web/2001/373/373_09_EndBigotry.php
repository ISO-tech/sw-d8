<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can we end bigotry through education?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE <i>HOUSTON Chronicle</i> recently reported on a "No Place for Hate" campaign in local schools in which young kids are taught to respect diversity. A Hate Awareness Quilt bearing the names of James Byrd and Matthew Shepard--two victims of racist and anti-gay hate crimes--hangs in the Lanier Middle School library. The hope is that if children learn early enough, they will reject bigotry for life.</P>
<P>Such programs fit with the ideas of many people outraged by racism, sexism or anti-gay bigotry, who believe the solution is more "education." They start from a positive place in believing that people's ideas can change--in contrast to the old clich&eacute; that "you just can't change the way some people think."</P>
<P>Diversity programs no doubt mean a lot for the kids who participate, and more schools need to take up these issues. But can education overcome bigotry?</P>
<P>While it would be a big step forward for schools to teach kids about the Black Power or gay liberation movements, this isn't enough. Racism has an impact throughout society--from racial profiling by police to discrimination on the job and more.</P>
<P>That's why socialists believe that backward ideas are like weeds--you have to get at the roots in order to keep them from sprouting again.</P>
<P>And the truth is that racism and sexism are rooted not in human nature but in the structure of the system we live under--capitalism. Our rulers depend upon sowing divisions among working people--to divert their attention from their real enemy and to keep them from uniting to fight back.</P>
<P>The bigoted ideas that some people accept don't pop magically into their heads. Rather, they're wrongheaded conclusions drawn from the concrete circumstances in which people live.</P>
<P>Racism, for example, isn't just about what's shown on TV or taught in schools but also the fact that workers are forced into competition with each other. Sometimes this is obvious--as when American employers have used Blacks as strikebreakers, for example.</P>
<P>But simply getting by in a dog-eat-dog world pits working people against one another. "Competition separates individuals from one another, not only the bourgeois but still more the workers, in spite of the fact that it brings them together," wrote Karl Marx. "Hence every organized power standing over and against these isolated individuals, can only be overcome after long struggles.</P>
<P>"To demand the opposite would be tantamount to demanding that competition should not exist in this definite epoch of history, or that the individuals should banish from their minds conditions over which in their isolation they have no control."</P>
<P>Marx didn't believe that workers were empty vessels to be filled up with good or bad ideas. He insisted that workers have the power to change the world--and in their struggles for change, they transform themselves as well.</P>
<P>Having to take up a fight means having to confront bad ideas or wrong beliefs--not only bigotry but also feelings of apathy and powerlessness--that get in the way. You can often see this in small struggles like strikes--where workers who were kept isolated from one another by management come together and learn what they have in common.</P>
<P>This doesn't happen automatically. It's important for individuals committed to fighting oppression and injustice to take on backward ideas whenever they come up.</P>
<P>But the logic of any struggle pushes people to confront backward ideas--and come up with new ways of viewing their fight and the world around them.</P>
<P>Contrary to the attitude that "workers have to be educated" out of their bad ideas, socialists recognize that what workers do is the key to abolishing oppression. It's workers who are central, not an enlightened few who stand above them and teach them.</P>
<P>As Marx wrote, "The revolution is necessary, therefore, not only because the ruling class cannot be overthrown any other way, but because the class overthrowing it can only in a revolution succeed in ridding itself of all the old crap and become fitted to found society anew."</P>
<P>Struggle is surely the best "educator" of all. </P>

