<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The right stuff on Fox</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 6</font></p>

<P><font face="Times New Roman, Times, serif" size="3">FOX NEWS claims to deliver objective reporting--"We report, you decide" is one of its slogans. But a study of <i>Special Report with Brit Hume,</i> Fox News' marquee news show, found that of 56 partisan figures interviewed in a five-month period, 50 were Republicans and six were Democrats--a greater than 8 to 1 ratio.</P>
<P><font face="Times New Roman, Times, serif" size="3">In fact, right-wingers outnumbered all other points of view, including nonpolitical guests, by a factor of more than 2 to 1. The lack of diversity in political opinion was matched by a lack of diversity among guests--only eight women and six people of color appeared on the show, adding up to a roster of "experts" that was 91 percent male and 93 percent white.</P>
<P>Fox News has an editorial mission to provide a counterweight to the supposed dominance of the media by "liberal" political voices. But this notion of liberal dominance of the media is as mythical as Fox's objective news coverage. Of 67 partisan guests interviewed by CNN's Wolf Blitzer, 38 were Republicans and 29 Democrats.</P>
<P>But Fox News anchors are also known for going beyond extending invitations to conservative guests--and doing their own right-wing ranting.</P>
<P>During the presidential campaign, Bill O'Reilly, host of <i>The O'Reilly Factor,</i> mused about whether Al Gore had "a quasi-socialistic platform" with "work and production being supervised by the government." </P>
<P>"President Bush ran on the slogan 'reformer with results,'" said O'Reilly. "That sounds good to me."</P>
<P>--Fairness and Accuracy in Reporting, July 2, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Gap direct action?</font></P>
<P><font face="Times New Roman, Times, serif" size="3">GAP IS trying out a new promotional campaign to tap into young consumers. As protesters have taken to the streets in Seattle, Washington, D.C. and Quebec City to protest corporate globalization, Gap has identified&#133;a marketing strategy.</P>
<P>With faded black jeans hanging in front of a red banner and the words "independence," "freedom" and "We, the People" scrawled in fake black spray paint across their store fronts, Gap introduced their new advertising strategy.</P>
<P>Gap is notorious for hiring sweatshop workers for pennies an hour, denying them basic health care and the right to form unions. Although many of Gap's clothes say "Made in the USA," most are produced in Saipan, a U.S. territory where U.S. labor laws don't apply. Apparently, "freedom" and "independence" apply more to their ability to make profits than to the workers who make them profitable.</P>
<P>--Independent Media Center Web site, June 20, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">George W. Bush blunders again</font></P>
<P><font face="Times New Roman, Times, serif" size="3">IN EARLY July, Bush stumbled through New York for the first time since he lost that state by nearly 25 points to Al Gore last November. Bush took the occasion to lead 29 new citizens in the Pledge of Allegiance at a naturalization ceremony stuffed to the gills with bigwig politicians and corporate backers.</P>
<P>Though his two public appearances in New York totaled no more than 20 minutes combined, he still managed a few good gaffes.</P>
<P>When Bush stood to lead the new Americans in the Pledge of Allegiance, he confused the Pledge with taking oath at the witness stand. "Right hand up, please," he said to the soon-to-be-citizens, raising his arm but then stopping himself. "Actually, right hand on your heart."</P>
<P>Moments later, Bush was posing for photographs with a handful of Democrats and Republicans in front of New York harbor when a reporter called out, "How do you like New York, Mr. President?" "It's a beautiful day," Bush responded tersely.</P>
<P>Even fellow Republicans--including New York City Mayor Rudolph Giuliani and Gov. George Pataki--froze for about five awkward seconds. After Bush reconsidered his response, he blurted out, "I love New York"--to the evident relief of his hosts.</P>
<P>During his trip, Bush also had to contend with 500 protesters who held signs reading, "He's not my president," and chanting, "Hey hey, ho ho, Bush and Cheney have got to go."</P>
<P>--<i>New York Times,</i> July 11, 2001<br>
