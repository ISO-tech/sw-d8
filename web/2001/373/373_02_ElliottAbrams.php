<P><font face="Times New Roman, Times, serif" size="4">Guess who's back in Washington?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Reagan's loyal war criminal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LANCE SELFA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The Bush gang must have had a good laugh when they picked Elliott Abrams for the National Security Council's office of democracy, human rights and international operations.</P>
<P>Using Abrams and the words "human rights" and "democracy" in the same breath is a sick joke.</P>
<P>As head of the State Department's Latin America desk during the reign of Ronald Reagan, he smugly defended the most reactionary and bloodthirsty thugs in Central America and went around the law to arrange his boss's dirty wars.</P>
<P>For anyone who dared to question U.S. intervention in Central America, Abrams was ready with smears and red-baiting.</P>
<P>For example, when <i>New York Times</i> and <i>Washington Post</i> journalists published stories about the 1982 El Mozote massacre in El Salvador---where death squads murdered an entire village suspected of sympathizing with left-wing rebels---Abrams accused the reporters of fabricating the story because they sympathized with the guerrillas. In response, the <I>New York Times</I>--that bastion of journalistic integrity--"reassigned" reporter Raymond Bonner.</P>
<P>In 1991, El Salvador's Truth Commission vindicated Bonner when it established that death squads killed nearly 800 people in and around El Mozote--almost half of them children.</P>
<P>All told, Abrams' buddies in the Salvadoran military and paramilitary death squads killed more than 40,000 civilians between 1980 and 1989. The death toll in Nicaragua was 40,000 or 50,000--and in Guatemala, 200,000.</P>
<P>But to Abrams, the Reagan administration's record in Central America was a "fabulous achievement."</P>
<P>In his new job, "this ethically challenged zealot"---as the <i>Philadelphia Inquirer</i> called Abrams in a recent editorial---will be dispatched to lecture other countries about "the rule of law."</P>
<P>But Abrams never practiced what he'll be preaching.</P>
<P>From 1984 onward, Congress barred U.S. government assistance for the murderous right-wing contras fighting the Nicaraguan government. To evade that restriction, Abrams and his cohorts--like Lt. Col. Oliver North and, almost certainly, then-Vice President George Bush--set up a secret program to aid the contras anyway.</P>
<P>The conspiracy began to unravel in 1986 when the Nicaraguan government shot down a CIA-linked cargo plane that was delivering arms to the contras. The administration dispatched Abrams to appear on television and testify in Congress under oath that the U.S. government had nothing to do with the flight. "That would be illegal," Abrams piously stated in one interview.</P>
<P>In a 1991 plea bargain, Abrams pleaded guilty to misdemeanor charges of misleading Congress. But a year later, then-President Bush pardoned Abrams, along with the other high-ranking Contragate crooks.</p>
<p>Back in the 1980s, Sen. Thomas Eagleton (D-Mo.) told Abrams that his dishonest testimony made Eagleton "want to puke."&#9; And not a lot about Abrams has changed since then.</P>

