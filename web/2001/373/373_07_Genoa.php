<font face="Times New Roman, Times, serif" size="4">G8 prepares for Genoa</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protesters vow to defy crackdown</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 7</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">MORE THAN 150,000 protesters are expected to march on the leaders of the world's most powerful countries when they meet this month in the Italian city of Genoa.</P>
<P>The demands of the umbrella protest group of 700 organizations, the Genoa Social Forum, include dropping the debt for developing countries, rights for immigrants, adoption of the Kyoto Protocol on global warming, funding to reduce world poverty and an end to the political dominance of multinational corporations.</P>
<P>The response of the right-wing Italian Prime Minister Silvio Berlusconi--along with his counterparts in the U.S., Britain, Germany, Canada, Japan, Russia and France--has been to mobilize for a massive show of force for the meeting, which will run from July 20-22.</P>
<P>As many as 16,000 cops will be on hand--armed with tear gas, water cannons and batons--to protect a "red zone" in the city center where all protests are banned.</P>
<P>Border authorities will suspend enforcement of a treaty that allows free passage from most neighboring European countries. Genoa's airport will be closed for the weekend, along with an elevated highway, a subway and much else.</P>
<P>More than 2,700 military troops will be deployed&#150;and authorities have ordered 200 body bags and built a temporary mortuary. Incredibly, even a missile defense system has been deployed--supposedly to stop a threatened terrorist attack.</P>
<P>But the danger isn't from protesters, but from the repression of authorities. At the European Union summit in Sweden last month, cops shot three protesters with live ammunition, injuring one critically. And just days later in Barcelona, Spain, police attacked a peaceful rally against the World Bank. </P>
<P>Opinion polls show that 50 percent of Italians sympathize with the protests. Meanwhile, Italian workers are flexing their muscle in a battle with employers. More than 100,000 workers went on strike July 5--including metalworkers, air traffic controllers, pilots and flight attendants--in fights over wages.</P>
<P>A big turnout for the G8 protests--and more action by workers--could give a boost to new struggles in Italy and across Europe in the weeks and months ahead.</P>

