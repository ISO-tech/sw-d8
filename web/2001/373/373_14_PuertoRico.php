<P><font face="Times New Roman, Times, serif" size="4">Feds target Puerto Rican student for anti-ROTC protest</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Drop the charges against Pedro Col&oacute;n!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ROBERTO BARRETO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 14</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN JUAN, Puerto Rico--A student at the University of Puerto Rico (UPR) is being targeted by the federal government for the "crime" of protesting. </P>
<P>In early July, a federal grand jury indicted Pedro Col&oacute;n on charges of assaulting a ROTC officer at a campus demonstration in April.</P>
<P>But this is clearly an attempt to criminalize protest. Pedro is a student leader and member of the Uni&oacute;n de Juventudes Socialistas (Union of Young Socialists). Bond was set at $5,000, and if convicted, Pedro faces a sentence of up to five years in prison.</P>
<P>Last April's demonstration at UPR was organized to prevent ROTC cadets from conducting exercises on campus. It was held at the same time as protests on the island of Vieques to demand that the U.S. Navy stop its target practice and get out.</P>
<P>ROTC cadets ended their exercises on the UPR running track, and demonstrators moved on to picket the ROTC building. Two ROTC officers approached the 100 young protesters in a threatening manner. The students defended themselves from the officers, who received only a few scratches. </P>
<P>But now Col&oacute;n is facing assault charges--leveled by the federal grand jury, which historically has been used in Puerto Rico against independentistas and socialists. </P>
<P>Though the alleged "assault" took place at a state university, the investigation was conducted by federal agents--including FBI agent V&iacute;ctor Franco, who has a record of building secret files on independentistas and other activists. And Col&oacute;n's case will be heard by U.S. District Judge Hector Lafitte, who has a long history of persecuting the left.</P>
<P>Everyone knows that this case is being used to intimidate students, who have played an important role in the struggle to kick the Navy out of Vieques. Fellow students and other activists are organizing a campaign to demand that the charges against Pedro be dropped.</P>

