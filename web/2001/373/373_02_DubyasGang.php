<p><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Going to bat for the anti-gay bigots</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 20, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The Bush administration retreated in July after its plan to allow religious charities to break laws prohibiting discrimination against gays was exposed.</P>
<P>According to the <i>Washington Post,</i> the Salvation Army--the country's largest religious charity--went to its buddies in the Bush White House to ask for a federal regulation to protect it from state and local laws barring workplace discrimination.</P>
<P>The White House made a "firm commitment" to issue the regulation, according to a Salvation Army document obtained by the <i>Post.</i> In return, the charity promised to spend almost $1 million lobbying Congress to pass Bush's so-called "faith-based" initiative to shift government social services to private control.</P>
<P>The White House caved fast after the story came out. But the scandal underlined the ties between the Bush gang and the Christian right. Salvation Army officials went straight to the top--reportedly going through White House puppet-master Karl Rove, Bush's leading political adviser.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">The right to bear machine guns</font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--It's a voluntary agreement, and even United Nations (UN) Secretary General Kofi Annan admits that its "not going to be as strong as we would have liked."</P>
<P>But the Bush gang isn't compromising on the sacred right to bear...mortars and anti-tank missiles. </P>
<P>At a UN-sponsored conference in July to negotiate restrictions on the International trade in small arms and light weapons, U.S. officials declared that the Bush gang was dead set against the agreement. Undersecretary of State Robert Bolton told attendees that the U.S. has a proud "cultural tradition" of gun ownership and would never sign an agreement "contrary to our constitutional right to bear arms."</P>
<P>To underline the point, right-wing screwball Rep. Bob Barr (R-Ga.), a board member of the National Rifle Association, was part of the delegation.</P>
<P>Of course, the UN conference is mainly aimed at weapons used in military and paramilitary conflicts--like grenade launchers, assault rifles, mortars, shoulder-fired, anti-tank missiles and heavy machine guns.</P>
<P>But in a repeat of last year's opposition to a ban on land mines, the U.S.--which happens to be the world's largest exporter of small arms--is having none of it.</P>

