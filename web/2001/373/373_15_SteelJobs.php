<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why concessions won't save steel jobs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2001 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE UNITED Steelworkers of America (USWA) this month agreed to take massive concessions in wages and benefits in an effort to keep bankrupt steelmaker LTV Corp. from shutting down for good.</P>
<P>The deal will have a major impact on the steel industry, which has seen 19 bankruptcies and more than 15,000 jobs lost since 1997. And with layoffs mounting in unionized manufacturing companies, the concessions will encourage employers in other industries to demand givebacks from their unions, too--just as they did in the 1980s and early 1990s.</P>
<P>LTV had threatened to close its doors for good unless the UWSA agreed to a freeze in wages and a 31 percent cut in health care benefits for 7,700 active workers and 56,000 retirees.</P>
<P>An alternative deal--negotiated by the USWA with LTV's creditors over the heads of management-would cut 1,300 jobs. It would include a big loan to the company from a union-run retiree health benefit plan and a freeze in wages--and give union members ownership of 20 percent of company stock.</P>
<P>The agreement--which must be approved in bankruptcy court before it can be submitted to union members for a vote--was hailed by USWA President Leo Gerard, as was an agreement by the Bush administration to investigate a surge in low-priced imported steel into the U.S.</P>
<P>"This agreement creates opportunities for our members to turn a sow's ear into a bit of a silk purse," Gerard said. </P>
<P>But the bitter truth is that both concessions by the USWA and import restrictions over the last 20 years have failed to save steel jobs. And LTV's own history of attacks on the USWA shows this all too clearly. </P>
<P>Created in the 1970s from the merger of second-tier operators, LTV first filed for bankruptcy in 1986 and tried to terminate health benefits. A strike at a key Indiana plant forced the company to retreat--and pressured Congress into passing a law that companies must get a bankruptcy judge's permission before tearing up its agreements with unions.</P>
<P>But that was only the beginning of the struggle. The company instituted a new pension plan--but a years-long legal battle between LTV and the government kept pension funds in question for years.</P>
<P>Meanwhile, LTV slashed employment from 40,000 to 16,000 by 1992. Then, it sought a bankruptcy court's permission to tear up all of its union contracts. </P>
<P>Under pressure, the USWA agreed to new concessions--including a managed health care plan and work rules changes--in exchange for 7 percent of stock and a seat on the company's board. But today, there are just 10,600 union jobs remaining at LTV. </P>
<P>The pattern is the same across the industry. There are less than 150,000 steelworkers in the USWA, compared to 400,000 in 1980.</P>
<P>But the steel bosses cashed in during the boom years of the 1990s. According to <I>BusinessWeek, </I>the value of steel output in the U.S. surged from about $167 billion in 1995 to $178 billion in 2000, while industry-wide employment declined by 50,000. </P>
<P>Even during the height of the "import crisis" of 1998, U.S. steel mills shipped 102 million tons, the second highest total in 25 years. </P>
<P>During those years, steelworkers tried to make up for the concessions and layoffs of earlier years by working massive amounts of overtime.</P>
<P>Employers cleaned up while they could. Just last December, LTV management handed out $31 million to top executives-including $1.4 million for former CEO Peter Kelly when he retired. </P>
<P>What's more, some 20 percent of imported steel is purchased by U.S. steelmakers themselves to be used in other products. </P>
<P>And just what is "American" steel, anyway?  National Steel is owned by the Japanese firm NKK. Inland Steel is owned an Indian company, Ispat International, while U.S. Steel owner USX also owns a Japanese steel company. </P>
<P>Import controls and concessions haven't saved steelworkers' jobs. They've been used as clubs to beat the unions into giving back even more. If there's one lesson to learn from organized labor's defeats in the 1980s, it's that concessions only lead to more concessions.</P>

