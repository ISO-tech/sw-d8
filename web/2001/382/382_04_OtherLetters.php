<p><a name="back"></a><font face="Times New Roman, Times, serif" size="4"><b>Socialists should back this war</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I am a socialist, and I believe in most of the things other socialists believe in, but this war is the one case where I must disagree.</P>
<P>There is strong proof that Osama bin Laden was responsible for the World Trade Center attacks; he even confessed to it!</P>
<P>We sat on our hands when the two U.S. embassies and the warship were bombed, and now look what has happened.</P>
<P>The government of Afghanistan knows about all of these things, and they still will not hand bin Laden over to us.</P>
<P>If we let this go, bin Laden will bomb cities next. We have got to protect our nation and make it safe again.</P>
<P>I am against violence and war myself. But they are necessary in this case.</P>
<B><P>Devin Love-Andrews,</B> sent via e-mail</P>

