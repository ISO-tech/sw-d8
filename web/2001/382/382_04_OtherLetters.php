<P><font face="Times New Roman, Times, serif" size="5"><b>Stepping up the war on immigrants</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>This morning, one of my coworkers who lives in Tijuana told me that he waited from 2:30 a.m. to 5:30 a.m. to cross into San Diego, when it usually only takes minutes.</P>
<P>Since the attack on the World Trade Center and the Pentagon, Mexican workers who rely on jobs in the San Diego area have been suffering. Long lines, intrusive searches and frequent closures of the border are adding aggravation and humiliation to the days of most Mexican workers here.</P>
<P>Meanwhile, the environment of hysteria has given confidence to every right-wing and nationalist crank out there to exploit the tragedy by targeting immigrants.</P>
<P>Besides reports of attacks on Muslims and Arabs (which are hardly mentioned in the mainstream press), racists are going after Mexican Americans. A family in San Diego county that was flying a Mexican flag in celebration of Mexico's Independence Day had a rock thrown through their window--with a note that said, "How dare you fly a foreign flag on a day like today?"</P>
<P>I'm sure that I'm joined by many, many people in my disgust at this kind of racism. The right may feel confident now, but we can discredit them before a public that rejects this kind of racist trash.</P>
<B><P>Chuck Stemke, </B>San Diego</P>

