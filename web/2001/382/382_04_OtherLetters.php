<p><a name="Nazis"></a><font face="Times New Roman, Times, serif" size="4"><b>Targeted for opposing Nazis</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>"We may not want them marrying our daughters, but anyone who is willing to drive a plane into a building to kill Jews is alright by me." This is what Billy Roper, deputy membership coordinator of the neo-Nazi National Alliance said about the September 11 tragedy.</P>
<P>On July 29, many D.C. activists, myself included, helped organize a counter demonstration to the National Alliance's rally at the German Embassy.</P>
<P>These neo-Nazis tried to portray their demonstration as a rally for "free speech" in Germany and for the freedom of so-called "political prisoner" Hendrik Moebus (an admitted neo-Nazi who strangled a 15-year-old boy to death in Germany in 1994 because the boy wasn't "Aryan").</P>
<P>As the demonstration started, a scuffle broke out, and Roper got a well-deserved gash over his eye. I felt no mercy for this guy, but I also didn't injure him.</P>
<P>About 45 minutes into the demo, the cops pulled me out of the crowd and illegally asked Roper to identify me. He did so, even though he knew I was innocent. Me and another kid got charged with "simple assault."</P>
<P>My case looks pretty good, but the cops had no right to arrest me.</P>
<P>On top of all that I said, these scumbags are planning to hold a rally at the Israeli embassy three days after my trial. I am no friend of Israel--a state that is racist to the core--but I am no anti-Semite, either. If this means I have to defend the Israeli embassy against Roper and the rest of his crew, so be it.</P>
<B><I><P>SW </I>reader,</B> Washington, D.C.</P>

