<font face="Times New Roman, Times, serif" size="5"><b>Police attack antiwar march</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson and Sarah Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>POLICE ATTACKED an antiwar demonstration of more than 200 students and peace activists in Hartford, Conn., on October 26.</P>
<P>Although the march was peaceful, police claim that a few protesters strayed from the sidewalk. In response, the cops began using batons and pepper spray on marchers.</P>
<P>Vittorio Lancia was beaten in the head until he was bloody and was repeatedly pepper-sprayed, although he was curled up on the street yelling, "I have asthma!"</P>
<P>Seventeen demonstrators were arrested and taken to jail on charges ranging from disorderly conduct to breach of peace. When 40 protesters went to police headquarters to demand the release of their fellow activists, Wesleyan University student Adam Hurter was arrested--and charged with "conspiracy to incite a riot."</P>
<P>In a clear effort to discourage protest, bail for the demonstrators was set at between $15,000 and $25,000--and a judge raised the amounts even more at their arraignment. "The police oppression here in Hartford is a sign of the hypocrisy of the U.S. leaders calling the United States the land of the free," Hurter said.</P>
<P>The activists say they won't be intimidated. And their determination was matched by protesters across the country in recent days. People turned out for spirited protests in both Oakland, Calif., and New York City in late October that numbered approximately 2,000 each. Elsewhere, hundreds turned out for vigils, teach-ins and organizing meetings.</P>
<P>At a teach-in at a New York City church, well-known African American author and activist Manning Marable told a crowd of more than 200, "Here at the Riverside Church in 1967, Martin Luther King came out against the Vietnam War. He didn't say, 'I am going to wait till all of America sees that it's not a just war.' He took a stand and that's what we have to do today, no matter how unpopular this is."</P>

<P><font face="Times New Roman, Times, serif" size="4">CCNY activists say no to witch-hunts</font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Alexis Logsdon</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STUDENTS AND faculty at the City College of New York (CCNY) are fighting back against a witch-hunt of opponents of the U.S. war. On October 22, students from campuses across the city rallied outside a Board of Trustees meeting to protest a resolution that condemns CCNY professors who took part in a teach-in against the war.</P>
<P>The right-wing <I>New York Post </I>has led the attack on antiwar activists, calling CCNY a "breeding ground for idiots" in a story that distorted the facts about the teach-in.</P>
<P>In their resolution, trustees called the teach-in "un-American" and "seditious." But members of the CCNY Coalition for Peace have vowed to fight back against this clampdown on free speech.</P>

