<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>From hype to horrors</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"IS THIS baby a Taliban fighter?" That was the caption that <I>Akhbar el Yom,</I> one of Egypt's biggest newspapers, gave to a photo showing an Afghan child whose family was killed by U.S. bombs.</P>
<P>The comment captured the growing anger with Washington's war on Afghanistan as its true cost is exposed.</P>
<P>After a month of nonstop air strikes, the number of civilian casualties has climbed into the thousands. Almost every day brings stories of a new "mistake"--like the second air strike last week on a clearly marked Red Cross warehouse.</P>
<P>When the war started, pundits predicted that the Taliban would surrender in a matter of weeks, if not days. Now a top British military chief says that the war may go on for four&#133;<I>years!</P>
</I><P>The much-hyped first U.S. ground attack in Afghanistan--a Special Operations assault carried out in mid-October, mainly for public relations purposes--nearly ended in disaster, with American commandos beating a hasty retreat, according to reports emerging in the British media.</P>
<P>Meanwhile, plans for a post-Taliban government of U.S. stooges are stalemated--with a summit meeting of opposition warlords put off until "sometime before the end of November," according to a Northern Alliance leader.</P>
<P>The reality of this war is exposing Washington's rhetoric about justice and democracy--and fueling outrage within Afghanistan and around the region. "Instead of a thankful Afghan population, popular support for the Taliban appears to be solidifying," the <I>Wall Street Journal</I> admitted.</P>
<P>Little of this questioning has leaked into the gutless U.S. media. And Democrats in Washington have fallen in line behind Bush at every turn.</P>
<P>Because of this, many people in the U.S. with doubts about Washington's war may still feel isolated. But the U.S. slaughter in Afghanistan--and the relentless war at home--will only cause more polarization.</P>
<P>If demonstrations have been smaller recently than during the opening week of the war, veteran antiwar activists say there is an unprecedented "appetite for information," as author Howard Zinn put it.</P>
<P>The challenge facing the antiwar movement is to reach out and answer the questions of this growing audience--to turn doubts into anger, and anger into action. That means building our presence on campuses, in unions and workplaces, and in communities--and spreading this struggle to every corner of society.</P>

