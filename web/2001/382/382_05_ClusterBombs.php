<font face="Times New Roman, Times, serif" size="5"><b>Pentagon's weapon of mass terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"SOME OF the most savage weapons of modern warfare." That's how one BBC journalist described cluster bombs, the U.S. military's latest weapon of choice in Afghanistan.</P>
<P>Each 1,000-pound bomb contains up to 200 individual "bomblets" that are designed to scatter over an area the size of 20 football fields when the bomb is released. On impact, the bomblets explode into 300 fragments of jagged shrapnel capable of penetrating the armor of tanks--and of tearing human beings to pieces.</P>
<P>"Traditional mines may remove an arm or a leg," the <I>Times</I> of London reported. "A cluster bomblet is a killing field in a canister. It is designed to massacre anything within 100 feet."</P>
<P>Informal estimates suggest that cluster bombs miss their target areas as often as 50 percent of the time, the <I>Times</I> reported. And as many as one in eight bomblets fall to earth without exploding on impact--becoming the most lethal land mines known.</P>
<P>A civilian is killed once a week in Kosovo by a cluster bomblet left over from the 1999 NATO war. And one person dies every month in Laos from a leftover bomblet--from cluster bombs dropped more than 30 years ago.</P>
<P>Residents of the Afghan village of Shakar Qala know firsthand the destructive power of cluster bombs. Last week, a U.S. cluster bomb went off course and exploded in the village. Of 45 homes in the village, 20 were partially or completely destroyed, according to the United Nations.</P>
<P>Eight people were killed in the attack. And a ninth, said UN spokesperson Stephanie Bunker, "was killed--as happens in these cases--when he went to look at [an unexploded bomblet], touched it and blew up."</P>
<P>Afghanistan was already the world's second most heavily land-mined country. The U.S. government's cluster bombs are adding to the horrors--both now and for decades to come.</P>

