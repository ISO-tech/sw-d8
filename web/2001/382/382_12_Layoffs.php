<font face="Times New Roman, Times, serif" size="4">Corporations use crisis as excuse to slash jobs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Layoffs USA</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>LEE SUSTAR reports on Corporate America's job massacre.</P>
</B>
<P>LAYOFFS ANNOUNCED this year have already exceeded the levels of the 1990-91 recession. According to Challenger, Gray &amp; Christmas, a company that tracks reports of job cuts, U.S. employers announced nearly 600,000 layoffs in the third quarter alone. And as <I>Socialist Worker</I> went to press, unemployment was expected to top 5 percent in government reports to be issued November 2.</P>
<P>Much of the rise in joblessness has been blamed on the September 11 attacks, with the travel and airline industries particularly hard hit. But job losses were mounting long before, as the U.S. sank into recession.</P>
<P>About 1 million manufacturing jobs have been eliminated over the last year. "What's more, layoffs have spread well beyond manufacturing and high-tech firms, which have been in retreat for the last year," the <I>Los Angeles Times</I> reported October 23. "In the last two weeks, a broad range of service companies, including McDonald's, Fidelity Investments, Kinko's and Pebble Beach Co., have been among about 200 employers that have announced cuts of at least 85,000 jobs, according to compilations by JWT Specialized Communications, an employment and marketing firm."</P>
<P>Initial claims for unemployment benefits shot up to 538,000 in the last week of September, the equivalent of about 1.6 percent of the total labor force. According to Economy.com, that percentage of workers is higher than in 1990, when the last recession began.</P>
<P>But the number of laid-off workers who need assistance is even greater. Today, just 39 percent of all employees are even eligible for unemployment benefits.</P>
<P>In New York City alone, about 25,000 workers have applied for Disaster Unemployment Assistance, an emergency benefit system for those who aren't eligible for regular unemployment benefits and who lost their jobs as the result of the World Trade Center attack.</P>
<P>Those who do get the standard 26 weeks of unemployment insurance face other problems--like choosing between continuing health insurance and rent. That's because the COBRA law allows workers to maintain their medical coverage for up to 18 months--but only if they pay the full cost of the premium plus a 2 percent administration fee.</P>
<P>"If you're losing your income, chances are you're not going to be able to come up with the $7,000 you need to cover your family's health insurance needs," Gail Shearer of the Washington, D.C.-based Consumer Union told the <I>San Francisco Chronicle</I>.</P>
<P>Other laid-off workers--like those whose employers closed--don't even have the option of getting COBRA. As a result, health insurance experts predict that many more people will soon be added to the approximately 45 million who lack coverage today.</P>
<P>You might expect that the government and employers would be gearing up to help those who lost their jobs--in the name of the "national unity" that we've heard so much about since September 11.</P>
<P>Instead, bosses are taking advantage of the situation. "I can now cherry-pick," said Matt Prentice, president of the Unique Restaurant Corp. near Detroit.</P>
<P>In New York City, employers who normally pay $2,000 to $3,500 for booths at job fairs got to set up for free at the second Twin Towers Job Fair last week. Then they lied to the city about the number of people that they would hire from among the 10,000 people who showed up.</P>
<P>According to the <I>New York Times,</I> Buck Consultants, a subsidiary of Mellon Financial, was reported to have 30 "prospective hires"--but later admitted that only three got jobs.</P>
<P>Talk is cheap. Laid-off workers need jobs--now. An economy that is rich and powerful enough to mobilize billions of dollars in military equipment and troops to go thousands of miles away is capable of providing work for everyone who needs it.</P>
<P>Millions of people could be put to work fixing our crumbling schools, for example. And more could be employed rebuilding a public health system that's been wrecked by HMOs.</P>
<P>But if the employers get their way, they'll use September 11 as an excuse for job cuts to protect their profits--no matter how many millions of lives they wreck. We can't let them get away with it.</P>
