<p><a name="Seattle"></a><font face="Times New Roman, Times, serif" size="4"><b>Seattle Pacific Science Center</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Nat Gibbons</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--The Seattle Pacific Science Center, after having its most successful summer ever, laid off nearly one-quarter of its staff. The museum--whose mission is supposed to be educating its young visitors--eliminated its entire Visitor Education department in the layoffs.</P>
<P>Executive director George Moynihan blamed the layoffs on a February earthquake, the September 11 attacks and the recession. Meanwhile, across the water in Bellevue, the Science Center has a multimillion-dollar expansion project underway.</P>
<P>Workers received no advance notice of the layoffs.</P>
<P>The directors expected these employees to hang their heads and walk away from jobs they loved. Instead, the workers organized a rally, handing visitors an informational flyer with the executive directors' phone numbers.</P>
<P>Despite the obvious attempt to intimidate them, former educators talked to visitors and members, many of whom turned around and went to other places once they heard what had happened.</P>
<P>About 40 former employees and friends attended the rally. In a final gesture, the workers threw their uniforms over the museum gate into the ponds.</P>
<P>The Pacific Science Center, although a not-for-profit foundation, follows the same formula as large corporations when it comes to choosing expansion over quality. They spend millions on advertising, but when it comes to paying workers, the well is dry.</P>

