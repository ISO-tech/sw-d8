<P><font face="Times New Roman, Times, serif" size="5"><b><i>Providence Journal</I></b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Chris Grogan</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PROVIDENCE, R.I.--More than 150 members of the Providence Newspaper Guild and other activists rallied outside the <I>Providence Journal Bulletin</I> October 20 to protest the newspaper's stalling on contract negotiations.</P>
<P>Workers there have been without a contract for nearly two years. The workers are seeking an increase in pay and health benefits, while the company is launching a campaign of harassment and union-busting tactics, such as abolishing union dues checkoff.</P>
<P>"They have been committing corporate terror for a long time," said Tony DePaul, a reporter at the <I>Journal.</I> He points out that the company has been posting record profits of 20 percent during the last two years.</P>
<P>The union has launched a campaign asking subscribers of the <I>Providence Journal</I> to show their solidarity by signing cards pledging to cancel their subscriptions in the event that a boycott is approved by the union's membership.</P>

