<p><a name="teachers"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City teachers</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--As election day approaches, mayoral candidates Mark Green and Mike Bloomberg are both making promises about improving education in the city's overcrowded schools. But neither candidate has a real solution for the city's 1.1 million students or the 80,000 teachers who have gone without a contract for almost a year.</P>
<P>Both candidates have called for pay raises for teachers while simultaneously calling for various forms of "performance incentives" and new measures to get rid of "bad teachers." This divide-and-conquer strategy is an attempt to blame teachers for problems caused by chronic underfunding.</P>
<P>Bloomberg, a Republican, has recently claimed that the city didn't have enough money to reduce class size. Mark Green, a Democrat, has received the endorsement of the United Federation of Teachers. He has promised to raise teachers' salaries and reduce class size. But he has tied part of these raises to "bonus pay" or performance incentives.</P>
<P>"Teachers should expect to spend more time at school," Green said during a speech at City University of New York.</P>
<P>Neither candidate has a solution for already overworked teachers who haven't had a pay raise in nearly two years. But Randi Weingarten--the president of the teachers' union--has done little to get a new contract but endorse Green and put hopes in a contract fact-finding process that has stalled in the wake of September 11.</P>
<P>Frustration with negotiations was evident at a recent delegate's assembly when a couple of delegates raised a banner that read, "Don't sacrifice education! No war contracts!"</P>
<P>We need a strong rank-and-file movement to win changes in education and improve teachers' living standards.</P>

