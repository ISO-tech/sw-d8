<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rolling back our rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"RARE ARE the moments in American history when a Congress has surrendered so many cherished freedoms in a single trip to the altar of immediate fear." That was what reporter John Nichols wrote in the <I>Nation</I> magazine after George W. Bush signed the Uniting and Strengthening America by Providing Appropriate Tools Required to Intercept and Obstruct Terrorism (USA PATRIOT) Act into law.</P>
<P>Despite the freedom-loving acronym, the bill is a nightmare of civil rights restrictions and police powers. In short, it's a dream come true for the Bush gang. "September 11 was the biggest boon federal law enforcement could ask for," said Laura J. Murphy, executive director of the American Civil Liberties Union (ACLU).</P>
<P>Under the guise of "antiterrorism protection," the act expands the ability of the police and FBI to conduct telephone wiretaps and secret searches of people's medical, financial and student records --as well as their homes, offices and computer files.</P>
<P>It also allows for foreign citizens to be thrown in jail for seven days without being charged with a crime--and a loophole allows the detention time to be repeatedly extended by six-month periods during an "emergency."</P>
<P>Most disturbing is the crackdown on freedom of speech and political dissent. The new law allows the attorney general to create a broad new category of "domestic terrorism"--and to classify all kinds of political organizations and acts of normal political protest as "terrorist."</P>
<P>"Under the definition, groups such as the World Trade Organization protesters who engage in minor vandalism&#133;or protesters at Vieques, Puerto Rico, who damage a fence would be deemed terrorist organizations," said the ACLU. If you donate money or pay membership dues to a "terrorist" organization, you could be thrown in jail--or deported if you're an immigrant.</P>
<P>Washington claims that the new law is necessary to protect us from terrorism. But who's going to protect us from overzealous cops, FBI goons and prosecutors?</P>
<P>"We have fought too hard for our civil rights and civil liberties to let them be taken away in the name of [Osama bin Laden]," said Rev. Jesse Jackson Sr. last week. "These are our rights."</P>
<P>We have to organize now to protect our hard-won civil rights.</P>

