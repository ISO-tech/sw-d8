<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why we oppose all U.S. interventions</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"SO WHAT should the U.S. do?" Most opponents of the U.S. government's war on Afghanistan have been asked this question. If the bombing campaign is only causing more misery and making the crisis worse, then what do we propose?</P>
<P>Several answers come to mind right away.</P>
<P>Instead of bombing Afghanistan, the U.S. could feed the country's millions of starving people. If just a tiny fraction of the resources devoted to destruction were devoted to distributing food, every malnourished Afghan would be fed.</P>
<P>But this isn't really answering the question. The people who ask it figure that the U.S. has to do <I>something</I> to respond to the September 11 attacks--that some form of political or military response is needed from the U.S. government to punish those responsible and to strike a blow for justice in the country itself.</P>
<P>If bombing is too indiscriminate, maybe a guerrilla operation to assassinate Osama bin Laden is the answer. And what about nonmilitary options--couldn't the U.S. bring political and economic power to bear on Afghanistan to bring about change that would improve the lives of ordinary Afghans?</P>
<P>This way of thinking--that something has to be done--is common, even among some people who oppose Washington's war machine in most circumstances. But the truth is that no form of U.S. intervention--military, diplomatic, economic or otherwise--will promote justice or democracy or any of the other lofty principles used by the politicians.</P>
<P>Why not? The U.S. government is largely controlled by a small elite--made up of corporate chiefs, leaders of mainstream political parties, generals and so on. Their interests, both at home and abroad, lie in protecting and increasing their own power and wealth.</P>
<P>Of course, America's rulers couldn't get anyone to support a war if they put it this bluntly. So they appeal to ideals that ordinary people believe in.</P>
<P>"When and where has there been a war&#133;in which each and every belligerent party did not with a heavy heart draw the sword from its sheath for the single and sole purpose of defending its Fatherland and its own righteous cause from the shameful attacks of the enemy?" asked Polish revolutionary Rosa Luxemburg. "This legend is as inextricably a part of the game of war as powder and lead."</P>
<P>The U.S. government wants to preserve its political and economic domination of the globe. And it always looks for the tools that are right for the job. So when the U.S. wanted to oppose the former USSR's invasion of Afghanistan, it didn't support democratic organizations, but the most conservative elements of the Islamist movement.</P>
<P>Likewise today, the U.S. isn't supporting groups like the Revolutionary Association of the Women of Afghanistan--which has opposed both the ex-USSR's occupation and the tyranny of the hard-line Islamists--but the bickering warlords of the Northern Alliance, who are cut from the same cloth as the Taliban.</P>
<P>In effect, the U.S. is acting like a mafia crime family that relies on one street gang to do away with another--all in the name of stopping crime.</P>
<P>Socialists are accused of being "knee-jerk" opponents of U.S. imperialist adventures. We are--because we believe that the U.S. government will never act except in the interests of injustice, tyranny and greed.</P>
<P>We believe that it's up to the people of Afghanistan, the Middle East and Southwest Asia to settle accounts with local rulers and determine their own fates--free from the meddling of the "great powers."</P>
<P>As the American socialist and journalist John Reed put it: "Uncle Sam never gives anybody something for nothing. He comes along with a sack stuffed with hay in one hand and a whip in the other. Anyone who accepts Uncle Sam's promises at their face value will find that they must be paid for in sweat and blood."</P>

