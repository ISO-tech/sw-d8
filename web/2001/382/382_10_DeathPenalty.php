<font face="Times New Roman, Times, serif" size="5"><b>Abolish the death penalty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE DEATH ROW 10 are a group of Black men on Illinois death row. Former Chicago police commander Jon Burge and his underlings tortured these men into giving "confessions." To win justice in their cases, the Campaign to End the Death Penalty has organized a Death Row 10 Tour.</P>
<P>In New York City, more than 100 people attended the event at Columbia University.</P>
<P>Louva Bell, mother of Death Row 10 member Ronnie Kitchen, brought many in the audience to tears as she described the pain of dealing with her son's torture, unjust imprisonment and possible murder by the state.</P>
<P>Death Row 10 member Stanley Howard spoke "Live from Death Row" via telephone about the torture he suffered. He also criticized the U.S. bombing of Afghanistan, speaking about the futility of the cycle of killing and violence.</P>
<P>Joan Parkin from the Campaign to End the Death Penalty echoed Stanley's words, questioning how the U.S. government can be expected to pursue justice abroad when the racist death penalty is a prime example of injustice carried out at home.</P>
<P>Darby Tillis, who was exonerated after spending nearly a decade on Illinois death row, spoke about how he--a young, poor, Black man--was used as a tool to build the careers of people in the "criminal justice" profession.</P>
<P>In Washington, D.C., 175 attended the event. Death Row 10 members Grayland Johnson and Leonard Kidd called in "Live from Death Row," and Marlene Martin of the Campaign to End the Death Penalty spoke about the fight for justice.</P>
<P>David Bates, also a Burge torture victim, told the audience about how officers barged into his home, held guns to his mother's head and arrested him for murder. He was taken to a police station, brutally beaten and suffocated with a plastic bag. At age 17, he was convicted on the basis of a false confession and spent the next 11 years wrongfully imprisoned.</P>
<P>In Iowa, the Campaign to End the Death Penalty sponsored panels at Grinnell College and the University of Iowa. More than 50 students at Grinnell and 30 students at the University of Iowa turned out.</P>
<P>"I was forced to become a man in prison," Bates told students. "I had to learn how to survive. Imagine using a toilet as a refrigerator. Imagine washing your clothes in toilet water--because you had no other running water. That was my reality."</P>
<P>At each of the events, many decided that the time is right to get involved in the fight to end the death penalty. In Iowa, students at the University of Iowa have already launched a new chapter of the Campaign at their school, and several students from Grinnell plan to do the same.</P>

