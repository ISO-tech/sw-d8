<font face="Times New Roman, Times, serif" size="4">CEO Goodwin forced out, but the attacks continue</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Unions face battle at United</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>JENNIFER BIDDLE, a member of IAM Local 1781 and a furloughed engine inspector at United, reports on the challenge facing the unions at the company.</P>
</B>
<P>LITTLE MORE than a month after the September hijackings of two of its jets, United Airlines declared its own war: against its workers.</P>
<P>In a letter addressed to employees (but leaked to the press two weeks before any of us received it in the mail), James Goodwin, then CEO, wrote: "Today, we are literally hemorrhaging money. Clearly, this bleeding has to be stopped--and soon--or United will perish sometime next year."</P>
<P>Goodwin was ousted by the United board of directors September 28 under pressure from United's unions, which have seats on the board through an employee stock ownership plan (ESOP).</P>
<P>He had claimed that a $15 billion bailout by the government, a $350 million insurance policy payoff and $4.3 billion in concessions from its workers over the past eight years won't cover United's losses. Is it a surprise that most workers at United considered Goodwin a crook?</P>
<P>No sooner had 20,000 of United's employees received pink slips for October 19, than the company paid a French business jet manufacturer $11 million on a $70 million investment in the luxury aircraft. Is it a surprise that most workers at United considered Goodwin a liar?</P>
<P>The airline survived the "summer from hell" last year and long strikes in the past without the company resorting to layoffs or work-rule changes. And the company continues to outsource hundreds of millions of dollars worth of aircraft maintenance.</P>
<P>Yet management at United now is using an emergency clause in its contracts to force most mechanics to work a rotating day off schedule with six days on, two off, with no overtime pay--and very few weekends. The company has also taken away flexible shift starting times.</P>
<P>And management will expect those lucky ones not laid off to pick up the slack for the thousands of employees whose jobs "perished" October 19.</P>
<P>Laboring under a concessionary contract signed in 1994, United's workers have not received a raise in 10 years. Before September 11, most employees expected at least 20 to 30 percent raises just to bring them up to where they should be had they received annual cost of living increases.</P>
<P>Goodwin may be gone, but his comments signal management's hardball tactics in an attempt to keep United workers' wages low and squeeze more work out of them.</P>
<P>United's workers are full of anger and resentment. The unions, unfortunately, seem to be floundering.</P>
<P>Word is that the Air Line Pilots Association would like to engineer another employee buyout while the stock is low. The International Association of Machinists (IAM), representing mechanics and other service personnel, is providing no leadership to counter the attack by United's management. The Association of Flight Attendants seems to be the only union on track, publicly criticizing the company for the layoffs.</P>
<P>If the IAM and the other unions fail to provide clear leadership, all of us will lose. It would be the PATCO for the new millennium.</P>
<P>But there is no doubt that United's workers are willing to fight against more concessions to the company. The potential to win big is there.</P>

<P><font face="Times New Roman, Times, serif" size="4">IAM needs to use its muscle</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>PART OF the problem in the IAM now, aside from the lack of leadership, is the deep animosity between "skilled" mechanics and "unskilled" baggage handlers, storekeepers, utility personnel and customer service representatives.</P>
<P>Recently defeated, the Aircraft Mechanics Fraternal Association (AMFA) campaigned to unseat the IAM for representation of mechanics at United. The main organizing points in AMFA's campaign were to scapegoat "unskilled" workers for low wages and to argue that mechanics are "skilled" and therefore deserve more.</P>
<P>In the process, AMFA did much to turn employees against one another. For instance, even now, at some line stations, baggage handlers routinely find holes at the last minute in aircraft cargo bays that mechanics supposedly miss on their walk-around inspections, potentially delaying flights due to maintenance.</P>
<P>At the same time, many of the gripes mechanics have about the IAM are justified. The IAM needs to be more democratic and responsive to its members.</P>
<P>And let's face it: The IAM will never live down negotiating the ESOP agreement with the company during the biggest boom in the history of capitalism.</P>
<P>In order for the IAM to win back the confidence of its mechanics--who will be key to winning any labor dispute--it must begin organizing a fightback. The union must find creative ways to exercise its muscle, even under the threat of the existing temporary restraining order that prevents mechanics from organizing a work slowdown during contract negotiations.</P>
<P>A grassroots campaign based on a majority of the rank and file that builds support with the rest of the labor movement and in our communities is the best way to rebuild solidarity on the shop floor.</P>

