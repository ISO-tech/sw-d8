<font face="Times New Roman, Times, serif" size="4">Washington's class war: From anthrax crisis to tax cuts</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Protecting their own</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 2, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government doesn't treat postal workers like dogs. It treats them worse.</P>
<P>When anthrax was discovered on mail to the U.S. Capitol, police dogs were tested for infection and the House of Representatives voted itself a week off. But postal workers were told to carry on as usual--until two died from the inhalation form of the disease.</P>
<P>Federal officials claimed that they didn't have the expertise that would have led them to shut down and clean postal facilities as they did the Capitol. But in reality, the double standard shown by the government toward victims of the anthrax crisis is a reflection of the class divide in the U.S.</P>
<P>"I think it reflects a difference in the class perspective," said Larry Adams, president of Mailhandlers Local 300 in New York and a member of New York Labor Against War. "The rulers are more important than the workers. Those who are in charge have the authority to do what is necessary to protect themselves," he told <I>Socialist Worker</I>.</P>
<P>In fact, the politicians and employers have seized on the crisis to advance their interests in every way. Consider the latest package of tax cuts for corporations and the wealthy that's been wrapped in the American flag and sold as a $100 billion "economic stimulus bill."</P>
<P>If the bill passed last week by the House becomes law, Corporate America will get $25 billion in cash rebates based on the repeal of the alternative minimum tax. IBM would get $1.4 billion, while General Electric--which made $9.8 billion in the first nine months of this year--would rake in $671 million. And Enron--the oil giant that was the top donor to George W. Bush's presidential campaign--would net $254 million, even as it faces fire for ripping off investors.</P>
<P>The tax rebates for corporations in the bill are worth nearly twice as much as the break slated for 37 million low-income families. "Who would have thought that a national emergency would set off a feeding frenzy by corporations and the wealthy?" said Robert McIntyre of Citizens for Tax Justice. "And who could have imagined that so many of our nation's elected officials would eagerly go along with this monstrous demonstration of greed?"</P>
<P>And while politicians justified their move on the grounds that business will use the extra money to invest, there's nothing in the bill that forces them to. "If the companies want to, say, pay huge bonuses to top executives, they can," wrote <I>New York Times</I> columnist Paul Krugman. "[C]orporations and wealthy individuals [who] were so richly rewarded no longer feel the need to disguise their payoffs."</P>
<P>While they're at it, the employers and their political hit squads are attacking organized labor, too. House Majority Leader Dick Armey (R-Texas) opposed a federal government takeover of airport security because, he said, "What the Democrats want is 30,000 new dues-paying union members."</P>
<P>The Democrat-controlled Senate may eliminate the worst excesses of the stimulus bill. But don't count on it. After all, it was Northwest Airlines lobbyist Linda Daschle--wife of the Senate majority leader--who successfully lobbied for an airline bailout bill that protected CEO pay while 100,000 airline workers got no extra unemployment compensation.</P>
<P>In the days after September 11, the press and the politicians demanded "national unity" to fight a "war on terrorism." Nearly two months later, they're charging ahead with a different kind of war--a class war against working people.</P>
<P>The war in Afghanistan "isn't our war," said Adams. "It's not in the interests of workers in this country, or workers anywhere else in the world."</P>

