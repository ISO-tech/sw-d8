<font face="Times New Roman, Times, serif" size="4">Top care for lawmakers while postal workers die</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A double standard about anthrax</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S CALLED a double standard. Members of Congress were whisked out of Capitol buildings and given a battery of tests after anthrax spores were detected in an envelope addressed to Senate Majority Leader Tom Daschle (D-Mo.). But at the Brentwood Road post office that sorts the mail that goes to Capitol Hill, workers were told to keep working.</P>
<P>Last week, two men who worked at the Brentwood facility paid for that decision with their lives. The two postal workers died of respiratory failure from inhalation anthrax.</P>
<P>And it was only then that local health officials recommended testing postal workers and dispensing the antibiotic Cipro. "Somebody in there believed it was serious enough to do that," letter carrier Malcolm X. Lee told New York's <I>Newsday. </I>"What are we supposed to feel? We have families, too."</P>
<P>Apparently the government didn't want postal workers to lose a day of work.</P>
<P>The same was true at the Morgan Station facility in New York City, where four sorting machines were contaminated with anthrax-laced letters sent to offices at NBC and the <I>New York Post</I>.</P>
<P>"The building should have been closed down," Larry Bass, a 20-year machine mechanic at the station, told <I>Socialist Worker</I>. "The situation was mishandled from the beginning," added James, who also works at Morgan Station and is taking Cipro. "The union should force them to close the building down."</P>
<P>That's exactly what the executive board of the city's largest postal union thinks as well. On October 27, it filed a lawsuit to get Morgan Station closed until it's thoroughly cleaned.</P>
<P>"It's time for the Postal Service to start putting workers first," said William Smith, president of the New York Metro Area Postal Union. "They want the workers of New York Metro to be guinea pigs, and I'm not going to stand for that foolishness."</P>
<P>Smith urged some 5,500 workers at the Morgan Station facility not to report to work. Although, as federal employees, postal workers are barred from striking, their contract allows them to refuse to work in unsafe conditions.</P>
<P>You might think that the Bush administration would be embarrassed by its naked double standard on anthrax scares. Yet White House officials used the deaths of the two workers to whip up more fear.</P>
<P>They continued to fuel speculation that the anthrax in the Daschle envelope came from Iraq--even as FBI officials were beginning to admit, at least anonymously to reporters, that the culprit could be a homegrown Nazi. Meanwhile, Attorney General John Ashcroft refused to meet with representatives of Planned Parenthood, which has years of experience dealing with anthrax threats from anti-abortion fanatics.</P>
<P>George W. Bush told a conference of business leaders that he mourned the deaths of "two postal officers who lost their lives in the line of duty." But that's a lot of hot air--coming from a government that rushed to provide top-notch health care for Capitol Hill lawmakers but waited until postal workers died before even testing them.</P>

