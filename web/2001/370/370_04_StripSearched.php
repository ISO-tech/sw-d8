<P><font face="Times New Roman, Times, serif" size="5"><b>Students strip-searched on D.C. jail tour</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P><b>Dear <i>Socialist Worker,</i></b></P>
<P>It's hard to imagine our education and prison systems sinking to new lows, but that's exactly what has happened recently in Washington, D.C.</P>
<P>On May 17, nine of 13 boys from Evans Middle School took a tour of a D.C. jail and were subjected to strip searches by corrections officers. When the news reported the story, other students came forward with similar stories.</P>
<P>The tours are outrageous--including handcuffing and shackling, forced strip searches and strip searches in front of inmates. "I felt scared," said eighth-grade student Domonique Whittiker. "[The guards] were cussing and hollering at us. They said if we don't take off our clothes, they were going to beat us and make us take them off."</P>
<P>Thirteen-year-old Jos&eacute; Ross described the experience of one of his friends. "He didn't want to take off his shirt, and a big guard grabbed him by his shirt and took it off. 'Get off! Get off! I ain't taking my pants off,'" screamed Jos&eacute;'s friend. "Then [he] was put into a jail jumpsuit and tennis shoes, and the guard took him back with the inmates."</P>
<P>In-school suspension coordinator Dorothy Simpkins set up the tours to discipline misbehaving students. In a letter to the jail requesting the tours, she said she wanted students to "experience the way they will live when [they] are punished by the law."</P>
<P>Children have it rough enough already--with many subjected to poverty, inadequate health care and overcrowded schools. Sending students on humiliating and inhumane prison tours can only add to the intense pressure they face on a daily basis.</P>
<P>It's time for the government to stop solving problems with more prisons and punishment, and start providing our children with the resources they need to live a happy and sane life.</P>
<P><b>John Coursey,</b> Washington, D.C.</P>

