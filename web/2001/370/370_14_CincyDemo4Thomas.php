<P><font face="Times New Roman, Times, serif" size="4">Cincinnati protest against killer cops</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We want justice for Timothy Thomas!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 14</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CINCINNATI--Some 2,000 people gathered June 2 to protest police brutality in the wake of the cop murder of a Black teenager that touched off days of rioting in April. Much of the multiracial crowd came from Cincinnati and the surrounding area, but demonstrators traveled from as far away as Buffalo, New York City and Chicago.</P>
<P>The protest was organized by the March for Justice Committee, a collection of different organizations that came together after the April 7 murder of 19-year-old Timothy Thomas.</P>
<P>Officer Stephen Roach says Thomas ran away from him and into an alleyway--but Roach felt "threatened," so he gunned Thomas down.</P>
<P>Why did Roach pull Thomas over in the first place? Outstanding traffic violations.</P>
<P>But Thomas' real crime was to be Black. Cincinnati cops have killed 15 Black men in the last five years.</P>
<P>The bottled-up anger at one police murder after another bubbled over into nights of rioting after cops descended on a crowd gathered outside police headquarters for a peaceful vigil--and began firing tear gas and shotguns loaded with "beanbags."</P>
<P>Anti-police brutality activists have continued to organize since the upheaval. "I'll continue to fight, not just for my son, but for everybody else's son," Angela Leisure, Thomas' mother, told the crowd as demonstrators gathered June 2 at Fountain Square.</P>
<P>Victoria Straughn, of the Coalition of Concerned Citizens for Justice, expressed the demonstrators' determination to win real change. "It's time to put an end not only to police terrorism, which is going on at a national level, but also to end the war on the poor," Straughn said. "We're citizens. We pay taxes. We pay them to do a job, not kill us."</P>
<P>After the speeches, protesters took to the streets for a march--with people joining in all along the route. The demonstration stopped for a moment of silence in front of the alley where Thomas was killed, then moved on to march past the police station in the mostly poor African American West End neighborhood.</P>
<P>The rally showed that anti-police brutality activists aren't satisfied with the politicians' vague promises. We want an end to police violence now! Justice for Timothy Thomas!</P>

