<P><font face="Times New Roman, Times, serif" size="4">Working poor in Bush's America</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Struggling just to get by</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PHIL GRAMM is worried about the forgotten victims of discrimination. "There's one form of bigotry in this country that remains unchallenged," the Republican senator from Texas said last month.</P>
<P>So Gramm and his buddies in Washington set about righting this wrong. By passing a $1.35 trillion tax cut whose benefits will go to the rich. Gramm hopes the tax cut will cushion the blow of what he calls "bigotry against the successful."</P>
<P>Of course, America's suffering superrich have had their flourishing fortunes to comfort them. According to the Congressional Budget Office, the annual after-tax income of the richest 1 percent of the population came close to tripling between 1979 and 1997--to $677,900.</P>
<P>Other Americans make do with a bit less. "Gail is sharing a room in a well-known downtown flophouse for $250 a week...Claude, the Haitian cook, is desperate to get out of the two-room apartment he shares with his girlfriend and two other, unrelated people...Tina, another server, and her husband are paying $60 a night for a room in the Days Inn...</P>
<P>"Joan, who fooled me with her numerous and tasteful outfits (hostesses wear their own clothes), lives in a van parked behind a shopping center at night and showers in Tina's motel room." These are descriptions of some of the people that author Barbara Ehrenreich met in researching her new book <i>Nickel and Dimed: On (Not) Getting By in America</i>.</P>
<P>Ehrenreich worked several low-wage jobs to investigate the day-to-day reality of life for America's working poor. Her findings? Just getting by is a struggle.</P>
<P>Fully 30 percent of the U.S. workforce--more than 40 million people--make less than $8 an hour. About the only way to provide for a family at that wage is long hours--10- to 12-hour shifts, often without breaks, or multiple jobs.</P>
<P>Then there's the problem of housing. When you can't make enough money to save for a deposit on an apartment, Ehrenreich found, then you end up spending more to rent week to week, or day to day.</P>
<P>No wonder one out of five homeless people works a full- or part-time job, according to the National Coalition for the Homeless--and two-thirds of adults who request emergency food aid are employed, according to the U.S. Conference of Mayors.</P>
<P>Then there are the abusive managers, the often unsafe conditions, and the dirty, unhealthy, monotonous grind of a rotten job.</P>
<P>In a just world, people in these jobs would be paid more than anyone else, not less. But you won't hear this from Phil Gramm or George W. Bush or any of the other politicians in Washington.</P>
<P>They want to give away the federal government's surplus in tax cuts for the "successful"--even though they know this will strangle government programs for the not-so-successful in the years to come.</P>
<P>What a sick system! That's why we have to organize for a living wage for all workers--now!</P>

