<P><font face="Times New Roman, Times, serif" size="5"><b>America's killing machine exposed</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TIMOTHY McVEIGH had given up his right to appeal last December and was set to die May 16. And pro-death penalty politicians were looking forward to it.</P>
<P>They hoped that the McVeigh case would give the death penalty a new lease on life. After all, they reasoned, what could be more popular than executing a racist who carried out the Oklahoma City bombing, killing 168 people?</P>
<P>Then it all blew up in their faces. The FBI revealed last month that it withheld 4,449 pages of documents that McVeigh's defense lawyers were entitled to see.</P>
<P>Now McVeigh has decided to ask for a delay of his scheduled June 11 execution. Judge Richard Matsch was expected to rule on the request as <i>Socialist Worker</i> went to press.</P>
<P>Whatever happens, McVeigh's case has cast a spotlight on precisely what the politicians had hoped to bury--the flaws and injustices of the death penalty system.</P>
<P>If anything, prosecutorial misconduct and violations of due process are even more common among the typically poor and disproportionately minority population of death row.</P>
<P>Take Juan Raul Garza, the federal death row prisoner scheduled to die June 19. Last month, Garza's lawyers asked for a stay of execution because prosecutors in his original trial argued for a death sentence based in part on alleged crimes in Mexico that Garza was never even charged with. Yet Garza was denied the stay.</P>
<P>There are similar stories in every state that has capital punishment, according to an Amnesty International report released in late May that blasted the U.S. death penalty system.</P>
<P>For example, the report ranked the Texas Board of Pardons and Parole among the world's "human rights scoundrels," saying that the board is notorious "for operating a flawed and unfair review of death penalty cases, reviewing clemency petitions in secret and voting by fax or telephone without due process procedures."</p>
<P>As William Schulz, Amnesty's U.S. director, noted: "It's no wonder that the U.S. was ousted from the United Nations Human Rights Commission." Schulz said that the U.S. is the only industrialized country that still executes people, putting it "in the same shameful death penalty league as China, Iran and Saudi Arabia." Together, the U.S. and these three countries account for 88 percent of all known state killings.</P>
<P>Since 1977, when the U.S. resumed executions, more than 60 countries have abolished the death penalty. It's high time that the U.S. joined the civilized world. But we'll have to fight to make that a reality.</P>

