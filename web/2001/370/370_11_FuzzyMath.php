<P><font face="Times New Roman, Times, serif" size="5"><b>Exposing Bush's tax cut con job</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Paul Krugman, <B><I>Fuzzy Math: The Essential Guide to the Bush Tax Plan</B></I>. W.W. Norton &amp; Company, 2001, 112 pages, $17.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by SARAH KNOPP</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH sold his $1.35 trillion tax cut plan as something that would help ordinary people. In front of dozens of audiences, Bush claimed that the "average family" would get a check for $1,600.</P>
<P>Nonsense, says liberal economist and <I>New York Times</I> columnist Paul Krugman.  Bush's tax breaks are skewed to go to the rich. In fact, according to a study by Citizens for Tax Justice, more than half of families on the bottom end of the income ladder won't get any benefits at all.</P>
<P>Anyone who follows Krugman's columns knows that he isn't a radical. But when he isn't bashing antiglobalization protesters, denouncing Ralph Nader for throwing Election 2000 to Bush or pushing for free trade, he writes a good column now and then. And he's certainly right about the fraud underlying Bush's tax plan.</P>
<P>With Bush's Treasury Department suppressing information about how the tax cut will affect people of different incomes, Krugman's arguments make for a refreshing change of pace.</P>
<P>The taxes that hit working-class and poor people the hardest--like the payroll tax on wages and the sales tax--won't be reduced at all under the Bush plan.</P>
<P>But the biggest dirty secret of the Bush tax cut plan is the way that it will affect Social Security and Medicare. The huge giveaway to the rich will strangle the government budget--leading to the likelihood that funds for Medicare will have to be cannibalized to pay for government services.</P>
<P>Republican and Democratic promises of Medicare coverage for prescription drugs suddenly seem vague and unlikely.</P>
<I><P>Fuzzy Math</I> is written with the confidence that ordinary people can understand economics. And it's refreshing to read a mainstream writer who so unapologetically attacks Bush.</P>
<I><P>Fuzzy Math</I> successfully rips the veil off the Bush gang's tax cut con job.</P>

