<P><font face="Times New Roman, Times, serif" size="4">Chicago Peoples Gas workers enter week two of strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fed up with their corporate greed</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Gas workers from Service Employees International Union (SEIU) Local 18007 are in their second week of a strike as <I>Socialist Worker</I> went to press. Strikers are focusing their pickets on the downtown corporate offices of Peoples Gas, which has refused to negotiate.</P>
<P>Attendance at pickets has been uneven, but workers came together for a community rally of 200 on Memorial Day weekend and a rally of about 500 on May 31. "Hey, hey, ho, ho! Corporate greed has got to go!" chanted workers as they circled the corporate headquarters and stopped other union members from crossing the picket line into the building.</P>
<P>Gas workers are fed up with the greed of Peoples Gas. "It isn't fair," said Jeanette McGee, a service specialist. "This is all about corporate greed...This corporation is so arrogant. They [already] laid off 75 union members," McGee added. "It is all about union busting."</P>
<P>Peoples Gas made almost $100 million in profits in first six months of this fiscal year--and yet it still wants to take away overtime pay, force workers to pay more for health care and lay off clerks.</P>
<P>James Wreglesworth, a locator who's worked for Peoples Gas for 32 years, is concerned about the safety of workers and the public. "I deal with all kinds of [construction] contractors, and they don't know where the high-pressure mains are," said Wreglesworth. "[During this strike,] the scabs--the supervisors--aren't doing this job properly...This is like an accident waiting to happen."</P>
<P>But the company is playing hardball. The company sent letters to union members urging them to resign from the union and come back to work.</P>
<P>Their reward? A polo shirt with an embroidered company logo! </P>
<P>For now, everyone sees these kinds of bribes as an insult. But the union has no strike fund, so the longer the strike goes on, the more pressure union members will feel to return to work.</P>
<P>So far, no working union members have scabbed, but two retirees have crossed the picket line.</P>
<P>Peoples Gas has refused to negotiate, even as they entered a meeting with the union and a federal mediator. "This is not a resumption of negotiations...We're approaching this as an informational session," said a company spokesperson.</P>
<P>To win the strike in the face of such stubbornness, the union must go on the offensive and use the media to show that Peoples Gas cares more about money than safety.</P>
<P>Chicago residents are no doubt receptive to hearing the argument that Peoples Gas is a greedy company--given that most of them had to pay outrageous heating bills this past winter as the company made huge profits.</P>
<P>The union also needs to put pressure on Mayor Richard Daley and other politicians to hold Peoples Gas accountable for endangering city residents and to force the company to negotiate in good faith. </P>
<P>Finally, more aggressive pickets to stop Peoples Gas from sending out poorly trained scabs to endanger the public will pressure the company to return to the table with a contract that gas workers deserve.</P>

