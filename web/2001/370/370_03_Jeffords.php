<P><font face="Times New Roman, Times, serif" size="4">Jeffords' defection throws the Senate to the Democrats</font><br
<font face="Times New Roman, Times, serif" size="5"><b>Wrench in Dubya's Plans</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"A SEISMIC shift." "Turning Washington upside down." "Bush's agenda in peril." Clich&eacute;s like these filled political commentary following Vermont Sen. James Jeffords' decision to leave the Republican Party, turning control of the U.S. Senate over to the Democrats.</P>
<P>It was certainly a pleasure to see Jeffords hand Bush and his henchmen their biggest setback since they stole the presidency. For a president who lost the popular vote and was installed in the White House thanks to a 5-4 vote of the U.S. Supreme Court, Bush has been acting as if he has a mandate for a hard-right agenda.</P>
<P>Bush only got close enough to steal the election last fall because he camouflaged his real ideas with rhetoric about "compassion" and "changing the tone" in Washington. Yet once in office, the Bush gang pushed so hard so fast that even Republican moderates like Jeffords got a little queasy.</P>
<P>Jeffords' defection should throw a wrench in their plans. The new Democratic-controlled Senate is expected to put an end to Bush's dreams of drilling for oil in the Arctic National Wildlife Refuge and handing out federal judgeships to anti-abortion fanatics.</P>
<P>But the switch isn't necessarily the political earthquake it's been made out to be. For one thing, Jeffords waited until after Congress passed Bush's most cherished proposal--a $1.35 trillion tax cut giveaway to the richest Americans. This is hardly a victory for the "moderation" that Jeffords--who voted for the tax cut--claims to represent.</P>
<P>Then there's the new Senate leadership to consider. Since Bush took over in January, Democrats have pleaded that they're powerless to promote their agenda with Republicans running the show in both houses of Congress.</P>
<P>That was always a dubious argument. Democratic votes confirmed John Ashcroft as attorney general, repealed workplace safety regulations and passed reactionary bankruptcy "reform" and the tax cut bill.</P>
<P>Now Democrats say they'll stand up to Bush and push campaign finance reform, a "patients' bill of rights" and a Medicare prescription drug benefit. If they did make a real fight on these issues, they'd certainly be more in tune with ordinary people--even if their proposals fall far short of what's needed.</P>
<P>But don't hold your breath. The Democrats are now in line to rake in more money from corporations looking for "access" to the majority party in the Senate. And Majority Leader Tom Daschle (D-S.D.) says he'll "work with" Bush in a spirit of "bipartisanship."</P>
<P>Already, Democratic so-called "centrists" like Sens. Joseph Lieberman (D-Conn.) and John Breaux (D-La.) are cautioning their colleagues not to "move too far to the left." That's another way of saying that Democrats shouldn't support positions that might help millions of ordinary people--but wouldn't be popular with the big corporations that pull the strings in Washington.</P>
<P>The Democrats want to "moderate" Bush's agenda--when it should be rejected wholesale. We have to build an opposition at the grassroots that challenges both parties of big business in Washington.</P>

