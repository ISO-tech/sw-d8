<p><a name="Get your"></a><font face="Times New Roman, Times, serif" size="4"><b>Get your government out of my union!</b></font></P>
<P><b>Dear <i>Socialist Worker,</i></b></P>
<font face="Times New Roman, Times, serif" size="3"><P>For the first time in modern New York City labor history, members of a union told the New York district attorney (DA) to get out of their business.</P>
<P>AFSCME District Council (DC) 37 Local 768 President Helen Greene was indicted for refusing to pay back $2,400 for charges on her union credit card she says are union expenses.</P>
<P>In response, members passed the following resolution: "The local goes on record as rejecting the interference of the District Attorney's office in the internal affairs of the local."</P>
<P>DA Robert Morgenthau--famed for hiring and training a prosecutor named Rudy Giuliani before he became mayor--has been indicting and jailing various officials of the 125,000-member DC 37 union for looting millions from union treasuries. The dispute over $2,400 is peculiarly small in comparison.</P>
<P>Union activists may be interested to know that the argument that carried the day simply expressed the deep anger and disgust felt by our members about a corrupt Supreme Court putting George Bush in office, a corrupt criminal injustice system turning loose the police who murdered Amadou Diallo, and complete hostility to the idea that the same court system that threatened to put New York City transit workers in jail for striking can kick out one of our union leaders. That was just common sense.</P>
<P>The public-sector union newspaper <i>The Chief</i> reported the motion I made, but it also made the larger case about what's wrong with government intervention in our unions. "Mr. Barton also questioned whether Ms. Greene was being prosecuted because she was one of only two prominent DC 37 local presidents who opposed the union's recent wage contract with the city because it included language allowing the Giuliani administration to implement a merit-pay program."</P>
<P>As city employees, we shouldn't allow a city agency to say who our leaders are. If we do, we give them the power to selectively prosecute leaders they don't like. </P>
<P><b>Thomas Barton,</b> Local 768 DC 37, New York City</P>
