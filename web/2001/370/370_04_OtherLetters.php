<p><font face="Times New Roman, Times, serif" size="5"><b>Immigrant workers deserve amnesty</b></font></P>
<P><b>Dear <i>Socialist Worker,</i></b></P>
<font face="Times New Roman, Times, serif" size="3"><P>The recent shocking deaths of 14 Mexican workers trying to cross the desert in Arizona made clear to many people the horrible human cost of the absurd U.S. immigration laws.</P>
<P>Here at the University of Wisconsin (UW), more than 20 of my fellow union members were targeted by the UW administration because their names "sounded Latino."  UW demanded that they provide documentation of their legal status, then fired them when the union--AFSCME Local 171--asked for an extension of the deadline.</P>
<P>The state of Wisconsin has had trouble filling many positions in recent years. Immigrant workers have taken up the slack and helped keep institutions like UW running. Yet when the state wants to claim a budget shortfall, immigrant workers are the first to be targeted!</P>
<P>Since the firings, UW has made it clear they plan on playing hardball. They have refused to help the union contact the workers, and rumors of more firings are circulating.</P>
<P>We need to put pressure on UW. Already, a May 5 rally drew more than 100 people to protest the firings.</P>
<P>This came not long after a rally at the capitol that drew together hundreds of state workers and students--to protest the state's pitiful offer of a 1 percent raise, the UW's plan to raise tuition and its failure to address the lack of diversity. This is the kind of solidarity we need to win justice for these workers. </P>
<P>But the events in Madison, as well as the incident in Arizona, show that we also need to support the AFL-CIO's call for a general amnesty for all immigrant workers. As long as workers live in fear of deportation, we'll see more injustices like these.</P>
<P><b>Mike McHugh,</b> AFSCME Local 171, Madison, Wis.</P>
