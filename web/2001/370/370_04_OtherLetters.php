<p><a name="Hollander"></a><font face="Times New Roman, Times, serif" size="4"><b>Hollander strikers are an inspiration</b></font></P>
<P><b>Dear <i>Socialist Worker,</i></b></P>
<font face="Times New Roman, Times, serif" size="3"><P>When I got a chance to visit the celebration of the Hollander Home Fashions workers at the UNITE union hall after their victory, I was overcome by how important this strike could be for the labor movement.</P>
<P>More than 400 mostly immigrant workers in the huge LA garment industry held on for more than two months against a company that was gunning for them. The workers won a 401(k) retirement plan from Hollander--even after it had said that such a plan was "not negotiable."</P>
<P>The strikers won most of their demands, even though many people were skeptical about the possibilities for this kind of organizing in the low-wage garment industry.</P>
<P>The strike was won through solidarity with Hollander workers across the country. Though they came from different ethnic backgrounds, workers from LA were able to travel to Hollander plants in Georgia and Pennsylvania and help to start strikes there.</P>
<P>The workers--who make pillows, sheets and blankets that end up in places like J.C. Penney and Ikea--went back to work with higher expectations. They have elected union reps for each of their departments and are currently fighting management over workplace fairness issues.</P>
<P>The only unfortunate thing about this strike is that it has not been a lightning rod for other garment workers because it has received almost no attention. Immigrant workers can expect to be ignored by the mainstream media, but the LA labor movement also failed to publicize the strike in any large way.</P>
<P>The excitement, confidence and pride that we saw when we visited the picket line should be an inspiration to all of us.</P>
<P><b>Sarah Knopp,</b> Los Angeles</P>
<P><i>Workers may be available to speak about their experiences. Contact Christina Vasquez at UNITE at 213-380-5498.</i></P>

