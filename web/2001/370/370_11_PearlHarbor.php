<P><font face="Times New Roman, Times, serif" size="4">Hollywood blockbuster peddles lies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pearl Harbor according to Mickey Mouse</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Pearl Harbor,</B></I> directed by Michael Bay, written by Randall Wallace, starring Josh Hartnett, Ben Affleck, Kate Beckinsale and Cuba Gooding Jr.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by ASHLEY SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IF PEARL Harbor is good for anything, it's proof that you should never get your history lessons from Disney Corp. The $135 million blockbuster that opened on Memorial Day weekend promotes every myth about the U.S. and its involvement in the Second World War.</P>
<P>Luckily, it does so badly. The acting is bad, the script unbearable, the romances unbelievable, and the history pure propaganda for the Pentagon.</P>
<P>This lumbering three-hour movie feels like it was pieced together from the worst outtakes of <I>Titanic</I> and <I>Saving Private Ryan</I>. It follows the adventures of best friends Rafe McCawley (Ben Affleck) and Danny Walker (Josh Hartnett). The two go to military flight training school, where Rafe falls for plucky nurse Evelyn Stewart (Kate Beckinsale).</P>
<P>But then Rafe volunteers for Britain's Royal Air Force to fight the Germans. Lonely Evelyn gets shipped off to the Pearl Harbor--the main U.S. military base in the Pacific--where dashing young pilots and nurses cavort in the sun.</P>
<P>All are devastated to hear that Rafe died in a dogfight against the Germans. Yet Evelyn overcomes her grief to be swept off her feet by Danny.</P>
<P>But wait! Rafe survived the dogfight--and has come to Hawaii, throwing the three into a torturous love triangle.</P>
<P>While the romantic clich&eacute;s pile up, the evil Japanese are plotting a surprise attack against the frolicking flyboys. On December 7, 1941, the Japanese attack Pearl Harbor. They buzz American children playing baseball, blow up unsuspecting soldiers and ships and throw a wrench into the innocent love lives of American service members.</P>
<P>Polio-stricken President Franklin Roosevelt struggles to his feet, rallies the nation and orders a triumphant revenge attack on Tokyo. You can practically hear the "Star-Spangled Banner" in the background.</P>
<P>Before you blame the Big Mouse for all this schlock, remember that the Pentagon contributed historical advice, use of its Pearl Harbor base and even ships used for some of the filming. When Pearl Harbor producers asked to use the modern aircraft carrier <I>USS Constellation</I> for one scene, the brass set a reasonable price--$1 million.</P>
<P>And no wonder the Pentagon was so generous. Like producer Jerry Bruckheimer's 1986 film <I>Top Gun, Pearl Harbor</I> is a glowing tribute to the U.S. military. It revives unadulterated racist depictions of the Japanese and peddles every lie about the Second World War that U.S. imperialism ever dreamed up.</P>
<P>The filmmakers even flirt with suggestions that Japanese Americans aren't to be trusted--with a Japanese American dentist in Hawaii reporting ship positions in a phone conversation with the Japanese military.</P>
<P>Once Pearl Harbor is attacked, the movie explodes with anti-Japanese slurs--with "Jap bastard" this, "dirty Jap" that, dominating the rest of the film. The racism is so thick that the producers are releasing a different version of the film in Japan. </P>
<I><P>Pearl Harbor</I> recycles the myth of the peaceful U.S. versus the evil Asian Empire bent on conquest. But nothing could be further from the truth. The U.S. and Japan were locked in a struggle for economic control of the Pacific.</P>
<P>Countless studies show that Roosevelt provoked Japan into attacking. The recent book <I>Day of Deceit</I> shows convincingly that officials at the highest levels of the U.S. government knew about Japan's plans for a Pearl Harbor attack.</P>
<P>Yet they sacrificed the lives of thousands of American service people to get the war they wanted. But don't expect any of these unpleasant truths in this film.</P>
<I><P>Pearl Harbor</I> is Mickey Mouse history at its worst.</P>

