<P><font face="Times New Roman, Times, serif" size="5"><b>Justice for Jaggi Singh!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JAGGI SINGH, a leader of the Canadian direct-action group CLAC, was grabbed off the streets of Quebec City during April protests against the Summit of the Americas.</P>
<P>Singh was kept behind bars for three weeks on trumped-up charges of participating in a riot and possession of a weapon--which turned out to be a catapult that other activists were using to launch teddy bears at riot police.</P>
<P>Now Singh is free--and he thinks authorities will drop the most serious charges against him.</P>
<P>Singh told <i>Socialist Worker</i> that he thought the Quebec City demonstrations were a step forward for the antiglobalization movement. "In spite of the fear campaign, in spite of the border becoming an obstacle to our friends in the United States, in spite of attempts to marginalize various groups--despite all of that, there was a strong radical presence in the streets," he said.</P>
<P>"There's a crisis of legitimacy right now, and Quebec helped accelerate that."</P>

