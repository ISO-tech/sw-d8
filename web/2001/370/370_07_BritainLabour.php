<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BRITAIN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What's next after New Labour victory?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PHIL GASPER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PRIME MINISTER Tony Blair's Labour Party government was headed for a landslide victory in Britain's June 7 general election as <i>Socialist Worker</i> went to press.</P>
<P>When Blair was elected four years ago, voters danced in the streets celebrating the end of 18 years of rule by the Conservative Party, known as the Tories. This time around, however, there won't be dancing.</P>
<P>Though Labour is running nearly 20 points ahead in most polls, there's widespread dissatisfaction with the party's performance in office. Instead of building on the mood for change shown in the last election, Blair embraced the neoliberal policies of his Tory predecessors.</P>
<P>His "New Labour" government has presided over growing inequality and deteriorating public services. Blair has also dismantled democracy within the Labour Party itself--symbolized in the current election by his imposition of Shaun Woodward, a former Tory millionaire, as Labour's candidate in the northern England constituency of St. Helen's, over the protests of local members.</P>
<P>Labour's lead in the polls reflects the fact that the Tories remain even more discredited. The current Conservative Party leader, William Hague, is widely seen as an incompetent buffoon.</P>
<P>In an effort to rally his supporters, Hague has tried to stoke anti-immigrant sentiment. But in response to Hague's scapegoating, Labour is defending its own appalling record as tougher than that of the Tories.</P>
<P>The two parties' immigrant bashing has given confidence to the far right. Members of the National Front and other Nazi groups were responsible for racist attacks on Asians in the city of Oldham, near Manchester, which provoked two nights of rioting last month.</P>
<P>Asian youth fought pitched battles with police, who have done nothing at all to stop the fascists.</P>
<P>But while Labour's policies have encouraged the growth of the far right, they've also created openings on the left. For the first time in 50 years, there's a serious alternative to the left of Labour--the Socialist Alliance in England and Wales and the Scottish Socialist Party together are running more than 160 candidates.</P>
<P>While they are unlikely to win any seats, these groups are offering a left-wing alternative to New Labour--which can help form the nucleus of activist campaigns after the election to challenge Labour's plans to further attacks.</P>

