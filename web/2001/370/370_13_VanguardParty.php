<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why we need to build a "vanguard" party</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOCIALISTS WHO consider themselves Leninists are often criticized for wanting to create a "vanguard party."</P>
<P>To the extent that critics of Leninism are denouncing what is, in fact, a caricature of Lenin--that any vanguard party will be top down and autocratic--there's little to be said. There are, no doubt, self-declared "vanguard" organizations of a few hundred people that lead nothing and repeat worn-out cliches.</P>
<P>But Lenin himself was a leader of a mass party in Russia that led a successful revolution. Lenin and the Bolsheviks were a vanguard in the true sense of the word--not isolated cranks.</P>
<P>Lenin's insistence on the need for a revolutionary party is based on the idea that the working class can't be liberated by anyone standing over or outside its ranks.</P>
<P>That's why Lenin opposed individual terrorism, for example--since it created a passive majority waiting on a small minority to take action for them.</P>
<P>He also rejected parliamentary socialism for viewing socialism as something accomplished by politicians on behalf of the working class.</P>
<P>In short, for Lenin--as for Karl Marx before him--the emancipation of the working class must be the act of the working class itself.</P>
<P>But there are obstacles to working-class self-emancipation. Otherwise, capitalism would have been done away with long ago.</P>
<P>The employers can depend on the state to use force to keep people in line when necessary. But often, force isn't necessary--because the majority of people more or less accept society as it is. Simple inertia is built into the structure of society--because people can't imagine things being any other way.</P>
<P>Plus, the competitive nature of the capitalist system can pit workers against each other. And there's what Marx called "the ruling ideas of society"--pushed by the corporate-run media and schools to try to convince us that we live in the best of all possible worlds.</P>
<P>Given this, workers have different degrees of consciousness about the possibility of change at any given moment. Some accept the profit system as the best system, while others reject it outright. Some reject racism in the name of solidarity among all workers, while others blame foreigners for their problems. This is why workers don't change their ideas overnight.</P>
<P>Capitalism forces workers to fight--whether they're gas workers in Chicago or autoworkers in Brazil. In the process of struggle, ideas of solidarity, equality and opposition to oppression come to the fore.</P>
<P>But workers don't become aware of their position and power in society at the same time. Some move faster than others and are ready to take the lead.</P>
<P>So, in any struggle, there will always be some kind of leadership. The question is what kind?</P>
<P>Without a clear alternative to the belief of most workers that they have to rely on others to change things for them, potentially revolutionary movements can be sidetracked by moderate leaders who want to keep the fight within the boundaries of existing society.</P>
<P>At the heart of Lenin's concept of the "vanguard" party is the simple idea that working-class militants and other activists who have come to the conclusion that the whole system must be dismantled must come together into a single organization in order to centralize and coordinate their efforts against the system.</P>
<P>In his famous 1969 pamphlet <I>Listen, Marxist!</I> anarchist Murray Bookchin attacks Leninism, or a caricature of it, but then concludes: "[We] do not deny the need for coordination between groups, for discipline, for meticulous planning and for unity in action. But [we] believe that [these] must be achieved voluntarily, by means of self-discipline nourished by conviction and understanding, not by coercion and a mindless unquestioning obedience to orders from above."</P>
<P>Revolutionaries, Bookchin argues, must be organized to "present the most advanced demands" and "formulate the immediate tasks that should be performed to advance the revolutionary process," providing "the boldest elements in action and in the decision-making organs of the revolution."</P>
<P>Ironically, this sounds like a description of Lenin's Bolshevik Party in 1917!</P>

