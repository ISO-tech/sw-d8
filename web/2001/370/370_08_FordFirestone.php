<P><font face="Times New Roman, Times, serif" size="4">Ford and Firestone try to shift the blame</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Family feud!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEIGHTON CHRISTIANSEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 8</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DETROIT--With a press release on May 22, tiremaker Bridgestone/Firestone CEO John Lampe ended his company's 95-year-old corporate family affair with Ford Motor Co.</P>
<P>The next day, Ford bosses fired off their own press release--announcing a recall of all 13 million Firestone Wilderness AT tires on any Ford vehicle.</P>
<P>What could have ended this profitable business relationship that dates back to the dawn of the auto industry? Greed.</P>
<P>Ford and Firestone have been scrambling to protect their profits since the deadly record of Ford's Explorer SUV was exposed last year, leading to a voluntary recall of 6.5 million tires in August. Tire blowouts and rollovers on the Explorer have caused thousands of accidents and at least 174 deaths in the U.S.</P>
<P>Like two schoolyard bullies called into the principal's office, the two companies are blaming each other. Except these corporate giants use full-page newspaper ads and high-tech Web sites to plead their cases.</P>
<P>Ford says that the Explorer's tires are the problem. At high speeds and temperatures, the tread on Firestone's Wilderness AT, ATX and ATX II tires can separate from the tire body, causing blowouts that lead to rollovers. In a full-page newspaper ad, Ford Chairman Bill Ford Jr. and CEO Jacques Nasser promised to make Firestone fix the problem, because "customer safety always has been--and always will be--our #1 priority."</P>
<P>But Firestone says that the problem lies in the design of the top-heavy Explorer, the most popular SUV in the U.S. "Ford Explorers on non-Firestone tires continue to experience tread separations and tragic rollover accidents," read the tiremaker's ad in response. "The tires Ford is replacing under this program are safe. But, for the Ford Explorer, the question of safety remains."</P>
<P>But the posturing should fool no one--both companies are guilty.</P>
<P>According to industry experts, Firestone's tire troubles are the result of factory speedups that have left less time for inspections and driven workers to the breaking point. Plus, many of the faulty tires were apparently made by scabs during a bitter strike at Bridgestone/Firestone's Decatur, Ill., plant in the mid-1990s.</P>
<P>Meanwhile, Ford executives--desperate to squeeze every ounce of profit out of the growing SUV market--hid evidence that the Explorer was prone to rollovers in emergency turn situations because of its high center of gravity and narrow wheel base. In fact, the company recommended that drivers let air out of the tires to help stabilize the vehicle--which only made the tread separation problem worse.</P>
<P>Both companies did their best to cover up the fact that the Explorer-Wilderness tire combination led to hundreds of rollover accidents in other countries--particularly in the warmer climates of the Middle East and Central and South America.</P>
<P>Venezuela's consumer protection agency Indecu issued a report last year that held both companies responsible for 46 deaths in road accidents involving Explorers equipped with Firestone tires. Since that report, most of the 30,000 Explorers on the road in Venezuela were fitted with new tires.</P>
<P>But a recent follow-up report from Indecu stated that Explorers have been involved in 50 crashes on Venezuelan roads since August, causing 37 deaths. Only one of those Explorers had Firestone tires.</P>
<P>Some industry analysts are blaming Ford's cost cutting and outsourcing for its ongoing quality problems. Meanwhile, Firestone's grab for profits at all costs has led to other tire recalls earlier this year--prompting General Motors, Nissan and other automakers to look for a new supplier.</P>
<P>All of the finger pointing will make for a strained Christmas dinner at the Ford mansion this year. Bill Ford Jr.'s mother Martha is the granddaughter of Harvey Firestone, the founder of the tire company.</P>
<P>As Ford Jr. whined to the Associated Press, "Firestone was an important part of my family, so on a personal level, this is tough. To see this all taking place is deeply disturbing."</P>
<P>But no one should feel too bad for Bill Ford. His cover-ups and blame-shifting are part of a grand family tradition--of scrambling after profits, no matter what the cost to workers and consumers.</P>

