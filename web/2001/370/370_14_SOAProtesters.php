<P><font face="Times New Roman, Times, serif" size="5"><b>Jailed protesters tell SOA they won't be silenced</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by KATHERINE DWYER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>COLUMBUS, Ga.--"I refuse to be intimidated for standing for what is right." Those were the words of Erik Robinson, a 21-year-old from Spokane, Wash., as he was sentenced to six months in jail for demonstrating against the School of the Americas (SOA).</P>
<P>Robinson is one of 26 protesters who will serve jail time and pay up to $3,000 in fines for the "crime" of peacefully walking onto the Fort Benning military base last fall to protest the SOA.</P>
<P>Judge G. Mallon Faircloth handed down the maximum six-month jail sentence for criminal trespassing to 21 of 26 defendants--including an 88-year-old nun and a 19-year-old college student.</P>
<P>The 26 were among some 10,000 people who traveled to Fort Benning last November to protest continued training of Latin American dictators and military thugs at the "School of the Assassins."</P>
<P>In his testimony, John Ewers, one of the 26, talked about witnessing the results of SOA violence. He described seeing tens of thousands of Colombians living off an abandoned garbage dump in Cartagena--after they were forced off their land by SOA-trained soldiers. Ewers said that one Colombian was told, "We will offer to buy it from your widow," when he refused to give up his land.</P>
<P>SOA supporters claim that the school promotes democracy by teaching U.S. "values" to Latin American soldiers.</P>
<P>"It is ironic that this military base, where the expression of democratic dissent is prohibited, harbors an institute that claims to teach democracy but whose deadly fruit is not just 'a few bad apples,' as they claim, but a systematic poison that attempts to cover murder with a shroud of lies," said Clare Hanrahan, a 53-year-old from Asheville, N.C., who was also sentenced to six months in jail.</P>
<P>Father Roy Bourgeois, founder of the organization School of the Americas Watch, which coordinates the annual protests at Fort Benning, told reporters: "It baffles me how [Faircloth] can sleep at night when an 88-year-old nun is going to prison...[and] assassins and soldiers who have tortured and raped get pardons."</P>
<P>The long jail sentences are clearly meant to silence protesters, whose growing ranks over the past several years have drawn national attention to more than 50 years of SOA atrocities.</P>
<P>But activists have vowed to keep coming back--and breaking the law--until the SOA is closed for good.</P>

