<P><font face="Times New Roman, Times, serif" size="5"><b>Embassy bombing trial exposes hype about bin Laden</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ANTHONY ARNOVE</b></font>
<font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 2</font>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Blame Osama bin Laden. That's Washington's response to any international attack against the U.S. these days.</P>
<P>Though he wasn't charged, the Saudi businessman and alleged "international terrorist mastermind" was at the center of the case against four defendants convicted May 29 in federal court for the August 1998 bombings of U.S. embassies in Kenya and Tanzania.</P>
<P>Two of the convicted, Mohamed Rashed Douad al'Olwhali and Khalfan Khamis Mohamed, could face the death penalty--even though Mohamed was illegally deported from South Africa, a country that bars capital punishment and protects suspects from deportation to countries that might execute them.</P>
<P>The jury is expected to deliver its sentence this month. But the defendants' lawyers have been barred from introducing evidence in the penalty phase of the trial that their clients were motivated in part by outrage at the devastating consequences of U.S. sanctions against Iraq.</P>
<P>Judge Leonard Sand ruled that photographs of maimed and dead Iraqi children were "really irrelevant gore for the sake of gore." During the trial, Sand also barred testimony about the impact of sanctions on Iraq.</P>
<P>The outcome of the four-month trial was never in question given the "anti-terrorist" hysteria that surrounded it. Yet despite its best efforts, the government's obsession with bin Laden exposed the contradictions of the U.S. hype.</P>
<P>"To listen to some of the news reports...you would think bin Laden was running a Fortune 500 multinational company--people everywhere, links everywhere," said Larry Johnson, former deputy director of the State Department's Office of Counterterrorism.</P>
<P>As one unnamed Western intelligence analyst told Britain's <i>Independent</i> newspaper, "I just don't buy the theory that he is sitting in a cave somewhere organizing global terror. The Americans can't cope without a clear target. So they've created one."</P>
<P>In response to the embassy bombings, the Clinton White House ordered missile attacks on Sudan and Afghanistan, killing civilians and destroying a vital pharmaceutical factory that turned out to have no links to bin Laden.</P>
<P>But the terrorists in the White House haven't been put on trial.</P>

