<P><font face="Times New Roman, Times, serif" size="5"><b>Oil and gas White House pushes nuclear power</b></font><br>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 5</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>SUMMER OF the blackout. That's what the months ahead hold for California, where the possibility of repeated interruptions in electricity with little or no warning has hospitals, schools, businesses, families--in short, everyone--wondering how they'll get through the summer.</P>
<P>And California isn't alone in its struggle to keep the lights on at a reasonable price. Seattle residents have been hit with three utility rate hikes already this year--and officials say another increase is likely this fall. In New York City, transmission bottlenecks have pushed electricity prices to sharply higher levels.</P>
<P>George W. Bush has a simple answer to the problem: Get rid of environmental regulations--and drill, mine and drill some more. And if that doesn't work, build nuclear power plants!</P>
<P>But Bush's proposal to plunder the planet won't do anything for people suffering from the energy pinch.</P>
<P>That's because high energy prices don't have anything to do with a lack of supply. In California, for example, blackouts began this spring when demand was less than 30,000 megawatts--despite the fact that California has 55,000 megawatts of power-generating capacity.</P>
<P>The real problem is corporate greed. In 1996, the energy industry "helped" California lawmakers write a deregulation law that was supposed to bring lower prices by making utility companies buy power on the free market.</P>
<P>Five years later, the opposite has happened--a small and powerful cartel of power-generating companies is withholding electricity supplies in order to drive prices to astronomical levels.</P>
<P>The companies claim that they're only following the law of supply and demand. This is true in a sense--the bosses control the supply, so they can demand whatever they want.</P>
<P>Now California's utility companies are crying bankruptcy. But they weren't complaining five years ago when the deregulation bill set electricity rates artificially high.</P>
<P>With the help of their servants in the White House, the power bosses want us to pay for the disaster of their free market. We can't let them get away with it.</P>

