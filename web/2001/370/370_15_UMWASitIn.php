<P><font face="Times New Roman, Times, serif" size="5"><b>UMWA sits in at Massey</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, W.V.--Seventeen United Mine Workers of America (UMWA) members were arrested in a sit-in May 23 at Massey Energy's Elk Run mining complex. About 250 people took part in the march to protest Massey's disregard for workers' rights and the environment.</P>
<P>"Massey's attitude toward its workers, the environment and Appalachia's economic well-being have not changed since the UMW engaged in a bitter strike with them in the '80s," UMWA President Cecil Roberts said. "In fact, its attitude may be worse."</P>
<P>Twenty years ago, Roberts and other UMWA members were arrested several times at the same complex during a month-long strike in which then-A.T. Massey Coal refused to negotiate with the union.</P>
<P>Massey is the nation's seventh-largest coal company and the largest in central Appalachia. It operates 18 mining complexes in West Virginia, eastern Kentucky and southwestern Virginia.</P>
<P>It was Massey's Martin County, Ky., slurry impoundment that collapsed last October, releasing 250 million gallons of sludge into the Tug, Big Sandy and Ohio Rivers. Massey representatives called the disaster "an act of God."</P>
<P>In February, 100 residents of Sylvester, W.Va.--half the town--filed suit against Massey over dust and noise at the local coal-loading facility. "Each morning, cooks at Sylvester Elementary School must rewash the cooking and eating materials they had washed the past afternoon to make them clean and free of coal dust that accumulates on them overnight inside the school building, so that the children can avoid eating on dust-filled dishes," the lawsuit charges.</P>
<P>Massey is even less of a friend to its workers. Last October, Massey lobbyist K.O. Damron read "an enemies list" to the West Virginia Chamber of Commerce, with the UMWA featured at its top.</P>
<P>But Massey is Public Enemy Number One. Of seven mining deaths so far in 2001, two have been at Massey facilities. The company has yet to pay the $30 million it owes to the West Virginia Workers Compensation fund.</P>
<P>Massey can afford to pay. CEO Don Blankenship makes about $4.4 million a year in salary and bonuses, according to the AFL-CIO's Executive PayWatch. And when the <I>Charleston Gazette</I> added in stock options and stock pay-outs from Massey's spin-off from Fluor Corp. last year, Blankenship was raking in about $16 million.</P>
<P>Coal prices have risen substantially over the last year and could see even greater increases because of concern over energy shortages and the Bush-Cheney administration's plan to turn to coal.</P>
<P>Massey attributes much of its success to keeping most of its operations nonunion. Only 5 percent of its workforce are represented by the UMWA.</P>
<P>The UMWA's sit-in shows the way to force Massey to provide a decent quality of life for its workers and the people who live near its mines.</P>
<P>"The UMWA believes the entire Massey workforce should demand to share in the company's economic prosperity," the UMWA's Roberts said at the protest. "If need be, the UMWA is more than willing to help them get their piece of the pie."</P>

