<font face="Times New Roman, Times, serif" size="5"><b>Two strikes for Jenna</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JENNA BUSH'S hankering for a cold, frosty one has got her in trouble--for the second time in two months. This time, she dragged into the fray her twin sister Barbara who is home from Yale for the summer.</P>
<P>Though young people can be tried in criminal court as an adult at 16 or even younger, the drinking age is still 21. So when 19-year-old Jenna Bush tried to use false identification to buy alcohol, the employees at Chuy's Mexican restaurant called 911 to report the Bush twins.</P>
<P>This was Barbara Bush's first criminal citation, but Jenna had to plead no contest last month after she was arrested with a beer at an Austin nightclub while Secret Service agents waited outside. She was ordered to attend alcohol awareness classes and pay $50 in court costs.</P>
<P>Jenna also made headlines in March when she had her Secret Service agents pick up a friend from jail after his arrest at a Fort Worth frat party.</P>
<P>With this second criminal charge, Jenna could face a potential 60-day suspension of her driver's license, more rigorous community service and a $500 fine.</P>
<P>If she gets a third strike, the zero-tolerance policy instituted by her father when he was Texas governor means that she would automatically lose her driver's license for 90 days and could spend up to six months in jail.</P>
<P>The White House is keeping quiet on the matter. "The president views this as a family matter, a private matter, and he will treat it as such," White House spokesperson Ari Fleischer told reporters. "I would urge you to be very careful because any reaction of the parents is parental; it is not governmental," he said. "It is family. It's private, and the American people respect that."</P>
<P>Maybe Fleischer should call up Bill Clinton and tell him about his change of heart.</P>
<P>--Agence France Presse, June 1, 2001</P>

<P><font face="Times New Roman, Times, serif" size="5"><b>Bush: You could be president, too</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN GEORGE W. Bush received an honorary degree from Yale University--his alma mater--he had this advice for graduating seniors. "To those of you who received honors, awards and distinctions, I say, well done," Bush pontificated. "And to the C students, I say to you: you, too, can be president of the United States."</P>
<P>But Bush forgot to add that you'll also need to choose your parents wisely. They should be wealthy, white and politically connected. And it won't hurt if your dad was also president.</P>
<P>--<I>Newsweek,</I> June 4, 2001</P>

<P><font face="Times New Roman, Times, serif" size="5"><b>Racist sicko heads to Agriculture Dept.</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DESPITE THE obvious racism of his nominee, George W. Bush is standing by his man. Bush nominated Thomas Dorr as undersecretary of agriculture for rural development.</P>
<P>But in December 1999, Dorr told an Iowa State University conference that three of the most successful "rural economic environments" in Iowa were "not particularly diverse." </P>
<P>"At least not ethnically diverse," he told the conference. "They're very diverse in their economic growth, but they're very focused, have been very nondiverse in their ethnic background and their religious background. And there's something there that has enabled them to succeed and to succeed very well."</P>
<P>But the Bush administration still gives Dorr its full support, according to White House spokesperson Scott Stanzel. "Those comments were taken out of context and are an unfair representation of Mr. Dorr's views," Stanzel said. "We urge the senators to listen to Mr. Dorr during his confirmation hearing because any rush to judgment would shortchange the process and a fine nominee."</P>
<P>--<I>New York Times,</I> May 31, 2001</P>

