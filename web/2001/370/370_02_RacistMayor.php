<P><font face="Times New Roman, Times, serif" size="5"><b>Racist cop-turned-mayor charged with murder</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by NICOLE COLSON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 2</font>
<font face="Times New Roman, Times, serif" size="3"><P>YORK, Pa.--"'White power' isn't a racist statement." That's what York Mayor Charlie Robertson had the gall to tell the NBC's "Today Show" after he was charged with murder May 18 for the July 1969 killing of an unarmed Black woman named Lillie Belle Allen. </P>
<P>Robertson's indictment has thrown the city into an uproar--and cast a spotlight on the organized racists who infested its police department and political establishment three decades ago.</P>
<P>Robertson, a police officer at the time of the murder, is accused of supplying rifle ammunition to members of white street gangs who shot and killed Allen. The murder took place July 21, 1969--four days after riots broke out following the police killing of an unarmed Black teenager.</P>
<P>When a white police officer was shot and killed during the turmoil, cops and white gangs escalated their attacks on Black residents. Then-Officer Robertson attended a rally called by the gangs, where, he now admits, he shouted, "White power!" </P>
<P>Witnesses say that he told gang members, "If I weren't a cop, I would be leading commando raids against niggers in the Black neighborhoods." The next day, witnesses say, Robertson handed out the ammunition and told at least one gang member, "Kill as many niggers as you can."</P>
<P>Later that day, Lillie Belle Allen was shot to death after the car that she and her family were riding in stalled. </P>
<P>When his role in the killings first gained press attention earlier this year, Robertson defended his actions. "To say 'niggers'--it was common," he told reporters. Later, he tearfully claimed that police "sensitivity training" had cured him of his racist views.</P>
<P>But Denise Thompson knows the truth about Robertson. On the day her 15-year-old son was gunned down in 1997, Robertson--by then the mayor of York--used his death as an opportunity for a sound bite. </P>
<P>"Where was the mother?" Robertson said to reporters--before speculating, inaccurately, that Thompson's son was on drugs.</P>
<P>Neo-Nazi thugs who organize in York and the surrounding area are making their feelings known. Several days after Robertson's arrest, bright orange stickers were plastered around town that read: "Earth's Most Endangered Species: The White Race. Help Preserve It." </P>
<P>Outraged residents ripped the stickers down immediately, said Cathy Ash, of the city's Human Relations Commission. "They used clippers, putty knives, fingernails, anything to rip down that message," Ash said.</P>
<P>When Robertson's trial gets underway, York is likely to become a battleground in the fight against bigots and racists.</P>

