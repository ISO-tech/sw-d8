<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Politicians demand blood after Palestinian suicide bombing</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The bloody toll of Israeli repression</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ERIC RUDER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN THE deadliest attack on Israelis in five years, a Palestinian suicide bomber set off an explosion that killed 17 people and wounded 90 more outside a Tel Aviv nightclub June 1.</P>
<P>Within hours, Israeli politicians were baying for blood--demanding that Prime Minister Ariel Sharon end his supposed policy of "restraint" toward Palestinians. In the U.S., George W. Bush condemned the bombing as a "heinous terrorist attack." And pundits in both countries proclaimed that the suicide bombing proved that Palestinians are "beasts"--with no regard for human life.</P>
<P>But when the Israeli military used U.S.-made F-16s to bombard a Palestinian police station last month, no one talked about "terrorism."</P>
<P>This double standard--in which Palestinian violence is denounced while Israeli violence is tolerated--helps to explain why some Palestinians, living in misery and despair, have turned to suicide bombings as the only hope for striking back. "When you oppress people enough, they get tired of living," Ali Abunimah, vice president of the Arab American Action Network, said on a Chicago radio show.</P>
<P>"And that's the situation many Palestinians are facing today. They've become so desperate. They've stuck it out through 10 years of the peace process, but instead of seeing peace, they've seen more Israeli aggression, more settlements, more Israeli expansionism onto their land, more poverty, more siege, more closure. They're saying that if that's peace, we have nothing to lose from fighting for our rights and trying to resist this occupation with all our strength."</P>
<P>Israeli hard-liners used the suicide bombing as a pretext to demand retaliation--just weeks after Sharon, battered by international criticism, started talking about "restraint." This was never anything other than a public relations exercise to capture headlines and deflect attention from an investigation by former U.S. Sen. George Mitchell into the causes of the latest round of bloodshed that began last September.</P>
<P>The idea that Israel has shown any "restraint" in its ongoing repression of the Palestinian resistance is ridiculous. Sharon's "policy change" amounted to limiting "preemptive" attacks against Palestinians--but maintaining the Israeli army's blockade of Palestinian villages.</P>
<P>"Sharon is stopping the fire and continuing the strangulation," said Palestinian Information Minister Yassir Abed Rabbo. "A cease-fire takes place between two armies, not between an army and a people under occupation. It's a game of Sharon's."</P>
<P>Mitchell's report on the conflict attempted to lay blame evenly on the Israeli and Palestinian sides. But Sharon was shaken by it--because Israel rarely faces any criticism at all from U.S. political leaders.</P>
<P>For example, the report highlighted Israel's stepped-up construction of settlements on Palestinian land in the Occupied Territories as a cause of the violence. Since the beginning of "peace" negotiations in 1993, the number of Israeli settlers in the West Bank and Gaza has shot up from 125,000 to 200,000--and in Arab East Jerusalem, from 150,000 to 200,000.</P>
<P>"Many of the confrontations during this conflict have occurred at points where Palestinians, settlers and security forces protecting the settlers meet," the report concluded. "Keeping both the peace and these friction points will be very difficult."</P>
<P>The Mitchell report doesn't call for dismantling Israeli settlements--only freezing their growth. But even that was too much for Sharon. In a clear slap at the Mitchell report's conclusions, he announced plans last month for the construction of 700 more units.</P>
<P>Such actions show that Sharon never cared about peace. His government has only looked for excuses for war--stoking the fury and determination of Palestinians to resist.</P>

