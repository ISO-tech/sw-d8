<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A power greater than their hoarded gold</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ALAN MAASS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE OF the most exciting developments in the movement against corporate globalization has been the participation of organized labor. The demonstrations that shut down the World Trade Organization (WTO) summit in Seattle in late 1999 brought together union members with environmentalists, students, anti-sweatshop activists and others.</P>
<P>Many had never worked together before. In Seattle, they found themselves arm in arm confronting a police riot--and successfully disrupting the bosses' meeting to promote corporate power.</P>
<P>This "Teamster-Turtle alliance" was a rebuttal to arguments that defenders of the system use to discredit us--that unionists only care about "bread-and-butter issues," for example, or that other activists all think the "hard hats" are part of the problem.</P>
<P>The experiences of the last 18 months have naturally led to discussions about where the antiglobalization movement is headed. Many questions loom--above all, how can we put more pressure on political and business leaders?</P>
<P>Few people who try to answer such questions would dismiss the importance of labor's participation--or the new links built between unions and other groups. But for many, labor seems like just another movement--one in a long list of "causes" that come together at the big demonstrations.</P>
<P>Revolutionary socialists approach the question differently. At the heart of our tradition is the belief in the centrality of the working class to making change.</P>
<P>In <i>The Communist Manifesto,</i> Karl Marx and Frederick Engels wrote that workers are the "gravediggers" of capitalism--because, when united, they have the power to overthrow the system. Workers produce the profits that make the capitalist system tick. So, therefore, the working class as a whole has a special power--to paralyze the profit system by not working.</P>
<P>You can see this power in situations that fall well short of revolution. In 1996, General Motors provoked a strike of 3,200 autoworkers at two Dayton, Ohio, factories that made brake parts for most GM vehicles.</P>
<P>It was a huge mistake. Within a week, the walkout had crippled GM's production across North America. GM lost about $1 billion in profits in 15 days--and management had to give in.</P>
<P>Because of their economic role, struggles that involve workers exercising their power as workers can have a deeper impact than other fightbacks. That was the case in apartheid South Africa, for example--where the rise of Black workers' struggles in the 1980s shook the system more dramatically than all the battles that came before, ushering in the final days of the racist regime.</P>
<P>What's more, whether they're employed in factories, offices, stores or elsewhere, the majority of people in capitalist society are workers. They don't control what they do or what they produce--and therefore face a conflict with those who do. So a movement that unites workers in struggle is potentially a movement of the majority in society.</P>
<P>By its nature, such a movement is bound to be more uneven. There will be many views about society and the possibility of change, including backward ones--passivity, apathy, scapegoating and so on.</P>
<P>Anyone committed to winning social change has to see it as their duty to challenge this unevenness, through argument and struggle.</P>
<P>But because capitalism inevitably forces workers into conflicts with employers, the system actually pushes people to confront these problems--and figure out collectively how to overcome them. Workers forced out on strike, for example, have to deal with the divisions built among them--and grapple with the wider issues at stake.</P>
<P>Potentially, workers' struggles open up a whole new world of possibilities for wider numbers of people who didn't think they were "political"--people who wouldn't have dreamed that they would be involved in changing society.</P>
<P>This is why socialists see the working class as central to the fight for change. Not only do workers have the economic power to challenge the system at its core, but the working class united poses a different way of organizing society--based on real democracy and the collective power of the majority of people.</P>

