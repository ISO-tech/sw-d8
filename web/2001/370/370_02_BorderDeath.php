<P><font face="Times New Roman, Times, serif" size="4">Politicians' war on migrants is responsible</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Death at the border</b></font></P>
<font face="Times New Roman, Times, serif" size="2"><b><P>by JUSTIN AKERS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 2</font>
<font face="Times New Roman, Times, serif" size="3"><P>YUMA, Ariz.--Jose Isodoro Colorado managed to trudge on in the desert heat despite the grief of leaving behind the dead bodies of family and friends.</P>
<P>Jose was one of the dozen survivors of a group of about 28 migrant workers who crossed the U.S.-Mexico border near Yuma in late May. The group had to get across a desert region the size of the state of Delaware. </P>
<P>Halfway into their journey, they were abandoned by the <i>polleros,</i> or smugglers, who led them. All told, 14 people died of dehydration after wandering lost for days in the blazing heat.</P>
<P>U.S. officials vowed to "punish those responsible" for abandoning the migrants. But they're ignoring the real culprits.</P>
<P>Since 1994, the federal government has fortified the U.S.-Mexico border, particularly in urban areas that are popular crossing points for migrants. The border has been sealed for miles from the Pacific Ocean into the mountains above San Diego with double and triple walls. </P>
<P>This has pushed the flow of migrants into more dangerous areas.</P>
<P>Meanwhile, the Border Patrol has been beefed up to more than 8,500 agents--and equipped with the latest military hardware, including high-powered rifles, helicopters and night-vision gear. </P>
<P>All told, the war on migrants has led to the deaths of more than 640 people in the California region since 1994. The number is closer to 1,500 if other border areas in Texas, New Mexico and Arizona are included.</P>
<P>Yet this gross violation of human rights isn't simply tolerated. It's part of the plan for "Operation Gatekeeper." As Doris Meisner, former chief of the Immigration and Naturalization Service who presided over Operation Gatekeeper, said: "We did believe geography would be an ally."</P>
<P>But it wasn't for Jose Isodoro Colorado and his companions.</P>

