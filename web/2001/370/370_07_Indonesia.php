<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDONESIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Impeachment vote sets off a political crisis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by TODD CHRETIEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A FRESH political crisis erupted in Indonesia last month as the country's parliament voted overwhelmingly to begin impeachment proceedings against President Abdurrahman Wahid.</P>
<P>The impeachment process will take months. But right now, Vice President Megawati Sukarnoputri--the daughter of Indonesia's first president and leader of the country's largest political party, the Indonesian Democratic Party of Struggle--is the favorite to take over from Wahid.</P>
<P>The impeachment vote came as the economy lurched still deeper into a depression that began in 1997. According to the BBC, some 40 percent of the working-age population of roughly 90 million is jobless or underemployed. Yet the Wahid government has continued to push International Monetary Fund(IMF)-backed austerity measures.</P>
<P>Megawati remains the most popular politician in Indonesia. In the country's first-ever democratic election in the fall of 1999, her party won roughly 40 percent of the seats in parliament.</P>
<P>Only a deal between the former ruling party GOLKAR, the military and Wahid's forces stopped her from becoming president--though mass student protests forced Wahid to take her on as vice president.</P>
<P>But while Megawati was seen as an opposition leader during the upheaval that toppled the 32-year dictatorship of Gen. Suharto in 1998, she's no radical. Megawati supports virtually the same economic program that Wahid has pursued for the past 18 months.</P>
<P>During the brutal massacres in East Timor last year, Megawati attacked Wahid for withdrawing the army. She has vowed to repress independence movements in other provinces.</P>
<P>So it's little surprise that Megawati has the support of Indonesia's military elite, which has made it clear that it won't carry out a crackdown ordered by Wahid.</P>
<P>Megawati still faces obstacles to taking over as president. Wahid has the support of hundreds of thousands of rural Muslims, including a powerful far-right militia. On April 29, almost 200,000 of Wahid's supporters--some in paramilitary uniforms--demonstrated in the capital of Jakarta as a show of force. Meanwhile, GOLKAR and the more urban-based Muslim followers of Amien Rais continue to maneuver for power in parliament.</P>
<P>In a positive sign, as many as 50,000 workers took part in May Day rallies in Indonesia last month. Many of the protests were led by independent unions and members of the Party of Democratic Revolution, Indonesia's largest left-wing group. </P>
<P>But as important as these mobilizations are, they were relatively small. The vast majority of workers remain in the old state-dominated unions, whose leaders have so far refused to translate verbal opposition to the Wahid government into action. Students have been active, protesting the continued role of the armed forces in the government.</P>
<P>Unfortunately, at the moment, the forces of the right seem to be growing more rapidly. A coalition of right-wing Muslim groups calling itself the Anti-Communist Alliance has succeeded in forcing bookstores to remove Marxist literature from their shelves and has stoked street violence against left-wingers.</P>
<P>If Megawati becomes president, this could give confidence to workers and students who see her as a symbol of the successful fight against Suharto--and touch off a new round of struggle. But her backroom deals with the military, alliances with the right and commitment to IMF-style austerity cuts could also disillusion ordinary people--creating an opening for the far right or the military to impose a solution on their terms.</P>
<P>Dangerous times lay ahead in Indonesia. But they will also present workers and students with the opportunity to disentangle themselves from the mainstream parties--and to build a fight for their common interests.</P>

