<P><font face="Times New Roman, Times, serif" size="5"><b>Mobilize against the globalizers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 8, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GLOBAL JUSTICE campaigners won a victory last month when the World Bank cancelled its planned June conference in Barcelona, Spain. Though they tried to pass off their retreat with the explanation that they would hold an Internet-only "cyber-conference," the Bank's bureaucrats are clearly feeling the heat.</P>
<P>In April, more than 50,000 trade unionists and other activists disrupted the Summit of the Americas in Quebec City, Canada. And the World Trade Organization remains so spooked by the demonstrations that shut down its 1999 meeting in Seattle that it will hold its next conference in the Gulf dictatorship of Qatar.</P>
<P>The corporate globalizers and their media mouthpieces may denounce protesters as unelected members of a "flat-earth society" looking for a "1960s fix." But the movement against them is growing.</P>
<P>The planned July 20-22 demonstrations at the Group of Eight summit in Genoa, Italy, are shaping up to be the biggest protests yet for the antiglobalization struggle. Unions across Europe are working with social justice groups to mobilize for a big turnout.</P>
<P>Meanwhile, in the belly of the capitalist beast, activists around the U.S. are already planning for protests against the joint International Monetary Fund-World Bank meeting in Washington, D.C., in late September.</P>
<P>These demonstrations will be an opportunity to bring together the labor movement, human rights campaigners, environmentalists and other activists to take on the corporate crooks and their chief figurehead, George W. Bush.</P>
<P>We should begin building now to show the corporate globalizers that our world isn't for sale.</P>

