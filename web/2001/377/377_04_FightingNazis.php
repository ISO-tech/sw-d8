<P><font face="Times New Roman, Times, serif" size="4">Protests in Midwest target white supremacists</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fighting the Nazis</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>When the KKK rallied in St. Paul, Minn., on the same day that the World Church of the Creator (WCOTC) held a meeting in Schaumburg, Ill., you would think that August 25 was some kind of Midwestern white supremacist holiday.</P>
<P>Fortunately, residents of these communities loudly opposed both these racist gatherings. My sister went to the demonstration against the Klan and told me how 1,500 protested, despite a campaign by the city to divert people to a festival across town.</P>
<P>Schaumburg's "solution" to the arrival of Matt Hale--the WCOTC's leader--for a meeting in the public library was to tightly regulate the flow of antiracists into the room and enforce a rule of silence. </P>
<P>But Hale's skinhead followers were allowed to yelp "White power!" and give the "Seig heil" salute--while Hale spewed racist garbage and the police forcibly removed anyone who verbally challenged the speaker.  </P>
<P>A friend of mine was literally carried out by four police officers, cuffed and held in a van outside for several hours. During one of Hale's rants, he said that he "didn't want to return to Schaumburg." Our side responded with cheers for five full minutes to show our agreement--while police removed people for the crime of clapping. The message was very clear--it's OK to perform a Nazi salute, but calling that person a fascist isn't.</P>
<P>Later, while waiting for my friend at the Schaumburg police station, Matt Hale himself walked right in, perhaps to personally thank the chief for a job well-done. One of his thugs shouted that I was a "gook who should go back to China and eat a dog."</P>
<P>The seven police officers witnessing this--who only minutes before reprimanded me for calling Hale a racist--weren't disturbed. When I asked what they thought about this double standard, one smirked and said, "I don't think anything about it."</P>
<B><P>Dennis Kosuth,</B> Chicago</P>

