<P><font face="Times New Roman, Times, serif" size="4">Teamsters rank and filers have to kick out Hoffa and...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vote for Tom Leedham</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by DONNY SCHRAFFENBERGER, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--The race for the president and executive board of the 1.4 million-member International Brotherhood of Teamsters is heading into its final stage. Members will mail in their ballots in October in an election that will pit incumbent James P. Hoffa against the Rank and File Power slate headed by Tom Leedham.</P>
<P>Hoffa--a lawyer and never a working Teamster--will have the backing of the vast majority of Teamsters officials, as recently witnessed at the union's Las Vegas convention.</P>
<P>Many one-time reform leaders--such as those in Local 705 in Chicago and Local 100 in Cincinnati--have opportunistically and cynically jumped on the Hoffa bandwagon. They believe it's in their best interest to back the powers that be rather than raise legitimate concerns over Hoffa's weak dealings with corporations.</P>
<P>Hoffa talks tough. During the last election, he promised to "restore Teamster power" and invoked the days when his father ran the union. But now members see his dismal track record--and the Hoffa name has lost its luster.</P>
<P>Poor contracts at Anheuser-Busch, Northwest Airlines, carhaul and the pathetic leadership at the almost 2-year-old Overnite strike show how weak and ineffective Hoffa really is.</P>
<P>What's more, Hoffa's top allies--including Detroit Teamster Mike Bane and Chicago-based aide Dane Passo--recently have been accused of corruption. No wonder that Hoffa only agreed to debate reformer Leedham after a federal official threatened to hold the debate with Leedham alone. Each side will receive 200,000 videotapes of the debate, and another 100,000 will be mailed to locals.</P>
<P>Leedham, who heads Local 206 in Portland, Ore., has allied himself with some of the best leaders and rank-and-file activists in the Teamsters. Leedham's slate includes Local 556's principal officer, Maria Martinez. She was the leader of a successful strike at Iowa Beef Processing in Washington state--a strike that Hoffa tried to sabotage while Tom Leedham walked the picket line in solidarity.</P>
<P>Leedham stands for rooting out corruption, taking on employers, organizing unorganized truck drivers and increasing union democracy. "I have a track record. I've worked as a Teamster and know what its like to lift boxes for ten hours a day," Leedham told <I>Convoy Dispatch,</I> newspaper of Teamsters for a Democratic Union. "I've organized rank-and-file contract campaigns that won industry-leading contracts. I was on the UPS picket line in '97. Hoffa was on CNN badmouthing the strike. I think that makes the choice pretty clear."</P>
<P>What's more, Leedham's campaign can be used to build rank-and-file activist networks that can make our union stronger at the grassroots. And if Leedham wins, we will have an easier job getting the support we need for a real fight that takes on UPS, freight employers and all the other corporate sharks.</P>
<P>Every Teamster interested in taking the union forward should work to turn out the vote for Leedham.</P>

<P>GEORGE W. Bush, representative of the superrich, thinks that Teamsters President James P. Hoffa is doing a good job. At a Teamsters Labor Day rally in Detroit, Bush said, "You've got a good man running the Teamsters in Jimmy Hoffa."</P>
<P>Bush gushed on, "Let me tell you another thing about Jimmy Hoffa. He's running a good union and in an aboveboard manner. And make no mistake about it, people are beginning to notice, particularly in Washington, D.C." Bush also hailed Hoffa's support of environmentally destructive Arctic drilling.</P>
<P>It's bad enough when a union-bashing president says he loves you, but when your main adversary, UPS, says it too, you know you're doing something wrong. UPS spokesperson Norman Black said, "We certainly are not dreading or fearing negotiations...The Teamsters under its current leadership has shown a willingness to work with us."</P>
<P>Corporate America and Bush know a good deal when they see one.</P>

