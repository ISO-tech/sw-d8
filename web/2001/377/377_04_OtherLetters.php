<p><a name="upside"></a><font face="Times New Roman, Times, serif" size="4"><b>A world turned upside down</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I have been reading and selling <I>Socialist Worker</I> for a few years now, and I eagerly await the arrival of the new issue every two weeks. From its international analysis to its coverage of debates in the various movements to its regular columns that explain Marxism or confront the issues facing working people, the paper explains the world like no other. </P>
<P>As a result, I--like many other readers--am very excited that our paper will be going weekly in the fall.</P>
<P>One recent article struck me as something that would improve <I>SW</I> if it was done more. At a recent <I>SW</I> discussion group, we were discussing the article "Patients before profits" (July 6, 2001). The article is a very insightful expos&eacute; of the AIDS crisis and the insidious way that Bush and the drug companies have kept drug prices sky high. Along with the article is a telling graph that illustrates the absurd amount the U.S. spends on the military compared to AIDS funding. </P>
<P>It is this type of article that clearly reveals the upside-down world we live in. A world where war is more profitable--and therefore more important--than human health. The graph alone outraged many people at a recent paper sale and opened up discussions about whether a world is possible where the numbers on that graph are switched.</P>
<P>It is because of this that I feel the paper would be more appealing if there were a column or some regular focus on this "upside-down world"--with similar graphs, charts and other devices to really expose the lunacy of capitalism.</P>
<P>With ordinary people in charge, those absurd numbers could be reversed and the world turned right-side up.</P>
<B><P>Justin Akers,</B> San Diego</P>

