<p><a name="traffic"></a><font face="Times New Roman, Times, serif" size="4"><b>Making profits off of traffic tickets</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Last year when I had to attend traffic school to clear up a speeding ticket, more than half the class was there for running red lights. They had been caught by automatic cameras and then received a photo and a $271 ticket in the mail. Some had entered the intersection mere hundredths of a second into the red light.</P>
<P>I didn't believe it when the instructor told us that Lockheed-Martin had built the cameras for San Diego at no cost--except that they wanted a cut from each ticket issued. But it's true--Lockheed installs and calibrates the systems, maintains them, develops the film, evaluates the pictures and rakes in $70 a pop.</P>
<P>The cameras made headlines recently when Judge Ronald Styn ruled them unlawful, because the city doesn't monitor Lockheed. "Lockheed is supposed to be a neutral evaluator of the evidence," the judge wrote. "As such, Lockheed should not have a financial interest in the outcome," said Styn, as he dismissed nearly a thousand cases.</P>
<P>Is Lockheed manipulating their sensors to ticket people who are actually innocent? As it turns out, they recently moved the sensors at three intersections without telling anybody--which could have affected their timing. This "is an example of what can go wrong with the system as presently operated," said Styn.</P>
<P>A lot can go wrong when you let a greedy corporation loose and expect them to act in the public interest--did somebody say energy deregulation?</P>
<B><P>Chuck Stemke,</B> San Diego</P>

