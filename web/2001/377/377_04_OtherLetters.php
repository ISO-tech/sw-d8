<p><font face="Times New Roman, Times, serif" size="5">Bush's fake tax cut won't help most of us</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>When I received a letter two months ago saying that President Bush had ordered the IRS to send me a check for $300, I had mixed emotions. I knew that this tax cut was coming at the expense of Social Security, education and a host of other programs that benefit workers and the poor. And I knew the main beneficiaries would be the rich, who would pocket millions.</P>
<P>But I wasn't going to turn down an offer of $300. Instead I'd donate it to <I>Socialist Worker</I>.</P>
<P>But when the envelope finally arrived, not only did I find out that I wasn't getting a check, I was told that I owed money. Because the $300 isn't really a refund. It's just an advance on your expected returns for this year.</P>
<P>As it turns out, I owed $320 for a tax return from 1996 that I didn't know about. The IRS would probably never have come looking for such a small amount. But my $300 credit was immediately applied to my balance. Instead of receiving a check, I was informed that I owed $20.</P>
<P>Millions of working families are going to suffer from Bush's social spending cuts. And most will see little, if any, relief from this tax cut. It's just another reason we have to make sure that at every turn Bush is met with humiliation and resistance.</P>
<B><P>Geoff Bailey,</B> Somerville, Mass.</P>

