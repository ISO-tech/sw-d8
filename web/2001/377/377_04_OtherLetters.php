<p><a name="blame"></a><font face="Times New Roman, Times, serif" size="4"><b>There's plenty of blame to go around</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>It's no coincidence that President Bush's largest campaign contributors include Enron Corp., El Paso Energy, Reliant Energy and Dynegy--four of the greedy generators under investigation for manipulating the California power market.</P>
<P>Vice President Dick Cheney has used the manipulated energy shortage to push plans for 20 new nuclear plants--and to exempt them from unlimited liability in the case of accidents.</P>
<P>While it is easy to blame the Republicans, you can be assured that the Democrats have their hands in the proverbial cookie jar as well. On August 2, it came to light that Bruce Willison--UCLA's business school dean and California Gov. Gray Davis' appointee to head the California Electricity Oversight Board--holds $1 million of Enron stock. </P>
<P>William Keese--the chairman of the California Energy Commission, one of the state's top energy regulatory bodies--owned as much as $510,000 in energy company stock last year.</P>
<P>Even Gov. Davis' chief spokesperson, Steve Maviglio, recently put $12,000 into an energy corporation.</P>
<P>It seems that there are pirates everywhere. </P>
<B><P>Kenny Annis,</B> San Francisco</P>

