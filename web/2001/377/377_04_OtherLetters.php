<p><a name="front"></a><font face="Times New Roman, Times, serif" size="4"><b>The front page of SW has to connect better</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I coordinate sales of <I>Socialist Worker</I> for my branch, so it's my job to build interest in the paper. The last issue's headline (August 17, 2001) read "Cowboy George enjoys a vacation as thousands of New Yorkers are left on the street." The article was not the easiest to sell here in California because it was concerned with New Yorkers.</P>
<P>It also called Bush "President Moron." While I agree that he is a moron, we have to keep in mind that the front page should be the most inclusive, have the most credibility and be the easiest to sell off of.</P>
<B><P>Chris Hardnack,</B> San Diego</P>


