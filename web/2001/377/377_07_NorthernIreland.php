<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NORTHERN IRELAND</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Anti-Catholic bigots lash out at schoolchildren</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE SCENES from Northern Ireland in early September looked like Little Rock, Ark., more than 40 years ago as Protestant protesters harassed and abused Catholic children on their way to school in a Belfast neighborhood.</P>
<P>The confrontations showed the horrible consequences of anti-Catholic bigotry whipped up by Protestant political leaders.</P>
<P>Catholic children and their parents had to walk through a gauntlet of snarling demonstrators to get to Holy Cross School, which is located in the middle of a Protestant neighborhood. On the third day of the confrontations, a homemade grenade exploded 10 yards away from the column of children and parents.</P>
<P>Even Protestant hard-liners--who are known as Unionists or Loyalists because they support Northern Ireland's ties to Britain--had to criticize the grenade attack. Yet some politicians continued to criticize Catholic parents for refusing to bring their children to the back door of Holy Cross School to avoid the demonstrations.</P>
<P>But why should Catholic children or adults have to fear walking through a Protestant neighborhood? The reason is simple--for the 80 years since Ireland was partitioned, the Unionist political establishment has relied on anti-Catholic hatred to maintain control of the Northern Ireland statelet.</P>
<P>Ordinary Catholics and Protestants were inspired by the hope of an end to decades of violence when the Northern Ireland peace process began in the 1990s.</P>
<P>But Unionist leaders have played a dangerous game ever since--going along with moves toward a power-sharing arrangement in Northern Ireland for a time, only to play up anti-Catholic sentiment to extract further concessions. Now the Unionists' bigotry threatens to spin out of control.</P>
<P>Meanwhile, the British government has handed the Unionists a trump card over the peace process by making one concession after another--effectively encouraging the bigotry that exploded outside Holy Cross School.</P>
<P>Catholics and Protestants are right to hope for an end to the violence in Northern Ireland. But there will be no peace without justice for the oppressed Catholic minority. </P>

