<P><font face="Times New Roman, Times, serif" size="5"><b>Riley brings Charleston Five struggle to JwJ conference</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CLEVELAND--Charleston Five defense leader Ken Riley was the keynote speaker at the Jobs with Justice National Meeting September 7-9. "The reason this struggle has been successful so far is because of international solidarity," said Riley, president of International Longshoremen's Association (ILA) Local 1422 in Charleston, South Carolina.</P>
<P>The Charleston Five are ILA members who have been under house arrest for 19 months--and who face up to five years in prison on trumped-up charges following a police attack on their picket line. If the Five go to trial, dockworkers on the West and East Coasts have vowed to stop work--and will be joined by workers in Spain, Sweden, Denmark and many other countries. "We will show them how unified this labor movement can be," Riley said.</P>
<P>Also featured were workers at the Hollander Home Fashion furniture plant in Tignall, Ga., where a decision by mostly African American workers to honor picket lines in solidarity with a strike by immigrant workers in Los Angeles led to an inspiring victory.</P>
<P>The previous evening, United Steelworkers of America President Leo Gerard brought the crowd to its feet when he denounced the repressive tactics used against global justice activists as an attempt to "criminalize dissent."</P>
<P>About 1,000 people, including 150 students, attended JwJ's biggest and liveliest event. The meeting began with in-depth pre-conferences on a range of topics, such as global justice, health care and student-labor alliances. Workshops took up nuts-and-bolts topics from union organizing to fighting privatization.</P>
<P>Other sessions highlighted JwJ's role in helping to organize day laborers and outreach to community organizations. One highlight was an international solidarity panel, featuring speakers from Haiti, France, South Africa and Brazil and chaired by Barbara Shailor, a JwJ founder who is now director of the AFL-CIO's International Affairs Department.</P>
<P>"It has not been since the '30s, when communists and socialists played a major role in organizing the CIO, that leftists and liberals and trade unions joined forces in demanding greater rights for the poor and working people," she said. Shailor's remarks highlighted the changes from the days when the AFL-CIO's international policy reflected the Cold War agenda of the U.S. government.</P>
<P>The conference brought together some of the most committed activists in the labor and student movements. Rank-and-file workers were a minority at the conference, however. And some of the important informal debates over the weekend--from how to fight layoffs to how to counter some top union leaders' collaboration with Bush--deserved to be part of the formal agenda.</P>
<P>By taking up such issues among union members, JwJ can play an important role at a time when unions face their biggest challenges in years.</P>

