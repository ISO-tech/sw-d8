<P><font face="Arial, Helvetica, sans-serif" size="2"><b>AUSTRALIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Politicians spew filth at asylum seekers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FOR NEARLY two weeks, more than 400 refugees from Afghanistan became political hostages in a battle over the rights of asylum seekers in Australia.</P>
<P>The standoff began August 26 when a Norwegian freighter rescued the refugees from their sinking ship in the Indian Ocean. The freighter took them to Australia, where they hoped to get asylum.</P>
<P>But the Australian government refused to accept them. Showing not the slightest bit of sympathy for the desperate plight of the refugees, Australian political leaders took the opportunity to spew racist filth.</P>
<P>Prime Minister John Howard, who heads the right-wing coalition government of the Liberal and National parties, led the attack. He announced plans to build three more military-run concentration camps for asylum seekers. "We have to redouble our efforts to make it less attractive to come to Australia," Howard told a Melbourne radio station.</P>
<P>Sadly, the opposition Labor Party was no better. "For all their bravado and chest beating, [the government has] failed miserably to stem the numbers of asylum seekers arriving on our coastline," said Con Sciacca, the Labor spokesperson on immigration.</P>
<P>In the end, the Australian Navy dumped the refugees in Papua New Guinea. The media were filled with phony stories about "floods" of immigrants "invading" Australia. In fact, the number of refugees to the country last year didn't even fill the government's established quota.</P>
<P>Howard's real reason for attacking immigrants is scapegoating--to shift the blame for an increasingly unpopular economic program of austerity, privatization and union busting.</P>
<P>In several local elections this year, the Labor Party thrashed Howard's coalition at the polls. Another beneficiary of the government's unpopularity has been the racist, anti-immigrant One Nation Party, which has averaged about 7 percent of the vote in rural areas.</P>
<P>Howard hopes to steal One Nation's thunder to divert attention from his own sorry record. Australians have to stand up against the politicians' racist scapegoating.</P>

