<P><font face="Times New Roman, Times, serif" size="4">Thirty years after the revolt by men at the very bottom of society</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Attica Rebellion</b></font></P>

<font face="Arial, Helvetica, sans-serif" size="2"><B><I><P>The Ghosts of Attica,</B></I> directed by Brad Lichtenstein, produced by Lichtenstein and David Van Taylor, premiering September 9, at 9 p.m. (EDT) on Court TV.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by ALICE KIM</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON SEPTEMBER 9, 1971, some 1,200 inmates at Attica prison in upstate New York revolted against inhuman conditions. Thirty years later, a new full-length documentary called <I>The Ghosts of Attica</I> will premiere on Court TV in September.</P>
<P>With nearly 2 million people languishing behind bars in the U.S. today, the story of Attica needs to be told. In a standoff that lasted five days, the rebelling prisoners stayed strong in their determination to win their 28 demands for better conditions.</P>
<P>What began as a spontaneous riot quickly became a highly organized rebellion. Inmates took over Cell Yard D. There, they put up makeshift tents for sleeping, organized food distribution and even set up a hospital. They democratically elected a leadership to negotiate their demands.</P>
<P>But their hopes were crushed. Under orders from New York Gov. Nelson Rockefeller, about 500 state troopers attacked the prison compound, firing more than 2,000 rounds of ammunition in nine minutes. The assault left 39 people dead, including 10 guards who were being held as hostages. Photos of the scene show inmates being shot at even as they retreated.</P>
<P>Within minutes, the state retook the prison. But the repression was far from over. Prisoners were stripped naked and forced to crawl in the mud and walk on broken glass. They were physically assaulted, burned with cigarettes and threatened with castration and death. These torture practices continued for months.</P>
<P>State officials tried to cover up what took place at Attica. The first reports from prison authorities claimed that rebelling prisoners slit the throats of guards when the assault began--a lie that the mainstream media were quick to spread. But autopsies of the dead hostages revealed that they had been killed by bullets from Rockefeller's gunmen.</P>
<P>The Attica rebellion captured worldwide attention. Under heavy pressure, Rockefeller was forced to establish a commission to investigate the riot, its causes and the aftermath. The commission concluded that the prisoners' rebellion was the result of "frustrated hopes and unfulfilled expectations"--and that Rockefeller's assault caused "senseless killings."</P>
<P>These findings were obvious to the inmates. As Richard X Clark, a leader of the rebellion, later wrote: "I'll tell you what caused the riot at Attica: Attica." The prison was a fortress, with a 30-foot-high, 2-foot-thick wall and 14 gun towers surrounding the compound. Inside the walls, the atmosphere was charged with racism.</P>
<P>More than 60 percent of Attica's prison population--which numbered 2,200 inmates in 1971--was Black or Latino, while 100 percent of the guards were white.  Blacks received the worst job assignments and the harshest discipline. Forced to spend 14 to 16 hours in cells no bigger than a bathroom, the inmates were treated like caged animals. </P>
<P>During the rebellion, men who had been treated like the scum of society--both inside and outside prison walls--took charge of their own lives and organized a democratically run inmate community. These "Attica brothers" were radicalized by the massive social upheavals of the 1960s and 1970s--from the Black Power movement to the struggle against the Vietnam War.</P>
<P>And during their struggle, the inmates were themselves transformed. "Suddenly, the sun was shining, and everyone was smiling," Clark wrote later of the rebellion. "I felt liberated; I had a sense of freedom... I don't think I'll ever forget that first night out in the yard. There was a feeling of peace and security I had never known before. Certainly not in all the time I had been inside prison."</P>
<P>The inmates also overcame deep racial divisions. <I>New York Times</I> reporter Tom Wicker, who the prisoners asked to serve as an observer, wrote: "The racial harmony that prevailed among the prisoners--it was absolutely astonishing...That prison yard was the first place I have ever seen where there was no racism."</P>
<P>The story of the Attica Rebellion shows people fighting back under the most repressive circumstances--men at the very bottom of society who rose up and demanded an end to their oppression.</P>

