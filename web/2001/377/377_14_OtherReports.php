<P><font face="Times New Roman, Times, serif" size="4">Free Mumia Abu-Jamal</font><br></P>
<font face="Times New Roman, Times, serif" size="3"><P>PHILADELPHIA--Activists are building for a September 15 demonstration to support Pennsylvania death row political prisoner Mumia Abu-Jamal.</P>
<P>Mumia's new lawyers are asking state court judge Pamela Dembe to consider new evidence in the case--including a signed affidavit from a man named Arnold Beverly, who says that he, and not Mumia, shot Philadelphia police officer Daniel Faulkner in December 1981.</P>
<P>The lawyers will file their brief in early September, and prosecutors will have two weeks to respond. Mumia's supporters hope the demonstration will put pressure on Dembe to hear the new evidence.</P>
<P>An August 17 protest drew some 2,000 people to Philadelphia for a rally and march outside the courthouse where Dembe held an earlier hearing.</P>
<P>Mumia's supporters will gather September 15 at 1 p.m. on the east side of City Hall.</P>
