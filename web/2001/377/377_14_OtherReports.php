<P><font face="Times New Roman, Times, serif" size="4">Unwelcome Boeing</font><br></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Protesters threw an "unwelcome" party September 4 for Boeing Corp. at its posh new headquarters here. The Anti-Boeing Coalition formed to protest $4.5 billion in tax breaks that the state of Illinois and Chicago Mayor Richard Daley used to lure Boeing to Chicago.</P>
<P>More than 50 people marched and chanted, "Money for health care, not for warfare," and "Boeing, Boeing, we cry shame, Star Wars is not a game!"</P>
<P>Miguel Morales of the National Boricua Human Rights Network told the crowd that Boeing manufactured the F-18 Hornet jet that killed David Sanes two years ago in Vieques. "We cannot allow the manufacture, sale and distribution of military arms whether we are from Vieques, Puerto Rico, Kuwait or Chicago."</P>
<P>Palestinian activist Hakim Hussein connected Boeing to the massacres in Palestine. "We don't want a polluting, racist company in Chicago," said Hussein. "We don't want a company that produces weapons for the ethnic cleansing of Palestine."</P>
