<P><font face="Times New Roman, Times, serif" size="5"><b>Justice for Madison Hobley!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Prosecutors have been ordered to put up or shut up at a September 17 hearing into the case of Illinois death row prisoner Madison Hobley. Hobley was convicted and sentenced to death in 1987 for setting a fire that killed his wife, child and five other people.</P>
<P>But the evidence against him has fallen apart. The most spectacular example is the gas can that police found in the gutted building--and claimed Hobley used to set the fire. But the can shows no fire damage from a blaze that consumed an entire apartment building.</P>
<P>The other main evidence against Hobley is a "confession"--which was tortured out of him by Chicago cops. Hobley is a member of the Death Row 10--a group of Illinois death row prisoners who were all tortured and have organized themselves to demand justice.</P>
<P>Hobley's case has been scheduled for hearings throughout this year, but prosecutors requested one delay after another. In July, a judge finally told prosecutors that they would have to come to court prepared on September 17.</P>
<P>Hobley could walk free from this month's hearing. But it will be 14 years too late. And he will leave behind other victims of Chicago police torture on death row who deserve justice, too.</P>

