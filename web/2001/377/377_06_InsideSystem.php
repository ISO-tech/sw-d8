<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>In defense of empire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>POLITICIANS AND pundits generally go to great lengths to deny that the U.S. is an "imperialist" country. But recently, a few right-wing military strategists have argued that the U.S. is indeed imperialist--and should be proud of it.</P>
<P>"There's not all that many people who will talk about it openly," said Thomas Donnelly, deputy executive director of the Project for the New American Century. "It's discomforting to a lot of Americans. So they use code phrases like 'America is the sole superpower.'"</P>
<P>With the end of the Cold War, Donnelly observes, the U.S. actually expanded its global military presence. There are 20,000 troops in the Persian Gulf, 37,000 troops in South Korea, peacekeepers in the Balkans and battleships spread across the world--giving the U.S. a foothold on every continent.</P>
<P>Donnelly and his co-thinkers insist, therefore, that politicians should just admit that the U.S. is imperialist so they can get down to the business of putting more money into military spending. "We'd better understand the full range of tasks we want our military to do, from the Balkans-like constabulary missions to the no-fly zones [over Iraq] to maintaining enough big-war capacity," says Donnelly.</P>
<P>Andrew Bacevich, a retired army colonel and professor of international relations at Boston University, agrees that politicians need to come clean about the U.S.'s imperial role. "In all of American public life, there is hardly a single prominent figure who finds fault with the notion of the United States remaining the world's sole military superpower until the end of time," Bacevich recently wrote.</P>
<P>So, Bacevich concluded, "The practical question is not whether or not we will be a global hegemon--but what sort of hegemon we'll be."<BR>
   --<I>Washington Post,</I> August 21, 2001</P>

<P><font face="Times New Roman, Times, serif" size="5"><b>BBC bends to the butchers</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL NOT only enjoys a massive military advantage over the Palestinian resistance in terms of weaponry. It's also using its diplomatic influence in the West to win the war over words.</P>
<P>Israel's latest victory is to get the British Broadcasting Corp. (BBC) to ban its Middle East correspondents from referring to Israel's policy of murdering its opponents as "assassination." From now on, reporters are supposed to use the euphemism used by Israel itself--"targeted killings."</P>
<P>BBC assignment editor Malcolm Downing issued the new guidelines in a memo that stated the word "assassinations...should only be used for high-profile political assassinations." There are "lots of other words for death," Downing added.</P>
<P>Israeli death squads or missile-firing Israeli helicopter pilots have killed more than 60 Palestinian activists--and numerous civilians--in the last year. The Palestinian killing of Israelis, however, is regularly referred to as "murder" or "assassination."</P>
<P>Downing's memorandum suggests that the murder of the late Prime Minister Yitzhak Rabin--killed by an Israeli extremist--is worthy of the word "assassination" while the killing of Palestinians is not.</P>
<P>The ban followed a discussion between Downing and Vin Ray, deputy head of news gathering at BBC World TV. Israeli diplomats have repeatedly met with BBC officials to complain that its coverage was anti-Israeli and pro-Palestinian.<BR>
   --<I>Independent</I> (Britain), August 4, 2001</P>

<P><font face="Times New Roman, Times, serif" size="5"><b>Cop shoots cop</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT ALL started when a Seattle cop on a bike reported a police car stolen. A cop car sped off in pursuit--but lost the trail when it had to stop at an intersection to check for traffic.</P>
<P>Then another police car pulled up and--mistaking the cop car at the intersection for the stolen vehicle--rammed it.</P>
<P>The three officers involved--two in one car and one in the other--fired more than 20 rounds at each other before they discovered their "error."<BR>
   --Associated Press, July 11, 2001 </P>

