<P><font face="Times New Roman, Times, serif" size="5"><b>It's time to unite the fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>DAVE ZIRIN</B> talks about organizing for global justice in Washington, D.C.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ACTIVISTS IN Washington are organizing to spread the message of the global justice movement to communities across the District. We launched a Unite the Fight tour this summer with the goal of speaking at every church, community center, sports club or union hall that gave us 30 minutes to make our pitch.</P>
<P>The mainstream media have tried to downplay the series of protests against international financial institutions like the IMF and World Bank by highlighting the fact that they have so far been largely limited to students and youth. Our tour was an attempt to change that.</P>
<P>At each stop, the discussion centered on the issues raised by the global justice movement and how they're connected to struggles and grievances that D.C.'s overwhelmingly Black and Latino population face daily. As Rev. Graylan Hagler, a veteran civil rights activist in D.C., put it: "It's essential that we support these protesters. For those of us in D.C. who have seen an unelected Control Board trump the democratic process, privatization schemes close D.C. General Hospital--all while witnessing attacks on the city's most vulnerable people in the name of 'fiscal responsibility'--these issues ring very close to home."</P>
<P>After a summer of attending meetings at every imaginable location--from picnics to health clinics, from soccer clubs to schools, from bars to churches--I can say that, in a country as savagely unequal as the U.S., the issues raised by the global justice movement hit very close to home. D.C. residents had questions and concerns about protester violence and vandalism.</P>
<P>But it was the issues of the global justice movement that resonated. At one stop in a Latino health clinic, Sonia told us: "The IMF and World Bank are the reason many of us had to come to the United States. We would all like to live near our families, but the poverty and war that the IMF and World Bank create make that impossible."</P>
<P>At another stop, Mario told the meeting: "We've seen them shut down D.C. General. We're seeing them build a wall in our city. We need to show what side we, as a community, are on."</P>
<P>The steps we take now to unite the fight are laying the basis for a broader and more powerful new left.</P>

