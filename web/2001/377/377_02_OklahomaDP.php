<P><font face="Times New Roman, Times, serif" size="4">Oklahoma City police chemist faked the crucial evidence</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The innocent man they put to death</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ERIC RUDER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"I'M INNOCENT, and I've got peace in my heart, and I'm ready to go home." Those were among the last words uttered by Malcolm Rent Johnson before the state of Oklahoma took his life on January 6, 2000.</P>
<P>A year and a half later, his innocence is nearly proven. But it will come too late.</P>
<P>Johnson is one of 12 people executed in Oklahoma who were convicted on the basis of testimony by Oklahoma City police chemist Joyce Gilchrist. Gilchrist was exposed earlier this year for mishandling evidence and lying under oath in thousands of criminal cases over a 25-year period.</P>
<P>Johnson's case--and his near-certain innocence--came to light after state officials ordered a review of 1,700 convictions where Gilchrist's testimony played a part.</P>
<P>At Johnson's trial, Gilchrist testified that Johnson's blood type matched sperm collected from the apartment of Ura Thompson, a 76-year-old woman killed in 1981. But a reexamination last month of Gilchrist's "evidence" found that the slides she prepared contained no sperm at all!</P>
<P>The reexamination was conducted by Oklahoma City police DNA laboratory manager Laura Schile and endorsed by three other chemists. But after issuing the report, Schile resigned--following a confrontation with the lab's chief. "She was intimidated by the Oklahoma City Police Department and some of the lawyers involved in this case," said Schile's lawyer, Gavin Isaacs.</P>
<P>State officials are claiming that Johnson would have been convicted without Gilchrist's testimony. What garbage! The only other evidence against him was circumstantial.</P>
<P>Oklahoma authorities aren't willing to admit that they executed an innocent man. "We have used for the last 25 years bad science in this state to convict people, and we have stretched the truth," said James Bednar, head of Oklahoma's Indigent Defense System and a former assistant state attorney general. "It's got to stop."</P>
<P>Gilchrist's willingness to lie in order to get convictions is only the tip of the iceberg in the U.S. criminal injustice system. In West Virginia, forensic "specialist" Fred Zain is facing five felony fraud charges for false testimony. In Idaho, Charles Fain walked off death row last month after DNA tests proved that the forensic evidence used against him 17 years ago was faulty.</P>
<P>And in Illinois--in a case remarkably similar to Gilchrist's--officials are reopening at least nine cases in which police forensic scientist Pamela Fish gave either false or misleading testimony. The most stunning revelation so far is that Fish withheld evidence in a 1986 murder trial of four Black teenagers--who may now be released.</P>
<P>Every example of overzealous prosecutors and lying police scientists helps to make our case against the death penalty--and the rest of the rotten injustice system.</P>

