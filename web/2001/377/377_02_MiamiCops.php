<P><font face="Times New Roman, Times, serif" size="5"><b>How Miami cops tried to get away with murder</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MIAMI--Nearly a dozen cops were charged earlier this month with planting evidence and cover-ups in a string of police murders. And authorities say more indictments are on the way.</P>
<P>The 11 Miami officers were accused of planting guns at crime scenes, stealing evidence and lying to avoid prosecution. The indictments are the result of a federal investigation into the city's highest-profile police killings.</P>
<P>In one case, a 73-year-old man died in 1996 in a hail of 123 cop bullets during a drug raid that found no drugs. Police falsely claimed that the man had fired at them. In another, two Black teenagers were shot at 37 times while fleeing a robbery scene in 1995. Police planted guns near the bodies of their victims and claimed they were armed.</P>
<P>Two other cops have pleaded guilty, and officials in charge of the investigation say that more will be arrested. The scandal promises to rock Miami's police department--with a similar impact to the uncovering of a ring of corrupt killer cops in the LAPD Ramparts Division.</P>
<P>City officials say that the arrested officers are a few bad apples. But these departments are rotten to the core.</P>

