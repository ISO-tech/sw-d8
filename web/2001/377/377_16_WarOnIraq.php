<P><font face="Times New Roman, Times, serif" size="5"><b>Stop the war against Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 16</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE EARLY August, U.S. warplanes have pounded targets in northern and southern Iraq at the rate of three or four raids a week. But you'd barely know it to read the mainstream U.S. press. The media have stopped reporting on the U.S. government's war against Iraq.</P>
<P>Yet the U.S.--with the help of its British lapdog--still patrols the so-called "no-fly" zones over northern and southern Iraq. And United Nations sanctions--imposed in the run-up to the 1991 Gulf War and maintained mostly at the U.S.'s insistence--continue to kill 5,000 Iraqi children a month.</P>
<P>U.S. officials admit that the sanctions are "falling apart," in the words of Secretary of State Colin Powell. But the Bush administration has been to trying to win support for repackaging the embargo on Iraq--and using the same old phony explanation that sanctions will punish Iraqi President Saddam Hussein.</P>
<P>"Imagine that Saddam Hussein had taken over the World Trade Center in New York, and there were 10,000 Americans trapped inside with him," wrote Reggie Rivers, a former Denver Broncos football player and now a <I>Denver Post</I> columnist. "In an attempt to force him out, we turned off the electricity so the building boiled during the summer and froze during the winter...How can you agree with an embargo that aims to unseat Hussein by killing off the youngest, oldest and most infirm people in Iraq?" </P>

