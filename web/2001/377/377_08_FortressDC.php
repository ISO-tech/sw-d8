<P><font face="Times New Roman, Times, serif" size="4">Washington walled off</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>FORTRESS D.C.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>CYRUS FOREMAN</B> and <B>ALAN MAASS</B> report on the fence in D.C.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>


<font face="Times New Roman, Times, serif" size="3"><P>NINE FEET high, on top of concrete highway barriers. A two-and-a-half mile perimeter. Enclosing 220 acres of land. Twenty-seven streets blocked.</P>
<P>The U.S. government is building a fortress in the heart of Washington, D.C. Inside its chain-link walls, some of the world's most powerful men and women will meet at the end of September.</P>
<P>They'll discuss policies and programs that affect the lives of hundreds of millions of people around the world. And they don't want to be disturbed.</P>
<P>So George W. Bush and the gang are building them an enormous fence--to keep out the tens of thousands protesters who want to make it plain that they, in the words of one <I>Washington Post </I>writer, "doubt it is possible to end poverty while defending wealth."</P>
<P>The $2 million fence is only one small part of Washington's preparations to deal with protests during the joint meeting of the International Monetary Fund (IMF) and World Bank on September 29 and 30. The U.S. government and the District of Columbia are splitting the $29 million tab for a massive police operation.</P>
<P>Some 3,000 cops have been recruited from up and down the East Coast to reinforce the D.C. police department. One thousand cops are coming from New York alone--in a contingent organized under the gung ho name of the Washington Expeditionary Force. Fittingly, the NYPD Black and Blue will be housed on the U.S. Marine base at Quantico, Va.</P>
<P>"The alternative to this wretched excess," wrote the <I>Bangor (Maine) Daily News</I> in a biting editorial, "would be, of course, for the IMF and World Bank to stop looking for ways to separate themselves from those with concerns about globalization and to begin taking those concerns seriously. The concerns are genuine--despite massive amounts of what appears to be activity, the two organizations have little to show for their efforts to alleviate the persistent poverty, disease and lack of education that plague too many people in too many countries."</P>
<P>The IMF and World Bank's fence will enclose their headquarters and the White House--along with apartment buildings, stores, restaurants and a church. Also inside the perimeter: George Washington University (GW), where many students are critical of the IMF and World Bank's policies--prompting fears among federal officials that the campus could be an organizing center for protesters.</P>
<P>So President Stephen Trachtenberg came up with a compromise. Shut down the school. "What about academic freedom?" asked GW student Ryan Conley. "I thought we were allowed to express our thoughts on a college campus."</P>
<P>Just not when the IMF and World Bank want to meet. GW's 5,400 students who live in campus dormitories will be kicked out--to make room for police, who will have the run of the buildings during the protests. Administrators have also announced a "no guest" policy in the dorms, starting a week before the protests and ending a week after they're over.</P>
<P>The restrictions sparked anger at GW, where a hastily organized rally called the day after Trachtenberg made his announcement drew some 150 students. That response was mirrored by activists throughout Washington and around the country who are fed up with the government's attempt to silence opposition.</P>
<P>We'll be in Washington--to tell the IMF, the World Bank and the politicians that we don't want our world sold to the highest bidder.</P>

