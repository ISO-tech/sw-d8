<P><font face="Times New Roman, Times, serif" size="4">Protesting is not a crime</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why we're marching in Washington</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY SAY that the U.S. is a beacon of democracy and human rights for the whole world. But in the capital city of that "democracy," the authorities will do anything to keep us quiet.</P>
<P>When some of the world's most powerful men and women meet for the International Monetary Fund (IMF) and World Bank annual conferences in Washington, D.C., later this month, they'll be protected by a nine-foot-high, two-and-a-half-mile-long, $2 million, chain-link-and-concrete fence.</P>
<P>This wall of shame is only one part of Washington's preparations to stop demonstrators from making their voices heard. Federal and city officials are splitting the cost of a $29 million police operation. The cops recommended that the campus of George Washington University, which falls inside the fence perimeter, be closed--and to their shame, administrators are cooperating.</P>
<P>To justify all this, officials are spreading scare stories about demonstrators bent on "violent behavior." But it's the police who are preparing to be violent.</P>
<P>Meanwhile, inside the fence, the bankers and businesspeople and politicians will discuss policies that affect hundreds of millions of people around the globe. But they don't want to hear from any of them--because the real job of the IMF and World Bank is to serve the corporate and political elite that pulls their strings.</P>
<P>Around the world, these institutions are hated because of the poverty and desperation that their policies cause. Take their latest victim, Argentina, for example. Earlier this year, the IMF stepped in with bailout loans to "rescue" Argentina's financial system from a meltdown that would have shaken some of the world's biggest banks.</P>
<P>But there are strings attached--Argentina's government was ordered to slash wages for public workers, cut government spending and privatize state-run enterprises. "Those Argentines who can afford it least will be squeezed the most so that the IMF can hide the failure of its policies for a little longer," Mark Weisbrot, of the Center for Economic and Policy Research, wrote in the <I>Los Angeles Times</I>.</P>
<P>But the IMF's policies aren't just a failure. They've become the focus for intense opposition. Argentina itself has seen a series of general strikes. And the international meetings of global financial institutions like the IMF have become the focus of huge mobilizations of people who want to see global justice. That's why the IMF and World Bank are meeting in a fortress. They don't want their business to be disturbed.</P>
<P>Around the world, governments have gone to greater and greater lengths to silence opposition from the growing global justice movement. Their attacks reached a terrible new high point with the police murder of 23-year-old demonstrator Carlo Giuliani during protests against the Group of Eight summit in Genoa, Italy, in July.</P>
<P>This growing repression has led some in the movement to question whether activists should continue to mobilize for big demonstrations like the one planned for Washington. But to retreat would hand their side a victory--by allowing them to intimidate us into silence. In fact, the Genoa demonstrations this summer--like the protests in Seattle, Washington, Quebec and elsewhere before that--served as a focus for organizing that put forward our message on a global scale.</P>
<P>"Tens of thousands will march on Washington in the tradition of all people who have historically sought change and social justice," said Mara Verheyden-Hilliard of the Partnership for Civil Justice. "When people are excluded from decision-making, the only way to make change is to take to the streets, the sidewalks, the parklands, to make their voices heard."</P>
<P>Our message is simple: Another world is possible! Come to Washington, D.C., at the end of September and build the struggle for that alternative.</P>

