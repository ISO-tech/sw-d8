<P><font face="Times New Roman, Times, serif" size="5"><b>Explaining Darwin's dangerous idea</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><I><P>Evolution,</B></I> co-production of the WGBH/NOVA Science Unit and Clear Blue Sky Productions. The seven-part documentary series premieres September 24-27, 2001, on PBS stations. </P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by PHIL GASPER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN half of the U.S. population believes that the earth was created in the last 10,000 years, according to opinion polls. About the same number doesn't know that the earth orbits around the sun. And only 10 percent of the population accepts Charles Darwin's theory of evolution.</P>
<P>Such figures aren't surprising. The public schools in 18 states received Fs and Ds for their teaching of evolution in a recent study published in <I>Nature</I> magazine. Alabama, Texas and Nebraska teach evolution as just one possible explanation for the existence of life--and in Alabama, science textbooks include a warning that evolution is theory and not fact. But as study author Lawrence Lerner notes, "Nothing in biology makes sense except in the light of evolution."</P>
<P>During the past 25 years, Christian fundamentalists and the religious right have tried to block the teaching of evolution and smuggle religious ideas into the classroom under the guise of "creation science" and "Intelligent Design Theory." Think tanks like the Seattle-based Discovery Institute, led by conservative Republicans, churn out a steady barrage of propaganda defending these views.</P>
<P>Seattle is also the source of funding for <I>Evolution,</I> an eight-hour PBS mini-series designed to show why evolution is a scientifically compelling idea. Microsoft billionaire Paul Allen put up the money for the series. With Allen's bucks, PBS has come up with a spectacular explanation of Darwin's understanding of biology and the overwhelming evidence in its favor. </P>
<P>The two-hour opening program--"Darwin's Dangerous Idea"--weaves together a dramatic reconstruction of Darwin's life with documentary sequences about current scientific research. Later programs look at such issues as the role of competition and cooperation, mass extinction, the evolution of sex and human evolution.</P>
<P>As creationists renew their attacks on science, <I>Evolution</I> could not have come out at a better time. For many viewers, deprived of a decent science education, it will be their first in-depth exposure to the power and beauty of Darwin's ideas.</P>

