<P><font face="Times New Roman, Times, serif" size="4">Palestinians face savage violence</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's terror Made in the USA</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ERIC RUDER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AGAINST THE backdrop of Israel's stepped-up assassinations of Palestinian leaders, the Israeli Defense Ministry earlier this month announced a plan to create a "security zone" in the West Bank and Gaza. Israeli Prime Minister Ariel Sharon delayed the plan--but insisted that it was "meant to happen in the future."</P>
<P>With tensions at a fever pitch throughout August, the proposal for a "security zone" is the latest illustration of Israel's moves toward an all-out war against Palestinians. The plan calls for creating "a strip of dead ground--designated a 'closed military area'--in order to seal off the West Bank and Gaza from Israel," reported one writer for Israel's <I>Jerusalem Post</I> newspaper. "In other words, it substitutes a 'security zone' for the fence or wall that has been discussed for many years, but never built."</P>
<P>But the zone isn't "dead ground." "Thousands of Palestinians live in villages there, and they will find themselves in a closed military zone," according to an editorial in Israel's <I>Ha'aretz</I> newspaper. "The difficult conditions for the residents of a closed military zone and the restrictions that will be imposed on their freedom of movement and activity will make the cruelty of the occupation even more tangible."</P>
<P>The talk of a "security zone" followed Israel's escalation of its policy of assassinating Palestinian leaders--or as Israel prefers to refer to the killings, "pinpoint liquidations." Israeli officials say they are targeting known "terrorists." But the murders are clearly aimed at annihilating the leadership of the Palestine Liberation Organization (PLO).</P>
<P>On August 27, Israeli helicopters fired two laser-guided missiles into a West Bank office, killing Abu Ali Mustafa, general secretary of the Popular Front for the Liberation of Palestine, a key faction of the PLO. Four days later, the military tried to assassinate Khayis Abu Leila, the leader of the Democratic Front for the Liberation of Palestine, another main PLO faction.</P>
<P>Meanwhile, Israel's checkpoints and security blockades throughout the Occupied Territories continue to smother the Palestinian economy--and plunge the population into ever deeper poverty.</P>
<P>In the four years before the new Intifada began last fall, Palestinian unemployment dropped from 24 percent to 10 percent, according to Terje Roed-Larsen, the United Nations Special Coordinator for the Middle East. But with Israel's clampdown preventing more than 100,000 Palestinians from getting to their jobs inside Israel, joblessness rose to 38 percent in March, said Larsen.</P>
<P>Some 50 percent of the population of the Occupied Territories will be living below the poverty line by the end of 2001, according to the World Bank.</P>
<P>Because Israeli forces prohibit Palestinians from using tractors or cars to transport agricultural goods, Palestinian farmers have had to resort to using donkeys. "We've regressed by 50 years," said Ahmed Abu Aysheh, who has a vineyard outside Hebron. "People are moving around in airplanes, and we're going back to donkeys."</P>
<P>By every measure, Israel's cruelty has only grown more brazen. Yet the U.S. government has backed Israel every step of the way. Its latest show of support: walking out of the United Nations conference on racism when Arab delegates dared to criticize Israel's racist treatment of Palestinians.</P>
<P>When Israel murders a Palestinian leader, the missiles are likely to be "Made in USA"--since the U.S. government hands over billions of dollars a year to Israel, including the world's most sophisticated weaponry.</P>
<P>Activists in the U.S. are building the fight to stop U.S. aid to Israel--and are planning demonstrations in Montreal and New York City in September to demand justice for Palestinians. We have to oppose Israel's bloody violence.</P>

