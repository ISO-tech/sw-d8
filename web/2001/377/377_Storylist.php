<P><font face="Times New Roman, Times, serif" size="3"><b>Washington walled off</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="377_08_WashingtonWalled.shtml">Fortress D.C.</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Nine feet high, on top of concrete highway barriers. A two-and-a-half mile perimeter, enclosing 220 acres of land. The U.S. government is building a fortress in the heart of Washington, D.C., to keep protesters from disturbing some of the world's most powerful people as they discuss policies that affect our lives.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><A HREF="377_05_BoycottBell.shtml">Boycott the Bell!</A></b></font><BR>
