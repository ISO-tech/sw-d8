<P><font face="Times New Roman, Times, serif" size="3"><b>Protesting is not a crime</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377_01_ProtestingNotCrime.shtml">Why we're marching in Washington</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">They say that the U.S. is a beacon of democracy and human rights for the whole world. But in the capital city of that "democracy," the authorities will do anything to keep us quiet. When some of the world's most powerful men and women meet for the International Monetary Fund and World Bank annual conferences in Washington, D.C., later this month, they'll be protected by a nine-foot-high, two-and-a-half-mile-long, $2 million, chain-link-and-concrete fence.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Palestinians face savage violence</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="377_16_Israel.shtml">Israel's terror: Made in the USA</a></b></font><BR>
