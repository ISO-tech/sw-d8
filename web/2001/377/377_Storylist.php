<P><a href="377_16_WarOnIraq.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Stop the war against Iraq</b></font></a><BR>
<font face="Times New Roman, Times, serif" size="3">Since early August, U.S. warplanes have pounded targets in northern and southern Iraq at the rate of three or four raids a week. But you'd barely know it to read the mainstream U.S. press.</P>

