<P><font face="Times New Roman, Times, serif" size="4">Bush gang panics as economy slides toward recession</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>From miracle to mess</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FOR MONTHS, the Bush administration and economic experts have insisted that the U.S. economic slowdown was "bottoming out." Just wait until Federal Reserve Board interest rate cuts encourage investment and people start spending their tax rebate checks, they said. But the economy and hard-pressed consumers weren't listening.</P>
<P>So when the official unemployment rate jumped from 4.5 percent to 4.9 percent, Dubya and his minions hit the panic button. The White House threw together a press conference with Bush, Dick Cheney and Republican leaders in Congress--where Bush said that they were all "concerned" about the economy. "Any American out of work is too many Americans out of work," the commander-in-thief declared.</P>
<P>Dubya's solution? Get Congress to give him "fast-track" authority to ram through more free-trade deals and to pass his nuke-and-burn energy plan. Republican congressional leaders, meanwhile, called for more "pro-growth" tax cuts for the rich.</P>
<P>Besides being little more than handouts to big business, Bush's proposals will do little to revive the economy. That's because the recession that the U.S. faces is different from any slump in the past 25 years. "We've had one of the most severe profit recessions in 30 years, and you don't get over that one so quickly," Zurich Kemper Securities economist David Hale told <I>the Washington Post</I>.</P>
<P>For the first time since the mid-1970s, all of the leading economies in the world--including Europe and Japan--are entering recession at the same time. Japan's economy shrank by an annual average of 3.2 percent in the second quarter of 2001--while the U.S. economy grew at a barely perceptible 0.2 percent.</P>
<P>Corporations are talking about "retrenchment." But you can be sure that their executives won't be tightening their belts. In fact, CEOs now make 531 times the average factory worker's wage--compared to "only" 475 times last year.</P>
<P>These obscenely rich executives will try to get workers to pay the price for the recession. Already, more than 1.1 million workers in the U.S. manufacturing sector have lost their jobs in the last year. And the service sector of the U.S. economy is also shrinking, with layoffs hitting there as well. Personal bankruptcy filings in the second quarter of 2001 hit a record 400,000--25 percent higher than the quarter before.</P>
<P>All of this means disaster in working peoples' lives. Thanks to Washington's cuts, fewer than half of unemployed workers will even be eligible for unemployment benefits. And with welfare and food stamps slashed, hundreds of thousands more people will face homelessness and hunger.</P>
<P>Now you might expect Bush to use the bad economic news to scheme for more help for corporations and the rich. But the Democrats have little to offer. They've been happy to pin blame for the recession and the disappearing budget surplus on Bush.</P>
<P>But instead of calling for increased spending to boost Medicare benefits, fund child care or increase aid to the unemployed, the Democrats are not to touch money generated from the Social Security payroll tax. In essence, they've left themselves only one alternative--cutting back federal spending at a time when more spending is needed.</P>
<P>The recession is exposing Bush as a shill for Corporate America and a disaster for ordinary people. Yet because of its slavish devotion to the same corporations, the so-called "party of the people" could actually help to rescue Bush.</P>
<P>Most of the gains from the economic boom of the 1990s flowed to the rich. Now all the costs of the recession are being offloaded onto workers. What a sick system. There's no better argument for a socialist alternative--a rational society that puts people's needs first.</P>

