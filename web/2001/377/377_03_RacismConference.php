<P><font face="Times New Roman, Times, serif" size="4">U.S. walks out on UN racism conference in South Africa</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Racists and hypocrites</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. delegation to the United Nations World Conference Against Racism in Durban, South Africa, was ready to walk as soon as it arrived. Recognize the Israeli government's racism against Palestinians? Apologize for slavery? That was too much to ask.</P>
<P>Secretary of State Colin Powell denounced the conference for using "hateful language" when delegates tried to draft a resolution that identified Zionism as racist. The U.S., along with Israel, accused Arab delegates of "hijacking" the summit and turning it into what Israeli Foreign Minister Shimon Peres called a "smear campaign against Israel."</P>
<P>And most of the mainstream media went along for the ride. "The very fact that the United Nations had once again toyed with the sort of Israel-bashing that characterized an era thought to be finished was alarming to many," grumbled the <I>New York Times</I>.</P>
<P>So what was the disturbing language? A draft document identified Israel as a "racist, apartheid state...characterized by separation and segregation, dispossession, restricted land access, denationalization, 'bantustanization' and inhumane acts." This is an accurate description.</P>
<P>Israel was founded on the displacement of Palestinians, bans those expelled from returning to their homes, keeps 2 million people under occupation, targets its enemies with assassination and routinely blockades entire cities.</P>
<P>Today, it is waging a one-sided war using the world's most advanced weaponry against the entire Palestinian population. But because Israel is a key ally in the Middle East, Washington decided to wreck an international conference rather than admit the facts.</P>
<P>The U.S. also wanted to prevent the conference from stating another obvious truth: that slavery is a "crime against humanity." The Bush gang was worried that if such language were included in a conference resolution, the U.S. could be liable for lawsuits demanding reparations for slavery.</P>
<P>Never mind that the wealth and power of the U.S. was founded on the backs of African slaves--and that African Americans still keenly feel the racist legacy of slavery today. "Would I get compensation for slavery, or would I pay it?" Colin Powell sneered.</P>
<P>The UN conference exposed the hypocrisy of the U.S. on the question of racism--from its shameful history of slavery and segregation to its might-makes-right foreign policy.</P>
<P>The failure of the Durban meeting can be a call to action for those who hate discrimination and injustice. We need to step up the fight against racism in the U.S. today--and build a movement to cut off U.S. ties to Israel's apartheid state.</P>

