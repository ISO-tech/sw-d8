<P><font face="Times New Roman, Times, serif" size="5"><b>"Dollars come before our needs"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 14, 2001 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>D.C. GENERAL, Washington's only public hospital, officially closed its doors July 16. Despite opposition from most residents, D.C. Mayor Anthony Williams and his allies on the federally appointed Control Board forced through a plan to privatize the hospital--using arguments similar to IMF and World Bank officials when they push privatization in poor countries.</P>
<B><P>VANESSA DIXON</B> is a member of the Health Care Now Coalition that has been fighting around the issue of D.C. General. She talked to <B>JULIANA KARR</B> about why she and other D.C. activists are organizing for the demonstrations against the IMF and World Bank in September.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>WE BELIEVE there's a connection between the international policies of the World Bank and IMF, and the local policies in Washington, D.C., of the mayor and Control Board. </P>
<P>On both the local level and the global level, these policies result in reduced services for low-income residents, multimillion-dollar tax breaks to corporations and public funds being used to finance failing enterprises.</P>
<P>On the global level, 60 percent of World Bank projects fail. On the local level, the D.C. government has entered into a privatization contract with Greater Southeast, a for-profit hospital with legendary financial problems that's still recovering from bankruptcy.</P>
<P>The World Bank and IMF operate in a highly secretive manner in which substantial decisions aren't open to public scrutiny or input. In Washington, meetings of the mayor and the Control Board with regard to privatizing D.C. General were held in secret.</P>
<P>Just as the nine-foot walls will go up to separate the banking elite of the world from democratic protesters, on the local level, the citizens of Washington were denied access by the police force to a meeting held by the Control Board that determined the fate of D.C. General.</P>
<P>The privatization of D.C. General shows what privatization is really about--handouts to corporations at the expense of human well-being. It's the same with the IMF and World Bank, which go to other countries and tell the neediest people that there's not enough money to spend on health care or food.</P>
<P>Decisions are made in terms of dollars, not human needs. There's great concern about not spending too much money on human beings.</P>
<P>The World Bank and IMF demonstrations are an opportunity for the public to become aware of the disgraceful policies that are destabilizing the economies of other countries and leading to loss of life. And they offer the opportunity for the public to realize that there's a very real connection between what happens in other countries via the World Bank and IMF and the same antidemocratic policies instituted locally in places like Washington, D.C.</P>
<P>The World Bank and IMF and the mayor's office think that the public doesn't care what happens to the less fortunate. Wrong, wrong, incredibly wrong!</P>
<P>We remind the World Bank, the IMF and the mayor of Washington, D.C., that there's a clear and overriding responsibility of government to ensure the public health, welfare and safety--both locally and globally.</P>

