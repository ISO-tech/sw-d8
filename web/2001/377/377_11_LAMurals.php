<font face="Times New Roman, Times, serif" size="5"><b>An art museum on the streets of LA</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by STUART EASTERLING</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THERE'S AN art museum in Los Angeles whose exhibit space includes the walls of dry cleaning companies, auto repair shops, drainage canals, public housing projects and grade schools.</P>
<P>The objects on display are mural paintings. But in a city that boasts the glitter of Hollywood and the beaches of Santa Monica, few pause to look.</P>
<P>They should. With well over 1,000 public murals, LA is widely considered the "capital of muralism" in the U.S.</P>
<P>Most of the murals were painted by Mexican American artists during the 1970s at the height of the Chicano Power struggle. These artists were trying to create a collective, community-based art that would reflect the ideals and politics of the movement.</P>
<P>"Chicano Time Trip," for example, is a 90-foot-long work along Broadway Avenue, painted in 1977 in response to the celebration of the U.S. bicentennial. The mural depicts the history and struggles of Chicano people that were left out of the party.</P>
<P>On the walls of the Estrada Courts housing project is "Moratorium," a striking black-and-white mural consisting of a montage of images from the Chicano movement, some semi-surreal and some photo-realistic.</P>
<P>Perhaps the finest example of muralism in Los Angeles is located in the Tujunga Wash drainage canal--popularly referred to as "The Wall of LA." The mural shows the history of California from the dinosaurs to the 1970s. It was completed over a period of five years by a multitude of artists, led by the Chicana muralist Judith Baca.</P>
<P>The Wall of LA mural is 13 feet high and runs for more than half a mile. Among its 36 panels are "The Zoot Suit Riots," "Japanese Internment," "Farewell to Rosie the Riveter," "The Origins of Gay Rights," and "The Birth of Rock and Roll." </P>
<P>One panel, "Division of the Barrios and Chavez Ravine" shows the forced displacement of Mexican families from Chavez Ravine when Dodger Stadium was built in the 1950s. A menacing, spaceship-like Dodger Stadium flies over the community, while in the foreground, a cop drags off a young Chicana woman with her fist in the air.</P>
<P>These and many other murals make up a treasure of public art in LA. </P>

<I><P>Find out more about the LA murals on the Web at www.lamurals.org.</i></P>
