<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The trial of Ron Carey: An attack on our unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE TRIAL of former International Brotherhood of Teamsters President Ron Carey is a political attack aimed at the heart of organized labor. Federal prosecutor Mary Jo White is not only pursuing charges against Carey for perjury over alleged financial wrongdoing, but has implicated other top union leaders as well, including AFL-CIO Secretary-Treasurer Richard Trumka, AFSCME President Gerald McEntee and SEIU President Andrew Stern.</P>
<P>The charges against Carey center on allegations that money from the union treasury was laundered to provide funds for his reelection campaign in 1996. Yet no one has ever proved--or even argued--that Carey masterminded a deal that involved trading donations among the Teamsters, the AFL-CIO and the liberal group Citizen Action.</P>
<P>A Carey campaign aide who pleaded guilty for arranging this transaction said that Carey knew practically nothing about it. But that was enough for government officials to order a new election for president in 1998 and ban Carey from running. The Teamster's Internal Review Board--created in the 1989 consent decree that gave the government oversight of the union--later expelled Carey from the Teamsters.</P>
<P>James P. Hoffa was allowed to run in the new election and take office despite rampant wrongdoing in his own camp. A government overseer even accepted Hoffa's claim that $2 million in election funds were proceeds from "cake sales." Hoffa got only a slap on the wrist for allowing his top adviser, Richard Leebove, to work on the campaign full-time while remaining on the payroll of a Teamster local run by a key ally.</P>
<P>That amounts to using members' money to fund a campaign--the same charge that the government used to oust Carey. Hoffa got away with all this because he's a conservative who seeks collaboration with corporate chiefs. By contrast, Carey was willing to confront the employers--and proved it when he led the inspiring 1997 strike against UPS that proved labor had the power to beat one of the most aggressive outfits in Corporate America.</P>
<P>That earned him enemies among employers and labor-hating Republicans. The attack on Carey for election wrongdoing is outrageous considering that former President Clinton turned the Lincoln Bedroom into a cash cow and George W. Bush stole the White House.</P>
<P>The message couldn't be clearer: There's one law for top politicians and business leaders, and another for union leaders.</P>
<P>Rather than defend Carey and oppose the legal attack on unions, AFL-CIO President John Sweeney and other top officials have kept quiet and accommodated Hoffa in the hope of avoiding prosecution themselves. Nor has the Teamsters reform movement, led by Teamsters for a Democratic Union, or reform presidential candidate Tom Leedham taken up Carey's defense.</P>
<P>But if reformers don't take the issue of Carey's prosecution head on, Teamster members will only become cynical about the entire legacy of reform as Hoffa endlessly repeats charges of Carey's "corruption."</P>
<P>A defense of Carey has to be combined with a balanced view of his record. In the name of "realism," Carey made alliances with conservative Teamster officials such as George Cashman of Boston and John Morris of Philadelphia. Rank-and-file Carey supporters who had battled these figures for years were pressured into keeping quiet. And Carey increasingly relied on highly paid political consultants and Democratic operatives to run his disastrous 1996 reelection campaign.</P>
<P>This experience shows the limitations of reform from above--and underscores the need for an organized rank and file to keep the fight moving forward no matter who holds union office.</P>
<P>The Leedham campaign is continuing the Teamster reform movement--the effort to build a democratic, fighting union free of mob ties and corruption. The trial of Ron Carey is designed to discredit that movement--and weaken all of our unions as well.</P>
<P>Every Teamster--and every union member--should oppose this witch-hunt.</P>

