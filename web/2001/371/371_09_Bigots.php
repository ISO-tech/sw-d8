<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The bigots who claim sexism doesn't exist</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 9</font></p>
<p><font face="Times New Roman, Times, serif" size="3">"ARE YOU tired of male-bashing and victimology?" asked an ad, headlined "Take Back the Campus," which appeared in university newspapers last month.</P>
<P>"Have you been misled by factually challenged teachers?"</P>
<P>The ad--sponsored by the right-wing Independent Women's Forum (IWF)-- charges that university feminists are "a kind of cult" that cooks up facts about the frequency of campus rapes and women's low wages.</P>
<P>The <I>Columbia Daily Spectator </I>refused to run the ad; the <I>Yale Daily News </I>published it. </P>
<P>Women's rights activists at the University of California at Los Angeles (UCLA) organized protests when the ad ran in the <I>Daily Bruin, </I>demanding that the paper issue an apology and a retraction.</P>
<P>This prompted others to argue that the feminists were impinging on the IWF's right to "free speech."</P>
<P>It was a replay of the controversy that followed the publication of bigot David Horowitz's ads denouncing the idea of reparations for Blacks for the crime of slavery.</P>
<P>"The UCLA protesters insist on regarding women as a gender in peril and bristle when anyone suggests otherwise," concluded columnist Cathy Young in the <I>Boston Globe. </P>
</I><P>"As a result, what may be truly imperiled is the free exchange of ideas."</P>
<P>But the mainstream media's focus on free speech has shifted attention away from what is at the heart of the ad--an attack on anyone who takes a stand against sexism or violence against women.</P>
<P>Among the "10 most common feminist myths" the IWF's ad claims to debunk is that one in four women in college are the victims of rape or attempted rape.</P>
<P>Indeed, studies exist showing varying statistics--in 1998, the Centers for Disease Control said that one in five women are raped or are targets of attempted rape during their lifetimes. </P>
<P>Most of the IWF's numbers are rehashed from a 1995 book by Christina Hoff Sommers called Who Stole Feminism?</P>
<P>On the other hand, there's the reality that many women are too afraid or ashamed to report attacks to authorities.</P>
<P>Still, when it comes to uncovering the "truth" about anything, I'd tend to trust the WWF over the IWF.</P>
<P>The IWF is in the business of denying that sexism exists.</P>
<P>Conservative groups like the IWF have spent the last decade trying to prove that "political correctness" (PC) and women's "whiny over-sensitivity" to sexism is poisoning our universities, the media, the entertainment industry and governmental policy.</P>
<P>Maybe they didn't hear about the Dartmouth College fraternity whose newsletter named women that members had had sex with--and that promised ''patented date rape techniques'' in a future edition.</P>
<P>Perhaps they didn't follow recent high-profile cases of vicious sexual harassment--for example, at Mitsubishi Motors--over just the last couple of years.</P>
<P>Maybe they didn't know that 85 percent of counties in the U.S. don't have abortion providers.</P>
<P>Or that women on average earn just 75 cents to every dollar a man makes--a figure from the U.S. Bureau of Labor Statistics that the IWF still manages to take issue with.</P>
<P>And maybe they don't find it disturbing that advertisers see women's body parts as the best way to sell beer, movies and magazines.</P>
<P>The anti-PC crusaders' ultimate goal is to discredit the women's movement of the 1960s and 1970s--and roll back its gains, such as access to abortion.</P>
<P>They want to stamp out the idea that women's equality is something worth fighting for.</P>
<P>The fact that George W. Bush now occupies the White House--and began his four years by announcing anti-women policies like restricting the abortion drug RU-486--has made the right wing more confident to spout its sexist ideas.</P>
<P>Plus, there's Lynne Cheney--the wife of the vice president, who just happens to be a former board and now member-emerita of the IWF.</P>
<P>The UCLA activists did the right thing when they protested the IWF's sexist ad.</P>
<P>This is the only way to expose the bigots for what they are--and challenge sexism head-on.</P>

