<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Rallying for the Charleston Five</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We have to stand together"</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 16</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>MICHELE BOLLINGER and LEE SUSTAR report from Colombia, S.C.</B></P>
<P>COLUMBIA, S.C.--More than 7,000 union members marched on the South Carolina capitol June 9 to oppose the prosecution of five dockworkers on trumped-up riot charges.</P>
<P>AFL-CIO Executive Vice President Linda Chavez-Thompson was among the speakers at this spirited rally for workers' rights and racial justice.</P>
<P>When one speaker talked of the international day of action scheduled for the opening day of the workers' trial, the chant when up: "Shut the ports down! Shut the ports down!"</P>
<P>The five dockworkers--members of the International Longshoremen's Association (ILA) Local 1422--are being targeted by Attorney General Charlie Condon, who organized a police crackdown on an ILA picket line on January 20, 2000.</P>
<P>Known as the Charleston Five, they have been under house arrest for more than 17 months.</P>
<P>They are confined to their homes between 7 p.m. and 7 a.m., except to go to work or to attend union meetings--and have been forced to miss their children's school functions, a memorial service for a relative and much else.</P>
<P>But a growing defense campaign--endorsed by AFL-CIO President John Sweeney in January 2001--has already raised hundreds of thousands of dollars and spread word of the struggle.</P>
<P>And when Condon attacked union supporters and their "comrades" for organizing the rally, Sweeney and NAACP chair Julian Bond issued a statement denouncing Condon for red-baiting. </P>
<P>At the June 9 rally, speakers from the podium and rank-and-file union members in the crowd denounced Condon's effort to scapegoat an overwhelmingly Black union.</P>
<P>ILA Local 1422 has been central to both union organizing drives and the successful struggle to remove the Confederate flag from over the South Carolina statehouse.</P>
<P>Now Condon--backed by the fast-growing manufacturing employers in the state--wants to attack both labor rights and civil rights by jailing one white and four Black workers.</P>
<P>"Today, I'm really proud to see Black and white and Hispanics," Lawrence Lennon, a member of  ILA Local 1426 in Wilmington, N.C., told <I>Socialist Worker.</P>
</I><P>"I should say that all of the unions that you see represented here today know how important it is to be together, to stand together. </P>
<P>"The worst thing that happened to us was when Reagan fired the air traffic controllers. We were shut down and weakened then. But we're coming back now."</P>
<P>Unions from across the South turned out--including large delegations from the CWA, SEIU, UAW, Teamsters and UNITE, as well as the ILA.</P>
<P>Buses came from as far away as Detroit and New York City.</P>
<P>The Atlanta Central Labor Council paid for 20 buses, and the UAW organized busloads from three area plants.</P>
<P>The West Coast-based International Longshore and Warehouse Union (ILWU) brought more than 100 members--and ILWU President James Spinosa repeated his vow to shut the docks from Vancouver to Hawaii on the day that the case goes to trial.</P>
<P>Representatives from dockworkers' unions in Sweden and Denmark pledged to take action as well. </P>
<P>moving speeches</P>
<P>One of the most moving speeches was by Yu Kwang-jun of the Daewoo Auto Workers Union in South Korea.</P>
<P>"Workers in South Korea know about the vicious racism and repression faced by Black workers in South Carolina," said Yu, whose union has itself been the repeated target of police violence in its struggle to defend jobs.</P>
<P>Several top U.S. labor leaders--including United Mineworkers of America President Cecil Roberts and Coalition of Black Trade Unionists leader Bill Lucy--spoke at the rally.</P>
<P>Even conservative ILA President John Bowers--who was reluctant to endorse the campaign--was on hand.</P>
<P>Roberts got wild cheers when he vowed to go to jail with the Five, as did retired Southern Christian Leadership Conference leader Rev. Joseph Lowery.</P>
<P>The mood in the crowd--composed overwhelmingly of union members--was defiant.</P>
<P>"I'm here to support the Charleston Five--to fight the anti-labor practices that have been going on," Diane Jordan, a member of the UAW from Georgia, told <I>Socialist Worker.</P>
</I><P>"I've been a union activist for 22 years. To organize the South is a pretty tough job, but we're going to organize here. These right-to-work states are going to have to accept the fact that unions are here, and we're not going anywhere."</P>
<P>Diane Ballentine, president of the Flint Glass Workers Local 1028 in Raleigh, N.C., agreed.</P>
<P>"In South Carolina, you've got a predominately Black union that's doing very well, and I think this [attack] is to destroy that union," she said. </P>
<P>"It doesn't matter what color you are, what race, where you're from, whatever.  Everyone benefits when we support our unions."</P>
<P>As ILA President Kenneth Riley put it in his speech at the rally: "One message that I have for Charlie Condon: you saw a small, local union down in Charleston, South Carolina, and you decided that you could attack us. </P>
<P>"But you didn't know that the rest of the world was watching."</P>

