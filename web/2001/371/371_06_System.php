<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>INSIDE THE SYSTEM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dubya's European vacation</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 6</font></p>
<p><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH'S stumble through Europe makes Chevy Chase in <I>National Lampoon's European Vacation </I>look like a seasoned diplomat.</P>
<P>Bush's advisers knew there was a risk when they scheduled him to visit Slovenia, a country that he once confused with Slovakia. </P>
<P>"Every new president is some kind of caricature in Europe, until the first trip," said Andrew Card, Bush's chief of staff. "They'll come to know him."</P>
<P>But Bush proved all the speculation about his featherweight intellect wasn't mere speculation.</P>
<P>On the first stop of his five-day trip, Bush called Prime Minister Jose Maria Aznar of Spain "Anzar." </P>
<P>He then called NATO Secretary General George Robertson "Robinson." </P>
<P>He got snickers from listeners in Sweden when he said, "Africa is a nation," and suggested Europe should have "more countries." </P>
<P>During Bush's speech in Warsaw, electrify became "electrifly." </P>
<P>And instead of saying "Today, Poland's own Golec Orchestra," Bush spluttered, "Today's own, Poland's orchestra, called Golec's." </P>
<P>The blunders tended to happen when Bush didn't expect a question.</P>
<P>When he was asked, for example, whether Poles might soon travel to the U.S. without visas, he blurted out, "Chicago is a city with many, many people of Polish heritage."</P>
<P>In addition to his weak intellect, Bush's policies also occasionally came under some scrutiny. </P>
<P>One of Madrid's newspapers, <I>El Pa&iacute;s, </I>carried an opinion piece by a diplomat entitled, "No one has ever bothered more people in less time." </P>
<P>The British <I>Guardian </I>said that Bush would "risk rotten tomatoes" if he traveled to France, Germany or Britain. </P>
<P>--<I>Washington Post, </I>June 17, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">That big city feeling</font></p>
<p><font face="Times New Roman, Times, serif" size="3">IN ITS January-February issue, the <I>Utne Reader </I>had a story about nice places to visit on your next vacation.</P>
<P>Titled "The 10 Most Underrated Towns in America," the story gave <I>Utne Reader </I>readers some inside tips.</P>
<P>Cincinnati came in eighth and was described as an "old town [with] a surprisingly big city feel."</P>
<P>One of the neighborhoods to check out, the article said, is Over-the-Rhine, "a Lower East Side-style neighborhood &#133;seeing some hipster redevelopment without major displacement (yet) of low-income residents."</P>
<P>Within several weeks of the story's publication, the Cincinnati rebellion against police brutality broke out--centered in Over-the-Rhine.</P>
<P>--<I>Utne Reader, </I>January-February 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">The watchdog has no teeth</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE PENTAGON agency that's supposed to expose fraud destroyed documents in order to avoid embarrassment when its own operations were audited.</P>
<P>As part of a routine program, Internal Revenue Service (IRS) auditors came to review the Pentagon inspector general's work.</P>
<P>But they were given falsified documents.</P>
<P>The unsuspecting IRS reviewers found "no problems" with the Pentagon's audit work after poring over the phony documents. </P>
<P>"It's a very sad day indeed when the watchdog gets caught cheating," Sen. Charles Grassley (R-Iowa) wrote in a letter to Defense Secretary Donald Rumsfeld.</P>
<P>Grassley, former chair of the Senate Finance Committee, began an investigation after a Pentagon whistleblower brought it to his attention. </P>
<P>While the inspector general is supposed to root out fraud and waste, the report said that the 983 hours spent creating the fake documents cost taxpayers $63,000. </P>
<P>Grassley said he's not satisfied with the internal Pentagon review into the incident. </P>
<P>He wrote Rumsfeld that it "may have been unwise" for the Pentagon's deputy inspector general, Robert Lieberman, to have one of his senior deputies conduct the internal investigation and then conclude that Lieberman was not implicated. </P>
<P>--Associated Press, June 5, 2001<br>
