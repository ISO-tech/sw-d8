<p><font face="Times New Roman, Times, serif" size="4" color="#000000">The federal government's first executions in 38 years</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. death machine lurches into action</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by ERIC RUDER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 2</font></p>
<p><font face="Times New Roman, Times, serif" size="3">TERRE HAUTE, Ind.--For the first time in 38 years, the federal government's killing machine lurched into action in June with two executions in little more than a week's time.</P>
<P>Oklahoma City bomber Timothy McVeigh was executed June 11, and eight days later, Juan Raul Garza was put to death.</P>
<P>Prosecutors and politicians had hoped to use McVeigh's case to rehabilitate the death penalty's tarnished reputation.</P>
<P>But their plans backfired when the FBI revealed--just days before McVeigh was supposed to be executed in May--that it had failed to hand over more than 4,000 documents to McVeigh's lawyers.</P>
<P>Suddenly, McVeigh's execution became a reminder of the many miscarriages of justice that characterize death penalty cases across the U.S.</P>
<P>In contrast to McVeigh, Juan Raul Garza was the more typical death row inmate--because he was nonwhite and because prosecutors used improper means to win his death sentence.</P>
<P>Garza's lawyers appealed for a stay of execution on the grounds of the racism of the federal death penalty system--where 89 percent of death row prisoners are nonwhite.</P>
<P>But earlier in the month, Attorney General John Ashcroft had the gall to claim that a Justice Department study of racism on federal death row showed that there was no evidence of bias.</P>
<P>"Our analysis has confirmed that Black and Hispanic defendants were less likely at each stage of the department's review process to be subjected to the death penalty than white defendants," Ashcroft declared.</P>
<P>But he and his pals came up with this fantasy by cooking the numbers.</P>
<P>They said that the federal government asked for the death penalty for 27 percent of eligible whites, 17 percent of eligible Blacks and 9 percent of eligible Latinos.</P>
<P>But they didn't bother to mention that the vast majority of defendants eligible for the death penalty in the first place are Blacks and Latinos.</P>
<P>As a result, an astonishing 70 percent of those who face federal death penalty charges are Black or Latino. </P>
<P>Racism is far from the only problem with America's killing machine.</P>
<P>Since 1976, 98 people have been freed from death row after evidence of their innocence came to light.</P>
<P>Joaquin Jose Martinez is one of them.</P>
<P>The Spanish national spent three years on Florida's death row before he was found not guilty in a retrial.</P>
<P>Martinez returned home to Spain just days before Bush began his European tour in the Spanish capital of Madrid.</P>
<P>To highlight this injustice, Spanish human rights activists protested Bush's visit with signs reading "Bush, a compassionate killer."</P>
<P>Martinez has become a celebrity in Spain--where capital punishment was abolished in the late 1970s--and he's vowed to fight the U.S. death penalty.</P>
<P>Bush encountered similar opposition wherever he went in Europe.</P>
<P>The Council of Europe--the continent's leading human rights organization--called McVeigh's execution "sad, pathetic and wrong."</P>
<P>Christa Nickels, the head of the German parliament's human rights committee, lambasted the U.S. government for the circus atmosphere surrounding the McVeigh execution, saying that it was "staged like a spectacle from the Middle Ages with modern communications media."</P>
<P>In the U.S., the media were on hand in massive numbers to record every moment leading up to the execution.</P>
<P>But several family members of McVeigh's bombing victims who witnessed the execution refused to go along with the thirst for blood.</P>
<P>"I expected more of a sense of closure and relief than I had," admitted Larry Whicher, whose brother died in the blast. </P>
<P>Bud Welch, whose daughter died in the attack, opposed McVeigh's execution and said that, by his count, less than half of the families who lost a loved one wanted McVeigh to be executed.</P>
<P>"We took a human out of a cage and killed him," Welch told reporters. "I've yet to hear a good reason for this."</P>
<P>Of the 1,000 family members invited to watch the execution on closed-circuit TV, only 232 came. </P>
<P>"I don't believe witnessing the execution of another human being can be good for the human heart or soul," said Pat Reeder, who lost his wife.</P>
<P>We need to keep exposing the death penalty as barbaric--and organize the fight to abolish it.</P>

