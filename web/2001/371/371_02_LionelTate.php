<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Jeb Bush won't hear clemency plea</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Leaving a 14-year-old to rot in prison</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by MIKKI SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 2</font></p>
<p><font face="Times New Roman, Times, serif" size="3">TALLAHASSEE, Fla.--Like his big brother George, Gov. Jeb Bush doesn't seem to mind hinting that he'll do one thing, and then doing the exact opposite.</P>
<P>On June 6, Bush refused to grant an early clemency hearing for 14-year-old Lionel Tate, despite indicating to Lionel's lawyers that he would.</P>
<P>Lionel is serving a life sentence without the possibility of parole for accidentally beating a 6-year-old playmate to death.</P>
<P>He was only 12 years old at the time.</P>
<P>But that didn't stop zealous prosecutors from putting him behind bars for life.</P>
<P>When it became clear following the sentencing in March that millions of people were appalled by Lionel's fate, Bush said that he would consider commuting the sentence.</P>
<P>The fact that Lionel's trial took place in Broward County--ground zero for allegations of the disenfranchisement of thousands of African American voters in Election 2000--probably fueled Bush's quick expression of sympathy.</P>
<P>But within a few days of receiving the request for a hearing from Lionel's lawyers, Bush said that Lionel would have to wait until March 2003--in accordance with "normal" clemency procedure.</P>
<P>In typical blame-the-victim fashion, Bush pointed to several alleged incidents involving Lionel at the juvenile detention center where he's been held since his sentencing.</P>
<P>And to add insult to injury, Bush has also appointed a special prosecutor to investigate unspecified "criminal acts" that Lionel's mother may have committed in connection with the case.</P>
<P>In the hopes of shifting the spotlight from his cold-hearted decision, Bush signed a bill June 13 that would keep juveniles segregated from the adult prison population in Florida.</P>
<P>But this bill does nothing to address the outrageous sentences that children are receiving--or the lack of rehabilitation programs in adult facilities.</P>
<P>Lionel Tate deserves clemency now!</P>

