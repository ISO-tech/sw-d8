<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>SWEDEN</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Huge protests confront Bush visit</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Shot down for protesting</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by PHIL GASPER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 7</font></p>
<p><font face="Times New Roman, Times, serif" size="3">SWEDISH POLICE shot three demonstrators with live ammunition June 15 in a shocking escalation of repressive tactics against the antiglobalization movement.</P>
<P>The three were part of a mobilization of 25,000 people in the city of Goteborg to protest a visit by George W. Bush and a European Union (EU) summit meeting attended by political leaders.</P>
<P>The day before the shootings, a huge "Bush Not Welcome" march greeted the U.S. president's arrival in Sweden, with banners protesting globalization, capital punishment, environmental destruction and other issues.</P>
<P>The demonstrations were largely peaceful--until police broke an agreement with protest organizers and tried to prevent hundreds from leaving a school building where they were staying.</P>
<P>All three shooting victims were hospitalized, one of them with serious damage to his liver and kidneys. </P>
<P>As <I>Socialist Worker </I>went to press, he remained in critical condition after two emergency operations.</P>
<P>It was the first time that Swedish police had opened fire on their own citizens since 1931.</P>
<P>Government spokespeople at first claimed that the police had fired in self-defense.</P>
<P>But a televised videotape showed that the critically injured demonstrator was shot in the back.</P>
<P>Yet this didn't stop police spokesperson Ulf Goranzon from declaring: "We don't have any kind of problem with the way we handled the situation. We've no plans to stop using live ammunition."</P>
<P>Sweden's national police chief Sten Heckscher added, "The police have carried out their task in an absolutely fabulous way." </P>
<P>After their plans to dine at an upscale restaurant were disrupted, political leaders at the summit also defended police tactics.</P>
<P> "The only thing that helps is toughness," German Chancellor Gerhard Schr&ouml;der, a social democrat, told a news conference. "Any attempt to develop a de-escalation strategy with these desperados is senseless."</P>
<P>Britain's Labour Party Prime Minister Tony Blair dismissed the protests as "an anarchists' traveling circus that goes from summit to summit with the sole purpose of causing as much mayhem as possible."</P>
<P>But <I>El Pais, </I>Spain's largest daily newspaper, said the demonstrators were expressing "real and serious worries about, for example, EU militarization, Bush's positions and the negative effects of globalization.</P>
<P>"Many of the [leaders] at the EU summit are the sons of [the mass protests of] May 1968. Did they not learn anything in their youth?"</P>
<P>The demonstrations in Sweden were the biggest in a series of protests that confronted George Bush in every country he visited on his recent European tour.</P>
<P>In Spain, Bush was met by hundreds of demonstrators protesting his policies on Star Wars missile defense, global warming, the death penalty and sanctions on Iraq.</P>
<P>Protesters chanted, "The Yankee needs Vietnam medicine," and, "We want to see Bush underneath a missile."</P>
<P>Bush "leads a country that is supposed to defend human rights, but as governor of Texas, he was a man who approved of executions," said David Bonel, a 33-year-old airline steward. </P>
<P>"His hands are stained with blood."</P>

