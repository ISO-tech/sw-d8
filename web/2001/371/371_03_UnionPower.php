<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Charleston and Chicago show two faces of labor today</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Time to rebuild union power</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 3</font></p>
<p><font face="Times New Roman, Times, serif" size="3">CHICAGO IS famous for being a union town.</P>
<P>Yet when 1,000 gas workers went on strike against the utility giant Peoples Gas in May, the city's union leaders didn't lift a finger.</P>
<P>Peoples Gas is hated in Chicago. Last winter, it jacked up heating bills so high that it sparked protests that forced the company to extend payment plans.</P>
<P>So gas workers were in a good position to fight. But solidarity was critical to beat a corporate giant like Peoples Gas.</P>
<P>It didn't happen.</P>
<P>At several rallies, labor leaders gave militant-sounding speeches.</P>
<P>But fearful of offending Democratic Mayor Richard Daley, they didn't mobilize union members to support the strike--nor did they come up with much money to support Local 18007.</P>
<P>So demoralized gas workers felt that they had no choice June 8 but to vote for a rotten deal.</P>
<P>This lost opportunity in Chicago is another example of labor's retreat and disarray since George W. Bush stole the White House.</P>
<P>International Brotherhood of Teamsters President James Hoffa has embraced Bush's destructive energy plan--as has Carpenters President Doug McCarron, who led his union out of the AFL-CIO itself.</P>
<P>Even United Steelworkers' President Leo Gerard--the most left-wing member of the AFL-CIO Executive Council--says he wants to work with Bush.</P>
<P>But on June 9, organized labor showed another face--not in a union stronghold, but in viciously anti-labor South Carolina.</P>
<P>More than 7,000 union members and supporters rallied to defend the Charleston Five--dockworkers facing trial on felony riot charges for the "crime" of picketing scab labor.</P>
<P>The union involved--International Longshoremen's Association Local 1422--is overwhelmingly African American and a key force in the civil rights movement in South Carolina.</P>
<P>That's why the growing national campaign to defend the Charleston Five takes up racial justice as well as workers' rights.</P>
<P>The difference between the fights in Chicago and Charleston is that ILA Local 1422 organized to push the struggle forward and build solidarity, despite the opposition of top union officials.</P>
<P>By winning grassroots support from civil rights and social justice groups and rank-and-file activists in several unions, they succeeded in pressuring the AFL-CIO to endorse their fight.</P>
<P> Now the West Coast dockworkers' union, the International Longshore and Warehouse Union--along with international dockworkers' unions from Sweden to Spain--are preparing to shut down the ports if the case against the Five comes to trial.</P>
<P>That's the kind of muscle that can teach corporate bosses and racist politicians a lesson.</P>
<P>The struggle in Charleston is a fighting example for union members across the U.S. who are struggling to rebuild their unions from the bottom up.</P>
<P>It's time to draw the line against the attack on our unions--and Charleston is the place to do it.</P>

