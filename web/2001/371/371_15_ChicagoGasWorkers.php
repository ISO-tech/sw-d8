<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Chicago union leaders failed to provide support</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gas workers strike ends in rotten deal</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 15</font></p>
<p><font face="Times New Roman, Times, serif" size="3">CHICAGO--Union workers for Peoples Gas voted June 8 by a 606-239 margin to end their three-week strike against the hated utility company.</P>
<P>The strikers, members of Service Employees International Union (SEIU) Local 18007, had rejected two previous "final" contract offers from Peoples Gas.</P>
<P>But this time, they accepted a contract that included the same cuts in overtime pay, increases in health insurance payments and dangerous one-man crews that they had previously rejected.</P>
<P>Under the contract, 50 workers will be "reassigned" to other union positions with a wage freeze. </P>
<P>The only difference in the new offer was a plan for "voluntary" assignments to a schedule of four 10-hour workdays, which includes work on Saturdays.</P>
<P>Although the local's executive board recognized that the company's offer was "less than what we had hoped for when the strike began," it recommended that members accept the deal and return to work.</P>
<P>"I'm really proud of how our members stuck together--how the larger departments stood up for the smaller departments," said Local 18007 President Pat Colleta.</P>
<P>But this is one of the many reasons why workers shouldn't have had to accept a rotten contract.</P>
<P>The 1,050 union members were united against Peoples Gas' corporate greed and bad-faith bargaining. </P>
<P>They're a trained workforce that the city needs for services and safety. </P>
<P>And gas workers had consumers--who during the winter a few months ago had to pay gas bills that had doubled or tripled--on their side.</P>
<P>The union had a lot going for it.</P>
<P>But unfortunately, it didn't have the support it needed from the SEIU International or other leading unions in the Chicago labor movement.</P>
<P>Local 18007 is a small local that was unprepared for fighting a major corporation with deep pockets. </P>
<P>It has no strike fund--nor little experience getting members out to fight on the picket lines. </P>
<P>Local union leaders didn't hold a general membership meeting to discuss strategy, leaving many members feeling uninformed and frustrated.</P>
<P>But the bigger disappointment was the leaders of larger SEIU locals in the city and other unions.</P>
<P>They did nothing to mobilize for the gas workers.</P>
<P>SEIU pledged a paltry $6,000 for Local 18007--or about $6 per worker to a local without a strike fund.</P>
<P>And while Chicago labor leaders mouthed fine words about solidarity at several rallies, there was no help for the gas workers on the picket line.</P>
<P>Meanwhile, community groups that had mobilized to stop gas shutoffs during the winter months were conspicuously absent.</P>
<P>Unlike labor leaders, Peoples Gas was ready to play hardball. </P>
<P>The company terminated benefits, bought full-page ads in newspapers to denounce the strikers and threatened to bring in unskilled scabs. </P>
<P>With no real support from other unions, members felt unprepared for the onslaught. </P>
<P>"Before we voted [on June 8], the TV news announced that we'd settled," said Rudy Martinez, a 17-year veteran of the company. "My supervisor was so confident that he called and said, 'When this passes, report to work on Monday at 7:00 a.m.'"</P>
<P>This strike didn't have to end in defeat.</P>
<P>Unions need to be able to rely on concrete solidarity if we're to push back the attack of greedy corporations like Peoples Gas.</P>
<P>As James Wreglesworth, who has worked for Peoples Gas for 32 years, said: "You've got to keep the union strong. You've got to hang on to what we got. Otherwise, they'll just take it away."</P>

