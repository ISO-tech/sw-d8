<font face="Times New Roman, Times, serif" size="5"><b>Strike grounds airline industry</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by SCOTT JOHNSON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 7</font></p>
<p><font face="Times New Roman, Times, serif" size="3">SOUTH KOREA'S air transportation system ground to a halt in mid-June in the largest airlines strike in the country's history.</P>
<P>The walkout at Korean Air and Asiana Airlines was part of a nationwide strike of more than 50,000 workers in several industries.</P>
<P>Also on the picket line were staff workers at nine major hospitals.</P>
<P>The strike was called by the Korean Confederation of Trade Unions (KCTU) to demand higher wages and an end to layoffs.</P>
<P>But with the government using the country's drought to shift public opinion against them, most strikers returned to work after winning only a few of their demands.</P>
<P>The bosses also turned to repression--arresting four pilots on charges of organizing an "illegal" strike and issuing a warrant for the arrest of KCTU leader Dan Byoung-ho. </P>
<P>Dan remained at large as <I>Socialist Worker </I>went to press.</P>
<P>The KCTU strike was further evidence of the anger that workers feel at the employers' attacks on jobs, wages and conditions.</P>

