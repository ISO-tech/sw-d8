<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can we protect the environment?</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 13</font></p>
<p><font face="Times New Roman, Times, serif" size="3">FROM THE poisoning of rivers and seas by chemicals to the contamination of the earth by nuclear waste, from acid rain to the depletion of the ozone layer, from soil depletion to reckless deforestation--the evidence of environmental degradation is everywhere.</P>
<P>What causes this destruction?</P>
<P>One argument--put forward by environmentalist organizations like Earth First!--is that environmental destruction is caused by too many people on the planet.</P>
<P>They argue that industrial production for billions of people is not only fouling the planet, but is depleting its natural resources.</P>
<P>By contrast, Marxists argue that the way capitalism organizes production--not industrial production per se--is what causes environmental destruction.</P>
<P>In fact, the potential exists to eliminate poverty and hunger, once and for all. </P>
<P>And the technology exists to produce that abundance in a way that won't destroy the environment.</P>
<P>Let's look first at the idea that there are limited resources. Consider the question of oil.</P>
<P>In 1981, Lester Brown, a well-known ecologist, wrote a book that argued the world would run out of oil in 1996.</P>
<P>Yet in 1996, proven world oil reserves were about 170 percent larger than in 1976--in spite of the fact that per capita oil consumption and population grew considerably.</P>
<P>It is true that there's a finite amount of oil under the ground.</P>
<P>But there are other technologies that harness the sun, sea and wind for power that can take its place.</P>
<P>What about food production?</P>
<P>Some 800 million people in the world go hungry. But it isn't because there isn't enough food.</P>
<P>The latest figures provided in the United Nations' Human Development Report for 1999 show that food production outstripped population growth by 25 percent over the last decade.</P>
<P>According to the 1998 edition of the excellent book <I>World Hunger: Twelve Myths, </I>there's enough food produced in the world to give every person 3,500 calories a day--well higher than the medically recommended requirement.</P>
<P>People go hungry not because of shortages, but because they can't afford to buy enough food.</P>
<P>In 1844, Frederick Engels wrote that "every adult produces more than he can himself consume."</P>
<P>More than 150 years later, human productivity has leaped far beyond anything Engels could have imagined.</P>
<P>The irony of capitalism is that it isn't scarcity that produces hunger and unemployment but overabundance.</P>
<P>Capitalist crisis, Karl Marx and Frederick Engels wrote in <I>The Communist Manifesto, </I>produces the "absurd" situation of people thrown out of work because there's "too much means of subsistence, too much industry, too much commerce."</P>
<P>There is, in short, plenty to go around. </P>
<P>The shortages argument is used by the ruling class to convince us that inequality can't be overcome.</P>
<P>But what about pollution and environmental devastation? Aren't all the potential benefits of mass production undermined by pollution?</P>
<P>The scientific know-how exists to eliminate things like acid rain and ozone depletion and to prevent the poisoning of seas and rivers--not to mention our neighborhoods and workplaces. </P>
<P>It is simply not profitable to do so.</P>
<P>The unplanned character of the free market combined with the scramble for profits means capitalists give no thought or care to the effects of the production process. </P>
<P>"What cared the Spanish planters in Cuba," wrote Engels, "who burned down forests on the slopes of the mountains and obtained from the ashes sufficient fertilizer for one generation of very highly profitable coffee trees--what cared they that the heavy tropical rainfall afterwards washed away the unprotected upper statum of the soil, leaving behind only bare rock!"</P>
<P>The only way humans can establish a "oneness" with nature is not by turning our backs on technology and the scientific knowledge achieved so far but by interacting with our environment on the basis of the fullest scientific understanding of that relationship.</P>
<P>But as Engels added, this "requires something more than mere knowledge.</P>
<P>"It requires a complete revolution in our hitherto existing mode of production and simultaneously a revolution in our whole contemporary social order."</P>

