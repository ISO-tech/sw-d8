<font face="Times New Roman, Times, serif" size="4">The road ahead to Genoa:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The mobilization looks to be huge"</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 7</font></p>
<p><font face="Times New Roman, Times, serif" size="3">PROTEST ORGANIZERS are expecting up to 175,000 demonstrators to descend on Genoa, Italy, for a massive protest against the Group of Eight summit on July 20-22.</P>
<P>The Genoa demonstration looks like it could be a new high point for the movement against globalization.</P>
<P>ANTONIS DAVANELLOS is a member of the International Workers Left in Greece.</P>
<P>In early June, he chaired a meeting that brought together 600 Greek unionists and other activists with Genoa protest organizers--including Christophe Aguiton, of the French-based group ATTAC.</P>
<P>Antonis talked to <I>Socialist Worker </I>about Genoa.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P>"WE'RE TRYING to get Greek participation to Genoa as large as we can--but also to connect the message of the international demonstrations against globalization with the ongoing struggles in Athens. </P>
<P>That's what the discussions are about--first, organizing to go to Genoa, and second, what that means at the workplaces and at schools among the student movement.</P>
<P>The mobilization in Italy looks to be huge. </P>
<P>The Italian left is expecting 150,000 demonstrators from Italy. </P>
<P>And the Social Forum of Genoa is expecting at least 25,000 international demonstrators. </P>
<P>But we hear that [new Italian Prime Minister Silvio] Berlusconi is going to try to close up the borders and keep out all the international demonstrators. </P>
<P>He has declared that, for him, all the international demonstrators are criminals, and he will not let us protest in Genoa.</P>
<P>At the meeting, Christophe Aguiton said that the main task today is to relate the international movement against globalization to the labor movement. </P>
<P>It's good that this is understood. </P>
<P>I think that the unions will be in Genoa, and all the other groups of activists will be able to see that this is an important struggle with important social forces."</P>

