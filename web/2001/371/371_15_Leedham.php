<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why Teamsters should support Tom Leedham</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by DONNY SCHRAFFENBERGER, Teamster Local 705 steward</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 15</font></p>
<p><font face="Times New Roman, Times, serif" size="3">TOM LEEDHAM is running a grassroots campaign for general president of the International Brotherhood of Teamsters against incumbent James Hoffa, son of the notorious Jimmy Hoffa.</P>
<P>Leedham ran against Hoffa in 1998, and received nearly 40 percent of the vote in a three-way race--even though Hoffa outspent Leedham by millions of dollars and had years to run his campaign.</P>
<P>Hoffa's closest buddies have just been caught in an anti-union scam in which they routed good-paying Teamsters jobs in Las Vegas to a nonunion temp agency.</P>
<P>William Hogan Jr., president of Joint Council 25 among other positions, and Dane Passo, Hoffa's special assistant, are accused of diverting jobs from Teamster members in Las Vegas to the nonunion temporary labor firm where Hogan's brother is an officer.</P>
<P>A recent report claims that Hoffa might have had prior knowledge of the scheme.</P>
<P>Hoffa claimed that he would restore the power to the Teamsters.</P>
<P>Instead, he talks tough--sometimes--but delivers little.</P>
<P>Bad contracts for Anheuser-Busch workers and Northwest Airlines flight attendants and the failure of the year-and-a-half-long union recognition strike at the freight company Overnite have dimmed Hoffa's star.</P>
<P>Instead of relying on his daddy's erroneous legend of fighting for members, Hoffa now, like his mobbed-up father, has a track record that Teamsters can scrutinize.</P>
<P>Tom Leedham can punch holes through this record.</P>
<P>Leedham has put militants and progressives on his slate.</P>
<P>They include Maria Martinez, who led a wildcat strike at Iowa Beef Processing in Washington state, and Bob Hasegawa, the former president of Local 174 in Seattle who led a demonstration of Teamsters in the protests against the World Trade Organization in 1999.</P>
<P>These fighters are good additions.</P>
<P>On the other hand, Tom Metz is on the Leedham slate.</P>
<P>In his 1998 campaign, Metz attacked Leedham for his association with the reform group Teamsters for a Democratic Union.</P>
<P>Some Teamsters are cynical about the election.</P>
<P>They supported former Teamsters President Ron Carey, only to see him taken out by the federal government after he led the national strike against United Parcel Service in 1997, the most important victory for labor in the U.S. in decades.</P>
<P>Hoffa brags that he has 90 percent of the elected delegates to the Teamsters convention in Las Vegas.</P>
<P>Yet Ron Carey only had 15 percent of the delegates in 1991--and he went on to win the election.</P>
<P>Leedham only needs 5 percent of the delegates to get on the ballot.</P>
<P>Unlike the 1998 election, Leedham has more name recognition this time around.</P>
<P>He's a former vice president in the Carey administration and was also the national warehouse division director.</P>
<P>Leedham has promised to take on the likes of UPS, Northwest Airlines and Anheuser-Busch--and has a solid track record to prove he isn't just hot air like Hoffa.</P>
<P>Hoffa travels around with an entourage of crooks and scabs like Passo, Hogan and Frank Wsol, the highest-paid Teamsters official in the country who refused to strike UPS in 1997.</P>
<P>On the campaign, Leedham sleeps in the homes of rank-and-file Teamsters.</P>
<P>Hoffa is more at home when he's playing a round of golf with corporate executives.</P>
<P>Sadly, many Teamster officials once associated with union reform have sided with Hoffa.</P>
<P>They've capitulated under the weight of the attack by the government and the bosses against Carey.</P>
<P>Although it's very important to get Leedham elected--as well as any reform-minded union member that wants to take on the bosses--this by itself isn't enough.</P>
<P>We need to build on the shop floor to make sure that whoever wins feels our pressure, forcing them to fight the bosses.</P>
<P>But a victory for Leedham's reform slate would make our fight easier than if Hoffa's pro-company slate wins.</P>
<P>Also, we need grievance and contract campaigns that involve the membership.</P>
<P>That is what rank-and-file power--the slogan of Leedham's slate--has to be about.</P>

