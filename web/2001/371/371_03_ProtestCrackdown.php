<p><font face="Times New Roman, Times, serif" size="5" color="#000000"><b>Crackdown on protests</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 3</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE SHOOTING of three protesters by Swedish police in June marks a dramatic escalation in the attempt by authorities to crack down on international protests against globalization.</P>
<P>Since the huge mobilization in Seattle in late 1999, politicians, police and the media have tried to claim that protesters challenging corporate power and international financial institutions like the World Trade Organization are being manipulated by a fringe element bent on violence.</P>
<P>In fact, when violence has broken out, it has invariably been initiated by police.</P>
<P>But government officials are using the demonstrations in Sweden to campaign for even more draconian measures against demonstrators.</P>
<P>German Interior Minister Otto Schilly announced that he and his French counterpart, Daniel Vaillant, were preparing a "coordinated and hard response to this new form of extremist, cross-border criminality."</P>
<P>The Italian government, led by right-wing Prime Minister Silvio Berlusconi is planning to seal off the city of Genoa during next month's G8 world economic summit, shutting down airports, rail stations and motorways.</P>
<P>Meanwhile, the international police agency Interpol is compiling a list of the "ringleaders" of recent demonstrations, according to the <I>Guardian</I> newspaper.</P>
<P>This turn to repression shows how much our rulers fear protest.</P>
<P>They know that the pro-corporate policies they push are designed to make workers pay.</P>
<P>But we can beat back their attacks on our movement--by continuing to build solidarity between unionists, students and other activists across international borders.</P>

