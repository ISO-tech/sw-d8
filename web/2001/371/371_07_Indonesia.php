<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>INDONESIA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Detained in a Jakarta jail</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 7</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Socialist Worker columnist PAUL D'AMATO was among 32 people from a dozen countries detained by Indonesian authorities in June for attending a political conference outside Jakarta. Here, he describes what happened.</B></P>
<P>ON JUNE 8, dozens of police and right-wing militia members--with guns drawn--stormed into the Asia Pacific People's Solidarity Conference.</P>
<P>After two hours of tense negotiations, conducted courageously by conference organizers, police packed 32 international attendees into military vehicles and rushed us to police headquarters in central Jakarta.</P>
<P>After we were taken away, truckloads of right-wing Muslim militia members arrived at the conference site--armed with machetes and sickles--and attacked the Indonesian participants.</P>
<P>A number of people were injured, including two who went to the hospital with serious injuries.</P>
<P>At the police station, we were forced to sign a questionnaire about our activities in Indonesia and turn over our passports. </P>
<P>Police officials threatened us with fines, deportation and even imprisonment for allegedly violating our visas.</P>
<P>The next day, thanks to skillful negotiations by dedicated Indonesian lawyers, we were released.</P>
<P>The police ordered us to return on Monday morning, when we would be turned over to immigration authorities. </P>
<P>But because of a local and international campaign of pressure--which included demonstrations in Canada, Australia and the U.S., as well as a worldwide signature campaign that many prominent U.S. labor leaders signed onto--the police were forced to release us.</P>
<P>The police suffered a humiliating rebuke from the Indonesian Department of Immigration, which declared that our visas were perfectly in order and the conference had been legitimate.</P>
<P>The right wing and the military wanted to use the phony visa violations to restore the repression that they presided over under the dictatorship of Gen. Suharto, who was overthrown in 1998.</P>
<P>But the attempt backfired.</P>
<P>In Indonesia, there was widespread anger at the police action--well expressed by an editorial in the <I>Jakarta Post.</P>
</I><P>"The Sawangan incident drew a disturbing picture of Indonesia one year into the new millennium," the <I>Post </I>wrote. </P>
<P>"Was it not three years ago that this country renounced all forms of tyranny and repression? Was it not in 1998 that many young Indonesians paid with their blood, sweat and tears to put an end to the rule of one of the world's longest-ruling tyrants?" </P>
<P>The conference came amid a political crisis in Indonesia in which President Abdurraham Wahid and Vice President Megawati Sukarnoputri are locked in a power struggle.</P>
<P>The country's parliament is trying to impeach Wahid on accusations of corruption.</P>
<P>But his opponents are just as knee-deep in it as he is.</P>
<P>The military, the old state bureaucracy and officials of the former ruling party GOLKAR were thrown on the defensive by Suharto's overthrow.</P>
<P>But they have been trying to claw back power ever since.</P>
<P>After first backing Wahid and sidelining Megawati following elections in 1999, these forces have now swung behind Megawati--who was seen as an opposition leader in 1998 but who has since cozied up to the military.</P>
<P>Wahid is threatening to mobilize members of the mass Islamic organization he used to head if he's impeached.</P>
<P>But after presiding over a severe austerity program and not lifting a finger to prosecute Suharto, nor any of the militia and military leaders complicit in the massacres in East Timor in 1999, he has little support.</P>
<P>This "horizontal" crisis at the top was overshadowed by the eruption of working-class protests in several cities in mid-June.</P>
<P>In the Javanese city of Bandung, 50,000 workers rallied, and a section trashed local government buildings.</P>
<P>Workers were protesting new labor laws that reduce severance benefits and attack their right to organize.</P>
<P>These struggles--rather than the maneuvers at the top of society--are the key to pushing forward the fight to transform Indonesia.</P>

