<p><font face="Times New Roman, Times, serif" size="4" color="#000000">"Glorious harbinger of a new society"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Paris Commune</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 13</font></p>
<p><font face="Times New Roman, Times, serif" size="3">IN 1871, the working class in Paris rose up against their rulers and proclaimed the Paris Commune.</P>
<P>It was the first example in history of ordinary people taking control of society and running it themselves.</P>
<P>Though it survived for only 72 days, the Commune helped socialists understand the struggle ahead.</P>
<P>And it remains a living answer to the question of what workers' control over society might look like--a society without the rich, without the bosses, without the domination of official religion.</P>
<P>"Workingmen's Paris, with its Commune, will be forever celebrated as the glorious harbinger of a new society," Marx wrote.</P>
<P>CAMERON STURDEVANT tells the story of the Paris Commune.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P>FRANCE'S REVOLUTION of 1848 toppled the country's monarchy once and for all.</P>
<P>But the defeat of the revolution eventually led to Napoleon III--nephew of the famous Napoleon Bonaparte, who had ruled France at the beginning of the 19th century--taking power as emperor in a coup.</P>
<P>France's industry developed rapidly under Napoleon III, creating a Paris filled with contradictions--new wide boulevards, fancy stores and massive wealth for the corrupt few, repression and bitter poverty for the many. </P>
<P>In 1870, Napoleon--like so many rulers before and since--tried to divert attention from problems at home by launching a war abroad.</P>
<P>Napoleon hoped for an easy victory in going to war against Prussia--what is today Germany.</P>
<P>Instead, the Prussian army rapidly smashed French forces and advanced on Paris.</P>
<P>When Napoleon abdicated, the people of Paris, disgusted with their leaders' failure, planned their own defense of the city, arming themselves with antiquated weapons and taking collections to buy cannons.</P>
<P>Throughout the winter, the Prussians laid siege to Paris. </P>
<P>The wealthy fled to the countryside, while political leaders set up a national assembly at Versailles. </P>
<P>Ultimately, France's rulers decided that they hated the idea of armed French workers more than the Prussian "enemy."</P>
<P>So they convinced the Prussians to let them send French troops into Paris to retake control.</P>
<P>But when the troops arrived in the early morning hours of March 18, 1871, they were met with barricades.</P>
<P>"We were greatly mistaken in permitting these people to approach our soldiers, for they mingled among them, and the women and children told them: 'You will not fire upon the people,'" one general wrote.</P>
<P>When Gen. Lecomte ordered his men to fire into the crowd, they refused. </P>
<P>News spread rapidly through the capital as more barricades went up.</P>
<P>"It's the Revolution!" wrote revolutionary novelist and journalist Jules Vall&egrave;s, describing the first day of workers' power.</P>
<P>"So here it is, the moment hoped for and awaited since a father's first cruelty, since the first slap in school, since the first day without food, since the first night sleeping on the streets--here is revenge for school, for poverty, for the coup of December 1851."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P>EIGHT DAYS later, all the working men of Paris had a vote in elections to set up a Commune government. </P>
<P>What resulted was far different from parliamentary democracy.</P>
<P>For one thing, the Commune voted to limit the pay of all representatives to that of the city's skilled workers--preventing them from rising above those they represented.</P>
<P>Members were elected without fixed terms to make them more accountable--recallable at the vote of those who sent them.</P>
<P>A series of astounding measures followed.</P>
<P>On March 30, the Commune abolished the draft and the standing army and declared that the National Guard, made up of ordinary citizens, was the sole legitimate armed force of Paris.</P>
<P>Foreigners elected to the Commune were confirmed in office since "the flag of the Commune is the flag of the World Republic," the representatives declared.</P>
<P>On April 6, the guillotine was brought out by a battalion of the National Guard and publicly burned.</P>
<P>And in the weeks that followed, the Commune started collecting information about factories closed by manufacturers--with the goal of restoring production under the direction of workers.</P>
<P>There were areas where the Commune didn't go far enough.</P>
<P>For one thing, working women--despite their active involvement at every level of the struggle--weren't allowed to vote.</P>
<P>And fatally, the Commune allowed French troops to leave the city and return to Versailles.</P>
<P>"[T]he committee continued signing, neglecting this traditional precaution--the shutting of the gates--and lost itself in the elections," the revolutionary journalist Lissagaray wrote later in his <I>History of the Paris Commune. </P>
</I><P>"It saw not--very few saw as yet--that this was a death struggle with the Assembly of Versailles."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P>FOLLOWING THE example of Paris, communes were set up in the cities of Lyons, Marseilles and Narbonne. </P>
<P>But none of these lasted more than a few days.</P>
<P>Unable to win the support of the 60 percent of the French population that lived in the countryside, Paris was left alone.</P>
<P>On May 21, the government in Versailles declared war on Paris.</P>
<P>With the cooperation of the Prussians, French troops were again sent in--and this time, they were successful in reestablishing control.</P>
<P>Between 20,000 and 30,000 workers were massacred defending the Commune. </P>
<P>The rulers of France were determined to drown this symbol of resistance in blood.</P>
<P>But despite its defeat, the Commune showed for the first time that workers could take power into their own hands. </P>
<P>Karl Marx and Frederick Engels made their only significant correction to the ideas expressed in <I>the Communist Manifesto </I>based on the experience of the Commune. </P>
<P>In June 1872, they wrote in a new preface to the pamphlet: "One thing especially was proved by the Commune; that the working class cannot simply lay hold of the ready-made state machinery and wield it for its own purposes."</P>
<P>On the 130th anniversary of the Paris Commune, socialists can still take inspiration from the Communards and their heroic effort to make a society worthy of humanity. </P>

