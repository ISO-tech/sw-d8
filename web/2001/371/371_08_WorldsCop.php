<p><font face="Times New Roman, Times, serif" size="4" color="#000000">The U.S. military's bloody record</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The world's cop</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Pages 8 and 9</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE U.S. Navy cooperated fully with producers of the movie <I>Pearl Harbor </I>to help them make their summer blockbuster.</P>
<P>And no wonder.</P>
<P>The movie describes the Second World War exactly as the Pentagon wants it remembered--with the Japanese as the brutal and heartless villains, and the U.S. as the innocent victims who turn the tables on their attackers.</P>
<P>Nowhere in the film is there any mention of all the evidence that the U.S. government provoked the attack on Pearl Harbor--to provide an excuse for a war for control of the Pacific that it wanted all along.</P>
<P>Nor does this glamorized war story hint at the American crimes to come--the incarceration of Japanese Americans in concentration camps, for instance, or the incineration of the Japanese cities of Hiroshima and Nagasaki with atomic bombs.</P>
<P>But this official version of the Second World War is only one of many lies that the U.S. tells about its military adventures.</P>
<P>From the Second World War to the present, the U.S. has fought with every weapon at its disposal to remain the world's number one military power--no matter what the cost in human lives.</P>
<P>STUART EASTERLING looks at U.S. imperialism's bloody record from Hiroshima to Iraq.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Why did the U.S. drop the bomb?</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE OFFICIAL line on the atomic bombing of Hiroshima and Nagasaki is that the U.S. government had no choice.</P>
<P>Without the bombing, we're told, the U.S. would have had to invade Japan to force its surrender--at the cost of countless American lives.</P>
<P>But not even Dwight Eisenhower, the Supreme Commander of U.S. forces in Europe during the war and a future Republican president, bought this line.</P>
<P>"I voiced my misgivings, first on the basis of my belief that Japan was already defeated and dropping the bomb was completely unnecessary, and secondly, because I thought that our country should avoid&#133;the use of a weapon whose employment was, I thought, no longer necessary as a measure to save American lives," Eisenhower said.</P>
<P>Prominent U.S. figures knew that the Japanese were on the brink of defeat--because the U.S. military had broken Japan's code and was regularly intercepting messages indicating a desire to get out of the war.</P>
<P>There was even direct communication between Japan and the U.S. </P>
<P>President Harry Truman's diary has an entry on July 18, 1945--more than two weeks before the atomic bombing of Hiroshima--indicating that he discussed with British Prime Minister Winston Churchill a "telegram from Jap emperor asking for peace."</P>
<P>But Truman ordered the bombing anyway.</P>
<P>The U.S. leveled two cities with nuclear bombs--and remains the only country in the history of the world to do so. </P>
<P>Seventy thousand people were incinerated in Hiroshima, where the temperature on the ground reached 7,000 degrees Fahrenheit. </P>
<P>In Nagasaki, 50,000 were killed immediately. </P>
<P>Because of the effects of radiation, the death toll had reached more than 200,000 by the end of the year.</P>
<P>The most honest answer for why the U.S. dropped the bomb was given by Secretary of State James Byrnes. </P>
<P>"The atomic bomb will put us in a position to dictate terms at the end of the war," Byrnes said.</P>
<P>The USSR had already emerged as America's main rival. </P>
<P>After Germany surrendered in early 1945, the USSR was about to enter the Pacific war--something the U.S. wanted to avoid.</P>
<P>"After the atomic bomb," Byrnes said, "Japan will surrender, and Russia will not get in on the kill."</P>
<P>But the message that the U.S. sent with its use of the atomic bomb wasn't meant solely for Russia.</P>
<P>"If the world gets into turmoil," Truman warned, "it will be used again--you can be sure of that."<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">"High-class muscle man for big business"</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE U.S. has a long history of operating as the world's "cop." </P>
<P>Democratic President Woodrow Wilson explained why almost 100 years ago.</P>
<P>"Since trade ignores national boundaries, and the manufacturer insists on having the world as a market, the flag of his nation must follow him, and the doors of the nations which are closed against him must be battered down," Wilson said.</P>
<P>"Concessions obtained by financiers must be safeguarded by ministers of state, even if the sovereignty of unwilling nations be outraged in the process. Colonies must be obtained or planted, so that no useful corner of the world may be overlooked or left unused."</P>
<P>Over the course of the 20th century, the U.S. military "battered down" many doors.</P>
<P>The dawn of this era of building up the U.S.'s overseas empire--or "imperialism"--came around 1900, when the U.S. seized Cuba, Puerto Rico and the Philippines in the Spanish-American War.</P>
<P>Since then, in the Americas alone, the U.S. has invaded Honduras four times, Panama four times, Cuba five times, Nicaragua twice, Grenada once, Mexico once, the Dominican Republic twice and Haiti twice. </P>
<P>The media and the politicians have offered all sorts of justifications for these invasions--peace, justice, human rights, democracy, stability, civilizing the natives, fighting communism, stopping aggression, fighting drugs and on and on.</P>
<P>But a few people have been more honest.</P>
<P>Gen. Smedley Butler, who spent 33 years in the U.S. Marine Corps, described his role this way: "I spent most of my time being a high-class muscle man for big business, for Wall Street and for the bankers. In short, I was a racketeer for capitalism...</P>
<P>"Thus, I helped make Mexico...safe for the American interests in 1914. I helped make Haiti and Cuba a decent place for the National City Bank boys to collect revenues in. I helped in the raping of half a dozen Central American republics for the benefit of Wall Street. </P>
<P>"The record of racketeering is long. I helped purify Nicaragua for the international banking house of Brown Brothers in 1909-1912. I brought light to the Dominican Republic for American sugar interests in 1916. </P>
<P>"I helped make Honduras 'right' for American fruit companies in 1903. In China, in 1927, I helped see to it that Standard Oil went its way unmolested."<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">The biggest bully beaten</font></p>
<p><font face="Times New Roman, Times, serif" size="3">DURING ALL its adventures in the first 70 or so years of the 20th century, the U.S. government went unbeaten.</P>
<P>Until Vietnam.</P>
<P>The U.S. didn't lose Vietnam because it didn't try hard enough, as right-wingers are fond of claiming today.</P>
<P>In fact, the U.S. government did everything in its power to win.</P>
<P>It dropped 7 million tons of bombs on Vietnam--or twice as much as all the bombs dropped during the Second World War, including the atomic bombs at Hiroshima and Nagasaki.</P>
<P>The U.S. also used 18 million tons of chemical weapons, including Agent Orange, and dropped napalm in massive amounts.</P>
<P>Ultimately, the U.S. killed 3 million Vietnamese, Laotians and Cambodians. </P>
<P>If the long walls bearing the names of U.S. dead at the Vietnam War Memorial in Washington, D.C., included these victims, they would be 60 times longer.</P>
<P>But despite this campaign of mass murder, the U.S. government still lost in Vietnam. </P>
<P>There were three main reasons.</P>
<P>First, the U.S. couldn't defeat Vietnam's guerrilla army--because the Vietnamese were fighting for national liberation against U.S. domination and the corrupt puppet regimes established by the U.S.</P>
<P>This political commitment completely confused U.S. politicians and generals.</P>
<P>"The ability of the Viet Cong to continuously rebuild their units and make good their losses is one of the mysteries of the guerrilla war," said Gen. Maxwell Taylor.</P>
<P>Second, many U.S. soldiers rebelled when they began to understand the kind of war they were fighting. </P>
<P>U.S. forces were overwhelmingly from working-class and minority backgrounds.</P>
<P>As the casualty rate mounted, it became more and more obvious that they were being used as cannon fodder for a war that only benefited America's rulers.</P>
<P>The final factor in the U.S. defeat was an international protest movement that put massive pressure on the White House.</P>
<P>"It was a time of extraordinary stress," Henry Kissinger, President Nixon's man in charge of foreign policy, said of the atmosphere.</P>
<P>"Washington took on the character of a besieged city...The very fabric of government was falling apart. </P>
<P>"The executive branch was shell-shocked...I had to move from my apartment ringed by protesters into the basement of the White House to get some sleep. </P>
<P>"[President Nixon], deeply wounded by the hatred of the protesters...reached a point of exhaustion that caused his advisers deep concern...[He seemed] on the edge of a nervous breakdown."</P>
<P>In the end, the U.S. was forced to withdraw and admit defeat for the first time. </P>
<P>And ever since, it has faced what is known as the "Vietnam syndrome."</P>
<P>Despite all their efforts ever since to overcome it, U.S. rulers still fear the consequences of committing U.S. troops to a conflict that they can't win easily.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">How the U.S. went to war for oil</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE 1991 Gulf War against Iraq was the U.S.'s most ambitious effort yet to overcome the "Vietnam syndrome."</P>
<P>So U.S. officials were especially concerned with properly packaging the war.</P>
<P>After Iraq invaded Kuwait in August 1990, George Bush declared that the U.S. would have to "liberate Kuwait."</P>
<P>But once again, there were more honest assessments.</P>
<P>Business Week ran a headline during the buildup to the war that read: "Oil is Worth Going to War For." </P>
<P>Iraqi forces were defeated within two months after a massive aerial bombardment followed up by a deadly ground offensive.</P>
<P>Ever since Iraq's surrender, the U.S. has maintained a genocidal economic blockade.</P>
<P>The supposed purpose of sanctions on Iraq was to prevent Saddam Hussein's government from developing "weapons of mass destruction."</P>
<P>So angina and epilepsy medications--as well as vaccines for hepatitis, tetanus and diphtheria--are barred because these could supposedly be used to make chemical weapons. </P>
<P>The sanctions also bar pencils, ambulances, medical gauze, syringes, medical swabs, medical journals, surgical gloves, dialysis equipment and chlorinators for drinking water--all because these items could allegedly be used for military purposes. </P>
<P>The result, according to the United Nations (UN), is that more than 5,000 Iraqi children die every month of preventable causes. </P>
<P>In April of this year, for example, 5,696 children in Iraq died of diarrhea, pneumonia and malnutrition--compared to 347 in the same month in 1989.</P>
<P>The sanctions continue to this day--even though the last two UN humanitarian relief coordinators for Iraq, Denis Halliday and Hans von Sponeck, who between them had served the UN for more than 60 years, both resigned in protest at the embargo.</P>
<P>Even Scott Ritter, a former chief of the weapons inspection team in Iraq, says that the country "possesses no meaningful weapons of mass destruction."</P>
<P>But then again, "oil is worth going to war for."<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">The alternative to a world of war</font></p>
<p><font face="Times New Roman, Times, serif" size="3">OPPONENTS OF U.S. military adventures sometimes blame them on individual politicians who are especially bloodthirsty.</P>
<P>But that's not the real problem.</P>
<P>As the Russian revolutionary Lenin pointed out, military conflicts are an outgrowth of the economic system of capitalism.</P>
<P>As capitalists expand across borders, they look for raw materials, investments, new markets and cheap sources of labor.</P>
<P>Inevitably, there are conflicts between different capitalists--and the national governments that serve them--over the spoils. </P>
<P>These conflicts often get settled through war.</P>
<P>But of course, our rulers never fight these wars.</P>
<P>Workers and the poor are ordered to kill each other for the economic and political power of the wealthy. </P>
<P>The rulers of the U.S. can be pressured and contained, as they were because of Vietnam.</P>
<P>But their invasions and occupations and "peacekeeping actions" will never be stopped permanently until their economic power is taken away.</P>
<P>Antiwar movements have led to some of the great political struggles of the past.</P>
<P>We will need to organize again against the next U.S. military outrage.</P>
<P>But we also need to fight for a socialist society that will forever abolish the violence and bloodshed at the heart of the system.</P>

