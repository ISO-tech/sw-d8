<font face="Times New Roman, Times, serif" size="5"><b>White House non-vandal scandal</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 2</font></p>
<p><font face="Times New Roman, Times, serif" size="3">WASHINGTON--"Sliced phone and computer lines."</P>
<P>"Obscene messages left in copy machines."</P>
<P>And goodness, "Champagne flutes missing from an Air Force jet."</P>
<P>Such were the horrifying details of vandalism reportedly discovered by the incoming Bush administration during its first days in the White House.</P>
<P>Damage was so extensive, the <I>New York Daily News </I>hyperventilated, that "a telecommunications staffer with more than a quarter-century of service was seen sobbing."</P>
<P>At fault, apparently, were Clinton administration staffers who had trashed the White House before they cleared out.</P>
<P>The Republican Party's professional Clinton-haters latched onto the outrage.</P>
<P>Crackpot Rep. Bob Barr (R-Ga.) even demanded an official investigation of the vandal scandal.</P>
<P>Which, it turns out, was a non-vandal scandal.</P>
<P>According to press reports at the end of May, a formal review by the General Accounting Office "found no damage to the offices of the White House's East or West Wings."</P>
<P>Even Bush's own representatives could produce "no record of damage that may have been deliberately caused by the employees of the Clinton administration," Salon.com reported.</P>
<P>Turns out that White House reporters, itching for a scoop, got led around by the nose.</P>
<P>While officially refusing to comment, "anonymous" White House aides fed them stories about the vandalism.</P>
<P>And the non-vandal scandal became one more part of the smokescreen created by the Bush administration--to hide its right-wing agenda.</P>

