<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Bush's European vacation sparks hostility at every stop</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The ugly American</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 3</font></p>
<p><font face="Times New Roman, Times, serif" size="3">IT SEEMED that everywhere President Bush stopped during his five-country European tour, thousands turned out to take him on.</P>
<P>These demonstrations gave the lie to the president's American media courtiers, who claimed that the only criticism of Bush came from a snobby European elite.</P>
<P>"I think that [the Bush White House has] been surprised by the extent of the hostility--not just from the governments but from public opinion," one European official told Britain's Independent newspaper.</P>
<P>At least some leading European politicians actually criticized Bush.</P>
<P>It's been quite a different story in the U.S.</P>
<P>The Democratic Party--the so-called opposition party in Washington, has done next to nothing in the first seven months of Bush's reign to oppose him.</P>
<P>And if the first noises from the Democrat-controlled Senate are any indication, the rolling over for Bush won't end anytime soon.</P>
<P>Senate Majority Leader Tom Daschle (D-S.D.) wasted no time in announcing that he would schedule a vote on the bankruptcy "reform" bill, which will make it impossible for working people to free themselves from debt if they lose their jobs or get socked with big medical bills.</P>
<P>Perhaps it's not surprising that Daschle, an errand boy for Citigroup, should push this legislation.</P>
<P>But it doesn't say much for the Democrats' promise to contrast their program to help "working families" with Bush's administration for the rich!</P>
<P>Only a few days earlier, Bush won approval for an education "reform" bill that will sentence U.S. schoolchildren to endless standardized testing--and do little to improve school conditions that make it so difficult for millions to learn at all.</P>
<P>And for this, Bush can thank Sens. Edward Kennedy (D-Mass.) and Joe Lieberman (D-Conn.), who helped turn the Bush education bill into a "bipartisan" lovefest.</P>
<P>Meanwhile, Democrats handed Bush another gift days after he returned from Europe.</P>
<P>After the Federal Energy Regulatory Commissions announced June 18 that it would apply mild restrictions on electricity price-gougers in Western states, leading Democratic senators said they would call off their demand for caps on energy prices.</P>
<P>In other words, after stumping for price caps for weeks, the Democrats simply decided to let the energy profiteers line their pockets--as long as they're not too greedy about it.</P>
<P>While the Democrats continued their surrenders, organizations that could turn out big forces to oppose Bush's agenda--above all, the labor movement--remained in disarray in the face of the Republican offensive.</P>
<P>This has made Bush look stronger than he actually is.</P>
<P>In fact, Bush's approval rating dropped to 50 percent, according to a mid-June poll.</P>
<P>Millions of people in the U.S. feel the same about Bush as the Europeans who took to the streets to protest.</P>
<P>There's an opposition to Bush's right-wing agenda that's waiting to be built.</P>
<P>But the Democrats can't be relied on to oppose Bush. </P>
<P>It's up to us to take the fight into our unions, our schools and our communities.</P>

