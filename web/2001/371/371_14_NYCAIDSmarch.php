<p><font face="Times New Roman, Times, serif" size="4" color="#000000">AIDS activists organizing to tell drug companies:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>People before profits!</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by PRANAV JANI</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 14</font></p>
<p><font face="Times New Roman, Times, serif" size="3">NEW YORK--Activists are organizing a march and rally for June 23 to highlight the global AIDS crisis.</P>
<P>The demonstration will be part of New York City's Gay Pride Week, which means it will be all the more prominent.</P>
<P>The coalition of groups planning the event--which includes African Services Committee, Health GAP Coalition, ACT UP New York and Jubilee USA Network--shows the links being made, particularly between AIDS activists and anti-corporate globalization protesters.</P>
<P>Activists from all over the world will address the demonstration to describe how AIDS is still on the rampage--especially in poor countries, where the vast majority of new cases are now to be found.</P>
<P>The protest will mark the 20th anniversary of the first report of an AIDS case from the Centers for Disease Control.</P>
<P>And it will take place a few days before the United Nations (UN) General Assembly's Special Session on HIV/AIDS, to be held at UN headquarters in New York City. </P>
<P>Organizers want the demonstration to put pressure on wealthy nations like the U.S.--as well as international bodies like the UN, World Bank and International Monetary Fund--to take immediate action to stop the global spread of AIDS.</P>
<P>Three words highlight their demands: dollars, debt and drugs:</P>
<P>--The U.S. and other advanced countries should spend more on international projects to stop AIDS.</P>
<P>--These same countries should cancel the debts of poor countries, which drain valuable resources and weaken their health care systems.</P>
<P>--These countries should ensure that those living with HIV and AIDS have full access to life-saving drugs.</P>
<P>Let's tell the politicians and the drug companies that we won't let their profit-making agenda destroy the lives of people around the world.</P>
<P><I>The march will begin at 11 a.m. in Washington Square Park and proceed to Bryant Park, at 42nd Street and 6th Avenue, for a 1:30 p.m. rally. For more information, call 212-208-4533, or see www.stopglobalaidsnow.org.</I></P>

