<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Civil rights commission report shows:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush boys stole Florida</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by SHARON SMITH</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 6</font></p>
<p><font face="Times New Roman, Times, serif" size="3">MORE THAN six months after the Election Day debacle in Florida, the U.S. Civil Rights Commission has issued its report.</P>
<P>The verdict? George W. Bush's 537-vote margin of victory was made possible through the mass disenfranchisement of African Americans and other minorities at Florida's polling places.</P>
<P>After a thorough investigation of civil rights violations in the Florida election, the Civil Rights Commission concluded in its report released June 8: "Despite the closeness of the election, it was widespread voter disenfranchisement and not the dead-heat contest that was the extraordinary feature in the Florida election."</P>
<P>African American voters, who make up just 11 percent of the state's voters, accounted for 54 percent of the ballots that were disqualified as improperly marked on November 7.</P>
<P>The report showed that Black voters were 10 times more likely than white voters to have their votes rejected.</P>
<P>These findings concur with a computer analysis by the <I>Washington Post </I>this spring, which showed that the more Black and Democratic a precinct, the more likely it was to suffer high rates of invalidated ballots.</P>
<P>The Commission furthermore held Florida's top government officials--including its governor, Jeb Bush--responsible for the "pattern and practice of injustice, ineptitude and inefficiency" that delivered the state's pivotal electoral college votes to George W. Bush over Democrat Al Gore.</P>
<P>Jeb Bush, finding himself in the embarrassing position of having been caught stealing the votes that put his brother in the White House, nevertheless responded with righteous indignation to the Commission's report.</P>
<P>He called the report "sloppy and intellectually dishonest work...that needlessly fosters racial disharmony."</P>
<P>Bush claimed--with thinly veiled racism--that the Commission failed to take into account "the voter's education level, the voter's experience with voting, the ballot design and the voting machine used."</P>
<P>This is, of course, the same Jeb Bush who--knowing that a 65 percent increase in new Black voters was expected in November--refused to authorize a $100,000 voter education program.</P>
<P>And thousands of Latino and Haitian voters arrived at understaffed polling stations to find no bilingual ballots or translators, as required by law, preventing them from voting.</P>
<P>But Bush's role in disenfranchising Black and other minority voters runs far deeper than what took place on Election Day itself.</P>
<P>With advance planning, his administration engineered an electoral process based on systematic racial discrimination.</P>
<P>Two years before the election, Bush authorized a purge of "possible" and "probable" convicted felons from voter registration lists--knowing that African Americans, who vote overwhelmingly for the Democratic Party, would be disproportionately targeted.</P>
<P>Bush gave Database Technologies $3.3 million to compile the list.</P>
<P>The company came up with a list of 58,000 names--which the Florida Division of Elections ordered removed from county registries.</P>
<P>Blacks made up 44 percent of those who were removed from voting lists by the state in advance of the election.</P>
<P>Thousands of these names were people who were legally entitled to vote--for example, convicted felons from other states that, unlike Florida, restore voting rights to prisoners after serving time.</P>
<P>And many of the people purged from voter lists were guilty only of minor crimes decades ago.</P>
<P>One such individual was 81-year-old Ellic Land, a Second World War veteran who was convicted of carrying a concealed weapon in 1971--for which he paid only a fine at the time.</P>
<P>Thousands more--6,500, according to the Palm Beach Post--were purged from voting rolls in error.</P>
<P>Matt Frost, for example, was prevented from voting because his name matched an alias used by a convict named Chadwick Chowanetz.</P>
<P>Even Linda Howell, an election supervisor for Madison County, was listed as a felon.</P>
<P>There was a simple reason for all the confusion: Database Technologies had been instructed to compile its lists based not on an exact match of names, but only an 80 percent match.</P>
<P>"We were told that even if things didn't match to go ahead and consider them a convicted felon," said Miami-Dade Elections Supervisor David Leahy.</P>
<P>An estimated 200,000 Floridians, roughly half of them Black, were either not permitted to vote on Election Day or had their ballots thrown out--under the supervision of the president's brother.</P>
<P>The U.S. Civil Rights Commission has verified what we already knew: George W. Bush is an illegitimate president, whose election was only made possible by Jim Crow-style racism and massive fraud.</P>

