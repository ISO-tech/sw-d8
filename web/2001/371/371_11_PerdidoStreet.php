<p><font face="Times New Roman, Times, serif" size="5" color="#000000"><b>Novel of the future that sheds light on the world today</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2"><B>BOOKS: </B>China Mi&eacute;ville, <B><I>Perdido Street Station. </B></I>Random House, 2001, 710 pages, $18.</font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>Review by BRIAN VAUGHAN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 11</font></p>
<p><font face="Times New Roman, Times, serif" size="3">AT THEIR best, science fiction and fantasy describe alternate worlds that, by mixing the fantastic with the familiar, can allow us to better understand our own world.</P>
<P>China Mi&eacute;ville, a socialist from Britain, brings an understanding of class struggle to <I>Perdido Street Station</I>--as well as great imagination.</P>
<P>His latest novel comes from a genre called "steampunk"--which combines the exotic beings and magic of fantasy literature with the grim urban settings of "cyberpunk" science fiction.</P>
<P>New Crobuzon, the setting of <I>Perdido Street Station, </I>is a vast industrial city fouled with pollution. </P>
<P>The government, in the service of the rich, rules through a brutal militia, army and police force. </P>
<P>Convicted criminals and the desperately poor are "remade"--that is, parts of animals or machines are grafted onto their bodies. </P>
<P>Human bigotry against xenians--the intelligent nonhuman population of New Crobuzon--is encouraged and exploited by the bosses.</P>
<P>Isaac Dan der Grimnebulin, the central character of the novel, is a scientist on the outskirts of respectable society--his friends are other scientists, artists and political radicals.</P>
<P>He faces ostracism because of his love for a nonhuman--Lin, a "khepri" sculptor.</P>
<P>During his researches, he comes across a strangely beautiful caterpillar--which later transforms into a monstrous "slake-moth" that attacks one of Isaac's friends and escapes.</P>
<P>Isaac learns that the slake-moths can overcome anyone and devour their dreams and thoughts.</P>
<P>The one that escaped from his lab was among five that were used in a government research project and then sold to a crime lord.</P>
<P>Soon, all five are loose.</P>
<P>Unable to trust the government, Isaac and his friends set out to stop the slake-moths. </P>
<P>They find allies--the Weaver, a giant spider who perceives all reality as a vast and beautiful web; and the Construct Council, a machine that has become self-aware.</P>
<P>This plot plays out against a background of oppression and resistance.</P>
<P>At one point, Isaac tells the story of a photographer who revealed the horrible effects of a weapon used in a war long ago, spurring protests that nearly brought down the regime. </P>
<P>Radical agitators call for unity between humans and xenians in a dockworkers' strike, and "remade" guerrillas fight for their liberation.</P>
<P>There are no easy solutions in New Cobruzon, but there is hope in struggle.</P>

