<p><font face="Times New Roman, Times, serif" size="5">New York City Puerto Rican Day parade</font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by DOMINIC RENDA</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">NEW YORK--The struggle to stop the U.S. Navy from using the Puerto Rican island of Vieques for target practice was center stage at this year's Puerto Rican Day parade.</P>
<P>Organizers, hoping to keep the parade's image moderate, tried to keep Vieques activists from having a presence at the June 10 event.</P>
<P>But unionists from AFSCME District Council 37, the health care workers union Local 1199 and others opened their contingents to the activists.</P>
<P>The unions passed out signs saying, "Paz Para Vieques, Peace for Vieques"--which were held up by many parade-goers.</P>
<P>Press reports all noted that the hundreds of thousands who lined Fifth Avenue to watch the parade frequently broke out in chants of "Vieques S&iacute;, Marina No!" </P>
<P>"Our efforts at the parade were an incredible success," Melissa Mark Viverio, coordinator of Todo Nueva York Con Vieques, told <I>Socialist Worker. </P>
</I><P>"I looked at all the reports in the mainstream press last night and the written press today, and each story or article mentioned that the main message at the parade was the effort to stop the bombings in Vieques.</P>
<P>"This means we were able to impact and influence the parade despite the efforts on the part of the parade committee to silence us." </P>
<P>Luz Santiago, the field services director of AFSCME District Council 1707, said that he thought it was important for the unions to take up the issue of Vieques.</P>
<P>"The goal of unions isn't just for better salaries, but to make the lives of workers better in general," Santiago told <I>Socialist Worker.</P>
</I><P>Mayor Rudolph Giuliani ordered a massively stepped-up police presence for the parade, supposedly in response to the series of sex attacks in Central Park that followed last year's event.</P>
<P>But Vieques activists noticed that the cops took a special interest in them.</P>
<P>"We had a phalanx of cops on other side of the contingent, escorting us through the whole route of the parade," Viverio said. </P>
<P>"When I asked a spectator who was sympathetic to our message if he had witnessed this level of police presence with any other contingent his response was that no, only with ours."</P>
<P>The enthusiasm for the struggle to kick out the Navy shows why George Bush felt pressured to offer a compromise on Vieques in early June--and the potential to step up the mobilization to win immediate withdrawal.</P>

