<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Protesters tell Bush they won't wait until 2003</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Navy out of Vieques now!</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 14</font></p>
<p><font face="Times New Roman, Times, serif" size="3">VIEQUES, Puerto Rico--Protests continued June 18 against the U.S. Navy's practice bombing on the island of Vieques, days after George W. Bush promised to end military exercises by May 2003.</P>
<P>Widespread protests over the last few years from residents tired of 60 years of the Navy's presence forced Bush to agree to the pullout date.</P>
<P>But two years was far from soon enough for the more than 30 activists who broke onto the range to try to block the exercises scheduled for mid-June.</P>
<P>Maria Velazquez, an activist from Mount Carmelo on the edge of the shooting range, said some planned to fire signal flares when planes began their runs.</P>
<P>Jacqueline Jackson, the wife of Rev. Jesse Jackson and co-president of Rainbow/Push Coalition, was among those arrested.</P>
<P>Demonstrations have intensified over the last few years on the island, where U.S. Navy target practice is blamed for widespread environmental contamination and noise-related illnesses.</P>
<P>Opposition escalated in April and early May when more than 180 people were arrested for trespassing.</P>
<P>Police fired so much pepper gas that a child in a car nearby had to be revived after he stopped breathing.</P>
<P>Several demonstrators battled U.S. marshals--armed with pepper gas and rubber bullets--by heaving rocks and fishing weights with slingshots.</P>
<P>Among those arrested during the May protests were high-profile figures like civil rights activist Rev. Al Sharpton and U.S. Rep. Luis Guttierez (D-Ill.).</P>
<P>Many of the arrested were left in the sun for hours without water and were forced into dog kennels--to send a message about what happens if you challenge the U.S. Navy.</P>
<P>Bush's announcement of a 2003 pullout date was partly aimed at preempting a referendum set for November 6, when most of the island's 9,100 residents were expected to vote for the Navy to leave in 2003.</P>
<P>Navy Secretary Gordon England announced June 16 that the Navy will ask Congress to cancel the referendum, which he called "very bad public policy."</P>
<P>But local politicians are feeling the heat--and have called for their own non-binding referendum next month, in which residents would get to vote on whether the Navy should stay, leave in 2003 or leave immediately.</P>
<P>Most Vieques residents know the Navy's history all too well to trust them to keep their promises.</P>
<P>"We have enough experience to know it's not just that the Navy would leave; it's when they would leave," said Roberto Barreto of <I>Socialismo Internacional, Socialist Worker's </I>sister newspaper in Puerto Rico.</P>
<P>"It's not that they would stop, but that they would stop immediately. It's not that they give back that ship full of uranium; it's that they clean that ship.</P>
<P>The Navy's slogan for the island is "'one-stop shopping,'" said Barreto. "This means that you can do anything in Puerto Rico.</P>
<P>"If you are developing a new experimental weapon, you can go to the range in Puerto Rico and test your weapon. You can rent the island, and you can rent the sea&#133;</P>
<P>"We have too much information to be fooled again. Some people have hinted that we're approaching something of a Puerto Rican Intifada."</P>
<P>Bush's call for a 2003 pullout shows that he's feeling the heat.</P>
<P>But we have to turn up the fire to force him to get his Navy out of Vieques now!<br>
