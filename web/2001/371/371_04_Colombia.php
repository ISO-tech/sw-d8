<p><font face="Times New Roman, Times, serif" size="5" color="#000000"><b>Colombian death squad forces its victims to flee</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 4</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear Socialist Worker,</B></P>
<P>Carlos Casta&ntilde;o is the commander of a Colombian army of assassins known as Autodefensas Unidas de Colombia (AUC), which means United Self-Defense of Colombia.</P>
<P>Casta&ntilde;o's arrogance is as giant as his lack of shame. </P>
<P>In mid-April, he did a television interview in which he spoke about his plans to kidnap Camilo G&oacute;mez, Colombia's high commissioner for peace.</P>
<P>The interview was made possible by the Ardila Group, one of the most influential groups among the Colombian elite. </P>
<P>Hours later, 500 of Casta&ntilde;o's gunmen were carrying out an attack against an indigenous area--forcing 7,000 people to flee their homes for Buenaventura.</P>
<P>But this is only the most recent in a long list of atrocities perpetrated by Casta&ntilde;o.</P>
<P>In December 2000, there was an attempt on the life of Wilson Borja, president of the public-sector union.</P>
<P>Borja survived, but one of his guards and an innocent street vendor were killed. </P>
<P>Days later Carlos Casta&ntilde;o was interviewed and admitted sending the gunmen who shot Borja.</P>
<P>Although Casta&ntilde;o had claimed that the aim had been to kidnap--not kill--Borja, the gunmen in this "kidnapping attempt" fired more than 50 shots.</P>
<P>Borja is now out of the country. </P>
<P>He had to leave for fear that he or his family would be killed.</P>
<P>But the workers in his union haven't forgotten him. </P>
<P>And the best way they have to let him know that they have not been defeated is to fight back, as they did recently.</P>
<P>On March 22, they held a 24-hour strike against the brutal new "fiscal adjustment" proposed by President Pastrana. </P>
<P>This includes firing 70,000 public employees, slashing health and education spending and pushing through a new wave of privatization. </P>
<P>And during a related 48-hour strike, another union joined the public-sector union to protest the systematic killing of workers and union leaders.</P>
<P><B>Orlando Sepulveda, </B>Chicago</P>

