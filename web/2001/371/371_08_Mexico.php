<p><font face="Arial, Helvetica, sans-serif" size="2" color="#000000"><b>MEXICO</b></font><br>
<font face="Times New Roman, Times, serif" size="4">After strike victory for flight attendants:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What's ahead for Mexico's unions?</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by TODD CHRETIEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 7</font></p>
<p><font face="Times New Roman, Times, serif" size="3">AFTER 41 hours on strike, 1,500 union flight attendants for Mexico's largest airline, Aeromexico, returned to work victorious.</P>
<P>The walkout forced the company to give up a 9.5 percent wage hike and important work-rule changes.</P>
<P>As union president Alejandra Barrales told the assembled strikers after they voted to accept the deal, "They asked us not to declare this a victory over the company, but we can't avoid saying it, 'We won!'"</P>
<P>The strike was one of the highest-profile labor battles to take place in Mexico in recent years and could well set the tone for the future.</P>
<P>It was also the first major strike under the new government of Vicente Fox, leader of the conservative PAN party who won the presidency last year, toppling the ruling PRI party that had run Mexico as a one-party state for more than seven decades.</P>
<P>Fox is viciously anti-union.</P>
<P>But he has tried to portray himself as a democrat who favors dialogue over repression.</P>
<P>Interestingly, the Aeromexico strike was the first walkout in the transportation or communication industry in recent memory where the government didn't impose Mexico's version of the Taft-Hartley anti-union law--by declaring the strike a threat to national security and therefore illegal. </P>
<P>While not a huge workforce, the flight attendants' victory could give confidence to other sections of workers--helping to lay the basis for larger battles to come.</P>
<P>The strike came on the heels of two important events for the Mexican labor movement.</P>
<P>First, hundreds of thousands of workers marched on May Day in Mexico City, and when Fox tried to address the crowd, he was drowned out by whistles and chants--a sign of opposition to his proposal for a regressive value-added tax on consumer goods. </P>
<P>Unlike in years past, Mexico's independent unions and the PRI-affiliated unions--which were tightly connected to the state when the PRI was in charge--suddenly found they had a common enemy in Fox.</P>
<P>If Fox tries to carry though his broad attacks on workers, this could inspire a spirit of militancy within more conservative unions.</P>
<P>Second, Labor Secretary Carlos Abascal revealed the right-wing reality behind Fox's "democratic" facade in a speech arguing that a woman's place is in the home.</P>
<P>This sexist outburst undoubtedly played a role in bolstering the determination of the striking flight attendants, who are mostly female.</P>
<P>In fact, when one Aeromexico manager echoed Abascal's garbage by saying that giving the union a raise would be like "giving his wife more money for household expenses," union members only stiffened their resolve.</P>
<P>Not unlike his buddy George W. Bush in the U.S., Fox could unite an opposition if he pushes a hard right-wing attack.</P>
<P>This potential unity among workers will be crucial in resisting the employers' attacks as the Mexican economy continues to slide toward a recession.</P>
<P>Mexico is being hit hard by the economic slowdown in the U.S.</P>
<P>Some economists are predicting up to 100,000 layoffs out of a workforce of about 1.4 million in the maquiladora factories along the U.S. border by the end of the year.</P>
<P>Widespread unemployment would mean greater pressure on Mexican workers to come to the U.S. looking for work--at precisely the same time that U.S. workers face layoffs.</P>
<P>No doubt, bosses on both sides of the border will try to pit workers against one another.</P>
<P>That's why building cross-border solidarity--like the binational demonstration against the Free Trade Area of the Americas in San Diego and Tijuana in April--is so important.</P>
<P>We have to organize worker-to-worker solidarity against corporation-to-corporation globalization.</P>

