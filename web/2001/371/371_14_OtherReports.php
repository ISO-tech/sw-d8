<p><font face="Times New Roman, Times, serif" size="4"><b>BioJustice 2001</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by CHUCK STEMKE and SARAH McCLAIN</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">SAN DIEGO--Thousands of protesters from all over the country are expected to converge here on June 22-27 to expose the reckless greed of the biotechnology industry.</P>
<P>The target is BIO 2001, the Biotechnology Industry Organization's biggest trade convention ever. </P>
<P>Gene giants like Monsanto, DuPont and Dow Chemical plan to come together to display their latest developments in genetically engineered (GE) foods and genetically modified (GM) organisms.</P>
<P>These corporations plan discuss how they can establish and maintain intellectual property rights for information such as the genetic code of living creatures.</P>
<P>BIO claims the gene industry holds the answers to all the world's problems, from sickness to hunger--but protesters tell a different story.</P>
<P>Many GE foods produce their own pesticides.</P>
<P>It is widely believed that these foods could cause severe allergic reactions, and they're known to stunt growth in laboratory animals.</P>
<P>In the past year, GM corn that was only permitted for cattle feed contaminated the "people-food" supply, including taco shells from Taco Bell!</P>
<P>Nobody knows what effects GMOs will have on the environment, ecosystems and food chains, but the industry is willing to risk it for one reason: profit.</P>
<P>The protest--BioJustice 2001--will bring together activists, farmers, scientists and many concerned intellectuals for four days of activities, ranging from teach-ins, demonstrations and direct action to GE food-labeling brigades and organic potlucks.</P>
<P>Global justice advocate Vandana Shiva and author Jim Hightower are highlighted speakers.</P>
<P><I>Call 619-239-9130 for more information.</I><br>
