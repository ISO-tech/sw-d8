<p><font face="Times New Roman, Times, serif" size="2"><b>by JON VAN CAMP</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">WASHINGTON--More than 100 demonstrators marched in June in solidarity with Women in Black, an Israeli organization that protests the occupation of Palestine.</P>
<P>The demonstration occurred during rush hour and was well received, ending with a picket in front of the White House.</P>
<P>The crowd, dressed all in black, united Jews, Arabs, Blacks, Latinos and whites around the messages painted on signs in English and Arabic: "No to the Occupation" and "Stop Israeli terror."<br>
