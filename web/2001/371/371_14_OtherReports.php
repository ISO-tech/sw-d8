<P>Marshall, who is Black, reported the incident to management, feeling that the noose was a death threat. </P>
<P>Their response? The noose is merely a tool!</P>
<P>The <I>Times </I>says that it's using the noose for lifting and will acquire another, despite Marshall's complaints.</P>
<P>At a small protest, Carol Marshall and others spoke of the abuse they have suffered at the <I>Times. </P>
</I><P>One transsexual employee spoke about how he was not allowed to use the bathrooms at work and about discrimination against gays and lesbian at the <I>Times.</P>
</I><P>Marshall described how her sexual harassment complaint was laughed at and that her reports of racial slurs were ignored. </P>
<P>There has also been harassment of workers--including Carol Marshall--who struck in solidarity with the Writers' Guild.</P>
<P>A number of the <I>Times </I>protesters also attended a candlelight vigil for Aaron "Smokey" Roberts, a Black man killed by Seattle police. </P>

