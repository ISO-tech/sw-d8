<p><font face="Times New Roman, Times, serif" size="4"><b>Seattle police brutality</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by NAT GIBBONS</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">SEATTLE--After another death of a Black man at the hands of a cop, forces are mobilizing to hold police and city government accountable.</P>
<P>About 300 people attended a community meeting called by the People's Coalition for Justice.</P>
<P>Police stopped Aaron Roberts on May 31 for a traffic violation.</P>
<P>It is still unclear how Officer Greg Neubert's arm got stuck in the car as Roberts drove away.</P>
<P>What is clear is that Roberts was stopped by a bullet fired at point-blank range from the gun of Neubert's partner, Craig Price.</P>
<P>Testimonies at the May 31 meeting made it clear the officers are notorious in Seattle's Central District, a predominantly Black neighborhood.</P>
<P>A neighbor of Roberts described two instances in which Neubert used unnecessary force--leaving one man crippled and another dead. Both men were Black.</P>
<P>The People's Coalition has drafted a list of demands, including the formation of an independent, elected civilian review board with disciplinary power and patrol car video cameras and the immediate firing of Neubert and Price.</P>
<P>Earlier in the week, 500 people turned out for a rally.</P>
<P>Protesters marched to the intersection where Roberts was killed, and half of the group occupied the street, while the rest marched to the 12th Precinct.</P>
<P>On the police station steps--with cops in riot gear visible through the windows--members of the community spoke out.</P>
<P>The Coalition is calling for a boycott and picket of all businesses in the Central District that won't agree to work with their demands.</P>
<P>A picket was called for a Starbucks on June 13 to protest police who protect property while they brutalize Blacks in the neighborhood.<br>
