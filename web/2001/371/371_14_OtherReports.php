<p><font face="Times New Roman, Times, serif" size="4"><b>Money for schools!</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by JON VAN CAMP</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">PRINCE GEORGE'S COUNTY, Md.--More than 150 teachers, students and parents showed up at a rally in June to protest budget cuts in the county's schools.</P>
<P>The protest, sponsored by Stand for Children, came after the superintendent of Prince George's County schools called for cuts, which include a reduction of 150 teachers and the elimination of several programs.</P>
<P>After the rally, protesters packed the school board meeting. </P>
<P>Dozens of students, teachers and parents told the board about class sizes of up to 45 children and a lack of services.</P>
<P>The proposed cuts would mean even larger class sizes.</P>
<P>And administrators want to eliminate a peer mediation program at some schools, which has led to sharp drops in fights and suspensions.</P>
<P>County officials had announced an unprecedented school budget of more than $1 billion, but the superintendent claimed that teacher pay raises this year and new school construction meant there would still need to be cuts.</P>
<P>Two days after the board meeting, the superintendent announced that she was reducing the cutback to 75 teachers.</P>
<P>But activists aren't satisfied.</P>
<P>They are planning for another large turnout when the board votes on the budget June 21.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="4"><b>California utilities</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by TODD CHRETIEN</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--More than 150 people rallied on the steps of City Hall, kicking off the campaign to take over Pacific Gas &amp; Electric Co. (PG&amp;E) and make it into a publicly owned Municipal Utility District (MUD).</P>
<P>"This is a fight between money power and people power," San Francisco Labor Council Secretary-Treasurer Walter Johnson told the crowd. "We've committed our Labor Neighbor program to put people in the streets to make sure the word gets out. Is people power going to beat money power on November 6?"</P>
<P>"Yes!" shouted the rally.</P>
<P>President of the Board of Supervisors [city council] Tom Ammiano promised that MUD would "provide cheaper rates and cleaner, renewable energy."</P>
<P>The number of elected officials who now back MUD reflects the huge shift in public opinion since the start of the California energy crisis last year.</P>
<P>California Senate president John Burton and U.S. Congressperson Carole Migden--both San Francisco Democrats--recently announced their support for the public takeover. </P>
<P>The battle to take over PG&amp;E will be an uphill battle.&#9;</P>
<P>The company spent $200,000 to defeat MUD before it even began, and it's funding several anti-public power groups like "Citizens for Affordable Energy."</P>
<P>However, the recent 40 percent hike in residential rates and threats of blackouts this summer have PG&amp;E over a barrel.</P>
<P>PG&amp;E is one of the most powerful corporate forces in California and a leader in pushing deregulation in the electrical industry across the country.</P>
<P>Beating them on their home turf will send a signal that, when activists say "people before profits," we mean it.<br>
