<p><font face="Times New Roman, Times, serif" size="4"><b>Repeal New York drug laws</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by ROSIE CAMPOS</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">NEW YORK--Activists here are stepping up efforts to mobilize the growing opposition to the state's racist Rockefeller drug laws. </P>
<P>Passed in 1973, the Rockefeller drug laws are among the harshest in the country, requiring a minimum sentence of 15 years to life for minor possession. </P>
<P>The laws were supposedly passed to target major drug kingpins, but in reality serve to imprison largely first-time, low-level drug offenders, most of them poor Blacks and Latinos. </P>
<P>The latest activity follows on a March protest in the state capital of Albany that drew 2,000 people.</P>
<P>Now activists are gearing up for a June 30 rally at the state office building in Harlem.</P>
<P>On June 9, 30 people came to an all-day organizers' meeting at Harlem's St. Aloysius Church to prepare for the rally.</P>
<P>Jovanka Fairly, an activist with the group JusticeWorks, described how, after her imprisonment, the state made it impossible to get her children out of foster care.</P>
<P>"I've done everything they've asked me," she said, "and I still can't get care of my children."</P>
<P>The growing pressure has forced politicians to take notice.</P>
<P>Two proposals to change the drug laws are up for a vote in the state legislature, and Gov. George Pataki promised to "dramatically reform" the laws.</P>
<P>But while Pataki's plan would reduce mandatory minimum sentences for major drug offenses, it makes penalties stiffer for some low-level drug offenses--Class B felonies, which is the most commonly charged offense.</P>
<P>We need to keep up the pressure to expose Pataki's con job--and win support for a repeal of the drug laws, sponsored by Assemblyperson Jeffrion Aubry.<br>
