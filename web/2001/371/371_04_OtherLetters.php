<p><font face="Times New Roman, Times, serif" size="5">Books are barely tolerated in prison</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear Socialist Worker,</B></P>
<P>Steven Likosky is a New York Public Library (NYPL) librarian I met on Rikers Island.</P>
<P>As he pushes his cart down the hallway, the inmates all ask how they can get a book. </P>
<P>Likosky is forced to explain that they can't. </P>
<P>He tells them he's only authorized to serve the six dorms of the Rikers substance abuse program, but that he hopes to be expanding soon. </P>
<P>Likosky--who's been providing library service to New York City inmates for more than 20 years--admits he's being disingenuous. </P>
<P>The truth is, library service is shrinking here. </P>
<P>"Twenty years ago," says Likosky, "most jails and prisons had standing general libraries and library officers.</P>
<P>"Today corrections makes a conscious decision not to prioritize or mandate self-education in prison."</P>
<P>Despite the long history of celebrated writers who became readers in prison--from Jean Genet to Malcolm X--library service is at best tolerated.</P>
<P>Likosky can visit only a small number of the dorms at only four out of the 15 Rikers jails. </P>
<P>He estimates that he serves 5 percent of the inmates at most. </P>
<P>And the service he provides is limited, to say the least--one book a week, if you don't happen to be in the yard or eating when the cart arrives. </P>
<P>When speaking of NYPL, Likosky is a master of understatement. </P>
<P>"I think maybe prison service isn't one of our priorities," he says with an ironic smile. </P>
<P><B>Kristin Hart, </B>AFSCME DC 37 Local 1930, New York City<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Why cops today eat less fast food</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear Socialist Worker,</B></P>
<P>According to a front-page article in the May 23 <I>Wall Street Journal, </I>police officers are increasingly reluctant to eat at fast food restaurants. </P>
<P>This is not because doughnut-eating cops are concerned about their health.</P>
<P>It's because they are afraid that the employees will spit in their food!</P>
<P>There have been a number of incidents in which police officers complained of finding spittle on their burgers and in their nachos. </P>
<P>Many officers now bring their lunches or eat in cafeterias where they can see their food being served up. </P>
<P>The police try to attribute the spitting incidents to anti-police sentiment in the community.</P>
<P>"It's consistent with the erosion of respect for authority figures," says Jim Pasco, executive director of the national Fraternal Order of Police.</P>
<P>But this trend should really be no surprise. </P>
<P>Fast food jobs are low paying and are usually filled by teenagers--many of whom are members of minority groups. </P>
<P>Fast food employees are among the kind of people cops most like to harass.</P>
<P>Even the Oro Valley, Ariz., police department realizes the connection between its treatment of fast food workers and the treatment of police by those same workers.</P>
<P>So the department distributes memos warning its members away from particular restaurants, according to the <I>Wall Street Journal.</P>
</I><P>Says Det. Herb Williams: "I just say, 'Listen, be careful, we've seen a lot of people working [at that restaurant] that we've dealt with."</P>
<P><B>Evan Kornfeld, </B>Los Angeles<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">We need mass transit!</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear Socialist Worker,</B></P>
<P>The other day, a coworker told me that it took him 35 minutes--35 minutes!--to get to work that morning. </P>
<P>He averaged 80 miles an hour on the freeway because there was hardly any traffic.</P>
<P>I've been meaning to write to <I>SW </I>about the unbelievable traffic in the Bay Area. </P>
<P>How illogical--inhuman--it is to drive among the thousands of other single-passenger cars 80 miles or more each day to commute to work! </P>
<P>Why not build a mass transit system for us all? </P>
<P>That's what we would do under socialism. </P>
<P>We'd have downtime that could even be social on the way to and from work. </P>
<P>But what my coworker said next cast a pall on that complaint. </P>
<P>It's the 60,000 layoffs, he said. That's why there's no traffic these days.</P>
<P>Inhuman in both a "boom" and a slump--that's capitalism.</P>
<P><B>Eduardo Capulong, </B>Palo Alto, Calif.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">A lesson that school books don't teach</font></p>
<p><font face="Times New Roman, Times, serif" size="3"><B>Dear Socialist Worker,</B></P>
<P>I teach government at a public school in Prince George's County, Md.</P>
<P>The textbooks we use say the Constitution "guarantees" suspects the right to remain silent, the right to an attorney and the right to due process.</P>
<P>But these guarantees don't seem to apply to Prince George's County.</P>
<P>A series of articles in the Washington Post in early June detailed how police routinely deny suspects the right to see their attorney, physically intimidate them and subject them to continuous interrogation of up to 80 hours at a time in order to extract confessions.</P>
<P>DNA evidence recently exonerated four Black men who were convicted based on these coerced confessions.</P>
<P>"But how can they get away with it?" my students want to know.</P>
<P>"Isn't that unconstitutional?"</P>
<P>Unfortunately, Prince George's County is one of the few police forces that refuses to record its interrogations.  </P>
<P>So when a suspect makes an allegation of police abuse to a judge, it's his word against the police.  </P>
<P>And the judge almost always believes the police.</P>
<P>As I often tell my students, the Constitution doesn't "guarantee" any right unless people are willing to fight for it.</P>
<P><B>Jon Van Camp, </B>Washington, D.C.</P>

