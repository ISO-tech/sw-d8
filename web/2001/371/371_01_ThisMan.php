<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Thousands protest Bush in Europe</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>This man must be stopped!</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 1</font></p>
<p><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH was faced with something that he wasn't used to when he visited Europe in June.</P>
<P>Opposition.</P>
<P>Not so much from political leaders, who told reporters that Bush "left a very positive impression of knowing some facts," as one official put it.</P>
<P>Instead, the opposition came from tens of thousands of ordinary people, who mobilized at nearly every stop on Bush's five-day tour.</P>
<P>The demonstrators challenged Bush down the line--from the startup of the U.S. government's death penalty machine to the administration's insane plan for a Star Wars missile defense system.</P>
<P>In Sweden, where protests continued against a European Union summit meeting after Bush left, police shot three demonstrators with live ammunition, leaving one clinging to life as <I>Socialist Worker </I>went to press.</P>
<P>The establishment media dismissed the protests.</P>
<P>"Trying to build up Europe by acting outraged against America has become the European national sport," sniffed Gregg Easterbrook, of <I>New Republic</I> magazine.</P>
<P>Even Democrats like Sen. Dick Durbin (D-Ill.)--a darling of Washington liberals--said they were "rallying around the president."</P>
<P>But despite the careful staging of the visit, Bush showed plainly why so many people despise him and the policies he stands for.</P>
<P>Within hours of setting foot in Europe, Bush claimed to a reporter that he opposed the execution of the mentally handicapped.</P>
<P>Problem: Bush signed off on the execution of two mentally handicapped prisoners as governor of Texas, and he blocked legislation that would have barred putting the mentally handicapped to death.</P>
<P>White House aides spent days "clarifying" President Blockhead's remarks.</P>
<P>Bush and the gang refused to budge on the issue of global warming, claiming that scientists couldn't agree on the facts--when, in reality, the only scientists who don't agree are the ones cashing a paycheck from Big Oil.</P>
<P>Bush pushed his plans for Star Wars--which led Russian President Vladimir Putin to declare that his government would build enough nukes to overwhelm a U.S. missile defense shield.</P>
<P>And if that wasn't enough, Bush unexpectedly declared that he wanted the NATO military alliance expanded to include the Baltic countries of Latvia, Estonia and Lithuania--something Russia says it won't tolerate.</P>
<P>The mainstream media may have bought the White House spin doctors' talk about Bush's "charm."</P>
<P>But there's nothing charming about this man.</P>
<P>George W. Bush and his pals are pushing a vicious right-wing agenda.</P>
<P>They'll do anything to line the pockets of their corporate pals--even if it puts the future of the planet in jeopardy.</P>
<P>It's time to build an opposition to show Bush that we won't tolerate his attacks.</P>

