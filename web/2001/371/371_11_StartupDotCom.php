<p><font face="Times New Roman, Times, serif" size="4" color="#000000">Dot com bombs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Going from boom to bust</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2"><B>MOVIES: <I>Startup.com, </B></I>directed by Chris Hegedus, D.A. Pennebaker and Jehane Noujaim.</font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>Review by JASON YELLEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 11</font></p>
<p><font face="Times New Roman, Times, serif" size="3">IF YOU'VE wondered what the "dot com" boom looked like from the inside, <I>Startup.com </I>is a place to start.</P>
<P>But not to finish--there's more to the story than this new documentary is willing to tell.</P>
<P>The directors were able to film the birth of govWorks.com, a Web site whose stated mission was "to make government easier to use"--i.e., letting users pay parking tickets online and the like.</P>
<P>The film follows two of the company's cofounders, childhood friends Kaleil Isaza Tuzman and Tom Herman, as they raise money and hire employees--then lose money and fire employees.</P>
<P>After first considering an online wedding registry and a virtual tombstone company, the two started govWorks.com in 1999, at the peak of the Internet bubble.</P>
<P>The film shows people pouring their lives into this startup company--with relationships strained and friendships broken amid the turmoil.</P>
<P>The last third of the film shows the company's demise--with Kaleil firing Tom, his friend since high school.</P>
<P>Yet for all this focus on the human aspect of business, the movie rarely shows govWorks.com's actual workers, preferring to portray the trials and tribulations of top management.</P>
<P>The glimpses that peek through--the bored, tired faces of workers whose company is tanking--tell a different story than the one that <I>Startup.com </I>is interested in.</P>
<P>Of course, whittling down 400 hours of filming into 90 minutes isn't easy. But the directors left out some crucial information.</P>
<P>As a result, Kaleil and Tom come off as babes in the woods--rather than the incompetent con artists that they actually are. </P>
<P>For example, the film shows govWorks' founders raising money for the company--but doesn't show how the two burned up $60 million in less than two years.</P>
<P>Also unmentioned is the Web site that govWorks built for Alameda County, Calif.--which misspelled Alameda. </P>
<P>And the two contracts that Kaleil and Tom lost in Stamford, Conn., and Springfield, Mass., because they decided to issue press releases announcing deals that hadn't been signed yet.</P>
<P>Days before the company was set to launch a Web site of government information, a govWorks employee accidentally deleted all the files. </P>
<P>But management hadn't ensured that there were backups of the files, so four months of work was lost.</P>
<P>This, too, isn't mentioned. </P>
<P>In the end, we're left with a portrait of the Internet boom and bust--one that shows the headiness, silliness and excitement on the way up, and the chaos on the way down.</P>
<P>But <I>Startup.com </I>doesn't show the experiences of all sorts of people who go up and down with govWork's founders.</P>
<P>Nonetheless, if you want to see how the Internet bosses think (or don't) and the insanity that prevailed on Wall Street during the late 1990s, see <I>Startup.com.</I></P>

