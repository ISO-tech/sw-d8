<p><font face="Times New Roman, Times, serif" size="5" color="#000000"><b>Nurses strike over staffing shortages in Minneapolis</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>by EVE HIGHTOWER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 22, 2001 | Page 15</font></p>
<p><font face="Times New Roman, Times, serif" size="3">MINNEAPOLIS, Minn.--About 1,350 nurses at two Fairview hospitals here entered their third week on strike--their first walkout since 1984--largely over staffing concerns.</P>
<P>Along with control over staffing and better health care plans, nurses are demanding a 25 to 30 percent pay increase over three years.</P>
<P>The hospitals are offering 19.8 percent raises.</P>
<P>Nurses believe higher wages are needed if the hospitals are going to attract people to a stressful field desperately in need of workers.</P>
<P>"We can't take care of any more patients safely," registered nurse Lynne Riley told reporters outside of Fairview-University Medical Center's Riverside campus.</P>
<P>Nurses at two other hospitals were unable to strike due to hasty action on the part of union leaders.</P>
<P>Abbott Northwestern Hospital and Phillips Eye Institute nurses voted to strike, but Minnesota Nurses Association (MNA) officials said they already verbally agreed to the contract after they miscounted the contract ratification vote.</P>
<P>A recount showed that the contract was actually rejected by one vote, but because the hospitals had been notified of its acceptance, lawyers for both sides said, the contract was legally binding.</P>
<P>In response, infuriated nurses formed a group called RNs for Change to replace the union officials and ensure the same mistake does not happen again. </P>
<P>"We want to start from scratch. We want to make MNA work for all of us," said Vicki Gardner, a nurse at North Memorial and one of about 10 nurses who started the group in late May.</P>
<P>Since then, she said, "a couple of hundred" nurses have expressed interest in joining.</P>
<P>According to Gardner, MNA undermined the nurses' solidarity by recommending approval of the North Memorial contract while other hospitals were still bargaining.</P>
<P>Nurses from other area hospitals have joined the Fairview picket lines, and International Brotherhood of Teamsters members have agreed to honor the lines.</P>
<P>As <I>Socialist Worker </I>went to press, Federal Mediation and Conciliation Service Commissioner Jeannie Frank had called a meeting between union and Fairview representatives for June 18.</P>

