<p><font face="Times New Roman, Times, serif" size="4" color="#000000">The struggle to defend the Charleston Five</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We're ready to take action"</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 22, 2001 | Page 5</font></p>
<p><font face="Times New Roman, Times, serif" size="3">THE CAMPAIGN to defend the Charleston Five is growing.</P>
<P>More than 7,000 union members and supporters poured into the South Carolina capital of Columbia on June 8 to send the message that they won't allow union rights and civil rights to be trampled.</P>
<P>Already, the local that represents the Five--International Longshoremen's Association (ILA) Local 1422--has received hundreds of thousands of dollars for its defense fund.</P>
<P>The five dockworkers facing trial on felony riot charges--Kenneth Jefferson, Elijah Ford Jr., Peter Washington Jr., Ricky Simmons and Jason Edgerton--have been under house arrest for more than 17 months.</P>
<P>But the "riot" was a police attack on a peaceful picket line of 150 workers by 600 heavily armed cops in January 2000.</P>
<P>The initial charges against several ILA  members were dismissed in local courts.</P>
<P>But State Attorney General Charlie Condon--an ambitious politician who wants the Republican nomination for governor--wouldn't give up.</P>
<P>He got a grand jury indictment against the Five in a bid to win the support of multinational corporations that have invested in hundreds of new factories in South Carolina in recent years.</P>
<P>The ILA also played a key role in electing Democratic Gov. Jim Hodges, earning it the hatred of the state's Republican establishment.</P>
<P>And since Local 1422 is overwhelmingly African American, Condon thought he could ride a white backlash into the governor's mansion.</P>
<P>Instead, he's got a fight on his hands.</P>
<P>Thanks to a tireless effort by Local 1422 members, the campaign to defend the Charleston Five has won the backing of not only major unions in the Southeast, but the AFL-CIO, too.</P>
<P>What's more, Local 1422 has already won a union contract with the Denmark-based shipping company, Nordana, whose use of the nonunion stevedoring company WSI provoked the picket-line battle in January 2000.</P>
<P>And 80 percent of workers at WSI have themselves signed union cards in advance of a union election to be held next month.</P>
<P>With the case of the Charleston Five expected to go to court in September, all efforts are on raising funds and preparing for an International Day of Action on the first day of the trial.</P>
<P>The International Longshore and Warehouse Union has already pledged to shut ports on the West Coast--and Swedish, Danish, Spanish and other dockworkers' unions have vowed to take action as well. </P>
<P>The fight is on! Free the Charleston Five!</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><B>KENNETH RILEY is the president of ILA Local 1422. While in Chicago to speak at Socialist Summer School 2001, he spoke to LEE SUSTAR about this crucial battle.</B></P>
<P><B>WHAT DID the rally on June 9 represent to you?</b></P>
<P>I THINK that it accomplished everything that we set out to do--which is to send a message to Condon that we're part of a much broader movement.</P>
<P>Even though he may have thought we were isolated, we're not.</P>
<P>The labor community and the civil rights community isn't going to stand idly by while this type of repression continues.</P>
<P>I think that it's clear that we're prepared to take action. </P>
<P>For me, it was very important to put on that stage dockworkers from the East Coast, the West Coast and Europe.</P>
<P><B>THE DEMONSTRATION brought together people who have been doing grassroots organizing in the South for years, as well as organized labor. How did that come together?</B></P>
<P>THIS PARTICULAR issue is going to help the AFL-CIO and the international unions to once again begin to focus on the South.</P>
<P>When Bill Fletcher [then an assistant to AFL-CIO President John Sweeney] first came, we spent a day going over all the issues, talking about the South and showing him ad after ad of the major corporations that have moved to our area. </P>
<P>We let the AFL-CIO know that you had better start investing in the South.</P>
<P>You don't have to take your business to Third World countries. You can take it down South. </P>
<P>When we started, organized labor looked at us and saw that we were just 3.8 percent organized in South Carolina and said, "Why would we just waste our resources down there?"</P>
<P>But how can we grow, how can we change if we don't address that? </P>
<P>The jobs are moving here, and the corporations are moving here. </P>
<P><B>HOW DID you go about building support for the Charleston Five?</B></P>
<P>I HAVE to give credit to Bill Fletcher here. He's been brilliant. </P>
<P>He looked at this issue, and right then, he saw all the many different issues embedded in this one particular struggle. </P>
<P>It took a while to get to the mainstream.</P>
<P>But I'm still amazed at the outpouring of support we've gotten and how many people are interested in this thing. </P>
<P>I had to sit back and ask myself, "What makes this different from other struggles?" </P>
<P>Because there are a lot of significant struggles. </P>
<P>I heard [at Socialist Summer School] about the Campaign to End the Death Penalty--about people putting their lives on the line. </P>
<P>Basically, I went to people and told them about the conditions we face in South Carolina.</P>
<P>And that got them involved in the struggle.</P>
<P><B>AFL-CIO PRESIDENT John Sweeney and NAACP Chair Julian Bond put out a statement against the red-baiting by Charlie Condon. That was a significant change for the labor movement.</B></P>
<P>I THINK they're finally waking up to the reality that there are struggling workers in the South who are willing to fight. If you help us, we're willing to fight. </P>
<P>We need resources and backing, not only financially, but also just the weight of those names and those organizations--and their involvement.<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">What's at stake in this fight?</font></p>
<p><font face="Times New Roman, Times, serif" size="3">UNION RIGHTS. Racial justice. Organizing the South. Corporate globalization.</P>
<P>All these issues exploded into a police riot on the docks in Charleston, S.C., on the night of January 20, 2000--and remain today at the heart of the struggle of the Charleston Five.</P>
<P>With a unionization rate of less than 4 percent and wages 20 percent below the national average, South Carolina has been a powerful magnet for foreign multinational corporations like BMW, Robert Bosch and Michelin--as well as U.S. giants like DuPont, General Electric and W.R. Grace.</P>
<P>But ILA Local 1422 represents a threat to this corporate-friendly environment.</P>
<P>The union provided crucial assistance to organizing drives across South Carolina in recent years. </P>
<P>And as a nearly all-Black union in a state with a 30 percent African American population, the ILA has challenged the race-baiting, divide-and-conquer tactics used by employers to keep unions weak in the South for more than a century.</P>
<P>That is why many key unions in the region--mainly those grouped in the Atlanta Central Labor Council--have put serious resources into the campaign to defend the Charleston Five.</P>
<P>If Condon succeeds in putting the Five behind bars, it would have an immediate impact on organizing efforts and campaigns to improve labor laws.</P>
<P>A defeat in Charleston could set back the labor movement in the South for years.</P>
<P>But the case has implications across the U.S.--and around the world.</P>
<P>With a union-bashing president in the White House, a defeat for the ILA would only strengthen the Republicans' drive to weaken unions.</P>
<P>And shipping-line bosses--who in recent years have attacked dockworkers' unions in England, Australia, New Zealand and Brazil--would only be encouraged to go on the offensive around the globe.</P>
<P>As one of the Charleston Five campaign's slogans put it, "If these five men go to jail, your rights go with them."</P>
<P>That's why it's so important that every union member--and everyone committed to justice--joins this fight.</P>
<P>Free the Charleston Five!<br>
