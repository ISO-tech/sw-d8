<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush tries to kill Patients' Bill of Rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Kowtowing to HMOs</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ALL PUFFED up after Congress passed his $1.35 trillion tax cut, George W. Bush must have thought he could get away with anything. But it hasn't worked out.</P>
<P>Congress is once again considering the Patients' Bill of Rights. The legislation, sponsored by Reps. Charles Norwood Jr. (R-Ga.) and John Dingell (D-Mich.), is a package of limited and fairly non-controversial reforms.</P>
<P>But the HMO bosses don't like it. So George W. Bush doesn't like it.</P>
<P>In particular, the health care bosses are concerned with a provision that allows patients to sue their HMOs. So the Bush White House got Rep. Ernie Fletcher (R-Ky.) to offer an alternative bill limiting the right to sue--and started twisting the arms of every Republican it could. But it isn't working.</P>
<P>Dozens of House Republicans balked, and late last month, House Speaker Dennis Hastert (R-Ill.) was forced to delay a vote because he couldn't muscle through Fletcher's bill. "A dictatorship would be a heck of a lot easier--there's no question about it," the frat-boy-in-chief declared.</P>
<P>Clearly, some Republicans are worried that a vote against the Dingell-Norwood bill could bite them in the butt in the next election. They recognize at some level that Bush's kowtowing to every whim of Corporate America is unpopular--even when it's dressed up by the White House as an attack on greedy lawyers.</P>
<P>People might not like lawyers, but they despise HMOs.</P>
<P>Democrats are hoping to get some mileage out of this debate. But while their plan is better than Bush's, it's far from a solution for the victims of HMO greed--not to mention the 43 million people without insurance, who'll get nothing from the Patients' Bill of Rights.</P>
<P>A Gallup poll released last month showed that Americans support a Patients' Bill of Rights by a margin of better than five to one. That kind of sentiment could be mobilized to win real health care reform--no matter what maneuvers the White House tries to pull. </P>
<P>But don't expect the Democrats to do it. They're worried about offending the same HMO bosses. The fight for health care for all will have to be built from the bottom up.</P>

