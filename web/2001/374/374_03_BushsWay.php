<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's way or the highway</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">U.S. ARROGANCE was so thick at the G8 summit in Genoa that even U.S. allies found it hard to take. "Not only will [George W. Bush] be a target for the thousands of antiglobalization protesters massing in the Italian city," the <I>Financial Times</I> reported. "He has hardly made himself popular with several of the other Group of Eight industrialized nations."</P>
<P>Even as British Prime Minister Tony Blair was performing his usual service of smoothing things over for Washington, officials in his government privately told reporters that a U.S. missile defense system would "trigger a new arms race and harm relations with Russia."</P>
<P>And the supposed "breakthrough" on missile defense negotiations between Bush and Russian President Vladimir Putin turned out to be hot air. Dubya's handlers quickly dispatched National Security Adviser Condoleezza Rice to Moscow, where she repeated the administration's vow to push ahead with missile defense, no matter what.</P>
<P>Washington's "unilateralism"--diplomatic-speak for throwing its weight around--was also on display during the Bonn negotiations over global warming. Defying every other advanced country, Bush rejected the Kyoto Protocol to limit greenhouse gas emissions.</P>
<P>Virtually the entire scientific establishment regards global warming as an urgent problem. But the oil and gas barons who run the White House believe that Kyoto will cut into their profits. End of story.</P>
<P>Besides imposing a new arms race and more environmental destruction on the world, Bush's crew last month scuttled an international treaty to enforce a ban on biological weapons.</P>
<P>The reason? The U.S.--which imposed a decade of sanctions on Iraq for supposedly hiding biological and chemical arms--doesn't want anyone inspecting its own weapons of mass destruction.</P>
<P>All of this was a bit much even for the U.S.'s partners in crime in the G8. No wonder Washington is hated around the world as the enforcer of a rotten system.</P>
