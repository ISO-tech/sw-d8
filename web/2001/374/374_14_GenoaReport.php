<P><font face="Times New Roman, Times, serif" size="4">U.S. solidarity action with Genoa protests</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Take on one of us, you take on all of us!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 14</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS IN cities across the U.S. organized protests and meetings in solidarity with the global justice demonstrations at the G8 summit in Genoa, Italy. They also showed their outrage at the police murder of 23-year-old protester Carlo Giuliani and the beating of other demonstrators. Here are reports from a few cities.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>New York City </b></P>
<P><font face="Times New Roman, Times, serif" size="3">Activists organized two protests at the Italian Consulate at the end of July. On July 20, about 40 people attended a somber, funeral-like vigil, with marchers carrying a makeshift coffin. Some 200 people turned out three days later, with activists from the Direct Action Network, the Rainforest Action Network, ACT UP, the Global AIDS Coalition, the Green Party, the ISO and other groups.</P>
<P>Many are already organizing for the anti-World Bank/International Monetary Fund (IMF) protests in Washington, D.C., in late September and the New York-based demo against the World Trade Organization in November. </P>
<P>About 85 people turned out for a meeting that included Genoa eyewitnesses Nihar Bhatt and Meredith Kolodner, as well as speakers from the Mobilization for Global Justice, the ISO, the Brooklyn Green Party and Al-Awda.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>San Francisco </b></P>
<P><font face="Times New Roman, Times, serif" size="3">About 250 antiglobalization activists assembled at the Italian Consulate on July 23. Protesters spoke out passionately as they placed flowers on a memorial for Carlo Giuliani. Parallels were drawn between the brutality of the Italian police and the racist violence of the police in the U.S.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Chicago</b></P>
<P><font face="Times New Roman, Times, serif" size="3">More than 40 people rushed the Italian Consulate to demand the release of Genoa protesters held by Italian authorities. Coming just hours after police in Genoa invaded and battered activists at the Genoa Indy Media Center and the Genoa Social Forum, the demonstration called attention to the brutal methods used against people exercising their right to protest. </P>
<P>"I'm here on behalf of Carlo," said one protester. "But not in mourning. I'm here in rage."</P>
<P>After picketing in front of the building, protesters streamed inside, thwarting efforts by police and security to keep them from reaching the consulate's offices. Once outside the offices, protesters banged on the doors, demanded the immediate release of prisoners still being held in Genoa and insisted on a meeting with the consul general. But Chicago police declared our gathering "illegal" and threatened us with arrest. </P>
<P>"Hey Berlusconi, what do you say? How many kids did you kill today?" was our final chant as we returned to the street.</P>
<P>Later that week, 120 people attended a meeting, featuring Genoa eyewitness Ahmed Shawki, editor of the <I>International Socialist Review, </I>and Chris Geovanis, of the Chicago Indy Media Center.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles</b></P>
<P><font face="Times New Roman, Times, serif" size="3">About 30 people picketed the Italian Consulate here on July 20, the day that Carlo Giuliani was murdered. Later that week, a meeting was held that included eyewitness accounts from Genoa and speakers from the Fair Trade Network and the ISO.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Providence, R.I.</b></P>
<P><font face="Times New Roman, Times, serif" size="3">On July 28, the Rhode Island Green Party, the ISO and the Rhode Island Global Action Network cosponsored a panel discussion on the G8 protests. The panel featured Genoa eyewitness David Zirin, a member of the DC Teachers Union and the ISO; Margaret O'Donnell, co-chair of the Rhode Island Green Party and member of Rhode Island Global Action Network (RIGAN); and Andrew Cortes, member of Carpenters Union Local 94 and RIGAN. About 25 people attended and made plans for local outreach to build for the Washington, D.C., protests against the IMF and World Bank. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Portland</b></P>
<P><font face="Times New Roman, Times, serif" size="3">About 300 marched to the Italian Consulate here on July 23, chanting, "You take ONE of us on; you take ALL of us on!"</P>
<I><P>Anthony Arnove, Josh On, Eric Ruder and Lee Wengraf contributed to this report.</I></P>
