<P><font face="Times New Roman, Times, serif" size="4">How the Bush boys stole Florida</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bare-knuckle brawling for every vote</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ERIC RUDER</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH may be slow-witted, but he and his buddies know how to win a fight. That's the only conclusion that you can draw from reading the <I>New York Times'</I> investigative report, published last month, on the disputed presidential vote in Florida.</P>
<P>The <I>Times'</I> six-month-long investigation focused on yet another area where the Bush gang stole votes from Al Gore--overseas military ballots.</P>
<P>While claiming to take the high road after Election Day, the Bush gang waged a bare-knuckles fight for every vote.</P>
<P>Votes from military personnel stationed overseas--and sent back to Florida by mail--were especially prized, according to the <I>Times</I>. "Their goal was simple: to count the maximum number of overseas ballots in counties won by Mr. Bush, particularly those with a high concentration of military voters, while seeking to disqualify overseas ballots in counties won by Vice President Al Gore," wrote the <I>Times</I>.</P>
<P>In other words, Republican lawyers fought for looser standards in counting military votes--but only in Republican counties. Thus, according to the <I>Times</I>, counties that Bush won were four times more likely to accept overseas military ballots without witness signatures or addresses than counties won by Gore--and three times more likely to count ballots with no evidence that they had been mailed by Election Day.</P>
<P>To get all this done, Republican operatives set up a "war room" in the office of Florida Secretary of State Katherine Harris--who was responsible for overseeing the election--to put together their postelection strategy.</P>
<P>In Washington, D.C., Bush campaign officials pushed the Pentagon to speed up delivery of military ballots, and Republicans on the House Armed Services Committee helped the Bush team get in touch with military voters overseas.</P>
<P>As Bush lawyer Benjamin Ginsberg later put it, the fight for Florida was "as hardball a game as any of us had ever been involved in."</P>
<P>And it worked. Whenever a Democrat raised objections to the looser standards for military ballots pushed by Republicans, the Bush boys practically popped in patriotic rage. "If [soldiers] catch a bullet, or fragment from a terrorist bomb, that fragment doesn't have any postmark or registration of any kind," sputtered Fred Tarrant, a Republican city council member from Naples.</P>
<P>The Gore campaign immediately retreated--with vice presidential candidate Joe Lieberman going on TV to say that rejected military votes should be reconsidered.</P>
<P>This was typical--Gore's postelection strategy was as conservative as his campaign. The Democrats focused on legal bickering about recounts in a few southern Florida counties--and refused to touch the disenfranchisement of thousands of Black voters, engineered by Bush's baby brother Jeb through illegal "scrubbing" of voter lists and Jim Crow-style intimidation at the polls.</P>
<P>Meanwhile, the Bush gang was stealing every vote it could find.</P>

