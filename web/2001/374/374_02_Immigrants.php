<P><font face="Times New Roman, Times, serif" size="5"><b>Immigrants face higher injury rates in the workplace</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by NICOLE COLSON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Latino workers are 20 percent more likely to be injured or killed on the job than their white or Black counterparts. That's the grim fact exposed by recent studies on workplace safety.</P>
<P>Latino immigrants--both legal and illegal--are often forced to accept the most hazardous jobs. Industries like meatpacking--which had a terrible safety record to begin with--have transformed their workforces in recent years by recruiting immigrant workers.</P>
<P>"There's a long history of discriminatory hiring in the United States involving immigrants, with the result that for many, many years, immigrants have done the dirtiest and most dangerous jobs," said University of North Carolina professor Dana Loomis.</P>
<P>Researchers say several factors play into the high injury rate for Latino workers. One is the language barrier--Spanish speakers are less likely to get safety training or notification of hazardous conditions on the job.</P>
<P>Plus, researchers found, immigrant workers are often reluctant to report unsafe working conditions for fear of losing their jobs or--in the case of undocumented workers-being deported.</P>

