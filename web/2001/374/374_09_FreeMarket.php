<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Does the market help the poor?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by DAVID WHITEHOUSE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BEFORE THE G8 meeting in Genoa last month, George W. Bush declared that those who oppose the free market are hostile to the poor. "For those who want to shut down trade," he snarled, "I say you are hurting poor countries."</P>
<P>The nerve of this guy! The truth is that the penetration of the free market into all corners of the globe has brought huge riches to the wealthy--and huge misery to the poor.</P>
<P>Bush doesn't want us to remember examples like the Indonesian crisis of 1997-98. All of the ideologues' favorite ideas played a role--privatization, deregulation, cuts in services, the free flow of capital.</P>
<P>In the early 1990s, investors poured money into Indonesia--so long as the country offered high profits as a haven for sweatshops and destructive mining projects. But investors panicked when Indonesian exports piled up in a glutted world market in the late 1990s. </P>
<P>The International Monetary Fund's response to the free-market fiasco in Indonesia was to offer bailout loans--as long as the patient agreed to swallow more free-market poison.</P>
<P>The dictator Suharto was ordered to speed up privatization to make way for layoffs and wage cuts--and to stop using subsidies that "interfered" with the free-market prices for necessities such as rice, fuel and cooking oil. Indonesia's poor, already reeling from the crisis, were driven deeper into poverty.</P>
<P>But Indonesia isn't the only example of the free-market assault on the poor. The rich have always used the market to squeeze wealth out of people. The first real entry of the market into many societies has been the growth of the cash economy in agriculture--something that began 300 years ago in northern Europe and is still playing out today.</P>
<P>In a cash economy, peasants sell their goods on the market to pay rent and taxes--and to buy technology to stay competitive. This means that poorer peasants sell off the reserves that used to tide them over in rough times. This is how the era of market-driven famines begins.</P>
<P>If crops fail, shortages drive food prices up just when the poor need to start buying their food on the market. And to raise cash, peasants take out loans or begin to sell their livestock and land--just when the prices for these items fall because other peasants are doing the same thing.</P>
<P>The free market has killed hundreds of millions of people through famine and hunger-related diseases--from Ireland to India to Ethiopia. Food has been available in all these cases--but at prices too high for the poor to afford.</P>
<P>When peasants flee to the cities, they find an urban labor market that is a race to the bottom--because of the desperate competition for jobs. Even the most basic step for workers to defend themselves--joining together into unions--is seen as a violation of free-market purity.</P>
<P>In fact, the only way that workers have been able to raise themselves above sweatshop conditions has been by resisting the gospel of the free market. That means fighting the boss collectively for better conditions--and fighting politically in favor of government "interference" in the market, with unemployment benefits, welfare, free health care, subsidized housing and so forth.</P>
<P>Resisting the free market is the real way to fight poverty--until we can replace the bosses' system with a democratically run economy and banish poverty once and for all.</P>
