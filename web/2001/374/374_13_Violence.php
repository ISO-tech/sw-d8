<P><font face="Arial, Helvetica, sans-serif" size="2"><b>The MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is violence necessary to win social change?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 13</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE RULING class and their media are quick to condemn acts of violence as morally wrong or "counterproductive." </P>
<P>They have plenty of handy phrases to get the point across: "Don't take the law into your hands." "Violence only begets more violence." Or simply: "Thou shalt not kill." "Peaceful protesters" are applauded by the politicians and pundits, while "violent protesters" are condemned.</P>
<P>But Italian authorities exposed their own hypocrisy in their attacks on global justice protesters in Genoa last month. Despite their rhetoric about peaceful versus violent protestors, police gassed and beat thousands of people who came to protest peacefully.</P>
<P>Today, state terror is seen as a perfectly reasonable response to any kind of protest, peaceful or otherwise. "If terrorism is understood," wrote the Russian revolutionary Leon Trotsky, "as any action inspiring fear in, or doing harm to, the enemy, then of course, the entire class struggle is nothing but terrorism. And the only question remaining is whether the bourgeois politicians have the right to pour out their flood of moral indignation about proletarian terrorism when their entire state apparatus with its laws, police and army is nothing but an apparatus for capitalist terror!"</P>
<P>Usually, though, the mainstream media define "terrorism" as acts of violence committed by individuals or small groups. This violence is condemned as beyond the pale. </P>
<P>"[Socialists have] nothing in common with those bought-and-paid-for moralists who, in response to any terrorist act, make solemn declarations about the 'absolute value' of human life," wrote Trotsky. "These are the same people who, on other occasions, in the name of other absolute values--for example, the nation's honor or the monarch's prestige--are ready to shove millions of people into the hell of war. Today, their national hero is the minister who gives the sacred right of private property; and tomorrow, when the desperate hand of the unemployed workers is clenched into a fist or picks up a weapon, they will start in with all sorts of nonsense about the inadmissibility of violence in any form."</P>
<P>These words should be remembered when we think of Carlo Giuliani, the protester who was shot dead in Genoa on July 20. </P>
<P>But pointing out the hypocrisy of the ruling class on questions of violence doesn't exhaust the question. There remains the issue within the movement of what forms of struggle are effective in wresting concessions from those who rule.</P>
<P>Individual terrorism substitutes the acts of the few for the struggle of the many. In any confrontation, a handful of street fighters will be no match for the forces of the state. Our side must depend on the force of overwhelming numbers.</P>
<P>We don't renounce force--the slogan of the 19th-century Chartist movement is appropriate: "Peaceably if we may, forcibly if we must." But we do renounce the futile violence of the minority. </P>
<P>In Genoa, the street fighting carried out by the so-called "black bloc" helped not only to disorganize the protest, but to open all participants to police attack. The police understood this well and used it to their advantage--even using agent provocateurs dressed in black to stir things up. </P>
<P>The "black bloc" anarchists elevate property damage and street fighting by a tiny minority as not only the main tactics, but as political ends in themselves. Though they may hope that their revolt sparks wider struggle, the effect is in fact the opposite--to distract from the demands of the struggle and discourage mass participation. </P>
<P>As Trotsky explained: "In our eyes, individual terror is inadmissible precisely because it belittles the role of the masses in their own consciousness, reconciles them to their powerlessness, and turns their eyes and hopes towards a great avenger and liberator who some day will come and accomplish his mission.</P>
<P>"The anarchist prophets of the 'propaganda of the deed' can argue all they want about the elevating and stimulating influence of terrorist acts on the masses. Theoretical considerations and political experience prove otherwise. The more 'effective' the terrorist acts, the greater their impact, the more they reduce the interest of the masses in self-organization and self-education."</P>
