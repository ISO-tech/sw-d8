<P><font face="Times New Roman, Times, serif" size="4">West Coast activists build support for campaign</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Free the Charleston Five!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 15</font></p>

<P><font face="Times New Roman, Times, serif" size="3">KENNETH RILEY, president of International Longshoremen's Association (ILA) Local 1422, last month continued his tour to build support for the Charleston Five--members of his union under house arrest and facing five years in prison following a police attack on their picket line in South Carolina last year.</P>
<P>This defense campaign is backed by the AFL-CIO, and civil rights organizations have offered their support as well. The union was targeted by South Carolina Attorney General Charlie Condon in part for its role in the movement to remove the Confederate flag from the statehouse. </P>
<P>In July, Riley took his tour to the West Coast. Here, <I>Socialist Worker </I>reports on the latest solidarity efforts.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Seattle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by DARRIN HOOP, IBT Local 174, and STEVE LEIGH, SEIU District 925</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">Kenneth Riley brought the campaign to defend the Charleston Five here July 19 and 20 at three different events. </P>
<P>After appearing at a series of meetings for the International Longshore and Warehouse Union (ILWU) in Vancouver, B.C., Riley spoke at an afternoon barbecue hosted by ILWU Local 19 in Seattle attended by about 300 union members.</P>
<P>That evening, Riley spoke at the ILWU Local 32 hall in Tacoma, Wash., at an event organized by Washington State Jobs with Justice. About 90 people attended the event, which featured a performance by labor singer Anne Feeney. </P>
<P>Riley capped his stay in the Northwest the following evening in an exciting panel discussion at the Labor Temple in Seattle. Steve Williamson, Executive Director of the King County Labor Council, introduced the meeting. The panel included Willie Adams, a member of ILWU Local 32; Tyree Scott of the Northwest Labor and Employment Law Office; and Rev. Robert Jeffrey, Minister of New Hope Baptist Church. </P>
<P>Jeffrey, a leader of protests against recent police shootings in Seattle, began his speech by announcing that "we lost one of our own in Genoa"--and talked about how the police do the work of the corporations. </P>
<P>Solidarity greetings were given by Paul Blaski of the United Union of Roofers, Waterproofers and Allied Workers Local 54, who are on strike in Seattle, and Scott Sullivan, Secretary-Treasurer of Teamsters Local 174, who read a statement from the International Brotherhood of Teamsters General President James Hoffa. </P>
<P>The highlight of the evening was the presentation by Riley, who moved the 90-plus participants to several standing ovations. "I thought it was tough being Black in South Carolina," he said. "I found out that it's just as tough being union."  The audience was moved and inspired, and ended the evening with a rousing solidarity clap and cheer. </P>
<P>This event marked an important step forward in helping to unite the labor movement and civil rights activists in Seattle in the fights against racism and attacks on our unions. The three events raised $10,000 for the Charleston Five and helped launch the Seattle Committee to Defend the Charleston Five. Already, solidarity job actions are being built at the Port of Seattle for the International Day of Action planned for the first day of trial of the Five, expected in September.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>San Francisco</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SUE SANDLIN</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">About 100 people gathered at the ILWU Local 10 union hall to hear Kenneth Riley speak about the struggle of the Charleston dockworkers July 21. </P>
<P>The event, organized by the Northern California Labor Committee in Defense of Charleston Longshore Workers, brought together representatives from many different unions and community members. Speakers included ILWU Local 10 Executive Board member Jack Heyman; Chuck Mack, Secretary-Treasurer of Teamsters Joint Council 7; Karega Hart, an organizer for SEIU Local 790; and member of the Black Radical Congress; and Fran Beal, also with the BRC. </P>
<P>Almost everyone took sample resolutions in support of the Charleston Five to get passed in their unions, as well as brochures on the campaign to help spread the word. The event raised about $1,600 and inspired almost everyone present to get involved.<br>
