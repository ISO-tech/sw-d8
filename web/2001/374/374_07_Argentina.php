<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ARGENTINA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Argentina rocked by general strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JESSIE MULDOON</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ARGENTINA was rocked by two days of a general strike called last month to oppose planned austerity cuts. On July 19, public-sector workers across the country struck. They were joined the next day by the three main union federations in Argentina. </P>
<P>The general strike shut down government offices, schools and businesses across the country. Protesters even marched on the home of Domingo Cavallo, the country's current ecnomics minister. In Buenos Aires, Argentina's capital, protesters rallied in front of Argentina's stock exchange. Police attacked the demonstration, using water cannons to drive protesters away.</P>
<P>Argentina has been in a recession for three years and is close to defaulting on a $128 billion foreign debt. If Argentina's financial crisis gets worse, it's highly unlikely that it would remain contained there.</P>
<P>"If Argentina collapses, it won't collapse by itself," French President Jacques Chirac said at last month's Group of Eight summit. "It would spark a process like we saw when Thailand was in the same situation, which took us two years to get out of."</P>
<P>That's the nightmare that investors and world leaders alike live in fear of.</P>
<P>On the day that the strikes began, Argentina's economic minister announced that unemployment had risen from 15 percent to 16.4 percent, and that 10 million Argentines already live in poverty. The austerity plan would cut public employees' wages by 13 percent, drastically reduce retirement pensions and slash social spending.</P>
<P>Yet President Fernando de la Rua and his Radical Party government are delighted with the cuts. "I'm happy that all the intense work we've gone through has borne fruits, which were to make the austerity plans fair," said one Radical Party member of Congress.</P>
<P>But fair is the last thing that the plan is. Hugo Moyano, leader of Argentina's General Workers Confederation, said that the general strike "was the people's response to a plan that falls most on retirees, on families with fixed incomes--and we will not stand for it. "If he [de la Rua] wants those with less to pay for this plan, then we will continue to stand up against this decision."</P>
<P>De la Rua has now seen seven general strikes and countless protests against his economic plans since he became president. In March, former Economics Minister Ricardo Lopez Murphy--one of three people to hold the post in just the month of March-had proposed to cut spending on higher education.</P>
<P>But de la Rua's administration abandoned the plan when faced with massive protests. Now the vote to put his austerity measures into effect has been postponed. The strikes proved once again that de la Rua doesn't have the power to impose his policies without a challenge.</P>

