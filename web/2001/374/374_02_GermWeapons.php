<P><font face="Times New Roman, Times, serif" size="5"><b>White House buries treaty to ban germ weapons</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--First it was land mines. Then machine guns and mortars. And now those international do-gooders want to ban germ weapons.</P>
<P>But the Bush gang is stepping up to the plate once again.</P>
<P>Last month, the White House sent Donald Mahley to Geneva to tell representatives of 55 nations that the U.S. would reject a draft agreement to enforce an international ban on biological weapons. Mahley said the treaty--seven years in the making--had "serious substantive" flaws. </P>
<P>"In our assessment, the draft protocol would put national security and confidential business information at risk," Mahley said.</P>
<P>When it comes to demonizing Saddam Hussein, of course, no one is quicker than U.S. government officials to talk about the horrors of germ warfare. But at home, the risk to "confidential and business information" takes a higher priority.</P>
<P>What's the risk, you ask? Chemical and drug companies lobbied hard against the draft agreement--because it would allow inspections of plants suspected of producing biological weapons.</P>

