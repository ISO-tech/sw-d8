<P><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Showing the history of the Holocaust</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JEFF BALE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE ELEVATOR doors close as you start toward the fourth floor. A TV monitor shows horrific images of bodies--hundreds and hundreds of bodies--lying barely clothed on the ground. Then comes the voice of a soldier who was there on Liberation Day: "We had come upon these people--sick, dying, starving. You can't imagine. Things like this don't happen." </P>
<P>That is how a visit to the U.S. Holocaust Memorial Museum in Washington, D.C., begins. </P>
<P>Washington is filled with well-funded museums with nothing to say--when they aren't actively distorting history. But the Holocaust museum to document the Nazis' systematic murder of 6 million Jews and millions of others during the Second World War is very different.</P>
<P>The structure of the museum itself is designed to give visitors a sense of what it would have felt like to be a victim of the "Final Solution." The exhibit space is very dark, and hallways are narrow and winding, so that you feel herded through the exhibit. </P>
<P>The recreation is so intense that curators had to make changes. For instance, planks once led you through an original rail car that shipped Jews to the extermination camps. But the smell and cramped space was too much for many visitors, so now you can walk around it. </P>
<P>Pictures in the museum--of Nazi rallies and Holocaust victims, windows broken and books burned--are almost always life-sized. </P>
<P>Yet what makes the museum so impressive isn't only its dedication to realism. It tells the real history of the Holocaust--including political aspects that are often ignored in textbooks. </P>
<P>For example, the museum documents in detail how the Nazis' ability to carry out their extermination of Jews was possible only after they crushed communist, socialist and trade union fighters. The first of the museum's mini-documentaries on the "Nazis' Rise to Power" is a must-see. It shows how Germany's economic devastation--as well as political splits among left-wing parties--allowed Hitler to maneuver to power. </P>
<P>Considering that this museum is part of the Smithsonian network of federal museums, it's remarkable how much truth the exhibition tells about the indifference of Allied countries, especially the U.S., to the plight of Europe's Jews. </P>
<P>The most damning example is the journey of the <I>St. Louis, </I>an ocean liner packed with 900-plus Jewish passengers that was denied entry into the U.S. in 1939, despite urgent pleas to President Roosevelt. The ship returned to Europe, where many of its passengers were ultimately gassed. </P>
<P>But the museum's most impressive artifact to the indifference of the Allies is a wall-sized aerial photo of the Auschwitz death camp. At first, it seems insignificant. But when you read the caption, you learn that the photo was taken from a U.S. Air Force reconnaissance plane in April 1944. </P>
<P>Jewish organizations had repeatedly called on the U.S. to bomb the train tracks leading to Auschwitz. But the U.S. said that this would divert important resources.</P>
<P>The photo exposes the stark truth--that there was film to document the camp's position and there were bombs to drop on an I.G. Farben factory less than five miles away, but nothing would be spared to stop the trains to the death camps. </P>
<P>Not surprisingly, the final exhibits contain a justification for the state of Israel. Unfortunately, the words of Holocaust survivor Elie Wiesel at the exit to the exhibit--"For the dead and the living, we must bear witness"--are lost on war criminals like Israeli Prime Minister Ariel Sharon, who exploits the horrors that Jews endured in the Holocaust to justify the horrors that he inflicts on Palestinians every day. </P>
<P>No visit to Washington is complete without a trip to the Holocaust museum. But be prepared. To get tickets, you'll have to stand in line starting at 8:30 a.m. on the day of your visit--or buy them in advance by calling 1-800-400-9373. </P>
<P>The Holocaust museum is an important and moving testament--both to the barbarism that capitalism is capable of and to how ordinary people have fought back. </P>
<I><P>For information on the Holocaust museum, visit www.ushmm.org on the Web.</I> </P>

