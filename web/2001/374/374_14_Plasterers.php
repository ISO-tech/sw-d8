<P><font face="Times New Roman, Times, serif" size="5"><b>Striking plasterers speak out</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 14</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ALMOST 500 members of Plasterers Union Local 66 won a three-week strike against Bay Area construction firms in July, settling for a $9 an hour raise over four years. Striking plasterers Greg Ortiz, Alejandro Garcia, Mike Nickerson and Ian Burns spoke with <I>Socialist Worker's </I>Todd Chretien just before the ratification vote. </P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>WHAT WAS the key issue in the strike? </P>
<P>Alex: </B>Basically wages. </P>
<P><B>Ian: </B>Our last contract was a three-year deal with a dollar and some change raise every year. But like most working people, we've not even been keeping up with inflation. We looked at the contractors, who've made more in the past five years than in the past 20, and decided it was time to start making up what we've lost. </P>
<P><B>Mike: </B>You look at the last year in terms of the drastic rent and PG&amp;E electricity hikes, and our wages haven't gone up like that.</P>
<P><B>Alex: </B>The tapers started the whole thing when they got a $7.50 raise over three years. It started to snowball, and it's going to continue. That's what the contractors are afraid of.</P>

<P><B>HOW DID the strike affect the union? </P>
<P>Ian: </B>We got the best contract yet negotiated in the trades. More members are participating in the union than ever before.</P>
<P><B>Greg: </B>I'm one of those guys who never went to meetings, and now I see how important it is.</P>

<P><B>WHAT SUPPORT did you get from other unions? </P>
<P>Greg: </B>The majority of the other trades supported us and helped our cause, although there were a few bad apples.</P>
<P><B>Alex: </B>I was at the Sun Microsystems construction site. All the other trades there were with us, and most of them respected our picket line. </P>
<P>On the other hand, we had carpenters and laborers going in to work for DPR, a contractor we were striking. Their business agent should have gone and talked to them and explained that we're supposed to be a union, all together. In a year or two when their contract comes up, they're going to expect help from us. I for one will give it to them. If the picket signs go up, I'll walk out, but it really sucks that they crossed.</P>
<P><B>Mike: </B>The UPS drivers were great. They'd see our picket signs and just turn around. You didn't even have to say anything to them.</P>

<P><B>HOW ARE you preparing for your next contract fight? </P>
<P>Greg: </B>When we go back, we should thank the other trades, like the metal workers, the electricians and the plumbers who supported us.</P>
<P><B>Ian: </B>We need to beef up our strike fund. Also, solidarity with other trades doesn't just happen, we have to build it. Over the next four years, there's going to be other strikes, and we need to honor them to a tee.</P>

