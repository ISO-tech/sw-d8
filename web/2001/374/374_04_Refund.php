<P><font face="Times New Roman, Times, serif" size="5"><b>Send your refund check to help <I>Socialist Worker</I></b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</B></I></P>
<P>It's not everyday I get a $300 check in the mail from the government. But when I got my check from the IRS today--"Tax relief for America's workers"--I was more disgusted than relieved. What help would $300 be if I lost my health insurance and got sick?</P>
<P>This tax cut is not about "relief," it's about stealing from poor and working people. Under Bush's tax cut, his family will have to pay about $100,000 less a year.</P>
<P>As I see it, that's stealing $99,700 of our collective resources, with a $300 payoff to keep me quiet. It's like a rich banker robbing the bank where I deposit my paychecks--and handing me a lollipop to keep my mouth shut.</P>
<P>No way. The mainstream media swallow every idiotic thing to come out of Bush's mouth--from "tax relief" to Star Wars: Part II.</P>
<P>That's why I'm sending my refund check to <I>Socialist Worker</I> to keep bringing the news we need to change this system and its sick priorities. And I'm telling my friends to do the same.</P>
<P><B>Michael McGregor,</B> Providence, R.I.</P>

