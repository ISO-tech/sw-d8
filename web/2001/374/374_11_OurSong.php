<P><font face="Times New Roman, Times, serif" size="5"><b>Real lives during a hot summer in Brooklyn</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2"><B>MOVIES: <I>Our Song, </B></I>written and directed by Jim McKay, starring Kerry Washington, Anna Simpson and Melissa Martinez.</font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by HADAS THIER and MENEEJEH MORADIAN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A summer full of movies about robots, apes and video games, it's refreshing to see a film that reflects real life. </P>
<P>Director Jim McKay's <I>Our Song </I>follows three teenage girls--Lanisha (Kerry Washington), Maria (Melissa Martinez) and Joyceln (Anna Simpson)--during a hot summer in the Crown Heights neighborhood of Brooklyn. The film shows the girls' everyday lives as they balance practices for the Labor Day parade--where they'll march as part of the (real life) Jackie Robinson Steppers Band--with part-time jobs, family life and having fun.</P>
<I><P>Our Song </I>shows how everything about these young women's lives pushes them down. When Maria finds out that she is pregnant, Lanisha takes her to a clinic, and the counselor asks Maria what she wants to do. All that she can think to say is that she'll take care of her baby. "But what do you want to do with your life?" the counselor asks. "Get a job at a store, I guess," Maria says.</P>
<P>Meanwhile, as the summer draws to a close, Lanisha, Maria and Joyceln find out that their school will close because of asbestos--and that they'll be split up next year. The reactions of the girls and their parents show the shock and fear that an institution that is supposed to be about education is actually poisoning them. </P>
<P>There are no easy answers or tidy Hollywood endings. But despite heartbreaking situations, the compassion of the characters shines through--within families, between the band leader and steppers and among the three friends. </P>
<P>Such an honest movie about people's lives is a rare find. But rarer still is a movie in which young Black and Latina teenagers are the main characters. That makes <I>Our Song </I>a must-see.</P>

