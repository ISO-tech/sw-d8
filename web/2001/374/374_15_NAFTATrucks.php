<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should the U.S. ban "NAFTA trucks"?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LEE SUSTAR</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">TRENT LOTT is a Mexican truck driver's best friend--or so the Senate minority leader would have you believe. </P>
<P>The right-wing, race-baiting Mississippi senator last month denounced Senate Democrats for "anti-Hispanic" attitudes because of their opposition to "NAFTA trucks"--vehicles to be driven by Mexican drivers beyond the current 20-mile limit near the border.</P>
<P>"It bothers me that there's an anti-Mexican, anti-Hispanic, anti-NAFTA attitude among Democrats that says, 'We really don't want to allow Mexican trucks to come into this country,'" Lott said in opposing a Senate measure that would maintain restrictions on Mexican trucks on the grounds of safety. </P>
<P>Of course Lott, who never met a union he didn't hate, doesn't really care about truck drivers in Mexico or the U.S. He's simply fronting for George W. Bush and employers who want to take advantage of a low-wage pool of Mexican truck drivers. </P>
<P>The International Brotherhood of Teamsters is leading organized labor's opposition to this move. "Some claim the only reason the Teamsters care about Mexican trucks is our fear that lower-wage Mexican drivers will take our jobs away," Teamsters President James Hoffa said in testimony before the Senate. "However, we welcome an opportunity to talk to our Mexican brothers and sisters about better wages and working conditions. Ultimately, if there is parity in wages and benefits, as there is with Canadian drivers, then there is no incentive for employers to violate our labor laws." </P>
<P>The issue, according to Hoffa, is highway safety. But the truth is that the Teamsters' campaign against NAFTA trucks has played into the racist and anti-immigrant stereotypes that unions used against "foreign" workers decades ago.</P>
<P>"Our nation has surrendered control over access to U.S. highways to an outside panel that includes unelected representatives of foreign governments," Hoffa said earlier this year. "We must protect our citizens from those whose interests are not necessarily our own."</P>
<P>But if the Teamsters' real concern is truck safety, they should start with the U.S. A widely reported study that showed that nearly half of Mexican trucks failed safety inspection in 1997 also revealed that U.S. trucks had a 25 percent failure rate. </P>
<P>In fact, because U.S. truck drivers aren't covered by the Fair Labor Standards Act, employers can force them to drive for 10 consecutive hours without a break and 70 hours over an eight-day period.</P>
<P>And those concerned that government corruption could compromise truck safety don't have to look south of the border. After all, it was the staff of Illinois Secretary of State--and now Governor--George Ryan that routinely sold truck drivers' licenses as part of an illegal political fundraising scheme. </P>
<P>It was the deregulation of the U.S. trucking industry--15 years before NAFTA--that is mainly responsible for the gutting of unionization in freight, freeing employers to violate even those laws on the books.</P>
<P>Hoffa has shown far more energy in campaigning against NAFTA trucks than he did in building support for the strike against freight operator Overnite Express after calling workers out on the picket line in late 1998--a battle that continues to this day.</P>
<P>A campaign around Mexican truck drivers based on labor solidarity would center on vigorous efforts to assist Mexican drivers' own efforts to improve their conditions and pay. At the same time, it would aggressively seek to rebuild the Teamsters in the freight industry. </P>
<P>Hoffa, however, the conservative leader of the union's old guard, is unlikely to initiate such a campaign. But as economic links between the U.S. and Mexico grow, rank-and-file Teamsters can take up the issue of cross-border organizing within the union--and work to build links with Mexican truck drivers wherever possible. </P>
<P>Such an approach can build on the AFL-CIO's call for amnesty for undocumented immigrant workers. For example, the Hotel Employees and Restaurant Employees (HERE) union, whose membership includes a large and growing number of immigrants, has tried to deepen its commitment to Mexican workers rights. </P>
<P>It's time for the Teamsters and the entire labor movement to drop the remaining opposition to immigrant labor--and fight for workers interests on both sides of the border. </P>

