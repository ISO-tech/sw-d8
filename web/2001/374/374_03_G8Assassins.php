<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">We hold them responsible for the violence in Genoa</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>G8 assassins</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE GROUP of Eight (G8) leaders spoke of the "tragedy" of the deadly police shooting of Carlo Giuliani during demonstrations in Genoa, Italy, last month. But these politicians all but pulled the trigger.</P>
<P>At the European Union summit in Gothenburg, Sweden, a month earlier, three protesters were shot with live ammunition. One of them, a 19-year-old man, nearly died. Rather than apologizing, European leaders made crude threats. "We will not tolerate thuggery as seen in Gothenburg," declared one official.</P>
<P>A few weeks later in Barcelona, Spain, cops attacked a peaceful rally against the World Bank, which had already canceled its meeting in the city.</P>
<P>This set the stage for Italian Prime Minister Silvio Berlusconi to unleash lethal force on demonstrators during a meeting of the eight most powerful countries in the world.</P>
<P>It was a decision made well before the summit--and it couldn't have happened without the approval of the other G8 leaders, led by George W. Bush.</P>
<P>British Prime Minister Tony Blair had the gall to criticize those who were "far too apologetic" toward protesters. Translation: We'll meet anywhere we damn well please, even if it takes a 13-foot steel wall, 18,000 cops and soldiers, helicopters, armored cars, anti-aircraft missiles--and a couple of bullets in a protester's head.</P>
<P>The cops---and the politicians who control them--were out to raise the stakes for those who oppose their system.</P>
<P>But what Bush, Berlusconi and Co. didn't count on was the massive outpouring of support for protesters. Far from being intimidated by Carlo's murder, more than 300,000 people took to the streets July 21 to protest the G8. And more than a quarter of a million demonstrated across Italy July 24, the day before Carlo's funeral.</P>
<P>Meanwhile, the shooting of Carlo intensified the debate in the global justice movement over tactics--particularly those of the anarchist "black bloc," which engaged in property destruction and clashes with police.</P>
<P>Some activists said the violence in Genoa was a distraction from the real issues. But we should hold the G8 responsible for the violence.</P>
<P>The leaders who ordered the crackdown in Genoa are the same ones who back governments that shoot demonstrators against International Monetary Fund policies in countries like Brazil and Papua New Guinea. The G8 chiefs who denounce "violent protesters" are the same people who rain bombs on civilians in Serbia, order massacres in Chechnya and starve Iraq. So it's no surprise when they use repression at home. The debate over tactics has to start from this reality.</P>
<P>On the other hand, some in the movement want to avoid the discussion. They argue that the violence in Genoa was carried out by police provocateurs, dressed up as "black bloc" demonstrators.</P>
<P>To be sure, there's ample evidence of Italian authorities using infiltrators, whose violence served as the excuse to crack heads. But this ignores the fact that a minority of the movement believes that individual acts of violence can spur the struggle forward.</P>
<P>In confrontations with police, this minority can impose its tactics on the movement, even when the vast majority of demonstrators disagree with them--unless our movement develops a way to democratically decide on how to organize for mass protests and carries out its decision collectively.</P>
<P>After Genoa, some people expressed pessimism that this can ever be achieved--even calling for an end to mass demonstrations. This is mistaken. Mobilizations like Genoa are a marvelous way to demonstrate the scale of opposition to the system. The larger the protests, the harder it is for politicians and their media apologists to claim legitimacy for their system.</P>
<P>But the crackdown in Genoa did pose the question of how the movement should deal with repression--and ultimately, how to mobilize the social forces that can challenge the system itself.</P>
<P>The task now is to link the global justice movement that brought 300,000 onto the streets of Genoa with struggles in factories, offices, communities and campuses, both in the U.S. and around the world.</P>
<P>The protests in Genoa are an inspiration to everyone who wants to build a better world. And they showed the potential to build a movement that can do just that.</P>
