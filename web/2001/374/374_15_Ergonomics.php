<P><font face="Times New Roman, Times, serif" size="5"><b>Union members tell Bush: Ergonomics standards now!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 15</font></p>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--About 150 union members and supporters rallied July 20 outside hearings on ergonomics standards sponsored by the Bush administration. </P>
<P>The hearings, held at the University of Chicago, were one of three such meetings held around the U.S. But the hearings are only a smokescreen. </P>
<P>In March, Bush scrapped the ergonomics standard established by the Clinton administration. Even before the hearings began, Secretary of Labor Elaine Chao announced a one-year delay in establishing a definition of musculoskeletal disorders in order to conduct more research--even though there has already been a decade of such studies. Occupational Health and Safety Administration statistics show that more than half a million such injuries occur each year. </P>
<P>The judge and panel of management-dominated "experts" didn't even bother to hide their contempt for the union members and officials who testified about workplace injuries.</P>
<P> Sarah Amos, International Executive Vice President of the United Food and Commercial Workers (UFCW), challenged the panel to sample the repetitive strain common in meatpacking plants. </P>
<P>"With a jerking motion, thrust that pen or pencil towards you as if you were cutting something that offered a good deal of resistance," she said. "Make that exact same motion 60 times a minute for just an hour. That's just one-eighth of a workday. See what happens to your wrist, your hand, your elbow, your shoulder. I challenge anybody in this room who doubts the reality of ergonomic injuries--to do what I just described and do it just for the time that this panel testifies. You will never deny reality again."</P>
<P>Squirming in their chairs, the management panel cut off Amos five minutes before her speaking time was up. After Amos spoke, union members marched outside for a rally addressed by Chicago Federation of Labor President Don Turner and Rev. Jesse Jackson. </P>
<P>"I came down here to give a voice to the injured health care workers," said Maggie Flanagan, a nurse in Anchorage, Alaska, and a longtime activist for public health. "We are there if patients need us," Flanagan told <I>Socialist Worker. </I>"We are put in the impossible situation of taking care of patients without even taking a break."</P>

