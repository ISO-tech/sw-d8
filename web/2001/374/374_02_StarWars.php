<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA'S GANG</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The truth about the Star Wars scam</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Every test has been rigged so far"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration was crowing last month after a successful test of the national missile defense scam that they've been pushing since taking over the White House. It was one of the first times that any part of the so-called "Star Wars" system--which is supposed to shoot down missiles fired at the U.S.--worked.</P>
<P>Defense Secretary Donald Rumsfeld took advantage of the fawning media reaction to press for a speedup in the program. Of course, that means a speedup in how quickly billions of Pentagon dollars flow into the pockets of his defense industry pals.</P>
<P>But no one should believe the hype about Star Wars.</P>
<P>THEODORE POSTOL is a Massachusetts Institute of Technology professor, scientific adviser to the Pentagon--and a determined opponent of missile defense. He told MATT NICHTER why last month's Star Wars success was a scam.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>WERE YOU impressed by the success of the latest interceptor test?</b> </P>
<P>THIS IS the fourth time they've performed this particular test. Every one has been rigged, and in exactly the same way. They even conducted all of the tests at exactly the same time of day, to make sure the sun was oriented just so.</P>
<P>The [anti-missile] kill vehicle was pre-programmed to know it would encounter exactly two smears of light, one of which was cone-shaped--the warhead--and the other a round balloon decoy 10 times as bright. The general trajectory of each was also pre-determined. </P>
<P>So the fact that they can hit a target like this doesn't mean a whole lot. If an adversary wanted to evade a system like this, it would be quite easy. They could just launch a larger number of decoys, and they could mask the warhead to look like the decoys. </P>
<P>[National Security Adviser] Condi Rice is an old colleague of mine from Stanford. She's smart enough to know this thing can't work. But most of these people have no clue about the basic laws of physics. When I tell them about all the technical and logistical problems, they say, "We'll figure it out." As if technology can magically get around the laws of nature.</P>

<P><b>BUSH IS insisting that he wants to scrap the 1972 Anti-Ballistic Missile Treaty to pursue Star Wars. What will that mean for arms control?</b></P>
<P>I'M PRETTY amazed by this administration. They've already proved even less capable of thinking rationally about missile defense than the Clinton administration. That's not easy.</P>
<P>The deployment of this system will put pressure on China to expand its nuclear arsenal. That would push India to do the same, and Pakistan as well. The Russians, who for budgetary reasons would prefer to reduce their nuclear arsenal, will feel pressure to keep aging warheads online, which is dangerous from an environmental standpoint. And they'll sell weapons technology to China.</P>
<P>The president's attitude seems to be that treaties like this unfairly constrain us. It's the same reckless attitude that he took toward the Kyoto protocol on global warming.</P>
<P>Bush is sacrificing a historic treaty and jeopardizing everyone's security for the sake of a system with less than one iota of a chance of ever working.</P>

