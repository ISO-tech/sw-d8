<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDONESIA</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Megawati takes over as president</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Wahid pushed out in Indonesia</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by JOHN GREEN and TODD CHRETIEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2001 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THREE DAYS after he was impeached by Indonesia's parliament, Abdurrahman Wahid abandoned attempts to hold onto the presidential palace and boarded a jet for the U.S.</P>
<P>On July 23, lawmakers impeached Wahid on charges of corruption and incompetence--and elected Vice President Megawati Sukarnoputri to take his place.</P>
<P>Hours before the vote, Wahid declared a state of emergency and tried to dissolve parliament in a last-ditch effort to hold on to power. But the Jakarta police chief refused to follow the order, and Indonesia's powerful military opposed it. Wahid's government fell apart before his eyes. His cabinet secretary resigned in protest, and his security minister tried to do the same.</P>
<P>Now Megawati is president. She is the daughter of Indonesia's founding president Sukarno, who was driven from power by the U.S.-backed dictator Suharto in a 1965 coup. As many as 1 million leftists and ethnic minorities were slaughtered in the aftermath of the coup.</P>
<P>Megawati played the role of respectable opposition during the final years of Suharto's reign--and was popularly identified with the 1998 revolution that brought down the dictator. In Indonesia's first elections for parliament--which chooses the president--in 1999, Megawati's Democratic Party of Struggle got 35 percent of the vote, more than any other party.</P>
<P>But a coalition of Wahid's Islamist supporters, the former ruling party Golkar and the military, which is guaranteed representatives in parliament, banded together to back Wahid.</P>
<P>No supporter of democracy should shed a tear for Wahid. But his downfall doesn't represent a shift to the left.</P>
<P>In fact, Megawati's election as president was based on deals with the same reactionary forces that put Wahid in office. If anything, Megawati is more beholden to Indonesia's generals, who are itching to crack down on left-wing students and unionists and independence demands in provinces like Aceh and Irian Jaya.</P>
<P>In 1999, Megawati railed against independence for East Timor and supported the military's campaign of slaughter following a referendum in favor of breaking away. She also supports the economic austerity plans insisted on by the International Monetary Fund and U.S. banks. Maybe that's why the Bush administration publicly backed her even before Wahid had given up.</P>
<P>This latest round of infighting among the rich and powerful won't improve the desperate economic situation facing ordinary Indonesians. That's why there were virtually no street mobilizations to support or oppose Wahid's impeachment.</P>
<P>The real danger now is that Megawati, the supposed "democrat," will team up with the military to tighten the crackdown against forces that could upset the elite and its maneuvers.</P>
<P>Workers and students can't count on the sham democracy in parliament, where no party represents their interests. They will have to build a real fightback from the bottom up.</P>

