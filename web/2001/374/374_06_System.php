<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bonehead and Geldof</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 3, 2001 | Page 6</font></p>

<P><font face="Times New Roman, Times, serif" size="3">AS PROTESTERS massed on the streets to demonstrate against G8 leaders in Genoa, rock stars Bob Geldof and Bono had better things to do. They were hobnobbing with the heads of state that hundreds of thousands had gathered to oppose.</P>
<P>"The spectacle of Bob Geldof and Bono bear-hugging G8 leaders in Genoa on Saturday was revolting," wrote Vincent Browne in the <I>Irish Times</I>.</P>
<P>"It was not just the manic presumption that they would have an iota of influence, or the phoniness and the crass attention-seeking of the exhibition that was stomach-churning. It was their giddy association with the rulers of the world and their eloquent dissociation from the tens of thousands who had gathered to protest against the unfairness and inequities of the new world order."</P>
<P>Though Geldof and Bono came to Genoa as self-professed emissaries of justice, in the end they managed to do little more than lend their credibility--or whatever is left of it--to the very people who oversee and justify the inequalities that characterize our world.</P>
<P>--<I>Irish Times,</I> July 25, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Cellular stealth-killers</font></P>
<P><font face="Times New Roman, Times, serif" size="3">THE PENTAGON justifies the $2 billion price tag of each of its stealth aircraft by pointing to their ability to elude enemy radar. But a new invention by a British scientist could turn mobile phones into the perfect stealth aircraft locators--rendering the billion-dollar planes useless.</P>
<P>The Roke Manor scientists discovered that telephone calls sent between mobile phone masts detected the precise position of stealth aircraft with great ease.</P>
<P>"We use just the normal phone calls that are flying about in the ether," said Peter Lloyd, head of projects at Roke Manor Research at Romsey in Hampshire. "The front of the stealth plane can't be detected by conventional radar, but its bottom surface reflects well."</P>
<P>Mobile telephone calls bouncing between base stations produce a screen of radiation. When the aircraft fly through this screen they disrupt the phase pattern of the signals.</P>
<P>The Roke Manor system uses receivers, shaped like TV aerials, to detect distortions in the signals. A network of aerials large enough to cover a battlefield can be packed into a truck.</P>
<P>"It's remarkable that a stealth system that cost $60 billion to develop is beaten by 100,000 mobile phones," Lloyd said. A rough version of a similar system might have been used in Serbia to shoot down an American F-117 stealth fighter near Belgrade during the Kosovo campaign.</P>
<P>--<I>Telegraph</I> (London), June 12, 2001<br>
<br>
<p><font face="Times New Roman, Times, serif" size="5">Your death may be good for your country</font></P>
<P><font face="Times New Roman, Times, serif" size="3">TOBACCO GIANT Philip Morris has a proposal for how Czech citizens can contribute to their nation's economy. Smoke themselves to death.</P>
<P>That's the conclusion of a Philip Morris report, which found that the Czech Republic saved $147 million in 1997 through the premature deaths of smokers who wouldn't live to use health care or housing for the elderly.</P>
<P>"I think it's pretty egregious," said Richard Daynard of the Tobacco Products Liability Project. "You don't see other companies doing it...This is not the normal way we think about the lives of citizens."</P>
<P>Philip Morris defends itself by saying that the study "was part of an ongoing debate about the economics of cigarette excise tax policy in the Czech Republic." But, said Patti Lynn from corporate watchdog Infact, "even if it were true...it's a scary logic."</P>
<P>In the past, tobacco companies have used the same arguments fighting lawsuits from states demanding financial compensation for costs associated with treating smoking-related diseases.</P>
<P>"No other company in the world would claim that killing its customers is good for society," said anti-smoking advocate Joe Cherner.</P>
<P>--BBC Online, July 17, 2001<br>
