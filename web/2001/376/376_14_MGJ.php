<P><font face="Times New Roman, Times, serif" size="4">Activists prepare for protests against IMF/World Bank</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mobilize for global justice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by GEOFF BAILEY</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--Student organizers from six area campuses met to coordinate action in the run-up to the protests against the International Monetary Fund (IMF) and World Bank meetings in late September in Washington, D.C.</P>
<P>They met to plan a touring teach-in during the month of September to help build for the 50 Years is Enough Caravan which will arrive in Boston on September 15-16. Students also discussed ways to connect opposition to the IMF/World Bank with local union struggles.</P>
<P>The New England Labor Committee for Global Justice, a union coalition formed to mobilize union members for the protests against the Free Trade Area of the Americas in Quebec last April will be making arrangements for union members to travel to Washington.</P>
<P>Jeff Crosby, president of the North Shore Labor Council and IUE/CWA Local 201, will be travelling to Washington, where he was hired by the AFL-CIO as a full-time organizer for the protests.</P>
<P>A citywide planning meeting is going to be held in early September to help coordinate plans between a number of local coalitions involved in the Washington mobilization.</P>
<P>The meeting will include representatives from the Boston Global Action Network/BankBusters, Jobs with Justice, Latin American Action Coalition, Immigrant Amnesty Coalition, the New England Labor Committee for Global Justice and the Massachusetts AFL-CIO.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>Activists in New York have begun to organize and mobilize for the upcoming protests against the IMF and World Bank in Washington, D.C., focusing on public education and outreach through a series of teach-ins from September 11 through 14.</P>
<P>The Committee for Global Justice evolved out of the NYC Coalition Against the FTAA (Free Trade Area of the Americas) that mobilized for the protests in Quebec this past spring.</P>
<P>The events are planned to take place at a number of sites around the city, including Columbia University, NYU, CUNY and the National Action Network. The teach-ins are coordinated with the national tour initiated by a coalition of groups--which include 50 Years is Enough, Essential Action, Global Exchange and Jobs with Justice--that are mobilizing buses for the demonstrations in late September.</P>
<P><i>The Committee for Global Justice meets every Tuesday at 7 p.m. at the Urban Justice Center, 666 Broadway (between Bleecker and Bond), 5th floor. Subscribe to the listerv at nycftaa-subscribe@topica.com or call Dao at 212-610-5171 for more information.</i></P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>In the Northeast, students groups at several campuses that mobilized for antiglobalization protests in the past, including UMass-Amherst and Yale, are getting ready to protest.</P>
<P>Students at several campuses, including the University of Vermont, Brown and South Connecticut University, will kick off new Students for Global Justice groups with teach-ins planned for the coming weeks.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>Anti-IMF protesters are also preparing in the South. In Winston-Salem, North Carolina, activists are launching a statewide mobilization to D.C. And in Atlanta, activists held a teach-in that drew about 30 people.</P>

<P><i>Dao Tran contributed to this report.</i></P>

