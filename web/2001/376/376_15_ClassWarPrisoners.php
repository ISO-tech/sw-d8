<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Class-war prisoners--Haymarket to today</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN SOUTH Carolina, five longshoremen--members of ILA locals 1422 and 1771--are under house arrest, awaiting trial for allegedly causing a riot on January 20, 2000. In reality, 600 riot cops, with dogs, snipers, helicopters and armored vehicles, attacked union dockworkers picketing a nonunion shipping company.</P>
<P>The union members--and the local--are being targeted by politicians and employers in South Carolina to keep the state a low-wage, nonunion haven for global corporations. They cannot allow a powerful, relatively well-paid, mostly African American union to set an example for other workers.</P>
<P>This kind of legal repression--or criminalization of dissent--is not new. The history of class struggle in the U.S. is, in part, a history of trumped-up legal cases designed to go after the labor movement. To list just a few: the Haymarket tragedy, the Mooney-Billings case, Joe Hill's murder trial, the Moyer-Haywood-Pettibone case, Sacco and Vanzetti, and so on.</P>
<P>The most famous is the tragic 1886 Haymarket affair in Chicago. On May 1, 1886, 340,000 workers were involved in strikes and protests across the country for a shorter workday. Chicago was the hub--65,000 workers struck there.</P>
<P>The employers counterrattacked. On May 3, 200 police attacked a group of strikers at the McCormick Reaper works, killing four.</P>
<P>In protest, 3,000 workers gathered in Haymarket square the following day. As the protest was breaking up, someone, a police agent or an angry individual, tossed a bomb into the police ranks.</P>
<P>Police then went on another rampage, killing several workers and injuring hundreds. Four working-class radicals--August Spies, Albert Parsons, Adolph Fischer and Louis Engel--were tried and executed for their alleged involvement in the bombing, even though there was no evidence connecting them to it.</P>
<P>But that didn't matter to the employers, politicians and their press. As leaders in the labor movement, anarchists and socialists all, an example had to be made of them.</P>
<P>August Spies' last words still ring powerfully today: "If you think that by hanging us you can stamp out the labor movement...then hang us! Here you will tread upon a spark, but there and there, behind you and in front of you and everywhere, flames blaze up. It is a subterranean fire. You cannot put it out."</P>
<P>Though less known today, the Tom Mooney case is instructive. Mooney was a militant trade unionist and socialist in San Francisco.</P>
<P>When he set out to organize the streetcar workers, the employers set out "to neutralize him," according to a <I>New York Times</I> report.</P>
<P>Mooney, his associate Warren K. Billings, and three other trade unionists were arrested after a bomb killed 10 people at a pro-war "preparedness" parade in San Francisco on May 22, 1916.</P>
<P>Immediately after the bombing, the local employers' Law and Order Committee immediately began circulating a petition to outlaw picketing. Wrote left-wing cartoonist Robert Minor, "The prisoners are in the hands of men who consider labor unionism itself a crime. They are now proving this by making peaceful picketing a prison offense."</P>
<P>The case against the five was based entirely upon what was revealed to be perjured testimony by so-called "eyewitnesses." As Mooney pointed out, he and others were being victimized for being "some of the most aggressive fighters in labor's cause," not for committing a crime.</P>
<P>The campaign was part of an effort, in the words of one sympathetic investigator, "to crush trade unionism in the Pacific Coast metropolis."</P>
<P>After Mooney was sentenced to death, a worldwide campaign of demonstrations and petitioning--as well as the threat of hundreds of protest meetings and a national strike in the U.S.--prompted President Wilson to intervene in the case.</P>
<P>Mooney's execution was indefinitely stayed, twice, commuted to life in prison in 1918, and after years of protest he was eventually pardoned in 1939.</P>
<P>Today, the Charleston Five case has some parallels to the Mooney-Billings case. And like that case, it must be taken up by the whole labor movement.</P>

