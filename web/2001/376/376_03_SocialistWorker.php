<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The new weekly <I>Socialist Worker</i></b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P><i>SOCIALIST WORKER</I> is going weekly. Starting in October, we'll give you everything you've come to expect from <I>SW</I>--twice as often. This is an important and exciting step for us.</P>
<P>There's certainly plenty to write about. Every two weeks, we cram the 16 pages of <I>SW </I>to the bursting point--and still have to leave out stories that need to be told and discussions that need to be had.</P>
<P>With the mainstream press as servile as ever to the corporate and political elite, the need for a paper like <I>Socialist Worker</I> is as pressing as ever. But there's more to the change that we're making.</P>
<P>Over the past several years, we've seen the birth of a new left--a growing layer of people who think of themselves as activists fighting for social change. Their struggles are on many fronts--from the global justice movement battling corporate globalization, to environmentalists who want to stop the destruction of our world, from a new civil rights movement fighting racism, to union activists taking on the employers' anti-labor attacks.</P>
<P>With the rise of this new left has come a flowering of publications--books, pamphlets, magazines, newspapers--to report the news of the movement and debate our strategy and tactics. We're proud of the role that <I>SW </I>has played in promoting these discussions.</P>
<P>And with a weekly paper, we can respond that much more quickly--with the immediacy and urgency that the struggle demands. Our movement needs a socialist voice--and we want <I>SW</I> to meet the challenge.</P>
<P>But to do this, we need the help of our readers. We need reports from all the struggles that take place--in workplaces, in communities, on campuses and in every corner of this society. We need contributions that take up the questions facing our movement and debate the way forward.</P>
<P>Papers like <I>SW</I> rely on their readers--what they experience, what they think and what they plan to do about it. So in the coming weeks, you'll find <I>SW</I> sellers telling people about our plans for going weekly--and, of course, asking for the financial contributions to make it possible.</P>
<P>We hope you'll help out. And we hope you'll join in the valuable discussion that takes place in these pages. After all, <I>Socialist Worker</I> is your paper--and in October, it'll be your paper twice as much as before.</P>

