<FONT FACE="Courier New" SIZE=2><P><font face="Times New Roman, Times, serif" size="4">Keep the Dream Alive rally in NYC:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We're going to mobilize"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ARIELLA COHEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Some 2,000 people gathered outside United Nations (UN) headquarters August 25 at a demonstration for racial and economic justice across the world.</P>
<P>The rally was called by the National Action Network (NAN) to take up a wide range of issues, including police brutality and racial profiling, poverty, crumbling schools and more.</P>
<P>One prominent demand was the call for the U.S. Navy to stop using the Puerto Rican island of Vieques as a bombing range. NAN leader Rev. Al Sharpton recently finished a 90-day jail sentence for his participation in a civil disobedience protest in Vieques.</P>
<P>"Why go to Vieques?" said Sharpton. "We go to protest the environmental effects on the people of Vieques, which are well documented. But even more importantly, we go because the same system that put profits over human need in Vieques, is the same system that shoots an unarmed man in New York 41 times, is the same system that uses the death penalty to murder innocent people. We're going to mobilize and organize." </P>
<P>Sharpton's words underlined one of the most exciting aspects of the demonstration--the fact that it drew together people concerned with a wide range of issues. Among the multiracial ranks of the marchers were Palestinian rights supporters, groups calling for slavery reparations, opponents of the death penalty and global justice activists.</P>

