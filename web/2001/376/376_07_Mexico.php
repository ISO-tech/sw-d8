<P><font face="Arial, Helvetica, sans-serif" size="2"><b>MEXICO</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Volkswagen workers strike for wage hike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by TODD CHRETIEN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 12,500 workers at Volkswagen's crucial assembly plant in Puebla, Mexico, went on strike August 18 to demand better wages and working conditions.</P>
<P>As <I>Socialist Worker</I> went to press, the Sindicato Independiente de Trabajadores de la Industria Automotriz Volkswagen (Independent Union of Volkswagen Autoworkers) was demanding a 19 percent raise to make up for years of wage cuts. Management was offering only 5.5 percent, which is well below Mexico's inflation rate.</P>
<P>The factory is one of the biggest auto factories in the world, producing cars for Mexico as well as the popular new Beetles for the U.S. Well over 100,000 workers in Puebla--a city of about 2 million--work in industries supplying Volkswagen with parts, services or transportation, making it by far the most important industry in the area.</P>
<P>The battle at Volkswagen is being closely watched in Mexico because it comes as the economy is slumping. More than 500,000 workers have been laid off since January, and industrial production has fallen for five straight months due to the slowdown in the U.S., the destination for 85 percent of Mexico's exports.</P>
<P>"This strike will determine the wages policy for the whole country," said one union leader. Unions across Mexico have pledged to organize solidarity protests, and Volkswagen workers in Brazil have threatened to go out on a sympathy strike if the workers in Puebla don't get a fair contract.</P>

