<P><font face="Times New Roman, Times, serif" size="4">Anti-Star Wars activists face felony charges</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A crackdown on protesters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BILL NEAL</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--More than a dozen people arrested at Vandenberg Air Force Base in California are facing a trial on felony charges of conspiracy.</P>
<P>But the defendants aren't military personnel accused of spying or the generals who are threatening a new arms race. The federal government is going after nonviolent protesters who want to stop George W. Bush's Star Wars madness.</P>
<P>Fifteen Greenpeace activists and two journalists were arrested in July after making a boat landing at the Vandenberg base to disrupt a test of the military's national missile defense scheme.</P>
<P>They were hauled before a federal judge in August and officially charged with felonies that could land them in jail for up to six years.</P>
<P>The felony prosecutions are part of a growing crackdown on civil disobedience protests. For example, 29 people who crossed onto Vandenberg in May in another anti-Star Wars protest were given long probation periods for misdemeanor trespass charges.</P>
<P>When two defendants rejected the lengthy probation, they were ordered held behind bars for six months. "I think they're threatened by the notion of democratic scrutiny of a flawed program that's going to spark a new arms race, and that's why they're coming down on us like a ton of bricks," said Nic Clyde, a leader of Greenpeace in Australia.</P>
<P>Vandenberg is being targeted for protests because it's the main test site for the missile defense system. "The actions are necessary to prevent a greater evil," Scott Galindez, of the Vandenberg Action Network, told <I>Socialist Worker</I>.</P>
<P>"If a nuclear war started, missiles would hit another continent in 30 minutes. There isn't time to protest after a launch. The danger is always imminent."</P>
<P>Activists believe that Star Wars is certain fuel a new arms race--and that opponents of Star Wars will have to step up protests to stop them. "It's clear letter writing and lobbying aren't going to affect Bush on this issue," Galindez said.</P>
<P>"Even foreign leaders are telling him they're opposed, and he doesn't care. Like the protests against the U.S. Navy testing in Vieques in Puerto Rico, it's going to take lots and lots of people risking arrest. People hear about Rev. Al Sharpton and a Kennedy getting arrested over Vieques, but 65 others were arrested at the same time--and hundreds of others over a long period. It will take that kind of commitment to even budge the Bush administration."</P>
<P>The Feds understand this--which is why they're pushing for harsh sentences. But activists say that they're ready to match the government's resolve.</P>
<P>"Bush is leading us right down the wrong road," said John Aguilar, a former Marine and one of the 15 Greenpeace defendants. "I don't feel left with an option but to protest."</P>

