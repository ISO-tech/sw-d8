<P><font face="Times New Roman, Times, serif" size="4">The raving bigot from North Carolina retires</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Good riddance!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LANCE SELFA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--The only better outcome would be his timely death. In August, Sen. Jesse Helms (R-N.C.) announced that he'll retire when his term ends in 2003.</P>
<P>Like that other Senate dinosaur, South Carolina's Strom Thurmond, Helms is a throwback to the segregationist politics of the Jim Crow South.</P>
<P>He got his start in politics in 1950, when he managed the Senate campaign of segregationist Willis Smith. Helms personally doctored a photo to show the wife of Smith's opponent's dancing with a Black man.</P>
<P>Throughout the 1960s, Helms appeared as a TV commentator on Raleigh station WRAL, regularly denouncing Martin Luther King, "Negro hoodlums" and "welfare bums."</P>
<P>A U.S. senator since 1972, Helms often stood alone to block legislation or appointments because of one right-wing peeve or another. He forced cuts in federal arts funding by objecting that National Endowment for the Arts projects furthered "the homosexual agenda."</P>
<P>He regularly denounced foreign aid to poor countries as good money thrown down "foreign rat holes."</P>
<P>But he never met a military boondoggle or right-wing dictator he didn't like. He once complimented Salvadoran death squad leader Roberto D'Aubuisson--a Hitler lover and organizer of the 1980 murder of Archbishop Oscar Romero--as "a free enterprise man and deeply religious."</P>
<P>But the core of Helms' politics remained vicious racism. In his 1990 Senate campaign against Black Democrat Harvey Gantt, he used an anti-affirmative action TV ad showing white hands tearing up an employment application--because the "job had to go to a minority."</P>
<P>In 1993, when Sen. Carol Moseley-Braun (D-Ill.)--the only Black woman to serve in the U.S. Senate--opposed a federal charter for the Daughters of the Confederacy, Helms said he would whistle the Confederate anthem "Dixie" to her "until she cries."</P>
<P>After his retirement announcement, members of the political establishment--from President Bush to Sen. Joe Biden (D-Del.)--lined up to praise Helms. That these people could have anything positive to say about Helms is an indictment of them.</P>
<P>A C-Span viewer said it best in an e-mail to the network: "I have only two words to sum up Jesse Helms' career. Good riddance."</P>

