<P><font face="Times New Roman, Times, serif" size="5"><b>Activists block Italian ship to support jailed G8 protesters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by SUE SANDLIN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OAKLAND, Calif.--Earlier this month, Bay Area global justice activists delayed the unloading of an Italian ship to protest police violence against protesters at the July G8 protests in Genoa.</P>
<P>Bay Area activists got the ideas from their counterparts in Portland, Ore., who managed to delay the ship for several hours on August 15. The Portland protesters set up a picket line that International Longshore and Warehouse Union (ILWU) members refused to cross.</P>
<P>An arbitrator called on the scene then ruled that the police--called out against the protesters--constituted a "health and safety risk" to the longshore workers.</P>
<P>Activists hoped with this action to put pressure on the Italian government to release the remaining G8 political prisoners and to drop all charges against them.</P>
<P>The ship that was targeted, the <I>Cielo di San Francisco,</I> is managed by a company called Italia Di Navigazion. The company was privatized in 1998, and the owners have close ties to Silvio Berlusconi's right-wing government, which directed the police actions in Genoa.</P>
<P>After receiving news that the <I>Cielo di San Francisco</I> was headed for the Port of Oakland, activists hastily organized a picket for August 17. Unfortunately, the picket had to be organized with complete anonymity, in order to avoid the kind of lawsuits filed by the Pacific Maritime Association--a shipping bosses' organization--against individual activists and their organizations after the successful <I>Neptune Jade</I> action of 1997.</P>
<P>Nevertheless, word got out through activist networks, and by 7 a.m., about 50 people were at the Howard terminal with signs saying, "Free G8 Protesters" and "No Fascist Cargo."</P>
<P>It turns out that the <I>Cielo di San Francisco</I> had been delayed, and wasn't going to be pulling into port until later that afternoon. However, it was learned that the ship in the berth that morning, the <I>Cielo di Espania,</I> was managed by the same shipping company, so the picket began at 7 a.m. as scheduled.</P>
<P>Activists organized a spirited picket line at the gate just outside port property, chanting "No justice, no peace, til G8 prisoners are released!" and blocking trucks carrying Italian cargo from leaving the port.</P>
<P>The real showdown came later that afternoon as the <I>Cielo di San Francisco</I> pulled into the berth. The numbers on the picket line began to swell. This time, after advice from some veterans of earlier labor struggles, about 20 protesters crossed onto port property and set up a picket line at an inner gate.</P>
<P>Hoping for another favorable arbitration ruling of a health and safety threat, picketers beat sticks on the ground and chanted, "Nothing in, nothing out, that's what a picket line's all about. We don't only scream and shout, try to cross and you'll find out!"</P>
<P>Picketers managed to hold the inside gate for about four hours before being threatened with arrest. The picket at the port entrance continued, and at about 10:30 p.m. longshore workers announced that the arbitrator had ruled that the picket constituted a health and safety threat, and that their shift was being sent home!</P>
<P>Dockworkers were very supportive of the action, and were paid for the hours that they were standing by. West Coast longshore unions have a radical tradition--from refusing to unload fascist cargo and leading a general strike in the 1930s, to shutting down the ports in support of Mumia Abu-Jamal, to the anti-WTO demonstrations in Seattle, to their pledge to shut down the ports in support of the Charleston Five later this year.</P>
<P>In all, activists were able to delay vessels managed by Italia Di Navigazion for about 24 hours. The ship also met a small picket at the Port of Long Beach in Los Angeles. It's estimated that the action cost the shipping line about $60,000 per hour.</P>
<P>Labor and global justice activists on the West Coast once again demonstrated the union slogan, "An injury to one is an injury to all!"</P>

