<font face="Times New Roman, Times, serif" size="5">A judge who has no judgment</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TENNESSEE JUDGE Walter Williams doesn't like back talk. When a defense lawyer in his Chattanooga courtroom objected that Williams had ordered her client to pay four times the maximum fine for speeding, Williams sent the lawyer to jail for 10 days for contempt of court.</P>
<P>Another judge freed the lawyer, and a third ruled that Williams abused his powers. So Williams...issued a warrant to have the lawyer rearrested.</P>
<P>By the time he appeared before a special panel, Williams had racked up 24 charges of misconduct. But Williams cut a deal, and all but four of the complaints--which included charges of bias and unprofessional and demeaning behavior--were dropped.</P>
<P>In return, Williams agreed to go to "judge school"--a three-week course to improve his performance.</P>
<P>--<I>The Guardian,</I> July 25, 2001<br>

