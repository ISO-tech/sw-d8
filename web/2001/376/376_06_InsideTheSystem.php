<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>It's the cheesiest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PHILIP MORRIS has raised self-promotion to new heights. To film its new television ad about its relief efforts for Kosovar refugees, Philip Morris hired the swank Leo Burnett advertising firm.</P>
<P>The 60-second ad, which began running in April, is part of the cigarette maker's effort to promote its non-tobacco brands, such as Kraft cheese. A military helicopter lands, and its doors fly open to expose its cargo of huge boxes marked "Kraft."</P>
<P>"Hi, I'm Molly from Philip Morris," yells an actress flown in from Atlanta to a huge set outside Prague where a massive, fake refugee camp was built for the shoot. </P>
<P>Leo Burnett made the ad in the Czech Republic, in part because "we wanted access to plenty of extras that look like they come from that part of the world," says director John Gatti. "For the commercial's cathartic final frames, 350 extras--hand-picked for their dark olive complexions--huddled over bowls of Kraft Macaroni and Cheese prepared by a team of local assistants who had never before seen the bright-yellow noodle dish," wrote the <I>Wall Street Journal</I>.</P>
<P>Philip Morris wouldn't reveal the value of the five tons of donated food, but wholesale costs for this amount would imply a value of about $125,000--dwarfed by the more than $1 million spent to produce the ad.</P>
<P>"The idea that they've re-created a human tragedy to promote a corporate triumph strikes us as fundamentally offensive," says Raymond Offenheiser of the hunger-relief organization Oxfam. </P>
<P>--<I>Wall Street Journal,</I> July 24, 2001<br>

<br>
<font face="Times New Roman, Times, serif" size="5">N.C.'s Bible-thumping racist</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"TWO THINGS made this country great--white men and Christianity," read the e-mail. State Rep. Don Davis from North Carolina thought that this was so clever and original that he felt it necessary to forward it to every member of the state House and Senate. </P>
<P>"There's a lot of it that's truth, the way I see it," Davis said. "Who came to this country first--the white man, didn't he? That's who made this country great."</P>
<P>But other lawmakers didn't see the truth in it. "It absolutely destroys the racial harmony that we are trying to foster in this state and in this nation," said Rep. William Wainwright, who's vice chairman of the Legislative Black Caucus.</P>
<P>Rep. Ron Sutton--the only American Indian in the General Assembly--said the e-mail was totally out of place. "It just shows his white-supremacist, Gestapo mentality," Sutton said.</P>
<P>The e-mail goes on to say that the Bible was the basis for the nation's founding and early success, but that now the U.S. is in decline. "Every problem that has arrisen (sic) can be directly traced back to our departure from God's Law and the disenfranchisement of white men."</P>
<P>Davis--who sponsored legislation to post the Ten Commandments in public schools--was angered by the charges of racism. "Listen, there's nothing racist about it. And don't give me that mess," Davis said.</P>
<P>--Associated Press, August 22, 2001<br>

