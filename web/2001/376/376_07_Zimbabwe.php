<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ZIMBABWE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the uproar over arrest of white farmers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by CHRIS FAGAN</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HEALTH WORKERS were on strike in Zimbabwe throughout August in a battle over the terrible state of the country's health care system.</P>
<P>But when Zimbabwe caught the attention of the Western media, the focus was on the arrest of 21 white farmers. The farmers were arrested for attacking Black squatters, who are part of a wave of occupations of white-owned farms.</P>
<P>These farms are a legacy of the white-run regime of Ian Smith, when the country was known as Rhodesia. Smith's regime ended in 1980, but white farmers still have a dominant position in Zimbabwe. Though they account for less than 0.5 percent of the population, they own 32 percent of the country's cultivable land.</P>
<P>But while land distribution is a pressing issue for the desperately poor rural Black population, the occupations are part of a cynical campaign organized by President Robert Mugabe and his ZANU-PF government.</P>
<P>Mugabe was a leader of the liberation struggle in the old Rhodesia and came to power in 1980. But his government, which promised so much hope in its early years, is now widely hated for its corruption and repression.</P>
<P>Mugabe has gone along with the International Monetary Fund's "structural adjustment" plans for Zimbabwe, which have caused sharp drops in living standards for most of the country's population.</P>
<P>When growing anger with the regime exploded in the 1990s in massive strikes and community rebellions, Mugabe responded with a savage crackdown. And to deflect anger away from the government, Mugabe's supporters began carrying out the land seizures.</P>
<P>This caused outrage among Western leaders. Yet these same governments cheered on austerity cuts--and remained silent when Mugabe attacked workers and activists fighting for democracy and decent lives. Apparently, Washington only cares about human rights in Zimbabwe when white farmers are the ones being arrested.</P>
<P>The strikes and protests have shaken Mugabe's control. Last year, ZANU-PF nearly lost power in the country's parliament, when the opposition Movement for Democratic Change (MDC) won almost half the seats. The MDC would have won a clear majority but for widespread fraud and government-sponsored violence against opposition candidates.</P>
<P>Unfortunately, the MDC's main leaders are looking for support from Western governments and back the IMF's free-market "reforms." This has only helped Mugabe, who is hoping to whip up support by posing as an opponent of the white farmers--even as he steps up repression against his critics.</P>
<P>Mugabe clearly hopes this two-pronged strategy will help him weather the storm. But the country's economy is in a tailspin, and the controversy over the land occupations has only added to the chaos.</P>
<P>Large numbers of people in Zimbabwe want Mugabe to get out--and small but important struggles like the health care workers' strike show the potential for an alternative that can win real change.</P>

