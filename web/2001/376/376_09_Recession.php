<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why the economy is  headed for recession</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by LANCE SELFA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH took time out from his vacation to declare that the slowing U.S. economy was undergoing a "correction."</P>
<P>Don't worry about those 10-year highs in the numbers drawing unemployment benefits, Bush declared. When the U.S. economy feels the effect of those tax refund checks, it'll be well on its way to recovery.</P>
<P>Bush's happy talk mirrored the standard claptrap of Wall Street commentators. The economy may be going through a rough patch, they say, but it will soon return to the booming ways of the 1990s.</P>
<P>Of course, these were the same people who told us a few years ago that we were living in a "new economy" that had transcended capitalism's tendency to booms and slumps.</P>
<P>The pundits swing between moods of great optimism and pessimism--with their day-by-day pronouncements resting on evidence as flimsy as yesterday's Dow Jones stock price index.</P>
<P>Though he wrote more than a century ago, Karl Marx's description of the workings of capitalism is steadier--because it explains both today's slump and yesterday's boom.</P>
<P>Capitalism is a dynamic system, Marx argued. It can't avoid booms and slumps because its driving force--the capitalist search for profits--is never ending.</P>
<P>Capitalists have to compete with each other to make as much profit as they can. And because no authority is directing this anarchy of competition, individual capitalists try to expand their investments to take over market share from the others. This is the source of booms.</P>
<P>But as each firm pumps money into their businesses, they add to the overall economy's productive capacity--eventually reaching a point where the system as a whole has the ability to produce more goods than can be sold at a profit. The result is a slump--to be precise, what Marx and Frederick Engels called an "epidemic of overproduction."</P>
<P>"In these crises, a great part not only of the existing products, but also of the previously created productive forces, are periodically destroyed," they wrote in <I>The Communist Manifesto</I>. "And why? Because there is too much civilization, too much means of subsistence, too much industry, too much commerce."</P>
<P>The current slump has its roots in a collapse of business investment--most of all, corporate investment in information technology. In the boom years of the 1990s, corporations borrowed billions to spend money on information technology. Hi-tech high flyers like Lucent, Cisco Systems and Hewlett Packard grew rapidly on the basis of these investments.</P>
<P>But when the dot-com bubble burst, these companies were left with far more capacity than they needed to meet reduced demand. Their only option was mass layoffs.</P>
<P>But it's important to remember that overcapacity isn't unique to the hi-tech sector. Leading U.S. capitalists like General Motors' John Smith and General Electric's Jack Welch have talked for years about a global glut in industrial capacity.</P>
<P>In 1995, GM projected that, by 2000, the auto industry would have the worldwide capacity to produce 79 million cars--yet worldwide demand would be only about 57 million cars. This gap is equivalent to the capacity of the entire U.S. auto industry.</P>
<P>For the bosses, there's only one way to deal with overcapacity--stop production. That means shutting down plants and laying off workers. In the U.S. alone, 600,000 manufacturing jobs have been eliminated over the last year, and U.S. industrial production has dropped for 10 consecutive months--the longest streak since 1982.</P>
<P>Because the central problem with the U.S.--and the world--economy is the tendency toward overproduction that Marx described, interest rate cuts by the Federal Reserve Board and tax refund checks will have only a limited impact, if any.</P>
<P>The economy's developing recession illustrates the irrationality of a system that its defenders claim there is no alternative to. But there is an alternative.</P>
<P>Marx and Engels argued that workers should take control of a system that uses the wealth they create to serve the greed of its corporate masters.</P>

