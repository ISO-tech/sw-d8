<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE DIRT ON DUBYA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>White House tries to pull a fast one</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ELIZABETH SCHULTE</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 2</font></P>
<P>WASHINGTON--George W. Bush tried to sneak one through, and he's getting caught.</P>
<P>Veering just slightly away from his most fanatical anti-abortion supporters, Bush okayed federal funding for research on embryonic stem cells in August. The condition: Scientists can only use stem cells from embryos destroyed before August 9.</P>
<P>Bush said that there were 60 existing "cell lines" for researchers to obtain stem cells from--enough for experiments that scientists hope will lead to new treatments for illnesses like Alzheimer's.</P>
<P>One problem. Scientists who actually work with stem cells say the number of cell lines is nowhere near 60. "We both almost fell off our chairs," said Andy Cohn, spokesperson for a University of Wisconsin research foundation, who watched Bush's speech with James Thomson, the scientist who in 1998 first isolated human embryonic stem cells.</P>
<P>Scientific literature has identified fewer than a dozen cell lines. And since Bush's announcement, several news organizations made their own counts and came up with 23 at most.</P>
<P>But the White House has an answer for these doubting Thomases. "The burden of proof is on anyone who doubts," said White House flak Ari Fleischer.</P>
<P>Even if Bush and his gang somehow turn out to be right in their estimate, researchers say that limiting experiments to 60 stem cell lines is like telling mathematicians to keep up the good work--but never use numbers bigger than 10.</P>

