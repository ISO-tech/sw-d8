<P><font face="Times New Roman, Times, serif" size="4">Victory in the death penalty capital</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Texas halts execution of juvenile offender</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by ALAN MAASS</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HUNTSVILLE, Texas--Opponents of the death penalty won an important victory last month when Texas' highest court halted the execution of Napolean Beazley.</P>
<P>Beazley was writing a letter in a tiny cell within sight of the death chamber when a chaplain told him that he had won a stay--with just four hours to spare. "I feel like a ghost--like I've died and come back," Napolean told a Houston TV station.</P>
<P>Napolean's case highlights many of the injustices of the death penalty system. Most prominently, it raises the question of juvenile offenders being put to death. The U.S. is one of a handful of countries that executes people who were under 18 when the crime that they were condemned for was committed.</P>
<P>Beazley was 17 when he shot businessman John Luttig in a botched carjacking. But prosecutors sought the death penalty despite his age--and despite the fact that Napolean had no criminal record, was president of his senior class and had just graduated 13th in his class.</P>
<P>In fact, like so many other death row prisoners, Napolean was sentenced to death because of who he killed. John Luttig was white. Moreover, he was the father of Michael Luttig, a fanatically pro-death penalty federal judge who played a hands-on role in pressuring Texas prosecutors to seek a death sentence.</P>
<P>Anti-death penalty activists are celebrating this victory in Texas--the heart of the U.S. execution machine. It's a sign of how far the climate around capital punishment has shifted that the stay came from the Texas Court of Criminal Appeals--which has rarely wavered in imposing death sentences, no matter what the circumstances.</P>
<P>Beazley's lawyers have a broad appeal before the U.S. Supreme Court, which asks the justices to rule that the execution of juvenile offenders is cruel and unusual punishment. The Supreme Court is supposed to debate a similar issue regarding execution of the mentally retarded.</P>
<P>The death penalty used to be central to the politicians' law-and-order agenda. But pressure from activists and media attention on the gross injustices of the system have turned the tide. We need to keep up the fight to stop America's killing machine. </P>
