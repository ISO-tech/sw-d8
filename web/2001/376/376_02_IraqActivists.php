<P><font face="Times New Roman, Times, serif" size="5"><b>Arrested in NYC for opposing sanctions on Iraq</b></font>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 2</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--A dozen people were arrested on the steps of the U.S. mission to the United Nations (UN) in August for protesting deadly sanctions against Iraq.</P>
<P>The U.S. government has been the main supporter of a UN embargo on Iraq imposed before the 1991 Gulf War and maintained ever since.</P>
<P>Though supposedly designed to punish Saddam Hussein's regime, the sanctions--which block the import of critical goods like food, medical supplies and materials for purifying water--have had a devastating impact on ordinary Iraqis.</P>
<P>According to the United Nations Children's Fund, some 5,000 Iraqi children under the age of five die every month because of the sanctions.</P>
<P>Members of the group Voices in the Wilderness were in the midst of a fast to bring attention to this situation. On August 15, they brought a meal of cooked lentils and rice and asked mission staff members to join them and discuss the issue.</P>
<P>"We will also have with us untreated water from the East River," the group said in a statement. "We don't want to serve or drink it, but rather remind ourselves of how vulnerable Iraqi civilians are to water-borne disease."</P>
<P>Staffers called in New York cops--who held the activists for up to 10 hours and charged them with criminal trespass and obstruction. The activists have a trial date in late September.</P>
