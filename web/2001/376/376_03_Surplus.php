<p><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Democrats and Republicans bicker over who's to blame</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pop goes the surplus</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 3</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration's announcement that the federal government budget surplus is disappearing fast set off the usual partisan fight in Washington over who was to blame.</P>
<P>Predictably, Bush called on Congress "not to go hog wild" on spending. Except, of course, for the $18 billion increase that he wants for the Pentagon.</P>
<P>Health and Human Services Secretary Tommy Thompson said the disappearing surplus meant that there was no money to expand the Children's Health Insurance Program--and that it would be virtually impossible to add a prescription drug benefit to the government's Medicare health program.</P>
<P>Congressional Democrats relished the opportunity to take Bush to task over the budget. But their attack was entirely focused on the fact that the government's surplus could soon become a deficit.</P>
<P>"It's sad that the budget debate seems to have come down to Bush and the Republicans handing out tax cuts skewed to the very rich, and the Democrats complaining that he's squandering the surplus," said Doug Henwood, editor of <I>Left Business Observer</I>.</P>
<P>"It's like a debate between two kinds of Republicans-Reaganite supply-siders and Hooveresque austerity hounds. Why isn't anyone saying that a flush government could afford to spend on badly needed health care, child care and environmental initiatives?"</P>
<P>When they arrived in the White House, the Bush gang put its tax cut plan front and center. Whatever he might have said during the campaign, Dubya had no intention of using the surplus to provide a prescription drug benefit or to insure the uninsured.</P>
<P>He knew he couldn't say this openly, because such programs are overwhelmingly popular. So he used a tax cut for the rich to drain off the surplus. Now, when pressed to deliver programs that people really want, Bush and his pals can say that "there's no money."</P>
<P>But in attacking Bush, the Democrats have no credibility. Under Bill Clinton, the Democrats made a virtual religion out of preserving the government's budget surplus.</P>
<P>The same party that still blames Ralph Nader and the millions of people who supported him for putting Bush in the White House gave Dubya his margin of victory on the tax cut. In fact, before taking control of the Senate last May, the Democrats even agreed to pass the tax cut first.</P>
<P>What kind of an "opposition" is this? Clearly one that's more interested in pocketing corporate cash than in doing anything more than pay lip service to the concerns of ordinary people.</P>

