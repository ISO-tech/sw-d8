<P><font face="Times New Roman, Times, serif" size="4"><b>WBAI</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PETRINO DiLEO</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Amy Goodman, host of the left-wing radio show <I>Democracy Now!,</I> has been suspended without pay in the latest chapter in an ongoing struggle at the WBAI radio station.</P>
<P>Following repeated harassment and a reported physical confrontation with station staffers, Goodman refused to record her daily news show at WBAI's studios, producing it instead at an independent studio. Management retaliated by refusing to air live editions of <I>Democracy Now!</I> and suspending Goodman and her staff.</P>
<P>WBAI is part of the Pacifica Radio network, whose pro-corporate board has been trying to tighten its grip over member stations through a campaign of firings and intimidation.</P>
<P>Bob Knight, Robert Lederer and Angela Davis are among the latest victims of Pacifica's purge at WBAI, which began late last year. Goodman, whose <I>Democracy Now!</I> is Pacifica's main national news show, has faced ongoing harassment for helping to lead opposition to the board.</P>
<P>In mid-August, Goodman says that she found two WBAI staffers going through personal possessions of fired WBAI Program Director Bernard White. This led to a confrontation with station manager Utrice Leid, who took a camera from Goodman and shoved her, Goodman reported.</P>
<P>After enduring a yelling match with WBAI Operations Director Sidney Smith and morning show host Marjorie Moore, Goodman and her staff decided not to record at WBAI.</P>
<P>Pacifica is claiming that this breaks a contract provision in the station's agreement with the American Federation of Television and Radio Artists (AFTRA). But the union has backed Goodman, stating that it's "no longer satisfied that the WBAI studios are a safe and appropriate working environment for the <I>Democracy Now!</I> staff."</P>
<P>WBAI workers and listeners who support them are fighting back--with plans for a National Day of Solidarity on August 28.</P>
<I><P>Live editions of </I>Democracy Now!<I> can be heard on the Internet radio station www.wbix.com. For more information, go to www.savepacifica.net.</P>
</I>
