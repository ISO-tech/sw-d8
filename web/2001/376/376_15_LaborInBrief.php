<p><a name="Davenport"></a><font face="Times New Roman, Times, serif" size="4"><b>Davenport Machinery</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BRIAN ERWAY and ARIEL CRUZ</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ROCHESTER, N.Y.--Davenport Machinery is typical of small manufacturers feeling the bite of the U.S. economic slump, and its answer is to make workers pay. But 75 members of International Association of Machinists Local 1868 are saying, "No way!"</P>
<P>Their last contract with Davenport expired more than a year ago, but work continued under a 15-month extension that won the union a guaranteed staffing level of 82.</P>
<P>"The goal was to keep everyone working," explained Lloyd, Local 1868's vice president. "We went down to a 32-hour week, and then a 24-hour. For this contract, management wanted us to lose 30 jobs right up front. That's almost half of us."</P>
<P>The bosses also want to scrap seniority to be able to pick and choose workers. They want changes in workplace rules to be at their whim, instead of having to bargain with the union. </P>
<P>And they want to get rid of the "succession clause," which promises that union representation and collective bargaining rights would continue if the company gets sold to a new owner.</P>
<P>But Davenport has a fight on its hands. Sentiment for the strike was overwhelming among workers, who voted 69 to 4 to walk. Strikers set up 12-hour picket lines on August 14, and within a couple of days, boosted them to 24-hour pickets. </P>

