<p><a name="Supremo"></a><font face="Times New Roman, Times, serif" size="4"><b>V &amp; V Supremo Foods</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by BRIDGET BRODERICK</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Teamsters Local 703 members have been on strike for nearly three months against V&amp;V Supremo Foods.</P>
<P>Throughout the summer, the cheese factory workers have stayed on the picket line--and few have scabbed on the strike. Politicians, union members, and community and student activists have joined workers at various rallies in their support.</P>
<P>But the August 24 rally at the V&amp;V Supremo warehouse was noticeably smaller and less enthusiastic. Although no more workers have crossed the picket line, many were absent because they were working other jobs to make ends meet.</P>
<P>The union has left them little choice. Although the local has called for consumer boycotts and organized rallies of hundreds, negotiations with the company have produced little. </P>
<P>V&amp;V Supremo refuses to discuss salaries, which workers feel is key. Workers also need solidarity action from locals, such as Teamsters stopping deliveries of V&amp;V Supremo products and United Food and Commercial Workers members refusing to handle them. </P>
<P>The Chicago Day Labor Organizing Committee has committed itself to refuse temporary work at V&amp;V Supremo. "We're not going to be their scabs or help this company out," said organizer Dennis McFarlane. "We stand with you 100 percent." </P>
<P>But a victory will depend on whether the union comes up with a strategy to put more pressure directly on V&amp;V Supremo.</P>
