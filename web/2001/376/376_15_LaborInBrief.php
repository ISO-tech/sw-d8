<p><a name="Bay"></a><font face="Times New Roman, Times, serif" size="4"><b>Bay Area Rapid Transit</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PHIL GASPER</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OAKLAND--Chanting "We did it before, we'll do it again!" more than 300 union employees of Bay Area Rapid Transit (BART) rallied on August 15 to demand a decent contract.</P>
<P>The rally was called by Amalgamated Transit Union (ATU) Local 1555, which represents 800 station agents and train operators; SEIU Local 790, which represents 1,700 maintenance, clerical and professional workers; and AFSCME Local 3993, which represents 230 professional workers.</P>
<P>The unions have been in negotiations with BART management since March, but the two sides remain far apart over wages, benefits and BART's use of temporary workers and consultants to fill union positions.</P>
<P>The unions are calling for a 26.5 percent pay raise over three years to catch up with the Bay Area's soaring cost of living. BART has offered 11 percent over four years.</P>
<P>In July, Gov. Gray Davis imposed a 60-day "cooling-off" period on the two biggest unions, which expires on September 4.</P>
<P>Four years ago, BART workers struck for six days to win their demands. Workers at the rally made clear that if management refuses to budge, they are prepared to do the same again.</P>

