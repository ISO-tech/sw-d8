<P><font face="Times New Roman, Times, serif" size="4">As union workers fight to defend their gains...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Verizon gets nasty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I am a technician at Verizon, one of 140,000 who went on strike a year ago to win a decent contract. We were proud of our victory.</P>
<P>In addition to a decent wage increase, we won an agreement to allow workers in the company's wireless unit to hold a union drive through a "card-check" agreement. That means that wireless workers get a union once a majority of workers sign union cards--and Verizon also agreed to remain neutral during the union drive.</P>
<P>The fact that we won these promises was a resounding rebuttal to those who argued at the time that unions had no place in the "New Economy." But life at Verizon has gotten ugly since our victory.</P>
<P>First, the company refused to honor its commitments on wireless. They've gone on the offensive to sway workers against the union--holding "captive audience" and one-on-one meetings with employees and setting up an anti-union Web site.</P>
<P>Then, this spring, they announced that they were $1 billion short of this year's profit goal. The company's first step was to eliminate overtime--and to force us to rotate our schedules, which hurts since overtime pay is the only way many of us pay our bills.</P>
<P>In the meantime, the company announced to the press that it would lay off 10,000 people. They stated that 3,000 of these layoffs would come through performance-related firings, and that several thousand more would be accounted for by the reduction in paid overtime hours.</P>
<P>This drive to fire people has produced a climate of stepped-up harassment and discipline in the field. Larger numbers of people have been fired or suspended for petty infractions during the last two months. I recently had four managers ride out to my job sites on just one day.</P>
<P>But the most insulting and worrisome development came August 1, when the company implemented something called the "Service Excellence Program." Under this scheme, we must meet productivity standards--and face eventual suspension and dismissal if we fail to meet the standards.</P>
<P>This is unprecedented. And despite its name, it has nothing to do with service, but everything to do with increasing the amount of work we do and meeting their firing quotas for the year.</P>
<P>What makes this so outrageous is that the company is not broke. Verizon's CEO, Ivan Seidenberg, made an incredible $73,542,225 last year.</P>
<P>So in many garages in New York City, we've decided that we're not going to pay for their supposed financial straits. We've begun a massive grievance campaign over the new productivity plan--aimed at making it too difficult to implement.</P>
<P>We rallied in front of Verizon's headquarters to demand neutrality at wireless, and many of us want to do much more than that.</P>
<P>Verizon has crossed a line--and we're committed to beating them back.</P>
<B><P>Member of CWA Local 1109</B>, Brooklyn</P>

