<P><font face="Times New Roman, Times, serif" size="4">Walkout by UAW sends a message to Mitsubishi</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This is solidarity"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 16</font></p>
<font face="Times New Roman, Times, serif" size="3"><P><b>LEE SUSTAR</b> reports on the strike at Mitsubishi.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NORMAL, Ill.--The 2,600 workers at Mitsubishi Motor Corp.'s plant here stunned their new hard-line management with a victory in a two-day strike.</P>
<P>The new contract, approved by 83 percent of workers at a ratification vote August 26, forced plant bosses to drop demands for concessions on benefits, including vacation time, and won improvements on safety and ergonomics at the injury-plagued plant.</P>
<P>The strike was the first ever for the workers, whose United Auto Workers (UAW) Local 2488 was founded when the plant opened in 1989. Since Mitsubishi management and the UAW follow the DaimlerChrysler standard for wage rates and a four-year deal, ergonomics and benefits were the main issues.</P>
<P>"This is a company that has, in the last couple of years, let go of roughly 500 workers, cranked up the production line, increased profitability and had record sales of products out of this plant," Local 2488 President Justin West told <I>Socialist Worker</I>. "And still they wanted takeaways. Our members told us that they wouldn't budge on that."</P>
<P>West--a union reformer who was elected last year--said that the local had lost an arbitrator's ruling over ergonomics last year when it tried to challenge plant boss Rich Gilligan's move to slash the number of job rotations from 20 to four or five.</P>
<P>"We had the Japanese practice of job rotation, which masked a lot of bad ergonomics," West said. "If you only saw a particularly bad job every four or five days, there wasn't as much incentive to do something about it. But now you're doing it twice a day."</P>
<P>Gilligan--a former boss at a Ford plant--also slashed 500 jobs through early retirement buyouts and increased mandatory Saturday overtime to 10 hours. And by speeding up the assembly line, Gilligan forced workers to produce 450 cars in eight hours, compared to 480 cars in 10-hour shifts in 1995.</P>
<P>Injury rates in the plant soared. As much as 10 percent of the workforce is out on disability or workers' compensation at any given time.</P>
<P>One worker in the plant, Diane Hern, has had to undergo surgery as a result of the punishing pace of work. She was told by management that her most recent injury--cysts in the ganglia of her wrists--weren't work related, even though many coworkers suffer from precisely the same injury. "They denied me workers' compensation, and disability isn't very much money," Hern told <I>Socialist Worker</I>.</P>
<P>Mike Bishop, who works in the paint shop, has had surgery several times for plant-related injuries on both wrists and both elbows. "You can't just use up employees like me and throw them away," he said. "But they consolidated jobs. They got to where they had three people doing a job, and divided it up and gave it to two."</P>
<P>While West acknowledges that the union didn't get all it wanted on ergonomics, the contract does force management to finally address the issue.</P>
<P>Under the agreement, management must work with the union to investigate any job that causes an injury within two weeks--and must re-evaluate jobs every two years to improve health and safety.</P>
<P>The decision to take on management with a strike reflects a big change in Local 2488. In 1996, Mitsubishi was the target of a federal Equal Employment Opportunity Commission (EEOC) lawsuit over horrible abuse suffered by women in the plant.</P>
<P>The officials who ran Local 2488 at the time failed to oppose management when it bused hundreds of workers to a pro-company demonstration against the EEOC in Chicago. Since then, the atmosphere in the plant has improved, and women workers are treated with respect, Diane Hern told <I>Socialist Worker</I>.</P>
<P>Some 97 percent of workers voted to authorize a strike when negotiations began earlier this year--and on the picket line, men, women, Black and white workers stood together.</P>
<P>Some of the workers had participated in solidarity rallies for the Illinois "War Zone" battles of the mid-1990s-at Caterpillar, A.E. Staley and Bridgestone-Firestone. "But there are so many people here who have never been through anything like this before," said Jeff Bailey, who has worked at the plant for eight years. "You can read as much as you want about it. But until you experience it, you just don't know. This is the most solidarity I've ever seen here."</P>

