<P><font face="Times New Roman, Times, serif" size="4">"Peacekeepers" go into Macedonia</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>NATO soldiers won't solve crisis</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 31, 2001 | Page 13</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NATO TROOPS moved into Macedonia in August to try to stop the latest Balkans conflict from spinning into an all-out civil war between ethnic Albanians and the country's Slavic majority.</P>
<P>Western troops may limit the fighting for now. But as <I>LEE SUSTAR</I> explains, the occupation by 3,500 NATO troops once again exposes the fraud--and failure--of the West's "humanitarian" intervention in the Balkans.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>NATO GOVERNMENTS pressured the Slav-dominated Macedonian government and the ethnic Albanian National Liberation Army (NLA) into a peace agreement last month.</P>
<P>Under the deal, the rebels would disarm. In exchange, the parliament would pass laws to grant official status for the Albanian language in some areas and to integrate the country's police force with Albanians, who have long faced discrimination in Macedonia, as in other Balkans countries.</P>
<P>NATO troops claim that they will pull out if either side shoots at them--but this is hot air to overcome opposition to deployment in NATO countries like Germany.</P>
<P>"NATO troops have actually been in Macedonia all along," <I>Time</I> magazine noted. "[The Macedonian capital of] Skopje houses the major logistics base for the entire Kosovo peacekeeping operation, and they're not about to withdraw."</P>
<P>NATO governments hope a show of force will keep the lid on further conflicts in Macedonia. But there's no guarantee that the NLA--an offshoot of the Kosovo Liberation Army in neighboring Kosovo--will turn in its weapons.</P>
<P>The NLA claims it has only about 2,500 guns to turn in, while the Macedonian government puts the NLA's arms supply at 85,000.</P>
<P>And many Macedonian Slav politicians have vowed to oppose any new laws in parliament that would address the grievances of Albanians, who are about 30 percent of the population. In fact, government troops kept bombarding Albanian guerrillas and targeting Albanian villages as the cease-fire was negotiated.</P>
<P>Just six days before a peace deal was signed August 13, hooded Macedonian police executed five suspected Albanian guerrillas in Skopje. And as NATO troops stationed in Kosovo rolled into Macedonia, Slav nationalists blockaded a road crucial to supplying U.S. troops</P>
<P>Just a few weeks earlier, mass protests swept through Skopje when U.S. troops evacuated a group of Albanian guerrillas in order to enforce a cease-fire. Plus, Macedonia's interior minister has been accused of handing out guns to paramilitary groups.</P>
<P>But despite the appearances, the U.S. isn't interested in protecting the rights of Albanians. Early this year, Washington and NATO officials denounced the NLA as "terrorists" and gave the Macedonian government a green light for a brutal crackdown.</P>
<P>At the same time, they gave the Serbian-run Yugoslav Army permission to enter a "buffer zone" on the Kosovo border to attack Albanian guerrillas there. But the crackdown in Macedonia--including artillery bombardments and helicopter attacks on Albanian villages--only served to increase popular support for the NLA, which by all accounts had been small and relatively isolated.</P>
<P>With fighting threatening to erupt into an all-out civil war, the U.S. and NATO decided to move in troops.</P>
<P>Formerly part of Yugoslavia, Macedonia--a tiny country of barely 2 million people--has been a flash point for more than a century. It occupies territory long claimed by its neighbors--Serbia, Greece, Bulgaria and Albania--with Turkey claiming a historic role as protector of the mostly Muslim Albanians.</P>
<P>When ethnic Albanian refugees from neighboring Kosovo streamed to the Macedonian border during the 1999 war, Slav government authorities often treated them brutally.</P>
<P>One of the main reasons Washington launched the war over Kosovo was fear that Albanians in Macedonia would be swept into a war against Serbia and their own government--potentially drawing in NATO allies Greece and Turkey onto opposite sides.</P>
<P>When NATO troops occupied Kosovo at the end of the war, the U.S. took control of the border area with Macedonia precisely to head off any new conflict. Their fortress in Kosovo, Camp Bondsteel, is the biggest new U.S. military base built since the Vietnam War.</P>
<P>But because Western intervention has failed to solve the problems of nationalism and poverty for Slavs or Albanians, fighting erupted anyway. The NATO troops that came into the Balkans claiming to be peacemakers are increasingly seen as the occupiers that they are.</P>

