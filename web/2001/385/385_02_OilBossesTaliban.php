<P><font face="Times New Roman, Times, serif" size="5"><b>Helping oil bosses court the Taliban</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT A difference a few years makes.</P>
<P>Today, Zalmay Khalilzad is a special assistant to the president and a leading voice in the U.S. war on the Taliban government in Afghanistan. Four years ago, he was helping the U.S. oil industry court the Taliban.</P>
<P>A veteran bureaucrat in the administrations of Ronald Reagan and Papa Bush, Khalilzad was working for an energy consulting firm when the Taliban came to power in 1996. Big oil welcomed the Taliban, hoping that the hard-line Islamists would impose stability in Afghanistan, making it possible for U.S. energy companies to get access to oil and natural gas from the Caspian Sea, via pipelines across Afghanistan and Pakistan to the Indian Ocean.</P>
<P>In December 1997, Unocal flew Taliban leaders to Houston to make its pitch. By one report, the Taliban were offered a 15 percent cut of the profits from the pipelines--and the promise that Afghanistan would become "like Saudi Arabia."</P>
<P>Khalilzad was part of the Unocal entourage on hand to flatter the Taliban, and he helped to flak for the oil industry with an op-ed article in the <I>Washington Post</I>. "The Taliban does not practice the anti-U.S. style of fundamentalism practiced by Iran," Khalilzad wrote four years ago. "We should&#133;be willing to offer recognition and humanitarian assistance and to promote international economic reconstruction."</P>
<P>Today, of course, Iran is a respected part of Washington's "coalition against terrorism"--and the Taliban are the hated enemy.</P>

