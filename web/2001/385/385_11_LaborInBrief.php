<P><font face="Times New Roman, Times, serif" size="4"><b>V&amp;V Supremo Foods</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--After seven months on strike, workers at V&amp;V Supremo Foods won an important victory. Not only did workers in the distribution unit win raises between 25 and 35 percent over the life of the three-year contract, but the company finally conceded that workers in the production unit had voted to join the union and agreed to negotiate a contract.</P>
<P>For months, the company tried to delay contract negotiations in the distribution unit and refused to recognize the production workers' election, who voted to join Teamsters Local 703.</P>
<P>"This is a company that for seven months spent millions of dollars fighting against a first contract, fighting against union recognition," said Nelson Soza, field representative for the AFL-CIO. "Their goal was to crush this effort," he told <I>Socialist Worker</I>. "It's a partial victory because we don't have everything we wanted to have, but we get back to work and get big raises."</P>
<P>The distribution workers' first contract also establishes a grievance procedure and the use of independent arbitrators if workers don't feel the company is abiding by the contract.</P>
<P>"For Latino immigrant workers, this is huge because it gives them some kind of voice on the job," said Soza. "They understood that they were the front line in this war&#133;to begin to deal with the exploitation of immigrant workers, which so many employers take for granted," said Soza.</P>
<P>Support from community leaders, clergy and political figures was also crucial. "We haven't seen this kind of support since the farm workers' struggles," said Soza.</P>
<P>Starting in the summer, V&amp;V Supremo workers had rallies of 300 to 400--and nearly 400 turned out to hear Rev. Jesse Jackson and U.S. Rep. Luis Gutierrez (D-Ill.) speak at a church. In the final analysis, said Soza, "the bosses realized they weren't just up against 120 immigrant workers, but against an entire community."</P></FONT></BODY>

