<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Boston hotels</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Annie Levin</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--Hotel workers, members of Hotel Employees and Restaurant Employees (HERE) Local 26, are ready to strike. They voted overwhelmingly November 19 to authorize a walkout if an agreement can't be reached by November 30, when their old contract expires.</P>
<P>Local 26 represents more than 3,000 hotel waiters, reservation clerks and dishwashers covered under a single master contract at nine major hotels here. Since September 11, roughly 1,000 members of Local 26 have been laid off, and many more have seen their hours slashed.</P>
<P>Now the hotel owners want to use the tragedy to ram through a concessionary contract. Management wants the right to assign some workers to shifts shorter than eight hours while forcing others to work overtime. And the owners are refusing to guarantee health benefits.</P>
<P>The bosses are pleading poverty due to the impact of September 11--but workers aren't buying it. "The hotels have had five years of profits shooting through the roof," Local 26 Secretary-Treasurer Henry Green told <I>Socialist Worker</I>.</P>
<P>At the November 19 vote rally, the multiracial, mostly immigrant workforce waved signs in four languages--all demanding justice. "Boston hotel workers will say no to givebacks, and in doing so you will win justice for all hotel workers all over North America," HERE President John Wilhelm said at the rally.</P>
<P>Lou Mandarini of Laborers Local 22 added, "Forty-three HERE members lost their lives in the World Trade Center. Thousands more have been laid off with not so much as a handshake. At the same time, airline and hotel corporations have gotten economic stimulus packages that haven't given a damn about working people&#133;You have got to get a contract that can keep you surviving."</P>
<P>Rev. Jesse Jackson also spoke. "Workers of Boston, it's time to stand up and fight back, it's time to act," he said. "If you believe it is time to strike, then stand up"--which brought the whole room to its feet, cheering.</P>
<P>After the rally, workers poured out of the church and marched to the Park Plaza Hotel, chanting "No contract, no peace!" Sheryl Debarros, a hospital worker and member of SEIU Local 509, told <I>Socialist Worker</I> that she attended the rally because "we're all fighting for fairness for all working people. The rich made money, now it's our turn to make money."</P>

