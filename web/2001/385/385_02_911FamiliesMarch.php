<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NO TO WAR! NO TO RACISM!</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Victims' relatives say:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Not in our name!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>RELATIVES OF some of the victims of the September 11 attacks are marching from Washington, D.C., to New York City this week to protest the U.S. war on Afghanistan. Marching under the banner "Our grief is not a cry for war," participants say they want to show that there is opposition to George W. Bush's "war against terrorism" even among those who lost loved ones September 11.</P>
<P>Amber Amundson, whose husband Craig was killed in the attack on the Pentagon, led the march. "Craig would not have wanted a violent response to avenge his death," she told reporters. "And I can't see how good can come of it."</P>
<P>The mainstream media constantly invoke the memory of those killed on September 11 in cheerleading the slaughter in Afghanistan. But as <I>Socialist Worker</I> went to press, no major newspaper or wire service had reported on the antiwar march of family members.</P>

