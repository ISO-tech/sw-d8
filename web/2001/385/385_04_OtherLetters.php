<P><font face="Times New Roman, Times, serif" size="4"><b>Stand in solidarity with Palestinians</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>November 29 is International Palestinian Solidarity Day. I urge <I>SW</I> readers to take action and show our support for the people of Palestine.</P>
<P>On November 11, delegates at a student antiwar conference in Berkeley, Calif., voted overwhelmingly to endorse and participate in activities on November 29.</P>
<P>The argument can and must be made for a free Palestine. We would be fooling ourselves if we think that Palestine and Afghanistan are entirely separate issues, or that support for Palestinian liberation does not exist in the antiwar movement.</P>
<P>All out on November 29! Free Palestine!</P>
<B><P>John Green, </B>Davis, Calif.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Virginia gives green light to racism</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Racist bigotry won a victory this month when the Virginia Supreme Court overturned convictions of a KKK leader and two others, ruling that the state's ban on public cross burning was an unconstitutional bar to "free expression."</P>
<P>Virginia's ban, dating from 1952, was a response to rampant racist intimidation by the KKK and other white supremacists during the civil rights movement. But racists are still at work spreading their hate in Virginia--now with the court's blessing.</P>
<P>In one of the cases thrown out by the court, two men in Virginia Beach put a cross in a Black neighbor's yard and tried to set it on fire. Even in this case, the court declared that a burning cross is a "symbol" and is protected by the First Amendment.</P>
<P>It's as clear now as it was during the civil rights struggle that cross burning isn't "symbolic"-- it's a racist attack and a promise of more violence, from beatings and lynchings to church burnings and police brutality.</P>
<P>While opening the door for racists, the ruling has another sinister effect. According to legal analysts, it paves the way for a flood of free expression cases by school boards and local legislatures rushing to promote patriotism following September 11.</P>
<P>Apparently, now that the racists can proceed unfettered by law, state-sanctioned patriotism, flag-waving and allegiance to the war will be a regular part of the curriculum in Virginia.</P>
<P>We've got to organize to stop the racists at home, as well as the nationalist bigotry and racism that fuel Bush's war on Afghanistan.</P>
<B><P>Bruce Cooley, </B>Takoma Park, Md.</P>

