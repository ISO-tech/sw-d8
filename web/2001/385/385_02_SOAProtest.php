<font face="Times New Roman, Times, serif" size="5"><b>Thousands protest School for Assassins</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ADRIANA PORTILLO-BARTOW described her emotions as she stood at the gates of Fort Benning, the site of the U.S. Army's School of the Americas (SOA).</P>
<P>"As the mother of two disappeared children in Guatemala, I was deeply touched by the solidarity expressed here," she told <I>Socialist Worker</I>. "And at the same time, I feel very engaged, because I'm at this place that is responsible for the deaths of so many people."</P>
<P>Some 10,000 people traveled here for actions on November 17 and 18 in Columbus, Ga., where the SOA--recently renamed the Western Hemisphere Institute for Security Cooperation--trains Latin American military personnel in kidnapping, torture and assassination.</P>
<P>For the last 12 years, the human rights group SOA Watch has organized a funeral procession to mark the deaths of thousands murdered by SOA graduates. This year, using the U.S. war in Afghanistan as an excuse, the mayor of Columbus, Ga., did all he could to shut down the demonstration, getting a restraining order against the procession.</P>
<P>But activists won a surprise victory in court--before the same federal judge who last year sentenced an 88-year-old nun to six months in prison for civil disobedience. About 5,000 people took part in the procession.</P>
<P>Unfortunately, organizers asked marchers to gather close to the gates of Fort Benning--so the march that was fought for in court didn't take place. Many protesters quietly walked to the 8-foot-high barbed-wire fence erected at the gate to place crosses, photos, banners and signs.</P>
<P>For most demonstrators--who had traveled from as far away as Minnesota--this was an opportunity to speak out against Bush's "war against terrorism" at the site of the U.S. government's own terrorist training camp.</P>
<P>"The violence we're facing in the world can be traced back to our actions in poorer parts of the world," said Tim Marchiasava, who came with 16 others from Louisiana State University in Baton Rouge, La. "Our government supports corporate agendas through the SOA. Maybe because of the current war, more people came out this year who weren't aware before."</P>
<P>Police arrested more than 50 people who took part in civil disobedience. "They can keep protesters from getting in, but they can't keep the truth from getting out," said SOA Watch founder Father Roy Bourgeois. "We aren't going away. We'll be back in greater numbers until the school is shut down."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Detained for a wrong turn</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3"><b>KEITH ROSENTHAL was among 11 activists who traveled in a van from Vermont to Georgia to protest the SOA--only to be held for 12 hours after they accidentally drove onto the base. He described what happened for <I>Socialist Worker</I>.</b></P>
<P>YOU KNOW these are interesting times when you can be detained, interrogated, searched, denied a lawyer, psychologically tortured and have your vehicle confiscated--all for making a wrong turn! But that's just what happened to us when we went down to Fort Benning, Ga., for the annual protest against the SOA.</P>
<P>A small group of us from Burlington, Vt., decided to endure the 22-hour drive so that we could raise our voices and say, "Stop training terrorists in our name!"</P>
<P>After making the wrong turn, we pulled up to the entrance and asked if we could turn around. They let the car behind us turn around. We later found out that people accidentally pull into the base entrance all the time.</P>
<P>They're allowed to turn around. But not us. The military police said that it was because other cars didn't have peace signs on them!</P>
<P>Because of those peace signs, we spent the next 12 hours in military custody--without food or access to a lawyer. We were searched repeatedly, yelled at and humiliated, and had our van confiscated.</P>
<P>One thing that we did learn was that the military considers itself above the law. If they would so easily disregard the rights of 11 white college students, imagine how they treat immigrants or the poor in Latin America.</P>
<P>Upon leaving Fort Benning, most of us only had our resolve hardened to come back next year--and demand that the School of Assassins be shut down!</P>

