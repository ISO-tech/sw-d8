<P><font face="Times New Roman, Times, serif" size="4">U.S. official's message at IMF-World Bank meeting:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bombs not food</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 4,000 people marched in the Canadian city of Ottawa November 17 in a lively protest of the joint meetings of the International Monetary Fund (IMF) and World Bank. The march was the climax of a week of teach-ins and other meetings hastily put together after the terrible twins of global finance chose a new site for their meeting.</P>
<P>Tens of thousands of activists had been preparing for protests at the end of September in Washington, D.C., but the IMF and World Bank canceled their meetings after the September 11 attacks. The meetings were rescheduled on short notice--and set for the same weekend that thousands of U.S. activists planned to attend the anti-School of the Americas demonstration in Georgia.</P>
<P>Inside the barricaded meeting site, the U.S. government showed once again why it--and the institutions it controls, such as the IMF and World Bank--are hated the world over. Amid discussions of how to pursue corporate globalization, representatives of 183 countries voted almost unanimously for a token resolution calling on more-developed countries to sharply increase aid to less-developed nations.</P>
<P>The one vote in opposition came from the U.S. "Over the last 50 years, the world has spent an awful large amount of money in the name of development without a great degree of success," U.S. Treasury Secretary Paul O'Neill whined in his speech to delegates.</P>
<P>Meanwhile, the U.S. has spent more in its last 50 days of bombing Afghanistan than it spends all year long on aid to poor countries.</P>

