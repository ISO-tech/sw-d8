<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Powell's fake peace plan for Palestine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HOURS AFTER Secretary of State Colin Powell delivered a much-anticipated speech on U.S. policy toward the Middle East "peace" process, the bulldozer engines kicked to life in southern Gaza, flattening several houses. "We didn't do anything to have been left homeless this winter," said Sarah Tawashi, as she picked through the rubble to salvage any remnants of her belongings. "This is a crime."</P>
<P>Within a week of Powell's speech, Israeli forces had killed at least 12 Palestinians--including five schoolboys, aged six to 14, who likely tripped an unexploded Israeli tank shell, scattering their body parts and schoolbooks across a hilltop.</P>
<P>Another of the dead was Hamas military leader Mahmoud Abu Hanoud, murdered by Israel as part of its policy of "targeted assassinations," which has taken the lives of more than 70 Palestinians in the last year. With the U.S. preparing to send "peace" envoys, the assassination was an obvious attempt by Israel to torpedo any agreement.</P>
<P>About 50,000 Palestinians marched through the West Bank to protest the killing, and another 30,000 protested in Gaza City.</P>
<P>Don't hold your breath waiting for the U.S. government to denounce these atrocities. While Powell called on Israel to make a renewed commitment to peace negotiations by stopping the construction of illegal settlements in the Occupied Territories, he nevertheless reserved his sharpest criticism for Yasser Arafat and the Palestinian Authority.</P>
<P>"The Palestinian leadership must make a 100 percent effort to end violence and terror," said Powell. "There must be real results, not just words and declarations." But for years, all the U.S. has offered Palestinians are "words and declarations"--while Israel carries out a relentless campaign of terror.</P>
<P>The U.S. government is in the contradictory position of trying to draw Arab countries into its international "coalition against terrorism" while maintaining its unswerving support for Israel, whose savage suppression of the year-old Palestinian uprising has angered people throughout the Middle East.</P>
<P>Powell's speech and Bush's recent use of the word "Palestine" are transparent attempts to appear sensitive to Palestinian demands. But these words don't change the U.S. government's fundamental commitment to Israel as its most valued ally in the drive to dominate the Middle East--and keep its claws on Middle East oil.</P>

