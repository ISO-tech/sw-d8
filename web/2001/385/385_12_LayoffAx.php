<P><font face="Times New Roman, Times, serif" size="4">Layoff ax leaves more workers...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Struggling to put food on the table</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ELIZABETH SCHULTE reports on the spreading impact of the recession.</P>
</B><P>JOHN HARRIS will do whatever it takes to put food on the table. That's why he's hoping for a cleanup job at Ground Zero--the tangled mass of rubble that remains after the September 11 attack on the World Trade Center.</P>
<P>He's eager to work, despite the cancer-causing fumes that engulf the area. "I have to feed my family some kind of way," Harris, who was laid off from his maintenance job in June, told the <I>New York Times</I>. "I can't worry about 20 years down the road. I need a job now."</P>
<P>Harris is among the many workers hired by privatized welfare-to-work contractors to do the hazardous cleanup at the World Trade Center site. With the economy sliding into a recession--one that was in the making long before September 11--many families are now reaching the five-year lifetime limit on welfare, a provision of Bill Clinton's 1996 welfare "reform" legislation.</P>
<P>"We now have a welfare system in which time limits will be hitting in a majority of states at precisely the time when the labor markets are the weakest and when families are in the most trouble," said Deepak Bhargava, director of the National Campaign for Jobs and Income Support.</P>
<P>According to the Bureau of Labor Statistics, 415,000 people lost their jobs in October, the most for a single month in nearly two decades. An estimated 25 percent of those job cuts were a direct result of the September 11 attacks.</P>
<P>The Coalition for the Homeless reports that, in New York City alone, the number of people forced to sleep in municipal shelters and welfare hotels exceeds 29,000 a night--the highest number ever. More than 12,000 of them are children.</P>
<P>President George W. Bush says that he feels their pain. During a visit to a Washington, D.C., soup kitchen last week, he urged Americans to "dig a little deeper in their pockets" during the holiday season.</P>
<P>But no ordinary person has pockets deep enough to solve this crisis. Even before September 11, nonprofit organizations that feed and house the poor were already stretched.</P>
<P>A recent study by America's Second Harvest, a national network of food banks, reported that 2 million more people sought food help last year than three years earlier. On average, one out of five New Yorkers visited food pantries or soup kitchens this year.</P>
<P>Now more people get food from private charities than participate in the federal government's food stamp program. "People are turning to us as an alternative, a replacement when we had considered ourselves a backup or emergency system," said Second Harvest's director of public policy and research Douglas O'Brien.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Bush's charity for the bosses</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BUSH MAY talk about Americans "digging a little deeper." But he's really interested in another kind of charity--for the rich.</P>
<P>The "economic stimulus" package passed by House Republicans in November and endorsed by Bush is nothing more than corporate welfare. Among its proposals are reductions in the capital gains tax rate and the repeal of the corporate alternative minimum tax--passed under Ronald Reagan, no less, in 1986 to close tax loopholes.</P>
<P>A list of the recipients of Bush's generosity reads like a who's who of Corporate America--General Electric, Microsoft, Walt Disney. Many of the companies that would gain from the stimulus package were big contributors to Bush's campaign.</P>
<P>According to a report by Public Campaign, Citizens for Tax Justice and the Institute on Taxation and Economic Policy, the 16 companies that will receive immediate rebates of $7.4 billion contributed $45.7 million to federal election campaigns since 1991--and half a million to Bush's campaign alone.</P>
<P>Senate Democrats say that their proposal will help working Americans. But they're offering many of the same handouts. And no wonder--corporate donations filled Democratic campaign coffers as well.</P>
<P>All of Washington is using September 11 as an excuse to line the pockets of their corporate masters. It's time to say no way!</P>

