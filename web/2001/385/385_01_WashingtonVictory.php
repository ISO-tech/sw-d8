<P><font face="Times New Roman, Times, serif" size="4">Massacres and misery</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This is what Washington calls victory</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 30, 2001 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"WE MUST unite in opposing all terrorists, not just some of them." So George W. Bush declared in a speech to the UN General Assembly in November.</P>
<P>Meanwhile, Washington was celebrating the terrorists of the Northern Alliance as they took over Afghanistan. These same warlords killed 50,000 people the last time they "liberated" the capital of Kabul in 1992.</P>
<P>This time, Bush assured reporters, the U.S. will "work with the Northern Alliance commanders to make sure they respect the human rights of the people that they're liberating." Don't hold your breath.</P>
<P>In Washington's "war against terrorism," the Taliban are the enemy. So the Northern Alliance are "our friends."</P>
<P>And the U.S. government tolerates the war crimes of its friends--like the string of massacres that began when the Northern Alliance took over Mazar-i-Sharif and killed hundreds of surrendered Taliban fighters. In fact, it joins in.</P>
<P>U.S. warplanes helped in the slaughter of more than 500 prisoners of war in a fort outside Mazar-i-Sharif last weekend--dropping bombs to crush what may or may not have been a "riot."</P>
<P>Washington's smug and arrogant warmongers are ready to move on. Defense Secretary Donald Rumsfeld says that he has rejected his advisers' proposals for new countries to target after Afghanistan as "not radical enough."</P>
<P>"Think the unthinkable," Rumsfeld told the Pentagon. So the U.S. will prepare for more war--more unthinkable atrocities against new enemies, maybe in Iraq, maybe in Somalia, maybe in another of the 40 or 50 countries on the Bush gang's growing list.</P>
<P>"[N]o remembered wrong can ever justify the deliberate murder of the innocent," Bush said at the UN. But this is precisely what the U.S. government is doing--in exploiting the memory of the September 11 dead to justify deliberate murder of the innocent.</P>
<P>And still U.S. officials and their media mouthpieces crow about how "bombing has worked." From conservatives to liberals, the politicians and pundits are celebrating--and sneering at anyone who dared to oppose the war.</P>
<P>"What do these self-regarding commentators, who witness virtually nothing of the struggles of the outside world, have to say to the families of refugees bombed to death in the dusty town of Gardez the other day, long after it fell to anti-Taliban forces?" antiwar journalist John Pilger asked.</P>
<P>"What do they say to the parents of the dead children whose bodies lay in the streets of Kunduz last Sunday?&#133;What do 'humanitarian interventionists' say to people who will die or be maimed by the 70,000 American cluster bomblets left unexploded?"</P>
<P>Don't believe Washington's liars. This war is obscene. And we can't let them get away with it.</P>
<P>We have to stand up and say no to Bush's barbaric war!</P>

