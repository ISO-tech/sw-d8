<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The importance of a real debate</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2001 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE SOCIALISTS want to "hijack the movement." This not just gospel according to J. Edgar Hoover of FBI fame, but also popular currency among some anarchists in the antiwar movement.</P>
<P>Our sins consist of having an "agenda" that we try to "impose" on others, of seeking to create a centralized leadership under our control, and of trying recruit people to our cause above all else. These criticisms are made not in public but most often in Internet chat rooms.</P>
<P>The statements are clearly designed with an "agenda" of their own--to discredit socialists in the movement. But do they have merit?</P>
<P>First we must strip away the loaded language and speak of things plainly. Any political organization (or loose association) worth its salt must try to convince others of its ideas. It may do this well or badly, but if it wishes its ideas to become a real social force, then it must seek to organize people around its banner.</P>
<P>You may choose any language you like to describe this--pushing a "line," having an "agenda," trying to recruit. But it's clear that anarchists who collectively argue for consensus decision making, against delegated structures or any form of centralization, or that the antiwar movement must be "anticapitalist," are in fact pushing a line.</P>
<P>It is a line, I would argue, that weakens the movement, which needs democracy and some degree of centralization and coordination, but it is nevertheless a line.</P>
<P>We socialists also have a "line." We seek to organize others around the politics of working-class self-emancipation. We believe that a socialist movement can only be built by bringing together those elements in the various struggles that have drawn the conclusion to move from challenging a particular aspect of the system to challenging the system as a whole.</P>
<P>We therefore do not limit ourselves to building one movement or struggle, but seek to organize wherever workers, students and oppressed people are fighting. We believe, in short, that the minority that wants to destroy capitalism and replace it with a collective, democratic and socialist society must organize, in the struggles of the day, and win others to that goal.</P>
<P>Yesterday, there were tens of thousands protesting corporate globalization. Today, the economic arms of imperialism have given way to military ones, and we therefore turn our attention to Washington's new war.</P>
<P>In the antiwar movement, we seek to convince a layer of activists to oppose the war not only on the basis of deploring the killing of innocent civilians, but on the basis of rejecting U.S. imperialism in all its guises.</P>
<P>We argue that there can be unity around a broad set of questions (no war, no racism), but that within that there should be vigorous and open debate about issues the movement raises.</P>
<P>Revolutionary socialists do not seek to "take over," nor do we try to pull people away from the struggle and into "ours." We actively build different struggles precisely because we believe that "without struggle, there is no progress."</P>
<P>It is through experience in struggle that ordinary people develop confidence in their own capacity to change society, and it is only through the many partial struggles of today that we can hope to build a mass movement that can put an end to capitalism.</P>
<P>These experiences move people leftward, so that today's activist who is outraged by a cluster bombing of an Afghan village but still retains illusions in the United Nations will tomorrow conclude that the UN is merely another useful tool for the U.S. to cover its imperial designs.</P>
<P>So we do not counterpose our ideas and our organization to different movements and struggles because they are intricately connected. As Marx and Engels wrote in <I>The Communist Manifesto</I>: "The Communists fight for the attainment of the immediate aims, for the enforcement of the momentary interests of the working class; but in the movement of the present, they also represent and take care of the future of that movement."</P>

