<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's new witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2001 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON NOVEMBER 19, Rep. Saxby Chambliss (R-Ga.) suggested that state police should "arrest every Muslim that comes across the state line." Chambliss isn't an isolated bigot in Congress--he chairs the House Subcommittee on Terrorism and Homeland Security. His remark exposes the racist nature of the Bush administration's "war against terrorism" at home, which is nothing but a witch-hunt against Arabs and Muslims.</P>
<P>That witch-hunt has expanded significantly over the last two weeks, since Bush and Co. handed themselves sweeping authority to act as judge, jury and executioner of any noncitizens they choose, under a cloak of secrecy.</P>
<P>Bush's November 13 executive order instituting military tribunals gives him the sole authority to order a secret military tribunal for any "individual who is not a United States citizen with respect to whom I determine."</P>
<P>Meanwhile, Attorney General John Ashcroft is single-handedly overseeing the biggest roundup of immigrants since the internment of 120,000 Japanese Americans during the Second World War.</P>
<P>Just 10 months ago, a <I>Newsweek</I> magazine poll showed that 40 percent of Americans opposed Ashcroft's nomination because he was deemed too right wing. People polled cited his opposition to school desegregation, abortion and gay rights, not to mention his sympathies with far-right militias and anti-immigration groups.</P>
<P>Last month, Ashcroft summarily announced that he will no longer issue a public tally of those detained in the roundup of mainly Middle Eastern men since September 11, who numbered roughly 1,200 at last count.</P>
<P>Most are being held under the guise of visa violations--and few, if any, have been shown to have any connection to the September 11 terrorist attacks. Nevertheless, the Pentagon and Justice Department say that they have already begun preparing to move some of those detained into military custody for possible trial by military tribunal.</P>
<P>Ashcroft has also begun questioning 5,000 more Arab and Muslim men between the ages of 18 and 33 who entered the country legally since January 2000.</P>
<P>Muslim and Arab communities across the country are gripped by fear--facing racial harassment encouraged by the government's hysteria, coupled with the prospect that many people could face detention on the pretext of immigration irregularities.</P>
<P>In some cases, reporting racial harassment has led to detention. Mohammad Yaseen Haider, a 22-year-old, was beaten on September 16 by three men who shouted racial slurs outside the Norman, Okla., convenience store where he was working. Haider, a junior at the University of Oklahoma, complained to the local police. Now he has been detained on immigration charges.</P>
<P>The FBI apparently requested copies of Haider's complaint, then began investigating him on the grounds that he once lived next door to two men who are among those detained since September 11.</P>
<P>The FBI and Justice Department have also continued to use the anthrax scare to target Muslims and Arabs--despite mounting evidence of a homegrown far-right perpetrator. On November 10, FBI officials announced that the anthrax terrorist is probably a white male much like the "Unabomber," Ted Kazcynski, with a grudge against the government.</P>
<P>Yet three days later, 30 armed FBI agents descended on the home of a Pakistani family after a tip that they were seen dumping a cloudy liquid on the ground behind their home in Chester, Pa. The cloudy liquid, according to the family of Asif Kazi--Chester's city accountant--was soapy water from a clogged sink.</P>
<P>FBI agents held Mrs. Kazi at gunpoint while a decontamination team spent 10 hours searching their home--seizing her prescription for Cipro, which she uses to treat endometriosis.</P>
<P>Two blocks away, another team of FBI agents broke down the front door to the home of another Pakistani native, Dr. Ishad Shaikh--who has been the city health commissioner since 1994.</P>
<P>Bush has claimed that the terrorists of September 11 targeted the U.S. because of "our freedoms." Since September 11, the war at home has made a mockery of that claim.</P>

