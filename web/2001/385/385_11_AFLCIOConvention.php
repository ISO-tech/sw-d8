<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What's ahead at the AFL-CIO convention?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"NO ONE should seek to take advantage of this painful moment in our national life and of our collective desire for unity to push through costly, divisive and ill-advised tax cuts for the well-to-do that ignore the needs of working families and that do little, if anything, to improve the economy." So said AFL-CIO President John Sweeney November 14 in testimony before Congress.</P>
<P>Of course, George W. Bush and congressional Republicans aim to do exactly that. The question is whether Sweeney and other labor leaders meeting at the AFL-CIO convention in Las Vegas next week are able to meet this challenge.</P>
<P>AFL-CIO conventions are usually sleepy, stage-managed affairs. Four decades passed after the merger of the AFL and the CIO before there was an open fight over the federation's leadership in 1995.</P>
<P>Then, Sweeney ousted the old leadership in a campaign to reverse the decline in union membership and revive labor's political clout within the Democratic Party. He tried to balance a get-tough approach to anti-union employers with a policy of partnership with those that were more cooperative. That fight was limited, however. An overnight backroom deal patched up the differences on a unity slate for the AFL-CIO Executive Council.</P>
<P>The next AFL-CIO convention in 1997 celebrated the Teamsters' crucial strike victory over UPS and focused on new organizing. Labor's biggest growth under Sweeney was a net gain of 265,000 in 1999. But this was far short of labor's goal of 700,000 per year--and overall union membership declined slightly the following year.</P>
<P>The boom of the late 1990s had allowed Sweeney to paper over the contradictions in his program. At least a few employers were willing to go through the motions of partnership as long as they were making record profits.</P>
<P>But with recession-driven layoffs on the rise and a million manufacturing jobs lost since July 2000, unions face an enormous fight to rebuild membership--now just 13.5 percent and just 9 percent in the private sector.</P>
<P>And while the AFL-CIO political efforts failed to help the Democrats take Congress, they did make gains. The 1999 AFL-CIO convention was devoted almost entirely to support for Al Gore's presidential campaign--and Gore did win more votes than Bush.</P>
<P>Yet soon after Bush stole the White House, key unions like the Teamsters, the International Association of Machinists and the United Auto Workers--and even the AFL-CIO itself--lined up with Bush on key issues. The United Brotherhood of Carpenters pulled out of the AFL-CIO altogether, and refused to pay $5 million in back dues.</P>
<P>In the weeks before September 11, labor was rife with rumors that Sweeney would face a challenger from a more conservative figure--someone friendlier to employers and Bush. Plenty of union leaders oppose Sweeney's more progressive policies, such as support for amnesty for immigrants and his limited but real openings to students and the left.</P>
<P>The reality is that Sweeney's steps to the left were extremely small--for example, he bashed Ralph Nader's pro-worker presidential bid and went all-out for Gore. Now Sweeney's strategies are unraveling just as the struggle is becoming more difficult.</P>
<P>Since September 11, Sweeney has alternated between offering support for the war and voicing bitter attacks on Bush's anti-worker policies at home. That's a reflection of the deep bitterness of millions of union members who got little or nothing from the record 1990s boom--and who are now being asked to sacrifice yet again.</P>
<P>That anger may even be voiced in speeches by a few union leaders in Las Vegas--and the crisis could force some genuine debate to the surface. But the real change in labor's direction will come when workers begin to take a stand and demand that their leaders put up a real fight.</P>

