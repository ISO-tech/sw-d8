<P><font face="Times New Roman, Times, serif" size="5"><b>South Central LA fight for schools</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michal Myers, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2001 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--More than 200 high school students and community members descended on the LA Unified School District board meeting on November 13.</P>
<P>The protest was organized by the South Central Community Coalition to demand that the board build new high schools to end tremendous classroom overcrowding. Every high school in South Central is currently at double its intended student capacity--in some cases, triple--even with students attending schools year-round.</P>
<P>Organizers expressed a desire to work with teachers and other school personnel to spread the struggle.</P>
<P>School board member Genethia Hayes addressed the crowd, saying residents would have to help pass a $1 billion bond measure if they wanted to build new schools. "They don't scrap about $1 billion bonds when they want to build a new prison," observed South Central resident Herman Henderson.</P>
<P>"They tell us there isn't land to build on, but new McDonald's are going up all over," said Manual Arts High School senior Manuel Hernandez. "It's like they want us to drop out and get bad jobs. I'm an honors student, but there would be more good students if we had classrooms and desks!" Hernandez added.</P>
<p>"It seems like these protests are the only way we can be heard and we're going to keep them up."</P>

